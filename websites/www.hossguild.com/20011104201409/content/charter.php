	It is expected that members of the guild treat other members with respect at all times. Problems arise when people make off the cuff remarks in guildchat. There is often more to a situation than is readily apparent, and unless you are sure you know exactly what is going on, members should refrain from making derogatory comments about other members in guild chat. If a member has a problem with another member, it should be addressed with an officer. 
</p>
'Bashing' other guilds in guildchat is highly discouraged. Regardless of what you think of another guild, their methods, tactics, etc, it never accomplishes anything to talk derogatively of an entire guild. This is not to say you can't have personal opinions, but it is expected that members keep those opinions out of guildchat. 
</p>
Topics discussed on our private forums are generally not to be seen by or told to any non guild members. Compromising the security of the guild's website and/or private forums will lead to disciplinary action and may lead to deguilding if the intent was malicious in nature. 
</p>
No posts should be made regarding Hoss' accomplishments to other message boards. We will accomplish what we accomplish, we can be proud of what we do without having to brag to others publicly. 
