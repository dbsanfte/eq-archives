	<td height=20 width=* background=m.jpg style="font-family:arial;font-size:9pt;color:#FFFFFF"><font face=Arial size=2>
	The guild policy on loot is divided by category. The categories are defined by how many people are 
	required to obtain an item. So while a particular item may be extremely rare, if it only requires a 
	single group to obtain, it is considered common loot.</p>
	
	Distribution of common loot (i.e. Hierophant cloak, bloodpoint, etc) will be entirely up to those
	people involved in hunt. Uncommon loot (i.e. most pieces of planar armor) will be distributed by 
	a rotation. The individual classes will decide on the order of the rotation, and be responsible 
	for maintaining the rotation. Exceptions, such as a person missing a single piece, etc, are up 
	the class to decide about. Absence will not result in you getting extra pieces when you are present
	(i.e. you are third on list to receive an item. You miss a hunt and the 2 people above you each 
	get 2 items. The next hunt you are there, you will be up for the first item, but do not get 3 items 
	in a row to bring you up to parity).</p>
	
	If a problem arises in distribution of uncommon loot, the officers will make final decision. 
	Distribution of rare loot (God/dragon/planar BPs/robes) will be done by the officers. This 
	decision will take into account need, participation, usefulness, etc. In most cases the decision 
	will be made beforehand (i.e. before a known dragon raid). While the officers will make all final 
	decisions about all rare loot (even in cases where there is overwhelming agreement as to who should
	get item, in order to maintain consistency), this decision will not be made in a vacuum. Rare loot 
	distribution can be discussed on the guild's private message board.</p>
	
	All rare loot is considered guild loot. This means that if a person gets an item they are expected 
	to use the item. If a time comes when said person no longer needs/wants, or regularly uses the item
	, it will be redistributed just as if it were a new item (decided by the officers). This does not 
	apply to items being used part time (i.e. shaman using Nagafen hammer sometimes, paw other times...or 
	warrior switching between 1h and 2h weapons as situation dictates). It is also expected that valuable 
	items obtained with a group (i.e. common or uncommon as defined above) not be traded away for personal 
	benefit if other members of the group that helped obtain said item, still want/need it.</p>  
	
	No lotto's will be used for uncommon or rare loot...ever</font>
	
