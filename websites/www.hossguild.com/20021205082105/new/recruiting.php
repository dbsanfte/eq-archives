<p>Hoss raids nearly every day from about 5:00 pm pacific to 10:00 pm pacific. We are primarily a raiding guild, and it is expected that members attend a minimum of 4 raids per week. If these hours don't meet with your play schedule, then you may want to look elsewhere.

<p><b>The Application</b>


<p>If you feel you meet the requirements above, please send us your application to <a href=mailto:hossapp@hossguild.com?subject="Character Name - Class">hossapp@hossguild.com</a>. Make sure to include your character name and class in the subject in the following format: "CharName - Class".
<br>You will be contacted within two weeks with our response.



