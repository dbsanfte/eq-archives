</b>

<p>Once we receive your application, we place it on file. Class demands change over time, and as your character's strength grows (as reflected in your magelo - keep it up to date), your chances of being invited as a Guest will change. Once there is demand for your class (this may be immediately, or a long time after applying), and your AA's and Level meet the requirements, you will be contacted about guesting with Hoss.  If you ever have questions about your status or what you can do to build the strength of your application, or if you wish to withdraw your application, contact Lola in game or on the forum.

Again, you will NOT be contacted unless you are being asked to Guest with us. We will, over time, continue to consider any and all applications we have recieved until we are requested to withdraw them.

<p><b>Pre-trial (Guest)</b>

<p>When you are invited as a Guest to Hoss raids, you are given the guildtag for the duration of the raid, to facilitate communication. This is the opportunity for you to get to know us, and for us to get to know you. Pre-trials are where we first get to know you and evaluate your skills. We encourage our guests to group with members outside of raids, hang out in <a href="http://hossguild.com/forums/showthread.php3?s=&threadid=1996">IRC</a>, etc.

<p>The length of the pre-trial will be no less than 3 weeks. At the 3 week mark, all Hoss members vote on the issue of promoting a Guest to Trialee status. If a Guest does not receive 75% or more Yes votes in the first poll, then there will be a second poll 2 weeks later. If there are still not enough Yes votes, then that person will no long be considered for a Trial, and let go as a Guest.

<p><b>Trial </b>

<p>Trials are where we see how well you fit into the guild. Trials last at least 1 month, with a review at the 2 week mark. Trialee's will be informed of any issues that were brought up during the review. At the end of the first month, all Hoss members vote on the issue of promoting a guest from Trial to Full Membership status. If a Trialee does not receive 85% or more Yes votes on the first poll, there will be a second poll 2 weeks later, and then, if necessary, a third and final poll again 2 weeks later. If there are still not enough Yes votes then they will be let go from Trial.</p>		
