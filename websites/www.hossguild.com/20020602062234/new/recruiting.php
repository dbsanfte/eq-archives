<p>
You must have a connection that can withstand raid intensive situations. Even exceptionally skilled people, with wonderful personalities, are more of a liability than an asset if they 
go linkdead every fight. This is especially true for clerics. 
<br>
<br>
Hoss raids nearly every day from about 5:00 pm pacific to 10:00 pm pacific. We are primarily a raiding guild, and it is expected that members attend a minimum of 4 raids per week. If these hours don't meet with your play schedule, then you may want to look elsewhere. 
<br>
<br>
<br>
<b>Application</b>
<br>
<br>
If you feel you meet the requirements above, please email your application to <a href=mailto:hossapp@hossguild.com>hossapp@hossguild.com</a>. You will be contacted within a few days with our response. 
<br>
<br>
Please include the following in your application: 
<br>
<br>
- what your normal playtime hours are <br>
- what type of connection you have <br>
- guild history/raid experience <br>
- if possible, list 1-2 members who can vouch for you <br>
- magelo profile (go <a href=http://www.magelo.com>here</a> if you need to make one) <br>
- anything else you have to say! <br>
<br>
If all of our requirements are met and you have made it past our initial screening process, you will then be subject to our pre-trial/trial process. 
<br>
<br>
<br>
<b>Pre-trial </b>
<br>
<br>
Pre-trials are where we first get to know you and evaluate your skills. Those wishing to trial with Hoss will first raid with us as a guest. We encourage our guests to try and group up with members outside of raids, hang out in irc, etc. 
<br>
<br>
The length of the pre-trial will be from 3-5 weeks. After 3 weeks, we will take our first vote in regards to trialing. We require 75% yes votes to trial. If an applicant does not have 75% yes votes, then we will repoll 2 weeks later. If there are still not enough votes, then that person will no longer be considered for trial. 
<br>
<br>
<br>
<b>Trial </b>
<br>
<br>
Trials are where we see how well you fit into the guild. Trials last 1 month, with a review at the 2 week mark. Trialee's will be informed of any issues that were brought up during the review. At the end of that time, we vote in regards to guilding. Trialee's must receive 85% yes to be guilded. If they do not have 85% votes, then they will be let go from trial.</p>		
