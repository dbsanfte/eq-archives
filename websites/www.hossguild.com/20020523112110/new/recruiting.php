<p>If you feel you meet the qualifications above, please contact Sylvia in EQ or via email at <a href=mailto:sylvia@hossguild.com>sylvia@hossguild.com</a> for more information.<br>
<br>
If all of the initial requirements are met and you have made it past our initial screening process, you will then be subject to the process of our trial and final membership voting system, as described below.<br>
<br>
<br>
<b>TRIALEE POLLS / VOTING REQUIREMENTS</b><br>
<br>
In order to obtain a Hoss trial, a candidate must recieve at a minimum the following votes: <br>
<br>
- Poll Choices will be as follows: Yes trial, Never trial, and Not enough info to decide. <br>
<br>
- All decisions will be made from a pool of 30 votes or higher, polls will last 4 days. <br>
<br>
- If there are not enough votes after a period of FOUR days, the poll will be closed. A new poll may be started 2 weeks later. <br>
<br>
- If there is 30% or higher Never trial, the candidate will be told we are not interested in guilding them. <br>
<br>
- If there is a 75% or higher Yes trial votes, a trial will be started immediately. <br>
<br>
- If there is 30 votes, and neither 75% or higher YES or 30% or higher NEVER, then we will continue pre-trialing the candidate. A new poll may be started 2 weeks later. <br>
<br>
- There is a maximum of 3 trialee polls per candidate. If after 3 polls, 30+ people do not vote or have enough information to base an opinion, we will tell the candidate we are not intertested in guilding them. <br>
<br>
<br>
<b>GUILDING POLL REQUIREMENTS</b><br>
<br>
- Trial must last a minimum 2 weeks before any guilding poll is made. <br>
<br>
- Guilding polls must have a minimum 30 votes to be valid. <br>
<br>
- Polls will have the options: Yes - Guild, No - End trial now, No - Remain on trial at this time. <br>
<br>
- Trialees must achieve a 90% YES vote or higher to be guilded. <br>
<br>
- Any trialee with 30% or higher No - End trial now votes will be guildremoved and let go. <br>
<br>
- If there is neither 90% or higher Yes votes, or 30% or higher No votes, the trialee will remain on trial 2 more weeks. A new poll will be started 2 weeks later. <br>
<br>
- There is a maximum of 3 voting sessions per trialee. If a firm decision is not made by that time, the trialee will be deguilded and let go. (IE - maximum trial length is 6 weeks) <br>
		
