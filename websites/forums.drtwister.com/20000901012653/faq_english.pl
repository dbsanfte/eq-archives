  <h3><a name="register">Why should I register a Username?</a></h3>
<p>
</td></tr><tr><td bgcolor="#dcd9bc">
By registering a Username, you will be able to edit your profile and preferences.  You will get the most out of your time here if you change your profile to suit your individual tastes.  There are alot of options in your profile to make your experience here more enjoyable, so please take a few moments to try the various settings.  Also only people with registered Usernames can take advantage of the "New Posts" feature upon each visit.  

  
