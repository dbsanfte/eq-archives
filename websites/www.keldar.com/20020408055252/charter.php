<font size="4"><b><center>Guild Charter</center></b></font><br>

<b>Introduction</b>

<p>By decree of the members present at the formation of the guild known as
Dragons of Kel`Dar we acknowledge our government herewith and declare
ourselves unified with all the powers and judgment of formal assembly.
Recognition of name shall be observed herewith as Dragons of Kel`Dar.</p>

<p>We of Dragons of Kel`Dar bind ourselves together in friendship and
camaraderie for the purpose of adventure and fun. We stake our claim to the
principles of honor and valor. We abide by the law set forth in the Writ of
Dragons of Kel`Dar and honor our leaders with our duty. We shun spoilers of
fun. We do not propose war with any other. We will defend our members and the
honor of Dragons of Kel`Dar at all costs. We will uphold the virtues of the
Guild's way in all things.</p>

<p>The Charge of Dragons of Kel`Dar describes and ordains the individual duties
of each rank and office within the Guild of Dragons of Kel`Dar.</p>

<b>The Guild Leader</b>

<p>The Guild Leader is the highest-ranking position within the Guild. His/her
duties are to lead Dragons of Kel`Dar with honor and valor, to oversee the
actions of all members, to uphold the Writ of The Dragons of Kel`Dar, to
regulate the guild's relations with foreign guilds, and to settle disputes within
the ranks. The Leader may implement any actions they deem worthy. The Leader
may perform promotion and induction ceremonies. Upon the advice of the
Council of Justice of Dragons of Kel`Dar, the Leader may implement his/her
recommendation for action against a member. The Leader has the right to veto
any action of the other members with the exception of a decision by majority
of the Council of Justice. The members of the guild agree to abide by the
leader decisions or decree. The guild leader is elected by popular vote of
all members in the guild, with the exception of those in probation. Every (6)
months, on the First of the month, a popular election shall be held to
confirm or replace the Guild Leader.</p>

<b>The Council of Justice</b>

<p>The Council of Justice has 5 members with distinct roles but still acting as
unity to make decisions concerning the guild. Councilors are elected by
popular vote and confirmed or replaced, individually or completely, every 6
months, on the First day of the month. Councilors must be Officers before
they can ascend to the Council of Justice. The roles performed by the Council
of Justice are recruitment, liaison with the Xegony Alliance and coordination
of raids and events. In order to fulfill these roles, Councilors can request
the assistance of volunteers (Officers or Members). The Council of Justice or
the Councilors individually hold the leadership of Dragons of Kel`Dar in the
absence of the Guild Leader. Their decisions must always come from consensus,
and their discussions are private unless they choose to make them public.
Even by consensus, the Councilors cannot invite new members who have not been
previously approved by the Guild Leader and/or by popular vote. The Council
of Justice can veto a decision made by the Guild Leader if they reach
unanimity in that decision. The Councilors may rotate in their duties or
perform more than one role. A Councilor can be dismissed by the Guild Leader
or the remaining Councilors in case of inappropriate conduct or lack of
interest/dedication in performing his/her role as a councilor.</p>

<b>The Officers</b>

<p>It is the Officers duty to provide advice to the Council of Justice upon
their request. This may include holding one or more voluntary duties,
assisting councilors with duties, reporting on guild status, reporting on
foreign guild activity, reporting on guild morale and welfare, reporting on
guild activities and events, and any and all other needs as deemed necessary
by the council. The Officers may perform ceremonies to invite new members if
the councilors and Leader are unavailable.</p>

<b>The Members</b>

<p>The members of Dragons of Kel`Dar have voting rights to confirm/replace the
Guild Leader and Council of Justice, as well as the removal or invitation of
a guild member. They must go to the Council of Justice to request assistance
or provide feedback on guild rules and structure.</p>

<b>Recruitment</b>

<p>All members are encouraged to recruit. Members should refer applicants to the
recruitment officer. The officer will inform the interested party of our
theme and what we represent and assign a mentor. Mentors will help the
recruit to interact with DoK members and answer any questions they might
have. It is mandatory that the recruit read and agree to the rules of this
charter and the lotto/raid rules also present on this website. The
recruitment officer will post all information available on the recruit on our
private voting forum, and all members will be encouraged to vote. In order to
become a DoK member, a recruit must have 15 positive votes and no negative
votes. Every application will be active for a minimum of one week and a
maximum of 2 months. Should the recruit receive the necessary votes before
one week of his application, still he will not be guilded until his week is
up. If a recruit does not receive 15 votes in 2 months, his application is
denied and he will need to re-apply if still interested. Votes given during
his first application are reset and do not count towards the new application
requirement. Should the recruit be elected to become a member, he will serve
Dragons of Kel`Dar on a probationary period to be determined by the Leader or
Council on a case by case basis but no less than 1 month. During this
probationary period, the new member will be afforded every right and
responsibility as that of any member of Dragons of Kel`Dar. An initiate can
be removed from the guild during his/her probationary period for any reason
and without notice. Any items given to Dragons of Kel`Dar or its members will
not be returned as they are considered guild property.</p>

<b>Our Moral Standards</b>

<p>The Members of all rank must conduct themselves with honor at all times. They
must carry the name of Dragons of Kel`Dar with pride and power, spread the
values of the guild and always serve his/her brothers and sisters in loyalty
and friendship.</p>

<p>Marriages will be observed and respected by all of Dragons of Kel`Dar in and
outside of our ranks. Marriages of a character between more than two persons
will not be recognized. Marriages between a member and a non-member and
between different races are allowed. Marriage does not require a formal
ceremony. If persons wish to make known their marriage in order to have it
acknowledged and recognized by Dragons of Kel`Dar, said persons should inform
the Council of their status. All members will recognize divorce. It is the
responsibility of the divorcees to inform the Council of their status if they
wish to make known their separation.</p>

<b>Grouping with Guildmates</b>

<p>We strongly encourage guild grouping. There is a forum named The Assembly on
our message board for this purpose. As a DoK member, you are expected to
always look for guildmates to group with or promote togetherness by
scheduling groups in advance using the message board or emails.</p>

<b>Life as a Dragon</b>

<p>Helping other citizens of Norrath is part of the life of a DoK member. When
helping someone (with buffs, heals, a port, items or even simple
information), Dragons are encouraged to shout DOK on guildsends and share the
happiness of being a good citizen. Just like we shout DING when a new level
is achieved!</p>

<b>Factions</b>

<p>Factions are not mandatory. Guild members are free to post their
recommendations or suggestions, but <i>pushing</i> other members into
one faction or another is considered harassment and will not be tolerated.</p>

<b>Alliances and Friendly Guilds</b>

<p>Dok currently has formal alliances with Clan Phoenix and Seekers of Lore. We
share raids and the use of our calendar of events. Every member of allied
guilds will be treated as guildmates. Friendly guilds are those we cooperate
with, although we do so without a formal alliance. There is no shared
calendar, but invitations for raids or events are fairly common. Those guilds
are Enigma, The Society, The Commune and Black Tower.</p>

<b>Multiple Characters, Multiple Guilds</b>

<p>The presence of characters in other guilds is strongly discouraged in Dragons
of Kel`Dar. We believe your loyalty should lie with your family and
confidential guild information may be jeopardized. If a member should decide
to request membership in other guilds with a different character, he/she
should contact the Council or the Leader immediately. Councilors and/or
Leader will vote privately on allowing multiple guilds for that particular
member. Members with characters in other guilds will not be accepted into the
Council of Justice or allowed to hold officership in Dragons of Kel`Dar.</p>

<b>Account Sharing</b>

<p>Account sharing is absolutely prohibited, with rare exceptions. Those are,
for example, sharing account information with guildmates or unguilded blood relatives.
If you share your account information without the Council’s previous
agreement, you will be subjected to guild removal. Please note that you are
fully responsible for the actions of your characters no matter who is playing
them. If your character is involved in unacceptable actions, you may be
deguilded immediately.</p>

<b>Buying Accounts</b>

<p>Dragons of Kel’Dar discourages the purchase of accounts. Not only this
violates the service agreement, it is also a potential risk to the reputation
of our guild. If you purchase an account with flawed reputation, the
characters will NOT be guilded, and any association with DoK (joining raids
or events) will not be allowed.</p>

<b>Promotion of a Member</b>

<p>Officer status will be granted to members of Dragons of Kel`Dar by decision
of the Council of Justice and/or the Guild Leader, often with recommendations
from Officers. Promotions are based on dedication and services to the guild,
time in the guild, advancement of your character and relationship with the
other guild members. The qualities looked for in an officer are leadership,
diplomacy, good temper, dedication, willingness to serve and respect to the
rules and guidelines of DoK.</p>

<b>Guild Meetings</b>

<p>Dragons of Kel`Dar members are requested to join monthly guild meetings, usually
held at the Lake Rathe Arena or East Commonlands Tunnel. During the meeting,
the Guild Leader and Councilors will have an agenda of topics to be
discussed, and will request participation of all members. Duels, off topic
comments and unrelated emotes are considered inappropriate behavior and a
member may be requested to leave the meeting. The Councilors or Guild Leader
may call additional meetings to address emergency issues. The Council of
Justice will meet with the Guild Leader on a regular basis. Officers and
volunteers may be invited to attend some or part of these meetings. Officers
are encouraged to hold their own meetings and request the presence of
councilors and Guild Leader if they find necessary.</p>

<b>In-Guild Trades and Guild Vault</b>

<p>Trade and donations between guildmates is encouraged. However, charging for
any spell casting that does not require a reagent, such as SoW, teleports,
Resurrection, Buffs, is considered inappropriate conduct. We encourage all of
our members to offer items to the guild before offering them for general
sale. If possible, we encourage offering items sold to guildmates at a
reduced price. Such in guild discounts serve to keep us better equipped
without being fleeced in Gfay. Dragons of Kel`Dar maintains a guild vault (mule
characters) usually around the East Commonlands tunnel. We strongly encourage
donations of spells and quest gems, but armor and weapons are also welcome.
Donations to the guild vault are appreciated but keep in mind the items you
donate will become property of the guild and may not be available should you
need them in the future. Guild members are welcome to borrow any item from
the vault, provided they do not sell or trade the item and will return it to
the vault as soon as they are able to buy/get a better item for themselves.
(rule does not apply to spells and quest gems. Please see specific rules for
withdrawing those items)</p>

<b>OOC Communication</b>

<p>By joining Dragons of Kel`Dar, one will commit to participation in all OOC
communication via e-mail or message board, or any tools used by the guild
during your membership. Ignorance of these rules, lotto/raid rules, recruits
in evaluation, guild news or calendar of events is not acceptable.</p>

<b>Leaving Dragons of Kel`Dar</b>

<p>The Councilors are requested to submit their resignation to the Guild Leader,
and the Guild Leader should submit his/her resignation to the Council of
Justice either in person or via email. All members are requested to submit
their resignations to the Council or Leader privately. After a brief interview,
that member will be encouraged to share his/her decision with the whole
guild. Dragons of Kel`Dar is a family and a commitment. Should a member
choose to leave and later decide to return, it will be up to the Guild Leader
and/or Council of Justice to restore or not his membership. A probationary
period may apply. Officers may be consulted for advice. Members who leave the
guild and return will not have membership restored more than twice. After
that, he/she will be subject to the normal recruitment process. If this
member chooses to leave once again, a future application for membership will
not be accepted. Officers and Councilors who leave the guild twice will also
lose their officership.</p>

<p>Members who are found to be applying to other guilds while still
wearing the DoK tag will be immediately deguilded. We consider "guild
shopping" a disrespect to your current guild and a clear violation of
our charter, where you are requested to inform the councilors or Leader of
your wish to deguild.</p>

<b>Removing a Guild Member</b>

<p>Dragons of Kel`Dar membership may be terminated in case of inappropriate
conduct or disrespected to the rules of this charter. Some cases where this
may occur are (but not limited to): aggressive cursing or disrespect to any
citizen of Norrath, proven Kill-Stealing or Ninja-looting, stealing from
guild members or guild mules, providing other guilds or players with
confidential guild information or any other conduct that could bring shame to
our guildmates or stain our reputation.</p>

<p>Any member regardless of rank may recommend the removal of a guild member to
Council or Leader. Detailed information and proof of inappropriate conduct
will be required. Screenshots or game logs may be acceptable proof, as well
as testimony of other parties. The Council of Justice and/or Guild Leader may
choose to remove a member immediately and without previous consideration if
the guild reputation or welfare is at risk. Depending on the accusation,
however, the member may be invited to a chat room interview with Councilors
and/or Leader to provide explanation before a decision is made on his
membership.</p>

