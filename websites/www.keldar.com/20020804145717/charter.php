<font size="4"><b><center>Guild Charter</center></b></font><br>

<b>Introduction</b>

<p>By decree of the members present at the formation of the guild known as 
House Kel’Dar we acknowledge our government herewith and declare ourselves 
unified with all the powers and judgment of formal assembly. Recognition of 
name shall be observed herewith as House Kel’Dar.</p>

<p>We of House Kel’Dar bind ourselves together in friendship and camaraderie 
for the purpose of adventure and fun. We stake our claim to the principles of 
honor and valor. We abide by the law set forth in the Writ of House Kel’Dar 
and honor our leaders with our duty. We shun spoilers of fun. We do not propose 
war with any other. We will defend our members and the honor of House Kel’Dar at 
all costs. We will uphold the virtues of the Guild's way in all things.</p>

<p>The Charge of House Kel’Dar describes and ordains the individual duties of 
each rank and office within the Guild of House Kel’Dar.</p>

<b>The Guild Leader</b>

<p>The Guild Leader, or Queen of Kel’Dar, is the highest-ranking position within 
the Guild. Her duties are to lead House Kel’Dar with honor and valor, to oversee 
the actions of all members, to uphold the Writ of The House, to regulate the guild's 
relations with foreign guilds, and to settle disputes within the ranks. The Queen 
may implement any actions she deems worthy. The Queen may perform promotion and 
induction ceremonies, as well as take action against a member. The Queen has the 
right to veto any action of the Royal Officers or Royal Advisors. The members of the 
guild agree to abide by the Queen’s decisions or decree. The Queen will hold leadership 
of The House until the day the guild no longer exists or she chooses to resign.</p>

<b>The Royal Advisors</b>

<p>The Royal Advisors are the Queen’s Councilors. These members have distinct roles 
but still act as unity to help the Queen make decisions concerning the guild. Royal 
Advisors are elected by the Queen and may be replaced by her at any time. Royal Advisors 
must be Royal Officers before they can ascend to the Council. The roles performed by 
the Royal Advisors are determined by the Queen, and may include supervision and election 
of the Royal Officers. The Royal Advisors individually hold the leadership of the House 
in the absence of the Queen. Their decisions must always come from consensus, and their 
discussions are private unless they choose to make them public. Even by consensus, the 
Advisors cannot invite new members who have not been previously approved by the Queen 
and by popular vote. A Royal Advisor can be dismissed by votes of his Advisor peers in 
case of inappropriate conduct or lack of interest/dedication in performing his/her 
duties.</p>

<b>The Royal Officers</b>

<p>It is the Royal Officers duty to provide advice and guidance to members of the House. 
This may include holding one or more voluntary duties, reporting on guild status, 
reporting on foreign guild activity, reporting on guild morale and welfare, reporting 
on guild activities and events, and any and all other needs as deemed necessary by 
the Queen. The Royal Officers may perform ceremonies to invite approved new members 
if the Advisors and Queen are unavailable.</p>

<b>The Members</b>

<p>The members of House Kel’Dar have voting rights to approve membership requests. 
They are encouraged to provide feedback on guild functions and other members, using 
the proper channels provided by the House. Members must always contact the Royal 
Officers to settle disputes within or outside the guild, or to request action from 
the Queen.</p>

<b>Recruitment</b>

<p>All members are encouraged to recruit. Members should refer applicants to the 
recruitment officer. The officer will inform the interested party of our theme and 
what we represent and assign a mentor. Mentors will help the recruit to interact 
with House members and answer any questions they might have. It is mandatory that 
the recruits read and agree to the rules of this charter, the lotto/raid rules and 
epic rules also present on this website. The recruitment officer will post all 
information available on the recruit on our private voting forum, and all members 
will be encouraged to vote.</p>

<p>Members can vote White (positive), Grey (neutral, with concerns) or Black 
(negative).</p>

<p>In order to become a House member, a recruit must have:
<br>- Sponsorship from a House member in good standing (not on probation)
<br>- 15 White votes from House members and no Black votes.
<br>- Approval of at least 2 senior members (Royal Officers, Advisors or the Queen)
<br>- Presence at 3 or more guild raids or events.</p>

<p>Every application will be active for a minimum of one week and a maximum of 
2 months. Should the recruit receive the necessary votes before one week of 
his application, still he will not be guilded until his week is up. If a recruit 
does not receive 15 votes in 2 months, his application is denied and he will need 
to re-apply if still interested. Votes given during his first application are 
reset and do not count towards the new application requirement.</p>

<p>Should the recruit be elected to become a member, he will serve the House on 
a probationary period of no less than 30 days. For each Grey vote on his application, 
the probation period will be increased in 30 days.</p>

<p>During this probationary period, the new member will be afforded every right 
and responsibility as that of any member of the House, with the exception of 
sponsorship of new members. An initiate can be removed from the guild during his/her 
probationary period for any valid reason and without notice. Any items given to the 
House or its members will not be returned as they are considered guild property.</p>

<b>Sister Guild</b>

<p>House Kel’Dar has a sister guild, Dragons of Kel’Dar, where we place our alternate 
characters who are under level 30. When you become a member of the House, you can guild 
all your characters, of all levels. Please contact an Officer to request a Dragons of 
Kel’Dar tag for your alternates under level 30. Upon reaching level 30, a member of 
Dragons of Kel’Dar can request a House Kel’Dar tag.</p>  

<b>Obligations of a Member</b>

<p>The Members of all rank must conduct themselves with honor at all times. They 
must carry the name of House Kel’Dar with pride and power, spread the values of 
the guild and always serve his/her brothers and sisters in loyalty and friendship.</p>

<p>Once a member of the House, you are obligated to keep your roster information 
accurate and updated. You are also requested to provide additional information 
such as trade skills and factions.</p>

<p>By joining House Kel’Dar, you are sworn to follow the Queen’s ruling at all times 
without debate or questioning. You must trust the leadership of your House, or you 
may choose to leave it. But while wearing the House Kel’Dar tag, you’re bound to 
obedience to our rules.</p>

<p>Marriages will be observed and respected by all members of the House, in and outside 
of our ranks. Marriages of a character between more than two persons will not be 
recognized. Marriages between a member and a non-member and between different races 
are allowed. Marriage does not require a formal ceremony. If persons wish to make 
known their marriage in order to have it acknowledged and recognized by the House, 
said persons should inform the Council of their status. All members will recognize 
divorce. It is the responsibility of the divorcees to inform the Royal Advisors of 
their status if they wish to make known their separation.</p>

<b>Grouping with Guildmates</b>

<p>We strongly encourage guild grouping. There is a forum named The Assembly on our 
message board for this purpose. As a House member, you are expected to always look 
for guildmates to group with or promote togetherness by scheduling groups in advance 
using the message board or emails.</p>

<b>Life in the House</b>

<p>Helping other citizens of Norrath is part of the life of a House member. When 
helping someone (with buffs, heals, a port, items or even simple information), 
members are encouraged to shout "KELDAR" on guildsends and share the happiness of 
being a good citizen. Just like we shout DING when a new level is achieved!</p>

<p>Your opinions and concerns are important to the Queen. There are two ways you can 
provide her with feedback - via email or via Private Message on our message board. 
The Queen may decide to make the discussion public - but you are requested to initiate 
the debate privately. Open criticism about guild policies, leadership or guildmates, 
especially on message board or guildchat, may result in disciplinary actions. Arguing 
with other guild members on guildchat is prohibited. If there is a dispute, please 
turn on your /log and contact a Royal Officer or Advisor immediately.</p>

<b>Factions</b>

<p>Factions are not mandatory. Guild members are free to post their
recommendations or suggestions, but pushing other members into
one faction or another is considered harassment and will not be tolerated.</p>

<b>Alliances and Friendly Guilds</b>

<p>House Kel’Dar currently has formal alliances with Clan Phoenix and Seekers of 
Lore. We share raids and the use of our calendar of events. Every member of allied 
guilds will be treated as guildmates. Friendly guilds are those we cooperate with, 
although we do so without a formal alliance. There is no shared calendar, but 
invitations for raids or events are fairly common. Those guilds are The Society 
and Black Tower.</p>

<b>Multiple Characters, Multiple Guilds</b>

<p>The presence of characters in other guilds is strongly discouraged in House Kel’Dar. 
We believe your loyalty should lie with your family and confidential guild information 
may be jeopardized. If a member should decide to request membership in other guilds 
with a different character, he/she should contact the Royal Advisors, Royal Officers 
or the Queen immediately. The Royal Advisors will vote privately on allowing multiple 
guilds for that particular member, and a final decision will be issued by the Queen. 
Members with characters in other guilds will not be allowed to hold officership or 
any titles in House Kel’Dar.</p>

<b>Account Sharing</b>

<p>Account sharing is absolutely prohibited, with rare exceptions. Those are, for 
example, sharing account information with guildmates or unguilded blood relatives. 
If you share your account information without the Queen’s previous agreement, you 
will be subjected to guild removal. Please note that you are fully responsible for 
the actions of your characters no matter who is playing them. If your character 
is involved in unacceptable actions, you may be deguilded immediately.</p>

<b>Buying Accounts</b>

<p>House Kel’Dar discourages the purchase of accounts. Not only this violates 
the service agreement, it is also a potential risk to the reputation of our guild. 
If you purchase an account with flawed reputation, the characters will NOT be 
guilded, and any association with the House (joining raids or events) will not 
be allowed.</p>

<b>Promotion of a Member</b>

<p>There are several steps to be achieved within the ranks of the House.</p>

<p>Members start as Knights or Maidens, and can be honored by progressive titles, 
at the Queen’s discretion, as a result of outstanding services to the guild. The 
ranks of the House are as follows:

<br>Queen
<br>Prince / Princess (Royal Advisors)
<br>Duke / Duchess (Royal Officers)
<br>Count / Countess
<br>Baron / Baroness
<br>Lord / Lady (recipients of the Order of Kel’Dar)
<br>Knights</p>

<p>Promotions and titles may be given to members who show constant raid leadership 
and organization, constant exp groups organization, trade skill services to guild 
and allies, services to guild (CRs, TPs), demonstrated friendship and loyalty to 
guildmates, constant presence on epic raids or raids that do not benefit them, and 
constructive feedback on guild issues.</p>

<p>Officership may be given to members who display qualities such as leadership, 
diplomacy, good temper, dedication, willingness to serve and respect to the rules 
and guidelines of the House.</p>

<b>Guild Meetings</b>

<p>House Kel’Dar members are requested to join monthly guild meetings, usually held 
at the Lake Rathe Arena or East Commonlands Tunnel. During the meeting, the Queen and 
Advisors will have an agenda of topics to be discussed, and will request participation 
of all members. </p>

<p>Duels, off topic comments and unrelated emotes are considered inappropriate behavior 
and a member may be requested to leave the meeting. The Advisors or the Queen may call 
additional meetings to address emergency issues. The Advisors will meet with the Queen 
on a regular basis. Royal Officers and volunteers may be invited to attend some or part 
of these meetings. Royal Officers are encouraged to hold their own meetings and request 
the presence of Advisors and/or the Queen if they find necessary.</p>

<b>In-Guild Trades and Guild Vault</b>

<p>Trade and donations between guildmates is encouraged. However, charging for any spell 
casting that does not require a reagent, such as SoW, teleports, Resurrection, Buffs, 
is considered inappropriate conduct. We encourage all of our members to offer items to 
the guild before offering them for general sale. If possible, we encourage offering items 
sold to guildmates at a reduced price. Such in guild discounts serve to keep us better 
equipped without being fleeced in Gfay. </p>

<p>House Kel’Dar maintains several guild vaults (mule characters) usually around the 
East Commonlands tunnel. These mules are managed by volunteer Officers or Advisors, and 
will be made available for withdrawing or donations at least once every 2 weeks. The 
inventory of those vaults will be made public on a weekly basis.</p>

<p>We strongly encourage donations of spells and quest gems, but armor and weapons are 
also welcome. Donations to the guild vault are appreciated but keep in mind the items 
you donate will become property of the guild and may not be available should you need 
them in the future. Guild members are welcome to borrow any item from the vault, provided 
they do not sell or trade the item and will return it to the vault as soon as they are 
able to buy/get a better item for themselves. (this rule does not apply to spells and 
quest gems. Please ask about the specific rules for withdrawing those items)</p>

<b>OOC Communication</b>

<p>By joining the House, one will commit to participation in all OOC communication via 
e-mail or message board, or any tools used by the guild during your membership. Ignorance 
of these rules, lotto/raid rules, recruits in evaluation, guild news or calendar of 
events is not acceptable. </p>

<b>Actions against a House Kel’Dar Member</b>

<p>The Queen and Royal Advisors will take disciplinary action against members, depending 
on the seriousness of the offense. The action will range from a verbal or written warning, 
to probation, and finally removal.</p>

<p>Members who are put on probation will be assigned a Probation Officer from the Royal 
Officers rank. This officer will monitor the progress and behavior of that member for at 
least 30 days. At the end of this period, the Probation Officer may recommend additional 
probation time (up to a total of 90 days), restoration of membership rights, or guild 
removal.</p>

<p>Members will only be put on probation once every 6 months. A second incident while 
on probation, a second incident within 6 months, or a second incident of the same nature 
at any time, may result in loss of membership with the House.</p>

<p>Members on probation will have mandatory services to the guild assigned by the Royal 
Officers. These services can include farming quest gems, organizing and lead raids or 
events, donating low-end items to newbies, etc.</p>

<p>Probation also means that member will be demoted one or more levels, according to 
the seriousness of the offense. Royal Advisors will be demoted to Officers, Royal 
Officers to Count / Countess, and so forth.</p>

<b>Leaving House Kel`Dar</b>

<p>The Advisors are requested to submit their resignation to the Queen, and the Queen 
should submit her resignation to the Royal Advisors. All members are requested to submit 
their resignations to the Advisors or Queen privately. A public farewell is inadequate 
until the Queen has been informed of your decision.</p>

<p>House Kel’Dar is a family and a commitment.</p>

<p>Should a member choose to leave and later decide to return, it will be up to the Queen 
and Royal Advisors to restore or not his membership. A probationary period may apply. 
Royal Officers may be consulted for advice. Members who leave the guild and return will 
not have membership restored again, if they leave once more. Royal Officers and Advisors 
who leave the guild will lose their titles and privileges if they return.</p>

<p>Members who are found to be applying to other guilds while still wearing the House 
Kel’Dar tag will be immediately deguilded. We consider "guild shopping" a disrespect 
to your current guild and a clear violation of our charter, where you are requested to 
inform the Advisors or Queen of your wish to deguild.</p>

<b>Removing a Guild Member</b>

<p>House Kel’Dar membership may be terminated in case of inappropriate conduct or 
disrespected to the rules of this charter. Some cases where this may occur are (but 
not limited to): removing your guild tag for raids without previous consent from the 
Queen, aggressive cursing or disrespect to any citizen of Norrath, proven Kill-Stealing 
or Ninja-looting, stealing from guild members or guild mules, providing other guilds or 
players with confidential guild information or any other conduct that could bring shame 
to our guildmates or stain our reputation.</p>

<p>Any member regardless of rank may recommend the removal of a guild member to the Queen 
or Royal Advisors. Detailed information and proof of inappropriate conduct will be required. 
Screenshots or game logs may be acceptable proof, as well as testimony of other parties. </p>

<p>The Queen and Advisors may choose to remove a member immediately and without previous 
consideration if the guild reputation or welfare is at risk. Depending on the accusation, 
however, the member may be invited to a chat room interview with the Queen and Advisors to 
provide explanation before a decision is made on his membership.</p>

