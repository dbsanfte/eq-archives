<b>Guidelines</b>

<p>-Before you schedule an epic raid, you must have attended at least 3 guild epic 
raids.</p>
<p>-You must wait 2 weeks before scheduling another epic raid after the guild helps 
you once.</p>
<p>-Before requesting an epic raid, you must have completed all steps you can do on 
your own or with a single group.</p>
<p>-Only one of your characters may request epic help. After that character has finished 
the quest, another may request help.</p>
<p>-You may gather regular groups to help with small epic encounters as much as people 
are willing to attend, but we suggest using the Assembly, not the calendar. Save the 
calendar for raids or guild events where 3 or more groups are needed.</p>
<p>-Putting pressure on people to help with your epic encounters or whining on guild 
sends about lack of support for your quest may affect your membership with DoK.</p>
<p>-You may only request help with a specific piece once every two weeks (this means 
if you don’t get your piece you may ask again, this will not penalize people with very 
hard quests). Exceptions will be made for rare/difficult camps.</p>
<p>-We STRONGLY suggest that you lead your own "events" and not request others to lead 
them for you (exception are very hard zones, like Chardok, or POA, this does NOT include 
Plane of Hate and Plane of Fear).</p>
<p>-Minimum level is the minimum level set by Verant for your quest, if you cannot complete 
it until level 50, do not ask for major help till you achieve 50.</p>
<p>-Every request for help will indebt you to a similar service to the guild (lead a raid, 
do lotto captain, help a lower level member with a CR or quest).</p>
<p>-For questions or concerns about getting help with your epic, please contact an officer, 
councilor or the guild leader.</p>

