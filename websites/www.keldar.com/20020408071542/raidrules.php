<font size="4"><center><b>Raid and Lotto Rules</b></center></font><br>

<b>Raid Rules</b>

<p>- Unless you have editing privileges to the calendar, you are required to email 
fill out the form available on this site to book an event or raid on the calendar.</p>
<p>- It is recommended that you always schedule raids on the calendar to control 
sign-ups and reserve the date and time.</p>
<p>- Pay attention to starting time. We suggest that our raid leaders don’t accept 
late arrivals. If you are not there on time, you may not get a group. Starting time 
doesn’t mean the time you should log on and start running. It means the time you 
should be at the meeting point with all the reagents you will need and a coffin.</p>
<p>- If something comes up and you won’t be able to attend, make sure you email the 
raid leader or post on the board to give them as much notice as possible.</p>
<p>- Never invite a guest without authorization from the raid leader.</p>
<p>- We suggest to always have a backup location in case your raiding spot is taken. 
This location must be communicated on the signup page. By signing up to Location 1, 
you are committing yourself (save faction issues) to move to Location 2 if needed.</p>
<p>- Traditionally, only Pullers, Raid Leaders, Lotto Captain and people with tasks 
(snarer, mezer, slower) should use /shout. Also avoid using public channels unless 
absolutely necessary.</p>
<p>- Don’t talk to the puller. Don’t make "suggestions" on what mobs to pull or 
complain your mobs are not getting pulled. This is ground for immediate removal 
from the raid.</p>
<p>- Bring your own reagents. This is a golden rule. You don’t bring peridots, you 
don’t get symbol or rune. You don’t bring wings, you don’t get a levitate. No scales? 
You better have a fishbone earring. It is YOUR responsibility to find out what reagents 
you will need for the raid. The raid leader may choose to remove you if you don’t 
have the necessary reagents.</p>
<p>- Bring a coffin. It is mandatory. Bring a coffin. Make sure you have a coffin. 
Did I mention it is mandatory? Good. If you don’t have a coffin, you will be on your 
own doing CR. The raid will not stop and wait for you to go buy one.</p>
<p>- If you don’t know the zone you will be raiding, make sure you print a map or do 
a little research. If you get lost, you are out of the raid and your corpse will be 
summoned AFTER the raid is over.</p>
<p>- Don’t go AFK without telling your group leader. Put someone on auto-follow if 
you absolutely have to.</p>
<p>- Experience in a raid is bonus. Don’t expect it, don’t hog it.</p>
<p>- Death in a raid is common. Avoid if possible, don’t get pissed off if it happens.</p>

<b>Lotto Rules</b>

<p>- Lotto rules are set in advance by the raid leader. If you are not clear on how it 
will work, ask him. Make sure you go over every point (cash items, gems, items…) and 
insure you are on the same page.</p>
<p>- DoK is a NBG guild. That means don’t get into the lotto if you already have the 
item, or will not use the item. If you are caught selling an item you recently won during 
our raids you may be asked to leave.</p>
<p>- Spells are always defaulted (or lottoed) amongst people who can immediately scribe it. 
In the absence of someone who can scribe the spell, it will be an open NBG lotto. Be 
considerate and don’t roll against a lvl 54 cleric for a lvl 55 spell if you are only lvl 
40. It is very unlikely you will use the spell before they can.</p>
<p>- The Lotto Captain will /shout item and stats at least twice. Pay attention. If you 
don’t give him a TELL until after he rolls, it is your loss.</p>
<p>- If the raid is a BYO reagents, cash items should be lottoed for the whole raid.</p>
<p>- We suggest assigning all participants a number before the raid starts for raid-wide 
lottos.</p>
<p>- You will be allowed to win ONE Item per raid, until everyone else has something. 
Gems, cash items, spells and items belong to different lotto tables. If you win a Blue 
Diamond, it will not prevent you from winning a spell, and after that a breastplate, if 
you are lucky enough.</p>
<p>- Nothing rots. If you are the only person who can use an item but you already won 
something, it doesn’t matter. You can get the new item.</p>
<p>- If you win something and later on something else drops that you’d rather have, you 
should announce your intention to throw the original item back into lotto if you win the 
roll for the new item. If you don’t win the second lotto, you don’t have to give up the 
item you won previously.</p>

