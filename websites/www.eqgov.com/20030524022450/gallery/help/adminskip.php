<p align="center"><font size="1" face="Verdana, Arial, Helvetica, sans-serif">Admin 
  Skip during Upload</font></p>
<p align="left"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"><i>Skip
  upload and process the files in your directory?</i> Administrators 
  can FTP files directory into their Uploads directory and then have PhotoPost 
  process those files. This is especially useful when an Administrator wants to 
  upload many files (typically more than the 2MB limit for a ZIP upload).</font></p>
<p align="left"><font face="Verdana, Arial, Helvetica, sans-serif" size="1">The 
  first step is to FTP the files to your upload directory. This is listed under 
  the first line &quot;Files should already be in:&quot;. Upload all your files 
  into the directory listed here and then check the box to the right (this says 
  to skip the upload process and just process the images in your directory).</font></p>
<p align="left"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"><i>Click here
  to bypass approval process and upload all files?</i> If you want to just bulk upload
  all your files without having to approve then, just check this box.</font></p> 
<p align="left"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"><i>Show 
  thumbnails during processing?</i> If you want to see a thumbnail of each image, 
  leave the selection at YES. If you are process a large amount of files and do 
  not want to take the extra time to see the thumbnails, select NO.</font></p>
<p align="left"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"><i>Number 
  of thumbnails to process next?</i> This option tells the upload system how many 
  files you wish to process at a time (this can be changed during processing, 
  too). The default is 10, which will show you 10 files at a time and let you 
  change the category or title for each individual image.</font></p>
<p align="left"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"><i>Upload
  photos as user?</i> This option allows an administrator to uploads photos as
  another user. Enter the name of the user who should be given ownership of the images.</font></p>
