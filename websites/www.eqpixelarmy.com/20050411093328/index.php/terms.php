	<br><br>	<p>Greetings and well met, travelers! I am glad you found us.
	<p>What exactly is going on here? Well, I’ll tell you. One day, I was relaxing in the sun of the beautiful beach of the Plane of Tranquility. I was enjoying the company of a good friend of mine, Kritta Didymus, and we were exchanging stories of friends long lost and battles long ago fought. I told her, “I wish there were some way to capture the memories of those friends and keep them, long after my memory fades. You know? Like making a memory into a painting with the snap of an arrow.” You see, I am a ranger and I live a simple life. The workings of pen and paper and paints are lost on me. Though the beautiful portraits I see intrigue me, I cannot create them myself.
	<p>Kritta thought to herself for a few minutes, and said, “I think I can do it.”
	<p>Shocked, I said, “What?” I was just thinking out loud. I didn’t think there was a serious way to do it!	
	<p>“I think I can do it.” Kritta spent the next few hours scratching pictures in the sand, making big gestures with her tiny gnomish hands to indicate whooshing, whirling gears and great tubes of color. I have to confess, my eyes glazed over at some of it, and I really didn’t understand most of it, except to know that this innovative gnome was quickly inventing a way to mass-produce portraits of friends, loved ones, and even ourselves with a few pushes of some buttons and an incredible machine. Cogs began to stir in the vision that was to become the Gnomish Ultra-High-Powered-Unique-Personification-And-Portrait-Likeness-Compositional-Arraying-General-Use-Machine.
	<p>“We’ll need lots of help,” Kritta said.
	<p>We wandered together into the massive library in the Plane of Knowledge, found ourselves a proper quill and parchment, pulled huge dusty volumes of the history of civilization on Norrath and the intricate details of gnomish machine technology and made lists. What would we need? Where could we get it? How can we do it, and most notably, who can help?
	<p>The two of us parted ways, heads swimming with ideas, and wandered the faces of Norrath, Luclin, and the planes to find volunteers for our crazy idea. Luckily, I happened upon a remote artists’ guild called Norrath’s Studio, and there I found an army of artists willing to join our cause.
	<p>For months we toiled over this amazing machine -- the artists all busy creating likenesses of the races and creatures of our world, and Kritta, crouched in the candlelight, night and day in her laboratory with a strangely innovative wood elf, Lasairduan, putting the machine together.
	<p>Here it is. With only your thoughts and a willing hand, you can push the buttons and crank the levers of our machine to create likenesses of any friend or foe that walks the lands that we call home. Remember that lady human bard that made your heart pound, so many moons ago? Push a button, and her portrait can be yours forever. Remember the proud ogre warrior that fell in battle that you have admired all your life? Turn a few cogs, and his portrait will inspire you to the end of your days. Too poor to commission an artist to show you and your love in a painting together? A whir of the gears, and the portrait springs to being.
	<p>This is the Gnomish Portrait Machine. The EQPA brought it to life. We welcome you.



