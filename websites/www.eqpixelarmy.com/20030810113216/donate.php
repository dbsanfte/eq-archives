	<p>The EQPA has decided to offer our hard work and art to the EQ community free of charge. However, bandwidth doesn't grow on trees! We do need a source of funding to maintain our bandwidth and web space, and so we ask you... if you like this site, donate! 
	<p><center><img src="images/dollar.jpg" width="450" height="192"></center>

	<p>Wait.. there's more to donating than just a warm and fuzzy feeling in your heart!
	<BR><hr width="50%"><BR> 
	<p>In an effort to provide more services for our generous donators (and likewise encourage donations, and enable us to continue to offer EQPA as a free-to-view site), we have come up with the following new-and-improved system for donations.

<p>Existing supporters will be "grandfathered" in to the new system, being granted the access and priveleges of the Supporter level automatically, regardless of amount of donation.  Folks who have already donated $30 or more at this point will also automatically be rewarded with the benefits of their new "donation level."

<p>Donations are handled separately from Commission orders and funds from one do not count for or give credit for the other.  However, a charitable donation of $10 followed by a later donation of $20 will be counted together to determine a contributor's donation status and privelege level.

<p><b>New Donation Structure </b>

<p><b>Level One - Donator</b>: $5 donation - Donator title and EQPA Supporter rank icon.

<p><b>Level Two - Sponsor</b>: $10 donation - Access to/preview of upcoming pieces in Preview Pavillion, Sponsor title and EQPA Supporter rank icon. 

<p><b>Level Three - Supporter</b>: $20 donation - Same as Sponsor + priority when requesting new pieces, Supporter title and EQPA Supporter rank icon. 

<p><b>Level Four - Benefactor</b>: $30 donation - Same as Supporter + "tech support" as in personal EQPA assistance in making simple signatures, cutting images out, making the images transparent, recoloring armor, Benefactor title and EQPA Supporter rank icon.

<p><b>Level Five - Philanthropist</b>: $40 donation - Same as Benefactor + hosting of up to 5 full signatures and 10 separate avatars on our web space , Philanthropist title and EQPA Supporter rank icon.
<BR><hr width="50%"><BR> 
	<p>We're not trying to be greedy, and believe me, we'd love to not have to ask for donations at ALL, but that's just not the way it works. Most of us are artists of the starving variety, and as much as I would have liked to include that independently wealthy and incredibly generous gentleman as part of our team, his pixel art just wasn't up to par. 
	<p>The simple fact of the matter is that if we run out of donations, the site goes away. We don't want it to go away, and we hope you don't want it to go away either. Any donation, small or large, is equally appreciated, even if it's just a dollar. Dollars add up! 

	<p>Thank you as always,
