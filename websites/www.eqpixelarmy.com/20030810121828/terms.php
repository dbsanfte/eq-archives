	<p>We are very interested in helping you to have the personalized pixel portrait that you want! We tried our best to include a wide enough variety to include most of the armor colors and types, hairstyles, weapons, pets, and accessories seen in Norrath (and even a few more fun ones!). All of us are artists, and we're supportive of the intellectual propery rights and copyright protection of all art forms. 

