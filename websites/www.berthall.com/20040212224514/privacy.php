<p><font face="Arial">What level of privacy can I expect in my online activity?</font></p>
</b>
<p><font face="Arial">Often the level of privacy you can expect from an online activity will be 
clear from the nature of that activity. Sometimes, however, an activity that 
appears to be private may not be. <i>There are virtually no online activities or 
services that guarantee absolute privacy</i>. This guide informs you about ways 
to maximize the privacy of your online activities and avoid common pitfalls.

</font> </p>
<u><b>
<p><font face="Arial">Public Activities</font></p>
</b></u>
<p><font face="Arial">Many online activities are open to public inspection. Engaging in these types 
of activities does not normally create an expectation of privacy. In fact, 
according to federal law, it is not illegal for anyone to view or disclose an 
electronic communication if the communication is &quot;readily accessible&quot; to the 
public (Electronic Communications Privacy Act, 18 USC § 2511(2)(g)(I)).</font></p>
<b>
<p><font face="Arial">Newsgroups.</font></b><font face="Arial"> For example, a message you post to a public newsgroup or 
forum is available for anyone to view, copy, and store. In addition, your name, 
electronic mail (e-mail) address, and information about your service provider 
are usually available for inspection as part of the message itself. </font> </p>

<p><font face="Arial">Most public postings made on the Internet are archived in searchable files, 
for example, <font COLOR="#0000ff"><u><a href="http://groups.google.com">http://</a></u></font><a href="http://groups.google.com">groups.google.com</a>. 
Thus, on the Internet, your public messages can be accessed by anyone at anytime 
-- even years after the message was originally written. Before you post a 
message to a public forum, ask yourself if want an employer or family member to 
be able to read your posting in years to come. (See <font COLOR="#0000ff">
<a href="#Links">Additional Resources</a></font> at the end of this guide.)</font></p>
<b>
<p><font face="Arial">Listserves.</font></b><font face="Arial"> Other public activities may allow your message to be sent to 
multiple recipients. Online newsletters and &quot;listserves&quot; are sent to a mailing 
list of subscribers. If you wish to privately reply to an individual who has 
posted a message in an online newsletter or listserve, be sure you address it 
specifically to that person's address, not to the newsletter address. Otherwise, 
you might find that your message has been sent to everyone on the mailing list.</font></p>

<b>
<p><font face="Arial">Subscriber directories.</font></b><font face="Arial"> You should not assume that your service account 
information will be kept private. Most ISPs provide online member directories 
that publicly list all subscribers to the service. Some of these directories may 
list additional personal information. Most service providers will allow users to 
remove their information from these directories upon request. Be aware that some 
service providers may sell their membership lists to direct marketers. </font> </p>
<b>
<p><font face="Arial">Domain registration.</font></b><font face="Arial"> Many individuals obtain their own website name, 
called domain names, for example, www.XYZfamily.org. Domain registrations are 
public information. Anyone can look up the owner of a domain name online by 
using a service such as<font COLOR="#ff0000"> </font>
<a href="http://www.checkdomain.com">www.checkdomain.com</a> or 
<a href="http://www.internic.net/whois.html">www.internic.net/whois.html</a><font COLOR="#ff0000">. </font>To see how easy it is 
to find out who owns a web address, use this service to check our domain name, 

<a href="http://www.checkdomain.com/cgi-bin/checkdomain.pl?domain=privacyrights.org">privacyrights.org</a>. Don’t use personal e-mail or home address information when 
you register for a personal domain name. Just be sure you can be reached when 
the service sends you the annual reminder to update the domain name. </font> </p>
<u><b>
<p><font face="Arial">&quot;Semi-Private&quot; Activities</font></p>
</b></u>
<p><font face="Arial">The presence of security or access safeguards on forums or services can lead 
you to believe that communications made within these services are private. Some 
forums are restricted to users who have a password. While communications made in 
these forums may initially be read only by the members with access, there is 
nothing preventing those members from recording the communications and later 
transmitting them elsewhere.</font></p>
<p><font face="Arial">One example of this kind of activity is the real-time &quot;chat&quot; conference, in 
which participants type live messages directly to the computer screens of other 
participants. Often these activities are described as &quot;private&quot; by the service 
provider. However, chat room users may capture, store, and transmit these 
communications to others outside the chat service. Additionally, these 
activities are subject to the same monitoring exceptions that apply to &quot;private&quot; 

e-mail (see next section). For chat safety tips, visit the Cyber Angels web site 
at <a href="http://www.cyberangels.org/101/chat/">www.cyberangels.org/101/chat/</a></font></p>
<u><b>
<p><font face="Arial">&quot;Private&quot; Services</font></p>
</b></u>
<p><font face="Arial">Virtually all online services offer some sort of &quot;private&quot; activity that 
allows subscribers to send personal e-mail messages to others. The federal 
Electronic Communications Privacy Act (ECPA) makes it unlawful under certain 
circumstances for someone to read or disclose the contents of an electronic 
communication <font SIZE="2">(18 USC § 2511)</font>. This law applies to e-mail 
messages.</font></p>
<p><font face="Arial">But, ECPA is a complicated law and contains many exceptions. It makes a 
distinction between messages in transit and those stored on computers. Stored 
messages are generally given less protection than those intercepted during 
transmission. Here are some exceptions to the ECPA:</font></p>

<ul>
  <li><font face="Arial">The online service may view private e-mail if it suspects the sender is 
  attempting to damage the system or harm another user. However, random 
  monitoring of e-mail is generally prohibited.<br>
&nbsp;</font></li>
  <li><font face="Arial">The service may legally view and disclose private e-mail if <i>either</i> 
  the sender or the recipient of the message consents to the inspection or 
  disclosure. Many ISPs require a consent agreement from new members when 
  signing up for the service. <br>
&nbsp;</font></li>
  <li><font face="Arial">If the e-mail system is owned by an employer, the employer may inspect the 
  contents of employee e-mail on the system. Therefore, any e-mail sent from a 
  business location is probably not private. Several court cases have determined 
  that employers have a right to monitor e-mail messages of their employees. 
  (See PRC Fact Sheet 7 on employee monitoring, 
  <a href="http://www.privacyrights.org/fs/fs7-work.htm">www.privacyrights.org/fs/fs7-work.htm</a>.)
  <br>

&nbsp;</font></li>
  <li><font face="Arial">Services may be required to disclose private information in response to a 
  court order or subpoena.<br>
&nbsp;</font></li>
  <li><font face="Arial">The USA PATRIOT Act, passed by Congress after the terrorist attacks of 
  September 11, 2001, reduces the checks and balances of ECPA regarding law 
  enforcement access to records about online activity. And it expands the types 
  of records that can be sought without a court order. For additional 
  information about the USA PATRIOT Act, visit the web sites of the Electronic 
  Frontier Foundation, <a href="http://www.eff.org">www.eff.org</a>, the Electronic Privacy Information Center, 
  <a href="http://www.epic.org">www.epic.org</a>, the Center for Democracy and Technology, 
  <a href="http://www.cdt.org">www.cdt.org</a>, and the 
  American Civil Liberties Union, <a href="http://www.aclu.org">www.aclu.org</a>.
  </font> 
  </li>

</ul>
<p><font face="Arial">To summarize: Your e-mail message may be handled by several different online 
services during delivery. The administrator of each of these systems may view 
e-mail under the exceptions to the ECPA, explained above. Law enforcement can 
access your online records without your consent. Additionally, an e-mail message 
may be disclosed to the ISP if either the sender or recipient consents. </font> </p>
<b>
<p><font face="Arial"><br>
<a name="PART TWO">PART TWO</a>: ONLINE TRACKING AND MONITORING</font></p>
<p><font face="Arial">Can online services track and record my activity?</font></p>
</b>
<p><font face="Arial">Yes. Many people expect that their online activities are anonymous. <i>They 
are not</i>. It is possible to record virtually all online activities, including 
which newsgroups or files a subscriber accesses and which web sites are visited. 
This information can be collected by a subscriber's own ISP and by web site 
operators. </font> </p>

<b>
<p><font face="Arial">Cookies.</font></b><font face="Arial"> When you &quot;surf&quot; the web, many web sites deposit data about your 
visit, called &quot;cookies,&quot; on your hard drive When you return to that site, the 
cookie data will reveal that you’ve been there before. The web site might offer 
you products or ads tailored to your interests, based on the contents of the 
cookie data. </font> </p>
<p><font face="Arial">Most cookies are used only by the web site that placed it on your computer. 
But some, called third-party cookies, communicate data about you to an 
advertising clearinghouse which in turn shares that data with other online 
marketers. Your web browser and some software products enable you to detect and 
delete cookies, including third-party cookies. (For additional information about 
cookie blocking, see <font COLOR="#0000ff"><a href="#Links">Additional Resources</a></font> at the 
end of this guide.)</font></p>

<p><font face="Arial"><b>Web Bugs.</b> 
A web bug is a graphic in a web site or an &quot;enhanced&quot; e-mail message that 
enables a third party to monitor who is reading the page or message. The graphic 
may be a standard size image that is easily seen, or it may be a nearly 
invisible one-pixel graphic. E-mail messages that include graphic displays like 
web sites are known as enhanced messages, also called stylized or HTML e-mail. 
The web bug can confirm when the message or web page is viewed and record the IP 
address of the viewer. The IP address is a multi-digit number that uniquely 
identifies a computer or other hardware device (such as a printer) attached to 
the Internet.</span></font></p>
<p><font face="Arial">You can defeat web-bugs by reading your email while offline, an 
option on most email programs. You can also install a software program that 
detects web bugs. To learn more about web bugs, visit
<a href="http://www.bugnosis.org/">www.bugnosis.org</a>. This site offers a free 
bug detection program. Many software products that detect and delete third-party 
cookies are also able to detect web bugs. The latest version of Microsoft 
Internet Explorer enables users to turn off third-party cookies and disable web 
bugs.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </font></p>
<b>
<p><font face="Arial">Marketing uses and &quot;spam.&quot;</font></b><font face="Arial"> Records of browsing patterns are a potentially 
valuable source of revenue for online services and commercial web site 
operators. Direct marketers can use such data to develop targeted lists of 
online users with similar likes and behaviors. Such data can also lead to 
unsolicited e-mail, known as &quot;spam.&quot; Additionally, browsing data may prove 
embarrassing for users who have accessed sensitive or controversial materials 
online.</font></p>

<b>
<p><font face="Arial">Browsers.</font></b><font face="Arial"> It’s important to be aware of the information transmitted to 
remote computers by the software you use to browse web sites. The major browsers 
are Netscape Navigator and Microsoft Internet Explorer. </font> </p>
<p><font face="Arial">Most web browsers invisibly provide web site operators with information about 
your ISP as well as information about other web sites you have visited. Some web 
browsers, particularly if they have not been updated with security fixes, may be 
tricked into reporting the user’s default e-mail address, phone number, and 
other information in the &quot;address book&quot; if the browser also handles your e-mail. 
(See the demonstration in <font COLOR="#0000ff"><a href="#Links">Additional Resources</a></font> to 
learn more about the information transmitted by your browser.)</font></p>
<b>
<p><font face="Arial">Privacy policies and web seals.</font></b><font face="Arial"> The Federal Trade Commission urges 
commercial web site operators to spell out their information collection 
practices in privacy policies posted on their web sites. Most commercial web 
sites now post policies about their information-collection practices. Look for a 
privacy &quot;seal of approval,&quot; such as TRUSTe (<a href="http://www.truste.org">www.truste.org</a>), on the first page 
of the web site. TRUSTe participants agree to post their privacy policies and 
submit to audits of their privacy practices in order to display the logo.</font></p>

<p><font face="Arial">Other seals of approval are offered by the Council of Better Business Bureaus 
(BBB), <a href="http://www.bbbonline.org">www.bbbonline.org</a>, the American Institute of Certified Public 
Accountants, WebTrust, <a href="http://www.cpawebtrust.org">www.cpawebtrust.org</a>, and the Entertainment Software 
Rating Board, <a href="http://www.esrb.org/privacy">www.esrb.org<u>/<font COLOR="#0000ff">privacy</font></u></a>.</font></p>
<p></p>
<b>
<p><font face="Arial">Workplace monitoring.</font></b><font face="Arial"> Individuals who access the Internet from work 
should know that employers are increasingly monitoring the Internet sites that 
an employee visits. Be sure to inquire about your employer's online privacy 
policy. If there is none, recommend that such a policy be developed. (See the 
PRC Fact Sheet 7 on employee monitoring, 
<a href="http://www.privacyrights.org/fs/fs7-work.htm">www.privacyrights.org/fs/fs7-work.htm</a>, 
and Fact Sheet 12 on responsible information-handling practices,

<font COLOR="#0000ff"><u><a href="http://www.privacyrights.org/fs/fs12-ih2.htm">www.privacyrights.org/fs/fs12-ih2.htm</a></u></font>.)
</font> </p>
<b>
<p><font face="Arial">Law enforcement access.</font></b><font face="Arial"> In order for law enforcement officials to gain 
access to subscriber transactional records, they usually must obtain a court 
order demonstrating that the records are relevant to an ongoing criminal 
investigation (Communications Assistance for Law Enforcement Act, 18 USC § 
2703(d)). This provision prevents &quot;fishing expeditions&quot; by government officials, 
hoping to find evidence of crimes by accident. But, as described in Section One 
above, the USA PATRIOT Act, passed into law in November 2001 in the aftermath of 
the September 11 terrorist attacks, has weakened these provisions. </font> </p>
<b>
<p><font face="Arial">Can an online services access information stored in my computer without my 
knowledge?</font></p>
</b>

<p><font face="Arial">Yes. Many of the commercial online services such as AOL automatically 
download graphics and program upgrades to the user's home computer. The 
subscriber is notified of these activities. But other intrusions are not so 
evident. News reports have documented that some services have admitted to both 
accidental and intentional prying into the memory of personal computers. 
Companies typically explain that they collect information such as users’ 
hardware, software and usage patterns to provide better customer service.</font></p>
<p></p>
<p><font face="Arial">It is difficult to detect these types of intrusions. You should be aware of 
this potential privacy abuse and investigate new services thoroughly before 
signing on. Always read the privacy policy and the service agreement of any 
online service you intend to use. </font> </p>
<b>
<p><font face="Arial">Can hackers get into my computer?</font></p>
</b>
<p><font face="Arial">An increasing number of users are accessing the Internet via high-speed cable 
modems and telephone-based DSL connections. When you are using a broadband 
&quot;always-on&quot; service, you are particularly vulnerable to attacks by hackers. We 
advise that you install a firewall device that monitors your network activity 
and allows only the activities you have authorized. You should also check with 
your provider’s website for instructions on securing your computer by removing 
unnecessary services and installing security updates to protect your computer. A 
free firewall software product is provided by Zonelabs, 
<a href="http://www.zonelabs.com">www.zonelabs.com</a>. </font> </p>

<b>
<p><font face="Arial">What is spyware and how can I know if it’s on my computer?</font></p>
</b>
<p><font face="Arial">Spyware is any software or hardware device that reports your activity. &quot;Adware&quot; 
spyware is installed by software companies as an additional source of income. 
&quot;Monitoring&quot; spyware was originally intended for parents and employers to 
monitor computer activity, including file access and keystroke logging, to 
protect against improper usage by children and employees. &quot;Diagnostic&quot; spyware 
is used by software companies to log errors and usage habits to improve the next 
generation of software. The user is usually not aware that spyware has been 
installed – hence, its name. The <font COLOR="#0000ff"><a href="#Links">Additional Resources</a></font> 
section at the end of this guide lists programs that locate and remove spyware.</font></p>

<p><font face="Arial, Helvetica"><b>What about cybercafes, airports, and other 
publicly-available Internet terminals? <br>
<br>
</b></font><font face="Arial">We advise that you do not use public terminals to 
access your bank account, check your credit card statement, pay bills, or access 
any other personally or financially sensitive information. Publicly-available 
Internet terminals are not likely to be closely supervised to ensure online 
privacy and security. They are used by many individuals every day. Ask the 
company that operates the public terminal how often they check their computers 
for spyware. Find out if they have installed a program that clears Internet 
caches, deletes cookies, erases surfing history, and removes temporary files. If 
the program does not automatically activate when users logoff, find out how you 
can run the program before you end your session.</font></p>
<p>
<b>
<font face="Arial">
<a name="PART THREE">PART THREE</a>: TIPS FOR SAFEGUARDING YOUR PRIVACY ONLINE</font></p>
<p><font face="Arial">What can I do to protect my privacy in cyberspace?</font></p>
</b>
<p><font face="Arial">When you are sitting alone at your computer, &quot;surfing the Net&quot;, sending 
electronic mail messages and participating in online forums, it's easy to be 
lulled into thinking that your activities are private. Be aware that at any step 
along the way, your online messages could be intercepted and your activities 
monitored in the vast untamed world of cyberspace.</font></p>

<b>
<p><font face="Arial">1. </font> </b><font face="Arial">Your account is only as secure as its <b>password</b>. Create 
passwords with nonsensical combinations of upper and lower case letters, numbers 
and symbols, for example tY8%uX. Do not use the same or variations of the same 
password for different applications. One way to create a password that is easier 
to remember is to use the first or last letters in a favorite line of poetry. 
Intermingle these letters with numbers and punctuation marks. &quot;Mary had a little 
lamb&quot; becomes m*ha2ll or y!dae5b. </font> </p>
<p><font face="Arial">Change your password often. Don't let others watch you log in. Don’t print 
your password on a post-it note and attach it to your video monitor. If you must 
write down or record your password, take steps to secure or disguise the 
information. </font> </p>
<b>
<p><font face="Arial">2. </font> </b><font face="Arial">Look for the <b>privacy policy</b> of the online services you use. 
Most Internet Service Providers (ISP) have adopted privacy policies that they 
post on their web sites and other user documentation. When you surf the web, 
look for the privacy policies posted on the web sites you visit. Also look for a 
privacy &quot;seal&quot; such as TRUSTe or BBBOnline. If you are not satisfied with the 
policy, or if there is no policy or seal logo posted, avoid using the site.

</font> </p>
<b>
<p><font face="Arial">3. </font> </b><font face="Arial">Check your browser’s <b>cookie settings</b>. We’ve come a long way 
from the days when browsers hid their cookie activity and gave users no options. 
Now you may accept or reject all cookies, or you may allow only those cookies 
generated by the website you are visiting. Be aware that when you use cookie 
management options, you might delete cookies for websites you trust. You may 
want to set a security level for trusted websites while blocking cookie activity 
for all others.</font></p>
<b>
<p><font face="Arial">4. Shop around</font></b><font face="Arial">. Investigate new services before using them. Post a 
question about a new service in a dependable forum or newsgroup. Use a search 
engine such as <a href="http://groups.google.com">http://groups.google.com</a> to find archived discussions and 
newsgroup postings about the service that you are considering. Bad reputations 
get around quickly in cyberspace. If others have had negative experiences with a 
service, you should get the message.</font></p>
<b>

<p><font face="Arial">5. </font> </b><font face="Arial">Assume that your online communications are <b><i>not</i> <i>private</i></b> 
unless you use encryption software. But most encryption programs are not 
user-friendly and can be inconvenient to use. If you do <i>not</i> use 
encryption, at least take the following precautions: Do not provide sensitive 
personal information (phone number, password, address, credit card number, 
Social Security number, your health information, date of birth, vacation dates, 
etc.) in chat rooms, forum postings, e-mail messages, or in your online 
biography.</font></p>
<b>
<p><font face="Arial">6. </font> </b><font face="Arial">Be cautious of <b>&quot;start-up&quot; software</b> that registers you as a 
product user and makes an initial connection to the service for you. Typically, 
these programs require you to provide financial account data or other personal 
information, and then upload this information automatically to the service. 
These programs may be able to access records in your computer without your 
knowledge. Contact the service for alternative subscription methods. </font> </p>

<b>
<p><font face="Arial">7.</font></b><font face="Arial"> Note that public postings made on the Internet are often <b>archived</b> 
and saved for posterity. It is possible to search and discover the postings an 
individual has made to Usenet newsgroups </font> <font face="Arial, Helvetica">
and blogs (web logs)</font><font face="Arial">. (See 
<a href="http://groups.google.com">http://groups.google.com</a>.) Ask 
yourself if you want an employer, family member, or a marketer to be able to 
link <i>you</i> to your public postings. Use a pseudonym and a nondescriptive 
e-mail address when you participate in public forums. Consider obtaining an 
e-mail address from one of the free web-based e-mail services such as 
<a href="http://www.hotmail.com">www.hotmail.com</a> or 

<a href="http://www.yahoo.com">www.yahoo.com</a>. Create a non-identifying e-mail address and 
use it when you participate in newsgroups and other public forums. </font> </p>
<b>
<p><font face="Arial">8. </font> </b><font face="Arial">The <b>&quot;delete&quot; command</b> does not make your e-mail messages 
disappear. They can still be retrieved from back-up systems. Software utility 
programs can retrieve deleted messages from your hard drive. If you are 
concerned about permanently deleting messages and other files on your program, 
you should use a file erasing program such as the freeware program at 
<a href="http://cleanup.stevengould.org">http://cleanup.stevengould.org</a> 
or the cleanup features of general utility software such as Norton's (<a href="http://www.symantec.com/sabu/ncs/">http://www.symantec.com/sabu/ncs/</a>) CleanSweep.</font></p>

<b>
<p><font face="Arial">9. </font> </b><font face="Arial">Your <b>online biography</b>, if you create one, may be searched 
system-wide or remotely &quot;fingered&quot; by anyone. If for any reason you need to 
safeguard your identity, don't create an online &quot;bio.&quot; Ask the system operator 
of your ISP to remove you from its online directory.</font><font COLOR="#ff0000"><font face="Arial">
</font> </p>
</font><b>
<p><font face="Arial">10.</font></b><font face="Arial"> If you publish information on a <b>personal web page</b>, note that 
marketers and others may collect your address, phone number, e-mail address and 
other information that you provide. If you are concerned about your personal 
privacy, be discreet in your personal web site.</font></p>

<b>
<p><font face="Arial">11. </font> </b><font face="Arial">Be aware of the possible <b>social dangers</b> of being online: 
harassment, stalking, being &quot;flamed&quot; (emotional verbal attacks), or &quot;spamming&quot; 
(being sent unsolicited messages). Women can be vulnerable if their e-mail 
addresses are recognizable as women's names. Consider using gender-neutral 
e-mail addresses and pseudonyms.</font></p>
<b>
<p><font face="Arial">12. </font> </b><font face="Arial">If your <b>children</b> are online users, teach them about 
appropriate online privacy behavior. Caution them against revealing information 
about themselves and your family. (See the <a href="#Links">Additional Resources</a> section at the 
end for details.)</font></p>

<b>
<p><font face="Arial">13. </font> </b><font face="Arial">Use only <b>secure web sites</b> when you transmit sensitive personal 
information over the Internet. When you provide your credit card account number 
to a shopping site, for example, be sure that the transmission is secure. Look 
for the <i>unbroken</i> padlock at the bottom right of the screen. Also make 
sure the web address has the letter ‘s’ after http in the address bar at the top 
of the page. For additional online shopping tips, read the PRC’s e-commerce 
guide at <a href="http://www.privacyrights.org/fs/fs23-shopping.htm">www.privacyrights.org/fs/fs23-shopping.htm</a>
</font> </p>
<b>
<p><font face="Arial">14. </font> </b><font face="Arial">Be aware that online activities leave <b>electronic footprints</b> 

for others to see. Your own ISP can determine what search engine terms you use, 
what web sites you visit, and the dates, times, and durations of your online 
sessions. Web site operators can often track the activities you engage in by 
placing &quot;cookies&quot; on your computer. They can learn additional information if 
they ask you to register on their site. Your web browser also can transmit 
information to web sites. </font> </p>
<p><font face="Arial">You can avoid leaving tracks when you surf the web by using &quot;anonymizing&quot; 
services. Take advantage of privacy protection tools, often called 
privacy-enhancing technologies (PET). Discussed here are <b>encryption, 
anonymous remailers, anonymous surfing services,</b> and<b> storage protection 
software</b>. You can find <font COLOR="#0000ff"><a href="#Links">Additional Resources</a></font> at 
the end of this guide. </font> </p>

<b>
<p><font face="Arial">Encryption</font></b><font face="Arial"><u>.</u> Encryption is a method of scrambling an e-mail message 
or file so that it is gibberish to anyone who does not know how to unscramble 
it. The privacy advantage of encryption is that anything encrypted is virtually 
inaccessible to anyone other than the designated recipient. Thus, private 
information may be encrypted and then transmitted, stored, or distributed 
without fear that it will be read by others. Strong encryption programs such as 
PGP (Pretty Good Privacy) are available online. </font> </p>
<b>
<p><font face="Arial">Anonymous remailers</font></b><font face="Arial"><u>.</u> It is relatively easy to determine the name 
and e-mail address of anyone who sends e-mail or who posts messages on public 
forums. Anonymous remailers are intermediaries that receive e-mail, strip off 
all identifying information, then forward the mail to the appropriate address.
</font>
</p>
<b>
<p><font face="Arial">Anonymous surfing services.</font></b><font face="Arial"> By combining the functions of remailers, 
disposable email addresses, and proxy servers, these ISP services mask your 
identity by acting as an agent to transfer data between an Internet website and 
your browser.</font></p>

<b>
<p><font face="Arial">Storage security and protection software</font></b><font face="Arial"><u>.</u> Software security 
programs help prevent unauthorized access to files on your personal computer. 
For example, one program encrypts every directory with a different password so 
only the person who knows the password can open it. These programs may include 
