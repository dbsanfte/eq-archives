<p><font face="Arial"><br>
<a name="PART ONE">PART ONE</a>: EXPECTATIONS OF PRIVACY IN CYBERSPACE</font></p>
<p><font face="Arial">What are &quot;online communications?&quot;</font></p>
</b>

<p><font face="Arial">&quot;Online communications&quot; are communications over telephone, cable networks, or 
wireless systems using computers. Examples of online communications include 
connecting to the Internet through an Internet Service Provider (ISP) such as 
America Online or Earthlink, or accessing the 
Internet from a public library or community computer center. Mobile access to 
the Internet is increasing via hand-held PDAs, pagers, and other devices. (For a 
directory of ISPs, see <font COLOR="#0000ff"><a href="#Links">Additional 
Resources</a></font> at the end of this guide.)</font></p>
<p><font face="Arial">The Internet raises some unique privacy concerns. Information sent over this 
vast global network may pass through dozens of different computer systems on the 
way to its destination. Each of these systems is operated by its own 
administrator and may be capable of capturing and storing online communications. 
Furthermore, your online activities can potentially be monitored by your 
Internet Service Provider (ISP) and by web sites that you visit.</font></p>
