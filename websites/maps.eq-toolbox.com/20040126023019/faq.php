	</tr>
	<tr>
		<td class=title>Soe maps are maps in Sony Online Entertainment's proprietary format, used from within Everquest itself. SEQ maps are maps used by ShowEQ, an external linux application, generally frowned upon by Sony. In the interest of compatibility and the most accurate maps from (and for) both communities, we have chosen to support both formats. </font><BR><BR></td>
