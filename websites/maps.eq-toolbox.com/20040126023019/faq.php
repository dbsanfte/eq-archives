		<td class=default bgcolor='#EAD697'><a name='5'></a><B>How does the cart work?</B></td>
	</tr>
	<tr>
		<td class=title>It works much like a standard shopping cart, except you don't have to pay for it :) When you are viewing a map, there is an icon and some text that appears in the upper right portion of the page. If the icon is green, Then the map is not currently in your cart. Click on the icon to add it. Once added, the icon will turn red. You can click this icon to remove the map from your cart. Once you are done collecting maps, you can click the My Cart link in the menu and you will be taken to the cart page where you can review your cart and make any last minute changes. Once you are happy with your maps, you have the choice to download your maps as SOE or SEQ maps, and either in Gzip (Linux) or Zip (Win32) format. The cart is cookie based, and temporary. Once you shut down your browser session, your cart contents go away. </font><BR><BR></td>
