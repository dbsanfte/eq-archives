  <tr>
    <td BGCOLOR="#D1F0D5" ALIGN="LEFT" WIDTH="75" VALIGN="TOP">
    <a class="pn-normal" href="modules.php?op=modload&amp;name=News&amp;file=index&amp;catid=&amp;topic=10"><img src="images/topics/government.jpg" border="0" Alt="Government" align="left" hspace="5" vspace="5" ></a></td><td BGCOLOR="#D1F0D5" ALIGN="LEFT" VALIGN="TOP">
    Yeah, I mean you. You are from the lower 90% of the economy. If your in the top 10% then your assistant is reading this, and they are part of the lower 90%. So lets get that fact out in the open.<br>
<br>
And your income and net worth is <a href="http://www.thenation.com/docprint.mhtml?i=20040105&amp;s=krugman">7% less than it might have been in 1973</a>. While the media and the government love to tell us that the economy is booming, they fail to point out that it isn't booming for you and me. It is booming for that top 10 percent who have increased their annual incomes by 148%, and REALLY booming for the top 1% or even .001% most of all who are making 599% more than they would have in 1973!<br>
<br>
How is this? Well consider the way the economy has shifted recently: everything has gone up but your wages. I am sure everyone got their average 2-5% increases that companies throw at you and everyone gladly took it. But consider what you are going to do with that 3% on average: your going to lose it in the almost 8% increase in the cost of living.<br>
<br>
Consider that the government has subsidized the use of personal vehicles (to help a huge and wasteful industry grow no less) which now cost 25% more to operate than it did a year ago (gas alone can account for that). And so the average Joe who has to drive to work loses literally dollars a day just getting to that job that doesn't even cover the cost of this change alone.<br>
<br>
But more amazing is the fact that people who are economically worse off under economic Conservatives (or Republicans - since both words are starting to have the same meaning) will continue to vote for people who are going to insure that the bottom 90% stay on the bottom. Why? Because supposedly conservatives represent the same morality as the majority of the country.<br>
<br>
The Democrats would do well to pay attention now. Because you see, even the Democratic (or Liberal) lawmakers are normally from the top 10% as well. And to be fair, they want to keep it that way too, but at least they make some efforts to let the other 90% lead decent lives. The more Consevative/Republican factions; they would rather you become their indentured servants. And you will.<br>
<br>
But the Democrats fail to win some big elections because they forget to go on about issues that people can actually have the warm-fuzzies on - morality, patriotism and banal assurances that the 90% are going to enjoy economic growth. It's easy to polarize people and get them riled up over the evil-doers. Very few people would even jokingly say &quot;I am evil.&quot; We all want to be the good guys. Most people can identify with a polititian who has a clear message spoken with small words. My little bit here is probably producing glazed looks because I am talking about income and economics, and ... YAWN.... But if I shift to something inane like &quot;freedom haters,&quot; eyes open up and we have a topic people can get passionate about! Yay &quot;freedom lovers!&quot; What the hell does that mean anyway?<br>
<br>
Back on the subject matter; People continue to follow the misguided notion that a growing economy means growth for them. This means they will support things like privatized retirement plans, removal of the estate taxes and tax shelters for the wealthy. And then we grumble and bear with tax cuts to public schools (the top 10% go to private schools, thank you), reduced scholarships, cuts in healthcare (which is unaffordable to anyone who isn't wealthy or currently indentured - I mean employed) as well as reduced economic incentives to small businesses, etc. <br>
<br>
Add to this the growing reliance upon credit, which has to be paid eventually; people are probably going to die with their school debts unpaid soon. But you need to keep the economy strong! You need to go buy stuff! Spend money! Why? who really benefits from larger retail activity? Why Walmart does! Yay for Walmart....huh? The largest purveyor of dead-end, low paying, menial treatment careers? They get so many tax breaks it is outragous. But they are an example of where the economy is growing the most.<br>
<br>
Only the largest corporations have the venture capitol to open a new business, and many of them do so by putting their headquarters off-shore. Anyone who starts a business prays some big huge conglomerate comes along and buys them out. That is hitting the motherload! Well only if your an owner, and that means your likely from an economically higher level family anyway. <br>
<br>
But thats ok, the rest of your employees will be swallowed up and left in their dead-end jobs until they retire and are forced to conserve whatever they have until cat food starts looking like a viable meal. Hmm...does this make them Conservatives?<br>
<br>
What does this all lead up to? Well it means you and me (and the other 90%) are going to support less than 10% with out meagre share of the pie. When more pie is made available (due to our efforts by the way), we will likely not get any more pie. We might even lose a bit here and there. But when is enough wealth enough? I just asked the wrong question really, the real question is why horde all the wealth into one place. Why not allow a more level society, with better public transport, less waste, more true and real incentives to achieve?<br>
<br>
Power. Pure and simple.<br>
<br>
If you spend every waking hour toiling under the yoke, then what else is there? You live to work, and work to live. You will die working. Because you believe that the economy is growing, and that you might one day get your big break, you will break yourself to succeed. <br>
<br>
What if you found out that for every dollar you earn, you are making a bunch of other people else $600? I don't mean one person getting $600 but a group of people getting it...All those managers, lawyers, middle managers, CEOs or just goverment cronies take $600 before they give you one. You would be pretty pissed off wouldn't you?<br>
<br>
Here's a dollar...<br>
