<center><h3>Universal Guild Charter</h3></center>
<p>
<ol>
<li><a href="#introduction">Introduction</a>
<li><a href="#structure">Universal Guild Structure</a>
<li><a href="#conduct">Conduct</a>
<li><a href="#roster">Roster and Recruitment</a>
<li><a href="#promotions">Promotions/Demotions</a>
<li><a href="#changes">Changes to the Charter</a>
</ol>
<p>
<a name="#introduction" href="#top"><b>Introduction:</b></a><br>
Darkenbane is a Massively Multiplayer Online Gaming Guild that specializes in the area of player vs player (specifically pk). Membership to the guild is free, but there are expectations of its members in both conduct and gameplay.<br>
<br>
The guild has been historically ruthless by nature to its foes and has few allies. Those desiring to understand a member of Darkenbane would truly have to assume the mind of a killer; one who only knows war and pain, no mercy, and only finds peace in death. Stories have been told about these killers who feed off fear and lurk in the shadows until the right time to lash out at their victims tearing them to shreds.<br>
<br>
The guild will play as an organized unit in online games that have a heavy involvement in PvP. For a game to be supported by Darkenbane, the game, its guild leaders for that game, and its “Code” for that game must be approved by the Council of Overlords.<br>
<br>
<a name="#structure" href="#top"><b>Universal Guild Structure</b></a><br>
The guild hierarchy is as follows:<br>
<br>
<b>Overlords:</b><br><br>
Because the guild will be involved in more than one game, some with a large number of people, some not, it’s important for a group of leaders to oversee that any games supported by Darkenbane are being conducted properly by that division’s leaders. These individuals will be considered Overlords and have the following responsibilities:
<ol>
<li> Approving a new game to be supported by Darkenbane. Approving the new game's initial leadership, officers and "Code" (a ruleset for the guild specific to that particular game). If this approval is reached, the game will be added to our forum list. The game will receive private forums and the guild in that game will be recognized as an authorized representative of Darkenbane. 
<li> Making additions or subtractions to the Universal Guild Charter, which is a ruleset adhered to by Darkenbane across all games.
<li> Removing Darkenbane Support from a game. This will take a unanimous vote.
<li> Replacing the leadership structure of a supported game. This will take a unanimous vote and can only occur if the leadership of said game is violating the Charter and/or Code.
</ol>
Overlord is a lifelong position; The requirements are as follows:
<ol>
<li> An Overlord cannot be inactive for more than a month. They MUST be active in the guild to maintain the position. If they become inactive as determined by the other Overlords, they can be voted out (with a unanimous vote of the remaining Overlords). 
<li> Any Overlord found to have violated the guild Charter or a Game's Code can be reviewed by the other Overlords for potential removal. This, again, will need a unanimous vote. 
<li> Candidates for the Overlord position must be active in the guild for a period of at least one year before they can attain the position.
<li> Overlords will be selected via a unanimous vote of the standing Overlords. Five Overlords MUST always be sitting and active in the guild at all times. If one Overlord is removed and/or becomes inactive, then another MUST be chosen. If the remaining Overlords cannot decide upon (unanimously) a new person to fill the position, a Guild Vote will occur in whichever private board is the most active. The two people who receive the most votes will have a "run off" election and the winner will be named as an Overlord.
<li> One Overlord position will always be the website administrator (currently, Meter).
</ol>
<b>Core:</b><br>
<br>
These members are the elite and have the privilege to bear the mark of true loyalty. The Core have the same privileges as members, but they also vote on many issues that drive the guild as a whole. Each and every one of them is expected to take some type of leadership responsibility. This is the core and voice of the guild. If none of the OL are present in a particular game, division leaders will arise out of this section.<br>
<br>
<b>Member:</b><br>
<br>
These are people that have been with the guild for maybe one or two games and have yet to establish the rank of Core. Normally a recruit will be bumped to member from one game to another and then eventually from member to core. There are some exceptions but only time will tell for the most part.<br>
<br>
<b>Recruits:</b><br>
<br>
These are Darkenbane fodder. They will have to prove themselves to attain membership. Good luck! 
<br>
<br>
<a name="#conduct" href="#top"><b>Conduct</b></a><br>
All Darkenbane members across all games will be required to follow our conduct requirements which involves the following:
<ol>
<li> Understanding anything you say or do in any game, chat room, and forums can be used against you.
<li> Anything said by a member that may demoralize the guild may result in the member being disciplined. This includes, but is not limited to, the following:
<ol type="a">
<li> Begging.<br>
<li> Grovelling.<br>
<li> Asking for mercy, requesting a truce with the enemy and/or making any deals of non-aggression.<br>
<li> Frequent acts of ignorance and among other things, stupidity.<br>
<li> Hacking or cheating in a supported game to gain an unfair advantage.<br>
<li> Generally acting stupid over time.<br>
<li> Hacking a supported game which actually alters the gameplay of said game.<br>
<li> Disloyalty - Threatening to quit the guild or quitting the guild. Attempting to join other guilds playing a supported game . Re-admission will be decided at the discretion of the Overlords and a warning will be given if re-admitted. If it happens again, they will be permanantly banned from Darkenbane.<br>
<li> Treason - Participating in any act that may counter the interests of the guild. This includes, but is not limited to: posting private board information, giving out account information to non-Darkenbane members, joining enemy guilds, attacking guild members in an aggressive manner or giving any information that is deemed private by the Overlords to non-guildmates.<br>
<li> Racism - Racist remarks often cause drama and annoy some people, even when used jokingly. Refrain from utilizing racist remarks or racism in any form of communication involving the guild. Basically, use your head and think before you spout off at the mouth. The level of discipline for a violation of this will result in warnings and possible removal from the guild.<br>
<li> Disruption - Any overall disruption of the guild will not be tolerated. This includes: abuse of other members, public flaming of the guild or its members, constant whining or anything else viewed as disruptive.<br>
</ol>
</ol>
<p>
Each division’s "Code" will address conduct more specifically. The Overlords may take action if the division does not handle complaints against a member within a reasonable period of time. Decisions of the Overlords always override the decisions of Division Leaders.
<br>
<br>
<a name="#roster" href="#top"><b>Roster and Recruitment</b></a><br>
There will be no universal recruitment requirements across all games for Darkenbane. Instead, this will be specific to a particular game and must be addressed in that division's "Code". However, because Darkenbane is a premium PvP guild, we will be invite only.<br>
<br>
<br>
<a name="#promotions" href="#top"><b>Promotions/Demotions</b></a><br>
Core to Overlord: Done as needed and takes an unanimous Overlord vote.<br>
<br>
Member to Core: Has shown leadership qualities, loyalty and is deemed worthy by his higher rank peers (Core/Overlord ). There will be a majority vote on the Core board.<br>
<br>
Recruit to Member: Has been in at least one game with Darkenbane and is an active player. The vote will take place on the Core or private board at the Overlord's discretion.<br>
<br>
Any member or below is subject to removal on grounds of breaking the charter or any other reason via a majority Core vote.<br>
<br>
All promotions will be done with a 66% majority vote and all removals/demotions will be done by a true majority vote (50.1%). All threads and all votes in any matter will stay up for a minimum of one week.<br>
<br>
Lastly, do not apply or ask for promotions. You will need to be nominated by a Core member or above for a promotion and most will come in time. The amount of time varies depending on loyalty and other amiable qualities. This is a subjective policy and it will remain that way - so aim to impress.<br> 
<br>
<br>
<a name="#changes" href="#top"><b>Changes to the Charter</b></a><br>
This can only occur after all 5 sitting Overlords unanimously agree on a change. Any guild driven decision that requires a vote will have the appropriate weights:<br>
<br>
Overlord - 3<br>
Core - 2<br>
Member - 1<br>
