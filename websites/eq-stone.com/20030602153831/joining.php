       <blockquote>



<p>&nbsp;</p>
<span class='content-title'>Get to Know Stone</span><br>
We are not in such a hurry to grow that we will sacrifice the bond of friendship between our members. 
Therefore, if you are <b>interested</b> in joining Stone, we ask that you:
<ol>
 <li>meet, or be willing to meet, our <a href='memberreqs.php'>Membership Requirements</a>.

 <p>
 <li>read the following to learn about our policies and playstyle: 
     <br><span style='font-size: 10px;'>(If you have any questions after reading these, send a tell to an officer for clarification or ask on the boards.)</span>
     <br><b>&#183;</b> <a href='membershiprules.php'>Membership Rules</a>
     <br><b>&#183;</b> <a href='lootpolicy.php'>Loot Policies</a>
     <br><b>&#183;</b> <a href='guildstructure.php'>Guild Structure</a>

 <p>
 <li>post a hello/introduction message on our <a href='http://eq-stone.com/forums/'>message boards</a>. 
     Tell us about yourself and let us know that you would like to hang out with us.

 <p>
 <li>Join our open guild channel, stone:raid , to help find members to group with.

 <p>
 <li>continue to keep a presence on the message boards by getting involved in our discussions.
</ol>



<p>&nbsp;</p>
<span class='content-title'>How to Apply</span><br>
After having done the above, and you have <b>decided</b> that you wish to join Stone, send a tell to a member 
of the <a href='guildstructure.php'>Membership Council</a> to make sure we have room for your class in the guild.
If you are invited to submit an application, do so in the /Guildinvite forum on our message boards.
Make sure to include the following information:<br>
<ol>
 <li>Name, Level, Class, Race <span style='font-size: 10px;'>(for the roster if you are accepted for trial period.)</span>
 <li>Who gave you the go ahead on posting your application?
 <li>Who do you know in Stone? Who referred you? 
 <li>Former guild affiliations <span style='font-size: 10px;'>(If you are fresh from another guild, we'd really like to know your motives. We don't wish to become known as a guild that is recruiting from other guilds. We are not asking for you to be disrespectful to any other guilds. But we'd like to know why you would want to drop someone else for us.)</span>
 <li>What is your interest in Stone? What will you be expecting from this guild?
 <li>Do you meet our membership requirements already? <br><span style='font-size: 10px;'>(i.e. Mobility, Resist Gear, PoP flags, Insta-clicks, etc.)</span>
 <li>Achievements <br><span style='font-size: 10px;'>(e.g. Epic equipped, raid experience, VP key, ST key, tradeskill grandmater, etc.)</span>
 <li>Anything else you'd like us to know?
 <li>Acknowledgement that you have read our rules and reqs. If you do not state you have read the <a href='membershiprules.php'>Membership Rules</a> and <a href='memberreqs.php'>Membership Requirements</a> pages, it will be assumed you have not done so, and will be informed to read them before your application is considered.
</ol>
 <p>
 Lastly, don't be applying with other guilds at the same time. If we find out you have an application to another guild at the same time as Stone, your application will be denied.



<p>&nbsp;</p>
<span class='content-title'>Initiation Period</span><br>
If you are accepted for a trial period, then we'll invite you to wear our Tag and join in our /GUILDCHAT so that we may get 
to know you even better. Read about the <a href='initiateprogram.php'>Initiate Program</a> for more details. 
If you make it through the trial period and pass the vote, then you will be accepted as a full member of Stone.









