       <span class='content-header'>General Membership Rules</span>
       </center>


       <blockquote>


<span class='content-title'>Behavior</span><br>
 <b>&#183;</b> First and foremost, at all times observe the <a href="http://everquest.station.sony.com/support/policies/conduct.jsp" target="Everquest">Rules of Conduct</a> set forth by Verant.<br>
 <b>&#183;</b> Always keep in mind that your actions reflect upon your guild brothers and sisters. Act in a mature and respectful manner toward the other players of the server. It may be unfair that others sharing your guild tag are judged by your actions, but the truth is we are.<br>
 <b>&#183;</b> Do not curse in open channels (i.e. /SHOUT, /OOC, /SAY). Note this does not apply in /GUILDCHAT.<br>
 <b>&#183;</b> Never truly disrespect your guildmates. We joke around with each other quite a bit, but it is always in jest. If you aren't sure how far you can joke around with someone, don't do it.<br>
 <b>&#183;</b> Respect others' playtime. If you are stupid enough to get your ass stuck in some predicament that you can't get yourself out of, don't <b>expect</b> your guildmates to drop everything they are doing right at that moment to come get you. Of course we will help, but not necessarily on your time frame. If your character is in immediate danger and no one can come to your rescue immediately, log on (create if need be) an alternate character and keep in contact with the guild to see when a rescue is possible. Also, don't <b>expect</b> that a porter (i.e. druid or wizard) has nothing better to do than port you around the world. We are friendly in this guild, and we do help eachother. And it is certainly fine to ask for help. But if no one can help you and you display an attitude about it, expect a quick /tell from an officer discussing your attitude.<br>

<p>
<span class='content-title'>No /ANONYMOUS</span><br>
 We are proud of our name and so should be our members. We do not want to see our members hiding our guild name. So, if you want to hide your level, class, and race, we ask that our members use /ROLEPLAY. There are very few exceptions to this and none of them include the primary characters of our members. An example of an exception would be a banker character that a member desires to have tagged purely for communication purposes while this character does not meet our level requirement.

<p>
<span class='content-title'>Forum Participation</span><br>
 We communicate a lot through our online forums. We schedule and organize raids there, and we use them to disseminate information about the zones/mobs we will be raiding. We feel it unfair that we should take time out of the game to go over details that have already been discussed on the forums, so members and initiates are required to visit the boards often enough to know details of what we are doing.

<p>
<span class='content-title'>Character Profile</span><br>
 Members should establish a profile on <a href="http://www.magelo.com" target="Magelo">Magelo</a> and <b>keep it current</b>. This will help our Loot Council in determining loot distribution and our War Council in determining possible raids. Make a profile with your normal gear, and your resist gear. After establishing your character profiles, update your character info for the roster. 

<p>
<span class='content-title'>Extended Time Off</span><br>
 We ask that you post in the forums if you are going on vacation for any extended amount of time. Besides caring about our members, we also need to know who would normally be available for planned raids.

<p>
<span class='content-title'>Leaving the Guild</span><br>
 Sometimes a player determines that his goals in this game do not meet the goals of the guild. We have no problem when this occurs. As long as separation is done in a professional manner, we will maintain good relations with our former members. We ask that members simply provide us with a going-away message in our forums so that we in turn can say our goodbyes. If, however, the guild tag is dropped in anger without first discussing it with a guild leader, do not expect to ever get it back.



