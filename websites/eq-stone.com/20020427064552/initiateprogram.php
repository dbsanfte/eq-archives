       <span class='content-header'>Initiate Program</span>
       </center>


       <blockquote>


<span class='content-title'>When to Apply</span><br>
 First, make sure you meet or are willing to meet the regular membership rules and requirements.<br>
 Next, speak with a Membership Council officer to make sure we are currently accepting your character class. Many guilds do not like to see that applicants have applied with several guilds in a short period of time, and since Stone follows a class balance system, it is recommended that you make sure we have room for you first.

<p>
<span class='content-title'>Sponsorship</span><br>
 You will be assigned a sponsor for the duration of your Evaluation Period. Your sponsor is there to answer your questions about the guild. Additionally, your sponsor will be in charge of making sure you are striving to fulfill the membership requirements prior to the expiration of your Evaluation Period. And at the end of your Evaluation Period, what your sponsor has to say about you will be a large factor in determining whether or not you are accepted for full membership.
<p>
 Sponsors are a voluntary position. Guild officers will never <b>require</b> a member to act as a sponsor, but if there is no one willing to sponsor you, you might be "put on hold" until a member is willing. Also, sponsors must be approved by guild officers as only certain members will be allowed to act as a sponsor.

<p>
<span class='content-title'>Evaluation Period</span><br>
 Initiates will serve out an evaluation period of atleast 30 days (unless the conduct of an Initiate is found to be unnaceptable before the 30 days), but not to exceed 60 days. You will wear the Stone guild tag during this time. Remember, /ANON is not allowed, but /ROLEPLAY is acceptable. During the evaluation period the Initiate should strive to group with Stone members as much as possible. This is our time to get to know you. The quicker we get to know you, the shorter your evaluation period can be. If you have not made sufficient attempts to get to know us after 60 days, you will most likely be denied full membership. During this time, officers will be looking for desireable traits from you. This includes knowledge of your class, gameplay, and the world of Norrath; and personality traits.
<p>
 Also, during this time, you should work to complete your resist gear requirements or any other membership requirement that is lacking. If you need help with this, get with your sponsor and attempt to gain his/her help or advice.

<p>
<span class='content-title'>Loot</span><br>
 Initiates are not <b>entitled</b> to loot privileges during their evaluation period. However, if an item will rot for not being looted or it is a general consensus among guild members and officers that the item should go to that individual, then an exception may occur.

<p>
<span class='content-title'>Epic Assistance</span><br>
 Initiates are not entitled to guild assistance on epic mobs during their evaluation period. This does not mean that it will not happen. Just don't expect it. 

<p>
<span class='content-title'>Forum Posting</span><br>
 During your Evaluation Period, you will be restricted to posting in the non-member only forums. 

<p>
<span class='content-title'>Exceptions</span><br>
 Exceptions will be determined on a case by case basis. 

<p>
<span class='content-title'>How to Join</span><br>
 If you want to be considered for membership into Stone, post in the "I Wish to Join Stone" forum with the following information:<br>
<ol>
 <li>Name, Level, Class, Race (just so we know what to put on the roster if you are accepted for initiation.)
 <li>Who do you know in Stone? Who referred you? 
 <li>Former guild affiliations (If you are fresh from another guild, we'd really like to know your motives. We don't wish to become known as a guild that is recruiting from other guilds. We are not asking for you to be disrespectful to any other guilds. But we'd like to know why you would want to drop someone else for us.)
 <li>Why are you wanting to join Stone? What will you be expecting from this guild?
 <li>Do you meet our membership requirements already? (i.e. Mobility, Resist Gear, Faction)
 <li>Achievements (e.g. Epic equipped, higher-end game knowledge, dragon raids, etc.)
 <li>Anything else you'd like us to know?
 <li>Acknowledgement that you have read this entire page. If you do not state you have read this Membership Rules and Requirements page, it will be assumed you have not done so, and will be informed to read this page before your application is considered.
</ol>
 <p>
 Lastly, don't be applying with other guilds at the same time. If we find out you have an application to another guild at the same time as Stone, your application will be denied.

<p>
After posting your desire to be a part of Stone, it is your responsibility to get to know us. We have our events listed on the calendar and or the raid forums. We'll want you to hang around us for a few raids so we can get to know your attitude. If we like you enough, then we'll invite you to wear our Tag and join in our /GUILDCHAT, then start the trial/probationary period. 







