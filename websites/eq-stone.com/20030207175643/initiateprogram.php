       <span class='content-header'>Initiate Program</span>
       </center>


       <blockquote>




<p>&nbsp;</p>
<span class='content-title'>Definition</span><br>
 An Initiate is a player that is wearing our guild tag for the purpose of closer examination by the guild members of Stone. We feel allowing 
 a player into our guildchat is a better way to get to know this person, and for this person to get to know us. It is important to know 
 that an Initiate is not a "member" of Stone and does not have the same privileges of guild members.


<p>&nbsp;</p>
<span class='content-title'>Evaluation Period</span><br>
 Initiates will serve out an evaluation period of atleast 30 days (unless the conduct of an Initiate is found to be unnaceptable before the 30 days), but not to exceed 60 days. You will wear the Stone guild tag during this time. Remember, /ANON is not allowed, but /ROLEPLAY is acceptable. During the evaluation period the Initiate should strive to group with Stone members as much as possible. This is our time to get to know you. The quicker we get to know you, the shorter your evaluation period can be. If you have not made sufficient attempts to get to know us after 60 days, you will most likely be denied membership. During this time, officers and members will be looking for desireable traits from you. This includes knowledge of your class, gameplay, and the world of Norrath; and personality traits.
 <p>Also, during this time, you should work to complete your resist gear requirements or any other membership requirement that is lacking.



<p>&nbsp;</p>
<span class='content-title'>Loot</span><br>
 Initiates are not full guild members, rather fall under the same category as non-members.
 <p>Therefor, in guild raid situations, do not expect that you will get loot just because no one else attending needs it. It might be held for a full member that has put much time into the guild, but was not able to attend that particular raid. If you get something at a raid, feel lucky.
 <p>In group situations, it is up to the participants of the group to decide what happens with loot before it drops (i.e. /random, NBG, save for a needy guildie, etc.) If your not sure how loot will be handled in a group you join, its acceptable to ask. Be aware that our members often form groups with the intent to "farm" certain items for guild members that aren't present, not to make anyone rich, rather to help equip our guild mates with better gear, thus making the guild stronger. Our focus is increasing the overall success of the guild, not the individual.



<p>&nbsp;</p>
<span class='content-title'>Epic Assistance</span><br>
 Initiates are not full guild members, rather fall under the same category as non-members. 
 As such, Initiates are not entitled to assistance with epic mobs. This does not mean that it may not happen. Just don't expect it.
 Stone has no intentions of outfitting every player on Zebuxoruk with epics, so members might want to see more proof from you that you are here for the guild, and not just until you get your epic.



<p>&nbsp;</p>
<span class='content-title'>Forum Posting</span><br>
 During your Evaluation Period, you will be restricted to posting in the non-member only forums. But you are encouraged to keep your board presence 
 active. This is another way we get to know you. Also, we use the boards as a means of communication for setting up raids/events.



<p>&nbsp;</p>
<span class='content-title'>The Vote</span><br>
 No sooner than 30 days into your trial period, you may become eligible to be voted on for full membership into the guild. 
 The voting process is multi-step. First, each member votes on a point scale of how they feel about the Initiate. 
 The results from the general vote are considered by each officer along with his own feelings about the Initiate and the officer 
 vote is held. (This process is analogous to the general and Electoral voting process of the USA.)


<p>&nbsp;</p>
<span class='content-title'>Exceptions</span><br>
 Exceptions may be allowed on a case by case basis. 








