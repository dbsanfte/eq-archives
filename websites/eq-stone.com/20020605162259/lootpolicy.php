       <span class='content-header'>Loot Policy</span>
       </center>


       <blockquote>


<span class='content-title'>Loot Council</span><br>
 A council has been established by the guild leaders to manage the distribution of loot obtained during guild raids/events in a manner that is in the best interest of the guild as a whole.

<p>
<span class='content-title'>Disbursement</span><br>
 Many factors are considered by the Loot Council for distribution. Participation in guild events is certainly one of them. The more active you are with the guild, the more reason we have of giving you loot. Issuing loot to those that never attend guild events is not in the best interest of the guild. Another consideration is "who has what already". The leadership of Stone trusts that the members of Loot Council are doing their best to be fair in distribution. It is quite a hard job, and if you honestly think you have been passed over too many times, discuss it with an officer of Loot Council, but <b>NOT</b> right then. Take time after a raid/event is over to express your concern and desire for such and such piece of armor/weapon/quest piece. Be assured the Loot Council has only the best interest of the guild at heart, and their job gets very hectic. They will try to make everyone happy in the long haul.

<p>
<span class='content-title'>Epic Weapon Mobs</span><br>
 Certain parts of Epic Weapon quests require long camps. Stone will do what we can to kill the mob, but members are required to camp their own mobs. So if you need a rare mob, either camp it yourself, or work out a buddy system deal with a friend.

<p>
<span class='content-title'>Ownership</span><br>
 Loot, once given, will be owned by the person receiving it. People need the certainty of ownership of their equipment. Keep in mind tho, that if you are found to be selling loot given out on guild raids/events rather than using it, logically Loot Council will think the loot given to you could have benefitted the guild better by rewarding it to someone else, and will take this into consideration in the future. Also, we do encourage trading down gear you receive from the guild when the guild is rewarding you with an upgrade to that gear. No, it is not required, but certainly the Loot Council will keep this in mind when determining what best suits the guild. 

<p>
<span class='content-title'>Retaining Items for the Guild</span><br>
 Some loot will be retained by the guild for sale to raise funds for raids and operating expenses (i.e. coffins, hate/sky stones, peridots, etc.). This includes all coin, gems, and even occasionally some weapons/armor that are acquired on official guild events/raids. Gems will be resold to guild members first and foremost. If you need it for some tradeskill, quest, etc., speak up and the gem will be offered to you at lower than market value. Armor and weapons the guild chooses for resale will be posted on the guild forums and guild members can purchase it at lower than market value.


