<br />
To view your attendace rate simply click on your name.<br />
<br />
===================================<br />
<br />
From now on officers will assign value to the items and Tier 1 members will have priority if they bid on item at that price. If more than one Tier 1 member wants item auction will be held from that value up.<br />
<br />
If Tier 1 members do not chose to bid Tier 2 will have chance on item at announced price. If more than one Tier 2 member bids auction will be held from that price up.<br />
<br />
If tier 1 and 2 members do not bid at the item - item will be open to all bidders. Alts of tier 1 and 2 members will be included in this auction. If there will be no bids on the item item will be either destroyed or will go to the guild bank.<br />
<br />
