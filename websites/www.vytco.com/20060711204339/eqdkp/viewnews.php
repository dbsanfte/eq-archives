      It is not a secret that we had many experienced and seasoned veterans leave the guild in the past few weeks, Some have retired, some got burnt out with the weight of leadership, some simply wanted to progress faster...<br />
<br />
Of course, there were those who stopped by for a few to get their flags and move on, but this is not about them. <br />
<br />
To all who were in LoP with me for a long time and went through good and bad times - I have nothing but gratitude and appreciation for your efforts, your support, your friendship. I hope you all will find that joy of game you are searching for.<br />
<br />
However, this time I want to talk about those who stayed and decided to re-build this guild and carry the LoP tag over their heads. This is what makes Lords what this guild always were, is and will be. Lords are not about bigger sticks or more keys and flags, LoP is not about leeter lewt or individual progress... LoP always was a community, place where we meet, forge our friendships, share our feelings and emotions. LoP was always about "us" not "me"...  Lords always wwere about giving average player chance to do things that they never dreamed possible... Those of you who carry this spirit and continue walking this path - You are the reason why wearing LoP tag is a huge honor!<br />
<br />
Yesterday we had to step back and start doing Time again. We have many new guys and gals who still need this key... But we also had a huge support from all of our friends, who are willing to sacrifice their time to help LoP carry on. We will finish the re-flag process and will re-key new members. Experience and gear will come and will rot again... Important is that LoP continued it's traditions and carried it's spirit even through the rough times like we just experienced.<br />
<br />
New players will get experience and knowledge, they will aquire gear and skills, level up and earn AAs... Important is that they get to see what LoP is all about and learn our traditions... Soon enough new leaders will become experienced veterans, gain the respect and authority... New generation of LoP will proudly say - "We achieved!"...<br />
<br />
This is what makes me so proud and thankful for oportunity to say "I am member of Lords".<br />
<br />
Congrats to all of you who saw Quarm die for the first time last night!
