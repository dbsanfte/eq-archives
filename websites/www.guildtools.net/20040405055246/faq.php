	Why did you start a site like this?<br>
	Well, to be quite honest, when I started this site, there weren't many (if any) like it. Soon after I got the site finished for the first time GuildMagic popped up and did what I was doing better. Since I didn't have the ability to use any scripting language except CGI, the proverbial bucket had pretty much been kicked. I stopped working on the site for a while after that because it wasn't really going anywhere, and the first time I got the script done not very many people seemed to like it. 
	<br><br>
	I decided to stick with it and at least learn what I could form the tools I had available to me. Which at the time I started this site was only Perl. As of March, 2003 the site supports a few more tools (PHP, MySQL), which I am striving to pick up in my spare time. 
	<br><br>
	How can you afford to do this for free?<br>
	It's actually not too expensive. I think the hosting is about $20 a month and that includes my MySQL and PHP stuffs. I'm not too sure why these things make my hosting cost $10 more a month, considering they are free... The only expensive part is the hours and hours I spend working on this site when I could be working and making real money (don't worry... that'll never happen). 
	<br><br>
	Do you need help with the site?<br>
	No, not really. I will always welcome praises and criticism though. If you have any ideas for the site I would be happy to hear them. 
	<br><br>
	Why is your site so barren?<br>
	If there are no graphic images on the site, it is because I believe that a site such as this shouldn't be about how 'catchy' it is, but about how quick and easy it is for you to find what you are looking for. I have the ability to make this site entirely in Flash if I felt the need to. Obviously, I do not feel that urge. One other reason is because of bandwidth costs. Sites like EQMaps.com and allakhazams.com have recently switched to pay sites because they cannot afford to keep the servers running. Of course their database hosting costs them a good deal of money, but they also have not taken the steps to reduce the bandwidth available to the public. For instance, how many useless images are loaded on every Allakhazam page? Every time your browser reloads these images, that is money out of their pocket (YOUR pocket now).
	<br>
	If there are graphic images on this site, then they are done by me (from edited SS's or from scratch). 
	<br><br>


	<br>What is RDB?<br>
	RDB [Roster Database] is a cgi script intended to make the managing of a guild roster much easier.
	Being the Leader of any guild can be stressful, whether it's a group of friends or a larger guild taking on the end-game.
	I have done my best to modify this script so that you have one less thing to worry about. With this script your guild
	members have the power to add/modify/delete their character profiles at any moment in real time (assuming the server
	isn't being flaky). You, as the admin, also have the ability to assign your officers admin status of the roster as well. 
	That way if something doesn't look right, or notes need to be added to a characters profile, you don't have to be the only
	one 'stuck' with the job of fixing it. Say goodbye to manually updating an HTML table week after week. Say hello to RDB!
	<br><br>
	All versions of the RDB are a modified form of DBMan that <a href="http://www.gossamer-threads.com" class="link" target="_blank">Gossamer-Threads</a>
	has so kindly allowed free use/modification of for non-profit organizations.
	The Guildtools Network is definitely a non-profit organization. In fact, you could call us an in-debt oganization.<br>
	<br><br>
	Would love to see a version of this for Dark Age of Camelot<br>
	This is now possible with RDB 6.0, I just need to make the templates for people to go off of.
