<P CLASS="P1">
	Mönche ("Monks") sind eine der Klassen in EverQuest, die sich nur zu gut als pure 
	Killermaschinen mißbrauchen lassen, zum "PowerGaming" eignen sie sich recht gut. 
	Allerdings entgeht einem damit eine der -meiner Ansicht nach- besten Möglichkeiten 
	zum Rollenspiel.
</P>
	
<P CLASS="P1">
	Sicher kann man Mönche als "prügelnde Mönche" spielen, aber es gibt da einige signifikante
	Unterschiede, die den Mönch von allen anderen Klassen unterscheiden:
</P>

<UL CLASS="P1">
	<LI>
		Im Gegensatz zum klassischen "Fantasy-Priester" leben die Mönche abgesondert von der
		restlichen Welt; die Spieler-Charaktere unter den Mönche fallen also schonmal aus dem
		Rahmen und sind eigentlich schon Außenseiter: denn Mönchsleben zeichnet sich durch
		Klosterleben oder andere Distanzierung vom täglichen Tun der Welt aus. Der Mönch wendet
		sich vom weltlichen Tun ab, und auch vom weltlichen Besitz.<BR>
		Die erste Frage stellt sich schonmal: Warum ist mein Mönch nicht im Kloster, sondern
		treibt sich mit zweifelhaften Schwertschwingern in der Ro-Wüste herum?<BR>
		<BR>
	</LI>
	<LI>
		Für den Mönch ist das durchschnittliche Leben einer menschlichen Stadt nicht weniger
		fremd als es für einen Waldelf wirken muß; er<FONT COLOR="#FF0000">*</FONT> hat seine
		Kindheit behütet hinter Klostermauern verbracht, in denen es völlig andere Regeln und
		Gesetze gab - alles war dort geregelt.<BR>
		Alles ist neu für ihn - er hat vielleicht auch nicht die Vorurteile, die andere Leute
		schon früh lernen - er wird einem Troll ziemlich unvoreingenommen gegenübetreten,
		genauso einem Halbling. Jedenfalls bei der ersten Begegnung. Er muß die Überlebensregeln
		erst noch auf die harte Tour lernen ;)<BR><BR>
	</LI>
	<LI>
		Ein Mönch gehört oft einer Religion an - das ist aber nicht unbedingt zwangsweise
		so (Den Aspekt Religion möchte ich hier nicht näher besprechen, ich verweise auf die
		Texte zum Kleriker und zum Paladin). Was immer der Fall sein wird, ist der feste
		Glaube an eine bestimmte Philosophie und/oder Moral, z.B. das Ideal der Balance. Man
		kann ihn durchaus mit dem Paladin vergleichen, aber er wird kein Anführertyp sein, 
		und nie im Mittelpunkt stehen wollen. Vielleicht ist "Konzentration auf das Wesentliche"
		eine treffende Beschreibung.<BR><BR>
	</LI>
	<LI>
		Ein Mönch wird immer Distanz zu anderen Personen halten, und es ist schwer, seine
		Mauer aus Höflichkeit zu durchbrechen und eine echte Freundschaft aufzubauen. Der Mönch
		wird selten aktiv die Nähe zu anderen suchen, sondern sich eher in sich selbst
		versenken... ihn über eigene Problem reden zu hören, wird nicht so schnell vorkommen.<BR><BR>
	</LI>
	<LI>
		Der Mönch ist sich vielleicht durchaus darüber im klaren, daß ihn die Leute für einen
		naiven Spinner halten, zu ihn heraufsehen (was ihm mit Sicherheit peinlich ist - er
		ist kein "Hirte" wie ein Priester), oder in ihn für ein wehrloses Opfer sehen - er
		ist anders, und er weiß es.<BR>
		Aber sein Idealismus macht ihn erst zu dem, was er ist. Ein Mönch geht keine
		Kompromisse ein, wenn seine moralischen Grundsätze in Frage gestellt werden.
</UL>

<P CLASS="P1">
	Sehen wir uns mal zwei völlig unterschiedliche Mönche aus den Medien an, die sicher den
	meisten der geneigten Lesern ein Begriff sind: Kwai Chang Caine aus "Kung Fu" und Bruder
	Tuck, dem Begleiter von Robin Hood.
</P>
	
<P CLASS="P1">
	Sicher wissen wir es von Caine, bei Tuck können wir es vermuten: Beide sind in einem
	Kloster aufgewachsen. Beide haben das Kloster verlassen, ab dann verläuft alles völlig
	anders. Caine ist ein Wanderer, Tuck betrieb bis zu seiner Rekrutierung durch Robin
	eine einsame Kapelle. Caine versteht die Menschen nicht vollständig, und kommt oftmals
	durch seine scheinbare Naivität in Probleme; er lebt in Askese und völliger Armut. Die
	Distanz zu seinen Mitmenschen ist allgegenwärtig, und er wird erst teilweise
	akzeptiert, wenn er jemandem geholfen hat. Verstanden wird er eigentlich nie. Tuck
	dagegen ist nichts menschliches fremd; er liebt Speis und Trank, was sich auch in
	beträchtlichem Körperumfang äußert. Aber auch er lebt ohne großartige weltliche
	Besitztümer. Wenn Caine kämpfen muß, dann nur als letztes Mittel zur Verteidigung
	seines oder anderer Leben. Tuck hingegen nimmt an den Angriffen auf die Schergen des
	Sheriffs teil, und das mit großem Enthusiasmus. Caine ist zwar im Kloster an Waffen
	ausgebildet worden, benutzt aber eigentlich nie welche; der Gegner wird geschont. Tuck
	hat das Kämpfen mit Sicherheit nicht im Kloster gelernt, und er benutzt oft 
	einen Stab.
</P>
	
<P CLASS="P1">
	Allein diese kurze Gegenüberstellung sollte deutlich machen, wie unterschiedlich
	Mönche verschiedener Kulturen, Glaubensrichtungen und Zeiten sein können. Das
	sollte reichen, um ein paar kurze Charakterskizzen zu entwerfen:
</P>
	
<P CLASS="P2">Der Desillusionierte<BR><BR></P>
<P CLASS="P1">
	Er hat im Kloster strenge Regeln und rigide Moral gelebt, und jetzt ist er allein in
	der großen weiten Welt. Plötzlich erkennt er, daß er nicht für dieses Leben
	vorbereitet ist. Keiner will ihm zuhören, und langsam glaubt er selbst immer weniger
	an seine Lehren... das hält ihn aber nicht davon ab, für sie zu kämpfen. Wenn 
	er nur nicht so einsam wäre...<BR><BR>
</P>

<P CLASS="P2">Der fröhliche Trinker<BR><BR></P>
<P CLASS="P1">
	Er hat das Leben im Kloster ziemlich genossen... er erinnert sich noch, wie er als
	kleines Kind gehungert hat. Das Leben im Kloster war hart, aber er ist immer noch
	dankbar, essen und trinken zu haben. Ja, er glaubt schon an diese ganzen Geschichten,
	die sie ihm beigebracht haben... aber kann das bißchen Wein wirklich so schlimm sein?
	Nein, sicher nicht. Und seitdem er die schützenden Klostermauern verlassen hat, lernt er 
	soviel neues kennen! Das Leben ist so gut zu ihm, und er ist gut zu den Leuten, die
	es brauchen. Wenn es nötig ist, wird er kämpfen.<BR><BR>
</P>

<P CLASS="P2">Der Verbitterte<BR><BR></P>
<P CLASS="P1">
	Seitdem Orcs das Kloster dem Erdboden gleichgemacht haben, lebt er nur noch für die
	Rache an ihnen. Dazu sind seine Kampffähigkeiten nützlich... aber oft genug denkt
	er, daß sein Handeln falsch ist, daß sein Abt nur Mitleid für ihm empfinden würde,
	aber keinen Stolz. Aber der Abt ist tot, und seine Brüder wohl auch. Was bleibt ihm
	noch? Der Kampf und die Kameradschaft gleichgesinnter Gefährten.<BR><BR>
</P>

<P CLASS="P2">Der "Student des Lebens"<BR><BR></P>
<P CLASS="P1">
	Er hat das Kloster verlassen, weil er die Menschen kennenlernen möchte. Warum sind
	sie so? Und warum tun sie, was sie tun?  Warum sind sie so grausam zueinander, und
	warum können sie auch so gut sein? Er hält sich aus allem heraus, solange er kann und
	beobachtet das Treiben in Kneipen, auf Märkten, Festen... Er liebt die Menschen, und
	sie erschrecken ihn gleichzeitig.
</P>

<P CLASS="P0">
	und so weiter, und so weiter...
</P>

