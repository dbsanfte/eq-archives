<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Clawdeen<BR>
	<BR>
</P>

<P CLASS="P1">
	Clawdeen liess die Beine von der Plattform baumeln und blickte gedankenverloren
	in den dunklen Wald von Greater Faydark.
	<I>&quot;Milady ? Ähm.....Milady ?&quot;</I>
	Verärgert wandte sie sich der Stimme zu und blickte in das Gesicht eines Gnomes.
	Aufgrund dessen Grösse blickten sich die beiden beinahe in die Augen.
	<I>&quot;Darf ich um den Geist des Wolfes bitten, Tochter Tunares ?&quot;</I>
	Clawdeen murmelte unverständliche Worte und drehte sich wieder um, ohne auf seine
	Frage weiter einzugehen.
	Der Gnom verstand wohl diese ablehnende Geste und ging davon.
</P>

<P CLASS="P1">
	Sie verlor sich wieder in ihren Gedanken. 
	Sie dachte an ihre Hochzeit in den Butcherblock Mountains, die erst wenige Tage
	zurücklag ... und ihre Augen füllten sich mit Tränen.
	Die Zeremonie war wunderschön gewesen, doch traurig war sie, dass sich Escados
	und ihre Wege - wie so oft - gleich wieder trennten. Seine Freunde warteten auf
	Kunark, ebenso seine Feinde, die er zu bezwingen suchte. Dort, in diesen fernen
	Ländern, wo die Echsenartigen lebten und den anderen Völkern von Norrath nicht
	gerade freundlich gesonnen waren.
	Sie wäre so gern noch länger mit ihm in Faydwer geblieben, hätte so gern mit
	ihm Stunden und Tage verträumt ... im Sleepy Willow Inn, auf einer kleinen
	romantischen Lichtung ... wo auch immer. Ihn nur bei sich wissen, zusammen sein ...<br>
	Sie hatte sich immer bemüht, ihn gewähren zu lassen, was immer er tat. Ein Ranger
	war nun einmal, was er war ... ein stetiger Läufer, ein Suchender, eine rastlose
	Seele. Escado würde ohne seine Reisen und Kämpfe erlöschen wie eine Kerze ohne
	die Luft zum Atmen, das hatte sie immer gewusst und berücksichtigt.
</P>

<P CLASS="P1">
	Lächelnd hatte sie Aphrodisia´s Einwurf vernommen, die wollüstigen Zeiten seien nun
	für ihn vorbei, doch wenn diese Worte nun in ihr widerhallten, gaben sie ihr einen Stich.
	<I>&quot;Ich würde Euch gar reich belohnen, wenn Ihr meine Haut in Stahl verwandeln
	könntet, Dienerin Tunares&quot;</I> erklang eine Stimme dicht hinter ihr.
	Bei der Grossen Mutter, es reichte !!!
</P>

<P CLASS="P1">
	Clawdeen hatte immense Schwierigkeiten, ihr Mana zu mobilisieren ob ihrer Wut, doch
	es gelang ihr doch irgendwie und sie drehte sich zu der fremden Gestalt in der
	Wolfsform um.
	Ein Hochelf, auch das noch! Die Erscheinung des Zauberers vor ihr gab ihren
	Emotionen den Rest, und als sie die Lefzen aufriss und ihn böse und furchteinflössend
	anblickte, schien der Elf noch ein wenig blasser zu werden und er ging eiligen
	Schrittes hinfort.
	Clawdeen galoppierte über die Rampen der Stadt und weiter durch die Wiesen von
	Greater Faydark, bis sie an eine stille Lichtung kam. Dort nahm sie Platz, verwandelte
	sich zurück und hoffte, nun endlich wirklich ungestört zu sein.
	Sie konnte das alles nicht begreifen ...
</P>

<P CLASS="P1">
	Heute hatte sie Thiameerah, ihre Freundin, zufällig in Kelethin getroffen. Seit
	ihren Tagen in Solusek´s Eye hatte sie sie nicht mehr gesehen und umso grösser
	war die Freude. Mit einigen Met und bestem Elfenbrot bewaffnet hatten sie es sich
	in der Nähe von Stonehenge gemütlich gemacht und über allerlei geplaudert.
	Plötzlich hatte Thiameerah ihr erzählt, sie habe Escado in Kelethin am frühen
	Mittag getroffen. Er habe letzte Reisevorbereitungen getroffen für seine Rückkehr
	nach Kunark und sie bei Merchant Kwein angesprochen ...
</P>

<P CLASS="P1">
	<I>&quot;Angesprochen? Wie das?&quot;</I> hatte Clawdeen sich gewundert.<br>
	<I>&quot;Ich weiss nicht. Er fragte, ob ich Thiameerah sei, und als ich überrascht
	bejahte, lächelte er mich an und sagte, das habe er sich gedacht. Er berichtete, du
	hättest schon so viel von einer guten Freundin namens Thiameerah erzählt, dass
	er gar nicht umhin gekommen sei, schon ein komplettes Bild von mir vor
	Augen zu haben.&quot;</I><br>
	Clawdeen lächelte.<br>
	<I>&quot;Hihi, ja, das stimmt wohl, ich habe viel von dir erzählt ... und von
	unseren Abenteuern in Solusek´s Eye natürlich. Und was weiter ?&quot;</I><br>
	Thiameerah zuckte mit den Schultern und fuhr fort:
	<I>&quot;Hm, nichts weiter. Wir redeten wenig über Belanglosigkeiten, dann
	verabschiedete er sich mit einem Kuss und ging.&quot;</I><br>
	Clawdeens Lächeln erstarb.
	<I>&quot;Bitte ?&quot;</I><br>
	Thia, wie sie kurz genannt wurde, sah Clawdeen fragend an.<br>
	<I>&quot;Er hat dich geküsst ?&quot;</I> fragte Clawdeen ehrlich erschüttert.
	<I>&quot;Ja, hat er.&quot;</I> lachte Thia, doch als sie Clawdeens Blick
	bemerkte, fügte sie rasch hinzu:
	<I>&quot;Nun ja, nur auf die Wange natürlich, und auch nur sehr flüchtig ... ich
	meine ... wir haben ja kaum miteinander geredet.&quot;</I><br>
	<I>&quot;Aber dich einfach zu küssen !&quot;</I><br>
	Ob er das bei jeder machte?<br>
	<I>&quot;Aber in einer Sache muss ich dir recht geben, meine druidische
	Freundin ... er hat wundervolle Augen ... wie er einen ansehen kann ... ich wäre
	ja beinahe von der Plattform gefallen, wo mir in den Höhen Kelethins doch
	eh immer so schwindlig wird. Ich kann jetzt völlig verstehen, warum du immer
	so von ihm schwärmst; ein wirklich toller Mann ist er ...&quot;</I> fuhr
	Thiameerah unbeschwert fort.
</P>

<P CLASS="P1">
	Ein toller Mann ... nur, toll in welchem Sinne? Tollwütig, liebestoll,
	tollkühn, Tollkirsche ... Clawdeen war kaum fähig, einen klaren Gedanken zu fassen.
	Sie sah das Picknick mit Thia noch vor sich, als fände es eben erst statt.
	Wundervolle Augen ... wie er einen ansehen kann ... und diese Sätze stammten
	von ihrer Freundin!
	Clawdeen hatte es nicht lang aushalten können mit diesen Ungewissheiten. Mit
	einem Vorwand verabschiedete sie sich recht rasch von der Enchantress und ging
	nach Kelethin, wo sie in einem der Baumhäuser versuchte, telepathischen Kontakt
	mit ihrem Liebsten aufzunehmen.
	Es dauerte eine Weile, doch dann war der Kontakt hergestellt und
	Escado´s <I>&quot;Grüsse, mein Engel&quot;</I> erreichte sie.<br>
	<I>&quot;Du hast Thia geküsst?&quot;</I> fragte sie direkt und unverblümt.<br>
	<I>&quot;Ja, habe ich. Heute morgen, warum ?&quot;</I> kam prompt zurück.<br>
	<I>&quot;Genau, Escado, das frage ich mich auch : Warum ?&quot;</I><br>
	<I>&quot;Weil sie nett ist ... weiss nicht ... mir war halt danach.&quot;</I><br>
	<I>&quot;Ach, und ist dir öfter danach, andere Frauen zu küssen? Gibt es da
	mehr, von dem ich wissen sollte ?&quot;</I><br>
	Escado sandte ihr ein Lachen, dann antwortete er:
	<I>&quot;Nein, nicht wirklich. Nun ja, ich bin mit einer Raya befreundet, ebenfalls
	eine sehr nette Dame ... also, die bekommt auch schonmal das eine oder andere
	Küsschen, aber sonst ... hm, lass mich nachdenken ... &quot;</I><br>
	<I>&quot;Ist diese Raya zufällig auch Hochelfe?&quot;</I><br>
	<I>&quot;Oh ja, das ist sie, und etwa so alt wie ich.&quot;</I><br>
	<I>&quot;Schön, dann hättest du sie vielleicht heiraten sollen!&quot;</I> gab
	Clawdeen wütend zurück und brach den Kontakt ab.
</P>

<P CLASS="P1">
	Nun sass sie allein auf dieser kleinen Lichtung im Dunkel der Nacht.
	Clawdeen wusste nicht einmal, ob sie nun wütend, traurig, enttäuscht oder
	eifersüchtig war, doch sie fühlte sich schlecht.
	Ihre Gefühle waren immer ehrlich gewesen und niemals hätte sie an ihnen
	gezweifelt. Nach seinem Antrag in der Oasis von Marr war sie sich auch seiner
	ewigen Liebe sicher gewesen ... bis jetzt.
	Was sollte sie tun? Tatenlos bleiben? Gleiches mit Gleichem vergelten? Sich von
	ihm trennen? Klärende Worte verlangen? Oder schweigen, warten, hoffen?
	Ob sich Escado überhaupt bewusst war, wie sehr sie diese neuen Erkenntnisse belasteten?
</P>

<P CLASS="P1">
	<I>&quot;Eh ich deinen Mann becierce oder ähnliches, würde ich mir Arme und
	Beine abhacken und als Trollkörper mit Kopf durch Norrath wandeln auf
	ewig, Clawdeen! Du bist doch meine Freundin!&quot;</I> waren Thiameerah´s Worte
	zum Abschied gewesen.<br>
	<br>
	<I>&quot;Ich, Escado, schwöre vor den Göttern, den Zeugen und allen Wesen
	der Wälder: Clawdeen wird niemals hungern, in ihrem Leben nicht unglücklich
	sein. Ich werde in guten Zeiten und in Notfällen an ihrer Seite stehen und
	somit möchte ich, Escado Schattensturm, dich, Clawdeen, bitten, meine
	Gefährtin zu sein.&quot;</I> hatte sein Schwur vor den Göttern gelautet.<br>
	<br>
	Und als Clawdeen in einen unruhigen Schlaf fiel, mitten auf der Lichtung, da
	prägte sie im Traum ein Wort: <b>Drachenaura</b>.
</P>

<P CLASS="P1">
	Asche zu Asche, Staub zu Staub ... und nie mehr so reif für die Sehnsucht als gestern ...
</P>

<p class="P0" ALIGN="CENTER"><I>Clawdeen</I></p>

