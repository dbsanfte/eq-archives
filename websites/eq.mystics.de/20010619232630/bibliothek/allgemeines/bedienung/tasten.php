<P CLASS="P1">
	Eines vorweg, die Funktion Auto-Attack liegt auf dem Buchstaben 'a', durch diese Belegung	hat schon mancher, eigentlich gesetzestreuer Bürger Norraths sein Leben gelassen, wenn er mit Guards oder einem anderen NPC gesprochen hat. Man vergisst einfach zu leicht, vor dem Sprechen mit den NPCs 'ENTER' oder '/' zu drücken und greift dann, wenn ein A im Satz	vorkommt natürlich den gerade anvisierten NPC an. Und der nimmt einem das ziemlich übel.<br>
	Es empfiehlt sich deshalb, die Auto-Attack-Funktion gleich zu Beginn auf eine andere Taste zu legen.
</P>

<P CLASS="P1">
	Standardmäßig ist in EverQuest das amerikanische Tastaturlayout eingestellt. Es gibt aber die Möglichkeit ein länderspezifisches Tastaturlayout voreinzustellen, indem man einfach die Datei <b>Keyboard.txt</b> im EverQuest-Verzeichnis austauscht. Ihr könnt bei uns das <A HREF="maps/keyboard.txt" TARGET="main">deutsche Tastaturlayout</A> downloaden!
</P>

