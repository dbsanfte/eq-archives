<P CLASS="P1">
	Die Hotkey-Bank enthält 6 Slots je Bank (es gibt insgesamt 6 Banken), die jeweils mit den Tasten 1-6 oder durch einen Linksklick auf den gewünschten Slot aktiviert werden. Um eine Fertigkeit, einen Zauberspruch oder eine allg. Aktion (Social Key) auf einen Hotkey-Slot zu legen, bewegt man den Mauszeiger über die gewünschte Taste und hält die linke Maustaste für etwa 2 Sekunden gedrückt. Die gewählte Taste 'klebt' dann auf dem Mauszeiger und kann auf dem gewünschten Slot abgelegt werden, indem man einen Linksklick darauf macht.<br>
	Um eine der 6 Hotkey-Banken auszuwählen, drückt man die gewünschte Zifferntaste (1-6) bei gedrückter Shift-Taste (z.B.: Shift+3 = Hotkey-Bank 3)!
</P>	

