<P CLASS="P1" ALIGN="CENTER">
	Von Soloron<BR>
	<BR>
</P>

<P CLASS="P1">
Lange ist er über die Erde Norraths gewandert. Aus Tagen wurden Wochen, aus Wochen Monate und aus Monaten Jahre. 
Viele Abenteuer hat er erlebt und oft stand er dem Tode näher als er selber glauben wollte. Viele Freunde hat er
gefunden und auch so manch einen Feind der sich Freund nannte. 
Wie oft er sein Lager aufgeschlagen hat und zusah wie das Feuer niederbrannte weiss er nicht mehr. Gezählt hat er
auch nie wirklich. Er hat die Zeit genoßen die ihm zur Verfügung gestanden hat. Er hat nie nach dem Warum oder dem
Wieso gefragt. Viele Sachen waren einfach so und manche wollte er auch nicht hinterfragen. 
Und als er älter un älter wurde bat Rallos Zek der Kriegsgott ihn zu einem Gespräch. Rallos Zek sagte mit donnernder
Stimme :" Früher Soloron, ja früher da warst du ein Krieger mit Herz und Seele, doch nun ist dieses Herz fort.
Es ist zu alt geworden. Es liebt den Kampf nichtmehr. Mir selber schmerzt es zu sehen das ein Krieger die Lust
am Kampf verloren hat doch ich muss auch sagen das du mir lange Zeit sehr gut gedient hast. Du hast deinem Namen
alle Ehre gemacht und in meinem Namen die Kreaturen der Welt bekämpft. Nicht nur für dich war es eine Ehre
in meinem Namen zu kämpfen sondern auch für mich war es eine Ehre einen Krieger wie dich als Diener zu haben." 
Soloron schaute nicht traurig und auch nicht entsetzt. Mehr als einmal sah er Rallos Zek den Kriegsgott in seinen
Träumen. Soloron antwortete mit fester und klarer Stimme: "Du hast recht Rallos Zek. Ich liebe den Kampf nicht mehr
so wie früher. Es war eine schöne Zeit und oft hab ich das Blut meiner Feinde die Klingen meiner Schwerter runter
laufen sehen und oft habe ich zugeschaut wie sie blutend auf der Erde lagen. Doch diese Zeit ist vorbei. Dein Kampf
ist nicht mehr mein Kampf. 
Ich bin einfach alt geworden und es gibt würdige Nachfolger die meinen Platz einnehmen werden." 
Rallos Zek schaute nochmal herrunter auf den Krieger Soloron und sprach erneut : "Deine Entscheidung scheint 
festzustehen und ich werde wohl nichts dran ändern könne. So entlasse ich dich Warlord Soloron Wolfsheim geborener 
Knochenbrecher aus meinen Diensten auf das du in einer andern Welt wiedergeboren wirst und mir erneut zu Diensten sein wirst." 
</P>
<P CLASS="P1">
Soloron antwortete nicht doch er in Gedanken widersprach er Rallos Zek nicht. Er weiß genau das er wieder im Dienste 
von Rallos Zek kämpfen wird. Jedoch nicht mehr auf dieser Welt. 
Rallos Zek verschwand und noch lange starte Soloron dem Blitz hinterher mit dem Rallos Zek verschwand. 
Als er den Blitz nichtmehr sehen konnte begann er nachzudenken. Er dachte an die vielen schönen Dinge die er in 
seinem Leben als Krieger im Namen Rallos Zeks erlebt hatte. Wieviele Feinde er erschlagen hatte, wieviele große 
Schlachten er überlebt hatte und natürlich dachte er auch an die Liebe die er in seinem Leben gefunden hatte. 
Junia Wolfsheim, die süße, zärtliche Waldelfe die ihn vom ersten Augenblick in ihren Bann gezogen hatte. 
Die, die weiche Seite in dem großen Krieger erweckt hat. Die so ganz anders war wie alle sonst. 
Er kann sich noch an jeden Moment erinnern und wird auch keinen von diesen Momenten vergessen. 
Wie er sie in den Ruinen von Sebilis kennengelernt hat, was wohl ein Wink des Schicksals gewesen sein, denn es war 
das Beste was ihm passieren konnte. Er wird nie vergessen wie sie aussah, wie ihn ihre Augen faszieniert haben in 
denen er ertrank. Den Moment in Oasis als er ihr das erste mal seine Liebe gestand und den Moment in den Butcherblock 
Mountains als er sie fragte ob sie ihn heiraten wolle. Er wird das Gefühl das er im Bauch hatte nicht vergessen als sie "Ja" sagte und er wird nie vergessen wie gut jede Berührung von ihr tat. Wie ihm jedes ihre Worte geholfen hat die Welt und das Geschehne zu verstehen. Er wird nie den Abend in der Plane of Air vergessen wo er mit ihr verträumt den Sonnenuntergang betrachtete. Er wird nie vergessen wie es war sie zu berühren und wie es war sie zu küssen. Keinen dieser Momente wird er vermissen und selbst die Momente in denen er nicht ganz bei der Sache war hat er immer an sie gedacht.
Es wird weh tun sie nicht mehr zu sehen, doch sie wird einen anderen finden und Soloron hofft einfach nur das sie mit 
diesem anderen genauso glücklich wenn nicht sogar glücklicher wird wie mit ihm. Keinen Moment mit ihr will er 
vermissen und keinen Moment mit ihr bereut er. Alles was war, war gut und das ist ein Trost der es ihm leichter macht 
diese Welt zu verlassen.
</P>
<P CLASS="P1">
Erst als es Nacht wurde rührte er sich und mit einem mal wurde ihm klar das seine Frau Junia neben ihm stand. 
Wie lange sie wohl schon da stand?? Sie schaute traurig aus und sprach leise mit zitternder Stimme: 
"Du wirst diese Welt verlassen nicht wahr?" und Soloron antwortete nun ebenfalls traurig: "Ja meine Liebe. 
Doch du sollst wissen das ich dich nie vergessen werde und ich dich ewig liebe." 
So schauten sie sich lange in die Augen. Beide genossen ihren letzten gemeinsamen Abend auf DIESER Welt und als der 
Morgen kam gab sie ihm einen langen Kuss, schaute ihm tief in die Augen und verschwand im Wald. 
Soloron wollte sie noch zurück rufen denn er wollte sie nicht jetzt schon verlassen doch andereseits war er irgendwie 
froh das sie gegangen war. Er wollte nicht das sie dabei war wenn er seinem Dasein auf dieser Welt ein Ende setzte. 
Leise zwitscherten die Vögel im Wald und in der Ferne hörte er Donnern. Dann sagte er zu sich: 
"Niemals wird mich ein Schwert des Bösen töten doch ich will nicht mehr auf dieser Welt sein."
Noch ein letztes mal erinnerte er sich an all seine Freunde und in Gedanken küsste er Junia zart auf die Lippen. 
Als er sich innerlich von allen Freunden und seiner Frau verabschiedet hatte, stürzte er sich in sein eigenes Schwert. 
Sein Blut floss über die Erde Norraths. Es war nicht das erstemal das er sein Blut fliessen sah, aber zum ersten 
Mal war er froh darüber das es floss. Er schloss die Augen lauschte nochmal den Vögeln und starb. 
Er starb nicht als gebrochener Mann sondern mit aller Würde. Es began zu regenen und das Blut Solorons das über die 
Erde Norraths lief wurde weggeschwemmt. In einen nahen Fluß und von dort aus ins Meer. Sein Körper ist nun tot. 
Doch solange seine Freunde ihn nicht vergessen wird Soloron immer weiterleben. Wenn nicht wirklich dann aufjedenfall 
in den Köpfen und Herzen seiner Freunde. Und er wird irgendwann in einer anderen Welt wieder erwachen...
wo das wird er dann sehen..........................
</P>
<P CLASS="P1">
So das ist die Geschichte. Ich selber denke sie ist ziemlich gut gelungen. Will sie aber noch ausbauen. 
Ich will das ganze "Leben" von Soloron niederschreiben. Keine Ahnung wieso aber das ist noch ne Sache die ich 
machen will bevor ich mich zufrieden gebe :D
Wäre nett wenn ihr mir schreibt ob euch die Geschichte gefällt oder nicht. Email ist Soloron@gmx.de
</P>

