<P CLASS="P0" ALIGN="CENTER">
	<B>Autor</B>: Exist &#149; <B>Übersetzer</B>: MacArt<BR>
	<BR>
</P>

<P CLASS="P1">
	Exist sandte seine Erlebnisse - unterlegt mit Screenshots - aus einem PvP-Event in 
	der Gegend um Freeport auf Rallos Zek (PvP):
</P>
 
<P CLASS="P1">
	Die Schlacht vom 8. Mai 1999 war der bisher intensivste PvP-Event, an dem ich in einem Online
	Game teilgenommen habe - er übertraf bei weitem alle Schlachten in Größe und Intensität, 
	die es in den besten Zeiten Ultima Onlines gab. Mindestens 300 Leute waren über eine
	Zeitspanne von 4 Stunden und länger beteiligt.
</P>
 
<P CLASS="P1">
	Die Schlacht begann, als die Streitkräfte des Guten aus ganz Norrath sich versammelten, 
	um die Stadt Freeport gegen einen angekündigten Angriff durch Ogres, Trolls und Dark
	Evils unter der Leitung der Gilde Darkenbane zu verteidigen, welche geplant hatten, gegen
	19 Uhr MEZ in diesem Areal aufzutauchen. Aber sowohl die Heere des Guten als auch des
	Bösen konnten sich nicht zurückhalten. Ungefähr um 14:30 MEZ fingen die zahlreichen
	Streiter, die sich bereits seit 2 Tagen in Freeport versammelt hatten, an, die Dark Elves
	und die ansässigen Trolls und Ogres, die sich in Neriak niedergelassen hatten, anzugreifen.
</P>
 
<P CLASS="P1">
	Eine riesige Schlacht entbrannte in den East Commonlands in der Nähe des Eingangs in 
	den Nektulous Forest. Über eine Stunde lang hielt die Seite der Dunkelheit die Stellung,
	doch immer mehr Verteidiger (sie hatten einen direkten Angriff auf die Tore West Freeports
	erwartet) warfen sich in den Kampf. In der zweiten Stunde schließlich wurden die
	Streitkräfte des Bösen zurückgeschlagen. Der Blutzoll war hoch, auf beiden Seiten fiel
	eine große Anzahl von Kämpfern.
</P>
 
<P CLASS="P1">
	Die Streitmacht des Bösen wurde in die Nektulos Forest Zone zurückgedrängt. Dort entwickelte 
	sich ein Stellungskrieg mit 100 Leuten auf jeder Seite der Zone. Eineinhalb Stunden lang
	versuchten tapfere (und dumme) Einzelgänger und Gruppen durch diese Zone zu gelangen, nur
	um wieder und wieder niedergemetzelt zu werden.
</P>

<P CLASS="P1">
	Ungefähr gegen 17:00 MEZ tauchte ein Kontingent von über 20 Ogres und Trolls
	(Gilde Darkenbane) von Oggok und Grobb vor East Freeport auf; es versteht sich von
	selbst, daß dort sofort heftige Kämpfe entbrannten. In der Befürchtung von hinten
	attackiert zu werden, brach ein Teil der guten Streitmacht den Kampf im Nektulous 
	Forest/East Commonlands ab und stellte sich diesen neuen Gegnern. Aufgrund dieser
	neuen Situation machten die Kämpfer im Nektulous Forest einen heftigen Ausfall in
	Richtung der East Commonlands und überrannten dabei die Streiter des Lichts; diese
	hatten auf dem Rückzug zu Freeports Stadttoren Dutzende Tote zu beklagen.
</P>

<P CLASS="P1">
	Die dunklen Streitkräfte rückten schließlich wieder bis in die West Freeport Zone vor, wo
	sich eine neue Kampfeslinie bildetete.
</P>
 
<P CLASS="P1">
	Inzwischen war es bereits 18 Uhr MEZ und langsam setzte ein gewisser Kampfesüberdruß ein. 
	Der Wille, die Massen von Freeport Militia Guards, welche in der abgelaufenen Woche als
	Antwort auf das Gemetzel des letzten Wochenendes offenbar verstärkt worden waren, zu
	bezwingen, fehlte.
</P>

<P CLASS="P1">
	Um 18:30 MEZ gab es nur noch einige kleinere Geplänkel, als Helden versuchten, in die
	East Commonlands zu gelangen, nur um dort geschlachtet oder wieder in die Sicherheit
	Freeports zurückgetrieben zu werden.
</P>
 
<P CLASS="P1">
	Diese Schlacht war nicht nur eine außergewöhnliche PvP-Erfahrung, sondern auch 
	rollenspieltechnisch für alle ein Riesenspaß, als die Kämpfenden ihre Götter und
	Gefährten um Hilfe anriefen, die Feinde zu schlagen - ganz in Übereinstimmung mit
	den reichhaltigen Rassen- und Gruppenkonflikten, die Norrath so eigen sind.
</P>

