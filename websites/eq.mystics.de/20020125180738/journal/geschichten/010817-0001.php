<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Hereticus Stillborn<BR>
	<BR>
</P>

<P CLASS="P1">
	Necroport - LOADING, PLEASE WAIT . . . (man lernt nie aus)
</P>

<P CLASS="P1">
	Zur Einführung sei gesagt: Es ist im Spiel eingeplant, daß man auch ohne besondere Ausrüstung porten kann. Man verliert dabei einige Bubbles an Erfahrung, aber es erhöht auch den allgemeinen Adrenalinausstoß (wenn man zum Choleriker neigt).
</P>

<P CLASS="P1">
	Eines Nachts, erschaffen aus verrottendem Fleisch und modernden Knochen, erblickten 2 kleine Necrokinder die Düsternis von Neriak City. Nennen wir die 2 Yevil und Hereticus, um den Leichen einen Namen zu geben. Nach langem, planlosen umherirren in der Stadt fand man dann mit Hilfe eines Ortskundigen die örtliche Necrogilde, und man machte sich mit Eifer daran, den Guildmaster totzuquasseln um an die ersten Spells zu kommen. Nur irgendwie antwortete er nicht.
</P>

<P CLASS="P1">
	Yevil: <I>"Hier, ich glaub man muss <b>HAIL</b> sagen.</I><br>
	Hereticus: <I>"Ok, A"</I> (loading, please wait)<br>
	<br>
	Da Yevil und Hereticus im selben Raum spielen, ist die Kommunikation auch über mehrere Zonen ohne Chat möglich.<br>
	<br>
	Hereticus (im RL): <I>"Was is jetzt los ??"</I><br>
	Yevil (im RL): <I>"Da liegt deine Leiche. Wo bist denn ?? Was war denn ??"</I><br>
	Hereticus (im RL): <I>"Da is Wald rund um mich. 0 Plan."</I><br>
	Yevil (im RL): <I>"Schau mich net so an, ich weiss auch net was passiert ist.</I><br>
	<br>
  ... nach einigem Fragen an jemanden MIT Robe erfuhren wir: <b>A = ATTACK</b><br>
	Man sollte es nicht in der Stadt anwenden, vor allem nicht wahrend eines Gespräches mit einem NPC.
</P>

<P CLASS="P1">
	Wieder zurück, such Dir Deinen Weg durch die Stadt, Spell einschreiben, ALLE Punkte vergeben (natürlich auf die falschen Skills) und raus zum Fight.<br>
	Es muss ja nicht näher gesagt werden, daß Spiders, Fire Beetles und andere Geschöpfe eines bösartigen Gottes hinterhältigerweise einander helfen, wenn man probiert seine Fähigkeiten an IRGENDETWAS zu testen. So manche Aktion bewirkt eine äußerst unangenehme Reaktion. Aber auch das muss man lernen, vor allem, wenn man nicht andauernd sein eigener Leichenfledderer sein will.<br>
	Über kurz oder lang wird auch der dümmste Necro Level 8. Irgendeinem unserer 2 Spezialisten kam die Idee, daß es ja auch noch andere Zonen gibt. Auf nach EAST COMMON. HURRAAAAA !!!<br>
</P>

<P CLASS="P2">
	Level 8 oder "Kaum bist Du am Druid Spot, da ereilt Dich schon der Tod".
</P>

<P CLASS="P1">
	Bald war die Euphorie verflogen. Da gibt es Löwen (ROT) , Orcs (ROT) , Spiders (ROT aber nicht aggro), und plötzlich sieht man auf einem Hügel 3 Gestalten (NPC) stehen.<br>
	Yevil: <I>"Da stehen welche oben. Gehn wir mal schauen ??"</I><br>
	Hereticus: <I>"Klaro. Los gehts."</I><br>
	... 2x Loading, please wait ...<br>
	<br>
	Naja, seitdem wissen wir, DE Necro und Druiden sind nicht die besten Freunde (das hatte dann BÖSE Folgen für die Druiden, als wir höhere Levels erreichten. ANM.D.Verfassers).
</P>

<P CLASS="P2">
	Level 20 oder "Binde Dich nicht am Giant Fort, denn sonst gibt es Multimord".
</P>

<P CLASS="P1">
	Unsere 2 Necros haben, nach langem Suchen, so manchen unnützen Tod und VIEEEEL zu wenig Know How endlich die Zone Warsilk Woods erreicht. Da wird beschrieben, es gibt ein Giant Fort, in welchem ein Level 20+ TOLLE Erfahrungspunkte bekommt.<br>
	(Keiner hat gesagt, daß es eigentlich ERFARUNGSFLECKEN sind, und zwar solche, welche man in Form von Blut unter der Leiche seines Körpers hinterlässt)<br>
	Nach einigem Suchen fanden die 2 das besagte Fort und sahen (aus sicherer Entfernung) einer kleinen Gruppe bei der Bemühung zu, ihr kärgliches bisschen Leben gegen eine Übermacht von Giants zu verteidigen. Wohl gemerkt, es war kein einziger DE darunter, so traf es niemanden, daß die Population an Druiden, Barbarischen Kriegern und anderem Gesocks, in dieser Zone drastisch abnahm. Nach reiflicher Überlegung kam man zu dem Schluß, sich knapp VOR dem Eingang des Forts zu binden (NUR sicherheitshalber, versteht sich). 
</P>

<P CLASS="P2">
	Und das Schicksal nahm seinen Lauf........
</P>

<P CLASS="P1">
	Lange Zeit ging alles gut und sie 2 wurden (wer kann es ihnen verdenken) leichtsinnig. Hereticus ereilte der wohlbekannte Tod in Form von äußerlicher Gewaltanwendung mittels eines Giant Tree Flayer, verursacht von einem dumpf dreinblickenden, wild um sich herumschlagenden Giant.<br>
	Loading, please wait...<br>
	Hereticus (im RL): <I>"Na gut, ich bin eh vor dem Fort gebunden. Komm gleich wieder :)"</I><br>
	Yevil (im RL): <I>grinst</I><br>
	Hereticus: <I>"SCH......, da haut mich ein Giant und ich hab keine Spells gememmed.</I><br>
	Yevil: <I>"RENN, 20 mana und ein pet hab ich."</I><br>
	Hereticus: <I>"Pech, loading please wait...."</I><br>
	Yevil: <I>"So ein Mist."</I><br>
	Hereticus: <I>"HEEEE, da haut mich schon wieder einer. ich glaub heut is net mei Tag. Blödes Spiel."</I><br>
	Yevil: <I>"Schreib als 1. Gate rein und hau mal ab. Dann koennen wir wiederkommen und den Körper holen."</I><br>
	<br>
	Das war die beste Idee des Tages.
</P>

