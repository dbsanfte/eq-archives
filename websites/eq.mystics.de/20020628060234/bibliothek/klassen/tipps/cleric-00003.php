<P CLASS="P0" ALIGN="CENTER">
	Diese Tips wurden von Finin Elfenlicht erstellt.
</P>	

<TABLE BORDER="0" CELLPADDING="0" CELLSPACING="0" WIDTH="100%">
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
<TR><TD BACKGROUND="/pics/pageback2.jpg" ALIGN="CENTER" CLASS="P5">Ein paar Kleriker - Tips</TD></TR>
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
</TABLE>

<P CLASS="P1">
	Ein Kleriker sein bedeutet nicht immer viel Spaß zu haben. Die meiste Zeit ist der Kleriker derjenige, der während des Kampfes in einer stillen Ecke sitzt und meditiert, lediglich zum Heilen und Buffen mal kurz aufsteht und seiner Rolle -die Gruppe am Leben zu halten- nachgeht. Unterbrochen wird das ganze von einigen Beschimpfungen von seiten der Nahkämpfer, wenn es mit dem Heilen einmal nicht hingehauen hat, und einer anschließenden frustrierten Wiederbelebung durch den Priester.
</P>

<P CLASS="P1">
	Aber nicht alles muß so langweilig oder frustrierend sein, wenn man sich auf sein Handwerk besinnt und auf seine Fähigkeiten konzentriert. Damit meine ich jetzt nicht die ausgezeichneten Möglichkeiten als Kleriker andere Spieler zu "Powerleveln".  In den höheren Leveln hat man sowieso von den 50 Tells pro Spielabend ("Can you do an EXP RES on me?" / "Can you power-level me") genug und wird die meiste Zeit anonym durch Norrath reisen.
</P>

<TABLE BORDER="0" CELLPADDING="0" CELLSPACING="0" WIDTH="100%">
