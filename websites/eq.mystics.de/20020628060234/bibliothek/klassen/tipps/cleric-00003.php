<P CLASS="P1">
	Du solltest IMMER deine beiden DIVINE Zaubersprüche (ab 29) gespeichert haben. Das ist kein Zeichen von Feigheit oder Schwäche, erst recht keine Platzverschwendung. Es wird Dir oftmals das Leben retten, und damit auch sehr häufig deiner Gruppe.
</P>
<P CLASS="P1">
	Du kannst diese Sprüche natürlich auch auf der Flucht einsetzen um deine eigene Haut zu retten, wenn es die Situation verlangt, aber ich nutze sie meistens im Kampf. Es kommt immer einmal zu Situationen, wo Du früher heilen mußt als eigentlich sinnvoll, um einem Kameraden das Leben zu retten. Danach wirst Du meistens von einer ganzen Horde verprügelt. Jetzt einen Divine Spruch eingesetzt und es gibt zwei Möglichkeiten. Die Monster lassen umgehend von Dir ab und verhauen jemand anderes, womit Du Zeit zu einem Stellungswechsel und/oder meditieren bekommst, oder sie verhauen Dich weiter wo Du je nach Situation den Enchanter oder Krieger seine Arbeit machen lassen, bzw. die Meute von Dir lenken lassen kannst, oder Du gibst das Kommando zum geordneten Rückzug. (Einen "Evac" casten, oder rennen).
</P>
<P CLASS="P1">
	Generell solltest Du, wenn Du die Flucht antrittst, das deiner Truppe mitteilen. Machst Du das nicht, sterben sie meistens, bzw. können Dir nicht helfen. So etwas spricht sich schnell herum, und Du wirst selbst als alter Kleriker Probleme bekommen eine Gruppe zu finden.
</P>
<P CLASS="P1">
	Auch solltest Du immer mehrere Versionen deiner Heilzauber bereit halten. Die großen für die Tanks und die kleinen für die Caster, für die ein dicker Heal nur Verschwendung wäre. (Wenig Manaverbrauch = Wenig Downtime) Manchmal wirst Du auch nicht die Zeit haben, einen dicken Heal zu casten. Complete Healing (sehr effektiv ab 1300 zu heilenden Lebenspunkten) dauert zum Beispiel 10 Sekunden.
</P>
<P CLASS="P1">
	Die anderen Sprüche wechsle ich je nach Situation und Gegner. Ein ROOT ist oftmals praktisch	um sich einzelne Gegner vom Hals zu schaffen, wenn der Krieger mal wieder keinen Durchblick hat. Ebenso kann man einem Caster damit helfen, wenn dieser Agro geworden ist. Ein eingespieltes Team mit Cleric/Caster kann zum Beispiel "root-pingpong" spielen. D.h. solltest Du aufgrund deiner Root Versuche AGRO werden, castet der Magier nun seinerseits ein Root.
</P>
<P CLASS="P1">
	Ebenfalls oftmals ungeheuer nützlich ist deine Reihe an STUN Sprüchen. Je nach Situation und Gegner habe ich bis zu drei auf einmal davon gespeichert. Stuns kosten wenig Mana und manchmal ist es sogar sparsamer einen Gegner unter Dauerstun zu halten, als Hinterher den verprügelten Tank zu heilen.<br>
	Cancel/Dispell Magic kann auch sehr nützlich sein, kann jedoch auch sehr gut von vielen anderen Klassen übernommen werden.
</P>

