<TR><TD BACKGROUND="/pics/pageback2.jpg" ALIGN="CENTER" CLASS="P5">Das Verhalten des Klerikers im Kampf</TD></TR>
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
