<P CLASS="P1">
	Von Level 1-20 unterscheidet sich der Kleriker nicht sonderlich von den anderen Klassen, die ebenfalls in den Nahkampf gehen können. Er schlägt je nach Lust, Laune und der aktuellen Gruppenzusammensetzung auf die Monster ein, casted den ein oder anderen Nuke und heilt natürlich seine Kameraden. Ich denke hier gibt es eine Menge Freiheiten, wie man spielen und jagen will. Auch das "solo" Jagen ist noch möglich, steigt aber im Schwierigkeitsgrad mit jedem Level an.
</P>

<P CLASS="P1">
	Meiner Meinung nach sollte man hier so spielen, wie es einem selbst am meisten Spaß macht. Durch Herumprobieren findet man auch am besten seine eigene Taktik. Fehler haben noch keine dramatischen Auswirkungen, ein Tod zuviel ist hier noch leichter überwunden.
</P>

<P CLASS="P1">
	Ab den "niedrigen 20ern" ändert sich jedoch die Spielweise eines Klerikers dramatisch. Er wird als Heiler in der Gruppe immer schwerer durch "Sekundärheiler" ersetzbar, daher findet man nun auch leichter eine Gruppe. Der entscheidende Nachteil ist aber, man beginnt nun die Gruppe zu brauchen. War es vorher einfach nur schwer solo zu jagen, wird es nun mit jedem neuen Level immer unmöglicher. (Ja, ich weiß, an manchen, ganz wenigen Orten kann ein Kleriker auch mit Level 50 noch solo UNTOTE Gegner jagen... aber das ist die Ausnahme).
</P>

<P CLASS="P1">
	Es mag ein paar Phasen geben, wo nun vielleicht noch Druiden / Schamanen z.B. wegen SoW noch bevorzugt werden, aber spätestens wenn man auf die 30 zugeht (und revive bekommt) findet man leicht eine Gruppe. Mit 40+ bekommt man eigentlich immer eine Gruppe, da man ohne Kleriker in den seltensten Fällen auskommen kann. Auch wird natürlich ab 39 die Wiederbelebung mit EXP Wiedergabe sehr gefragt. Ab 50 sei noch erwähnt, daß eine Gruppe ohne das Grundgerüst "Kleriker - Krieger - Enchanter" nicht mehr auskommt.
</P>

<TABLE BORDER="0" CELLPADDING="0" CELLSPACING="0" WIDTH="100%">
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
<TR><TD BACKGROUND="/pics/pageback2.jpg" ALIGN="CENTER" CLASS="P5">Das Verhalten im Kampf</TD></TR>
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
</TABLE>

<P CLASS="P1">
	Vergiß niemals, für jeden Heilzauber den Du auf einen deiner Mitspieler sprichst, werden die MOBs gegen die Ihr gerade kämpft denken, Du hättest ihnen diese entsprechende Menge an Schaden zugefügt.
</P>
<P CLASS="P1">
	Ein Heal verhält sich in der AGRO-Liste der Monster, wie ein AE (Area Effekt) Schadenszauber. Zur falschen Zeit ausgesprochen, werden Dich alle Monster rund um Dich herum anspringen. Sogar Monster die von anderen Gruppen gepullt wurden, haben Dich dann unter Umständen zum Fressen gerne.
</P>
<P CLASS="P1">
	Nach Level 20 bist Du nicht mehr in der Lage in den Nahkampf zu gehen, und falls Du das doch machst ist der Schaden verglichen mit einem echten Nahkämpfer lächerlich. Und vor allem kannst Du bei weitem nicht so viele Treffer einstecken wie ein Krieger. Setze dich hin, meditiere und heile/buffe die Nahkämpfer.
</P>
<P CLASS="P1">
	Heilen und Buffen, das ist unser Beitrag für die Gruppe. Auch deine Nukes (Direct Damage Sprüche) kannst Du getrost vergessen. (einzige Ausnahme deine Serie gegen untote Gegner). Wenn Du deine DD unter dem Schaden/Mana Aspekt betrachtest, ist es eine reine Verschwendung deiner blauen Kraft. (Ja, ich weiß, auch hier mag es wieder Situationen geben, in denen ein Nuke schon einmal angebracht ist, aber das ist nicht die Regel).
</P>
<P CLASS="P1">
	Beobachte immer die Lebensenergie deiner Gruppengenossen im Kampf, ein kurzer Augenblick der Unachtsamkeit und es ist um einen Deiner Freunde geschehen,...
	Vergiß dabei aber nie, auch einen Blick auf die eigene Lebensenergie zu werfen. Zu schnell achtet man nur auf seine Freunde und ist dann selber tot....
	Wichtig ist, heile deine (Nahkampf-) Kameraden immer erst bei ca. 40% (oder weniger) Lebenspunkten. Unter Level 30 ist daß vielleicht noch nicht ganz so wichtig, aber über Level 30 unumgänglich, da Du sonst die Monsterhorde nach einem Heilzauber auf der eigenen Pelle haben wirst.
</P>
<P CLASS="P1">
	Wenn Du ständig der einzige Tote in deiner Gruppe bist, gibt es nur zwei Möglichkeiten. Entweder Du heilst jedesmal viel zu früh, oder deine Kameraden wissen nicht, wie sie sich selbst verhalten müssen.
</P>
<P CLASS="P1">
	Wenn ein Monster meint dich zum Frühstück zu verspeisen, so ist es die Aufgabe eines ENCHANTERS oder eines Nahkämpfers (TANK) mit der Fähigkeit TAUNT dieses davon abzubringen. Hilfreich in unübersichtlichen Zonen haben sich dafür Hotkeys erwiesen, mit denen Du deiner Gruppe ohne viel Tippen zu müssen, mitteilen kannst, daß Du gerade gefressen wirst.
</P>
<P CLASS="P1">
	Wenn ein Tank daraufhin meint: "Sofort, bin gleich soweit, mein Gegner ist fast tot." Mache ihm EINMAL klar, daß so etwas nicht gibt und er ERST taunten muß, dann seinen ersten Gegner wieder bearbeiten kann. Ein toter Kleriker bedeutet meistens auch eine tote Gruppe...
	Will er das nicht verstehen, solltest Du Dir andere Partner suchen, es ist es einfach nicht wert sich auf so etwas einzulassen.
</P>
<P CLASS="P1">
	Wenn Du in einer großen Gruppe jagst und das Risiko auch recht gering ist, wirst Du nur EXP im Spiel sammeln, wenn ihr konstant und ununterbrochen am pullen seid. Meistens ist es da aber auch nicht nötig besonders häufig im Kampf zu heilen, meditiere so lange es geht und heile nur wenn es nötig ist oder nach dem Kampf. Eine geordnete Manaeinteilung verschafft geringe Downzeiten und eine Reserve, sollten einmal mehr MOBs kommen als bestellt.
</P>
<P CLASS="P1">
	Wenn Du das Mana hast, dann buffe deine Kameraden. Die dicksten HP/AC Buffs auf die Tanks und die Pets. Gut gebuffte Pets sind eine nette Bereicherung für Eure Gruppe, was in manchen Situationen entscheidend sein kann. Außerdem sparen deine Kameraden ihrerseits Mana, wenn sie nicht ständig neue Skelette oder (Hund-) Elementare erschaffen müssen.
</P>
<P CLASS="P1">
	Wenn Du mal FM bist (so etwas gibt es????) buffe auch die Händefuchtler mit deinen HP Buffs. Nichts stirbt schneller als ein besprungener Caster. Da können deine Buffs entscheidend für den Ausgang der ganzen Schlacht werden. Außerdem hast Du so mehr Zeit einen kleinen Heal anzubringen.
</P>

