<P CLASS="P1">
	Praktisch ist auch die Möglichkeit seine Kameraden mit seinen Buffs zu heilen. Oftmals kostet das weniger Mana als ein Heal und die Gruppe bleibt dadurch mit den Buffs versorgt.
	Viele versuchen zuerst sich, und anschließend den Rest der Gruppe zu buffen, so daß sie sehen, wann ein Buff geht. Das ist eine tolle Sache, wendet man das Buff/Heil Prinzip an, aber unter Umständen schon nicht mehr nötig. Lieber lasse ich mir von meinem Lieblingstank Bescheid sagen, daß er wieder versorgt werden möchte, als einen Batzen Mana durch Gruppenbuffs in nur einer Minute zu verlieren, da daß wiederum oftmals Wartezeit bedeutet.
</P>
<P CLASS="P1">
	Sollte ich FM sein, verteile ich meine Buffs so oder so gleich wieder auf die Leute, bei denen der Buff als nächstes wieder verschwinden würde. FM herumsitzen und nichts tun, ist reine Verschwendung von Zeit und Energie.
	Ebenfalls praktisch und nützlich ist es, wenn man einen Sekundärheiler in der Gruppe hat. Ich habe mir für hektische Situationen ein paar Hotkeys angelegt. Diese haben folgenden Text:
</P>
<UL CLASS="P0">
<LI>Zeile1:<br>
		/g I'm casting SUPERIOR HEAL now on %t<BR></LI>
<LI>Zeile 2:<br>
		/cast 5<BR></LI>
</UL>
<P CLASS="P1">
	Das %t wird im Chat durch dein aktuelles Target (Ziel) ersetzt. Das "/cast 5" führt den an 5ter Stelle gespeicherten Zauberspruch aus. Natürlich mußt Du dabei stehen,...
</P>
<P CLASS="P1">
	So kann man mit nur einem Hotkey der Gruppe oder dem nervösen Krieger anzeigen, das ein HEAL unterwegs ist. Auch werden sie nach einiger Zeit ein Gefühl für die Castzeit deiner Sprüche bekommen. Besonders sinnvoll ist dies bei einem "COMPLETE HEALING" da die Spruchzeit hier ganze 10 Sekunden beträgt.
</P>
<P CLASS="P1">
	Ist ein besagter Sekundärheiler in der Gruppe, kann er Dir nämlich unterstützend eingreifen, ohne Mana zu verschwenden oder doppelt zu heilen. Währen Du am casten bist, kann er den unter miesen Umständen ebenfalls gerade verprügelten Wizzard heilen, ODER ein kleinen Heal auf dein Hauptziel sprechen, damit dieser nicht stirbt, bevor Du fertig mit Casten bist. Das ist insbesondere bei Gegnern die mächtig Schläge austeilen, oder einem gegnerischen Caster effektiv. Der schönste Complete Heal bringt nichts, wenn er eine Sekunde zu spät ankommt, oder der nervöse Paladin bereits sein LH (Lay Hands) verbraucht hat.
</P>
<P CLASS="P1">
	Zu deiner Ausrüstung sei auch noch etwas gesagt. Versuch so viel WIS zu bekommen wie möglich. Das WIS bestimmt dein Mana und damit ab Level 20 deine Effektivität. Mehr Mana bedeutet mehr Heilung. STR solltest Du, insbesondere als zierlicher Hochelf auch steigern, jedoch nicht um in den Nahkampf zu gehen, sondern einfach um deine besten Rüstungen vom Gewicht her auch tragen zu können.
</P>
<P CLASS="P0">
	Meine persönliche Rangfolge als Kleriker in den STATS ist:
</P>
<OL CLASS="P0">
<LI>WISDOM ... Weisheit (bis 200, danach nur noch auf Mana)
<BR></LI>
<LI>AC ... Rüstungsklasse
<BR></LI>
<LI>MANA ... Mana
<BR></LI>
<LI>STRENGTH ... Stärke
<BR></LI>
</OL>
<P CLASS="P1">
	Geschicklichkeit (DEX) mag auch ganz nett sein, oder die Gewandtheit (AGI), jedoch konnte ich keine nennenswerten Unterschiede ob mit oder ohne feststellen. Der AC Bonus durch AGI ist für Kleriker nicht besonders hoch.
</P>

