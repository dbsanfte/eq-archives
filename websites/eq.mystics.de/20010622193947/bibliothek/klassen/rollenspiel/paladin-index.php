<P CLASS="P1">
	Seine strahlende Erscheinung und seine gütige, zugleich aber auch unnahbar scheinende 
	Ausstrahlung machen den Paladin zu einer beeindruckenden Gestalt. In seinem Kampf für 
	das Gute ist er bereit, sein Leben für den Schutz anderer zu geben. Den Schwachen zu 
	helfen ist seine Aufgabe, dafür lebt er. Stets bemüht, den Einfluß der bösen Mächte 
	zu beschränken, ohne dabei aber das alles bestimmende Gleichgewicht zu verändern bedient 
	sich der Paladin zahlreicher Mittel. Seine Fähigkeiten im Kampf gereichen denen eines 
	Kriegers zur Ehre, er weiß mit zahllosen Waffen umzugehen. Die Götter, vor allem Tunare, 
	die Mutter allen Irdischen, sind dem Paladin wohlgesonnen. Der Paladin erhält eine 
	umfassende und gründliche Ausbildung, damit er seinen Aufgaben möglichst gerecht 
	werden kann. Wenn er genügend Erfahrung besitzt, kann er einige Sprüche der Kleriker 
	erlernen, die ihm auf seinem Weg helfen. Er erwirbt die Fähigkeit, Menschen von 
	Krankheiten und Verletzungen zu heilen und besitzt die Macht, schändliches untotes 
	Leben vom Angesicht der Erde zu vertilgen.
</P>
	
<P CLASS="P1">
	Sein Ziel verfolgt der Paladin mit Entschlossenheit und Hartnäckigkeit. Niemand kann ihn 
	von seinem Weg abbringen. Gerüchten zufolge gibt es einige wenige, die vom wahren Glauben 
	abgefallen sind und nun als Ketzer ihr Dasein fristen. Dies wird einem Paladin kaum 
	widerfahren, da er unglaublich willensstark agiert und sein Glaube an die guten 
	Gottheiten unerschütterlich ist. Paladine geben stets ein beeindruckendes Bild, 
	welches allein schon ausreicht, bösartige Wesen abzuschrecken. Geschützt durch 
	eine strahlende Rüstung, bewaffnet und von den Göttern gesegnet stellt sich der 
	Paladin allen Aufgaben und meistert sie. Wer immer Hilfe benötigt, er wird sie 
	finden, wenn einer der Krieger des Lichtes in der Nähe ist. Ein Paladin wird niemals 
	einen Hilferuf ignorieren oder seine Hilfe verweigern, außer es handelt sich um ein 
	Wesen, das einer bösartigen Rasse angehört. Dunkelelfen, Trolle und Oger vom Angesicht 
	dieser Erde zu entfernen zählt mit zu seinen Aufgaben und deshalb wird er diesen Rassen 
	niemals Hilfe gewähren oder sich auch nur mit ihnen abgeben, da er es für unter seiner 
	Würde betrachtet, diesen niederen Kreaturen auch nur Beachtung zu schenken. 
</P>
	
<P CLASS="P1">
	So gehet denn hin Reisende, gewiß mit der Zuversicht, daß ein Paladin über euer Wohlergehen 
	wacht. Und ihr Anbeter der Dunkelheit, verkriecht euch in euren Höhlen und zittert vor Angst, 
	denn die Macht des Lichtes wird auch euch durch das heilige Schwert eines Paladins erreichen 
	und euch wird Reinigung widerfahren. Möge Tunare ihre schützende Hand über all jene halten, 
	die ihres Schutzes bedürfen.
</P>
	
<P CLASS="P0">
	<I>Caradryan Eataine</I>
</P>

