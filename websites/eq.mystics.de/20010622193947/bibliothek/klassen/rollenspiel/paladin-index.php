<P CLASS="P1">
	Der Paladin ist in etwa das, was man im Mittelalter allgemein als einen Templer bezeichnet hat. 
	Demzufolge aus einem Priesteroden geborenen Ritter mit unerschütterlichem  Glauben an Gott, einen 
	fanatischen Krieger, der  für seinen Glauben und seine Mission  alles  geben würde. Der Paladin ist 
	ein heiliger Beschützer und in der Tat, genau das bedeutet das Wort Paladin auch.
</P>

<P CLASS="P1">
	Im Fantasy-Rollenspiele ist der Paladin in erster Linie ein Ritter, der für Recht und Ordnung sorgt. 
	Er beschützt die  Schwachen und bekämft die Bösen, auch wenn es seinen Tod  bedeuten sollte. Da auch
	der Paladin, wie der Templer im Mittelalter, aus einem Priesterorden heraus geboren ist, öffnen sich 
	ihm ab der neunten Erfahrungsstufe die Pforten der klerikalen weißen Magie. Dies soll ihn von einem 
	einfachen Ritter (Krieger) unterscheiden.
</P>

<P CLASS="P1">
	Die Paladine  können in zwei Unterklassen differenziert werden: die geborenen Anführer und die 
	geborenen Diener des  Volkes. Als rechtschaffen  gute Charaktere, haben sie einen ausgesprochenen 
	Sinn für Recht, Ordnung, Gerechtigkeit, Fairneß und das Gute. Sie vertrauen nicht auf Glück, sondern
	nur auf ihre Fähigkeiten. Da Paladine stets den Eindruck erwecken müssen, daß ein heiliger Krieger 
	(Engel) vom Himmel gekommen ist, um den Menschen zu helfen, ist für sie ein hohes Charisma unverzichtbar. 
	Obwohl relativ emotionslos, sind die  Paladine ausgesprochen redselig, im Gegensatz zu Waldläufern. Dies 
	liegt wohl daran, daß sie um ihr Werk  zu beenden auf Kommunikation angewiesen sind und diese sowieso gut 
	beherrschen müssen, um ihrem Job als Anführer gerecht zu werden.
</P>

<P CLASS="P1">
	Aufgrund ihrer  Fähigkeiten und  einfach aufgrund der Tatsache,  daß sie Paladine sind, werden vor 
	allem in sie als Anführer hohe Erwartungen gestellt. Ein Paladin der seine  Gruppe verhältnismäßig 
	oft in ausweglose Situationen führt, in denen viele den Tod finden, verliert sehr schnell an 
	Glaubwürdigkeit. In  Kämpfen ist es oft der Paladin, der die Gegner an sich bindet, um seine Gefährten  
	bestmöglich vor Schaden zu bewahren und sie sind es, die gegen übermächtige Feinde als letze das Feld halten, 
	um ihren Kameraden Zeit zur  Flucht zu verhelfen. Allerdings ist auch zu erwähnen, daß es oft Paladine sind, 
	die eine Abenteurergruppe überhaupt erst in solche  Situationen reinbringen. Aufgrund ihrer nicht vorhandenen 
	Furcht  und dem Ehrgefühl, Schande zu erfahren, wenn man wegläuft, greifen sie alles an,  was ihrer 
	Sichtweise zuwider spricht ohne dabei die Folgen für sich selbst und für die Gefährten abzuschätzen.  
	Dennoch sind sie nicht töricht oder blöd, wenn sie einen Drachen sehen, so ist es ihnen schon klar, daß 
	ein Kampf aussichtslos wäre und es an der  Zeit ist erst einmal Kräfte zu sammeln, viel anders sieht es 
	aber aus, wenn eine Meute Mumien in Sicht kommt, da ist  der Paladin nicht zu bremsen. 
</P>

<P CLASS="P0">
	<I>Maya, Paladin der Erollisi Marr</I>
</P>

