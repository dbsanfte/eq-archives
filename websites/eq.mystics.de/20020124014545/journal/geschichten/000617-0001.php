<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Thiameerah Lenitas<BR>
	<BR>
</P>

<P CLASS="P1">
Er ist eigentlich ein guter Elfe. Umso schlimmer ist das Schicksal,
welches in ereilte. Hart getroffen hat mich die Verwandlung, die ein liebenswerter
Waldbewohner, der sein Streben auf das Wohl der anderen ausgerichtet hatte,
durchmachen musste. Immer versuchte er Tunare treu zu dienen und in seinem
Sinne zu handeln. Nie wurde ein Bittsteller, egal ob er milde Gaben oder den
Geist des Wolfes benötigte, von ihm abgewiesen.
</P>

<P CLASS="P1">
Tanuk freute sich seiner Fähigkeiten und vollbrachte wahre Wunder mit
seinen magischen Gaben. Oft wurde mir die Gelegenheit gegeben ihn zu beobachten,
wie er selbst in den aussichtslosesten Momenten die nötige Ruhe bewahrte
und nicht nur einzelne zu retten vermochte, sondern oft auch eine ganze
Handvoll seiner Mitstreiter, auch wenn es seinen eigenen Tod bedeuten konnte.
</P>

<P CLASS="P1">
Meine kämpferisches Selbstvertrauen und mein Handeln wurden immer
bestärkt, wenn Tanuk in der Nähe war. 
Das letzte Mal, als wir uns trafen, es mag wohl zwei oder drei Jahre; her
sein, befand ich mich in Oasis of Marr. Hinterhältig wurde ich von einem
Krokodil angegriffen, dass aus der Tiefe des Meeres gekommen war, um sich ein
Opfer für seine unendliche Fressgier zu suchen. Mein Ende war nahe, als das
Wunder, dem ich mein Leben verdanke, geschah. Tanuk wusste auf irgendeine
unerklärliche Art, dass ich mich in Gefahr befand und erreichte mich im letzten
Moment, bevor das Ungetüm mich endgültig vernichten und in sein
Vorratslager schaffen konnte. Er lies die guten Kräfte Tunares auf mich einwirken und
vernichtete die Kreatur mit wenigen Handstreichen.
</P>

<P CLASS="P1">
Wie gesagt, es war das letzte Mal, dass ich Tanuk sah. Lange Zeit war es
nicht möglich, Kontakt zu ihm aufzubauen. Keine Nachricht und Botschaft
erreichte ihn oder kam zurück. Dann, endlich.... ein Lebenszeichen. Doch
absonderliches muss geschehen sein. Nicht Tanuk war es, der telephatischen Kontakt
zu mir aufnahm, sondern ein Fremdling, namens Renatus. Ich spürte sofort,
dass dieses Wesen die Seele Tanuks in sich aufgenommen hatte. Auch erkannte ich
Tanuks Wesen in dieser Kreatur wieder, die Art wie er im Geiste mit mir
sprach und wie er sich verhielt. Jedoch spürte ich auch die Qual, der er
ausgesetzt war.
</P>

<P CLASS="P1">
Grosse Enttäuschung muss Tanuk erlebt haben, schlechtes muss ihm
widerfahren sein. Vielleicht von Freunden im Stich gelassen oder ein grosses
Schicksal, dass ihn ereilt hatte.... ich fand keine Antwort
</P>

<P CLASS="P1">
Innoruuk hatte das Leid des guten Tanuk erkannt und ihn mit all seinen
schlechten Versprechungen in eine Falle gelockt. Die Seele ist Tanuk gestohlen
und ein Wesen der bösen Art damit geschaffen worden. Ein gutes Herz in einem
bösen Körper. Ich fühle den inneren Krieg, die Auseinandersetzung, das
Leid, mit dem jeden Tag aufs neue konfrontiert wird. Und ich höre das hämische
Lachen Innoruuks, der nicht nur ein Wesen vernichtet hat. Mit Tanuk ist auch
ein kleiner Teil meines eigenen Wesens verloren gegangen, denn ich habe
viel mit ihm erlebt.
</P>

<P CLASS="P1">
Noch schafft es Tanuks Seele, dann und wann die Oberhand über das Böse zu
gewinnen. In seiner unendlichen Stärke ficht er einen harten Kampf, den er
aber über Kurz oder Lang verlieren wird. Diese Zeit können wir im Moment noch
gemeinsam nutzen, um uns Botschaften zukommen zu lassen. Doch spüre ich
bereits jetzt, dass Tanuks Kraft nachlässt und das restliche Gute vom Bösen in
naher Zukunft endgültig vernichtet werden wird.
</P>

<P CLASS="P1">
Ein Freund ist mir verloren gegangen. Mein Wille, gegen das Böse zu
kämpfen ist stärker denn je. Innoruuk soll durch meine Taten bestraft werden, sein
Volk, die Dunkelelfen sollen büssen. 
</P>

<P CLASS="P1">
Aber ich fürchte den Moment, in dem Innoruuk meine Schwäche erkennt und
die Wahl meiner Gegner lenkt.....
</P>

<P CLASS="P0" ALIGN="CENTER"><I>Thiameerah Lenitas</I></P>

