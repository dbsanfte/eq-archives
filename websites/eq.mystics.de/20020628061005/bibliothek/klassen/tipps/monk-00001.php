<P CLASS="P1">
	Monks sind wohl die beste Alternative zu einem Warrior und durch seine Spezial Attacken 
	flexibler als dieser.
	Ein Monk startet mit 21 HPs, Armorclass 52 und 32 Attackskill. Resistenzen: Poison 15, 
	Magic 35, Disease 15, Heat 35, Cold 35. 
	Der Monk kann nur max. die Lederrüstung tragen. Waffen kann der Monk auch benutzen, aber 
	die besten Waffen die er hat sind seine Hände und Füsse. Der Monk ist mit seinen 
	Fähigkeiten eine gute Klasse für Einzelkämpfer. Er hat nicht viele HPs und seine AC ist 
	auch nicht hoch, aber er hat dafür die besten und meisten Paraden bzw. Ausweichmanöver.
</P>

