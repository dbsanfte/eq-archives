<P CLASS="P1">
	Man kann zwischen Qeynos und Freeport wählen.
</P>

<P CLASS="P1">
	<b>Qeynos</b> liegt südlich von Halas und dem Gnolldungeon Blackbarrow. Östlich liegen die Karana und das versteckte Minotaurenkönigreich.<br>
	Bei North Qeynos, wo das erste Respawngebiet ist, sind Bats, Fire Beetles, Klicnik Workers, 
	Klink Drones, Klicnik Prince, Klicnik Queens, Snakes, Large Rats, Dec. Skeletons und Gnoll 
	Pubs. Aber es tummeln sich auch einige höhere Gnolls und Evil NPC herum.  Klicnik Queens 
	sollte man nicht alleine in den unteren Levels angreifen. Ab Level 2 kann man alles bis auf 
	die Klicnik Queens und die NPC PKs  alleine killen.<br>
	In den Qeynos Hills, das nächst Jagdgebiet werden alle in Qeynos des öfteren besuchen, 
	dort  gibt es mehrere Gegner, die man gut jagen kann. Aber man sollte dort auf die Evil 
	Npcs achten, die dort herumlaufen.<br>
	Besonders zu erwähnen wären hier auch noch Cros Treewind und Holly, diese beiden NPCs 
	beschützen die Bären und Wölfe in den Hills. Beide sind sehr stark, Cros ist ein Druide, 
	was eine Flucht in den unteren Levels unmöglich macht, Holly ist "nur" eine Kriegerin.
</P>

<P CLASS="P1">
	Qeynos Monk Quest: Die Monks Guild in Qeynos  vergibt Stirnb&auml;ndrt, um eines dieser 
	Bänder zu bekommen, muss man erst ein Quest erfüllen.<br>
	Für die ersten Stirnbänder muss man dafür Putrid Rip Bones finden, diese findet man nur 
	bei Putrid Skelletons. Putrid Skelletons sind bei den Ruinen nahe Surfall Glades zu finden, 
	aber Achtung vor Dread Corpse und einem Gnome Necromancer namens Pyzjin.
</P>

<P CLASS="P1">
	<b>Freeport</b> liegt zentral in Norrath und man kann problemlos per Schiff bis nach Faydwer 
	fahren. Man kann aber auch nach Osten durch die Commonlands zu dem berühmten Dungeon 
	Befallen reisen, was aber durch die vielen Griffins sehr gefährlich ist. Nördlich ist 
	Neriak, das Heim der Dunkelelfen. Südlich liegt Oggok und Grobb. Bei East Freeport dem 
	ersten Respawngebiet sind folgende Gegner zu finden: Deathfist Pawns, Fire Beetles, 
	Snakes, Bats und Large Rats.<br>
	Das nächst größere Respawngebiet sind die East Commonlands, wo sich manchmal Griffins 
	aufhalten die jeden killen der langsamer ist als sie. Griffins sind Level 50 Monster die 
	man auch nicht alleine in Level 50 killen kann, da sie gegen fast alles resistent sind.<br>
	Die Monk Quest in Freeport scheint noch nicht zu funktionieren.
</P>

