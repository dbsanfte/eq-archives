<P CLASS="P1">
	Diese Klasse hat zweifellos die meisten Angriffsformen, die es in EQ gibt. Er macht fast 
	soviel Schaden mit seinen Händen wie Krieger mit ihren schweren Waffen.<br>
	Außerdem ist er immer gleich einsatzbereit, wenn er stirbt, da er keine Waffen braucht.<br>
	Der Monk bekommt alle paar Levels eine neue Angriffsform, die aber immer erst neu erlernt 
	werden muss. Also wenn man den "Round Kick" im Level 5 
	bekommt, ist das neu zu erlernen 
	und er hat nichts mit den anderen Kickskills gemeinsam. Der Monk besitzt wegen 
	seiner Rasse keine Nachtsicht und muss zu Laternen, Fackeln, Lightstones oder zu 
	Spells befreundeter Magier greifen. Laternen, Fackeln und andere Lichtquellen die man in 
	die Hand nehmen muss, wirken sich sehr schlecht auf den "Dual Wield"-Skill aus, wenn möglich nehmt einen Lightstone oder fragt einen Zauberer.
</P>

<P CLASS="P5">
	Heilung:
</P>

<P CLASS="P1">
	Er besitzt schon ab Level 1 einen guten Heilungsskill das sich "Mend" nennt. Man sollte dieses Skill aber in den unteren Levels immer gleich ausführen, wenn man kann, denn dieser Skill ist sehr zeitaufwendig bzw. am Anfang scheitert man ziemlich oft. Ab Level 5 wirkt dieser Skill manchmal, aber es kann auch die Wunden verschlechtern und das kann bei einen Kampf tödlich enden. Der "Binding Wounds"-Skill ist eine gute Alternative am Anfang aber etwas kostspielig.
</P>

