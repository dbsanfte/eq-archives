<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Tyrail<BR>
	<BR>
</P>

<P CLASS="P1">
Nur noch wenige Zentimeter war die Schlaufe vom Ende des Bogens entfernt. Man sah die
Anspannung im Gesicht von Wolfspfote, die Konzentration, aber man sah auch die Gelassenheit
und Freude, die ein Ranger beim Bogenbau empfindet.<br>
Ich schaute mit erwartungsvollen Augen meinen Vater an und konnte es kaum erwarten, den neuen
Bogen in meinen Händen zu halten. Kaum war mein Vater fertig riss ich ihm die Waffe aus der
Hand und rannte so schnell ich konnte. Ich rannte beinahe meine Mutter um die gerade die
Rüstung meines Vaters reinigte. Die Tür flog auf uns es dauerte keine Minute und ich hatte
die Strecke von unserem Haus bis zum Lift des Priesters der Zwietracht hinter mich gebracht
und fuhr herunter.<br>
Mein Vater lachte laut und meine Mutter rief mir nach: <I>"Tyrail, renne nicht so und sei
vorsichtig im Wald.....hörst Du !"</I>.
</P>

<P CLASS="P1">
Mein Vater, Ranger des alten ehrwürdigen Orden und Mitglied der Familie Tauglanz, war sehr
stolz auf mich und lehrte mich viel. Er bereitete mich so gut er konnte auf das Leben in
Norrath vor. Meine Mutter, Butterblume, eine sehr starke Druidin, war froh mich glücklich zu
sehen, aber ihre Sorgen um mich brachten Sie manchmal um den Verstand. Sie war mehr als eine
Mutter, sie war Freundin, Spiel- und Jagdgefährtin in einem.
Sie gab uns alle Liebe zu der Sie fähig war, da Sie wusste wie es war alleine zu sein, ohne
Familie. Ihre Eltern starben als sie noch ein Baby war.
</P>

<P CLASS="P1">
Im Greater Faydark angekommen machte ich mich auf den Weg zu den Ruinen der alten Elfenstädten
unserer Vorfahren, sie liegen nicht weit entfernt vom Lift des Priesters der Zwietracht hinter
einem Hügel.<br>
Die Freude und das ekstatische Gefühl, wie es nur ein Waldelfe fühlen kann, machten sich in
mir breit. Es war herrlich mit dem neuen Bogen, meinem kleinen Schwert und dem Amulett meines
Vater durch den Wald zu ziehen. Natürlich wollte ich meinen Bogen gleich ausprobieren und
suchte nach einem Ziel. Und da sah ich es auch schon, eine Fledermaus hing an einem Ast und
schlief. Ich nahm einen Pfeil und spannte meinen Bogen. Ich atmete ruhig und zielte. Schwiffff
und der Pfeil machte sich auf seinen Weg, doch die Fledermaus erwachte und flog davon. Mein
Pfeil flog mit voller Wucht an ihr vorbei. Ich erkannte warum sie wegflog. Hinter ihr hatten
drei Riesenwespen begonnen ihr Nest zu bauen.
</P>

<P CLASS="P1">
Mein Pfeil flog direkt hinein, mit einem lauten Krach fiel das Nest zu Boden. Die Riesenwespen,
sichtlich erzürnt flogen auf mich zu um mir ihre langen Stachel durch den Körper zu jagen. Ich
stand da wie erstarrt und rang nach Luft. Kaum hatte ich mich gefangen riss ich mich herum um
das Weite zu suchen. Da traf es mich wie eine Klinge direkt ins Herz. Ich war starr vor
Schreck. Vor mir stand ein Orc Pawn. Er grunzte und Sabber rann an seinen Mundwinkeln herab.
Ich sah nur noch wie er die Hand hob, darin befand sich ein Knüppel mit Dornen des
Arkamusstrauchs daran. Er würde meinen Schädel zerschmettern wie eine reife Frucht. Alles was
ich noch hörte war das Summen der Wespen in meinem Rücken und das Grunzen des Orcs.
</P>

<P CLASS="P1">
Da blitzte auf einmal ein heller Schein herab, Blut spritzte überall auf mich und der Schädel
des Orcs lag vor meinen Füssen. Ich hörte ein Pfeifen als fuhr eine Sense durch die Luft und
sah die Wespen tot zu Boden fallen. Plötzlich stand er vor mir, ich war starr und wusste nicht
wer oder was er war, auf alle Fälle sah er Göttlich aus. Eingehüllt in eine blau metallic
schimmernde Rüstung, lange dreischneidige Krallen an beiden Händen, einem wunderschönen mit
Hörnern bestückten Helm, der aussah als wäre er ein magischer Spiegel.<br>
<br>
<I>"Tapfer bist du kleiner Waldelf, aber Zielen musst du noch lernen."</I><br>
Ich wusste nicht was ich sagen sollte und starrte ihn nur an.<br>
<I>"Du bist der Sohn von Wolfspfote Tauglanz dem Ranger ?"</I><br>
Ich nickte nur ohne einen Ton über die Lippen zu bringen.
<I>"Ich habe hier etwas für Dich."</I><br>
Er nahm eine seiner Krallen ab und gab sie mir. Ich schaute die Kralle an und war fasziniert
vom Anblick dieser wunderschönen Waffe.<br>
<I>"Lerne sie zu führen und du wirst auf dem richtigen Weg bleiben, für immer."</I><br>
Als ich aufschaute war er verschwunden. Ich hielt seine Waffe in der Hand, das Blut des Orcs
lief an meiner Kleidung herab und ich wusste nicht mal wer er wahr.
</P>

<P CLASS="P1">
Aber ich glaubte seinen Namen zuhören......Ganlul.......Gadlul, ich konnte ihn nicht genau
erfassen. 
</P>

