<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Shandor Darkangel <BR>
	<BR>
</P>

<P CLASS="P1">
	Mein Name ist Shandor Darkangel, ich wurde als Dunkelelf geboren, so heißt es zumindest. Ich 
	habe dunkle Haut, spitze Ohren und alles was dazugehört, dennoch habe ich ein Geheimnis das 
	mich den Kopf kosten könnte.
</P>

<P CLASS="P1">
	Meinen Vater habe ich nie kennengelernt an meine Mutter erinnere ich mich noch sehr 
	schwach; sie war nicht wie wir, sie war nicht wie ich, sie war etwas anderes. Damals 
	wußte ich es nicht, ich verstand auch nicht warum damals viele Männer in unsere 
	versteckte Hütte gekommen sind und sie töteten und mich nach Neriak verschleppten. Dort 
	lehrte man mir die übermächtige Kunst der Nekromantie. Aber ich merkte auch schon sehr 
	früh, daß ich ein Elf des Schwertes war. Sehr bald konnte ich meine Freunde im Schwertkampf 
	besiegen, was meinen Lehrmeistern der Nekromanten gar nicht gefiel.<br>
	Dadurch daß ich alle besiegen konnte, wurde der Neid in den anderen Kindern wach und 
	sie nannten mich einen ausgestoßenen, weil ich keine Eltern hatte  und den Zorn der 
	Necromanten zog ich auf mich, weil ich mich zuviel prügelte, als daß ich mich um meine 
	Bücher kümmerte.
</P>

<P CLASS="P1">
	Bis zu dem Tag als ich mich im Wald ein Wolf anfallen wollte. Ich hatte Angst und Hass und 
	unvorstellbare Wut und als der Wolf zum letzten Sprung ansetzte tötete ich ihn mit einer 
	bloßen Berührung aus meinen Händen. Die anderen Kinder kamen nun an und betrachteten mich und 
	den Wolf abwechselnd, als ob sie nicht wüßten, ob sie sich freuen sollten oder mich fürchten.
</P>

<P CLASS="P1">
	2 Tage später kamen Männer in mein Zimmer in dunklen Rüstungen und düsteren Blicken und 
	schleppten mich abermals in die Hallen der Angst in denen die Necromanten studierten und 
	bildeten mich zum dunklen Ritter des Innoruuk aus.
</P> 

<P CLASS="P1">
	Aber irgendwie wußte ich trotz allem, daß ich anders war und als ich einmal den Weg 
	auskundschaften sollte, ob unsere Leute in der Nacht in Rivervale einfallen können, 
	ohne auf großen Widerstand seitens der Halblinge zu stoßen, sah ich sie, eine Waldelfe, 
	und ich erinnerte mich an meine Mutter.
</P>

<P CLASS="P1">
	Heute bin ich ein Shattenkrieger und werde auch für immer einer bleiben, ich diene dem Hass 
	und der Hass dient mir, um meine Feinde zu besiegen. Niemand schert sich darum, wer ich war 
	oder woher ich komme, nur wer ich bin zählt für sie und für meine Gegner zählt das auch nur
	wenige Sekunden, wenn sie mein Schwert in ihrer Brust fühlen.
</P>

<P CLASS="P0">
	Finstere Grüsse<br>
	<I>Shandor Darkangel von Vallon Zek</I>
</P>

