<P CLASS="P1">
	Die wahre Power des Nekromanten kann nicht darin bestehen, einfach in die nächste Gilde zu gehen und um neue Zaubersprüche zu betteln. Nein, die wahrhaft schrecklichen Spells, welche den Nekromanten zur am meisten gefürchteten Kreatur machen, sind über ganz Norrath verstreut und warten darauf rekonstruiert zu werden.
</P>	

<P CLASS="P1">
	Die uralten Words bilden das Research-Fundament des Nekromanten. Sobald der Nekromant den fünften Zirkel (Level 16) erreicht hat ist es ihm möglich die Grundlagen des Research-Skills zu erlernen. Research steigt sehr schnell, und deshalb reicht es nur 1 Punkt beim Guild-Trainer zu investieren um den Skill zu aktivieren.
</P>	

<P CLASS="P1">
	Für Research benötigt der Nekromant zwei Dinge: Das <b>"Book of Dark Bindings"</b>, welches man in der Nekromanten-Gilde kaufen kann, und die entsprechenden "Words". Diese können bei diversen NPC's und Monstern überall in Norrath gefunden werden. Grundsätzlich findet man die "Ancient Words" für den Spell eines bestimmten Levels auf Mobs, die im gleichen Level bis ca. 5 Levels höher sind.
</P>	

<P CLASS="P1">
	Um einen Spell herzustellen gibt man die entsprechenden Komponenten (Words) in das Buch und drückt "Combine". Bei Erfolg hält man die Zauberspruchrolle in Händen und kann ihn im Zauberbuch verewigen. Versagt man, sind die Komponenten verschwunden.
</P>	

<P CLASS="P1">
	So, nachdem Du nun weißt wie das geht, gibt es zwei Möglichkeiten. Entweder Du stürzt Dich gleich ins Spells erzeugen, oder Du versuchst zuerst mit den <A HREF="ersteschritte.php">Research-Quests</A> den Skill zu steigern.
</P>

<P CLASS="P1">
	Viele sind der Meinung es ist einfacher, einfach möglichst viele Words zu kaufen. Man kommt oft sehr leicht an eine der benötigten Words und hat dann mehrere davon, aber die zweite Seite will einfach nicht droppen. Dann ist es am Einfachsten, die fehlenden Words per /auction von anderen Spielern zu kaufen. Ausserdem können einige Research-Zaubersprüche auch bei anderen Castergilden direkt gekauft werden.
</P>	

