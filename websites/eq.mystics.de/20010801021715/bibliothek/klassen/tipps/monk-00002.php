<P CLASS="P1">
	Monks sind definitiv zusammen mit den Rogue die Klasse die den meisten Schaden macht. Sie brauchen nicht wie andere
	Klassen Waffen sondern sind auch ohne R&uuml;stung oder Waffen eine Gefahr (auch wenn sie ohne R&uuml;stung mehr einstecken
	m&uuml;ssen). Wir haben nicht viele Hitpoints und auch nicht so gute R&uuml;stung wie andere Klassen, das Beste was wir
	tragen k&ouml;nnen ist z.B. Leder, Cured Silk (Monk only, Playermade), Mesh Armor und nat&uuml;rlich diverse magische Gegenst&auml;nde.<br>
	<br>
	Die Armorclass errechnet sich bekannterweise zum Teil aus der AC der R&uuml;stung und zum Teil aus Agility. Ein Problem
	der Monks ist: Sobald sie mehr als 14 an Gewicht tragen sinkt die AC um einige Punkte. Je schwerer wir also &uuml;berladen
	sind desto weniger k&ouml;nnen wir einstecken, der Schaden den wir machen wird dadurch allerdings nicht beeintr&auml;chtigt.
	Monks sind Meister im verhindern von Schaden, sie weichen aus, blocken etc.
</P>

