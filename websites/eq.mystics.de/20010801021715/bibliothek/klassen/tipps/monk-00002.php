<P CLASS="P1">
	<b>Pulling:</b><br>
	Der Monk ist ein Meister des Pullens. Durch die F&auml;higkeit Feign Death kann er Trains splitten und so statt
	10 Mobs nur 1-2 mitbringen. Um das effizient tun zu k&ouml;nnen m&uuml;ssen die anderen Gruppenmitglieder eins wissen:
	Sobald ein Spell (oder Song) auf den sich totstellenden Monk gecastet wird wird der Monk aufgedeckt, die
	Monster bemerken da&szlig; es nur ein Fake ist und hauen wieder auf ihn ein. Auch kann FD mi&szlig;lingen (obwohl nicht
	die Nachricht &quot;XY has fallen to the ground&quot; kommt) wenn der Monk unter 2 Bubbles Hitpoints hat, irgendwie lassen
	sich die Monster dann wohl nicht mehr so leicht reinlegen. Das Beste wenn ein Monk pullt ist, da&szlig; die Gruppe an
	ihrem Platz bleibt und auf den Monk wartet!<br><br>
	<b>Feignsplitting:</b><br>
	Bei Mobs unter Level 32 ist Pullen einfach f&uuml;r den Monk: Er greift einen Mob an (wahrscheinlich attackieren
	ihn daraufhin einige andere in der N&auml;he) l&auml;uft ein St&uuml;ck bis zu einem Platz und macht FD. Dann beobachtet er
	wann die Monster weggehen, das Attackierte bleibt meistens am l&auml;ngsten da... voila, ein einzelner Pull der nicht
	so Mana- bzw. HP-aufwendig ist wie eine ganze Horde.<br><br>
	<b>Chainpulling:</b><br>
	Diese Methode wird wohl meistens bei Mobs &uuml;ber Level 32 angewendet (so auch in den Planes), die den Monk sobald
	er FD gemacht hat nicht sofort vergessen. Der Monk greift auch hier wie sonst einen Mob an, l&auml;uft, FD... warten.
	Ein anderer Monk (oder eine andere Klasse mit Fernangriff) steht in einiger Entfernung und beobachtet. Sobald
	alle Mobs bis auf eines sich entfernt haben greift der zweite den Mob an der geblieben ist, so hat man auch einen
	einzigen Mob. Der Monk der FD gemacht hat wird jetzt aufstehen und so lange FD machen bis kein Monster mehr zu
	ihm zur&uuml;ckkommt (die meisten tun dies nicht wenn sie 1. Zu ihrem Spawnpunkt zur&uuml;ckgekehrt sind oder 2. Durch
	multiples FD den Monk vergessen haben)
</P>

