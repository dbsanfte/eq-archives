<P CLASS="P1">
	Spells die auf den Monk gecastet werden solange er FD macht (dazu z&auml;hlen auch Songs) decken sein FD auf, die
	Monster attackieren ihn wieder!<br>
	Auch sollten die Gruppenmitglieder den Monks vertrauen... wenn ein Monk RUN ruft, sollten sie dies befolgen auch
	wenn der Monk stehenbleibt! Der Monk kann die Mobs so lange aufhalten bis die anderen in relativ sicherer
	Entfernung sind und dann FD machen, es bringt keinem etwas wenn sie versuchen dem Monk zu helfen und dabei sterben!<br>
	Monks sind keine richtigen Tanks dazu fehlen ihnen die n&ouml;tigen Hitpoints! Sie k&ouml;nnen diese Rolle &uuml;bernehmen wenn
	es sein mu&szlig;, aber ansonsten sind sie eigentlich besser geeignet als Schadenausteiler! Am besten ist es wenn noch
	ein richtiger Tank in der Gruppe ist der Taunten kann! Und das ist ein harter Job, ich habe schon Warrior
	gesehen die 5 Level &uuml;ber mir waren und Probleme hatten einen Mob von mir wegzutaunten, weil wir einfach &quot;zu viel&quot;
	Schaden machen, und dazu kommt noch da&szlig; Flying Kick auch ein Taunt ist. Monks und Rogue sind die besten DoT die
	es gibt, man mu&szlig; sie nur am Leben halten *g* . Meistens wenn ich zuviel Schaden bekomme und es ist ein Taunter
	in der Gruppe gehe ich einfach ein paar Schritte zur&uuml;ck und lasse den Tank erst einmal etwas Schaden machen und
	komme dann wieder in den Kampf.
