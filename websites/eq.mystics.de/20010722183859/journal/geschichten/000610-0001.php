<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Tedadar Flammenpfeil<BR>
	<BR>
</P>

<P CLASS="P1">
Diese Geschichte beschreibt den Raid vom 25.03.00 aus den Augen des Rangers Tedadar
Flammenpfeil, welcher natürlich keinen kompletten Überblick über Verluste und Siege
auf seiner Seite hatte. Deshalb sind die Angaben wohl auch etwas verwischt mit der
Wirklichkeit,...<br>
<br>
<b>1.Der Weg nach Freeport</b>
</P>

<P CLASS="P1">
Gerade wieder erwacht von seinem Traum, und noch etwas schlaftrunken, drang an
Tedadars Ohren Kampflärm. Das kaum überhörbare schrille Sirren von magischen
Energien die durch wissende Hände gebündelt werden, das unechte Lachen eines
Skelettes und die knurrenden Laute eines kämpfenden Wolfes. Erwacht aus seinem
Dämmerzustand sah Tedadar sich um, bevor er die Ursache des Kraches sah,...
Granor ein Necromant und sein Pet kämpfen gegen einen War Wolf.
</P>

<P CLASS="P1">
Doch schon auf den ersten Blick erkannte er das Granor wohl kaum Hilfe brauchen
würde. Valeria Holyguard, ein silberglänzender Paladin, lenkte den Wolf von
seinen Angriffen ab und wich den Fängen geübt und sicher aus, und obwohl ihn
der Wolf auch manchmal erwischte so waren seine Kiefer und Krallen kaum stark
genug um seine Rüstung zu durchdringen. Auch ein kaum erfahrener Kämpfer würde
sehen wie dieser Kampf wohl ausgehen wird, es wirkte vielmehr wie ein Spiel mit
einem gefährlichen Spielzeug als wie ein Kampf um Leben und Tod.
Ja, der Palladin beschränkte sich sogar darauf nur das Tier weiter zu ärgern
und ihm auszuweichen. Sein 2-händiges Schwert mit dem er sonst schreckliche
Hiebe austeilte, verwendete er nur um einen Biss abzuwehren, oder fast sanft
eine Pfote wegzuschieben.
</P>

<P CLASS="P1">
Die rasende Wut des Tieres machte es blind für den wirklichen Angreifer.
Granor entzog dem Wolf immer wieder ein wenig von seiner Lebensenergie, um
sich damit zu stärken, und sein Pet, ein ekelerregendes Skelett, hieb immer
wieder mit den knochigen Fingern auf den Pelz des noch lebenden, aber schon
stark geschwächten Wolfes ein. <I>Und Druff!</I> sagte der Necromant auf
einmal. Valeria spannte die Muskeln, holte aus und ein Gewaltiger Hieb riss
eine klaffende Wunde in den Körper des Wolfes, welcher geplagt aufschrie, und
sich schon wegdrehte um der schrecklichen Klinge zu entgehen die rasend
zurückschwang. Diesmal entwischte das Tier nocheinmal dem Schwert und lief
schon humpelnd aus der Reichweite des Paladins. Tedadar der bisher nur der
Schlacht zugeschaut hatte sprach ein paar mystische Worte, und kleine
gruenschimmernde Wirbel schlangen sich um die Füße des Wolfes und verlangsamten
ihn, bis das Tier wegen der Erschöpfung keinen Fuss mehr vor den anderen setzen
konnte. Da war der Paladin schon heran und mit einem kraftvollen Hieb riss er
diesmal eine solch starke Wunde in den Leib des Wolfes, dass der Wolf winselnd
tod zusammenbrach.
</P>

<P CLASS="P1">
Während sich Granor dem Tier zuwendete und sorgfältig schaute ob er nicht noch
den Pelz verwenden könnte, drehte sich Valeria zu Tedadar um. <I>Ah, Hallo Ted!</I>,
sprach er erfreut, <I>da bist du ja</I>, und Tedadar antwortete lächelnd
<I>Na, hilfst du Granor, damit er stark genug für unseren Raid heute wird?</I>
Der Raid, schon das Wort klang spannend in den Ohren des Rangers, und er errinnerte
sich an einen Raid vor ein paar Wochen, den sie größtenteils erfolgreich an der
Grenze zu den Ländereien der Dunkelelfen gemacht hatten.
</P>

<P CLASS="P1">
<I>Ja</I> sagte er lachend zu dem Ranger <I>net, dass ihn die Darkies niedermachen.</I>
Auch er war schon gespannt auf den Raid, der dem Gildentreffen folgen würde.<br>
<I>Sollten wir uns nicht langsam auf den Weg machen?</I> fragte Tedadar.<br>
<I>Nein noch nicht, ich muss noch ein paar jüngere Lichter einsammeln</I> antwortete
Valeria. <I>Also hilf solange Granor. Ich geb dir dann Bescheid wenn ich zurückkomme</I>.
Und schon eilte er davon und verschwand glänzend im Wald.<br>
<I>Hallo Tedadar</I> sagte da Granor, der mit dem toten Wolfskörper fertig war und
einen Pelz in seine Tasche schob.<br>
<I>Hallo Granor, na wie gehts voran?</I> sprach Tedadar, der in einem grünen und
metallisch leuchtenden Kettenhemd gekleidet war.<br>
<I>Ja klappt ganz gut, Valeria hat mir gut geholfen.</I> antwortete der Necro
und sagte <I>Sollen wir weitermachen?</I><br>
Gesagt getan; Tedadar übernahm nun das Ablenken der Gegner und so töteten sie
noch ein paar der Wölfe und Löwen in der Umgebung, wobei sie aber Abstand zu
den Gnollen hielten, weil sie zu stark für Granor waren.
</P>

<P CLASS="P1">
<I>Ich bin auf dem Weg zur Brücke</I> hörte Ted auf einmal eine Stimme in seinem
Kopf. <I>Komme!</I> antwortete er Valeria, der nun scheinbar alle jüngeren Leute
aus der Gilde zusammengesammelt hatte, und wohl an der Brücke nach Nord Karana
eingetroffen war.<br>
<I>Tedadar! HILFE!!!</I> erklang Granors Stimme in Tedadars Kopf, und er eilte auch
schon den Hügel hoch von dem Granor noch einen Wolf herunterlocken wollte.
Granor war umringt von Gegnern. Überall rissen sich Fänge duch die Robe des
Necros, diesem heftigen Angriff konnte er wohl nicht mehr lange standhalten.
Tedadar kam nichteinmal mehr zum Nachdenken, er warf sich gleich in die Wölfe
und lockte einen von Granor weg und kämpfte gegen die Bestie. Der Wolf hatte keine
Chance; Tedadars Axt und Schwert rissen fürchterliche Wunden in den Wolf, und mit
einem gut gezielten Pfeil konnte der Ranger den Wolf noch auf der Flucht töten. Doch
im selben Moment hörte er ein menschliches Aufkeuchen hinter ihm und das dumpfe
Aufprallen eines leblosen Körpers auf den Boden,... es war zu spät, Granor fiel
unter den Angriffen der Wölfe. Tedadar konnte seinen Augen nicht glauben, harmlose
Wölfe die für ihn sonst kein Problem waren, hatten den Necro zerstückelt während
er thelepatische Nachrichten versenden musste! Das Gefühl des Versagens stieg ihn
ihm hoch,... <I>Tedadar? was ist los?</I> ... Valeria musste sich schon wundern
warum Tedadar noch nicht da war. Er erzählte ihm von Granors Tod. Betroffen schwiegen
sie einen Moment, aber Sie wussten sie konnten Granor nicht mehr helfen wieder
zu Kräften zu kommen, sondern mussten nach Freeport aufbrechen um ihr Gildentreffen
abzuhalten.<br>
<I>Schon der erste Tote noch bevor wir auf die Darkies gestossen sind,....fängt ja gut
an!</I>, sagte Tedadar zu Valeria mit einem bitteren Lächeln.
</P>

<P CLASS="P1">
Der Weg nach Freeport war einfach. Es hatte sich schon eine komplette Gruppe an
der Brücke eingefunden, die verschiedensten Klassen waren vertreten. Manchen sah
man ihre Jugend noch an, am fast wilden Blick in den Augen wenn sie in Richtung
mancher Monster spähten. Und mancher der Jüngeren schwor sich warscheinlich in
diesem Moment, einmal zurückzukommen und seine Kräfte mit denen der Kreaturen zu
messen. Aber es waren auch ein paar erfahrenere Helden anwesend, Crudelis zum
Beispiel, welcher ein schon stärkerer Druide war.<br>
<I>Ich kann uns alle zu den Commonlands Gaten</I>, sprach er, <I>dann sind wir
schneller da, wir sind eh schon spät dran!</I> Alle stellten sich auf und ringten
sich um ihn um auch mit erfasst zu werden wenn der Teleport beginnt. Schon sprach
der Druide ein paar unverständliche magische Worte und verschwand vor den Augen
der Versammelten,... aber nur er verschwand.<br>
<I>Verdammt, ich habe mich versprochen und nur mich teleportiert, bin gleich wieder
bei euch!</I> Tedadar und Valeria konnten ein Lachen nicht unterdrücken, und
grinsten noch immer schief als Crudelis wieder eintraf. <I>So nun sollte es klappen,
los gehts!</I> Der Ranger und der Paladin grinsten sich nocheinmal an, als auf
einmal ein Portal erschien und die gesamte Gruppe verschlang ...<br>
Orientierungsverlust und Benommenheit legten sich um die Sinne von Tedadar als er
wieder erschien, aber nach ein paar Sekunden stellte er fest, daß der Teleport
diesmal geklappt haben musste. Sie waren alle da und auch den Tieren und den Pflanzen
nach zu schliessen in den Commonlands angekommen. Der Druide sprach auf alle einen
Spruch, der die Leute agil und schnell machte, und schnell wie eine Meute Wölfe
rannten sie alle in Richtung Freeport zu dem Gildentreffen,...
</P>


