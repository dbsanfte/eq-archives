<P CLASS="P1">
	Research ist ein Skill um Zaubersprüche herzustellen. Die einzigen Klassen, die Research beherrschen (sollten) sind:
</P>	
<UL CLASS="P0">
<LI><A HREF="enchanter.php">Enchanter</A><BR></LI>
<LI><A HREF="magician.php">Magician</A><BR></LI>
<LI><A HREF="necromant.php">Nekromant</A><BR></LI>
<LI><A HREF="wizard.php">Wizard</A><BR><BR></LI>
</UL>
	
<P CLASS="P1">
	Der Skill ist ab Level 16 verfügbar, und um ihn zu aktivieren muß 1 Skillpunkt beim Guild-Trainer investiert werden. Danach steigt Research sehr schnell, also ist es nicht nötig mehrere Skillpunkte darauf zu verschwenden.
</P>	
	
<P CLASS="P1">
	Da die "Zutaten" für das Researchen teilweise recht schwer zu bekommen sind, gibt es eine
</P>	
<UL CLASS="P0">
<LI><A HREF="quest-good.php">Research-Quest für gute Rassen</A> bzw.<BR></LI>
<LI><A HREF="quest-evil.php">Research-Quest für böse Rassen</A>,<BR></LI>
</UL>
<P CLASS="P1">
	um den Research-Skill zu steigern. Schließlich gibt es nichts ärgerlicheres als beim Versuch, den schon dringendst benötigten Spell herzustellen, zu versagen. Die "Zutaten" sind nämlich auf alle Fälle weg, egal ob der Versuch erfolgreich war oder fehlgeschlagen ist!
</P>	
	
