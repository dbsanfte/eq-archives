<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Clawdeen<BR>
	<BR>
</P>

<P CLASS="P1">
	Cazicite .... dazu war ich geschl&uuml;pft, das wollte ich sein !
</P>

<P CLASS="P1">
	Ich nahm den langen Weg auf mich und watschelte nach Cabilis. Dort sollte es eine Gilde
	geben, die sich den Diensten Cazic-Thules verschworen hatte...und ich fand sie.
	Ich erlernte die Kampfschreie, die Lust am Leid der anderen. Wir folterten zun&auml;chst
	junge Schuppenw&ouml;lfe, sp&auml;ter dann Skorpione. Wir lernten, uns ihr Gift zunutze
	zu machen, indem wir ihm geschickt auswichen, unseren ganzen K&ouml;rper der Bewegung
	widmeten. Wir zerquetschten die giftigen Stachel der Skorpione mit einer einzigen
	Fussbewegung und erg&ouml;tzten uns am Jaulen der W&ouml;lfe, wenn wir unsere F&auml;uste auf
	ihre M&auml;uler hieben, erbarmungslos, Befriedigung empfindend.
	Viele Jahre waren vergangen, bis auch ich endlich zu den Aussserw&auml;hlten geh&ouml;rte, die
	in das Feld der Knochen gehen durften, um dort die Lehren des Gesichtslosen
	weiter zu vertiefen.
</P>

<P CLASS="P1">
	Nun ging es an die wahren Lehren des Lords....<br>
	Im ersten Zirkel beobachteten wir. Wir standen nur Handgriffe von Schattenkriegern
	entfernt, die unter den Angriffen der Skarab&auml;en ihr Leben aushauchten. Einen
	Kick entfernt von Schamanen, deren Flammenexplosionen versagten und auf die
	am Ende ihres Manas auch das Ende ihres Lebens wartete.
	Und wir lernten aus all ihren Toden, verbesserten unsere Kraft, erweiterten
	unsere Macht, versanken in exzessive Trance ob ihrer Qualen und letzten Atemz&uuml;ge.
	Der Orden war mein Leben und der Tod seiner Feinde, so stand es geschrieben und
	so wollte ich ewig sein !
</P>

<P CLASS="P1">
	Cazic Thule war nicht sehr angetan von seinen weiblichen Anh&auml;ngern. Den Schriften
	zufolge erdachte er all ihnen ein anderes Schicksal und nur dem Abt verdanke
	ich, dass ich den Lehren des Allm&auml;chtigen n&auml;her kam.
	Ich musste mich in Geduld &uuml;ben, mehr Schmerz ertragen, mehr Blut opfern ... doch
	eines Tages war es dann doch auch f&uuml;r mich so weit und ich sollte den ersten
	Ritus der Initiierung erfahren.
	Der wahre Glaube einer Cazicite erweist sich in den vier heiligen Riten. Vier
	Finger hat ein Iksar, vier Riten hat er zu erf&uuml;llen. Die Iksar sind die einzig
	wahre Gefolgschaft des Lords....
</P>

<P CLASS="P1">
	Ich betrat den Tempel, nur von den Schuppen bekleidet, wie mein Ei mich entliess
	und Cazic-Thule mich zu sich kommen liess.
	Die erste Initiierung ist mit der Geburt gleichzusetzen, nur dass sie der erste
	Schritt in die Welt des Gottes ist.
	Mein Abt nahm mich in Empfang und f&uuml;hrte mich zur ersten Aufgabe innerhalb des Ritus.
	Ein Schuppenwolfwelpe kauerte im Kreis der Kerzen und meine Aufgabe war es, ihn mit
	meinen F&auml;usten zu zertr&uuml;mmern ... ich erntete anerkennende Blicke der M&ouml;nche, als
	sein verkr&uuml;ppelter K&ouml;rper vor ihnen lag.
	Die zweite Aufgabe dann bestand im Auftragen der ewigen Maske mit den geheimen
	Farben, die noch &uuml;ber den Tod hinaus bestehen bleiben. Man w&auml;hle seine Farben
	und sein Muster genau, denn dies ist es, was einen Iksar ewiglich kennzeichnet
	und es den Weisen erlaubt, tief in die dunkelste Seele zu schauen.
	Meine Wahl aus dem Blau des Meeres und dem Orange des Ewigen Sonnenunterganges
	fand Gefallen, und so wurde ich zur dritten Aufgabe gebracht.
</P>

<P CLASS="P1">
	Ein Nest voller Eier lag zu meinen F&uuml;ssen und selbst ein Fremder der anderen
	Kontinente h&auml;tte erkannt, dass es sich um Eier von uns, den Iksar, handelt.
	Ich z&ouml;gerte, mein eigenes Volk zu vernichten, doch nicht lang genug, um den
	Argwohn der Gilde zu wecken ... dann zertr&uuml;mmerte ich sie nach und nach. Beim
	vierten und letzten Ei dann war ein leises Pochen zu vernehmen, doch noch bevor
	das Junge sich einen Weg nach draussen bahnen konnte, versanken meine F&auml;uste
	in seinem sch&uuml;tzenden Heim ... und in seinem Sch&auml;del.
	Mein K&ouml;rper war bereits geziert von einigen Spritzern Blut und von meinen Fingern
	tropfte noch wenig von dem Hirn des Nichtgeschl&uuml;pften, als man mich in den
	letzten Raum zur letzten Aufgabe f&uuml;hrte.
</P>

<P CLASS="P1">
	Ich stockte. Inmitten des Raumes lag ein junges Iksarm&auml;dchen auf dem Boden. Sie
	muss vor etwa vier Wochen geschl&uuml;pft sein, doch irgendetwas mit ihr schien nicht
	zu stimmen. <I>&quot;Sie ist 8 Wochen alt und steht kurz vor der Geschlechtsreife.
	Unsere J&uuml;nger haben sie in Cabilis entdeckt. Sie ist Tochter eines Schamanen
	und einer Kriegerin, doch schw&auml;chlich und mangelhaft. Man nannte sie Cazice, welch
	Blasphemie. Gib ihr einen neuen Namen !&quot;</I> sprach der Abt.
	<I>&quot;Miniwatu&quot;</I> antwortete ich spontan.
	<I>&quot;Gut, so soll sie heissen.&quot;</I> nickte der Abt. <I>&quot;Und nun ... t&ouml;te
	sie !&quot;</I>
</P>

<P CLASS="P1">
	Ich stand wie versteinert. Sie war unschuldig und jung ... warum sollte ich das tun ?
	<I>&quot;Cazic Thule duldet keine minderwertigen Kreaturen, schon gar keine Iksar. Wir
	sind das Volk des Gesichtslosen und f&uuml;hren ihn zu Ruhm auf ganz Norrath ... wie soll
	das werden, wenn solche Kreaturen unser Volk mit Nachkommen besudeln ? T&ouml;te sie !&quot;</I>
	Ich ging langsam auf sie zu und blickte ihr direkt in die Augen. Sie verstand, sie
	hatte Angst ... doch es befriedigte mich nicht.
	Ich hatte viele Sterbende und Tote gesehen, doch sie waren Getier oder unf&auml;hige
	Krieger. Wer in den Krieg zieht oder K&auml;mpfe f&uuml;hrt, ohne das Risiko abzuw&auml;gen, der
	sollte in Cazic-Thule´s H&ouml;llen leiden und lernen ... aber eine Iksar, die
	offensichtlich nicht nach Ruhm oder Ehre, sondern nur nach Leben trachtete?
	Die schon dachte und wollte und &uuml;ber all unsere Emotionen verf&uuml;gte? Das war nicht
	dasselbe wie die Aufgaben zuvor !
</P>

<P CLASS="P1">
	Diesmal war es mir nicht gelungen, den Argwohn der Gilde nicht zu wecken.
	<I>&quot;Du z&ouml;gerst ?&quot;</I>
	Ich holte tief Atem, dann antwortete ich:
	<I>&quot;Ihr Ehrw&uuml;rdigen, ich kann nicht glauben, dass der Lord den Tod einer
	Unschuldigen von uns w&uuml;nscht, nur weil sie Unschuld in sich tr&auml;gt. Deshalb lehne
	ich diese Aufgabe ab und bitte darum, sie zu verschonen ... mein Leben dagegen
	liegt nun in Euren H&auml;nden.&quot;</I>
	Ein Raunen ging durch den Tempel, ich hatte Unfassbares getan.
	Der Abt trat auf mich zu und fragte :
	<I>&quot;Glaubst du denn etwa an ein Paradies ?&quot;</I>
	Ich &uuml;berlegte kurz, dann erwiderte ich :
	<I>&quot;So wie es 44 G&ouml;tter gibt, so gibt es 44 Himmelsreiche. Die H&ouml;lle von
	Cazic-Thule ist es, was wir zu ertragen streben, doch diese da wird ihr Paradies
	woanders finden. All das ist ein Paradies, was die inneren W&uuml;nsche erf&uuml;llt.&quot;</I>
</P>

<P CLASS="P1">
	Dem langen Schweigen folgte nach einer Weile eine aufgeregte Unterredung, dann
	hatte man entschieden:
	<I>&quot;Du wirst weiter Cazicite sein d&uuml;rfen, doch bist du zu den unteren
	Dienern verdammt und wirst dir sein Wohlwollen erbetteln m&uuml;ssen. Nie wieder
	wird dir der Ritus offenstehen und du wirst gebranntmarkt als Gl&auml;ubige eines
	Paradieses, gar mehrerer. So tr&auml;gst du nun f&uuml;r alle sichtbar den Namen Cherubia
	und deine Maske soll dich ewig an die Unterschiede zwischen uns, den wahren
	J&uuml;ngern, und dir erinnern. Du bist entlassen.&quot;</I>
	Ohne ein weiteres Wort verliess ich den Tempel und ging ins Haggle Baron in
	Cabilis, um nachzudenken.
	Jedes weitere Wort an dieser Stelle h&auml;tte mir den Tod gebracht, und nach dieser
	Erfahrung und in dieser pl&ouml;tzlich entstandenen Glaubenskrise wollte ich lieber
	erst herausfinden, welche Paradiese es wirklich gab und welches das f&uuml;r mich bestimmte ist.
</P>

<P CLASS="P1">
	Cherubia ... und ich werde durch Norrath wandern und mein Paradies finden, eines Tages.
</P>

<p class="P0" ALIGN="CENTER"><I>Clawdeen</I></p>

