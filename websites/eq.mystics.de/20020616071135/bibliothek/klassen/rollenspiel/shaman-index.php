<P CLASS="P0">
	Brüder und Schwestern!
</P>

<P CLASS="P1">
	Seid mir gegrüßt.<br>
	Mein Name ist Boromier, und ich spreche zu euch als Vertreter der Shamanen von Halas.
</P>

<P CLASS="P1">
	Nicht umsonst gelten wir unter den Barbaren als die Führer, unsere Weisheit und unsere Macht 
	verschaffen uns den Respekt, den wir verdienen. Denn wir wurden von der Macht des Tribunals 
	berührt, wir haben die Gabe, die Kraft der Götter in uns zu kanalisieren und so Wunder zu wirken, 
	die einem normalen Sterblichen nicht möglich sind.
</P>

<P CLASS="P1">
	Jedoch sind wir nicht annähernd so schwach wie die Scharlatane der Menschen und Elfen, deren 
	Magie verderbt ist, die nicht anerkennen, dass allein den Göttern und den von ihnen Begünstigten 
	die Fähigkeit gegeben sein sollte, Magie, wie sie es nennen, zu wirken. Sie reißen mit Gewalt an 
	sich was ihnen nicht bestimmt ist und verärgern somit die Götter. 
</P>

<P CLASS="P1">
	Zu Beginn Eurer Laufbahn werdet ihr Euch im Vergleich zu Euren kettenrasselnden, 
	messerschwingenden Kameraden der Kriegerkasten oder den lauten, unbeherrscht um sich 
	zaubernden Mitstreitern der Wizardkaste eher benachteiligt fühlen.
</P>

<P CLASS="P1">
	Die Schwäche ihrer Magie ist daran zu erkennen, dass sie nicht fähig sind, Rüstungen aus dem 
	Fleisch der Erde zu tragen. Schon ein klein wenig Metall in einer Rüstung macht es ihnen 
	unmöglich, die geraubten magischen Kräfte einzusetzen. 
</P>

<P CLASS="P1">
	Doch auch Leder, gewonnen aus der Haut der toten Tiere, verhindert den wirksamen Einsatz ihrer 
	"Zaubereien". Der Umgang mit Waffen ist ein weiterer Makel auf dem Antlitz jener Magiewirker. 
	Da ihre Magie im Gegensatz zu der unseren von den Göttern gestohlen ist, sind sie gezwungen 
	ständig über Büchern zu sitzen und ihre Zauber weiter zu studieren und sie an sich zu klammern, 
	wie das Kleinkind es mit dem Holzschwert tut, dass der Vater ihm nehmen möchte.
</P>

<P CLASS="P1">
	Wir, die Gesegneten des Tribunals, sind von jenen Fehlern unbetroffen. Die Macht der Götter 
	strahlt durch uns, sodass wir auch die stärkste Panzerung tragen können, ohne fürchten zu 
	müssen, davon Einbussen tragen zu müssen.<br>
	Auch müssen wir nicht um unsere Magie mit den Göttern kämpfen, sie machen sie uns zum 
	Geschenk, auf dass wir sie weise einsetzen und damit unserem Volke vor allem anderen 
	Helfen. 
</P>

<P CLASS="P1">
	Dieser Umstand befähigt uns, auch den Umgang mit Waffen zu trainieren. Unser Kodex verbietet es 
	jedoch, Schnitt- oder Stichwaffen zu verwenden, denn wir erhielten von den Göttern ein Zeichen: 
	Nicht die Zerstörung soll unser Antrieb sein, nein wir sollen heilen und helfen. Da wir in einer 
	Welt des Kampfes, des Verrats und des Todes leben haben wir jedoch gelernt, uns zur Wehr zu 
	setzen. So verwenden wir Waffen, die allein unsere Kraft in Schaden umsetzen. Diese Wuchtwaffen 
	mögen Knochen brechen, niemals jedoch werden wir so dem Gegner einen Arm abtrennen oder ein 
	Auge ausstechen. Den Götter gefiel diese Entscheidung, denn Sie liessen uns unsere Macht auch,
	als wir unsere Kriegerbrüder im Kampf gegen die Horden der Ice-Goblins unterstützten.
</P>

<P CLASS="P1">
	Unsere Fähigkeiten sind sowohl für unsere Freunde hilfreich wie für unsere Feinde tödlich. Wer 
	auf unserer Seite steht, dem verleihen wir die Kraft des Bären, das Auge des Falken oder die 
	Schnelligkeit des Wolfes. Wir können die Seele eines Freundes an einen Ort binden und seine 
	Wunden heilen. Den Feind jedoch lassen wir die Macht der Götter auf ganz andere Weise erfahren: 
	Die Kraft der Flamme versengt ihn, die Kälte des Eises lässt ihn schaudern und erstarren, wir 
	infizieren ihn mit dem Gift der Schlange und nehmen ihm die Kraft, die ihn die Waffen führen 
	lässt.
</P>

<P CLASS="P1">
	Die Fähigkeit zu heilen jedoch ist es, die die Götter uns allem voran zugestehen. Bereits auf 
	niedrigen Stufen der Ausbildung und Weihe ist es uns möglich, Wunden zu heilen und Krankheiten 
	ein Ende zu bereiten. Auch Gifte oder Blindheit verlieren durch uns ihren Schrecken. 
</P>

<P CLASS="P1">
	Dies also ist es, wozu das Tribunal uns auserkoren hat: Unseren Freunden und Kameraden zu 
	helfen. Wenn dies am Besten durch Waffengewalt getan ist, dann jedoch ist uns auch dies nicht 
	verboten.
</P>

<P CLASS="P1">
	So lebet nun wohl, Brüder und Schwestern. Möge das Tribunal über Euch wachen und Euch auf Euren 
	Pfaden beschützen.
</P>

<P CLASS="P0">
	<I>Boromier</I>, Ratsmitglied der Schmanen von Halas
</P>

