<P CLASS="P1">
	Magier beschwören die geheimen Künste und zaubern so Dinge und Lebewesen herbei. Sie nutzen diese Macht um
	vom simplen Brot bis hin zum Monster, das sie für sich kämpfen lassen, alles herauf zu beschwören. Magier
	besitzen nicht die offensive Macht, wie sie die Zauberer besitzen, ihr Können auf dem Gebiet ist dennoch
	beachtlich.
</P>	

