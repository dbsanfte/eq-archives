<P CLASS="P5">
	So Du hast Dich also entschieden!
</P>

<P CLASS="P1">
	Du möchtest der edlen Rasse der einzigen und wahren Herrscher der Dunkelheit 
	angehören? Dann solltest du Dir ein paar Dinge hinter die Ohren schreiben, solange Du sie 
	noch hast. Viele Dämonen beherrschen die Seele eines Dunkelelfs, nicht aber der Dämon 
	der Feigheit! Merk es Dir gut, denn Du wächst auf in einer Welt von Feinden. 
	Das einzige was zwischen Dir und dem Tot steht, ist deine eigene erbarmungslose 
	Konsequenz. Nur ein toter Feind ist ein guter Feind. Nun, wer ist Dein Feind? 
	Präge es dir tief in deinem Herzen ein, JEDER. Dort draußen in der Welt wimmelt 
	es von ignoranten Irren, die sich anmaßen, Dein Richter und Henker zu sein.
</P>

<P CLASS="P1">
	Merk Dir noch ein zweites. Du bist ein Dunkelelf! Du bist kein hirnloser Barbar, 
	kein hochnäsiger Hochelf und kein dünkelhafter Erudit zu sein. Du bist auch kein
	saufender grölender Zwerg (verzeih D.). Du bist kein Menschen Paladin, du mußt 
	niemandem etwas beweisen, DU bist der wahre Herrscher. Du gehörst einem 
	Kulturvolk an. Du bist kein Sadist und kein Schlächter! Solltest du jemand um 
	einen Kopf kürzer machen, dann nur weil du ein neues Trinkgefäß brauchst. Nichts 
	was Du tust sollst Du aus Selbstzweck tun. Deine Grausamkeit soll eine kalte 
	sein. Ein Dunkelelf verfällt nicht in Blutrausch. Er beleidigt und verspottet 
	keine Feinde. Oder beleidigst du einen Regenwurm bevor du ihn zertrittst? Und 
	umgekehrt? Beachte nie einen Wurm der Dich beleidigt! Schneid im die Kehle durch, 
	aber beachte ihn nicht.
	So, das sind die Grundvorraussetzungen Deines Charakters. Kannst du Sie erfüllen?
</P>

<P CLASS="P5">
	Überlege Dir genau woher Du stammst
</P>

<P CLASS="P1">
	Du gehörst einer Rasse an, für die der Tot zum Alltag gehört. Er ist nichts 
	erschreckendes für Dich. Du bist mit Ihm aufgewachsen. Er gehört so zusagen zur 
	Familie. Wenn Du als Kind beim Abendessen warst kam dein toter Großvater als 
	Lych zu Besuch. Untote Necromanten haben dich zu Bett gebracht. Andere Kinder 
	hatten Angst vor dem schwarzen Mann, Dir hat er Gute - Nacht - Geschichten 
	erzählt.
</P>

<P CLASS="P1">
	Du bist in einer strengen Hierarchie aufgewachsen. Die Führer deines Volkes 
	sind alle den schwarzen Künsten verschrieben. Sie bestimmen Dein alltägliches 
	Leben.
</P>

<P CLASS="P1">
	Dein Volk lebt in einer unterirdischen Stadt. Laß dir nie anmerken, daß die 
	Oberwelt dir fremd ist, aber sie ist dir fremd! (Tip: Stell dir vor du bist 
	ein japanischer Samurai, der im frühen Mittelalter aus einer feudalen 
	kultivierten, ritualisierten Gesellschaft ins barbarische Westeuropa kommen 
	würde).
</P>
	
<P CLASS="P5">
	Die Klassen deiner Rasse
</P>

<P CLASS="P1">
	<B>Der Necromant</B><BR>
	Du bist das Maß aller Dinge, Du stehst an der Spitze der besten Rasse 
	Deiner Welt, dann verhalte Dich entsprechend! Sprich nicht mit dem Pöbel, 
	außer du erteilst Befehle.
</P>
	
<P CLASS="P1">
	<B>Alle anderen magiebegabten Klassen</B><BR>
	Ihr habt es leider nicht ganz geschafft. Ihr seit zwar die besten eurer Klasse, 
	aber tief in eurem Inneren ärgert es euch keine Begabung zur Necromantie 
	bekommen zu haben. Um so mehr verachtet ihr alle Nichtmagier.
</P>
	
<P CLASS="P1">
	<B>Der Shadowknight</B><BR>
	Mag die ganze Welt zittern, wenn sie Dich sieht. Mögen Trolle weiche Knie 
	bekommen und Paladine den Tag verwünschen. Du bist in einer Rasse von Magiern 
	nur jemand mit minderen Fähigkeiten.
</P>
	
<P CLASS="P1">
	<B>Der Krieger</B><BR>
	Ihr seit ein Nichts, ein Niemand, Ihr tut Dienst in den Grenztruppen oder 
	ihr seit Leibwächter der Magier, Ihr seit na ja, nützlich ist das passende 
	Wort. Als Krieger bleibt dir nur das Streben nach Perfektion mit der Waffe 
	(Die Regeln erlauben alle Arten von Waffen, ich finde das Schwert ist die 
	einzige Waffe die sich für einen Krieger ziemt)
</P>
	
<P CLASS="P1">
	<B>Der Dieb</B><BR>
	Es gibt Diebe bei uns, ich bin erstaunt. Wenn es sie gibt, was ich als Krieger 
	mir nicht vorstellen kann, dann könnt ihr Dank den Fähigkeiten unseres Volkes 
	nur zu den besten eurer Klasse gehören. Mir ist zu Ohren gekommen daß irgend so 
	eine Winzlings-Rasse ebenfalls in Anspruch nimmt gute Diebe zu sein. Du hast es 
	nicht nötig, Dich mit solchem Volk abzugeben, außer sie erfüllen für Dich einen 
	Zweck (Hmm, Essen auf Beinen?)
</P>

<P CLASS="P5">	
	Religion
</P>

<P CLASS="P1">
	Darüber kann und darf ich als Krieger nichts sagen, sollte ich etwas erfahren 
	erzähl ich es ein andermal, wenn es mir die Priester gestatten.
</P>

<P CLASS="P5">
	Interaktion mit anderen Rassen
</P>

<P CLASS="P1">
	Im Gegensatz zu anderslautenden Gerüchten reden wir natürlich schon ab und zu 
	mit diesen anderen Völkern. Aber wir biedern uns nicht an.
	Du willst an einer Jagdgesellschaft gegen Gewürm teilnehmen. Nur zu, sie 
	brauchen Dich. Sag niemals bitte, ein Darkelf bittet nicht! Das kann doch 
	nur heißen: Es sieht so aus, als ob ihr ohne mich verloren seit! Dir gefällt 
	eine Waldelffrau, schwer verständlich aber es kann ja sein, dann schmalz Sie 
	im Namen der Hölle nicht an, wie ein liebestoller Barde, bring ihr die Köpfe 
	ihrer Feinde oder überreiche ihr ein Schwert, damit kann sie mehr anfangen, 
	als mit Blumen. Wenn sie nicht ganz blöd ist, weiß sie eh das Du Sie magst, 
	schließlich lebt Sie noch. Ich finde das muß auch reichen als Zuneigung anderen 
	Völkern gegenüber "Sie am Leben lassen".
</P>
	
<P CLASS="P1">
	Noch eins obwohl es so selbstverständlich ist, daß ich es beinah vergessen habe 
	zu erwähnen:
	Für einen Dunkelelf ist ein Wort ein Wort wir sind keine Schwätzer und 
	keine Lügner. Wir sind ein Volk von Ehre. Gibst Du einem Wurm Dein Wort, 
	dann gilt es! Aber laß Dich nicht täuschen in der weiten Welt. Prüfe mit wem 
	Du Dich einläßt, auf daß deren Unehre nicht auf Dich abfällt
</P>
	
<P CLASS="P0">
	<I>Erlic, Darkelf Warrior</I>
</P>

