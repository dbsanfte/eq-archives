<P CLASS="P0" ALIGN="CENTER">
	Von Clawdeen<BR>
	<BR>
</P>

<P CLASS="P1">
	Auch, wenn ich noch sehr jung bin, so gibt es doch bereits vieles aus meinem Leben zu berichten. Im Grunde
	begannen meine ganz pers&ouml;nlichen Abenteuer bereits bei meiner Geburt.
</P>

<P CLASS="P0">
	In Kelethin, der Stadt der Waldelfen, ist es &uuml;blich an in der Nacht geborenen Kindern ein Ritual zu
	vollziehen. Das hat schon eine uralte Tradition, um zu verhindern da&szlig; Waldelfen aufwachsen, die sich
	dem Wesen nach zu Teir'Dal entwickeln w&uuml;rden, den von Grund auf b&ouml;sen Dunkelelfen. Bei jenem Ritual
	nimmt die Amme das Neugeborene mit hinauf zur h&ouml;chsten Platform der Stadt. Dort st&ouml;&szlig;t sie einen
	markersch&uuml;tternden Schrei aus der von den Ammen in jahrelanger Ausbildung erlernt wird, um das Volk
	der Waldelfen zu alarmieren. Jede Elfe der Stadt wei&szlig; nun, da&szlig; ein Kind in der Nacht geboren wurde
	und die Tradition gewahrt werden mu&szlig;, und so versammeln sich die B&uuml;rger Kelethins unter den Platformen
	der Stadt am Boden von Norrath und warten.
</P>

<P CLASS="P0">
	Die Amme wirft das schreiende Kind hinab in die Tiefe und entweder wird es von einer Waldelfe am Boden
	aufgefangen und &uuml;berlebt, oder aber es stirbt. Sei es beim Sturz an sich, oder an einem Trauma, das dem
	Sturz meist binnen zweier Tage folgt.
</P>

<P CLASS="P0">
	So war es auch an mir, diesen Sturz zu &uuml;berleben oder als zuk&uuml;nftiges Wesen der Teir'Dal zu sterben,
	bevor es soweit kommen konnte. Jedoch war gleich diese erste Stunde meines Lebens nicht ohne
	Besonderheiten, denn mich hatte der schrille Schrei der Amme derart erschreckt, da&szlig; ich mein emp&ouml;rtes
	Geschrei &uuml;ber diese gr&auml;&szlig;liche Erfahrung der Geburt schlichtweg verga&szlig; und so unwissentlich in fast
	selbstm&ouml;rderisch anmutender Weise stillschweigend hinab in die Tiefe sauste.
</P>

<P CLASS="P0">
	W&auml;hrend die Elfen am Boden angestrengt auf ein n&auml;herkommendes Gebr&uuml;ll lauschten, geno&szlig; ich
	nichtsahnend den Wind, der mir um die Ohren pfiff. Pl&ouml;tzlich war mein Flug beendet und ich fand mich
	in den starken Armen eines alternden Mannes wieder.<br><br>
	<I>&quot;Ich wu&szlig;te genau, da&szlig; Tunare mich nicht narrte, als sie mir eine Vision von Dir sandte, mein Kind.
	Genau zu dieser Zeit und an diesem Ort w&uuml;rde sie mir eine Sch&uuml;lerin schicken, so lie&szlig; sie mich tr&auml;umen.
	Sei willkommen, Tochter Tunares.&quot;</I><br>
</P>

<P CLASS="P0">
	Mit diesen Worten schlo&szlig; mich der Mann in seine Arme und w&auml;rmte mich unter seinem Mantel. Dann machte
	er sich mit mir auf den Weg zum Geburtshaus. Das Volk der Waldelfen war &uuml;ber meine Ankunft in Norrath
	sehr erstaunt, denn nie zuvor hatte es bei den Ritualen ein lautlos fallendes Kind gegeben.
	Aufgrund dieses Vorfalles nannte man mich Clawdeen, die auf Samtpfoten Wandelnde.
</P>

<P CLASS="P0">
	Nachdem es neue Abenteuer und Kuriosit&auml;ten zu berichten gab und das allgemeine Interesse an mir
	abebbte, verschwand der wundersch&ouml;ne Zusatz mit den Samtpfoten schon wieder aus meinem Leben und
	geblieben ist Clawdeen. Der &auml;ltere Mann blieb, umsorgte meine Mutter Sulamon und mich.
	Sein Name war Gereus und er war Druide. Mit allerlei materiellem Gut, von dem er sagte, er k&ouml;nne
	derlei nicht gebrauchen, beschenkte er meine Mutter und als nach meinem ersten Geburtstag ihre
	m&uuml;tterliche Milch versiegte, nahm er mich mit sich um seine Visionen zu erf&uuml;llen.
</P>

<P CLASS="P0">
	Ich habe meine Mutter nie wieder gesehen. Jahre sp&auml;ter erz&auml;hlte man mir, sie sei fortgezogen, um
	meine Wege, die Gereus f&uuml;r mich geplant hatte, nicht zu st&ouml;ren und mich zu vergessen.
	An sie erinnere ich mich nicht, wohl jedoch an eine wundervolle Kindheit, die ich nahe Kelethin
	in einer Waldh&uuml;tte verbrachte. Ich lief Stunden barfu&szlig; durch die Wiesen, lernte Kr&auml;uter und Pilze
	kennen und sammeln, spielte nachts mit schwarzen W&ouml;lfen und lauschte vertr&auml;umt den Geschichten der
	B&auml;ume um mich herum.
</P>	

<P CLASS="P0">
	Als ich dann das 5. Lebensjahr erreichte, begann Gereus mit meiner Ausbildung. Er lehrte mich im
	Kn&uuml;ppelkampf gegen riesige Wespen und Flederm&auml;use und f&uuml;hrte mich langsam an die Schule der Magie heran.
	Er zeigte mir, wie ich Mana dazu nutze, kleine Wunden zu heilen und wie man dem gr&ouml;&szlig;ten Organ unseres
	K&ouml;rpers, der Haut, die Kraft und St&auml;rke der B&auml;ume gab.
	Es waren Jahre des Kampfes, der Disziplin, der Magie und der Spiritualit&auml;t, die ich von da an bei und
	mit Gereus erlebte, bis er mich zu den Soldaten Tunares in Kelethin brachte.<br><br>
	<I>&quot;Unser gemeinsamer Weg ist hier zu Ende, Clawdeen. Ich habe dich gelehrt, was ich als
	alternder Mann dich lehren kann. F&uuml;r deine weitere Ausbildung wird diese Gilde sorgen, die einst
	auch mich lehrte, was immer ein Druide wissen mu&szlig;. Ich bin &uuml;berzeugt, da&szlig; du einmal unserem Stand
	Ehre machen wirst und werde deine Wege nicht aus den Augen verlieren, auch wenn du dies vielleicht
	einmal denken wirst. Und nun leb wohl, mein Kind.&quot;</I><br><br>
	V&auml;terlich k&uuml;&szlig;te Gereus meine Stirn, dann lie&szlig; er mich allein zur&uuml;ck.
</P>

<P CLASS="P0">
	Jahre sp&auml;ter, als man mich zur vierten Stufe der Magie bef&ouml;rderte, glaubte ich, sein Gesicht in der
	Menge bei den Feierlichkeiten zu entdecken und war mir von da an sicher, ihn immer bei mir zu haben.
	Danach, mit meinen jungen 9 Lenzen, f&uuml;hlte ich mich schon bereit, die ersten gr&ouml;&szlig;eren K&auml;mpfe zu wagen.
	Ich wollte ausziehen, um die K&auml;mpfe gegen die Orks zu unterst&uuml;tzen und traf bald Mitstreiter auf meinen
	noch kindlich-kriegerischen Wegen.
</P>

<P CLASS="P0">
	Doch nicht nur Mitstreiter, sondern auch Freunde bereicherten mein Leben, doch dies ist eine andere Geschichte.....
</P>

<P CLASS="P0">
	<I>Clawdeen</I>
</P>

