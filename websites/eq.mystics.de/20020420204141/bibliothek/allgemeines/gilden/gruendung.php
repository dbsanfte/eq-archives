<P CLASS="P1">
	Wenn Du Dich entschieden hast, daß keine der bestehenden Gilden deinen Wünschen und Zielen entspricht, hast Du die Möglichkeit eine eigene Gilde zu gründen. Dazu mußt Du einen passenden Namen haben, der nicht gegen die Namensregelung von Verant verstößt. Ebenso mußt Du die Ziele deiner Gilde schriftlich vorbereiten, für welches (Un-)Recht Du mit Deinen Kameraden in Norrath stehen möchtest. Ebenso benötigst Du insgesamt 10 Personen, um deine Gilde zu gründen.
</P>	

<P CLASS="P0">
	Die folgenden sieben Punkte mußt Du zur Gründung beachten und einhalten:
</P>

<OL CLASS="P1">
	<LI>
		Die Person die der Gildenmeister werden soll, muß sich auf dem Chatserver einloggen und den Befehl #makeguild eingeben. Auf diese Weise wird ein privater Gildenkanal ("Raum") erstellt. Der Name des Raumes sollte dem späteren Namen der Gilde entsprechen, kann aber während der Erstellung der Gilde noch geändert werden.
	<BR><BR></LI>
	<LI>
		Sobald der Chatraum erstellt ist, muß der Gildenmeister die neun weiteren Mitglieder in diesen Raum einladen. Dafür muß der Gildenmeister ihnen den Namen des Raumes und das Paßwort sagen.
	<BR><BR></LI>
	<LI>
		Sobald die 10 Personen im Raum sind und jeder von ihnen den Befehl #guildapprove ausgeführt hat, kann der Gildenmeister mit der #makeguild-Funktion zum Gildenerstellungsmenü kommen. 
	<BR><BR></LI>
	<LI>
		Im Gildenerstellungsmenü muß der Gildenmeister nun eine Zusammenfassung der Ziele seiner Gilde eingeben. Diese Zusammenfassung sollte die Rolle und wie man sie in Norrath spielen will beschreiben. Details über die innere Struktur und den Aufbau der Gilde müssen nicht in diese Zusammenfassung. In diesem Menü kann auch noch einmal der Name der Gilde editiert werden. Sobald man den FINISH Button anklickt, werden die Daten zur Überprüfung an Verant übermittelt.
	<BR><BR></LI>
	<LI>
		Der Gildenmeister muß nun regelmäßig den Status seiner Gilde überprüfen. Dies kann er auf dem Chatserver mit dem GUILDS Button machen. Es ist möglich, daß er Nachbesserungen durchführen muß. Dies kann den Namen oder die Ziele betreffen. Sollte dies der Fall sein, wird er entsprechende Anmerkungen vorfinden und kann seine alten Eingaben editieren und erneut absenden.<br>
		<B>WICHTIG:</B> Wenn Änderungen durchgeführt werden müssen, aber der Gildenmeister dies später durchführen will, unbedingt auf CANCEL klicken. FINISH würde die ungeänderten Daten erneut absenden und ein erneutes Betreten des Gildenmenüs unmöglich machen.
	<BR><BR></LI>
	<LI>
		Wenn der Name und die Ziele der Gilde in Ordnung sind, wird der Gildenmeister über Email darüber informiert, daß die Gilde nun gegründet werden kann. Bitte darauf achten, daß eine korrekte Email in den Accountdaten des Gildenmeisters angegeben ist. Notfalls über "Account --> Change Account --> Edit Registration" im Login-Bildschirm überprüfen und ändern.
	<BR><BR></LI>
	<LI>
		Wenn die entsprechende Email eingegangen ist, ist es nötig online auf seinem Spielserver mit dem /petition Befehl seinen Server-GM zwischen 21:00 Uhr und 08:00 Uhr MEZ (2pm und 11pm PST) zu treffen um die Gilde offiziell in Everquest zu gründen. Es müssen mindestens 10 (zukünftige) Mitglieder, alle in derselben Zone, anwesend sein wenn der GM eintrifft! 
	<BR><BR></LI>
</OL>

