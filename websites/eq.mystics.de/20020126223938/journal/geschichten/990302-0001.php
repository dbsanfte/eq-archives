<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von ZaGor<BR>
	<BR>
</P>

<P CLASS="P1">
	Aus der Traum vom Faulenzen und reichlichen Mahlzeiten. Um einen Beitrag für unsere
	Halblings - Gemeinschaft zu leisten bin ich der Druiden - Gilde
	beigetreten - auch mit dem Hintergedanken dadurch mal etwas mehr von der Welt zu sehen
	als nur unsere kleine Sieldlung Rivervale. Wenn man den Abenteurern die hier bisher Rast
	machten glauben darf ist das Leben draußen sehr abwechslungsreich und viele haben von anderen
	Ländern und großen
	Schätzen gesprochen die nur darauf warten mit einem Halbling wie mir Bekanntschaft zu
	machen. Es soll aber auch nicht ungefährlich sein durch die Lande zu reisen und deshalb
	will mich meine Familie erst ziehen lassen wenn ich eine solide Ausbildung hinter mir habe.
</P>

<P CLASS="P1">
	Also habe ich - nachdem ich die schlichte Kutte eine Druiden - Novizen übergezogen
	habe - begonnen meine ersten zwei Zaubersprüche zu lernen und etwas den Umgang mit
	der Keule zu üben. Der eine Zauberspruch versetzte mich in die Lage mich selbst von
	einigen einfacheren Verletzungen zu heilen und der andere vermochte meine Haut so hart wie
	Holz werden zu lassen damit ich mir erst gar nicht sonderliche Verletzungen zuziehen
	würde. Eine Ration für 5 Tage wurde mir zur Verfügung gestellt mit dem Kommentar daß ich
	für den restlichen nötigen Unterhalt selbst aufzukommen hätte. Auf was habe ich mich da
	eingelassen.
</P>

<P CLASS="P1">
	Meine erste Aufgabe bestand darin vor der einen Haustür unserer
	Siedlung - Misty Thicket - die lästigen kleinen Biester zu 'entfernen' die sich immer
	wieder irgendwo durch die große Mauer schlichen.
</P>

<P CLASS="P1">
	Geschützt durch Magie wagte ich mich
	zuerst an die Fledermäuse und Moos-Schlangen nachdem ich schmerzlich erfahren mußte
	daß Feuerkäfer, kleine
	Spinnen und Schwarze Wölfe zu gefährlich für einen noch ungeübten Druiden wie mich
	waren. Da meine Tätigkeit nicht entlohnt wurde war es nur recht daß ich Teile der
	getöteten Biester beim Händler für ein paar Kupferstücke verkaufte.
</P>

<P CLASS="P1">
	Ich lernte ziemlich schnell dazu die Biester an der richtigen Stelle zu treffen und
	auch den Umgang mit der Magie sorgsam einzuteilen. Auch Ratten und Bienen hatten sich
	nur vor mir in Acht zu nehmen. Eine bessere Ausrüstung würde bestimmt meine Tätigkeit
	effektiver gestalten und so ging ich mit meinen Kupferstücken in den nächsten Laden
	um eine zu erwerben. Als ich die Preise sah erschrak ich fast zu Tode. Das konnte ja
	Jahre dauern bis ich mir auch nur ein paar Stiefel leisten konnte. Zumal knurrte mal
	wieder mein Magen und erschreckt stellte ich fest daß meine Rationen aufgebraucht waren.
	Da auch die Nahrungsmittel sehr teuer waren beschloß ich mit meinem Geld Angel und
	Köder zu kaufen und erst mal gemütlich am See zu angeln. Es bissen nicht viele an
	aber es reichte aus um ein paar weitere Tage zu überstehen. Den Plunder der sonst noch
	an der Angel hing wie rostige Messer und alte Latschen verkaufte ich und deckte mich
	auch noch mit Wasser ein um wieder
	ein paar Tage in Misty Thicket kleine Monster zu jagen. Beim Angeln bemerkte ich ein
	paar erfahrenere Halblinge die sich in den See stürzten und gegen die Phiranhas
	kämpften. Ein netter Zeitvertreib dachte ich mir und sprang auch mal ins Wasser. Was
	aber von außen so leicht aussah kostete mich fast das Leben. Ich konnte mich gerade noch
	an Land retten - der Fisch hatte nur
	ein paar Kratzer.
</P>

<P CLASS="P1">
	Also zog ich wieder aus um kleine Monster zu erschlagen. Um mehr finanzielle
	Effizienz zu erzielen wagte ich mich an die Schwarzen Wölfe. Fast hätte ich einen
	erschlagen - schleicht sich doch ein mieser Goblin - Arbeiter von hinten an. Meinen Tod
	vor Augen rannte ich so schnell ich konnte in Richtung Rivervale. Die Wachen am Eingang
	kamen mir zu Hilfe und erledigten den blöden Goblin (hätte der sich auch denken
	können - die doofe Nuß) und den
	Wolf.
</P>

<P CLASS="P1">
	Von nun konzentrierte ich mich bei einem Kampf nicht nur auf meinen Gegner sondern auch
	auf mein Umfeld. Die Wölfe die ich die nächste Zeit erlegte brachten eine ganze
	Menge Geld. Zwar war bei den meisten das Fell ruiniert aber ab und zu konnte ich
	auch einen mit einem besonders schönen Fell erschlagen und das Silber klingelte in meiner
	Börse.
</P>

<P CLASS="P1">
	Das Geld investierte ich in eine Schneider - Ausrüstung da ich immer noch nicht glauben
	konnte mir jemals ein ledernes Rüstungsteil kaufen zu können. Schmerzlich mußte ich
	feststellen daß es gar nicht so leicht war aus den ruinierten Fellen einzelne
	Rüstungsteile zusammen zu flicken. Viele Felle und Schablonen gingen kaputt bis ich in
	der Lage war mich mit einer Leder - Flicken - Rüstung selbst auszustatten. Ich habe erst
	gar nicht versucht meine Investitionen darin zusammen zu rechnen. Sicher wäre die
	gleiche Rüstung im Laden billiger gewesen. Aber was man lernt lernt man fürs Leben
	und vielleicht kann ich damit mal das eine oder andere Goldstück verdienen. Zum Beispiel
	in Zeiten in denen ich meine Verletzungen auskuriere (wenn ich nicht gerade beim angeln
	bin). Erfreut mußte ich feststellen daß mein Kapital mir erlaubte mich von meinem
	Holzknüppel zu trennen - ich erwarb eine (leider schon schwer angerostete) Metall - Keule.
</P>

<P CLASS="P1">
	So ausgestattet machte ich mich wieder auf den Weg zu den kleiner Biestern in Misty
	Thicket. Nur waren auch die Feuerkäfer nicht mehr vor mir sicher. Vor den Spinnen
	hatte ich aber noch etwas Respekt. Meine Magie wurde auch immer besser und ich kam
	auch ab und zu in die Lagen anderen Halblingen zu
	helfen indem ich sie heilte oder ihre Haut so hart wie Holz werden lies.
</P>

<P CLASS="P1">
	Eine der Spinnen denen ich bisher noch nie was getan hatte sah mich blöd an und ich
	dachte mir daß es wohl besser für sie wäre von dieser Welt zu verschwinden. Es war
	ein harter Kampf doch kurz vor ihrem Tod gesellte sich eine andre Spinne dazu. Dieses
	fiese Ding nutzte die Situation aus - ich war ja durch den Kampf schon schwer
	verletzt. Den Tod vor Augen und weit und breit keiner der Wächter die mich schon
	einmal von meinem sicheren Tod bewahrt hatte begann ich weg zu rennen. Leider war die
	Spinne genauso schnell und immer wieder konnte sie mir erneut Schaden zufügen. Ich sah
	schon mein Ende nahen als ich den Klang der Magie vernahm. Ich wurde auf einmal nicht
	mehr verfolgt und sah mich nach der Spinne um. Die konnte sich nur noch langsam
	bewegen - war wie durch die Magie ausgebremst. Da sah ich einen anderen
	Druiden - Halbling der in der Nähe der Spinne einen weiteren Zauber herauf beschwor.
	Ein lauter Donner ertönte und die Spinne war auf der Stelle tot. Ich dankte ihm für
	die Rettung und stellte fest daß er schon viel weiter ausgebildet war als ich. Da wußte
	ich daß ich auf dem richtigen Weg war - diese Magie wollte ich auch lernen. Ich ging
	zurück nach Rivervale zur Druiden - Gilde um den Spruch zu erwerben. Enttäuscht mußte
	ich feststellen daß ich noch nicht so weit war diesen Spruch zu nutzten - ich
	brauchte noch mehr Erfahrung. Aber den Spruch 'Burst of Flame' durfte ich erlernen und
	lernte ihn sehr zu schätzen. Ich mußte nicht immer einem Monster hinterher laufen sondern
	konnte ihnen erst mal aus der Entfernung eine 'überbraten' bevor es zum Nahkampf kam.
</P>

<P CLASS="P1">
	Die folgende Zeit verbrachte ich damit weiter die nähere Gegend zu 'säubern', meine
	Fertigkeit im Nähen zu üben und ab und zu ein paar Fische zu angeln. Um nicht immer so
	weite Wege wegen ein paar 'Beuteteile' zurück legen zu müssen kaufte ich mir einen
	Rucksack und einen Großen Sack. Damit konnte ich mehr verstauen und ich konnte
	mehrere Tage im Freien verbringen. So langsam fand ich Geschmack an diesem Leben und
	ich begann auch noch nachts zu jagen - mit der Fackel in meinem Gepäck war das kein
	Problem. Ab und zu kam auch einer dieser Goblin - Arbeiter vorbei um seinen Schädel an
	meiner Keule zu reiben. Um das kleine Getier kümmerten sich inzwischen neue Novizen
	also kümmerte ich mich mehr um die Größeren und half den Anfängern wenn sie in der Not
	und ich in der Nähe war.
</P>

<P CLASS="P1">
	Eines Tages erreichte ich eine Stufe die mir ermöglichte
	mächtigere Magie zu erlernen. Ich erwarb die Zauber 'Burst of Fire' und 'Invoke Lightning'
	und hatte damit ein mächtiges magische Vernichtungs - Potential das auch ausreichte
	2 - 3 Spinnen ohne größere Gefahr zu beseitigen.
	Auch die gemeinen Ratten die ab und zu irgendwie die große Mauer passierten hatten
	keine Chance mehr gegen mich. Da sie einen mit einer Krankheit ansteckten erlernte ich
	noch den Spruch 'Cure Disease'. Langsam kam Langeweile auf und ich dachte mir bei
	meiner Beförderung auf Stufe 6 daß ich nun reif genug wäre um zu erkunden was hinter
	der Mauer liegt. Als ich durch den Torbogen schritt wurde mir klar warum von draußen
	so viele Verletzte kamen. Spinnen größer als eine Hütte, größere Schlangen, Bären und
	anderes Getier tummelten sich da. So langsam begriff ich wozu die
	Mauer gebaut worden war.
</P>

<P CLASS="P1">
	Da ich viel Erfahrung mit den Moos - Schlangen hatte machte ich mich auf einer
	Baum - Schlange die gerade des Weges kam etwas 'einzuheizen'. Die fand das gar nicht
	gut und anders als ihre kleinen Geschwister ergriff sie nicht die Flucht. Ihr Angriff riß
	mich zurück in die Realität. Nach ihrem ersten Biß spürte ich Gift in meinen Adern. Damit
	hatte ich nicht gerechnet. Unter Einsatz meines gesamten magischen Potentials konnte ich
	sie erlegen und zug mich (immer noch vergiftet) schnell hinter die Mauer zurück. Das
	Leder der Schlange nahm ich mit aber es stellte sich heraus daß es gar nicht so
	Wertvoll war. Zum Glück hatte ich durch die Vergiftung keinen größeren Schaden
	erlitten und so zog ich zurück nach Rivervale um den Spruch 'Cure Poison' zu
	erlernen. Ich wollte mein Glück nicht noch mal auf die Probestellen.
</P>

<P CLASS="P1">
	Geheilt zog es mich wieder nach draußen vor die Mauer. Es mußte doch dort etwas geben
	was nicht unbedingt meinen Tod herausforderte. Leider waren keine anderen Halblinge
	meiner Ausbildungsstufe da. Es war mir inzwischen klar daß ich das hier draußen
	nur in einer Gruppe überleben konnte. Ein Schwarzbär kam des Weges. Er hatte ein
	schönes Fell und ich dachte mir daß es bestimmt einen guten Preis bringen würde. Da er
	es nicht freiwillig ausziehen wollte kam es zum Kampf. Dank dem Spruch
	'Invoke Lightning' hatte er am Ende doch seinen Pelz verloren und wollte
	anscheinend nackt nicht weiterleben. Mit dieser Trophäe kehrte ich erst mal diesem
	gefährlichen Gebiet den Rücken. Ich werde wohl ein paar andere Halblinge für eine
	kleine Jagdgesellschaft suchen müssen.
<P>

<P CLASS="P0">
	<I>Zagor, Halbling Druide Stufe 6</I>
</P>

