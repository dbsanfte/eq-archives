<P CLASS="P1">
	Die Quest startet in <b>North Karana</b> bei <b>Mrysila</b> im Gypsy Camp (Loc -672, -162).
</P>	

<P CLASS="P1">
	Das Ziel dieser Quest ist die <b>"Practice Runes"</b> zu bekommen.
</P>	

<P CLASS="P1">
	Um die Research-Quest zu erfüllen sind drei Schritte notwendig:
</P>
<OL CLASS="P0">
<LI>Zuerst bringst du Mrysila einen <b>Greater Lightstone</b>, zu finden als Loot bei <b>Wisps</b> oder bei anderen Spielern kaufen. Als Belohnung bekommst Du von Mrysila die <b>Concordance of Research</b>. Diese Box wird benötigt um <b>Practice Runes</b> zu erstellen.<BR></LI>
<LI>Der zweite Schritt ist optional. Wenn du Mrysila normale <b>Lightstones</b> bringst gibt sie dir verschiedene "Rezept-Bücher". Darin ist beschrieben welche Komponenten man für die Erstellung der <b>Practice Runes</b> benötigt. Aber eine ausführliche Liste folgt weiter unten.<BR></LI>
<LI>Zuletzt mußt du noch sämtliche Ingredienzien besorgen. Für jede <b>Practice Rune</b> werden fünf Komponenten benötigt. Drei davon sind in den Rezept-Büchern aufgelistet. Die anderen beiden sind ein <b>Quill</b> (Feder) und ein <b>Parchment</b> (Pergament). Die Feder (<b>Quill</b>) kann man immer wieder verwenden, aber das Pergament (<b>Parchment</b>) wird bei jedem Versuch verbraucht.
<BR></LI>
</OL>	
<P CLASS="P1">
	Sobald Du die fünf Komponenten beisammen hast, gibst du sie in die <b>Concordance of Research</b> und drückst auf "Combine". Daraus entsteht dann die entsprechende <b>Practice Rune</b>.
</P>

