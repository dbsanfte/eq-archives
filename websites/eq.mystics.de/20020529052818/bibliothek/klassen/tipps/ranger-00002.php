<P CLASS="P1">
	Wird es euch in Northqeynos zu langweilig, solltet ihr in die Hills gehen. Am Eingang von Blackburrow (BB) spawnt häufig ein einzelner Gnoll. Den Eingang findet ihr am einfachsten, wenn ihr von SG aus nach links immer an der Bergwand langgeht. Die macht einen Bogen nach Süden und irgendwann stolpert ihr dann über den Eingang zum Dungeon. In das Dungeon selber solltet ihr aber noch lange keinen Fuß setzen, hier gibt es viele höherlevelige Monster und Trains.<br><br>
	Ab ca. Level 6 bis 7 sind dann die Plains angesagt. Erst einmal young lyons und mist wolves. Das Faction Standing mit unserer Gilde verschlechtert sich übrigens nicht, wenn man Bären oder Wölfe tötet. Lediglich in Surfall Glade sollte man die Tiere auf jeden Fall in Ruhe lassen. Gebt auf eurem Radar besonders in den Plains obacht, ob ihr einen hill giant, bandit oder brigand tracken könnt. Dann solltet ihr die nähere Umgebung warnen und euch vom Acker machen. Selbst die Wachen an den Türmen bieten hier nicht unbedingt ausreichend Schutz.<br>
	Ansonsten sind die Türme natürlich die optimale Raststätte, und man kann sich am besten die Locations der Türme aufschreiben, um sie immer wiederzufinden ( Befehl hierfür ist /loc). Wenn man sich nicht ganz so weit von ihnen entfernt, kann man natürlich auch einfach die Wachen tracken, um zurückzufinden. Qeynos liegt von hier aus gesehen im Westen, also immer den Wolken nach, die auch stets nach Westen ziehen.
</P>

