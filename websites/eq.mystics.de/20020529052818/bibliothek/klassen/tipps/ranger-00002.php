<P CLASS="P1">
	Ranger können Menschen, Halbelfen und Waldelfen sein. Menschen und Halbelfen starten in Surfall Glade nahe der Stadt Qeynos, Waldelfen in Kelethin. Meine Tips werden sich alle auf die Umgebung von Qeynos beziehen, da ich  als Halbelf, 11. Level, noch nie in Kelethin war.<br>
	Ich würde beim Punkteverteilen den Hauptaugenmerk auf Stärke legen, damit man später eine vernünftige Rüstung tragen kann, ohne gleich darunter zusammenzubrechen. Ein paar Punkte sollte man vielleicht auch auf Weisheit legen, wenn man gleich im 9. Level alle Sprüche zaubern können will, denn Weisheit ist für die Manapunkte verantwortlich. 
</P>

