<P CLASS="P5">
	2.1 Die Gilde
</P>

<P CLASS="P1">
	Man beginnt also in Surfall Glade, einem abgeschiedenen, ruhigen Talkessel. Zunächst einmal reichlich orientierungslos geht man ein wenig umher. Man sollte darauf achten, daß es Tag ist, wenn man die ersten Schritte tut, damit man sich die Umgebung richtig anschauen kann. Eine Nacht endet gegen 5 Uhr EQ-Zeit und dauert ca. 20 Minuten Realtime. Zunächst geht man zur Gilde und gibt dort das Schreiben an den Gildenmeister ab. Er heißt Hager Sureshot. Die Gilde befindet sich mitten im kleinen See und ruht auf Holzpflöcken. Bei ihm kann man künftig auch trainieren.
</P>

<P CLASS="P5">
	2.2 Einkaufen
</P>

<P CLASS="P1">
	Einkaufen und Verkaufen kann man einen Raum weiter. NSC rechtsclicken und los geht's.<br>
	Hier bekommt man auch Nahrung und Wasser her. Ich empfehle, rations und Wasser zu kaufen, die reichen völlig. Allerdings wird man als Ranger nur selten darauf zurückgreifen müssen, denn in den ersten 3 Leveln bekommt man bei jedem Sterben automatisch Newbienahrung und -wasser, danach hat man dafür seinen Forage-skill.
</P>

<P CLASS="P5">
	2.3 Der Durchgang und NSC
</P>

<P CLASS="P1">
	Nun folgt dem Pfad durch den Höhlengang, da, wo vorne die Tonne steht. Er endet am Eingang zu Surfall Glade, wo zwei Wachen stehen. Sie werden alle Monster, die ihnen zu nahe kommen, töten. Ihr könnt euch also zu ihnen hinretten, wenn ihr fliehen müsst und die Monster an ihnen abstreifen. Lediglich NSC wie Tovax Vmar und Pzyn werden von ihnen in Ruhe gelassen, obwohl die sehr gefährlich sind und auch euch angreifen. Benutzt euren Tracking-Skill, um ihnen auszuweichen und die Umstehenden im say-Modus zu warnen. 
</P>

<P CLASS="P5">
	2.4 Der Pfad nach Qeynos
</P>

<P CLASS="P1">
	Nun weiter auf dem Pfad nach Süden. Er führt an der Cottage, einem kleinen Händlerhaus vorbei. Danach kommt eine Kreuzung, die links in die Plains führt. Weiter geradeaus. Hier gibt es eine Zonengrenze kurz hinter der Kreuzung, wo man ebenfalls böse Monster hinter sich lassen kann. Wir verlassen also die Hills und kommen nach North Qeynos. Das ist das Gebiet, in dem sich Anfängercharaktere tummeln können. <br>
	Um festzustellen, ob ein Monster angemessen schwer ist, wählt es mit F8 oder Mausklick aus. Nun C drücken oder rechtsklicken. Die Farbe gibt an, wie euer Level im Vergleich zu dem des Monsters ist, der Text läßt außerdem Rückschlüsse auf die Faction standings zu. 
</P>

<P CLASS="P5">
	2.5 Monster beurteilen
</P>

<P CLASS="P1">
	<b>Grün</b> = sehr leichter Gegner, so gut wie keine Erfahrungspunkte<br>
	<b>Blau</b> = i. d. R. leichter Gegner, etwas Erfahrung<br>
	<b>Weiß</b> = ebenbürtiger Gegner<br>
	<b>Gelb</b> = i.d.R. überlegener Gegner<br>
	<b>Rot</b> = haushoch überlegener Gegner<br>
	Wenn man alleine unterwegs ist, empfiehlt es sich, blaue zu nehmen oder weiße, wenn man in der Nähe der Wachen ist, so daß man schnell fliehen kann. Flieht rechtzeitig, denn auf den letzten beiden Bubbles Lebensenergie werdet ihr sehr langsam, so daß es dann schon zu spät sein kann. <br>
	Wenn ihr Level 1 seid, gibt es logischerweise nur Monster, die mindestens weiß, also auch Level 1 sind. Beispiele für Erstlevelmonster sind rat, snake, gnoll pup, decaying skeleton und bat. Level 2 sind z.B. Klicknic worker, Klicknic drone und fire beetle.
</P>

<P CLASS="P5">
	2.6 Die Bank und das nächste Geschäft
</P>

<P CLASS="P1">
	Zwei wichtige Stellen in Qeynos seien noch genannt: Zum einen der Laden von <i>Sneeds</i>, der sich geradeaus durch die Torbögen direkt rechts befindet. Zum anderen die Bank. Sie ist in Südqeynos. Falls ihr die Banker tracken wollt: sie heißen Copperhold.
</P>

<P CLASS="P5">
	2.7 Questen
</P>

<P CLASS="P1">
	Ein Satz noch zu Questen: NSC anklicken, H für Hail drücken und Text lesen. Begriffe in Klammern sollten in der Frage, die ihr stellen müßt, wiederholt werden, und zwar buchstabengetreu.<br>
	Es hat sich bewährt, sich Notizen zu machen, welche Gegenstände man von wem wofür bekommen hat und wo man sie abliefern soll. Sonst kann man schon mal den Überblick verlieren. Hilfreich ist auch ein langer Rechtsclick auf den entsprechenden Gegenstand.
</P>

<P CLASS="P5">
	2.8 Waffen
</P>

<P CLASS="P1">
	Für den Anfang reicht die Newbiewaffe völlig, irgendwann werdet ihr bei einem Monster vermutlich ein rostiges Langschwert finden. Ihr solltet als Skill 1 Hd slash wählen, evtl. auch 2 Hd slash. <br>
	2 Hd slash Waffen haben den Vorteil, daß sie mehr Schaden machen, dafür aber wesentlich langsamer sind. Außerdem kann man bei ihnen natürlich kein Schild in der anderen Hand halten.
</P>

<P CLASS="P5">
	2.9 Rüstung
</P>

<P CLASS="P1">
	Für Rüstungen sollte man alles Geld ausgeben, das man erübrigen kann. Erst nach und nach alles in loth und Patchwork armour kaufen, dann zu Leder übergehen. Hier lohnt es sich besonders, mit auction die Angebote anderer Spieler zu erfragen, oder auf deren Angebote zu achten. Nachteil einer guten Rüstung ist lediglich, daß sie mit in die Bewertung hinsichtlich der Farbe des Monsters einbezogen wird. Hier machen natürlich nicht 2 Punkte RS eine Farbe aus, aber 20 Punkte mehr oder weniger kann schon was bewirken.
</P>

<P CLASS="P5">
	2.10 Skills
</P>

<P CLASS="P1">
	Generell reicht es bei allen Skills, 1 Punkt zu investieren und sie dann regelmäßig anzuwenden. Sie steigen dann recht schnell von alleine. Im 3 Level bekommt man forage. Nicht verzweifeln, es dauert sehr lange, ehe man zum ersten Mal damit überhaupt etwas findet, etwa ab einem Wert von 25 ist das der Fall. Mittlerweile habe ich forage 55 und schleppe ständig einen ganzen Lebensmittelladen mit mir herum. Nur Wasser finden ist immer noch etwas schwierig. Die Obergrenze liegt für die skills bei etwa Level mal 5 + 5. Eventuell sind hiervon aber einige Skills wie fishing ausgenommen, das ich bisher nur auf 35 steigern konnte. 
</P>

