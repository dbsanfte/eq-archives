<P CLASS="P0" ALIGN="CENTER">
	Diese Tips für Anfänger wurden von Yerisa erstellt. Der Stand der Infos ist 22.05.99.
</P>	

<TABLE BORDER="0" CELLPADDING="0" CELLSPACING="0" WIDTH="100%">
