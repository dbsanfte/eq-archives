<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Feldim Silvertree<BR>
	<BR>
</P>

<P CLASS="P1">
	Als ich so um die 2 Level alt war, erlaubten mir meine Mutter Demahim und mein Vater Eomer, daß ich mit meinen Freunden vor den Toren meiner Heimatstadt spielen, und nicht nur am See direkt neben dem Kloster Steine ins Wasser werfen dürfe .
</P>

<P CLASS="P1">
	Mann, habe ich mich damals darauf gefreut. Zusammen mit meinen Freunden Elam und Nekam spielten wir im sicheren Auge der Stadtwachen Fangen an der Strasse. Wir betrachteten die kräftigen Erwachsenen Krieger unseres, und auch anderer Völker, die auf der Strasse von weit her angereist waren .
</P>

<P CLASS="P1">
	Mein Vater hatte mir einst erzaehlt, auch er, meine Mutter und ich seien von weither hierhin gereist, weil es meiner Mutter, einer eleganten Menschin, in der Heimatstadt meines Vaters, in Surefall Glade nicht gefiel. Meine Mutter war einst eine grosse Zauberin gewesen, und hatte mir oft erzählt, wie sie in ihren guten Jahren riesige Drachen und Riesen erlegt, und einst sogar gegen einen Gott gekämpft hatte.
</P>

<P CLASS="P1">
	Mein Vater war ein alter Wald Elf Ranger. Er kannte die ganze Welt besser als jeder andere, den es jeehhh in Norrath gegeben hat und geben wird. Doch meine Mutter wollte nicht, daß ich als Krieger aufwachse. Sie wollte mich später als einen weisen Mönch oder einen normalen Handwerker, wie Schmied oder Bauer sehen .
</P>

<P CLASS="P1">
	Doch eines Tages, als ich mit meinen Freunden Elam und Nekam vor der Stadt spielte, und hinter einer Fledermaus herrannten, sahen wir in größerer Ferne plötzlich etwas aufblitzen. Das Blitzen am Horizont kam immer näher zu uns ran, und wir konnten erkennen, daß dort etwas angeflogen kam. Ehe wir uns versahen, schwebte eine Gestalt mit einem komisch gefärbten blauen Gesicht und in einer schwarzen Robe for uns. Wir erschraken, waren aber zugleich beeindruckt von der Gestalt, die dort vor uns schwebte .
</P>

<P CLASS="P1">
	Die grinsende Gestalt schwebte vor uns und fing plötzlich an, in einer uns unbekannten Sprache langsam zu sprechen. Sie versuchte uns mit Handbewegungen etwas zu erklären, aber wir verstanden es nicht. Doch plötzlich erschrack die Gestalt und verscheuchte uns. Wir wussten nicht warum, doch wir merkten daß es schon spät war, und Vater uns oft vor der Nacht gewarnt hatte. Die Gestalt zeigte nach Nord-Ost, und wir sahen wie ein knochiges Skelett mit einem Schwert und einem Schield auf uns zugerannt kam .
</P>

<P CLASS="P1">
	Die Person stellte sich schützend vor uns, und plötzlich konnte man sie etwas murmeln hören. Ehe wir es uns versahen, leuchteten die Hände der Person grell grün auf, und sie sah angespannt aus. Aber dann schoss auf einmal ein hellgruener Strahl aus den Händen, direkt auf das Skelett zu. Eine kleine Explosion und das Skelett sank zu Boden. Die Gestalt blickte zu uns runter und grinste nur.
</P>

<P CLASS="P1">
 Da fing sie an, zwar stotternd aber verständlich zu uns in der Menschensprache zu sagen: <I>"Geht schnell wieder nach Hause Kinder, es ist zu gefährlich für euch zu dieser Zeit noch draussen herumzutollen."</I> Wir folgten dem Rat der Gestalt und rannten so schnell wir konnten zur Stadt zurück. Unsere Eltern standen schon am Tor, und durch die Bäume vor der Stadt streunten Wachen um uns zu suchen.
</P>

<P CLASS="P1">
	Da schrie mein Vater los: <I>"DA SIND SIE!! DA SIND SIE!!!"</I> und kam mit 2 gezückten Waffen auf uns zugerannt. Wir wussten nicht warum, bis wir merkten, daß die dunkle Gestalt, welche hinter uns hergekommen war noch hinter uns stand. Nach einem kurzen Gespräch mit der Person, in einer Sprache, die wir mal wieder nicht verstanden, steckte mein Vater die Waffen ein, und bedankte sich bei der Person .
</P>

<P CLASS="P1">
	So gingen wir alle zusammen nach Hause ...
</P>

<p class="P0" ALIGN="CENTER"><I>Feldim Silvertree</I></p>

