<P CLASS="P1">
	Wir Gnome sind eine der intelligentesten Rasse von Norrath, daß wir auch die kleinste sind, naja 
	irgend einen Nachteil muß man wohl haben.
</P>

<P CLASS="P1">
	Unsere Fähigkeit zu allem Möglichen und Unmöglichen ein Gerät zu bauen ist berühmt!!! Man kann 
	wirklich sagen, daß die viele Gnome einen Großteil ihrer freien Zeit in ihrer Werkstatt beim 
	Konstruieren, Bauen oder Reparieren von Maschinen zubringen. Trotz allem sind wir den leiblichen 
	Genüssen nicht abgetan, denn ein gutes Essen und ein gutes Bier läßt die grauen Zellen besser 
	arbeiten.
</P>

<P CLASS="P1">
	Jetzt fragt man sich natürlich, warum sollte ein Gnom Heim, Herd, seine Werkstatt und seine 
	Lieblingskneipe in Ak'Anon verlassen und auf Abenteuer durch die Welt von Norrath ziehen?? Nun, 
	man muß zugeben, daß dies nicht allzu häufig vorkommt, aber es gibt auch für Gnome einige gute 
	Gründe Abenteurer zu werden!!
</P>

<P CLASS="P1">
	Da gibt es den Typ dem die ganze Bastelei über ist, er will was neues sehen - und manche kommen 
	gerade dann wieder zum 'basteln'.
</P>

<P CLASS="P1">
	Und es gibt Gnome, die suchen neue Ideen für Maschinen und sehen sich deshalb draußen um.
</P>

<P CLASS="P1">
	Ein Gnom-Abenteurer kann bei weitem nicht so komplexe Maschinen bauen wie in einer Werkstatt, 
	aber trotzdem bringt auch er einiges zustande. Er wird auf jeden Fall bei allen seinen Handlungen 
	als erstes seinen Verstand einsetzten, trotzdem wird er auch von einer starken Neugier getrieben. 
	Wir gehen Kämpfen soweit möglich aus dem Weg und wenn es doch sein muß, dann kämpfen wir nur, wenn 
	wir auch eine Chance haben gewinnen zu können.
</P>

<P CLASS="P1">
	Ein Gnom in einer Gruppe wird diese so gut es geht unterstützten, aber man sollte keinen 
	Selbstmordaktionen erwarten.
</P>

<P CLASS="P5">	
	Folgende Berufe können von uns ergriffen werden:
</P>

<P CLASS="P1">
	Gnom-<B>Kleriker</B> ziehen meist in "Brell Serilis"' Namen herum, d.h. sie werden jedem,
	der es hören will erklären das so eine große Oberwelt doch absolut unnütz ist.
	Komischerweise gibt es aber viele die trotzdem gerade da zu finden sind - Seltsam??
</P>
	
<P CLASS="P1">
	Der Gnom-<B>Entchanter</B> versucht nicht nur mit seiner Magie Andere zu 'verbessern' - seine
	Geräte sind meist sehr 'phantasievoll'.
</P>
	
<P CLASS="P1">
	Der Gnom-<B>Magican</B>
	tendiert dazu seiner Erfindungen mit seinen Beschwörungen zu kombinieren. Er 
	benutzt den Feuerelementar zum Betreiben einer Esse oder einer Destille, benutzt Luft- oder 
	Wasserelementare um sein Bier kalt zu halten ... 
</P>
	
<P CLASS="P1">
	Gnom-<B>Nekromanten</B>
	betrachten auch Lebewesen nur als Maschinen die er, nach dem sie zerstört wurden, 
	einfach reparieren. Die meisten Gnome betrachtet diese morbide Einstellung als, im besten Falle, 
	sonderbar und sind insbesondere von der Vorstellung selbst auf diese Weise 'repariert' zu werden 
	nicht sehr begeistert - Nekromanten werden zwar geduldet, aber diese werden wohl trotzdem selten 
	echte Freunde unter den Gnomen finden.
</P>
	
<P CLASS="P1">
	Gnom-<B>Rogue</B>
	hatten meist Probleme das Geld für eine Erfindung zusammen zu bekommen und 
	so 'borgen' sie sich halt alles Nötige. 
</P>
	
<P CLASS="P1">
	Gnom-<B>Krieger</B>
	sind von Waffen und Rüstungen fasziniert und sie bemühen sich diese ständig zu verbessern und 
	diese Verbesserungen sofort in der Praxis zu erproben. Daher kämpfen sie sehr viel lieber als 
	andere Gnome. 
</P>
	
<P CLASS="P1">
	Ein Gnom-<B>Wizard</B>
	ist von der Magie an sich fasziniert und widmet viel Zeit der Suche nach neuen Zaubern und 
	der Verbesserung ihrer Magiefertigkeiten. Da ein Großteil seiner Sprüche Kampfzauber sind, 
	gilt für ihn ebenfalls, er kämpft wirklich sehr gerne. 
</P>
	
<P CLASS="P1">
	Viele von uns mögen zwar die Musik, doch scheinen wir für den Beruf des Barden nicht sehr 
	geeignet zu sein. Obwohl ich von zwei musikalisch hochbegabte Gnome gehört habe die die 
	Laufbahn des Barden einschlagen wollten. Leider ist der Lehrer des einen im Ink Sea baden 
	gegangen, leider konnte er gar nicht schwimmen - muß er wohl vergessen haben. Und der zweite 
	Gnom sagte während des Unterrichts: "Da müßte man doch eine Maschine für bauen können!!", 
	dann fing er sofort mit der Planung an und hat den Unterricht nicht mehr fortgesetzt. Dessen 
	Lehrer scheint das nicht betrübt zu haben, denn er wurde beobachtet wie er singend Ak'Anon 
	verließ - leider lief er einer Gruppe Oger in die Arme und wurde von ihnen sofort zum Essen 
	eingeladen. 
</P>
	
<P CLASS="P5">
	Unser Verhältnis zu anderen Rassen:
</P>

<P CLASS="P1">
	Wir haben gegen die meisten anderen Rassen keine besonderen Vorurteile, sind allerdings 
	grundsätzlich vorsichtig.
</P>

<P CLASS="P1">
	Allerdings versuchen wir Trollen, Oger und Dunkelelfen wenn möglich aus dem Weg zu gehen, da wir 
	diese Rassen als doch sehr gefährlich einstufen. Allerdings haben wir den Vorteil, daß 
	Dunkelelf und Troll kaum mit einem toten Gnom angeben können und für einen Oger sind wir 
	höchstens als Vorspeise zu gebrauchen - im Notfall versuchen wir also unauffällig zu bleiben, 
	mit etwas Glück übersieht man uns, oder man verkrümelt sich während sie gerade herzlich über 
	den kleinen Gnom lachen.
</P>

<P CLASS="P1">
	Wald- und Hochelfen stehen wir neutral gegenüber, da wir nicht so recht wissen was wir mit ihnen 
	anfangen sollen. Allerdings betrachten wir die Umweltschutzdiskussion, besonders von Seiten der 
	Waldelfen, mit Besorgnis - allerdings sind auch bereits einige Gnome dabei Maschinen für den 
	Umweltschutz zu bauen.
</P>

<P CLASS="P1">
	Mit Zwergen und Halblingen verbindet uns außer unserer Größe noch die Vorliebe für Essen und 
	Trinken. Meist vertragen wir uns recht gut mit ihnen.
</P>

<P CLASS="P1">
	Die Eruditen werden von einem Teil der Gnome dafür bewundert das ihre Intelligenz unsere noch 
	übersteigt, allerdings ist die sehr stark geistige Einstellung der Eruditen wieder ein Grund 
	für Unverständnis - ein etwas gespaltenes Verhältnis.
</P>

<P CLASS="P1">
	Ebenfalls ein zwiespältiges Verhältnis haben wir mit den Menschen, einerseits belächeln sie uns 
	wegen unserer Größe, andererseits sind viele Menschen fasziniert von Gnom-Maschinen.
</P>

<P CLASS="P1">
	Auch Barbaren belächeln uns meist nur, es gibt aber auch viele die lachen ziemlich laut - am 
	besten ignoriert man sie.
</P>

<P CLASS="P0">	
	<I>Agor Togas - Gnom-Magican </I>
</P>

