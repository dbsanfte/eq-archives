<P CLASS="P1">
	Ich - Saulk, menschlicher Händler - wurde wurde von Agor gebeten meine Sicht über die Gnome 
	niederzuschreiben:
</P>
	
<P CLASS="P1">
	Also zuerst sollte erwähnt werden, daß die Gnome nicht alle für ihre Erfindungen leben, doch ein 
	nicht unerheblicher Teil von ihnen tut es.
</P>

<P CLASS="P1">
	Es gibt viele Gnome die sich ganz dem Bau bzw. dem Erfinden einer Maschine verschreiben die 
	eine ganz bestimmte Aufgabe erfüllen soll. Z.B. der Gnom, der Barde werden wollte, hat sich 
	ganz der Erfindung einer Maschine verschreiben die gut Singen können sollte, sie wurde bis 
	heute nie vollendet weil er mit keinem Ergebnis zufrieden war. Gnome streben hier nach 
	absoluter Perfektion - manchmal habe ich das Gefühl, daß sie diese sich selbst gestellte 
	'Quest' niemals wirklich abschließen wollen.
</P>

<P CLASS="P1">
	Sollte ein Gnom in einem Gespräch etwas wie "dazu müßte man eine Maschine erfinden" sagen, 
	so kann man das Gespräch meist als beendet betrachten, da er sofort in die Planungsphase 
	übergeht - und dann fast alles um sich herum vergißt.
</P>

<P CLASS="P1">
	Die Maschinen die Gnome bauen sind meist verblüffend, manchmal unnütz und in seltenen Fällen 
	können sie auch gefährlich sein - man sollte also etwas vorsichtig sein wenn ein Gnom einen 
	Freiwilligen sucht.
</P>

<P CLASS="P1">
	Eine eigentlich verblüffende Tatsache - zumindest für mich als Mensch - ist, das Gnome ihre 
	geringe Größe nicht im geringsten als Nachteil, sondern meist sogar als Vorteil ansehen - 
	Bemerkungen über ihre Größe prallen von den Meisten ab, wie ein Wattebausch von einem 
	Plattenpanzer.
</P>

<P CLASS="P1">
	Nach dem was ich gehört habe werden Gnome in Gruppen sehr unterschiedlich gesehen. Dem 
	Einen sind sie einfach viel zu vorsichtig, den Anderen gefällt das überlegte Vorgehen...
</P>

<P CLASS="P1">
	Eine Warnung wenn man mit einem Gnom in eine Kneipe geht, es ist schier unglaublich was diese 
	kleinen Kerle an Essen und Trinken vertragen!!!
</P>

<P CLASS="P0">
	<I>Saulk</I>
</P>

