<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Clawdeen<BR>
	<BR>
</P>

<P CLASS="P1">
	Gem&uuml;tlich schlenderte ich durch die Weiten der West Commonlands, als links
	des Weges ein kleines Schauspiel meine Aufmerksamkeit erregte. Ein Kampf fand
	dort statt. Einer dieser arroganten Kodiaks k&auml;mpfte gegen einen dunkelelfischen
	Krieger, der mit blossen F&auml;usten k&auml;mpfte. Warum, das wei&szlig; ich gar nicht, doch
	irgendetwas fesselte mich an diesem Kampf derart, da&szlig; ich fassungslos
	stehenblieb und zusah.
</P>

<P CLASS="P1">
	Ich muss eine ganze Weile dort verharrt sein, denn pl&ouml;tzlich sprach der
	Dunkelelf, ohne sich dabei zu mir umzudrehen: <I>&quot;Bevor Ihr mich verdammt,
	Druide, sollt Ihr wissen, dass er mich angriff, nicht umgekehrt.&quot;</I>
	Sein Reden riss mich aus meiner Starre. Ich &uuml;berlegte kurz, dann begriff ich,
	was er gemeint hatte. Er glaubte, ich beobachte sein Handeln voller Wut und
	sei bereit, jederzeit auf Seite des Kodiaks zu treten, was ein &quot;druidisches&quot;
	Verhalten sei.
</P>

<P CLASS="P1">
	Innerlich lachte ich, denn der Fremde konnte ja nicht ahnen, wie unsympathisch
	mir die Kodiaks sind. Sie sind arrogant, zwar halten auch sie einen Teil
	des nat&uuml;rlichen Gleichgewichts, was jedoch auch der einzige Beweggrund f&uuml;r mich
	ist, sie nicht zu jagen. Ich sah sie nicht nur majest&auml;tisch durch die Commonlands
	laufen, nein, ich habe auch Freunde vor meinen Augen durch ihre Tatzen und ihr
	Maul sterben sehen. Und die Kodiaks lie&szlig;en danach noch immer nicht von ihnen ab,
	nein. Sie folgten ihrem Trieb und begannen, ihre Beute zu zerfleischen.....
	Ich sch&uuml;ttelte hastig den Kopf, um diese Erinnerungen aus meinem Kopf zu bannen,
	und da geschah es auch schon: Der Kodiak fiel st&ouml;hnend zu Boden und hauchte sein Leben aus.
</P>

<P CLASS="P1">
	Ich musste mich beherrschen, dem Dunkelelfen nicht zu applaudieren, der
	geradewegs nach diesem Kampf auf mich zukam. <I>&quot;Verurteilt mich nicht, Druide.
	Ich war einst ein Paladin, der den Pfad des Guten verfolgte, doch furchtbare
	Ereignisse in diesem Leben zwangen mich dazu, diese Gestalt anzunehmen und
	meine Seele an Innoruuk zu geben.&quot;</I> Scheinbar reum&uuml;tig senkte er sein Haupt.
</P>

<P CLASS="P1">
	Nun l&auml;chelte ich doch. So ein starker Krieger, der sich offenbar vor mir
	unscheinbarer Sch&uuml;lerin Tunares entschuldigt. <I>&quot;Ihr habt einen
	ehrenhaften Kampf gezeigt, Teir`Dal. Ich bewundere euren Mut, diesem Kodiak
	mit blossen F&auml;usten entgegen zu treten. Und wenn er euch angriff, so wie
	Ihr sagt, so hat er wohl den Tod verdient, denn weder Magie noch Stahl
	fand Platz in Eurem Kampf.&quot;</I> Ich verbeugte mich vor dem Krieger,
	dann schloss ich meine Augen, um das Mana in mir zu mobilisieren. Ich
	heilte seine Wunden bis auf den letzten Kratzer, denn das hatte er sich
	meiner Ansicht nach wirklich verdient.
</P>

<P CLASS="P1">
	Der Schattenkrieger blickte erstaunt. <I>&quot;M&ouml;ge es mehr Eurer Art geben,
	Druide. Und m&ouml;get Ihr ewig verschont bleiben von den b&ouml;sen M&auml;chten von Norrath.
	M&ouml;ge Tunare Euch sch&uuml;tzen und Euch vor jedem Unheil, das mir selbst
	widerfuhr, bewahren.&quot;</I> Mit diesen Worten kniete er ehrf&uuml;rchtig
	vor mir nieder ... und verschwand dann ins Nichts.
</P>

<P CLASS="P1">
	Es machte mich traurig, die Bekanntschaft mit einem so ehrenhaften Teir`Dal
	nicht vertiefen zu k&ouml;nnen, doch setzte ich sogleich meinen Weg fort. Ich focht
	in Oasis und der Northern Desert of Ro, doch seine Worte hallten stets in
	meinem Inneren. Wenn ich doch zumindest seinen Namen kennen w&uuml;rde, um ihn
	in Ehren zu halten....
</P>

<p class="P0" ALIGN="CENTER"><I>Clawdeen</I></p>

