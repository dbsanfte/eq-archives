<P CLASS="P1">
	Der Shadow Knight ist der Champion der Dunkelheit. Gesegnet mit Macht vom Prinzen
	des Hasses, Innoruuk.  Er lebt um seine beachtlichen k&auml;mpferischen F&auml;higkeiten zu
	verbessern und um die b&ouml;se Macht zu vergr&ouml;&szlig;ern, die Ihm seine dunkle Profession
	zugesteht. Als Mensch, Oger, Troll oder Dunkelelf verf&uuml;gt er &uuml;ber eine gro&szlig;e
	&quot;Rassenauswahl&quot;.
</P>

<P CLASS="P1">
	Er verl&auml;&szlig;t sich gr&ouml;&szlig;tenteils auf seine St&auml;rke und schwei&szlig;t die Truppen, &uuml;ber die
	er gebietet mit eisernem Willen zusammen. Immer auf der Suche nach Verrat in den
	Augen derjenigen, die sich Ihm aus Angst oder Gier angeschlossen haben, f&uuml;hrt er
	sie in die tiefsten H&ouml;llen. Kommt dabei ein Gruppenmitglied ums Leben ist er es,
	der die Gruppe wieder auf das Ziel lenkt. Mitleid und Freundschaft sind Ihm genauso
	fremd wie einem Fisch das fliegen. Erwarte keine Dankbarkeit, keine Freundschaft
	sondern erfreue Dich am Sterben des Guten und der Übernahme der Macht durch das B&ouml;se.
</P>

<P CLASS="P1">
	Angst vor seinem Zorn und Neid auf seine F&auml;higkeiten sind die h&auml;ufigsten Gef&uuml;hle,
	die dem Shadow Knight entgegenschlagen. Sein einziges Ziel ist es f&uuml;r seinen Herrn
	Innoruuk zu t&ouml;ten. Und wenn notwendig bei der Erf&uuml;llung eines Auftrages f&uuml;r ihn zu
	sterben. Sollte Ihm ein Paladin, oder ein Angeh&ouml;riger einer &quot;guten&quot; Rasse &uuml;ber den
	Weg laufen, w&auml;re es besser f&uuml;r diesen sehr vorsichtig und extrem h&ouml;flich, wenn nicht
	unterw&uuml;rfig zu sein. Der Zorn eines Shadow Knights ist schnell entfacht und seinen
	vernichtenden Angriffen k&ouml;nnen sich nur die St&auml;rksten erwehren. Legion ist die Zahl
	seiner Feinde, aber sie alle zittern, wenn sein Name genannt wird.
</P>

