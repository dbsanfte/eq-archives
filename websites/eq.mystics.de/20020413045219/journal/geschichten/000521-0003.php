<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Thiameerah<BR>
	<BR>
</P>

<P CLASS="P1">
	Sie saß an ihrem Lieblingsplatz in Felwithe gegenüber des &quot;Bait and Tackle&quot; am
	See. Wie so oft träumte sie davon wie die Welt dort draussen wohl sein werde. Gut
	behütet hatte sie ihre Kindheit verbracht, im geschützten Felwithe. Kein Leid und
	nichts von der Grausamkeit der Welt Norraths war ihr bisher wiederfahren. Doch
	nun, beim heranwachsen zu einer Frau, musste sie sich entscheiden, welchen Weg sie
	einschlagen wolle. Die Entscheidung fiel ihr leicht. Schon immer war sie fasziniert
	von der Natur und den Veränderungen der Natur. Die Entscheidung war bereits gefallen.
</P>

<P CLASS="P1">
	Zu dieser Zeit kam es auch zur ersten Begegnung. Lange sollte sie noch daran
	denken und lange sollte der Eindruck noch währen, ohne genau zu wissen, was es
	war. Träumend sass sie dort. Der See erstreckte sich vor ihr. Versunken in ihre
	Gedanken dachte sie wieder einmal daran, wie ihre Ausbildung zur Enchantress
	wohl verlaufen, wie grausam die Welt vor den Toren Felwithes sein werde und wie
	sie ihrer Göttin Erollisi Marr am besten dienen könne. Die Strasse hinter ihr
	war immer gut besucht und normalerweise störte sie das auch nicht, doch heute
	war es anders. Sie spürte etwas. Einen Blick. Kein gewöhnlicher Blick. Sie
	konnte es nicht zuordenen. So etwas hatte sie noch nie gespürt.
	Intensiv. Leidenschaftlich. Grossherzig. Warm. Sie sprang erschrocken hoch und
	sah nur noch einen grauen Schatten, der verschwand. Einen Schatten, den sie
	nicht zuordnen konnte, der nicht in die Hochelfenstadt passte.
</P>

<P CLASS="P1">
	Jahre mögen vergangen sein. Thiameerah hatte ihre Ausbildung beendet und
	versuchte der Göttin der Liebe - %quot;Erollisi Marr&quot; - mit all ihrer Kraft
	zu dienen. Ihre erste Aufgabe war es, Felwithe vor den Feinden zu schützen, doch
	bedingt durch ihre Neugierde hielt sie es nicht lange dort aus. Sie folgte
	den Pfaden, die ausserhalb Felwithes verlaufen und kam bald zu einer Stadt.
	Eine Stadt in den Bäumen: Kelethin. Fasziniert von dem geschäftigen Treiben
	der offensichtlich verwandten Rasse, die in den Bäumen mit unglaublicher
	Gewandtheit umherliefen, entschloss sie sich ihrer Göttin hier zu dienen. Auch
	kam es hier zur zweiten Begegnung. 
</P>

<P CLASS="P1">
	Nicht weit entfernt von Kelethin liess Thia sich auf der Wiese nieder um mit
	mit Erollisi Marr zu sprechen. Plötzlich fühlte sie sich bedroht, drehte sich
	um und sah nur ein weit aufgerissenes Wolfsmaul, das sie jeden Moment zu
	verschlingen drohte. Sie mobilisierte sofort all ihre Kräfte und liess all ihre
	Gewalt auf das Untier nieder. Als sie sah, dass das nichts nutzte versuchte sie
    sich in der Flucht und erreichte kurz darauf die Wächter der Stadt Kelethin. Doch
	was war geschehen. Das Untier war ihr nicht gefolgt. Sie wartete, bis sich ihre
	Kräfte wieder gesammelt hatten und ging nochmal zurück. Doch der Wolf war
	verschwunden. Verschwunden? Nein. Sie spürte seine Anwesenheit und war sich
	unsicher. Unsicher darüber, was sie sonst noch spürte. Thiameerah war noch
	jung, aber sie konnte trotz ihrer geringen Erfahrung zwischen Gut und Böse
	unterscheiden. Warum spürte sie keine Gefahr von dem Wolf?
</P>

<P CLASS="P1">
	Einige Wochen später schritt Thiameerah wieder einmal über die Hängebrücken von
	Kelthin, erstaunt über das Geschäftsleben und das bunte Treiben der
	Schausteller, die mit unglaublicher Sicherheit in den Bäumen ihre Kunststücke
	vorführten. Thia musste aufpassen. Eine Hochelfin in den Bäumen, das ist
	gefährlich. Zumal sie nie ihre Angst vor der Höhe völlig überwinden konnte. Auch
	liebte sie das geschäftige Treiben vor der Bank. Dort sass sie oft stundenlang
	und sah interessiert zu. Dort war es auch, wo sie die dritte und entscheidende
	Begegnung hatte.
</P>

<P CLASS="P1">
	Gerade, als sie sich auf den Weg machen wollte, um ihrer Göttin wieder einen
	Dienst zu erweisen, stand er da. Der Wolf. Derselbe Wolf, den sie schon früher
	getroffen hatte. Ein Wolf? Hier? Wie? Was? Wo? Tausend Fragen schossen Ihr durch
	den Kopf. Keiner der Hüter Kelethins schritt ein. War sie verloren?
</P>

<P CLASS="P1">
	Das Tier schien ihre Gedanken zu erraten und verzog seine Lefzen zu so etwas wie
	einem Lächeln. Thiameerah wurde sich unsicher. Was sollte sie tun? Angreifen? Weglaufen?
	Doch der Wolf begann zu sprechen. <I>&quot;Habe keine Angst mein Kind. Ich bin
	Kinween. Kinween Talonhand.&quot;</I> Gleichzeitig mit diesen Worten verwandelte
	er sich in einen Waldelfen. Ein Waldelfe? Thiameerah war nicht klar was passiert
	war. War es nun ein Wolf, oder war es nun ein Waldelfe. <I>&quot;Ich bin ein
	Druide, und mir wurde von Tunare die Gabe gegeben mich in die Form eines Wolfes
	zu verwandeln, fürchtet Euch nicht.&quot;</I> Thiameerah war erleichtert.
</P>

<P CLASS="P1">
	Sie musterte den Waldelfen. Er war anders. Irgendwie anders. Er strahlte eine
	Faszination aus, die sie fesselte. Sein Köper machte einen gestählten Eindruck. Hie
	und da war eine Narbe zu sehen, die auf schwere Kämpfe hinwiesen. Er hatte
	dunkles Haar, ein schönes Waldelfengesicht und Augen in denen sich Thiameerah
	sofort verlor. Wenn Thia etwas faszinierente dann waren es schöne Augen. Noch nie
	zuvor hatte sie solche wundervollen Augen gesehen und sie konnte ihren Blick nicht
	mehr abwenden.<br>
	<I>&quot;Lange beobachte ich Euch schon&quot;</I> sagte Kinween. <I>&quot;Ich bitte
	Euch mir die Ehre zu erweisen. Darf ich Euch ein Weílchen begleiten? Meine
	Gefährten sind zur Zeit auf Wanderung und ich würde Euch gerne näher
	kennenlernen.&quot;</I> Thiameerah wusste nicht, was sie sagen sollte. Sie war
	froh, endlich das Rätsel dieses Wolfes zu kennen. Sie blieb ihm die Antwort
	schuldig. Kinween zögerte nicht lange und lud sie nochmals ein, ihm zu folgen.
	Thiameerah blieb auch diesmal die Antwort schuldig und so nahm Kinween ihr die
	Entscheidung ab. Er öffnete ein Portal und brachte sie an einen Ort, den sie
	nicht kannte. Butcherblock, wie er ihr sagte. Thia erwachte wieder aus ihrer
	Unfähigkeit zu handeln und war völlig verblüfft über die Dreistigkeit des Waldelfen.
	Sie, eine edle Hochelfin ... Er, ein einfacher Waldelfe. Viele Geschichten waren
	ihr erzählt worden über die Baumbewohner. Sie war neugierig. Unsicher blickte
	sie sich um. Natürlich wusste Thiameerah, dass niemand in der Nähe war, der sie kannte. 
</P>

<P CLASS="P1">
	Doch die Geschichten, die sie gehört hatte und die Meinung, die sie sich in
	Kelethin gebildet hatte wurden mit einem Male unwahr. Mit ausgewählter Höflichkeit
	und mit Manieren, die nur aus einer Hochelfenerziehung entsprachen behandelte
	Kinween Thiameerah. Er breitete sein Cape aus und machte es ihr so angenehm wie
	möglich. Die Unterhaltung, die folgte war amüsant sowie interessant. Beide
	vergaßen die Zeit und so verging Stunde um Stunde. Kinween verstand es, in einer
	unglaublichen Weise von seinen Reisen zu berichten. Thiameerah lauschte gebannt
	seinen Ausführungen und hoffte, dass er niemals aufhören würde zu reden. Sie
	versank wieder in seinen wunderschönen Augen.
</P>

<P CLASS="P1">
	Von diesen Moment an trafen sich die Beiden so oft es nur ging. Keine Stunde
	verging, in der sich Thiameerah Kinween nicht herbeisehnte. Oft ging sie zu
	den Plätzen mit den wundersamen Portalen und wartete Stunde um Stunde
	darauf, dass er kommt. Sie wusste, dass Kinween unterwegs war um Tunare zu
	dienen. Sie wusste, dass Kinween mit seinen Freunden grosse Taten vollbrachte.<br>
	Es vergingen Wochen und Monate des Glücks. Beide schworen sich ewige Liebe.
</P>

<P CLASS="P1">
	Drei Jahre waren wohl vergangen. Kinween, der Thiameerah in einem der zahlreichen
	Inns in Kelethin über seine jüngsten Erlebnisse erzählte, erhielt eine
	Botschaft. Er öffnete sie und bekam ein Leuchten in den Augen, dass Thiameerah
	erschreckte. <I>&quot;Endlich&quot;</I> sagte er. Seine Freunde ließen ihm die
	Botschaft zukommen. Kinween erzählte Thia von einem Gegner, der gewaltiger und
	grösser sein solle, als jeder Drache, der auf Norrath jemals gesehen wurde. Thiameerah
	überkam Angst. Grosse Angst. Sie hatte eine Vorahnung. Sollte sie flehen und
	betteln, dass Kinween nicht gehen solle? Nein, sie wusste, dass sie ihn nicht
	aufhalten konnte. Kinween war kaum noch zu halten.
</P>

<P CLASS="P1">
	Tage vergingen, aber keine Botschaft kam von Kinween. Thiameerah versuchte ihn
	zu finden. Doch wo? Sie wartete und hoffte auf ein Zeichen von ihm.
</P>

<P CLASS="P1">
	Es waren neun schlaflose Nächte und rastlose Tage, als plötzlich ein Raunen
	durch Kelethin ging. Ein Dwarf war in Kelethin angekommen und erzählte von
	einer Schlacht. Einer großen Schlacht. Thiameerah wusste sofort, dass es sich um
	die Schlacht handelte, in die auch ihr geliebter Kinween gezogen war.
</P>

<P CLASS="P1">
	Der Krieger erzählte von dem aussichtslosen Kampf, wie Freunde und Mitstreiter
	starben. Doch die Allianz Norraths, die aus den verschiedensten Rassen gebildet
	worden war schien Erfolg zu haben, als schreckliches passierte.<br>
	<I>&quot;Der Gegner mobilisierte noch einmal alle Kräfte.&quot;</I> Der Krieger
	erstarrte bevor er fortfuhr. <I>&quot;Er schuf ein Portal, ein riesiges Portal&quot;</I>
	beschrieb er mit zitternder Stimme. <I>&quot;Ein Portal, dass ich so noch nie
	zuvor gesehen habe. Magie gegen Magie, das schien das einzige Mittel&quot;</I> sagte
	er. <I>&quot;Unsere magischen Freunde stellten sich dem Gegner und versuchten ihn
	mit all ihren Kräften endgültig zu vernichten, jedoch gab ihm die magische Energie
	noch einmal für kurze Zeit Kraft und er floh durch das Portal. All die, die ihn in
	dem Moment zu vernichten suchten, wurden vom Sog der Magie ergriffen und durch das
	Portal geschleudert.&quot;</I>
</P>

<P CLASS="P1">
	Thiameerah versuchte den Krieger zu erreichen um von ihrem Liebsten zu
	erfahren, jedoch wusste sie auch ohne Antwort, warum keine Botschaft mehr ankam ...<br>
	<br>
	Und verdammt sei die Liebe, denn sie währt ewig......
</P>

<p class="P0" ALIGN="CENTER"><I>Thiameerah</I></p>

