<P CLASS="P1">
	Du sollest nur Sprüche kaufen die du brauchst. Denn die Geldsituation ist für Kleriker schwierig, da sie Sprüche und Rüstung benötigen. Man bekommt immer mehrere Sprüche auf einmal. Die Sprüche der 3 Spruchstufe kosten ungefähr 6 Platin pro Spruch.<br>
	Spruchstufe 1 (Level 1-4) Kaufe dir nur den Spruch "Strike", den aber so schnell wie möglich. Es ist ein guter Kampfspruch und du kannst zaubern während des Kampfes üben.<br> 
	Spruchstufe 2 (Level 5-8) Hier gibt es viele gute Sprüche. Der wichtigste ist "Leichte Heilung" danach "Heilige Rüstung". "Wasser beschwören! ist absolut nötig um Geld zu sparen. Kauf "Wasser beschwören" als erstes um das Geld für Wasser zu sparen, dann kannst du dir eher die andern Sprüche kaufen. Wenn du dann noch "Leichte Heilung" und "Heilige Rüstung" hast, kannst du dir noch "Ward Undead", ein guter Spruch gegen Untote, und "Gate", der nützlich ist für Leute die nicht gern laufen, kaufen.<br>
	Spruchstufe 3 (Level 9-13) Die Sprüche werden teuer. Kauf als erstes "Essen beschwören", der ist nicht so teuer und hilft dir Geld sparen. Dann kauf "Center" der gibt dem Ziel 50 Trefferpunkte dazu. Als nächstes "Hammer of Warth", eine Waffe. Der Spruch "Root" ist auch noch ganz nützlich. Er hält dein Opfer fest.
</P>

