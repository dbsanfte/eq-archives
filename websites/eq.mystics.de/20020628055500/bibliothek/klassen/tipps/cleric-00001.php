<P CLASS="P1">
	Hat sich eine Gruppe gefunden, sollte als erstes der Anführer festgelegt werden, da es, wenn jeder ein anderen Gegner angreift, ein schlimmes Ende nimmt. Alle sollten dem Anführer folgen und nur die Gegner angreifen, die der Anführer als erstes angreift.<br>
	Der Anführer ist im besten Fall ein "tank". Das ist ein Kämpfer der, der den Schaden des Gegners auf sich nimmt. Krieger und Mönche sind die besten "tanks" da sie die meisten Trefferpunkte haben.<br>
	Nimmst du die Hälfte von deine Trefferpunkten Schaden, ziehe dich aus dem Kampf zurück und überlasse es den andern. Das Monster greift dann jemand anderen an (meistens).<br>
	Das "looten" des besiegten Gegner wir meistens abwechselnd nach dem Alphabet gemacht. Der Anführer sagt meistens an wer mit dem "looten" dran ist.
</P>

