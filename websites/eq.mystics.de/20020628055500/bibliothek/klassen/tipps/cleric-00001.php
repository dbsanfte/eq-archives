<P CLASS="P1">
	Die erste Rüstung die du bekommen kannst ist Lederrüstung. Die kann von anderen Spielern günstig (2-4 gp) erworben werden und bietet einigermaßen Schutz. Da du nicht so oft der "tank" der Gruppe sein wirst brauchst du keine so gute Rüstung. Gebe dein Geld lieber für Zaubersprüche aus. Eine Möglichkeit deine Rüstung zu verbessern, ist es mit einer Gruppe in ein Dungeon zu gehen. Dort kann man Rüstungen finden. 
</P>

