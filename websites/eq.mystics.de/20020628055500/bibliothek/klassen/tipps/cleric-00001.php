<P CLASS="P0" ALIGN="CENTER">
	Diese Tips sind von Ben Stahlauge von EQ Stratics Infos &uuml;bersetzt und mit 
	eigenen Informationen erg&auml;nzt worden. Der Stand der Infos ist Betaphase 4, 26.02.99.
</P>	

<TABLE BORDER="0" CELLPADDING="0" CELLSPACING="0" WIDTH="100%">
