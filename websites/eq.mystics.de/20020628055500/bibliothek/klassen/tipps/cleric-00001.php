<P CLASS="P1">
	Als erstes musst du deine Gilde finden und deine Brief, den du zu Beginn erhältst abgeben. Dafür bekommst du eine Stoffrüstung. Die ist nicht so wichtig, bringt aber etwas Rüstung. Sie wir bald durch eine Lederrüstung ersetzt, deshalb muß du dir keine Sorgen machen, wenn du sie verlierst. Sobald du den Brief überbracht hast bekommst du Erfahrungspunkte und 5 Fertigkeitspunkte die du verteilen kannst. Wenn du auf dein Lehrer mit der rechten Maustaste anklickst kannst du die Punkte verteilen. Gebe sie alle für deine einhändige Schlagwaffe (1H Blunt) aus, denn damit beginnst du.
</P>

<TABLE BORDER="0" CELLPADDING="0" CELLSPACING="0" WIDTH="100%">
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
<TR><TD BACKGROUND="/pics/pageback2.jpg" ALIGN="CENTER" CLASS="P5">Jagen und Lernen</TD></TR>
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
</TABLE>

<P CLASS="P1">
	Wie jede andere Klasse in dem Spiel auch, werden Kleriker die ersten 1-3 oder 4 Level alleine verbringen. Die ersten Level sollten nicht nur dazu dienen Erfahrungspunkte zu bekommen, sondern man sollte sich auch mit seine Klasse vertraut machen. Du solltest so schnell wie möglich den Zauberspruch "Strike" kaufen. Wenn du ihn hast wende ihn so oft als möglich im Kampf an, damit du lernst die Sprüche zeitlich richtig einzusetzen. Du musst lernen, deinen Zauberspruch im Moment zwischen den Schlägen deines Gegners zu sprechen, da du sonst unterbrochen wirst.<br>
	Bevor du ein Gegner angreifst, schau ihn dir mit /con (Consider)an. Dadurch lernst du, welche Gegner du bezwingen kannst und welche nicht. Auch Gegner von der gleichen Art sind unterschiedlich Stark. Der Tod bringt in den ersten 4 Leveln keine Nachteile, also ist es nicht zu schlimm. Merk dir nur, wo du gestorben bist, damit du deine Gegenstände wieder bekommst. Im 1 und 2 Level solltest du dich an Gegner deren -con "looks like you would have the upper hand" anzeigt halten. Die bringen genug Erfahrungspunkte und sind bezwingbar in den ersten Leveln. Im Level 3 und 4 solltest du dich auf "looks like an even fight" konzentrieren um mehr Erfahrungspunkte zu erhalten.<br><br>
	Übe in Kampf zu zaubern. Du kannst dich im Kampf selber heilen, speziell bei einfachen Gegnern ist das Risiko gering. Drücke F1 um dich auszuwählen. Dann heile dich zwischen den Schlägen. Danach mit F8 wieder den Feind auswählen. Wenn du die Technik beherrscht kann es dir später das Leben retten.<br><br> 
	Wenn du dich dem 5 Level näherst solltest du dich auf Gegner konzentrieren, die möglichst viel Geld einbringen. Denn die Zaubersprüche kosten Geld und im 5 Level brauchst du 1-2 Platin.
</P>

