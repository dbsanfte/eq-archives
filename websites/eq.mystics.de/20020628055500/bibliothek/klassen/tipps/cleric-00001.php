<P CLASS="P1">
	Nach dem ersten Training (alles auf 1H Blunt, das Haus der Kleriker befindet sich in Northkaldim von der Miene aus einen Gang entlang) und dem lernen der Zaubersprüche beginne direkt vor dem Eingang von Kaladim Fledermäuse und Schlangen zu jagen.<br>
	Lege dir deine Zaubersprüche auf die "Shortcuts" damit du sie schneller nutzen kannst. 
	Benutze F8 um deine Feinde auszuwählen und F1 um dich auszuwählen.
	Benutze /con (Consider) um die Stärke der Feinde anzuschauen bevor du angreifst. Übe im Kampf zu Zaubern.<br><br>
	Bald danach kannst du Decaying Sekeletons, goblin welps, lager skunks und dann die Spinnen jagen.<br>
	So ab dem 3. Oder 4. Level nimm ein Fertigkeitspunkt auf Tailoring um die Grundfertigkeit zu erwerben.
	Dann geh nach Greater Faydark in die Newbiegebiete, denn dort gibt es Wölfe. Aus den RUINED Fellen kannst du dann Patchwork Armor machen (falls du Geld für Schnittmuster hast). Du solltest vermeiden dort zu sterben, da du sonst etwas weiter laufen musst.<br>
	So ab dem 4 Level ist es einfacher in Gruppe zu jagen. Da kann man schwierigere Monster jagen und hat deutlich höhere Überlebenschancen.  
</P>

