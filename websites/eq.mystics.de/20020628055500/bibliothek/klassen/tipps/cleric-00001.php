<TR><TD BACKGROUND="/pics/pageback2.jpg" ALIGN="CENTER" CLASS="P5">Einleitung</TD></TR>
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
</TABLE>

<P CLASS="P1">
	Kleriker sind Priester und Priesterinnen denen die Kraft von ihren Göttern gegeben wurde zu Heilen und anderen zu helfen. Sie verstärken jede Gruppe in ihrer Rolle als Heiler. Sie sind wie keine andere Klasse für den Kampf mit Untoten geeignet. 
</P>

<TABLE BORDER="0" CELLPADDING="0" CELLSPACING="0" WIDTH="100%">
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
<TR><TD BACKGROUND="/pics/pageback2.jpg" ALIGN="CENTER" CLASS="P5">Charakter erstellen</TD></TR>
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
</TABLE>

<P CLASS="P1">
	Der Zeitpunkt an dem du dein Charakter erstellst, die Rasse und die Startattribute bestimmst ist der wichtigste Teil bei der Entwicklung deines Charakters und sollte nicht unterschätzt werden.<br>
	Da der Kleriker in erster Linie Zaubersprüche benutzt sollte man sich hauptsächlich auf Weisheit konzentrieren. Als Kämpfer ist er auch ganz brauchbar. Er wird zwar nie so gut sein wie Krieger aber in der Gruppe können sich Kampffertigkeiten als nützlich erweisen. Deshalb solle man auch ein paar Punkte für Stärke und Ausdauer ausgeben.
</P>

<P CLASS="P1">
	Ok, was tun mit den Fähigkeitspunkten ? Also wir haben 3 Attribute zum verteilen: Stärke, Ausdauer und Weisheit. Die Punkte auf 3 Attribute zu verteilen wäre unklug. Also lassen wir Ausdauer weg, denn die wirkt sich auf die Trefferpunkte aus. In den unteren Leveln ist das ganz nützlich aber auf lange Sicht Verschwendung. Die Kleriker sind nicht dazu da den Schaden zu absorbiert. Das ist die Aufgabe der Krieger und Mönche, die "Tanks" genannt werden. Also bleiben Stärke und Weisheit. Je nach dem welche Rasse du gewählt hast solltest du die Punkte verteilen. Niedrige Stärke hohe Weisheit, dann alles auf Stärke oder andersherum. Sollten die Attribute ungefähr gleich sein solltet du Weisheit bevorzugen, da das das Primärattribut ist. 
</P>

<TABLE BORDER="0" CELLPADDING="0" CELLSPACING="0" WIDTH="100%">
