<P CLASS="P1">
	Es ist wichtig seine Fertigkeitspunkte richtig auszugeben. Bis zum 4 Level sollten alle für die einhändige Schlagwaffe (1H Blunt) ausgegeben werden. Während des Kampfes wird die Fertigkeit auch steigen. Es sollten solange Punkte dafür ausgegeben werden, bis es was kostet. Im 4 Level sollte man ein Punkt für Channeling ausgeben. Channeling wird beim Schreiben der Zaubersprüche und beim einprägen benutzt. Und es verhindert das du beim Zaubern unterbrochen wirst. Mit dem einen Punkt lernst du die Grundlagen. Von da an steigt der Skill von selber.<br>
	Im 8 Level bekommst du dann Meditation. Du kannst alle 5 Punkte dafür ausgeben, da es ein wichtiger Skill ist oder wieder nur 1 und dann steigt er (langsamer als Channeling) von selber.<br><br>
	Wenn du dann Fertigkeitspunkte übrig hast, kannst du sie für deine schwächeren magischen Fertigkeiten einsetzen. Z.B Conjuration ist bei einem Klericker eher meistens schwach. Wenn du da Punkte investierst vermeidest du, daß du beim beschwören von Nahrung und Wasser unnötig Mana verschwendest.
</P>

