<P CLASS="P1">
	Zwerge sind verhältnismäßig kleine Wesen, etwa zu vergleichen mit Halblingen, 
	jedoch nicht so "gemütlich dick" wie diese, sondern stämmig gebaut und sehr kraftvoll. 
	Ein Zwerg ist stolz auf seine Herkunft, hält die Menschen für ungeschickt die Halblinge 
	für albern, die Elfen für arrogant und sieht keinen Sinn in den ständigen Studien der 
	Zauberkundigen. Zwerge sind ungeheuer begabte Handwerker, die sich perfekt auf das schmieden 
	von Stahl und das bear-beiten von Holz verstehen. Auch das Brauen von Schnaps und Bier ist 
	eine der Künste welche die Zwerge perfektioniert haben, und die Menge an Alkoholitäten die 
	sie vertragen spottet jeder Beschreibung.
</P>

<P CLASS="P1">
	Sie leben den größten Teil ihres Lebens in großartigen Hölen, die sie selbst aus dem Berg 
	gehauen haben. Im Bergbau sucht man vergeblich nach ähnlich geschickten Arbeitern und ihre 
	Kenntnisse über Lagerstädten von Metallerzen, Gold- und Edelsteinadern haben sie zu einem 
	reichen und oft beneideten Volk werden lassen.
</P>
	
<P CLASS="P1">
	Die Bartmode der Zwerge ist einmalig auf Norrath und ein Scherzkeks, der so unvorsichtig 
	ist vor einem Zwerg Witze über dessen Bart zu reißen, hat seinen letzten Scherz gemacht. 
	Ansonsten sind Zwerge zwar brummelige, aber treue Weggefährten, die aber doch immer mal 
	einen Seitenhieb austeilen müssen, besonders in Richtung Elfen, etwa wie mit Worten: 
	"Machen wir es wie die Elfen, spitzen wir die Ohren." Es heißt sogar, daß man  nie einen 
	Zwerg und einen Elf in einem Zimmer übernachten lassen darf, sonst hat der eine am Morgen 
	eine Axt im Kopf oder einen Pfeil im Rücken und der Andere doppelte Marschverpflegung. ;-)
</P>

<P CLASS="P1">
	Überhaupt scheint die Axt eine der beliebtesten Waffen der Zwerge zu sein und wenn ein Zwerg 
	diese zum Vorschein bringt, ist es als ob ein Gaukler ein Kaninchen der Größe 4 aus einem 
	Hut der Größe 3 holt. :-) Im Kampf ist ein Zwergenkrieger einer "fürs Grobe", er stürtzt 
	sich mutig aber überlegt in das Getümmel und versucht alles um seine Kammeraden zu 
	unterstützen. Niemals, unter keinen Umständen würde ein Zwerg seine Gefährten verraten oder 
	im Stich lassen.
</P>
	
<P CLASS="P1">
	Im RPG ist man als Zwerg am besten in der Kaste der Krieger aufgehoben. Zwerge sind 
	ungeheuer Resistent und besitzen die Fähigkeit der Nachtsicht. In einer Party steht ein 
	Zwerg in der ersten Reihe des Kampfes, ist stets treu zu seinen Gefährten und hat immer 
	spitze Bemer-kungen für einen Elf übrig. Er läßt sich nicht unter den Tisch trinken und 
	antwortet auf Belei-digungen schlagfertig oder mit der Axt, je nach Grad der Verunglimpfung. 
	Ansonsten trägt ein Zwerg immer ein mürrisches Gesicht zur Schau, aber wer ihn näher kennt, 
	lernt ihn schnell als Freund schätzen und sieht in das im Grunde gute Herz.
</P>
	
<P CLASS="P0">
	<I>Vagnard aka Heinrich Kemmler</I>
</P>

