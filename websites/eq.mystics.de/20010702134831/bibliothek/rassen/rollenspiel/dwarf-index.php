<P CLASS="P1">
	Zwerge sind ein Volk, das wie die Hobbits oft unterschätzt wird. Durch ihre kleine Statur, 
	von 1,10m bis 1,60m (das sind die "kleinen Riesen" unter den Zwergen) und ihrem ziemlich 
	robusten Körperbau wirken sie recht schwerfällig, ungelenkig und ungeschickt. Wer 
	allerdings einmal einen Zwergendieb bei seinem Handwerk zugesehen hat, der wird schnell 
	vom Gegenteil überzeugt! Aber der Vorteil des Zwergs liegt doch in seiner, für seine Größe, 
	enormen Körperkraft, was sie zu zähen Kriegern macht. Wenn ihr schon einmal einen 
	Zwergenveteranen seine Axt habt schwingen sehen, wisst ihr was ich meine, da bleibt kein 
	Stein auf dem anderen. Vom Aussehen her, kann man gewisse Ähnlichkeiten mit den Hobbits 
	ausmachen, die aber, im Gegensatz zu den Zwergen  eher wie kleine Menschen aussehen. Zwerge 
	sind, wie schon gesagt, sehr robust gebaut und tragen grundsätzlich einen Bart, den sie hegen 
	und pflegen, und auf denen sie nichts kommen lassen... oder kann jemand berichten, das jemals 
	jemand überlebt hat, der eine abfällige Bemerkung über einen Zwergenbart gemacht hat? Ich 
	kenne keinen. Aber der Bart ist auch schon das einzige was sie pflegen, von Körperpflege 
	halten sie insgesamt recht wenig und Wasser verabscheuen sie wie die Pest. Das kann in einer 
	Gruppe schon ein paar Probleme geben, wenn man versucht in eine Herberge zu kommen, das 
	Gebäude muss danach nämlich erst einmal einige Wochen gelüftet werden! Die einzigen, die 
	sich möglicherweise regelmäßig waschen, mögen Zwergendiebe sein, denn es ist schon leicht 
	verräterisch, wenn auf einmal das ganze Haus nach Zwerg "duftet" in das man einbrechen 
	möchte.
</P>
	
<P CLASS="P1">
	Zwerge sind, anders als die Hobbits, nicht sonderlich wählerisch, was ihr Essen angeht, 
	mit einer ordentlichen Hausmannskost sind sie schon zufrieden und wenn man dann auch noch 
	einige Becher Zwergenbier dazu reicht, rülpsen sie laut und werden richtig gemütliche 
	Gesellen (und dann fangen sie an, eine Gruppe richtig anzuheizen und aufzumuntern). 
	Überhaupt sind Zwerge recht lustige Gesellen, zwar Fremden gegenüber immer etwas mürrisch, 
	aber wer sie näher kennenlernt wird merken, dass hier eine "harte Schale, aber ein weicher 
	Kern" vorliegt, aber natürlich, Ausnahmen bestätigen die Regel.
</P>
	
<P CLASS="P1">
	Ihre Behausungen haben Zwergen in jahrhundertelanger Arbeit in die Felsen von Kaladim auf 
	Faydwer eingegraben. Überhaupt sind sie Meister des Bergbaus und der Verhüttung von 
	Erzen, die sie auch gleich verarbeiten, indem sie an ihnen ihr Können als Schmiede auslassen. 
	Die besten Waffen sind von zwergischen Meistern erstellt, oft in jahrelanger Arbeit. Aber, 
	da Zwerge gute 400 Jahre alt werden können (was wirklich kein Alter für Zwerge ist, man 
	berichtete schon oft von 500 Jahre alten Zwergen), macht ihnen das eine oder andere Jahr 
	nichts aus. Mit 50 Jahren werden Zwerge mündig und dürfen ihre Familien verlassen.
</P>

<P CLASS="P5">
	Der Zwerg und seine Klassen
</P>

<P CLASS="P1">
	<B>Der Rogue</B><BR>
	Zwerge sind durch ihre Größe und ihre Geschicklichkeit im Umgang mit Dietrichen wie dazu 
	geschaffen anderer Leute Dinge zu finden, die diese noch gar nicht verloren haben. Ihre 
	Neugier zwingt sie förmlich dazu, in verschlossenen Truhen, Räumen oder anderen Orten nach 
	Schätzen und anderen wertvollen Dingen zu suchen.<BR>
	Schwierigkeiten können sie meistens mittels ihres 6. Sinns (der sprichwörtlichen "Zwergnase") 
	aus dem Weg gehen. Der Zwergendieb sieht sein Handwerk nicht als kriminell an, er ist 
	einfach nur der Meinung, dass der Gegenstand, der den Besitzer wechselt, einfach nicht zu 
	seinem früheren Besitzer passte...<BR>
	In einer Gruppe kann dies zu einigen kleinen Problemen führen, wenn der Dieb mit seinem 
	Anteil nicht ganz einverstanden ist, obwohl das nur sehr, sehr selten vorkommt.
</P>
	
<P CLASS="P1">
	<B>Der Krieger</B><BR>
	Zwergenkrieger sind wahre Meister ihres Faches. Im Umgang mit einer Axt ist ihnen keiner 
	gewachsen, nicht einmal die Barbaren. Durch ihre Größe werden sie allerdings leicht 
	unterschätzt, aber meistens fehlt dem Irrenden die Zeit, um sich vor der wütenden kleinen 
	Kampfmaschine in Sicherheit zu bringen, und muss böse Verwundungen hinnehmen, wenn nicht 
	schlimmeres....<BR>
	Im Kampf sind Zwergenkrieger ihrer Gruppe absolut loyal gegenüber. Eher würden sie sterben, 
	als ihre Freunde dem Gegner zu überlassen. Allerdings, dumm sind sie durchaus nicht, und 
	wissen auch, wie man mit einer Armbrust vortrefflich umzugehen weiß, und Hinterhalte legt, 
	auch wenn sie sonst ihre Ehre über alles stellen.<BR>
	Wer einen Zwergenkrieger mit in seiner Gruppe hat, sollte darauf achten, ihn nicht allzu 
	sehr zu reizen, ein kräftiger Ellenbogenhieb in die Magengegend raubt auch einem Barbaren 
	schnell die Atemluft.
</P>
	
<P CLASS="P1">
	<B>Der Kleriker</B><BR>
	Ein Kleriker ist durch seine heilende Magie ein äußerst wichtiges Mitglied einer 
	Abenteuergruppe. Immer auf das Wohl seiner Freunde bedacht, setzt er auch sein eigenes 
	Leben ein, um das seiner Freunde zu schützen.<br>
	Durch seine Magie kann er einige brenzlige Situationen noch herumreißen und auch die eine 
	oder andere schwere Verletzung versorgen. Auch im Kampf leistet der Zwergenkleriker recht 
	gute Arbeit, hie und da eine Verwünschung hat schon so manchen Darkelv davon überzeugt 
	frühzeitlich das Schlachtfeld zu verlassen...
</P>
	
<P CLASS="P1">
	<B>Der Paladin</B><BR>
	Die "heiligen" Krieger unter den Zwergen kämpfen wie die Krieger lieber mit den Waffen, 
	als mit irgendwelchem "Flim Flam Funkel", aber machen sich die Kraft ihres Gottes zu nutze.<br>
	Sie sind, wie jeder Zwerg eine Bereicherung jeder Abenteuergruppe und haben diese schon oft 
	aus Gefahren befreit.
</P>

<P CLASS="P5">
	Der Zwerg und die anderen Rassen
</P>

<P CLASS="P1">
	Anderen Rassen gegenüber verhalten sich Zwerge eher zurückhaltend und mißtrauisch.
	In den Elfen haben sie bekanntermaßen ihren Erbfeind gefunden, "Spitzohr" "Elfenragout" 
	und "bleicher Zaubererschnösel" sind nur einige von hunderten von Schimpfwörtern, die 
	sich Elfen gefallen lassen müssen. Die beiden Rassen gehen sich meistens aus dem Weg, und 
	wenn sie sich den doch begegnen, dann ignorieren sie sich normalerweise demonstrativ, das 
	hat vielleicht schon einigen Elfen das Leben gerettet ;)
</P>

<P CLASS="P1">
	Den Menschen gegenüber sind sie neutral eingestellt. "Lulatsch" werden sie oft genannt, und 
	wenn sie, zum Beispiel mit einem Eruditen Freundschaft schließen, dann werden auch gerne 
	Ehrennamen gegeben "Tharkûn" zum Beispiel, was "Stabmensch" bedeutet. Den Barbaren begegnen 
	sie nur wenig, aber wenn, dann artet das Treffen meistens in ein Wettrinken  aus, wobei der 
	Wirt meistens den Kürzesten zieht und eine neue Einrichtung bestellen darf.
</P>

<P CLASS="P1">
	Halblingen gegenüber haben sie schon ein besseres Verhältnis, was wohl an der Größe liegt 
	und den damit verbundenen Problemen, was sie zusammen mit den Gnomen, mit denen sie 
	allerdings der Streit, wer nun wirklich Brell Serillis Söhne sind, trennt, 
	zusammenschließt.
</P>

<P CLASS="P1">
	Aber natürlich seien auch hier 1, 2 Sticheleien für Gnome und Halblinge aufgeführt:
</P>

<UL CLASS="P1">
	<LI>Halblinge: "kleiner Plattfuß"  "Mondgesicht"<BR></LI>
	<LI>Gnome: "Winzling" (Bei knappen 1,20 ist das noch gestattet ;) "Oh, bin ich da auf was 
	getreten!?"<BR></LI>
</UL>

<P CLASS="P1">
	Ogger und Trolle mögen sie beide nicht sonderlich, was ja auch kaum verwunderlich ist, wer 
	mag schon jemanden, dessen Fuß schon fast so groß ist, wie man selbst.... Eine freundliche 
	Begrüßung à la "Muskelprotz mit Libellenhirn" wäre hier wohl angebracht.
</P>

<P CLASS="P1">
	Nichts desto trotz sind sie recht Gastfreundlich, wenn man sich zu benehmen weiß.
</P>
	
<P CLASS="P1">
	Natürlich ist das nur eine kurze Zusammenfassung von einem Zwergen, und auch keine 
	verbindliche Anleitung, aber so in etwa sieht ein Zwerg aus, und ich finde, in Everquest 
	sollte man schon einigen Wert auf Roleplay legen, also sich Gedanken über den eigenen 
	Charakter machen, und nicht ständig sein Verhalten ändern...
</P>
	
<P CLASS="P0">
	Ich hoffe euch hat diese Zusammenfassung ein wenig geholfen, ich hoffe wir sehen uns 
	in EQ, euer<BR>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<I>Durim</I>
</P>

