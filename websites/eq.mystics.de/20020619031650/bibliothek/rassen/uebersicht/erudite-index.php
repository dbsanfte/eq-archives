	<P CLASS="P1">
		Diese Abkömmlinge der Menschenrasse kennen nur zwei Ziele: Das Sammeln von Wissen, und die Perfektionierung 
		ihrer magischen Fähigkeiten. Sie gleichen mehr den Hochelfen, als dem normalen Menschen, und mit den, ihrer 
		Meinung nach, niederen Barbaren, lassen sie sich gar nicht erst vergleichen. Die Möglichkeiten der Magie 
		verstehen nur noch die Hochelfen besser zu Meistern, doch scheint dies langsam nicht mehr so sicher zu sein. 
		Sie besitzen eine hochentwickelte Kultur, und da die Eruditen auf einem abgelegen, eigenen Kontinent leben, 
		geben sie sich nur selten mit anderem Volk ab. 
	</P>
