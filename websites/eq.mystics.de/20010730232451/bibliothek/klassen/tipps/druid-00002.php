<P CLASS="P5">
	Die Charakter Wahl:
</P>

<P CLASS="P1">
	Viele sagen Halflinge wegen den Stats, Human's wegen dem Exp Bonus, Wald Elfen wegen Infravision. 
	Sorry aber alles Müll. Das könnt ihr wirklich alles den Hasen geben ;-)<BR>
	Ich habe mir mal alles angekuckt und hier mein Fazit zu den Rassen.
</P>

<P CLASS="P1">
	<U>Halfling:</U><BR>
	Vielleicht bessere Stats, aber sein Startpunkt ist Müll, er sieht "komisch" aus und am Anfang 
	ist er schwer, weil er dauernd verreckt, wenn er wo hin will. Er muß leider durch Mid-LvL Zonen 
	rennen um zu guten Newbie Zonen zu kommen. Das kann man von keinem verlangen also Halfling unten 
	durch.
</P>

<P CLASS="P1">
	<U>Human:</U><BR>
	Vergeßt es bitte sofort wieder. Ok, sie haben coole Gesichter. ABER, sie haben mist Stats, 
	sehen in Leder aus wie ein Gärtner und sind Nachts so Blind das man kaum 10 Meter weit kucken 
	kann. 
</P>

<P CLASS="P1">
	<U>Half Elf:</U><BR>
	Naja, fast das gleiche wie mit dem Human nur das er noch 25% Elfen Blut in sich hat. Leider 
	merkt man kaum was davon.
</P>

<P CLASS="P1">
	<U>Wood Elfen:</U><BR>
	Meiner Ansicht nach die beste Druiden klasse. Warum? Sie haben Hide, Forage und sind nicht 
	blind wie ein Fisch nachts. Es dauert sehr lange als Druide bis man ein Glühwürmchen solo 
	Killen kann, wegen dem Lightstone. Naja er sieht geil aus (der WE Druide) und er hat ein 
	perfektes Newbie Gebiet. Man rennt nicht aus versehen in ein High Lvl Monster rein und fällt 
	einfach um wie bei den Humans und Halflingen. 
</P>

<P CLASS="P5">
	Fazit:
</P>

<P CLASS="P1">
	Macht was ihr wollt ;-)) Ich kann nicht sagen NIMM xxx weil. Ich muß sagen es bockt echt net 
	was man für ne  Rasse nimmt. Wenn sie dir Spaß macht Spiel weiter ;-)
</P>

