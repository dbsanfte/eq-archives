<P CLASS="P1">
	Alles Wisdom! Wisdom = Mana und Mana = Überleben. Und wir Druiden haben das Problem das es 
	nicht so super duper Wisdom Items gibt. Also keine 20 Wis Tunic oder einen Glowing Stein mit 
	10 Wis. Also alles rein was geht. Die übrigen Punkte auf Str. Warum Str. ? Wenn ich mir 
	überlege was ich als Newbie alles aus Crushbone rausgeschleppt hab und wie überladen ich 
	war ;-) Naja alles was geht auf Wis und mit dem Rest könnt ihr spielen ;-)
</P>

