<P CLASS="P3">
	(Die Info's beziehen sich nun auf Wood Elfen Druiden)
</P>

<P CLASS="P1">
	Also du startest als armer Schlucker Druide, der nicht mal genug Geld hat sich, gescheite 
	Hosen zu kaufen. Was nun? Ok nimm deine 2 Spells und speicher sie. Dann geh zu deinem 
	Gildenboss und gib ihm die Rolle dafür bekommst du eine Tunic. Ok, dann geh mal Richtung 
	Bank und zaubere immer und immer wieder deine Buffs auf dich ( Dein Schild Spell ). 
	Das machst du deshalb, damit dein Skill hoch kommt. Wenn du einen High LV drunten siehst (
	lvl 14+) frag ihn höflich ob er dir ein paar Buffs spendiert. Wenn ich einen low LvL 
	Druiden an der Bank oder so treffe gebe ich ihm immer SoW einen Skin Buff und so. Das 
	tut mir nicht weh und der freut sich. Nach 10 Minuten kannst du dann los ziehen. Geh 
	(in der Nähe der Guards ) n bissl auf Jagt. Kill am Anfang NUR Bienen und so ein Zeugs 
	bis du verstehst wie der Kampf funktioniert. Und vergiß NIE(!) alles zu looten. 
	Du brauchst alles Geld was du bekommen kannst. Dein erstes Ziel sollten deine 
	Zaubersprüche sein. Wenn du nicht weist, wo die Gildenhalle ist hier die Anleitung:
</P>

<P CLASS="P1">
	Gehe zum PoD ( Priest of Discord [ das ist der Kerl in der schwarzen Robe beim zweiten Lift] ) 
	und gehe den Lift Hoch. Dann grade aus über die Brücke und links die Brücke hoch. Dann siehst 
	du rechts die Bank. Links gehst du noch eins hoch. Dann geht es rechts runter und du siehst 
	schräg rechts (ca. auf 2 Uhr) die Ranger Gilde. Nun gehe grade aus weiter noch eins runter 
	und auf 2 Uhr ist die Druiden Gilde. 
</P>

<P CLASS="P1">
	So, nun bist du daheim. Setzt dich hin, Rauch eine oder trink n Kaffee ;-) Welche Spells sollst 
	du nun kaufen ? Keine ahnung was ? Hmm kein problem ;-)<BR>
	Hier die Kauf Reihenfolge deiner lvl 1 Spells:
</P>
<UL CLASS="P1">
	<LI><I>Flame Lick</I>: Weil er AC Senkt (Gegner hat weniger Rüstung) und er macht mehr Schaden als Burst of Flame.<BR></LI>
	<LI><I>Snare</I>: Wenn ihr einen Gegner killen wollt und er rennt auf einmal weg. Hmm, das ist dumm, deshalb Snare immer benutzen. ( ich benutze ihn immernoch und bin lvl 35, ach ja lvl 50 Player nehmen Snare auch noch)<BR></LI>
	<LI><I>Burst of Flame</I>: Dein erster Nuke und macht 4-5 Schaden. Nicht die Welt aber naja ;-)<BR></LI>
</UL>
<P CLASS="P1">
	Und dann langsam den Rest kaufen.<BR><BR>
	<I>TIP: Versuche alle zu kaufen. Man weiß nie, wann man einen Spell mal gebrauchen kann.</I>
</P>

<P CLASS="P1">
	So nun hast du deine Spells. Was nun? Wie du gemerkt hast haben Bienen und so n Zeugs nichts 
	gescheites dabei. Deshalb killen wir nun Decayon Skeli's und Orcs. Die haben ab und zu sogar 
	Cloth dabei und manchmal auch ne Waffe. Da kommt dann so langsam das Geld ins Haus. <BR>
	Kauft euch langsam eure ersten Rucksäcke und sowas ein und wandert n bissl umher. In den 
	niedrigen Lvl's nicht Gruppen. Bringt euch echt nix. 
</P>

<P CLASS="P1">
	So das war mal der erste Teil im Leben eines Druiden. Ich hoffe ich konnte euch ein bißchen helfen.
</P>

