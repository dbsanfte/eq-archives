<P CLASS="P1">
	Ich möchte hier eine Druiden Einführung schreiben die etwas anders ist als die, die ihr schon 
	kennt.<BR>
	Jede "Einführung für die Klasse XXX" ist irgendwie gleich deshalb gestalte ich diese hier 
	etwas anders. Ich möchte neuen Druiden erklären und zeigen wie sie die ersten LvL's gut meistern 
	können.
</P>

