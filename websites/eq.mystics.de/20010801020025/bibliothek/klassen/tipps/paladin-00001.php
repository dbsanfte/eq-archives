<P CLASS="P1">
	Der Paladin ist anfangs ein schwacher Krieger. Er muß sich bis Level 9 mit dem durchschlagen, was ihm von 
	Anbeginn gegeben wurde. Sein einziger Trumpf ist der Spezialskill Lay Hands, der ihm einmal am Tag (also 
	alle 90 Minuten Spielzeit) ein "zweites Leben" schenkt. Dieser Skill wird dem Paladin und seinen Gefährten 
	noch sehr, sehr oft helfen. Man darf allerdings nicht den Fehler machen und den Lay Hands Skill als 
	Heilzauber mißbrauchen, um gegen viel stärkere Gegner zu bestehen. Ich rate davon tunlichst ab, es 
	ist ein Skill für den puren Notfall.<br>
	Der Weg bis Level 9 ist hart, aber dort angekommen tun sich dem Paladin ganz neue Möglichkeiten auf. 
	Heilung und Verstärkungszauber sind sein Repertoire. Zuerst bleibt nicht viel Mana um viel zu zaubern, 
	aber spätestens in Level 11 sieht es schon etwas besser aus. Nächster Halt Level 10 für Dodge und Bind 
	Wounds. Dodge ist ein wichtiger Skill um die Kampftauglichkeit etwas zu verbessern.<br>
	Level 12 bekommt man endlich Meditate und Bash (für Paladine mit vorwiegend 1H und Schild nützlich).<br>
	Level 17 beschert uns Parieren und Level 20 endlich Double Attack.<br>
	In Level 15 gibt's die nächsten Zaubersprüche, bessere Buffs und endlich den lang ersehnten Ward Undead, 
	sowie einen besseren Heilzauber.<br>
	Spätestens in Level 20 kann der Paladin schon ansehnlich kämpfen.<br>
	Mit Level 30 kommt noch Riposte dazu, steigt sehr schnell und ist extrem nützlich. Level 40 bring dann die 
	letzte Fertigkeit des Paladins, Disarm.<br>
	Ab Level 30 gibt's einen Stunspell, der sehr nützlich sein, kann sowie einen 100HP-Heal. Ab Level 39 hat 
	der Paladin endlich seinen Greater Heal.<br>
	Es sei noch kurz erwähnt, das der Paladin einen kleinen Erfahrungsmalus hat, der so im Bereich von 20% 
	liegt. Im direkten Vergleich z.B. mit einem Krieger wird er also etwas langsamer leveln.<br>
	Zur Rassenwahl: es stehen zur Auswahl Zwerge, Eruditen, Halbelfen, Hochelfen und Menschen. Erstmal sollte 
	die Wahl der Rasse unter rollenspielerischen Gesichtspunkten stehen und dann entschiedet man sich, welche 
	Attribute man mit den Bonuspunkten füttert (derer 20 nicht besonders viel sind).<br><br>
	<u>Der Zwerg</u> ist stark und geschickt aber "dumm", man sollte Wisdom sicher am stärksten pushen 
	(12 auf WIS bis 100, Rest auf DEX bis 98). Die niedrige Intelligenz läßt ihn Skills etwas langsamer 
	lernen, was sicher im Bereich der Magieskills merkbar wird, später aber kaum ins Gewicht fallen wird 
	da die Verweildauer in den einzelnen Levels recht lang wird und genügend Zeit für Training bleibt.<br>
	<u>Der Erudite</u> ist ähnlich dem Menschen, nur mit sehr viel höherer Intelligenz und Charisma. Ich würde 
	hier die Kriegerseite etwas verstärken.<br>
	<u>Der Halbelf</u> ist ähnlich wie der Zwerg die eher kriegerbetonte Variante des Paladins. Ich würde hier 
	Wisdom steigern.<br>
	<u>Der Highelf</u> glänzt durch hohes Wisdom, Charisma und Intelligenz. Seine körperlichen Attribute gilt 
	es etwas zu pushen, besonders Stärke (STA auf 80 und Rest auf AGI). Seine geringe Stärke macht dem 
	Hochelfen-Paladin in den unteren Leveln am meisten zu schaffen, es gibt allerdings etliche Gegenstände 
	im Spiel die seine Stärke auf weit über 100 bringen können.<br>
	<u>Der Mensch</u> ist überall "Mittelmaß", was einem die Spezialisierung in einen Bereich vereinfacht 
	(also entweder Kriegerseite pushen oder Klerikerseite). Zudem macht er mit der Möglichkeit schneller 
	zu leveln den Paladinmalus in der Erfahrung wieder weg. Rein optisch ist der Mensch als Paladin der 
	Ritter nach der arthurischen Tradition.<br><br>
	Wohin das alles führt: Paladine werden im Laufe der Zeit zu den mächtigsten Charakteren überhaupt, da sie einige 
	sehr besondere Waffen erhalten können, die NUR Paladine tragen dürfen: Unsere <b><i>Heiligen Schwerter</i></b>. 
	Es gibt etliche Klassenbezogene Waffen im Spiel, doch über keine wird sich so der Mund zerrissen wie über die
	Schwerter der Paladine (neidet man uns die guten Dinger? Liegt es daran das der Fiery Avenger vielleicht 
	ein Paladin only Schwert ist?).
</P>

