<P CLASS="P1">
	Nun da ihr den Weg des edlen Paladins gewählt habt laßt mich euch einiges mit auf den Weg geben: Ihr seid ein 
	"Heiliger Mann". Weniger wie die Kleriker verbreitet ihr das Wort eurer Gottheit in der Welt, aber ihr seid 
	auf einer Mission um Gutes zu tun. Das heißt nicht, das ihr jedem gleich sofort entgegeneilen müßt, um ihm 
	zu helfen. Auch ein Paladin kann eigennützig handeln, und das solltet ihr auch. Jedoch gebietet es unser 
	Anstand die Augen und Ohren offen zu halten nach Hilferufen jeder Art.<br>
	Und seid gewahr dessen, das ihr ein echter Ritter seid. Die letzten Ritter sind wir, und damit an eine Art Codex 
	gebunden. Ein Paladin hilft einem anderen Paladin IMMER. Und ihr solltet immer mit einer gewissen Würde durch 
	die Welt streifen. Leider wird dies oft als Hochnäsigkeit mißgedeutet, aber laßt euch von Unwissenden nicht 
	auslachen, denn es ist der blanke Neid der aus ihnen spricht.<br>
	Das Leben eines Ritters birgt viele Gefahren. Seid niemals feige oder ängstlich. Und ganz wichtig: Steht zu euren 
	Gefährten. Wir sind nicht unfehlbar und man wird es gedankt kriegen seine Freunde fair und gerecht zu behandeln. 
	Fairneß und Gerechtigkeit gehören genauso zu euren Tugenden wie Moral und Ehre.<br>
	Wenn ihr alles befolgt seid ihr bald eine echte Bereicherung für unsere Ritterschaft.<br><br>
</P>

<P CLASS="P1">
	In einer Gruppe hat der Paladin 2 Aufgaben: Er ist einerseits ein Tank der die Monster von den Klerikern 
	und Zauberern fernhält; aber auch ist er ein Heiler wenn es brenzlig wird. Spätestens ab Level 30 kann er 
	mit dem besseren Heilzauber diese Aufgabe gut erfüllen. Sein Lay Hands gehört natürlich genauso zum 
	Repertoire der Gruppe wie seine anderen Fertigkeiten, auch wenn dieser Skill nur für Notfälle ist.<br>
	Zum Solospiel ist der Paladin nicht geeignet. Selbst an blauen Gegnern tut man sich sehr schwer sie alleine 
	zu besiegen. Ab Level 30 sieht es etwas besser aus, sehr bedingt können blaue Gegner allein besiegt 
	werden, allerdings meist unter Aufbringung allen Manavorrates für Heilung, Root und Stun.<br>
</P>

