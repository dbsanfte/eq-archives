<P CLASS="P1">
	Der Paladin ist ein heiliger Mann, ein Holy Knight. Er ist zu ¾ Krieger und zu ¼ Kleriker, was ihn zu einer 
	außergewöhnlichen Gestalt macht. Es ist sicher eine kleine Herausforderung ihn zu spielen, denn seine Stärken 
	offenbart der Paladin erst spät. Rollenspielerisch bietet der Paladin ebenfalls eine gute Basis, allerdings 
	würde ich schon einige Kenntnisse im Bereich "Paladine in Fantasywelten" voraussetzen.
</P>

<TABLE BORDER="0" CELLPADDING="0" CELLSPACING="0" WIDTH="100%">
