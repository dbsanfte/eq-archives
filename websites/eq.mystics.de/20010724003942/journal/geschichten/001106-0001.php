<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Caratac Nomenasno<BR>
	<BR>
</P>

<P CLASS="P1">
Sonntags in East Karana oder wie man gründlich 18 wird ...
</P>

<P CLASS="P1">
Geschickt schwang er mit aller Kraft das Schwert und schlug dem grausigen Gnoll mit einem einzigen Hieb den Kopf ab. Röchelnd brach das haarige Ungetüm zusammen.<br>
Nun kann man darüber streiten, ob man ohne Kopf noch fähig ist zu röcheln; doch der Atmosphäre wegen lassen wir es hier mal so stehen. Und außerdem, wer von uns weiß schon was über die Biologie von Gnollen?
</P>

<P CLASS="P1">
<I>"Keine Exp"</I>, sagte der Shadow Knight verstimmt. Er wischte sein besudeltes Schwert am toten Körper des Gnolls ab. <I>"Da macht man die Drecksarbeit und kriegt nicht mal eine Belohnung dafür!"</I><br>
Cazhira lachte. <I>"Ich hab' Exp bekommen! Trotzdem dauert's noch bis 18."</I> Die Enchanterin seufzte und schlug ihr Spellbook auf. Immer wieder oom! Mittlerweile kannte sie jedes Detail des Spellbooks auswendig. Einmal zur Manaregeneration etwas anderes als das Spellbook lesen! Zum Beispiel "Die Irrlehren des Innoruuk", eine ketzerische Schrift, die sie als Agnostikerin ungestraft lesen durfte; Caratac hingegen, ein fanatischer Anhänger Innoruuks, wäre mit diesem Gedanken kaum einverstanden gewesen.
</P>

<P CLASS="P1">
<I>"Ich hab nicht mehr viel zu 18. Ich ..."</I>, begann Caratac, doch er wurde vom wütenden Knurren eines Gnolls unterbrochen, der des Shadow Knights Rücken dazu benutzte, seine Klauen zu schärfen. Der Kampf war kurz und blutig, doch da in dieser Welt strenge Moralgesetze herrschen, müssen wir auf eine detaillierte Beschreibung verzichten. Der geneigte Leser kann selbst seine Anmerkungen im Bedarfsfall hinzufügen.<br>
<I>"DING!"</I> schrie Caratac mit solcher Lautstärke, dass es auch seine Gildenfreunde zu Ohren bekamen.<br>
<I>"Grats!"</I> schallte es aus allen Himmelsrichtungen zurück.<br>
<I>"Grats, Cara!"</I> rief auch Cazhira und freute sich für ihren Freund. <I>"Na, wie fühlt man sich mit 18?"</I><br>
<I>"Großartig. Allerdings gibt's neue Spells erst mit 22."</I> Caratac seufzte. <I>"Und die Gnolle hier geben nicht all zuviel her ..."</I>
</P>

<P CLASS="P1">
Da hatte Cazhira eine Idee. Sie war so genial (die Idee), dass sie an Genialität kaum mehr zu überbieten war, um nicht zu sagen, sie (die Idee) war die Königin aller je von Dunkelelfen ersonnenen Ideen.<br>
<I>"Wie wär's wenn wir nach East Karana gingen?"</I> fragte die Enchanterin. <I>"Dort gibt's sicher noch genug Exp für uns zu holen."</I><br>
Es war unvorstellbar, wie sie auf solch eine grandiose Idee kommen konnte. Caratac war erschüttert ob dieses begnadeten Einfalls, und nach einem kurzen, untertänigen Kniefall vor Cazhira zogen sie los. Für die körperliche Ertüchtigung war gesorgt, da der Weg zur Ebene East Karanas durch eine Schlucht ziemlich lang war, wenngleich die beiden Dunkelelfen durch die Aussicht auf mächtige Felswände, die in strahlenden Farben das Sonnenlicht reflektierten, belohnt wurden.<br>
Cazhira unterbrach ihren Lauf in kurzen Abständen immer wieder mit einer Pause, da Caratac wieder einmal hoffnungslos überladen war und man seine Fortbewegungsart mehr als Kriechen denn als Laufen bezeichnen konnte. In der Ebene angekommen verschnauften sie erst einmal ein paar Stündchen.
</P>

<P CLASS="P1">
<I>"Also, mal sehen, was es in East Karana so alles gibt"</I>, meinte Caratac enthusiastisch.
Ein Blick auf die Karte brachte ein Gnoll Camp zutage, von dem sie beide sofort begeistert und sich einig waren, zuerst dort ihr Glück zu versuchen. Das Camp war rasch gefunden, es war frei, und zur Freude aller lungerten auch einige Gnolle herum (zwei oder drei, zählte Caratac), die sogar Namen trugen.<br>
Mit wildem Kriegsschrei warf sich Caratac in die Gnoll-Versammlung und merkte um eine Spur zu spät, dass a) je ein roter und ein gelber Gnoll darunter waren, und b) es mehr als nur drei waren, nämlich fünf.<br>
Cazhira, die Caratacs ungestümes Verhalten glücklicherweise nicht teilte (Dunkelelfinnen waren im allgemeinen besonnener als Dunkelelfen), war etwas zurückgeblieben und musste tatenlos mit ansehen, wie der Shaodw Knight von den Gnollen regelrecht in Stücke gehauen wurde.
</P>

<P CLASS="P1">
Innoruuk war etwas missgestimmt, weil wieder einmal ein Möchtegern-Held sich überschätzt hatte, und dem Gott dadurch Arbeit verschaffte. Die Aufgabe eines Gottes bestand nämlich im wesentlichen darin, die Bewohner der von ihm beschützten Welt wiederzubeleben. Ein öder Job, den Innoruuk schon lang nicht mehr ausüben wollte, doch das Arbeitsamt hatte im Moment keine andere Stelle für ihn frei. Außerdem durfte er sich hie und da an ein paar mächtigeren, aber immer noch Möchtegern-Helden rächen, die sich in seinen Garten verliefen, und das machte ihm höllischen Spaß.<br>
So führte also Innoruuk seine ihm zugewiesene Arbeit aus, und Caratac erwachte an dem Punkt, an dem er gebunden war, zu neuem Leben. East Karana, direkt an der Zone zu Highhold Pass. Am oberen Ende der Schlucht. Und er war wieder 17.
</P>

<P CLASS="P1">
<I>"Mist! Downgelevelt!"</I> rief er.<br>
<I>"Arghs! Gib mir mal consent!"</I> sagte Cazhira zu Caratac über Grouptell.<br>
<I>"Hm, du müsstest zu nah ran, Caz"</I>, meinte der wiederauferstandene Shadow Knight. <I>"Warte wenigstens, bis sie weg sind."</I> Nichtsdetsotrotz gab er unüberlegterweise die Consent-Erlaubnis, und die mutige Dunkelelfin nutzte sie sogleich aus, wobei sie erkennen musste, dass Mut allein gegen eine Übermacht an Gnollen nicht unbedingt ausreichend war.
</P>

<P CLASS="P1">
Kurz darauf fand sie sich, ebenfalls von Innoruuk wiederbelebt, neben Caratac wieder. (Hier muss man anmerken, dass Cazhira zwar Agnostikerin war, aber Innoruuk sich einen Dreck darum scherte, ob jemand an ihn glaubte, oder nicht. Auch er hatte seine Befehle ...)<br>
Die im Lichte strahlenden Felswände kamen ihnen bei weitem nicht mehr so beeindruckend vor, als sie ein zweites Mal die Schlucht hinunterliefen. Die Strecke zog sich, aber endlich gelangten sie zurück zur Ebene und machten sich daran, ihre toten Körper des Vorlebens zu suchen, um ihre Gegenstände wieder an sich zu nehmen. Die Gnolle waren inzwischen verschwunden, denn es war ihnen zu langweilig gewesen, auf die beiden mageren Dunkelelfen zu warten.<br>
<I>"So, vergessen wir das mal"</I>, meinte Caratac nach dem Loot. <I>"Gibt's hier sonst noch was Interessantes?"</I><br>
Sie hatten Glück und fanden einige blaue und schwarze Mobs, die ganz gute Exp gaben, schlugen sich mit ein paar Löwen und Schlangen, bis Caratac über Gildentell schließlich verkündete: <I>"DING!"</I><br>
<I>"????"</I> kam zurück, oder Fragen ähnlich <I>"Was? Wie? So schnell?"</I>
</P>

<P CLASS="P1">
Verlegen (und ziemlich kleinlaut) teilte der Shadow Knight seinen Gildenfreunden mit, dass er durch eine Verkettung unglücklicher Umstände gezwungen war, downzuleveln, selbst daran aber natürlich völlig schuldlos sei, und jetzt Level 18 noch einmal erreicht hatte. Peinlichkeit saß am Straßenrand und lachte sich ins Fäustchen.<br>
Cazhira medete inwischen, da weit und breit kein ernstzunehmender Gegner ins Sicht war; glücklicherweise, denn ohne Mana wären die beiden Dunkelelfen nur Beute anstatt Jäger. Sie las in ihrem Spellbook und spielte nebenbei mit ihren tiny daggers, mit denen sie gelangweilt an ihrer Halsschlagader herumstocherte. Ein Geräusch ließ sie aufblicken.
</P>

<P CLASS="P1">
<I>"Wie wär's mit dieser Spinne?"</I> rief sie, die Aufmerksamkeit des Shadow Knights auf die wesentlichen Dinge des Lebens lenkend. Caratac und sein frischgewonnener Mut waren sofort begeistert und stürzten sich auf das schwarze Ungetüm, das so groß war, dass sie nicht einmal seinen Namen erkennen konnten. Konzentriert unterstützte Cazhira ihren Freund, indem sie abwechselnd ihn positiv und die Spinne negativ buffte.<br>
Leider hatten die beiden Dunkelelfen eine äußerst wichtige Lektion anscheinend nicht gelernt: Spinnen mögen es nicht, wenn man eine ihrer Art angreift. Da können sie ziemlich sauer werden. Bereits in Nektulos Forest brachte man diese Lektion den kleinen Dunkelelfenkindern bei, man bläute sie ihnen geradezu ein.<br>
Cazhira und Caratac dürften offensichtlich "Spinnenkunde für Anfänger" geschwänzt haben; zu der eben angegriffenen Spinne gesellte sich eine zweite, dann eine dritte. Und keineswegs welche, die nur grün oder blau waren ...
</P>

<P CLASS="P1">
Caratac, mit etwas schwerfälligem Intellekt (wie gläubige Shadow Knights eben waren - die Gilde der Shadow Knights möge mir verzeihen), erkannte erst nach einer Weile, dass etwas ganz und gar nicht in Ordnung war. Als seine Hitpoints nur mehr aus einem Bubble bestanden, beschloss er, die Flucht zu ergreifen. Mal abgesehen davon, dass er, wie bereits erwähnt, überladen war, hatte er kaum mehr die Kraft zu kriechen, was aber nicht erwähnenswert ist, da er sowieso gestunned wurde.<br>
Auf irgendeine unerklärliche und völlig unergründliche Art und Weise freute er sich doch über dieses "you lost a level", denn so oft bekam man diese Mitteilung ja auch nicht zu sehen.
Die Spinnen verloren keine Zeit mit der Leiche Caratacs und wandten sich Cazhira zu, die ihrerseits von den grausigen Fängen flugs ins Jenseits befördert wurde.
</P>

<P CLASS="P1">
Am binding point (hatte ich schon erwähnt, dass dieser am oberen Ende der Schlucht lag?) fluchten sie ausgiebig über ihr Schicksal. Caratac war wieder 17, und Cazhira hatte die Hoffnung schon längst aufgegeben, je 18 zu werden. Die das strahlende Licht reflektierenden Felswände hatten diesmal äußerste Schwierigkeiten, den beiden Dunkelelfen zu gefallen und wurden so gut wie nicht beachtet, was schade war, denn die Felswände waren auf diese Fähigkeit besonders stolz.<br>
Nachdem der Weg zwar lang, aber ereignislos war, kürzen wir die Sache ab und fahren nach dem Loot der toten Körper fort.
</P>

<P CLASS="P1">
Die beiden setzten sich an den Wegrand und berieten ihr weiteres Vorgehen. Während sie diese und jene Möglichkeit abwogen, beobachtete Cazhira hinter Caratacs Rücken einen Punkt, der rasch näher kam. Hinter dem Punkt befand sich ein etwas größerer Punkt, der den ersten offenbar verfolgte. Die Punkte wurden zu Strichen, und diese entpuppten sich sogleich als ein flüchtender Warrior, der von einem riesigen, einäugigen Etwas verfolgt wurde. Selbstverständlich genau in die Richtung der beiden Dunkelelfen. Zufall oder Schicksal? Nun, man wusste vom Schicksal, dass es eine Vorliebe für genau solche bissigen Zwischenfälle hatte, wenn es nicht gerade mit Innoruuk Schach spielte.
</P>

<P CLASS="P1">
Die Enchanterin sprang auf, deutete an Caratac vorbei und schrie: <I>"Ein Cyclops!!!!"</I> Es spricht für die Intuition der Dunkelelfin, dass sie dieses große, gefährliche Etwas beinahe auf Anhieb identifizieren konnte, obgleich sie ja nie zuvor ein so großes, gefährliches Etwas gesehen hatte. Abgesehen von der Tatsache, dass Caster von Natur aus mehr Intelligenz besaßen, konnte sie lesen, und der Name des großen, gefährlichen Etwas stand in riesigen Lettern deutlich über ihm.
</P>

<P CLASS="P1">
Sie reagierte überraschend schnell, indem sie das einzig Richtige tat: sie gatete ...<br>
Während Caratac sich umdrehte und sagte: <I>"Was is'n ein Cyclops?"</I><br>
Der Schlag war kurz, aber heftig, die Hälfte der Hitpoints des Shadow Knights waren weg. Im Laufen dachte Caratac noch kurz darüber nach, dass er endlich mal sein Zeug verkaufen musste, das er so mit sich herumschleppte, und dass er dringend etwas brauchte, das ihm mehr Stärke verlieh. Der neben ihm flüchtende Warrior war schneller als der Shadow Knight, leider auch der Cyclops. Innoruuk fluchte und setzte den Namen des Shadow Knights auf seine Schwarze Liste. Sollte dieser je eines Tages zu den Planes kommen, dann ...
</P>

<P CLASS="P1">
Allerdings war dies zum Zeitpunkt mehr als unwahrscheinlich. Level 18 war in noch weitere Ferne gerückt. Während Cazhira am binding point wartete, in ihr Spellbook starrte und sich dabei mit ihren tiny daggers verstümmelte, lief Caratac ein weiteres mal die langgezogene Schlucht hinunter. Die das Sonnenlicht in strahlenden Farben reflektierenden Felswände hasste er inbrünstig. Die Felswände ihrerseits schmollten und straften ihn mit Mißachtung.<br>
Nach dem obligatorischen Loot eilte er den weiten Weg wieder hinauf, soweit man mit 140/93 eben eilen konnte. Die Felswände hatten demonstrativ begonnen, Karten zu spielen.
</P>

<P CLASS="P1">
<I>"Das war's dann wohl"</I>, meinte Cazhira, als Caratac schnaufend neben ihr zusammenbrach.
East Karana war für die beiden gestorben. Würden sie eben bei den Gnollen bleiben müssen.<br>
Plötzlich wurde es still um sie herum. Keine Vögel zwitscherten mehr, der Wind hörte auf zu pfeifen, die Sonne ging unter.<br>
<I>"Hm!"</I><br>
Die Welt stand still, da sich offenbar wieder eine dieser berühmten und außergewöhnlich genialen Ideen Cazhiras anzubahnen schien.<br>
<I>"Wie wär's mit Kithicor Forest? Wir müssen nur darauf achten, von dort zu verschwinden, bevor es Nacht wird ..."</I>
</P>

<p class="P0" ALIGN="CENTER"><I>Caratac Nomenasno</I></p>

