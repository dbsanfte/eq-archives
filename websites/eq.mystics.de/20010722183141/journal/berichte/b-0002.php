<P CLASS="P0" ALIGN="CENTER">
	Eingeschickt von Milaan Rabaa
</P>

<P CLASS="P1">
	Die Gilde <A HREF="http://www.drachenfels.com/" TARGET="_blank">Der Dunkle Pakt</A> veranstaltete
	am 25. September 1999 ein großes Event, dessen Ziel es war den Herrscher über Nagafen's
	Lair - Lord Nagafen persönlich - zur Strecke zu bringen.
</P>
 
<P CLASS="P1">
	Die Schlacht gegen den riesigen Drachen Nagafen dauerte ganze 3 Stunden an. Drei lange Stunden, bei denen
	viele der tapferen Helden ihr Leben lassen mussten! Die Bilder und Beschreibungen sind uns von Milaan Rabaa
	zur Verfügung gestellt worden - vielen Dank nochmal an dieser Stelle. Aber nun seht selbst...
</P>

