<P CLASS="P1">
	Der Kleriker ist ein heiliger Mann, von seiner Gottheit mit der Kraft zu schützen und zu heilen ausgestattet.
	Aber er kann die Kräfte seines Gottes auch anrufen, um Verdammung und Elend über seine Feinde zu bringen. Er
	ist kein Priester, so wie wir sie kennen, sondern ein Streiter für seinen Glauben, den er auch mit Waffengewalt
	durchsetzen kann wenn er muss. Er kann zwar niemals so gut kämpfen wie ein reiner Krieger, deshalb wird er sich
	wohl eher auf die Hilfe seines Gottes verlassen.
</P>	

