<P CLASS="P5">Playerinformationen</P>

<P CLASS="P1">
	Um einen andern Spieler anzuvisieren, muß man diesen anklicken. Darauf erscheint dessen 
	Namen mit dem roten Balken für die Hitpoints daneben, rechts in der Mitte des Bildschirms. 
	Ein Doppelklickt offenbart einem, wie schwer es wäre, diesen Player zu besiegen. Wenn man 
	nahe genug an dem Player steht, wird dessen Inventory angezeigt und man kann es 
	untersuchen. Im unteren Bereich des Bildschirms steht eine kurze Playerbeschreibung, 
	die jeder Player selbst editieren kann. Um seine eigene Beschreibung zu ändern, muß man 
	F4 drücken, damit man sich selbst sieht, dann muß man sich selbst inspizieren, 
	doppelklicken und den Text editieren. Dieser Text ist für alle sichtbar, die den 
	Charakter inspizieren.
</P>
	
<P CLASS="P5">Handel</P>

<P CLASS="P1">
	Um mit einem anderen Spieler zu handeln, muß man den Gegenstand, den man übergeben will, 
	anklicken und damit aufnehmen, dann auf den anderen Player ziehen, klicken und damit 
	fallen lassen. In dem dann erscheinenden Trade Menu ist es dann möglich, noch weitere 
	Gegenstände einzufügen. Um den Handel perfekt zu machen, muß man den Trade-Knopf 
	drücken, dadurch wird der eigene Name hellgrün auf dem Trade Window dargestellt. Der 
	Handel ist dann vollzogen, wenn der andere Player ebenfalls den Tradeknopf betätigt. 
	Wenn man weitere Gegenstände in das Tradewindow zieht, bevor der andere den Knopf 
	betätigt hat, wird der Tradebutton resetet und beide müssen neu bestätigen.
	Hat einer der Player zu wenig Inventarsslots (Bags und Backpacks eingeschlossen), fällt 
	das überschüssige einfach auf den Boden und ist für alle zugreifbar.
</P>
	
<P CLASS="P5">Grouping</P>

<P CLASS="P1">
	Um sich mit anderen Player zu einer Gruppe zusammenzuschließen, muß man diese anvisieren 
	und den 'Invite'-Knopf auf der 'Windowed'-Ansicht betätigen. Wenn der andere Player 
	akzeptiert, wird dessen Namen und sein HP-Balken am Bildschirm rechts in der Mitte 
	erscheinen.
</P>

<P CLASS="P1">
	Wir man selbst eingeladen, wird aus dem 'Invite'-Knopf ein 'Follow'-Knopf. Klickt 
	man diesen, wird man in die Gruppe des anderen aufgenommen. Um eine Gruppe wieder zu 
	verlassen, muß man den 'Disband'-Knopf klicken. Wenn man selbst der Führer einer Gruppe 
	ist, kann man einzelne Mitglieder 'rausschmeißen' indem man ihren Namen im Group Menu 
	anklickt und auf 'Disband' klickt. Wenn man sich als Leader selbst anklickt oder kein 
	Ziel hat und auf 'Disband' klickt, wird die Gruppe aufgelöst.
</P>

<P CLASS="P1">
	Während man in einer Gruppe ist, wird jeder Erfahrungspunkt, der von irgendeinem 
	Mitglied der Gruppe erworben wird, unter den Partymitgliedern aufgeteilt, egal, wie 
	iel der einzelne für das Bekommen dieser Experience Points getan hat. Wenn alle 
	Gruppenmitglieder die gleiche Levelstufe haben, erhält jeder gleich viel von diesen 
	Punkten. Unterscheiden sich die Level, bekommt ein Charakter mit höherem Level mehr Punkte 
	als ein Charakter eines niedrigeren Levels. Das Ganze ist eine ziemlich komplexe Rechnerei 
	und ändert sich aktuell während der Beta auch desöfteren. Das wichtigste dabei ist, daß 
	Player in der Gruppe höheren Levels mehr Erfahrungspunkte abbekommen, als Player eines 
	niedrigeren Levels.
</P>

<P CLASS="P1">
	Zum Grouping: um das Puschen von Charaktern zu vermeiden wurde mal einen Begrenzung 
	des Levels beim Groupping eingeführt. Ein Level 5 Char konnte max. mit einem Level 8 
	Char zusammen in einer Gruppe sein, die Differenzen wurden dann mit steigen der Level 
	auch immer größer, also. Bei Level kleiner 10, war die maximal möglich Differenz 3 Level 
	und später dann in den 30ern war die Differnz max. 8. Mittlerweile ist diese Einschränkung 
	ber nicht mehr vorhanden, jeder kann mit jedem zusammen in einer Gruppe spielen. Aber da ein 
	Charakter höheren Levels in der Gruppe immer mehr Erfahrungspunkte abbekommt, als ein 
	Charakter mit niedrigerem Level, kommt es irgendwann zu einem Punkt, wo der Player mit dem 
	niedrigerem Level so gut wie nichts mehr abbekommt. Ein Level 3 Char wird von einem Level 
	30 Char in einer Gruppe wohl nicht viel haben.
</P>

