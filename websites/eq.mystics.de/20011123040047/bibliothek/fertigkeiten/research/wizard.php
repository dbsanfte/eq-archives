<P CLASS="P1">
	Vor langer Zeit waren die Wizards die mächtigsten Zauberer in Norrath. Sie waren Herr über Raum und Zeit. Aber sie waren auch arrogant und gierig. Die Rivalität zwischen den Wizards wurde immer größer. Jeder wollte der mächtigste Zauberer sein und schon bald kam es zum größten Krieg in der Geschichte dieses einst so friedlichen Reiches. Unter der Führung Innoruuks sendete der Prinz des Hasses sein Gefolge um den günstigen Moment auszunutzen. Kometen stürtzten vom Himmel. Die Tier'Dal wurden zurückgetrieben in ihre dunklen Wälder, aber nicht ohne Verluste. Viele der größten Wizards starben in diesem Krieg und mit ihnen ging auch das meiste an Wissen und Macht verloren. Die überlebenden Wizards gelobten, daß ihre Gilde nie wieder solch machtvolle Sprüche an die jungen Zauberer weitergeben würde.
</P>	

<P CLASS="P1">
	Aber dieser Vorsatz geriet im Laufe der Zeit immer mehr in Vergessenheit. Die Gilden haben über die Jahre hinweg Bücher verfasst in denen die Geheimnisse zur Wiederherstellung dieser Spells beschrieben sind. Unglücklicherweise sind sie in einer uralten Sprache geschrieben, welche nur die erfahrendsten Wizards entziffern können. Was bisher übersetzt wurde ist, daß das <b>Lexicon</b>, in dem alle Zaubersprüche mittels Runen erstellt werden können, auch heute noch funktioniert um diese verlorenen Spells herzustellen.
</P>	

<P CLASS="P1">
	Die Gilden arbeiten fieberhaft an der Herstellung, um allen Wizards ein <b>Lexicon</b> zur Verfügung zu stellen damit wieder Ruhm und Macht erlangt werden kann.
</P>	

<P CLASS="P1">
	Der Research-Skill ist für Wizards ab Level 16 erlernbar. Um den Skill zu aktivieren muß mind. 1 Skillpunkt beim jeweiligen Guild-Trainer investiert werden. Da Research sehr schnell steigt, sollte man nicht mehr Punkte investieren.
</P>	

<P CLASS="P1">
	Nachdem der Skill aktiviert wurde muß noch ein <b>"Lexicon"</b> gekauft werden. In dieses Buch müssen die jeweiligen Komponenten gegeben werden und durch das Drücken auf "Combine" entsteht ein neuer Spruch oder man versagt und bekommt nichts ausser evtl. einer Skillsteigerung. Kaufen kann man das Buch immer in der Wizard-Guild.
</P>	

<P CLASS="P1">
	Diese Researchkomponenten können bei diversen NPC's und Monstern überall in Norrath gefunden werden. Grundsätzlich findet man die magischen Runen für den Spell eines bestimmten Levels auf Mobs, die im gleichen Level bis ca. 5 Levels höher sind.
</P>	

<P CLASS="P1">
	So, nachdem Du nun weißt wie das geht, gibt es zwei Möglichkeiten. Entweder Du stürzt Dich gleich ins Spells erzeugen, oder Du versuchst zuerst mit den <A HREF="ersteschritte.php">Research-Quests</A> den Skill zu steigern.
</P>

<P CLASS="P1">
	Viele sind der Meinung es ist einfacher, einfach möglichst viele Runen zu kaufen. Man kommt oft sehr leicht an eine der benötigten Runen und hat dann mehrere davon, aber die zweite Rune will einfach nicht droppen. Dann ist es am Einfachsten, die fehlenden Runen per /auction von anderen Spielern zu kaufen. Ausserdem können einige Research-Zaubersprüche auch bei anderen Castergilden direkt gekauft werden.
</P>	

