<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Elric<BR>
	<BR>
</P>

<P CLASS="P1">
	Kalter Rauch hing schwer unter der Höhlendecke, das trübe Licht einer einzelnen Talgkerze
	vermochte kaum das Gewölbe zu erhellen. Dies stellte aber für mich einen gerade erwachenden
	Darkelf kein Problem dar, mit seiner gottgegebenen Fähigkeit erhellten sich selbst die
	tiefen Bereiche der Höhle.
</P>

<P CLASS="P1">
	Der anheimelnde Geruch der glimmenden Scheiterhaufen vermischte sich mit dem Gestank ranziger
	toter Menschlinge. Gut das wir am Vortage noch den letzten Überlebenden in die Flammen
	geworfen hatten. Sein Gewimmere hätte an diesem diesen besinnlichen Morgen nur gestört.
	Ich kramte Pergament und Feder hervor angeregt durch den Anblick des Todes entwarf ich
	meine Grabinschrift. Ein Mann der sich dem Weg des Schwertes verschrieben hat muss sich
	schließlich  immer ins Gedächtnis rufen, daß jeder Tag der letzte seines Lebens sein kann,
	oder wie mein verstorbener Großvater zusagen pflegte: "Dieser Tag ist der erste Tag des
	Restes deines Lebens".
</P>

<P CLASS="P1">
	Was zeigt, daß auch Necromanten sich eines gewissen Humors nicht verschließen können.
</P>

<P CLASS="P1">
	Ahem wie ihr seht meine werten Zuhörer, macht mich der Anblick des Schlachtfeldes ein
	wenig sentimental. Das ist auch der Grund warum ich, Elric der Krieger, gerne noch auf
	den Feldern der Ehre verweile. Außerdem kann man den einen oder anderen unvorsichtigen
	Plünderer, der von den Früchten der Arbeit eines anderen profitieren will, einen Kopf
	kürzer machen. Als praktisch denkender Elf versuche ich immer das Angenehme mit dem
	Nützlichen zu verbinden.
</P>

<P CLASS="P1">
	Hab ich schon gesagt das mir diese goldgeilen Schacherer zuwider sind. Das war auch
	ein Grund unseres Angriffs. Da graben sich diese Ungläubigen in ihrer Gier nach den
	Geheimnissen der Tiefe einen Stollen, ohne die wahren Herrscher zu fragen. Nicht daß wir
	Ihnen die Erlaubnis gegeben hätten. Aber es hätte auf beiden Seiten viel Arbeit gespart.
	Diese Schollenbrecher und Himmelblaugucker hätten gleich gewusst woran sie sind wenn
	ihre Abgesandten ohne Nase und Ohren zurück gekrochen wären. Ha und mein schöner Dolch wäre
	nicht an der Panzerrüstung dieses sogenannten Paladins zerbrochen, was für eine
	Verschwendung. Wenn ich nur wüßte was es mit diesem Götzensymbol auf sich hat, an das er
	sich verzweifelt klammerte als sein Leben auf seine Füße tropfte. Selbst als ehrenvoller
	Krieger spüre ich die Ausdünstung von Magie. Ich bekomme immer Gänsehaut und es läuft
	mir kalt über den Buckel wenn ich die Auswirkungen von Zauberei verspüre. Dieses Bildnis
	eines Götzen der Oberwelt werde ich wohl vernichten.- Hmm ja die Oberwelt - neugierig wäre
	ich schon, wenn meine Mutter nicht strikt dagegen wäre. Sie meint die Sonne würde meine
	Haut verbrennen, kann ich mir gar nicht vorstellen so einen Feuerballzauber steck ich doch
	lässig weg. Hängt am Himmel, was soll das heißen, dann muss ich eben rauf klettern und
	sie runter holen so schwer kann das doch nicht sein. Außerdem hat mir so ein Zwerg der
	hier mal vorbeikam, wie hieß er doch gleich Dungloblash, Duglobash oder so ähnlich, diese
	fremden Namen merk ich mir nie, erzählt, daß dort oben noch andere Elfen leben. Uff an das
	Gezeter meiner Eltern darf ich gar nicht denken. Komm mir ja nicht mit so einer nachhause.
	Pff als ob ich mich mit nem Unterelf paaren würde. Aber interessieren würde es mich schon,
	Waldelfen, wie die wohl aussehen. Großvater, ich mag Großvater, war draußen. Als ich
	klein war hat er mir immer Geschichten erzählt, daher weiß ich auch daß der Himmel blau
	ist. -- Hmm also eines Tages vielleicht--. Ich kann das Gemeckere meiner Eltern eh nicht
	immer ertragen.--- neulich warst du beim Blutopfer unaufmerksam --- du bist so dumm du
	kannst noch nicht mal den kleinsten Beschwörungszauber --- deine kleine Schwester kann
	jetzt schon Tote erwecken --- Ich kann's nicht mehr hören. Mein Vater --- lern einen
	anständigen Beruf als Necromant hast du immer Arbeit --- bäh Ich sag euch als Krieger hat
	man es in einer Familie von Necromanten nicht leicht. Außerdem hat der Zwerg erzählt, daß
	man da oben nicht gleich vermählt wird wenn man bei einer Frau verweilt. Ich glaub zwar,
	daß er lügt, aber das wäre schon ein Grund oder?
</P>

