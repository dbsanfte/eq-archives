<P CLASS="P1">
	Kelethin ist die Wood Elf Stadt und alles ist auf Bäumen. Man sollte vermeiden das man 
	runterfällt denn das ist tödlich und unter der Stadt seinen Corpse zu finden dauert. Es 
	gibt 3 Lifte zur Stadt an denen jeweils Guards stehen. Der erste ist der "Newbie Lift" 
	um die Ecke ist der "PoD Lift" (weil da der Priest of Discord steht) und im Norden ist der 
	"Orc Lift".<br><br>
	Das sind keine offiziellen Namen aber praktisch jeder Spieler verwendet sie und deshalb sollte 
	man wissen von welchem Lift die rede ist.<br><br>
	Ich würde empfehlen eine Karte von Kelethin (z. B. die aus dem Karthograph) immer zur Hand zu 
	haben denn das macht das Woodelfenleben viel leichter.<br><br>
	Jetzt muss man seinen Gildenmeister finden. Man fährt mit dem PoD Lift hoch und dann geradeaus 
	über eine Brücke. Dann links die Rampe hoch und man ist bei der Bank. Nochmal links eine Rampe 
	hoch dann rechts eine runter und schon ist man an der Ranger Gilde. Geradeaus durch zum 
	Gildenmeister.<br>
	Dem gibt man den Brief und erhält etwas EXP, seine Gildentunic (die man dann sofort anlegt) und 
	5 Trainingspunkte.<br>
	In welchen Skill man sie verwendet ist eigentlich egal, ich würde aber 1hslash empfehlen damit 
	man mit dem Schwert auch was trifft.<br><br>
	In der Gilde sollte man die Leute alle mal "Hail"en um das "Orc-Hatchet Quest" zu bekommen.<br><br>
	Nebenbei sollte man sich noch Hotkeys (in der Box links unten mit den 6 Feldern) setzen. Unter 
	Abilities findet man Tracking, Sense Heading, Taunt, Hide und Forage. Mit einem Langen Klick 
	auf die Felder "nimmt" man sie mit dem Cursor auf und kann sie dann in die Hotkeybox setzen.<br> 
	Sense Heading sollte man eigentlich ununterbrochen anklicken da der Skill sehr langsam ansteigt.
	Wenn der Skill hoch genug ist kann man damit immer seine Laufrichtung erfahren und verläuft sich 
	nicht so leicht (wäre ja peinlich für einen Ranger).<br>
	Mit Forage findet man ab und zu Wasser oder Nahrung (Wurzeln und lauter Sachen die man halt 
	im Wald finden könnte). Am Anfang findet man recht selten was aber mit höherem lvl trägt an 
	immer sehr viel mit sich rum. Das spart auf Dauer viel Geld.<br>
	Tracking ist eine Art persönliches Radar das sehr nützlich ist wenn man einen bestimmten 
	NPC sucht.<br>
	Taunt reizt den Gegner und er greift einen selbst an (und nicht z. B. den Caster).<br>
	Hide macht einen unsichtbar, geht aber nur wenn man nicht kämpft und still steht.
</P>

