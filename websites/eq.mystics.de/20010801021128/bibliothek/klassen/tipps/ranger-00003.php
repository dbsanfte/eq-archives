<P CLASS="P1">
	Nun sollte man zurück zum "Newbie Lift" und die ersten Bats, Wasps, Decaying Skeletons, usw. 
	erlegen. Das potentielle Ziel aber immer erst considern. Wenn es gelb oder rot ist sollte man 
	es lieber in Ruhe lassen. Grüne Gegner geben keine EXP mehr deshalb sollte man sie auch in Ruhe 
	lassen. Den Loot den man erhält kann man bei dem Merchant am "Newbie Lift" verkaufen.<br>
	Man sollte recht schnell lvl 2 erreichen und kann dann auch Orc Pawns und Decaying Skeletons 
	schaffen.<br>
	Orc-Pawns haben ab und zu cloth armor bei sich die man anziehen kann. Man sollte sein Newbie 
	Schwert bald gegen eine rusty weapon austauschen die Orc Pawns und Decaying Skeletons manchmal 
	dabei haben.<br>
	Bei Orc Pawns findet man gelegentlich ein Orc-Hatchet. Diese Hatchets kann man paarweise in der 
	Gilde abgeben und erhält dafür meistens eine Tarsnished Waffe. Die sind etwas besser als die
	Rusty Waffen.
</P>

<P CLASS="P5">
	Level 1 - 5
</P>

<P CLASS="P1">
	Man kann bis ca. lvl 4 am newbie-lift jagen dann in einer kleinen Gruppe zu den "Orc-Hills" 
	nördlich vom "Orc-Lift" gehen. So ab lvl 5 kann man (niemals allein) am Eingang zu Crushbone 
	(CB genannt) Orcs erlegen.<br>
	Ab lvl 5 bekommt man Kick, eine zusätzliche Angriffsart, die man auch als Hotkey haben sollte.<br>
	Man muss zur Gilde und mindestens einen Punkt in Kick trainieren damit es geht.
</P>

<P CLASS="P5">
	Level 6 - 9
</P>

<P CLASS="P1">
	Jetzt kann man nach CB gehen und sich dort "Cents" und in einer vollen Gruppe auch mal einen 
	"Slaver" schnappen.<br>
	Vorsicht ist angesagt wenn jemand "Train" oder "DVinn" ruft. Dann sollte man aufpassen denn 
	wenn ein "train" kommt heißt es nix wie weg. Dvinn ist ein ca. lvl 22(?) Dark Elf Rogue(?), 
	der manchmal (wenn keiner ihn töten kann) die halbe Zone umnietet.<br>
	So ab lvl 9 kann man auch nach Butcherblock und am "Chessboard" Untote killen.<br><br>
	Level 8: man bekommt Dogde. Man muss in seine Gilde und die Basics lernen. Dogde ist zum 
	Ausweichen von Schlägen.<br>
	Level 9: ist die Stunde des Rangers.<br>
	Man geht zu seiner Gilde und trainiert in alle 5 Zauberskills jeweils mindestens einen Punkt.
	Jetzt kauft man sich die 7 Spells Scribed sie und merkt sie sich. Für Flame Lick braucht man 
	ein Fire-Beetle Eye, damit der Spell geht.
</P>

<P CLASS="P5">
	Level 10 - 12
</P>

<P CLASS="P1">
	Man kann jetzt in CB schon so ziemlich alles (außer den Orcs aus dem Throne-Room) mit einer 
	guten Gruppe schaffen.<br><br>
	Level 10: man bekommt Sneak (hotkey!). Mit Sneak kann man an Gegnern vorbei schleichen.<br>
	Level 12: der Ranger bekommt Mediate, was sehr nützlich ist, um sein Mana schneller zu 
	regenerieren.
</P>

<P CLASS="P5">
	Level 13 - 15
</P>

<P CLASS="P1">
	Man kann weiter in CB Legionaires killen oder mit dem Schiff in Butcherblock nach Freeport 
	fahren.<br>
	Wenn man in Freeport angekommen ist sollte man sich sofort binden lassen und dann kann man 
	in den Commonlands Dervs jagen.<br><br>
	Level 15: die 2te Spell-Line ist da. Man muss wieder in seine Gilde und die neuen 
	Spells kaufen.
</P>

<P CLASS="P5">
	Level 16 - 19
</P>

<P CLASS="P1">
	Man kann jetzt nach Oasis of Marr und dort Caimans und Crocodile killen. Am Anfang aber noch 
	keine Deepwaters. Man muss dort auf Sand Giants und Spectres aufpassen, denn die greifen 
	einen an und man kann nur mit Glück (oder SoW) lebendig entkommen.<br><br>
	Level 17: wieder ein großer Augenblick für einen Ranger. Man bekommt Dual Wield und kann 
	endlich das Schild wegwerfen (oder besser verkaufen) und eine 2te Waffe tragen. Wie immer 
	muss erst 1 Punkt trainiert werden.<br>
	Level 18: Man bekommt Parry was wie Dogde ist und natürlich muss 1 Punkt trainiert werden.
</P>

