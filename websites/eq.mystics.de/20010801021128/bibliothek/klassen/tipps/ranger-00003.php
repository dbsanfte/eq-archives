<P CLASS="P1">
	... die Charaktererschaffung:<br>
	da der Wood Elf recht schwach ist empfehle ich 15 Punkte in STR den Rest in STA zu investieren. STR ist ziemlich wichtig, denn sonst ist man immer überladen und die AGI geht in den Keller.
</P>

