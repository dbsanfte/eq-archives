<P CLASS="P1">
	Barden sind entgegen der Meinung vieler Leute keine Tankklasse. Wenn ihr also gerne mit vielen HPs rumläuft
	und Monster gerne reihenweise tötet, solltet ihr euch eine andere Klasse suchen, Warrior oder Rouge bieten
	sich da an.
</P>

<P CLASS="P1">
	Barden sind eine Klasse, die sehr viel kann aber keiner der spezialisierten Klasse das Wasser reichen könnte.
	Er wird niemals seine Waffen so gut schwingen lassen können wie ein Warrior oder ein Ranger, und seine
	Zauberlieder werden niemals so viel Schaden machen können wie die der Spellcaster, und alleine wird ein Barde
	nicht sehr weit kommen. Doch in einer Gruppe offenbart sich die wahre Kraft des Barden. Sei es indem er vorne
	hilft die Monster von den Spellcastern abzulenken oder von hinten seine Mitglieder heilt (ohne Manaverbrauch
	versteht sich) oder ihnen ein Kampfeslied vorsingt (-spielt) das den Gruppen Mitgliedern ihre verlorene
	Ausdauer wiedergibt, hier ist er die vielseitigste Klasse von allen.
</P>

