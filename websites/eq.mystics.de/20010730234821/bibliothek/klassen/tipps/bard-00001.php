<P CLASS="P1">
	Welche Rasse sollst denn sein? Die Barden sind nach den Monks und den Shamanen die beschränkteste Klasse
	was die Rassen Auswahl angeht. Nur Menschen, Halbelfen oder Waldelfen dürfen Barden werden. Welche Rasse
	ihr nehmt ist eure Entscheidung da beim Barden da keine sonderlich großen Vorteile entstehen können (wie
	die INT/WIS bei Spellcastern), der Mensch hat den klaren Nachteil das er keine Nachtsicht hat dies gleicht
	er natürlich durch das bessere Leveln aus aber ich als Woodelf kann natürlich nur zum Woodelf raten :o)
</P>

