<P CLASS="P5">Level 1 und 2</P>

<P CLASS="P1">
	Ihr startet mit eurem Short Sword und solltet erst einmal die Gilde finden diese ist zwischen dem Lift (A2 auf
	der Mystics Karte) mit dem Priester of Discord und dem Lift (A1 auf der Mystics Karte) an der Strasse nach
	Felwithe. Gibt ihr die Scroll, und trainiert bei ihr am besten erst mal 1-h Slash da ihr sonst keine großen
	Chancen haben werdet. Geht nun kleine Tiere töten, dies geht am besten am rechten Lift nahe den Ruinen und
	beschränkt euch zuerst auf Bats, Schlangen und Wölfe. Wenn ihr ein Decaying Skeleton seht, das nicht von einer
	wilden Horde angegriffen wird könnt ihr euch an ihnen versuchen, wenn ihr ausgeruht seit schafft ihr sie
	normalerweise (ansonsten ab zu den Guards). Ihr habt am Anfang keine Lieder also spart euer Geld bis ihr
	eure erstes Lied kaufen könnt, dies verstärkt eure STR und eure DEX, laßt dieses Lied immer an, erstens
	werdet ihr nicht mehr so oft getroffen und zweitens könnt ihr so den Skill Singing schnell steigern. Spart
	jetzt auf eine bessere Waffe an (Rusty Scimitar für 1h-Slashing bzw Rusty Rapier wenn ihr lieber mit
	Piercing Waffen kämpft). Jetzt solltet ihr auch so langsam Level 2 haben, spart aber künftig die
	Trainingspoints an für einen späteren Tradeskill. Auch solltet ihr erst mal auf das Level 2 Lied
	verzichten und das Geld lieber für eine rüstung sparen. Ihr könnt euch jetzt eigentlich an alles wagen
	nur von den Widow Hatchlingen und den Orc Pawns die Pfoten weglassen.
</P>

<P CLASS="P5">Level 3 und 4</P>

<P CLASS="P1">
	In Level 3 angekommen solltet ihr euch das zweite Gruppen Lied kaufen, Jaxan's Jig o' Vigor, es beschleunigt
	die Wiederherstellung der Ausdauer was in einem schweren Kampf einem durchaus das leben retten kann. Jetzt
	schafft ihr eigentlich alles was in der Newbiezone herumläuft. Außerdem könnt ihr euch von der Guildmistress
	auch die erste Queste abholen, bringt ihr 4 Spiderling Silk (zu finden bei Widow Hatchlingen) und ihr bekommt
	neben Faction und ein Goldstück außerdem noch euer erstes Musikinstrument, eine Hand Drum. Legt diese Hand
	Drum in eurer Bank Fach weil ihr den benötigten Skill erst in Level 5 bekommt. In Level 4 angekommen legt ihr
	euch am besten sofort das neue Lied zu namens Lyssa's Locating Lyric zu. Mit diesem Lied kann man Leichen
	anderer Spieler finden, auch sehr praktisch, wenn ihr mal sterbt und nicht wisst wo. Auch versucht euch eine
	Leather Whip zu besorgen diese sind nicht sehr teuer (kosten nur wenige Gold) machen aber 5dmg mit einem
	Delay von 33 und sind natürlich viel besser als eure Rusty Weapon.
</P>

<P CLASS="P5">Level 5 und 6</P>

<P CLASS="P1">
	Besorgt euch als erstes das neue Lied Selo's Accelerando und übt das wie irre, es wird euch und eurer
	Gruppe mehr als einmal das Leben retten! Auch solltet ihr jetzt eure Hand Drum schnappen, einen Punkt auf
	den neuen Skill Percussion Instrument legen und dann am besten mal in ruhe in Richtung Felwithe laufen
	(immer schön Selo's Accelerando anlassen) und so den Skill auf Maximum bringen. Außerdem legt euch
	spätestens jetzt eine vollständige Patchwork Armor zu (oder besser wenn euer Geld reicht), ob ihr sie nun
	selbst schneidert (wie ich) oder sie kauft ist da allerdings recht egal, auch ein Small Shield sollte
	langsam angeschafft werden, die mehr AC werdet ihr später dringend brauchen. Bleibt bis Level 6 weiterhin
	in der Newbie Area und killt Orc Paws, Pixie Trixters (aber nur wenn keine Fairy irgendwas da ist, sonst
	lebt ihr nicht lange!) und Widow Hatchlinge, nehmt auch ruhig die Decaying Skeletons weiterhin mit, diese
	geben zwar keine EXP mehr aber die Bone Chips geben gutes Geld und sie tragen oft Waffen mit sich rum.
	Groupen ist noch nicht nötig, man steigt immer noch sehr gut alleine (auch dank den Battle Song aus Level 1).
	Ab Level 6 solltet ihr euch eine kleine ausgewogene Gruppe suchen und in die Butcherblock Mountains gehen
	und dort Goblins (vorsicht vor den Shamanen und den Wizards, vor allem die Wizards sind abartigst da sie
	eine riesige Reichweite mit ihren Angriffszaubern haben!), Large Snakes und Spinnen jagen.
</P>

<P CLASS="P5">Level 7 und 8</P>

<P CLASS="P1">
	Langsam solltet ihr euch eine bessere Waffe kaufen, empfehlenswert ist hier die Minotaur Battle Axe, sie macht
	gut Schaden und kostet nicht die Welt (maximal 5PP solltet ihr dafür ausgeben ich hab sie für 4 bekommen), mit
	einer guten Gruppe könnt ihr jetzt auch Emerald Drakes und Lowland Baseliks schaffen (vergesst aber niemals
	zuerst zu considern, kann vorkommen das euch da ein highlevel Baselik beißt!).
	Auch könnt jetzt recht gut alleine Orc Centurions im Greater Faydark vor Crushbone schaffen (Finger weg von
	den Orcs im Lesser Faydark die sind sehr schwer, und wenn der Legionär auftaucht seid ihr schneller platt als
	ein Necromant in Felwithe!) denn wenn es mal eng werden sollte einfach Selo's Accelerando an und ab zu den
	Guards.
	Ein weiteres gutes Groupgebiet ist das Gebiet um die Mills in den Steamfoat Mountains aber greift auf keinen
	fall Skeletons an wenn daneben eine large Pleague Rat steht, die vieher groupen und die Ratte haut euch eine
	starkes und lang anhaltendes Desease rein, und wenn ihr die erste getötet habt kommen alle Ratten im Umkreis
	von 300 Meter um euch zu zeigen das man ihre Artgenossen in ruhe zu lassen hat. Verlaßt euch hier nicht auf
	die Guards, selten habe ich so Witzfiguren von Guards gesehen (Watchman XYZ hits a large pleage rat with
	5(!!!) dmg) und wenn einer ruft "HERO OUT!" dann weg von den Mills da ihr sonst Bekanntschaft mit dem Level
	35 Minotaur Hero machen werdet und das überleben nicht mal die Guards (OK bei denen wundert mich das auch
	nicht *g*).
</P>

<P CLASS="P1">
	Ab jetzt müsst ihr euren Weg alleine finden, weiter als Level 8 bin ich auch nicht gekommen :o)
</P>

