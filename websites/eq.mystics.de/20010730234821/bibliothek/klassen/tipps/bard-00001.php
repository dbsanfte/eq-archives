<P CLASS="P1">
	Wenn man einen Barden spielen will muss man sich klar sein das man niemals ein Schwertmeister oder ein
	Meister der Arcanen Künste wird, ein Barde ist ein Fun Char und er verbindet viele Klassen in sich,
	beispielsweise das Disarm Traps (Level 30) und Pick Lock (Level 40) der Rogues und das Tracking der
	Ranger (Level 35). Wie ihr ihn im Rollenspielsinne spielen wollt ist euch selbst überlassen, da es
	Barden und Barden gibt :o)
</P>

