<P CLASS="P1">
	Es gibt drei wichtige Attribute: DEX, WIS und CHA. Ich kann nur raten DEX als wichtigstes anzusehen da die
	LPs (Lebenspunkte) eines Barden ziemlich niedrig sind (die in STA reinzustecken wäre aber vollkommener
	Unsinn weil ihr dann öfters getroffen werdet und das bedeutet bei stärkeren Monstern meist das eure Songs
	aufhören und ihr manchmal ziemlich am fluchen seit *g*).
</P>

<P CLASS="P1">
	Wieso der Barde WIS geschweige denn Mana braucht verstehe ich nicht so recht da ich bisher kein Lied
	gefunden habe das Mana verbraucht (und mein Wissen geht bis an die Level 42 Lieder herran). Verwendet
	daher lieber nur einen kleinen teil für WIS und buttert den rest in CHA, es macht einfach Spaß mit 95
	CHA durch die Gegend zu laufen und von den meisten NPCs geliebt zu werden *g*.
</P>

