	Der Barde darf zwischen den meisten Göttern wählen (auch als Woodelf), ich rate hier zu Agnostic, so habt
	ihr keine grossen Schwierigkeiten mit anderen Rassen (Ausnahme sind hier die Hochelfen ihr bekommt als
	Agnostic leicht schlechtere Preise als Tunare Anhänger beispielsweise aber dies hält sich in Grenzen
	(meist nur wenige Copper oder Silber), außerdem schafft ihr es wenn ihr genug Orc killt das die Hochelfen
	euch auch wieder besonders mögen).
</P>

