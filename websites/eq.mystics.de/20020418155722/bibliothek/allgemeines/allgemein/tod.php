<P CLASS="P5">Experience Verlust</P>

<P CLASS="P1">
	In den Leveln 1-5 ist der Tod noch nicht schlimm, zumindest, wenn man nicht gerade an einer	unzugänglichen Stelle umgekommen ist. Bei den ersten 3 Toden innerhalb der Level 1-5 behält man alle Newbie Gegenstände, wie die erste Waffe usw. D.h. sie sind nach dem Wiedereintritt in	die lebende Bevölkerung wieder in eurem Besitz. Essens- und Trinkvoräte werden sogar wieder auf den Anfangsstand aufgefüllt, ihr startet also wieder mit je 5 Essens- und 5 Trinkeinheiten.
	Die neu erworbenen Sachen, wie Geld und neue Ausrüstung befinden sich in eurer Leiche und ihr	müßt sie looten, um wieder an sie zu gelangen.
</P>
	
<P CLASS="P1">
	Ab Level 6 verliert man mit jedem Tod Experience und es wird mit steigendem Level immer mehr,
	ich habe gehört, daß Player in Leveln 20+ über 1 Bubble an Experience bei einem Tod verloren
	haben. Also stay alive und passt auf euch auf ;)
</P>
	
<P CLASS="P5">Der Corpse und die Haltbarkeit</P>

<P CLASS="P1">
	Wenn man stirbt, bleibt der Leichnam noch für eine Weile an der Todesstelle liegen. Die Dauer ist wie
	folgt festgelegt:
</P>

<UL CLASS="P0">
	<LI>Alle Level: Leere Corpse verschwinden nach 3 Minuten.<BR><BR></LI>
	<LI>Level 1-5: Der Corpse bleibt 30 Minuten reale Zeit liegen und verschwindet dann. Das wurde auf Grund
	der hohen Sterberate unter den Newbies eingeführt. Ich denke mal, wenn das nicht wäre, wären die Newbiezonen
	mit Newbieleichen gepflastert.<BR><BR></LI>
	<LI>Level 6 und höher: Ist der Player online (also auch mit einem anderen Char!) bleibt der Corpse für 24 Stunden
	realer Zeit liegen. Ist er offline bleibt der Leichnam für 1 volle Woche an Ort und Stelle.<BR><BR></LI>
</UL>

