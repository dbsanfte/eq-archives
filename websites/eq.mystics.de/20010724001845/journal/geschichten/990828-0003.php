<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Oger Emma<BR>
	<BR>
</P>

<P CLASS="P1">
	Es begab sich zu einer Zeit, da gab es in Norrath noch ein Königreich, das im Überfluß 
	lebte und dem es an fast nichts fehlte. An Bier war kein Mangel, der Wein floß in Strömen 
	und der Branntwein war auch nicht von schlechten Tavernen.
</P>

<P CLASS="P1">
	Doch eines verdroß das Volk, den König und die unbeschreiblich schöne Königin. Es war weit 
	und breit kein ständig schreiender, nervender und nie tot zu kriegender Nachkomme in Sicht. 
	Tage, Monate und Jahre versuchte der Trottel von König seiner Gemahlin einen dicken Bauch zu 
	machen. Doch vergebens. Die Berater des Herrscherpaares wußten weder ein noch aus. <br>
	So beschloß der Versager sich den Strick zu nehmen, um sich nicht noch länger lächerlich 
	zu machen. Da aber im Schlafgemach der Königin kein Seil zur Hand war, er auch zu faul 
	war sich ein Laken in Streifen zu reißen, hielt er einfach so lange die Luft an bis er 
	jämmerlich erstickt war.<br>
	Damit war erst mal das Thema Versager erledigt, aber die Frage des Thronerben blieb immer 
	noch offen.
</P>

<P CLASS="P1">
	Die Weisen der Königin kamen nun zu dem Schluß, daß ein Mann her mußte, der mit der 
	Königin endlich ein Kind zeugen konnte. Das Resultat konnte man noch dem kürzlich 
	verschiedenen König in die Schuhe schieben wenn's schnell genug ginge und die Frage 
	der Alimente war somit auch gleich geklärt!
</P>

<P CLASS="P1">
	Also wurden flugs Boten in alle Himmelsrichtungen geschickt um den zu finden, der genügend 
	Manneskraft besitzt um der Königin Ihr Problem zu lösen.<br>
	Aus aller Herren Länder kam es heran geeilt um es der Obersten mal richtig zu besorgen. 
	Aber eines wurde bei allen Planungen und Überlegungen vergessen: der Königin Geschmack! 
	Einer war Ihr zu barbarisch, einer zu dünn und zu dunkel. Den kleinen Verschrumpelten mit 
	Bart mochte Sie auch nicht, den aus dem Süden mit der langen Nase fand Sie zu häßlich und 
	die Hochnäsigen mit den Kapuzen konnte Sie eh nie ab.
</P> 

<P CLASS="P1">
	Das war den Beratern denn doch zu viel und sie beschlossen um des Nachwuchses Willen Ihrer Chefin eins auszuwischen.<br>
	Anläßlich des Balles Ihres verstorbenen Nichtsnutzes machten sie Ihre Königin besoffen, legten Ihr ein kostbar in Leder gebundenes Schmuddelheft ins Schlafgemach und den muskulösen und starken 3 Meter großen Häuptling aller Ogerstaemme dazu.<br>
	Die ganze Nacht war ein herzzerreißendes Wimmern, Schreien und danach lustvolles Stöhnen zu vernehmen.<br>
	Und entstanden war, was die Königin glücklich in Ihren Armen hielt und vom Oberoger zufrieden begrunzt wurde:<br><br>
	<div align="center"><b>----------------------- EMMA ------------------</b></div><br><br>
</P>

<P CLASS="P1">
	Das Baby ganz der Vater, groß, muskulös und stark sowie von der Mutter die vollendete Schönheit.<br>
	Und wenn Sie nicht gestorben sind dann Ogern Sie noch heute har har !!
</P>
