<P CLASS="P1">
	Sobald du ein Paar Runen erzeugt hast kannst du sie verwenden um deinen Research Skill zu steigern. Dafür gibst du die <b>Azia</b> und <b>Beza</b> Rune in das entsprechende Research-Buch und drückst "Combine". Es dürfen nur diese beiden Runen im Buch sein, sonst nichts!
</P>	

<P CLASS="P1">
	Bei Erfolg entsteht eine <b>Solid Rune</b>. Diese kann wieder verkauft werden um die Trainingskosten etwas zu kompensieren. Sobald ein Practice-Runen-Set <b>trivial</b> wird musst Du zur nächsthöheren Stufe wechseln. <b>Trivial</b> heisst, daß keine weitere Skill-Steigerung mit diesem Runen-Set möglich ist.
</P>	

<P CLASS="P1">
	Nochmal zur Erinnerung: Verwende die <b>Concordance of Research</b> um die <b>Practice Runen</b> aus den drei Ingredienzien, der Feder und dem Pergament zu erstellen. Gib nicht die <b>Practice Runen</b> hinein, sondern gib diese in das <b>Lexicon, Tomb of Endless Enchantments, Book of Dark Bindings</b> oder <b>Elemental Grimoire</b> um sie zu "kombinieren".
</P>	

