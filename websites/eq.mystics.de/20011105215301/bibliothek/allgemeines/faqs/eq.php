<P CLASS="P2">
	3.1 Wieviel Chars kann man mit einem Account erzeugen ?
</P>	
<P CLASS="P1">
	Acht Chars pro Server.
</P>

<P CLASS="P2">
	3.2 Wenn man mehr als 1 Char hat, kann man mit beiden gleichzeitig online sein und Sachen austauschen ?
</P>	
<P CLASS="P1">
	Nein, man kann immer nur 1 Char pro Account gleichzeitig einloggen.
</P>

<P CLASS="P2">
	3.3 Basiert die Charentwicklung auf Skills oder Levels ?
</P>	
<P CLASS="P1">
	Beides, Skills und Level.
</P>

<P CLASS="P2">
	3.4 Wieviele Klassen und Rassen gibt es, und welche ?
</P>	
<P CLASS="P1">
	Es gibt 12 Rassen: Barbaren (Barbarian), Dunkelelfen (Dark Elf), Zwerge (Dwarf), Hochmenschen (Erudite), Gnome (Gnome), Halbelfen (Half-Elf), Halblinge (Halfling), Hochelfen (High Elf), Menschen (Human), Oger (Ogre), Trolle (Troll), Waldelfen (Wood Elf).<br>
  Es gibt 14 Klassen: Bard, Cleric, Druid, Enchanter, Magician, Monk, Necromancer, Paladin, Ranger, Rogue, Shadow Knight, Shaman, Warrior, Wizard.
</P>

<P CLASS="P2">
	3.4.1 Wird es Erweiterungen mit neuen Rassen/Klassen geben ?
</P>	
<P CLASS="P1">
	Absolut. Wir werden noch einiges zum Spiel hinzufügen.
</P>

<P CLASS="P2">
	3.5 Kann man einen Multiklassen-Char erzeugen, wie z.B. einen Krieger-Magier ?
</P>	
<P CLASS="P1">
	Es gibt einige Hybrid-Klassen wie Paladine (Krieger-Heiler) und Shadow Knights (Krieger-Nekromant) aber keine wirklichen Multiklassen wie bei AD&D.
</P>

<P CLASS="P2">
	3.6 Sind bestimmte Klassen bestimmten Rassen vorbehalten oder können alle Rassen jede Klasse wählen ?
</P>	
<P CLASS="P1">
	Ja, bestimmte Klassen sind bestimmten Rassen vorbehalten.
</P>

<P CLASS="P2">
	3.7 Kann man männliche und weibliche Char spielen ?
</P>	
<P CLASS="P1">
	Ja.
</P>

<P CLASS="P2">
	3.7.1 Welche Unterschiede gibt es zwischen männlichen und weiblichen Chars ?
</P>	
<P CLASS="P1">
	Der Unterschied liegt lediglich in der Grafik.
</P>

<P CLASS="P2">
	3.8 Kann man das Aussehen der Spielfigur auswählen (Augen, Haare, etc.) ?
</P>	
<P CLASS="P1">
	Es gibt jeweils 8 verschiedene Gesichter pro Rasse/Geschlecht aus denen man auswählen kann.
</P>

<P CLASS="P2">
	3.9 Sind Kleidung, Rüstung und Waffen für andere sichtbar ?
</P>	
<P CLASS="P1">
	Ja.
</P>

<P CLASS="P2">
	3.10 Kann man ein Geschäft haben oder ein Schmied werden wie in Ultima Online ?
</P>	
<P CLASS="P1">
	Es gibt keine "Schmied"-Klasse oder offizielle Berufe, weil wir glauben, das ist nicht unbedingt nötig. Jeder Spieler kann jederzeit Gegenstände tauschen und verkaufen. Es gibt verschiedene Skills (Fähigkeiten) im Spiel und das "Schmieden" ist einer davon.
</P>

<P CLASS="P2">
	3.11 Gibt es beim Erstellen der Spielfigur Möglichkeiten die verschiedenen Charakterwerte einzustellen, welche erhalten bleiben, wie Intelligenz, Stärke, etc. ?
</P>	
<P CLASS="P1">
	Ja. Es gibt Attribute wie Stärke, Charisma, Ausdauer, Weisheit, Intelligenz, Kondition und Wendigkeit - diese können außerdem mit magischen Gegenständen und Spells noch erhöht/herabgesetzt werden.
</P>

<P CLASS="P2">
	3.12 Kann man eine Biografie erstellen, welche die Leute sehen wenn sie mich anschauen ?
</P>	
<P CLASS="P1">
	Ja.
</P>

<P CLASS="P2">
	3.13 Altern die Spielfiguren und kann man daran sterben ?
</P>	
<P CLASS="P1">
	Nein.
</P>

<P CLASS="P2">
	3.14 Kann man etwas essen und ist es nötig um zu überleben ?
</P>	
<P CLASS="P1">
	Ja, man kann und muß essen. Man kann jedoch nicht verhungern oder verdursten.
</P>

<P CLASS="P2">
	3.15 Kann die Spielfigur schlafen ?
</P>	
<P CLASS="P1">
	Man kann rasten, aber es ist nicht nötig zu schlafen.
</P>

<P CLASS="P2">
	3.15.1 Werden durch das Rasten die Lebenspunkte wieder aufgefüllt ?
</P>	
<P CLASS="P1">
	Ja, aber mit einem Heiler geht es wesentlich schneller.
</P>

<P CLASS="P2">
	3.16 Kann man sich verkleiden ?
</P>	
<P CLASS="P1">
	Man kann die Kleidung tauschen, was sich auf das Aussehen auswirkt.
</P>

<P CLASS="P2">
	3.17 Gibt es einen Höchstlevel den man erreichen kann ?
</P>	
<P CLASS="P1">
	Ja, der aktuelle Höchstwert ist 50.<br>
	<br>
	Spieler, die das Erweiterungspaket "The Ruins of Kunark" besitzen können maximal Level 60 erreichen.
</P>

<P CLASS="P2">
	3.18 Wieviele Gegenstände kann man tragen ?
</P>	
<P CLASS="P1">
	Es sind 21 Stellen am Körper vorhanden. Zusätzlich gibt es noch ein generelles Inventar in dem man mit Containern noch einiges mehr tragen kann.
</P>

