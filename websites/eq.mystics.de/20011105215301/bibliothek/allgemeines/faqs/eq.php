<P CLASS="P2">
	1.17 Spielt das EverQuest Entwickler Team auch selber oder werden nur Infos der "spielenden" Designer verwendet ?
</P>	
<P CLASS="P1">
	Bei der Zusammenstellung des Teams wurde sehr darauf geachtet daß viele der Designer, Programmierer und Grafiker Ahnung vom Genre haben. Das komplette Team hat sehr viel zum Spiel beigetragen. Wir wären nicht im Geringsten überrascht, die meisten von ihnen sehr oft im Spiel zu sehen. Das ist ein Spiel, daß wir alle seit Jahren machen und SPIELEN wollten.
</P>

