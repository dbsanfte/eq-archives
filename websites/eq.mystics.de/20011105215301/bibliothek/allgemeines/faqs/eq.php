	2.1 Welche Auflösung und wieviel Farben sind möglich ?
</P>	
<P CLASS="P1">
	640x480 16bit Farbe (das sind 64000 Farben). 800x600 und 1024x768 werden von vielen 3D Karten unterstützt.
</P>

<P CLASS="P2">
	2.1.1 Die Grafik in den Screenshots ist vom Feinsten. Jedoch sieht man immer wieder, daß die meisten Spiele mit guter Grafik ein schlechtes Gameplay bzw. eine miese künstliche Intelligenz der NPC's aufweisen ...
</P>	
<P CLASS="P1">
	Dies ist die Aussage eines unserer Beta-Testerbezüglich künstlicher Intelligenz (AI = Artifical Intelligence):
</P>
<P CLASS="P1">
	<I>"I have seen many game companies striving toward a graphical work of art often overlook the basic AI of the opponents. This has not been the case in EverQuest. This game has the most advanced AI I've seen. Bar none. Even the little intricacies that make the game more believable were taken into account. As I am battling one creature (say a large bat for this purpose), I've seen a giant rat come up to me, stop, and watch the fight as if sizing me up. You really get the feeling that these things are looking you in the eye, and trying to decide if you are dinner, or something to be feared.<BR>
	<BR>
	Some attack without warning, others circle in from behind. Still others will act like they are going to go right past you, and suddenly turn around and lay into you with swords, fists, or teeth. The AI definitely appears to be modeled on creature intelligence, some just fight smarter than others. The smart ones will even retreat from a fight and go get help. I have no doubt that the AI programmer is somewhat of a sadist. A sadist with a sense of humor.<BR>
	<BR>
	You'll know that sense of humor when you see a hill giant way off in front of you in the distance, and 2 minutes later find that it has made a wide circle to sneak up on you from behind.<BR>
	<BR>
	Add to this some truly impressive tracking and following code, and SPEED. These creatures are FAST. There just isn't any outrunning some, you better hope you have the strength or the speed to get away quickly, and they will follow you for a great distance. I have seen one gnoll retreat from a fight, go get helpers, and return on two different tangents to surround the intruders. I've seen groups split up, half appearing to have lost interest in the fight, only to turn and rush in.<BR>
	<BR>
	I've really got to put my hats off to this AI programmer, I think he could give the ReaperBot guy pointers."</I>
</P>

<P CLASS="P2">
	2.2 Kann das Spiel mit einer höheren Auflösung betrieben werden wenn man die nötige Rechner Power besitzt ?
</P>	
<P CLASS="P1">
	Ja - man kann in 800x600 & 1024x768 spielen sofern dies von der Grafikkarte unterstützt wird.
</P>

<P CLASS="P2">
	2.2.1 Wird durch eine höhere Auflösung mehr Lag erzeugt ?  Werden mehr Daten bei einer höheren Auflösung übertragen ?
</P>	
<P CLASS="P1">
	Die Auflösung hat nichts mit dem Internet Lag zu tun - es wird exakt dieselbe Datenmenge zwischen dem Server und Client übertragen.
</P>

<P CLASS="P2">
	2.3 Wie groß ist die Spielwelt ?
</P>	
<P CLASS="P1">
	Riesig. Man benötigt sehr viel Zeit um vin einem Ende zum Anderen zu laufen.
</P>

<P CLASS="P2">
	2.3.1 Die aktuelle Mappe zeigt nur eine Halbkugel, was ist auf der anderen Hälfte ?
</P>	
<P CLASS="P1">
	Die ist reserviert für künftige Erweiterungen - die Welt wird wachsen und wachsen ...
</P>

<P CLASS="P2">
	2.3.2 Wird die Gesamtfläche der Welt größer werden oder werden nur mehr NPC's und Gegenstände in bestehende Zonen eingebaut ?
</P>	
<P CLASS="P1">
	Beides. Wir werden leere Bereiche in bestehenden Bereichen auffüllen, als auch neue Zonen und Planes hinzufügen.
</P>

<P CLASS="P2">
	2.4 Wie sieht die Kommunikation im Spiel aus ?
</P>	
<P CLASS="P1">
	Man kann folgendes tun:
</P>
<UL CLASS="P1">
	<LI><b>/say</b> ... etwas sagen, daß im näheren Umkreis von allen gehört wird.<BR></LI>
	<LI><b>/shout</b> ... schreien; wird in der ganzen Zone gehört.<BR></LI>
	<LI><b>/tell</b> ... eine private Nachricht schicken.<BR></LI>
	<LI><b>/gs</b> ... eine Mitteilung innerhalb der Gruppe.<BR></LI>
	<LI><b>/gu</b> ... eine Mitteilung an alle Anwesenden innerhalb der Gilde (sofern man in einer Gilde ist).<BR></LI>
</UL>

<P CLASS="P2">
	2.4.1 Wie kommuniziert man nun wirklich, z.B. wenn ein Char etwas sagt, wie sehen es die in der Nähe befindlichen Personen ?
</P>	
<P CLASS="P1">
	Wenn ein Char etwas sagt, sieht dies jeder Spieler in der Nähe im Eingabe-/Ausgabefenster (Chat- bzw. Textfenster) am Bildschirm.
</P>

<P CLASS="P2">
	2.4.2 Werden Programme wie ICQ od. IRC während des Spiels deaktiviert ?
</P>	
<P CLASS="P1">
	EverQuest läuft nur im Vollbildmodus und erlaubt keine anderen Tasks
</P>

<P CLASS="P2">
	2.4.3 Was macht man mit Spielern, die einen dauernd folgen und mit Tells belästigen ?
</P>	
<P CLASS="P1">
	Durch die Eingabe von <I>/ignore [name]</I> sieht man die Tells nicht mehr. Wird man weiterhin belästigt bitte dies mit /petition melden.
</P>

<P CLASS="P2">
	2.5 Kann ich auch auf einem anderen Server einloggen um meine Freunde dort zu treffen ?
</P>	
<P CLASS="P1">
	Ja.
</P>

<P CLASS="P2">
	2.6 Kann man auch samt den Chars auf einen anderen Server wechseln ?
</P>	
<P CLASS="P1">
	Normalerweise nicht. Jedoch wird Spielern auf überfüllten Servern, bei einem Serversplit, die Möglichkeit eines Char-Transfers gegeben.
</P>

<P CLASS="P2">
	2.7 Gibt es andere Möglichkeiten des Reisens außer zu laufen (Pferde, Schiffe od. Teleports) ?
</P>	
<P CLASS="P1">
	Schiffe und Teleports gibt es, aber Pferde sind nicht geplant.
</P>

<P CLASS="P2">
	2.7.1 Wird während einer Reise immer die First Person Perspektive aktiv sein oder gibt es auch die Möglichkeit einer Mappen-Ansicht (Übersicht) ?
</P>	
<P CLASS="P1">
	Die Ansicht bleibt immer die Gleiche, egal ob man sich in einer Stadt befindet oder eine lange Reise antritt. Natürlich gibt es einige Spells die das Reisen erleichtern bzw. beschleunigen.
</P>

<P CLASS="P2">
	2.8 Findet das Kämpfen in Echtzeit statt od. ist es rundenbasiert ?
</P>	
<P CLASS="P1">
	In Echtzeit. Es ist jedoch völlig egal wie schnell od. oft man mit der Maus klickt. Jede Waffe hat ihre fixen Werte in Bezug auf Geschwindigkeit. Der Kampf ist zwar Echtzeit, aber man kann die Geschwindigkeit nicht direkt beeinflussen. Mit einem Messer sticht man wesentlich öfter zu als mit einem Zweihänder, macht aber wesentlich weniger Schaden.
</P>

<P CLASS="P2">
	2.8.1 Also ist es kein Vorteil, wenn man eine gute Reaktion hat ?
</P>	
<P CLASS="P1">
	Jeder Spieler hat insofern Kontrolle über seine Figur, daß man sie mit der Tastatur und Maus kontrolliert. Alles andere wird jedoch von den Charakter- und Waffenwerten bestimmt und nicht vom Spieler selbst. Das Spiel ist in Echtzeit aber nicht in der Art wie Quake oder Unreal.
</P>

<P CLASS="P2">
	2.8.2 Schwingt man das Schwert od. andere Waffen automatisch ?
</P>	
<P CLASS="P1">
	Ja, wir nennen es "Auto-Attack". Es erlaubt dem Spieler, sich auf andere Kampfskills zu konzentrieren. Krieger können "kicken" und Mönche habe jede Menge Spezialattacken die man während des Kampfes ausführen kann.
</P>

<P CLASS="P2">
	2.9 Hat man mit einer besseren Verbindung gegenüber jenen mit einer schlechteren (langsameren) Verbindung ?
</P>	
<P CLASS="P1">
	Nein, für gewöhnlich nicht. Natürlich hat man mit einer schnelleren Verbindung weniger Lag aber sonst bringt es keine Vorteile.
</P>

<P CLASS="P2">
	2.9.1 Wie gut kann man mit einer 28.8er Verbindung spielen ? Bei vielen Online RPG's ist Lag ein Riesenproblem.
</P>	
<P CLASS="P1">
	Wir glauben nicht, daß der Lag auf Grund der 28.8er Verbindung entsteht. Normalerweise ist der Server oder der eigene Provider schuld, manchmal auch die Verbindung zwischen den beiden. Das Spiel ist so konzipiert, daß nicht mehr Daten über das Internet geschickt werden als ein ein 28.8er Modem verarbeiten kann. Außerdem versuchen wir den Lag weitestgehend zu reduzieren, indem wir einen sehr ausgereiften Netzwerkcode verwenden; außerdem versuchen wir die Spieleranzahl pro Server gering zu halten.
</P>

<P CLASS="P2">
	2.10 Gibt es Tag und Nacht und wie lange dauert ein EverQuest-Tag in Echtzeit ?
</P>	
<P CLASS="P1">
	Ja, es gibt Tag und Nacht. Ein EverQuest-Tag dauert 72 Minuten.
</P>

<P CLASS="P2">
	2.11 Wie dunkel ist es in der Nacht ? Brauche ich einen Zauberspruch oder eine Fackel um in der Nacht sehen zu können ?
</P>	
<P CLASS="P1">
	Naja, einige Zonen sind etwas heller ... aber in einem Dungeon ganz unten sieht man ohne Spell oder Fackel absolut nichts. Außer man hat Infravision ...
</P>

<P CLASS="P2">
	2.12 Gibt es eine Art aktives Wirtschaftssystem im Spiel ?
</P>	
<P CLASS="P1">
	Ja, hauptsächlich durch die Spieler selbst bestimmt. Preise werden durch Angebot und Nachfrage bestimmt. Typischerweise werden Gegenstände die öfter angeboten als gekauft werden immer billiger werden.
</P>

<P CLASS="P2">
	2.13 Gibt es unterschiedliches Wetter im Spiel ?
</P>	
<P CLASS="P1">
	Ja, es kann regnen, schneien und auch nebelig sein.
</P>

<P CLASS="P2">
	2.13.1 Kann man z.B. durch Frost und extreme Kälte (z.B. Velious) Schaden nehmen wenn man sich nicht ausreichend dagegen schützt ?
</P>	
<P CLASS="P1">
	Ja, dies wird durch die entsprechenden Werte in Kälte-, Feuer-Widerstand etc. bestimmt.
</P>

<P CLASS="P2">
	2.14 Wie viele Städte und andere Zonen gibt es ?
</P>	
<P CLASS="P1">
	Ursprünglich gab es 12 Städte und über 70 Zonen in der Originalausgabe von EverQuest.<br>
	Durch die EverQuest-Erweiterung, The Ruins of Kunark, wurden eine 13. Start-Stadt sowie über 25 neue Zonen hinzugefügt.
</P>

<P CLASS="P2">
	2.15 Wie werden die Spielfiguren gesteuert ?
</P>	
<P CLASS="P1">
	Tastatur und Maus.
</P>

<P CLASS="P2">
	2.16 Gibt es die Möglichkeit Screenshots im Spiel zu machen ?
</P>	
<P CLASS="P1">
	Ja, einfach auf die Taste "-" (rechts oben im Ziffernblock) drücken und es wird eine ".bmp"-Datei im EverQuest-Verzeichnis erzeugt.
</P>

<P CLASS="P2">
	2.17 Im Charakter-Erstellungsfenster gibt es Götter. Gibt es unterschiedliche Religionen in EverQuest ?
</P>	
<P CLASS="P1">
	Es gibt viele unterschiedliche Religionen.
</P>

<P CLASS="P2">
	2.17.1 Beeinflusst die gewählte Religion die Art wie man seinen Charakter spielen soll ?
</P>	
<P CLASS="P1">
	Wie man seine Rolle spielt ist jedem selbst überlassen.
</P>

<P CLASS="P2">
	2.17.2 Gibt es entsprechende Plätze für die unterschiedlichen Glaubensrichtungen ?
</P>	
<P CLASS="P1">
	Man kann überall in Norrath Tempel einer bestimmten Gottheit finden.
</P>

<P CLASS="P2">
	2.18 Werden Städte während der Nacht geschlossen, wie bei Daggerfall ?
</P>	
<P CLASS="P1">
	Nein.
</P>

<P CLASS="P2">
	2.19 Kann man Gruppen bilden ?
</P>	
<P CLASS="P1">
	Ja. Tatsächlich soll man unbedingt Gruppen bilden. Viele gefährliche Zonen sind nur für Gruppen zugänglich, weil man solo dort nicht lange überleben wird.
</P>

<P CLASS="P2">
	2.19.1 Was ist mit jenen, die lieber solo spielen ? Ist das Solo spielen weitestgehend unmöglich oder gibt es nur Einschränkungen in Bezug auf wohin man allein gehen kann ?
</P>	
<P CLASS="P1">
	Solo spielen ist etwas limitiert, speziell in gefährlichen Zonen, aber definitiv nicht unmöglich. Es gibt Rassen/Klassen Kombinationen in EverQuest die sehr gut zum Solo spielen geeignet sind.
</P>

<P CLASS="P2">
	2.19.2 Wird Experience unter den Gruppenmitgliedern aufgeteilt ? Und wie ?
</P>	
<P CLASS="P1">
	Ja. Es wird je nach Level jedes einzelnen Spielers aufgeteilt. Um so erfahrener (höher im Level) man ist um so größer ist der Anteil den man bekommt.
</P>

<P CLASS="P2">
	2.19.3 Wenn man in einer Gruppe ein Monster killt, ist die gemeinsame Experience gleich hoch als wenn zwei Leute, die nicht in einer Gruppe sind ein Monster killen ?
</P>	
<P CLASS="P1">
	Nein, es ist unterschiedlich. Man muß in einer Gruppe sein wenn man zusammen Monster killt.<br>
	Bitte beachten: Ein Krieger killt ein Monster und ein Kleriker heilt den Krieger - der Kleriker sollte ebenso Experience bekommen.
</P>

<P CLASS="P2">
	2.19.4 Gibt es ein Limit wieviele Personen in einer Gruppe sein können ?
</P>	
<P CLASS="P1">
	Eine Gruppe kann aus max. 6 Personen bestehen.
</P>

<P CLASS="P2">
	2.19.5 Gibt es ein Kommando um Geld in der Gruppe aufzuteilen ?
</P>	
<P CLASS="P1">
	Ja.
</P>

<P CLASS="P2">
	2.20 Gibt es Unterwasserzonen oder einen Unterwasserkontinent ?
</P>	
<P CLASS="P1">
	Keinen ganzen Kontinent aber eigene Zonen, ja.
</P>

<P CLASS="P2">
	2.20.1 Kann man Unterwasser kämpfen ?
</P>	
<P CLASS="P1">
	Ja.
</P>

<P CLASS="P2">
	2.21 Gibt es einen eigenen Rollsenspiel-Server ?
</P>	
<P CLASS="P1">
	Nein.
</P>

<P CLASS="P2">
	2.22 Kann man die Namen von anderen Personen sehen ?
</P>	
<P CLASS="P1">
	Ja, Name und optional die Gildenzugehörigkeit (wenn die Person in einer Gilde ist) wird über dem Kopf der Spielfigur dargestellt.
</P>

<P CLASS="P2">
	2.23 Wie wird verhindert, daß sich jemand ein Vermögen anschafft indem immer wieder eine neue Spielfigur mit der Startausrüstung erzeugt wird ?
</P>	
<P CLASS="P1">
	Die Startausrüstung kann nicht verkauft oder weggeworfen werden und sit somit wertlos.
</P>

