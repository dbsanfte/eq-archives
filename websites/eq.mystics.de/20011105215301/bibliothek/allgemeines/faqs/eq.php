<P CLASS="P2">
	1.1 Was versteht man unter FAQ ?
</P>	
<P CLASS="P1">
	FAQ steht für Frequently Asked Questions, also die am häufigsten gestellten Fragen. FAQ's 
	werden erstellt, um die meistgestellten Fragen und Antworten abzudecken.
</P>

<P CLASS="P2">
	1.2 Was ist <i>EverQuest</i> ?
</P>	
<P CLASS="P1">
	EverQuest ist ein echtes 3D Multiplayer Online Rollenspiel. Eine andere mögliche Beschreibung wäre, EverQuest ist ein grafisches MUD.
</P>

<P CLASS="P2">
	1.2.1 Wann wurde EverQuest fertiggestellt ?
</P>	
<P CLASS="P1">
	EverQuest wurde am 16. März 1999 released.
</P>

<P CLASS="P2">
	1.3 Was ist ein RPG ?
</P>	
<P CLASS="P1">
	RPG steht für Rollenspiel (engl. Role Playing Game). Es wird oft darüber diskutiert was das tatsächlich sein soll, also versuchen wir mal zu beschreiben was wir damit meinen. Wenn man EverQuest spielt erstellt man zuerst eine Spielfigur (Character). Man kann zwischen 12 Rassen und 14 Klassen wählen, weiblich od. männlich, und außerdem noch die Attribute anpassen (z.B. wie stark man sein will oder wie intelligent). Danach betritt man die virtuelle Welt und macht aus ihr od. ihm was immer man will. Man selbst definiert die eigene Rolle in EverQuest und wie gut man diese Rolle spielt ist jedem selbst überlassen. Es wird oft gesagt, daß RPG ein bestimmtes Thema od. eine bestimmte Einstellung ausdrückt. Obwohl wir nicht unbedingt der Meinung sind, daß dies immer zutrifft, EverQuest ist ein traditionelles RPG. Unsere Welt ist voller Phantasie, Magier und Drachen, mächtige Zauberer und mordender Horden Orks. Es gibt jede Menge Quests und Rätsel, aber man selbst entscheidet welche bzw. ob man überhaupt eine ausführt. Du kannst ein mächtiger Krieger werden, aber auch ein Händler, der seine Waren in den unterschiedlichen Ländern verkauft. Wir haben die Voraussetzungen geschaffen, aber was aus dieser Welt wird liegt an euch.
</P>

<P CLASS="P2">
	1.4 Was bedeutet 3D ?
</P>	
<P CLASS="P1">
	Wir haben festgestellt, daß die Bezeichnung 3D immer und überall verwendet wird. Bezogen auf EverQuest meinen wir das, was es wirklich ist. Unsere Engine ist echtes 3D - keine Pseude 3D Ansicht. Man kann komplett um ein Haus oder was auch immer laufen und es betreten, und jederzeit kann die Ansicht frei zwischen First-Person, allen Seiten oder einer 3. Person gewählt werden.
</P>

<P CLASS="P2">
	1.5 Was ist ein <I>Massives Mehrspieler Online Spiel</I> ?
</P>	
<P CLASS="P1">
	Während viele moderne Computerspiele Multiplayer-Spiele ermöglichen gibt es nur sehr wenige, die für weit über 1000 gleichtzeitig spielende Personen ausgelegt sind. Bei EverQuest startet man nicht das Server-Programm auf einem lokalen Netzwerk und lädt ein paar Freunde zum Spielen ein. Stattdessen startet man das EQ-Client-Programm und nutzt das Internet um sich mit den Welt-Server zu verbinden. Man ruft die Freunde an oder schickt ihnen ein E-Mail und teilt ihnen mit in welcher Stadt od. welchem Dungeon man sich trifft, oder noch besser, man loggt einfach ein und trifft Hunderte andere Personen aus aller Welt und gewinnt neue Freunde.<BR>
	Das ist EverQuest, und das ist es was man als "Massives Mehrspieler Online Spiel" bezeichnet.
</P>

<P CLASS="P2">
	1.6 Was ist ein MUD ?
</P>	
<P CLASS="P1">
	MUDs (Multi-User Dungeons/Dimensions) sind online RPG's die rein auf Text basieren und einige Jahre aktuell waren. EverQuest ist genau dasselbe, mit dem einzigen Unterschied, daß unsere Welt mit einer Echtzeit 16bit Farbgrafik, voll texturiert, ausgestattet ist. Außerdem, während die meisten dieser MUDs nur von wenigen Personen als Hobby betrieben wurden, ist EverQuest ein professionelles kommerzielles Produkt - unsere Mannschaft steht bereit um euch zu betreuen.
</P>

<P CLASS="P2">
	1.7 Wer seid ihr ?  Woher kommt EverQuest ?
</P>	
<P CLASS="P1">
	989 Studios (ehemals Sony Interactive Studios), verantwortlich für PSX Hits wie Rally Cross und NFL Gameday, begann im März 1996 mit der Entwicklung von EverQuest. Im Jänner 1999 entstand Verant Interactive, Inc., ein unabhängiges Entwicklungs Studio für Online Spiele. Verant hat auch Tanarus (http://www.tanarus.com) entwickelt, ein Online Panzer Kriegsspiel, sowie das kommende massive Multiplayer RTS Sovereign (www.station.sony.com/soverign) und Star Wars Online.
</P>

<P CLASS="P2">
	1.8 Welche Hard- und Software wird für EverQuest benötigt ?
</P>	
<P CLASS="P1">
	Der EverQuest Client benötigt einen Pentium 166 (empfohlen Pemtium 200+) mit einer Direct 3d kompatiblen 3D Grafikkarte und 32 MB RAM. Das Programm läuft unter Windows 95 od. 98. Wir empfehlen eine 3D Karte die mindestens so schnell wie eine Voodoo Grafikkarte von 3DFX ist.<BR>
	Die EverQuest Erweiterung, <I>The Ruins of Kunark</I>, benötigt einen Pentium 200, 64 MB RAM und eine 3D Grafikkarte die mindestens so schnell wie eine Voodoo2 ist. Wir empfehlen einen Pentium 266+ und 128 MB RAM.
</P>

<P CLASS="P2">
	1.8.1 Läuft EverQuest unter WindowsNT oder Windows2000 ?
</P>	
<P CLASS="P1">
	EverQuest sollte unter Windows 2000 laufen, wobei es immer noch Probleme mit einigen 3D Karten, den Treibern, und Windows 2000 gibt. EverQuest läuft nicht unter älteren Versionen von NT.
</P>

<P CLASS="P2">
	1.8.2 Läuft EverQuest unter anderen Betriebssystemen als Windows 9x ?
</P>	
<P CLASS="P1">
	Derzeit gibt es keine Pläne andere Betriebssysteme zu unterstützen.
</P>

<P CLASS="P2">
	1.9 Welche 3D Karten werden unterstützt ?
</P>	
<P CLASS="P1">
	EverQuest verwendet Direct3D und Glide. Direct 3D ist eine 3D API von Microsoft welche die meisten derzeit erhältlichen als auch zukünfitge 3D Karten unterstützt. Glide ist eine 3D API von 3DFX und arbeitet mit deren Karten (Voodoo, Voodoo2, etc.).
</P>

<P CLASS="P2">
	1.9.1 Ich bin kein technisches Genie - was ist D3D ?
</P>	
<P CLASS="P1">
	Direct3D (D3D) ist Teil von DirectX, ein Software Zusatz zu Windows 9x den die meisten aktuellen Spiele benutzen. Direct3D stellt eine vereinheitlichte Schnittstelle für die Grafikausgabe dar und die Programmierer müssen nicht mehr für jede einzelne Grafikkarte eigene Treiber entwickeln. Detaillierte Infos über DirectX und Direct3D gibt es bei Microsoft unter http://www.microsoft.com/directx/
</P>

<P CLASS="P2">
	1.9.2 Wird eine 3DFX Karte benötigt ?
</P>	
<P CLASS="P1">
	Es wird eine 3D Karte benötigt, aber es muß nicht unbedingt eine 3DFX Karte sein. Die Karte muß Direct3D-fähig sein und sollte mindestens so schnell wie eine 3DFX Karte sein. Am Besten informiert man sich in den diversen Artikeln sowohl im Internet als auch in Magazinen, wo 3D Karten untereinander verglichen werden.
</P>

<P CLASS="P2">
	1.9.3 Welche Version von DirectX wird benötigt ?
</P>	
<P CLASS="P1">
	EverQuest benötigt DirectX 6 oder neuer.
</P>

<P CLASS="P2">
	1.9.4 Kann mir jemand sagen ob EverQuest OpenGL unterstützt ?
</P>	
<P CLASS="P1">
	EverQuest verwendet Glide (API von 3DFX) und Direct3D (API von Microsoft, die die meisten modernen 3D Karten unterstützt). OpenGL wird nicht unterstützt (nichts gegen OpenGL, aber mit Glide und D3D ist kein echter Bedarf vorhanden).
</P>

<P CLASS="P2">
	1.10 Kann man das Spiel herunterladen oder gibt es nur eine CD Version ?
</P>	
<P CLASS="P1">
	CD-ROM.
</P>

<P CLASS="P2">
	1.11 Wird die Musik in Form von MIDI Files oder direkt von CD gespielt ?
</P>	
<P CLASS="P1">
	EverQuest verwendet MIDI und unterstützt die meisten Sound Karten, inklusive jene die MIDI Sound Fonts verwenden.
</P>

<P CLASS="P2">
	1.11.1 Gibt es in EQ 3D-Sound ?
</P>	
<P CLASS="P1">
	Ja - EverQuest unterstützt 3D Sound unter Verwendung von Direct Sound 3D. Es funktioniert beides, EAX und A3D.
</P>

<P CLASS="P2">
	1.12 Werden die Spieldaten am Server od. auf der eigenen Festplatte gespeichert ?
</P>	
<P CLASS="P1">
	Am Server. Wir sind hier sehr aufmerksam, da Hacker existieren und wir uns gegen sie so gut wie möglich schützen wollen.
</P>

<P CLASS="P2">
	1.12.1 Mit einer voraussichtlichen Zahl von 1500-1800 Spieler pro Server, sind da auch genug Server vorhanden ?
</P>	
<P CLASS="P1">
	Wir tun unser Bestes um genug Server zur Verfügung stellen zu können.
</P>

<P CLASS="P2">
	1.12.2 Welche Daten werden am Client liegen ?
</P>	
<P CLASS="P1">
	Grundsätzlich liegen, wie bei jedem anderen Spiel auch, die Grafik und Zonendaten am Client, und die kritischen Daten wie Chars am Server. Statische unveränderliche Informationen werden am Client abgelegt. Das ist größtenteils die Grafik - man wird diese nicht herunterladen wollen.
</P>

<P CLASS="P2">
	1.13 Gibt es Hotkeys oder programmierbare Makros ?
</P>	
<P CLASS="P1">
	Ja.
</P>

<P CLASS="P2">
	1.14 Wird das Spiel regelmäßig upgedated ?
</P>	
<P CLASS="P1">
	Natürlich. Die Welt von EverQuest wird sich regelmäßig weiterentwickeln und expandieren.
</P>

