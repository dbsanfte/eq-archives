<P CLASS="P1">
	Nachdem man sich nun in der Stadt seiner wahl befindet, sollte man zu aller erst einmal die Spells in sein 
	Buch übertragen. Jetzt sollte man sich auf die Suche nach seinem Guildmaster machen und diesem die 
	Questscroll überreichen. Bei der Gelegenheit kann man gleich auch ein bisschen seine Stadt erkunden.
	Nachdem man die wichtigsten Orte also Bank und Guildehalle gefunden hat, kann man sich ins Getümmel 
	stürzen. Vor eurer Stadt sollen wohl genug gegner sein, mit denen ihr es aufnehmen könnt. Aber vorsicht, 
	ihr habt doch wohl den Dagger und die Robe vom Guildmaster equipt oder? Außerdem solltet ihr immer 
	Minorshielding auf euch gecastet haben. 
</P>

<P CLASS="P1">
	Die ersten paar Tötungen als Enchanter sind sehr schwer, darum wärt ihr nicht schlecht beraten, wenn ihr 
	eure ersten 5 Skillpunkte oder auf jeden fall einen Teil davon auf Fencing legt um nicht erst immer 
	sterben zu müsst, bis ihr genug Skillpunkte in Fencing habt um zu Überleben. So sollte es möglich sein, 
	sich Pendrils Animation zu kaufen. Mit eurem Pet im Rücken ist das ganze Leben gleich viel leichter.
</P>

<P CLASS="P1">
	Als Enchanter solltet ihr mit den steigenden Levels immer wieder Skillpunkte auf Jewlerycraft legen, 
	genaueres dazu erfahrt ihr in der Tradeskillsektion von Mystics, da ihr ja später die Enchantment Spells 
	bekommt und ihr so magische Schmuckstücke herstellen könnt.
</P>

<P CLASS="P1">
	Leider sind die Enchanter Pets nicht so gut wie die des Magicans. Man kann ihnen keinerlei Befehle erteilen und 
	sich auch nicht zusätzlich mit Waffen ausstatten, aber sie erfüllen ihren Zweck genausogut.
</P>

<P CLASS="P0"><I>Marius</I></P>

