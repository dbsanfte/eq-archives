<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Clawdeen<BR>
	<BR>
</P>

<P CLASS="P1">
	Zusammen mit Ginluen und Guddace hatte ich mich auf den Weg nach Lesser Faydark
	gemacht, fest entschlossen, den Nybright Schwestern, die in Faydwer als
	gefürchtete Banditen bekannt waren, den Garaus zu machen.
	Wir erreichten Ihr Zeltlager und wollten uns gerade eine Strategie
	zurechtlegen, als Kayla uns entdeckte, ihre Schwestern alarmierte und wir
	unvorbereitet von ihnen angegriffen wurden.
	Kayla und Jayla fielen unseren Klingen zum Opfer, doch die anderen beiden
	schienen die Oberhand zu gewinnen. Unser Blut tropfte in das Gras zu unseren
	Füßen und meine magische Begabung war erschöpft.
	Tapfer zog Guddace das Interesse der Banditen auf sich, um uns einen
	Vorsprung zu verschaffen, und Ginluen und ich rannten so schnell wie nur
	möglich zum Außenposten der Waldelfen in Lesser Faydark.
	Bange Minuten verstrichen, doch dann erblickten wir auch Guddace in der
	Ferne, der dicht gefolgt von den beiden Nybright Schwestern mit letzter Kraft
	auf den Außenposten zulief.
	Wir riefen die Wachen zur Hilfe, und mühelos streckten diese unsere Gegner
	nieder. Mit Trauer über unser Versagen und mit Freude über unser gerettetes
	Leben versammelten wir uns um ein Lagerfeuer.
</P>

<P CLASS="P1">
	Während ich meditierte, sang Ginluen für uns ein heilendes Lied und Guddace
	verband unsere Wunden mit den Bandagen aus seinem Gepäck.
	Als wir uns gestärkt und erfrischt fühlten, kauften wir beim Händler für ein
	kleines persönliches Festmahl ein. Guddace kaufte viele Flaschen Met, und
	während ich beim Wasser blieb und mich zusehends über das alberne Verhalten
	meiner Gefährten wunderte, begannen Guddace und Ginluen miteinander zu tanzen
	und wurden immer ausgelassener. 
</P>

<P CLASS="P1">
	Guddace lehrte uns gerade ein wenig seiner Sprache, Barbarisch, als ich
	eine Stimme hinter mir hörte.<br>
	<I>&quot;Sei gegrüßt Clawdeen.&quot;</I><br>
	Verblüft sah ich mich um und erblickte Escado, ein Mitglied meiner Gilde.<br>
	<I>&quot;Die sind ja völlig betrunken&quot;</I>, lachte Escado und deutete
	auf meine Gefährten.<br>
	<I>&quot;Betrunken? Was ist das? Woher weißt du das?&quot;</I> erwiderte ich erstaunt.<br>
	<I>&quot;Sieh hin, Clawdeen. Sie torkeln, sind albern und sprechen undeutlich.
	Hm, das war wohl etwas viel vom Met.&quot;</I><br>
	Als Escado erkannte, dass mir offensichtlich nicht klar war, wovon er
	sprach, fügte er hinzu:<br>
	<I>&quot;Alkohol, Clawdeen, das ist es, was sie sich so verhalten lässt. Du
	liebe Güte, sag bloß, du hast noch nie Alkohol getrunken,! Wein? Bier? Met?&quot;</I>
</P>

<P CLASS="P1">
	Ich schüttelte stumm den Kopf und Escado brach erneut in Gelächter aus.<br>
	<I>&quot;Warte einen Augenblick hier, kleine Elfe, ich bin gleich zurück.&quot;</I>
	Mit diesen Worten verschwand Escado in die Taverne des Außenpostens. 
	Während ich seine Rückkunft erwartete, sah ich im Geiste sein Gesicht vor
	mir. Weiche Gesichtszüge umrahmt von blondem Haar ... wie Saphire leuchtende, große
	schimmernde Augen ...<br>
	<I>&quot;Hier!&quot;</I> riss Esacado mich aus meinen Gedanken.<br>
	<I>&quot;Was denn?&quot;</I> fragte ich irritiert.<br>
	<I>&quot;Träumst du? Die Flaschen hier habe ich für dich gekauft, nu trink.&quot;</I><br>
	<I>&quot;Alle drei Flaschen?&quot;</I><br>
	Escado nickte und sah amüsiert zu, wie ich die erste Flasche ansetzte und
	in einem Zug austrank. 
</P>

<P CLASS="P1">
	Der süße Met rann meine Kehle entlang und hinterließ ein leichtes Brennen, das
	sich nach der zweiten Flasche als feines Kribbeln entpuppte.
	Fast im selben Augenblick, in dem ich die dritte und letzte Flasche absetzte, geschah es. 
	<I>&quot;Oh ...&quot;</I> entfuhr es mir, doch auch das half mir nicht. Alles
	drehte sich um mich, Escado wurde von meinem Blick verzerrt, dann verschwamm
	er vor meinen Augen und plötzlich sah ich seine wundervollen blauen
	Augen, vier, nein, sechs Mal.
	Das nächste, was mich ergriff, war eine wohlige Wärme aus meinem Inneren heraus
	und ein Gefühl, das mich drängte, die ganze Welt vor Freude zu umarmen. Ich
	beschloss, damit gleich bei Escado anzufangen, sprang auf, umarmte ihn,
	errötete, begann mit ihm zu tanzen, lief um das Feuer herum, lachte wie
	verrückt dabei. Dann setzte ich mich Escado zu Füßen.<br>
	<I>&quot;Weiss Du, Escado, Du hass... uihhh... Du hass so richtig schöne
	Schaffire ... äh ... Augen, jawoll.&quot;</I> lallte ich und nickte begeistert
	und bestätigend, als mir plötzlich speiübel wurde.
	<I>&quot;Ich glaub, ich muss mich übergeben ... ohoh ... is mir schlecht gez. Is
	nich lustig mehr, Escado, mach wech den Met gez bidde.&quot;</I><br>
	Esacdo lachte aus vollem Halse.<br>
	<I>&quot;Ich fürchte da musst Du nun durch, Clawdeen.&quot;</I><br>
	<I>&quot;Durch? Wo durch?&quot;</I><br>
	<I>&quot;Man kann das nicht so einfach abstellen. Es hört von ganz allein wieder
	auf.&quot;</I><br>
	<I>&quot;Wann denn?&quot;</I><br>
	<I>&quot;Oh, eine Stunde kann es leicht noch dauern.&quot;</I><br>
	<I>&quot;So lange? Ich sterbe Escado, mir ist üüübel, so übel.&quot;</I><br>
	Das Drehen wollte nicht enden, ebenso wie sich meine gesamte Umwelt standhaft
	weigerte, mir unverzerrt vor die Augen zu kommen.
	Ich stöhnte und jammerte nur noch, und nach etwa einer halben Stunde hatte
	Escado schon soviel Mitleid mit mir, dass er mich umarmte und festhielt, was
	mein Gemüt unbeschreiblich beruhigte. 
</P>

<P CLASS="P1">
	Irgendwann dann fühlte ich mich wieder besser, wenn ich auch das Gefühl
	nicht los wurde, das mir sagte, ich hätte mich ziemlich blamiert.
	Leider zog die Besserung meines Zustandes auch die Trennung von Escado nach
	sich, der nun seine Reise nach Mistmoore fortsetzen wollte.
	Der Abschied fiel mir schwer, obwohl ich wusste, dass ich ihn schon wegen
	der Gilde nicht aus den Augen verlieren würde. Noch lange saß ich am Feuer
	und träumte mit offenen Augen von Escado, bevor ich Guddace und Ginluen allein
	zurückließ und mich auf den Weg nach Kelethin machte,
	Ich wusste, wenn überhaupt etwas, dann würde mich jetzt meine Heimatstadt trösten.<br>
	Trösten darüber hinweg, dass er unkontrollierbar durch meine Gedanken reiste
	und mir sein Gesicht nicht aus dem Kopf wollte.<br>
	Trösten darüber hinweg, dass ich mich wohl Hals über Kopf verliebt hatte, was mir doch
	nie mehr passieren sollte.<br>
	Trösten darüber hinweg, dass ich nicht einmal erahnen konnte, ob er dieses Gefühl
	je erwidern würde.
</P>

<P CLASS="P1">
	Aber eines wusste ich genau: Ich würde ihm nie erzählen, welche Gefühle er in mir geweckt hat.
</P>

<p class="P0" ALIGN="CENTER"><I>Clawdeen</I></p>

