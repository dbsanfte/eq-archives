<P CLASS="P1">
	Wie schon der Name sagt entstehen diese Mischlinge aus einer Verbindung von Menschen und Elfen. 
	Während ihre Anzahl zu Kriegszeiten und vor allem in den Jahren danach verhältnismäßig hoch ist, 
	bleibt sie in langen Perioden des Friedens klein. 
</P>
	
<P CLASS="P1">
	Zum größten Populationswachstum unter den Halbelfen kommt es nach Kriegen, wenn Söldnerbanden 
	heimkehren und marodierend und plündernd das Land durchstreifen. Dabei kommen sie kleinen 
	Elfensiedlungen nahe. Sie töten die Krieger und vergewaltigen die Frauen und wenn es ein Mädchen 
	nicht übers Herz bringt sich zu töten, das Leben ist bei allen Elfen heilig (ja, selbst bei 
	den Dunkelelfen, da ist es sogar so heilig, daß sie denken, daß ihre Opfer durch das Opfern an 
	ihren Gott diesem näherkommen), ist es an der Zeit einen weiteren Halbelfen in der Welt Willkommen 
	zu heißen. Auf der anderen Seite gibt es noch legale Mischehen zwischen Elfen und Menschen, dabei 
	ist der Mann meist menschlich und die Frau eine Elfe, doch diese sind so selten, daß sie nicht 
	erwähnenswert sind. Diese werden vor allem bei den Elfen überhaupt nicht gerne gesehen. 
</P>
	
<P CLASS="P1">
	Aus diesem Grund ist es nicht verwunderlich, daß diese "Bastarde" nirgends gerne gesehen werden. Bei den 
	Elfen werden sie nur geduldet, denn schließlich sind es zum Teil auch Elfen, aber wenn die Halbelfen ihre 
	Heimat verlassen, trauert ihnen keiner nach. Es ist nicht verwunderlich, daß deswegen viele, wenn nicht zu 
	sagen fast alle, Halbelfen ihre Elfenheimat verlassen. Die Arroganz und Hochmut der Elfen, ihre Intoleranz 
	und ihre reine "vernebelte" Seele lassen sie ständig ihre "schmutzige" Herkunft spüren und so kommt es, daß 
	sie zu den Menschen gehen, die für ihre Toleranz und neutrale Sicht der Dinge bekannt sind... 
</P>

<P CLASS="P1">
	Doch wie viele Dinge im Leben ist auch dies nur ein Traum. Der Großteil der Menschen ist eher guter 
	Gesinnung und sie erinnern sich gut und gerne daran, wie sie von Elfen herablassend behandelt werden, 
	als wären sie nichts anderes als Tiere. Sie erinnern sich daran, wenn sie einen Halbelfen sehen und wie 
	es nicht anders sein soll, sie erinnern sich auch an ihre dunkle Seite, die Söldner die viel zu dem 
	schlechten Verhältnis mit den Elfen beitragen und kein Volk will an das Schlechte in ihm gerne erinnert werden. 
</P>

<P CLASS="P1">
	Auf den ersten Blick sehen Halbelfen wie gewöhnliche Menschen aus, sie sind größer und robuster 
	als Elfen, aber nicht ganz so groß- und stark gewachsen wie Menschen. Im Gegensatz zu den Elfen 
	können sich männliche Halbelfen einen Bart wachsen lassen, was ihre Herkunft noch mehr vertuscht. 
	Einen Halbelfen kann man erst erkennen, wenn man ihm Angesicht zu Angesicht steht, denn ihre leicht 
	gewölbten Augenbrauen, die fein geschnittenen Gesichtszüge und die spitz zulaufenden Ohren, was viele 
	Halbelfen durch langes Haar zu verbergen versuchen, ( an diese Stelle ist es mir ein Rätsel, wieso die 
	Halbelfen Spielfiguren in EQ alle kurze Haare haben) erkennt jeder fast gleich, daß dies kein reiner 
	Mensch ist. 
</P>

<P CLASS="P1">
	Halbelfen sind von Geburt an wissbegierig und rastlos, was wohl an dem menschlichen Blut liegt, das in 
	ihren Adern brennt. Sie drängen zur Tat und können das Weltgeschehen nicht einfach so tatenlos 
	verfolgen, wie es die Elfen tun. Neugierde zählt eher zu den mager ausgeprägten Eigenschaften eines 
	Halbelfen, wo dagegen Mißtrauen und Argwohn weit verbreitet sind. Angehörigen anderer Rassen gegenüber 
	sind sie sehr verschwiegen, unter ihresgleichen und in Gesellschaft ihrer Freunde jedoch ausgesprochen 
	redselig. Da sie nirgends gerne gesehen werden, selbst die aus legalen Ehen nicht, reisen sie von Ort zu 
	Ort und eignen sich dabei ein erstaunliches Wissen über die Welt auf. In Abenteurergruppen sind sie stets 
	gerne willkommen, denn da zählt weniger die Rasse als viel mehr das Können und darin sind sie 
	unübertroffen da sie es verstehen das Wissen der Elfen und der Menschen zu verknüpfen. Oft findet 
	man Halbelfen auch an der Spitze solcher Gruppen, denn sie verstehen es ihrem Herzen zu folgen und 
	doch dabei ihren Verstand nicht zu vergessen, was anderen Rassen und Klassen fehlt, wie z.B. dem 
	geradlinig denkendem Paladin. 
</P>
	
<P CLASS="P1">
	Halbelfen sind geradezu prädestiniert Waldläufer zu werden. Aufgrund ihrer geerbten Liebe zur Kunst und 
	Musik auch Barden.  Sie werden auch oft zu Dieben, da sie die sozialen Umstände in denen sie leben zwingen, 
	viele gibt es aber nicht. Das gleiche gilt für Krieger, sie sind unter den Halbelfen relativ selten vertreten. 
</P>
	
<P CLASS="P0">
	<I>Maya, Paladin der Erulisi Marr</I>
</P>

