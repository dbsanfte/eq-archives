<P CLASS="P1">
<b>Strength</b>  Lt. Aussagen vieler Highlvlrogues ist dies die
wichtigste Stat, da man dadurch je höher destso öfter die Max.Dmg
macht. Später dauern die Kämpfe länger als 10 sec. ;-)<br>
<br>
<b>Stamina</b>  Stellt HPs / Ausdauer im Kampf und Tauchen da, ganz nett
aber net schlimm wenn da was fehlt. Es sei denn ihr kämpft mit
schweren Waffen, was bei Rogues aber seltenst vorkommt.<br>
<br>
<b>Agility</b>  Hilft beim Ausweichen (Parry/Dodge steigern), erhöht die
AC bei Rogues ab 101 erheblich.<br>
<br>
<b>Dexterity</b>  Nach Str, die 2.wichtigste Stat des Rogues. Je höher
die Dex umso mehr trifft man den Gegner, erhöht die Melee-Skills
schneller und läßt öfter den Max. Backstab treffen. Außerdem steigt
man schneller in den Rogue-only-Skill schneller auf. Desweiteren
gibt es viele Rogue Waffen die einen Effekt haben, sprich Procen,
wie oft dieser Effekt auftritt ist auch abhängig von der Höhe
Dexterity.<br>
<br>
<b>Wisdom</b>  Eher unerheblich, hilft bei Tradeskills besser die
Gegenstände herzustellen und man resisted besser Charm-Sprüche.<br>
<br>
<b>Intelligence</b>  Läßt die Tradeskills (Nicht die
Rogue-Skullduggery-Skills) schneller steigern.<br>
<br>
<b>Charisma</b>  Man kann Sachen besser kaufen und verkaufen und hat bei
nicht so freundlich gesinnten NPCs eher die Chance eine Quest zu
bekommen.
</P>

