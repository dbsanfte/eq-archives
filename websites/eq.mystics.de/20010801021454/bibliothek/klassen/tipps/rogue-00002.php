<P CLASS="P1">
Der Rogue ist nicht zum Tanken gedacht, auch wenn er kurzzeitig
sicherlich seine Rolle übernehmen kann. Er ist so ein Zwischending
der seine wahren Fähigkeiten erst in den späteren Levels zeigt.
Seine Skills in Melee steigen so schnell auf wie beim Warrior,
teilweise (!) sogar mehr. (Parry/Dodge/Dual Wield/Riposte) Dort
kommt der Rogue schneller auf seine Skills. Allerdings wird der
Rogue ohne Backstabben zu können niemals auf die Damage (in den
unteren Levels) kommen die ein Monk oder Warrior austeilt. Das
ändert sich immer mehr je weiter man als aufsteigt, ab lvl 45 soll
man angeblich sogar locker den Warrior überholen, trotz seiner
Critical hits bei seiner normalen Waffe. Allerdings ist der Rogue
nur in der Gruppe nützlich. Ein Rogue der soloed kann nur sehrrrr
selten Backstab machen und das ist DER entscheidende Skill für ihn.
Rogues können auch deshalb nicht mehr wirklich ab Lvl 20 als Tank
benutzt werden, weil die entsprechende Armor fehlt. Spätestens da
haben alle Warriors /Paladins, etc. ein komplettes Set Bronze Armor
(zusätzlich haben die auch mehr HPs als wir), während unsereins nur
mit Kettenhemdchen rumlaufen darf. Die richtige gute Rogue Armor
gibt es nur sehr selten und ist dann sehr teuer und zu becampt, da
meistens auch andere Klassen wie Barden, Ranger oder Shamamen die
dann tragen dürfen. Jetzt denken manche wiederrum, wozu nen Rogue
in die Gruppe einladen, wenn ich stattdessen nen Paladin o.ä.
bekommen kann. Das wird allerdings dann einigen klar wenn Sie in
den High Levels nen Türöffner brauchen oder nen Corpse-Holer
brauchen. Apropos Corpse holen: Rogues sind die einzige Klasse in
Norrath die bei Hide sowohl invis zu allen NPCs sind (undead und
living NPCs) Caster wieder ausgenommen-. Desweiteren haben Rogues
durch ihre Low Delay Weapons wesentlich häufiger die Chance
feindliche Caster zu disrupten wofür Tanks sie lieben in der Regel!
Durch Ihren Pickpocking Skill machen Sie in späteren Levels ne
Menge Geld (der dann meistens in Poison Training umgewandelt wird
<ggg>), allerdings sollte man Pickpocking in der Gruppe
unterlassen, sonst hat man diese nicht lange ;-) Auf Poisoning bin
ich bewußt nicht eingegangen, da dieser Skill neben BS vieles wett
machen kann. Es gibt dort Gifte die Schwächen können /Rooten
/Paralisieren / Mana vernichten/ Dots(Damage over Time)/DD (Direct
Damage) Kurzum alles was man braucht! Da diese Poisons ein paar PP
verbrauchen, sollte man dieses nur bei Lohnenswerten Gegnern
einsetzen. ;-)<br>
<br>
Rogues sind also ein bißchen Tausendsassa, in der Regel
komplizierter zu spielen als ein Warrior und man stirbt auch des
öfteren, aber es ist immer sehr spannend den Char zu spielen,
allein die ganzen Combat-Skills zu beherrschen ohne sich die Finger
zu verbiegen macht einfach Spaß. <ggg> Allein der Fun an ner
eigenen Guard mit Sow vorbeizuhuschen und dann noch dem nebenbei
5pp zu klauen um dann zu zonen bringt einem doch das hämische
Grinsen, gelle ?<br>
</P>

