<P CLASS="P1">
<b>Barbarian:</b><br>
Können stunnen und als einzige "Throwing Boulders" benutzen.
Während des Slam-Stunnens ist Backstab (BS) nicht möglich,
desweiteren sind sie aufgrund Ihrer Größe schon von weitem
erkennbar. (Tip: Nehmt bei Reisen immer einen Staff in die Hand /
Shamanen werden seltener im PvP angegriffen von PKs wegen Sow etc.)
Können im Dunkeln nix sehen.<br>
<br>
<b>Dark Elf:</b><br>
Bis auf die Stamina gute Stats und Faction in der eigenen Heimstadt
sowie Quests sind in Ordnung, aber wer will schon bööööse sein ;-)
Haben Ultravision = Beste Nachtsicht<br>
<br>
<b>Dwarfs:</b><br>
Mein heimlicher Favorit. Haben mit die besten Stats und
Infravision. Desweiteren gibt es gute Rogue u.ä. Quests in
Khaladim. Standardzusatz skill ist Sense Heading bei 50.<br>
<br>
<b>Gnome:</b><br>
Gute Stats in Dex und Agi aber ein wenig schwachbrüstig.
Infravision und Tinkering (Skill 50) vorhanden. Heimatstadt ist auf den ersten
Blick etwas konfus, aber dafür sehr nett anzuschauen.<br>
<br>
<b>Half-Elf:</b><br>
Nennen sich selber Scouts anstatts Rogues. Stats sind aber ok (Bis
auf Stamina)und haben Infravision. Als Heimatstadt besser Kelethin
nehmen, da dort die Rogue Quests sind.<br>
<br>
<b>Halfling:</b><br>
Sind die in Rivervale beheimateten Großfüße. Stats sind auch
hervorragend, man sollte nur schnell Gegenstände mit +Strength sich
dann holen. Haben Infravision, eine nette Heimatstadt, keine
richtig lohnenswerten (ggg) Rogue Quests und mit den besten Lowbie
(bis lvl 11) Hunting Ground meiner Meinung nach. Außerdem gibts
dort immer viele Druiden die buffen können wenn man sie anlächelt.
(Nur zum Schein versteht sich!) Außerdem haben Halflings ein
kleinen Exp-Vorteil, sprich sie lvln schneller.<br>
<br>
<b>Humans:</b><br>
Stats sind eher mäßig / Nachtblind sind sie auch. Sie haben
lediglich 2 Vorteile: Sie lvln am schnellsten von allen Rogue
Klassen und sie werden oft mit Barden verwechselt und daher schon
mal ohne Aufforderung in Gruppen eingeladen.<br>
<br>
<b>Woodelfs:</b><br>
Wer brauch schon Woodelfs, wenn man Half-Elfs spielen kann! Nehmt
dann lieber diese! Haben nahezu die gleichen Möglichkeiten aber HE
haben bessere Stats.
</P>

