<UL class="P0">
	<LI>
		Von Anfang an immer ALLE Melee 1 handed Skills (Bis auf Punch
    vielleicht) trainieren. Nachher findet man eine tolle Waffe in
    späteren lvls und kann diese dann nicht benutzen, wegen dem
		schlechten Skill. Wäre doch schade oder? 
	<BR><BR></LI>
	<LI>
		Manche Skills kann man auch so trainieren: Sneak und Hide beim
    resten ist am einfachsten. Pickpocking kann man auch während
    des Kampfes immer trainieren. Attack off/Pickpocket/Attack on
    z.B.
	<BR><BR></LI>
	<LI>
		Entscheidet euch direkt am Anfang ob Ihr lieber Throw oder Bow
    trainiert. Als "echter" Rogue hab ich Throw genommen, da man
    damit ab Lvl 12 critical oder deadly Strikes damit machen kann.
    (macht aber bis auf Throwing Boulders (max.dmg 350 bei deadly
    in einem! Wurf) weniger schaden und schlechtere Reichweite in
    der Regel)
	<BR><BR></LI>
	<LI>
		Rogues haben die Möglichkeit des "Stealth Pullings". Ist aber
    nicht ganz einfach. Funktioniert wahrscheinlich auch eher mit
    Throw als mit Bow. Ihr seht 2 oder mehr NPCs nahe beieinander
    stehen, wollt aber nur einen pullen, Ihr nehmt die Maximum
    Range eurer Range Weapon -je weiter destso besser- und benutzt
    diese und fast gleichzeitig den Hide skill direkt danach. Wenn
    Ihr Glück habt kommt dann nur der NPC der euch aufgrund eures
    Angriffs sehen kann obwohl Ihr invis seid ;-)
	<BR><BR></LI>
	<LI>
		Mit Sneak und Hide könnt ihr an "jedem" Händler oder Rogue
    Trainer euer Geld loswerden. Allerdings NUR dann den NPC von
    hinten anklicken zum Traden. Im Regelfall seid Ihr durch Sneak
    und Hide bei allen in Norrath indifferent, es sei denn ihr habt
    zu viele PKs auf eurem Konto. Ach ja, kleiner Hinweis noch: Bei
    Castern die laut Ihrem Zauberbuch See Invis casten könnten,
    klappt dieser Trick nicht!
	<BR><BR></LI>
