<P CLASS="P1">
<b>Level 1</b><br>
<br>
Ohne Kommentar / die meisten skills steigen mit 1 Punkt von
alleine.<br>
<br>
<b>Level 3</b><br>
<br>
Hide  1 Punkt steigt bei Praxis s.o. sehr schnell.<br>
<br>
<b>Level 4</b><br>
<br>
Dodge  1 Punkt reicht auch hier. Steigt wenn Ihr angegriffen
werdet ebenfalls sehr schnell.<br>
<br>
<b>Level 6</b><br>
<br>
Evade  Ruhig 4 Punkte investieren. Mit diesem Skill kann man in
der Gruppe beim Kampf dafür sorgen, dass der NPC jemand anders
angreift. Man benutzt diesen indem man Attack off/Hide macht.
Sozusagen der Anti-Taunt. Dieser "Skill" steigt nicht in seinen
Werten.<br>
<br>
Pick Lock  Schlösser knacken. Erst dann einen Punkt darin
investieren, wenn man diesen Skill wirklich braucht. Dann wird der
momentane Lvl noch als SkillPoints draufgezählt. (Gibt sehr wenige
Schlösser zu knacken)<br>
<br>
<b>Level 7</b><br>
<br>
Pickpocket  5 Skillpoints Damit kann man bei NPCs Taschen
ausräumen.<br>
<br>
(Tip Guards erst ab nem Skill level von 150 vorher wird man zu
häufig deswegen plattgemacht)<br>
<br>
<b>Level 8</b><br>
<br>
Sense Traps  1 Skillpoint, gibt nur SEHR wenige Traps in Norrath
:-(<br>
<br>
<b>Level 10</b><br>
<br>
Backstab  Juchhu! Nur mit Piercing Waffe!!! Alle verfügbaren
Skillpoints. Skill steigt nur langsam, macht aber viel Spaß! Alle 8
sec. Chance nen NPC von hinten mit sehr viel mehr Damage zu
treffen.<br>
<br>
<b>Level 12</b><br>
<br>
Parry  1 Skillpoint, Skill steigt von alleine sehr schnell.<br>
<br>
Safe Fall  4 Skillpoints. Bewirkt das man bei nem Freien Fall aus
Kelethin nur wenig Dmg nimmt. (Max bei Rogues liegt 94er Skill)<br>
<br>
<b>Level 13</b><br>
<br>
Dual Wield  Viele Punkte und in die 2 Hand ne low Delay Waffe
geben, damit der Skill vor allem am Anfang schnell steigt.<br>
<br>
<b>Level 16</b><br>
<br>
Double Attack  Viele Punkte investieren.<br>
<br>
<b>Level 18 + Level 20</b><br>
<br>
Apply Poison und Make Poison  Am besten bis Lvl 30 warten und dann
erst Skill trainieren, dann wird wieder Lvl draufgerechnet. Skill
kostet viele Platinum daher erstmal abwarten bis Lvl 30.<br>
<br>
<b>Level 21</b><br>
<br>
Disarm Traps  Siehe Sense Traps! Das gleiche in grün.<br>
<br>
<b>Level 22</b><br>
<br>
Instill Doubt  Fear-Spell als Skill funktioniert erst gut ab Skill
100, also immer fleißig trainieren im Kampf. 3 Skillpoints reichen
aber aus. Funktioniert leider nicht in PvP.<br>
<br>
<b>Level 27</b><br>
<br>
Disarm  5 Skillpoints oder mehr. Entwaffnet PCs und NPCs / Statt
KS auch nett um an das Item zu kommen ;-) NPCs droppen die Waffe
meistens während bei PCs die Waffe im Rucksack verschwindet oder
als Cursor erscheint (Wenn Rucksack voll ist)<br>
<br>
<b>Level 30</b><br>
<br>
Riposte  3 Skillpoints reichen, steigt schnell. Gibt den Schaden
des Angreifers auf Ihn selber wieder zurück.<br>
</P>

