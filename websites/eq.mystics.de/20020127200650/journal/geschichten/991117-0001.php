<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Yackuna van Kryn.<BR>
	<BR>
</P>

<P CLASS="P1">
	Es dämmerte in den weiten Ebenen von Karana, die letzten Sonnenstrahlen verblassten langsam, 
	die Schatten verdichteten sich, es wurde still.
	Meine Gruppe hatte sich bereits aufgelöst und so begann ich, ein stolzer Menschenpaladin namens 
	Arkanus, meinen Heimweg in die Wunderschöne Stadt Qeynos anzutreten.
</P>

<P CLASS="P1">
	Dummerweise war es doch schon zu dunkel... ich habe wohl bei dem wunderschönen Anblick der 
	untergehenden Sonne die Zeit vergessen und konnte den Heimweg nicht mehr finden. So beschloss 
	ich mich unter einen Baum zu setzten um die Nacht hier zu verbringen.<br>
	Ich suchte eine Weile bis ich an ein kleines Feld kam. Von weiten waren ein Paar tanzende 
	Lichter zu sehen, die mich aber nicht weiter stören sollten.
</P>

<P CLASS="P1">
	Nun... so setzte ich mich unter das Blätterdach eines Baumes. Ich wollte mich gerade anlehnen als 
	ich abrupt zu Boden viel. Es schien, als sei der Baum aus Luft, körperlos, ja eine Illusion.<br><br>
	Erschrocken wich ich zurück.<br><br>
	Der Baum begann sich zu drehen. Ich beäugte ihn misstrauisch... er schien sich immer in meine 
	Richtung zu drehen... 
</P>

<P CLASS="P1">
	Ich habe ja wohl schon viele komische Dinge gesehen, doch das war mir bis jetzt nie unter 
	gekommen.<br>
	Ich kante wohl die friedfertigen lebenden Bäume, die Treants. Doch sie waren mir irgendwie 
	größer in Erinnerung.
</P> 

<P CLASS="P1">
	Da geschah es. Der so friedfertig dreinschauende Baum wurde langsam durchsichtig.<br>
	Rot glühende Augen blickten mich aus seinem Inneren an. Die Umrisse des Baumes waren 
	schon nicht mehr zu sehen als ein Dunkelelf aus seiner Mitte trat.<br>
	Seine Hände begannen blau zu schimmern. Wie hypnotisiert stand ich da, konnte keinen 
	Finger mehr bewegen als er sagte, "<i>Na... was haben wir den da?</i>"
</P>

<P CLASS="P1">
	Er begann eine seltsame Zauberformel aufzusagen. Es schien, als verdichtete sich die Materie um 
	ihn.<br><br>
	Plötzlich wie von Geisterhand erschien ein Schild und ein Schwert.<br><br>
	Ich konzertierte mich auf mein Inneres, brach die Hypnose und begann zu rennen als sich auch 
	schon das lebende Schwert auf mich stürzte.<br><br>
	Eine klaffende Wunde riss es in meinen Rücken. Die Erde unter mir öffnete sich und umschloss 
	meine Füsse. Ich erinnerte mich just in diesem Augenblick an die Worte meines Vaters, 
	"<i>Der wahre Weg eines Paladins ist der Kampf, nicht der Rückzug.</i>"<br><br>
	So nahm ich all meinen Willen zusammen, drehte mich, um diesem Werk des Teufels Paroli zu bieten. 
	Mein Schwert surrte durch die Luft, doch schon war das Schild zur Stelle um den Schlag 
	abzufangen.
</P>

<P CLASS="P1">
	Ich tat alles Menschenmögliche, um den Schwerthieben entgegen zu setzten, doch es gelang einfach 
	nicht.<br>
	Blutübersudelt und schon auf Knien meinem Ende entgegen sehend hob ich fragend meine Hände gen 
	Himmel.<br>
	"Wieso ich Tunare, wieso ich... ?" Als auf einmal heftige Winde aus meinen Fingern 
	traten.<br>
	Wie kalte Küsse umwoben sie meinen Körper. Meine Wunden schlossen sich langsam, meine Stärke 
	kehrte zurück. Nach einem kurzen Augenblick ahnungsloser Benommenheit stand ich wieder, um dem 
	Treiben ein Ende zu setzen. 
</P>

<P CLASS="P1">
	Doch was war passiert. Das Lebende Schwert war weg, auch das Schild war nirgends auszumachen.<br>
	Ja... sogar der Dunkelelf mit seinen rotglühenden Augen war spurlos verschwunden.<br><br>
	Was war nur passiert, hat Tunare ihn verbannt?<br>
	Hat Inuruuk ihn zurück in seine Hallen geschickt, oder war alles nur ein Traum?<br>
	Ich weiß es nicht.<br><br>
	Ich umgriff mein Schwert fester und begann mit dem Heimweg.
</P>

<P CLASS="P1">
	Schon am Eingang zu den Hügeln von Qeynos angekommen, drehte ich mich ein letztes mal um.<br>
	Es ward mir als vernahm ich das blutige Lachen Inuruuks.<br><br>
	Ich begann zu rennen, so schnell mich meine Beine tragen konnten.
	In den Hills angekommen setzte ich mich an ein Feuer.<br>
	Die freundlichen Wachen brachten mir einen Krug Wasser um mich zu stärken.
	Freundlich dankend trank ich ihn aus und schlief ein.
</P>

<P CLASS="P0">
	<I>Yackuna van Kryn</I>
</P>

