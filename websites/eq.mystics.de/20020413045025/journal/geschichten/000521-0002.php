<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Clawdeen<BR>
	<BR>
</P>

<P CLASS="P1">
	Sulamon vergaß ihre Tochter Clawdeen nie und hörte auch niemals auf, sich zu
	fragen, was wohl aus ihr geworden sei.
	Doch so weh Sulamon auch die Trennung von ihrer Erstgeborenen tat, so sicher
	war sie auch, eine richtige Entscheidung getroffen zu haben, als sie Clawdeen
	mit Gereus gehen ließ.
	Trotzdem ertrug sie den Anblick der so gewohnten Paneelen ihrer Heimatstadt
	nicht mehr, so dass sie nach Felwithe zog, wo sie als Schneiderin arbeitete.
	Das Leben ging weiter, auch für Sulamon, und so traf sie eines Tages Clawdeens
	Vater wieder. Er war ausgezogen, um die Crushbone Orks zu besiegen und hatte
	nun seinen Teil beigetragen. Die alte Liebe entflammte aufs Neue, und doch lag
	ein Schatten auf ihrem Dasein, da Clawdeens Vater im Vergleich zu Sulamon wenig
	Verständnis für die magische Ausbildung Clawdeens aufbringen konnte.
	Die Zeit heilt alle Wunden, so heißt es, und so wurde Sulamon tatsächlich eines
	Tages wieder schwanger. Wieder gebar sie ein Mädchen, wieder nachts, doch
	diesmal hatte sie kein Ritual oder dergleichen zu befürchten, denn in Felwithe
	kennt man diese Tradition nicht.
	So wuchs Naena in Ruhe und Frieden auf.
</P>

<P CLASS="P1">
	Sie war ein sehr aufgewecktes Mädchen, das es zu zügeln galt. Ihr Temperament
	wahr gar unwahrscheinlich, und ihr Mundwerk stand dem in nichts nach.
	Während Sulamon sich häufig Sorgen um die unbeschwerte Art Naenas machte, ließ
	ihr Vater ihr alles durchgehen.
	Jarret liebte seine Tochter und gab ihr all die Liebe, die eigentlich für zwei
	Töchter bestimmt gewesen war. Lange Zeit konnte Naena nicht seine Anflüge an
	Melancholie und Depression verstehen.
	Umso älter Naena jedoch wurde, umso differenzierter nahm sie ihr Umfeld wahr.
</P>

<P CLASS="P1">
	Felwithe war ein sehr reiches Gebiet, doch all der Reichtum dieser Stadt konnte
	ihr nicht die klare Wahrnehmung rauben.
	Sie erlebte, dass ihre Mutter gute Aufträge bekam, doch auch, dass man sie
	oft von oben herab behandelte, und das mochte sie nun ganz und gar nicht.
	Irgendwann dann erfuhr sie, dass dies nicht ihre eigentliche Heimat sei, sondern
	Kelethin. Sie bat und bettelte, bis Jarret ihr versprach, mit ihr dorthin zu
	gehen. Ja, er würde ihr ihre wirkliche Heimat zeigen, und darauf freute sie sich sehr.
	Warum ihre Mutter nicht mitreisen wollte, verstand sie nicht. Zwar hatte Sulamon
	sich mit Bergen von Arbeit herausgeredet, doch ganz glauben konnte Naena ihr nicht.
	So brachen Jarret und Naena dann enes Tages auf und wanderten nach Kelethin. 
</P>

<P CLASS="P1">
	Stolz zeigte ihr Vater ihr die ganze Stadt und erklärte ihr, wen und was das
	Zeug hielt. An der Türe der Soldaten Tunares jedoch ging er ohne ein Wort
	vorüber, und die Melancholie, die Naena immer so traurig machte, erschien
	wieder einmal glänzend in seinen Augen.<br>
	<I>&quot;Vater, was ist das hier für ein Gebäude ?&quot;</I> fragte sie ihn.<br>
	<I>&quot;Die Soldaten Tunares hausen hier.&quot;</I> erwiderte er dumpf.<br>
	<I>&quot;Wer sind sie ?&quot;</I> ließ Naena sich nicht beirren.<br>
	<I>&quot;Das sind die Baumfetischisten unseres Volkes, die mir deine Schwester
	raubten.&quot;</I> sprach Jarret mit düsterem Gesicht.<br>
	<I>&quot;Was?&quot;</I> Naena blick vor Schreck stehen.
	<I>&quot;Meine Schwester? Ich ... ich habe eine Schwester?&quot;</I> flüsterte
	sie ehrlich entsetzt.<br>
	Jarret seufzte, dann erzählte er ihr die Geschichte Clawdeens, so wie sie ihm
	einst von Sulamon erzählt worden war. Als er die Geschichte beendet hatte, blickte
	Naena ihn erstaunt an.<br>
	<I>&quot;Hast du sie gesucht, Vater?&quot;</I><br>
	<I>&quot;Gesucht? Nein, wo schon in den Weiten von Norrath sollte ich sie
	suchen, geschweige denn finden, Naena?&quot;</I> Jarret schüttelte den Kopf.
	<I>&quot;Nein, Naena, das wäre ein durchweg sinnloses Unterfangen.&quot;</I><br>
	Naena wurde plötzlich überaus wütend.<br>
	<I>&quot;Wie kannst du das sagen!!! Nie hast du es auch nur versucht! Ihr habt
	sie einfach weggeschickt, meine Schwester. Und ich? Wann werft ihr mich den
	Orks zum Fraße?&quot;</I>
</P>

<P CLASS="P1">
	Jarret versuchte, Naena zu beschwichtigen, doch sie riss sich los und rannte davon.
	Sie lief und lief, soweit ihre Füße sie tragen mochten, dann sank sie erschöpft
	ins Gras und schlief sogleich ein.
	Als Naena am nächsten Morgen erwachte, machte sie sich auf den Rückweg nach
	Kelethin. Es war ein langer und beschwerlicher Marsch, denn sie hatte sich
	verlaufen. Zum Glück traf sie auf einen gut gerüsteten Paladin, der sie nach
	Kelethin begleitete.
	Sie ging zu den Soldaten Tunares und frug nach Clawdeen, doch man erteilte ihr
	keine Auskunft. Die ganze Stadt hatte sich gegen sie verschworen, so wie es ihr
	schien, denn von niemandem erfuhr sie auch nur das Geringste über Clawdeen.
	Letztlich traf sie auf Geeda, der sie ebenfalls ihre Geschichte erzählte, doch
	auch sie schien nie von Clawdeen gehört zu haben.
	<I>&quot;Mädchen, es wird bald Nacht. Ruhe heute in unserem Gildenhaus, morgen
	dann werden wir bereden, was wir weiter für dich tun können.&quot;</I> lächelte sie Naena an.
	Und so blieb Naena bei den Scouts, und das nicht nur für diese Nacht. Sie
	beschloss, Kampferfahrung zu sammeln, um dann durch die Weiten des Landes zu
	reisen und ihre Schwester eines Tages zu finden ... wenn sie denn noch lebte.
	Die Scouts waren über den Neuzugang sehr erfreut, und so bildete man sie aus.
	Nach ihrer Grundausbildung bei der Gilde trat Naena zunächst ihre eigenen Wege an.
</P>

<P CLASS="P1">
	Ihre Suche begann in Crushbone. Nicht nur nach ihrer Schwester wollte sie dort
	suchen, sondern sie wollte dort auch ihre Erinnerungen an ihren Vater
	auffrischen und bewahren.
	Sie war gerade nach Crushbone gekommen, als sich ein Mann vor ihr aufbaute.<br>
	<I>&quot;Im Namen der Götter, Mädchen, was tust du hier ? Es ist hier viel zu
	gefährlich für so ein junges Ding wie dich! Aber ... ich kenne dich doch?&quot;</I><br>
	Es war der Paladin, der sie vor Monaten nach Kelethin geleitet hatte. Er hiess
	Caman und Naena kam mit ihm ins Gespräch.
	Sie erzählte ihm nun ihre ganze Geschichte und auch von ihrem Vorhaben.
	Caman runzelte die Stirn.
	Naena glaubte, es sei das Unverständnis über ihre Pläne, die sich in Camans
	Falten abzeichnete, doch war es in Wahrheit sein innerer Zwiespalt, der ihn
	die Stirn runzeln ließ.
	Naena konnte ja nicht ahnen, dass Clawdeen, ihre Schwester, mittlerweile eine
	Gildenschwester Camans geworden war!
	Caman hielt Rücksprache mit der Gilde und man beschloss in Clawdeens Abwesenheit
	einstimmig, dass ihre Ausbildung keinesfalls gefährdet werden dürfe.
	Clawdeen dürfe noch keinen Kontakt zu ihrer Familie bekommen, denn das würde ihre
	weitere Ausbildung zur Druidin behindern. 
	Und doch war man sich auch einig darüber, dass man das junge Ding, dass nun
	verzweifelt seine Schwester suchte, nicht sich selbst überlassen durfte.
	Und so lernte Naena schliesslich die Dämonenfürstin kennen, die sie in die Gilde
	der Dunklen Schlangen aufnahm und zusammen mit Caman und Escado bemüht war, ihre
	ersten Schritte in die große Welt Norrath zu lenken .........
</P>

<p class="P0" ALIGN="CENTER"><I>Clawdeen</I></p>

