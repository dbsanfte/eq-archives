<P CLASS="P1">
	Vollkommenheit. Die Vollkomenheit des Körpers und des Geistes. Das ist es was ein Mönch anstrebt. Er benötigt
	weder Waffen noch Rüstung, nein beides ist ihm eher hinderlich. Sie sind die Meister des waffenlosen Kampfes.
	Ein Schwert nützt ihm sehr selten, wenn ihr einmal einen Mönch mit einer Drachenklaue, einer ihrer vielen
	mysteriösen Angriffe, habt zuschlagen sehen, wisst ihr was ich meine. Doch nicht nur der Kampf ist es, der
	ihn interessiert. Die Vervollkommnung des Geistes ist ebenso wichtig. Nur ein Geist, der mit sich und
	seinem Körper im Einklang ist, kann die Beschränkungen des letzteren hinter sich lassen, und Dinge
	vollbringen, die die meisten Menschen nicht zu glauben wagen.
</P>	

