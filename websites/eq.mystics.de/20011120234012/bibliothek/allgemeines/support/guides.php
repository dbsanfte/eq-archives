<P CLASS="P2">
	Was versteht man unter "Guide Programm" ?
</P>	
<P CLASS="P1">
	Ozymandius hat das Guide Programm während der Beta Phase erschaffen. Es besteht aus Freiwilligen, die mehr als normalerweise üblich zum Spiel beitragen wollen. Seit der Entstehung ist das Guide Programm extrem gewachsen und arbeitet eng mit Verant Interactive zusammen, damit allen Spielern eine Gruppe engagierter Personen zur Verfügung steht, die nichts anderes tun als zu helfen und ihre Erfahrung weitergeben. Heute besteht das Guide Programm aus Hunderten Freiwilliger, 1-3 Senior Guides pro Server, jeder einzelne verantwortlich für alle Guide Aktionen auf dem Server, und ein Team von "Elder Guides" welche für alle Server verantwortlich sind.
</P>

<P CLASS="P2">
	Wie wird man Guide ?
</P>	
<P CLASS="P1">
	Alle Interessenten, die mindestens 10 Stunden pro Woche für das Guide Programm aufbringen können, haben die Möglichkeit sich unter der Adresse http://guideapp.everquest.com zu bewerben.
</P>

<P CLASS="P2">
	Was kann ein Guide alles tun ?
</P>	
<P CLASS="P1">
	Guides in EverQuest haben weitreichende Möglichkeiten um Spieler so gut wie möglich zu unterstützen. Die am öftesten benötigten Aktionen sind:
</P>
<UL CLASS="P1">
	<LI>
		Wiederbelebung (Corpse recovery) wenn eine Spielfigur in Folge eines nachvollziehbaren Bugs gestorben ist.
	<BR></LI>
	<LI>
		Informationen einholen um bei Corpse-Verlust die Spielfigur wieder auszustatten.
	<BR></LI>
	<LI>
		Rettung auf See bei Problemen (Bug) mit den Booten.
	<BR></LI>
	<LI>
		Unterstützung um Spieler vor ungewollten Belästigungen zu befreien.
	<BR></LI>
	<LI>
		Unterstützung bei der Berichterstellung von gefundenen Bugs im Spiel.
	<BR></LI>
	<LI>
		Unterstützung bei der Weitergabe von Informationen über neue Spielfunktionen oder Änderung an bereits bestehenden Funktionen.
	<BR></LI>
	<LI>
		Berichterstattung und Dokumentation von Ausnutzung bzw. Missbrauch und Aussprechen von notwendigen Verwarnungen um EverQuest frei von Cheatern halten.
	<BR></LI>
	<LI>
		Hilfe beim Finden von Corpses wenn ein Spieler die Leiche seines Chars nicht sehen kann.
	<BR></LI>
	<LI>
		Befreiung von Spielfiguren, wenn diese irgendwo feststecken.
	<BR></LI>
	<LI>
		Koordinierung und Weitergabe von aktuellen Problemen mit höchster Priorität bzw. sehr oft auftretenden Problemen in Form einer Liste für Verant.
	<BR></LI>
	<LI>
		Beantwortung von immer wieder auftretenden technischen Fragen und Behebung von technischen Problemen.
	<BR></LI>
	<LI>
		Erstellung und Ausfürhung von dynamischen Quests im Spiel.
	<BR></LI>
</UL>

<P CLASS="P2">
	Was kann/darf ein Guide nicht ?
</P>	
<P CLASS="P1">
	Guides befolgen eine ausführliche Vorschrift in welcher genau festgehalten ist was sie dürfen und was nicht. Diese Regeln wurden unter Berücksichtigung aller möglichen Anfragen erstellt, danach wurden jene wieder entfernt, die Übervorteilungen und Bevorzugungen ermöglicht hätten. Es ist das Ziel des Guide Programmes einem Spieler in jeder möglichen Situation zu helfen solange:
</P>
<UL CLASS="P1">
	<LI>
		dies keine ungerechtfertigte Hilfestellung/Informationsbeschaffung, wie genaue Richtungsangaben, Quest Informationen, Informationen über Monster, Ausrüstung und Waffen darstellt. Das Wissen über diese Dinge soll von jedem selbst erarbeitet werden und ist Teil des Mysteriums des Spieles. Es ist Teil des Spiels zu forschen und die Wunder der Welt von Norrath kennenzulernen.
	<BR></LI>
	<LI>
		es keine ungerechte Situation hervorruft. Ein Beispiel wäre den PvP-Schalter einer Spielfigur auf non-PvP zu stellen. Ein Spieler könnte im PvP-Modus etwas angestellt haben und wenn er auf non-PvP zurückgestellt werden würde, könnte er so etwaiger Rache entgehen. Guides haben keine Chance die äußeren Umstände zu erfahren und dürfen deshalb den PvP-Schalter nicht ändern. Ein weiteres Beispiel wäre das Wiederbeleben wenn nicht ein nachvollziehbarer Bug am Tod schuld war. Wenn ein Spieler bekanntgibt, daß er Linkdead (LD) gegangen und gestorben ist , gibt es keinen Weg dies nachzuprüfen und somit gibt es auch keine Wiederbelebung. Spieler ziehen einfach den Stecker in brenzligen Situationen und bitten dann um Hilfe, was völlig ungerechtfertigt wäre.
		<BR></LI>
	<LI>
		niemand ungerechte Vorteile daraus zieht. Zum Beispiel werden Guides niemanden an einem bestimmten Ort binden, außer der aktuelle Bindepunkt des Spieler ist buggy. Es kann schon allein wegen Kapazitäts-/Zeitgründen nicht jeder Spieler von Guides gebunden werden und würde somit unfair sein, wenn nur einige wenige diesen Service erhalten. Außerdem würde es jene Spieler benachteiligen, die das Binden als Service gegen Gebühr anbieten.
	<BR></LI>
</UL>
<P CLASS="P2">
	Wie kontaktiert man einen Guide ?
</P>	
<P CLASS="P1">
	Mit dem Befehl <I>/who gm all</I> sieht man welche Guides gerade online sind.<br>
	<br>
	GMs und Guides müssen mit dem <I>/petition</I> Befehl kontaktiert werden. Guides sind instruiert <b>nicht</b> auf <I>/tell, /ooc</I> oder <I>/shout</I> zu antworten. Das dient einfach allein dazu, daß die Anfragen der Reihe nach bearbeitet werden können. Anfragen (Petitions) werden in der Reihenfolge bearbeitet in der sie eingetroffen sind.<br>
	<br>
	Die Anfrage-Warteschlange (Petition queue) wurde eingerichtet um einen direkten Weg zur Kontakaufnahme mit Guides und GMs zu schaffen. Sie dient zum Sammeln von Informationen und kann von allen GMs und Guides eingesehen werden um offene Fragen, Beanstandungen, Bugs oder Feedback zu bearbeiten. Es ist die Pflicht der GMs u. Guides <b>jede</b> Anfrage zu beantworten, egal wie trivial sie auch sein mag, und sei es auch nur die Antwort: <I>"Es tut mir leid, aber es ist uns nicht möglich hier zu helfen."</I> Alle Kunden sind wichtig und es wird jedem zugehört. Als Spieler sollte dies helfen die Funktionsweise der Warteschlange zu verstehen und wie man damit am besten Fragen, Kommentare und Probleme behandelt.
</P>
<BR>

