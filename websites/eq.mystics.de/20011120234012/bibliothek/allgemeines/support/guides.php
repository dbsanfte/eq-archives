<P CLASS="P1">
	Das Guide Programm versucht das Spiel frei von Bugs, Übervorteilungen, Mißbrauch und Belästigungen zu halten. Mit einer derart großen Anzahl von Spielern treten ab und zu solche Probleme auf und die Guides schreiten wenn nötig ein. Die folgenden Informationen dienen dazu, dem Spieler nahezubringen wie er mit vorhandenen Mitteln die meisten dieser Situationen meistern kann, und sollen dem Spieler helfen was zu tun ist wenn er mit den vorhandenen Mitteln nicht auskommt.
</P>

<P CLASS="P2">
	Was sind Namensregeln (naming policy) ?
</P>	
<P CLASS="P1">
	Die Namen der Spielfiguren sollen das Spielgenre wiederspiegeln. Fantasynamen sind erwünscht. Die folgenden Arten der Namensgebung werden nicht akzeptiert:
</P>
<OL CLASS="P1">
	<LI>
		Scheußliche, unanständige und obszöne Namen sowie bewußt falsch geschriebene Abwandlungen solcher Wörter.
	<BR></LI>
	<LI>
		Eine Kombination von Wörtern die ein offensives Ergebnis darstellen (z.B.: Hugeaz = Riesenar.....)
	<BR></LI>
	<LI>
		Religiöse Namen die von anderen als offensiv gesehen werden (Jesus, Allah).
	<BR></LI>
	<LI>
		Keine Fantasynamen von populären Personen/Figuren (Rambo, Darthvader).
	<BR></LI>
	<LI>
		Allgemeine Worte und Phrasen die nicht in die Umgebung passen (Phaser).
	<BR></LI>
	<LI>
		Eigennamen aus EverQuest (Rathe, Karana).
	<BR></LI>
	<LI>
		Populäre und leicht zu erkennende Namen aus bestehender Literatur (Merlin, Gandalf).
	<BR></LI>
	<LI>
		Namen, die Titel beinhalten (Ladygray, Sirtallon).
	<BR></LI>
</OL>

<P CLASS="P2">
	Was ist ein "Exploit" (=Ausbeutung) ?
</P>	
<P CLASS="P1">
	Ausbeutung ist definiert als Ausnutzung von Schwächen und Ungereimtheiten im Spielsystem.  Ausbeutung ermöglicht Vorteile für einen oder mehrere Spieler und diese Spieler nutzen dies in irgendeiner Form bewußt aus.<br>
	<br>
	Beispiele:
</P>
<UL CLASS="P1">
	<LI>
		<b>Duping</b> (duplizieren) - Erzeugung von Geld oder Items aus dem nichts oder durch Ungereimtheiten im Spiel, oder der Besitz von solchem Geld oder Items.
	<BR></LI>
	<LI>
		<b>Farming</b> - Verwenden von falsch funktionierenden Zaubersprüchen oder speziellen Zaubereffekten um Monster zu killen oder anzulocken, um Experience/Items von diesen zu bekommen.
	<BR></LI>
	<LI>
		<b>Safe Zones</b> (sichere Plätze) - Ausnutzung von Plätzen in denen, auf Grund von falschen Daten, ohne Gefahr Monster gekillt werden können, weil diese nicht richtig agieren (Laufen am Platz, zielloses Herumlaufen, stillstehend oder irgendein anderer Grund der verhinder, daß sich die Monster normal verhalten/verteidigen).
	<BR></LI>
	<LI>
		<b>Price Gouging</b> (Preistreiberei) - Items mit ungewöhnlichem Preis finden ausnutzen, sowie Items zu einem höheren Preis verkaufen als sie kosten.
	<BR></LI>
	<LI>
		<b>PvP Switch Avoidance</b> (PvP Schalder umgehen) - Methoden im Spiel anwenden, die es den PvP-Schalter zu umgehen und es non-PvP Spielern ermöglichen andere Spielfiguren zu killen, wie z.B. das Blockieren von Räumen, Abladen von Monstern oder Zaubereffekte die diese Grenze auf Grund eine Bugs überschreiten.
	<BR></LI>
</UL>

<P CLASS="P2">
	Was wird als Mißbrauch (abuse) angesehen ?
</P>	
<P CLASS="P1">
	Mißbrauch ist definiert als eine Aktivität die mit dem Vorsatz ausgeführt wird, einem od. mehreren Spielern den Spielspaß zu verderben.<br>
	<br>
	Beispiele:
</P>
<UL CLASS="P1">
	<LI>
		<b>Hate Mongering</b> (Haß verbreiten) - Teilnahme oder Verbreitung von Haßliteratur oder Propaganda mit Bezug auf die wirkliche Welt.
	<BR></LI>
	<LI>
		<b>Sexual Harassment</b> (sexuelle Belästigung)
	<BR></LI>
	<LI>
		<b>Attempting to Defraud a Customer Service Representative</b> (Betrugsversuch) - Anfragen (petition) mit falschen Informationen mit dem Vorsatz ungerechte Vorteile zu erzielen. Das beinhaltet das unberechtigte Melden eines Bug-Todes, Experience- oder Itemverlust, oder Anklage über falsches Verhalten anderer Spieler wenn es gar nicht stimmt.
	<BR></LI>
	<LI>
		<b>Using Threats of Retribution by GM Friends</b> (androhen von Vergeltung durch GM-Freunde) - der Versuch andere Spieler einzuschüchtern, durch die Aussage, daß eine Gruppe seitens Verant od. den Guides bevorzugt behandelt wird.
	<BR></LI>
</UL>

<P CLASS="P2">
	Was wird als Störung (Disruption) bezeichnet ?
</P>	
<P CLASS="P1">
	Störungen sind Aktivitäten die anderen den Spielspaß verderben, obwohl dies nicht unbedingt absichtlich geschehen muß. Es gibt Haupt- und Nebenstörungen.<br>
	<br>
	Beispiele für Nebenstörungen:
</P>
<UL CLASS="P1">
	<LI>
		<b>Keine Fantasy-/Offensiv-Namen</b> - Namen die nicht in das Fantasy Genre von EverQuest passen (siehe Naming Policy).
	<BR></LI>
	<LI>
		<b>Excessive Spam</b> - Wiederholter Mißbrauch von <I>/ooc, /shout</I> od. </>/auction</I> über längere Zeit, sodaß sich bereits viele Spieler beschweren.
	<BR></LI>
</UL>
<P CLASS="P1">
	Beispiele für Hauptstörungen:
</P>
<UL CLASS="P1">
	<LI>
		<b>Foul Language</b> - Exzessive Verwendung von widerlicher, schmutziger Sprache in unangebrachtem Zusammenhang, Verwendung von Schwüren, rassistischen Äußerungen und andere Wörter/Aussagen, die nicht in die Fantasyumgebung passen.
	<BR></LI>
	<LI>
		<b>Stalking/Harassment</b> (Belästigung) - Verfolgen eines Spielers nachdem dieser darum gebeten hat dies zu unterlassen, wiederholtes Stehlen von Kills einer Person und generell unangebrachtes Verhalten gegenüber anderer Personen.
	<BR></LI>
</UL>

<P CLASS="P2">
	Wie gehen Guides gegen Ausbeutung und Mißbrauch vor ?
</P>	
<P CLASS="P1">
	Wenn ein Spieler der Ausbeutung, Mißbrauch oder Störung angeklagt wird, ist die Vorgehensweise:
</P>
<UL CLASS="P1">
	<LI>
		Guides sammeln die gesamten Informationen von <b>allen</b> betroffenen Gruppen. Sie notieren die Namen, Rassen, Level und Account Namen von beiden Parteien (Ankläger u. Beklagten), sowie die Zone und Situation.
	<BR></LI>
	<LI>
		Guides treten nur als Ermittler und Reporter in Fällen von Belästigung, Mißbrauch und Störung auf. Sie sammeln Daten, sprechen Warnungen aus und nehmen den Vorfall auf.
	<BR></LI>
	<LI>
		Verant bearbeitet solche Vorfälle täglich.
	<BR></LI>
	<LI>
		Verants GMs nehmen dann entsprechende Disziplinär-Maßnahmen vor. Dies kann, je nach SChwere des Vergehens, eine vorläufige Suspendierung oder permanente Verbannung sein. Disziplinar-Maßnahmen können auch Änderungen an den Charakterwerten, Ausrüstung, Gegenständen oder Experience beinhalten. Verant kann auch noch andere Maßnahmen ergreifen. Man muß nicht unbedingt vorher eine Warnung erhalten. Spieler die wiederholt Ungereimtheiten zu ihrem Vorteil ausnutzen oder den Spielspaß anderer Personen beeinträchtigen, werden in EverQuest nicht toleriert.
	<BR></LI>
</UL>

<P CLASS="P2">
	Was kann ich gegen Verbalattacken anderer Spieler unternehmen ?
</P>	
<P CLASS="P1">
	Die beste Möglichkeit ist es den anderen Spieler auf <I>/ignore "name"</I> setzen. Es ist das mächtigste Werkzeug eines Spielers in diesem Fall. Sollten die Belästigungen in einer anderen Art und Weise weitergehen (man wird von dieser Person weiterhin verfolgt und belästigt), sollte man den <I>/petition</I> Befehl verwenden.
</P>

<P CLASS="P2">
	Was versteht man unter bestätigtem Bug-Tod ?
</P>	
<P CLASS="P1">
	Guides haben bestimmte Werkzeuge zu ihrer Verfügung um Bug-Tode herauszufinden.<br>
	<br>
	Einige Beispiele für bestätigte Bug-Tode:
</P>
<UL CLASS="P1">
	<LI>
		Man fällt durch die Welt und nimmt massiven Schaden (die Leiche befindet sich 10000 Fuß unter der Oberfläche).
	<BR></LI>
	<LI>
		Man ist im Wasser gefangen, dieser Ort ist als fehlerhaft bekannt, man kann nicht entkommen und ertrinkt.
	<BR></LI>
	<LI>
		Man wird von einem Schiff zerquetscht.
	<BR></LI>
	<LI>
		Man fällt während des Zonens vom Schiff, ertrinkt und die Leiche ist am Grund des Meeres.
	<BR></LI>
</UL>
<P CLASS="P1">
	In all diesen Fällen kann der Guide nachprüfen wie die Spielfigur gestorben ist.
</P>

<P CLASS="P2">
	Dürfen Guides helfen, wenn man auf Grund eines bekannten/nachvollziehbaren Bugs gestorben ist ?
</P>	
<P CLASS="P1">
	Im Falle eines bekannten/nachvollziehbaren Bugs darf und wird der Guide bei der Auffindung und Wiederbelebung helfen. Sollte das Auffinden/Wiederbeleben nicht möglich sein wird der Guide alle nötigen Informationen sammeln und ein Verant GM wird die Spielfigur wiederherstellen, damit man nicht die ganze Ausrüstung neu besorgen muß.<br>
	<br>
	Damit soll der Verlust von Experience und andere Unannehmlichkeiten möglichst gering gehalten werden, aber es ist ein Entgegenkommen durch das Guide Programm. Der Versuch einen Guide mit falschen Aussagen zu betrügen wird als Mißbrauch gewertet und als solcher gemeldet.<br>
	<br>
	"Linkdeath" (LD) ist ein clientseitiges oder Netzwerk-Problem, daß von Anfang existiert und programmtechnisch nicht abgefangen werden kann. "Linkdeath" kann nie überprüft werden und kann von Spielern ausgenutzt werden um ungerechtfertigt Konsequenzen aus ihren Aktionen abzuwenden. Der Tod auf Grund von "Linkdeath" wird konsequent <b>nicht</b> als Tod durch einen Bug bewertet. Die einzige Ausnahme ist der Tod durch "Linkdeath" auf einem Boot und man ertrinkt im Meer.
</P>

