<P CLASS="P1">
	Wir Hobbits sind ein kleines Volk auf großen Füßen. Unsere Körpergröße von bis zu 
	1,50m machen wir durch unsere Flinkheit und Geschicklichkeit mehr als wett. Die großen 
	Leute laßen sich oft durch unseren rundlichen Körper täuschen, aber bevor sie Käsekuchen 
	sagen können sind wir ihnen schon entwischt und haben uns versteckt.
</P>

<P CLASS="P1">
	Aus unseren gutmütigen Gesichtern kann man unsere Liebe für gutes Essen und Trinken 
	ablesen, unsere List aber bleibt durch sie verborgen.
</P>

<P CLASS="P1">
	Keine andere Rasse hat die Kunst des Tabakrauchens so verfeinert wie wir. Nur selten 
	kann der ein oder andere Magier unsere Rauchringe überbieten.
</P>

<P CLASS="P1">
	Unsere Füße sind unter den Bewohnern unserer Welt einmalig. Sie sind mit dichtem, krausen 
	Haar bewachsen und haben dicke ledrige Sohlen. Schuhwerk erachten wir daher als völlig 
	überflüssig und würden es nur widerwillig anziehen.
</P>

<P CLASS="P1">
	Unsere Heimat Rivervale liegt auf dem Kontinent Antonica. Als Hobbits neigen wir 
	dazu unsere Wohnungen als Höhlen zu konzipieren, die wir in die Seite von Hügeln 
	graben. Natürlich sind das keine kalten und feuchten Höhlen, sondern gut abgedichtete, 
	warme und mit allerlei Möbeln eingerichtete Hobbitbauten.
</P>

<P CLASS="P5">
	Der Halbling und seine Klassen
</P>

<P CLASS="P1">
	<B>Der Dieb</B><BR>
	Des Hobbits Konstitution prädestiniert ihn geradezu für diese Tätigkeit. Natürlich ist 
	er nicht mit dem gemeinen Beutelschneider vergleichbar, nein, das nicht. Seine 
	unerschöpfliche Neugier aber zwingt ihn manchmal einen Blick in Truhen zu werfen, 
	die vielleicht besser verschlossen geblieben wären und Dinge zu finden, die noch 
	niemand verloren hat. Außerdem klaut er ja nicht wirklich, er leiht sich nur ab und zu 
	etwas aus.
</P>

<P CLASS="P1">
	<B>Der Druide</B><BR>
	Die Hobbitdruiden lieben die saftigen Wiesen und die stämmigen Bäume ihrer Heimat. Wie 
	andere Rassen ihrer Profession würden sie nur in Notfällen Hand an ein Tier legen, etwa 
	wenn dieses schwer verletzt ist. Sie dienen ihrem Volk als Heiler und auch als Unterstützung 
	im Kampf gegen Feinde.
</P>
	
<P CLASS="P1">
	<B>Der Kämpfer</B><BR>
	Der Halblingkrieger muß gleich zweifach seinen Mann stehen. Auf der einen Seite muß er 
	sich mutig Monstern entgegenstellen die doppelt oder mehrmals so groß sind wie er selbst, 
	auf der anderen muß er dem Spott seiner größeren Kriegerkollegen begegnen.<br>
	So mancher Hobbit hat sich diesem Spott jedoch schon früh entzogen, indem er einem 
	ihn hänselnden Barbaren geschickt seinen Stock in die Kniekehlen hieb, um den am Boden 
	liegenden dann zu fragen: "Na, wer ist jetzt der kleine Mann?"
</P>
	
<P CLASS="P1">
	<B>Der Kleriker</B><BR>
	Die Hobbitkleriker kümmern sich um die Gesundheit ihre Volkes. Sie sind darin eindeutig 
	besser als die Druiden, die dies nur in Notfällen tun. Ein Hobbitkleriker ist für jede 
	Gruppe ein wertvolles Mitglied und seine selbstlose Hingabe hat schon so manchem Großen 
	aus der Klemme geholfen.
</P>
	
<P CLASS="P5">
	Der Halbling und die anderen Rassen
</P>

<P CLASS="P1">
	Ein berühmtes Hobbitsprichwort lautet: "Traue niemandem über 1,60m!!" Da bleiben dann 
	natürlich nur noch Zwerge und Gnome übrig, und auch die werden aus Hobbitaugen manchmal 
	argwöhnisch betrachtet. Das bedeutet aber lediglich, daß wir vorsichtig sind, und nicht 
	daß wir unsere Gastfreundschaft entehren. Tatsächlich wird einem freundlichen Fremden von 
	einem Hobbit immer mit Respekt und Achtung begegnet, und sei er auch vom großen Volk.<br>
	Den gelegentlichen Seitenhieb muß man uns aber zugestehen. Ein Ellenbogenknuff in den Magen 
	sollte ein, mit Hobbits reisender, Barbar vertragen können. Und auch der Elf muß ein 
	"Langohr" oder "Weissgesicht" wegstecken. Und da wer austeilt auch einstecken muß, ist 
	das Gegrummel eines als "Dreikäsehoch" bezeichneten Hobbits nicht weiter ernstzunehmen.
</P>

<P CLASS="P5">
	Beliebte Halblingsticheleien:
</P>

<UL CLASS="P1">
	<LI>Barbaren: "Packesel", "Trampel"<BR></LI>
	<LI>Elfen: "Langohr", "Spitzohr", "Püppchen"<BR></LI>
	<LI>Zwerge: "Hopp, hopp, Kurzbart"<BR></LI>
</UL>

<P CLASS="P5">
	Wie man einen Halbling spielt
</P>

<P CLASS="P1">
	Das hier ist natürlich keine verbindliche Anleitung. Jeder sollte seinen Charakter so 
	spielen, wie er es für richtig hält. Spontanität ist immer gut und witzige Kommentare 
	sind oft von der Situation abhängig. Leider befindet sich EQ zu diesem Zeitpunkt noch in 
	Phase 3 und ich bin erst ab Phase 4 dabei. Deshalb ist die folgende Beschreibung Treminderus 
	meiner Phantasie entnommen:
</P>
	
<P CLASS="P1">
	Treminderu ist gutmütig und ein bißchen vorwitzig. Er steckt seine Nase in Dinge die ihn 
	wenig angehen und holt sich dabei oft ein blaues Auge. Wie die meisten Hobbits liebt er 
	Geschenke und verteilt auch oft eine Kleinigkeit an seine Freunde.
</P>

<P CLASS="P1">
	Seinen Gefährten geht er häufig mit frechen Sprüchen auf die Nerven, und seine Tapsigkeit 
	macht ihn oft zu einer Gefahr für die Umwelt. In seinen Reden rühmt er oft seine großen 
	Taten, im Kampf ist er dann aber oft eher ängstlich als mutig.
</P>

<P CLASS="P1">
	Er flunkert gerne, sieht das aber selbst nur als "großzügige Auslegung der Wahrheit". Nach 
	bestandenen Gefahren zückt er zu allererst seine Pfeife und nichts ist für ihn schlimmer als 
	unregelmäßiges Essen und Trinken.
</P>

