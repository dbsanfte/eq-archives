	Ich weiß nicht wie und ob emotes in EQ funktionieren. Hier ein Beispiel, wie ich sie 
	in UO angwendet habe.
</P>

<P CLASS="P1">
	Tracking: *Treminderu puts his hand in the footprint. Promptly his eyes widen fearfully*
</P>

<P CLASS="P1">
	Mit Emotes kann man viel Atmosphäre 'rüberbringen ohne auch nur ein einziges Wort zu 
	tippen. Ab und zu sollte man sie aber ändern, da sie bei häufigem Gebrauch irgendwann 
	nerven.
</P>
	
