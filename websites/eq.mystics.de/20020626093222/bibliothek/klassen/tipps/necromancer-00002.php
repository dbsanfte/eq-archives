<P CLASS="P1">
	Meines Wissens gibt es 3 quests für Necromanten in Erudin:
</P>
<OL CLASS="P1">
	<LI>
		Rungupp auf der 2. Insel. Tötet Ihn und bringt seinen Kopf zu Eurem Gildenoberhaupt, Pluxia Vactus. Hierfür gibts Faction, Silver und einen Dolch (ja ein bisschen wenig, aber allein der Anblick, wenn der stinkende Troll unter Euren Spells zusammenbricht, ist Genugtuung!!!)
	<BR></LI>
	<LI>
		Der Palace Courier in der ersten Zone! (wie oben beschrieben) Tötet Ihn und bringt seine Robe zu Pluxia, Eurem Gildenoberhaupt. Auch hierfür gibt's Exp und Geld. (Im Moment scheint diese Quest broken!!!) Ach und nochwas: Tötet den Courier nicht zu oft! Dafür gibt's zwar Faction bei den Necromanten, aber beim eigenen Volk fällt Euer Ansehen rapide, was dazu führen kann, dass die Wachen Euch einfach niederstrecken!
	<BR></LI>
	<LI>
		Auf Kerra läuft eine Poacher Dame rum. Erschlagt sie und bringt ihren Kopf zu Pluxia. Das gibt Exp. und Kohle!<br>
		Habt Ihr dann endgültig genug von Erudin, dann nehmt ein Schiff nach Queynos (castet vorher aber "Gather Shadows" (invis Level 8) auf Euch, sonst erschlagen Euch die Wachen! und denkt dran auf Schiffen kann man nicht casten! Sprich: Falls der invis fällt und ihr noch nicht in Queynos seid, kurz vorm Hafen vom Schiff hopsen und Invis casten, dann könnt Ihr beruhigt an Land gehen!)
	<BR></LI>
</OL>

