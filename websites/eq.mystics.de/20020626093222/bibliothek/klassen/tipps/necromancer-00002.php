<P CLASS="P1">
Da man aus vier verschiedenen Rassen wählen kann, hängt der Startort immer von der jeweilgen Rasse ab.<br> 
Als erfahrener Necromant kann ich Euch nur die Gattung Erudite Necromant ans Herz legen! Womit Ihr dann in Erudin 
starten würdet.<br><br>
Die Insel Erudin ist die kleinste Insel Norraths und liegt westlich von Antonica. Euer Haupt Hunting Ground wird von 
Level 1 bis 7 hauptsächlich Toxxulia Forrest sein. Toxxulia ist aufgeteilt in zwei Gebiete. Das Newbie Gebiet und 
das Advanced Gebiet. Im Newbie Gebiet gibt es: Decaying Skeletons (tragen oft Knochen (für Eure Pets) und Waffen, 
die Ihr gut verkaufen könnt), Moss Snakes, Thistle Snakes, Kobold Runts, Widow Hatchlings, Spiderlings, Skunks, 
Infected Rats, Rats und Widow Wisps (PFOTEN WEG!!!) und Pixies (EBENSO). Außerdem treiben hier Newbie Poacher 
ihr Unwesen! Diese können für Anfänger ganz schön harte Brocken sein, aber gebt nicht auf! Die geben gut 
Experience! Weiterhin für die Necromanten interessant: Die Eruditen Regenten schicken Ihre jüngsten Recken 
nach Toxxulia Forrest, praktisch als ihre Reifeprüfung. Dummerweise sind diese die Erzfeinde der Necromanten. 
So kann es schonmal sein, dass sie Euch einfach angreifen. Andersrum gesehen: Wenn Ihr sie tötet, hinterlassen 
Sie ein Robe, die Ihr Eurem Gildenmeister gebt und dafür Faction und Geld sowie eine gewisse Experience bekommt.<br>
Auf der zweiten Insel, der "Advanced Zone" gibt es stärkere Gegner wie Kobold Sentrys, Kobold Watchers, 
Kobold Shamans, Kobold Scouts, Skeletons, Spiderlings, Widow Hatchlings. Nehmt Euch besonders am Anfang 
vor den Skeletons in acht!!! Die hauen böse rein und blockieren mit Stuns Eure Sprüche. Ab Level 6 
sollten sie mit Pet aber keine Gefahr mehr darstellen. Weiterhin gibt es hier 2 Kobold Camps in denen 
regelmäßig Gegner respawnen!<br>
Auch hier gibt es wieder Poacher (NPC Player Killer), die schon um einiges härter sind! Außerdem gibt es drei 
weitere NPCs. Zum Einen Shintar Vinlai, ein Magier mit Pets (der manchmal angreift), zum zweiten Rungup, einen 
Magiespeihenden Troll (siehe auch Necroquests unten) und einen Necromanten. Letzteren sollten Ihr niemals!!! 
angreifen, weil das erstens auf Eure faction gehen würde und zweitens weil seine pets (ja plural! der hat mehrere! 
fragt mich nicht wie!) für 77 damage zuschlagen!!! und da dürften 1 bis 2 Schläge bis zum finalen Aus genügen!<br>
Habt Ihr dann Level 8 erreicht geht's ab nach Kerra, Katzen schlachten. Hier findet Ihr die Gegner, die 
Ihr euch schon immer gewünscht habt! Hart wie Granit und unnachgiebig wie Epoxid! Ein Tip gebe ich Euch noch: 
Kerra immer nur in Gruppen angreifen! Und nehmt Euch genug Knochen mit! Ansonsten findet Ihr da Gegner bis Ihr 
Level 20 seid!
</P>

