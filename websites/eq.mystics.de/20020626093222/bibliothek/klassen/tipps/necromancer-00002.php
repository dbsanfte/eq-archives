<P CLASS="P1">
	In dem Fall des Necromanten gilt: Schickt das Pet vor und spielt Artellerie! Von hinten immer raufcasten! 
	Eine gute Taktik ist es auch, die Health seines Pets immer im Auge zu behalten und im Notfall selbst 
	einzuspringen, damit das Pet resten kann und sich regeneriert. Ab Level 8 kann man dann "Mend Bones" 
	und sein Pet damit HEILEN!<br>
	Im Gegensatz zu Wizards und anderen Magiern muss sich ein Necromant immer auf einen längeren Kampf gefasst 
	machen, da seine Spells meist DoT (Damage over Time) sind und etwas Zeit brauchen, um die volle, destruktive 
	Wirkung zu entfalten.<br>
	Das für mich aber genialste am Necromanten bleibt immer noch der vampirische Einfluss! Die Energie, die man 
	einem Monster mit einem Direct Damage Spell stiehlt, sei es Lifetap, Lifespike, Siphon Life oder oder oder, 
	wird einem selbst zugefügt! Es muss doch furchtbar deprimierend für ein Monster sein zuzusehen, wie es immer 
	schwächer wird und der Necromant kaum bzw. keine Health verliert und das alles durch seine eigene Lebenskraft!
</P>

