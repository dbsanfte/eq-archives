<P CLASS="P1">
	Der Necromant gehört bei Everquest mit Sicherheit zu den besten SinglePlayer Charaktären. Durch das Pet des 
	Nekromanten, welches man sogar schon ab Level 1 beschwören kann, erhält der dunkle Magier einen machtvollen 
	Verbündeten. Euer Pet ist Euer Freund, Weggefährte, Mitstreiter und Tank (Panzer) in einem. Er zieht nicht 
	nur die Gegner auf sich, damit Ihr in Ruhe Eure Sprüche casten könnt, sondern er teilt auch mit aufsteigendem 
	Level immer mehr Schaden aus. (In späteren Level stunned er sogar Eure Feinde, was ein RIESEN Vorteil ist.)<br>
	Ein weiterer toller Vorteil der Nekromanten sind seine Lifetap Spells. Diese fügen zum Einen Direct Damage zu und 
	zum Zweiten führen sie die Lebensenergie, die Ihr Euren Opfern entzieht, Eurer eigenen hinzu. So könnt Ihr einen 
	Kampf sehr viel länger überleben, als alle anderen Magierklassen und viele Nicht Magierklassen.<br>
	Die größte Stärke der Nekromanten jedoch liegt in ihren DoT Spells (Damage over Time - dauerhafter Schaden). Ab 
	Level 12 verfügt der Nekromant nämlich über 2 stackable Spells Engulfing Darkness und Heat Blood. Diese Spells 
	können aufeinander gecastet werden und verursachen einen Schaden.... Ich möchte nicht zuviel verraten! 
	(diabolisches Grinsen!) (Die ersten DoT Spells erhält der Nekromant übrigens schon ab Level 4!!! Sind auch 
	hervorragende Duel Spells, da Eure Gegner erblinden und weder casten noch schlagen können!)
</P>

