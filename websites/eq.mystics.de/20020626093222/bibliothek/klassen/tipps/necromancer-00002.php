<P CLASS="P1">
	Als Necromant könnt Ihr aus vier verschiedenen Rassen wählen: Dark Elf, Erudite, Gnome und Human. Um 
	rollenspielerisch richtig böse zu wirken, wählt man natürlich den Dark Elf, will man die grösste Firepower 
	(Magie) wählt man ohne zu zögern den Eruditen. (keine Angst! in Level 8 erhaltet Ihr den Spell Infravision, 
	der macht den Manko der Eruditen bzw. Menschen wieder wett!) Die besten Healthwerte bieten der Gnome und der 
	Mensch. Wobei ich vom Mensch als Necromanten nur abraten kann, da er über eine ziemlich miese Intelligenz verfügt.<br> 
	Die wichtigsten Stat-Werte des Necromanten sind ohne Frage INT (Intelligenz) und DEX (Geschicklichkeit).
	Ich würde dringend empfehlen alle Bonuspunkte auf INT zu verteilen und den Rest auf DEX.
</P>

