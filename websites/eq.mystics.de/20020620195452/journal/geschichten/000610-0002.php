	Geschrieben von Tedadar Flammenpfeil<BR>
	<BR>
</P>

<P CLASS="P1">
<b>2.Der erste Raid</b>
</P>

<P CLASS="P1">
Freeport, Hauptstadt der Menschen, und pulsierendes Zentrum von Norrath. Trotz der
kargen wüstenähnlichen Umgebung die wohl größte Stadt der Welt. An den Stadttoren
versuchen sich junge Menschen an Schlangen und Spinnen die es hier zuhauf gibt und
auch manch Ork der sich von den Commonlands neugierig auf die Sandfläche wagt, kehrt
nichtmehr zu seinem Haufen zurück. 
</P>

<P CLASS="P1">
Inmitten der Sandwüste nahe dem Stadttor rennt eine Gruppe Menschen mit ungewöhnlich
schnellem Tempo auf die Stadt zu, und stürmt durch das Tor, an den schwergepanzerten
und stark bewaffneten Wächtern vorbei, die aber keine Anstalten machen die Gruppe
aufzuhalten, und weiterhin wachsam in die Wüste schauen. Dass die Wächter nicht zu
unterschätzen sind, wird an den Leichen vor den Stadttoren deutlich, die aber alle
von den Dunkelelfen stammen die immer wieder in dieser Wüste Menschen oder Tiere jagen
und dabei den Wächtern zu nahe kommen.
</P>

<P CLASS="P1">
<I>Auf zur Bank</I> rief Valeria in die Gruppe <I>die ersten warten schon</I>.
Die Gruppe folgte dem silbergekleideten Paladin bei seinem Weg durch die Stadt,
welcher immer wieder einen Blick über die Schulter warf um sicherzugehen, daß sich
niemand in der verwinkelten Stadt verläuft. <I>Da sind wir ja schon!</I> sagte
Tedadar <I>und wir sind fast die Ersten</I> fügte er mit einem Lächeln hinzu. Als
die Gruppe mit den schon wartenden zusammenstieß, verfielen schon viele in Gespräche
und die ersten Flaschen Wein wurden geöffnet. <I>Alle mir folgen</I> rief Valeria in
den Haufen, <I>Das Treffen findet hier in der Nähe statt!</I> Tedadar und die
anderen folgten Valeria, der durch einen Laden auf einen Hinterhof ging welcher mit
grünem Gras bedeckt und von einer hohen Mauer umgeben war. Daladen schritt auf Tedadar
zu <I>Hallo Ted! Hier nimm!</I> sagte er und drückte dem Ranger ein paar Flaschen in
die Hand an denen er vorsichtig roch <I>... oh, dein selbstgebrannter Vodka</I> sprach
er mit einem Lächeln, <I>Ich habe auch noch etwas für dich</I> und Tedadar reichte
Daladen ein paar Früchte die er auf dem Weg eingesammelt hatte, er wusste dass Daladen
sie für seine Brauereiversuche benötigte. <I>Hehe, danke, ... hast dir noch eine Flasche
verdient</I> sprach Daladen und zog noch eine der Flaschen aus der Tasche und gab sie
Tedadar. 
</P>

<P CLASS="P1">
Inzwischen waren schon ein paar Leute eingetroffen, darunter auch ein paar Zwerge und
Elfen die auch zur Gilde gehörten. Erste Flaschen Alkohol wurden überall geleert und die
ersten begannen schon leicht zu schwanken. Scheinbar waren Daladens Brauereikünste schon
gut vorangeschritten. Der Vodka brannte Tedadar regelrecht in den Magen herunter, wo
er dann ein warmes Gefühl erzeugte, aber auch ein wenig benommen machte. Ein Grinsen
schlich sich über das Gesicht des Rangers der noch immer sein grünes Kettenhemd trug.
<I>Paladine wie du haben eure Ländereien ruiniert! Du wirst nicht mein Land schänden !</I>
rief er in Richtung Valerias der überrascht aus einem Gespräch hochschreckte und langsam
in Richtung Tedadar ging. <I>Ich fordere dich heraus zum Duell!</I>, sprach Tedadar mit
einem Lächeln auf den Lippen und spannte seine Muskeln an um mit dem Angriff beginnen zu
können. Valeria überlegte kurz und fügte dann mit einem Grinsen hinzu <I>Nagut, wenn du
willst</I> und schwang schon sein riesiges zweihändiges Schwert direkt auf den Ranger
zu. Ein heftiger Kampf entbrannte, nach und nach verebbten alle Gespräche in der Nähe
und alle schauten gespannt dem Duell zu. Tedadar versuchte erst mit seinem Schwert und
seiner Axt den Paladin zu bedrängen, aber nicht jeder Schlag traf die schwere
Rüstung, und Valeria konnte geschickt ausweichen. Da murmelte der Silbergekleidete ein
paar Worte und plötzliche Blindheit befiel den Ranger, er schlug aber mit seinen zwei
Waffen weiterhin in die Richtung wo er den Palladin hören konnte und sprach selber ein
paar seltsame Worte. Flammen hüllte die Gestalt von Valeria ein und ein bizarres rotes
Glänzen spiegelte sich an der Rüstung des Paladines, welcher aber immer wieder auf den
grüngekleideten Körper von Tedadar eindrang und schon einige blutende Wunden geschlagen
hatte. Doch die Kräfte auf beiden Seiten schwanden mit dem Kampf, welcher wohl durch das
Waffengeklirr und den Lärm der magischen Entladungen weit in der Stadt zu hören sein
musste. Schon schaute es so aus als ob der Ranger die Oberhand behalten könnte und immer
schwächer und in die Defensive gedrängt parierte Valeria die Hiebe. <I>Stirb!</I> sagte
Tedadar mit einem bösen Grinsen auf den Lippen und seines Sieges sicher, doch Valeria
keuchte: <I>Niemals!</I> ein Grinsen huschte über sein Gesicht und Tedadar sah wie der
Paladin aufeinmal von innen zu erstrahlen begann und die Götter ihm seine ganze
Lebenskraft wieder gaben, die er beschworen hatte. Schlagartig wendete sich das Blatt.
Der neugestärkte Paladin hieb auf den schwer verletzten Ranger ein, um ihn
niederzustrecken. Doch dann hielt er kurz vor dem Ende des Rangers inne und sagte:
<I>Halt! Es ist genug!</I> doch Tedadar antwortete: <I>Nein, töte mich!</I> und Tedadar
schwang nocheinmal seine Waffen in die Richtung von Valeria. Doch bevor diese treffen
konnten schlug das gewaltige Schwert schon wie ein Blitz in Tedadars Körper ein, und
der Geist des Rangers entwich aus seinem Körper,...
</P>

<P CLASS="P1">
Erwachen, wieder diese Benommenheit vor den Augen. Der Ranger richtete sich auf und
versuchte sich zu orientieren. Immer noch Freeport, sogar immer noch auf dem
Hinterhof. Doch ein zweiter Blick machte ihm klar dass er das Duell verloren hatte. In
einer Lache aus Blut lag sein Körper zu den Füssen des Paladins, der lächelte.
<I>Mit Lay Hands hab ich jetzt nicht gerechnet</I>, sagte Tedadar und kniete sich nieder
um seine Habseligkeiten wieder zu sich zu nehmen die noch in der Leiche waren. Valeria
sagte nur: <I>Gut wenn man die Kraft der Götter nie verschwendet! Aber guter Kampf!</I>
Tedadar hatte gerade die letzten Teile seiner Ausrüstung wieder aufgenommen als Valeria
zu ihm sagte: <I>Scheint so als ob wir die Höchsten wären die vorerst beim Raid
mitmachen!?</I> Tedadar Blickte in die Menge der versammelten Leute und ihm wurde klar
wie wenig von den älteren Chars überhaupt gekommen waren. Eigentlich waren nur lauter
Jüngere gekommen. <I>Wir sollten zusammen den Raid organisieren.</I> sagte er noch dazu,
und Tedadar antwortete: <I>Also bin ich nun stellvertretender Offizier</I> und lächelte. 
</P>

<P CLASS="P1">
Danach organisierten die zwei den Raid zu den Dunkelelfen. Sie hatten vor die jüngsten
Leute durch die Commonlands ziehen zu lassen um einzelne Darkies zu jagen, mit den
grösseren wollten sie zusammen eine Gruppe machen die die Grenze zu den Dunkelelfen
schliessen sollte, alle Beteiligten legten ihre wertvollen Dinge in der Bank ab, falls
sie erwischt werden sollten bei ihren Kämpfen. Valeria zog dazu seinen Bronzeharnisch
an und Tedadar seine Lederrüstung. Die Magier verstauten sicher ihre kostbaren Roben
und Schmuckgegenstände, alles machte sich bereit für einen Kampf. 
</P>

<P CLASS="P1">
Als dann letztendlich auch der Letzte bereit war schritten die Gruppen hinaus in die
Wüste vor Freeport um kurz darauf auf die bewaldeten Ebenen der Commonlands zu schreiten.
Die Gruppen teilten sich, die Jüngeren drangen tiefer in das Land vor, während Tedadars
und Valerias Gruppe an dem Gebirge entlangging das sich zu ihrer Rechten erstreckte.
<I>Gleich sind wir da und zeigen es den Darkies!</I>, sagte Tedadar und zustimmendes
Gemurmel erklang in der Gruppe. Da waren sie auch schon da,... ein tiefeingeschnittener
Pass spaltete das Gebirge. <I>Da geht es zu den Dunkelelfen rüber! Wir bleiben erstmal
auf dieser Seite!</I> Schon kam ein ahnungsloser Dunkelelf die Strasse entlanggelaufen.
Der Kampf war kurz und heftig, der Boden riss auf, und schien die Füße des Darkies zu
verschlingen, sodaß er sich nichtmehr rühren konnte. Er blickte erschrocken in unsere
Richtung den Tod vor den Augen. Magier begannen mächtige Energien zu beschwören, die
Kämpfer warfen sich mit erhobenen Waffen auf den einzelnen Dunkelelf und gemeinsam wurde
er geschlagen. <I>Hurra! der Erste!!</I> schrie Tedadar und wir alle waren froh, auch
wenn es nicht gerade ein fairer Kampf war. <I>Wär er in seinen Ländereien geblieben! Die
Commonlands sind lange genug von den Darkies belästigt worden</I>, sprach einer aus der
Gruppe, als auch schon wieder ein dunkelhäutiger Elf auf der Strasse erschien, der aber
die Gefahr rechtzeitig sah und sich wendete um zu flüchten. Doch grüne Wellen schienen
seine Füße schwer wie Blei werden zu lassen und er konnte sich nur noch langsam zu dem
Canyon zurückschleppen. Wieder griffen die Magier und die Krieger an, doch plötzlich
rannte der Darkie los und sprang durch den Einschnitt. <I>IHM NACH!</I> rief Valeria und
die gesamte Gruppe sprang weiter hinein in den Pass, hinüber zu den Dunkelelfen,...
</P>

