<P CLASS="P2">
	1.0 Was versteht man unter <I>"EverQuest: The Shadows of Luclin"</I> ?
</P>
<P CLASS="P1">
	<I>"EverQuest: The Shadows of Luclin"</I> ist eine Erweiterung zum weltbekannten Spiel <I>"EverQuest"</I>  
</P>

<P CLASS="P2">
	1.1 Welche Merkmale bzw. Möglichkeiten bietet diese Erweiterung ?
</P>
<P CLASS="P1">
	Hier ein kleiner Auszug. Diese Auflistung wird sich aber bis zur Veröffentlichung noch ändern.
</P>
<UL CLASS="P0">
	<LI>
		Eine stark verbesserte 3D Grafik, die es uns ermöglicht wesentlich mehr Polygone und detailliertere Texturen zu verwenden
	<BR></LI>
	<LI>
		Mehr als 25 neue Zonen inkl. einer neuen Start-Stadt
	<BR></LI>
	<LI>
		Komplett neue, sehr detaillierte Spielfiguren
	<BR></LI>
	<LI>
		Eine komplett neue Rasse: Die "Vah Shir" (Kerrans)
	<BR></LI>
	<LI>
		Eine komplett neue Klasse: Die "Beastlord"
	<BR></LI>
	<LI>
		Zusätzliche Möglichkeiten für all jene in Level 60
	<BR></LI>
	<LI>
		Neue Quests, Items, NPC's und mehr!
	<BR></LI>
</UL>

<P CLASS="P2">
	1.2 Werden die minimalen Systemanforderungen für Luclin gegenüber jenen für Velious erhöht?
</P>
<P CLASS="P1">
	Ja, Luclin ist ein sehr ehrgeiziges Projekt. Zusätzlich zu den reichhaltigen Charaktermerkmalen wird Luclin die weitaus beste Grafik besitzen, die je in einem Online-Spiel zu sehen war. Um dies zu ermöglichen werden die minimalen Systemanforderungen in etwa denen aktueller Spiele entsprechen.<br>
	Die genauen Spezifikationen werden kurz vor der Veröffentlichung bekanntgegeben.
</P>

<P CLASS="P2">
	1.3 Wann wird die Erweiterung erscheinen?
</P>
<P CLASS="P1">
	Im Dezember dieses Jahres.
</P>

<P CLASS="P2">
	1.4 Gibt es eine Betaphase und wie kann man daran teilnehmen?
</P>
<P CLASS="P1">
	Ja. Luclin wird ab ca. August in einer geschlossenen Beta getestet. Teilnehmer dazu werden individuell dazu eingeladen, basierend auf bekannte Fähigkeiten in der Vergangenheit. Als solche werden jene Leute, die aktiv am Test-Server spielen bevorzugt.
</P>

<P CLASS="P2">
	1.5 Muß Luclin gekauft werden und in welcher Form bekommt man es?
</P>
<P CLASS="P1">
	Ja, Luclin muß gekauft werden. In welcher Form steht derzeit noch nicht fest.
</P>

<P CLASS="P2">
	1.6 Wir haben in unserer Familie 4 Accounts. Muß Luclin für jeden Account extra erworben werden?
</P>
<P CLASS="P1">
	Ja, Luclin muß für jeden Account gekauft werden mit dem man die neuen Zonen betreten möchte.
</P>

