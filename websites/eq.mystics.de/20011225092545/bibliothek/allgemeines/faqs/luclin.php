<P CLASS="P2">
	5.0 Stimmt es, daß für jede Rasse neue Charakter-Modelle erzeugt werden?
</P>
<P CLASS="P1">
	Ja. Die neuen Charakter-Modelle sind wesentlich detaillierter, besitzen viel mehr Polygone und eine größere Texturtiefe. Sie wirken "lebendiger".
</P>

<P CLASS="P2">
	5.1 Was tut sich bei den Gesichtern?
</P>
<P CLASS="P1">
	Die neuen Modelle haben mehr Auswahlmöglichkeiten beim Gesicht. Für alle die bereits eine Spielfigur haben werden wir für die neuen Modelle Gesichter generieren, die sehr ähnlich sind. Evtl. werden wir diesen Leuten auch die einmalige Möglichkeit geben ein neues Gesicht zu wählen, aber das ist noch nicht sicher.
</P>

<P CLASS="P2">
	5.2 Was passiert, wenn man nach Luclin gehen will, aber das alte Charakter-Modell beibehalten möchte?
</P>
<P CLASS="P1">
	Obwohl wir sehr bezweifeln, daß dieser Fall tatsächlich eintritt, wird es eine Option geben, die es erlaubt das alte Charakter-Modell beizubehalten.
</P>

<P CLASS="P2">
	5.3 Ich laufe fast die ganze Zeit in Wolf-Form herum. Was bedeuten die neuen Modelle für mich?
</P>
<P CLASS="P1">
	Wir planen neue Modelle für alle Formen die ein Spieler annehmen kann.
</P>

<P CLASS="P2">
	5.4 Gibt es zusätzliche Animationen?
</P>
<P CLASS="P1">
	Ja, wir machen komplett neue Kampf- und Social-Animationen inkl. unterschiedliche Animationen basierend auf z.B. was man gerade in der Hand hält. In anderen Worten, ein Schlag mit einer Waffe wird anders aussehen als ein Schlag wenn man 2 Waffen in Händen hält.
</P>
<P CLASS="P1">
	Zusätzlich arbeiten wir an einer weiteren Art von Animationen welche Dich geradezu umhauen wird. Leider können wir darüber jetzt noch nichts verraten.
</P>

