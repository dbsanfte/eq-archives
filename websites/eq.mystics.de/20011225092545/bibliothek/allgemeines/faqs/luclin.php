<P CLASS="P2">
	2.0 Wo ist Luclin angesiedelt?
</P>
<P CLASS="P1">
	Luclin ist der Name des Mondes welcher von Norrath aus nur in der Plane of Sky gesehen werden kann. Wir erwarten, daß Al'Kabor bald den Vorhang, welcher den Mond vor Norrath verbirgt, öffnen wird.
</P>

<P CLASS="P2">
	2.1 Welcher Levelbereich wird von Luclin unterstützt?
</P>
<P CLASS="P1">
	Luclin wird für alle Level etwas bieten, ähnlich wie Kunark.
</P>

<P CLASS="P2">
	2.2 Wird die Levelhöchstgrenze von derzeit 60 erhöht?
</P>
<P CLASS="P1">
	Nein, aber wir planen einige levelunabhängige Optionen für all jene die ihre Spielfigur verbessern möchten. Diese Zusatzmöglichkeiten werde meßbar sein, aber nicht so stark um irgend jemand das Gefühl zu geben man muß Luclin kaufen um weiterzukommen, wie z.B. nach der Veröffentlichung von Kunark.
</P>

<P CLASS="P2">
	2.3 Wenn man Kunark und/oder Velious nicht gekauft hat, wird man mit dem Erwerb von Luclin Level 50 überschreiten können?
</P>
<P CLASS="P1">
	Ja.
</P>

<P CLASS="P2">
	2.4 Wie kommt man von Norrath nach Luclin?
</P>
<P CLASS="P1">
	Mittels von Gnomen gebauten, gigantischen und dampfgetriebenen Raketen...<br>
	<br>
	Nur ein Scherz! Immer wieder im "Journal of Al'Kabor" lesen. Irgendwas sagt mir, daß er eine Idee haben könnte wie es funktioniert.
</P>

