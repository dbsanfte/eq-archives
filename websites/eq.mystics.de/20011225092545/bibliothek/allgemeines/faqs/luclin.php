<P CLASS="P2">
	4.0 Wer oder was ist "The Beastlord" ?
</P>
<P CLASS="P1">
	Beastlords sind eine Hybrid Klasse mit einer großen Abhängigkeit von Pets. Mehr Informationen werden noch bekannt gegeben wenn wir die Details fixiert haben.
</P>

<P CLASS="P2">
	4.1 Welche Rassen können ein Beastlord sein?
</P>
<P CLASS="P1">
	Zumindest die Vah Sir werden die Klasse Beastlord spielen können. Es ist sehr wahrscheinlich, aber noch nicht sicher, daß diese Klasse auch von anderen Shaman-Rassen gespielt werden kann.
</P>

<P CLASS="P2">
	4.2 Muß man Luclin kaufen um einen Beastlord spielen zu können?
</P>
<P CLASS="P1">
	Ja. Auch wenn bereits bestehende Rassen einen Beastlord werden spielen können, muß Luclin unbedingt gekauft werden.
</P>

