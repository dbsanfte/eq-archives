<P CLASS="P2">
	3.0 Wer oder was sind die "Vah Sir" ?
</P>
<P CLASS="P1">
	Die Vah Sir sind eine noble Katzenrasse welche von Odus vertrieben und auf Luclin ausgesetzt wurden. Dies geschah vor vielen Jahren beim Testen einer starken Waffe der Eruditen. Diese Waffe, aufgestellt in der Heimatstadt der Vah Sir mit demselben Namen, versetzte die komplette Stadt und deren Einwohner und zurück blieb "The Hole". Die zurückgebliebenen Katzenmenschen, von den Eruditen als "Kerrans" genannt, wurden eingefangen und auf Kerra Isle ausgesetzt.
</P>

<P CLASS="P2">
	3.1 Muß man Luclin kaufen um die "Vah Sir" slebst spielen zu können?
</P>
<P CLASS="P1">
	Obwohl jeder Client das Charakter Modell der Vah Sir downloaden wird um sie sehen zu können muß tatsächlich Luclin gekauft werden um diese Rasse auch selbst zu spielen.
</P>

