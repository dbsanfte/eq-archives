<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Durim<BR>
	<BR>
</P>

<P CLASS="P1">
	Schon früh dunkel ward es an diesem Tage geworden, gar düster war der Greater Faydark, 
	keiner der letzten Sonnenstrahlen erreichte den Boden. Nur ein fahler Schein schimmerte 
	durch die dicht stehenden Bäume und Nebelfetzen waberten über den Boden. Viele Leute
	 waren nicht mehr unterwegs in dem Dunkel, den die Orcs waren zahlreicher geworden in
	 letzter Zeit und sie machten die Handelswege durch dieses Gebiet unsicher. Nur wenige 
	 Gruppen suchten noch ihren Weg nach Kelethin und Felwithe um dort für die Nacht in eine 
	 der vielen Herbergen einzukehren. So auch die kleine Gruppe von Kelethorn.
</P>

<P CLASS="P1">
	Er ware gerade am späten Morgen mit seinen Freunden im Hafen von Faydwer gelandet und 
	war gleich weiter gereist um noch am Abend Kelethin zu erreichen. Jedoch hatte er nicht 
	damit gerechnet, dass der Herbst in Faydwer schon so weit fortgeschritten war. Sein 
	bester Freund Kaldur, der Erudit war mit ihm und dem Mönch Shulong nach Faydwer 
	gekommen um sich einem Heer anzuschliessen, dass der König von Felwithe aufstellen 
	wollte um die Darkelfen in Mistmoor zu vertreiben und diese Feste der Göttin Tunare 
	zu weihen. Die drei Freunde kamen gerade von einem gemeinschaftlicher Reise durch die 
	Weiten Norraths und wollten sich ein paar Tage ausruhen, bis man in Mistmoor 
	einmarschieren würde.
</P>

<P CLASS="P1">
	"Glaubt ihr, dass wir viel Gegenwehr haben werden", fragte Kaldur, der im Kampfe auf dem 
	Schlachtfeld noch nicht viel Erfahrung gesammelt hatte. "Ach, sicherlich nicht", meinte 
	Shulong wärend er die Fackel ein wenig höher hielt und den Weg mit den Augen verfolgte. 
	"Es wird zwar kein Kinderspiel aber was ich so gehört habe, sollen die Dunklen Elfen 
	sich sehr sicher fühlen..." Shulong schaute ein wenig missmutig und stubste Kelethorn 
	an, "Sag mal, wie lang ist den das noch? Ich dachte wir brauchen nicht lange!" "Keine 
	Sorge, wir müssten gleich... da sind wir ja schon!" Ihre Gesichter hellten sich auf und 
	sie legten einen Schritt zu als sie die Feuer von Felwithe in der Ferne sahen.
</P>

<P CLASS="P1">
	Die Wachen am Eingang der Stadt sahen nicht sonderlich erfreut aus, als sie die 
	Dreiergruppe auf sie zustiefeln sahen. Anscheinend hatten sich schon mehrere Abenteurer 
	aus ganz Norrath eingefunden und sich in der Stadt einquartiert. "Ihr wollt wohl auch 
	euren Anteil haben was?" Kelethorn nickte und reichte einer der Wache seinen Pass um ihn 
	signieren und beglaubigen zu lassen. Die beiden anderen taten es ihm gleich. Die Wache 
	brummelt ein wenig, setzt dann aber wieder ein Lächeln auf und drückte den 3 jeweils 
	seinen Stempel in den Pass.
</P>

<P CLASS="P1">
	In der Stadt selbst war ein gar buntes Treiben. Überall liefen Angehörige fremder Völker 
	umher, Zwerge, Menschen, Eruditen, Hobbits, Barbaren, Gnome und Hoch-,Wald und Halbelfen 
	waren anwesend. Von verschiedenster Promotion huschten die Leute umher, geschäftig hier 
	und da noch Sachen einzukaufen, einige Schnäpchen zu erstehen oder sich noch einen 
	Schlafplatz für die Nacht zu sichern. Händler brüllten umher, priesen ihre Waren sogar 
	noch abends an, den die Sonne war schon nicht mehr am Himmel zu sehen, nur noch matter 
	Schein erhellte die Burgmauern, doch überall waren Fackeln und Kohlebecken aufgestellt 
	die eine angenehme Wärme und Helligkeit in das doch recht kalte Dunkel der Nacht brachten.
</P>

<P CLASS="P1">
	Die drei Freunde begaben sich in eine kleine Seitengasse, in der ein alter Bekannter 
	von Kelethorn eine kleine Taverne unterhielt, nichts grossartiges, aber sauber und 
	ordentlich, gutes Essen bekam man auch und die Betten waren ja sowieso nur zum schlafen 
	da. Der Bekannte begrüsste sie freundlich aber nicht übertrieben, zeigte ihnen die Zimmer 
	und riet ihnen hier momentan nichts zu kaufen, ausser er wäre dabei, viele Halunken trieben 
	sich momentan in der Stadt herum, die Wachen seien überlastet, überall würde man Diebe 
	sehen, die einen ausrauben, Händler die überteuerte Waren anböten die von miserabler 
	Qualität seien. Die drei schauten sich an, sowas hatte man schon erwartet und 
	vorsichtshalber alle Wertgegenstände schon in Freeport auf die Bank gebracht, 
	man würde ds Geld ja sowieso nicht brauchen, jeder hatte noch einige Gold und 
	Platin Stücke dabei, wenn er doch noch etwas kaufen wollen würde und um die Zimmer 
	zu bezahlen.
</P>

<P CLASS="P1">
	Nun, Krieg ist doch ein dreckiges Geschäft und zieht den Abschaum und die Geldgierigen an wie 
	das Plumpsklo die Schmeissfliegen.<br>
	Die drei begaben sich dann, nach einer recht guten Hausmannskost, die sehr nahrhaft und 
	oben drein noch recht gut war, in ihr Zimmer und legten sich schlafen. Am nächsten Tag 
	wollten sie sich die Stadt anschauen und einen Tagesausflug nach Kelethin machen.
</P>

<P CLASS="P1">
	Die Sonne schien durch das Fenster im Dachgeschoss der Taverne, Kaldur blinzelte 
	ihr entgegen, drehte sich um und wollte weiter schlafen, doch er konnte nicht. So 
	stand er den auf, wusch sich und zog sich an, danach weckte er die beiden anderen, 
	Shulong murmelte etwas von wegen "...alter Sklaventreiber...", Kelethorn blinzelte nur 
	und nickte, danach ging Kaldur die Treppe hinunter und nahm sein Frühstück ein. Die 
	beiden Freunde kamen die Treppe herunter, assen ebendfalls ein wenig und gingen dann 
	vor die Tür. Das Leben in den Strassen began langsam sich zu regen, langsam gingen in 
	den Strassen die Fenster auf, die Leute traten vor die Türen, reckten und streckten sich. 
	Die kleine Gruppe setzte sich in Marsch, man wollte sich noch die Magische Akademie von 
	Felwithe anschauen, Kaldur hatte noch einen Spell, von dem er behauptete, er müsse ihn 
	noch lernen und Shulong wollte bei einem reisenden Händler noch eine neue Kappe erstehen, 
	von der er gehört hatte, dass sie für ihn ideal wäre. So trennte man sich und verabredete 
	sich zu einem festen Termin wieder zu treffen.
</P>

<P CLASS="P1">
	So verging den die Zeit, man tat einen Ausflug nach Kelethin, stattete dem obersten 
	Heerführer, einem jungen und leicht aufbrausenden Hoch Elfen Paladin einen Besuch ab,
	trug sich in die Listen der Armee ein, erhielt ein Empfehlungsschreiben, seinen Sold 
	und wurde einer Gruppe zugeteilt. Die nächsten zwei Tage stieg die Ungedult unter den 
	Söldnern, es gab Prügeleien, einzelne wollten gleich aufbrechen und torkelten betrunken 
	durch die Strassen, wild brüllend wurden sie dann von den Wachen in den Kerker geworfen.
</P>

<P CLASS="P1">
	Nach einigen grossen Übung, knapp 5 Tage nachdem die drei Freunde angekommen waren, war 
	der Tag gekommen, an dem sich der Heerwurm Richtung Mistmoor bewegen sollte. Aus den 
	Toren Felwithes und aus der Umgebung, den kleinen Zeltlagern und Barackenbauten strömten 
	fast 10.000 tapfere Soldaten und Söldner, die den Dunkel Elfen eine Lektion erteilen und 
	sie für immer von Faydwer verbannen wollten. Nach einer grossen Ansprache wälzte sich der 
	Heerwurm, mit vielen Versorgungskarren und Belagerungsmaschienen der Gnome in seiner Mitte 
	gen Lesser Faydark.
</P>

<P CLASS="P1">
	In Lesser Faydark merkte man jedoch, dass man die Dunkel Elfen unterschätzt hatte, einige 
	von ihnen, um die 2000 Mann, mit Orcs zusammen gewürfelt hatte einen Hinterhalt gelegt und 
	beschossen den Heerwurm mit Pfeilen und Zaubersprüchen. Die Ahnungslosen gerieten in 
	Aufruhr, sofort gingen sie zum Gegenangriff über, die Angreifer jedoch zogen sich zurück, 
	ein kleiner unvorsichtiger Teil der Armee hinterher, man hörte nur noch aus der Ferne 
	einige Jubelgeschreie und das Grunzen von Orcs über den Sieg. 400 junge Krieger und Magier 
	hatten so ihr vorzeitiges Ende. Die drei Freunde waren froh und glücklich einen alten 
	erfahrenen Zwergen Krieger als Offizier ihrer Gruppe zu haben, der Leichtsinn des Menschen 
	Paladins der die Drows verfolgt hatte, war tödlich gewesen. Man beschloss nun, sofort 
	nach Mistmoor aufzubrechen und eine Belagerung zu beginnen.
</P>

<P CLASS="P1">
	Man spaltete die Armee auf. 6600 Mann zogen mit schwerem Kriegsgerät vor den Eingang 
	Mistmoors, die Dunkelelfen beschossen die Belegerer mit Brandpfeilen und Zaubersprüchen, 
	die Belagerer zogen sich danach ausser Reichweite zurück. Die andere Gruppe von 3000 
	Mann darunter auch die drei Freunde und ihre Gruppe, sollten als Entsatzheer auf dem 
	Weg nach Steamfont warten und den dortigen König um einen Teil seiner Armee bitten um 
	sie zu verstärken. 400 neue Männer kamen so hinzu, fast die gesammte Wache von Ak'Anon. 
</P>

<P CLASS="P1">
	Die Orcs, von der Belagerung Mistmoors aufgeschreckt wollten zusammen mit den Drows 
	die noch ausserhalb der Festung waren, die Belagerung sprengen in dem sie die Belagerer 
	angreifen wollten, Späher berichteten von etwa 3500 Drows und Orcs die nun Richtung 
	Mistmoor zogen, das Entsatzheer machte sich auf den Weg. Kelethorn, Kaldur und Shulong 
	zitterten der nun kommenden Schlacht entgegen. Von weitem hörten sie das Schlachtgebrüll 
	wilder Orcs und der eigenen Leute, Waffen klirrten, Schilder splitterten und man hörte 
	das Jaulen und Stöhnen der Verwundeten. Das war die Zeit in der sich der zweite Trupp 
	von seinem neuen Versteck auf den Hügeln rund um Mistmoor in die Ebene davor ergoss. 
	Sie fielen den verdutzten Orcs in den Rücken, mähten diese Reihenweise nieder, bevor
	diese begriffen und panisch ihr Heil in der Flucht suchten. Die Sieger verfolgten die 
	Orcs und tötete alle die noch am Leben waren, nicht einen liessen sie am Leben, sogar 
	Frauen und Kinder wurden getötet, zum Ruhme der Tunare. Welche düsteres Kapitel in der 
	Geschichte dieses Krieges.
</P>

<P CLASS="P1">
	Entsetzen machte sich breit in der Feste von Mistmoor als die Kämpfer zurückkehrten und 
	den Ring der Belagerung noch dichter zogen. Die Köpfe der erschlagenen Orcs auf Pfähle 
	gespiest, sollten sie den Mut der Verteidiger, der Dunkel Elen brechen. Doch nichts davon 
	geschah, die noch etwa 4000 Dunkel Elfen versuchten sogar einen Ausbruch, doch ihre 2000 
	tapferen, von der Dunkelheit verblendeten Kämpfer wurden von den noch etwa 8000 
	Belagerung niedergemäht, wie trockenes Heu auf einer Wiese. Die Leichen schoss man 
	mittels der Belagerungsmaschienen über die Mauer wieder zurück. Wie sich herausstellte 
	ein grosser Fehler.
</P>

<P CLASS="P1">
	2 Wochen waren inzwischen vergangen. Die Fronten hatten sich verhärtet, längst ging das 
	Gerücht eines Entsatzheeres auf Neriak durch die Reihen der Belagerer, doch auch der 
	König von Qeynos hatte knapp 500 Männer geschickt, die dem Licht zum Siege verhelfen 
	sollte. Kelethorn, Shulong und Kaldur war das Leben im Camp langweilig geworden. 
	Einzelne Scharmützel in der Nacht, aber keiner war bereit zum Angriff zu blasen um 
	endlich diesem Spuk ein Ende zu bereiten. Sie bedrängten ihren Führer, doch der alte 
	Zwerg meinte nur, er könne nichts tun, und dass die Elfen selber Schuld seien, weil da 
	wieder irgendwelche Handfuchtler die Planung übernommen hätten, die nicht die geringste 
	Ahnung hätten, wie man eine Festung einnehme. Noch weitere 3 Tage musste sich die 3 Freunde 
	gedulden, bis endlich an einem Abend die Vorbereitungen zum General Angriff getätigt wurden.
</P>

<P CLASS="P1">
	Alle Krieger rüsteten sich, keiner konnte Schlaf finden, die Magier meditierten, die 
	Klerikalen beteten zu ihren Göttern, die Mönche übten ihre Kampftechniken in einer Art 
	Luftkampf, die Krieger schärften ihre Waffen. Man wollte diesmal nicht versagen.
</P>

<P CLASS="P1">
	Der nächste Morgen war strahlend hell. Die Sonne schien auf den Boden vor Mistmoor, als 
	wenn es ein gutes Omen sein sollte. 1 Stunde nach Sonnenaufgang blies man zum Angriff. 
	Riesige Sturmleiterun wurden aufgestellt um die Mauern zu erklimmen, die 
	Belagerungsmaschienen warfen grosse Steinbrocken gegen die Türme, um sie zum 
	Einsturz zu bringen. Ein grosser Wurm aus Menschen ergoss sich gegen die Mauern 
	von Mistmoor, erstiegen die Leitern und..... fanden nichts vor. Nicht ein Dark Elf 
	lies sich blicken, noch war irgend ein Anzeichen zu sehen, dass je etwas hier gelebt 
	hatte. Die Krieger wurden unsicher, man öffnete das Haupttor, und auf einmal ergoss 
	sich ein Strom untoter Kreaturen, hässlich verstümmelte Leichen mit Schwert und Rüstung, 
	gräulich zugerichtete Körper, wiederbelebt durch die schwarze Kunst der Necromantie. 
</P>

<P CLASS="P1">
	Die Krieger stoben wild auseinander, die, die dem Tor am nächsten standen, wurden 
	niedergestreckt durch die Untoten, wurden getötet durch die Leute, die sie in einer 
	der Schlachten davor getötet hatten. Schreckliches passierte nun, der Strom der Untoten 
	schien nicht zu versiegen, immer mehr und immer schneller ergoss sich eine Flut von 
	zerfetzten Körpern aus den Tiefen von Mistmoor, die Sonne verfinsterte sich. Die 
	schreckenverströmenden Krieger erstürmten die Mauern, die Türme und den Innenhof der 
	Festung.  Auf den Mauern wurde erbittert gekämpft keinen Zoll breit wollte man weichen, 
	doch man wurde immer mehr zurück gedrängt. Die Krieger stolperten über die Leichen 
	ihrer gefallenen Kameraden, wateten im Blut der erschlagenen und mussten der Übermacht
	weichen. 
</P>

<P CLASS="P1">
	Die drei Freunde standen gerade etwas weiter weg,als sich eine Schar Krieger aus dem 
	Haupttore ergoss. Manche Schritt für Schritt zurückweichend, die Schläge der Zombies 
	ausweichend und parierend fast machtlos gegenüberstehend, andere wild ihr Heil in der 
	Flucht suchend. Etwa 200 Schritt vor den Toren standen die Fronten still, angefeuert 
	durch die Magier, die von einem erhobenen Turm, den die Gnome gebaut hatten, aus den 
	Feind mit Zaubersprüchen eindeckten und durch die Kleriker, die durch die Kraft ihres 
	Gottes die Wunden der Krieger heilten und die Untoten von ihrem Leid erlösten, aber auch 
	durch einen kleinen Zwergen Krieger, den Anführer einer Gruppe, der seine Leute, nur knappe 
	50 Mann Wagemutig in die Flanke der Untoten gestürzt hatte, und damit deren Front zum 
	schwanken brachte. 
</P>

<P CLASS="P1">
	Der kleine Zwergenkrieger war es, der seine Gruppe an die Flanke der Untoten geführt hatte, 
	die drei Freunde, ebenfalls in der Gruppe gerieten in einen Blutrausch. Kelethorn und 
	Shulong kämpften in der ersten Reihe, Kaldur unterstützte sie mit seinen Spells aus 
	einiger Entfernung. Schon 3 Gegner lagen erschlagen unter den Füssen Shulongs und 
	Kelethorns, der 4. sollte bald folgen. Kelethorn duckte sich, der Schwerthieb des 
	Drow ging knapp über seinen Kopf, Shulong drehte sich, die Luft sauste, als er sie
	mit seinem Bein durchschnitt. Der Tritt traf den Dunkelelfen direkt in die 
	Bauchgegend, er japste für einen Moment nach Luft, hob dann sein Schild und 
	trat einen Schritt zurück, sein Schwert zurück gezogen, jederzeit zum Stich bereit. <br>
	Kelethorn lies sein Schwert vorschnellen, der Dunkelelf parierte, schlug das Schwert 
	herunter, holte aus und wollte Kelethorn's Kopf mit dem Schwert spalten,  als Flammen 
	um ihn emporschnellten. Er schrie auf, seine Haut versengte und färbte sich noch 
	dunkler als sie zuvor war, seine Haare schmorten, er verzog sein Gesicht, das 
	Schwert immernoch erhoben, Kelethorn schaute, blass geworden, sein Blut war aus dem 
	Gesicht gewichen. Der Dunkelelf lies sein Schwert langsam sinken, ein verklärter 
	Ausdruck erschien auf seinem Gesicht, seine Knie gaben nach, er sackte zusammen, 
	kippte und hauchte sein Leben aus. Kelethorn blickte sich um, Kaldur stand auf 
	einer Anhöhe, kleine Flammen stiegen aus seinen zusammengeballten Fäusten, sein 
	Gesicht schien angestrengt, aber ein Lächeln umspielte seine Lippen.
</P>

<P CLASS="P1">
	Neben ihnen stand ein weiterer Dunkelelf, der gerade einem Hochelfen mit einem hämischen 
	Grinsen seinen Dolch in die Brust getrieben hatte, der Hochelf schaute erschrocken, war 
	er doch Siegessicher gewesen, der Dunkelelf zog seinen schwarzen, mit Runen verzierten 
	Dolch mit einem Ruck heraus, Blut sprudelte aus der Wunde, der Hochelf wurde blas, aschfahl
	war sein Gesicht, ungläubig schaute er auf die Wunde, seine Knie gaben nach, er knickte ein, 
	schaute den Dunkelelfen an, schüttelte leicht den Kopf und fiel dann zur Seite weg. Seine 
	Seele verlies ihn für immer. Eine Hand sauste auf das Genick des Drow, Shulong's Hand war 
	es, der dem Drow das Genick brach, es knackte, ein weiterer Schlag, ein Tritt gegen das 
	Knie und eine weitere dunkle Seele fand seinen Weg zu den Hallen Innoruuks. Die drei 
	Freunde kämpften erbittert weiter, um sie herum tobte der Kampf unerbitterlich weiter. 
</P>

<P CLASS="P1">
	Immer mehr lichteten sich die Reihen, auf Seiten der Krieger aus Felwithe wie der aus 
	Mistmoor, Drows, Ghouls, Zombies, Zwerge, Hoch-, Wald-, Halbelen, Menschen, Barbaren, 
	Eruditen, Hobbits und Gnome, lagen durch- über und untereinander. Der Tod machte vor 
	keinem Halt. Er machte sie alle gleich. Nur noch wenige Kämpfer waren von dem einst 
	riesigen Heerwurm übrig. Schliesslich, als nur noch knappe 500 Drows und noch etwa 
	1000 Krieger aus Felwithe übrig waren, wandten sich die Dunkel Elfen zur Flucht und 
	versperrten die Tore hinter sich. Die Verfolger mussten aufgeben und sich dank des 
	Pfeilhagels der nun folge, zurückziehen. Nur einer der drei Freunde hat überlebt. 
	Kaldur sass auf einem der Steine und weinte bitterlich. Er hatte in diesem Krieg 
	seine beiden besten Freunde für immer verloren. Die Dämmerung war hereingebrochen 
	und legte ihren Mantel des Schweigens über dieses Feld des Todes.
</P>

<P CLASS="P1">
	Es regnete in dieser Nacht, als wenn Tunare weinen würde ob ihrem grossen Verlust 
	treuer Diener. Der Regen spülte das Blut davon und es entstand ein roter Bach, 
	getränkt mit dem Lebenssaft der Gefallenen.
</P>

<P CLASS="P1">
	Am nächsten Morgen suchten die Krieger nach ihren gefallenen Freunden und Verwandten 
	um sie zu bestatten, viele Tränen flossen an diesem Tage, Kaldur überschaute das 
	Schlachtfeld von einem Hügel aus, um sich ob diesem Wahnsinn der seine liebsten 
	Seelen von ihm geholt hatte, zu begreifen. Ihm bot sich ein Bildniss des Schreckens. 
	Überall verstreut langen die Leichen der Gefallenen. Über 15000 tapfere Männer und 
	Frauen haben in dieser Schlacht ihr Leben verloren. Für einen Sieg, der kein Sieg war.
</P>

<P CLASS="P1">
	Lange noch erzählte man sich von dieser Geschichte des Schreckens und des Todes. Der 
	grossen Schlacht von Mistmoor. Zu viele sind gestorben, zu viele wurden geopfert, unnötig, 
	ohne Sinn und Zweck. Doch immer weiter wird es gehen, immer mehr Unschuldige werden ihr 
	Leben lassen, als Spielball der Götter, den nichts anderes sind wir, die wir klein und 
	machtlos sind.
</P>

