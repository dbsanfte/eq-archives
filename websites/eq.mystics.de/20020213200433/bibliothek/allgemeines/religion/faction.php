<P CLASS="P5">Allgemein</P>

<P CLASS="P1">
	Es gibt viele NPCs in Norrath und viele davon sind wichtige Mitbewohner dieser Welt. 
	Bei der Kommunikation mit ihnen erfährt man so einiges über die umfangreiche Geschichte 
	und die vielen verschiedenen Factions (Splittergruppe, soziale Gruppen) dieser Welt. 
	Jeder NPC gehört einer solchen Faction an und diese Faction hat sowohl Freunde als auch 
	Feinde. Hat man sich nun entschieden etwas für einen Faction zu tun, das Lösen eines 
	Quest zum Beispiel, wird man nach dem Lösen eine Erhöhung seiner Faction Standing 
	gegenüber dieser Gruppe erfahren, gegenüber den Feinden der Gruppe wird man aber 
	herabgesetzt. Tötet man z.B. einen Mitglied seiner Faction, wird das bewirken, daß 
	man in dieser Faction weniger angesehen ist, evtl. sogar getötet wird, wenn man in 
	die Stadt dieser Faction kommt, deren Feinde hingegen werden das natürlich gerne 
	sehen, was durch das Erhöhen der Faction Standing der Gruppe gegenüber zum Ausdruck kommt.
</P>
	
<P CLASS="P5">Neuer Char, was nun?</P>

<P CLASS="P1">
	Gleich nach der Erschaffung eines Charakters, wenn man das erste Mal die neue Welt 
	betritt, besitzt dieser einen initialen Faction Standing Wert, der sich aus den 
	Grundwerten der Rasse, der Klasse und der gewählten Gottheit ergibt. Was bei einem 
	menschlichen Shadow Knight z.B. bedeutet, daß er in der Startstadt Qeynos nicht 
	besonders gern gesehen wird, oder sogar gleich getötet wird. Qeynos ist an sich eine 
	'gute" Stadt, deren Bewohner auf 'böse' Charaktere entsprechend reagiert. (Es ist zu 
	beachten, daß das 'Leben' der NPCs auch an den Tageszyklus angepasst ist, d.h. manche 
	NPCs erscheinen eben nur Nachts oder nur am Tag. Diese Tatsache macht den Ort je nach 
	Tageszeit sicher oder unsicher, je nachdem welcher Faction man angehört und welche NPCs 
	gerade unterwegs sind.)
</P>

<P CLASS="P1">
	Nach der Charaktererschaffung liegt es an euch und eurem Charakter die Faction zu 
	beeinflussen. Wichtig dabei ist, zu beachten, daß Faction immer relativ ist, nicht 
	absolut. Beispiel: Dunkelelfen, Oger und Trolle werden sich kaum selbst als evil 
	bezeichnen, wohingegen Waldelfen, Hochelfen und Menschen usw. sagen werden, daß sie 
	es sehr wohl sind.
</P>

<P CLASS="P1">
	Es ist auch möglich (z.B. für einen Troll), durch Taten und Quests für eine Faction (z.B. 
	die Qeynos Guards) deren Wohlwollen zu erarbeiten. Dieser Troll würde dann, im Gegensatz 
	zu den anderen 'normalen' Trolls, nicht gleich angegriffen, wenn man sich einem Mitglied 
	dieser Faction nähert.
</P>

<P CLASS="P5">Die Auswirkungen</P>

<P CLASS="P1">
	Wird ein Charakter von einem NPC angegriffen, so passiert das normalerweise aus einen 
	der folgenden Gründe: der NPC ist von Natur aus aggressiv oder die Faction des NPCs 
	ist genau entgegengesetzt. Greift er auf Grund der Faction an und kann sprechen, so 
	wird er kundtun, warum er, sie oder es ist so schlecht auf den Charakter zu sprechen 
	ist. Wenn also ein Nekromant, der Paladine haßt auf einen solchen trifft, wird er ihn 
	angreiffen und etwas in der Art von ' Ich hasse alle Paladine mit ihrem heiligen Getue' 
	vor sich hin murmeln. Es ist also wichtig, darauf zu merken, was ein NPC sagt, damit 
	man auch mitbekommt, warum er etwas tut.
</P>

<P CLASS="P5">Was h&auml;lt der NPC von mir?</P>

<P CLASS="P1">
	Trifft man auf einen NPC und man wird nicht gleich angegriffen, möchte man manchmal 
	vielleicht doch wissen, was der NPC von einem hält. Klickt man doppelt auf den NPC 
	oder visiert ihn an und klickt dann 'C' wird ein sich immer ändernder Satz 
	angezeigt, der jeweils wiedergibt in welchem Grad er den Charakter haßt oder ihn mag.
</P>

<P CLASS="P1">
	Nachdem man einige Quests gelöst hat, kann es sehr informativ sein wieder mal auf den 
	Charakter doppelzuklicken, um herauszufinden, inwieweit sich die Haltung des NPCs 
	einem gegenüber geändert hat.
</P>

<P CLASS="P5">Keymembers</P>

<P CLASS="P1">
	Herauszufinden, ob ein NPC ein Keymember(Schlüsselfigur) der Bevölkerung Norraths 
	ist oder nicht, ist ziemlich wichtig. Keymember besitzen fast immer eigene Namen und 
	nicht, wie bei den normalen NPCs generelle Namen. Ein Beispiel für einen Gnoll Keymember 
	ist B'ranick Notai, der bekannt dafür ist, daß er sich meistens in der näheren Umgebung 
	der Qeynos Hills aufhält. Er ist ein Keymember, ein anderer Gnoll, der z.B. 'a patrolling 
	gnoll' oder 'a gnoll scout' genannt wird, ist das nicht. Würde man B'rankik töten, wird 
	das die Faction Standing des Charakter bedeutend mehr beeinflussen, als einen Non-Keymember 
	zu töten; das beeinflußt sehr wenig oder meistens gar nicht.
</P>
	
<P CLASS="P5">Quests</P>

<P CLASS="P1">
	Es existieren eine Unmenge an Quests in EverQuest und es werden tagtäglich mehr. Um Quests 
	zu verstehen und  überhaupt an sie heran zu kommen ist es unerläßlich, daß Faction-System 
	und das Keymember-System zu verstehen. Genau wie bei der Entscheidung, ob der NPC einen 
	Charackter angreift oder nicht, wird das Faction Standing herangezogen, ob überhaupt und 
	wenn ja, welche Questinformationen man in einem Dialog erhält. Die besten und meisten 
	Informationen erhält man von Keymembern, Non-Keymember werden nur spärlich oder gar keine 
	Informationen über Quests vermitteln.
</P>

<P CLASS="P1">
	Als erstes muß man den NPC anvisieren (target) und dann mit ihm über den /say-Befehl 
	kommunizieren. Vielleicht trifft man mal auf einen NPC namens Lisara, und diese NPC 
	mumelt immer etwas von einer verlorenen Halskette. Dann visiert man sie an und sagt 
	dann z.B. /say Lisara where is your necklace?'. Wenn man die richtigen Worte benutzt 
	hat, wird der NPC vielleicht antworten und einige Infos über die Kette von sich geben, 
	was dann meistens dazu führt, daß man an dem Quest (der Wiederbeschaffung der Kette) 
	mitwirkt. Es ist unbedingt wichtig, den ersten Namen des NPCs in die Fragen mit enzubauen. 
	Außerdem kann es natürlich sein, daß der NPC aufgrund der Faction verschiedene Quests an 
	verschiedene Charaktere vergibt, oder natürlich gar keine.
</P>
	
<P CLASS="P5">Zusammenfassung</P>

<P CLASS="P1">
	Das Verstehen des Faction Systems und das Benutzen der Doppelklick-Methode um 
	herauszufinden, wie die Haltung eines NPCs gegenüber des Charakters ist, ist ganz 
	klar und einfach ein wichtiger Bestandteil EverQuests. Dabei hängt die Wichtigkeit 
	stark von dem Spielstil des Players ab.  Nimmt man gerne an Quests teil, möchte die 
	Geschichte des Lands kennen lernen und möchte alles in allen Einzelheiten wissen, 
	dann ist der Umgang und die Kommunikation mit den Keymembern eine Grundvoraussetzung. 
	Wenn man sich aber mehr auf den Ausbau des Charakters und auf den Kampf konzentrieren 
	möchte, dann ist es wichtig sich nicht mit Keymembern abzugeben, da ein z.B. Angriff 
	auf diese das Faction Standing wesentlich beeinflußt und somit auch die Art, wie NPCs 
	dem Charakter gegenüber reagieren. Man könnte z.B. nicht mehr in einen Stadt gelangen, da 
	man sich mit den Guards eingelassen hat, also z.B. einen Guard gekillt hat. Dann hat man 
	keine Chance mehr die Stadt zu betreten, jeder Guard wird sofort angreifen.
</P>

