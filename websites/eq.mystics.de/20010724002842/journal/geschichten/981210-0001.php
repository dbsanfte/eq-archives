<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Ilion Sturmlied<BR>
	<BR>
</P>

<P CLASS="P1">
	Es war ein nebliger Tag im Greater Faydark, nahe Felethin und es war dunkel.
</P>

<P CLASS="P1">
	Ich ging durch das Stadttor der Stadt die ich so liebte und richtete meine Schritte nach
	Osten, ich wollte Kräuter suchen gehen, doch wollten meine Gedanken nicht bei dieser Aufgabe
	bleiben und ich legte mich unter einen großen Baum und zog das Buch, zu dem meine Gedanken
	die ganze Zeit abschweiften unter meinem Umhang hervor und begann zu lesen.
</P>

<P CLASS="P1">
	Das Buch handelt über die Kunst der Magie, wie die meisten meiner Bücher und während ich
	las merkte ich nicht, wie ich langsam zu dösen begann.
</P>

<P CLASS="P1">
	Ich wachte auf als es schon dunkel war. Ich stand auf und wollte meine Schritte wieder in
	Richtung der Stadt wenden, als ich  die Umrisse eines Orks im Nebel sah und da löste sich
	auch ein zweiter aus dem Nebel und noch ein Dritter.
</P>

<P CLASS="P1">
	Drei Orks standen dort auf dem Hügel und schauten durch den Nebel ich umfaßte meinen Stab
	fester und konzentrierte mich auf  einen Schutzzauber, aber die Orks bemerkten mich nicht,
	sie schienen auf etwas zu  warten.
</P>

<P CLASS="P1">
	Ich entschloss mich abzuwarten und versteckte mich hinter den Wurzeln des Baums vor dem
	ich eben noch friedlich geschlafen hatte.
</P>

<P CLASS="P1">
	Die Orks hatten sich in zwischen an einem
	Feuer niedergelassen und tranken etwas, was ich bis zu meinem Versteck riechen konnte und
	es roch nicht gut.
</P>

<P CLASS="P1">
	Irgendwie erinnerten sie mich an einige Zwerge die ich kannte nur haben die Zwerge
	bessere Manieren als diese Orks, wenigsten etwas bessere, aber diese Meinung änderte
	ich schnell, als ich sie betrunken sah, selbst der betrunkenste Zwerg hat bessere
	Manieren.
</P>

<P CLASS="P1">
	Es musste etwas Mitternacht sein, die Orks hatten sich ziemlich gehen lassen  und lagen nun
	total besoffen um das Feuer, als sich ein Schatten aus der Dunkelheit löste, ein riesiger
	Schatten.
</P>

<P CLASS="P1">
	Als der Schatten in das Licht der Feuers trat, erkannte ich was dieser Fleischberg war,
	ein Orger und zwar ein besonders häßliches Exemplar.
</P>

<P CLASS="P1">
	Der Orger trat mit seinen riesige Füßen die Orks aus ihren Träumen und sie waren nicht
	sonderlich erfreut über diese Störung, aber berühigten sich schnell, als sie sahen, wer
	ihnen gegenüberstand.
</P>

<P CLASS="P1">
	Es schien so, als hätten die Orks angst vor diesem Monstrum.
</P>

<P CLASS="P1">
	Die vier Unterhielten sich einige Zeit, doch ich konnte nichts von ihrem Gekrunze verstehen
	und als sie dann alle wieder besoffen ein geschlafen waren wollte ich mich endlich auf den
	Weg zurück nach Kelethin machen doch als ich mein Versteck verlassen wollte, wachte der
	Orger plötzlich auf und schaute in meiner Richtung und er sah mich.
</P>

<P CLASS="P1">
	In diesem Moment wachte ich auf.
</P>

<P CLASS="P1">
	Ich lag schweißgebadet unter  dem Baum, unter dem ich
	eingeschlafen war,  an meiner Seite mein Stab und das Buch das ich gelesen hatte, die
	Sonne versank langsam hinter den Bergen und ich ging langsam zurück in meine Heimatstadt
	immer diesen Traum in Gedanken, woher kannte ich diesen Orger und hatte dieser Traum
	etwas zu bedeuten???
</P>

