<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Shaiya Nightstorm<BR>
	<BR>
</P>

<P CLASS="P1">
Traurig saß ich an meinem 'geheimen' Platz am Lake Rathe und ließ mich von einer kühlen Brise
erfrischen. Die Sonne ging langsam unter und das 'sterbende' Licht versetzte mich in eine
Mischung aus Wohlgefühl und Schauder. Ich war ganz allein, keine Hektik um mich herum und
nur ein paar Tiere in meiner Nähe bereiteten ihr Nachtlager vor.<br>
<I>Zeit zum Nachdenken</I> dachte ich mir und versuchte mich an einem Resümee der letzten
Tage und Wochen. Viele Gerüchte verwirrten mich, hieß es doch <I>Wir Ranger werden langsam
aussterben</I>.<br>
Nichts sollte mehr richtig funktionieren, wenn wir - die Kämpfer Tunare's - nicht bald Hilfe
durch eine göttliche Macht erfahren würden ...
</P>

<P CLASS="P1">
Ich verstand dies alles nicht! Waren wir plötzlich so nutzlos? Wie konnten wir so lange
überleben, wenn wir wirklich so schlecht sind?<br>
Mein Gott, ja ... wir sind zierlich und klein und auch unsere Verteidigung ist beileibe
nicht so gut wie die manch anderer Weggefährten, aber muss ich mich denn nun schämen?
Haben nicht andere Streiter des Lichts auch ihre Fehler und Schwächen? Wer denkt an die 
Stärken des Einzelnen und haben wir Ranger mit unseren Gaben nicht schon oft anderen
beigestanden, wo jene hilflos waren?<br>
Meine Gedanken waren vernebelt bei so vielen Fragen und ich versuchte ständig diese zu
sortieren und zu analysieren um zu einer für mich akzeptablen Lösung zu finden.
</P>

<P CLASS="P1">
Plötzlich erschrak ich ... hörte ich doch leise Stimmen im Hintergrund, und es fiel mir
schwer, die genaue Herkunft zu orten, da die zuvor leichte Brandung immer stärker wurde.
Ich bemühte mich, die 'Eindringlinge meines Reiches' zu lokalisieren und plötzlich wurde
das Gespräch immer lauter. Schritte vernahm ich nicht und so glaubte ich, dass diese
ungewollten Gäste wohl eine Rast einlegten ... und ohne es gewollt zu
haben, wurde ich unfreiwillig Zeuge ihrer Unterhaltung:
</P>

<P CLASS="P1">
<I>Mann, war das ein Tag! Zweimal habe ich das 'Zeitliche gesegnet' und ohne die Hilfe
dieses Clerics würde ich wohl immer noch mit dem Gesicht am Boden liegen.<br>
<br>
Das kannst Du laut sagen, Luxxor. Ohne diesen Ranger wär das ganze wohl besser abgelaufen.
Diese Pfeife! Hält nix aus, aber zuhauen wie 'nen Grosser ....<br>
<br>
Och komm', Shurakai, seine Schuld allein war es nicht. Wo waren wir denn? Hätten wir mehr
auf diese Frösche eingeschlagen und uns ein wenig vorgedrängelt ...<br>
<br>
Wieso denn WIR? Er ist doch auch Tank ... gleiches Recht für alle ... har har.<br>
<br>
Denk doch mal nach ... er kann weniger aushalten als wir.<br>
<br>
Wo ist das Problem? Mich hat so ein 'Frooaaak' noch nie umgehauen. Lag wohl daran, dass
er nie Verstärkung holen konnte. Wurde 'gesnared' oder so ... irgendein Zauberspruch ...
frag' mal Shaiya, die ist Rangerin. Kann Dir das genauer erklären ...</I>
</P>

<P CLASS="P1">
'Huch' dachte ich ... das war ja mein Name. Hmm ... ich kannte diese beiden Recken
überhaupt nicht. Vielleicht mal im Kampf gesehen ... aber
es gab schon so viele Kämpfe. Wer kann sich da schon genau erinnern - leider!<br>
Nun versuchte ich das Gespräch mit noch mehr Aufmerksamkeit zu verfolgen und wünschte
mir ich könnte 'Silent Waves' zaubern!
</P>

<P CLASS="P1">
Luxxor schaute Shurakai immer noch verwundert an und pöbelte:<br>
<br>
<I>Was willst Du damit sagen? Ohne Ranger wären wir tot? Pahhh ... diese
Baumlutscher ... nutzlos und schwächlich sind sie!<br>
<br>
Luxx, bitte ... komm auf den Boden der Tatsachen zurück. Klar, können sie sich nicht so
gut verteidigen wie wir, aber hab' mal einen guten Ranger in der Truppe und Du wirst
sehen, mit deren Kombination aus Nahkampf und Magie geht vieles einfacher.<br>
<br>
Du spinnst ja ... Ranger sind ein Risiko für jede Gruppe und ein 'Fass ohne Boden' für
jeden Heiler ... und im Endeffekt sterbe ich dann, weil der Cleric keine Reserven mehr
für mich hat ... Na Klasse: My Life for a Ranger ... Luxxor begann lauthals zu lachen.</I>
</P>

<P CLASS="P1">
Ich wurde wütend! Genau das war es, was ich die letzten Tage immer und immer wieder
hörte. Zwar wusste ich, dass dies alles Vorurteile waren, aber ich erlag die letzte Zeit
auch immer mehr dem Gefühl, eine 'Last' zu sein. Nein, bislang lehnte mich noch niemand
ab, vielleicht weil ich wusste wie ich zu kämpfen hatte. Aber jede Heilung die ich bekam
wurde für mich zur Pein. Immer dachte ich daran, dass wir zuviel Heilkraft schlucken.
Mal jemand daran gedacht, wie viel Heilung so ein Frosch benötigt, wenn WIR zuhauen?
Grrrrrr ... ich war stinkig und in dem Moment das Paradebeispiel für eine 'Zicke' ...
'Keine Gnade für Niemanden' dachte ich und erschrak über meinen eigenen Unmut ... passte
es doch so wenig zu mir!<br>
<br>
Langsam beruhigte ich mich wieder und hoffte auf ein paar tröstende Worte von Shurakai
und 'spitzte' meine Ohren erneut:
</P>

<P CLASS="P1">
<I>LUXX </I>brüllte Shurakai los! <I>Schluss jetzt mit Deiner Polemik. Wir sind alle
Streiter des Lichts und keiner ist so vollkommen, dass er ohne den anderen
auskommen könnte. Lass uns GEGENSEITIG helfen und wir sind stark. Wir müssen zum
gegenwärtigen Zeitpunkt einfach umdenken und uns mehr auf deren Schwäche einstellen.
Überleg mal: Einer von uns in der 1. Reihe und ein Ranger (na ja ... ‚ne Rangeress wäre
mir lieber ... hihi) als 2. Tank!<br>
<br>
Du langweilst mich, </I>sagte Luxxor. <I>Es gibt noch andere Tanks als Ranger. Nimm
einen Paladin oder Monk ... was weiß ich ... ach, ich hab jetzt Hunger.
Kannst mal gucken, wo was zum Essen ist?</I><br>
<br>
Shurakai kramte in seinem Rucksack, aber er fand nichts.
<I>Sorry, unsere Vorräte sind alle ... gehen wir was jagen.<br>
<br>
Wie komisch sagte Luxxor. Du weißt doch, wie schwer wir uns tun, was zu finden ...</I>
</P>

<P CLASS="P1">
In dem Moment lachte ich laut auf! 'Diese Dussel' dachte ich und verließ mein Versteck, das
ohnehin durch mein herzhaftes Lachen verraten wurde.<br>
<I>Kann ich helfen?</I> fragte ich ziemlich selbstsicher, wohlwissend, dass mich meine
'Suchfähigkeit' selten enttäuscht hatte. 
Ironischerweise war es Luxxor, dessen Augen am hellsten strahlten, als er mich sah. Er
kam direkt auf mich zu und sagte voller Freude:
</>Dich schickt der Himmel. Wir sind hungrig und tun uns so schwer, etwas Essbares zu
finden. Entweder können wir nichts erspähen oder aber unsere
Opfer sind so schnell, dass wir das Nachsehen haben. Möchtest Du Dich uns anschließen?</I><br>
Shurakai grinste und warf mir gleichzeitig ein liebevolles Lächeln zu.
Ich konnte nicht umhin, Luxxor einen sarkastischen Seitenhieb zu geben: <I>Ich, die nutzlose,
schwächliche Baumlutscherin soll DIR bei Deiner barbarischen Nahrungsaufnahme helfen?</I><br>
Luxxor errötete, warf Shaurakai einen hilflosen Blick zu und brachte Worte wie <I>Oehem,
grrreemmg, ochfss und grummmpff</I> heraus.
</P>

<P CLASS="P1">
Ich sah Shurakai an und er nickte mir wohlwollend zu. Ein Zeichen dafür, dass sein bester
Freund wohl verlegen wurde und seine Worte überdenken wollte.
Zwar war ich nach wie vor verärgert über all diese Gerüchte und nicht ganz waren meine
Zweifel verflogen, aber ich dachte mir, dass dies vielleicht ein Anfang ist und noch
viele mehr erkennen werden, dass wir - die Kämpfer Tunares - mehr zu bieten haben, als 'Schwäche'. 
Freundschaft ist das, was uns ALLE am Leben erhält und die Akzeptanz, mit den Fehlern
des anderen zu leben ...
</P>

<P CLASS="P1">
to be continued ...
</P>

<p class="P0" ALIGN="CENTER"><I>Shaiya Nightstorm</I></p>

