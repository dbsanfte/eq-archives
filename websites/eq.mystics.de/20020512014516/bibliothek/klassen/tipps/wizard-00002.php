<TR><TD BACKGROUND="/pics/pageback2.jpg" ALIGN="CENTER" CLASS="P5">3. Level 3 und 4 - die Nutzlosen Levels</TD></TR>
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
</TABLE>

<P CLASS="P1">
	Irgendwann werdet ihr euch endlich auf dem 3 Level befinden. Cool nicht? Ja schon, bis 
	man merkt das die Monster die man alleine erledigen kann, fast keine Experience Points 
	mehr geben, und die Orc Pawns euch gefährlich nahe an den Tod bringen wenn ihr sie 
	angreift ( nur bei voller Gesundheit und Mana angreifen ) und noch dazu einenander - 
	sehr zu eurem Leid - zu Hilfe eilen, da wird's vielen Leid und sie wechseln den Char. 
	Wenn man doch bis Level 4 durchhält bekommt man endlich die neue, langersehnte Zaubern 
	der 2. Kreis. Wenn ihr genug zusammenhabt, kauft sofort Fade ( beamen macht spass ), 
	Root und Schock of Fire. Falls ihr nur 2 kaufen könnt, dann SF und Fade, Root ist noch 
	nicht so Lebenswichtig in diesem Stadium. Trainiert 1 Practice point in Meditation, ihr 
	werdet es nicht bereuen. Mehr als 1 Punkt braucht es nicht, diese Skill steigt schnell.
	Und jetzt habt ihr wieder ein Problem.<br>
	Jetzt könnt ihr auch diese lästige Orc Pawns locker schlagen, doch die bringen auch 
	nicht mehr soviel ExP wie früher. Und lange hält euer Mana Vorrat auch nicht an wenn 
	ihr Feuerbälle rumschleudert. Zwar müsst ihr nicht mehr solange warten bis euer Mana 
	wieder aufgefüllt ist - dank Meditaion - doch alleine kommt ihr fast nicht mehr weiter, 
	und die meisten Gruppen brauchen keine lvl 4 Wizards, da wir noch nicht lohnenswerte 
	Schäden zufügen können. Doch ihr müsst nur durchhalten, bald wird es wieder besser.
</P>

