<TR><TD BACKGROUND="/pics/pageback2.jpg" ALIGN="CENTER" CLASS="P5">4. Level 5 und 6 - jetzt geht was - und ein paar Tips zum Kampf</TD></TR>
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
</TABLE>

<P CLASS="P1">
	Wenn ihr es bis hier durchgestanden habt, dann habt ihr ( vorerst ) das schlimmste 
	überstanden.<br>
	Den ab Level 5 habt ihr endlich mehr Mana ( 137 wenn ihr alles in INT gesteckt habt ), 
	und ihr könnt euch langsam dran machen, bei Gruppen auch nützlich zu sein. Nämlich auf 
	Orc Jagd ( hehe ). Inzwischen solltet ihr auch eine volle Satz Cloth armor haben, und euch 
	mit eurem Stab zumindest wehren. Sucht euch eine Gruppe, und ab geht's.<br>
	Noch ein paar Bemerkungen zu den Taktiken, die ihr anwenden solltet:<br>
	Ihr seid nicht die Tanks. Denkt daran. Die Versuchung ist gross, pausenlos Feuerbälle 
	auf den Gegner zu werfen. Macht es nicht.
</P>
<OL>
	<LI>
		Das hat die schlechte Folge, das sich der Monster auf euch stürzt, da ihr euer Tank hinter euch liegen lässt im Schaden austeilen.
	<BR></LI>
	<LI>
		Neu eintreffende Monster gehen eher auf euch los, aus oben erwähntem Grund.
	<BR></LI>
	<LI>
		Ihr habt die wenigste Hit Points. Der Tank kann noch lachen und 18 Trefferpunkte wegstecken - ihr nicht. 
	<BR></LI>
	<LI>
		Ein Schlag unterbricht ziemlich sicher euer casten. Nicht gut, oder?
	<BR></LI>
	<LI>
		Ein rennender Wizard ist ein nicht zaubernder Wizard ( schlecht Wizard, Platz! Mach Platz!...Ihr wisst was ich meine ). Genug gesagt.
	<BR></LI>
	<LI>
		Manaverbrauch. Ihr verbratet euer ganzes Mana auf ein lvl 4 Orc Centurion ( /con Blau ). Und dann wird euer Gruppe von 2 lvl 5/6 und ein lvl 8 Orc Centurion ( /con Gelb und Rot ) und ein Orc Oracle lvl 5 angegriffen. Schnell wollt ihr ein Feuerball abfeuern - Ooops, insufficient mana for this spell...Wenn ihr schnell seid, überlebt ihr, der Tank wahrscheinlich nicht.
	<BR></LI>
</OL>

<P CLASS="P1">
	Ihr habt einen Mund. Benutzt es zum reden. Sagt es wenn ihr neue Gegner auf euch zurasen 
	seht. Teilt es den anderen mit wenn ihr fast kein Mana mehr habt, bevor sie ein neues 
	Ziel angreifen. Ruft um Hilfe wenn ihr es braucht, usw. Ihr könnt sehen was ich meine. 
	Ein gutes Team redet während eines Kampfes immer, und es macht ausserdem ungeheuer viel 
	Stimmung und Spass ( wenn ihr mal erlebt, wie es ist in einem guten Team zu operieren, 
	so werdet ihr sehen was ich meine ;).<br><br>
	Die Gruppe verlässt sich darauf, das du weisst, wie du dir dein Mana einteilen müsst. 
	Sterben dauernd Mitglieder weil du OOM ( Out of Mana ) bist, geht das Vertrauen der 
	Gruppe in euch flötten. Und ein schlechter Ruf verbreitet sich schneller als ein guter.<br><br>
	Schiesst nur gelegentlich, aber dafür wenn es zählt. Z.B. euer Tank ist bei etwa 
	40% HP, euer Gener bei 80, die Heiler schreien OOM und es ist WEIT bis zum ausgang. 
	Da ihr bis jetzt nicht soviel unternommen habt gegen den Monster, ist sein Hass auf 
	euch relativ gering, und euer Mana Vorrat ist fast unberührt. Nun kommt eine der 
	Glanzmomente im Leben eines Wizards. Den nun könnt ihr hemmungslos nuken, und glaubt mir, 
	sobald das Viech tot ist, wird sich niemand in der Gruppe in Zukunft darüber beschweren, 
	das Wizards nur nutzlos herumstehen bei einem Kampf ;). Treffender gesagt, wird sind die 
	Unterstüzung, nicht die Hauptstreitmacht. Wenn euch eine solche Rolle nicht gefällt, dann 
	werdet ihr weniger Freude am Wizard haben, auch in den höheren Levels. Wir können zwar in 
	kurzer Zeit unglaubliche Mengen an Schaden austeilen ( v.a. in den höheren Levels ) aber 
	wir werden nie sehr viel einstecken können.<br><br>
	OK, soviel zum Thema bis und mit Level 6. Und was immer ihr macht, geht noch nicht in 
	Crushbone selbst, da seid ihr noch zu schwach. Bleibt vor dem Ausgang, da wird es oft 
	happig genug.<br><br>
	Wie es weitergeht, muss ich es erst noch selbst besser durchspielen, dann werd ich es 
	auch in schriftliche Form niederlegen. Falls ihr euch mal auf dem Quellious Server 
	befindet, so besucht mich mal. ( Kommentare und Anregungen/Verbesserungen bitte an 
	<a href="mailto:Likimeya@usa.net">Likimeya@usa.net</a> schicken, Danke )
</P>

