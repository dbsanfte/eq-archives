<TR><TD BACKGROUND="/pics/pageback2.jpg" ALIGN="CENTER" CLASS="P5">1. Charaktererschaffung</TD></TR>
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
</TABLE>

<P CLASS="P1">
	Man kann darüber streiten, welche Rasse die besten Wizards abgeben, aber diese kleine 
	Einführung bezieht sich Hauptsächlich auf der Hochelfische Variante des Wizards.<br> 
	Bei der Charaktererschaffung sollte man alles auf INT setzen, den weniger INT = 
	weniger Mana ( Mana = (INT/5+2)*Lvl ), und ein Wizard ist noch mehr als andere Klassen 
	ohne Mana hilfslos. Die restlichen 5 Bonuspunkte setzt man dann auf Stam ( als ob man 
	eine Wahl hätte... ). Danach geht es ab nach Norrath.
</P>

<TABLE BORDER="0" CELLPADDING="0" CELLSPACING="0" WIDTH="100%">
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
<TR><TD BACKGROUND="/pics/pageback2.jpg" ALIGN="CENTER" CLASS="P5">2. Aller Anfang ist schwer</TD></TR>
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
</TABLE>

<P CLASS="P1">
	Nun steht Ihr auf der Strasse in Felwithe um, mit nichts ausser 5 Brot, 5 Wasser, ein 
	Brief für euer Gildenmeister, ein Schock of Frost und Minor Shielding scroll und euer 
	Book of Discord. Wenn Ihr vorhabt,irgendwann PK zu werden, so legt das Buch in euer 
	Bankfach ab, sonst könnt ihr es getrost zerstören. Schreibt euer Scrolls auch gleich 
	in euer Spellbook ein, nur für den fall das ihr aus irgendeinem Grund sterben solltet. 
	Danach macht ihr euch auf die Suche nach euer Gildenmeister ( seine Name ist in dem Brief 
	erwähnt ). Nein, ich sag nicht wo er ist, es ist lehrreich in der Stadt rumzulaufen 
	damit man mal alles kennenlernt.<br> <br> 
	Nachdem Ihr den Zettel abgegeben habt, zieht Ihr den Guild Tunic an und memoriert die 
	ersten beiden Sprüche. Auch der Dagger sollte ausgerüstet werden, den bei dem was 
	kommt, zählt jede mickrige Schadenspunkt. Dann geht es ab auf Monsterhatz. Auserhalb 
	der Stadt solltet Ihr euch vorerst nur an Fledermäuse halten, den - im Gegensatz zu 
	den anderen Viecher in der Umgebung - die werden sich nicht gegenseitig unterstützen. 
	Übt euch darin, die eure Zaubersprüche zwischen die Schläge der Fledermaus zu casten, 
	und benutzt auch euren Dolch. Macht weiter bis ihr Level 2 erreicht, dann könnt ihr auch 
	auf einzelne Wespen und Skelette ran machen. Vorsicht wenn 2 oder mehr von diesen Monster 
	( also 2 Wespen od 2 Skelette ) in Sichtweite von einander sind, den sie WERDEN euch zu 
	zweit angreifen, und das wird euch nicht sehr gut bekommen. Ebenfalls sollte man sich in 
	Acht vor den bewaffnete Skelette nehmen, den sie schlagen um einiges härter zu als die 
	normale skelette. Geht nie zu weit weg von den Wachen, den die sind eure besten Freunde. 
	Scheut euch nicht zurückzurennen wenn ihr euch plötzlich in eine schlechte Situation 
	befindet.<br> 
	Fängt jetzt schon an Geld zu sparen für die Zaubersprüche, die ihr ab level 4 
	benutzen könnt. Von den Zaubern der 1. Kreis ist nur noch Numbing Cold noch 
	lohnenswert zu kaufen, falls man sich mal von Gegnern umringt findet. Und sobald wie 
	möglich solltet ihr ein Skelett mit Stab umnieten, denn der Stab ist eine der besten 
	Waffen, die ihr momentan kriegen könnt. Und kauft euch hin und wieder ein Stück cloth 
	armor, der beste - da einzige Schutz die ihr kriegen könnt, solange ihr in Greater 
	Faydark sind.
</P>

