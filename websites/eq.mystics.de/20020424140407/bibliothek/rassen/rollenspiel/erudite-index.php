<P CLASS="P1">
	Die Menschen glauben, die Krone der Schöpfung zu sein. Nun, wir Eruditen WISSEN, daß wir 
	es sind. Das ist keine Arroganz, sondern eine simple Tatsache. Während sich die Menschen 
	mit ihren kleinlichen Zänkereien, Fehden, Orgien und wissen die Götter was noch die Zeit 
	stehlen, nutzen wir sie um unsere Studien voranzutreiben. Nur die geballte Macht des Wissens 
	sichert uns unsere überlegene Stellung in dieser Welt. Es ist also nicht verwunderlich, daß 
	fast jeder unserer Rasse ein Zauberkundiger ist. Die Magie ist eine sehr mächtige und 
	angemessene Weise, sein Wissen umzusetzen und wird von den meisten Wesen (ich nenne nur 
	mal die Zwerge als Beispiel) sehr unterschätzt. Aber, so frage ich Dich, was will mir so 
	ein Raufbold mit seiner Axt antun, wenn ich ihn schon von weitem mit einem Feuerball 
	grille oder meinen untertänigen Wesen erlaube, ihn zum Mittag zu verspeisen? Nein, die 
	Narren, die sich nur auf ihr Schwert verlassen, sind keine Bedrohung für uns. Viel 
	gefährlicher ist es, Magie in unkundigen Händen zu finden, wie bei den hochnäsigen Elfen, 
	die vor Jahrhunderten aufhörten ihr Wissen zu mehren, anstatt den Vorsprung zu nutzen 
	den ihr Volk hatte. Auch kam mir zu Ohren, daß sich neuerdings diese kleinen Irren - wie 
	nennen sie  sich doch gleich, Halblinge Gnome..., na egal - mit Zauberei befassen, wo soll 
	das hinführen, sind zu albern ein ordentliches Leben zu führen aber bilden sich ein, die 
	Kräfte der Magie beherrschen zu können. Was nun die Menschen betrifft, wären sie in der 
	Lage über ihren Horizont zu sehen, könnten sie es weit bringen, aber dann wären es ja 
	Eruditen. Eine Ausnahme bilden bei allen Rassen die Schamanen. Ihre Kräfte stützen sich 
	auf Beobachtung und überliefertes Wissen. Außerdem sind ihnen die Geister der Natur zu 
	Diensten. Da sie zudem geschickte Kämpfer sein sollen, sollte man sie nie unterschätzen. 
</P>
	
<P CLASS="P1">
	Wie setzen wir unsere Kräfte ein? Nun, da wären am Anfang die Magier. Sie machen 
	Unsichtbares sichtbar und haben ganz ordentliche Zauber drauf, um ihre Haut zu retten. 
	Häufig begegnete ich aber auch Zauberern, welche die Schulung ihrer magischen Fähigkeiten 
	ganz dem Kampf widmeten und sehr verheerende Sprüche entwickelten. Doch hütet Euch, oh 
	Unwissende, vor den Nekromanten. Sie erwecken tote Gebeine zu unnatürlichem Leben und 
	können Euch sogar erkranken lassen und vergiften. Ihr solltet ihnen am besten aus dem Weg 
	gehen. Nützlich erweisen sich auch die Enchanter mit ihrer Fähigkeit Dinge zu verzaubern 
	und Verwunschenes zu identifizieren. Zu einigen von uns haben sogar die Götter gesprochen, 
	weshalb diese als Kleriker ihr umfangreiches Wissen ganz in den Dienst einer Gottheit 
	stellen. Die Götter wissen schon, wo sie fähiges Bodenpersonal herbekommen. ;-) Und da es 
	selbst in einem Volk der Denker hin und wieder kampfeslustige Krieger gibt, haben einige 
	beschlossen diesen Dienst als Paladin zu leisten.
</P>
	
<P CLASS="P1">
	Nun noch ein paar letzte Worte: Obwohl uns auf Grund unserer Intelligenz kaum jemand 
	das Wasser reichen kann, ist es doch mitunter sinnvoll, sich mit ein paar "Schlägern" 
	zusammen zu tun. Hat sich ein Erudit einmal entschlossen einer Party beizutreten, wird 
	er diese mit all seiner Macht unterstützen - und seine Macht ist gewaltig. Ein Erudit 
	achtet jeden der nach mehr wissen strebt, aber er tolleriert auch die reinen Kämpfer. 
	Seine Verachtung ziehen sich aber Diebe und Bogenschützen zu, da diese es offensichtlich 
	zu nichts gebracht haben und deshalb ihrem mehr oder minder schmutzigen Handwerk nachgehen 
	müssen. Wen er einmal als würdig empfunden hat, sein Freund zu sein, dem ist er ein guter 
	Freund bis in den Tod. Der Feind eines Eruditen möchte ich nicht sein... :-)
</P>

<P CLASS="P0">
	<I>Vagnard aka Heinrich Kemmler</I>
</P>

