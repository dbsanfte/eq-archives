<P CLASS="P5">
	Wie spiele ich einen Ranger
</P>

<P CLASS="P1">
	Die nachfolgende Darstellung stellt natürlich nur meine Meinung dar. Sie
	erhebt keinen Anspruch auf Korrektheit oder Vollständigkeit. Ich lasse
	bei der nachfolgenden Betrachtung die Religion vollkommen außer acht,
	da sie meiner Meinung nach in jedem Falle dem Spiel des Rangers und dem
	Spiel allgemein (Tötungszwang zum Erfahrungsanstieg) widerspricht.
</P>

<P CLASS="P1">
	Der Ranger ist dem Druiden sehr nahe. Er hat nicht seine magischen
	Fähigkeiten, dafür jedoch höhere kämpferische Fertigkeiten. Jedoch sind
	beide in einer sehr starken Liebe zur Natur vereint. Es ist dem Ranger
	aber durchaus bewußt, daß in der Natur aus Gründen der
	Nahrungsbeschaffung permanent getötet wird. Dies ist für den Ranger auch
	durchaus der normale Lauf der Dinge. Dies akzeptiert und nutzt er. Er
	tötet ohne Gewissensbisse wenn er Hunger hat. Jedoch zieht er
	pflanzliche Nahrung der tierischen Nahrung vor. Er wird niemals aus
	niederen Gründen ein Lebewesen töten. (Hier besteht für mich das größte
	Problem. Ich muß auch Tiere töten  um Erfahrung zu sammeln. Ich habe für
	mich einige Tiere auf die "Schutzliste" gesetzt um diesem Gedanken
	gerecht zu werden). Der Ranger sollte in jedem Falle alle Bären schützen
	und da er in späteren Leveln Wolfsprüche erhält scheint er eine
	besondere Affinität zu Wölfen zu haben. Also werde man dieser gerecht.
	Somit sind Holly Windstalker und Cros Treewind automatisch Freunde eines
	Rangers und deren Taten bewundernswert.
</P>

<P CLASS="P1">
	Der Ranger ist stark introvertiert. Er meidet die Nähe zu anderen
	Menschen. Er mag sich durchaus einer Gruppe anschließen, jedoch wird er
	die Nähe zu anderen nicht lange aushalten. Dann braucht er wieder seine
	Ruhe. Der Ranger spricht nicht viel. Er mag die Natur und hört dieser
	lieber zu als selber zu reden. Leute die permanent reden oder sich mit
	dem Offensichtlichen plagen sind ihm ein Greuel.Vom Spielgedanken ist
	der Ranger durchaus ein Einzelgänger.<br>
	Schließt der Ranger sich einer Gruppe an, so übernimmt er die Rolle des
	Fährtenlesers in dieser Gruppe. Dafür ist er da. Stets rennen dann alle
	hinter ihm hinterher. Der Ranger verliert niemals die Orientierung (haha
	(Lernt die Gegend auswendig und benutzt Sense)). Er findet stets zum
	sicheren "Hafen" zurück. Dies macht den Ranger zu einem guten Ranger.<br>
	Der Ranger betrachtet das Leben seiner  Gruppenmitglieder als ebenso
	heilig wie jedwedes Leben. Er wird also stets eine Heilung seiner
	Kameraden ohne zu zögern durchführen. Auch im Kampf ist der Ranger stets
	seinen Kameraden gegenüber hilfreich. Um Schaden von ihnen fernzuhalten
	stürzt er sich auch mal in die erste Reihe und lenkt die Angriffe des
	Gegners auf sich.<br>
	Sollte die Gruppe aber Tiere töten, die dem Ranger schützenswert
	erscheinen, so wird er die Gruppe ohne großes Federlesen verlassen.
</P>

<P CLASS="P1">
	Anderen Rassen oder Berufen gegenüber, hegt der Ranger keinen Groll. Die
	Taten jeder einzelnen Person bestimmen sein Verhalten gegenüber dieser
	Person. Hier allerdings spielt die Religion eine wichtige Rolle.<br>
	Für mich allerdings hat der Ranger keine Religion, da die Natur  jede
	Religion ersetzt. Natur oder die Liebe zu ihr ist für meinen Ranger
	gleichbedeutend mit Religion oder mit Gott. Somit hat jede Person die
	sich aus dem Blickwinkel meines Rangers korrekt verhält die Gleiche
	Religion wie der Ranger selber. Diese Religion bietet EQ allerdings
	nicht. Auch keinen Gott ,der diese Idee verkörpert. "Mother of All"
	kommt dem Gedanken nahe verbietet aber im Grunde jegliches Töten ohne
	gegen die eigene Religion zu verstoßen.
</P>

<P CLASS="P1">
	Die Waffen des Rangers sind die Einhandwaffen. Hierbei wird er aus
	praktischen Gründen stets eine Axt den Schwertern vorziehen. Zum
	Feuer machen hackt er immer sein eigenes Holz, sofern er kein totes Holz
	findet.<br>
	Zum Töten oder besser gesagt zur Nahrungsbeschaffung nutzt der Ranger
	den Bogen.<br>
	Obwohl der Ranger Chainmail tragen darf, mag er diese Art der Rüstung
	nicht. Er zieht die Lederrüstung oder Studded der harten, schweren
	Chainmail vor. Die Chainmail macht ihn unbeweglich und widerspricht
	einfach seinem Bewegungsdrang (sehr schwer durchzuhalten).
</P>
	
<P CLASS="P1">
	Der Ranger ist und muß autark sein, da er vorwiegend in der Natur lebt
	und Städte nur in absoluten Ausnahmefällen besucht. Er ist in der Lage
	seine Kleidung selber zu erstellen. Ebenso beherrscht er die Grundzüge
	der Schmiedekunst. Er ist durchaus in der Lage seine Waffen zu schärfen
	und funktionsfähig zu erhalten. Pfeile erstellt sicher der Ranger seit
	urdenklichen Zeiten selber her. Fischen stellt für den Ranger überhaupt
	kein Problem dar. Eine Angel führt er stets mit sich. Auch ist er in der
	Lage seine erbeutete Nahrung äußerst schmackhaft zuzubereiten. Er ist
	kein Meisterkoch aber es ernährt ihn.
</P>

<P CLASS="P1">
	Der Ranger hat keinerlei Beziehung zum Geld, da die Natur ihm alles
	bietet was er benötigt. Geld ist für ihn Tand und eigentlich gar nicht
	verständlich wofür so ein "Müll" nützlich sein soll. Ihm geht jegliches
	Talent zum Handeln ab. Er zahlt was er hat sofern er etwas käuflich
	erwirbt.
</P>
	
<P CLASS="P0">
	<I>Euer Lyanfar.</I>
</P>

