<P CLASS="P1">
	Invravision ist ungemein wichtig, da man sich viel in dunklen Dungeons herumtreibt und 
	keinen Spell bekommt der einem so was verleiht. Damit fallen meiner Meinung nach der Human 
	und der Barbar weg. Der Zwerg hat viel zu wenig Agilität und fällt somit auch weg :) Über 
	bleiben also Gnom, Halbling, Halbelf, Waldelf und Dunkelelf. Von denen hat der Halbling die 
	besten Kämpferstats, doch er ist etwas doof und hat somit Probleme bei den Skills. Die 
	klügsten Rassen sind Dunkelelf und Gnom, doch ihre Kampfstats sind nicht gerade berauschend. 
	Als Halbling, Wald und Halbelf hat man den Vorteil der guten Rasse und gute Stats, doch ich 
	persönlich finde Halblinge lächerlich (sorry, hehe, aber wer allein schon mal die 
	Hintergrundmusik in Rivervale gehört hat oder sich ihre Füße genauer angesehen hat 
	weiß was ich meine), weshalb meine Empfehlung letztlich der Dunkelelf, Waldelf (hat von 
	Natur aus zusätzlich forage), Halbelf  und Gnom sind, wobei der Dunkelelf der coolste 
	und intelligenteste (wichtig!!!) von denen ist. Ausserdem sind Dunkelelf Rogues in ihrer 
	Heimatstadt Neriak sehr beliebt weshalb sie keine Probleme haben bei allen Händlern 
	einzukaufen.
</P>

