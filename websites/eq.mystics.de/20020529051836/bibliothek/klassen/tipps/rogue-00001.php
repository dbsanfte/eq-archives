<P CLASS="P1">
	Rogues sind die Klasse für Leute denen Krieger zu eintönig und Magier zu .. naja ... 
	schwächlich *g* sind, bzw. man sich nicht mit Spellgefizzel und Manaabhängigkeit herumärgern 
	will.<br><br>
	Leute die einen Rogue spielen wollen sollten Spaß an Folgendem haben: Lautlos an Feinde 
	heranschleichen, hinterhältige Anschläge begehen, viel kämpfen, und viel viel weglaufen :).<br><br>
	Als Rogue ist man längst nicht so gut im Kampf wie ein reiner Krieger. Man hat viel 
	weniger HP (gut 1/3 bis 1/4), ist schwächer und darf kein Plate tragen (doch davon kann 
	man auch als Krieger unter Level 30 bis 50 eigentlich finanziell gesehen nur träumen). 
	Man hat auch keine Spells mit denen Zauberer ihrerseits solche Schwächen ausgleichen können. 
	Doch all diese Schwächen machen Rogues vielfach wieder wett durch ihre schier unglaubliche 
	Agilität und ihre nicht enden wollende Anzahl an Skills. Trotzdem ist ein Rogue in einer 
	Gruppe besser aufgehoben als Solo, da ihm seine HP sonst selbst bei manchen blauen Monstern 
	einen Strich durch die Rechnung machen.<br><br>
	Die HP die man bekommt (bei Stamina 65 jedes Level 22) reichen aber aus, um gerade noch den 
	Angriff eines Wizards gleichen Levels zu überleben (naja, manchmal) und ihn dann 
	niederzustechen. Er kann zwar auch kämpfen doch ihm fehlt parry, dodge, dual wield, 
	double attack und die hohe AC. Hehe. Solange der Wizzi also kein Root hinbekommt, 
	was jedoch so gut wie immer klappt, kann man ihn auch ohne großen Schaden vernichten. 
	Gegen die 2 anderen reinen Kämpferklassen Warrior und Monk wird der Rogue durch seine 
	hohe Agilität weniger oft von den harten Schlägen getroffen und kann seine eher schwachen 
	Treffer durch hohe Dexterity öfter landen, was seine geringeren HP wieder ausgleicht. Aber 
	als Rogue sollte man es auf solche Zweikämpfe erst gar nicht ankommen lassen sondern seiner 
	Klasse gerecht werden und einen hinterhältigen Angriff starten. Backstabbing eignet sich 
	dafür ungemein, da man, wenn man damit von hinten ein Opfer trifft, 3 bis 4 fachen Schaden 
	machen kann. Am besten tut man dies wenn ein Opfer gerade mit einem Monster zugange ist. 
	Man schleicht sich an, wobei er die Schritte nicht hört, und backstabbt. In dem Kampfgewirr 
	in dem er sich befindet bekommt er die Treffer erst mit wenn er merkt, daß seine HP 
	plötzlich im Keller sind und er tot ist :).
</P>

