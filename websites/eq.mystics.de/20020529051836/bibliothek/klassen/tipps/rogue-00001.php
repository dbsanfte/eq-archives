<P CLASS="P1">
	<B>Stärke</B> ist nicht so wichtig für den Rogue wie für andere reine Kämpferklassen, da 
	man nicht seltene, starke Hiebe austeilt sondern schnelle, eher schwache Stiche und auch 
	kein Plate tragen muß, welches viel wiegen würde, sondern nur Chain oder Banded Armor. 
	Daß einzige was mich persönlich an meiner geringen Stärke stört ist die etwas kleinere 
	Menge an Beute die man schleppen kann ohne überladen zu sein. Doch auch überladen ist man 
	noch so schnell wie andere Klassen ohne so hohe Agility.<br>
	Minimum sollte 65 sein, nicht mehr als10 reinstecken.<br>
	<B>Stamina</B> ist auch nicht wichtig für nen Rogue. Ich hab mal Rogues mit verschiedener Stamina 
	gestartet und die unterschiede der HP waren minimal. Für 10 Punkte mehr in Stamina bekommt 
	man pro Level 1 HP mehr als sonst. Also in Level 50 hat man 50 HP mehr als normal. Wow *g*. 
	Bei dem Schaden den Wizzis da machen, so ca. 1100, sieht man wie lachhaft das ist. Also 
	keine Punkte rein, wäre Verschwendung.<br>
	Minimum je nach Rasse, nichts reinstecken.<br><br>
	<B>Agility</B> ist der wichtigste Stat für diese Klasse. Sie bestimmt den Bonus an AC den man 
	bekommt (mit 110 Agility bekommt man wenn man ein AC 3 Teil anzieht 5 AC dazu!!!) und 
	bestimmt die Laufgeschwindigkeit, welche man als Rogue öfters mal in Anspruch nimmt 
	(ua. hat man ja kein Gate, aber es gibt noch andere Gründe).<br>
	Minimum 100, besser 110+.<br><br>
	<B>Dexterity</B> ist der 2t wichtigste Stat für einen Rogue. Sie bestimmt die Treffsicherheit 
	und auch etwas die Fähigkeit in den Rogueskills wie z.B. dem Entschärfen von Fallen. Die 
	Treffsicherheit mit Fernwaffen ist besonders davon abhängig, welche man ohne Spells zum 
	anlocken von Feinden oft brauchen wird.<br>
	Minimum 90, besser 100+.<br><br>
	<B>Wisdom</B>. Dazu muß ich glaube ich nichts sagen :) Unwichtig ohne Ende<br><br>
	<B>Intelligenz</B> scheint auf den ersten Blick unwichtig, doch als Rogue ist man von 
	seinen Skills abhängig. Deren Steigerung wird von der Intelligenz bestimmt und für 
	einen Rogue ist nichts schlimmer als mit den Skills hinter seinem Level herzuhinken. Daher 
	empfiehlt sich hier besonders der DE, der die höchste Intelligenz aller Roguerassen hat :)<br><br>
	<B>Charisma</B> ist unwichtig, da die Beliebtheit kaum vom Charisma sondern vielmehr von 
	der Gottheit abhängt.
</P>

