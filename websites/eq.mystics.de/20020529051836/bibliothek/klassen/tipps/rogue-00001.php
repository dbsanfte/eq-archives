<P CLASS="P1">
	Die Gottheit ist weitaus wichtiger für die Einstellung von NPCs euch gegenüber als die 
	Rasse, Klasse und das Charisma. Z.B werden gottlose Dunkelelfen von den Qeynos Wachen 
	nicht angegriffen, wohingegen Menschen mit einer bösen Gottheit KOS sind. Es reizt zwar 
	aus rollenspielerischen Gründen einen fiesen Gott als Rogue zu haben, doch dies sieht 
	euch eh keiner an und interessieren tuts auch keinen, außer die NPCs natürlich. Also tut 
	euch selber einen Gefallen und nehmt agnostic als Gottheit.
</P>

