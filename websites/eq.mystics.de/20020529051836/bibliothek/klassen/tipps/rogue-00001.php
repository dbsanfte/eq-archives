<P CLASS="P1">
	Da alle Klassen ziemlich gleich mit einer Waffe und nem Billighemd oder Robe vom 
	Gildenmeister anfangen gibt es dazu auch nichts weiter für den Rogue zu sagen. 
	Durch die geringen HP sollte man jedoch versuchen seine AC hochzubekommen und dies 
	macht man durch bessere Rüstung. Daher empfehle ich als tradingskill ab Level 10 
	(vorher reicht das Geld eigentlich nicht) auch blacksmithing, da man sich damit günstig 
	(naja, wer's schon mal gemacht hat weiss wie relativ das Wort "günstig" hierbei ist :)) 
	banded armor machen kann. Als Rogue kann man mit seinem Geld ja eh nichts anfangen außer
	Waffen und Rüstung zu verbessern. Als Waffen reichen bis Level 15 eigentlich die bei 
	stärkeren Snakes zu findenen oder für 5 Gold bei Spielern zu kaufenden Gaint Snake Fangs. 
	Dann sollte man sich schon mal nach Waffen umgucken deren Grundschaden (und vor allem der 
	Preis) höher ist, wie z.B. fine steel Waffen (piercing selbstverständlich).<br>
	Da man ja zur Zeit nicht von Spielern klauen kann und die Sachen im Laden ziemlich teuer 
	sind, nimmt es euch als Rogue keiner übel (naja, jedenfalls solange ihr eure eigene Rasse 
	in Ruhe lasst) wenn ihr euch schöne Rüstungsteile von anderen Spielern mehr oder minder 
	gewaltsam "ausleiht" :). Wie schon gesagt hat backstabbing bei solchen "Verhandlungen" 
	seine Vorteile. Und sofern ihr euch die Sachen von Halblingen besorgt juckt das eh keinen *g*.
</P>

