<P CLASS="P1">
	<B>Level 1:</B><br>
	<I>Piercing</I>: Sollte auf jeden Fall der Hauptwaffenskill sein, da ohne ihn Backstabbing nicht funktioniert und Piercing im Endeffekt die besten Waffen bietet. Am Anfang sollte man keine Punkte reinstecken da man diese später noch braucht bei all den vielen Skills und man als Darkelf so Intelligent ist, daß Piercing nur so hochflutscht :)<br>
	<I>Hand to Hand</I>: Neben Piercing mit hochziehen, da man sich somit nach dem Tod auch noch verteidigen kann und es auch Monster gibt die einen entwaffnen können.<br>
	<I>Hide</I> (Nur beim DE, alle anderen erst Level 7): Wichtig! Damit kann man sich vor Feinden verstecken oder Opfern auflauern. Leider bekommt man keine Nachricht ob der Skill beim Anwenden funktioniert hat oder nicht, also ob der Feind einen nun sieht oder nicht. Wenn man es dauernd anwendet steigt es regelmässig weshalb keine Punktverschwendung (mehr als 1) notwendig seien sollte.<br>
	<I>Sneak</I>: Erlaubt es einem sich an Nicht-Untote und Nicht-Geruchsversierte Monster (z.B. Gnolle, Bären gehen merkwürdigerweise weshalb ich mir mit dem Geruchssinn nicht so sicher bin) heran oder vorbei zu schleichen. Dies erlaubt einem in eine günstige Backstabbing Position zu gelangen oder unbemerkt zu stehlen. Man wird beim sneaken auch von Spielern nicht gehört, allerdings gesehen. Man kann sich aber an NPCs vorbeischleichen, die Unsichtbare sehen, da man ja nicht unsichtbar ist und trotzdem Geräusche macht, sondern sich unbemerkt und geräuschlos bewegt.<br>
	<I>Archery</I>: Wichtig, da man keine Spells bekommt um ein Monster anzulocken. Wenn es wegrennt sollte man jedoch eher backstabben als mit dem Bogen zu schießen, ist effektiver. Außerdem kann man so auch Spellcaster beim Zaubern unterbrechen die einen gerootet haben!<br><br>
	<B>Level 4:</B><br>
	<I>Dodge</I>: Ein sehr nützlicher Kampfskill, steigt sehr schnell von alleine weshalb ein Punkt reicht.<br><br>
	<B>Level 6:</B><br>
	<I>Pick Lock</I>: Angeblich wollen die EQ Macher mehr Türen einbauen die verschlossen sind, doch in meinem 14ten Level hab ich bisher erst eine gefunden (in Befallen). Daher drängt es nicht den Skill schnell hochzubekommen. Jedoch war ich erst in der Lage diese eine Tür zu öffnen als ich den Skill beim Trainer auf 11 gesteigert hab. Mit Pick Lock ist man sehr nützlich für Partys oder sich selber, sollte man sich in Dungeons herumtreiben.<br><br>
	<B>Level 7:</B><br>
	<I>Pick Pocket</I>: Ein witziger Skill. Leider kann man bisher nicht von anderen Spielern stehlen, was die EQ Machen aber ändern sollten (auch im PvP Modus kann man es nicht). Den Skill kann man nur an Monstern anwenden, die Hosen tragen *g*. Stimmt echt, sonst klappt es nicht. Wieviel man klaut kommt ganz auf das Monster an. Also bei Gnollen so 10 bis 35 Copper und bei Halblingen (in Nektolus Forest) bis zu 1 Gold. Bei anderen Monstern weiß ich es nicht, doch angeblich bis zu 1pp auf einmal. Man kann auch Gegenstände bekommen, die das Monster im Inventory trägt, wie z.B. Gnollzähne, doch was man bekommt ist immer Zufall. Bei diesem Skill kommt oft auch die hohe Laufgeschwindigkeit zum Einsatz, da es vorkommt, daß ein Monster euch beim Klauen erwischt und dies dann laut herumschreit. Damit aktiviert es alle seine Freunde in der Nähe, was manchmal recht schmerzhaft enden kann. Doch Rogues sind meist schneller als die Monster. Steigt bei regelmäßigem anwenden recht schnell.<br><br>
	<B>Level 8:</B><br>
	<I>Sense Traps</I>: Steigt auch ohne daß eine Falle in der Nähe sein muß. Ich persönlich hab bisher noch keine Falle gefunden (ausgenommen falscher Böden, die jedoch nicht als Fallen zählen). Doch da die meisten Fallen, die existieren, tödlich seinen sollen ist eine Party gut beraten einen Rogue mitzunehmen wenn sie in einen Dungeon geht.<br><br>
	<B>Level 10:</B><br>
	<I>Backstabbing</I>: Endlich, das ist DER Skill überhaupt. Wenn man hinter einem Monster steht macht man damit 2, 3 oder 4 fachen Waffenschaden (je nach Skillhöhe) was so manche Billigmonster wirksam mit einem Schlag erledigt. Da gucken die Krieger neidisch :). Zum Backstabben ist eine Piercingwaffe Voraussetzung. Da man nicht immer Backstabben kann, weil sich ein Monster nach dem ersten mal umdreht, steigt der Skill eher langsam weshalb man hier ruhig mal ein paar Punkte investieren kann, es sollten ja noch jede Menge übrig sein. Der Skill wirk außerdem zusätzlich wie Taunt auch wenn man nicht trifft, ist also auch nützlich um Monster von den hilflosen Magiern abzubringen.<br><br>
	<B>Level 12:</B><br>
	<I>Safe Fall</I>: Ein sehr lebensrettender Skill. Ist er hoch kann man sich von Gebäuden oder Sonstwas werfen, bzw., fliehen ohne auch nur den geringsten Schaden zu nehmen. Da wo andere 50 Demage Fallschaden nehmen, nimmt man selber, hmm, moment ... , aehm, NICHTS! Hehe. Dies ist jedoch sehr von der Höhe des Skills abhängig. Also ob ich mit 75 einen Fall vom höchsten Baum in Kelethin überleben würde weiß ich auch nicht, aber angeblich nimmt man nur minimalen Schaden, während andere bei draufgehen. Manche Monster sind so doof einem in einen Abgrund nachzuspringen und draufzugehen, so daß man an schönen Loot kommt ohne sie killen zu müssen. Den Skill zu trainieren ist auch recht witzig, da man sich irgendwo runterschmeißen muß. Am Stadttor versammelt sich dadurch öfters ne Menschenmasse die sich fragt ob der Rogue da oben bekloppt ist :)<br>
	<I>Parry</I>: Ein weiterer wichtiger Kampfskill der bei entsprechender Klugheit schnell steigt.<br><br>
	<B>Level 13:</B><br>
	<I>Dual Wield</I>: Man kann 2 Waffen tragen. Sieht unglaublich cool aus und außerdem macht man damit, wenn der Skill hinhaut, pro Runde doppelten Schaden. Lahmt etwas hinterher der Skill.<br><br>
	<B>Level 16:</B><br>
	<I>Double Attack</I>: Damit kann man 2 mal pro Runde angreifen wenn es klappt. In Verbindung mit Dual Wield macht man also in einer Runde wenn es optimal läuft 4fachen Schaden! Aua :) Mehr kann ich noch nicht zu sagen.<br><br>
	<B>Level 18:</B><br>
	<I>Apply Poison</I>: Gift wurde noch nicht in Everquest eingebaut, daher kann ich auch nichts zu sagen. Allerdings wird dies die Rogueklasse gewaltig aufwerten, da man die HP die ein reiner Krieger mehr als man selber hat durch das Gift wegbekommt und somit die Chancen ausgleicht. Oder es könnte auch Gift geben das verlangsamt, lähmt etc. Das macht Rogues dann extrem stark!<br><br>
	<B>Level 20:</B><br>
	<I>Make Poison</I>: siehe oben.<br><br>
	<B>Level 21:</B><br>
	<I>Disarm Traps</I>: Damit kann man Fallen entschärfen, doch wie schon bei Sense Traps gibt es bisher kaum welche in Everquest. Doch dies soll sich in Kürze ändern. Jedenfalls ist dies dann ein sehr nützlicher Skill in Dungeons den sonst nur noch Barden bekommen.<br><br>
	<B>Level 22:</B><br>
	<I>Instill Doubt</I>: Angeblich bringt der Skill Feinde dazu wegzulaufen. Aber genau weiß ich das nicht. Jedenfalls wäre dies sehr praktisch in Verbindung mit Backstabbing :)<br><br>
	<B>Level 27:</B><br>
	<I>Disarm</I>: Damit kann man Feinde entwaffnen. Mehr kann ich nicht zu sagen.<br><br>
	<B>Level 30:</B><br>
	<I>Riposte</I>: Kann ich nichts zu sagen. Wahrscheinlich aber eine Art Parade im Kampf.
</P>

<P CLASS="P1">
	Da das Spiel bisher bis Level 50 geht hoffe ich mal das dies nicht alle Skills waren und man auf entlegenen Inseln noch welche erlernen kann *hoff*<br><br>
	Also viel Spaß mit eurem Rogue und klaut euch ein paar schöne Sachen :)
</P>

