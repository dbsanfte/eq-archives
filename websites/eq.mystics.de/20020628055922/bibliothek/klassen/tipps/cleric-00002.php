<P CLASS="P5">
	Level 1 bis 4:
</P>
<P CLASS="P1">
	Am besten in der Newbiezone vor Felwhite, der Hochelfen Stadt alles jagen was dort herumläuft. Am Anfang etwas vorsichtig sein, bei den Ork Pawns oder Spinnen, die recht stark sein können. Die Level 1-4 durchschreitet man alleine am schnellsten.
</P>

<P CLASS="P5">
	Level 5 bis 7:
</P>
<P CLASS="P1">
	In Greater Faydark gibt es ein Spawn Gebiet, das man Orkhügel (Orc-hill) nennt. In diesem Gebiet läßt es sich sehr gut zu 3. oder zu 4. Orks killen. Aber man muß aufpassen, weil ab und zu ein High-Level Ork Centurion spawnt (3 Level höher als die anderen). Je höher das eigene Level ist, desto weniger Mitglieder sollten in der Gruppe sein, sonst bekommt man zu wenig EXP Punkte.
</P>

<P CLASS="P5">
	Level 8:
</P>
<P CLASS="P1">
	Als ich Level 8 war, ging ich in Crushbone um da ein paar EXP Punkte zu machen, leider war es jedoch sehr schwer, da die ganze Zeit immer wieder Trains ausgelöst wurden. (Es ist empfehlenswert, den Spruch "Divine Aura" zu speichern, da dieser mir schon ab und zu das Leben gerettet hat.)
</P>

<P CLASS="P5">
	Level 9:
</P>
<P CLASS="P1">
	Als ich nun endlich Level 9 war, und neue Spells bekommen habe, beschloß ich von Greater Faydark nach Freeport zu gehen. Es ist eine sehr gute Gegend, aber leider hat es auf PVP Servern dort auch sehr viele PK. Ich wurde mehrere Male getötet, aber sehr viel ist mir nicht gestohlen worden, wenn man Divine Aura anwendet, hat man ca. 15 bis 30 Sekunden Zeit, alle Items in seine Bags einzupacken.<br>
	Am besten kann man hier EXP machen, wenn man sich mit ein oder zwei Personen zusammenschließt und in East Commons bei den Ork Camps Orks jagt. Der erste Schritt ist jedenfalls der schwerste, da meistens, zu Beginn alle Spawnplätze besetzt sind. Es gibt dort ca. 3-4 Ork Centurions (Sie sind Weiß/Schwarz bei Level 9), 1-2 Ork Legionnaire und 1-2 Ork Oracle. Es ist aber mit einem Ranger machbar, da er Snare hat und ein einzelnen Ork aus der Gruppe holen kann. Alternativ fragt man einen High-Level Charakter, ob er das Ork-Camp mal eben leerkämpfen kann
</P>

<P CLASS="P5">
	Level 10:
</P>
<P CLASS="P1">
	Man kann immer noch Orks jagen, oder man geht nun auf ASP (große Klapperschlangen) Jagd. Diese geben sehr gute EXP und auch sehr guten Loot (Giant Fang und Venome Sack sind je 5 Gold wert), allerdings sind sie sehr gefährlich, da sie ein starkes Gift haben. Als Kleriker braucht man daher den Spruch "Cure Poison" hat. Es ist jedoch empfehlenswert, sie nicht alleine anzugreifen. Die ASP's befinden sich in der Nähe des Tunnels nach Northern Ro. Wenn man die Vergiftung heilen will, braucht man total 60 Mana, da man 3 mal auf das Target Casten muß, damit die Vergiftung verschwindet.
</P>

<P CLASS="P5">
	Level 11:
</P>
<P CLASS="P1">
	Nun kann man die ASP's alleine killen, sollte aber nur die blauen ASP angreifen, da die weißen noch immer relativ stark sind. Was auch recht gut ist, sind die Willowisps, da man ab und zu eine Lightstone oder Greater Lightstone bekommt. Man muß zusätzlich auch noch eine magische Waffe besitzen, da Willowisps sonst unverletzbar sind. Ein guter Standpunkt um Willowisps zu töten ist in East Commons, in der Nähe der Ruinen (Ghouls und andere Untote), da ab und zu sogar noch ASP's vorbeikommen, oder in West Commons, in der Nähe der Druiden Ringe. Die Willowips unterstützen sich gegenseitig im Kampf.
</P>

<P CLASS="P5">
	Level 12:
</P>
<P CLASS="P1">
	Wenn man bei Level 12 angekommen ist, wird es schwierig ASP's zu killen, da die meisten nun grün sind. Man kann jetzt nach Oasis gehen und dort Krokodile in Gruppen killen. <br>
	Am besten geht es aber, wenn man nach Befallen geht und dort den Skeleton Lord im Einzelkampf tötet. Der ist meistens weiß/schwarz und manchmal gelb. Er ist immer auf Level 11 bis 13 und als Cleric mehr oder weniger einfach zu killen (ich hatte 280 HP, 280 Mana und 340 AC). Man Rootet ihn, und greift ihn mit Ward Undead an. Er hat so ca. 300 HP und spawnt ca. alle 8 Minuten, dies genügt um alle Mana Punkte und HP zu regenerieren. Der Skeleton Lord gibt jedoch KEINEN LOOT wie auch die Krokodile nicht. Man macht mit dem Skeleton Lord ca. 1 Bubble EXP in der Stunde, wenn man ihn alleine killt.
</P>

<P CLASS="P5">
	Level 13:
</P>
<P CLASS="P1">
	Man kann noch immer in Oasis bleiben, oder man bleibt beim Skeleton Lord, bekommt nun natürlich jedoch ein bißchen weniger EXP, dafür ist er aber auch einfacher zu töten.
</P>

<P CLASS="P5">
	Level 14 bis 17
</P>
<P CLASS="P1">
	Am besten killt man die Scarecrows in West Karanas. Die sind untot und man wird nicht KOS. Sie gruppen auch nicht untereinander, das heisst, man kann sie ziemlich gut solo killen. Das einzige Problem besteht darin, dass man meistens ein bisschen lange medden muss, was jedoch mit Breeze oder Clarity schneller geht. Als loot geben die nur ein paar silber Conins und rusty Weapons... aber gute EXP.
</P>

<P CLASS="P1">
	Ich hoffe, dass ich euch die Levels 1-17 gut beschrieben habe. 
</P>

