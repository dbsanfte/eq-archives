<P CLASS="P1">
	Ihr solltet nun Level 6 erreicht haben, einige Stücke von Cloth Armour euer Eigen nennen und einen 
	sogenannten "dead buffer" besitzen. Was ist ein dead buffer? Ab level 6 verliert ihr mit jedem 
	Tod etwas an Erfahrung. Solltet ihr also gerade frisch aufgestiegen sein und gleich darauf 
	wieder sterben ist es mit den Levelfreuden gleich vorbei - es wirft euch wieder auf den 
	vorherigen Level zurück. Bevor man also irgendwelche Risiken eingehen will, sollte man sich 
	an einfacher Stelle diesen dead buffer zulegen (die erste Hälfte der ersten bubble eures 
	Erfahrungsmeters sollte erstmal reichen).<br>
	Eure Kampfskills sollten ebenfalls alle auf Levelmaximum stehen. Es wird Zeit für euren ersten 
	Dungeon, Blackburrow (BB), Heimat der Gnolle des Sabertooth Clans, zu welcher übrigens auch die 
	bekannte Familie Darkpaw gehört. Der Eingang nach BB befindet sich in den Queynos Hills, gar nicht 
	weit entfernt vom Eingang nach Surefall Glades.  Auf dem Weg dorthin kommt ihr an Ruinen vorbei, 
	wo sich öfters mächtige Untote aufhalten. Sollten diese euch mal verfolgen, setzt auf keinen Fall 
	euren Weg nach BB fort um euch dort in Sicherheit zu zonen, sondern kehrt um zum Eingang nach 
	Surefall Glade wo sich die beiden Guards um eure Verfolger kümmern. Am Eingang zu BB ruhen und rasten 
	oft viele Spieler, geschwächt vom Kampf gegen die Gnolle - diese Spieler wären natürlich hocherfreut 
	über die nette Begleitung die ihr da anschleppt und würden euren Namen in alle Ewigkeiten verfluchen 
	(Ich habe das mal miterlebt, das Ergebnis waren ein Haufen Leichen an besagtem Eingang und ne Menge 
	mächtig saurer Spieler).<br>
	Außerdem sollte sich ein Fire Beetle Eye in eurem Besitz befinden (nicht verpackt sondern 
	direkt in einem Inventoryslot) um dort besser sehen zu können. BB ist übrigens nicht nur ein 
	Dungeon, sondern auch die Verbindung zwischen Queynos und den Everfrost Peaks, Heimat der Barbaren.<br>
	Nun also rein! Ihr werdet schnell feststellen, daß ihr euch auch weiterhin unter freien Himmel 
	befindet. Dort werdet ihr auch erstmal bleiben. Erkundet den "Top Level" und findet den Exit nach 
	Everfrost Peaks. Weiterhin werdet ihr einen hohlen Baum und ein Zelt sehen. NIEMALS dort hineintreten. 
	Eine Falltür und das Gesetz der Schwerkraft befördern euch fix tief nach unten, wo gar unangenehme 
	Gesellschaft auf euch wartet. Habt auch immer ein Auge auf die Textmeldung anderer Spieler - 
	sollte dort einmal das Wort "Train" auftauchen, so solltet ihr eure Beine in die Hände nehmen und ganz 
	fix zum näheren Exit fliehen.<br>
	Wie vorhin schon gehört unterstützen sich die Monster auch untereinander und wenn ein Unvorsichtiger 
	den falschen Gnoll angreift, so hat er kurz darauf eine ganze Horde am Hals. Verständlicherweise 
	sucht er sein Heil in der Flucht und die Gnolle ihm hinterher. Auf ihrem Weg schließen sich noch 
	andere an und so entsteht ein "Train". Laßt euch nicht täuschen, wenn dieser an euch vorbei rennt 
	und euch keiner der Gnolle angreift - sobald ihr erstes Opfer geflohen oder tot ist, erinnern sie sich 
	an alle die ihnen begegneten und kommen zurück! Die einzigen Möglichkeiten einem "Train" zu entkommen 
	sind:
</P>

<OL CLASS="P1">
	<LI>völlige Vernichtung alle beteiligten Gnolle<BR></LI>
	<LI>Flucht, also zonen<BR></LI>
	<LI>Tod<BR></LI>
</OL>

<P CLASS="P1">
	Was also sind eure ersten Opfer in BB? Die sogenannten Scrawny Gnolls (SG). Bekämpft sie im top level und 
	findet heraus wo sie überall auftauchen. Vorsicht, hin und wieder sind auch Gnolle (GN) dort, die ihr 
	momentan nur in einer starken Gruppe bekämpfen solltet. Ein Fluchtweg der SG führt durch eine Tür, 
	welche in tiefere Gefilde führt, folgt ihnen (noch) nicht, denn direkt kurz hinter der Tür warten 
	meistens zwei weitere SG oder gar GN.<br>
	Nach ein bißchen sterben am Anfang solltet ihr schnell den Dreh raus haben und bald Level 8 erreichen. 
	Ab hier könnt ihr auch GN alleine schaffen. Nun wird es Zeit etwas tiefer hinabzusteigen, am besten 
	mit ein paar Freunden und gruppiert. Geht durch die Tür und folgt den langen Gang, welcher steil bergab 
	führt. Ihr erreicht eine große Höhle. An der Stelle wo der Gang endet, werdet ihr auf zwei Brücken stoßen, 
	eine lange und sehr enge führt auf eine Insel, wo sich zwei GN aufhalten. Die andere ist nur sehr kurz und 
	führt in die eigentliche Haupthöhle mit einem See. Das Gebiet um diese beiden Brücken ist nun die nächste 
	Zeit euer Jagdrevier. Hier gibt es Gnolle en masse, auch eine neue Art, den Patroulling Gnoll (PG). 
	Kloppt euch hier bis ihr Level 9 erreicht habt (und achtet jetzt noch sorgfältiger auf "Trainmeldungen" - 
	euer Fluchtweg ist nun länger).<br>
	Mit Erreichen des neunten Levels könnt ihr endlich Sprüche erlernen. Also flugs die Taschen mit Geld 
	gefüllt und auf nach Surefall Glade zur Gilde. Dort investiert je einen Skillpoint in:
</P>

<UL CLASS="P1">
	<LI>Alteration<BR></LI>
	<LI>Abjuration<BR></LI>
	<LI>Channeling<BR></LI>
	<LI>Divination<BR></LI>
	<LI>Evocation<BR></LI>
</UL>

<P CLASS="P1">
	Danach werden die Sprüche in dem großen Pfahlbau am See gekauft und memoriert. Memorieren ist recht 
	langwierig, solange man noch nicht den Skill Meditation (ab Level 12) besitzt. Auch müssen die 
	Sprüche nach jedem Tod neu memoriert werden. Ab jetzt solltet ihr jede ruhige Minute nutzen und 
	zaubern was das Zeug hält, um die Skills möglich an den Rest anzugleichen (alle Zauberskills 
	starten mit 10, während der Rest sich schon bei 50 befinden sollte - viel Arbeit also).
	So gewappnet also zurück nach BB und in der Gegend um die beiden Brücken sich auf Level 10 schlachten.<br>
	Mittlerweile solltet ihr einen ganzen Batzen an Geld angesammelt haben (die Gnolle haben oft 
	Waffen dabei, welche sich gut verkaufen lassen ,achja zum verkaufen immer zurück nach Queynos 
	oder Surefall Glades) und über den Kauf von Lederrüstungsteilen nachdenken. Dazu geht ihr nach Süd 
	Queynos zum lokalen Lederdealer. Kauft nicht von den beiden am Marktplatz, die verkaufen nämlich nur 
	small und large size, ihr benötigt aber medium.<br>
	Gerüstet mit Leder wieder zurück nach BB. Mit Level 10 und am besten einem guten Freund gleichen 
	Levels laßt ihr nun mal ab von Gnollen und wendet euch den auch in BB wohnhaften Giant Snakes zu. 
	Diese haben in BB einige eigene Plätze, zu denen ihr euch am besten durchfragt. Der Kampf gegen sie 
	kann noch etwas riskant sein, aber die gute Erfahrung und vor allem die gute Beute (Vergeßt die eggs 
	und rattles, behaltet die giant snake fangs und venom sacs, diese lassen sich für gutes Geld verkaufen) 
	rechtfertigen das hohe Risiko. Kämpft gegen die giant snakes, bis ihr Level 12 erreicht habt. das mag
	zwar etwas langweilig sein, ist aber sicher, alleine auch möglich und gibt gutes Geld.<br>
	Nun solltet ihr euch von eurer rusty weapon trennen, sofern ihr sie noch besitzt. Achtet auf 
	Verkaufsangebote anderer Mitspieler und wenn möglich und ihr einhändig kämpft sichert euche eine 
	Minotaur Axe - die beste nichtmagische Einhandslashwaffe. Zahlt aber nicht mehr als 7 Platinum dafür.
	Außerdem solltet ihr den Kauf eines Kiteshields in Betracht ziehen.<br>
	Mit Erreichen von Level 12 (samt eines dicken dead buffers) stehen euch nun mehrere Möglichkeiten offen:
</P>

<OL CLASS="P1">
	<LI>
		Ihr könnt weiterhin in BB bleiben und mit einer Gruppe euch den Elite Gnoll Guards (EG) und sogar den Gnoll Commanders (GC) zuwenden. Die Gruppe sollte dann aber gut ausbalanciert sein und mindestens einen Heiler sowie drei "Tanks" (Tanks sind alle Klassen, die genügend Hitpoints haben um sich in den direkten Nahkampf zu stürzen, also Warriors, Rangers, Bards, Paladins, Shadowknights) haben.
	<BR></LI>
	<LI>
		Ihr könnt euch nach West Karana begeben und dort, ebenfalls mit einer guten Gruppe, das Banditen Camp (befindet sich nördlich vom Zoneexit nach Queynos Hills auf einem Hügel) ausheben. Dies ist wohl das risikoreichste Unternehmen momentan, da die Banditen allesamt gut zuhauen und auch Magier in ihren Reihen haben, die schon mal einen Spruch mit 200 Schaden ablassen. Aber auch hier lohnt sich das Risiko, da die Beute (Bronzene Waffen, haben einen Wert von 2 - 5 Platinum ) und Erfahrungspunkte umfangreich ist.
	<BR></LI>
	<LI>
		Begebt euch nach Süd Queynos zu den Docks und wartet auf das Schiff dort. Stellt sicher, daß ihr mit einer guten Gruppen unterwegs seid - für dieses Unternehmen empfehlen sich 2 Tanks, 2 Magier, 1 Heiler. Mit dem Schiff, welches von Süd Queynos über Eruds Crossing nach Erudite Palace fährt, fahrt ihr bis nach Eruds Crossing. Dort hält das Schiff an einer kleinen Insel, welche wir in Ermangelung eines Vorhandenen Namens einfach mal Cat Isle nennen (Die Einwohner sind Katzen). Auf dieser Insel, wo meistens so gut wie keine anderen Spieler sind, gibt es massenhaft Wisps und Zombie Sailors. Mit den beiden Magiern sollte es kein Problem sein die Wisps zu erledigen, die hin und wieder einen Greater Lightstone fallen lassen, den man für satte 4 Platinum verkaufen kann. Die Zombies findet ihr bei dem versunkenen Schiff direkt an der Küste. Um sie an Land zu holen, muß einer hinabtauchen und sie herauslocken. Die Zombies hauen auch gut zu (würde ich auch, wenn ich als Untoter auch noch im Salzwasser schmachten müßte), geben aber Lederrüstungsteile als Beute.<BR>
		Es sei noch gesagt, daß ihr keine Möglichkeit habt auf Cats Isle vor einem Kampf zu fliehen; die Katzen helfen euch nicht und das Meer ebenso wenig. Jeder Kampf hier ist im wahrsten Sinne des Wortes ein Kampf auf Leben und Tod.
	<BR></LI>
</OL>

<P CLASS="P1">
	Egal für welche Möglichkeit ihr euch entscheidet, ihr solltet bald in greifbare Nähe von Level 15 rücken.<br>
	Ich hoffe bald diesen Guide zu erweitern, aber dafür muß meine Wenigkeit auch erstmal weiterkommen.<br>
	Erwähnt sei noch, daß der Ranger ab Level 17 so richtig interessant wird, da er dann den Dual Wield skill bekommt.
</P>

<P CLASS="P1">
	Grüße und gute Jagd
</P>

