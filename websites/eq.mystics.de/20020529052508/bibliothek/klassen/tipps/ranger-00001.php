<P CLASS="P0" ALIGN="CENTER">
	Diese Tips für Anfänger wurden von Leiria erstellt. Der Stand der Infos ist, 30.04.99.<br>
	Er geht hauptsächlich auf den menschlichen Ranger ein.
</P>	

<TABLE BORDER="0" CELLPADDING="0" CELLSPACING="0" WIDTH="100%">
