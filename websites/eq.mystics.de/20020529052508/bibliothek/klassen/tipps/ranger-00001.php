<TR><TD BACKGROUND="/pics/pageback2.jpg" ALIGN="CENTER" CLASS="P5">Der Anfang (Level 1 - 5)</TD></TR>
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
</TABLE>

<P CLASS="P1">
	Ihr startet in Surefall Glades oder Kelethin, wenn ihr einen Waldelfen gewählt habt.<br>
	In Surefall findet ihr recht fix euren Gildenchef, Hager Sureshot. Gebt ihm das Schreiben und 
	erhaltet von ihm etwas Erfahrung und eine Gildentunic. Guckt auch gleich nach, ab welchem 
	Level ihr den nächsten neuen Skill verwenden könnt (hier ab Level 5 Kick) um dann zurückzukehren 
	um wieder die "basics" zu erlernen.<br><br>
	Verlaßt danach Surefall Glades und ihr "zoned" nach Queynos Hills. Beginnt nun sofort den Tracking-,
	Foraging- und Sense Headingskill zu benutzen. Man sollte möglichst früh die höchstmöglichste Punkteanzahl 
	in jedem Skill erreichen - diese berechnet sich wie folgt für den Ranger:<br><br>
	Offense und Defense skill: Level x 5 (bei Level 2 also max. 10)<br>
	Alle anderen: Level x 5 + 5 (bei Level 2 also max. 15)<br><br>
	Folgt nun der Straße, welche quer durch Queynos Hill führt um nach ein paar Minuten nach North Queynos 
	zu zonen. Hier werdet ihr die nächste Zeit verbringen, handelt es sich hier doch um eine sogenannte 
	"newbie area".<br>
	Geht nun hier auf die Jagd: Snakes sind erstmal euer bevorzugtes Opfer. Ach ja, ihr solltet 
	nun auch anfangen alles und jeden was ihr angreifen wollt, vorher zu "considern ". Und 
	Finger weg von allem was gelb oder gar rot ist. Nach ca. einer halben Stunde intensiver Verminderung der 
	Schlangenpopulation solltet ihr Level 2 erreicht haben. Dies beschert euch mehr Hitpoints und auch die 
	Skills können wieder gesteigert werden. Ihr seid nun bereit für Gnoll Pups und Decaying Skeletons. Die 
	kleinen Gnolle gibts tagsüber, die Skelette nachts. Vorsicht - bekämpft ihr gerade eins und ein anderes 
	kommt vorbei, es wird seinem Artgenossen helfen (gilt in North Queynos für Gnolle, Skeletons und alle 
	Arten von Käfern). Um da nicht böse überrascht zu werden, hilft hin und wieder ein Wechsel der 
	Kameraperspektive um zu sehen, was sich da hinter seinem Rücken zusammenbraut.
	Die Decaying Skeletons sind übrigens auch eure besten Waffenlieferanten und bald solltet ihr euer 
	short sword gegen ein rusty bastard sword (momentan für euch die beste 1H-slash-Waffe) eintauschen können.<br>
	Wundert euch übrigens nicht, daß ihr euer "Startsword und die Gildentunic nicht verkaufen, traden, 
	auf den Boden oder in die Bank legen könnt - die einzige Möglichkeit sie loszuwerden ist 
	sie zu destroyen).<br>
	Mittlerweile sollte euer Inventar voll sein von Gegenständen und euch der Drang überkommen diese gegen 
	klingende Münze einzutauschen. Direkt hinter dem Stadttor befindet sich ein alleinstehender Händler, 
	der wohl eher den Namen Willi Wucherpfennig verdient hätte (Tubal Weaver heißt der Knilch). Auf keinem 
	Fall bei ihm ver- oder einkaufen!! Dazu fragt ihr euch zu Sneed Galliway durch, welcher sein Geschäft 
	in North Queynos, direkt gegenüber dem "Priest of Discord" hat. Dort verkauft ihr eure Beute an seine 
	reizende Tochter. Ob ihr das Geld nun auf die Bank bringt oder behaltet, sei euch überlassen.<br>
	Inventory geleert - und wieder auf zur fröhlichen Monsterhatz!<br><br>
	Ihr solltet nun wie gewohnt weitermachen, ab Level 3 jedoch auch Fire Beetles versuchen. Ab Level 4 kommen 
	dann auch Drohnen und Worker dazu. Spätestens nun solltet ihr euch nach Freunden umsehen, mit 
	denen Ihr eine Gruppe bilden könnt. Mit einer Gruppe lassen sich nämlich auch die frechen 
	Mitglieder der Darkpaw Familie, welche von Zeit zu Zeit erscheinen beseitigen.<br>
	Solltet ihr euch einmal übernehmen und euer Gegner zu stark werden oder unvorhergesehener Weise 
	Verstärkung erhalten, so flieht zum Stadttor, wo euch die beiden Guards unterstützen werden (aber 
	ihr verspielt auf diese Weise auch eure Beute und Erfahrung). Sobald ihr Level 5 erreicht habt 
	geht es zurück nach Surefall Glades, wo ihr die Grundlagen des Combatskills "Kick" erlernt. 
	Dermaßen gestärkt sollte es euch um einiges leichter fallen in North Queynos Level 6 zu 
	erreichen.
</P>

