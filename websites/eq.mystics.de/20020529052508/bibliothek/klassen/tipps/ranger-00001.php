<P CLASS="P1">
	Es wird vorausgesetzt, daß ihr das Tutorial durchgespielt habt und euch mit der Steuerung, sowie 
	allen Interaktionen im Spiel auskennt. Außerdem solltet ihr das Manual durchgelesen haben.<br>
	Der Ranger ist eine Hybridklasse, bestehend aus Warrior und Druid. Sein Trackingskill macht 
	ihn zum Freund jeder Gruppe und durch seinen Foragingskill erlangt er früh eine 
	nahrungstechnische Unabhängigkeit.<br>
	Ab Level 9 bekommt der Ranger seine ersten Sprüche. Mit dabei der Minor Heal Spell, welcher 
	euch lange Zeit der Hitpointregeneration erspart. Hier gleich mal alle Sprüche die ihr 
	ab Level 9 erhaltet:
</P>

<UL CLASS="P1">
	<LI>Skin like Wood - Mehr Rüstungsklasse und mehr Hitpoints<BR></LI>
	<LI>Glimpse - Wirkt kurzzeitig wie ein Fernglas<BR></LI>
	<LI>Minor Healing - Heilt 6 bis 10 Hitpoints<BR></LI>
	<LI>Endure Fire - Erhöht euren Wiederstand gegen Feuerschaden<BR></LI>
	<LI>Flame Lick - Macht den Gegner verwundbarer und macht etwas Schaden<BR></LI>
	<LI>Lull Animal - macht ein Tier weniger aggressiv<BR></LI>
	<LI>Snare - Ziel kann sich nur langsam bewegen<BR></LI>
</UL>

<P CLASS="P1">
	Es sei noch bemerkt, daß die Ranger wahre Naturfreunde sind; tötet also niemals irgendwelche Tiere, 
	oder eure Gilde wird euch bei übermäßigen Abschußquoten nicht mehr akzeptieren. Was sind also 
	Tiere in Everquest? Vorrangig Bären und Wölfe aller Art. Zweitrangig alle Säuger die da so 
	kreuchen und fleuchen (Löwen, Ratten, Katzen, etc.). Nicht als Tiere zählen Reptilien und Insekten.
</P>

<TABLE BORDER="0" CELLPADDING="0" CELLSPACING="0" WIDTH="100%">
