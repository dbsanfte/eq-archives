	Geschrieben von Caratac Nomenasno<BR>
	<BR>
</P>

<P CLASS="P0">
	Interview eines CodhNetworkNews-Reporters mit einem Sarnak:
</P>

<TABLE NOBORDER CELLSPACING=1 CELLPADDING=4 WIDTH=647 ALIGN="CENTER">
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">CNN:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Wie werden Sie Weihnachten heuer verbringen?</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">S:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Oh, geplant ist eine gemütliche Feier im engsten Familienkreis.</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">CNN:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Wie viele Sarnaks werden das sein?</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">S:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Schätze mal so ca. 230 ... zu Beginn.</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">CNN:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Wie meinen Sie das, zu Beginn?</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">S:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Naja, naturgemäß werden wir bei solchen Feierlichkeiten immer gestört von Elfen, Zwergen, Menschen, Ogern und solchem Gesocks. Die pullen dann immer den Einen oder Anderen von uns weg. Das ist etwas lästig.</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">CNN:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Wie viele werden dann am Ende der Feier noch übrig sein?</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">S:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Kann ich nicht sagen, letzte Weihnachten waren's ungefähr 20. Die Schwerverletzten mitgerechnet.</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">CNN:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Wie läuft denn die Weihnachtsfeier bei Ihnen ab?</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">S:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Och, zuerst warten wir, bis die gesamte Familie da ist. Dann wird gebetet.</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">CNN:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Gebetet?</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">S:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Ja, dass nicht zu viele von uns bei der Feier draufgehen.</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">CNN:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Aha, zu wem beten Sie eigentlich?</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">S:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Zum Großen Ver-Ant, der uns allen das Leben geschenkt hat. Ein genialer Revolutionär, der allerdings für seine Ideen an irgendwas genagelt wurde.</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">CNN:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Und wie setzen Sie die Feier fort?</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">S:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Dann trinken wir Blut aus den Schädeln unserer getöteten Feinde.</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">CNN:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0"><i>*entsetzt*</i> WAS?</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">S:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0"><i>*kichert*</i> Nein, war nur Spaß. Wir singen.</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">CNN:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0"><i>*beruhigt*</i> Ah, gut ... singen. Was singen Sie denn?</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">S:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Stille Zone, Heilige Zone. Eine Ode über ein Land, das nicht überfüllt ist mit Helden aus den anderen Völkern. Eine Ballade über ein weites, leeres Land, in dem nur wir Sarnaks leben.</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">CNN:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Interessant. Wie geht es weiter?</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">S:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Dann zählen wir mal, wieviel von uns noch da sind. Falls es sich auszahlt, beginnen wir mit dem Essen.</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">CNN:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Rohes Fleisch, nehme ich an?</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">S:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Nein, gedünstetes Gemüse in Wurzelsauce. Sehr schmackhaft und ausgesprochen vorteilhaft für die Figur.</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">CNN:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Was kommt nach dem Essen?</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">S:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Nun, das Trinken, Lachen, Tanzen. Wir Sarnaks sind ein sehr fröhliches Volk, müssen Sie wissen. Und friedliebend.</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">CNN:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Friedliebend? Da hört man aber ganz andere Sachen ...</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">S:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Das sind Gerüchte, von Menschen oder Elfen in die Welt gesetzt. Die sind nur neidisch auf unsere fortgeschrittene Kultur und unsere überragende Intelligenz. Sehen Sie, wir hassen Krieg und Kampf und wehren uns nur, wenn wir angegriffen werden. Meist schenken wir den Gegnern unser Leben, da wir es verabscheuen, jemanden zu verletzen. Es gibt doch nichts Primitiveres als Kampf. Manchmal allerdings geschieht es durch einen unglücklichen Zufall, dass wir unsere Gegner töten. Dann sinken wir vor Scham sofort zu Boden und beten zum Großen Ver-Ant, damit unsere Schuld vergeben wird.</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">CNN:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0"><i>*mit Tränen in den Augen*</i> Wirklich rührend.</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">S:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Ja, nicht wahr? Wir sind doch alle gleich. Menschen, Elfen, Sarnaks, Iksar ... alle entspringen wir der gleichen Wurzel. Und die Liebe sollte es sein, die uns verbindet. Lassen Sie sich umarmen!</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">CNN:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0"><i>*schluchzend den sarnak umarmend*</i> ...</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">CNN:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0"><i>*schniefend*</i> Wie ... wie geht denn die Weihnachtsfeier aus?</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">S:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0"><i>*dem CNN auf die Schulter klopfend*</i> Nun, der Rest von uns, der von den Pullern verschont bleibt, geht nach Hause. Dabei erwischt es noch den einen oder anderen, aber im Grunde ist es immer ein gemütliches und frohes Fest. Und wenn wir zuhause sind, dann ... <i>*flüsterflüster*</i> ... schließlich müssen wir dafür sorgen, dass wir nicht aussterben.</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">CNN:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Wirklich interessant. Vielen Dank für das Gespräch.</FONT></P></TD>
	</TR>
	<TR>
		<TD VALIGN="TOP"><P><FONT CLASS="P2">S:</FONT></P></TD>
		<TD VALIGN="TOP"><P><FONT CLASS="P0">Nichts zu danken. Möge der Große Ver-Ant mit Ihnen sein!</FONT></P></TD>
	</TR>
</TABLE>

