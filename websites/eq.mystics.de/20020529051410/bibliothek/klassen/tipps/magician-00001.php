<P CLASS="P0" ALIGN="CENTER">
	Diese Tips f&uuml;r Anf&auml;nger wurden von Ilion Sturmlied erstellt.
  Der Stand der Infos ist Betaphase 3, 26.01.99.
</P>	

<P CLASS="P1">
	Der Beginn als Magier ist  nicht viel anders, als bei andern Klassen. Man
  steht dort in seiner Heimatstadt mit seiner spärlichen Newbieausrüstung.
</P>

<UL CLASS="P1">
	<LI>Je 5 Portionen Wasser und Brot<BR></LI>
	<LI>Ein Messer<BR></LI>
	<LI>Eine Quest Scroll<BR></LI>
	<LI>Eine PK Scroll<BR></LI>
	<LI>Eine Lesser Shielding Scroll<BR></LI>
	<LI>Eine Burst of Flame Scroll<BR></LI>
</UL> 

<P CLASS="P1">
	Als erstes sollte man die beiden Spells in sein Spellbook eintragen und
  sich dann auf die Suche nach seinem Guildmaster machen. Anhaltspunkte, wo
  sich dieser Guildmaster aufhält findet man auf der Quest Scroll (sie haben
  verschiedene Bezeichnungen).
</P>

<P CLASS="P1">
	Diesem Guildmaster gibt man die Scroll und bekommt im Gegenzug ein wenig
  EXP und eine Robe.
</P>

<P CLASS="P1">
	Dann sollte man sich dort gleich seinen Nahkampf Skill trainieren.
  Normalerweise wird das "1H Blunt" sein. Wer
  die Möglichkeit hat sollte sich aber schon am Anfang einen Stab besorgen
  und "2H Blunt" trainieren.
</P>

<P CLASS="P1">
	Dann ist es an der Zeit sich in das erste Abenteuer zu stürzen, diese
  sollte man vor der Stadt suchen. Dort kann man kleinere Tiere jagen, diese
  bringen das erste Geld und EXP.
</P>

<P CLASS="P1">
	Vor dem Kampf sollte ist es ratsam Minor Shielding zu casten. Wenn der
  Gegner geschwächt ist kann man ihm den Todesstoß am besten mit Burst of
  Flame versetzten... So steht einem schnellen Aufstieg nichts im Weg.
</P>

<P CLASS="P1">
	Als Magician hat man den Vorteil, daß man sich keine Nahrung kaufen muss,
  denn man bekommt schon in Level 1 die Spells <I>"Summon Food"</I> und
  <I>"Summon Drink"</I> und spart eine Menge Geld.
</P>

<P CLASS="P1">
	Ein weiterer Vorteil des Magican sind seine "Pets". Diese Spells sollte
  sich jeder Magican als erstes in jedem Level zulegen.
</P>
	
<P CLASS="P1">
	Durch die Elemente ist der Magier auch in der Lage alleine gut zu
  überleben, indem er sein Pet in den Nahkampf schickt, während er selbst
  den Gegner mit offensiv Sprüchen bedeckt.
</P>

<P CLASS="P1">
	So hat er den Vorteil, das er nicht selbst kämpfen muss und so weniger
  unterbrochen wird als z.B. ein Wizard der Solo spielt.
</P>

<P CLASS="P1">
	Allerdings kann das Pet keinen Warrior ersetzten, ein Warrior macht mehr
  Schaden und ist felxibler als ein Elementel.
</P>

<P CLASS="P1">
	Der Magier sollte auch nicht vergessen, daß er sein Pet auch mit Waffen
  ausrüsten kann :)
</P>

