<P CLASS="P1">
	Die Existenz der Götter und ihre Erschaffung mag immer ein Geheimnis bleiben. Doch 
	das was ein Gott zu seiner Existenz benötigt ist der wahre Glaube.
</P>

<P CLASS="P1">
	Seit es Götter folglich gibt, gibt es Wesen, die an sie glauben, die sie verehren und
	die Ideologie ihres Gottes befolgen. Jene, die über den normalen Glauben an einen Gott
	hinausgehen, diejenigen die Ihr Leben, ja ihre Seele, ihre pure Existenz einem Gott
	widmen, werden als Kleriker bezeichnet. Sie gehören in jedem Volk zur geistigen Elite. 
	Man mag ihnen zwar eine gewisse Engstirnigkeit vorwerfen können, da das Wort ihres
	Gottes über alles für sie wiegt, doch an Wissen, Weisheit und Macht stehen sie weit
	oben.
</P>

<P CLASS="P1">
	Während bei guten Götter die Kleriker in der Regel bescheidene und freundliche Wesen
	sind, die tolerant , aber bestimmt den Weg ihres Gottes mit Wort oder auch Taten
	predigen, sind die Kleriker der bösen Götter meistens wahre Tyrannen. Sie sind das
	eigentliche Übel innerhalb einer bösen Gesellschaft, ja die eigentlichen Führer
	ihres Volkes. Sie treffen im Versteckten Entscheidungen Ihres Volkes und 'beraten'
	die jeweiligen Führer dieser Gesellschaften. Ihre Götter gewähren ihnen nicht die
	Macht, um einen Berg zu versetzen, wie es vielleicht ein Magier einmal schaffen
	könnte, nein, ihre Macht ist subtiler, versteckter, weniger offensichtlich, 
	aber keineswegs schwächer als die eines Magiers. Im Gegenteil, ihre Macht kann dazu
	führen, daß sich ganze Völker in Kriegen vernichten. Die hellsten oder die dunkelsten
	ihrer Vertreter stehen im engen Kontakt zu ihren Göttern. Auch sind die Kleriker die
	einzigen, deren Macht über den Tod hinausgeht. Ein Necromant mag mit dem Tod spielen, 
	ihn pervertieren und sich seine Macht nutzbar machen, doch die klerikale Macht reicht
	über den Tod hinaus und kann ihn gar unwirksam machen. Ihre Macht kommt direkt von
	den Göttern!
</P>

<P CLASS="P1">
	Nicht jeder wird den Weg eines Klerikers gehen können. Den die Aufgaben der 
	Kleriker verlangen eine hohe Disziplin die nicht jeder Aufbringen kann.
</P>

<P CLASS="P0">
	<I>Ted</I>
