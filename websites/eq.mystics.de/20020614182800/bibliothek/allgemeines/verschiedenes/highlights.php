<UL CLASS="P0">
	<LI>
		Ein Rollenspiel, das vom Beginn an dazu entworfen wurde, ein großes Onlinespiel zu werden.
		Die Welt von EverQuest kann über 2000 Spieler pro Server verwalten.<BR><BR>
	</LI>
	<LI>
		13 w&auml;hlbare Rassen und 14 w&auml;hlbare Klassen, darunter Zwerge, Ogres, Trolle,
		Ranger, Zauberer und Diebe.<BR><BR>
	</LI>
	<LI>
		Über 40 einmalige Skills und ungewöhnliche Fähigkeiten.<BR><BR>
	</LI>
	<LI>
		Tausende von Gegenständen, magische und einmalige, darunter magische Artefakte von
		großer Macht.<BR><BR>
	</LI>
	<LI>
		Es gibt fünf Kontinente zu entdecken, aber auch andere Dimensionen und unbekannte
		Kontinente.<BR><BR>
	</LI>
	<LI>
		Sowohl das alleinige losziehen, als auch das Zusammenschließen in Gruppen für das
		Lösen von gro&szlig;en, komplexen Quests wird unterstützt.<BR><BR>
	</LI>
	<LI>
		Jede Klasse und Rasse hat ihre Stärken und Schwächen. Die starken Gruppen werden
		diejenigen sein, die ihre Gruppe ausgeglichen formen und lernen als Team zu
		arbeiten.<BR><BR>
	</LI>
	<LI>
		Es gibt hunderte von Zaubern zu lernen kann, die fünf verschiedenen Schulen der Magie
		zugeordnet sind.<BR><BR>
	</LI>
	<LI>
		Mittlerweile weit &uuml;ber 100 einzigartiger Monster und Tiere!<BR><BR>
	</LI>
	<LI>
		Das Töten von anderen Spielern wird kontrolliert indem man selber entscheiden kann ob
		man es will. Diejenigen die ohne Player killing spielen möchten, können nicht von
		anderen Spielern getötet werden.<BR><BR>
	</LI>
	<LI>
		Echte 3D Umgebung mit variablen Kamerasichten. Es ist möglich aus der "Ich-Perspektive"
		zu spielen, aber auch aus verschiedenen Sichten von oben und der Seite um so taktischen
		Kampf umzusetzen.<BR><BR>
	</LI>
	<LI>
		Wünderschöne SVGA (640x480, 800x600 oder 1024x768, 16Bit Farbtiefe) Grafikumgebung, mit
		Abenteuerzonen wie Höhlen, Eislandschafen, trockene Wüsten und sogar komplette
		Unterwassenwelten.<BR><BR>
	</LI>
	<LI>
		Echte Polygon Charakter, NPC´s genauso wie Spieler. Es wurden keine Sprites verwendet.<BR><BR>
	</LI>
	<LI>
		Optimiert für die neueste Grafikbeschleunigungstechnik mit 3D-Unterstützung.<BR><BR>
	</LI>
</UL>

