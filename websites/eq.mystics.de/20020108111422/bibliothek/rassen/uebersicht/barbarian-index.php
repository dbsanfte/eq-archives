	<P CLASS="P1">
		Das Rauhe Volk. Manche sagen, sie seien die Urväter der Menschen und Eruditen. Doch ob dies stimmen
		mag ist unwesentlich, denn die Barbaren sind ein eigenständiges Volk. Im kalten, unfreundlichen
		Norden lebend haben sie sich zu Kriegerischen Stämmen entwickelt. Ihre Kampfschreie erfüllen
		oftmals das Ödland, und die Herzen ihrer Feinde mit schrecken. Barbaren sind ein hartes Volk. Nur
		die Stärksten überleben in ihrer Heimat, und so verwundert es nicht, das sie wenig wert auf sozialen
		Schick legen. Sie sind zurecht stolz darauf Barbaren zu sein, kaum ein normaler Mensch könnte in der
		eis- und schneebedeckten Tundra, die dieses Volk Heimat nennt, überleben.
