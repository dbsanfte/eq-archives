<P CLASS="P1">
	Einige nützliche Spells des Shamanen in den unteren Levels
</P>
<UL CLASS="P1">
	<LI>
		<B>Burst of Flame</B> ist der erste Angriffszauber des Shamanen und richtet nur minmalen Schaden aus, und er wird oft resistet.
	<BR></LI>
	<LI>
		<B>Cure Blindness</B> dieser Level 9 Spell hilft gegen Blindheit
	<BR></LI>
	<LI>
		<B>Cure Disease</B> ein sehr nützlicher Level 1 Spell, denn sehr viele Gegner verspüren das Bedürfnis einen die Pest an den Leib zu wünschen. Da sich bei Disease keiner regenerieren kann bzw. nur sehr langsam, ist das ist in einen Dungeon oder bei mehrern Gegenern recht gefährlich.
	<BR></LI>
	<LI>
		<B>Cure Poison</B> ein Level 5 Spell. Vorwiegend Spinnen vergiften einen ziemlich schnell und das wirkt sich sehr auf das regenerieren der HPs aus, wie auch Disease.
	<BR></LI>
	<LI>
		<B>Dexterous Aura</B> ist ein wertvoller Level 1 Spell, der sich im Nahkampf sehr positiv auf das Kampfverhalten des Bezauberten auswirkt.
	<BR></LI>
	<LI>
		<B>Drowsy</B> vermindert die Angriffsgeschwindigkeit des Gegners und man kann dann leichter Angriffsspells durchbringen, ohne gestört zu werden. Dieser Spell ist im Level 5 erhältlich.
	<BR></LI>
	<LI>
		<B>Endure Cold</B> ist ein Level 1 Spell der die Cold Resist anhebt. 
	<BR></LI>
	<LI>
		<B>Endure Disease</B> ist ein guter Level 9 Spell der die Disease Resist erheblich anhebt und man ist schwerer zu diseasen.
	<BR></LI>
	<LI>
		<B>Endure Fire</B> ist ein Level 5 Spell der die Fire Resist anhebt.
	<BR></LI>
	<LI>
		<B>Enduring Breath</B> ist ein Level 14 Spell mit dem man unter Wasser atmen kann. Wenn man öfters baden geht bzw. sich im Unterwasserdungeon herumtreibt ist der Spell nur zu empfehlen. Ansonsten braucht man den nicht unbedingt da der Shamane eine sehr hohe Ausdauer hat und dadurch schon mit einen kleinen Skill sehr lange schwimmen kann.
	<BR></LI>
	<LI>
		<B>Feet like Cat</B> mit diesen Level 5 Spell bekommt man mehr Agility, was einem bei weiten Reisen sehr hilft. Je höher dieser Wert ist desto schneller läuft man einen Gegner davon oder hinterher.
	<BR></LI>
	<LI>
		<B>Flash of Light</B> ist ein Level 1 Spell, der einen Gegner nur kurzzeitig blind macht. Aber diese 5 Sekunden Blindheit zahlen sich nicht recht aus.
	<BR></LI>
	<LI>
		<B>Flight</B> ist ein Level 14 Spell mit dem man angeblich fliegen kann. Ich bin schon recht gespannt wie das geht, denn ich habe noch nie jemanden fliegen sehen.
	<BR></LI>
	<LI>
		<B>Frost Rift</B> ist der 2 Angriffsspell, der schon einigen Schaden für dieses Level macht. Dieser Level 5 Spell macht 14 Dmg.
	<BR></LI>
	<LI>
		<B>Gate</B> ist ein Level 5 Spell den jeder Zauberer hat. Dieser bewirkt das man sich zu seinem Heimatpunkt bzw. Bindepunkt teleportiert.
	<BR></LI>
	<LI>
		<B>Inner Fire</B> ist ein guter Schildspell, der im Level 1 erhältlich ist. Er ist einer der beiden Grundspells die jeder Shamane am Anfang bei sich trägt. (+HPs und +Armorclass)
	<BR></LI>
	<LI>
		<B>Light Healing</B> bekommt der Shamane im Level 9.
	<BR></LI>
	<LI>
		<B>Minor Healing</B> ist der Level 1 Heilspell den auch jeder Shamane nach der Erschaffung bei sich trägt.
	<BR></LI>
	<LI>
		<B>Scale Skin</B> ist ein Level 5 Spell der sich positiv auf Armorclass auswirkt.
	<BR></LI>
	<LI>
		<B>Sense Animals</B> ist ein Level 9 Spell, der einen beim Auffinden von Tieren hilft.
	<BR></LI>
	<LI>
		<B>Serpent Sight</B> ist einer der wertvollsten Spells den der Shamane im 9 Level bekommt. Mit diesem Spell  kann der Shamane Infravision herbeizaubern. Die Nachtsicht ist einer der größten Nachteile der Rassen die nicht in der Nacht sehen können. Dieser Spell reduziert diesen Nachteil erheblich und ist nur zu empfehlen. Jeder der mal in der Nacht ohne Nachtsicht herumlaufen musste wird sich diesen Spell wünschen.
	<BR></LI>
	<LI>
		<B>Sicken</B> nennt sich der Level 5 Spell mit dem man einem die Pest an den Leib zaubern kann.
	<BR></LI>
	<LI>
		<B>Spirit of Bear</B> ist ein Level 9 Spell der einem die Stamina eines Bären bringt und das wiederum gut für den Nahkampf ist.
	<BR></LI>
	<LI>
		<B>Spirit Sight</B> hilft einem selbst und anderen beim Suchen von unsichtbaren Leuten.
	<BR></LI>
	<LI>
		<B>Strenghten</B> ist ein Level 1 Spell der einen bis zu 10 Stärkepunkte bringt. Je mehr Stärke desto mehr kann man tragen und es wirkt sich sehr positiv im Nahkampf aus.
	<BR></LI>
	<LI>
		<B>Summon Drink</B> ist ein weiter sehr nützlicher Level 5 Spell. Jeder der nicht andauernd in die Heimatstadt zurücklaufen muss, um Wasser zuholen ist für diesen Spell dankbar.
	<BR></LI>
	<LI>
		<B>Summon Food</B> ist ein wertvoller Level 9 Spell mit dem man problemlos Nahrung herbeizaubern kann. Nun kann der Shamane lange durch die Gegend ziehen, ohne auf eine Stadt angewiesen zu sein.
	<BR></LI>
	<LI>
		<B>Tainted Breath</B> ist ein Level 9 Spell. Falls wer einen vergiften will ist er mit diesem Spell gut bedient.
	<BR></LI>
</UL>

