<P CLASS="P1">
	<B>Halas</B> die Heimat der Babariens. Diese Stadt liegt von 
	allen am nördlichsten, im ewigen Eis und Schnee. Man kann sie nur über den gefährlichen 
	Dungeon Blackbarrow erreichen. Blackbarrow ist die Heimatdungeon der Gnolls und sehr 
	gefährlich in den tieferen Ebenen. Halas liegt mitten in dem weitem Land Everfrost das 
	südlich an Queynos Hills endet. Gleich neben Halas sind einige wilde Kreaturen und Monster 
	zu finden. Decaying Skeletons, Gnoll Pups, Icegoblin Welps, Wooly Spiderlings, White Wolfs 
	und kleine Icebears. Ab Level 2 kann man als Shamane da alles killen was da keucht und 
	fleucht. Wenn man sich etwas weiter von Halas entfernt, kommt man zu dem Land der mächtigen 
	Mammuts, Snow Leopards, Snow Wolfs, Giant Woolyspiderling und zu dem Gebäude der 
	fürchterlichen Ice Giants. Man sollte sich vorsehen, denn dieses Land ist sehr gefährlich 
	und da der Shamane erst im 9 Level einen guten Lightspell bekommt, haben schon viele in 
	der Dunkelheit vergeblich nach einen Guard gesucht als ihnen ein Icetrooper Snoworc oder 
	was noch schrecklicheres auf den Fersen war. Am nächsten Morgen gleicht das Feld einen 
	großen Friedhof, wo die Toten gefroren im ewigen Eis liegen. Manchmal sieht man nur noch 
	mehr die Blutsspuren.... Es kommt auch manchmal vor das ihr einem Skeleton oder 
	dem gefährlicheren IceBone Skeletons über den Weg lauft.
</P>

<P CLASS="P1">
	<B>Oggok</B> die Heimatstadt der Ogres. Das erste Respawngebiet 
	nennt sich Feerott. Freerott ist ein reiner Dschungel in der ewigen Nacht, wo sich viel 
	merkwürdige Gestalten herumtreiben. An Monster bzw. Tieren gibt es da Black Wolfs, Jungle 
	Spiderling, Jungle Spider, Alligatoren,  Lesser Scarebs usw.......... Man sollte sich 
	besser merken, wo man hinläuft, denn in diesem Dschungel verläuft man sich sehr schnell, 
	wenn man keinen hohen Sense Heading Skill hat. Näheres ist mir leider nicht bekannt. 
</P>

<P CLASS="P1">
	<B>Grobb</B> ist Oggok sehr ähnlich, aber die Heimatstadt der Trolle 
	hat was an sich, daß sie wunderschön wirken lässt (zumindest für Trolle). Sie liegt weit im 
	Süden Norrath , umgeben vom Innothule Swamp. Wie der Name es schon sagt, ist alles rund um 
	die Stadt ein einziges Sumpfgebiet, wo man schnell in die Vergessenheit versinkt. Viele 
	Gefahren lauern vor den Toren von Grobb, und der Sumpf ist noch das harmloseste. Ich denke 
	da an die fürchterlichen Froglok Tads, Schlangen, Kobolde, Decaying Skeletons, Large Rats, 
	Young Water Moccasin und natürlich den Swamp Alligatoren, um nur einige zu nennen. 
	Der Sumpf liegt im Reich des ewigen Schattens. Auch das geheime unerforschte Reich der
	noch unbekannten Frogloks liegt an der Grenze von diesen Sumpf. Nördlich davon liegt 
	Southern Desert of Ro mit den Orcs, Giant Spiders, Tree Snakes, Giant Scarebs und den 
	Shadow Wolfs. Sogar Orc Clerics sind da zu finden.<br>
	Mehr ist mir leider nicht bekannt, außer das es da von Orc Camps und Orcs nur so wimmelt. 
	Souther Desert of Ro sieht nach einen sehr guten und gefährlichen Respawngebiet aus.
</P>

