<P CLASS="P1">
	Diese Klasse zeichnet sich durch die guten Partyspells und Kampfkraft aus. Er und der 
	Barde sind Chars die diese Eigenschaften am besten vermischt haben. Am Anfang ist 
	das Zaubern etwas mühseliger, als mit einem Zauberer oder Mage. Er kann nur stumpfe 
	Waffen benutzen z.B. Keule, Morgenstern, 1Hand Stab usw. ....., und der beste Armor den 
	er tragen kann, ist der Kettenpanzer und nicht wie der Cleric auch Plate. Waffenmässig 
	sind beide gleich. Die Shamanenrassen haben aber gute Werte bei der Ausdauer und Agility 
	die die gute Kampfkraft bewirkt.<br>Die Shamanenrassen sind Zauberer mit den meisten 
	HPs und der wenigsten Mana, dennoch haben sie gute Angriffs-, Verteidigungs- und Heilspells, 
	die sie aber nicht so oft zaubern können. Ranger, Paladine und Shadow Knights haben 
	noch weniger Mana, aber das sind im Vordergrund Kämpfer da sie erst in den höheren Levels 
	ihre Spells bekommen.<br>
	Die Shamanen können auch Gegner gleichen Levels ohne größere Probleme im Nahkampf 
	besiegen, ohne ihre Magie zu gebrauchen. Sie sind aber jedem Krieger bei weitem im 
	Nahkampf ohne ihre Magie unterlegen. Jeder der auch gerne Nahkämpfer ist sollte den Shamanen 
	in Betracht ziehen.<br>
	In den höheren Levels ist ihm aber jeder andere reine Zauberer in Bereich Angriffsspells 
	überlegen. 
</P>

