<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Tarmantar<BR>
	<BR>
</P>

<P CLASS="P1">
	Es war in den Ebenen von Karana, als meine Gruppe sich entschloss ihrer eigenen Wege zu gehen und ich mich nach einem Platz zum schlafen umsehen mußte. Nach einiger Zeit fand ich eine wohnlich ausschauende Hütte. Ich öffnete die Tür und trat ein. Die Bewohnerin hatte nichts einzuwenden als ich sie nach einer Übernachtung fragte.
</P>

<P CLASS="P1">
	So legte ich mich in dieser Nacht zum ersten mal nach Wochen wieder in ein richtiges Bett. Am nächsten Morgen, ausgeruht von den Anstrengungen des letzten Tages, brach ich auf um eine neue Abenteurergruppe oder eine einfache Beute zu finden. Nun was soll ich sagen, nach einiger Zeit des Herumlaufens, setzte ich mich um zu rasten und ein wenig zu essen. Wie ich nun so da saß hörte ich das Brüllen eines Löwen. Ich fuhr auf und schaute mich suchend um, als ich mich schon inmitten eines Löwenrudels befand. Also, was soll ich sagen, drei Löwen und ich LV 20 Warrior! Was konnte ich tun? Weglaufen? Wenig ehrenhaft für einen Warrior, zumal die Biester sicher schneller waren als ich. So entschloß ich mich ihnen die Stirn zu bieten! Es war wohl mein Schicksal an diesem tage zu sterben, denn die Biester mit ihren gewaltigen Tatzen und ihrem wohl noch ungestilltem Hunger fielen regelrecht über mich her. Ich wollte mich schon in mein Schicksal fügen als ich eine Stimme hörte!
</P>

<P CLASS="P1">
	<I>"BRAUCHST DU HILFE?"</I> Ich fuhr herum, doch niemand zu sehen! Da hörte ich es wieder! <I>"ES SIEHT NICH GUT AUS! BRAUCHST DU HILFE?"</I> Woher kam diese Stimme? Eine Einbildung? Nein, das konnte nicht sein! Waren es die Götter, die mich doch nicht sterben sehen wollten? Ich weiß es nicht! Noch etwas mehr als 2 Bubbs an Lebenskraft blieben und ich hatte noch keinen der Löwen besiegen können. <I>"JA! JA! Hilf mir!"</I> stieß es aus mir heraus! Just in diesem Moment begann die Erde zu beben, begleitet von einem merkwürdigem Singsang. Die Energie mußte gewaltig sein, denn meine Haare standen zu Berge und mit einem gewaltigen Getöse stand plötzlich ein Wesen der Erden, nein, aus Erde neben mir und machte einen der Löwen nach dem anderen Fertig. Nach getaner Arbeit verschwand das Erdwesen am Horizont. Was war geschehen? Mit meinen mir zur Verfügung stehenden geistigen Fähigkeiten brachte ich es in mein Gedächtnis zurück: "ein Löwe trifft dich, ein Löwe trifft dich, du triffst einen Löwen, Arach beginnt seinen Zauber, Xenobartik trifft einen Löwen, der Löwe wurde von Xenobartik getötet".
</P>

<P CLASS="P1">
	Arach! Was war ein Arach? Ich nahm all meine Konzentration und geistigen Fähigkeiten zusammen! "WHO ARACH ALL", "WHO ARACH ALL", dann kam es mir in den Sinn! Arach, ein LV 34 Magier! Von ihnen hatte ich schon gehört und einige Male war ich mit diesen sogar schon jagen. Sie sind Meister im Erschaffen von allerlei nützlichen Dingen, auch jener Erdwesen, das mich gerettet hat! Nun, es war kein Gott der mich rettete, doch möchte ich hier meinen Dank sagen, denn dieser Tag war im Ganzen gesehen ein glücklicher Tag. Ich fand noch eine Gruppe in der ich mein 21 LV erreichte.
</P>

<p class="P0" ALIGN="CENTER"><I>Tarmantar</I></p>

