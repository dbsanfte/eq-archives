<P CLASS="P1">
	Trolle sind ein, für EQ-Verhältnisse sehr primitives Volk! Sie werden alleine noch von den Ogern übertroffen was schlechte Tischmanieren und andere Dinge, die ich hier nicht erwähnen möchte, angeht. Trolle haben von Natur aus Regeneration, was oft zu Missverständnissen führen kann, da Trolle auch kaum ein Schmerzempfinden haben. Man hört dann oft von seinen Gruppenmitgliedern (vorausgesetzt ihr spielt EQ mit RP) <I>"O GOTT SNAGLOX... DEIN ARM!! ER HAT IHN ABGEBISSEN!!"</I> Darauf ich <I>"Oooch, das wächst wieder nach."</I> Das Einzige was wir wirklich hassen ist FEUER!! Erstens ist es das einzige was uns wirkliche Schmerzen bereiten kann, und zweitens stoppt es unsere natürliche Regeneration.
</P>

<P CLASS="P1">
	Ausserdem kann ein Troll ruhig mal begriffstutzig sein (eignet sich sehr für Warrior), und eine Aufgabe einfach nicht verstehen. Trolle sind nicht zwingend böse, doch ihre Angewohnheit einfach alles zu essen (und sogar zu verdauen) kann dazu beitragen, dass sie einerseits gefürchtet werden, andererseits von diesen vermaledeiten Eruditen-Gelehrten dauernd wegen ihrer Magensäure gejagt werden. Trolle wachsen in einer äusserst brachialen und brutalen Gesellschaft auf. Wenn 3 Troll-Kinder zur Welt kommen, wird nur das Stärkste das zehnte Lebensjahr erreichen.
</P>

<P CLASS="P1">
	Trolle sind, wegen der oben genannten Schmerzunempfindlichkeit, äusserst brutal. Sie verstehen einfach nicht, wieso der kleine Hochelf jetzt schreit wenn man ihm das Rückgrat bricht. Diese Verhaltensweisen machen Trolle (ok ich gebs zu, auch die Körperhygiene ein wenig) nicht gerade zu einem der beliebtesten Völker in EQ. Aber es macht einfach Spass, wenn du vor Freeport sitzt und kein Mensch traut sich aus der Stadt, nur weil du Troll bist.
</P>

<P CLASS="P1">
	Zu den verschiedenen Klassen:
</P>

<P CLASS="P5">
	Shadow Knight:
</P>
<P CLASS="P1">
	Nicht wie bei anderen Völkern, werden die Shadowknights bei den Trollen nicht verachtet und ausgestossen, sondern sind ein einfacher Bestandteil ihrer Gesellschaft (das mag hauptsächlich daran liegen, dass die anderen Trolle die Gefahr, die von einem SK ausgeht nicht begreifen). Die SK sind die Intelligentesten Trolle und können sogar sehr gewitzt sein, aber sonst sind sie immer noch Trolle.
</P>

<P CLASS="P5">
	Der Warrior:
</P>
<P CLASS="P1">
	Was soll ich sagen? Dumm wie Brot, kein Fünkchen taktisches Verständnis, und trotzdem: Die meisten Leute fühlen sich sicher mit einem dieser beschränkten Gesellen in der Nähe, vorausgesetzt er haut die anderen. Sie sind das Musterbeispiel für den Troll. Gross, stark und stinkig. Ein Troll-Warrior haut so gut wie alles kurz und klein, und wenn er hungrig ist sollte man nicht vor ihm stehen. Wie man weiss isst er alles .... sogar DICH!!!
</P>

<P CLASS="P5">
	Der Schamane:
</P>
<P CLASS="P1">
	Die Schamanen sind die spirituellen Führer des Trollvolkes. Sie heilen, können übermenschliche Stärke verleihen und können auch noch Tränke brauen. Troll-Schamanen sind zwar Trolle, haben aber ein grösseres Verständnis für ihre Umwelt. Was nicht heissen soll, dass sie nicht alles in ihrem Weg niedermetzeln würden um an einen leckeren Elfenbraten zu kommen. Aber Troll-Schamanen sind oft die Umgänglichsten ihres Volkes. Nicht so düster wie die SKs und nicht so stinkig und verfressen wie die Warrior, aber sie sind immer noch Trolle, das sollte man nie vergessen.
</P>
	
