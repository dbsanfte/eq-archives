<P CLASS="P0" ALIGN="CENTER">
	Von und mit Elminsteer.<BR>
	<BR>
</P>

<P CLASS="P1">
	Tja, nun war es also soweit. Meine Eltern hatten sich endlich von mir trennen können und 
	ich machte mich auf in die große, weite Welt. Da ich nun schon immer gern mit dem magischen 
	Potential, dass mir angeboren war, experimentierte, dachte ich mir: "Warum nicht sich an der 
	Akadmie der Enchanter anmelden?"
</P>

<P CLASS="P1">
	Gesagt, getan, und schon kurze Zeit später stand ich vor den Toren der "heiligen" Hallen des 
	arkanen Wissens in Felwithe. Ich war jedoch nicht der einzige, was meine Pläne ein klein wenig 
	durcheinanderbrachte :-). Als ich  endlich als Bewerber Nummer 726 an die Reihe kam, vor dem 
	Gildenmeister zu sprechen gab ich mein Bestes. 
</P>

<P CLASS="P1">
	Scheinbar hatten ihm meine Worte imponiert, denn ich wurde als einer von insgesamt 
	121 Kandidaten in die engere Auswahl genommen. Den restlichen Bewerbern wurde geraten 
	zu üben und im nächsten Jahr wiederzukommen. Hier  standen wir nun, 121 Elfen und Elfinnen, 
	und harrten unseres Schicksals. Wir wurden für ein paar Stunden in einen großen Raum 
	gebracht, in welchem wir uns für die kommenden Prüfungen vorbereiten konnten. Es waren 
	auch Betten und Erfrischungen bereitgestellt für jene, die die erste der 5 Prüfungen 
	bereits hinter sich gebracht hatten.<br>
	Mehrere Wächter standen an der Tür und in Nischen des Raumes um zu verhindern, dass jene, 
	die bereits Prüfungen absolviert hatten, den anderen darüber berichten konnten.
 
</P>

<P CLASS="P1">
	So kam ich nun nach nur drei Stunden an die Reihe meine erste Prüfung abzulegen. Man führte 
	mich in einen Raum, der weder Fenster noch Fackeln enthielt. Nachdem die Türe geschlossen 
	wurde war darin nichts weiter zu sehen als tiefste Dunkelheit. Eine Stimme ertönte plötzlich 
	und gebot mir, zu einem Tisch zu gehen, welcher sich 5 Schritte vor mir befand. Ich leistete 
	diesem Befehl Folge und mir wurde aufgetragen, die Gegenstände dieses Tisches mit meinen 
	Händen zu untersuchen. 
</P>

<P CLASS="P1">
	Was ich fühlte, waren fünf verschiedene Dinge:<BR>
	<UL CLASS="P0">
	<li>Zum ersten ein kleiner Dolch, schartig und rostig, jedoch, wie mir schien, von recht ungewöhnlichem Aussehen (was ich allerdings nicht mit Sicherheit beurteilen konnte, da ich mich ja auf meine Hände verlassen musste).<br></li>
	<li>Als Zweites fühlte ich einen Ring, klein und zierlich, mit einem Edelstein in der Größe einer Walnuss darauf.<br></li>
	<li>Der dritte Gegenstand war ein Buch, es war dick, schien alt, und in Leder gebunden zu sein. Ich war bereits der Ansicht, die Antwort auf die mir gestellte Aufgabe zu kennen und beinahe hätte ich "Ich weiss es, es ist das Buch" hervorgerufen, doch nachdem ich diesen kurzen Moment der Euphorie überwunden hatte, machte ich mit dem Test weiter.<br></li>
	<li>Nummer vier war eine Krone, schwer, vermutlich aus Gold, mit Juwelen besetzt und einer Gravur, deren Bedeutung ich damals noch nicht kannte.<br></li>
	<li>Als ich beim fünften Gegenstand ankam, hätte ich vor Überraschung beinahe eine Schrei ausgestoßen. Noch bevor ich den Gegenstand auch nur berührt hatte, konnte ich bereits fühlen, dass er etwas besonderes war. Ich wußte nicht, was mich zu diesem Schluss veranlasste, jedoch war ich sicher, das dies des Rätsels Lösung war. Als ich die Oberfläche berührte war ich erstaunt, einen kleinen Zweig aus Holz zu fühlen. Das merkwürdige Schwingen, dass von ihm ausging, jedoch, blieb weiter bestehen, ja, es schien sich sogar zu verstärken.<br></li>
	</UL>
	Ich nahm den Zweig in die Hand, hielt ihn über meinen Kopf und sagte laut: "Dies ist der 
	wertvollste Gegenstand in auf dem Tisch, denn in ihm schwingt Magie."
</P>

<P CLASS="P1">
	Die Tür wurde geöffnet, ein Elf in einer weißen Robe kam herein und führte mich wortlos wieder 
	in den großen Warteraum zurück. 
</P>

<P CLASS="P1">
	Es vergingen zirka zwei weitere Stunden, bis man mich zur nächsten Prüfung abholte. Ich wurde 
	in einen Raum gebracht, in welchem mich ein Elf in roter Robe erwartete. Der gesamte Raum war 
	voll mit den edelsten, seltensten Waffen, die das Auge eines Norrathers je gesehen hat. Er sah 
	mir durchdringend in die Augen und fragte dann:<br>
	"Von all den Waffen in diesem Raum, welches ist die mächtigste?" Ich sah mich um und was ich 
	sah, ließ mir den Atem stocken. Von Hämmern und Schwertern aus feinem Stahl über magische 
	Klingen bis hin zu den sagenumwobenen Waffen wie etwa dem "Shining Star of Light" oder dem 
	"Soulfire" war alles hier vertreten. Ich überlegte recht lange bis mir plötzlich eine Idee 
	kam. Mit leeren Händen ging ich zu meinem Prüfer und legte dann eine Finger an meinen Kopf: 
	"Dies ist die mächtigste Waffe in diesem Raum, denn nur mit Hilfe meines Verstandes kann ich 
	die Magie kontrollieren." Er sah mir erneut durchdringend in die Augen, sagte jedoch kein 
	Wort und führte mich zurück in den Warteraum.
</P>

<P CLASS="P1">
	Kurz darauf, es mögen nur ein paar Minuten verstrichen sein, wurde ich erneut abgeholt. 
	Man brachte mich vor eine Tür und mit den Worten: "Viele ginge hinein, nur wenige kamen 
	heraus." verschwand mein Führer. Seine Worte bereiteten mir Unbehagen, allein, welche Wahl 
	hatte ich denn? Ich hatte mich für diesen Weg entschieden und es war nun an mir, ihn auch zu 
	gehen. Ich öffnete die Tür und sah vor mir einen langen Treppenabgang, dessen Ende ich nicht 
	erblicken konnte. Ich schluckte den Kloß, der sich in meinem Hals gebildet hatte, herunter und 
	ging langsam aber bestimmt hinab in die Finsternis. 
</P>

<P CLASS="P1">
	Nach geraumer Zeit kam ich an das Ende der Treppe. Der Gang, der sich nun vor mir auftat war 
	spärlich beleuchtet, selbst für die geübten Augen eines Elfen. Vorsichtig ging ich weiter, 
	ständig mit der Furcht im Nacken. Ich erreichte jedoch zu meiner Verwunderung ohne große 
	Mühen die andere Seite des Ganges. Dort war erneut eine Türe zu sehen. Der Türknauf jedoch 
	befand sich hinter einer Art mechanischer Schutzvorrichtung. Zehn kleine Messer bewegten 
	sich mit großer Geschwindigkeit vor dem Türknauf, und einem Unvorsichtigen wäre die Hand 
	ohne weiteres abgetrennt worden. Ich sah mich im Gang um, ob ich einen Schalter oder 
	ähnliches übersehen hätte.
</P>

<P CLASS="P1">
	Da bemerkte ich, dass unter jeder Fackel ein Wort in der Sprache der Alten geschrieben stand. 
	Ich ging also zurück an den Beginn des Ganges und las jedes der Worte, ohne ihren Sinn zu 
	verstehen. Als ich erneut bei der verschlossenen Türe ankam nahm ich mir ein Herz, schloss 
	die Augen, legte den Kopf in den Nacken und murmelte mehrmals die Worte in der Reihenfolge, 
	wie sie an den Wänden gestanden hatten. Ein Kribbeln durchfuhr meinen Körper und ich sah an
	mir herunter. Es schien, als ob ich mich um ein Vielfaches schneller bewegte, als noch vor 
	wenigen Sekunden. Ohne zögern (wurde auch mein Denken und Entscheiden beschleunigt?) griff 
	ich zu, drehte den Türknauf und öffnete die Türe. Meine Hand bewegte sich schneller als die 
	Schutzvorrichtung der Tür und ich konnte unbehelligt von den rasiermesserscharfen Klingen, 
	die dieser Konstruktion zu eigen waren, den Gang verlassen. Im Geiste verfluchte ich die 
	Gnome von Ak'Anon, denn nur diese kleinen Tüftler konnten eine solche Vorrichtung 
	konstruieren. 
</P>

<P CLASS="P1">
	Am Ende der Treppe erwartete mich bereits der Elf, der mich zu der Türe geführt hatte. Er 
	schien sichtlich beeindruckt von mir, sagte jedoch kein Wort. (Heute weiß ich, dass ich 
	während der gesamten Prüfung beobachtet wurde und dass, falls ich einen Fehler begangen 
	hätte, ich einen Sekundenbruchteil, bevor ich mich selbst verletzt hätte wäre ich in 
	Sicherheit teleportiert worden. Damals jedoch war ich mir dieser Tatsache nicht bewusst 
	und entsprechend negative Gedanken machte ich mir über die Unvorsichtigkeit und Brutalität 
	der Prüfer.)
</P>

<P CLASS="P1">
	Im Warteraum angelangt legte ich mich auf eines der bereitgestellten Betten und ruhte mich 
	von dieser Prüfung ein wenig aus. Ich betrachtete die anderen Kandidaten und während einige 
	ebenso erschöpft wie ich schienen war auf den Gesichtern der meisten Verbitterung zu sehen. 
	Während ich mir so meine Gedanken darüber machte, bei welcher der Prüfungen sie wohl versagt
	hatten wurde die Türe geöffnet und mein Name zusammen mit dem einiger anderer aufgerufen.
</P>

<P CLASS="P1">
	Wir wurden in Gruppen zu je elf Prüflingen vor einen Elfenmeister geführt, der selbst nach 
	elfischen Verhältnissen bereits alt wirkte. Er sah uns lange Zeit an, ohne auch nur ein 
	Wort zu sagen (dies schien mir hier irgendwie Sitte zu sein, obschon ich damals noch nicht 
	die Wichtigkeit der Ruhe und des Schweigens verstand). Die Stille schien nicht nur mich, 
	sondern auch meine Kameraden unruhig zu machen, allein es wagte keiner sie zu brechen. 
	Nach geraumer Zeit schließlich erhob der Meister seine unerwartet feste, tiefe Stimme und 
	meinte: "Begebt Euch nun in die Arena", er wies mit seiner linken Hand in Richtung eines 
	Steingebäudes, und sagte weiters: "Eure Aufgabe besteht darin, den gefährlichsten Gegner, 
	der sich in dieser Arena befindet zu besiegen und mir einen Beweis Eures Sieges zu 
	überbringen."
</P>

<P CLASS="P1">
	Wir wurden bis an den Eingang der Arena gebracht, danach wurden wir uns selbst überlassen.
	Das Gebäude war ein wahres Labyrinth, das einem jeden Minotauren Freude bereitet hätte, uns 
	jedoch wurde bei jedem Schritt mulmiger im Magen. Nach nur kurzer Zeit hatte ich meine 
	Kameraden verloren und musste mich so alleine auf die Jagd nach dem gefährlichsten Feind 
	machen.
</P>

<P CLASS="P1">
	Ich erlegte einige Schlangen, eine Kanalratte und konnte nur mit knapper Not einem Kobold 
	entkommen, indem ich ein Fallgitter auf seinen Körper herabfallen ließ. Ich war der festen 
	Überzeugung, dass dies der Gegner sei, von dem die größte Gefahr ausging, und so trennte ich 
	eine seiner Pfoten ab und begab mich Richtung Ausgang, als ich plötzlich in einem 
	Spiegelkabinett stand. Es durchfuhr mich wie ein Blitz: Auch diese Aufgabe war eine 
	des Geistes, und nicht des Körpers. Achtlos warf ich die Koboldpfote zu Boden und 
	verließ die Arena. Ich trat vor den Meister hin und meinte mit fester Stimme: "Dies 
	ist der Gegner, von dem die größte Gefahr ausgeht!" Ich kniet vor und meinte hernach: 
	"Doch ich kann Euch leider keinen Beweis meines Sieges über ihn bringen, denn der Kampf 
	gegen die eigene Angst und Unbeherrschtheit, der Kampf, den mein Verstand gegen meine 
	Instinkte zu führen verdammt ist, dieser Kampf währt bis in alle Ewigkeit und nie kann 
	einer der beiden Kontrahenten einen Sieg davontragen."
</P>

<P CLASS="P1">
	Des Meisters Augen ruhten auf mir, er nickte beifällig mit dem Kopf und mir wurde erlaubt, mich 
	wieder in den Warteraum zu begeben. Dort angekommen sah ich, das viele der Mitkandidaten vom 
	Kampfe gezeichnet waren. Ich erlaubt mir jedoch nicht, mich in Selbstverherrlichung zu üben, 
	ob der, meiner Meinung nach, bestandenen Prüfung. Erneut nahm ich ein Nickerchen auf einem 
	der Betten und vergönnte mir auch einige der Erfrischungen, bis schließlich die letzte 
	Prüfung zu absolvieren war.
</P>

<P CLASS="P1">
	Ich wurde zu einem der Meister der Akademie gebracht. Er trug mir auf die Augen zu schließen 
	und als ich sie wieder öffnete waren wir an einem Ort, der mir als die Klippen von Karana 
	vorgestellt wurde. Es ging Hunderte von Fuß hinab, der Grund der Klippen war nicht zu 
	erblicken. Der Meister sagte mir, dies sei der letzte Test, ein Test des Vertrauens in 
	die Götter und des Vertrauens in meine Meister. Er gab mir einen Trank und trug mir auf, 
	in zu trinken während ich von dieser Klippe sprang.<br>
	Als ich dies hörte, war meine Überzeugung, Enchanter zu werden völlig von mir gewichen. 
	Ich sollte von einer Klippe ins Nichts springen?!? Doch nach kurzem Überlegen kam ich zu 
	dem Schluß, dass die Meister wohl kaum eine Prüfung abnehmen würden, die die Prüflinge 
	nicht überleben würden. Ein letzter Blick von der Klippe ließ mich dennoch erschaudern, 
	doch mit der Überzeugung, dass meine Annahme richtig war und einem Gebet an Mithaniel Marr 
	auf den Lippen sprang ich in einem weiten Satz nach vorne über den Rand der Klippe. Während 
	des Falls war es mir, als ob ich aufgehört hätte zu existieren. Zeit und Raum waren 
	bedeutungslos, gestern heute, oben, unten, nichts war wirklich und doch alles das selbe.
	Ein Funke Erinnerung blitzte plötzlich wieder in mir auf: "Während des Falls sollst du den 
	Trank in einem Zug leeren." Das waren die Worte meines Meisters.
</P>

<P CLASS="P1">
	Ich nahm die Flasche aus einer Falte meiner Robe, öffnete sie und trank und verlor das 
	Bewußtsein. Als ich die Augen wieder öffnete stand ich am Rand der Klippe. Neben mir 
	befand sich der Meister, der mich hierher geführt hatte. War alles bloß ein Traum gewesen? 
	Hatte die Vorstellung einer so gefährlichen Tat meinen Verstand verwirrt? Verwundert sah ich 
	meinen Meister an.
</P>

<P CLASS="P1">
	Der jedoch, wie konnte es auch anders sein, sagte nichts, trug mir lediglich auf, die Augen zu 
	schließen und im nächsten Moment befanden wir uns wieder in der Akademie in Felwithe. Ich wurde 
	in den Warteraum geführt und musste dort nun an die vier Stunden ausharren, bis sich wieder 
	jemand meiner annahm.
</P>

<P CLASS="P1">
	Als die Tür geöffnet wurde, wurden elf Namen vorgelesen, welche den Wächtern zu folgen hatten.<br>
	"Arborlon, Sohn von Creteus!, Theroa, Tochter der Newa!, Hanola, Tochter von Onala!, Thisteus, 
	Sohn des Thorgarion!, Nisilion, Sohn des Tirbenus!, Kona, Tochter von Regnora!, Sicorus, Sohn 
	von Kalwaneus!, Hecola und Hepheus, Kinder des Orgawan!, Kio, Sohn von Jowerus! und Elminsteer,
	Sohn von Orberion! Folgt mir."
</P>

<P CLASS="P1">
	Die Aufregung überkam uns sichtlich. Würden wir die Auserwählten dieses Jahrganges sein? 
	Waren wie die ersten, die ausgeschieden waren? Niemand war bereit uns Auskunft zu geben. 
	So bracht man uns also in die Heiligen Hallen der Akademie des Arcanen Wissens. Wir begaben 
	uns in die Mitte des Rates der Meister, elf an der Zahl, die uns alle begutachteten. Der 
	älteste unter den Meistern erhob sich von seinem Stuhl und mit klarer Stimme verkündete er:
</P>

<P CLASS="P1">
	"Ihr elf, die ihr euch durch euer besonders starkes magisches Gespür, euer Verständnis für die 
	Magie und die Macht der Magie, eure Geistesschärfe, euer Vertrauen und euren Glauben von den 
	anderen hervorgehoben habt, ihr habt das Privileg erworben, in die Akademie des Arkanen Wissens 
	aufgenommen zu werden. Was ihr bis zum jetzigen Augenblick als euer Leben betrachtet, ist 
	Vergangenheit, ein Funke, der in einem Traum aufzuckt, während ihr ruht. Denn heute ist eure 
	Wiedergeburt. Willkommen, in der Gemeinschaft der Enchanter!"
</P>

<P CLASS="P1">
	"Ihr elf, die ihr euch durch euer besonders starkes magisches Gespür, euer Verständnis für die 
	Magie und die Macht der Magie, eure Geistesschärfe, euer Vertrauen und euren Glauben von den 
	anderen hervorgehoben habt, ihr habt das Privileg erworben, in die Akademie des Arkanen Wissens 
	aufgenommen zu werden. Was ihr bis zum jetzigen Augenblick als euer Leben betrachtet, ist 
	Vergangenheit, ein Funke, der in einem Traum aufzuckt, während ihr ruht. Denn heute ist eure 
	Wiedergeburt. Willkommen, in der Gemeinschaft der Enchanter!"
</P>

<P CLASS="P1">
	Ruckartig sprangen alle verbliebenen zehn Meister von ihren Stühlen auf, und begannen alle zur 
	selben Zeit mit demselben Spruch. Wir elf, die wir nun in der noblen Gilde der Enchanter 
	aufgenommen waren, wurden von gleißendem Licht umgeben, das in allen Farben des Regenbogens 
	strahlte. Eine wohlige Wärme überkam mich, die Erinnerung an das, was vor meinem Eintritt in 
	die Akademie war, verblasste, und ich betrachtete die Gemeinschaft, in der ich mich nun 
	befand zunehmend als meine Familie.<br>
	Als das Leuchten verblasste, waren wir gekleidet in feinst gewobene Roben aus rotem Samt. 
	Dies waren die Roben, die uns als Neulinge in der Akadmie auswiesen.
</P>

<P CLASS="P1">
	Die folgenden Jahre, die ich asketisch führen musste, waren nur mit Training ausgefüllt, 
	allein, es ist mir verboten, Details darüber preiszugeben. Es genügt jedoch, wenn ich sage, 
	dass es, wenn auch hart und oftmals einsam, doch eine wundervolle Zeit der Erleuchtung und des 
	Lernens war.
</P>

<P CLASS="P1">
	Nach elf Jahren hatte ich meine Ausbildung also endlich abgeschlossen und mit den 
	Glückwünschen meiner Meister, einem Zauberbuch mit den zwei elementaren Zaubern unserer 
	Kaste, der Gildentunika und einem Dolch zog ich in die Welt um zu lernen und andere zu 
	lehren. Ich erreichte also eine Kneipe im Herzen von Felwithe und freundete mich schon 
	nach nur wenigen Minuten mit einem Paladin an, der ebenfalls auf Abenteuer auszuziehen 
	plante. Sein Name war Tehbion. Wir beschlossen, am nächsten Morgen aufzubrechen und uns 
	die Stadt der Waldelfen anzusehen, die Kelethin heißt. 
</P>

<P CLASS="P1">
	Am nächsten Morgen zogen wir noch vor Sonnenaufgang los und hatten zunächst auch eine 
	ungestörte Reise. Doch nach zwei Stunden Fußmarsch attackierten uns plötzlich drei 
	finstere Orcs vom Crushbone Clan. Während mein Freund zwei der Schurken in Schach hielt 
	hatte ich Mühe, mich gegen diesen letzten verbleibenden zu verteidigen. Als mich ein Schlag 
	meines Kontrahenten auf die Schläfen traf verlor ich das Bewußtsein. Bevor alles schwarz um 
	mich wurde dachte ich bereits: "Dies also ist das Ende eines jungen, vielversprechenden Lebens."
</P>

<P CLASS="P1">
	Doch plötzlich fühlte ich, wie mich göttliche Macht durchfuhr. Thebion hatte den dritten 
	Gegner erlegt und mir mittels der Kraft, die ihm die Götter liehen, meine Wunden geheilt. 
	Doch trotz allem sah er sehr erschöpft aus. Wir beschlossen, die Orcs auf Wertgegenstände 
	zu untersuchen, ihre Leichen zu verbrennen und anschließend Rast zu machen. 
</P>

<P CLASS="P1">
	Nach einer halben Stunde Rast fühlten wir uns bereit weiterzureisen. Der Rest der 
	Reise verlief ohne Zwischenfälle, dank einer patrouillierenden Wache, die die 
	Straßen sicher hielt. So kamen wir endlich in Kelethin an, nahmen uns in einer 
	Schenke ein Zimmer bevor ich in das Land des Traumes entfloh dachte ich bei mir: 
	<b>"Enchanter werden ist nicht schwer, Enchanter bleiben leider sehr."</b>
</P>

<P CLASS="P0">
	<I>Elminsteer</I>
</P>

