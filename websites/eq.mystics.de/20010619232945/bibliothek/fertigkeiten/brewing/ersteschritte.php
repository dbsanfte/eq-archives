<P CLASS="P1">
	Eigentlich ist es nichtmal notwendig auch nur einen Skillpunkt in "Brewing" zu stecken. Mach einfach andauernd "Bog Juices" und der Skill fängt bald an zu steigen. Den Skill dann weiterhin mit "Bog Juices" bis auf 21 steigern, dann mit "Short Beer" weitermachen, etc.<br>
	Eine der schnellsten und einfachsten Methoden um Braumeister zu werden, ist sein Lager in West Freeport aufzuschlagen und folgende Abfolge einzuhalten:
</P>
<OL CLASS="P0">
<LI>Bog Juices<BR></LI>
<LI>Short Beer<BR></LI>
<LI>Mead<BR></LI>
<LI>Short Ale<BR></LI>
<LI>Ale<BR></LI>
<LI>Ol' Tujim's Fierce Brew<BR></LI>
<LI>Ginesh<BR><BR></LI>
</OL>
<P CLASS="P1">
	Einfach jedes Getränk solange brauen bis der Trivial-Wert erreicht ist (man bekommt eine entsprechende Meldung im Chatfenster) und dann mit dem Nächsten weitermachen. Die "Snake Scales" für den "Bog Juice" können in der ersten Hütte in "East Commonlands" gekauft werden. Danach kann bis zum Skill 135 in "West Freeport" gebraut werden, ohne die Zone jemals verlassen zu müssen. Die Zutaten kauft man bei "Krystin Charcoal", die Behälter im "Hog Callers Inn" und die "Brew Barrel" steht rechts vor dem "Hog Callers Inn".
</P>

<P CLASS="P1">
	Die Zutaten für die meisten Getränke bekommt man in Faydwer bzw. Odus nicht, also sollte man sich in Antonica aufhalten. Für die weitere Skillsteigerung benötigt man die Hilfe eines Rangers oder Druiden oder man verbringt viel Zeit damit sie am Markt (/auction) zu kaufen. Man beraucht jede Menge Zutaten, welche nur durch "Forage" gefunden werden können um schließlich durch das Brauen von "Ginesh" den max. Skill von 168 zu erreichen.
</P>

