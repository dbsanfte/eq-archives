<P CLASS="P1">
	ist eigentlich total einfach. Man benötigt einen <b>Behälter</b> (Faß, Flasche, Glas), ein paar <b>Zutaten</b> und eine <b>"Brewing Barrel"</b> (sieht aus wie ein Destillationsgerät; engl. "Still") worin man das Getränk zusammenmixt. In jeder Stadt, außer Grobb (und Paineel???), gibt es eine <b>"Brewing Barrel"</b>. Man gibt den <b>Behälter</b> und die <b>Zutaten</b> in die <b>"Brewing Barrel"</b> und drückt "Combine". Bei Erfolg erhält man ein oder mehrere Getränke. Falls es nicht klappt sind die Zutaten futsch, aber den Behälter bekommt man zurück.<br>
	<br>
	<b>Anmerkung:</b><br>
	Es darf immer nur <b>EINE EINHEIT</b> jeder Zutat in einen Slot der "Brewing Barrel" gegeben werden. Benötigt man als Zutat z.B. 2 "Short Beers", muß je 1 "Short Beer" in einen Slot gegeben werden!
</P>

<P CLASS="P1">
	Es gibt nur sehr wenige andere Möglichkeiten bezüglich der Verwendung von Getränken, außer dem Trinken. Diese Ausnahmen sind:
</P>
<UL CLASS="P0">
<LI><b>Bog Juices</b> ... benötigen Paladine für die Soulfire-Quest.<BR></LI>
<LI><b>Tumpy Tonics</b> ... benötigen Ranger für die Trueshot Bow-Quest.<BR></LI>
<LI><b>Heady Kiolas</b> ... brauchen Schneider (Tailor) um den Stoff haltbar zu machen.<BR></LI>
<LI><b>Gnomish Spirits</b> ... brauchen die Gnome-Tinkerer um "Firewater" zu erzeugen.<BR><BR></LI>
</UL>

