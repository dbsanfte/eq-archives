<P CLASS="P1">
	Brewing, die Braukunst, ist eine der leichtest zu erlernenden und billigsten Fertigkeiten. Die meisten Zutaten sind sehr oft zu looten oder sehr billig zu kaufen, und dann ist da noch das "Forage". Natürlich, die Kehrseite davon ist, daß man mit Brewing auch ganz sicher nicht reich wird. Aber in Bezug auf das Rollenspiel ist Brewing sicher sehr wertvoll und schließlich benötigen manche Klassen auch Brewing-Produkte für ihre Quests. Wie auch immer, die meisten Leute lernen das Brauen hauptsächlich aus Spaß an der Sache und um die Alkohol-Toleranz zu erhöhen.
</P>	

