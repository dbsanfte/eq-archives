<P CLASS="P1">
	Hach die Barden. Welch seltsames Volk sie doch sind. Selten einmal trifft man einen von ihnen alleine. Sie
	scheinen die Anwesenheit anderer zu atmen. Barden sind Philosophen, Dichter, Geschichtenerzähler, und
	Musiker. Sie reisen durch die Lande, verbreiten Neuigkeiten, und sammeln Geschichten des Ruhmes, die sie
	dann in einer gemütlichen Taverne oder des Nachts am Lagerfeuer in Form von Liedern oder Geschichten darbieten.
	Doch wer jetzt glaubt, ein Barde sei nichts anderes als eine Jahrmarktsattraktion, irrt sich. Viele von ihnen
	haben auf ihren Reisen Zauberlieder gelernt. Sie stärken ihre Kameraden mit einem schallenden Kampflied, oder
	beruhigen feindlich gesinnte Kreaturen mit einer sanften Weise. Wenn ihr einen Barden trefft lauscht ihm gut,
	sie haben viel zu erzählen.
</P>	

