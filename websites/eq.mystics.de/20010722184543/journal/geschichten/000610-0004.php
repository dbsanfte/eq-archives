	Geschrieben von Tedadar Flammenpfeil<BR>
	<BR>
</P>

<P CLASS="P1">
<b>4.Die Rückkehr des Lichts</b>
</P>

<P CLASS="P1">
Auf der Wüstenebene vor Freeport tobte die Schlacht. Die Menschen wurden immer mehr in
Richtung der gewaltigen Stadtmauern zurückgedrängt. Magische Entladungen schlugen
vernichtend in Körper ein, und manch ahnungsloser Wanderer kam nicht mehr lebend zur
Stadt zurück.
</P>

<P CLASS="P1">
In sicherem Abstand zu den Stadtwachen lagerte eine Gruppe von Dunkelelfen, vermischt
mit ein paar Trollen, und schlug immer wieder die Verteidungungsversuche der Menschen
nieder, welche die Besetzung nicht hinnehmen wollten. Aber nie konnte ein einzelner
Kämpfer der Gruppe etwas Ernsthaftes antun. 
</P>

<P CLASS="P1">
Gehetzt sprang Tedadar durch die karge Landschaft in Richtung der Stadt, und er versuchte
so gut wie möglich und mit wachsamen Blick den feindlichen Belagerern auszuweichen. Er
erreichte das Stadttor unbeschadet und sah auch schon seine Freunde die sich wieder im
Schutze der Tore versammelt hatten. <I>Verdammt, so haben wir das aber nicht geplant!</I>
sagte Valeria. <I>Eigentlich sollten Die vor uns flüchten!</I> und deutete hinaus auf die
Ebene, in die Richtung wo er die Gegner vermutete. <I>Ja, wir sind zu schwach für die
alle, ohne Verstärkung kommen wir da nicht weiter!"</I> antwortete Tedadar. <I>Hallo
Gilde!</I> konnte da der Ranger auf seinem Gildenstein lesen, der zur Kommunikation
innerhalb der Gilde diente. Travalgar und Nunja, ein Wizard und ein Magier, waren wieder
in der Welt erschienen. <I>Kommt sofort nach Freeport!</I>, schrieb Tedadar. <I>Wir haben
hier ein Problem mit ein Paar Darkies!</I><br>
<I>Wir sind aber noch in Butcherblock Moutains, und müssen erst mit dem Schiff
übersetzen!</I> antwortete Travalgar, <I>machen uns aber gleich auf den Weg!</I> Neuen
Mut erfasste Tedadar. Mit diesen 2 Freunden würden sie sich wohl eher behaupten können, da
sie auch eine grössere Erfahrung hatten wie manch anderer in Ihrer Gruppe, und ihre
Talente besser einsetzen konnten.
</P>

<P CLASS="P1">
<I>Bis das Schiff angekommen ist wird es schon noch ein Weilchen dauern</I>, sagte
Valeria, <I>wir sollten versuchen bis dahin noch ein paar Freunde zu finden!</I><br>
<I>Wenns noch mehr werden dann können wir die Darkies wieder in ihre Heimat verdrängen</I>,
sprach Tedadar. Doch noch hatten sie keine Verstärkung. Die Gruppe versuchte noch mit
einem oder zwei Ausfällen die Dunkelelfen und Trolle zu erwischen, doch ausrichten konnten
sie nichts. Es gab sogar wieder Tote auf ihrer Seite. Da kamen Venia, eine Encharterin, und
Miriamil, eine Druidin, zur Gruppe. Beide waren ungefähr so stark wie Tedadar. Die Gruppe
freute sich über ihre Verstärkung und leichter Optimismus machte sich breit. <I>Wenn Trav
und Nunja da sind müssen wir unsere Gruppen neu formieren!</I> sagte Miriamil, </I>Wenn wir
gut miteinander arbeiten sollten wir es schaffen können die Darkies zu vertreiben!</I><br>
<I>Wir sind nun fast in Freeport</I> schrieb Nunja über den Gildenstein. <I>Uns sollte
jemand abholen kommen, in Freeport kennen wir uns nicht so gut aus.</I><br>
<I>Ich gehe und hole sie am Hafen ab!</I>, sagte Valeria und rannte auch schon in die Stadt
hinein in Richtung des Hafens, wo das Schiff andocken würde. 
</P>

<P CLASS="P1">
<I>So, und wir planen schon einmal unseren Rückschlag!</I>, sagte Miriamil und sie setzten
sich zusammen und redeten über die Vorgehensweise ihrer Gruppe. Kurze Zeit später war
Valeria auch schon wieder zurück und hinter ihm waren auch Travalgar und Nunja, die ihre
sonst prächtigen Roben gegen schlichte Mäntel getauscht hatten. <I>Nun wollen wir mal sehen
ob wir die Dunkelelfen diesmal nicht schaffen!</I>, sagte Travalgar und lächelte. 
</P>

<P CLASS="P1">
Die Vorgehensweise war folgende: Tedadar und Valeria sollten an der Gruppenspitze bleiben
um die ersten Angriffe von den Magiern auf sich zu lenken, damit die anderen vier aus der
Gruppe, die nicht soviel einstecken konnten, ungehindert ihre Zaubersprüche auf die Gegner
richten konnten. Mit dieser Taktik hatten sie mehr Erfolg. Tedadar und Valeria stürzten auf
ein Ziel zu, welches dann fast sofort von verschiedenen magischen Entladungen getroffen
wurde. So konnten sie 2 der dunklen Elfen töten, und konnten ungehindert sich zur Stadt
zurückziehen, damit die Magier der Gruppe ihre magischen Energien sammeln konnten und die
2 Kämpfer sich wieder heilen konnten.
</P>

<P CLASS="P1">
Immer wieder schlug die Gruppe zusammen zu, und die Dunkelelfen konnten ihre Belagerung
nicht mehr halten und mussten wieder zurückweichen.
</P>

<P CLASS="P1">
<I>Los! Auf in die Commonlands! Lang genug haben sie sich dort breitgemacht!</I>, sagte der
Ranger. Die Gruppe stimmte zu, und alle zusammen liefen weiter in das Land hinein. Zuerst
sammelten sie sich am Rande der Wüste dann gingen sie wieder in Richtung des Gebirges wo
der Pass in das Dunkelelfenland lag. Die Kämpfe waren kurz und schrecklich, oftmals hüllten
die Zauberer ihre Gegner in prasselnde Flammen oder in alleserfrierende Kälte, so daß die
Kämpfer kaum noch Arbeit hatten, eigentlich nur die Erstangriffe der Opfer auf sich
lenkten, damit die Magier ungehindert ihre Sprüche sprechen konnten.
</P>

<P CLASS="P1">
Nun war die Gruppe wieder an dem Einschnitt im Gebirge, wo sie jeden Wiederstand
niedermachten. Voller Kampfeslust zogen sie weiter in das Gebirge hinein und drangen in das
Gebiet der Dunkelelfen vor, wo auch schon 2 der Elfen auf sie warteten. Aber diesmal
gnadenlos zurückgeschlagen wurden. Die Gruppe war nun wieder an dem Hügel angelangt und
wartete auf vorbeikommende Gegner die sie bekämpfen konnten. <I>Die Lighties sind wieder
da! Scheinbar haben sie noch nicht genug!</I> schrie wieder irgendeiner der
Dunkelelfen, und die Gruppe wusste das sich der Wiederstand nun wieder vergrössern würde. 
</P>

<P CLASS="P1">
Die ersten Angriffe waren aber leicht abzuwenden, und nach und nach rissen die Attacken
der Feinde ab. <I>Scheinbar haben sie diesmal erkannt, dass wir nicht zu unterschätzen
sind!</I>, sagte Valeria, <I>aber jetzt trauen sie sich nichtmehr her.</I><br>
<I>Sollen wir wieder zurückgehen?</I>, fragte Tedadar, <I>und noch ein paar einzelne
Darkies in den Commonlands jagen?</I> Das schien momentan der interresanteste Weg zu
sein, und so gingen sie alle wieder durch den Pass auf die andere Seite des Gebirges. 
</P>

<P CLASS="P1">
Nach einigen vereinzelten Kämpfen gegen verstreute Teile der Dunkelelfenarmee konnte er
sogar noch einen interessanten Ohrring erbeuteten, den er gleich anlegte. <I>So, genug
gekämpft für Heute</I>, sagte Tedadar. Und nachdem er sich von der weiterziehenden Gruppe
verabschiedet hatte, ging er wieder in Richtung der Menschenstadt. Die Belagerung hatte
kaum einen Einfluss auf das Leben in der Stadt, alles ging weiter seinen Weg. Müde vom
Kämpfen ging der Ranger in eine Herberge, in welcher er sich ein Zimmer mietete und fast
sofort in einen tiefen Schlaf fiel,...
</P>

<P CLASS="P1">
<b>ENDE</b>
</P>

