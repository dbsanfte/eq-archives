<P CLASS="P1">
	So, Du willst also ein Zauberer werden, mein Freund? Hmmm.... Dir sollte klar sein, daß dies 
	ein langer und gefahrvoller Weg sein wird, bis Du ein richtiger Zauberer bist. Natürlich 
	hast Du deine Begabung für Magie von Geburt an, aber die arkanen Energien zu kanalisieren 
	und auf die rechte Weise einzusetzen, das bedarf eines intensiven Studiums und einer hohen 
	praktischen Erfahrung. Und obwohl Dir einfache Sprüche von Anfang an gelingen werden, wirst 
	Du ständig nach größerem Wissen streben müssen, denn Dir muß klar sein, daß nur die 
	gehobenen Zauber Dir wirklich helfen können. Diese sind aber so machtvoll, daß sie eben 
	nur von geübten Hexenmeistern angewendet werden dürfen. Auch solltest Du Dir in den 
	Tagen des Beginns Deines Studiums des Arkanen Freunde suchen, die Dich beschützen. Denn 
	konzentriert auf Deine Beschwörungsformeln bist Du eine leichte Beute und es gibt viele 
	Dinge in dieser Welt, die Dich für eßbar halten. Bedenke auch, daß die Magie nicht 
	allmächtig ist, aber doch sehr vieles vermag. So wirst Du eines Tages in der Lage sein, 
	die mächtige Zerstörungskraft des Feuers herbeizurufen oder Portale zu öffnen, die Dir 
	ein schnelles Reisen ermöglichen. Doch all das hat seinen Preis: die Schwerter und Lanzen 
	werden Dir zu schwer sein und Du wirst auf immer von dem Wissen welches Dir die Bücher 
	vermitteln abhängig sein. Darum suche Dir Freunde in der Welt, mächtige Krieger oder 
	scharfäugige Bogenschützen. Zusammen werdet ihr eure Talente ergänzen und manches Abenteuer 
	bestehen. Vertraue Deinen Freunden und stehe ihnen mit Rat und Tat zur Seite. Sollte sich 
	Dir aber jemand in den Weg stellen, Deine Meditationen unterbrechen, Deine Zauber 
	verhindern oder gar Deine Bücher gefährden, so laß ihn die ganze Macht geballter magischer 
	Energie spüren. Vernichte Deine Feinde schnell und gründlich und laß sie nie an Dich heran 
	oder gar in Deinen Rücken kommen. Da Du Dir die Elemente untertan machst, versäume niemals 
	Karana oder Solusek Ro um Beistand zu bitten, nicht daß sie unbedingt eingreifen, aber es 
	kann zumindest nicht schaden. Macht willst Du und Macht bekommst Du, doch vergiß darüber 
	hinaus nie, daß auch andere große Fähigkeiten haben. Sieh niemals jemanden verächtlich an, 
	man muß ihm seine Begabung nicht ansehen. Unterschätze am allerwenigsten Deine Feinde, 
	denn dies ist der sicherste Tod dem Du erliegen kannst. Dein Wissen ist das höchste Deiner 
	Güter, hüte und mehre es. Du mußt Dein ganzes Streben danach ausrichten und Dich voll und 
	ganz der Magie hingeben. Entweder beherrschst Du sie, oder sie vernichtet Dich früher oder 
	später.
</P>
	
<P CLASS="P1">
	Du willst also immer noch ein Zauberer werden? So nimm diesen Folianten und lerne. Lerne 
	und experimentiere Tag und Nacht und komme mir nicht wieder unter die Augen, bis Du jede 
	einzelne Beschwörung selbst im Schlaf noch richtig zelebrierst. Und dann bekommst Du Band 
	II ...
</P>

