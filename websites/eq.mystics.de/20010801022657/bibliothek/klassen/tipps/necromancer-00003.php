<P CLASS="P0" ALIGN="CENTER">
	Diese Tips f&uuml;r Necromancer Level 8 bis 12 wurden von Sharookan erstellt. Der Stand der Infos ist 27.10.99.
</P>	

<P CLASS="P1">Über Startpunkt, Rasse etc. lasse ich mich hier nicht 
	aus, das haben andere schon getan.<br>
	Ich habe als Dark Elf in Neriak gestartet, weil es da die besten Hunting Grounds gibt 
	(meiner Meinung nach).<br>
	Level 8. Ein großer Augenblick, denn der Necro bekommt seine ersten guten Sprüche.
	Die wichtigsten möchte ich hier mal kurz erwähnen.
</P>

