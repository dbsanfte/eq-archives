<UL CLASS="P1">
	<LI>Dark Pact
	<UL>Verwandelt Hitponts in Mana.<br>
			Dieser Spell macht den Necro zu einem "non stop" Magier. Da durch diesen Spell
      Hit Points in Mana umgewandelt werden, und ihr euch von euren Gegnern Hitpoints 
			mittels Lifetap zurück holen könnt, kommen die langen MEDDING Phasen bei euch 
			wesentlich weniger häufig vor als bei den anderen Spellcastern.<br>
	</UL><BR></LI>
	<LI>Dark Empathy
	<UL>Zieht dem Caster HP ab und überträgt sie aufs Target.<br>
			Was gibt es schöneres als Gruppenmitglieder mit Hitpoints zu heilen, die man vorher
			einem Gegner abgezogen hat?<br>
	</UL><BR></LI>
	<LI>Impart Strenght
	<UL>Einer der wenigen Necro buffs.<br>
			Man überträgt 10 STRG von sich selbst auf das Target (auch das pet!).
			Mittels Siphon Life hat man das schnell wieder ausgebügelt...ist es nicht schön
			böse zu sein ? ;)<br>
	</UL><BR></LI>
	<LI>Gather shadows
	<UL>Endlich ein Spell mit dessen Hilfe man auch endlich mal in andere Städte gehen kann
			Aber Vorsicht! Der Spell hat keine bestimmte Wirkungsdauer, er kann nach 5 Sek. 
			wieder verschwinden oder nach 5 Minuten, egal welchen Level man hat !!<br>
	</UL><BR></LI>
	<LI>Ward Undead
	<UL>Der erste DD Spell eines Necros.<br>
			Er funktioniert leider nur gegen Untote, macht aber 41 DMG und das ist schon ganz 
			ordentlich, zumal er nur sehr wenig Mana kostet.<br>
	</UL><BR></LI>
</UL>

<P CLASS="P1">
	Man sieht schon, daß der Necro im 8. Level einiges mehr zu bieten hat als andere Spellcaster. 
	Durch seine Möglichkeiten des Heilens etc. ist er auch ein gern gesehener Gruppen Charakter, 
	tut sich aber im Solo Spiel leichter, da er allein grössere Gegner angreifen kann als andere 
	im selben Level.<br>
	Das ist jedoch Einstellungssache =)
</P>

