<P CLASS="P5">1. Homerun</P>

<P CLASS="P1">
	Der Necro und sein Pet. Ein gutes Team zusammen, ab dem 8. Level sollte man jedoch sein Pet 
	nicht mehr mittels "Follow me" hinterher schleifen.<br>
	Man stellt das Pet mit "Pet guard here" an einen leicht zu erreichenden Platz. Achtet drauf das 
	dieser Platz nicht unbedingt in den Laufruten von diversen mobs steht, da es sonst sein kann das 
	euer Pet weg ist wenn ihr wieder kommt. In Eurer Spell Leiste sollten sein: 
</P>

<UL CLASS="P1">
	<LI>Lifetap<BR></LI>
	<LI>Siphon Strenght<BR></LI>
	<LI>Poison Bolt<BR></LI>
	<LI>Clinging Darkness<BR></LI>
	<LI>Mend Bones<BR></LI>
	<LI>Dark Pact<BR></LI>
</UL>

<P CLASS="P1">
	So jetzt lauft ihr los und sucht euch einen Gegner, darf ruhig weiß oder gelb sein. Castet Poison 
	Bolt und Clinging Darkness auf ihn und rennt zurück zu eurem Pet. Wenn ihr bei ihm seid wird es 
	den Gegner automatisch angreifen. Jetzt einen Siphon Strenght casten, damit der Gegner nicht 
	mehr all zu hart trifft.<br>
	Falls ihr Dark Pact auf euch habt beschießt den Gegner dann und wann mit Life Tap um eure HP 
	wieder aufzufüllen.<br>
	Das Pet und die 2 Dot's sollten bei einem blauen reichen, bei gelben solltet ihr auch selbst 
	mit in den Nahkampf gehen.<br>
	Ist der Kampf vorbei, eben das Pet heilen lassen und weiter geht's.
</P>

<P CLASS="P5">2. Kiting</P>

<P CLASS="P1">
	Das Kiting ist ungefährlicher als der Homerun, dauert aber wesentlich länger und erfordert SOW. 
	Wenn ihr SOW habt, einfach alle DOT's auf den Gegner und solange laufen und nach-casten bis er 
	umfällt.<br>
	Wie gesagt, dauert sehr lange, man kann so aber auch gelbe Gegner sehr gefahrlos plätten =)
</P>

