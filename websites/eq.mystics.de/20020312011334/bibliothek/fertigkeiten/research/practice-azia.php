	<b>Lapis Lazuli</b> (stapelbar)<br>
	Am einfachsten bei einem Juwelier kaufen. Lapis Lazuli kann zwar auch manchmal bei Mobs gelootet werden, aber bevor man hier zuviel Zeit investiert, ist es besser diese billige Komponente einfach zu kaufen.
