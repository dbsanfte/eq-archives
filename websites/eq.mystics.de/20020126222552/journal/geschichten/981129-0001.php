<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Dugobalosh<BR>
	<BR>
</P>

<P CLASS="P1">
	Heute ist mir etwas seltsames widerfahren, nun das ist nichts ungewöhnliches da ich auf
	meinen Reisen ständig von einem Abenteuer ins nächste stolpere. Doch heute war ich mit
	einem guten alten Freund unterwegs, er ist ein Freund aus meiner Jugend und hat schon
	viel erlebt. Damals war er ein junger Gnomenzauberer, er konnte mit seinen Sprüchen gerade
	mal das Feuerholz entfachen, allerdings war er danach so erschöpft, daß er keinen Hunger
	mehr hatte und sofort einschlief.
</P>

<P CLASS="P1">
	Als wir damals auf unser erstes Monster trafen kippte er vor Angst aus den Schuhen und ich
	mußte mit meinen unbeholfenen Axthieben nicht nur den armen kleinen Babygoblin zurückdrängen,
	sondern auch noch meinen Freund vor hinterlistigen Baumwurzeln schützen, die waren schon
	früher seine größten Feinde.
</P>

<P CLASS="P1">
	Nun seit diesem ersten Abenteuer ist viel Zeit verstrichen, er ist alt geworden und scheint
	auch heute noch schnell außer Atem zu kommen, aber heute kippt er dann nicht einfach
	um, NEIN er schwebt dann ein wenig in der Gegend rum und findet nebenbei noch Zeit mich
	mit seinen klugen Sprüchen zu belehren.
</P>

<P CLASS="P1">
	Seine Magie scheint mit der Zeit mehr als ein nutzloser Zeitvertreib geworden zu sein, er
	zündet keine Kochfeuer mehr an, NEIN er zaubert ein Dreigängemenü auf den Tisch, nunja
	da er schon alt ist und etwas senil hatte er diesmal ERST das Essen, dann den Tisch
	herbeigezaubert, aber ich will nicht kleinlich sein.
</P>

<P CLASS="P1">
	Wir waren frisch gestärkt und ein netter Golem erschien auf Tarugars Befehl, so heißt
	mein Freund, oder hatte ich das schon gesagt? ... Naja ich sehe die Zeit ist auch nicht
	ganz spurlos an mir vorbei gezogen ... Wo war ich nochmal? AJA also der Golem übernahm
	die erste Wache und weil er das so fein konnte hat er auch gleich die 2. übernommen
	und sich netterweise am Morgen aus dem Staub gemacht um uns nicht weiter die Luft
	zum Atmen zu stehlen, ein feiner Diener war er ich werde mir auch einen anschaffen, wenn
	ich den Trick mit dem BESCHWÖREN erlernt habe.
</P>

<P CLASS="P1">
	Nun wir gingen also weiter und kamen auch bald an der alten Höhle an die laut Aussage
	eines Schäfers ein Ungeheuer beherbergen sollte. Es ist üblich, daß in einer
	Abenteuergruppe immer der Krieger als erstes in den Tod ... ähm ich meine in das
	Unbekannte hineinstolpert, also schulterte ich meine Axt und ging ein Liedchen trällernd
	in die dunkle Höhle, Tarugar erschuf ein Lichwölkchen und schickte es mit mir in die
	Dunkelheit, leider hatte die Wolke Angst im Dunkeln und versteckte sich gleich hinter
	dem Eingang.
</P>

<P CLASS="P1">
	Dann trat ich in etwas Weiches, ich konnte nichts sehen, also fühlte ich mit der Hand und
	stellte fest, daß es AUSSCHEIDUNGEN waren, nun war ich einerseits angeekelt aber auch
	erstaunt, denn wer auch immer solche großen Haufen machen konnte...
</P>

<P CLASS="P1">
	Ich konnte diesen Gedanken nicht beenden denn ein furchtbarer Gestank drang von oben auf
	mich herab und eine gurgelnde Stimme war zu hören: "Na was haben wir da? Feines Futter
	für den kleinen Gor?"
</P>

<P CLASS="P1">
	Ich bin ja kein Feigling, und haute ihm wegen dieser unfreundlichen Begrüßung und wegen
	des Geruchst erst einmal meine Axt in sein Gesicht, nun ich dachte es wäre sein Gesicht,
	aber er mußte wohl doch größer gewesen sein, denn meine Axt traf seinen KNÖCHEL, dabei
	hatte ich nach OBEN geschlagen.
</P>

<P CLASS="P1">
	Lange Rede kurzer Sinn, ich wurde von dem Untier gepackt und dachte noch HUI WELCH EIN
	GESTANK, als endlich mein Freund der Zauberer Anstallten machte sich in den Kampf
	einzumischen.
</P>

<P CLASS="P1">
	Ich dachte nun würde er einen gar mächtigen Zauber auf das Monster werfen als ich ein
	Jucken am ganzen Körper verspürte, ich konnte nun die Augen des Riesen erkennen und
	seine Hand führte mich auf direktem Wege in seinen Mund als ich gerade den schlechten
	Zustand seiner Zähne bewundern durfte, bemerkte ich wie der Riese zu schrumpfen anfing.
	Erst ganz langsam und ich merkte, daß der Riese Probleme hatte mich in seinen Rachen zu
	stopfen, dann wurde die Veränderung immer deutlicher und er wurde immer kleiner.
</P>

<P CLASS="P1">
	Ich wollte eben mit meiner Axt seinen Schädel spalten, als ich beim Ausholen gegen
	die Höhlendecke stieß.
</P>

<P CLASS="P1">
	Ich bin vielleicht nicht der klügste, aber da dämmerte es mir, ich war plötzlich groß.
</P>

<P CLASS="P1">
	Der Riese schlängelte sich durch meine Beine und schlüpfte aus der Höhle, ich nahm
	sofort die Verfolgung auf und prallte gegen den Eingang. Meine Faust war schon
	draußen, aber mehr paßte auch nicht hindurch.
</P>

<P CLASS="P1">
	Der Zauber hatte mich in dem Maße wachsen lassen wie er den Riesen verkleinert hatte.
</P>

<P CLASS="P1">
	Ich rief nach meinem Freund, der aber war draußen und wurde gerade von einem ZWERG mit
	einer Keule verprügelt, und nach kurzer Zeit war von Tarugar nur noch ein kleiner
	Fleischklumpen übrig.
</P>

<P CLASS="P1">
	Der Zauber war nicht von langer Dauer, ich wurde wieder klein und er Riese wieder groß.
</P>

<P CLASS="P1">
	Der Riese war nun frei, wie sich später herausstellte hatte ein anderer Magier ihn hier
	kurz vor uns eingesperrt damit er keinen Schaden mehr anrichten konnte.
	Nun und was lernen wir daraus ? Ich für meinen Teil werde die Finger von der Magie lassen,
	es sieht immer toll aus wenn ein Zauberkundiger seine Magier einsetzt und sie entwickeln
	mit der Zeit einen Hang zum Dramatischen, warum auch einen kleinen lausigen Feuerball
	auf ein Monster werfen, wenn es noch andere viel unterhaltsamere Zauber gibt mit denen
	man sein Können unter Beweis stellen kann.
</P>

<P CLASS="P1">
	Nun ich bin für starke Rüstungen und immer eine handbreit Stahl zwischen mir und meinen
	Gegnern, das ist mir lieber, obwohl ich dann diese Geschichte aus dem Bauch des Riesen
	erzählen würde, aber ich hätte dort immernoch meine Axt mit der ich mir den Weg freihacken
	könnte :-)
</P>

