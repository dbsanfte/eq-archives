<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Kleo Klaunix<BR>
	<BR>
</P>

<P CLASS="P1">
	An einem lauen Abend in der Gegend südlich der Stadt Freeport, ich hatte gerade meine Beförderung in die 16te Stufe in der Hierarchie der Rouges erhalten, beschloss ich den Gerüchten nachzugehen, die erzählten, dass in den Sümpfen südlich der Wüste in einem Dungeon voller Froesche eine Maske nur fuer Rogues und Barden zu holen ist, die mit der mächtigen Magie der Illusion ausgestattet ist.
</P>

<P CLASS="P1">
	So eine Beute lässt sich natuerlich keine Rouge die etwas auf sich hält entgehen, und so beschloss ich den Dungeon aufzusuchen. Am Ort des Geschehens angekommen, schlich ich mich durch die Gänge und Höhlen (war doch gut, dass ich das versteckte Schleichen so oft geübt hatte), bis ich in den tiefer gelegenen Teil des Dungeons kam. Hier gab es eine Menge untoter Frösche und ich traute meinen Fähigkeiten, mich ungesehen an ihnen vorbeizuschleichen, nicht ganz. Deshalb erbat ich mir von einem netten Priester, der an der Grenze zum oberen Teil rastete, einen magischen Spruch der mich vor den Untoten verstecken sollte. Derart gerüstet machte ich mich auf in die Gegend wo diese geheimnissvolle Maske zu finden sein sollte.
</P>

<P CLASS="P1">
	Nach einigem Suchen fand ich einen Platz wo ein Ghoul Assasin auftauchen sollte und dort sollte diese Maske zu finden sein. Ich traf dort einige Abenteurer, die um einiges maechtiger waren als ich und erkundigte mich nach der Maske. Ein kleiner Giftzwerg, der wohl sowas wie einen Rouge darzustellen versuchte schimpte sofort <I>"NEIN, DIE GEHOERT MIR!"</I> und so weiter. Naja dacht ich, ich schau mir das mal an. Die Leute dort waren zwar mächtig aber wenige und soo mächtig waren Sie auch wieder nicht. Also ging ich wieder, aber nur um mich hinter der Ecke zu verstecken und ungesehen zurückzuschleichen. Es dauerte auch nicht lange bis der gesuchte Assasin auftauchte. Der Zwerg jubelte und alle attackierten den Untoten und schafften es auch, ihn etwas weiter weg zu besiegen. Nur leider oder auch Gott sei Dank (fragt sich nur welcher Gott, eigentlich glaub ich an gar keinen) wurden diese unhöflichen Leute gleich darauf von einer Bande untoter Wizards überrascht und alle in Sekunden getötet. Der Zwerg starb mit den Worten auf den Lippen: <I>"MY MASK, MY MASK"</I> und dan ging er hin. *eg*
</P>

<P CLASS="P1">
	Die kluge Rogue, und ich bin seeeeeeehr klug :)) wartete in ihrem Versteck bis sich die Lage beruhiget hatte. Dann schlich ich zu den Leichen und schaute ob vielleicht ein Priester den Zwerg wiederbeleben wollte. Aber da keiner da war und die Gefahr bestand, dass die Leiche des Assasins verschwand durchsuchte ich sie und fand die gesuchte Maske. Schnell nahm ich sie an mich und zitterte mehr als ich schlich bis zur Grenze in den relatiev sicheren oberen Bereich. 
</P>

<P CLASS="P1">
	Wieder im Sumpf angekommen, beschwor ich die Magie in der Maske und musste feststellen, dass ich in eine Dunkelfin verwandelt wurde und auch so gut Sehen konnte wie diese, aber zum Glueck meine überragende Ausdauer und Stärke, die uns Barbaren so auszeichnete behalten hatte. Alles in allem finde ich war das ein guter Abend für eine Rouge auf Abwegen. :))
</P>

<P CLASS="P0" ALIGN="CENTER"><I>Kleo Klaunix</I></p>

