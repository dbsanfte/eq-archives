<P CLASS="P1">
	Während man als Magier sehr viele Dinge auf einfachste Weise einfach summonen kann, gibt es Zeiten in denen die Entscheidung Magier zu werden auf eine harte Probe gestellt wird. Ich spreche von "Research". Viele der benötigten Zaubersprüche sind ziemlich aufwendig zu erstellen.
</P>	

<P CLASS="P1">
	Nachdem Research beim Guild-Trainer aktiviert wurde, benötigt der junge Magier ein <b>"Elemental Grimoire"</b>. Dieser Container kann in der Magier-Guild gekauft werden. Um einen Zauberspruch zu erstellen gibt man alle erforderlichen "Zutaten" in diesen Container und drückt auf "Combine". Es gibt eine Unmenge von Büchern, die man bei den lokalen Händlern kaufen kann, und in denen die jeweils benötigten Ingredienzien für jeden einzelnen Zauberspruch beschrieben sind. Diese Bücher sind in unterschiedlichen Sprachen geschrieben.
</P>	

<P CLASS="P1">
	Jeder Zauberspruch benötigt Komponenten welche nicht immer einfach bei Händlern zu bekommen sind. Der Erwerb von einigen "Words" und benötigten Items kann als vollwertige Quest angesehen werden. Grundsätzlich sind die Komponenten bei Monstern des jeweiligen Spell-Levels zu findenen werden. Eine weitere Möglichkeit ist das Kaufen der Komponenten von anderen Spielern. Sobald man alle Komponenten für einen Spell beisammen hat, gibt man diese in das "Elemental Grimoire" und drückt "Combine".
</P>	

<P CLASS="P1">
	Um den Research-Skill zu steigern, und Fehlversuche zu reduzieren, kann der Magier wiederholt Low-Level-Spells erzeugen. Oder er tut dies mit den <A HREF="ersteschritte.php">Research-Quests</A>.
</P>

