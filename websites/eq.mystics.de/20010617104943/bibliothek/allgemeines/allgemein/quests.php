<P CLASS="P5">Vorbereitungen</P>

<P CLASS="P1">
	Als erstes solltet ihr euch 2 Sachen neben euren Computer legen, wenn ihr eine Quest 
	erledigen wollt, die grösser ist, als die kleinen Newbie Questen. Falls ihr keine 
	Komplettlösung habt, aus dem einen oder anderen Grund, ist das um so wichtiger, da 
	man sehr leicht die Namen der NPCs, von denen man etwas besorgen, oder denen man 
	etwas bringen soll, vergisst. Diese beiden Sachen wären:
</P>
<UL CLASS="P0">
	<LI>ein Schreibblock<BR></LI>
	<LI>Stift<BR></LI>
</UL>
<P CLASS="P1">
	Eventuell solltet ihr auch noch ausgedruckte Karten der Zonen bereithalten, falls die 
	Quests umfangreicher sind.<BR><BR>
	Dann kann es ja los gehen.
</P>
	
<P CLASS="P5">Von wem erhalte ich eine Quest?</P>

<P CLASS="P1">
	Als erstes muss man feststellen, dass nicht jeder NPC eine Quest vergibt. Die meisten Quest 
	NPCs sind in Gilden zu finden, oder herumstreifende NPCs, die eigentlich keine Bedeutung 
	haben. Schreibt euch den Namen dieser NPCs sorgfältig auf, und auch möglichst deren 
	Standort, wenn er den angegeben sein sollte.
</P>

<P CLASS="P5">Was muss ich nun tun?</P>

<P CLASS="P1">
	Geht nun zu einem dieser NPCs, die eine Quest vergeben. Nehmt ihn als Target und drückt die Taste 
	'H' um ihm ein fröhliches Hail entgegen zu schallen. Er wird sich euch zudrehen und 
	euch erstmal mustern, sollte eure Faction hoch genug sein, wird er euch eine Quest vergeben. 
	Wie das so ungefähr abläuft will ich euch hier zeigen. Den Spieler nenne ich jetzt einfach mal 
	Fussel und der NPC hört ab jetzt auf den schönen Namen Grum.<br><br>
	<b>Fussel</b> "<i>Hail Grum</i>"<br>
	<b>Grum</b> "<i>Ah, Hail Fussel, what are you doing out here? Hunting orcs too? I hate these vile 
	beasts, I always search for new honorable warrior who will help me to destroy 
	[the clan of lulatsch]!</i>"<br>
	(So, nun schaut mal. Es gibt einen Abschnitt der in eckigen Klammern eingefasst ist. Um 
	hier weiter zu kommen muss man in seiner Antwort diesen Abschnitt mit einbeziehen.)<br>
	<b>Fussel</b> "<i>What about the clan of lulatsch ?</i>"<br>
	(Wenn ihr Schlüsselwörter in eurer Frage unterbringt, achtet darauf, daß abschließende Punkte und
	Fragezeichen nicht direkt im Anschluß an die Schlüsselwörter schreibt. Laßt eine Leerzeichen 
	dazwischen oder laßt sie komplett weg, sonst wird es womöglich nicht richtig erkannt.)<br>
	<b>Grum</b> "<i>Ah, these vile orcs, they killed my brother. We were in their castle up the 
	hills. With some other friends we wanted to destroy this castle of terror, but after we got 
	deeper into the castle, we met an [evil Troll] who killed my brother and all my friends. 
	I was the only one who survived.</i>"<br>
	<b>Fussel</b> "<i>What evil Troll</i>"<br>
	<b>Grum</b> "<i>Garg is his name, he is a shadowknight who is the leader of these orcs. I want 
	you to kill him and [bring his head] back to me.</i>"<br>
	<b>Fussel</b> "<i>I will bring his head back</i>"<br>
	<b>Grum</b> "<i>Good, I knew you would do it! I will wait for you to revenge my brother and my 
	friends.</i>"<br><br>
	So, nun habt ihr die Quest, oder besser, ihr wisst was ihr tun müsst. Denn die meisten Questen 
	sind immer "freigeschaltet" und man kommt ohne das 'Hail' aus. Aber, dann macht sie natürlich 
	nicht soviel Spass.<br>
	Manche Questen müssen aber erst "freigeschaltet" werden, also es muss eine 
	kleine Quest davor erledigt werden. Andere wiederum werden nie mit Schlüsselwörtern angeboten,
	bei diesen muß man dann durch Hinweise von anderen NPCs oder durch irgendwelche Beschreibungen
	auf Schriftrollen o.ä. draufkommen.
</P>

<P CLASS="P5">Was für Questen gibt es?</P>

<P CLASS="P1">
	Es gibt in den Landen Norraths verschiedene Questen. Man kann sie in 2 große Arten unterteilen. 
	Da wären einmal die Newbiequesten. Diese geben keine großen Items, aber man kann als Newbie 
	schon recht gut Geld damit verdienen, da man ab und zu Rusty Weapons und Geld kriegt. Als 
	Newbie sind sie eine nicht zu unterschätzende Geldquelle. Zum anderen die Questen für Spieler 
	höheren Levels. Sie haben immer brauchbare bis hervorragende Items als  Ziel und sind recht 
	begehrt.<br><br>
	So, ich hoffe euch haben diese kleinen Ausführungen ein wenig geholfen und ihr könnt nun eure 
	ersten Quests ohne Probleme lösen.<br><br>
	So long...
</P>

