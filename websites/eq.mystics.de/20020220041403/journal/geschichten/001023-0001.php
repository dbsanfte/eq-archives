<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Tyrail<BR>
	<BR>
</P>

<P CLASS="P1">
Swifffft, haarscharf fuhr die Klinge der Sense über meinen Kopf hinweg, der Orc Emissionarie
grunzt laut vor Wut, aus der gebeugten Haltung stieß ich meine Kralle nach oben. Ich spürte
wie sie durch das Fleisch, zwischen den Rippen in den Orc eindrang und genau in sein Herz
traf. Bombaxx, ein starker Zwerg, drang mit lautem Geschrei auf den Orc ein und schlug ihm
noch im Fallen den Kopf ab.
</P>

<P CLASS="P1">
Unsere Gefährten wurden von allen Seiten von Orc´s bedrängt. Einer unsere beiden Magier wurde
von zwei Centurios angegriffen. Mit einem weit ausgeholten Schlag mit meiner Kralle schnitt
ich beiden den Rücken auf. Voller Zorn wandten sie sich von ihrem Caster ab und schlug auf
mich ein. Das gab unserem Caster die Möglichkeit sie beide in einem glühenden Feuerball zu
vernichten.
</P>

<P CLASS="P1">
Der Kampfeslärm schien kein Ende zu nehmen, aber die Anzahl der Orc´s wurde immer geringer.
Alle starben sie unter der Axt von Bombaxx, den Feuerbällen unserer Caster und durch die
tödlichen Schnitte der Kralle.
</P>

<P CLASS="P1">
Als alle Orc´s besiegt waren sammelten wir alle Gegenstände ein und versorgten unsere
zahlreichen Wunden. Alle meine Begleiter waren mit wunderschönen Magischen-Roben und hell
schimmernden Rüstungen bekleidet. Ich dagegen besaß nur meine Leder-Tunic und die
Leder-Hosen. Verwundert dachte ich über die Anzahl der Fähigkeiten nach, die meine Begleiter
haben mussten, ich sollte eines besseren belehrt werden. Alle meine Mitstreiter waren noch
nicht viel älter wie ich. Ich ließ es mir nicht nehmen nach der Quelle ihres Reichtums
zu fragen. Eine klare Antwort jedoch hatte keiner für mich. Manche Gegenstände hatten sie
ihren gefallenen Gegnern entnommen, andere getauscht und andere wiederum erbettelt. Um solche
wundersame Gegenstände von Monster zu bekommen war ich noch zu schwach und zu betteln war
mir zuwider. So entfernte ich mich von meinen Kampfgefährten und verließ Crushbone in
Richtung Kelethin. Auf meinem Weg machte ich mir Gedanken über das Erlangen solch edler
Gegenstände und beschloss meine wenigen Habseligkeiten zu verkaufen und dafür ein paar
anständige Rüstungsteile, Ringe, Schuhe und was es noch so gab, zu ersteigern. 
</P>

<P CLASS="P1">
Meine Gedanken lagen bei Elaine und Quelbor, eine wunderschöne Hochelfin und ein Gnom, die
mich bei vielen Schlachten am Orc-Hill, als noch junger Ranger, unterstützten. Sie waren
nach Freeport ausgezogen um weitere Abenteuer zu bestehen. Ich war voller Trauer über ihre
Abreise. Es sollte jedoch nicht lange dauern bis ich sie wieder sah.
</P>

<P CLASS="P1">
In Kelethin angekommen suchte ich sofort die Bank auf um meine Gegenstände abzulegen und
mein Geld zu deponieren. Ich schlenderte über den Vorplatz der Bank wo viele Rassen sich
trafen um mit, nicht selten sehr bedeutungsvollen, Gegenständen zu handeln. Es kam auch
nicht selten vor, dass Dunkelelfen an diesen Platz kamen, jedoch sah man nicht lange darauf
irgendwo zwischen den Handelnden ihren verwesenden Leichname liegen.
</P>

<P CLASS="P1">
Es war einer namens Balvaran, der aussah wie einst Gadlul, der mir die Kralle schenkte, der
mir viele Gegenstände anvertraute, darunter auch ein Dwarven Ringmail Tunic, Banded Mail
Leggins, Sleeves, Cloak´s und viel weitere nützliche Gegenstände. Als ich ihm meinen wenigen
Besitz, um die 24pp, geben wollte lachte er nur und sagte <I>"Du wirst es nötiger brauchen
wie ich!"</I>. Voller Freude über dieses Glück legte ich die wunderschön glänzenden
Ringpanzer an und bestaunte mich selbst. Ich verkaufte meine zerfetzte Leder-Kleidung um mit
dem Erlös noch weiteren Handel zu treiben.
</P>

<P CLASS="P1">
Das Glück sollte mich noch nicht verlassen. Ich fragte höflich einen Herrn nach einer
2H Slashing Waffe und bot ihm all meinen Reichtum dafür zu geben, der leider nur 42pp
betrug. Er bat mich nach Felwithe zur Bank um sich mit mir zu unterhalten. Ich machte mich
in meiner neuen Kleidung auf den Weg und dachte er wird mir nichts billiger lassen wenn er
mich sieht. Zu meiner Überraschung war er ein einfacher Mensch, der aber eine riesige Lanze
bei sich trug und in einer so gehärteten Rüstung steckte wie ich sie noch nie sah. Er
belächelte mich und reichte mir ein Schwert wie ich es ebenfalls noch nie sah. Teils aus
Metall und teils aus durchsichtig schimmerndem Kristall. Die Magie umrang die Waffe, weitere
voller Magie steckende Gegenstände reichte er mir und zum Schluss noch ein Beutel mit 150pp.
Ich war der Ohnmacht nahe da ich solch freundliches Tun nicht von einem Mensch erwartete.
Ich verbeugte mich mehrfach und sagte ihm, dass ich, sollte ich je so mächtig und stark
werden einem Krieger wie er es war zu helfen, werde ich da sein und meine Schuld begleichen.
Er sagte nur: <I>"Ich bin 55 lvl alt, bis Du soweit bist wird meine Seele ewigen Frieden
gefunden haben und nicht mehr in diesem schrecklichen Leben weilen müssen!"</I>.<br>
So kam es, dass ich der Kralle untreu wurde und ein mächtiges Schwert führe.
</P>

<P CLASS="P1">
Diese Gegenstände sind mir noch heute ein treuer Begleiter im Kampf. Und jede Nacht wenn
ich mich in den Wäldern zur Ruhe lege, bete ich für meine Kampfgefährten, für die beiden so
gleichen Krieger Gadlul und Balvaran und nicht zu guter Letzt für denn heldenhaften Menschen
dessen Name ich nie behalten habe (Vor Freude über die Waffen *g*)
</P>

<P class="P0" ALIGN="CENTER"><I>Tyrail, Waldelfen Ranger</I></P>

