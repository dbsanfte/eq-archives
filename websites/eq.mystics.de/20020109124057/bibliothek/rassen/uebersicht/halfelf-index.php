		von Menschen mit Waldelfen entstanden, gehört diese Rasse zu keiner der beiden Urahnen. Sie sind, man könnte fast 
		sagen heimatlos, leben sie doch bei den Menschen als auch Waldelfen. Halbelfen besitzen keinen eigenen Staat und 
		keine eigene Stadt, was sie zu Wanderern der Welt macht. Äußerlich sind Sie kaum von einem Menschen zu 
		unterscheiden, jedoch haben sie zugespitzte Ohren, und ihre Gesichtszüge sind schärfer als die eines normalen 
