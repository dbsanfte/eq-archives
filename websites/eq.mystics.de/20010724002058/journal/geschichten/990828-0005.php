<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Wismerhill<BR>
	<BR>
</P>

<P CLASS="P1">
	Tja, wo soll ich da anfangen. Meine Erfahrungen mit dem Volk der Zwerge sind mannigfaltig. 
	Es fing alles damit an, das ich ,junger Hochelfen Paladin, durch die Berge der Butcherblocks 
	wanderte auf der Suche nach diesen gefürchteten Untoten Zwergen-Skeletten. Als ich 
	Schließlich auch den Ort der dunklen Brut entdeckt hatte traf ich auf einen 
	Zwergenkrieger und einen Gnomenmagier. Nun, wir begrüßten uns wie es der Anstand 
	verlangte; ich war etwas zugeknöpft, hatte ich doch Zwerge bisher nicht oft zu 
	Gesicht bekommen und wußte nichts über dieses kleine Volk. Der Platz schien gut 
	und es gelang uns nebeneinander gut zu kämpfen ohne uns zu stören. Dann tauchten 
	weitere Hochelfen auf, und wie ich gestehen muß waren sie alles andere als 
	Paradebeispiele meines Volkes. Sie waren frech, unhöflich und unverschämt. Da wir nun 
	nicht alleine dastanden die Untoten in ihr endgültiges Grab zu befördern, entschloß 
	sich der Zwerg sich mit mir zu gruppieren. Gesagt getan waren wir zusammen mit dem 
	Gnom ein recht gutes Team und behaupteten uns lange gegen die Schar der Skelette. 
	Der Zwerg war mir schnell sehr sympathisch, ebenso der Gnom, und ich dachte bei mir 
	"Eine nette kleine Rasse, und als Gefährten durchaus zu gebrauchen".
</P>

<P CLASS="P1">
	Nun begab es sich, das urplötzlich die Ogerin Zarchoomi, eine Geißel dieser Regionen, 
	vor uns stand und kräftig auf meine Gefährten begann einzuschlagen. Da sie noch jung 
	und unerfahren waren lenkte ich die Aufmerksamkeit des Monsters auf mich und gebot 
	den anderen zu fliehen. Und das war sicher eine der tiefgreifendsten Entscheidungen 
	die ich in meinem jungen Leben bisher getroffen hatte.<br>
	Unaufhörlich schlug Zarchoomi auf mich ein während ich immer wieder meinen Gefährten 
	hinterherrief "Lauft, Lauft, rennt um euer Leben!". Als ich sie in Sicherheit wußte 
	war ich schon schwer angeschlagen, aber Tunare schenkte mir ein letztes Aufbäumen, 
	ich murmelte einige Worte und mit einer Geste gelang der helfende Zauber: Ich stand 
	wieder frisch und kampfbereit mit einem zweiten Leben vor dem Ungeheuer.
</P>

<P CLASS="P1">
	Doch der Kampf lief schlecht für mich, unaufhörlich hagelten Schläge und Zaubersprüche auf 
	mich ein und ich sah bald ebenfalls keine andere Alternative als zu fliehen. Ich 
	versuchte also Zarchoomi zu entkommen, doch wehe, mein Glück hatte mich verlassen und 
	ich verlief mich, so daß ich den rettenden Wachposten nichtmehr erreichte. Da starb 
	ich also, erschlagen von der Ogerin Zarchoomi.<br>
	Doch Tunare wollte mich noch nicht in ihren Hallen, denn schickte sie mich zurück in die Welt.<br>
	Ich sammelte alsbald meine Habseligkeiten wieder auf und traf auch meine Gefährten 
	wieder, die ich vor dem sicheren Tod bewahrt hatte. Sie waren unaussprechlich 
	dankbar für meine Hilfe. Sie konnten es kaum fassen das ich ihnen das Leben gerettet 
	hatte und meines dafür gegeben habe ihnen die Flucht zu ermöglichen. Nichts wollte 
	ich haben für meinen Dienst, so blieb ihnen bloß mir ihre Freundschaft anzubieten und 
	ich zögerte nicht diese dankbar anzunehmen.<br>
</P>

<P CLASS="P1">
	Das alles ist schon lange her, aber der kleine Zwergenkrieger Dugobalosh, dem ich damals 
	zusammen mit Sentik, dem Gnomen-Magier, das Leben rettete, ist einer meiner besten und 
	treuesten Gefährten geworden und wir haben schon etliche Schlachten zusammen geschlagen. 
	Wir Zanken, wir Lachen und wir Sterben zusammen, aber seine Freundschaft möchte ich um 
	keinen Preis missen.
</P> 

<P CLASS="P1">
	Jedesmal, wenn ich wie nun über diesen Tag nachdenke, weiß ich, das ich jederzeit wieder 
	so handeln würde.
</P>

<P CLASS="P0">
	<I>Wismerhill, für alle Elfen und Zwerge die in Freundschaft miteinander leben.</I>
</P>

