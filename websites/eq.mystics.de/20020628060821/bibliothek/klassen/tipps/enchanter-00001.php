<P CLASS="P1">
	Die Skills des Enchanters unterscheiden sich im wesentlichen nicht von denen anderer
	Charaktere! Der einzige Unterschied wie oben erwähnt ist der, daß der Enchanter ab dem 
	8 Level magische Gegenstände erschaffen kann. Dieser Skill ist eigentlich ein Muß für
	jeden Enchanter, bringt aber bedauerlicher Weise immense Kosten mit sich. Als Hinweis
	hierfür schlage ich vor den Skill, soweit das Geld vorhanden ist beim Meister bis auf
	20 Punkte zu steigern, da dies billiger ist, als ihn durch das erschaffen von Schmuck-
	stücken zu trainieren, zumal man dann eine Basis hat, auf der man beim erschaffen von
	Gegenständen nicht mehr zu oft versagt. Magische Gegenstände mit Silber sollten erst ab
	einem Skill-level von ca 30 Pkt und Electrum von 40 Pkt versucht werden(weiter 
	Informationen siehe Skill gem crafting).
</P>

