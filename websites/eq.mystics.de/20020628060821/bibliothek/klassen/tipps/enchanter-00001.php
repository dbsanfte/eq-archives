<P CLASS="P1">
	Der Enchanter ist ein Magier Charakter, der sich sowohl auf den Umgang von offensiven,
	vielmehr aber auf den Umgang gruppenstärkender sowie Verwandlungs-, Bezauberungs- und 
	Illusionszauber versteht. Er steht nur dem Wizard in den Angriffszaubern nach, welches
	er aber durch die Fähigkeit ein Pet herbeizuzaubern mehr als wett macht, da es seinerseits
	im Gegensatz zu dem Pet des Necromanten Wesen treffen kann, die nur durch magische Angriffe
	verwundet werden können. Der Enchanter ist in jeder Lebenslage überlebensfähig, so er denn
	keinen Kampf gegen zahlenmäßig überlegene Gegner aufnimmt! Er kann sich überall Mithilfe
	seiner Illusionen und Unsichtbarkeitszauber bewegen und Handel betreiben, was anderen
	Charakterklassen nicht zwangsläufig aufgrund des Faction Standings möglich ist.<br>
	So sein Erschaffer denn will, ist der Enchanter später in der Lage magische Gegenstände 
	selbst herzustellen! Da der Enchanter mit dieser Fähigkeit ausgestattet ist und die größte
	Anzahl an Zaubersprüchen hat, sowie später den sehr teuren Skill Research erhält, ist er
	der wahrscheinlich teuerste Charakter im Spiel!
</P>

