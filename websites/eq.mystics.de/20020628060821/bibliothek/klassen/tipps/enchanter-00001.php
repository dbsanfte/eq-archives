<P CLASS="P1">
	Den Angriff übernimmt der Enchanter selbst, da die Pet Kommandos zumindest bei mir
	nicht funktionieren! Er eröffnet den Kampf mit Sprüchen, die den Gegner schwächen und 
	nachdem er den ersten Angriff erdulden durfte, zieht er sich ein Stück zurück, da dann das
	Pet den Nahkampf übernimmt. Dann beginnt der Enchanter damit den Gegner mit offensiven
	Sprüchen niederzukämpfen!<br>
	Die Verteidigung ist durch die ständige Anwesenheit des Pets gewährleistet
	(merke: nach dem man eine neue Zone betritt ist das Pet verschwunden und ohne Pet ist
	der Enchanter nur halb so stark, also immer ein Pet zaubern!) und durch die Verwendung
	von Schutzsprüchen. Sollte es mal brenzlich werden, so gibt das Pet meist dem Spieler
	die Distanz zum Gegner um sich zurückzuziehen!
</P>

