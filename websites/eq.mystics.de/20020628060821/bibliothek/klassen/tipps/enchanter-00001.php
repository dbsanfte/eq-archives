<P CLASS="P1">
	Man sollte sich eine Liste der Zaubersprüche ausdrucken und sich die Zauber nach irgend-
	welchen Gesichtspunkten markieren, da der Enchanter die meisten Zaubersprüche von allen
	Zauberern hat.<br>
	Als Hinweis: Der Pet Spruch sowie der Angriffs Zauber sind die wichtigsten Sprüche. Da
	man nicht alle Sprüche überall bekommen kann, will ich als Tip die Zone High Hold 
	erwähnen, in der sich High Hold Keep befindet, dort bekommt man alle Pet Sprüche!<br>
	Der Spruch Mesmerize, dessen Wirkung noch unbekannt ist (4 lvl) fügt dem Gegner eine 
	Benommenheit zu! 
</P>

