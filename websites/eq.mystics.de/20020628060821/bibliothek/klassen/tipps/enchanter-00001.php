<P CLASS="P1">
	Die Rasse ist eigentlich nicht von Bedeutung, da der Enchanter durch Illusionen jegliche
	annehmen kann bzw. Infravision (12lvl) sprechen kann. Wichtig für alle Magiebegabten Wesen
	ist die Intelligenz, da sie Maß für die Anzahl an Manapunkten ist!<br>
	Zu den Rassen ist nur zu sagen, daß ich persönlich von einem Menschen abraten würde, da
	er eine zu geringe Intelligenz hat, eigentlich gar nicht verständlich, wo die Erschaffer
	dieser Welt doch solche sind!? Da die Rassen Gnom, High Elf, Dark Elf eine niedrige 
	Stamina haben sollten (nicht zwangsläufig) ein paar Punkte hierauf verwandt werden!<br>
	Als Beispiel habe ich 21 Pkt. auf INT, 5 Pkt auf STA und 4 Pkt auf CHA aufgewandt!
	Der Erudit macht seine mangelnde Agility und Stamina durch höhere Intelligenz wett.
</P>

