<P CLASS="P1">
Der Shadow Knight hat einen besonderen Skill - Harm Touch - dieser macht massiven Schaden
(Lvl * 10 Dmg), kann aber nur einmal am Tag benutzt werden. Dieser Angriff sollte nur genutzt
werden um sein Leben zu retten (wenn man ihn verwendet, um einen gelben Gegner zu killen,
dann benötigt man ihn mit fast 100%iger Sicherheit einen Kill später und stirbt dann ;-) ).
Als Iksar hat man von Anfang an einen Swimming Skill von 100 und einen Forage Skill von 50
(dieser kann aber nicht gesteigert werden!). Der Bind Wounds Skill ist eine kostspielige
(Bandages kosten 6-7 Silber), aber gute Art der Heilung (wenn man unter 50 % HP hat, danach
funktioniert der Skill nicht mehr!). Archery ist der Fernwaffen Skill des Shadow Knight und
man sollte ihn so früh wie möglich mit trainieren. Zwar kann man den Archery Skill nicht
weiter trainieren als bis 75, aber trotzdem ist er wichtig, da er eine Möglichkeit zum
Pullen ist und kein Mana kostet. Taunt ist DER Skill, um den Gegner von einem anderen Char
wegzulocken und die Aufmerksamkeit auf sich zu ziehen; ein MUSS! 1H Slashing und 2H Slashing
sollten die beiden Waffenskills sein. Durch seine hohe INT ist das Maximieren der Skills
leicht und durch seine weiteren Werte ist der Iskar Shadow Knight ein guter Schmied.
Smithing ist sicherlich kein billiger Tradeskill, aber welcher ist das schon :-).<br>
<br>
In Level 6 bekommt man seinen einzigen Angriffs Skill Bash, mit ihm kann man zusätzlichen
Schaden machen und hat die Chance den Gegner zu stunnen (In Level 8 macht Bash max. 7 Dmg).
In Level 9 kommen dann die Magie Skills hinzu, nur Conjuration, Alteration und Channeling
sind hier wichtig! Divination wird erst aber Level 22 wichtig (für den Ward Undead Spell).
In Level 10 kommt Dodge, ein Verteidigungs Skill, das Ausweichen. In Level 12 Meditate, nun
dauert das Regenerieren von Mana nicht mehr so lange. In Level 17 kommt Parry, der nächste
Verteidigungs Skill, das Abwehren mit der eigenen Waffe. In Level 20 kommt Double Attack, was
ermöglicht in einer Runde doppelt anzugreifen. In Level 30 kommt Riposte, ein
Verteidigungs-/Angriffs Skill, wie beim Fechten wird hierbei der Angriff abgewehrt und man
selbst landet einen Treffer. In Level 35 kommt Hide, das Verstecken, hiermit kann man sich
auch vor Wesen die Unsichtbares sehen können verstecken. In Level 40 kommt Disarm, mit Hilfe
dieses Skills kann man den Gegner entwaffnen.
</P>

<P CLASS="P0">Die folgenden Level-Tips beziehen sich auf einen Solo-Shadow Knight:</P>

<P CLASS="P5">
	Level 1-4:
</P>
<P CLASS="P1">
Man sollte in Field of Bones beginnen. Man kann, wenn man 100%ig fit ist schon in Level 1
Scorpions killen (diese sind gelb)! Damit steigt man schnell in Level 2 auf, aber es ist
sinnvoller die Scaled Wolf Pups zu jagen. Aber Vorsicht, denn die Scaled Wolf Pups/Cubs
groupen und man hat schnell einen Haufen von Ihnen am Hals, wenn man nicht aufpaßt! Denn mit
8 Ihrer Felle ist man jeweils um ein Teil der Curscale Armor reicher und bekommt dabei noch
EXP und ein wenig Geld. Bis Level 4 bringen die Scaled Wolf Pups EXP, wenn auch extrem wenig
zum Schluß. Ab Level 3 auch Scaled Wolf Cubs jagen. Auch sollte man die Decaying Skells
jagen, um an Bones für den Pet Spell ab Level 9 und an Waffen zu kommen. 
</P>

<P CLASS="P5">
	Level 5-7:
</P>
<P CLASS="P1">
In Level 5 sollte man nach Lake of III Omen umziehen. Hier sollte man Sabertooth Kitten und
Sarnak Hatchlings jagen (nicht vergessen 4 Ihrer Köpfe in den Sarnak Head Pack zu legen).
Ab Level 6 bringen die Kitten keine EXP mehr und man muß Sabertooth Cubs, Sarnak Hatchlings,
sowie die Skells jagen. Achtung bei den Goblin Scouts und Goblin Watcher, diese greifen einen
an, sobald man gesehen wird und groupen dazu auch noch! Um sicher zu jagen haltet Euch am
Besten in der Nähe des Weges nach Cabilis auf, damit Ihr die Chance habt zu den Guards zu
rennen, wenn es im Kampf knapp wird. Solltet Ihr getötet werden landet Ihr nämlich im Field
of Bones und müßt, um an Euren Corpse zu kommen, wieder durch ganz West und Ost Cabilis
rennen!
</P>

<P CLASS="P5">
	Level 7-12:
</P>
<P CLASS="P1">
Jetzt geht es wieder in das Field of Bones. Wenn man dem Pfad von Cabilis folgt landet man
in einer Art Tal. Hier sollte man vorsichtig sein, denn viele Gegner greifen einen an, sind
aber noch zu stark. Man sollte sich auf Scaled Wolfs, Skells, Large Scorpions, Carrion
Shredder und Bonecrawler konzentrieren. Ab Level 8 ist es Zeit sich einen Partner zu suchen,
um Giant Scorpions und Scalebone Skells zu jagen. Mit einer 3er Gruppe kann man auch Scaled
Wolf Hunters jagen gehen. Man kann in Level 8 auch Solo spielen, aber dann dauert es länger
mit dem Levelaufstieg. Ab Level 9 kann man wieder alleine jagen gehen und wie in Level 8
Giant Scrpions und Scalebone Skells jagen. Bis Level 12 geht es so weiter ... am Besten
groupen und jagen was die Kondition hergibt.
</P>

