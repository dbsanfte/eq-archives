<P CLASS="P1">
Man startet nahe der Shadow Knight Gilde (wo genau diese ist verrate ich nicht, aber wenn
man sie absolut nicht findet hilft sicherlich jemand) mit 5 Essen, 5 Trinken, einer Note,
einem Kurzschwert und einem Buch für PvP (Spieler gegen Spieler). Die Note (Briefbogen) gibt
man dem Guild Master und bekommt dafür sein Khukri of Pawn und etwas EXP. Seine ersten 5
Skillpoints sollte man wie folgt verteilen: 3 1H Slashing, 1 Taunt und 1 Archery. Man sollte
nicht vergessen Lord Gikzic zu hailen, um die Quest für den nächsten Khukri zu erhalten. Man
bekommt somit auch einen 4 Slot Kontainer, der kleine Dinge (z.B. Essen, Trinken oder
Scorpion Pincers) aufnehmen kann. Dann sollte man los ziehen und sich auf die Monsterjagd
machen. Dabei sollte man den Forage Skill so oft wie möglich benutzen, um an kostenloses
Essen und Trinken zu kommen. Bei jedem Tod bis Level 5 bekommt man neue 5 Essen und Trinken
ohne EXP Verlust. Ab Level 6 kostet jeder Tod EXP (Aber zu sterben ist immer extrem
nervig :-) )
</P>

