<P CLASS="P1">
Der Shadow Knight ist ein guter Puller (außer wenn ein Necromant in der Gruppe ist, dieser
kann besser mit seinen DOTs pullen) und einer der Charakter die in der ersten Reihe stehen,
Schaden machen und einstecken müssen. Durch seine Vampir Spells kann er länger kämpfen als
viele andere der Kämpferklassen. Das Beste was einem Iskar Shadow Knight passieren kann ist
ein Shamane in der Gruppe. Dieser bufft den Shadow Knight, kann am Anfang ein wenig
mitkämpfen und sich dann zurück ziehen, um zu heilen. Das Pet ist ein guter Schutz für den
Shamanen. Dieses wird mit <I>/Pet guard here</I> in die zweite Reihe gestellt und wenn der Shaman
sich zurück zieht. Kommt dann trotzdem ein Monster durch, springt das Pet ein und gibt dem
Shadow Knight Zeit zur Hilfe zu eilen. Wird es knapp im Kampf kann man das Pet schnell mit
<I>/Pet attack</I> zur Hilfe holen und sich selbst zurückziehen. In einer größeren Gruppe als
2 Mitgliedern sollte der Shamane sich aber vollständig aus dem Kampf raushalten, als Heiler
fungieren und seine Angriffsspells nur zum Killen des Gegners benutzen. Dabei sollte der
dritte Char dann auch zu einer Kämpferklasse (Warrior, Monk oder Shadow Knight) gehören. Bei
der Nutzung des Pets sollte man beachten, dass diese ein Teil der EXP für sich beanspruchen,
also sollte man vorher mit der Gruppe absprechen, ob ein Pet vonnöten ist. 
</P>

