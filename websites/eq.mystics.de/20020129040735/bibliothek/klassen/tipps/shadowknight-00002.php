<P CLASS="P1">
Für den Fernkampf sollte man sich einen einfachen Bogen und die billigsten Pfeile kaufen
(beides kann man in der Warrior Guild kaufen), um Gegner anzulocken. Ein Schild gibt
zusätzliche AC und mit Bash hat man ab Level 6 einen zusätzlich Angriffs-Skill. Da der
Shadow Knight nicht wie der Warrior Dual Wield (Kampf mit zwei Waffen) bekommt ist der Kampf
mit einer 2H Slash Waffe ratsam, da diese mehr Schaden macht. Die Curscale Armor welche man
durch eine Quest bekommen kann ist für die ersten Levels ausreichend. Es ist nicht leicht
eine komplette Curscale Armor durch die Quest zu bekommen, daher sollte man doppelte
Armorteile per /OOC zum Tausch gegen fehlende Teile anbieten. Ein besserer Kopfschutz ist,
wenn man schmieden lernen will, Shestar´s Scaled Coif / AC 4 LORE ITEM (Guard Shestar Hailen,
5 GP und 3 Loose Scales geben um Shestars Unfinished Coif zu bekommen. Guard Shestar befindet
sich an einem Zugang in Cabilis nach The Field of Bones. Dann den Unfinished Coif mit
weiteren 3 Loose Scales (zu finden bei Scaled Wolfs in Field of Bones) schmieden -> klappte
bei mir erst bei einem Smithing Skill von 15).<br>
<br>
Auf KEINEN FALL sollte man sein Khukri zerstören! Die erste Quest ist einfach (4 Sarnak
Heads sammeln) und man bekommt einen besseren Khukri (Knave´s Khukri Dmg 5 / Dly 26). Der
danach folgende Khukri (Squire´s Khukri; dauert lange um die geforderten 10 Seiten und den
Binder des Tome zu bekommen, aber es lohnt sich!) macht dann schon Dmg 6 / Dly 25. Der vierte
Khukri (Knight´s Khukri) hat Dmg. 6 / Dly 24 und +3 INT. Der fünfte Khukri (Zealo´s Khukri)
hat Dmg. 7 / Dly 25, +3 INT, +3 SV. Magic. Man sollte sich auch eine Waffe mit einem höheren
Grundschaden besorgen (am besten Dmg 8). Diese sind oft bei Skells zu finden. Sieht man ein
Skell mit einem Schild... nix wie hin! ... der Schild den er trägt ist ein AC 7 Iskar Schild
... ein MUSS für jeden Iskar Shadow Knight!
</P>

