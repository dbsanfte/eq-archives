<P CLASS="P1">
Der Shadow Knight ist ein dunkler Krieger (das Gegenteil eines Paladin), er vereinigt den
Warrior und den Necromaten in sich. Das hört sich erstmal nach einem sehr mächtigen
Charakter an, aber er wird nie so gut wie ein Warrior kämpfen können und nie so mächtige
Zauber sprechen können wir der Necromant. Der Shadow Knight kann mit fast allen Waffen
umgehen (außer Throwing Weapon - Wurfdolch, Shuriken usw.) und alle Rüstungen tragen. Wenn
Euch der Sinn nach einer guten Mischung aus Kampf und fieser Magie liegt, dann ist der
Shadow Knight das Richtige für Euch! Da die Iksar im allgemeinen neutral böse sind hat man
durch die Wahl des Berufes Shadow Knight auch keine Nachteile, wie bei einigen anderen
Rassen. Ihr könnt also bei allen Händlern einkaufen und verkaufen.
</P>

