<P CLASS="P1">
Der Shadow Knight bekommt seine ersten Spells in Level 9. Als erstes sollte man immer den
Vampir Spell kaufen, da dieser der Heilungs Spell des Shadow Knight ist und außerdem noch
den gleichen Schaden macht (5 Dmg./Heilung in Lvl 9; 10-12 Dmg./Heilung in Level 15). Dann
sollte man sich den Pet Spell kaufen. Auch wenn einige der Meinung sind er macht wenig Sinn,
da das beschworene Pet im Level weit hinter einem selbst ist. DAS stimmt schon, aber das Pet
ist wie ein DOT und wenn man fliehen muß, dann gibt es einem die paar Sekunden die zwischen
Sterben und Zonenwechsel/ Flucht zu den Guards entscheiden kann. Dann die DOTs und
schließlich den Rest. In Level 9 sollte man die Mittel haben alle Spells zu kaufen. Wenn man
möchte kann man einige Spells weglassen, aber da die Spells in Level 9 noch nicht so teuer
sind rate ich jedem alle Spells zu kaufen. Man weiß nie ob man einen Spell nicht vielleicht
doch einmal braucht, daher IMMER ALLE Spells kaufen. Ob man mit einem Spell oder dem Bogen
den Gegner anlockt ist Geschmackssache, da der Shadow Knight aber nicht so viel Mana hat wie
der Necromant ist das Pullen mit dem Bogen sinnvoller. Das Mana sollte man sparen, um den
Gegner mit den Vampir Spells zu bekämpfen und dabei auf ihn einzuschlagen. In den späteren
Levels ist das Pullen mit einem DOT der Dooming Serie (verlangsamt den Gegner) sicherlich
besser, aber wie gesagt, das ist Geschmackssache. 
</P>

