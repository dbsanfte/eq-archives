<P CLASS="P1">
Das Wichtigste beim Kampf in einer Gruppe ist die Kommunikation! Redet mit einander, ruft
"Taunt" wenn jemand anderes die Aufmerksamkeit auf sich ziehen soll. Dies sollte aber nicht
erst geschehen, wenn die HP weit unten sind, da dann der Haß auf Euch schon so groß ist, dass
niemand mehr mit Taunt die Aufmerksamkeit auf sich lenken kann. Bittet Magie nutzende
Mitglieder darum Euch immer mitzuteilen, wieviel Mana sie noch haben. Ein Magieuser ohne Mana
ist nutzlos! Magieuser sollten immer in der zweiten Reihe stehen und nicht mit in den
Nahkampf gehen. Zu schnell geht die Übersicht verloren, wenn ein Heiler sich mit in den
Kampf stürzt. Extrem wichtig ist, dass Ihr nicht kopflos umherrennt, wenn Ihr dem Tod nahe
seid. Wenn Ihr lauft, dann folgt Euch der Gegner und die Mitglieder der Gruppe irren
hinterher beim Versuch den Gegner zu treffen. Solltet Ihr in so eine Situation kommen:<br>
Grinst den Gegner diabolisch an, blickt dem Tod gelassen ins Auge und hofft das Ihr es
gemeinsam schafft den Gegner zu töten, bevor Ihr getötet werdet. Der Tod gehört zum Spiel
dazu!
</P>

<P CLASS="P1">
Weiter bin ich leider auch noch nicht gekommen. Mir macht ein Shadow Knight sehr viel Spaß,
da er eine gelungene Mischung aus Warrior und Necromant ist. Als Solochar ist er zwar zu
verwenden, aber die Wartezeiten, um die HPs zu regenerieren sind dann extrem lang!<br>
Als letzter Tip also: Sucht Euch einen Freund, am besten einen Shamanen, oder auch mehrere
gleichen Levels. EQ macht in einer funktionierenden Gruppe doppelt so viel Spaß!<br> 
Solltet Ihr auf Solusek Ro einmal Lust zum Groupen haben tellt mich einfach an :-)
</P>

