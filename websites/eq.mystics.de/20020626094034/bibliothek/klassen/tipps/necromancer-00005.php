<P CLASS="P5">Level 1-4</P>

<P CLASS="P1">
Man sollte nach "The Field of Bones" gehen und hier jagen gehen. In Lvl 1 sollte man die Scaled Wolf Pulps (Vorsicht diese groupen!) und Decaying Skellis jagen. Die Pelts der Wolfs sollte man sammeln für eine Quest welche Scaled Armor und gute Exp bringt. Die Armor kann zwar von einem Necro nicht getragen werden, läßt sich aber super für ein paar GP an andere Player oder Händler verkaufen. Ab Level 2 sollte man Scorpions jagen, diese sind in Lvl 2 noch weiß aber sehr leicht zu killen. In Lvl 3 und 4 sollte man Alles killen was einem begegnet, nur Achtung vor den Scaled Wolf Cubs und Large Scorpions (selten zu finden) diese sind zu stark.
</P>

<P CLASS="P5">Level 5-8</P>

<P CLASS="P1">
Nun sollte man in den Lake of III Omen wechseln. Hier jagt man Sabortooth Kitten und Sarnak Hatchlings. Die Sarnak Hatchlings hinterlassen hin und wieder Brains. Sammelt 4 von den Brains, um den 1st Rank Skullcap zu bekommen. Sehr selten lassen diese auch Torns fallen (es gibt 2 verschiedene, beide sind LORE ITEMS), auch diese solltet Ihr auf sammeln, da Ihr diese für den 2nd Rank braucht. Ab Lvl 6 müßt Ihr Sabortooth Cubs und Skellis jagen. Nach erreichen von Lvl 7 ist es ein langer Weg bis Lvl 8 und man kann auch wieder in The Field of Bones jagen gehen. Ich habe gewartet bis ich ein besseres Pet hatte (Lvl 8) und bin dann zurück ins The Field of Bones.
</P>

<P CLASS="P5">Level 9-12</P>

<P CLASS="P1">
Mit dem neuen Pet im Rücken geht es wieder zurück ins The Field of Bones. Hier könnt Ihr nach Herzenslust in einem Tal jagen gehen, denn es gibt vielen Blaue Mobs (Einfach dem Pfad folgen, um zu dem Tal zu kommen). ACHTUNG hier sollte man nicht unbedarft umher streifen, da viele Mobs zu stark sind und einen angreifen. Also vorsichtig bewegen und mit einem DOT den Mob zum wartenden Pet holen.
</P>

<P CLASS="P1">
Da ich selbst auch noch nicht weiter bin weiß ich nicht wie es danach weiter geht, aber ich bin sicher das es weitere geniale Hunting Grounds gibt. Dunkle Grüße an Euch Necros und vielleicht trifft man sich mal...
</P>

