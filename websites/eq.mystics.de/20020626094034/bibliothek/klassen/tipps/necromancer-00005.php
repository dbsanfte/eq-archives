<P CLASS="P1">
In einer Gruppe ist der Necro ein guter Puller (derjenige der die Monster anlockt), da diese durch die DOTs des Necro schon geschwächt bei der wartenden Gruppe ankommt. Beim Pullen sollte man NIE sein Pet mit nehmen, sondern mit /pet guard here in der Nähe der Gruppe warten lassen. Als Necro ist man niemals in der ersten Reihe, dafür sind die Tanks (Warrior, Monk, Shadow Knight, Paladin oder auch Rouge) zuständig. Man unterstützt die Gruppe durch seine DOTs oder den wenigen DD-Spells. Wichtig ist das Ihr der Gruppe immer mitteilt wieviel Mana Ihr noch habt (am schnellsten über eine einfache Prozentzahl, z.B. 20 bedeutet noch 20 Prozent Mana = 1 Bub). Wenn einem Mitglied der Gruppe so langsam die HPs ausgehen, dann heilt ihn mit Eueren eigenen HP und holt Euch einfach mit dem Vampier-Spell die HPs von den Monstern zurück. 
</P>

