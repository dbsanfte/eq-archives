<P CLASS="P1">
Als erstes sollte man immer den Pet Spell kaufen (außer den Lvl 1 Pet Spell, da es in den ersten 4 Levels auch gut ohne ein Pet geht). Danach den Vampire Spell, da dieser eine Superart der Heilung ist und gleichzeitig noch Schaden macht. Dann die Schild und DOT Spells. Kauft Euch nicht Alle Spells (True North (Lvl 4) macht wenig Sinn; Shadow Step (Lvl 8) ist gefährlich, da man nicht weiß wohin man teleportiert wird; Sight Graft (Lvl 12) wozu sollte man mit den Augen seines Pets sehen wollen?; Wave of Enfeeblement (Lvl 12) der Gegner muß sehr nahe stehen, um STR abgezogen zu bekommen; Voice Graft (Lvl 16) Jetzt soll ich auch noch über mein Pet sprechen...och nö :)).  
</P>

