<P CLASS="P0" ALIGN="CENTER">
	Diese Tips für Iksar-Necromancer Level 1 bis 12 wurden von Razharr erstellt. Der Stand der 
	Infos ist 16.10.2000.
</P>	

<P CLASS="P1">
Der Necromant ist einer der reinen Spelluser / Zaubererklassen. Seine Besonderheit ist sein Skelett-Pet, welches er mit Befehlen für sich kämpfen lassen kann und seine große Auswahl an DOT (Schaden über eine bestimmte Zeit) Spells. Die Verwendung der DOTs bringen den Nachteil, daß ein Kill wesentlich länger dauert, da die DOTs zwar Schaden machen, der gesamte Schaden aber über einen bestimmten Zeitraum gemacht wird und der DD Schaden immer sehr gering ist. Wer schnell viel Schaden machen möchte ist mit einem Wizard oder Magician besser dran. Die DD-Spells (Direkter Schaden) des Necros sind nicht sehr mächtig und er bekommt sehr wenige davon. Auch Buffs bekommt der Necro sehr wenige und seine Schilde und Schadenschilde sind Self Only. Wenn man lieber viele Buffs und Schilde haben möchte sollte man einen Schamanen oder Druiden wählen. Ist der Necro im Kampf auf sich Alleine gestellt, dann sieht es finster um ihn aus, da er nicht sehr viel einstecken kann und im Umgang mit einer Waffe (wie alle Magieuser) nicht gerade ein Meister ist oder es gar irgendwann einmal werden kann.<br>
<br>
Warum dann trotzdem einen Necro spielen? <br>
<br>
Da prügelt man auf jemanden ein der von einem Skelett beschützt wird und dann zaubert dieser, schädigt einen und wird dabei noch selbst geheilt oder schwächt einen und wird selbst stärker. Ja ja all dies machen wir Necros ... *böse grins*. Am schönsten sind aber die DOTs. Ein DOT schädigt und verlangsamt den Gegner und gibt einem die Zeit einen zweiten DOT zu casten, um dann locker zu seinem Pet zu rennen welches dann den Rest macht. Mit den Spells die HP in Mana verwandeln kann man länger als alle anderen Magieuser zaubern ohne medden zu müssen. Die so verlorenen HP kann man sich aber schnell mit seinem Vampire Spell vom Gegner wieder holen. Ist es nicht schön so richtig fies zu sein? *diabolisch grins*
</P>

