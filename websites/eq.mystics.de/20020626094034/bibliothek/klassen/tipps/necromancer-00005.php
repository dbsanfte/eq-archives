<P CLASS="P1">
Gleich nach der Erschaffung landet man in der Nähe seiner Guildhall. Hier sollte man zuerst die Note für den Guildmaster abgeben, um seine Guildtunic (eine Robe und der erste Ausrüstungsteil der die AC steigert) zu bekommen. Hier sollte man gleich nach Master Xydoz Ausschau halten und ihn hailen. Man bekommt von Ihm die erste Quest (4 Sarnak Brains bringen) und nach Abschluß einen Helm (Apprentice Skullcap - First Rank / AC 2). Von Ihm bekommt man auch nach Vollendung der ersten Quest die Zweite. Dann sollte man los ziehen und die bösen, bösen Monster jagen *grins*.
</P>

<TABLE BORDER="0" CELLPADDING="0" CELLSPACING="0" WIDTH="100%">
