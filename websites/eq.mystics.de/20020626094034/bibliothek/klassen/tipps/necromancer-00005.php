<P CLASS="P1">
Das Pet ist der beste Freund und der Tank in einem Wesen für jeden Necro. Ein Solo-Necro kann nicht ohne sein Pet überleben. In einer Gruppe macht ein Pet evtl. nicht so viel Sinn, da es auch einen Anteil der EXP für sich beansprucht. Nach der Erschaffung des Pets solltet Ihr es mit /con auf seine Stärke überprüfen. Solltet Ihr ein zu schwaches Pet haben (Grünes Pet), dann schickt es zurück wo es herkommt und erschafft lieber ein Neues (und hoffentlich besseres :)). Das normale Pet kostet 2 Bones, nur die besseren im Lvl vor den nächsten Spells kann 3 Bones kosten. Das Pet mit Waffen zu bestücken macht nur dann Sinn, wenn der Schaden der Waffe höher ist, als der Grundschaden den das Pet ohne Waffe machen würde. Auch werden die Angriffe durch nicht magische Waffen nicht magisch, auch wenn der eigentlich Pet-Angriff ohne Waffen magisch wäre (ab höheren Lvl 12 Pets). Über die Pet- Befehle lasse ich mich hier nicht aus, da es schon eine gute Erklärung dazu gibt.
</P>

