<P CLASS="P1">
Ab Lvl 4 ist Euer Pet Euer bester Freund. Erschafft zuerst immer ein Pet bevor Ihr in den Kampf zieht. Checkt immer mit /con die Stärke Eures Gegners und übernehmt Euch nicht. Pullt mit einem DOT, am besten mit einem DOT aus der Dooming-Reihe, da dieser den Mob verlangsamt und Ihr die Chance habt einen zweiten DOT hinterher zu casten. Ab Lvl 8 macht es Sinn sein Pet nicht mehr mit sich herum zu schleppen, sondern mit /pet guard here an einem Ort zu "parken" an dem kein Mob vorbei kommt. Dann mit DOTs den Mob anlocken und zum Pet laufen. Dieses stürzt sich dann auf den Feind und man selbst kann medden, dabei sollte man die HP des Pets im Auge behalten und gegebenenfalls mit in den Nahkampf gehen. Als Iskar Necro kann man lange Solo unterwegs sein (wenn man es denn möchte *grins*), da es in den Zonen nur so vor Blauen Mobs wimmelt und man mit seinen Vampir Spells gut mit in den Kampf gehen kann... jedenfalls in den ersten Levels. 
</P>

