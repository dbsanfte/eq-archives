<P CLASS="P1">
Für den Necro ist die Intelligenz der wichtigste Stat, daher sollte man so viel wie möglich (25 Punkte) auf INT legen. Die übrigen 5 Punkte können beliebig verteilt werden. Ich habe sie auf AGI gelegt, da man seine STR durch einen Low-Level Spell steigern kann; DEX nicht so wichtig ist, da man eher im Hintergrund steht und castet; bei STA 5 Punkte keinen Unterschied machen und WIS sowie CHA für den Necro unwichtig sind.
</P>

