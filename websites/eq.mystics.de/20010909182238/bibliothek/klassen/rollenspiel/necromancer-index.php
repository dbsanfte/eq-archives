<P CLASS="P1">
	Das Böse in der Welt besitzt viele Gesichter und findet nur allzu leicht Unterschlupf. Viele 
	lassen sich mit den Mächten des Bösen ein und scheinen unfähig, die Folgen ihres Handelns 
	abzusehen. Das Studium der Geheimnisse von Leben und Tod verleidet viele Zauberer zum 
	Mißbrauch ihres Wissens und sie wenden sich der dunklen Seite der Magie zu. Schon bald 
	kehren sie der Gesellschaft den Rücken und führen in der Einsamkeit der Wildnis ihre 
	abscheulichen Experimente durch. Sie versuchen tote Körper durch die Macht ihres Willens 
	wieder zu beleben, indem sie widernatürliche Rituale und Beschwörungen zelebrieren. Ebenso 
	beschwören sie mit diesem verbotenem Wissen die Geister Verstorbener und zwingen sie in ihren 
	Dienst. Selbst wenn ihre Motive zu Anfang noch ehrenhafter Natur waren, beginnt für Nekromanten 
	schnell der Abstieg in die finstersten Abgründe des Daseins. Die finsteren Mächte des Todes 
	erfüllen die Menschen mit Entsetzen. Im Kampf kennen Untote weder Angst noch Panik und lassen 
	sich nur sehr schwer besiegen. Doch vor allem ist der Zauberer der sie schuf ein mächtiger Magier 
	und ernst zu nehmender Gegner. Einige Hexenmeister besitzen genügend Macht, um selbst dem Tod, 
	dem größten aller Diebe, zu entkommen. Nach ihrem Ende kehren sie aus dem Reich des Todes zurück 
	und sind mächtiger als je zu-vor. Obwohl ihre toten Körper verwesen und zerfallen, verfügen ihre 
	halb wahnsinnigen Seelen weiterhin über erstaunliche Kräfte. Auch nach ihrem Tod besitzen sie 
	genügend Macht, um ihre bösen Zauber zu be-nutzen.
</P>

<P CLASS="P0" ALIGN="RIGHT">
	<I>[Auszug aus Warhammer Armeebuch Untote]</I>
</P>
	
<P CLASS="P1">
	O.K., soviel zur Einstimmung. Nun ein paar Vorschläge, wie ich einen Nekromanten spielen würde 
	(und werde). Wie wir gesehen haben, sind die Motive eines solchen äußerst eigennützig und er nimmt 
	keinerlei Rücksicht auf seine Umgebung. Der Zweck heiligt die Mittel, auch wenn sie noch so 
	widerwärtig sind. Der Lohn ist dafür aber eine überaus große Macht, die einem schon zu Anfang des 
	Spieles nützlich ist. Als Nekromant kann man nämlich ab dem ersten Level Wesen heraufbeschwören, 
	die dann für einen kämpfen. Man bastelt sich quasi seine eigene Party am Anfang und kommt so zügig 
	zu seinen Erfahrungspunkten. Je höherstufiger der Nekromant, umso mächtiger werden seine Sprüche. 
	Auch ist er bereits im 4. Level in der Lage, ein Portal in seine Heimatstadt zu öffnen. Ein 
	Nekromant wird anderen Mitspielern grundsätzlich mißtrauisch begegnen, da er ja ein Außenseiter 
	der Gesellschaft ist. Andererseits ist er mächtig genug sich seiner Haut zu erwehren und kann das 
	Risiko eingehen, anderen zu vertrauen - sofern ihn jemand in seiner Party haben will. Hochelfen 
	dürften wohl einen unvergänglichen Haß auf diese dunkle Seite der Magie empfinden, so daß es nie 
	zu einer echten Freundschaft zwischen einem Hochelfen und einem Nekromanten kommen kann. Einen 
	Nekromanten kümmert es wenig, wenn einer der Partymitglieder das Zeitliche seg-net, im 
	Zweifelsfalle kann er ihn ja wiederbeleben. Überhaupt sollte die Todesfurcht seine geringste 
	Sorge sein. Als Gott meiner Wahl würde ich Bertoxxlous nehmen, das ist eigentlich der einzige 
	"richtige" Totengott in Norrath. Ein Nekromant wird auch nicht durch höfliches Bitten oder gar 
	durch Bettelei sein Zeugs beschaffen, sondern er wird vielmehr die Leute ordentlich einschüchtern. 
	Man sollte immer daran denken, daß man böse und nicht dumm ist. Soll heißen, man muß nicht jeden 
	anpöbeln, der einem über den Weg läuft und man braucht sich auch nicht alle Welt zum Feind zu 
	machen. Vielmehr reicht es aus nicht gut zu sein, schwarze Magie zu benutzen und das eigene Wohl 
	über das aller anderen zu stellen. In einer Party wird ein Nekromant also z.B. sehr darauf 
	hinweisen wie wichtig seine Kunst für das Gelingen der Opera-tion war und einen entsprechenden 
	Anteil fordern. Und da niemand aufwachen will um festzustellen, daß er ein Zombie ist, wird der 
	Schwarzmagier auch bekommen was ihm zustet, hähähä. 
</P>
	
<P CLASS="P1">
	Denkt immer daran, das einzig Wahre in Norrath ist die Tatsache, daß jeder und alles stirbt.
</P>

<P CLASS="P0">	
	<I>Vagnard</I>
</P>

