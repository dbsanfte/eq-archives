	Stand vom 6.4.2000
</P>	

<P CLASS="P2">
	1.1 Was versteht man unter FAQ ?
</P>	
<P CLASS="P1">
	FAQ steht für Frequently Asked Questions, also die am häufigsten gestellten Fragen. FAQ's 
	werden erstellt, um die meistgestellten Fragen und Antworten abzudecken.
</P>

<P CLASS="P2">
	1.2 Was ist <i>EverQuest: Die Ruinen von Kunark</i> ?
</P>	
<P CLASS="P1">
	Das EverQuest Erweiterungspaket ist im wesentlichen ein Addon zum Multiplayer Spiel EverQuest.
</P>

<P CLASS="P2">
	1.2.1 Was ist EverQuest ?
</P>	
<P CLASS="P1">
	EverQuest (oder kurz EQ) ist ein Echtzeit 3D Multiplayer Online Rollenspiel. Eine andere 
	richtige Beschreibung ist auch, EverQuest ein grafisches MUD (Multi User Dungeon) zu nennen. 
	EverQuest ist am 16. März 1999 erschienen.
</P>

<P CLASS="P2">
	1.3 Welche neuen Features wird es in der Erweiterung z.B. geben ?
</P>	
<UL CLASS="P0">
	<LI>Ein komplett neuer Kontinent, genannt Kunark. Er wird südlich von Faydwer zu finden sein.<BR></LI>
	<LI>Mehr als 20 neue Zonen, einschließlich Newbie Zonen bis hin zu Zonen, in denen 
	highlevel Drachen hausen.<BR></LI>
	<LI>Eine große Anzahl neuer und sogar viel besser aussehender NPCs.<BR></LI>
	<LI>Eine detailliertere Welt, die aus mehr Polygonen und größeren Texturen besteht.<BR></LI>
	<LI>Mehr animierte Objekte überall auf dem neuem Kontinent.<BR></LI>
	<LI>Eine neue spielbare Rasse, die Iksar, das Volk der Lizards.<BR></LI>
	<LI>Das Volk der Lizards sind neutral/böse und können Monks ausbilden.<BR></LI>
	<LI>Neue Schiffe, die sowohl von Antonica als auch von Fazdwer nach Kunark segeln.<BR></LI>
	<LI>Neue Cover und Schachtelkunstwerke von dem berühmten Künstler Keith Parkinson <i>(Anm. 
	des Übersetzers: Hmm, darauf haben wir aber auch wirklich lange gewartet *eg*)</i>.<BR></LI>
	<LI>Die Möglichkeit für die Spieler das Levellimit von 50 zu überschreiten.<BR></LI>
</UL>

<P CLASS="P2">
	1.4 Welche Möglichkeiten werden die Kunden die bereits EverQuest spielen haben, um auf die Erweiterung upzugraden ?
</P>	
<P CLASS="P1">
	Alle bestehenden EverQuest-Kunden können auf die Kunark-Erweiterung mittels Kauf des Upgrade-Pakets upgraden. Dieses Paket beinhaltet die Upgrade-CD-ROM und eine neue Weltkarte und ist im Station Online Store erhältlich, solange der Vorrat reicht.<br>
	<b>WICHTIG:</b> Das alte EverQuest-Paket wird durch ein neues ersetzt, welches EverQuest inkl. Erweiterung enthält. EverQuest allein wird nicht mehr erhältlich sein! Wenn Du ein bestehender EverQuest-Kunde bist brauchst Du NICHT das komplette Paket kaufen.
</P>

<P CLASS="P2">
	1.4.1 Wird der Kauf der Erweiterung die monatlichen Kosten beeinflussen ?
</P>	
<P CLASS="P1">
	Nein.
</P>

<P CLASS="P2">
	1.4.2 Was ist, wenn ich die Erweiterung nicht kaufe ?
</P>	
<P CLASS="P1">
	Man kann weiterhin EverQuest spielen, wie gewohnt. Man wird die Charaktere der neuen Rasse 
	sehen und mit den neuen Items von Kunark Handel treiben können, aber man wird weder 
	einen neuen Charakter der neuen Rasse erstellen noch nach Kunark reisen können.
</P>

<P CLASS="P2">
	1.5 Werden die grafischen Neuerungen, die in der Erweiterung benutzt werden auch den Rest der EverQuest Welt beeinflussen ?
</P>	
<P CLASS="P1">
	Nein. Um von den neuen, erweiterten Techniken, die bei der Umsetzung der Erweiterung benutzt 
	wurden, zu profitieren, müßten wir die bisherigen EverQuest Zonen komplett neu aufbauen. Wir 
	haben uns dazu entschlossen uns dabei nur auf den neuen Kontinent zu konzentrieren.
</P>

<P CLASS="P2">
	1.6 Wird Kunark mehr Char-Slots ermöglichen ?
</P>	
<P CLASS="P1">
	Nein. Kunark ist eine Erweiterung des bestehenden Spiels. Es beinhaltet neue Zonen und eine neue Rasse. Es ändert sich nichts am ursprünglichen Spiel EverQuest, außer daß es neue Kunark bezogene Quests und Boote dorthin gibt. Somit wird das Limit von 8 Chars pro Server nicht geändert.
</P>

<P CLASS="P2">
	1.7 Woraus besteht das Kunark Erweiterungs Paket Upgrade ?
</P>	
<P CLASS="P1">
	Das Upgrade erlaubt bestehenden EverQuest Spielern die neuen Features von Ruins of Kunark zu nutzen. Die neuen Features beinhalten z.B. über 20 neue Zonen sowie eine komplett neue Rasse, die Iksar. Das Upgrade wird mit der Kunark CD, Upgrade Account Key und einer Stoffkarte mit alten und neuen Zonen ausgeliefert. Der Upgrade Key ermöglicht bestehenden EverQuest Spielern Ruins of Kunark zu betreten und Zugriff auf alle neuen Features.
</P>

<P CLASS="P2">
	1.8 Was ist der Unterschied zwischen dem Kunark Upgrade und der Retail Kunar Box Edition ?
</P>	
<P CLASS="P1">
	Die Kunark Box Edition enthält beides, EverQuest und Kunark sowie einen Account Key zum Erstellen eines neuen Everquest Accounts. Das Upgrade beinhaltet nur die Kunark Software und den Upgrade Account Key welche bestehenden Spielern erlaubt Kunark zu aktivieren. Wenn man noch kein EverQuest Kunde ist empfehlen wir die Kunark Box Edition zu erwerben.
</P>

<P CLASS="P2">
	1.9 Muß ich das Upgrade kaufen um weiterhin EverQuest spielen zu können ?
</P>	
<P CLASS="P1">
	Nein, Man kann weiterhin EQ spielen, aber man kann den neuen Kontinent nicht betreten und die neue Rasse nicht spielen.
</P>

<P CLASS="P2">
	2.0 Wenn ich das Kunark Paket habe, was muß ich jetzt tun ?
</P>	
<P CLASS="P1">
	Installiere die Kunark Software von der CD und registriere Dich mittels dem Account Key auf der CD-Hülle. Nicht vergessen, mit dem Kunark Upgrade können nur bestehende EverQuest Accounts upgedated werden. Man kann den Account updaten indem man entweder beim Server Auswahl Fenster auf Register Kunark klickt oder man klickt auf Account - Change Account. Wenn man die Retail Version "EverQuest: The Ruins of Kunark" gekauft hat kann man entweder einen komplett neuen Account mit EQ und Kunark Zugriff anlegen od. einen bestehenden EQ Account updaten. BEIDES GEHT JEDOCH NICHT !
</P>

<P CLASS="P2">
	Ich erhalte dauernd die Meldung "Invalid Account Key" beim registrieren. Was muß ich tun ?
</P>	
<P CLASS="P1">
	Manchmal wird die Ziffer "0" mit dem Buchstaben "O" oder die Ziffer "1" mit dem Buchstaben "I" verwechelt. Mit dem verwendeten Font wird der Buchstabe "O" eher vieleckig und die Ziffer "0" eher rechteckig dargestellt. Am Besten einfach ausprobieren. Gibt es dann immer noch Probleme muß der Customer Service kontaktiert werden.
</P>
<P CLASS="P1">
	<b>Anmerkung:</b> Bei der Auslieferung wurde oftmals ein falscher Key auf die CD-Hülle mitgeliefert. Die letzte Stelle wurde vergessen. Um langes (und teures) Rumtelefonieren mit Verant zu vermeiden muß man einfach den kompletten Key von der CD eingeben PLUS alle Ziffern und Buchstaben nacheinander durchprobieren bis es klappt.
</P>

