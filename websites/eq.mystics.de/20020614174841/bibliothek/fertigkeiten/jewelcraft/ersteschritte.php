<P CLASS="P1">
	Mittels Jewelcraft kann wertvoller magischer Schmuck hergestellt werden.
</P>	

<P CLASS="P1">
	Wie jede andere Fertigkeit auch, muß Jewelcraft beim jeweiligen Guild-Trainer aktiviert werden. Bei Jewelcraft ist es jedoch empfehlenswert die max. Anzahl von möglichen Skillpunkten (20) zu investieren, bevor man sich an die Erstellung des ersten Schmuckstückes macht. Damit verhindert man, daß es nicht gleich am Anfang schon heißt: "Ausser Spesen nix gewesen!"
</P>	

<P CLASS="P1">
	Jewelcraft ist eine sehr teure Fertigkeit, weil man sehr oft bei der Erstellung versagt und die teuren Edelsteine zerstört werden. Später, wenn der Skill hoch genug ist, kann es jedoch auch Profit bringen!
</P>	

<P CLASS="P1">
	Um magischen Schmuck herstellen zu können wird ein <b>"Jewelry Kit"</b> benötigt welches in jedem Juwelier-Laden gekauft werden kann. Dort gibt es auch das Buch <b>"Legacy of Jewelcraft"</b> mit allen Hintergrundinformationen bezüglich Jewelcraft (aber man muß es nicht kaufen!).
</P>	

<P CLASS="P1">
	Wie erstellt man nun ein Schmuckstück:
</P>	
<OL CLASS="P0">
<LI>Besorge dir einen Edelstein (entweder als Loot bei einem Monster oder einfach kaufen)
<BR></LI>
<LI>Kaufe eine <b>"Metal Bar"</b>. Um magischen Schmuck herzustellen muß das Metall erst von einem Enchanter "enchanted" werden. Enchanted Bar's schauen genau gleich aus wie unbearbeitetes Metall, aber mit einem Rechts-Klick auf das Item sieht man, ob es wirklich "enchanted" ist (z.B.: "Enchanted Silber Bar").
<BR></LI>
<LI>Öffne das <b>"Jewelry Kit"</b> (Rechts-Klick mit der Maus auf das Kit)
<BR></LI>
<LI>Gib den Edelstein und das (enchanted) Metall hinein.
<BR></LI>
<LI>Drücke/Klicke auf "Combine"
<BR><BR></LI>
</OL>
<P CLASS="P1">
	Warst du erfolgreich, hältst Du nun das neue Schmuckstück in Händen. Bei Versagen sind alle Zutaten weg!
</P>	

<P CLASS="P2">
	Ein paar nützliche Informationen:
</P>
<UL CLASS="P0">
<LI>
	Ist der Skill unter 20 versagst du wesentlich öfter als du Erfolg hat, und das geht ganz schön ins Geld. Fang mit billigen Edelsteinen (z.B. Malachite od. Lapis Lazuli) und Silver Bars an und arbeite dich langsam nach oben zu den immer teurer werden Edelsteinen/Metallen.
<BR><BR></LI>
<LI>
	Am Besten fängst du mit einfachen Schmuckstücken an (z.B. Silver Malachite Ring) und steigerst so den Skill. Ist dein Skill bereits höher als für den aktuellen Schmuck erforderlich, bekommst du eine entsprechende Meldung und du musst mit dem nächstschwierigeren Schmuck weitermachen. Den erzeugten Schmuck verkaufst du entweder an andere Spieler oder einem Merchant. Es ist zwar ein Verlustgeschäft aber immer noch besser als gar nix.
<BR><BR></LI>
<LI>
	Die Reihenfolge der verwendeten Metalle sollte sein: zuerst Silver, danach Electrum, weiter mit Gold, Platinum und schlussendlich Vellium. Immer erst den Skill soweit steigern bis man <b>"Trivial"</b> (keine weitere Steigerung mehr möglich) erreicht hat; erst dann mit dem nächsten Metall weitermachen.
<BR><BR></LI>
<LI>
	Merchants (NPC) zahlen für "Nicht magischen Schmuck" etwas weniger als die dafür benötigten Kompenenten kosten.
<BR><BR></LI>
</UL>
