	<P CLASS="P1">
		Das Erste was einem an einem Troll auffällt, ist seine grüne Haut und abgrundtiefe Häßlichkeit. Selbst die Oger 
		sehen noch irgendwie menschlich aus, doch diese Rasse hat kaum Ähnlichkeiten mit einer der anderen Rassen. Trolle 
		sind etwas kleiner als Oger, doch genauso dumm. Allerdings soll es sogar Kleriker unter ihnen geben, so 
		scheinen sie eigene Götter zu verehren, vermutlich einfache, primitive Gottheiten der Natur. Dies bedeutet 
		allerdings, daß man sie vielleicht was ihre geistigen Fähigkeiten angeht etwas unterschätzt. 
