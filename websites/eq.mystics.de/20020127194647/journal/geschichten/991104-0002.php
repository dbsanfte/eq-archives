<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Shadowpriest<BR>
	<BR>
</P>

<P CLASS="P1">
	Dunkelheit - überall war nur erquickende Dunkelheit, die mich beruhigte, mich in Sicherheit 
	wog und die mir vertraut war, wie die dunkle Stimme meines Herrn, die schon so oft mit 
	gewissenhafter Macht zu mir gesprochen hatte. Mein Herr und Schöpfer, seine Namen waren 
	viele, und alle waren sie groß und brachten Angst, Verzweiflung, und Tränen und Blut 
	unter die rechtschaffenen Völker von Norrath. <br>
	Immer wenn er zu mir sprach konnte ich die Saat des Leidens unter den Lebenden spüren, 
	die sein Name allein unter sie alle brachte. <br>
	Ich liebte diese Dunkelheit, denn ich war schon immer ein Teil von ihr gewesen.
</P>

<P CLASS="P1">
	Doch ich spürte eine Befürchtung in mir wuchern. Ich wußte, daß mein Meister mich eines Tages 
	in die Welt der Sterblichen senden würde, um seine Ankunft vorzubereiten.<br>
	Nur der Auftrag, der Befehl meines Herrn war der Grund für mich zu gehen. Es war ein 
	unwiderstehlicher Befehl, wenn auch mein größter Wunsch ihn zu befolgen. Dennoch behagte 
	es mir nur wenig, daß ich mich dazu in die Welt der Sterblichen begeben mußte. 
</P>

<P CLASS="P1">
	Und so kam es, daß ich, die dunkelste Seele an der Seite meines Herrn, sein Hohepriester, 
	einen Körper erhielt, bereit in die lichte Welt der Vergänglichkeit zu wechseln, um Innoruuks 
	Ankunft zu bewirken, damit er seine fürchterliche Macht an ihnen zum Vergnügen demonstrieren 
	konnte. Alleine mich zu entsenden sollte eine Herausforderung an die ANDEREN sein, die sich 
	alle selbst gut und edel oder gar als Spendergötter bezeichneten, den Menschen und Elfen, 
	Halblingen und Gnomen mit Weisheit vorangingen, sie dazu brachten sich ihnen in Glauben und 
	Demut zu unterwerfen. Wer, der eine wahrhaftig dunkle Seele sein eigen nannte, konnte dies 
	gutheißen?
</P>

<P CLASS="P1">
	Und so erwachte ich langsam in einem hinderlichen Körper. Der Meister war gnädig gewesen, 
	denn ich spürte sofort, daß selbst an meinem Körper etwas böses zu erkennen sein mußte. Ohne 
	Bekleidung lag ich auf dem Boden, ich spürte, wie meine Brust sich gegen den nackten, 
	felsigen Untergrund preßte, mit jedem Atemzug mein Herz begann zu schlagen. Ich stand 
	auf und betrachtete mich vor meinem geistigen Auge. Es war der Körper eines Dunkelelfen, 
	in den der Meister meine Seele gesandt hatte. Doch noch immer unterschied ich mich von 
	allem anderen Leben. Ich befahl meinem Körper, und nicht wenig, denn mein böses Herz schlug 
	nur, weil ich es wollte, ich mußte schließlich wirken, als sei ich einer von ihnen. Diese 
	Narren hätten mich für einen Gott gehalten, hätten sie meine wahre Herkunft erahnt.
</P> 

<P CLASS="P1">
	Doch lag es nicht in meiner Absicht das dunkle Volk zu verachten. Die Dunkelelfen waren 
	seine Kinder, ergebene Diener des Meister, und sie huldigten ihm, waren Werkzeuge mit 
	denen er SEINE Gerechtigkeit walten lassen konnte. Sie waren es, die Haß, Verzweiflung, 
	Angst und solch wunderbar zu beobachtende Empfindungen unter die Leute brachten. Ich sage 
	Euch, nichts ist fröhlicher zu betrachten, als einen stolzen Krieger, der beginnt seine 
	Trauer offen zu zeigen, weil ein tüchtiger Dunkelelf sein Familie geschändet und erschlagen 
	hat, nur weil er Freude daran empfand. Die Hilflosigkeit die dieser Krieger zeigt, wenn er 
	die Frage an seine vermeintlichen Göttern stellt, warum sie nicht die Macht hatten, diese 
	Schandtat zu verhindern. Und sie hatten diese Macht wahrhaftig nicht, denn nichts ist größer 
	und stärker, als die Macht von Takish-Hiz!
</P>

<P CLASS="P1">
	Und nun war ich Teil von ihnen, aber auch noch immer ein Teil meines Herrn. Doch wurde mir 
	bewußt, daß ich zunächst lernen mußte meine Fähigkeiten durch diesen hilflosen Körper zu 
	entfesseln. Meine Macht war vorhanden, doch ich spürte sie nur sehr langsam aus der 
	Dunkelheit in diesen Körper strömen. 
</P>

<P CLASS="P1">
	Nun war es an der Zeit, zu lernen. Zu lernen, wie diese Hülle, das Werkzeug, dieser Körper 
	zu benutzen war, um meinen Aufgaben zu dienen, und dem Meister gefällig zu werden. 
</P>

<P CLASS="P1">
	Ich schaute mich um und gewahrte sogleich, daß es eine Zeit dauern würde die Gezeichneten zu 
	finden. Doch mußte ich mich leider dem Umstand fügen, nicht mehr die ganze Einsicht über das 
	Leiden in der Welt zu genießen, sondern nunmehr einen stark eingeschränkten Blickwinkel hatte. 
	Ich war gezwungen mit Beinen zu laufen, mit Augen zu sehen, mit der Nase zu riechen, und mit 
	den Händen zu tasten. Arg beschränkt waren meine Möglichkeiten, denn nicht zuletzt war ich nun 
	verwundbar!!! Die Hülle in der ich mich nun befand war sterblich...  Ich fror... !!!
</P>

<P CLASS="P1">
	Doch ich wußte genug über die Vergänglichen, also begab ich mich in die Nähe eines meiner
	neuen Brüder, denn vom Herzen her waren wir gleich, Dunkelelfen und ich. Vielmehr noch, ich 
	war nun SELBER ein Dunkelelf. Als mich der erste von ihnen erblickte verstand er die 
	Hilflosigkeit, die ich auf Grund noch mangelnder Kontrolle über meinen Körper zeigte, 
	als Schwäche, und ein Blick der Verachtung traf mich. Niemand hatte das Recht, einen der 
	wahren Schatten Innoruuks mit verachtendem Blick anzusehen. Doch ich mußte mich dem Moment 
	fügen. 
</P>

<P CLASS="P1">
	Geschürt von Haß, der eigenen Schwäche gegenüber, setzte ich mich wieder auf den Boden. Ich 
	fühlte den kalten Stein am Gesäß nicht mehr, denn ich lenkte meine Gedanken in die Dunkelheit, 
	aus der mir Kraft und Wissen wachsen würden. Und so löste ich den Geist vom Körper. Pure Energie 
	sammelte ich aus den Spähren der Dunkelheit und bündelte diese in meinen neuem Körper. Zwei 
	Mondläufe verbrachte ich in diesem Zustand der Trance und als ich zum zweiten Mal erwachte 
	war ich stark.  
</P>

<P CLASS="P1">
	Ich erhob mich, und entschloß als Dunkelelf das zu erreichen, weshalb ich gekommen war. Die 
	Inkarnation des Meister vorzubereiten, und seinen Weg hierher, in die Welt der Sterblichen zu 
	ebnen.<br>
	Ich hatte die Macht gefühlt, die ich nun wenigstens wieder zum Teil besaß, doch ich mußte sie 
	nun auf neuen Wegen lenken. Ich mußte Zaubersprüche sprechen, und zu diesem Zwecke mußte ich 
	sie zunächst erlernen. Innoruuk verzeih mir, doch ich spürte Schwäche. Zaubersprüche, wo ich 
	doch Äonen lang die Macht in Ihrer reinen Form zu lenken gewohnt war.<br>
	Ich hatte nichts, außer dem Wissen um Deine Ankunft, Meister. 
</P>

<P CLASS="P1">
	Ich erhob mich, und entschloß als Dunkelelf das zu erreichen, weshalb ich gekommen war. Die 
	Inkarnation des Meister vorzubereiten, und seinen Weg hierher, in die Welt der Sterblichen zu 
	ebnen.<br>
	Ich hatte die Macht gefühlt, die ich nun wenigstens wieder zum Teil besaß, doch ich mußte sie 
	nun auf neuen Wegen lenken. Ich mußte Zaubersprüche sprechen, und zu diesem Zwecke mußte ich 
	sie zunächst erlernen. Innoruuk verzeih mir, doch ich spürte Schwäche. Zaubersprüche, wo ich 
	doch Äonen lang die Macht in Ihrer reinen Form zu lenken gewohnt war.<br>
	Ich hatte nichts, außer dem Wissen um Deine Ankunft, Meister. 
</P>

<P CLASS="P1">
	So verlief die erste Zeit schleppend, ich bekam von einem Bruder etwas Kleidung, sowie Brot und 
	Wasser. (Als lästig empfand ich die Notwendigkeit mich ernähren zu müssen!)<br>
	Durch das Wissen was mir geblieben war, konnte ich sogar etwas Geld verdienen, indem ich 
	Heilkunde praktizierte, und nutzte somit die Schwäche aus, die alle Sterblichen inne haben - 
	verletzlich zu sein. Ich sammelte einiges Gold und erkaufte mir somit etwas Wissen, was für 
	mich vollkommen belanglos war, bevor ich hierher gekommen war. Schnell begann ich mit der 
	Ausbildung meines Körper, denn ich mußte den Kampf lernen, und die Magie zu beherrschen 
	erforderte ebenfalls vielmehr Übung als ich befürchtet hatte. Und so sitze ich hier nun, 
	um mir selbst vor Augen zu halten wie weit ich seitdem kam, in dem Bestreben die übrigen 
	Gezeichneten zu finden, derer ich nur ihre Namen kenne, so wie sie der Meister mir selbst 
	genannt hat, um mich mit ihnen zu verbünden, sie zu lenken, um SEINE Ankunft zu bewirken. 
</P>

<P CLASS="P1">
	Ich erhob mich, und entschloß als Dunkelelf das zu erreichen, weshalb ich gekommen war. Die 
	Inkarnation des Meister vorzubereiten, und seinen Weg hierher, in die Welt der Sterblichen zu 
	ebnen.<br>
	Ich hatte die Macht gefühlt, die ich nun wenigstens wieder zum Teil besaß, doch ich mußte sie 
	nun auf neuen Wegen lenken. Ich mußte Zaubersprüche sprechen, und zu diesem Zwecke mußte ich 
	sie zunächst erlernen. Innoruuk verzeih mir, doch ich spürte Schwäche. Zaubersprüche, wo ich 
	doch Äonen lang die Macht in Ihrer reinen Form zu lenken gewohnt war.<br>
	Ich hatte nichts, außer dem Wissen um Deine Ankunft, Meister. 
</P>

<P CLASS="P1">
	Ich bin der erste der Gezeichneten, mein Name soll als Shadowpriest gefürchtet werden. Ich 
	werde trainieren, mich in der Gestalt des Dunkelelfen zurechtfinden um die übrigen Schatten 
	aufzuspüren und als die Gezeichneten zu erkennen, um mit ihnen den großen Plan zu 
	verwirklichen...... Shadoweye, meinen Schwesterschatten fand ich bereits. Unsere Macht 
	beginnt bereits zu erstarken. Doch soll sie Ihre Geschichte selbst berichten, denn ich 
	muß die Kunde des Meisters weiter verbreiten, und SEINE Schatten weiter um mich versammeln, 
	bis auch der letzte von Ihnen an meiner Seite steht, um die Legende von Innoruuk's 
	Auferstehung wahr werden zu lassen......
</P>

<P CLASS="P0">
	<I>Shadowpriest, Innoruuk</I>
</P>

