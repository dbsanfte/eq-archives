<P CLASS="P1">
	Der Research-Skill ist für Enchanter ab Level 16 erlernbar. Um den Skill zu aktivieren muß mind. 1 Skillpunkt beim jeweiligen Guild-Trainer investiert werden. Da Research sehr schnell steigt, sollte man nicht mehr Punkte investieren.
</P>	

<P CLASS="P1">
	Nachdem der Skill aktiviert wurde muß noch das Buch <b>"Tome of Endless Enchantments"</b> gekauft werden. In dieses Buch müssen die jeweiligen "Zutaten" gegeben werden und durch das Drücken auf "Combine" entsteht ein neuer Spruch oder man versagt und bekommt nichts ausser evtl. einer Skillsteigerung. Kaufen kann man das Buch immer in der Enchanter-Guild.
</P>	

<P CLASS="P1">
	Enchanter verwenden für Research 4 Arten von Pages:
</P>	
<UL CLASS="P0">
<LI>Tasarin's Grimoire<BR></LI>
<LI>Velishoul's Tome<BR></LI>
<LI>Sali's Writ<BR></LI>
<LI>Niliitm's Grimoire<BR><BR></LI>
</UL>

<P CLASS="P1">
	Diese Researchkomponenten können bei diversen NPC's und Monstern überall in Norrath gefunden werden. Grundsätzlich findet man die Pages für den Spell eines bestimmten Levels auf Mobs, die im gleichen Level bis ca. 5 Levels höher sind. So findet man z.B. die Komponenten für den Level 16 Spell <i>Disempower</i> bei Level 16 bis 21 NPC's. Deshalb findet man in Oasis auch die meisten Level 16 Research Komponenten bei den Caimans und Crocodiles.
</P>	

<P CLASS="P1">
	Es gibt 3 Arten von Combine:
</P>	
<UL CLASS="P0">
<LI>Man besitzt beide Hälften einer Page. Zum Beispiel können die linke und rechte Seite der Page 23 zu einem Spell kombiniert werden.<BR></LI>
<LI>Man kombiniert ganze Seiten mit aufeinanderfolgenden Nummern, wie z.B. Page 51 und 52.<BR></LI>
<LI>Man stellt eine Seite wieder her indem man eine "Faded"-Page allein in das Buch gibt und auf "Combine" drückt. Die so restaurierte Seite kann somit wieder, zusammen mit der entsprechenden zweiten Hälfte od. ganzen Seite, verwendet werden.<BR><BR></LI>
</UL>

<P CLASS="P1">
	So, nachdem Du nun weißt wie das geht, gibt es zwei Möglichkeiten. Entweder Du stürzt Dich gleich ins Spells erzeugen, oder Du versuchst zuerst mit den <A HREF="ersteschritte.php">Research-Quests</A> den Skill zu steigern.
</P>

<P CLASS="P1">
	Viele sind der Meinung es ist einfacher, einfach möglichst viele Pages zu kaufen. Man kommt oft sehr leicht an eine Seite oder Hälfte und hat dann mehrere davon, aber die zweite Seite oder Hälfte will einfach nicht droppen. Dann ist es am Einfachsten, die fehlenden Seiten(hälften) per /auction von anderen Spielern zu kaufen. Ausserdem können einige Research-Zaubersprüche auch bei anderen Castergilden direkt gekauft werden. Den Spell <i>Disempower</i> gibt es z.B. in der Shaman Guild.
</P>	

