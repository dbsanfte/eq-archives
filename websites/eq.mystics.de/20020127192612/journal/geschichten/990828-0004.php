<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Fabari<BR>
	<BR>
</P>

<P CLASS="P1">
	Mein Name, Fremder? Viele derer habe ich bei den verschiedenen Völkern. Und viele werde ich 
	mir noch verdienen. Fabari nennt mich mein Volk, es ist der, den mir das hohe Konzil der 
	Eruditen am Tage meiner Geburt schenkte. 'Prinzessin' heißt es in der Übersetzung in die
	gemeine Sprache, denn jeder hat einen Namen, den er bei der Geburt geschenkt bekommt. 
	Aber jeder wird sich in den Jahren nach seiner Geburt auch einen Namen verdienen.
</P>

<P CLASS="P1">
	Willst Du die Geschichte meines ersten verdienten Namens hören, so lausche meinen Worten....
</P>

<P CLASS="P1">
	Unsere Länder sind dunkel, zu jeder Zeit. Das ewige Laubdach unserer Wälder versteckt 
	die Sonne vor unserem Volk. Eine Strafe dafür, daß wir Eruditen den Nekromanten die Kenntnis 
	der Magie gewährten, wenn auch unbeabsichtigt. Doch wer bereit ist das Wissen an die Weisen 
	zu bringen, der soll die Sonne erblicken dürfen, sagt eine alte Sage meines Volkes. 
	Oft hörte ich diese Geschichte von den Meistern in unseren Tempeln, und früh regte sich 
	der Wunsch in mir, daß wahre, warme Licht der Sonne zu sehen und auf meiner Haut zu spüren, 
	damit Ihre gleißende Kraft mir zuteil werden würde.
</P>

<P CLASS="P1">
	Warum sollte ich nicht hinausziehen und die Welt erkunden, um die Sonne zu finden? Pasu, eine 
	weise Druidin vom Volke der Elben hatte mich schließlich genügen gelehrt, um die Wildnis 
	zu durchreisen, und einigermaßen über die Runden zu kommen. Natürlich durfte ich dem hohen 
	Konzil nichts über meine Pläne verraten, denn niemals hätten sie eine Prinzessin ziehen 
	lassen, nach dazu ohne Wachen und Geleit. Nein, ich mußte warten und in Verschwiegenheit meines 
	Weges ziehen, was ich auch eines Tages tat.....
</P> 

<P CLASS="P1">
	Vor langer Zeit erzählte mir meine Mutter die Geschichte von	fernen Ländern, in denen die 
	Sonne scheint, und daß es derer	viele gibt. Das nächste dieser Reiche war die Insel Kerrana.	
	Oftmals wanderte ich die Küsten unserer Reiches ab, denn	angeblich konnte man die Insel an 
	manchen Tagen vom Ufer	aus schemenhaft erblicken. Doch so sehr ich auch meine Augen	aufhielt, 
	ich sah nichts.
</P>

<P CLASS="P1">
	Eines Tages kam ich an eine Hütte am Strand. Ein prächtiger	Steg war dort erbaut worden, und 
	ein alter Mann saß darauf	und angelte nach Fischen.
</P>

<P CLASS="P1">
	"Was suchst Du, schönes Kind? Du scheinst mir verzweifelt zu	sein!", sagte der Mann, ohne 
	mich auch nur einmal angesehen	zu haben, denn sein Blick war starr auf die schäumende 
	Gischt	zu seinen Füße gerichtet. "Ich suche den Weg zur Insel	Kerrana, denn ich möchte 
	die Sonne sehen. Ich will das Licht	einfangen und es meinem Volke bringen, damit es sich 
	an	seinem Strahlen erfreuen kann. Doch schon lange suche ich,	und der Glauben daran 
	schwindet mir bereits.", erwiderte ich	traurig. Plötzlich kam mir eine Vision, in der ich 
	sah, wie	derselbe Mann, welcher nun alt und gebückt vor mir saß, als	ein junger Mann bereits 
	hier war, immer in der Hoffnung,	eines Tages die Insel erblicken zu können, da auch er	
	ausgezogen war, um die Sonne zu sehen. Der Traum des	Mannes, die Insel mit seinen eigenen 
	Augen zu entdecken	erfüllte sich jedoch nie, denn er war erblindet.
</P>

<P CLASS="P1">
	"Du hast es nicht geschafft die Insel zu finden, mein Kind?" 
	"Nein, also teile ich wohl dein Schicksal, alter Mann, auch ich scheine blind zu sein." 
	Der Greis stand auf, dreht sich zu mir
	und ich sah in seine blinden, weißen Augen, welche reglos in
	seinem Schädel lagen. "Blind?", lachte er, ohne auch nur ein
	wenig verbittert zu wirken. "Ich denke, es ist an der Zeit, daß
	dich ein Blinder das Sehen lehrt", spottete er, wenn auch mit
	einer unerwarteten Freundlichkeit in seiner rauhen Stimme.
	"Niemand ist hier blind, weder ich, noch Du, Prinzessin!" 
</P>

<P CLASS="P1">
	Ich war verwirrt, denn ich wußte daß der Mann blind war.
	Zitternd stand ich vor ihm, denn der kühle Wind des Frühlings
	blies mir den letzten Geruch des Winters unter mein Hemd. 
</P>

<P CLASS="P1">
	Der Mann legt mir seine Hand auf die Augen, und drückte
	mich langsam und sanft zu Boden, und deutete mir somit, daß
	ich mich setzen sollte. Seine Hand ließ er jedoch über meine
	Liedern ruhen.
</P>

<P CLASS="P1">
	"Und höre auf, Deinen Augen zu glauben, sondern höre,
	rieche, und sieh mit dem Herzen, dann wird Dir die Insel nicht
	länger verborgen bleiben.", flüsterte mir der weise Alte ins
	Ohr, und ich gehorchte. Am Anfang war nichts als der Wind.
	Doch da, der sanfte Geruch des Meeres, der immer stärker in
	mich drang, und das Kreischen der Möwen, das Rauschen der
	Brandung......
</P>

<P CLASS="P1">
	"Die Brandung!", schrie ich. Plötzlich wurde es mir klar.
	Kerrana lag unmittelbar vor uns, nur durch die Nebel des
	Meeres verdeckt. Nicht gesehen, gefühlt hatte ich die lang
	gesuchte Insel, mit allen Sinnen danach geforscht, und sie
	beinahe mit dem Herzen erblickt, mit geschlossenen Augen
	neben einem alten Mann sitzend.
</P>

<P CLASS="P1">
	Der Alte hatte mir die Hände längst von den Augen genommen.
	Lächelnd saß er neben mir. "Du hattest recht, weiser Mann,
	ich war nicht blind mit den Augen, sondern mit meinem
	Herzen, denn zu verbissen war, zu bedacht auf schnöden
	Erfolg, besessen von dem Gedanken das was ich suche nicht zu
	sehen, obwohl es vor meinen Augen lag.", erlag ich der
	Freude.
</P>

<P CLASS="P1">
	Der Mann stand auf, blickt mich mit seinen blinden Augen an
	(?), und sprach: "Höre Kind, mein Werk ist vollbracht, Du bist
	geheilt von der Vergangenheit, die Dich mit Blindheit aus
	Lügen und Täuschung geschlagen hat, doch Du hast sie
	besiegt. Lerne von nun an, nicht leichtfertig zu wissen, und
	schnell aufzugeben, denn Du bist Fabari Leah'sar, die
	Prinzessin, die mit dem Herzen sieht. Trage die Sonne mit
	diesem Namen in Dein Reich und sähe Glück mit ihr, so
	spreche ich, Sha-hurfin der Blinde!"
</P>

<P CLASS="P1">
	Dunkelheit umgab mich, und als ich wieder sehen konnte, war
	der Alte verschwunden. Irgend etwas hatte mir die
	Erinnerungen ein wenig vernebelt. War der Alte jemals
	hiergewesen, oder hatte ich mir seine Anwesenheit nur
	eingebildet? Doch dies war nicht wichtig, sondern nur die
	Tatsache, daß ich nun wußte, wo Kerrana, die Insel der Sonne
	lag.......... 
</P>

<P CLASS="P0">
	<I>Fabari, Innoruuk</I>
</P>

