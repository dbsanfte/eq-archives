<TR><TD BACKGROUND="/pics/pageback2.jpg" ALIGN="CENTER" CLASS="P5">Charm</TD></TR>
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
</TABLE>

<P CLASS="P1">
	Ebenfalls eine sehr n&uuml;tzlich Eigenschaft des Enchanters, aber ein zweischneidiges Schwert. Zu empfehlen ist es,
	sich mit der Gruppe abzusprechen was man charmen wird, und was zu tun ist wenn der Charm bricht. Auch sollte der
	Heiler in diesem Fall beide Augen auf den Enchanter richten, denn der Mob wird mit gr&ouml;&szlig;ter Wahrscheinlichkeit
	versuchen sich zu r&auml;chen. Und wenn man es dann nicht schafft sich mit Hilfe einer Stun Kombination (Color Flux
	- Mezmerize) aus der Aff&auml;re zu ziehen, mu&szlig; einem die Gruppe helfen, und zwar schnell.<br>
	Trotz dieser Gefahren bieten sich mit Charm M&ouml;glichkeiten, &uuml;ber die andere Gruppen nie nachgedacht h&auml;tten, weil
	der gecharmte Mob meist viel besser im Schaden austeilen und nehmen ist, als ein Pet. Zu beachten ist aber, da&szlig;
	er nicht taunted, also entweder von den Gegnern etwas Abstand halten, oder mit Chain stunnen (fast immer zu empfehlen).
</P>

<TABLE BORDER="0" CELLPADDING="0" CELLSPACING="0" WIDTH="100%">
