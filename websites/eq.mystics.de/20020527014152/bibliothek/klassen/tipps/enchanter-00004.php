<P CLASS="P1">
	Willst Du mit Feuerb&auml;llen um Dich schmei&szlig;en, Blitze herabregnen lassen, Dein Pet den Gegner zerfetzen sehen, auf
	Dich allein gestellt sein, dann spiele eine andere Klasse.<BR>
	Magst Du aber Taktik, hektische Situationen, glaubst Du auch im hei&szlig;en Gefecht einen klaren Kopf zu behalten und
	Entscheidungen treffen zu k&ouml;nnen, die &uuml;ber das Wohlergehen Deiner Gruppe entscheiden, dann nur zu. Versuche die
	Kunst der totalen Kontrolle &uuml;ber Deine Gegner zu erlernen, blende die Gegner mit Deinem Glanz und verhelfe Deiner
	Gruppe zu Ruhm.<BR>
	Ohne Gruppe bist Du aber ein Nichts ...
</P>

