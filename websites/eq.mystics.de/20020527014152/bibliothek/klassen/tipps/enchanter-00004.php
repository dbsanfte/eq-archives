<TR><TD BACKGROUND="/pics/pageback2.jpg" ALIGN="CENTER" CLASS="P5">Zwei verschiedene Arten von Stuns</TD></TR>
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
</TABLE>

<P CLASS="P1">
	Ein Enchanter bekommt zwei verschiedene Arten von Stuns
	(Gegner kurzzeitig benommen machen):
</P>

<P CLASS="P1">
	<b>A)</b> Zum Einen w&auml;re da ein Area-Stun, der den Caster als Zentrum hat und mit dem man (hat man mal mehrere davon)
	die Gegner eigentlich 100% der Zeit gestunt halten kann, wenn sie den Zauberspruch nicht allzu oft resisten. W&auml;hrend
	dieser Zeit kann die Gruppe die Mobs angreifen.<br>
	<b>B)</b> Die 2. Stun Reihe sind die Mezmerize Spells (Mezmerize, Enthrall, Entrance und Mezmerisation). Mit ihnen
	kann man einen, oder im Fall von Mezmerisation, mehrere Mobs f&uuml;r einen l&auml;ngeren Zeitraum au&szlig;er Gefecht setzen, aber
	nur so lange bis diese wieder Schaden nehmen oder aufwachen.<br>
	Deshalb ist es beim Einsatz der 2. Spruchreihe wichtig, da&szlig; die Gruppe in der Ihr gerade seid, wei&szlig;, wie sie sich
	verhalten mu&szlig;:<br>
	Alle m&uuml;ssen den gleichen Mob attackieren bis dieser tot ist, und sich dann dem n&auml;chsten zuwenden.
	Auf diese Weise kann immer nur ein Gegner Schaden machen, und der Heiler hat genug Zeit zwischen den Heals zu
	meditieren. Dadurch kann die Gruppe wesentlich mehr Mobs in der gleichen Zeit ausschalten, wie etwa eine Gruppe
	ohne Enchanter.
</P>

<P CLASS="P1">
	Schaden zu machen wird immer weniger wichtig je &auml;lter man wird, und auch von der Nutzung der Pets sollte man in
	vielen Situationen absehen. Sie bringen h&auml;ufig nur mehr Probleme, und ich meine dabei nicht, da&szlig; sie mal wieder
	abwandern und den halben Dungeon mit sich zur&uuml;ckbringen, sondern da&szlig; sie die Mobs die den Enchanter angreifen
	aufs Korn nehmen und es dadurch unm&ouml;glich ist sie mit Entrance, etc. zum Schlafen zu bringen. Wenn die Gruppe
	dies aber wei&szlig; und immer das Pet assistiert, kann man auch damit klar kommen.
</P>

