<TR><TD BACKGROUND="/pics/pageback2.jpg" ALIGN="CENTER" CLASS="P5">Debuffs</TD></TR>
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
</TABLE>

<P CLASS="P1">
	Man bekommt 3 verschiedene Reihen: <b>Attack Speed Debuff</b>, <b>Strength Debuff</b>, und den <b>Strength and
	Agility Debuff</b>.<br>
	Die letzteren zwei sind eigentlich nur f&uuml;r PvP geeignet, da man meist nicht genug Zeit hat alle Debuffs auf
	einen Mob zu casten, und der Unterschied im Schaden den er dann noch austeilt den Aufwand nicht rechtfertigt.
	Zu empfehlen ist der &quot;Attack Speed Debuff&quot;, anfangs auf alle Ziele die deine Gruppe bek&auml;mpft, sp&auml;ter nur noch
	auf die, auf die es sich lohnt (200 Mana auf einen Wan Ghoul Knight in einer Level 45+ Gruppe ist unsinnig).
</P>

<P CLASS="P1">
	Um Eure Aufgabe wahrnehmen zu k&ouml;nnen ist es wichtig, da&szlig; Ihr immer wi&szlig;t wie lange das Mana langt, ob du den Train, der
	Euch gerade &uuml;berrascht hat lange genug kontrollieren kannst, bis alle Mobs tot sind, oder ob du deiner Gruppe sagen
	mu&szlig;t abzuhauen. Es ist wichtig immer zu meditieren wenn du kannst, zwischen den einzelnen Mezmeration-Refreshs zum
	Beispiel.<br>
	Am Besten bei jeder Gelegenheit, aber ebenso wichtig kann es sein den Mob mit Flux zu stunnen, um den Heiler zu
	entlasten. Manchmal langt aber auch einfach nur der <b>Attack Speed Debuff</b>. Es ist immer abh&auml;ngig von der
	Situation, das mu&szlig;t du lernen.<br>
	Man sollte auch der Gruppe klarmachen, da&szlig; Dein Wort Gesetz ist. Wenn du sagst &quot;Dieser Mob als n&auml;chstes&quot;, oder
	&quot;Ignoriere den Zol&quot; (...der dich gerade f&uuml;r 60 Schaden trifft...), dann m&uuml;ssen sie das tun, anderenfalls k&ouml;nntest
	du die Kontrolle &uuml;ber die Situation verlieren. Denn wenn sie den gerade frisch eingeschl&auml;ferten Mob angreifen, weil
	sie nicht h&ouml;ren, und in der selben Sekunde ein anderer wieder aufwacht, den Du extra nicht refresht hattest um Mana
	zu sparen, dann ist das nicht nur &auml;rgerlich, es kann auch deine Mana Planung durcheinander bringen und Dich das
	Leben kosten, und das Deiner Gruppe.
</P>

