<P CLASS="P0" ALIGN="CENTER">
	Diese Tips wurden von Thintalle Clique, Enchantresse auf E'ci, erstellt.
</P>	

<TABLE BORDER="0" CELLPADDING="0" CELLSPACING="0" WIDTH="100%">
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
<TR><TD BACKGROUND="/pics/pageback2.jpg" ALIGN="CENTER" CLASS="P5">Allgemein</TD></TR>
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
</TABLE>

<P CLASS="P1">
	Das hier soll kein Guide sein in dem du erf&auml;hrst wie man am besten die ersten Level bew&auml;ltigt, sondern er
	soll Dir eher mit der Entscheidung helfen, ob Du &uuml;berhaupt einen Enchanter spielen willst, und wenn Du dir
	sicher bist, einige Tips geben.
</P>

<P CLASS="P1">
	Auch wenn es in den ersten paar Leveln den Anschein haben mag, da&szlig; Enchanter nicht allzu schlecht im Schaden
	austeilen sind, ist dem nicht so. Sie bekommen im Laufe ihres sp&auml;teren Lebens alle 8 Level einen neuen DD-Spell
	(Direct Damage), und versetzt alle 8 einen Dot (Damage over time). Der DD ist nicht besonders Manaeffizient,
	der Schaden m&auml;&szlig;ig, die Regenerationsdauer lang. Dennoch werden diese Spells anfangs h&auml;ufig genutzt werden,
	aber nicht um Schaden zu machen. Es ist nicht die Hauptaufgabe eines Enchanters diesen auszuteilen, sondern seine
	Gruppe davor zu bewahren, Schaden zu nehmen.
</P>

<P CLASS="P1">
	Dies macht man durch:
</P>

<UL CLASS="P1">
	<LI>Stuns<BR></LI>
	<LI>Debuffs<BR></LI>
	<LI>Geschicktes Einsetzen von Charm und Fear<BR></LI>
</UL> 

