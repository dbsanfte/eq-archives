<P CLASS="P1">
	Es ist nicht die Intelligenz, die das wichtigste Attribut f&uuml;r uns Enchanter ist, es ist das Charisma. Alle unsere
	Stuns (beide Reihen), sowie Charm und Fear sind vom Charisma abh&auml;ngig. Deshalb setz es Dir als Ziel sowohl CHAR als
	auch INT zu maximieren.<BR>
	Zum Einen kannst Du daf&uuml;r Deine Buffs einsetzen, die Du bekommst (Brilliance +12 INT, und CHAR Buffs, bis zu +40 Char),
	aber ich w&uuml;rde immer dem Charisma den Vorzug geben. Was bringt es Dir ewig viel Mana zu haben, wenn jeder 2. Stun
	resisted wird, oder Charm/Fear nach 4 Sekunden brechen? Das w&auml;re nicht besonders effizient und w&uuml;rde Dich h&auml;ufig das
	Leben kosten.
</P>

