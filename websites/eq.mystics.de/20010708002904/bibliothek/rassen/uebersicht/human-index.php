	<P CLASS="P1">
		Was soll man über sie erzählen? Sie sind wohl die am meisten verbreitete Rasse in Norrath. Ähnlich den 
		Barbaren und den Eruditen, bilden sie doch die Mitte aus beiden. Weder überdurchschnittlich magisch begabt 
		und intelligent wie die Eruditen, noch so einfach und kriegerisch wie die Barbaren. Sie sind neben den Iksar das einzige 
		Volk, bei dem es Mönche gibt. Doch ergreifen Menschen selbstverständlich auch jeden anderen Beruf. Nur 
		Schamanen sind bei ihnen gänzlich unbekannt. 
	</P>
