<P CLASS="P1">
	"Das Grün des Waldes, das Singen der Vögel in den Bäumen... Eins mit der Natur...
	Bereit das Leben für Gerechtigkeit und den Schutz des Waldes zu Opfern...
	Ein Leben in Weisheit und Naturverbundenheit..."
</P>
	
<P CLASS="P1">
	Die Historie unseres Volkes, wie ich sie in meiner Jugend von den älteren Elfen erzählt 
	bekommen habe, lautet wie folgt:
</P>

<P CLASS="P1">
	Als die Hochelfen auszogen gegen ihre Vettern die Dunkelelfen zu kämpfen blieben einige 
	Kolonien in der Alten Welt und zogen sich in den Wald zurück. Langsam lernten wir den Wald 
	und seine Bäume zu lieben und im Geiste der Natur und der magischen Ursprünge des Landes
	zu leben. Nichts von der arroganten und überheblichen Art unserer Vettern der Hochelfen 
	blieb zurück.
</P>
	
<P CLASS="P1">
	Wir bevorzugen im Kampf den Bogen, obwohl wir auch nicht vor dem Nahkampf zurückschrecken. 
	Unsere Ranger (die ich lieber Waldläufer nenne und denen ich mich auch zugehörig fühle) 
	verstehen es meisterlich mit dem Bogen umzugehen und haben auch keine Probleme sich mit 
	dem Schwert zu Verteidigen. Es gibt sogar Kämpfer in unseren Reihen die zwei Handwaffen 
	zur gleichen Zeit führen können. Auch sind die Waldläufer Meister im Spurensuchen und 
	Fallenlegen. Unzählige Eindringlinge haben schon in den Fallen der Waldläufer einen 
	frühen Tod gefunden.
</P>
	
<P CLASS="P1">
	Unsere Waldelfenkrieger sind gefürchtete Kämpfer, die mit ihrer überragenden 
	Schnelligkeit die den Waldelfen angeborene Körperschwäche ausgleichen.
</P>
	
<P CLASS="P1">
	Ein Barde des Waldes versteht es wie kein Anderer in seinen Liedern die Schönheit der Natur 
	einzubinden. Jeder Ton enthält die Magie des Waldes und vermag gar seltsame Dinge zu bewirken. 
</P>
	
<P CLASS="P1">
	Die Druiden sind die Magier unter den Waldelfen. Sie beziehen ihre Kräfte aus der Natur und 
	haben gelernt die Energien der Erde in sich aufzunehmen und zu nutzen. Kein Tier des 
	Waldes würde einen Druiden ohne Grund angreifen.
</P>
	
<P CLASS="P1">
	Wir haben keinen Nutzen für weltliche Güter und Reichtümer und doch gibt es einige unter uns 
	die sich darauf verstehen friedlichen Wanderern die Taschen zu erleichtern. Es geschieht 
	immer wieder das ein Kind des Waldes der Versuchung erliegt. Leider läßt sich die enge 
	Verwandtschaft zu unseren prunkliebenden ‚Weissen' Vettern doch nicht ganz verschweigen.
</P>

<P CLASS="P1">
	Um ungehindert den Wald zu durchqueren, benutzen wir keine schweren Rüstungen aus Metall. 
	Wir verarbeiten an sich nicht gerne diesen Stoff. Ist doch zum Schmieden ein Feuer notwendig 
	und wir müßten uns an der Natur vergehen. Unsere Waffen sind meist aus Kupfer, da es sich 
	leichter formen läßt und kein Baum sein Leben lassen muß. Wir benötigen auch diese Art des 
	Schutzes nicht. Wir gleichen fehlende Rüstung durch unsere Schnelligkeit und Beweglichkeit 
	aus.
</P>

<P CLASS="P1">
	Anderen Rassen begegnen wir mit dem nötigen Mißtrauen. Wir beobachten und nehmen Kontakt 
	auf ,wenn sich die andere Person als vertrauenswürdig und ehrlich erweist. Viele Menschen 
	zählen zu unseren Freunden. Auch die immer hungrigen und neugierigen Halblinge dürfen 
	unsere Siedlung in den Wäldern betreten.
</P>

<P CLASS="P1">
	Nur wenn ich an die Rasse der Zwerge, der ‚Zu kurz Geratenen' denke. Sind sie doch so anders 
	wie wir. Sie streben nach Reichtum, sind rohe und rauhe Gesellen. Die Flüssigkeiten die sie 
	in ihren Rachen schütten würden wir nicht mal unseren größten Feinden vorsetzten. Ihre 
	Getränke erinnern mich vom Geruch her an das, was unsere Pferde hinterlassen, auch die 
	Farbe würde stimmen. Sie betrinken sich und prahlen mit ihren ‚Heldentaten'. Mit Ihren 
	Äxten fällen sie ohne nachzudenken unsere Freunde die Bäume, um ihre mächtigen Hochöfen 
	zu schüren. Gut, ihre Waffen sind meisterlich geschmiedet. Das ist aber wohl auch einer der 
	wenigen Punkte in denen sie sich von den Orcs unterscheiden. Wir haben aufgehört zu zählen 
	wieviel dieser Raufbrüder in unseren Wald eindrangen um Gold und Juwelen zu finden. Erst 
	kurz vor ihrem Tod erkannten sie was die wahren Schätze der Waldelfen sind, unser Wissen 
	und Kultur. Genug über diese Rasse von Trunkenbolden und Angebern.
</P>

<P CLASS="P1">
	Mit unseren Vettern den Hochelfen verstehen wir uns wohl, aber halten uns eher fern von 
	Ihnen. Ihre arrogante und hochnäsige Art widerstrebt uns einfachen Waldelfen.<br> 
	Da wären auch noch unsere anderen Vettern die Dunkelelfen. Solange sie nichts von uns 
	wollen, ignorieren wir sie, obwohl wir ihre Gottheiten und Ansichten verabscheuen.
</P>
	
<P CLASS="P1">
	Wenn ihr gute Absichten habt und mehr über das Leben der Waldelfen lernen wollt besucht uns 
	in den Wäldern. Wenn wir euch wohlgesonnen sind werdet ihr auch unsere Siedlung finden.
</P>

<P CLASS="P0">
	Gehabt euch wohl,<BR>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<I>Sceolan ,Waldläufer</I>
</P>

<P CLASS="P1">
	PS: Meine Quellen über die Waldelfen habe ich aus ‚Herr der Ringe' und ‚Warhammer'. 
	Wie sie dort dargestellt werden  entspricht meinen Vorstellungen dieses Volkes. Ich bin 
	nicht der ‚supertolle' Rollenspieler und kann hier nur meine Ansicht wiedergeben.
</P>

<P CLASS="P1">
	Ein Waldelf würde nie grölend durch die Städte ziehen. In Wirtschaften sitzt er eher in 
	der Ecke und beobachtet die Anwesenden. Ein Waldelfen-Barde wird natürlich auf Wunsch auch 
	einmal ein Lied vortragen, aber nie auf der Straße stehen und jeden der vorbeilauft 
	belästigen oder anpöbeln. Auch sollte er die Natur achten, also nicht jeden Bären den 
	er im Wald findet sinnlos abschlachten. Die wohl herausstechendste Charaktereigenschaft 
	ist (meiner Meinung nach) sein zurückhaltendes Wesen. Einen Elfen umgibt auch immer 
	etwas geheimnisvolles und das sollte man auch beachten, wenn man einen Waldelfen spielt. 
	Nichts wäre schlimmer für mich als in Everquest einen Waldelfen zu Spielen und meine 
	‚Rassengenossen' benehmen sich wie Zwerge ;-) ...
</P>

<P CLASS="P1">
	Ok... das waren meine Gedanken zu den Waldelfen... Wir sehn uns in Everquest...
</p>

