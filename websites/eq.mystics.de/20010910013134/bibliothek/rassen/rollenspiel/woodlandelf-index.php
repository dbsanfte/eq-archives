<P CLASS="P1">
	Die Waldelfen leben in Kelethin, auf dem Kontinent Faydwer, am Rande des Faydark Forest. 
	Kelethin ist eine wunderschöne Stadt, die auf Plattformen in den Baumwipfeln gebaut ist, die 
	wiederum durch Hängebrücken verbunden sind. Wem die Wendigkeit und das Geschick der Waldelfen 
	fehlt, der sollte sich vorsichtig bewegen, da ein Sturz aus dieser Höhe zu schweren 
	Verletzungen oder gar zum Tode führen kann.
</P>

<P CLASS="P1">
	Aus der Ferne betrachtet sind Elfen nicht immer gleich von Menschen zu unterscheiden, wer es 
	allerdings wagt sich ihnen auf Bogenschußweite zu nähern, wird sie an ihrer grazilen Gestalt
	und ihren leicht spitz zulaufenden Ohren leicht erkennen.
</P>

<P CLASS="P1">
	Die Waldelfen sind im Vergleich zu ihren Vettern, den Hochelfen, von dunklerer Hautfarbe und 
	können im Allgemeinen als naturverbundener gelten. Sie verwenden mehr Zeit auf die Tier- und 
	Pflanzenwelt als auf theorethische Überlegungen. Vielleicht kennen sie deshalb ihren Platz 
	in der Welt besser als ihre Verwandten und tragen ihre Nase nicht ganz so hoch.
</P>

<P CLASS="P1">
	Die Waldelfen lieben ihre Unabhängigkeit und sind im Umgang mit Fremden häufig etwas 
	zurückhaltend. Ist diese erste Hürde jedoch überwunden sind sie treue und verläßliche 
	Gefährten. 
</P>

<P CLASS="P5">
	Die Klassen der Waldelfen
</P>

<P CLASS="P1">
	<B>Der Barde</B><BR>
	Die Barden aus Kelethin gelten zu Recht als ausgezeichnete Vertreter ihrer Zunft. Ihre 
	sanften und manchmal melancholischen Lieder umweben die Seele und erzählen vom Verlust 
	ihrer alten Heimat. Neuere Stücke haben dagegen oft einen fröhlichen, lebendigen Charakter 
	und haben die Jagd, die Natur oder, wie könnte es anders sein, die Liebe zum Thema.
</P>
	
<P CLASS="P1">
	<B>Der Druide</B><BR>
	Bedenkt man ihren Charakter, so ist der Druide vielleicht eine typische Profession für den 
	Waldelfen. Der ständige Umgang mit Flora, Fauna und den Naturgewalten Norraths bereitet 
	unserer Rasse einfach Freude.
</P>
	
<P CLASS="P1">
	<B>Der Krieger</B><BR>
	Der Waldelfenkrieger muß seine vergleichsweise schwache Konstitution, durch Geschicklichkeit 
	und geschärfte Reaktionen ausgleichen. Als meisterhafter Schwertkämpfer und zielsicherer 
	Bogenschütze ist er für sein Volk ebenso wertvoll wie für eine Gruppe von Abenteurern.
</P>
	
<P CLASS="P1">
	<B>Der Waldläufer</B><BR>
	Jeder Waldelf verbringt viel Zeit im Wald von Faydark, aber nur der Waldläufer hat die dazu 
	benötigten Fertigkeiten vervollständigt. Sein Orientierungssinn, seine Sicherheit im 
	Spurenlesen   und seine Fähigkeit von den Gütern der Natur zu leben, machen ihn zu einem 
	relativ  ungefährdeten Einzelgänger und zu einem gesuchten Führer.
</P>

<P CLASS="P5">	
	Das Verhältnis zu den anderen Rassen
</P>

<P CLASS="P1">
	Es gibt eine Rasse die wir Waldelfen zutiefst verabscheuen und das sind die Dunkelelfen. 
	Alles was an den Hoch- und Waldelfen schön und edel ist, ist bei den Dunkelelfen pervertiert. 
	Ihr abgrundtiefer Haß allen Lebens und ihre blutigen Rituale entspringen ihrem verdrehtem, 
	irrem Geist. Was sie als ihre Ehre und ihren Stolz betrachten, ist in Wahrheit nur maßlose 
	Eitelkeit.
</P>

<P CLASS="P1">
	Daß Oger und Trolle unsere Feinde sind, steht außer Frage und bedarf keiner weiteren 
	Erklärung.
</P>

<P CLASS="P1">
	Allen anderen Rassen treten wir dagegen aufgeschlossen entgegen. Den Hochelfen werfen wir 
	zwar ab und zu einen spöttischen Seitenblick zu, aber ihre Selbsteinschätzung würden wir 
	natürlich nie offen in Frage stellen. Schließlich sind sie unsere engsten Verwandten und 
	auch unsere Nachbarn. Fehler die man bei seinem Bruder sieht, sollte man ja vielleicht bei 
	sich selbst suchen.
</P>

<P CLASS="P1">
	Das Verhältnis zu den Zwergen ist zwar etwas gespannt, aber von echter Feindschaft kann 
	man hier bei weitem nicht sprechen. Wer einen Elfen und einen Zwerg einmal über längere 
	Zeit beobachtet hat, wird zu dem Schluß kommen, daß die ständigen gegenseitigen 
	Verwünschungen im Grunde nur eine Fassade sind.
</P>

<P CLASS="P1">
	Die anderen kleinen Völker und auch die Menschen betrachten wir meist mit Wohlwollen und 
	kommen gut mit ihnen aus.
</P>

<P CLASS="P5">
	Wie man einen Waldelfen spielt
</P>

<P CLASS="P1">
	Natürlich liegt es auch diesmal an euch, wie ihr euren Charakter spielen wollt. Die Waldelfen 
	bieten eine reiche Palette verschiedenartiger Verhaltensmuster, von hochnäsigen 
	Schwertkämpfern über liebestolle Barden bis zu wortkargen Waldläufern. Eins sollte man aber 
	vielleicht nicht vergessen, Elfen sind selbstsicher und von ihrem Auftreten überzeugt. 
	Wenn sie einen Fehler erkennen, sind sie zwar bereit diesen einzusehen, zu einer 
	Entscheidung lassen sie sich aber von niemandem drängen.
</P>

<P CLASS="P1">
	Außerdem sind Elfen von Natur aus neugierig, und auch wenn sie diese Neugierde mit der 
	nötigen Vorsicht befriedigen, werden sie sich doch nicht von einer Herausforderung 
	abschrecken lassen.
</P>
	
<P CLASS="P1">
	Leeta liebt die Natur, ist aber im Umgang mit Leuten etwas schüchtern. Sie ist was Elfen 
	angeht quasi noch ein Teenager und versucht sich gerade von ihren Eltern abzugrenzen. Ihre 
	kleine Rebellion hat sie zu der Entscheidung gebracht eine Karriere als Waldläuferin zu 
	verfolgen, da sie für den stillen Druidenzirkel ihres Vaters kein Interesse aufbringen kann 
	und im Umgang mit Menschen nicht so offen ist wie ihre Mutter, die als Heilerin arbeitet.<br>
	Ihre Fertigkeiten sind im Moment noch beschränkt, aber sie ist sich sicher, daß ihr 
	unbändiger Wille sie zu ihrem Ziel führen wird.
</P>
	
<P CLASS="P1">
	Wenn ihr noch wenig Erfahrung mit Rollenspielen habt und nicht die Zeit oder Lust 
	aufbringen könnt eine Pen&amp;Paper Gruppe zu bilden, könnt ihr ja mal an einem 
	Chat-Rollenspiel teilnehmen. Auf dem EQVault Server werden mehrere angeboten und im 
	Zweifelsfall bringt einen schon das Zuhören auf ein paar Ideen.
</P>
	
<P CLASS="P0">
	Auf bald im Wald von Faydark,<BR>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<I>Leeta gal Arien</I>
</P>

