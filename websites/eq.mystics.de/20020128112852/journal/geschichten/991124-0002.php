<P CLASS="P1" ALIGN="CENTER">
	Von Brallayan Shadowmaster<BR>
	<BR>
</P>

<P CLASS="P1">
	Geboren wurde ich in eine Familie mit uralter Tradition in der Nekromantie. Auch mir wurde 
	bald klar, das die Nekromantie das ist, wozu ich berufen bin. Meine Eltern erkannten schon 
	früh meine Begabung (spätestens als ich begann die von mir erschlagenen Fliegen wieder zu 
	beleben) und förderten meine Schulung in der Schwarzen Kunst.
</P>

<P CLASS="P1">
	Das Leben war wunderbar, Blutopfer, Angstschreie, Haß und nicht zu vergessen die Rituale. 
	Aber es ward nicht von langer Dauer. Diesen Tag werde ich nie vergessen, ich war gerade mit 
	meinen Studien beschäftigt, als ein alter Darkelf die Tür aufriss, schnell ein Gate nach 
	Neriak castete mir befahl dadurch zu gehen, man erwartete mich bereits auf der anderen Seite. 
</P>

<P CLASS="P1">
	Wie ich kurze Zeit darauf erfuhr, hatte eine Gruppe von diesen so abstoßend guten Paladinen 
	meine Eltern im Schlaf überrascht und sie getötet. Auch ich wäre jetzt tot, wenn dieser alte 
	DarkElf, dessen Namen ich nie erfahren habe, mich nicht durch das Tor geschickt hätte.
</P>

<P CLASS="P1">
	Von nun an ließ mich ein unbeschreiblicher Haß, auf alles Gute und vor allem auf alles was 
	sich Paladin schimpft, meine Studien noch stärker vorantreiben und meine Lehrmeister 
	betrachteten meine Fortschritte mit wohlwollen.
</P>

<P CLASS="P1">
	Alsbald war ich bereit meinen eigenen Weg zu gehen und ich verließ die schützenden Mauern 
	Neriak's, um den Namen des Nekromanten, der mich damals rettete zu erfahren und auch die 
	der hinterhältigen Paladine, um den Tod meiner Eltern zu rächen und Sie dem Zorn Inoruuks 
	zu zuführen.
</P>

<P CLASS="P1">
	Doch es dauerte noch lange, bis ich die damaligen "Helden" zu Gesicht bekam. Ich durchstreifte 
	weite Teile Norrath's, brachte Angst und Schrecken auf meinen Wanderungen und suchte Hinweise, 
	die mich auf eine Spur brachten, aber die Zeit verstrich und weit und breit keine Spur.
</P>

<P CLASS="P1">
	Als ich die Hoffnung schon aufgeben wollte, kam der rettende Hinweis. Ich saß gerade in einer 
	Taverne und stillte meinen Hunger und Durst, als ein alter DarkElf die Taverne betrat. Obwohl 
	ich mich nicht erinnern konnte ihn schon mal gesehen zu haben, war mir doch so als ob er mir 
	altbekannt wäre.
</P>

<P CLASS="P1">
	Ich konnte nicht anders und ging, sobald er sich gesetzt hatte, zu ihm. Ich fragte ihn, ob er 
	mich kennen würde. Er sah mich minutenlang mit glitzernden Augen an und fragte mich dann, ob 
	ich mich nicht an ihn erinnern würde, habe er mir doch damals das Leben gerettet, so wie er es 
	meinen Eltern geschworen hatte. Leider konnte er Ihnen nicht mehr helfen. Er selbst konnte nur 
	sehr schwer verletzt entkommen. 
</P>

<P CLASS="P1">
	Als er das sagte, kam die Erinnerung an jene Nacht zurück und begann nur noch mehr diese 
	"guten Seelen" von Paladine zu hassen. Und nun, nach so langer Zeit konnte er mir sagen, 
	wie die Namen derer waren, die diesen Frevel begingen. Nachdem wir noch etwas zu uns 
	genommen hatten, machte ich mich auf, mich meinem Schicksal zu stellen und meinen Schwur, 
	den Tod meiner Eltern zu rächen, zu erfüllen.
</P>

<P CLASS="P0">
	<I>Brallayan Shadowmaster</I><br>
	Darkelf Nekromant, Vallon Zek<br>
	Armee der Finsternis
</P>

