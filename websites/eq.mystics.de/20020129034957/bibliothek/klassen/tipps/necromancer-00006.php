<P CLASS="P2">
  a) Ohne Cleric:
</P>
<P CLASS="P1">
  Wenn das Monster gepullt wird, dann werden folgende Spells gecastet:
</P>
<UL CLASS="P1">
	<LI>
    <u>BoD</u> auf das Ziel des Pullers.
	</LI>
	<LI>
		Den Mainpuller anvisieren und sofort <u>Shadowbound</u> u. <u>Pact of Shadow</u> casten. 850HP/30 Sekunden sind nicht zu verachten und sparen Cleric-Mana. Ausserdem könnt ihr einen Tank damit ohne Probleme heilen, wenn ein Slower da ist, ohne eigene HP zu verlieren. Ihr nehmt euch vom Monster quasi 850HP und gebt sie an den Gruppenpuller weiter.
	</LI>
</UL>
<P CLASS="P1">
	Das sind die wichtigsten Schritte, wenn kein Cleric da ist. Diese Taktik - Chaincasten von Pact Spells - könnt ihr aber auch ohne weiteres anwenden, wenn ein Cleric und Chanter vorhanden sind. Damit könnt ihr wunderbar die Caster heilen, damit der Cleric sich auf die Tanks konzentrieren kann.
</P>

<P CLASS="P2">
	b) Ohne Chanter:
</P>
<P CLASS="P1">
	Wenn der Puller mit mehreren Monstern kommt, versucht ihr erstmal das Monster als Ziel zu nehmen, welches NICHT das Ziel des Pullers ist. Danach castet ihr ST drauf, sucht das Ziel des Pullers wieder und schickt euer Pet drauf los. Danach haltet ihr die anderen Monster gemezzt und sagt dem Shamanen - falls vorhanden - dass er sie slowen soll. Wenn es keine Caster Monster sind, kann man sie auch rooten (Root-parken sozusagen). Danach können sie kein Unheil anrichten, auch wenn ST vorbei ist. In dem Fall müsst ihr aber aus der Nahkampfreichweite des Monsters gehen.
</P>
<P CLASS="P1">
	Ihr könnt auch, wenn der Cleric oder Chanter dringend Mana braucht euren Subversion Spell memen. Es ist aber NUR für Notfälle, weil die Transfer-Rate einfach grottenschlecht ist. Es kann aber helfen, damit der Cleric einen CH zur rechten Zeit casten kann oder der Chanter einen letzten Mezz casten kann. Es ist aber kein Spell, welchen man permanent nützen sollte. Ihr könnt mit eurem eigenen Mana viel mehr für die Gruppe tun. 
</P>

