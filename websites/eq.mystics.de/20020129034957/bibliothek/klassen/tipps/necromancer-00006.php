<P CLASS="P0" ALIGN="CENTER">
	Diese Tips für Necromancer in Gruppen-Indoor (Dungeons) wurden von Verina erstellt. Der Stand der 
	Infos ist 14.02.2001.
</P>	

<P CLASS="P1">
  Hallo erstmal!<br>
  Ich habe mich entschlossen, meine Erfahrungen als Necro an andere weiterzugeben, weil ich doch immer wieder feststellen muss, dass es richtige Level50er Dungeon-Newbies gibt. Das ist nicht gerade toll für den Ruf einer Klasse. Deswegen hoffe ich, dass ich einigen dabei helfen kann, ihre Klasse optimal zu spielen.
</P>

<P CLASS="P1">
  Die Strategie in High Level Indoor-Zonen - Sebilis zum Beispiel - ist völlig abweichend von der in weiten und grossen Outdoor-Zonen, weil Fear-Kite Indoor keine Option ist. Es sorgt nur dafür, dass die Gruppe von Trains überrannt wird und dann stundenlang Corpse recover betreiben muss.
</P>

<P CLASS="P1">
  Ich versuche das mal logisch und übersichtlich aufzubauen.
</P>

