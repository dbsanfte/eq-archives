<P CLASS="P2">
  a) für eine Gruppe ohne Cleric aber mit Chanter/Bard:
</P>
<P CLASS="P1">
  <u>Dooming Darkness</u> - Snare ist essentiell Indoor. Wenn ein Monster low on Hit-Points ist, versucht es abzuhauen. Wenn es verlangsamt ist, bleibt es auf der Stelle kleben und kann nicht weglaufen. Damit vermeidet man Trains. Es sollte aber immer ein Druide/Ranger/Wizzard Snare casten, weil ihr Snare einfach eine längere Dauer hat. Ausserdem ist es kein Dot. Deswegen kann man einen gesnarten Mob immer noch mezzen.
</P>
<P CLASS="P1">
  <u>Deflux</u> oder andere <u>Drain DDs</u> - Deflux ist insofern genial, weil es eine sehr kurze Casttime hat. Dadurch kann man sich sehr schnell heilen und die Gefahr von Interrupts ist reduziert.
</P>
<P CLASS="P1">
  <u>Bond of Dead</u>
</P>
<P CLASS="P1">
  <u>Shadowbound</u> - 500 HP zum Ziel binnen 24 Sekunden.
</P>
<P CLASS="P1">
  <u>Pact of Shadow</u> - 350 HP zum Ziel binnen 30 Sekunden.
</P>
<P CLASS="P1">
  <u>Envenoment Bolt</u> - Schneller Dot-Spell. Weil die meisten Monster keine Minute stehen, ist es sinnlos, langsame Dots zu casten. Achtet aber darauf, dass ihr nicht sofort EB castet. Wartet einige Runden, bis die Tanks genügend Damage gemacht haben. Danach könnt ihr EB casten, ohne dass das Monster sich euch zuwendet. Also, nicht zu schnell Dotten.<br>
  Optional zu EB könnt ihr auch <u>Lich</u> benutzen, ABER nur wenn ein Chloroplast/Regrowth-Caster in der Gruppe ist. Sonst seid ihr bei 3 Bubble HP und wenn euch dann ein Frosch anspringt, ist es meist aus. Ich persönlich ziehe Clarity2 Lich vor.
</P>
<P CLASS="P1">
  Pet Buff <u>Augment Death</u> oder <u>Augmentation of Death</u> (wenn manns hat). Ein Chanter ist damit beschäftigt Mobs zu mezzen, slowen, Tanks zu buffen, Clarity zu casten ... da könnt ihr dem armen Chanter etwas Arbeit abnehmen, wenn ihr eure Pets selber bufft. Wenn aber ein Shaman da ist, der das Slowen der Mobs übernimmt, sollte der Chanter schon eure Knochis buffen. In dem Fall könnt ihr EB hier memen.
</P>
<P CLASS="P1">
  <u>Gate</u> - Dazu brauche ich wohl nichts zu sagen. Wenn die Lage hoffnungslos ist, dann ist es immer noch besser zu gaten, als heldenhaft zu sterben. Wenn einige aus der Party überleben, ist die Corpse recovery einfacher, als wenn die ganze Gruppe stirbt.
</P>

<P CLASS="P2">
  b) für eine Gruppe ohne Chanter aber mit Cleric:
</P>
<P CLASS="P1">
  <u>Dooming Darkness</u>
</P>
<P CLASS="P1">
  <u>Deflux</u>
</P>
<P CLASS="P1">
  <u>Screaming Terror</u> - Sehr wichtig! 18 Sekunden Mezz ist nicht zu verachten. Mit den Level werden auch die Resists gesenkt. Ich hatte auf Level 52 6xResists bei den normalen Froeschen. Mittlerweile mit 56 gibt es dort kaum noch Resists.<br>
  Mit einem Slower - also Shamanen, wenn kein Chanter da ist - könnt ihr locker 2 Mobs unter Kontrolle halten.
</P>
<P CLASS="P1">
  <u>EB</u>
</P>
<P CLASS="P1">
  <u>Feign Death</u> - Wenn ein Mezz mal resistet wird, könnt ihr FD casten und hoffen, dass es klappt. Danach greift das Monster einen anderen aus der Gruppe an. Dann steht ihr sofort auf und recastet Screaming Terror. Wichtig: Mobs die gedottet sind (von euch) fallen nicht auf FD rein!
</P>
<P CLASS="P1">
  <u>Pet Buff</u>
</P>
<P CLASS="P1">
  <u>Lich</u> - Wenn ein Chloroplast/Regrowth Caster da ist.
</P>
<P CLASS="P1">
  <u>Gate</u>
</P>

