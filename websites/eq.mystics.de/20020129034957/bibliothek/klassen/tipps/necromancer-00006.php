<P CLASS="P1">
	Ich habe folgende 10 Hotkeys belegt und komme eigentlich gut klar mit denen:
</P>
<P CLASS="P0">
	<b>snare</b><br>
	<i>/g snaring %t</i> - Damit teilt ihr der Gruppe mit, dass ihr das Ziel verlangsammt. Information ist extrem wichtig in so einer Gruppe.<br>
	<i>/cast #</i> - # = Nummer von Dooming Darkness (z.B. 1 in dem Fall. Also wäre euer text: <i>/cast 1</i>).
</P>
<P CLASS="P0">
	<b>deflux</b><br>
	<i>/cast #</i> - # = Nummer von Cast-Slot mit Deflux drin.
</P>
<P CLASS="P0">
	<b>bod</b><br>
	<i>/cast #</i> - # = Nummer von Cast-Slot von Bond of Death, ODER halt EB, wenn ihr einen Cleric und noch Backup-Healer habt (Druid, Shaman).
</P>
<P CLASS="P0">
	<b>pact</b><br>
	<i>/g pacting %t</i> - Das Ziel wird damit geheilt. Das teilt ihr eurer Gruppe mit, damit nicht 2 Leute zugleich jemanden heilen. Das wäre Manaverschwendung.<br>
	<i>/cast #</i> - # = Nummer von Pact of Shadow.<br>
	<i>/pause 50</i><br>
	<i>/cast #</i> - # = Nummer von Shadowbound.<br>
	... mit einem Click heilt ihr das Ziel für 850HP/30 Sekunden.
</P>

