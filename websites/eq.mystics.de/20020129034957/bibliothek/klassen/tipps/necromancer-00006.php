<P CLASS="P0">
	<b>follow</b><br>
	<i>/pet follow</i> - Euer Pet folgt euch. Wichtig ist, dass ihr nicht zu schnell rennt, wenn die Gruppe irgendwohin laufen will. Langsam laufen und immer in der Nähe des Pets bleiben. Dann ist die Gefahr, dass euer Pet ziellos rumläuft und probleme verursacht minimiert.
</P>
<P CLASS="P0">
	<b>assist</b><br>
	<i>/assist #</i> - # = Name des Mainpullers. Damit visiert ihr das Ziel des Pullers an. Immer einige Sekunden warten, bis der Puller das Ziel einige Male trifft. Dann habt ihr das richtige Ziel.
</P>
<P CLASS="P0">
	<b>attack</b><br>
	<i>/pet attack</i> - Direkt nach /assist drücken. Damit greift euer Pet das Ziel des Pullers an.
</P>
<P CLASS="P0">
	<b>back off</b><br>
	<i>/pet back off</i> - Euer Pet lässt vom Ziel ab. Wichtig, wenn euer Pet das falsche Ziel angreift und der Chanter das Ziel mezzen will. Mehrmals draufdrücken und es klappt irgendwann mal ... meistens.
</P>
<P CLASS="P0">
	<b>guard</b><br>
	<i>/pet guard here</i><br>
	<i>/pet sit down</i><br>
	... damit bleibt euer Pet auf der Stelle und setzt sich hin. Das spart viel Ärger. Ich denke, dass die Monster ein Pet das sitzt weniger angreifen, als wenn das Pet stehen würde. Das Pet wird NIE im Pullweg geparkt. IMMER hinter euch parken. Am Besten in einem Raum hinter euch parken.
</P>
<P CLASS="P0">
	<b>st</b><br>
	<i>/g mezzing %t</i> - Ihr teilt der Gruppe mit, dass ihr das Ziel mezzen wollt.
	<i>/cast #</i> - # = Nummer von ST Cast-Slot.
	<i>/g mezzing %t, please root/slow it</i> - Der Gruppenshaman sollte das Ziel nun Slowen oder das Ziel sollte gerootet werden.
</P>

<P CLASS="P2">
	So, nun noch einige Grundsätze zu Pets:
</P>
<UL CLASS="P1">
	<LI>
		Immer "Pet taunt off". Eure Pets können ein 48er Monster keine 20 Sekunden tanken. Euer Tank sollte pullen und eure Pets sind dann starke Dot-Spells. Die besten, die ihr habt.
	</LI>
	<LI>
		Immer vorbereiten "Gather Shadow" zu casten. Sobald euer Pet nicht mehr zu sehen ist, sofort Gather Shadow memen und casten. Damit wird das Pet zerstört und hoffentlich werden die Monster nicht auf eure Gruppe aufmerksam. Danach am Besten sofort relogen.
	</LI>
	<LI>
		Immer nahe beim Pet bleiben, wenn die Gruppe sich bewegt. Euer Pet kann euch leichter folgen, wenn ihr langsam geht und nicht rennt.
	</LI>
	<LI>
		Pets NIE im Pullweg stehen lassen. Am Besten irgendwo hinter euch oder direkt neben euch.
	</LI>
</UL>

<P CLASS="P1">
	Grundsätzlich kann man sagen, dass das Spielen eines Necros Indoor sehr schwer ist.
</P>

