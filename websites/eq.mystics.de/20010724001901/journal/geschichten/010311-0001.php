<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Sunchin Skytanker<BR>
	<BR>
</P>

<P CLASS="P1">
	Als Sunchin die Augen öffnete war es ein Tag wie jeder andere. Die Sohne schien durch die Fenster des Inn´s und draussen hörte man Geschrei von Kindern und das Zwitschern der Vögel. Sunchin stand aus seinem Bett auf und suchte im Schrank nach seiner Foremans Tunic und seinen Drake-Hide-Leggins, dann ging er die Treppe hinunter und setzte sich an einen freien Tisch um etwas zu essen. Denn heute musste er gestärkt sein. Es war der Tag wo sie, also er und seine Freunde, den Gnollen in Paw mal so richtig eine reinhauen wollten.
</P>

<P CLASS="P1">
	Während er ass kam seine Elfen-Freundin Celinah, eine erfahrene Klerikerin und setzte sich zu ihm und sagte: <I>"Na? Lust nen paar Gnollen den Hintern wegzupusten?"</I><br>
	<I>"Klar, nachdem sie mich gestern nochma erwischt haben, habe ich um so mehr Lust dazu!"</I><br>
	Zusammen machten die beiden sich auf den Weg zum Stadt-Tor von Freeport um ihre beiden Freunde, den Wizard Peztilence und die Druidin Leihna zu treffen.<br>
	<I>"Alle Mann fertig Leute?"</I>, fragte Leihna.<br>
	<I>"Jo leg los!"</I>, rief ihr Sunchin zurueck.<br>
	Sofort fing Leihna an viele magische Worte zu sprechen es hörte sich etwa so an:<I>"Kla, Ma, Wena, Menta Par, Flam Karana, Porta Mea!"</I><br>
	Plötzlich fühlte Sunchin wie irgendetwas an ihm zog und ehe er sich versah stand er zwischen mehreren Säulen und überall liefen beschäftigte Druiden und auch Tiere rum.
</P>

<P CLASS="P1">
	Er war in North-Karana.<br>
	<I>"Wisst ihr den Weg?"</I> hörte er Leihnas Stimme durch den Gruppen-Chat.<br>
	<I>"Ja wir sehen uns bei Paw!"</I>, rief Sunchin. Und rannte gefolgt von Celinah und Peztilence über die Brücke nach South-Karana. Dann immer nur nach Süden und man konnte den Eingang nicht verpassen. Als sie alle vor den 3 Spires, die bei Paw standen, stehen geblieben waren, hörte Sunchin Leihna durch den Gruppen-Chat rufen: <I>"Gnoll Gnoll Gnoll, hier is´n Gnoll hinter mir her! Hilfe!"</I><br>
	<I>"Komm zum North-Spire!"</I>, rief Sunchin.<br>
	Und er konnte Leihna schon einige Meter hinter sich sehen, dicht gefolgt von einem Tesch-Mas-Gnoll. Als er nah genug war, traf ihn auch schon ein Feuerball von Peztilence, und Sunchin gab ihm eine kräftige Faust ins Gesicht. Der Gnoll jaulte und schlug auf Sunchin ein, doch der konnte durch geschicktes Ausweichen verhindern, dass der Gnoll ihm etwas antat. Und als der Gnoll schon müde und schlapp war, sprang Sunchin hoch und gab ihm einen mächtigen Tritt ins Gesicht. Der Gnoll ging zu Boden und ehe Sunchin das mitbekam hörte er von Leihna durch den Gruppen-Chat: <I>"DING DING DING Welcome to Level 30 !!!!!"</I><br>
	<I>"Mega!"</I> meinte Sunchin und auch die 2 anderen Freunde gratulierten Leihna.<br>
	<I>"Lass ma da rein gehen."</I> meinte Peztilence und die 4 Freunde gingen zum Eingang nach Paw.
</P>

<P CLASS="P1">
	Sunchin hörte durch den Gruppen-Chat Peztilence: <I>"So´n blöder Scheiss Gnoll, immer von hinten auf den Wizard!!!"</I> Auch Leihna war gestorben, nur Sunchin der sich schnell genug auf den Boden geworfen hatte und nun wie tot da lag, und Celinah die so schnell gerannt war wie nie zuvor, hatten überlebt.<br>
	<I>"Wenn Sunchin die Viecher von euren Leichen pullt kann ich euch Rezzen!"</I><br>
	<I>"Immer muss ich die Drecks-Arbeit machen *g*"</I>, meinte Sunchin. So musste der lvl 30 Monk alleine die Gnolle von Pezs, und Leihnas Leichen wegholen. Wenige Sekunden später fand er sich neben Leihna und Pez in West Feeport wieder.<br>
	<I>"Seit wann können die Backstabben ??????"</I><br>
	Jetzt konnte nur Noch Celinah den 3 helfen. Wie auch immer sie es schafft, wenige Sekunden später waren alle 4 wieder in South-Karana bei Paw.<br>
	<I>"Ich habe mein Level verloren"</I>, meinte Leihna.<br>
	<I>"Das holen wa uns wieder!!!"</I>, rief Sunchin während er frohen Mutes wieder reinmaschierte.
</P>

<P CLASS="P1">
	Sunchin war kein Mensch mehr, so kam es vielen vor. Nach einigen Buffs von Leihna und Celinah hatte sich der Monk in eine Kampf-Maschine verwandelt. Drei Gnolle stürzten auf ihn ein, doch dem ersten trat er die Waffe aus der Hand, schlug mit seiner bloßem Faust in seinen Magen und die spitzen Seiten des Knuckle Dusters durchtrennten Kettenhemd, Lederarmor und Fleisch. Dem zweiten Gnoll verpasste Sunchin einen mächtigen Fusstritt in den unteren Bereich, bevor er ihn mit 2 Schlägen ausser Gefecht setzte. Als der dritte Gnoll von hinten nahte, schlug Sunchin um sich und traff den Gnoll 4 mal am Kopf. Dieser sank danach zu Boden.<br>
	<I>"Miese Viecher!"</I>, brüllte der Monk und jagte tiefer in den Dungeon. Gerade wollte er einem Gnoll mal so richtig die Nase zerdonnern, als dieser jedoch unter einer grossen Explosion zusammen sackte und Peztilence sich die Finger rieb. Nach vielen Stunden, in denen der Monk tausenden von Gnollen die Fresse eingeschlugen hatte, gingen die 4 zurück nach Freeport, weil es schon Nacht war. Und in einem schönen Traum freute sich Sunchin über den heutigen Tag.
</P>

<P CLASS="P0" ALIGN="CENTER"><I>Sunchin Skytanker</I></p>

