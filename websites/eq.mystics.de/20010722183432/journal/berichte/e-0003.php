<P CLASS="P0" ALIGN="CENTER">
	<B>Autor: Nadyawin</B><BR>
	<BR>
</P>

<P class="P0">
	Hi Mystics Team,
</P>
 
<P class="P0">
	ich wollte mal einen kleinen Aufruf von allen Tailorn starten.
</P>
 
<P class="P1">
	Zu aller erst: Man geht doch beim Namen Studded Leather Armor (genietete Lederrüstung)
	davon aus, daß es in Bezug auf Leather Armor (Lederrüstung) mehr Schutz bietet. Dieser
	Artikel wird euch eines Besseren belehren. Denn hier ist aufgeführt, daß
</P>

<OL CLASS="P0">
	<LI>...der Name für diese Rüstung ungerechtfertigt ist.<BR><BR></LI>
	<LI>...der Tailoring Skill ab LVL 6 nicht mehr rentabel ist und sich jeder, der irgenwann mal Reinforced Leather Armor herstellen möchte, sich mit dem Gedanken abfinden muß, daß er sehr viel Geld und Zeit investieren muß und dieses Geld niemals mit dem Verkauf der Rüstung wieder einbringen wird.<BR></LI>
</OL>
 
<P class="P1">
	Zur Geschichte: Mir ist es letztends zum erstan mal gelungen studded leather armor zu tailorn.
	Und was sehen meine Augen? Sie bringt genau so viel AC wie normales Leder, welches man beim
	NPC kaufen kann. Ich frage mich ob es gerechtfertigt ist.
</P>
 
<P class="P1">
	EQ ist ein Spiel, welches auch darauf aufbaut, daß Spieler untereinander Handel treiben und
	nicht zum NPC Händler gehen, um dort ihr selbsthergestelltes Zeug zu verkaufen.
</P>

<P class="P1">
	Nun frage ich mich, ob der Skill Tailoring überhaupt diesen Sinn erfüllt. Am Anfang mag er
	noch ganz nett sein, aber spätestens bei der Herstellung von studded leather schreibt man
	rote Zahlen, welches meiner Meinung nach dazu führt, daß Tailoring das Zusammenspiel nicht
	erfüllt. Der Grund ist, wie bereits erwähnt, daß sie nicht mehr, auch nicht ein kleines
	bißchen mehr AC bringt wie normale, vom NPC Händler zu erwerbenden Leather Armor bringt.
	Dies ist einer der Begründungen, warum sich ein SC eher eine Lederrüstung zulegt, die er
	beim NPC Händler erwerben kann. Er muß sich nicht mit Studded Leather Armor abgeben, die
	mehr kostet und genau so viel AC bringt.
</P>
 
<P class="P0">
	Wobei wir jetzt beim Thema kosten wären:
</P>

<P class="P1">
	Eine normale Leather Armor vom NPC kostet ca. 1-3 Platin pro Teil (Tunic mehr als gorget).
	Es gibt caps, masks, gorgets, gloves, sleeves, tunics, belts und pants zu kaufen. leather
	boots habe ich keine gesehen bisher. Also insgesamt gibt es 8 Teile zu kaufen. Macht im
	Schnitt ca. 20 Platin.
</P>
 
<P class="P1">
	Studded Leather kann man nur herstellen, es gibt sie nicht beim NPC zu kaufen, es sei denn
	jemand verkauft sie unter Riesenverlusten beim NPC. Man kann, nach den patterns, die ich
	bisher gesehen habe, 7 Teile herstellen: gorget, cap, gloves, sleeves, tunic, pants, boots.
	Es soll auch andere patterns geben, aber die erhält man wahrscheinlich nur in Questen und
	diese sind nicht für jederman zugänglich.
</P>

<P class="P1">
	Die patterns kosten im Regelfall 6sp und 3cp, macht für 7 patterns 4gp, 4sp und 1cp. Wenn
	man das Glück hat von Tieren medium qualitz pelts zu ergattern entfallen da die Kosten,
	ansonsten kosten sie im Schnitt vom SC, je nach Gnadenstufe &lt;g&gt; ca. 3-5 gp. Macht
	für 7 Teile 2pp 1gp bis 3pp 5gp.
</P>

<P class="P1">
	Und jetzt kommen wir zum spaßigen Teil. Umgerechnet braucht man für diese 7 Rüstungsteile
	25 studs. Dies kosten nach Gnadenstufe zwischen 9 gp und 1pp. Macht also umgerechnet
	zwischen 22pp 5gp und 25pp.
</P>

<P class="P1"> 
	Alles in allem zahlt man also allein um eine Studded leather armor herzustellen zwischen
	22 pp 9gp 4sp 1cp (wenn man die pelts hat und der SC gnädig sie studs für 9 gp verkauft)
	und 28pp 9gp 4sp 1cp (wenn man alles kauft und das zum hier höchst genannten Preis).
</P>

<P class="P1">
	Wenn man jetzt auch nicht failed und alles glatt läuft, dann kostet es "nur" den oben
	genannten Preis. Sollte man aber failen (gerade bei der Tunic), dann sind 5 pp allein
	für die studs futsch.
</P>

<P class="P1">
	Wer würde es verübeln, wenn man also für eine komplette Studded Leather Armor 30 pp verlangt?
	Niemand!
</P>
 
<P class="P1">
	Aber diese Frage stellt sich erst gar nicht, weil die Mitspieler sich nie eine Studded
	Leather armor zulegen würden, weil eine Normale Leather Armor, die genau soviel bringt wie
	Studded Leather, was AC angeht. Hier hat also der Tailoring Skill meiner Meinug nach
	sein dead end erreicht und den Sinn von Zusammenspiel verloren. Man muß ein Haufen
	Geld ausgeben, um den Skill über 35 zu bekommen, um studded ohne große Verluste
	herzustellen, die im nachhinein niemand mehr haben will. Wann wird nie in den Genuß
	kommen Reinforced Leather herzustellen, weil man den Skill Tailoring ab Skill 35 nicht
	mehr finanzieren will.
</P>

<P class="P1">
	Sollte man irgenwann genug Geld haben, wird man bereits nicht mehr auf den Skill angewiesen
	sein. Demnach ist Tailoring kein rentabler Skill.
</P>

<P class="P1">
	Ich bin kein Powergamer, daß möchte ichhier inaller deutlichkeit sagen, es geht nur darum,
	daß jeder meistens, den Weg des geringsten Widerstandes geht und sich hier eine normale
	Leather Armor kauft als eine studded leather.
</P>
 
<P class="P1">
	Ich fände es gerechtfertigt, wenn die Studded Leather Armor etwas mehr AC bringen würde
	als Normal Leather Armor. Inwieweit es realisierbar ist weiß ich nicht, aber unter
	solchen Umständen wird der Skill Tailoring aussterben. Ringmail Armor ist doch auch besser
	als Chainmail und kostet weniger. Warum geht es nicht bei Studded Leather?
</P>

<P class="P1"> 
	So, dies war eine lange Geschichte. Ich hoffe ihr alle, insbesondere Schneider, versteht
	das Problem. Mir geht es nicht darum viel Geld zu machen. Man kann "handgemachte" Sachen
	nicht mit Gewinn an NPCs verkaufen, und so soll es auch bleiben, meiner Meinung nach.
	Es geht mit nur darum, ein wenig mehr "Gerechtigkeit" den Tailorn zukommen zu lassen,
	die sich mit Mühe und Not und unter Geldverlusten ihren Skill weiter ausbilden müssen,
	um anschliessend ihre Studded Armor zu vernichten, weil sie sonst zuviel Platz auf der
	Bank wegnimmt. Dies ist meiner Meinung nach nicht Sinn und Zweck des Spiels.
</P>
 
<P class="P1">
	Es gibt ja die Möglichkeit Petitions und Feedbacks und Bug Reports zu schreiben. Nutzt diese
	Möglichkeiten!! Es ist sehr wichtig, daß die GMs und Organisatoren des Spiels mitbekommen,
	welche Probleme wir haben. EQ ist immer noch nicht aus den Kinderschuhen entwachsen und es gibt
	immer noch große und kleine Probleme. Diese Feedbackfeatures ermöglichen uns aktiv am Bild des
	Spiels mitzuarbeiten. Bitte helft uns Tailors indem ihr ein kurzes Feedback and die Orga
	schickt. Helft, daß studded Armor auch studded ist und keine Abklatsch,
	interactionszerstörender, normaler Leather armor.
</P>
 
<P class="P1">
	Es wäre sehr nett, wenn diese Mail bei euch auf der HP landen würde. Ich werde sie nochmal
	unter den Foren veröffentlichen, aber ich denke auf der Newsseite würde sie für einiges
	Aufsehen sorgen.
</P>
 
<P class="P0">
	<BR>
	Danke für euer Verständnis,
</P>
 
<P class="P0">
	<I>euer Nadyawin, frustrierter Woodelftailor</I>
</P>

