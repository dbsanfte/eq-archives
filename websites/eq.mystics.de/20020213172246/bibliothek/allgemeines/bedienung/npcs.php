<P CLASS="P5">Handel</P>

<P CLASS="P1">
	Um einen NPC anzuvisieren, muß man diesen anklicken. Wenn der NPC ein Händler ist, 
	erscheint nach einem Doppelklick das Handelsmenü, ist er ein Guildmaster, so wird das 
	Trainingsmenü angezeigt. Im Handelsmenü kann man Gegenstände, die der Händler im Menü 
	auf der linken Seite anbietet, kaufen. Natürlich geht das nur, wenn man genügend Geld und 
	Platz im Inventory hat. Um einen Gegenstand zu erwerben, muß man diesen anklicken und auf 
	den 'Purchase'-Knopf klicken, der unter dem Menü zu finden ist.
</P>

<P CLASS="P1">
	Der Preis zu dem die Händler ihre Waren anbieten, ist von vielen Faktoren beeinflußt: 
	dem Faction Standing/Reputation, dem Charisma und von dem, wie sich die Rasse des Händlers 
	allgemein gegenüber der Rasse eures Chars verhällt. Einem Dunkelelf zum Beispiel wird es 
	möglich sein, bei einem Menschen einzukaufen, die Preise werde aber extrem hoch sein, da 
	Menschen den Dunkelelfen überhaupt nichts abgewinnen können.
</P>

<P CLASS="P1">
	Das Verkaufen läuft in gleicher Weise ab, man klickt den Gegenstand an, den man verkaufen 
	möchte und klickt auf den 'Sell'-Knopf unter dem Menü, wenn einem der Preis passt, den man 
	angeboten bekommt. 
</P>

<P CLASS="P5">Charakterinformationen</P>

<P CLASS="P1">
	Doppelklickt man einen NPC oder auch ein Monster, so bekommt man einen allgemeine 
	Einschätzung seines Levels verglichen mit der eigenen als Info angezeigt. Ist es ein 
	NPC, könnte dieser Informationen für einen haben oder Teil eines Quests sein. Man sollte 
	einfach versuchen mit ihm per /say-befehl zu kommunizieren und sehen, was er anbietet. 
	Handelt es sich hingegen um ein Monster oder Tier, kann man sich auch in einiger Entfernung 
	halten und es abschätzen. Dadurch hat man die Möglichkeit, vorher zu entscheiden, ob man 
	sich mit ihm einlassen will oder es lieber sein läßt.
</P>

<P CLASS="P1">
	Der darauffolgende Text gibt Auskunft über die Gefahr des Monsters bezüglich des Players. 
	Die Farbe des Textes gibt dabei die Menge der Erfahrungspunkte wieder die man durch das 
	Töten des Monsters bekommen würde. Ein grüner Text würde dann zum Beispiel bedeuten, daß 
	man keinerlei Erfahrungspunkte bekommen würde, weil das Töten des Monsters zu einfach für 
	den Charakter ist.
</P>

