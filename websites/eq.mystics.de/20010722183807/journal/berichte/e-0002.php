<P CLASS="P0" ALIGN="CENTER">
	<B>Autor:</B> Mumpitz<BR>
	<BR>
</P>

<P CLASS="P1">
	Was ist das faszinierende an einer Online Welt? Bevor es Online-Games gab, existierten bereits
	etliche Vorreiter, die bewiesen, daß eine fiktive Realität einen Mordspaß machen kann.
	Pen&amp;Paper-Rollenspiel, Live-Rollenspiel oder sogar ein Besuch im Laserdrom oder das
	Räuber-und-Gendarme-Spiel.
</P>

<P CLASS="P1">
	Alle diese Dinge haben eins gemeinsam: Es macht einen Heidenspaß und man stellt sich der
	Intelligenz von echten Menschen. Ob man nun Räuber-und-Gendarme in Jeanshosen oder
	Live-Rollenspiel in echten, sauteuren Kettenhemden und in echten Burgen spielt. Ob man
	seinen Magier nur in der Fantasie oder auf Papier hat, oder ihn "real" mit Hut und
	Zauberstab auf dem Bildschirm sehen kann.
</P>

<P CLASS="P1">
	Jedes dieser Games wird individuell von
	jedem anders gespielt. Und meistens hat der Eine kaum Verständnis für die Spielweise des
	Anderen. Die einen brauchen Hack´n Slay mit intelligenten Gegnern. Die anderen gehen dem
	Kampf aus dem Weg und haben mit "Handeln &amp; Labern" auch ihren Spaß. Wie oft bin ich
	auf verständnisloses Kopfschütteln gestoßen, wenn ich einem Kämpfer klarzumachen
	versuchte, daß mir die "Handel&amp;Laber"-Interaktion Spaß macht. Wie oft muß ich aber
	hören, daß Leute, die noch nie etwas mit Live-Rollenspiel zu tun hatten, dieses als
	Live-Quake bezeichnen - weit gefehlt!
<P>

<P CLASS="P0">
	Die Mischung machts!
</P>
	
<P CLASS="P1">	
	Vielleicht rede ich Euch etwas zu viel vom Live, aber ich denke, daß EverQuest und Ultima
	Online eher mit LARP verwand sind, als mit den herkömmlichen solo-PC-Games. Die ersten
	SAGA-Lives, die wir veranstalteten, waren so, wie jetzt EverQuest. Es gab Horden von
	NPC-Monstern, NPCs, die die Spieler auf Quests in den Wald oder ins Dungeon schickten.
</P>

<P CLASS="P1">
	Jedes Monster wird komischerweise sofort als NPC erkannt - als trügen sie ein Schild um
	den Hals: "Monster! Killt mich!". Und das ist auch bei EQ und UO so. Es macht einen
	wahnsinnigen Aufwand, alle Spieler zu beschäftigen und es ist fast unmöglich, die Monster
	noch intelligent erscheinen zu lassen. Die Spieler stumpfen ab, warten auf neue Monster und
	beschweren sich, daß davon viel zu wenige sind und diese keine Herausforderung mehr
	darstellen. Kill-Steals sind die Folge und Adrenalin ist bald ein Fremdwort. Adrenalin
	kam nur noch dann, wenn ein PK hinter Euch her war - und dann nicht zu knapp. Genau so
	ist's doch auch bei UO gewesen, oder?
</P>

<P CLASS="P1">
	Wenn Ihr ein multi-player-game spielen wollt, erwartet Ihr doch auch die Interaktion mit
	intelligenten, echten Personen, oder? Um dies zu erreichen, haben wir folgendes getan:
</P>

<UL CLASS="P1">
	<LI>
		1. Wir haben unser Budget in das Ambiente gesteckt. "Handel&Laber"-Charaktere bekamen die
		Chance, sich in Burggewölben breitzumachen. Echte geprägte Münzen waren im Umlauf und
		sorgten für echte Interaktion mit echten Spielern. Es wurde geklaut, überfallen, beschützt,
		gekämpft, sich gegenseitig beschissen, angeklagt und hingerichtet... Diesbezüglich hat
		UO (noch) die Nase vorn.<BR><BR>
	</LI>
	<LI>
		2. Wir tauschten 80 NPC-Monster (mit kill-mich Schild um den Hals) gegen 10 unerkannte NPCs.
		Der Hauptmann der Garde wurde von allen als Spieler gehalten. Man streue einfach ein paar
		Schneebälle unter´s Volk und wartet, daß daraus Lawinen entstehen. Ein paar ungerechte
		Festnahmen und siehe da, die Spieler warten nicht mehr auf dumme Monsterhorden, sondern
		überlegen sich, wie sie ihren Freund befreien, den (intelligenten) Hauptmann überlisten
		oder ob sie eventuell gar die Barbaren, die vor der Burg ihr Lager haben, dazu bringen,
		die Burg anzugreifen um in der Hitze des Gefechts den Gefangenen aus dem Kerker zu holen.
		Oder die Suche nach dem obligatorischen magischen Schwert (oder was auch immer): Man
		kann es verstecken und Quests und Schatzkarten unter's Volk streuen. 250 Leute suchen dann
		1 Schwert L. Bewacht wird es dann womöglich von 20 stumpfsinnigen "schlag-mich-tot"-Skeletten.
		Oder man kann es anders machen. Das Schwert besitzt ein Spieler (ein geheimer, nicht als
		solcher zu erkennender NPC). Diesen zu überlisten kann eine größere Herausforderung sein,
		als sich mit 20 NPC-Monstern zu messen. Was brauche ich publik-Quests, wenn ich auch mit
		echten intelligenten Spielern interagieren kann? Und diese Interaktion muß nicht immer
		zwangsläufig mit dem Schwert ausgetragen werden.<BR>
	</LI>
</UL>

<P CLASS="P1">
	Bei UO gab es die Möglichkeit, sich diese selfmade-Quests selbst zu kreieren. Aber das war's
	dann auch. Wer das nicht tat, blieb auf der Strecke. Wenn Ihr nur Computer-generated-Quests
	wollt, wartet lieber auf Ultima 9 - das wird billiger.
</P>

<P CLASS="P1">
	Ich befürworte Aktionen wie Mystics-Roleplay. Echtes Rollenspiel anstatt
	Computer-generated-Quests.
</P>

<P CLASS="P1">
	Schade, daß es noch so wenig Möglichkeiten für die "Handel&amp;Laber"-Charaktere gibt. Ich
	würde gerne eine Schmiede oder eine Taverne eröffnen, mich dort mit den Kunden unterhalten
	oder mich mit den Halunken herumschlagen oder dafür reale Söldner anheuern. Ich würde
	gerne einen Händler spielen, der sein Geld mit Handelskarawanen verdient. Ore von
	Grobb nach Kaladim oder so. Ich würde bei meinen Reisen gerne von echten Wegelagerern
	überfallen werden, anstatt nur den blöden Monstern aus dem Weg gehen zu müssen. Ich würde
	anschließend gerne mit einer Heerschar von Söldnern die Räuber jagen. Ich würde gerne mein
	Leben lassen um meine Heimatstadt vor anderen "bösen" Spielern zu verteidigen.
</P>

<P CLASS="P1">
	Leider steht die jetzige PvP-Regelung da ein bisschen im Weg. Und die Trading- und
	Hausbaumöglichkeiten könnten auch noch etwas ausgereifter werden. Wie soll ich auf Dauer
	Spaß am Ore-Import haben, wenn ich jedes mal nur 8 ore transportieren kann? Wenn ich
	(außer hohen online-Kosten) auf der langen Reise nur ein paar dumme Monster befürchten
	muß?
</P>

<P CLASS="P1">
	Interaktion zwischen +PvP und -PvP wird keine Konfrontation sein, welche zu Rollenspiel
	zwingt. Da renne ich als -PvP durch die Gegend und verhöhne jeden Dunkelelfen, der mir
	über den Weg läuft. Da könnt ich gleich in den Zoo gehen und die bösen, bösen Krokodile
	hinter dem Gitter mit (verbalen) Kieselsteinen bewerfen. Vielleicht macht´s ja ne Weile
	Spaß. Vielleicht fauchen die Krokodile mich ja auch an - da hab ich dann aber ganz schön
	was erlebt. Anschließend kommt dann ein Zoo-Mitarbeiter im Krokodilkostüm an, trägt
	ein "NPC-Schild" um den Hals und jeder Besucher darf einmal draufhauen. Ich könnte
	auch in den Käfig gehen, da würd ich mit den PK-Krokodilen mehr erleben. Aber irgendwie
	ist das auch nicht das Wahre. Da werd ich von allen Zoobesuchern außerhalb des Käfigs
	angeguckt, als sei ich selbst eine Krokodil.
</P>

<P CLASS="P1">
	So, wie UO den Newbieschutz vernachlässigt, wird es bei EQ auf Kosten des Rollenspiels übertrieben.
	Bitte wählen Sie:
</P>

<UL CLASS="P0">
	<LI>Blümchen-Modus</LI>
	<LI>Freiwild-Modus</LI>
</UL>

<P CLASS="P1">
	Auch wenn so mancher das nicht wahrhaben will: Bei EQ fehlen viele wichtige Features, welche
	das Rollenspiel fossieren würden. Entweder durch "Weglassen" bei etlichen Trading-Skills,
	Hausbaumöglichkeiten etc. oder durch die Rollenspiel-tötende PvP-Regelung, welche zugunsten
	des Newbie-Schutzes und als anti-just-for-fun-killing Regelung ersponnen (und meiner
	Meinung nach übertrieben) wurde. Newbieschutz beim Rollenspiel wär besser gewesen als
	Newbiesschutz statt Rollenspiel. Naja, ziemlich hart gesagt, aber ich denke, EQ hat doch
	schon mehr zu bieten als UO. Warten wir noch ne Weile und ein paar Patches ab. Das
	Rollenspiel wird bestimmt nicht auf der Strecke bleiben - es wird nur anders sein als bei
	UO. Viele, die bei UO das Rollenspiel vermißt oder gar nicht gefunden haben, weil man es
	sich selbst machen mußte (und konnte!), werden aber jetzt schon bei EQ das finden, wonach
	sie bei UO immer vergeblich gesucht haben.
</P>

<P CLASS="P1">
	Mystics-Roleplay geht in die richtige Richtung. Rollenspiel, intelligente Quests und
	Interaktion. Ich träume von dem Köttel im Hintern, der vor Angst schon 10 cm herausguckt! *g*
</P>

<P CLASS="P0">
	<BR>
	<I>Mumpitz</I>
</P>

