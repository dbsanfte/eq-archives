    <TD>&nbsp;</TD>
    <TD VALIGN="TOP" NOWRAP><FONT CLASS="P2">/pet as you were</FONT></TD>
    <TD>&nbsp;</TD>
    <TD VALIGN="TOP"><FONT CLASS="P0">Dieses Kommando setzt alle vorherigen Befehle ausser Kraft.</FONT></TD>
	</TR>
	<TR>
    <TD>&nbsp;</TD>
    <TD VALIGN="TOP" NOWRAP><FONT CLASS="P2">/pet attack</FONT></TD>
    <TD>&nbsp;</TD>
    <TD VALIGN="TOP"><FONT CLASS="P0">Die Erklärung erübrigt sich fast; das Pet attackiert euer 
			anvisiertes Ziel. Vorsicht, ist das Ziel zu stark für euer Pet, verweigert es den Befehl!</FONT></TD>
	</TR>
	<TR>
    <TD>&nbsp;</TD>
    <TD VALIGN="TOP" NOWRAP><FONT CLASS="P2">/pet back off</FONT></TD>
    <TD>&nbsp;</TD>
    <TD VALIGN="TOP"><FONT CLASS="P0">Damit wird die aktuelle Aktion abgebrochen und das Pet kehrt zu 
			euch zurück.</FONT></TD>
	</TR>
	<TR>
    <TD>&nbsp;</TD>
    <TD VALIGN="TOP" NOWRAP><FONT CLASS="P2">/pet follow me</FONT></TD>
    <TD>&nbsp;</TD>
    <TD VALIGN="TOP"><FONT CLASS="P0">Nach diesem Befehl folgt euch das Pet; werdet ihr angegriffen springt 
			es ein und attackiert den Feind.</FONT></TD>
	</TR>
	<TR>
    <TD>&nbsp;</TD>
    <TD VALIGN="TOP" NOWRAP><FONT CLASS="P2">/pet get lost</FONT></TD>
    <TD>&nbsp;</TD>
    <TD VALIGN="TOP"><FONT CLASS="P0">Das Pet begeht Selbstmord.</FONT></TD>
	</TR>
	<TR>
    <TD>&nbsp;</TD>
    <TD VALIGN="TOP" NOWRAP><FONT CLASS="P2">/pet guard here</FONT></TD>
    <TD>&nbsp;</TD>
    <TD VALIGN="TOP"><FONT CLASS="P0">Das Pet bewacht die aktuelle Position, d.h. alles und jeder, der in 
			seine Nähe kommt, wird angegriffen. Anschließend kehrt das Pet wieder auf diese Position zurück.</FONT></TD>
	</TR>
	<TR>
    <TD>&nbsp;</TD>
    <TD VALIGN="TOP" NOWRAP><FONT CLASS="P2">/pet guard me</FONT></TD>
    <TD>&nbsp;</TD>
    <TD VALIGN="TOP"><FONT CLASS="P0">Das Pet bewacht euch nun, d.h. alles und jeder, der in eure Nähe 
			kommt, wird angegriffen! <B>Vorsicht:</B> Kann zu einem Gemetzel ausarten, wenn Guards in der Nähe
			sind *g*</FONT></TD>
	</TR>
	<TR>
    <TD>&nbsp;</TD>
    <TD VALIGN="TOP" NOWRAP><FONT CLASS="P2">/pet health report</FONT></TD>
    <TD>&nbsp;</TD>
    <TD VALIGN="TOP"><FONT CLASS="P0">Der Gesundheitsstatus (HP's) des Pets wird im Display angezeigt.</FONT></TD>
	</TR>
	<TR>
    <TD>&nbsp;</TD>
    <TD VALIGN="TOP" NOWRAP><FONT CLASS="P2">/pet report health</FONT></TD>
    <TD>&nbsp;</TD>
    <TD VALIGN="TOP"><FONT CLASS="P0">Das Pet meldet den akt. Gesundheitsstatus (HP's)</FONT></TD>
	</TR>
	<TR>
    <TD>&nbsp;</TD>
    <TD VALIGN="TOP" NOWRAP><FONT CLASS="P2">/pet sit down</FONT></TD>
    <TD>&nbsp;</TD>
    <TD VALIGN="TOP"><FONT CLASS="P0">Das Pet setzt sich hin und regeneriert schneller.</FONT></TD>
	</TR>
	<TR>
    <TD>&nbsp;</TD>
    <TD VALIGN="TOP" NOWRAP><FONT CLASS="P2">/pet stand up</FONT></TD>
    <TD>&nbsp;</TD>
    <TD VALIGN="TOP"><FONT CLASS="P0">Damit steht das Pet nach dem Sit-down-Befehl wieder auf. Wenn ihr 
			keine weiteren Befehle gebt bleibt es an der Stelle stehen, also wieder <I>guard</I> oder
			<I>follow</I> befehlen!</FONT></TD>
	</TR>
	<TR>
    <TD>&nbsp;</TD>
    <TD VALIGN="TOP" NOWRAP><FONT CLASS="P2">/pet target</FONT></TD>
    <TD>&nbsp;</TD>
    <TD VALIGN="TOP"><FONT CLASS="P0">Visiert das Pet an (alternativ zur F1-Taste).</FONT></TD>
	</TR>
	<TR>
    <TD>&nbsp;</TD>
    <TD VALIGN="TOP" NOWRAP><FONT CLASS="P2">/pet taunt<BR>/pet no taunt</FONT></TD>
    <TD>&nbsp;</TD>
    <TD VALIGN="TOP"><FONT CLASS="P0">Damit kann das "Taunten" des Pets ein- bzw. abgeschaltet werden.</FONT></TD>
	</TR>
