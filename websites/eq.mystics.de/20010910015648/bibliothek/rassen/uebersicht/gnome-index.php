	<P CLASS="P1">
		Mancher hat schon gelacht, als er einmal einen Gnom traf, doch meistens blieb ihm das Lachen im Halse stecken. 
		Selten genug einmal bekommt man einen Gnomen zu Gesicht, verbringen sie doch die meisten ihr Leben unter der
		Erde, in ihrer Stadt Ak´Anon. Ihr äußeres mag zum lachen reizen, besitzen Gnome doch eine bräunliche Haut, 
		knorrig wie die Rinde einer Eiche, und nur spärliches, meist weißes Haar. Auch ihre geringe körperliche Größe, 
		trägt nicht dazu bei den Respekt vor ihnen zu vergrößern. Doch wenn man einmal einen Gnomischen Magier belächelt 
		oder Witze über einen Nekromanten aus diesem Volke gemacht hat, wird man diese Rasse nie wieder unterschätzen. 
