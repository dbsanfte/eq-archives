<P CLASS="P0" ALIGN="CENTER">
	<BR>
	<B>Autor:</B> Mumpitz
</P>

<P CLASS="P1">
	Lest Euch einfach jeden Punkt einzeln für sich allein durch und denkt Euch ein
	"kann ich zustimmen" oder "nee, mein ich nicht" dazu:
</P>

<UL CLASS="P0">
	<LI>Im Mittelalter war es auch nicht ungefährlich, von A nach B zu kommen. Es lauerten einem dort nicht nur dumme NPC-Wölfe, sondern auch intelligente Räuber auf - jedem!</LI>
	<LI>Abgesehen davon soll jedoch Everquest ein Spiel und keine Simulation sein. aber weiter:</LI>
	<LI>Räuber wurden hart bestraft.</LI>
	<LI>Mörder noch viel härter als Diebe.</LI>
	<LI>Je tiefer man in die Wildnis kam, desto gefährlicher. Je berüchtigter ein Halunke war, desto mehr mußte er sich verstecken - von der Zivilisation entfernen.</LI>
	<LI>Wer sich mit Halunken abgab, galt schon fast selbst als einer.</LI>
	<LI>Newbies brauchen ungefährliche Zonen.</LI>
	<LI>Diese Zonen dürfen nicht abrupt an den Stadttoren enden.</LI>
	<LI>Wenn ein Opfer überfallen wird, sollte dieses nicht zusätzlich zum Verlust seines Lebens mit Erfahrungs-Verlust bestraft werden!!!</LI>
	<LI>Einen Newbie zu töten, macht keinen Sinn, ein Newbie bringt keine Erfahrung, hat kein Geld...pures Lust-Killing ist also das, was hauptsächlich verhindert werden muß.</LI>
	<LI>(arme) Newbies müssen für Räuber uninteressant sein.</LI>
	<LI>Mörder (und auch kleineres Gesindel) sollten bestraft werden - als vogelfrei gelten.</LI>
	<LI>Jeder, auch ein aufrichtiger Bürger, durfte Im Mittelalter einem Vogelfreien eine Lektion erteilen ohne selbst kriminell zu werden.</LI>
	<LI>Wird ein Mörder gefaßt (oder besiegt) muß die Strafe hart ausfallen. Die Strafe muß dem Mörder RICHTIG weh tun.</LI>
	<LI>Kopf ab entspricht Accout weg....also eine andere harte Strafe.</LI>
	<LI>Das Opfer leben zu lassen, muß für einen Räuber interessanter sein, als es zu töten. Wenn er hat, was er will, warum noch einen Mord begehen, der hart bestraft wird?</LI>
	<Li>Das Töten eines Unschuldigen darf auf keinem Fall Erfahrungspunkte bringen... vielleicht sogar im Gegenteil....</LI>
	<LI>Das Töten eines Mörders muß Erfahrung, Ruhm, Ehre, Gold usw. bedeuten.</LI>
	<LI>Das "just-for-fun-Pking" muß unterbunden werden.</LI>
	<LI>Würden PKs Rollenspiel betreiben, könnte man sie eher Akzeptieren.</LI>
</UL>

<P CLASS="P0">
	Folgende Ideen könnten das PKing gerechter regulieren:
</P>

<UL CLASS="P0">
	<LI>Es gibt mehrere Abstufungen von Guard-Protekt-Zones. Je mehr ein Halunke auf dem Buckel hat, desto weiter muß er sich von der Zivilisation entfernen. Nur erfahrene Wanderer wagen sich in den düstersten aller Wälder. Newbies brauchen cleanes Gebiet auch außerhalb der Stadt.</LI>
	<LI>Ein Mord darf dem Räuber nicht leicht fallen. Ein schlechter Ruf, weniger Freunde, die sich mit einem abgeben wollen (weil sie sonst selber in der Gesellschaft abrutschen). Das Opfer leben zu lassen, muß für den Räuber eine akzeptable Variante darstellen. Durch harte Strafen wird sich ein PK zwei mal überlegen müssen, ob er einen Newbie tötet, der eh nichts besitzt. "Ach-ist-mir-langweilig-Kills" wird es nicht mehr geben dürfen.</LI>
	<LI>Im Mittelalter war es die Todesstrafe, was im ORPG einem gelöschten Account gleichkommt. Das ist nicht machbar. Es muß eine andere HARTE Strafe gefunden werden. Wie wär´s mit einer 1/4 Todesstrafe? (Ein viertel seines Lebens...entspricht das nicht 1/4 seiner Erfahrung und Leveln?....je nach Delikt könnte dies variabel gestaltet werden)...oder 1/10 oder 1/100 oder 1/2 oder pauschal ein Level.......ist jetzt auch egal.</LI>
	<LI>Das Fangen oder Töten eines Mörders muß richtig Erfahrung, Ruhm und Geld bedeuten.</LI>
	<LI>Töten sich die Gesetzeslosen untereinander.....sollen sie doch.</LI>
	<LI>Geld-oder-Leben Tading Fenster können einem Opfer die Möglichkeit geben, noch mal mit ´nem blauen Auge davongekommen zu sein.</LI>
</UL>

<P CLASS="P0">
	Wie könnte das Sony jetzt noch einbauen?
</P>

<UL CLASS="P0">
	<LI>variable Guard Protekt zones sind wohl eher weniger möglich. Vielleicht ein paar in der stadtnahen Wildnis herumlaufende Guards.</LI>
	<LI>Gemixte +PvP und -PvP Parties gibt es nicht. Wer sich mit Räubern abgibt, gilt vor dem Gesetz selbst als einer.</LI>
	<LI>+PvPs dürfen auch -PvPs angreifen. Solch ein Mord jedoch wird mit schlechtem Ruf (worauf die Guards reagieren) und Verlust von Erfahrung bestraft.</LI>
	<LI>-PvP gegen -PvP geht nicht, oder der Angreifer wird zum +PvP.</LI>
	<LI>-PvPs dürfen +PvPs angreifen und erhalten Ruhm, Erfahrung und Gold. (Oh, dann werden die PKs ja Freiwild! Hehe...natürlich....sie sind ja vogelfrei.)</LI>
	<LI>+PvPs dürfen von allen gelootet werden.</LI>
	<LI>-PvPs dürfen nur von -PvPs der eigenen Party gelootet werden - außer:</LI>
	<LI>Nur der Mörder persönlich darf ein -PvP Opfer looten...mit entsprechenden Strafen für ihn selbst. Es wird verhindert, daß PK-Gruppen jeweils einen looting-chr mitnehmen, um sich vor diesen Strafen zu schützen.</LI>
	<LI>Zu einer "Geld-oder-Leben" Verhandlung ist kaum Zeit. Deshalb wird ein -PvP nicht durch einen +PvP sterben - nur bewußtlos und bewegungsunfähig. Es wäre ein weiterer Hieb - ein Todesstoß - notwendig (mit "are you sure?" Abfrage). Der +PvP öffnet das "Geld-oder-Leben" Fenster. Das Opfer kann Geld (oder Gegenstände) anbieten. Wird von beiden der Deal akzeptiert, so bekommt das Opfer für eine Fluchtzeit Immunität. Diese Immunität muß allgemein gegen alle +PvPs gelten, da sonst der nächste Räuberhalunke gleich nachsetzt. Die Immunität kann z.B. so lange dauern, bis daß das erste mal wieder eine Stadt betreten wird. So ist auch geregelt, daß man nicht von einem Überfall in den nächsten stolpert. Ferner bleibt beim Geld-oder-Leben-Deal für das Opfer die Möglichkeit, zu pokern: "Ich hab eh nix...für 10 Kupfer setzt der nicht 10.000 Erfahrungspunkte aufs Spiel". Das ist realistisch, denn wer überfällt schon eine Bank, die 100 DM Wechselgeld hat - Fort Knox ist interessanter - und gefählicher. Bei Ablehnung des "Geld-oder-Leben" deals darf der Räuber den Todesstoß ausüben, welcher ihm natürlich wegen der exp-Strafe nicht leicht fällt....das muß schon ein lohnendes Opfer sein (und das ist nicht leicht zu besiegen). Ein stinkendreicher, schwächlicher Händler sollte sich lieber nicht ohne Begleitung auf lange Handelsreisen begeben.</LI>
	<LI>Eine weitere (optionale) Idee zu den Exp-Strafen. Wie wärs, wenn eine Straftat eine bestimmte Menge Erfahrung kostet. Z.B. 1000 Punkte (keine Ahnung, ob das viel oder wenig ist in EQ) oder z.B. ein Level? Diese Punkte werden nicht sofort abgezogen. Sie werden auf einem "Konto" vermerkt. So sammelt sich von Straftat zu Straftat eine immer höhere Strafe an. Wird er geschnappt (getötet), wird eine Unsumme an Exp bzw. Leveln abgezogen! Eventuell sollte diese Erfahrung oder ein Teil davon dann seinem Buonty-Hunter (bzw. der bounty-hunter-party) zuteil werden (nur wenn diese -PvP sind). Je mehr ein Halunke auf dem Gewissen hat, um so mehr geht ihm die Muffe! Jetzt hätten wir folgendes: Einem kleinen Räuber wurde die Hand abgehackt...sprich - sein Leben war zum Teil im Eimer und nach ner Zeit lernt er wieder damit umzugehen. Einem Massenmörder wurde der Kopf abgeschlagen - das Leben war im Arsch :).</LI>
	<LI>Die Höhe der Strafe könnte auch variieren. Bezugsgröße für die Höhe der Strafe könnte die Differenz der Erfahrung des Angreifes und des Opfers sein - mindestens jedoch einen Level. Dies schützt zusätzlich Newbies - nix zu holen und hohe Strafen.</LI>
</UL>

<P CLASS="P0">
	Wird das Spiel dann so aussehen?
</P>

<UL CLASS="P0">
	<LI>+PvPs untereinander agieren so, wie Sony es mit dem Switch geplant hat</LI>
	<LI>-PvPs untereinander ebenso</LI>
	<LI>Newbies brauchen sich kaum zu fürchten.</LI>
	<LI>PKs lungern nicht unmittelbar vor den Stadttoren herum.</LI>
	<LI>-PvPs brauchen sich vor fast nichts zu fürchten, es sei denn, sie tragen wahre Schätze mit sich herum.</LI>
	<LI>+PvPs haben keine Narrenfreiheit gegenüber -PvPs mehr, die sich nicht trauen, den Schalter umzulegen.</LI>
	<LI>+PvPs sind gezwungen Rollenspiel zu betreiben. Mit möglichst wenigen Kills in der Welt berüchtigt zu werden um dann zu hoffen, daß die Opfer den Geld-oder-Leben-Deal eingehen und es nicht auf einen Kill ankommen lassen.</LI>
	<LI>Durch diese Regeln sind also hauptsächlich die PKs beschnitten - im Vergleich zu der puren Switch-Regel. Gefällt das den +PvPs nicht, können sie ja einfach auf + schalten und brauchen keinen -PvP anzugreifen. Dann bleibt für sie alles bei der alten sony-Regel.</LI>
</UL>

<P CLASS="P1">
	Nun, ein Schlußwort. Ich habe weder die Weisheit mit Löffeln gefressen, noch glaube ich, daß
	dies das non plus ultra ist. Auch möchte ich bestimmt Niemandem meine Meinung aufzwängen.
	Ebenso ist es recht unwahrscheinlich, daß Sony jetzt noch solch ein System implementieren wird.
<P>

<P CLASS="P1">
	Es beschäftigt mich nur, ob es möglich ist, ein PvP System zu erspinnen, welches den Schutz
	des Switches beibehält und dennoch die Narrenfreiheit einer Familie gegenüber der anderen
	aufhebt, welche der Switch in seiner jetzigen Funktion mit sich bringt.
<P>

<P CLASS="P1">
	Zumindest sollten Kontakte zwischen den zwei Gruppen möglich sein und eine gewisse
	(Haß-) Akzeptanz. Bei dem Switch, wie er jetzt ist und der Intoleranz vieler Spieler der
	anderen Gruppe gegenüber, könnte man gleich eine "Ignore all +PvP" oder "Ignore all -PvP"
	Funktion einbauen. Man bemerkt die jeweils andere Gruppe nur, wenn man sich über Lag auf
	dem Server ärgert.
<P>

<P CLASS="P0">
	...wir unterhalten uns hier über ungelegte Eier....guten Appetit!
<P>

<P CLASS="P0">
	<BR>
	<I>Mumpitz (-PvP)</I>
</P>
	
