<P CLASS="P1">
	Alchemy kann nur von Shamanen ab Level 25 gelernt werden. Im Gegensatz zu manch anderen Fertigkeiten gibt es bei Alchemy ein sogenanntes <I>"Skill Cap"</I> (= max. Wert eines Skills, den man in einem bestimmten Level erreichen kann).
</P>	

<P CLASS="P1">
	Der <I>"Skill Cap"</I> berechnet sich wie folgt:
</P>	

<P CLASS="P2" ALIGN="CENTER">
	(Akt.Level - 24) * 5
</P>	

<P CLASS="P1">
	Der max. Skillwert für Level 25 ist also dementsprechend 5, im Level 26 dann 10, etc.
</P>	

<P CLASS="P1">
	Da die einfachsten Tränke bereits einen Trivial-Wert von 18-21 haben, sollte man mit Alchemy warten bis Level 29 erreicht ist. Dann alle möglichen Skillpoints (21) beim jeweiligen Guild-Trainer investieren und es kann losgehen.
</P>	

<P CLASS="P1">
	Um einen Trank herzustellen benötigt man <b>Kräuter</b> (engl.: Herbs) und ein <b>Medicine Bag</b>. Die benötigten Kräuter werden in den <b>Medicine Bag</b> gegeben und durch Drücken auf <I>Combine</I> erhält man - bei einem erfolgreichen Versuch - einen Trank. Der Versuch gleich mehrere Tränke auf einmal zu machen schlägt garantiert fehl!
</P>	

