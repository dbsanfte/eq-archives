<P CLASS="P0" ALIGN="CENTER">
	<B>Autor: Taaris D`Arc </B><BR>
	<BR>
</P>

<P class="P0">
	Ich stelle mich kurz vor:<br>
</P>
 
<P class="P1">
	Auf Solusek Ro bin ich bekannt als Tarus, den es nicht mehr geben wird. Taaris D`Arc auf Brell Serilis ist sein Nachkomme. Ich bin Human/Paladin und habe mich seit Meridian59 mit ORPGs befasst aber niemals Hardcore gespielt.
</P>

<P class="P5">
	EQ RPG Kompitabel?
</P>
 
<P class="P1">
	Ich finde persönlich nicht, kein Häuserbau, keine Berufe, ein PvP-System. Alles verhindert das RPG. Ich meine, man kann gut als mürrischer Zwerg oder als hochgestellter High Elf durch die Welt ziehen, aber das ist schon alles. Wo bleiben z.b. die Realitäten? Dark Elf Shadow Knight mit einem High Elf Paladin in einer Gruppe oder Gilde? Ich meine das findet man jeden Tag. Also, die meisten Spieler spielen kein RPG mehr!!<br>
	Persönlich würde ich so gerne RPG spielen aber mein Gildenboss ist auch schon ein Necromant :)
</P>

<P class="P5">
EQ RPG Realismus?
</P>
 
<P class="P1">
	Jein, um es klar zu sagen SH und Paladin passen nicht in eine Gruppe oder Gilde zusammen. Lustigerweise findet man das überall, sogar bei mir. Um den RPG Realismus zu steigern sollte man auf allen Servern die PvP Systeme lassen. Wie wollen Dark Elfs gegen High Elfs fighten ohne +PvP zu sein? Ich persönlich würde einen Schutz bis lvl 20 vorschlagen, wie es weiter aussieht weiss ich auch nicht. Vielleicht das man nur alle töten kann die 1 lvl unter einem sind aber dafür alle oberen lvler? Oder der Ermordete gibt nur ExP und Gegenstände ab wenn er nicht überraschend gekillt wurde. Ich weiss es selber nicht. Dazu müsste man ein ausgetüfteltes System erfinden, aber nicht das  von 989 Studios.
</P>

<P class="P2">
	Berufe?
</P>
 
<P class="P1">
	Es wäre doch schön als Händler oder Architekt durch die Welt zu ziehen. Karawanen in den Wüsten, Überfälle von Banditen. ÜBERFALL auch so ein RPG Thema?
</P>

<P class="P2">
	Überfall?
</P>

<P class="P1">
	Mal ein Orc Überfall auf Kelethin wäre doch toll nicht? Oder das Dunkelelfen NPCs Freeport angreifen oder halt was spannendes z.b. Drache greift Halas an oder halt sonst was? TEAMWORK-Thema kommt hervor. Vielleicht nicht zum RPG aber zum Thema "War-Grouping, Teamwork" würde es passen zu dem ich mich auch äussern werde.
</P>

<P class="P5"> 
	Fazit zu RPG bei EQ
</P>

<P class="P1">
	Um es klar zu sagen, die "Ruins of Kunark"  werden entscheiden. Sollte es sich bessern (Natürlich mit der Zufriedenheit des Kunden, versteht sich doch J) nehme ich die verbesserten Punkte zurück. Ansonsten melde ich mich wieder...
</P>

<P class="P1">
	Bis dann (EQ macht auch so sehr viel Fun).
</P>
 
<P class="P1">
	May by the Force of Kranaan with us.
</P>

<P class="P0">
	<I>Taaris D`Arc Paladin / Human</I>
</P>

