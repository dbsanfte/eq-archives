<P CLASS="P2">
	2.1 Gibt es neue Rüstungsdarstellungen für alle Rassen, inklusive Iksar ?
</P>	
<P CLASS="P1">
	Es gibt tatsächlich sechs neue Armor-Texturen für jede Rasse und Geschlecht. Also sind es eigentlich 12 neue Texturen pro Rasse, je 6 für das weibliche und 6 für das männliche Geschlecht.
</P>

<P CLASS="P2">
	2.2 Gibt es neue Fertigkeiten (Skills) mit dem Velious-Update ?
</P>	
<P CLASS="P1">
	Es gibt keine neuen Skills, aber die bestehenden wurden stark erweitert.
</P>

<P CLASS="P2">
	2.3 Können Spieler, die Kunark nicht besitzen, durch den Kauf von Velious höher als Level 50 werden ?
</P>	
<P CLASS="P1">
	Ja.
</P>

<P CLASS="P2">
	2.4 Wenn ich Velious nicht besitze, kann ich trotzdem die Items von dort kaufen und benutzen ?
</P>	
<P CLASS="P1">
	Ja, ausgenommen Items die z.B. "NoDrop" sind.
</P>

<P CLASS="P2">
	2.5 Es gibt Gerüchte, daß die Levelhöchstgrenze nun auf Level 70 erhöht wurde, stimmt das ?
</P>	
<P CLASS="P1">
	Nein, das ist wirklich nur ein Gerücht. Es gibt auch keine Pläne dies in der nächsten Zukunft zu ändern.
</P>

<P CLASS="P2">
	2.6 Ich habe gehört, daß die neuen Spells, die mit Velious eingeführt wurden nur in Velious Zonen funktionieren; das kann z.B. bei den Teleport-Spells ja nicht stimmen, oder ?
</P>	
<P CLASS="P1">
	Mit den Spells ist es immer so eine Sache. Grundsätzlich muß daß jeder selbst herausfinden, aber grundsätzlich sollten die Spells auch ausserhalb von Velious einsetzbar sein.
</P>

