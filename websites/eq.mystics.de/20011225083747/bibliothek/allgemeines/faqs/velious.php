<P CLASS="P2">
	1.1 Stimmt es, daß die Velious-Zonen linear aneinandergereit sind, und braucht man bestimmte Schlüssel oder muß man Quests erfüllen um von einer Zone zur nächsten zu gelangen ?
</P>	
<P CLASS="P1">
	Nein, die neuen Zonen sind nicht linear, aber durch Erforschen kann man einige spezielle Bereiche entdecken, die man als linear aufgebaut bezeichnen könnte.
</P>

<P CLASS="P2">
	1.2 Wieviele Zonen gibt es in Velious ?
</P>	
<P CLASS="P1">
	Zum momentanen Zeitpunkt enthält der neue Kontinent 19 Zonen.
</P>

<P CLASS="P2">
	1.3 Es wird gemunkelt, daß Barbaren eine neue Start-Stadt erhalten, stimmt das ?
</P>	
<P CLASS="P1">
	Nein. Die neuen Zonen sind alle für Level 30-60 ausgelegt, somit gäbe es keine Jagdplätze für Newbies.
</P>

<P CLASS="P2">
	1.4 Wenn der neue Kontinent nur für Level 30 und höher ausgelegt ist, weshalb sollten Low-Level-Spieler die Erweiterung kaufen ?
</P>	
<P CLASS="P1">
	Obwohl der Kontinent nicht für Low-Level-Charaktere ausgelegt ist, gibt es keinerlei Beschränkungen um ihn zu betreten.
</P>

<P CLASS="P2">
	1.5 Gibt es irgendwelche Erweiterungen in Bezug auf das Wetter, wie Hagel, Sturm, Tornados, etc. ?
</P>	
<P CLASS="P1">
	Nein.
</P>

<P CLASS="P2">
	1.6 Wie schauen die neuen Planes in Velious aus und welche Götter sind dort ?
</P>	
<P CLASS="P1">
	Die Planes of Growth und Mischief, mit deren Göttern Tunare und Bristlebane sind für die Abenteurer Norraths erreichbar. Was in den Planes alles zu finden ist, gilt es für die Spieler selbst zu erforschen.
</P>

<P CLASS="P2">
	1.7 Gibts es Banken und Händler für alle Rassen in Velious ?
</P>	
<P CLASS="P1">
	Ja, aber nicht vergessen: Die Faction hat, wie überall sonst auch, natürlich einen großen Einfluß.
</P>

<P CLASS="P2">
	1.8 Wie kommt man nach Velious und wieder zurück ?
</P>	
<P CLASS="P1">
	Das Schiff "Icebreaker" wird die Bewohner Norraths von und nach Velious transportieren. Es ist momentan das einzige Schiff für diese Reise. Natürlich werden bestimmte Abenteurer auch andere Methoden für diese Reise entdecken.
</P>

<P CLASS="P2">
	1.9 Wo dockt das Schiff "Icebreaker" an ?
</P>	
<P CLASS="P1">
	Das Dock befindet sich in der "Northern Desert of Ro".
</P>

<P CLASS="P2">
	1.10 Stimmt es, daß die Faction des "Ring of Scale" auch für Velious ausschlaggebend ist, und somit alle jene, die bereits Drachen auf den alten Kontinenten erschlagen haben, auch von den Velious-Drachen als Feinde betrachtet werden ?
</P>	
<P CLASS="P1">
	Die Drachen waren schon immer ein großes Rätsel für alle Bewohner Norrath. Generell ist immer Vorsicht angeraten, wenn man einem Drachen begegnet. Wie auch immer, Details müssen von den Spielern selbst herausgefunden werden.
</P>

<P CLASS="P2">
	1.11 Gibt es Bereiche in denen man sich nicht "binden" kann ?
</P>	
<P CLASS="P1">
	Neben den grundsätzlichen klassenspezifischen Einschränkungen gibt es auch ziemlich sicher Bereiche in denen sich niemand binden kann.
</P>

<P CLASS="P2">
	1.12 Da Velious ein Schnee- und Eis-Kontinent ist; heißt das, daß die Bewohner dort eine höhere Resistenz gegen Eis- und Kälte-Spells haben sowie weniger resistent gegenüber Feuer- und Hitze-Spells sind ?
</P>	
<P CLASS="P1">
	Noch eine Frage, die durch Erforschen selbst herausgefunden werden muß.
</P>

<P CLASS="P2">
	1.13 Wie groß sind die neuen Zonen in Relation zu den bestehenden Zonen in Kunark und der alten Welt ?
</P>	
<P CLASS="P1">
	Einige sind sehr groß, andere nicht. Ein genereller Vergleich ist nicht möglich, aber insgesamt wird die Welt durch Velious um über 20% erweitert.
</P>

<P CLASS="P2">
	1.14 Gibt es in Velious mehr "Einbahn-Zonen" wie Chardok ?
</P>	
<P CLASS="P1">
	Erforschen und Experimentieren ist der einzige Weg das herauszufinden.
</P>

<P CLASS="P2">
	1.15 Ist Velious die Heimat für eine spezielle Rasse oder Spezies ?
</P>	
<P CLASS="P1">
	Die "Coldain Dwarves" leben dort seit Jahrhunderten, und es werden täglich neue Spezies entdeckt.
</P>

