<P CLASS="P2">
	3.1 Können aktive EQ-Spieler das Update im Online-Store bestellen ?
</P>	
<P CLASS="P1">
	Ja.
</P>

<P CLASS="P2">
	3.2 Muss man die Erweiterung "Ruins of Kunark" besitzen um "Scars of Velious" installieren und betreten zu können ?
</P>	
<P CLASS="P1">
	Nein, jede Erweiterung ist extra und jedes kann ohne das andere installiert und betreten werden.
</P>

<P CLASS="P2">
	3.3 Kann "Scars of Velious" extra als Update gekauft werden, oder muß man wieder das komplette Spiel kaufen, wie es bei Kunark der Fall war ?
</P>	
<P CLASS="P1">
	SoV ist ein Update und kann separat gekauft werden.
</P>

<P CLASS="P2">
	3.4 Was passiert, wenn ein Spieler versucht Velious zu betreten, ohne das Update gekauft bzw. installiert zu haben ?
</P>	
<P CLASS="P1">
	Der Char wird zurückteleportiert an einen Punkt in der Zone, von wo aus er versucht hat Velious zu betreten. Es ist genauso, als versucht man eine Zone zu betreten, die gerade "down" ist.
</P>

<P CLASS="P2">
	3.5 Kann man Kunark betreten, wenn man sich nur das Velious-Update kauft, also Kunark eigentlich nicht besitzt ?
</P>	
<P CLASS="P1">
	Nein, jede Erweiterung ist unabhängig von der anderen und muß extra gekauft und installiert werden.
</P>

