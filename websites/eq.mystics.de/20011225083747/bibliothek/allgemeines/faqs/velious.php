<P CLASS="P2">
	4.1 Was sind die Mindestanforderungen für Scars of Velious ?
</P>	
<P CLASS="P1">
	Die Mindestanforderungen sind: Windows 9x od. 2000, ein Pentium 200 Prozessor, 64 MB RAM, Direct3D oder Glide-kompatible Grafikbeschleuniger Karten mit 8MB RAM, eine DirectX 7.0a kompatible Soundkarte, 28.8k bps Internetverbindung, 2x CD-ROM und 600MB+ freie Festplattenkapazität. Wir empfehlen aber ein etwas besseres System mit einem Pentium 350, od. höher , Prozessor, 128 MB RAM, 16MB Grafikkarte und 4x CD-ROM.
</P>

<P CLASS="P2">
	4.2 Ist die Grafikengine durch das Update verbessert worden ?
</P>	
<P CLASS="P1">
	Es sind einige Veränderungen gemacht worden, wie z.B. das Interface, aber die Game-Engine selbst wurde nicht verändert.
</P>

