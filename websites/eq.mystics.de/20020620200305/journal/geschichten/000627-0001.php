<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Syriliss Zoranthul<BR>
	<BR>
</P>

<P CLASS="P1">
Im tiefsten Herzen von Neriak erblickte sie das Licht, damals noch unter einem anderen Namen.
Ihre Eltern, die beide Necromanten waren und hohes Ansehen genossen erzogen sie im Geiste
Innoruks, doch sie wurden immer ungeduldiger, da ihre Tochter keinerlei Begabung für die
Necromantie zeigte.<br>
Als sie fünf Jahre alt wurde, belauschte sie ein Gespräch ihres Vaters mit einem der hohen
Priester. Der Priester sagte ihrem Vater, da sie sich offensichtlich als unwürdig erweist, sei
dies ein Wink Innoruks sie zu opfern. Nach einiger Beratung willigte ihr Vater ein, nachdem
ihm der Priester zugestand, sie wiederzubeleben und ihren Körper als Sklaven für die Hausarbeit
zu benutzen. Von Grauen erfüllt, machte sich die Kleine davon, soweit sie ihre kleinen Füsse
tragen mochten. Es ist ein wahres Wunder, daß keines der Monster die durch Nektulos streifen
sie als zweites Frühstück genoss und schließlich erblickte sie das Licht der Commonlands als
sie ohnmächtig zusammenbrach.
</P>

<P CLASS="P1">
Dunkle Träume bewegten sie und sie hatte Angst gefunden und geopfert zu werden, doch ihr Geist war zu schwach um den Körper zu finden. Doch nach langer Zeit, sie konnte nicht sagen wielange es gedauert hatte, hörte sie Worte: 'Ugh, klein Blauchen erwachen, mussen grossnes Blauchen findet und saget.' Als sie die Augen aufschlug sah sie eine große grüne Gestalt, die sie aber mit seltsam freundlichen Augen anblickte. 'Du gut bleibe liegen, ich sich kümmert olles schen. Koomme zu Kraft damit kenne gehe bashin mit da ondere Blaue.' Die Worte waren schwer zu verstehen, aber sie begriff, daß ihr kein Leid geschehen wird, als plötzlich ein Teir Dal den Vorhang ihres Zimmer zurückschlug und sie seltsam ansah. Sie dachte ihre Stunde sei gekommen. Doch er sprach:'Sei gegrüßt. Wer bist du und woher kommst du? Mein Name ist Zoranthul.' Sie fühlte sich so ausgeliefert, daß sie sich entschloß die Wahrheit zu sagen und ihr Schicksal anzunehmen. Sie berichtete dem Fremden alles was sie erlebt hatte und er hörte nachdenklich und sehr gelassen zu. 'Nun denn. Wie es aussieht hat das Schicksal mich auserwählt dich zu finden. Von nun an sei deine Vergangenheit ausgelöscht, denn heute wurdest du als meine Tochter wiedergeboren und Syriliss soll dein Name sein und wenn ich dich durch Leben oder Tod behüten kann, so soll es geschehen.'
Die Worte ergriffen Syriliss und sie konnte ihr Glück kaum ausdrücken, dachte sie doch sie müsse jeden Augenblick sterben und nun diese Worte. Der Troll sagte nun:'Ich Enketem. Gut bashin Troll und Kumpel von lange Namen Blauchen. Eine funkel Troll habe dich gefunkelt und nun du wach. Du könne sein Gast in meine hier, solange du brauchst.'
</P>

<P CLASS="P1">
Zoranthul entdeckte rasch die wirkliche Begabung von Syriliss und weihte sie in die Kunst der Zauberei ein. Ihre Kräfte wuchsen rasch und die Jahre vergingen in denen sie Zoranthul's Lehren in sich aufnahm und gemeinsam jagten sie in Innothule und brachten allerlei Ungeziefer zur Strecke. Zwar hatte sie gefunden was nur wenige Teir Dal finden, ein Gefühl der Freude und Freundschaft mit ihrem neuen Vater und mit Enketem, der sein Haus mit ihnen teilte, jedoch konnte sie noch immer mit ihrem ganzen Hass ihre Feinde zur Strecke bringen ohne danach einen Gedanken daran zu verlieren.
</P>

<P CLASS="P1">
Eines Morgens erwachte sie und Enketem saß an ihrem Bett. Mit bedrücktem Gesicht sagte er:'Du Vater sein weg. Er in fernes Land müssen, ich nix wissen wo. Er dir ausrichtet, du nun auf rechte Weg, könne aufpassen auf dich selbst. Du müssen Wissen vergrößern und weiter sein brav lernen viel. Er sein immer stolz.'
Syriliss war traurig an jenem Morgen und sprach wenig. Enketem versuchte sie zu trösten und lief sogar zu den Ogern nach Oggok um für sie Muffins zu besorgen.
Von nun an streifte sie allein durch Innothule und machte jagt auf Frogloks und Kobolde. Sie konnte ihn zwar nicht oft erblicken, aber Enketem folgte ihr oft und wachte über sie. Mehr als einmal tauchte er aus dem nichts auf in einer prächtigen Rüstung und mit einer riesigen Axt bewaffnet und hackte ihre Gegner in Stücke, wenn sie in Bedrängnis geriet.
</P>

<P CLASS="P1">
Wieder vergingen Jahre und Syriliss' Kräfte wuchsen, als sie jagte und die Bücher des Wissens studierte.
Als sie eines Abends mit Enketem beim Essen saß sah er sehr nachdenklich aus und sagte schließlich:'Du nun schon sehr gut funkeln und brenne Frogloks sehr gut ist sich schon. Morgen du gehen. Müssen sehen mehr von der Welt. Müsse mehr Wissen, nicht sitze bei alte Enketem ganze Leben. Enketem gehen jagen, du könne besuchen alte Bashin Troll wenn Lust hast.'
Da ging Enketem in die Nacht hinaus. Syriliss schlief nur kurz und machte sich bald auf den Weg um als erstes nach Neriak zurück zu kehren. Nach einer gefahrvollen Reise gelangte sie schließlich nach Neriak, wo sie nach all den Jahren natürlich niemand mehr erkannte. Sie ging zu einem Freund Zoranthul's und überbrachte ihm ein Schreiben ihres Vaters, so geschah es, daß Syriliss in die Gilde der Zauberer von Neriak aufgenommen wurde, von nun an konnte sie bei ihnen ihr Wissen vergössern.
</P>

<P CLASS="P1">
Aber bald zog es Syriliss wieder in die Welt hinaus und sie erlebte noch viele Abenteuer. Nach langen Jahren des Lernens und der Abenteuer sollte es schließlich ein Zwerg und ein Waldelf sein, die ihr noch mehr über das Leben beibrachten, aber das ist eine andere Geschichte und sie wird später erzählt.
</P>

<P CLASS="P0" ALIGN="CENTER"><I>Syriliss Zoranthul</I></P>

