<P CLASS="P0">
	Hail, Brüder und Schwestern.
</P>

<P CLASS="P1">
	Dies richtet sich an all jene unter Euch, die sich der hohen Kunst des "Verzauberns" und 
	"Veränderns", auch "Enchanting" genannt verschrieben haben oder mehr über die mächtigste 
	aller Kasten in ganz Norrath wissen wollen.
</P>

<P CLASS="P1">
	Ich werde hier nicht arkane Geheimnisse von mir geben, deren Studium jahre- und jahrzehntelang 
	dauert und deren Ausübung auch nach jener Zeit für den Anwender immer noch sehr kraftraubend 
	und mitunter gefährlich ist.
</P>

<P CLASS="P1">
	All jene unter Euch, die sich erhofft haben, nun die Mysterien der "Ver-Zauberei" enthüllt zu 
	bekommen sind hier fehl am Platze und mögen getrost das Bild auf dem magischen Würfel vor 
	ihnen verschwinden lassen.
</P>

<P CLASS="P1">
	Zu Beginn Eurer Laufbahn werdet ihr Euch im Vergleich zu Euren kettenrasselnden, 
	messerschwingenden Kameraden der Kriegerkasten oder den lauten, unbeherrscht um sich 
	zaubernden Mitstreitern der Wizardkaste eher benachteiligt fühlen.
</P>

<P CLASS="P1">
	Dieser Zustand jedoch ist eine unserer größten Stärken. Denn schon nach kurzer Zeit, liebe 
	Freunde, gehören wir Enchanter zu den mächtigsten Wesen auf Norrath's Antliz.
</P>

<P CLASS="P1">
	Die Macht, unser Aussehen zu verändern, und so unbemerkt in die Reihen unserer Feinde 
	vorzudringen ist allein uns gegeben. Auch ist es uns bereits zu Beginn unserer Laufbahn 
	ein Leichtes, das Licht zu beugen und um uns herum zu strahlen, sodass wir aus dem Auge 
	des Gegners verschwinden und ungesehen in Regionen der Angst und des Todes vordringen können, 
	die anderen Kasten noch lange Zeit verwehrt sind.
</P>

<P CLASS="P1">
	Das Schadenspotential, welches unseren Zaubern innewohnt, wird nur allzuhäufig unterschätzt. 
	Mögen sie den Gegner auch nicht mit der Stärke eines Feuerballs versengen, so ist er doch, 
	geschwächt von Hustanfällen oder plötzlicher Atemnot ein leichtes für uns. Auch reine Magie, 
	die uns umgibt, können wir so nutzen, dass die Muskelmasse des Gegners für einen begrenzten 
	Zeitraum zu schwinden beginnt.
</P>

<P CLASS="P1">
	Auch ist uns die Macht gegeben, ähnlich dem Magier oder dem verruchten Nekromanten, tote 
	Materie mit Scheinleben zu beseelen, welche sich sodann bedenkenlos für unseren Schutz zur 
	Verfügung stellt. Da dies jedoch die Spezialität der erwähnten Kasten ist, sind die 
	beseelten Objekte, welche wir erschaffen, nicht unserem Willen unterworfen. Wohl dienen 
	sie dem Zwecke, uns zu verteidigen uns unser Leben mit dem ihrigen, oder eher mit dem, was 
	sie scheinbar leben lässt, zu verteidigen, jedoch ist dies auch schon der einzige Befehl, 
	dem sie unterworfen sind. Sie folgen uns, wohin wir auch gehen, doch werden sie erst dann in 
	einen Kampf eingreifen, wenn der Feind offenkundig versucht, uns Schaden zuzufügen. Dem Befehl, 
	einen Gegner zu attackieren, leisten sie nicht folge, da die über ihren Verstand hinaus geht. 
	Doch sie sind in jedem Falle ein vorzüglicher Schutz in unwirtlichem Gelände.
</P>

<P CLASS="P1">
	Desweiteren besitzen wir die wohl besten Zauber, um die Fähigkeiten unserer Freunde zu erhöhen. 
	Sei es nun eine Erhöhung der Stärke, mehr Atemluft oder ein Adrenalinstoss, der mehr Schaden 
	ertragen lässt, all jenes ist kein Hindernis für uns. Auch die Fähigkeit zu verleihen, mit den 
	Augen der Elfen oder gar noch besser sehen zu können ist uns gegeben.
</P>

<P CLASS="P1">
	Was uns jedoch von allen anderen Kasten abhebt, ist die einzigartige Kunst, Metalle mit Magie 
	zu versehen. So beschaffene Metalle können wir hernach in magischen Schmuck verwandeln, der die 
	unterstützenden Zauber, welche weiter oben erwähnt wurden, enthält. So kann ein unscheinbarer 
	Ring die Widerstandskraft gegen Feuer erhöhen, ein Halsband uns für unsere Mit-Norrather 
	attraktiver erscheinen lassen oder ein unscheinbarer Ohrring unser magisches Potential erhöhen.
</P>

<P CLASS="P1">
	Nach diesem kurzen Überblick über die schiere, unbändige Kraft, die uns Enchantern zur 
	Verfügung steht, wer wagt es da noch, uns als schwache Kaste zu bezeichnen?
</P>

<P CLASS="P1">
	All jene unter Euch, die zweifelten, sind nun, so hoffe ich, die Augen geöffnet worden.<br>
	So hoffe ich, einen kurzen Überblick über die Macht des Enchanters gegeben zu haben.
</P>

<P CLASS="P0">
	Ich verbleibe der untertänigste Diener des großen Marr,<br><br>
	<I>Elminsteer</I>, Meister-Enchanter Extraordinare
</P>

