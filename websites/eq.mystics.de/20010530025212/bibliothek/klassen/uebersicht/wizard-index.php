<P CLASS="P1">
	Wissen. Wissen und die damit verbundene Macht. Diesen beiden Dingen gilt das ganze Streben des Zauberers.
	Sie sind die Suchenden, immer auf der Jagd nach neuen Formeln und Zaubern. Dabei lassen sie sich von
	niemandem aufhalten. Sie beherrschen die gefährlichsten der Kampfzauber. Auf seinem Weg zum Ziel sollte man
	dem Zauberer nicht im Wege sein. Doch stellen sie wohl mit die mächtigsten Verbündeten dar, die man haben
	kann. Selbstverständlich unterliegt ein Zauberer sogar einem Barden im Nahkampf, doch soweit lassen sie es
	selten kommen. Ebenso beherrscht er einige Zauber der Bewegungsmagie. Ein Zauberer ist oftmals lieber mit
	einer Gruppe unterwegs, sie unterschätzen die Fähigkeiten der anderen Klassen nicht im geringsten.
</P>	

