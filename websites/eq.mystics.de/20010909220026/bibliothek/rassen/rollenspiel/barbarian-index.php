<P CLASS="P5">
	Sein oder nicht sein:
</P>

<P CLASS="P1">
	Was Du bist und was Du auf keinen Fall bist, entscheiden Deine Fähigkeiten im Kampf. 
	Entweder Du siegst, oder Du stirbst. Man erzählt sich über die Barbaren, sie seien 
	dumm und schwerfällig. Diese lächerlichen Argumente lassen sich leicht widerlegen. 
	Erstens ist ein Barbar nicht dumm - jemand der alle Möglichkeiten erwägt und letztendlich 
	keine voreiligen Schlüsse zieht kann gar nicht dumm sein. Zweitens glaube ich kaum, daß 
	jemand der schon einmal mit oder gegen einen Barbaren gekämpft hat, diesen als schwerfällig 
	bezeichnen würde...<br>
	Ein Barbar ist etwas impulsiv - daher tendiert er zu recht extremen Gemütsschwankungen 
	(Jeder kennt den Barbaren, der Dich knuddelt und wuddelt bis Dir die Luft wegbleibt, und 
	der Dir im nächsten Moment mit seinem Schwert oder seiner Axt Deinen Schädel spalten will, 
	weil Du ihn unbeabsichtigt beleidigt hast...)
</P>

<P CLASS="P5">
	Wie solltest Du nun Deinen Barbaren spielen?
</P>

<P CLASS="P1">
	Du bist als freier Mensch aufgewachsen. Dein Leben war hart, denn Du wurdest schon früh 
	im Kampf unterwiesen. Die Barbaren haben eine patriarchalische Gesellschaft. Das bedeutet 
	nicht, das die Frau in dieser Gesellschaftsform unterdrückt werden würde. Die Barbarenfrau 
	wird ebenso im Kampf gefürchtet wie ein Barbar.<br>
	Du bist eigentlich recht umgänglich. Man sollte es allerdings meiden:
</P>

<UL CLASS="P1">
<LI>Dich zu beleidigen (Ehre, Stolz usw.)<BR></LI>
<LI>Dich oder Deine Freunde anzugreifen<BR></LI>
<LI>unfreundlich zu Dir zu sein oder Dich gar herauszufordern (es sei denn, man hätte eine Chance zu siegen...)<BR></LI>
</UL>

<P CLASS="P5">
	Die Klassen der Barbaren
</P>

<P CLASS="P1">
	<B>Der Warrior (Krieger)</B><BR>
	Du bist eine Kampfmaschine. Deine Waffe ist der Rasenmäher und Du bist der/die 
	Rasenmäher(-mann/-frau). Feinde sind die Grashalme die gestutzt werden müssen. 
	In jedem Kampf mit Waffen bist Du überlegen - an Kraft, an Technik, an Übung... 
	an allem halt. Im Kampf bist Du immer an mitten im stärksten Getümmel. Du verläßt 
	das Schlachtfeld als letzter... oder gar nicht!<br>
	Allerdings hast Du einen Nachteil (?). Nun ja, magisch gesehen bist Du eine Null.
</P>
	
<P CLASS="P1">
	<B>Der Rogue (Dieb)</B><BR>
	Du hast Dein gesamtes Leben an Deiner Fingerfertigkeit gearbeitet. Kein Schloß und 
	Geldbeutel ist vor Dir sicher. Obwohl Du im Kampf sehr gewandt bist, bevorzugst 
	Du lieber einen Angriff aus dem Hinterhalt. Wenn Du einmal an etwas dran bist, das 
	Du haben möchtest, dann bekommst Du es auch.
</P>
	
<P CLASS="P1">
	<B>Der Shaman (Schamane)</B><BR>
	Meistens verbringst Du Deine Zeit mit Schütteln irgendwelcher Runenknochen, rauchen 
	eines seltsamen Krautes und murmeln irgendwelcher Dinge. In Deinem Stamm bist Du hoch 
	geachtet. Als Heiler des Stammes hast Du bei den Barbaren recht viel zu tun. In Zeiten 
	des Kampfes stellst Du Deine Fähigkeiten mit Schutz- und Heilzaubern zur Verfügung.<br>
	Du weißt, das viele Rassen Dich als recht "primitiv" ansehen. Milde lächelst Du darüber 
	und hältst Zwiesprache mit den Vorfahren Deines Stammes.
</P>

<P CLASS="P5">	
	Religionen
</P>

<P CLASS="P1">
	Es gibt bei den Barbaren diverse Glaubensrichtungen, wobei die Agnostiker dieses Volkes 
	recht große Zweifel an der Existenz der Götter haben.<br>
	Rallos Zek - Der Kriegsgott - Die Anhänger des Warlords sind meist Krieger und man kann 
	sie mit den Spartanern unserer Welt vergleichen. Nur die Starken überleben. Solange sie 
	am Leben sind und die Erleuchtumg im Kampf suchen, verdienen die Krieger sich Achtung 
	und Respekt. Ein Krieger der stirbt, hat die Gunst des Kriegsgottes verloren und verliert 
	alle Privilegien.<br>
	Das Tribunal - Die sechs Hämmer - Die Anhänger des Tribunals erstreben eine Sache vor 
	allem anderen: Gerechtigkeit. Anhänger des Tribunals sind sie geduldig, gerecht und 
	gehen ihre Aufgaben und Pflichten systematisch an. Und das müssen sie auch, denn es ist 
	ihr Glaube, daß das Tribunal sie verurteilen und zu einer Ewigkeit in unendlicher Qual 
	verdammen würde, wenn sie einen Unschuldigen bestrafen.<br>
	Bristlebane - Der Herr der Diebe - Die Anhängerschaft Bristlebane's besteht zumeist aus 
	Barden, Dieben, Spaßvögeln (Gaukler und Narren), Glückspieler und Zigeuner. Nun, was gibt 
	es noch wichtiges über Bristlebane zu sagen... außer: Halte Deinen Geldbeutel gut 
	versteckt ... laß Dir nichts andrehen ... werde nicht zum Mittelpunkt eines Streiches!<br>
</P>

<P CLASS="P5">
	Barbaren und die anderen Völker
</P>

<P CLASS="P1">
	Minneyar - Kriegerin: "Es gibt nicht viel, was uns mit den anderen Völkern verbindet. 
	Entscheidend sind nur folgende Dinge: Vertrauen, Loyalität, Freundschaft und Spaß. Wer 
	unser Vertrauen genießt, hat es sich zweifellos verdient. Freunde sind wichtig. Man kann 
	Freunde beschützen, Freunde helfen einem, mit Freunden kann man das Bett und die Nahrung 
	teilen. Freunde genießen unabdingliche Loyalität und müssen selbstverständlich auch selbst 
	loyal sein um Loyalität zu verdienen. Das ist doch ganz einfach! *schaut leicht verwirrt*<br>
	Was wäre das Leben ohne Spaß? Kein Kampf ohne Spaß - sei es auf dem Schlachtfeld oder im 
	Bett *lacht*. Gib niemals Dein Wort, wenn Du es nicht halten kannst oder Du beschmutzt die 
	Ehre Deines Stammes. Gehe keinem Kampf aus dem Weg!"
</P>
	
<P CLASS="P0">
	<I>Jadzia</I>
</P>

