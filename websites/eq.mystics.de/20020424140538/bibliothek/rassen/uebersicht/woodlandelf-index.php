	<P CLASS="P1">
		Dieses Elfenvolk lebt in den weiten Wäldern Faydwers. Diese Rasse ist wie keine andere der Natur 
		verbunden. Sie bauen ihre, zwar etwas einfachen, jedoch beeindruckenden, Städte in den Wipfeln der 
		höchsten Bäume. Dies ist für einen durchschnittlichen Wanderer äußerst gefährlich, kann doch ein 
		einziger, unbedachter Schritt zu unweigerlichem Absturz und damit dem Tod führen. Jedoch ist diese 
		Gefahr für einen Waldelfen recht gering, verfügt dieses Volk doch über verblüffende Agilität und 
		erstaunliche Geschicklichkeit. Sie verlassen sich eher auf ihr Geschick und ihre Intelligenz, 
		anstatt auf rohe Gewalt. Das Waldvolk scheint eine besonderes ehrliche Ader zu haben, was nicht 
		bedeutet, das es bei ihnen keine Diebe gäbe! Jedoch fällt es einem schwer zu glauben, das diese 
		charismatische Rasse notorische Lügner und ruchlose Mörder hervorbringt. 
	</P>
