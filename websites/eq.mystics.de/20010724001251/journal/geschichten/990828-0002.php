<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Oger Emma<BR>
	<BR>
</P>

<P CLASS="P1">
	Nach langem hin und her hatte Emma sich wieder mal dazu durch gerungen, ihrer Sippe ein 
	Festmahl zu bereiten. Da bot sich ihr das unten angegebene Menü geradezu an.<br>
	Sie sah sich in ihrer Küchenhöhle um und überlegte, ob sie auch alles beisammen hatte um 
	zu beginnen. Die Hauptzutaten lagen gefesselt in der ecke und hatten alle einen dicken 
	Froglog im Mund. So konnte sich Emma ganz auf ihre Arbeit konzentrieren. Gewürze waren 
	auch alle da Aufregen, Dummheit, Besserwisserei, ein Bund Duellkite, mehrere Stücke 
	Unfairniss, ein wenig Luft usw.<br>
	Der Akt der Kunst und Kreation konnte beginnen!
</P>

<P CLASS="P1">
	Als erstes klatschte Emma die Geschichte auf den Tisch und bestreute sie beidseitig mit 
	überflüssigen Kommentaren. Danach wurde sie richtig breitgetreten und nochmals mit 
	Flüchen und Dummheit gewürzt. Nun nahm sie Chaos und Dugobalosch in ihre Finger, 
	spickte sie vorsichtig mit gut durchwachsenen 386ern, Hass dazu und Öffentlichkeit 
	darüber. Damit das Aroma nicht verflog schnell die beiden in die Geschichte eingerollt 
	und mit einem rusty-Short-Speer verschlossen. Da Emma auch eine Leidenschaft für das 
	Zielschiessen hatte, bestand der letzte Gang der beiden aus einem 5 m langen Flug in 
	den Kessel! Das Hauptmenü war angerichtet!
</P>

<P CLASS="P1">
	Mit leuchtenden Augen sah Emma auf Tandarion, Savaros und Fuenyan. Mit schreckgeweiteten Augen 
	sahen alle 3 zurück! Das konnte Emma nicht verstehen, da alle 3 für die Vollwertkost vorgesehen 
	waren . Und das die lecker wird, war ja nicht von der Hand zu weisen. Mit jedem Geräusch, das 
	der Wetzstein auf dem Messer verursachte kamen die Augen der 3 immer weiter raus. Schnell die 
	3 in feine Streifen geschnitten, halbiert und rein in die Salatschüssel. Dazu für 2 Pfennig 
	Torfköppe, mehrere Stangen Langeweile und eine Prise Sympathie aus dem tiefen Keller. Da 
	ihr das in der Menge noch zu wenig erschien, schnappte sie sich noch die Lol-anna, würfelte 
	diese fachgerecht und rührte sie unter den fertigen Salat.
</P>

<P CLASS="P1">
	Dann sah sie sich wieder in der Ecke um, wo es zappelte und wackelte und ihr Blick fiel auf 
	Durim. Herrgott nochmal, immer wo Emma auftauchte war dieser bärtige, nach schlechtem 
	Branntwein riechende halbe Meter anwesend! Da kam ihr eine Idee. Die Soße!! Genau. den 
	Zwerg richtig püriert, die Fertigsoße von Chief Dooga beigegeben und ein bisschen mit 
	*hört einfach auf* abgeschmeckt. Das war die beste Verwendung für Tavernenzwerge. Man 
	brauchte nicht ewig drauf rum zu kauen und eine Soße wurde immer begruesst!
</P> 

<P CLASS="P1">
	Nun war die ecke wieder dran. Diesmal sah Emma genauer hin und bemerkte Hellkeeper. 
	Hellkeeper?? Damit konnte sie nichts anfangen. Der war nicht zu beurteilen. Im großen 
	Kochbuch stand auch nichts über die Handhabung von Wesen, die man nicht beurteilen konnte 
	und somit steckte sie Hellkeeper einfach in den Backofen rein, wo schon fröhlich die 
	Zustimmung brannte! Temperatur war wichtig für das nächste Gericht. Die Pfanne stand 
	schon auf dem Ofen und das Asp-Öl war auch schon richtig heiß. Bevor sie richtig wußten was 
	ihnen geschah, hatte Emma die Steaks Mirko und Pagan mit dem 1 hd blunt breitgeschlagen, 
	mit lächerlichen FadeNukeFadeNuke bestreut und beide in die Pfanne gelegt. Nach 5 min waren 
	beide gar. Außen braungebraten und innen saftig.
</P>

<P CLASS="P1">
	Im letzten Winkel der Ecke, Emma traute ihren Augen kaum, saß ein Troll. Hmm, den hätte sie 
	doch gleich von Anfang an sehen müssen . Zweifel stiegen in ihr auf und ihr erster Gedanke 
	war Rezept-spionage. Und Emma hatte recht. Wie durch ein wunder fielen alle fesseln von 
	Honkori ab. Wutentbrannt griff sie sich den Grobbcleaver und prügelte den Spiogenten 
	durchs ganze Freerott bis in den Swamp.<br>
	Atemlos und keuchend kam sie wieder in ihrer Küche an und freute sich auf die leckeren 
	Häppchen. Doch was sahen ihre Augen. Alle Töpfe und Pfannen waren leer und der Fußboden 
	war voll mit vollgefressenen Ogerbaeuchen. Innerlich tobte in ihr ein Streit zwischen der 
	Wut, das sie leer ausgegangen war und der Anerkennung ihrer phantastischen Kochkünste.
</P>

