<P CLASS="P1">
	Endlich bekommt der Necro BIND und seine ersten STACK spells. Vorbei sind die Zeiten der Lauferei! Man kann sich überall binden, ob vor dem Dervish Camp oder in er Bank in Neriak überall. Gruppenmitglieder kann man auch binden, allerdingst nur in city-zonen!<br>
	Die wichtigsten Spells im kurzen Überblick.
</P>

<TABLE BORDER="0" CELLPADDING="0" CELLSPACING="0" WIDTH="100%">
