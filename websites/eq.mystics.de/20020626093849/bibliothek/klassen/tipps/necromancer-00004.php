<UL CLASS="P1">
	<LI>Bind Affinity
		<UL>muss ich dazu was sagen? ;)<br>
		</UL>
	<BR></LI>
	<LI>DEngulfing Darkness
		<UL>Ein DOT (Damage Over Time). Er macht ca. 200 dam, und verlangsamt den Gegner!<br>
			Mit diesem Spell wird ein SoW unnötig, da der Gegner einen nicht mehr einholen
			kann.<br>
		</UL>
	<BR></LI>
	<LI>Heat Blood
		<UL>Auch ein DOT.<br>
			Heat blood macht Insgesamt mehr dam. als Engulfing Darkness, aber er verlangsamt
  		den Gegner nicht und wird von allen Lavastorm Bewohnern resistet!<br>
		</UL>
	<BR></LI>
	<LI>Convoke Shadow
		<UL>Das Pet. Ab diesem Zeitpunkt kann euer Pet magische Attacken.<br>
			Drückt ihm also keine Waffe mehr in die Hand, denn mit Waffe ist die Attacke
			nicht mehr magisch!!<br>
		</UL>
	<BR></LI>
	<LI>Wave of Enfeeblement
		<UL>Tja...den Sinn dieses Spells habe ich noch nicht herausgefunden.<br>
			Man zieht Gegnern Stärke ab, allerdings nur wenn sie SEHR nahe um einen stehen.<br>
		</UL>
	<BR></LI>
</UL>

<P CLASS="P1">
	Mit den 2 neuen DOT's sind Young Kodiacs und sogar die grossen Kodiacs kein problem mehr.<br>
	Und mit den magischen Attacken des Pets kann man sich getrost auf alle Willowwisps stürzen =)
</P>

