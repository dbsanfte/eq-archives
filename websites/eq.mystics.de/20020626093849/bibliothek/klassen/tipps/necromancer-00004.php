<P CLASS="P1">
	Ob das eine reine Necro Eigenschaft ist, oder ob das noch andere Chars können weiss ich nicht, 
	jedenfalls habe ich noch keinen anderen Spellcaster gesehen der 2 spells nahezu gleichzeitig 
	abfeuern kann. Ja ihr lest richtig...2 =)<br>
	Eine genaue Liste, welche Spells gestackt werden können kommt noch, ich muss das erst genau 
	austesten. Nehmt euch  Zeit und probiert ein wenig rum.<br><br>
	Ein Beispiel...castet auf einen wenig gefährlichen Gegner Poison Bolt...und siehe da...alle 
	anderen Spells sind grau (wie üblich) bis auf Clinging Darkness. Den könnt ihr sofort hinterher 
	feuern.<br>
	Manchmal funktioniert das nicht, wovon das stacken abhängt weiß ich auch nicht genau, aber 
	Poison Bolt und Clinging Darkness kann man DEFINITIV stacken...einfach ausprobieren!
</P>

