<P CLASS="P1">
	Ich habe mich von Level 12 an in den West Commons getummelt. Kodiacs, Dervish Camp, Orc 
	Camp und Willow Wisps sind eine gute Beute für Level 12.<br>
	Für diejenigen die eher klingende Münze, anstatt Expirience haben wollen empfehle ich Lavastorm.
	Die Feuerelementale sind nun vor eurem Pet nicht mehr sicher, da es magische Attacken kann.
	Fire Imps sind ab LV 14 schon blau (you might win) und Lavacrawlers habe ich als gelb geconned.<br>
	Bis Level 16 kann man hier wunderbar Geld bekommen, da wie schon erwähnt die Viecher oftmals 
	Edelsteine dabei haben.
