<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Elric<BR>
	<BR>
</P>

<P CLASS="P1" ALIGN="CENTER">
	Dies ist die erste Geschichte, die die Taten von Amaruk, Kental und Sashenga beschreibt.
</P>


<P CLASS="P1">
	Kental, seines Zeichen Zauberer der Hochelfen, betritt den Vorhof der gefürchteten Villa Unrest. 
	Größere Skelette und gequälte Untote sind hier noch die geringsten Probleme, denen er mit
	seinen Freunden begegnen wird. Doch davon wissen sie noch nichts.
</P>

<P CLASS="P1">
	<i>"Verflucht sei der Amaruk"</i>, sein Zwergenfreund, daß er ihn bittet hierher
	mitzukommen. Ermunterndes Nicken von Sashenga, dem Druiden des Waldvolkes und schauriges
	Lachen verschwimmen in dieser Atmosphäre des Todes miteinander. Die drei Helden
	(Dummköpfe?) schauen sich gegenseitig an und Wissen um die Gefahr flackert in ihren Augen. Der 
	schmale Gang der ins Innere des Berges führte knistert regelrecht als die drei ihre
	geistigen Kräfte freisetzen. Arkane Ströme fließen aus ihren Händen, umgeben ihre Körper.
	Der Zwerg, eben noch leicht zögernd ist nun ein Beispiel an Kampflust. <i>"Dann mal los,
	es wird Zeit diese armen Seelen endgültig zur Ruhe zur geleiten."</i> Sashenga schaudert
	als er die Kräfte des Waldes an diesen verdorbenen Ort zieht. Seine Haut wird grau,
	winzige Risse und Adern sind zu sehen. Er gleicht einem steinern Golem, doch seine
	Bewegungen sind die eines Wolfes, geschwind und beweglich tänzelt er kampfbereit. Kental
	erschafft mühelos einen schimmernden Schild: <i>"Ok, was können die uns schon 
	entgegensetzen, verrottendes Fleisch?"</i>
</P>

<P CLASS="P1">
	Amaruk schaut zu ihm hoch und ist augenscheinlich nicht sehr glücklich über den plötzlich 
	zunehmenden Tatendrang seines Freundes, war dies in der Vergangenheit doch stets mit
	Problemen verbunden. Erinnerungen an die Schlachten gegen die Orks von Crushbone tauchen
	auf, werden aber sofort wieder verbannt. Bloß nicht die Konzentration verlieren. <i>"Los
	Bleichgesicht, schau nach was uns erwartet."</i> <i>"Eines Tages Freund Rübennase wirst du
	Solusek gegenüber stehen und nach Bleichgesicht rufen."</i> Ein Flimmern umgibt Kental
	bei diesen Worten und er verschwindet vor den Augen seiner beiden Freunde. Erwartungsvoll
	durchquert er den Tunnel mit seinen Biegungen. Die drückende Dunkelheit ängstigt ihn
	nicht, aber diese fremden Gedanken in seinem Kopf, die ihn in die Villa locken wollen
	werden zunehmend stärker. Entschlossen folgt er der letzten Tunnelwendung.
</P>

<P CLASS="P1">
	<i>"Alles ruhig hier, ein paar Käfer, das war's auch schon."</i> teilt er den wartenden
	Freunden mit. Beruhigt folgen sie ihm und sehen nun auch einen Ort der gar nicht zu
	dieser drückenden Aura des Todes und der Angst passen will. Zwei Wasserweiher säumen
	die Seiten eines Pfads, der sich durch ein kleines, kunstvoll aus Hecken geschnittenes
	Labyrinth, hindurchzwängt. Sicher die Villa ist heruntergekommen, aber gefährlich kann es 
	hier kaum sein. Kental, der seine Unsichtbarkeit wieder fallen gelassen hat, beugt sich
	hinunter zu einem der Käfer, von denen es hier wimmelt, und schaut fragend zu Sashenga.
	<i>"Nun Baumspringer, kannst du uns etwas über diese kleinen Dinger erzählen?"</i>
	Nachdenklich betrachtet der Druide die kräftigen Beißzangen der immerhin armlangen Käfer.
	<i>"Fleischfresser würde ich sagen, du STADTelf. Ich frage mich wovon die sich ernähren,
	ich sehe hier.."</i>.
</P>

<P CLASS="P1">
	<i>"ACHTUNG, HINTER EUCH."</i> Amaruk deutet aufgeregt auf eine riesiges Skelett das aus
	dem Labyrinth hinter den Elfen auftaucht. Gleichzeitig erzittert das Skelett, getroffen
	von einem leuchtenden Strahl mit der Kraft des Lebens, der Amaruks Hände verläßt.
	Unglaublich schnell reagiert der eben noch erklärende Sashenga und stürmt auf den
	plötzlich aufgetauchten Gegner los. Zu schnell. Das Skelett ignoriert ihn einfach und
	richtet sich voller totem Haß auf den Kleriker. Gut 2,5 mal so groß wie der Zwerg scheint
	dies ein eher unausgeglichener Kampf zu werden. Sashenga und Kental reagieren gleichzeitig.
	Die Höhlenwände erzittern vom Hall des gewaltigen Donners der Kentals Blitz begleitet.
	Dornenranken sprießen aus dem Boden und umgeben den Körper des Zwerges wie eine Rüstung.
	Keine Zeit für Dankesreden. Amaruk schwingt seinen Streithammer, geformt aus dem Zorn
	seines Gottes gegen alle untoten Wesen. Nun damit hat das Biest nicht gerechnet.
	Behindert durch den pflanzlichen Schutz kann es den Zwerg kaum Schaden zufügen ohne 
	dabei selbst von stahlharte Dornen aufgespießt zu werden, sieht es auch bei der
	Verteidigung gegen die gezielten Schläge nicht sonderlich gut aus. Ein ums andere Mal
	fliegen die Knochensplitter, wenn der leuchtende Hammer sein Ziel findet. Kental schaut
	selbstzufrieden auf die geschwärzte Rückseite des Knochenwesens. Sashenga nimmt nun
	zusätzlich auch seinen Streitkolben in die Hand und gibt dem schwer angeschlagenen Skelett
	von hinten den Rest. Schweiß hat sich auf der Stirn des Zwerges gebildet und schwer geht
	sein Atem, <i>"Dreck, das Teil hat mich tatsächlich erwischt"</i>.
</P>

<P CLASS="P1">
	Unter den langsam verwelkenden Dornen sieht man deutlich einen roten Fleck, der langsam
	aber stetig größer wird. <i>"Na und, wir es doch auch."</i> Kental grinst, weiß er doch
	über die gewaltigen Heilkünste seines Begleiters genau Bescheid. <i>"Da hab ich mich ja 
	schon an meiner Orkhorntrophäe schlimmer verletzt."</i>, fällt nun auch Sashenga ein.
	<i>"Wußte gar nicht, daß ich noch 2 Ärzte in meiner Gruppe habe, ihr neunmalklugen
	Spitzohren. Verdammt das dauert wieder bis ich das Blut von der Rüstung bekomme. Also
	Rast für Pflege!"</i>. Ein kurzes Aufflackern mystischer Energie um die Wunde herum 
	läßt diese zu einer winzigen Narbe schrumpfen.
</P>

<P CLASS="P1">
	Kental setzt sich hin und taucht in die Tiefe seiner Gedanken ein. Sashenga tut es
	ihm gleich während Amaruk fluchend seine Rüstung reinigt. Völlig abgelenkt bemerkt er
	das nahende Unheil auch erst als ein lauter Aufschrei des Hochelfen ihn herumfahren läßt.
</P>

<P CLASS="P1">
	Mehrere Käfer stürzen sich auf den wehrlosen Zauberer. Aus mehreren Wunden blutend erhebt
	er sich, unfähig seine Gedanken vom schmerzenden Körper zu trennen.  Drei der als harmlos
	angesehen Vielbeiner machen sich an ihm zu schaffen, der Schild flackert ein letztes
	Mal und bricht dann unter der Wucht der Angriffe zusammen. Panisch sticht er mit seinem
	Dolch nach den herumwirbelnden Käfern, doch Staub und unglaublich harte Außenschalen
	lassen seine Angriffe äußerst wirkungslos verpuffen. Ein kurzes Gebet noch an Solusek
	gewandt, dann sieht er gelassen dem Tod entgegen. Seine Schmerzen verschwinden und der 
	Geist strebt dem Neuanfang entgegen. Völlig freigelassen erinnert er sich an seine
	Aufgabe in dieser Welt und wendet sich wieder um. Ein Blitz, der den vorherigen wie ein
	Kerzenlicht neben einem Lagerfeuer erscheinen läßt, nimmt er den Kampf um sein Leben wieder
	auf. Innerhalb einer einzigen Minute schlachtet er alle drei Käfer, die sich gegen seinen
	nun freigelassenen Zorn nicht wehren können.
</P>

<P CLASS="P1">
	Geruch von brennendem Fleisch liegt in der Luft als Kental wütend keuchend über den
	Leichen triumphiert. <i>"Wetten gleich behauptet er wieder, das wäre allein sein Werk
	gewesen"</i>. Amaruk nickt dem Druiden zustimmend zu: <i>"Jede Wette sogar."</i>
	Kental grinst die beiden überlegen an,<i> "Nun, ich hoffe ihr seht endlich ein, das sich
	niemand mit Zauberei eines Hochelf messen kann. Solusek selbst hat mich in diesem Kampf
	geführt."</i> <i>"Jo, klar, der muß dich wohl geheilt haben, die Käfer gelähmt und einen
	Schwarm winziger stechender Insekten gegen sie geschickt haben. Und gleichzeitig noch
	unsere Waffenarme durch dich geführt. Is schon recht Meister."</i> Murrend nimmt der
	Zwerg die Arbeit an der Rüstung wieder auf, langsamer aber aufmerksam das Labyrinth
	und die restlichen Käfer betrachtend. <i>"Arroganter alter Elf"</i>. Sashenga ist 
	außer sich vor Wut über soviel Unverfrorenheit, blutet doch auch er nun aus einigen
	frischen Wunden. <i>"Verdammt, hör mir gefälligst zu.. ach, zwecklos"</i>. Kental hat
	sich längst wieder hingesetzt und leises Summen zeigt dem Druiden, daß er ihm nicht mehr
	zuhört. Ein Blick zu den Käfern und Amaruk zeigt ihm, das die Gefahr für den Moment 
	gebannt ist. Zufrieden über den bisherigen Erfolg setzt auch er sich hin und sammelt
	sein Kräfte für die nächsten Stunden...
</P>

