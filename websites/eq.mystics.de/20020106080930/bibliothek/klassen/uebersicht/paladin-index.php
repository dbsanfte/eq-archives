<P CLASS="P1">
	Der strahlendste aller Helden. Er kämpft für das Gute, das Leben und das Licht, immer und überall. Niemals
	duldet er Verbrechen, dunkle Wesen und Machenschaften schon gar nicht. Ein Paladin ist stets ehrlich und
	fair. Von Geburt an in seinem Orden zum Guten erzogen, ist seinem Wesen alles Heimtückische und Böse
	fremd. Das bedeutet nicht, das er seine Feinde nicht kennt, oh nein. Die meisten Paladine wissen mehr
	über das Dunkle in dieser Welt als sonst irgendjemand! Die guten Götter stehen auf der Seite des Paladines,
	und unterstützen ihn in seinem Kampf mit kleinen Wundern und Gebeten.
