	<P CLASS="P1">
		Dies ist das dritte der kleinen Völker. Ähnlich groß wie die Zwerge, ähneln sie doch noch mehr den Menschen. 
		Halblinge sind äußerst geschickt, auch wenn man das aufgrund ihrer, nun ja, Rundlichkeit, gar nicht erwarten 
		mag. Sie sind ein der Natur recht verbundenes Völkchen, wenn auch nicht so wie die Waldelfen. Ach ja, noch 
		etwas wird dieser Rasse nachgesagt: Halblinge sollen manchmal... etwas... schneller zu... Fuß sein als andere, 
		wenn ihr versteht was ich meine? 
