<P CLASS="P1">
	Der Enchanter ist im Gegensatz zum Necromanten kein Solocharakter. Ab Level 5 bis 6 ist also 
	groupen angesagt.<br><br>
	Viele wissen gar nicht, daß auch Enchanter sogenannte DOT-Spells (Damage over time) haben, 
	wie z. B. "Shallow Breathe" oder "Choke", die zusätzlich zu den Direct-Damage-Spells gecastet 
	werden sollten.<br><br>
	Mit Level 29 bekommt der Enchanter den (neben "SOW") wohl beliebtesten Spell im ganzen 
	Game, nämlich "Clarity", der für eine schnelle Manaregenerierung sorgt. Es ist zwar ein 
	langer Weg bis dahin, aber wenn man den Spell endlich hat, sollte man damit nicht rumgeizen. 
	Obwohl ich letztens einen Druiden auf die Du-wirst-von-mir-nie-Clarity-bekommen-Liste gesetzt 
	habe, weil er mir doch glatt 2 Platin für SOW abknüpfen wollte =)<br><br>
	Das Enchanten von Metall kostet richtig Mana und deswegen solltet man sich nicht scheuen, 
	dafür auch was zu verlangen. Ich mit Level 26 kann gerade mal 5 Barren Electrum enchanten 
	und bin dann OOM. Mal 3 oder 4 Silberbarren kostenlos für 'nen Newbie zu enchanten sollte 
	kein Problem sein, aber wenn jemand 20 Barren braucht, dauert das schon richtig lange.<br><br>
	Ich habe mit Escaja keine Gottheit gewählt, was wohl der Grund dafür sein dürfte, daß ich 
	mich selbst in Neriak frei bewegen kann, ohne von den Guards attackiert zu werden.<br><br> 
	Zum Thema Hochzeit ein kleiner Tip von mir: haltet Euch da an Barden oder Druiden *g*<br><br>
	Und nochwas zum Thema "Gilden": Das muß jeder selbst wissen, aber ich bin aus 2 Gründen bis 
	jetzt unabhängig geblieben:
</P>
<OL CLASS="P1">
	<LI>
		Man kommt nicht in die Versuchung schon in niedrigen Leveln mit der supertollen Ausrüstung 
		rumzurennen. Schließlich hört man oft: "Ach die Items werden nachher an Newbies aus unserer 
		Gilde verschenkt". Als ich Level 14 war, wollte mir jemand eine FBR schenken, die ich dankend 
		abgelehnt habe. Selbst erkämpft habe ich sie dann mit Level 24, aber immerhin habe ich sie mir 
		verdient =)
	<BR></LI>
	<LI>
		Es gibt nix, was ich mehr hasse, als wenn mitten in der tollsten Gruppe plötzlich wieder 
		einer kommt mit dem Spruch: "Need to leave, Guild Stuff" abhaut.
	<BR></LI>
</OL>
<P CLASS="P1">
	So habe ich zwar nicht so viel Hilfe und muß mir vieles selbst zäh erkämpfen, habe aber auf 
	der anderen Seite keine Verpflichtungen =) Aber wie gesagt, das sollte jeder selbst entscheiden.
</P>

