<P CLASS="P1">
	Naheliegend ist natürlich Jewellery Making, da man als Enchanter sozusagen an der Quelle sitzt. 
	Aber man sollte am Anfang nicht zu viele Punkte darauf legen, da man eh noch nicht genug Geld 
	für die Steine und das Metall hat. Ab Level 4 sollte man aber alle Punkte dort reininvestieren, 
	da man ab Level 8 Silber enchanten und mit dem Üben beginnen kann.<br>
	Im Laufe der Zeit wird man wahre Horrorsummen in das Jewellery Making investieren, aber glaubt 
	mir, irgendwann bekommt man das auch wieder raus =)
</P>

