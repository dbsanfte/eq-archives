<P CLASS="P1">
	Die wichtigsten Stats für Enchanter sind und bleiben <b>Intelligence</b>, <b>Wisdom</b> und ganz hinten 
	<b>Charisma</b>. Wobei man am meisten Wert auf die ersten beiden legen sollte, den es gibt eine Menge 
	Items die Charisma mit vielen Punkten hochsetzen. Items die Wisdom und Intelligence erhöhen 
	sind erstens rar und selbst erkämpfen kann man sich diese erst in höheren Leveln.<br>
	Viele meinen immer noch, daß Wisdom unnötig ist, aber sorgt z. B. beim Herstellen von Schmuck 
	dafür, daß man nicht so oft versagt, was wiederum ne Menge an Kohle spart.
</P>

