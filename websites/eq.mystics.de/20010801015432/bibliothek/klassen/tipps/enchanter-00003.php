<P CLASS="P1">
	Die <b>Menschen</b> sind nicht wirklich empfehlenswert, weil nicht besonders intelligent. <b>Darkelfen</b> 
	haben halt immer das Problem, daß man sie fast nirgendwo richtig mag und man ständig 
	Faction sammeln muß. Und die <b>Gnome</b>... *g*, naja ich lache mich halt immer tot, wenn ich 
	die Kleinen in Roben und mit Stöckchen sehe *jk*. Nee, im Ernst: wirklich empfehlenswert 
	sind <b>Hochelfen</b> oder <b>Eruditen</b>. Ich habe mich auf Grund des höchsten Intelligenzwertes für das 
	Volk der <b>Eruditen</b> entschieden. Und was ist schöner anzusehen, als die <b>Eruditin</b> in ihrem 
	lilafarbenen Negligé? *g* Der einzige Nachteil ist die fehlende Infravision, doch ab Level 
	8 kann man das mit der Illusion Waldelf und ab Level 12 mit Infravision ausgleichen.
</P>

