<P CLASS="P1">
	<u>Newbiezonen:</u><br>
	Im Endeffekt sind alle Newbiezonen gleich, bis auf eine Ausnahme: Toxxulia Forest. 
	So schön Erudin als Stadt ist, die Newbiearea ist echt übel. Ewig dunkel, man verläuft 
	sich schnell und da auch meistens nicht besonders viele Spieler dort sind, fühlt man sich 
	schnell einsam. Aber Qeynos ist nah, also rauf aufs Schiff und sich in Qeynos binden lassen. 
	Dort sieht die Welt schon viel freundlicher aus.<br><br>
	<u>Spells:</u><br>
	Wie schon in dem anderen Guide beschrieben, sollte man sich die Liste der Spells ausdrucken. 
	Ich habe mir im Laufe der Zeit alle Spells gekauft, bis auf solche Sachen wie "Bind Sight" 
	oder "Sentinel". Obwohl mit "Sentinel" kann man andere Spieler wirklich gut ärgern *g*. 
	Schon mal den Megalag gesehen, wenn 3 Enchanter am Aviak Tree den Spruch casten? Hehe....   
	Auf jeden Fall sollte man alles aufsammeln, was sich verkaufen läßt, denn das Geld fliesst 
	besonders am Anfang nicht so reichlich.<br>
	Die Spells zum Enchanten von Silber, Electrum usw. habe ich bis jetzt nur in Neriak gesehen. 
	Alle Spells für die Pets ab Level 8 sind leider nur in Highkeep zu bekommen, was ziemlich blöd 
	gemacht ist, denn mit Level 8 nach Highkeep zu laufen, ist nicht wirklich einfach. Die Tiny 
	Dagger, die für die Pets gebraucht werden, sind auch rar gesät. Man bekommt sie in Erudin, 
	Freeport und irgendwo auf Faydwer. Wenn man also die Möglichkeit hat, sollte man sich einen 
	Vorrat der Dagger auf der Bank anlegen, denn man weiss nie, wann man wieder welche bekommt.<br><br>
	<u>Pets:</u><br>
	Der große Nachteil an den Enchanter-Pets ist natürlich, daß man sie nicht in der Gewalt hat. 
	Dauernd laufen sie so nervös rum, wie ein Mann, der auf die Geburt seines ersten Kindes wartet. 
	Daß sie dabei insbesondere in Dungeons gern Trains pullen, ist richtig übel, besonders wenn sie 
	einem Gegner durch den Dugeon folgen und auf dem Rückweg als Geschenk sämtliche anwesenden 
	Monster mitbringen. Pets also in Dungeons erst dann einsetzen, wenn man an der Stelle ist, wo 
	man bleiben will =)<br>
	Der Vorteil an den Pets ist, daß sie als einzige von allen Pets von Anfang an auch magische 
	Ziele, wie die Willowwisp attackieren und schädigen können. Man sollte den Pets auf keinen 
	Fall die Arbeit allein überlassen, denn sie sind lange nicht so stark, wie z. B. die Pets 
	der Necros. Wenn man also "out of mana" ist, sollte man mit Melee in den Kampf eingreifen. 
	Wenn sich abzeichnet, daß der Gegner nicht zu schaffen ist, dann sollte man sich in Gedanken 
	bei dem Pet bedanken und laufen *g*, denn bis das Pet tot ist, hat man vielleicht schon genug 
	Vorsprung =)<br>
	Achja, viele Gruppen mäkeln immer an den Pets rum, von wegen die würden Experience fressen. 
	Jeder sagt was anderes, aber soweit ich weiss, bekommen die Pets nur Experience, wenn sie den 
	letzten vernichtenden Schlag ausüben. Sicher bin ich mir nicht.... Auf jeden Fall sollte man 
	sich auf solche Diskussionen gar nicht einlassen, denn ohne das Pet ist die Überlebenschance 
	des Enchanters nicht wirklich hoch, wenn der Rest der Gruppe auf 3 Monster einprügelt und das 
	4. Monster sich auf den Enchanter stürzt...
</P>

