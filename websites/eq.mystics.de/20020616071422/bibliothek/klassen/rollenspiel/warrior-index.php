<P CLASS="P1">
	<I>&quot;Steh auf du Hund, und k&auml;mpfe wie ein Mann!&quot;</I>
</P>

<P CLASS="P1">
	Schwer atmend, das Schwert in der Faust, steht Gala von Wolfstein &uuml;ber den bleichen Mann gebeugt, der
	sich auf seinem Thronsitz zusammengekauert hat, so daß nur noch seine Nasenspitze aus dem Kragen seines
	schweren Mantels schaut.
	<I>&quot;Schlag ihn nieder! Was z&ouml;gerst du noch? Jeden Augenblick k&ouml;nnen die Wachen kommen!&quot;</I> dr&auml;ngeln Galas
	Gef&auml;hrten. <I>&quot;Er hat den Tod verdient! Hast du vergessen, daß er deinen Bruder auf dem Gewissen hat?&quot;</I>
	Gala wischt sich eine blutgetr&auml;nkte Haarstr&auml;hne aus dem Gesicht. <I>&quot;Und doch kann ich es nicht tun! Was
	wird sein, wenn ich dereinst vor das g&ouml;ttliche Tribunal trete? Soll es mit dem Finger auf mich zeigen
	und sagen: Du hast einen unbewaffneten Mann erschlagen...?&quot;</I>
</P>

<P CLASS="P1">
	Ohne Frage, in gewisser Weise ist der Krieger &quot;ein Mann f&uuml;rs Grobe&quot;, ein zuverl&auml;ssiger Freund immer dann,
	wenn es um handfeste Auseinandersetzungen geht, mit seiner schnellen Klinge unverzichtbar in vielen
	gef&auml;hrlichen Situationen.
	Spielen Sie den Krieger als einen selbstbewußten, ruhig etwas arroganten Charakter, der es gewohnt ist, daß
	sein Wort einem Befehl gleicht.
	Man hat den jungen Krieger in Stichwaffen, Klingenwaffen und stumpfen Waffen unterrichtet.
	Zudem hat er gelernt Verantwortung zu &uuml;bernehmen, das Kommando zu f&uuml;hren, wann immer es angebracht
	scheint. Auch Entschlußkraft und Tatendrang sollten zu den Wesensz&uuml;gen Ihres K&auml;mpfers geh&ouml;ren.
	Zaudern und zagen sollten ihm Fremdw&ouml;rter sein.
</P>

<P CLASS="P1">
	Selbstverst&auml;ndlich kann der Krieger seine Gef&auml;hrten durch seine Art auch oft genug in ernste
	Schwierigkeiten bringen: So etwa, wenn er darauf besteht, der 20-k&ouml;pfigen R&auml;uberbande offen, mit
	blanker Klinge, gegen&uuml;berzutreten. Man h&uuml;te sich, solchen &quot;Heldenmut&quot; als schiere Dummheit abzutun,
	denn eines sollte man nicht vergessen: Norrath ist eine Welt, in dem klassische Vorstellungen von
	Mut und Ehre herrschen. Oder f&auml;nden Sie einen Krieger besonders gelungen dargestellt, wenn er
	seine Feinde am liebsten durch ein schnell wirkendes Gift hinterr&uuml;cks aus dem Weg r&auml;umen w&uuml;rde?
</P>

<P CLASS="P1">
	Selbstverst&auml;ndlich gibt es gen&uuml;gend Situationen, in denen das Ziel besser auf einem Umweg, sprich
	durch List und T&uuml;cke, angestrebt werden sollte. Doch bis der Krieger so weit gereift ist, dass er
	seinen Stolz bez&auml;hmt und sein Verst&auml;ndnis von Edelmut und Ehrbarkeit der &quot;Wirklichkeit&quot; anpaßt, wird
	er manche schmerzhafte Erfahrung machen m&uuml;ssen.
</P>

<P CLASS="P0">
	<I>Carillion</I>
</P>

