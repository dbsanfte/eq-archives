<P CLASS="P1">
	Nun du bist also als Iksar in das Leben getreten. Sei dir bewusst, dass wir eigentlich die Beherrscher der Welt sein müssten, nur hat uns Verrat und die Klauen der gemeinen Drachen viel gekostet. Jedoch wir sind es die Schmerz gewohnt sind und wir werden wie Phönix aus der Asche ersteigen und unseren Kontinent von dem Getier das uns zusetzt zurückerobern.
</P>

<P CLASS="P5">
	Der Monk:
</P>
<P CLASS="P1">
	Sei dir bewusst du bist ein Swifttail. Ohne uns wäre alles den Bach hinunter gegangen. Durch unseren Glauben an Cazic Thule haben wir es unserem Volk vorgemacht, dass jeder Schmerz zu ertragen ist. Wir sind besonnen und handeln nie unüberlegt. Durch List und genaue Überlegung können wir selbst überlegene Gegner bezwingen. Schmerzen die andere zusammenbrechen lassen gleiten an unsrer überlegenen Konzentration herab. Es wird sogar gesagt, dass einer unsere Meister in brennender Lava lebt und den Schmerz besiegt hat.<br>
	Sei nie überheblich, aber sei dir deiner überlegenen Fähigkeiten und deinem überlegenen Intellekt jederzeit bewusst.
</P>

<P CLASS="P5">
	Der Shamane:
</P>
<P CLASS="P1">
	Du gehörst zu den geistigen Führern einer alten und starken Kultur. Ja wir wurden fast vernichtet, aber es ist an der Zeit den Spiess wieder umzudrehen und deine Verbindung mit der Geisterwelt wird dem Volk dienen um seine alte Macht wieder herzustellen. Darum verinnerliche deine Fähigkeiten bis du unserem Gott und den Geistern der hohen Welten nahe bist um uns mit deiner Weisheit zu dienen. Verliere niemals die Beherrschung, handle immer mit Ruhe und Effizienz. Sprich immer besonnen und lass dich nie zu abfälligen Bemerkungen hinreisen, denn die Schuppenlosen sind deiner Worte nicht würdig.
</P>

<P CLASS="P5">
	Der Warrior:
</P>
<P CLASS="P1">
	Du bist der kämpfende Rückhalt einer Kriegerkultur. Es wird dir nachgesagt, das du nahezu unvorstellbare Schmerzen ertragen kannst und deine Leichtfertigkeit im Umgang mit schweren Waffen ist unübertroffen. Selbst nackt hast du deine Schuppen als Panzer. Deine Lebensweise ist einfach und gezielt auf den Kampf ausgerichtet. Selbst unterlegenen Feinden lässt du den Funken der Hoffnung bevor du sie auslöscht um deinen Sieg vollkommen zu machen. Nicht so wie die Schuppenlosen vergehen wir uns in Saufgelagen, denn das könnte unsere Kampfkraft schwächen. Wir grölen auch nicht herum, wenn wir zuschlagen, dann eiskalt, schnell und tödlich.
</P>

<P CLASS="P5">
	Der Shadowknight:
</P>
<P CLASS="P1">
	Unser Gott hat dich nicht nur mit Kampfkraft sondern auch mit überlegenen geistigen Fähigkeiten ausgestattet. Als Mitglied der Crusader gehörst du zur Elite der Kampfkraft. Wo wir zuschlagen verbrennen wir die Erde und machen die getöteten Feinde zu unseren willenlosen Sklaven. Auch wenn manche sagen, daß wir etwas arrogant sind, so haben wir uns unsere Stellung verdient, denn jahrelange Entbehrungen und Training sind nötig um die Magie und den Kampf zur Perfektion zu führen. Deswegen können wir uns etwas Arroganz leisten, denn wir sind die besten. Jedoch lauschen auch wir den weisen Worten der Mönche und Shamanen und würden uns nie über diese lustig machen. Die Schuppenlosen jedoch sind nicht den Dreck an unseren Krallen wert und werden gnadenlos vernichtet.
</P>

<P CLASS="P5">
	Der Necromancer:
</P>
<P CLASS="P1">
	Wir haben es geschafft unsere Tradition selbst durch die Zeiten des Leids zu erhalten. Nicht wie die anderen Zauberer haben wir unsere Fähigkeiten behalten und sind stärker als je zuvor, denn im Gegensatz zu den Schuppenlosen können wir die Entbehrungen der Necromantie besser ertragen. Das ist es was uns weit über die Schuppenlosen stellt. Uns wird nachgesagt, dass wir schon zu mächtig sind. Ja es ist wahr, wer die Entbehrungen und Prüfungen des Lebens in unserem Orden meistert wird eines Tages einer der Mächtigen von Norrath und wenn wir genug sind werden wir losschlagen und alle Schuppenlosen in Schaaren von Kunark vertreiben und unseren Erzfeind Venril Sathir am Marktplatz pfählen.
</P>
	
<P CLASS="P1">
	Vergesst nie: Ihr seid Iksar, der Schmerz ist euer ständiger Begleiter, aber wir werden eines Tages wieder herrschen! 
</P>

