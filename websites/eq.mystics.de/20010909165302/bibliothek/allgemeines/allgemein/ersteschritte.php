<P CLASS="P1">
	Der Anfang ist wie in jedem Spiel nicht leicht. In EverQuest startet man nur mit einer
	Newbie Ausrüstung, diese Ausrüstung ist nicht zu verkaufen und man kann sie nicht
	ablegen, aber zerstören. Die Ausrüstung besteht aus:
</P>

<UL CLASS="P0">
	<LI>Eine Quest Scroll:<br>
	    Auf dieser Schriftrolle steht, wo man seinen Guildmaster findet, diesen sollte man als allererstes aufsuchen und ihm
			die Schriftrolle geben. Von ihm bekommt man etwas Erfahrung und eine Robe oder Tunic.</LI><BR><BR>
	<LI>Ein PK Buch:<br>
	    Das Buch muß man dem Priest of Discord geben, um +PvP zu werden.<BR>
			<U>Anm.:</U> Sollte man sich sehr gut überlegen, dieses Buch abzugeben, da man dadurch eigentlich nur Nachteile hat! Hat man es aber abgegeben und will es rückgängig machen, so geht das auch, indem man eine /petition schreibt. Meist ist dann eine kleine Quest zu erfüllen und der PvP-Switch wird wieder auf No-PvP gestellt.</LI><BR><BR>
	<LI>Eine Waffe:<br>
	    Die Art der Waffe ist von der Klasse und Rasse abhängig.</LI><BR><BR>
	<LI>Je 5 Essen und Trinken</LI><BR><BR>
</UL>

<P CLASS="P0">
	Spellcaster haben dazu noch 2 Spells. (Paladin, Ranger, Shadowknight und Bard starten
	<B>ohne</B> Spells/Songs).
</P>

<P CLASS="P0">
	Da sich die Klassen sehr unterscheiden hier nur einige Tips, die für alle Klassen gut sind.
</P>

<P CLASS="P1">
	Da man ohne Geld startet, sollte man sich erst einmal darum kümmern, das kann man auch gut
	alleine. Einfach hinaus gehen und kleinere Tiere jagen z.B. Snakes, Bats, Decaying
	Skeletons. Wer lieber in einer Gruppe jagen will, sollte sich mit ihr über die Verteilung
	der Beute einigen.  
</P>

<P CLASS="P1">
	Bis Level 4 kann man auch testen, mit welchen Monstern man sich anlegen kann, da man bei
	einem Tod nur die Ausrüstung (Newbie Items behält man) verliert. Tip: Bei einem Rechtsklick
	auf ein Gegner bekommt man eine Einschätzung, ob man eine Chance gegen ihn hat.
</P>

<P CLASS="P1">
	Um EXP zu bekommen, sollte man sich einer Gruppe anschließen, so kann man auch besser
	Monster töten und bekommt schneller EXP und hat auch mehr Spaß. Zudem sollte man auch
	Monster suchen, die bei einem Rechtsklick blau dargestellt werden und einen
	"...looks like a kind risky..." Text haben, diese sind zwar nicht leicht, aber meistens
	zu überwinden.
</P>

<P CLASS="P1">
	Gelb dargestellte Gegner sollte man nicht unbedingt angreifen, da man diese normalerweise
	nicht alleine schafft.
</P>

<P CLASS="P1">
	Einige Items, die man bei Monstern findet, sollte man nicht verkaufen, da man sie besser
	anderweitig benutzen kann, z.B. Pelze, die kann man mit Tailoring verarbeiten oder an
	Tailor verkaufen. Einige Monster (z.B. Deathfist Orcs) hinterlassen auch Trophäen (z.B.
	einen Gürtel) die man einem NPC geben kann um seine Stellung in einer Fraktion zu
	verbessern. 
</P>

<P CLASS="P1">
	Viele Gegenstände die erst nutzlos aussehen, könnten von einem anderen Spieler gesucht
	werden, da sie für seine Klasse Quest Items sind. Als Beispiel währen hier die Putrid
	Rib Bones genannt, diese können die Monks in Qeynos meistens gut gebrauchen da es ein
	Quest Item ihrer Gilde ist. 
</P>

<P CLASS="P1">
	Als Krieger sollte man sein Geld als erstes in eine Lederrüstung und eine bessere Waffe
	investieren. Magiebegabte Klassen sollten sich erst Spells anschaffen und dann um
	Kleidung/Rüstung kümmern.
</P>

<P CLASS="P1">
	Bei seinem Guildmaster kann man seine Skills trainieren, dazu benötigt man Trainingspunkte
	und später auch Geld. Am Anfang sollte man sich dort seinen Waffenskill trainieren lassen
	(Tip: Für Magier, besorgt euch einen Staff oder Great Worn Staff und trainiert 2H Blunt).
	Ansonsten ist es schwer am Anfang Monster zu treffen.
</P>

