<P CLASS="P0" ALIGN="CENTER">
	Geschrieben von Tedadar Flammenpfeil<BR>
	<BR>
</P>

<P CLASS="P1">
<b>3.Das Licht muss weichen</b>
</P>

<P CLASS="P1">
Ein Einschnitt im Gebirge. Es wurde langsam Nacht, vier Gestalten rannten eilig den Pass
entlang, den Blick immer wieder hastig über die Schulter werfend. Magische Explosionen
detonierten überall und kleine Gesteinsbrocken lösten sich. Inmitten des Chaos aus Flucht
ein Paladin und seine drei Mitkämpfer. Keine Spur des Kampfwillens ist in ihren Augen zu
sehen, wilde Flucht hat die Gruppe ergriffen. Valeria, der Paladin, der am Schluss der
Gruppe rennt um die Angriffe auf sich zu lenken, mag noch immer nicht glauben was er auf
der anderen Seite erlebt hat. Es begann doch so einfach und der Vormarsch war zuerst gar
nicht zu stoppen. Noch einmal gingen ihm die Ereignisse durch den Kopf ...
</P>

<P CLASS="P1">
Der Dunkelelf, der seine Füsse aus den Spalten im Boden befreien konnte rannte verzweifelt
der zu allem entschlossenen Gruppe davon. Als ihn ein Pfeil traf strauchelte er, fiel hin,
aber er konnte sich noch hochraffen bevor noch die Gruppe ganz an ihn herankommen konnte.
Tedadar sprach einen Spruch, magische Energie floss in seinen Körper. Er konnte fühlen wie
sich die Kraft in seinen Fingern entlud, und die Haut des Dunkelelfens begann zu brennen.
Aber der Dunkle Elf gab nicht auf. Er rannte noch ein gutes Stück durch den Pass, bevor
sich unter seinen Füssen der Boden wieder aufmachte und ihn gefangennahm. 
</P>

<P CLASS="P1">
Da war auch schon die Gruppe heran, vor denen er zu flüchten versuchte, und schlug mit
verheerender Kraft auf den Sterbenden ein, bis er regungslos in einer immer grösser
werdenden roten Lache liegenblieb.
</P>

<P CLASS="P1">
<I>Hurra!! Noch einer!!!</I>, sprach freudig Tedadar. <I>Und nicht der Letzte!!</I> sagte
Valeria der sein Schwert wieder in die Scheide zurücksteckte. <I>Wir sind aber nun auf der
anderen Seite des Gebirges, wir sollten aufpassen</I>, sprach mit wachsamen Blick Tedadar
und er drehte sich herum um mögliche Gegner ausfindig zu machen. <I>Die trauen sich
bestimmt nicht mehr hierher. Wir sollten ein leichtes Spiel haben, aber gehen wir wieder
etwas in das Gebirge hinein</I>, wand der Paladin ein, und sie schritten alle tiefer in
die Schlucht zurück. <I>DA! ein Darkie!!</I> schrie jemand aus der Gruppe, <I>Los,
ANGRIFF!!</I>, sprach Tedadar und sprang auch schon gefolgt von den anderen los um noch
einen der Dunkelhäutigen zu erlegen. Der Dunkelelf sah sie aber kommen und rannte davon
in das Innere des Landes. <I>Wir sollten uns nicht zu weit in das Land vorwagen. Wir sind
keine besonders starke Gruppe</I>, sagte Tedadar. Ein Blick auf die Gruppe sagte, daß er
zum Teil recht hatte. Ein paar Mitglieder waren noch nicht so erfahren wie der Paladin und
der Ranger, auch wenn sie bald soweit sein mochten. Valeria stimmte mit einem Kopfnicken
zu und sie zogen sich wieder zurück. Wie eine Schlange die auf ihr nächstes Opfer wartet
lauerten sie in dem Canyon als sie auf einmal einen Schrei hörten: <I>LIGHTIES IN UNSEREM
LAND! KOMMT BRÜDER UND SCHWESTERN, WERFEN WIR SIE EIN FÜR ALLE MAL RAUS, DAMIT SIE NIE
WIEDER EINEN FUSS AUF UNSER LAND SETZEN!!!!</I> Ein unruhiges Gefühl ging durch die
Gruppe, der Ranger zuckte zusammen, und er hoffte insgeheim, daß der Rufende keinen Erfolg
hatte Freunde zu finden die ihm halfen. Denn gegen einen ernstgemeinten Widerstand konnten
sie soviel ausrichten, wie ein Tropfen Wasser in der Wüste. <I>Da hat sich wohl gerade
unser Freund von vorher bedankt, aber so bekommen wir wenigstens was zu tun!</I>, meinte
noch der Paladin bevor er sein Schwert zog und aufmerksam die Gegend beäugte.
</P>

<P CLASS="P1">
Still und ruhig vergingen die nächsten Minuten. Es war aber eine unnormale Stille, nicht
einmal Tiere oder Vögel machten mehr einen Laut, als ob die Umgebung die Luft anhielt. Da
tauchte wieder ein Dunkelelf auf einem Hügel auf. <I>Hehe, da ist einer</I>, sagte Tedadar,
und mit einem Lächeln fügte er hinzu, <I>Da ist die Truppe des Feindes!</I>
</P>

<P CLASS="P1">
Der Dunkelelf kam näher und blieb etwa 20 Meter vor ihnen stehen, als er auf einmal etwas
unverständliches sprach und eine braungrüne magische Wolke Tedadar einhüllte. Von einer
Sekunde zur anderen fühlte sich Tedadar langsam und schwach, als ob ihm alle Kraft geraubt
worden war. Kaum war er sich seines Zustandes bewusst traf ihn noch eine magische
Entladung, aber der Rest der Gruppe war schon an dem Necromantendunkelelf heran und hieb
auf ihn mit schweren Schlägen ein. Ein blaues magisches Licht streifte Tedadar sanft und
er fühlte wie seine Kraft wiederkehrte. Er warf McWolf dem Heiler einen dankbarem Blick
zu, bevor er sich auch auf den Elf stürtzte. Der Dunkelelf würde sterben, lächerlich zu
glauben er würde mit seinem Angriff die Gruppe aufhalten können, als auch ein blauer
Schimmer über die Haut des bösen Elfen zog und ihn wieder stärkte. <I>Wer zur Hölle hat
ihn geheilt</I>, sprach Valeria. <I>Falsches Ziel, uns heil......</I>, stockte er aufeinmal
als er aufsah. Eine Dunkelelfin stand auf dem Hügel und sprach nocheinmal die magischen
Worte um den Necromanten zu heilen, aber sie war nicht allein. Ein gutes Dutzend anderer
Dunkelelfen und auch einzelne Trolle standen auf dem Hügel und verfolgten das Treiben der
Gruppe der Menschen, Elfen und Zwerge. 
</P>

<P CLASS="P1">
<I>Argh, lauft um euer Leben!</I>, schrie der Paladin und die Gruppe wandte sich ab um
davonzurennen. Tedadar bildete den Schluss, da er noch immer geschwächt vom Necromant
nicht gleich seine volle Geschwindigkeit erreichte. Da brach die Hölle los, die Dunkelelfen
die bisher gewartet hatten, griffen an,... der Ranger wurde gleichzeitig abgekühlt und
erhitzt, er glaute schon seine Haut würde nun einfach platzen als sich zu allem Übel
noch der Boden aufmachte und seine Füsse bis an die Knie verschlang. Schwerter fuhren
über seinen Leib, er konnt nicht mehr alle Schläge parieren oder ihnen ausweichen, und
aus etlichen blutenden Wunden sickerte sein Leben heraus. Unter dem Hagel des Angriffes
brach er tödlich verletzt zusammen, und noch bevor ihn die Dunkelheit umhüllte, die ihn
endlich vom Schmerz befreien würde, hörte er die böse Meute rufen: <I>Ihnen nach, nun
schlagen wir zurück!!</I>
</P>

<P CLASS="P1">
So rannten die 4 verbliebenen der Gruppe weiter durch den Canyon als plötzlich der Angriff
auf sie stoppte,... keine magischen Feuerbälle oder Pfeile eilten ihnen mehr nach.
<I>Scheinbar trauen sie sich nicht durch den Canyon hindurch</I> sagte der Paladin,
<I>lasst uns hier neue Kräfte sammeln!</I>
</P>

<P CLASS="P1">
Die Dunkelheit verging. Der Kälte des Abends nackt ausgesetzt erwachte Tedadar auf dem
Hinterhof wo durch Magie seine Seele gebunden war. Verdammt, die haben mich erwischt!
,dachte Tedadar, und jetzt muss ich meine Sachen wiederholen. Wenn ich nur an meinen
Körper komme vor lauter Feinden. Nicht sehr aufgebaut durch diese Gedanken lief er los und
rannte auch schon durch die Stattore von Freeport in Richtung des Gebirges, das sich in der
Dunkelheit kaum vom Nachthimmel unterschied. 
</P>

<P CLASS="P1">
Einige Minuten später traf er auf seine Gruppe, die vor dem Gebirge rastete. <I>Dein Tod
tut mir leid</I>, sagte Valeria, <I>aber die Darkies haben ihren Angriff gestoppt als wir
aus dem Canyon heraus waren. Warscheinlich trauen sie sich nicht hinaus</I>, aber Tedadar
antwortete: <I>Ich glaube sie haben was vor,.. passt bloss auf. Ich versuch reinzugehen
und meine Sachen aufzusammeln!</I> Mit diesen Worten rannte er los und in den Einschnitt im
Gebirge hinein. 
</P>

<P CLASS="P1">
Der Ranger lief immer tiefer in die Schlucht. Er musste schon den halben Pass überwunden
haben, als hinter einer Biegung das wohl schockierendste Bild seines Lebens wartete.
Mindestens 2 Dutzend Dunkelelfen und Trolle waren zusammengekommen und rasteten im Canyon.
Wie es Valeria draußen vor dem Gebirge getan hatte. Einer der bösen Elfen sah zu Tedadar
hinüber. <I>Na, wen haben wir da? Wenn das net unser Stopper von vorher war</I> sprach er,
und kaum als er ausgesprochen hatte traf Tedadar wieder dieser schwächende braungrüne
Nebel. <I>Ich will nur meine Sachen wieder holen und dann einfach gehen!</I> sagte Tedadar
verzweifelt, den immer mehr die Schwäche überkam. <I>Ihr habt mich schon einmal besiegt!</I>
... langsam ebbte der Angriff ab, der noch gar nicht zu seiner vollen Kraft gekommen war.
Die Trolle setzten ein paar Schritte zurück und gaben einen Weg auf den verstümmelten Körper
des Rangers frei. Unsicher ging Tedadar vor und begann seine Habseligkeiten von seinem
Körper zu nehmen, wenn man diesen zerhauenen Brocken Fleisch überhaupt so nennen konnte.
Dabei packte er aber die wertvolleren Sachen in eine Tasche und ging allgemein sehr langsam
vor. Nur 2 der dunklen Elfen beobachteten ihn noch. Für den Rest der Horde hatte er an
Interresse verloren. <I>Nun sollten wir langsam aufbrechen und schauen ob sich noch mehr
Lighties auf der anderen Seite aufhalten</I> sagte einer der Dunkelelfen in der dunklen
Sprache der bösen Rassen. Tedadar war wieder froh darüber, dass er diese Sprache schon
lange beherrschte, und das Gespräch verfolgen konnte. <I>Und dann belagern wir Freeport,...
die werden es bereuen uns angegriffen zu haben,... nun ist die Zeit gekommen um
zurückzuschlagen Brüder und Schwester!!!</I>
</P>

<P CLASS="P1">
<I>Valeria!! Pass auf! Die Darkies kommen gleich zu euch. Es sind verdammt viele, mehr als
20 Stück,...zieht euch zurück!!</I>, sprach in Gedanken Tedadar zu dem Paladin und stand
dann auf um mit schnellen Schritten von der Horde zu entfliehen bevor sie ihn doch wieder
als Ziel betrachteten. 
</P>

<P CLASS="P1">
Doch kaum dass er aus dem Canyon heraus war konnte er schon die Kampfschreie der Trolle
hören, und er wusste das die Darkies die Commonlands einnehmen würden. Beschleunigt durch
seine Angst rannte er auf ein paar abgelegene Hütten zu und ging in eine hinein um sich
ruhig zu verstecken.
</P>

<P CLASS="P1">
Überall konnte er die Kämpfe hören. Die Dunkelelfen brachen mit unglaublicher Kraft über
die Ebene hinein und töteten alle die nicht auf ihrer Seite waren. Immer wieder hörte man
das letzte Aufschreien unschuldiger Menschen die eigentlich nichts mit diesem Kampf zu tun
hatten. Aber die Lawine rollte und die dunklen Völker machten keinen Unterschied, wer der
ursprüngliche Angreifer war oder wer nur durch die Länderein laufen wollte. Aber wir haben
ja auch nie jemanden gefragt ob er kämpfen oder nur vorbeilaufen will. Auch wir haben alles
getötet was wir konnten, dachte Tedadar. Und bevor er seine Gedanken weiterführen konnte
wurde die Tür zu seiner Hütte aufgerissen und Tedadar schrak hoch, seine Waffen kampfbereit
gezückt als Sternenfunke, eine Eruidmagierin, in den Raum hereinhastete. <I>Du hast mich
ganz schön erschreckt</I>, sagte Tedadar und Sternenfunke antwortete: <I>Da bist du ja,
gut. Ich dachte die Dunkelelfen hätten dich nochmal erwischt. Aber McWolf hatte nicht
soviel Glück.</I> Tedadar schwieg betroffen. Für die Dunkelelfen musste der noch etwas
junge Heiler eine willkommene Beute sein. <I>Und was ist mit den Anderen?</I> fragte er.
<I>Der Rest ist nach Freeport gelaufen, gefolgt von einer Truppe Dunkelelfen</I> sagte
sie. <I>Es scheint so als ob sich das Blatt gewendet hat,... es ist nun nicht mehr unser
Raid.</I> Tedadar nickte nur und fragte sich wie es wohl gerade den anderen ergehen
mochte. Er hoffte, dass sie wenigstens noch sicher durch die dicken Mauern in Freeport
gekommen waren ohne von den Darkies erwischt zu werden. Um diese Falle zu knacken würden
sie wohl Hilfe brauchen,....
</P>

