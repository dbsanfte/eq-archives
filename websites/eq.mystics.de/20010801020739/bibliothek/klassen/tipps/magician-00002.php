<P CLASS="P1">
	Das Pet ist der beste Freund des Magican, allerdings wird es nie einen Tank richtig 
	ersetzen können. Achtet immer darauf, dass sich das Pet gut erholt, bevor ihr zum 
	nächsten Kampf übergeht. Castet auch immer einen Damage Shield auf euren kleinen Freund, 
	allerdings vor dem Kampf, der Gegner mag euch sonst nämlich nicht mehr und knabbert euch 
	noch an und ausserdem ist das verschwendete Zeit, die man mit einem DD-Spell sinnvoller 
	verbracht hätte. Gebt acht auf die Burnout Serie, die Pets hauen dann zwar stärker zu und 
	haben mehr AC und max. HPs, aber sie regenerieren sich auch bedeutend langsamer! Plant 
	das mit in eure Strategie ein. Wenn ihr ein Pet erschaffen habt, macht sofort "/pet follow 
	me" guard ist ein absolut unnützer Befehl, wenn ihr reisen solltet, und in der Nähe 
	mehrerer Gegner erst recht. Es hetzt sich dann alle Gegner um dich rum auf den Hals und 
	wer dann rennen darf ist ja wohl klar, solltest du nicht schnell gute Hilfe erhalten ;o) !
</P>

