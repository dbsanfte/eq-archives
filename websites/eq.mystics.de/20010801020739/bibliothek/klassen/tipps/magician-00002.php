<P CLASS="P1">
	Nun habt ihr also das nächste Level erreicht. Meinen Glückwunsch. Ab nun wird es immer schwerer 
	ein neues Level zu erreichen, allerdings sollte es kein Problem sein, innerhalb von ca. 
	3 bis 4 Stunden Level 4 zu erreichen. Die Trainingspunkte, die ihr nun bei eurem 
	Guildmaster erhaltet könnt ihr teilweise auf euren Hauptangriffsskill legen. Also 
	entweder Piercing, was ich euch nur empfehlen kann - es gibt einige wunderbare
	Dolche für Magier- , 1 H Blunt, wobei ihr sicherlich später ein wenig Spass an einem 
	Staff of Writhing haben werdet. Oder ihr investiert ein wenig in 2 H Blunt, einige 
	nette Stäbe, aber auch die Stäbe die ihr später beschwören könnt, sind 2 H Blunt. 
	Die anderen Trainingspunkte solltet ihr aufheben und später a) in eure Tradeskills 
	investieren, bzw. einige für Research aufheben, oder einige von ihnen auf eure 
	Hauptmagieskills wie Evocation legen, allerdings ist hier bei Skill 20 Schluss. 
	Zuviele solltet ihr nicht verschwenden, vor llem nicht bei Skills, die sowieso schnell 
	steigen wie zum Beispiel Evocation.<br><br>
	An neuen Spells sollte sich der Magican grundsätzlich 2 Spells zuerst holen.
</P>
<OL CLASS="P1">
	<LI>das jeweilige Pet<BR></LI>
	<LI>den jeweilig besten DD-Spell<BR></LI>
</OL>
<P CLASS="P1">
	Danach kommen die Spells je nach Geschmack und Vorhandensein des Geldes, ich persöhnlich 
	besorge mir wenn ich das Geld habe immer alle, manche lassen einige aus, was ich aber 
	nicht für sinnvoll halte, da man nie weis, wofür man den Spell irgendwann man 
	gebrauchen kann.
</P>

<P CLASS="P1">
	Hier eine kleine Liste von sehr brauchbaren Spells von Level 1 und 4.
</P>
<UL CLASS="P1">
	<LI>Level 1:<br><br>
		- Burst of Flame -- der erste DD den man bekommt (als Grundausstattung).<br>
		- Minor Shieldung -- der erste Shield Spell, ebenfalls Grundausstattung.<br>
		- Summon Food -- Der Name sagt alles, spart viel Geld! Der erste Spell den man kaufen sollte.<br>
		- Summon Drink -- Spart ebendfalls massenhaft Geld! Der zweite Spell.<br><br>
	</LI>
	<LI>Level 2:<br><br>
		- Burn -- ein nützlicher neuer DD-Spell.<br>
	 	- Petspell -- der wichtigste Spell eines Magican.<br>
		- Gate -- für die Lauffaulen unter uns, teleportiert zum Homepoint<br>
	</LI>
</UL> 

