<P CLASS="P1">
	Es gibt verschiedene Rassen, die Magicans ausbilden können. Es sind eigentlich, bis auf 
	die Menschen, immer recht intelligente Rassen, auch wenn sie teilweise dunkler Gesinnung 
	sind.<br>
	Allgemein gilt, je höher die Intelligenz der Rasse, desto mehr Mana hat man. Also ist 
	es oberste Priorität, seine Bonuspukte in die Int zu packen. Bis zum maximalen 
	Attributcap verbraucht man fast alle bis auf etwa 5 Bonuspunkte, diese sollte man 
	in eine der schwächeren Attribute einfliessen lassen.
</P>

<UL CLASS="P1">
	<LI>
		<i>Dark Elf</i>, die dunklen Geschwister der Hochelfen, hochintelligent aber böse 
		von Grund auf. Ihr grosser Vorteil ist, dass sie Hide von Anfang an besitzen, was beim 
		meditieren von unschätzbarem Wert ist.
	<BR></LI>
	<LI>
		<i>Erudite</i>, die intelligenteste Rasse in ganz Norrath, jedoch zu nichts anderem zu 
		gebrauchen, da sie gebrechlich wie ein trockener Strohhalm sind. Ausserdem haben sie, wie 
		keine menschliche Rasse, Nachtsicht, was ein nicht zu unterschätzender Nachteil ist!
	<BR></LI>
	<LI>
		<i>Gnome</i>, ein wahrlich kleines Volk, das kleinste, aber diesen körperlichen 
		Nachteil, wenn es den ein Nachteil ist, von Gegner übersehen zu werden, gleichen sie 
		durch ihr grosses Potential wieder aus. Ausserdem sind sie die erfinderischsten in der 
		ganzen Welt, sei es auf oder unter Norrath. Mein persöhnlicher Favorit :o)
	<BR></LI>
	<LI>
		<i>High Elf</i>, die Charismatischste Rasse in ganz Norrath, jedoch mit den Eruditen 
		und den Dunkel Elfen auch die eingebildetsten. Sie sind eine recht gut geeignete Rasse 
		für Anfänger, da sie nur wenig Nachteile bis garkeine Nachteile mit sich bringen.
	<BR></LI>
	<LI>
		<i>Human</i>, die wahrscheinlich durchschnittlichste Rasse in ganz Norrath. Kein 
		sonderlich hochfliegender Geist wie die Eruditen, dafür aber auch nicht so 
		gebrechlich, nicht so charismatisch wie die Hoch Elfen, dafür aber auch nicht so 
		hässlich wie die Trolle... Ihr grosses Manko ist, dass sie keine Nachtsicht 
		haben und für Anfänger eigentlich nicht sonderlich geeignet sind, da sie nicht 
		spezialisiert sind, also kein so volles Manakonto kriegen wie andere Rassen.
	<BR></LI>
</UL> 

