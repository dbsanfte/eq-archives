<P CLASS="P1">
	Man startet in der von einem selbst gewählten Heimatstadt mit nichts anderem als mit den 
	folgenen Utensilien:
</P>

<UL CLASS="P1">
	<LI>Je 5 Portionen Wasser und Brot<BR></LI>
	<LI>Ein Dagger<BR></LI>
	<LI>Eine Quest Scroll<BR></LI>
	<LI>Ein Tomb of Order and Discord<BR></LI>
	<LI>Eine Lesser Shielding Scroll<BR></LI>
	<LI>Eine Burst of Flame Scroll<BR></LI>
</UL> 

<P CLASS="P1">
	Als erstes sollte man sich rüsten, also den Dagger in den Melee-Slot packen und die Spells 
	scriben und dann memorizen. Dazu öffnet man sein Spellbook und zieht die Spells auf einen 
	der freie Slots. Sind sie gescribed, kann man sie memorizen, dazu zieht man die Spells aus 
	dem Spellbook in einen der freien Slots auf der linken Seite.<br><br>
	Als nächstes sollte man sich seine Umgebung anschauen und seinen Guildmaster suchen. Die 
	Quest Scroll, die ihr im Inventory habt, gibt euch einen kleinen Hinweis, wo er ist. Wenn ihr 
	ihn gefunden habt, gebt ihm die Scroll um eure Guildrobe zu erhalten und ein wenig Exp zu 
	kriegen. Ihr werdet damit sozusagen in eure Gilde aufgenommen. Schaut euch nun das 
	Angebot an, was ihr bei eurem Guildmaster lernen könnt. Legt als erstes alle eure 
	Trainingspunkte auf den Piercing Skill. Dies ist momentan der wichtigste Skill, den ihr habt, 
	denn die Spells fizzeln anfangs recht oft und ihr könnt die Monster noch nicht alleine mit 
	euren Spells töten, ihr erstecht sie dafür.<br><br>
	Ihr könnt euch auch schonmal bei den Vendors um euren Guildmaster umschauen, wo ihr die 
	Spells für dieses Level und für Level 4 bekommt. Nun macht ihr euch auf den Weg nach 
	draussen, vor die Stadttore. Wenn ihr den Weg nicht so schnell findet, was in einer grossen 
	Stadt wie in Neriak oder Freeport leicht passieren kann, dann fragt einfach einen der überall 
	rumlaufenden anderen Spieler, ob sie euch den Ausgang zeigen können.<br><br>
	So, nun seit ihr also vor der Stadt. Die Weite ist erstmal immens und alles sieht nicht sehr 
	vertraut aus, ihr solltet euch jetzt daran machen, die ersten Tiere zu erlegen. Nehmt ein Tier 
	als Target und considert es ( einfach C drücken ) es sollte ein schwarzer Text kommen. 
	Es gibt unterschiedliche Texte, ich erkläre jetzt hier nur kurz die Farben:
</P>

<UL CLASS="P1">
	<LI>
		<i>Rot</i>, greift diese Ziel bloß nicht an, nur in einer starken Gruppe kann man es erlegen, 
		es ist euch um einiges überlegen! Ihr erhaltet aber gute Erfahrung.
	<BR></LI>
	<LI>
		<i>Gelb</i>, dieses Ziel ist euch überlegen, aber mit ein bischen Hilfe oder viel
		Glück könnt ihr es auch alleine erlegen. Besser ist es allerdings mit Hilfe einer 
		Gruppe.Ihr erhaltet recht gute Erfahrung.
	<BR></LI>
	<LI>
		<i>Schwarz</i> ( manche sagen auch weis ), dieses Ziel ist euch ebenbürtig, allerdings sind die 
		Monster immer etwas stärker als die Spieler, für die es schwarz ist. Aber in niedrigeren 
		Leveln kein Problem. Alleine erhält man vom Zeitaufwand gesehen die beste Erfahrung.
	<BR></LI>
	<LI>
		<i>Blau</i>, dieses Ziel ist euch leicht unterlegen. Ihr könnt es sehr einfach alleine töten. Ihr 
		erhaltet noch Erfahrung.
	<BR></LI>
	<LI>
		<i>Grün</i>, dieses Ziel ist euch ein ganzes Stück unterlegen. Erfahrung erhaltet ihr nicht mehr, 
		also verschwendet eure Zeit nicht mehr damit.
	<BR></LI>
</UL> 

