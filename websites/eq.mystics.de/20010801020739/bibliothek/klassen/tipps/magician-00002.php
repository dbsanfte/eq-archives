<P CLASS="P1">
	Im allgemeinen will ich euch darauf hinweisen, dass der Magier kein Ersatzwizard ist. 
	Seine Stärke liegt nun einmal in seinem Pet, mit dem er in Verbindung einiger sehr guter 
	Damageshields und der recht guten (nach dem Wizard die 2. stärksten) DD-Spells ein recht 
	guter Solo als auch Support-Spieler ist, aber auch in seinem Summon Spells, die ihn zu 
	einem hervorragendem Versorgungs-Charkter machen. Ein Magican verzichtet NIEMALS auf 
	seine Summon Spells, sonst ist er nur eine abgemagerte Version des Wizards, allerdings 
	nicht mit der gleichen Vernichtungsmacht und auch nicht mit den Reisespells, die einen 
	Wizard so attraktiv machen.<br>
	Also, wer auf seine Summon Spells verzichten will, der sollte sich lieber einen Wizard 
	nehmen, der kann besser Sachen ausradieren, oder wer denoch ein Pet haben will, der soll 
	sich einen Enchanter machen.
</P>

