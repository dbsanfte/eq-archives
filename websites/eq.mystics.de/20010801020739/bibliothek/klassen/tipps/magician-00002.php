<P CLASS="P1">
	Jeder Magican hat die Wahl zwischen 4 verschiedenen Arten von Pets. Sie unterscheiden 
	sich nicht nur durch das Aussehen, sondern auch durch ihre Kampfkraft und 
	Geschicklichkeit, aber auch durch den Spruch den sie casten. Die 4 verschiedenen 
	Arten und ihre stärken und schwächen sind folgende:
</P>
<OL CLASS="P1">
	<LI><i>Das Earth Elemental</i><br>
		Dieses Elemental ist das stärkste Elemental, was existiert. Es hat die grösste Kraft und 
		die höchste Anzahl von Hitpoints, ideal um einen Low-Level Tank zu ersetzen. Allerdings geht 
		dies zu ungunsten der Beweglichkeit und Geschicklichkeit, weswegen es eigentlich immer 
		getroffen wird. Doch, es ist meistens das Elemental, was am längsten hält. Es ist 
		das ideale Support Pet für harte Kämpfe.<br><br>
	</LI>
	<LI><i>Das Air Elemental</i><br>
		Dieses Elemental ist das schwächste Elemental und fast das direkte Gegenteil des Earth 
		Elementals. Es hat die niedrigste Stärke, dafür aber die höchste Geschicklichkeit und 
		Beweglichkeit, was es für den Gegner schwerer zu treffen macht. Seine Hitpoints halten 
		bei weitem nicht mit denen eines Earth Elementals stand, jedoch, wer weniger getroffen 
		wird, der braucht auch nicht so viele. Sein grösster Vorteil ist es allerdings, dass es 
		sich Unsichtbar machen kann. Manchmal aber auch ein Nachteil! Recht gut für Solokämpfer...<br><br>
	</LI>
	<LI><i>Das Fire Elemental</i><br>
		Dieses Elemental ist das Elemental mit den meisten Nachteilen. Es ist zwar nicht 
		so schwach, wie das Air Elemental, doch auch nicht wesentlich stärker, bei 
		Geschicklichkeit und Beweglichkeit hinkt es allerdings weit hinterher und 
		wird nur noch von Earth Elemental unterboten. Sein Schutz ist ein FireShield, 
		den es casten kann, jedoch macht der auch nicht so viel Schaden, als das er es 
		gross herausreissen könnte. Gut für den Angriff auf mehrere unterlegene Gegner 
		(wegen dem DMG-Shield)<br><br>
	</LI>
	<LI><i>Das Water Elemental</i><br>
		Dieser Typ Elemental ist der ausgewogenste. Es liegt bei allen Stats nur unweit unter 
		den der Gruppenführern, und ist auch nicht schlecht mit Hitpoints bestückt. Diese 
		gehen zwar ein wenig schneller weg, aber durch seinen Bonus, die erhöhte 
		Regenerationsrate macht es dieses Manko leicht wieder wett. Dieses Pet 
		ist ideal, wenn es darum geht, schnell mehrere kleine Gegner auszuschalten.<br><br>
	</LI>
</OL>

