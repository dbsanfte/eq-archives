<P CLASS="P5">
	Kampf in Gruppen:
</P>

<P CLASS="P1">
Ein Nekromant ist kein Tank, der in der ersten Reihe steht und mitkämpft, vielmehr
steht er weiter weg vom Geschehen und zaubert seine Angriffsprüche. Sein Pet sollte
jedoch in der ersten Reihe mitkämpfen. Wie das geht? Siehe oben. Außerdem könnt ihr
Monster gut anlocken, da eure Sprüche eine gute Reichweite haben.
</P>

<P CLASS="P1">
So, ich hoffe der Einblick in das Leben eines Nekromanten hat euch wenigstens ein 
bißchen Gefallen.
</P>

