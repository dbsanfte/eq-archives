<P CLASS="P1">
	Begebt euch vor euere Stadt und geht am besten erst einmal nicht zu weit weg,
	so könnt ihr im Notfall zu einem Guard rennen und so euere Leben retten lassen.
	Tut Minor Shielding drauf und sucht euere ersten Opfer. Klickt auf das Monster
	euere Wahl und dann "C" (Consider); so erfahrt ihr die Stärke des Monster bzw.
	euere Überlebenschancen. Wenn ihr genug Beute zusammen habt, rennt in die Stadt,
	verkauft die Sachen und legts auf die Bank. Sobald ihr 1Gp 2SP 5CP *G* zusammen
	habt, kauft euch euer erstes Pet. Bis dahin solltet ihr auch schon Level 2 sein.
</P>

<P CLASS="P5">
	Levelanstieg:
</P>

<P CLASS="P1">
	Habt ihr Level 4 erreicht, könnt ihr euch neue Zauber kaufen, geht dabei immer wie
	folgt vor: Zuerst das Pet, dann den Vampirgriff und dann erst den Rest.
</P>

<P CLASS="P5">
	Kampf alleine:
</P>

<P CLASS="P1">
Eigentlich solltet ihr immer mit Pet kämpfen, aber wenn ihr mal keine Knochen
habt, beginnt ihr den Kampf mit einem Spruch wie Clinging Darkness, drückt "a"
für Auto-Attack und rennt auf das Vieh zu. Wenn euch der Gegner trifft, zaubert
ihr einen Vampirgriff, wie Life-Tap. Jetzt habt ihr den richtigen Rythmus, so
daß der Spruch nicht mehr so oft fizzled.
</P>

<P CLASS="P5">
	Kampf mit Pet:
</P>

<P CLASS="P1">
Wie oben schon erwähnt sollte das euere Hauptkampfmethode sein. Markiert eueren
Gegner, zaubert einen Spruch wie Clinging Darkness, aktiviert AUTO-Attack und 
rennt auf das Monster zu bis ihr nahe genug dran seit. Euer Pet greift auto-
matisch mit an. Wenn euere HPs fallen oder das Mana ausgeht geht ein Stück zurück,
der Gegner bleibt am Pet hängen, daß dann den Rest erledigen sollte.
Ihr könnt euer Pet, wenn ihr wollt auch direkt Befehle erteilen, diese lauten
wie folgt:<BR><BR>
