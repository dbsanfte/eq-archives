<P CLASS="P1">
	Nekromanten sind im Gegensatz zu vielen anderen Klassen, nicht von
	anderen Leuten oder Gruppen abhängig, da sie ihre Pets haben.
	Hinzukommt, daß ihr euere Pets selbst mit Waffen ausrüsten könnt.
	Außerdem ist es wesentlich interessanter einen bösen Nekromanten
	zu spielen als den guten Menschen/Paladin von Nebenan. :)
</P>

<TABLE BORDER="0" CELLPADDING="0" CELLSPACING="0" WIDTH="100%">
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
<TR><TD BACKGROUND="/pics/pageback2.jpg" ALIGN="CENTER" CLASS="P5">Erschaffung</TD></TR>
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
</TABLE>

<P CLASS="P1">
	Als Nekromant hat man 4 Rassen zur Auswahl: Mensch, Dunkelelf, Erudit
	und Gnom. Vom Menschen würde ich allerdings dringend abraten, er hat
	schlechte Stat-Werte
	und KEIN Infravision, was sehr wichtig für euch ist,
	denn es befähigt euch im dunkeln zu sehen. Eruditen haben da schon
	bessere Stats, allerdings liegt ihr Heimatort auf Odus und sie haben
	ebenfalls kein Infravision, Dunkelelfen haben eine hohe Intelligenz
	und dazu noch Infravision, was zu einer sehr guten Wahl für einen
	Nekromanten macht. Zugegeben: Gnome haben noch bessere Stats, aber ich
	persönlich möchte keinen kleinen komisch aussehenden Kerl spielen. Dies
	bleibt jedoch jedem selbst überlassen.
</P>

<TABLE BORDER="0" CELLPADDING="0" CELLSPACING="0" WIDTH="100%">
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
<TR><TD BACKGROUND="/pics/pageback2.jpg" ALIGN="CENTER" CLASS="P5">Stats</TD></TR>
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
</TABLE>

<P CLASS="P1">
	Die wichtigsten Stats für einen Nekromanten sind Intelligenz und
	Dexterity. Intelligenz ist dabei jedoch, um ein vielfaches wichtiger
	als Dexterity, deshalb würde ich vorschlagen 2/3 der Bonuspunkte
	in Intelligenz zu investieren und den Rest auf Dexterity.
</P>

<TABLE BORDER="0" CELLPADDING="0" CELLSPACING="0" WIDTH="100%">
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
<TR><TD BACKGROUND="/pics/pageback2.jpg" ALIGN="CENTER" CLASS="P5">Start</TD></TR>
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
</TABLE>

<P CLASS="P1">
	Ihr startet also, wie jeder andere auch in euerer Heimatstadt und müst
	euch erst einmal zurecht finden. Besonders Neriak bietet da einiges an
	Schwierigkeiten. Am besten druckt ihr euch vorher die Karten zu der
	jeweiligen Stadt aus, um zu erfahren, wo ihr seid und wo euere Gilden-
	halle ist. In Neriak befindet sie sich in 3rd Gate, keine Angst vor
	den Ghoulen, die tun euch nichts.
</P>

<P CLASS="P1">
	Euere Newbie-Ausrüstung besteht aus:
</P>
<UL CLASS="P1">
	<LI>Einer PK-Note<BR></LI>
	<LI>Einem Dolch<BR></LI>
	<LI>Eueren 2 Anfangssprüchen (Minor Shielding und Life-Tap)<BR></LI>
	<LI>Gilden-Note<BR></LI>
</UL>

<P CLASS="P1">
	Ihr begebt euch am besten sofort zur Gildenhalle, dort angekommen, übergebt
	ihr euerem Gildenmeister die Note. So werdet ihr in die Gilde aufgenommen
	und bekommt einen Newbi-Auftrag: Ihr sollt irgendwas mit einer Robe machen,
	am besten behaltet ihr diese aber. :) <BR>
	Nun geht es daran euere ersten 5 Skillpunkte zu verteilen. Klickt mit der
	rechten Maustaste auf den Gildenmeister. Links erscheint dann eine Leiste
	mit Skills, am besten verteilt ihr euere ersten 5 Punkte auf "Piercing",
	damit ihr mit dem Dolch besser umgehen könnt, indem ihr auf Piercing klickt
	und dann unten 5mal auf Practize.<BR>
	Jetzt müßt ihr euere Newbi-Spells ins Spellbook schreiben (scribing) und 
	dann memorizen, damit ihr sie immer verfügbar habt, indem ihr auf Spells
	klickt, einen Spruch aus dem Inventar auswählt und auf einen Slot im Spell-
	book klickt. Es läuft dann eine Leiste ab, kann etwas dauern, solange ihr 
	noch kein Meditate habt. Sind beide Sprüche ins Buch geschrieben, klickt auf
	einen und zieht ihn in einen der 8 Felder auf linken Seite. Wiederum läuft
	eine Leiste ab, allerdings ein wenig schneller. Immer wenn ihr jetzt auf das
	Symbol klickt, wird der Spruch ausgeführt. Minor Shielding erhöht eueren
	maximalen HP-Wert und euere Rüstungsklasse (AC). Life-Tap ist euer erster
	Angriffsspruch mit ihn fügt ihr dem Ziel 4 Schadenspunkte zu und bekommt
	diese als Lebenspunkte wieder. <BR>
	Jetzt müst ihr euch nur noch den Dolch in die Hand drücken und ihr könnt euere
	ersten Erfahrungspunkte sammeln.
</P>

