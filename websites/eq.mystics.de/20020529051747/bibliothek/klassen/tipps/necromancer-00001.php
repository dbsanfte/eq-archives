<P CLASS="P0" ALIGN="CENTER">
	Diese Tips f&uuml;r Anf&auml;nger wurden von Alamos erstellt. Der Stand der Infos ist 16.04.99.
</P>	

<P CLASS="P1">Dieser Guide soll dich in das Leben eines Nekromanten einführen.</P>

<TABLE BORDER="0" CELLPADDING="0" CELLSPACING="0" WIDTH="100%">
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
<TR><TD BACKGROUND="/pics/pageback2.jpg" ALIGN="CENTER" CLASS="P5">Gründe für einen Nekromanten</TD></TR>
<TR><TD HEIGHT="3" BACKGROUND="/pics/line0.jpg"><IMG SRC="/pics/temppx.gif" WIDTH="1" HEIGHT="3" ALT=""></TD></TR>
