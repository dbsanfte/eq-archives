<P CLASS="P1">
Kauft alle Spells , immer zuerst den Vampierspell ,weil es ab da eure Methode ist euch zu heilen und ab LvL 9 solltet ihr genug Geld haben um euch alle zukaufen.
sparsame Menschen koenne getrost den Spell True North weglassen weil er so ziemlich nutzlos ist und ihr ihn nie einsetzen werdet.
Zum Thema Pet kann ich nur sagen, ich habe viele SK gesehen die mit ihrem Pet durch die Gegend laufen und mich erstaunt fragten, ob ich keine Bones habe ,weil ich ohne Pet rummlaufe.
Ich persoehnlich empfinde unsere Pets bis so ca LvL 30 als sinnlos, weil wenn ihr euer 1.Pet habt(was ja das LvL1 Pet des Necros ist),seit ihr LvL9 und eure Gegner sind einfach zustark fuer das Pet.Gelegentlich erschaffe ich mir auch eins, es bringt euch immerhin ein Paar Sekunden Zeit wenn ihr mal fliehen muesst.
</P>

