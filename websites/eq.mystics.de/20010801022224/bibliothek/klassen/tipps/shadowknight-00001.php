<P CLASS="P1">
Ja am Anfang werdet ihr nicht die Mittel haben aber fragt andere Spieler und kauft euch soschnell wie moeglich eine Bandad Armor ( als Regel gilt 1 AC = 1 PP) aber Leder tut es am Anfang auch.Wenn ihr genug Geld uebrig habt kauft euch eine 2H Slash Waffe , weil ihr damit den meisten Schaden habt und wir nicht wie die Warrior den Skill Dual Wield bekommen , der es uns ermoeglicht 2 Waffen zutragen.
Am Anfang tut es ein Rusty 2H Sword , das ihr mit einwenig Glueck und fragen billig von Spielern kaufen koennt.
</P>

