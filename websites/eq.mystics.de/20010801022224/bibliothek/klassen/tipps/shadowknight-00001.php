Hier kann ich nur den Dunkelelfen empfehlen, er besitzt eine hervorragende Int und
Nachtsicht.Die "dicken Rassen" sind nichts fuer meinen Geschmack ( sind aber sehr gute
Kaempfer) und der Mensch ist zu durchschnittlich und Nachts blind wie ein Maulwurf.
