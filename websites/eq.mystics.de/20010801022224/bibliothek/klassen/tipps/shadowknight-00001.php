<P CLASS="P1">
Geht zu eurem Gildenchef (der Weg ist am anfang sehr schwer zu finden, fragt jemanden, euch wird sicher geholfen) und trainiert 2 Punkte auf 2H Slash, 1 auf Archery, 1 auf Bind Wound und einen auf Taunt ( tauntet sooft es geht um euren Skill hochzubekommen, es ist wichtig um den Mob von den Castern fernzuhalten).
Da ihr erst ab LvL 9 casten koennt ist Bind Wound die einzige Moeglichkeit euch zu heilen. Bandagen sind zwar teuer ( ca. 6-7 Silber) aber mit etwas Glueck findet ihr einen Mage , der euch welche herbeizaubern kann.
Archery gibt euch die Moeglichkeit den Mob aus der Entfernung zu pullen, ohne das ihr immer hinlaufen muesst.(Boegen kosten beim Haendler so ca. 6 gold und Pfeile 6-7 Kupfer, die billigen reichen da voellig aus)
Noch etwas zu eurer Faehigkeit Harm Touch, setzt sie weise ein z.b. wenn ihr merkt das der Kampf knapp wird , ihr aber nicht mehr fliehen koennt. Er macht in LvL 7 schon bis zu 70 Schaden , kann aber nur 1x am Tag eingesetzt werden.
</P>

<P CLASS="P5">
	Level 1-5:
</P>

<P CLASS="P1">
Geht nach Freeport ,lasst euch da neu binden , das Newbie Gebiet dort ist viel besser als das in Nektulus.Vorsicht vor den Deathfist Pawns und den Firebeetles, die sind im LvL1 noch zu stark fuer euch.
Wenn es eng wird rennt zu den Wachen ( keine Angst ihr seit dort zwar nicht willkommen aber nicht KOS).Geht nicht zu weit in die Stadt, einige Npc greifen euch an und dann habt ihr auch die Wachen auf dem Hals.Ausserdem koennt ihr in Freeport eh nix kaufen oder verkaufen.
Verkaufen koennt ihr in EC ,direct hinter der Zonengrenze ,bei den 3 Haeusern an der Strasse.( Es ist das in der mitte, links von der Strasse).
Wenn moeglich jagt alle Dec Skells, auch wenn sie kein Exp mehr bringen. Sammelt die Bonechips , die koennt ihr fuer bis zu 5pp fuer 20 Chips verkaufen.
Falls Euer Essen und Trinken zur Ende geht , lasst euch von einem Monster killen.(bis LvL 6 habt ihr keinen Exp verlust und bis LvL5 bekommt ihr immer je 5 Essen und Trinken)
</P>

<P CLASS="P5">
	Level 5-8:
</P>

<P CLASS="P1">
Geht zurueck nach Nektulus, gleich hinter dem Hohlweg nach der zone geht nach rechts.
Dort findet ihr einen Obelisken, macht eine Gruppe und jagt die Skells.
Wenn ihr 3 Leute seit koennt ihr auch die seltenen Zombies erlegen.
Wenn mal eine Mummy spawnt, die schafft ihr erst ab lvl 7 und dann auch nur mit min. 3 Man.
Tippt ein: / shout can anyone kill a mummy at obelisk?
Es kommt dann eigentlich immer jemand und toetet sie.
Nehmt euch auch vor den haeufig vorbeilaufenden Young Kodiaks in acht , die sind sehr hart, notfalls rennt zur Zone.
</P>

<P CLASS="P5">
	Level 8-11:
</P>

<P CLASS="P1">
Geht nach EC und schließt euch einer Orcgruppe an. Da gibt es guten Exp und Lot.
Die Belts koennt ihr an andere Spieler fuer ca 5pp verkaufen.
ACHTUNG vor den Griffin, wenn er kommt LAUFEN!!!!
</P>

<P CLASS="P5">
	Level 11-15:
</P>

<P CLASS="P1">
NRO sucht euch eine Gruppe in einem der 3 Derv Camps und jagt Dervs. Die geben wiederum guten Exp und Lot. Die Ringe oder die seltenen Bronze Waffen koennt ihr gut verkaufen.
Solltet ihr ein Bronze 2H Sword finden und noch nichts besseres haben , behaltet es ) .
</P>

<P CLASS="P5">
	Level 15-17:
</P>

<P CLASS="P1">
Oasis. Sucht euch eine Gruppe und jagt die Orcs oder bleibt am Strand und jagt Crocos.
Die Crocos geben keinen Lot aber gute Exp und ihr braucht keine sogrosse Gruppe,2 Leute reichen hier.
ACHTUNG!!! Passt auf die Giants und die Spectres auf, die killen euch mit 2 - 3 Schlaegen.
</P>

<P CLASS="P5">
	Level 17-19:
</P>

<P CLASS="P1">
Highpass Hold  Um dorthin zugelangen muesst ihr durch EC, WC ,und Kithicor ( nur am Tage, nach 08.00 betreten , sonnst seid ihr schneller Tod als ihr uh oh sagen koennt).
Gleich hinter der Zone befindet sich der Orc Hill.
Sucht euch eine Gruppe und jagt diese.
Vorsicht vor den Wachen und Npc´s ihr seid da KOS und sie werden euch sofort angreifen.
Wenn ihr genug Orcs killt werdet ihr aber nicht mehr Kos sein. ( so ca 100)
</P>

<P CLASS="P5">
	Level 19-35:
</P>

<P CLASS="P1">
Highkeep   Es befindet sich gleich hinter Highpass Hold .
Geht erst da hin wenn ihr nicht mehr Kos seid.
Fragt bei einer Gruppe nach und lasst euch auf die Liste setzten.(ja sowas gibt es da,weil dort immer viel los ist)
Manchmal geht es schnell, manchmal kommt ihr gar nicht ran.
Wenn ihr in einer Gruppe seid , fragt ob ihr alle Goblin Ohren bekommen koennt.
Diese gebt dann den Captain der Wachen , ihr erhaltet Exp, Geld und vorallen sehr gute Faction ( Ansehen ).
</P>

