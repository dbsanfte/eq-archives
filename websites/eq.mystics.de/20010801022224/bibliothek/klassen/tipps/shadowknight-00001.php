<P CLASS="P1">
Da der Dunkelelf von Hause aus eine gute Int besitzt, wuerde ich die verbleibenden Punkte auf Dex,Agi und Str verteilen.Stam finde ich nicht so wichtig ,weil der dadurch hoehere HP Anteil einfach zu gering ist und ihr , wenn ihr eine hohe Dex und Agi habt,
nicht sooft getroffen werdet. Außerdem bestimmen Dex und Agi auch euren AC wert.
</P>

