<P CLASS="P1">
Ihr seid der Tank. Man erwartet von euch das ihr vorne steht , den Mob aufhaltet und Schaden macht und dank eurer Vampier sprueche, koennt ihr das sehr lange machen .)
Wenn ihr einen Cleric in der Gruppe habt schuetzt ihn und haltet ihn aus dem Kampf raus.Er ist Heiler kein Tank und soll heilen und medden.(ich hab nen Cleric als Firstchar und weiss was ich sage) Denn ein Cleric der kaempft verliert leicht den Ueberblick oder wird niedergemacht.
Achtet auf den Manahaushalt eurer Heiler und Caster, sagt ihnen sie sollen euch ueber ihren Mana anteil auf dem laufenden halten.Denn nichts ist schlimmer als wen ihr den Mob pullt und alle Heiler und Caster OoM sind.
Im Falle einer Gruppe ist euer Pet nuetzlich, stellt es zu den Heilern und Castern ( die immer etwas abseits des Kampfes stehen sollten) und weisst es an hier zu wachen ( / pet guard here).
So haben sie einen Schutz ( wenn auch nur einen kleinen), der, fals der Mob sich auf sie stuerzt, sie schuetzt und euch die Zeit gibt ihnen zur hilfe zu eilen.
</P>

<P CLASS="P1">
Weiter kann ich euch nicht helfen und geleiten , weil ich selber noch nicht weiter bin.
aber wenn ihr bis hier hin gekommen seit, braucht ihr auch keine Hilfe mehr 8)).
</P>

