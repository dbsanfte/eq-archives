Before you request membership to Agony, you should understand that at this
current stage in Agony's lifecycle we have specific requirements to
join.<BR><BR>
The current requirements are as follows:<BR><BR>
<H2>
Level Requirement:<BR>
</H2>
Bard - 55<BR>
Cleric - 55<BR>
Druid - 60<BR>
Enchanter - 55<BR>
Magician - 55<BR>
Monk - 56<BR>
Necromancer - 55<BR>
Paladin - 56<BR>
Ranger - 60<BR>
Rogue - 55<BR>
Shadow Knight - 57<BR>
Shaman - 60<BR>
Warrior - 57<BR>
Wizard - 55<BR>
<BR>
<H2>
Raid Requirements:
</H2>
Our raids are very in depth.  On weekdays we can raid for 3-5 hours at a
time, non-stop.  On weekends our raids can last as long as 12 hours
depending on the scenarios.<BR><BR>
Our NORMAL Raid time on WEEKDAYS is 6-7PM PST,
till the raid ends which can be as late as 2-3AM PST.<BR><BR>
Our NORMAL Raid time
on WEEKEENDS is 11AM PST until the end of the raid, which could be 11PM
PST.<BR><BR>
We never let a weekend raid exceed 12 hours and in MOST cases don't ever let
it exceed 6-7 hours.  If a raid does exceed this time, we do take extended
breaks during the raid for food/water, munchies, whatever it may be.<BR><BR>
Agony understands the importance of "free" time, however we also have a list
of goals and accomplishments we want to attain.<BR><BR>
If you are a casual player
who plays 1-2 hours a day, then Agony may not be for you.<BR>  If you can not
attend at least 2 raids a week, then Agony may not be for you.<BR>  If you have
a bedtime curfew (due to parents or whatever the reason may be) and you
can't stay up past 10PM PST, then Agony may not be for you.<BR>
If you don't give the game/guild all the effort you can to succeed, then
Agony may not be for you.<BR>
If you have no desire in making it to level 60, or your time to get to level
60 is several months away, then Agony may not be for you.<BR><BR>
<H2>
Equipment/Misc Requirement:
</H2>
Sebilis Key<BR>
At least 1 Piece to your Howling Stones/Charasis key<BR>
Cobalt Scar Tooth (For porting to Cobalt Scar)<BR>
Some form of permanent enduring breath item (ie: Fishbone earing)<BR>
100+ Sense heading<BR>
180+ Swimming<BR>
At least one piece to your VP (Veeshan's Peak) key.<BR>
A solid connection.  If you continually go link dead in a 20 person battle,
you are of little use to a raid.  ISDN, Cable Modem, and DSL are all
excellent sources of higher bandwidth.<BR>

<H2>
Other:
</H2>
We are not deemed a "hardcore" guild.  We are a comfortable, STABLE guild
that will slaughter the end-game mobs, but we will do it on our time frame.
We are enjoying the game to its fullest.  We are seeing as many zones and
encounters as we can, trivial or not we want to experience them.  We have the
strategy, and the play experience to succeed and we will.<BR><BR>
This criteria is a guideline for the types of players we seek.  It is not
written in stone and can be flexible, but is a basis for our decision making.<BR><BR>
If you do not hear from a guild officer within 7 days from the time you
submit an application there is a good chance that it may have been
overlooked, or you do not fit the criteria we are seeking.<BR><BR>
Please fill out all fields in this form as it gives us a solid background
and saves us a bunch of question asking later.<BR><BR>

