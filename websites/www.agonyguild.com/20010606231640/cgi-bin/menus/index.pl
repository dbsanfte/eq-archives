you'll see the belts tightly attached to their waists.<BR><BR>
The next rumor going around is about the recent "Agony Rescue" we did for an
Ex-Celestae Member.  He requested our help, so we gathered a small team to
help him out, end of story nothing more, nothing less.  However it appears
that someone changed a Vazaelle IRC channel topic in regards to the incident
(We have a Vazaelle IRC Channel???) and of course Agony got blamed for
making that topic change.  Whatever.. guilty by association I suppose... :)<BR><BR>
The next rumor I heard was that since Agony was founded by RL friends, that
they hoard all the LOOT and don't give anything to their members... Hrm I
suppose that's why im still holding a Crude Stein and wearing a Braided
Cinch Cord while other members have been awarded items like Bone Clasped Girdles out of
Fear?  Our guild is based on a point system to prevent favortism.  Our first
guild epic went to a Ranger (A non-rl friend of the founders) but I could
see how we play favorites and give all our RL friends items right?  For
people that have NEVER even grouped with or attended an Agony raid it's
simple to pass judgement on us.   Oh did I forget to mention we usually don't invite
non-guild members to raids to begin with, so how would you even know?
Simple, you wouldn't.  :)<BR><BR>
It seems there is a lot of "negative" rumors floating around about Agony,
when the simple fact is most of it is perception and heresay.  I know that many folks do
not like a particular member of our guild, and thats ok you have every right
to not like him.<BR><BR>
My message is quite simple, if the community doesn't like a member of Agony .. ok
that's their opinion and right.  However, starting falsified rumors and
allegations about our guild really isn't necessary.  Agony does not go
around slandering, bashing, or creating falsified rumors about other guilds,
and we would appreciate the same in return.<BR><BR>
I am very open about almost everything Agony does, if you feel compelled to
address a "rumor" with me, please feel free to do so, Ill be more than happy
to clear up whatever allegation has been made about us.<BR><BR>
