September 24th,  Lots of Raidage.. no screenies!
</H2>

</td>
</tr>

<tr>
<td align=left>
<br>
Agony is back into the swing of slaughter.  We caught wind that the Fear
Mini's were up, and since the majority of our guild warriors are stuck on
the stupid ball of everliving golem, we figured we'd take our 21st attempt
at the mini's.  The break was the worst in Agony history, however it wasn't
necessarily our fault.  Over 15 people went Link Dead on the break, leaving
us roughly 7 people to break Fear.  Many
thought the zone had crashed but it was a big server burp.  Needless to say
we started the night off doing a CR, which was ghey.  Once that was all
done, we destroyed the plane and the mini-bosses.  Tonight, it paid off, Gratz Tress.. now go finish yer light
sabers bish!<BR><BR>
Shortly after we made a trip to PoG to take down those mini-bosses as well
before the server patch.  Gratz to Merlinian on your new Mage Robe,
Akethedar on yer new monk BP, and the other folks on yer legs/lootz.<BR><BR>
Until the next time ...<BR><BR>
</td></tr>


