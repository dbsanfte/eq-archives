dragon Lord Nagafen.  Just 1 day earlier Agony ran the troops down, only to
find that Lord Nagafen was no where to be found, he evaded us yet again.
Rumors had formed that Lord Nagafen had spawned, so we gathered the troops
up again and headed into battle.<BR><BR>
