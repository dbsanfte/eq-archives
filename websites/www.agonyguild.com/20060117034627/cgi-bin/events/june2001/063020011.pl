<H2>
June 30th, Gnome Battles!  Er no .. Plane of Air/Sky!
</H2>
<HR>
</td></tr>
<tr><td align=left>
<br>
Serafina, Agony's leader going hand to hand with Nisma the outcast gnome
rogue!  Nisma goes for the block little did she know Serafina had the right
hook coming .. and Nisma goes down!!<BR><BR>
Ok so lets talk about what we really did .. Agony decided to take a trial
run through the Plane of Sky to see just how far we could get in a shortly
planned time frame.  The target was distance .. the furthest island possible
in a single day.<BR><BR>
The guild held strong we wanted to make sure we got to see as much of the
plane as possible so we went the extra steps to kill certain mobs that would
in turn spawn others for our adventuring.<BR><BR>
Noble Djorn, Overseer of Air, Keeper of Souls, The Spiroc Lord, The Spiroc
Guardian, Gorgalosk, Bazzt Bztt, boss after boss Agony got to see, battle,
and defeat.<BR><BR>
Big gratz go out to Serval on the rare Necro cape needed to complete the
necro epic, Trueblade on the spiroc wingblade for the warrior epic, and a
HUGE gratz to Bruennor the guilds only Paladin on being the 2nd Paladin on
the server to finish the Fiery Avenger.<BR><BR>
All in all Agony had a major blast doing this raid.  The Plane of Air/Sky
has been much upgraded since our leadership visited it on our previous
servers and we ran into some difficulties learning a few new quarks about
the zone but once we figured out the tactics it was all out brutal ass
kicking on the mobs in Air/Sky.<BR><BR>
I want to thank every Agony member who attended the event for the short
raid, and congratulate the guild on a speedy accomplishment of making it TO
and THROUGH all the bee's on Bzzatt Island in under 2 days time.<BR><BR>
I also want to give a very special thank you to Aethina of The Endless for
coming up and hanging out with us on the Bzzatt island, your attendance and
help was much appreciated. I thank you, and the guild thanks you.<BR><BR>
