</H2>
<HR>
</td></tr>
<tr><td align=left>
<br>
March 14th will be a day long not forgotten.  With the guild being less than
3 months old, Agony suffered it's first defeat.  The founding members have
fought Lord Nagafen more times than they can count but today Nagafens
reactions to our attack were very different from the 'Good Ole Naggy' we
were used to defeating time and time again.  We made 2 attempts on the red
beast, managing to get him down to Lord in Lord Nagafen on the attempts.
Unfortunatly Nagafen was warping around the room so much that our tanks
could not control him, he would stay in battle for 2-3 seconds and then warp
to the door by the hallway, flap his wings around then warp to his bind
spot, then run around the lair then warp to the door again, and he repeated
this cycle continually through the entire fight, making it nearly impossible
to fight him with any melee.  The officers spent a good portion of time
after the event analying what caused his reactions so on our next attempt he
will fall for sure, revenge shall be ours.<BR><BR>
With all the chaos going on during the fight and the mass slaughter of
bodies strewn about his surrounding area I must give a large amount of
credit to the guild members for their continual positive attitudes and drive
to take that scaley chump down.  Agony never ceases to amaze me with the
teamwork and positive efforts continually shown to each other and to the
guild as a whole.  Good work team, we'll get him next time :)
