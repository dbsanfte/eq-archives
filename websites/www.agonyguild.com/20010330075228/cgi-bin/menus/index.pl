<H2>
March 11th, Agony battles non-stop all day/night. Fire Giants, Rokyl,
Efreeti, Cazel, Verina....
</H2>

</td>
</tr>

<tr>
<td align=left>
<br>
12:30PST Agony formed up outside Sol B to start the days events, the
original plan was to knock out the Efreeti and cycle shifts in and out to
outfit the guild with Golden Efreeti Boots and D-Rings. That happened and
much more. The guild zoned into Sol B and started heading towards Efreeti
when some of our guild druids issued tracks and noticed Fire Giants were up,
so we decided to make a stop over there and take them out before commencing
the boring Efreeti camping. The Fire Giants fell, Rokyl fell, and off to the
Efreeti the guild went. The break was a cakewalk, and somewhat humorous,
sending 17-18 people at a camp that only takes 4 to break was quite funny.
Mobs had no idea what hit them.<BR><BR>

Efreeti was broken, 1 Ring, 1 set of Boots dropped. Gratz to Khaotik on the
ring and Serafina on the boots.<BR><BR>

Shortly thereafter a guild member came across guild chat that Cazel was up
in Oasis, so Serafina grabbed the troops after the Efreeti was over and
headed to Oasis to get another piece for her Epic weapon.<BR><BR>

Cazel fell fast, we stormed him swords blazing. In the heat of the battle
the guild decided to get another piece for Serafina's epic, because one big
mob just wasn't enough for the day, so off to Neriak they went to take down
Verina Tomb, and let me tell you Verina was not expecting the pain she
received.<BR><BR>

Thanks to every guild member who attended off and on throughout the day to
help make yet another successful day of guild events.
<a
href=http://www.agonyguild.com/cgi-bin/events/march2001/031120011.pl>Click
here for details.</a><BR><BR>
