This epic has turned into a complete nightmare.  Agony has had several
clerics sitting on the last piece to this quest, Zordack Ragefire in Sol B.
Probably the most heavily sought after mob by all the large guilds on this
server.  The last 3-4 Ragefires that have spawned Agony was not able to get
their people to the zone before another guild had already engaged it.  The
last Ragefire was actually camped by another guild before he even spawned.
