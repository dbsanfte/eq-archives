<H2>
February 24th, En Medias Res calls in Agony's support to down Lodizal.
</H2>

</td>
</tr>

<tr>
<td align=left>
<br>
En Medias Res, an ally of Agony called in the support team for an early
morning slaying of Lodizal. Roughly 2AM PST the call to arms was made, and
within a matter of minutes Agony had all the forces online grouped together
and in Iceclad ready to take down this happy Turtle. Oxox, an Agony member
was graciously awarded the final piece he needed to complete his Eyepatch of
Plunder quest. Congratz to you Oxox, and a special thanks to En Medias Res for
inviting us to the party.
