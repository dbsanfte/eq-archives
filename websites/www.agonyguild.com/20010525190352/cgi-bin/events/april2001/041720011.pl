April 17th, Agony kills Lord Nagafen and Lady Vox back to back.
</H2>
<HR>
</td></tr>
<tr><td align=left>
<br>
A crazy night for Agony, with the emergency patch ruining our first trek
towards Lord Nagafen, Agony was determined to not give up.  The servers came
back online the guild formed up and started pushing through Fire Giants
towards Nagafen again.<BR><BR>
Arriving at Nagafen, team in full force we buffed and charged.  Nagafen fell
like a fire beatle to an ice comet.  An excellent execution of tactics and
the red beast fell.<BR><BR>
In the heat of the moment, we didn't want to give up on the momentum and so
we decided to head over to Vox's little realm and lay the Agony beat down on
her.<BR><BR>
The most painful thing about fighting Vox, is running to her lair, damn
Verant has gotta put a closer druid/wizard ring.<BR><BR>
The team slaughtered the Ice Giants, moved to the buffing point, prepped for
the fight and stormed the lair.  Poor Vox had no idea what she was in for...
thank you and good night.<BR><BR>

