So you want to know what Agony is all about?
<BR><BR>

Agony was formed in early January of 2001 on the EverQuest Vazaelle server
by just over 25 real life friends.  Friends that have known each other for a
great deal of time, all of which play EverQuest. This mass group of people
however faced one problem, they were not all on the same server so they
never got to enjoy each others time while online, and hence the birth of
Agony.

<BR><BR>
During the month of December 2000, the friends got together and started
discussing how we could all get to the same server and play together.  The
idea of power leveling on one server came up time and time again but between
Mith-Marr, Veeshan, Innoruuk, Fennin Ro, and The Nameless we just couldn't
decide who was going to have to sacrifice their characters and who was going
to get to keep them.  So in this mass dilemma, everyone said in order to make
it fair and have no hard feelings, everyone decided to start fresh on the
virgin server Vazaelle.<BR><BR>

The majority of Agony's founding members are PST/CST based.  We play very
heavily in the evenings and all the time on weekends.  We are expanding our
player base slightly to accommodate EST and other time zones that play the same
times we do.  The sole purpose behind this is that when Agony is online,
their force will be strong.<BR><BR>

So here we are on Vazaelle as one giant group, we have since gone back to
our old servers and recruited some of our closest EQ friends to also make
the switch and a good amount have, because they believe in what Agony is and
what Agony is doing as a group.  We are also recruiting new members for the
guild provided they meet the requirements at the time of joining.  Agony's
family is continually growing, but the size is limited.<BR><BR>

Agony is not your normal guild, yes I now you've probably heard this before,
but some of the major reasons that guilds fall apart is poor leadership and
organization.  This is not an issue with Agony, as both I (the leader
Serafina) and the officers have not only real life management/organization
skills but also EQ skills as well.  Many have lead groups into battle time
and time again.  Some members have pulled "uber encounters" and
various other dragon/god scenarios more times than they can count.<BR><BR>

Agony also has a large amount of communication within itself, for example
this website you are viewing right now, has a
message board, e-mail system, calendar system, strategy guides, event news,
screenshots, character profiles, and many other features on it for the
members to enjoy.  For all those people who work and can't play EQ from
work, they will have a web site to keep in communication with the guild so
when they are off work they are prepared for whats ahead of them.<BR><BR>

Agony understands the importance of 'free' time.  Time that members can go
and do whatever they want, whether it's exp, get loot, hang with friends,
whatever, and we make sure there is plenty of this.  However, when a call to
arms is made for an event, I like to see the majority of the guild attend
the event as it only makes things go quicker.  The faster the guild pulls
together and finishes the event, the faster people can go back to whatever
they were doing before hand.<BR><BR>

Agony Officers and some members also utilize Sidewinder Gamevoice's
(www.gamevoice.com) to communicate during play time.  These units are a
hardware sound compression
device that attaches to your USB port and allows you to do voice
communication while playing EverQuest.  This helps us continue to manage
large amounts of people smoothly so that events, raids, and general guild
related issues are handled very organized and efficiently.<BR><BR>

So Im sure you're asking yourself, what about loot?  How does Agony handle
loot?  We used to distribute loot on a complex point system, but after
trying it out for a while we decided that the "family" feeling of the guild
just didn't seem the same.  We switched back to the guild leaders awarding
loot.
<BR><BR>
Agony DOES NOT play favorites, everyone in the guild will be treated fairly
and have an
opportunity for loot.  If an item drops for an Enchanter on an event, and
only one Enchanter is present and needs the item, they will get the item by
default.  This goes for all classes on any event.<BR><BR>

Agony is also very fond of the "Hand me down" system.  It's not a rule, but
advised to take care of the other guild members.  If you get a nicer piece
of loot and your old piece is not NO DROP, hand it down to another member in
need, even if its just on loan until they get something better, take care of
the other members.  The stronger the members are, the stronger the guild is
as a whole.<BR><BR>

Agony is like a family, we respect each other while accomplishing goals at
the same time.  Due to the family nature of the guild, if for any reason a
member decides they want to leave the guild (after talking with Serafina
first), then they may choose to do so, however its heavily frowned up
inviting that member back. If you want to be a part
of our family, then support your family, don't bail on them.  We play
together and we stay together.<BR><BR>

So is Agony right for you?  Are you right for Agony?  If you think you are,
get in touch with Serafina or Fydar online after filling out the online application,
and we can discuss it further.
<BR><BR>

-Serafina<BR>
Leader of Agony<BR>
An EverQuest Vazaelle Guild<BR>
