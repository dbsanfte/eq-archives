      <b style="font-size:13px">Old McDonald had a farm...</b> <span class="small">(submitted by Ansar at 08:54am CDT)</span><br /><br />
      It's been way too long since we posted an update.  Nothing hugely exciting has happened in the way of new big kills.  But, we've been plugging right along.  We are currently in farm mode as we work on our strategies for killing the elemental gods.<br />
<br />
In the 28 days since the last update, we have had about 200 upgrades looted.  Now, a few of those were items for alts but not that many.  Thats a LOT of loot.  Especially considering there were a few slim days scattered in there when the competition gobbled up most of the spawns before we got on.  We made the most of the spawns we got a shot at.<br />
<br />
On the god front:  Coirnav should die the next time we have a spawn with a decent force on.  He should have been dead already but timing hasn't been our friend.  He keeps spawning either way before our raid window and is dead or blown before we get home from work, or really really late.  He'll screw up and spawn in our window though eventually.<br />
<br />
Xegony is also living on borrowed time.  We think we have the kinks worked out of our strategy now.  It's just a matter of getting a force up there and executing.<br />
