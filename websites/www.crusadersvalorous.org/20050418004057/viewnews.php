      <b style="font-size:13px">Riftseeker Royal family warns of Terrorist Oganization</b> <span class="small">(submitted by Ansar at 11:28am CST)</span><br /><br />
      The Royal house issued a press release recently warning of a very persistent terrorist organization apparently bent on destroying the royal family and throwing the kingdom into disarray.  In a press conference King Gelaqua stated "These bastards are just plain mean.  They have been torturing our guard dogs for weeks, but have been much bolder lately."<br />
<br />
Apparently, the organization has ransacked Queen Pyrilonis' private chambers multiple times, but until recently had not attempted to venture near King Gelaqua's throne room.  That changed recently as the organization evidently made it a priority to assaninate the King in his own chambers.  "It was like I was in a bad 'B' horror movie" the King is quoted as saying.  "My boys just kept disappearing one and two at a time over the course of a couple of days.  They would wander off and wouldn't come back.  I couldn't figure out what was going on."  The cause for the disappearance of the Princes became apparent later though, when the organization attacked the King himself in his own throne room.  "I was able to beat them back and run them off with the aid of my highly trained suicide bomb guards.  I thought that would be the end of it, but evidenly these Norrathians don't give up that easily".<br />
<br />
After the failed assasination attempt, the Princes were discovered battered and near death in various little used parts of the Royal Palace.  They were still being treated in their hospital rooms when the assailants returned 2 days later.  "I was alone in the throne room when I heard a sudden commotion outside" recalls King Gelaqua. "I looked up and saw this lizard thing running at me with a stick.  There had to be about 50 or 60 creatures from Norrath right behind him.  There was even this one big one in bright red armor out front that was actually holding one of my pet dog's teeth like it was a sword.  That really pissed me off, I loved that dog.  I was able to kill that one right away, but the rest of them had evidently improved their tactics since the previous attempt on my life.  I had to throw some of the crown jewels at them to distract them and run."<br />
<br />
The King is now living in exile, afraid to return to his throne until the interlopers depart.<br />
