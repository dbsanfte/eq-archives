<h3><font color="0099ff">Guild Rules and Regulations</font> </h3>
<p></p>
<p><u><strong>Guild Rules and Regulations </strong></u></p>
<p>&middot; Kill Stealing. No kill stealing will be tolerated by this guild. Members 
  will follow the appropriate steps in alleviating such difficulties. Arguing, 
  slandering, etc. are not appropriate acts. Members will petition the Guides 
  and will respect any authority and decision the Guides make. Failing to react 
  appropriately on these matters can result in immediate dismissal.</p><br><br>
<p>&middot; Roleplay. While this is not an exact demand, we are a roleplay based 
  guild, and therefore ask that you roleplay any communications between fellow 
  members and officers when speaking in general, shout, OOC or guild chat forums. 
  The respect and roleplaying you show here reflects on us all and establishes 
  the respect of the guild within Norrath. Respect shall be extended to all members 
  of our family, especially to or Barons and Counts.</p><br><br>
<p>&middot; Profanity. Members will not use profanity of any kind when speaking 
  in guild, shout, OOC or general formats. Engaging in any type of profanity will 
  result in the members being demoted or removed from the guild at the discretion 
  of the High Justice or Prince.</p><br><br>
<p>&middot; General Shouts. Members shall refrain from any topical discussions, 
  spamming, or general arguments within shouting or OOC formats. Again, we will 
  respect the foundation of the EQ Guides and will use their channels whenever 
  possible to alleviate mattes involving our members.</p><br><br>
<p>&middot; Obtaining Items. As a family and guild, we encourage one another to 
  succeed and to achieve levels and items along their paths. Members should refrain 
  from asking others to obtain an item for them. While some Counts and Barons, 
  and even the Prince are known to bestow items upon members, this is not required. 
  Gifts of this nature should be greatly appreciated and not expected.</p>
<p>&middot; Power Leveling. Again, as with Item Gathering, the Prince and others 
  are known to assist persons with their advancement from time to time. However, 
  this is done from the generosity and the desire of those Officers to see members 
  do well, but it is not required.</p><br><br>
<p>&middot; Problems With Members or Superiors. All complaints or concerns about 
  other guild members shall be brought to the immediate attention of the immediate 
  superior of that person. The Baron, Count or Prince will then take appropriate 
  action toward the person in question. Panels may be formed for major problems, 
  and investigations will be made about accusations toward any guild member.</p>
