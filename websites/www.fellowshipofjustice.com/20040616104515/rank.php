<h3><font color="0099ff">Rank and Structure</font><br>
  <font color="#FFCC00">Who's Who</font> </h3>
<p>&nbsp;</p>
<p><strong><u>Rank and Structure </u></strong></p>
<p>The Fellowship of Justice was founded in September of 1999, by Lord Astioch 
  Multovian. The guild has seen many changes and mergers in its storied history. 
  However, the heart of the Fellowship has never faded, and it is currently gaining 
  the strength it once had. The Fellowship is founded on a feudal system of ranking:</p>
<p>&mdash; Prince - Guild Leader<br>
  &mdash;Major Domo - Assistant Guild Leader </p>
<p>&mdash;High Justice- Overseer of all rules enforcement and investigation<br>
  &mdash;Duke - Highest Ranking Officer In Guild, Short of Prince<br>
  &mdash;Count - Appointed Class Supervisor, Usually receives appointment from 
  Duke</p>
<p></p>
<p><em>Each of the above has the power to invite guild members worth of the Fellowship 
  of Justice.</em></p><br><br>
<p><u>Additional Powers:</u></p>
<p><strong>Duke</strong><br>
  May remove any baron or member of the guild for conduct deemed unbecoming of 
  a FOJ member.</p>
<p><strong>Counts</strong><br>
  May remove members of their own class for conduct deemed unbecoming of a FOJ 
  member.</p>
<p><strong>The Prince</strong><br>
  Oversees all guild activities, may promote, remove, and demote any persons he 
  deems necessary.</p>
<p><strong>Major Domo</strong><br>
  Oversees operations and activities of the guild in the absence of the Prince, 
  and reports to the Prince any activities necessary to his awareness. Also has 
  power to remove and demote any persons below duke status as they see fit.</p><br><Br>
<p>Current Ranks within the guild</p><br>
