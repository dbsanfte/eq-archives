<h3><font color="#0099FF">Guild Ettiquete</font><br>
  <font color="#FFCC00">There's always rules...</font> </h3>
<p><br>
  <strong>Much of our guild roleplaying is found in the etiquette we use when 
  addressing members or royalty of the guild. The following information should 
  be used when addressing Fellowship members.</strong></p>
<p><strong><u>Etiquette Rules</u></strong><br>
  When approaching any member that is of higher station that you, you will kneel 
  in humility and reverence.</p>
<p>If you wish to speak to any member of higher station, you should request a 
  moment of their time in order to discuss something.</p>
<p>Whenever someone approaches you and kneels, it is customary to bow in return.</p>
<p>Whenever approaching someone of equal status, you should bow.</p>
<p>Whenever speaking with a member of the guild, you should address them with 
  their official rank or title. Knights shall be called Knight or Sir, regardless 
  of gender. Barons and Counts shall be called by their title, or Lady for women, 
  Lord for men. The guild Prince shall always be called Prince.</p>
<p>When addressing someone in the guild or general chat (any format that is exposed 
  to more than one viewer) these rules should be followed at all times. Direct 
  tells are of course free to any form of discussion and do not commonly adhere 
  to Etiquette Rules.</p>
<p><strong><u>Guild Meetings</u></strong><br>
  For persons physically reporting to the guild meetings, all persons should sit 
  during the meeting except the person holding the meeting. All persons should 
  refrain from using guild chat during the meeting until the host has asked for 
  questions. When questions are being taken, the person needing to ask a question 
  shall use guild chat and state &quot;I have a question&quot;. The host then 
  shall call on persons in turn to address the questions.</p>
<p><strong><u>Guild Raids</u></strong><br>
  This is one of the most vital areas we need to address, or many folks will get 
  themselves perished.</p>
<p>The raid organizer will do all the talking in set-up stages. Persons should 
  assemble at the pre-designated area on or before the time of the raid. </p>
<p>No one shall use guild chat for no-raid matters during a guild raid. Keep all 
  discussion to a minimum during raids and pay close attention to the raid organizer. 
</p>
<p>The raid organizer shall assign persons to groups; please do not group up on 
  your own if you wish to participate in the raid. </p>
<p>The raid organizer shall determine the qualifications for attending a guild 
  raid. Some raids require Knight status or above, and are guild exclusive. </p>
<p>Make sure you understand any and all rules for a specific guild raid prior 
  to attending. </p>
<p>More lives than your own may depend on following these rules.</p>
