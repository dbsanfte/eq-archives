						<blockquote>
<FONT SIZE=-1 Face=Verdana,Arial><b>Basic Info</b><br><STRONG>Level to solo the key quest</STRONG>: 45+. The only mobs that you need to kill are the new undead in OOT (which are light-blue to 
blue to a level 47) and the gnome sentries that patrol Iceclad.</P>
<P><STRONG>Time to quest the key</STRONG>: One - two hours. 
</P>
<P>
<ol>
<li><P>Take the boat to Erud’s crossing. As of Mar 28th, 2002, the shuttle in Erudin was still broken so you will have to get there via the boat from Qeynos. Talk to Idelia the Serene in Eruds Crossing just off the docks to the left by the ghost ship (n1759.94, p931.58).</P>

<P><B><I>You say, 'Hail, Idelia the Serene'</I></B>
<p><I>Idelia the Serene says 'Yes, hello. I am quite busy and afraid that I cannot... unless... do you know anything of the Marauder's Mire?'</I></P>

<P><I><B>You say, 'Yes I know about Marauder's Mire'</B></I>
<p><I>Idelia the Serene says 'My sister Iquea has been trapped there for ages. I have recently found a piece of a relic that will send people there and back, my sister holds the other piece, but I've yet to find a way to break that place's hold on her. I would gladly do what I could for you in exchange for some assistance, but I have little to offer in trade except perhaps a trip to the Mire, though I doubt you would want to go to that place. '</I></P>

<P><I><B>You say, 'I will assist you'</B></I>

<p><I>Idelia the Serene says 'Well, I have Jago leFar's notes on the day that he brought this curse back upon the world, but I am having trouble getting anything other than legend and lore about the day that Captains Vekeros and Dulein met in battle and created the Mire in the first place.'</I></P>

<P><B><I>You say, 'Who is Captain Vekeros?'</I></B>

<p><I>Idelia the Serene says 'Captain Greegar Vekeros was one of the most respected Captains ever to sail the Norrathian seas. A fine man, raised only by his mother for when he was a small boy his father had died by the hand of Dorian Dulein. On a journey seeking riches to shower upon his bride to be, his ship caught the sights of the Rotting Revenge, Dulein's ship, and he refused to run, he would avenge his father that day. The battle was indeed fierce and Dulein left Vekeros and his crew in an eternal state of undeath. Perhaps if you were to aid the captain in some way, he may share with you something that could help me in my mission. Talking with him is often fruitless, he seems to only want to talk about his lost love if anything at all.</I></P>

<li><P>Talk to Captain Vekeros (zombie, non-hostile). He's located across the island in the water on the bow of the sunken ship (approx n1200, p2130).</P>

<P><B><I>You say, 'Hail, Captain Vekeros'</I></B></P>
<p><I>Captain Vekeros stares off into the water, moaning softly.'</I> </P>

<P> <B><I>You say, 'who is your lost love?'</I></B></P>
<p><I>Captain Vekeros says 'Veddiccooo..Wwwwww... '</I> </p>

<P><I><B>You say, 'who is Vedico?'</B></I></P>
<p><I>Captain Vekeros says 'Ringgggg... loveeeeeee her... '</I> </p>

<P> <B><I>You say, 'what ring?'</I></B></P>
<p><I>Captain Vekeros says 'Worth... more than life... for... her to know truth... Finnnnnnd............ '</I></p>

<P>At this point he gives you a lore, magic, no drop ring.</P>

<LI><P>Head to the docks of Butcher Block Mountains and go inside the Port Authority house (on the docks). Give the ring to Vedico Windwisper at loc p1000, p3000 (who is Captain Vekeros’ widow, just doesn’t show the same last name).

<p><i>Vedico Windwisper grasps the ring in her hands and begins to sob. She reaches into her pocket and pulls out a shining white eyepatch and hands it to you. 'This was his, but I have no need of it now, 'tis a shame that the skeletons who attacked our ship tore the gem from it Would that I could see it repaired and returned to my lost love, wherever he may be. The ring will forever remind  me of his devotion... ' You gain experience!!</i> You will get a broken eyepatch at this point.</p>

<p><b><i>You say, 'what gem?'</i></b></p>

<p><i>Vedico Windwisper says 'As we passed through the Ocean of Tears, a horde of skeletons stormed the ship and grabbed what valuables they could find. The horrible things grabbed the gem and tore it from the eyepatch before making their way back to their island. Would that I could have mended it, but... well, I suppose I cannot dwell?</i></p>

<LI>Travel to OOT to what was previously Spectre Island. Kill mobs on the island. "a tortured revenant" and "a cutthroat skeleton" drop the gem that you need. It's simply&nbsp;called "eyepatch gem." Combine the eyepatch gem and broken eyepatch in a sewing kit (no skill required) to get the completed eyepatch.

<li><p>Take the completed eyepatch back to Captain Velkeros who says,&nbsp;'Take... do as.. want... ' He gives you&nbsp;a 'captains log entry.'</P>

<li>Give the log entry to Idelia the Serene.

<p><EM>Idelia the Serene says 'This is wonderful, I shall have some work to do restoring it to a readable condition, but it could be the missing piece of my quest- thank you for your help! Here is a piece of my relic, wrap it in the black spirit of only the coldest pirate to produce a stone with which I can send you to the Mire-though why you would want to venture over there I cannot imagine. Iquea is on the other side and will be able to use your piece of the relic to send you back without the black spirit. '</EM> Idelia will reward you with "a piece of relic." </P>

<p><STRONG><EM>You say, 'who is the coldest pirate?' </EM></STRONG></P>

<P>Idelia the Serene says 'No pirates sail colder waters than the pirates of the Iceclad Ocean. ' Head to Iceclad Ocean to the gnome pirate camp by the docks and kill patrolling gnome pirates. The drop of the black spirit is not a common drop but fairly frequent. It's a 2 slot container, it is lore and no rent, that you insert the relic into and combine, recieving both a key for the mire and the relic bit back. Simply hand the charged relic fragment (NO RENT) to Idelia and she'll send you to the mire.</P>
</OL>

<P>Your quest is done and you may return to the mire at any time by telling Idelia, "I wish to go to the mire."</P>

<p>** MORE SPOILERS &amp; THEORIES**</P>

<P>The Mire is basically an alternate dimension of Erud’s crossing: Same terrain, different mobs. Tell Idelia's sister on the other side, "I wish to go 
home."</P>

<P>Marauder’s Mire is NOT a hunting zone. It is a group oriented raid zone. There are four groups of mobs, akin to 
four mutinous Dulein captains. Some of these groups are rooted, a la North ToV. Precise group work is needed here obviously. Toruan the Uncursed speaks of 
Dulein’s treasures. And that Duelin himself still holds the hilt and seal of the lost sword and shield. Sounds like an epic encounter to me. Kill the groups of undead… spawn an undead captain… loot his treasure… kill all four captains to spawn DULEIN himself.</P><br><br>-- Compiled from information all over the place. Post Corrections and Comments to our message forums please!</FONT><br><p>

