<p align="center"><font face="Verdana" size="2"><strong>The Price of ’Man’ and ’Woman’: <br />
A Hedonic Pricing Model of Avatar Attributes in a Synthethic World</strong>
</font>
 </p>
<p align="center"><font size="1"><font face="Verdana">EDWARD CASTRONOVA <br />
California State University, Fullerton - Department of Economics; <br />
CESifo (Center for Economic Studies and Ifo Institute for Economic Research)</font>
 </font>
</p>
<p align="center"><font face="Verdana" size="1">June 2003 </font>
</p>
<p align="center"><font face="Verdana" size="1">CESifo Working Paper Series No. 957&nbsp; </font>
</p>
<p><font size="1"><font face="Verdana"><strong>Abstract:</strong>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <br />
This paper explores a unique new source of social valuation: a market for bodies. The internet hosts a number of large synthetic worlds which users can visit by piloting a computergenerated body, known as an avatar. Avatars can have an asset value, in that users can spend time to increase their skills; these asset values can be directly observed in online markets. Auction data for avatars from the synthetic fantasy world of EverQuest are used here to explore a number of questions, especially those involving the relative value of male and female avatars. In EverQuest, about 20 percent of the avatar population is female, and there are no sex-based differences in avatar capabilities. Many avatars (about one-fourth to onefifth of the population) are cross-gendered, being piloted by a person of the opposite sex. Nonetheless, relations between avatars are gender-based, and include chivalry, dating, and sex. Female avatars tend to be concentrated in highly sexualized Human and Elven races, with very few being present among such aesthetically-challenged races as Ogres and Trolls. Hedonic analysis of the auction price data suggests that gender labels are a less important determinant of avatar values than the "level," a game-design metric that indicates the overall capabilities of the avatar. Thus, ability seems more important than sex in determining the value of a body. Nonetheless, among comparable avatars, females do sell at a significant price discount. The average avatar price is 333 dollar; the price discount for females is 40 to 55 dollar, depending on methods. The discount may stem from a number of causes, including discrimination in Earth society, the maleness of the EverQuest player base, or differences in well-being related to male and female courtship roles. We do know, however, that these differences cannot be caused by sex-based differences in the abilities of the body, since in the fantasy world of Norrath, there are none.</font>
