<font size="2" color="#505050">** Spells ** 
<br> <br>
<b>- Added the ability for any caster to research dropped priest spells using the research books found in Gunthak. The research notes for these spells can be found in the Plane of Knowledge. Wizards, Enchanter, Magician, and Necromancers are able to create any of the researchable priest spells. <br>
- The recipe to create Familiar Enhancement will now actually create Familiar Enhancement instead of Firetree's Familiar Augment. </b><br>
- Beastlord pets can now be summoned in the Plane of Sky. <br>
- Changed the recipe for Form of the Great Bear, both in text and in practice. Please read the appropriate research book for the new recipe. <br>
- Removed the chance to fail on the two combines for the Pureblood quest. <br>
- Modified the Pureblood quest to return Blood of Nadox for Shaman characters. In addition, any Shaman who has previously done the quest and received Pure Blood will be able to obtain the Blood of Nadox scroll simply by hailing the quest NPC. <br>
- Increased the drop rate significantly on the quest items for the ShadowKnight and Necromancer Legacy of Ykesha spells. <br>
- Added an NPC to the list of monsters that can be summoned by Magicians in the Torgiran mines. <br>
- Force Shield will now stack with the Spiritual Purity line no matter what order they are cast in. <br>
<b>- O'Keil's Levity will no longer be overwritten by other damage shields. <br>
- O'Keil's Levity will no longer claim to be piercing people with thorns. </b><br>
- Shaman of Mithaniel Marr will now be able to scribe the Imbue: Diamond spell <br>
- Changed Strength of the Diaku to a single target strength and dexterity buff. <br>
- Changed Talisman of the Diaku to a group strength and dexterity buff. <br>
- Replaced the shaman spell Aim of the Ginda with Talisman of Alacrity, which is a group haste spell. <br>
- Replaced the shaman spell Talisman of the Gindan with Tiny Terror, which is a group shrink spell. <br>
- The Darkness line of spells should now have their sounds back. <br>
- Winged Death now has its particle effects back.<br> 
- Song of the Deep Seas now says: "The fury of the deep sea fills your arms" when sung (changed "see" to "sea"). <br>
- Changed the spelling of Malise to Malaise. <br>
- Boneshear spell messages changed to make more sense. <br>
<b>- Iceflame of E`Ci now gives a message when it wears off. </b>
