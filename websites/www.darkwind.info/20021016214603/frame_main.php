<br />
On tuesday it turns out that not only has the release date been pushed back to whenever but in addition to that it will be localized version of PoP. This is the version that <b>will not work</b> for european players on the current existing servers (US servers + Antonius Bayle + Kane Bayle). I don't know how many european Everquest players there are but it's probably a substantial number.<br />
<br />
How many of these european players will refrain from paying $29 for the actual expansion and another $30 on top of that just for shipping? Probably quite a few.<br />
<br />
How many of these european players will by mistake buy the localized version of PoP once it becomes available in their country, unaware of the fact that it will not work on their current server? Probably quite a few.<br />
<br />
Obviously there are numerous ways to obtain a legitimate copy of the US version of PoP, be it by ordering from Sony and paying a hefty sum for shipping or having a friend in the US buy an extra copy for you, email you the CD key and download an ISO of the CD.<br />
<br />
We are not worried about being able to actually obtain our copies of the <b>non-localized version</b> of the expansion, its more the BS we have to endure while doing so.<br />
