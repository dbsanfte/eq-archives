<th colspan="2" valign="top">Heroic Opportunities</th>
<tr><td><table width="85%" align="center">
<tr><td width="85%"><span class="postbody">Heroic Opportunities allow players to work together in order to trigger special effects while fighting.  They range from simple one archetype based combination to multiple archetypes working together in order to get the best effects.  A well working and cohesive group can really improve their effectiveness in battle by triggering the right combinations called for in the encounter.<p>

To first understand heroic opportunities, one must learn the <a href="scout.php" class="nav">basic symbols</a>(click link to see examples of the early scout symbols) for their archetype.  To find out your symbols you will want to right click and examine a combat art or spell.  On the examine display you will see two icons in the upper right.  The top one is the icon for the knowledge book and hotkey bar.  The lower one is what you will see appear in the heroic opportunity display.  Examine all of your spells and arts so you get an idea of how each is used.  Don’t worry about memorizing them right now because when you have an opportunity to advance a chain the correct icons will begin to flash. Through practice you will learn to associate the symbols with your spells and hotkeys to make you more efficient and not having to look for the flashing symbols.  To keep things organized and color coded, each archetype has a color corresponding to their symbols.  Fighters are <font color="#0000FF">blue</font>, mages are <font color="#FF0000">red</font>, priests are <font color="#FFFF00">gold</font> and scouts are <font color="#008000">green</font>.<p>

<img src="images/starter_chain.jpg" alt="" width="145" height="103" border="0" align="right">
Each archetype gets a heroic opportunity chain starter.  Fighters get Fighting Chance, Scouts get Lucky Break, Mages get Arcane Augur, and Priests get Divine Providence. Once you trigger a starter chain the symbol that triggered it will appear in the lower right of your screen.  Around it will be additional symbols that trigger the next step of the chain.<p><br></p>


<img src="images/wheel1.jpg" alt="" width="150" height="161" border="0" align="left">In order to advance the chain you or the group will need to use a combat art or spell with one of the symbols displayed.  Once you advance to the final step a wheel like box with one or more symbols listed will appear.  Below the circle is the name of the effect that is triggered upon completion of this step.  Also to the outside is a blue bar that goes around the wheel that acts as a timer. The heroic opportunity needs to be completed before the blue bar is gone or else the opportunity is cancelled.  Depending on the chain used the middle will see one of two symbols.  The first is an arrow that points in a clockwise direction while the second is a set of arrows which point to each symbol. The first arrow means the arts need to be executed in order while the second means you can complete them in any order. Once you complete the final chain, the heroic opportunity effect is finally triggered and the heroic opportunity is completed. The effects very by the chains the group completes.  Some are simple solo or group buffs, others may do direct damage to a target and some of the best ones do group health and power heals.<p>

<img src="images/shift.jpg" alt="" width="83" height="96" border="0" align="right">The scouts provide additional heroic opportunity resources.  The scout is the only class that can shift the heroic opportunity effect.  When the starter chain is initially completed and the heroic opportunity is displayed you will also see a small green coin symbol to the lower right of the circle.  The coin corresponds to several scout combat arts like evade, dirty tricks and cheap shot. Dirty Tricks is often your best shifting art.  It's a .5 sec cast with a 10 sec refresh so works well for shifting and doesnt disrupt your pace. If you can evade is nice as well since that will lower your aggro. If the scout can complete this before the first symbol in the heroic opportunity is completed then a new effect within that heroic opportunity set is chosen.  The choice is random but it does give the group a chance to get a better effect. A scout that knows the different effects can then help out the group by giving extra chances to get the better effects overall.<p>

This is a lot of information and may seem confusing at first but hopefully a simple example will clear up any confusion.  The scout solo heroic opportunity is first started by using their archetype skill, Lucky Break.  This brings up the scout starter chain.  One of the symbols is the green coin.  Since the scout is solo they choose to evade which advances the chain.  This brings up the effect.  The more common effect is Swindler’s Luck but the other two effects are Ringing Blow and the very rare Bravo’s Dance.  The scout has two options right now use a dagger skill like quick strike or try to shift the heroic opportunity to a better one.  If they choose to get a better one then they will need to execute a coin art like cheap shot or dirty tricks.  Once shifted or if the effect was good to begin with then they can execute the final dagger art with quick strike.<p>

This is just a basic summary of heroic opportunities but there is still much for you to learn.  The biggest and probably most difficult part to learn is the teamwork side and knowing when to use the various opportunities.  It helps out to have a group that works well together but even a pickup group can use heroic opportunities to their benefit with a little bit of work.<p>
