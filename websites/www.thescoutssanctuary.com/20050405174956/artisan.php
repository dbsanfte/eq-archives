<th colspan="2" valign="top">The Artisan</th>
<tr><td><span class="postbody"><span class="topictitle">Artisan:</span> The artisan is made up of 3 classes and 9 sub classes.  This is slightly different from the adventurer archetypes but still keeps to the same design.  The artisan branches out into 3 classes craftsman, outfitter and scholar. </span></td></tr>
<tr><td><span class="postbody">&nbsp;</span></td></tr>
<tr><td><span class="postbody"><span class="topictitle">Craftsman:</span> The craftsman is responsible for a wide array of items.  They range from basic foods to furnitures to bows and the wooden weapons. The three sub classes of the craftsman are provisioner, wood worker and carpenter.</span></td></tr>
<tr><td><span class="postbody"><span class="topictitle">Carpenter:</span> The carpenter specializes in making items for around your apartment and guild house.  So if you need some decorations and furniture for you place these are the guys to seek out.</span></td></tr>
<tr><td><span class="postbody"><span class="topictitle">Provisioner:</span> The provisioner is your baker and brewer combined into one.  They will have the skills to make you a good meal and also some fine drinks.</span></td></tr>
<tr><td><span class="postbody"><span class="topictitle">Woodworker:</span> The wood worker specializes in the wooden weapons and items.  They will be excellent fletchers but will also make the bard instruments and other wooden weapons and shields.</span></td></tr>
<tr><td><span class="postbody">&nbsp;</span></td></tr>
<tr><td><span class="postbody"><span class="topictitle">Outfitter:</span> The outfitter is the one who specializes metal weapons and the worn armors. Their armors can vary from the cloth armor and robes to the high end plate armors.</span></td></tr>
<tr><td><span class="postbody"><span class="topictitle">Armorer:</span> The armorer specializes in making the heavier armors.  So you will see them crafting fine sets of vanguard, plate and chain armors.</span></td></tr>
<tr><td><span class="postbody"><span class="topictitle">Tailor:</span> The tailor compliments the armorer by crafting the lighter armors.  If you need a robe, leather, woven or just something to wear around town then the tailor is the right person for you.</span></td></tr>
<tr><td><span class="postbody"><span class="topictitle">WeaponSmith:</span> The weaponsmith specializes in making the metal weapons.  So they will create the swords, axes, scimitars, maces, flails and other similar weapons which you might need.</span></td></tr>
<tr><td><span class="postbody">&nbsp;</span></td></tr>
<tr><td><span class="postbody"><span class="topictitle">Scholar:</span> The scholar is responsible for making many of the other items that are remaining.  They will create the spell/arts upgrades, jewelry, talismans, potions and poisons.</span></td></tr>
<tr><td><span class="postbody"><span class="topictitle">Alchemist:</span> The alchemist no longer for shamans only.  The alchemist now has a broader range of skills specializing in poisons and potions.</span></td></tr>
<tr><td><span class="postbody"><span class="topictitle">Jeweler:</span> The jeweler is responsible for creating fine jewelry and also the creation of talismans.</span></td></tr>
<tr><td><span class="postbody"><span class="topictitle">Sage:</span> Sages are the people who create the spell and combat arts upgrades for everyone.</span></td></tr>






