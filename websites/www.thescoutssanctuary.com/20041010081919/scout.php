<th colspan="2" valign="top">The Scout Archetype</th>
<tr><td><span class="postbody"><span class="topictitle">Primary attribute:</span>  Agility</span></td></tr>
<tr><td><span class="postbody"><span class="topictitle">City:</span> Either</span></td></tr>
<tr><td><span class="postbody"><span class="topictitle">Traditional Races:</span> Barbarian, Dark Elf, Froglok, Gnome, Half Elf, Hafling, High Elf, Human, Iksar, Kerra, Ratonga, Wood Elf</span></td></tr>
<tr><td><span class="postbody"><span class="topictitle">Primary roles:</span>  DPS, heroic opportunities</span></td></tr>
<tr><td><span class="postbody"><span class="topictitle">Secondary roles:</span>  Evacs, run speed buffs, opening treasure chests</span></td></tr>
<tr><td><span class="postbody"><span class="topictitle">Key Knowledge:</span>  Weapon knowledge, poisons, traps, reconnaissance</span></td></tr>
<tr><td><span class="postbody"><span class="topictitle">Weapons:</span> Swords, daggers, axes, flail, hammer, mace, spear, bows and many more</span></td></tr>
<tr><td><span class="postbody"><span class="topictitle">Armor:</span> Light (gets progressively higher with level)</span></td></tr>
<tr><td><span class="postbody"><span class="topictitle">Classes::</span> <a href="bards.php">Bard</a>, <a href="predator.php">Predator</a> and  <a href="rogue.php">Rogue</a></span></td></tr>
<tr><td><span class="postbody">&nbsp;</span></td></tr>
<tr><td><span class="postbody"><span class="topictitle">Damage:</span> Scouts rely on combat arts to inflict deadly bursts of damage.  Burst damage is much like an EQLive wizards spells where they had to refresh and also had a casting time.  Scouts won't have a casting time on arts but there will be refresh timers between using the more damaging arts.  The opportunistic attacks as they are called rely heavily on power to function.  SO to be efficient as a scout you will need to be aware of your power use on opportunistic attacks and keep some in reserver for the heroic opportunity shifts.<p></p>  
When discussing fighters and scouts its important to note they have very different styles of attack.  The fighter is a very consistent damager but tends to do mid range damage per hit which adds up.  The scout as noted above does burst damage with their opportunistic attacks but in between opportunistic attacks they do very low damage.  Overall it is said the fighters will do more by melee damage but there are still many questions to be answered regarding that.
</span></td></tr>
<tr><td><span class="postbody">&nbsp;</span></td></tr>
<tr><td><span class="postbody"><span class="topictitle">Heroic opportunities:</span> Heroic opportunities are started by anyone in the group by using special arts or spells.  The group members then follow the prompts and execute the correct art or spell.  The end result is a group bonus effect of some sort which varies from power or health restoration to dps bonuses.  Where the scout comes in is to shift the end result from the choice the game gives you to a better one depending on what is available.</span></td></tr>
<tr><td><span class="postbody">&nbsp;</span></td></tr>
<tr><td><span class="postbody"><span class="topictitle">Other Skills:</span> Scouts get a wide variety of useful utility skills. They have the ability to evac a part from a dungeon. They are also the ones who specialize in opening treasure chests.  Without a scout you are likely to break a chest and possibly destroy an item so a scout is invaluable in that situation.  Scouts also get a mix of skills using poisons and traps with the rogues later specializing in poisons and the predators in traps.</span></td></tr>
