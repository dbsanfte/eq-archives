	<B>Foreword:</B>

	<BR>

	Players must be level 70 AND meet the minimum requirements for their class before they can be considered 

	for application. There will be ABSOLUTELY NO EXCEPTIONS.  Players must be dedicated to Conviction and to its members. A 

	raid attendance <B>minimum</B> of 60% is <B>required</B> during all stages of 

	the process. We only accept applications from persons that are not 

	consecutively applying to any other guilds. It is also a requirement that you 

	are not tagged for any other guild at any time during our applications process.  

    Probably the most important

	requirement of all, is that you keep an UPDATED and COMPLETE magelo profile!  We will look at it for your flags,

	gear, AAs, stats, resists, etc.  An outdated magelo could possibly hurt your application, so keep it fresh!  Don't

	have one?  Make one at <a href="http://www.magelo.com" target="_blank">http://www.magelo.com</a>.  <BR>

	<BR>

	Please also note that Conviction is a Ventrilo based guild.  What does this mean?  Ventrilo is an internet

	based voice communication program.  We use it to communicate with eachother both in raids and out, and it helps

	to build a stronger bond between guild members.  Do you have to have Ventrilo to become a member of Conviction?  No,

	however all members and applicants are strongly encouraged to at least download the Ventrilo client program so they may

	at least listen in, as most raid strategy disscussion and instruction is communicated through Ventrilo.  We still do our 

	best to communicate all essential information in appropriate raid chat channels.<br>You can download the client free from <a href=http://www.ventrilo.com target="_blank">http://www.ventrilo.com</a>.<br><br>

	

	<B>What can I expect from Conviction?</B><BR>

	<BR>

	You can expect to be respected, valued, and treated as an individual. You can 

	expect a group of players and friends that will be striving to achieve their 

	utmost so that together, as a team, we can accomplish higher goals and 

	advancements without losing sight of what is most important. HAVING A GOOD TIME! You can expect 

	honesty; clear, well posted policies; officers that will listen to your 

	opinions, problems, and thoughts - and will take each into account. You can 

	expect that guild members know their classes and their roles. You can expect a 

	level of respect for other players, maturity, and honor. You can expect people 

	that want the same things that you do. What does Conviction expect from me? The 

	members of the guild expect those interested in joining their ranks to hold the 

	same beliefs and values. We expect that you will uphold and honor the name of 

	the guild and will do your best to enhance and preserve its good reputation. 

	Everyone will expect that you know how to play your character and are willing 

	to grow and learn along with the rest of us. We expect honesty, determination, 

	tenacity, drive, friendly nature, personal maturity, and conviction from you.

	<BR>

	<BR>

	<B>What does Conviction NOT expect from me?</B><BR>

	<BR>

	Our good reputation is of tantamount importance to us. We are a closely-tied 

	group and want people of likeminded ideals. We are not looking for those that 

	abuse others in any way, shape, or form. We are not looking for those that 

	break the rules -- spoken or unspoken of EQ -- such as posting inflamatory 

	remarks on other boards that may damage the guild's reputation, training 

	others, arguing in /shout or /ooc. We are not looking for drama queens or 

	troublemakers. If you do or are any of the above, don't come to us. We will 

	take harsh, decisive steps to protect our valued members from such things. If 

	you do gain access to the guild and then reveal these things to be in your true 

	nature, we will take definitive measures to remove the trouble from our midst, 

	up to and including guild disbandment. Remember, we're out to protect ourselves 

	and our members. They are most important.<br>

	<BR>

	<B>Procedure:</B>

	<BR>

	Conviction has a three-stage application procedure that 

	all applicants to the guild must undergo.<BR><BR>

	That procedure is as follows:<BR>

	Applicants submit an application to the guild. Honesty is required. Any dishonest answers that may 

	later be discovered as such are grounds for the immediate dismissal of any applicant.  This 

	application is reviewed by the officers and will be posted to the guild application forum

	if it meets current requirements and has been fully completed by the potential applicant.

	Once posted, applicants will enter into Phase 1 status.  <BR>

	<BR>

	<B>Phase One - Open Application:</B>

	<BR>

	Applicants are invited into the applicant channel and 

	are encouraged to join the guild on raids and/or in groups. Applicants receive 

	.75 (75%) DKP from any and all raids they attend during this period. (A one time 25% dkp deduction is made on the day of your acceptance as a full member) Applicants 

	are required to behave in a mature, polite fashion both in and out of guild. 

	Anything less is grounds for immediate dismissal by the officers.<BR>

	<BR>

	This phase will include our reflagging period if necessary at the time. During 

	this reflagging period an applicant must be sponsored by a full Conviction 

	member. Sponsorship may not be solicited. A sponsor will speak up for 

	candidates and the candidates will be notified by their new sponsor. An 

	applicant has 2 weeks to obtain a sponsor. If they 

	are unable to obtain a sponsor in that time, then their appliation will be 

	terminated.<BR>

	<BR>

	Upon sponsorship and the successfull completion of 

	reflagging, an applicant moves to Phase Two.<BR>

	<BR>

	<B>Phase Two - Sponsored Application:</B>

	<BR>

	Applicants are invited into the guild channel and are 

	given the Conviction guild tag. They are strongly encouraged to keep a high 

	attendance level for raids and to seek out members for adventuring or grind 

	groups.  Applicants are required to behave 

	in a mature, polite fashion both in and out of guild. Anything less is grounds 

	for immediate dismissal by the officers. At this stage in the application process applicants

	will also be given the password to the guild Ventrilo server.  After three weeks as a Phase Two applicant, candidates 

	will be voted upon by all members of the guild. If an applicant recieves two well explained

	no-votes then their application will be denied and they will be excused from the guild.

	Those accepted will pass into Phase Three.

	<BR>

	<BR>

	<B>Phase Three - Probationary Membership:</B>

	<BR>

	Applicants are given membership in the guild with a 

	one month probationary period during which they are expected to comport 

	themselves in a manner that is respectful of others both in and out of guild. During this time you are a FULL member of this guild. This is a time of further evaluation by the officers of the guild.<BR>

	<BR>

	Probationary members will receive full (100%) DKP from any and 

	all raids they attend during this period. Probationary members may also bid on all 

	loot as full members.  Probationary members are required to 

	behave maturely. They are expected to behave in a moral fashion and have only 

	honest dealings with members and non-members. They wear the guild tag and 

	represent us. Any infraction that is brought up in complaint to the officers 

	will be reviewed seriously and investigated. Serious charges are grounds for 

	immediate dismissal by the officers. Less serious charges may be brought before 

	the guild for discussion, and a vote may be taken to sever any association with 

	the offender. After four weeks in Phase Three, the probationary member 

	is granted full and unconditional membership by the officers of the guild.<BR><BR>

	

