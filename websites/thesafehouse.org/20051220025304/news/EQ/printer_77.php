	 <div class="article_title_prnt">EQ January 26, 2005 Patch</div>
     <div class="article_auth_prnt">By Kevin "Nocte" Dolley</div>
     <div class="article_details_prnt">Jan 26, 2005, 13:21</div>
	 <br><br>
     <p><div class=postbody align=left>Thanks to Shmoozneak for the heads up! <br><br><span style="FONT-WEIGHT:bold">*** Update Headlines *** </span><br>---------------------------------- <br>Mistmoore and SplitPaw revamps live for all servers <br>New and improved Lavastorm! <br><br><br><br><span style="FONT-WEIGHT:bold">Item Changes</span> <br>---------------------------------- <br></div>
<div class=postbody align=left>
<ul>
<li>Many Epic 2.0 effects have been modified. Please see the EQforums at Eqforums.station.sony.com for details. <br>
<li>These items are now primary and secondary slot usable: <br>
<ul>
<li>Totem of Shattered Hope <br>
<li>Wand of Twisted Fate <br>
<li>Infernal Staff of Fiery Fate <br>
<li>Bazu Claw Hammer </li></ul><br>
<li>Increased the drop rates on some items from Planes of Power zones that were too rare <br>
<li>The ending effect (casts when the rune wears off) of the Enchanter 1.5 and 2.0 epics has been changed so that it won't cause initial aggro on surrounding NPCs. It will only lower the enchanter's hate on NPCs in the area of effect that are already aggro on him/her. <br>
<li>Mace of Grim Tidings is now No-Drop <br>
<li>Added a Globe of Discordant Energy drop to Anguish. <br>
<li>Fixed the combine for the cleric epic 1.5, AA points should now be rewarded. People who missed their AAs can hail Tavon to receive their AAs, if they have spoken to Natvil already. <br>
<li>Revised the various Anguish armor sets. Some pieces now have effects added <br>
<li>Earring of Eternal Flame - This item now has the focus effect of Flame of the Lightbringer <br>
<li>Anuek Dagger of Eye Gouging - This item now has a +12% mod to backstab. <br>
<li>Deathwhisper - This item now has +2 mana regen and +2 regen <br>
<li>Upgraded the +damage augments from Tacvi </li></ul></div>
<p align=left><br><br><br><br><span style="FONT-WEIGHT:bold">Spell, Discipline &amp; AA Changes</span> <br>---------------------------------- <br></p>
<ul>
<li>
<div align=left>The skills windows will now show the modified skill of the player, including buffs/item mods, etc. <br></div>
<li>
<div align=left>Added a negative resist modifier to Crippling Strike, Stunning Kick, and Eye Gouge. <br></div>
<li>
<div align=left>Modified Hastened Purification of the Body to reduce the base time. <br></div>
<li>
<div align=left>The spell, Elemental Simulacrum Recourse, should no longer be removed when the recipient is hit in melee. <br></div>
<li>
<div align=left>Hide will work while levitated again. <br></div>
<li>
<div align=left>Tiny companion will again switch target to the pet correctly. <br></div>
<li>
<div align=left>An exploit with Lifeburn has been corrected. <br></div>
<li>
<div align=left>/stopsong, /cast, and anything starting a song other than /melody now clears the /melody buffer as intended. <br></div>
<li>
<div align=left>Bard Chants should once again generate the correct amount of hate. <br></div>
<li>
<div align=left>Fixed problem with the Ranger AA Guardian of the Forest where if you had maxed buffs this AA would not reset if it did not take. <br></div>
<li>
<div align=left>High level shaman pet should now automatically dual wield. <br></div>
<li>
<div align=left>Modified the effect slots for Hobble of Spirits so that it will stack with other contact innates. </div></li></ul>
<p align=left><br><br><br><span style="FONT-WEIGHT:bold">Code Changes</span> <br>---------------------------------- <br></p>
<ul>
<li>
<div align=left>Players will now receive a message when they are too close for ranged weapons to be used when /autofire is being used. <br></div>
<li>
<div align=left>Ranged weapons are no longer subject to additional z-distance limit checks. <br></div>
<li>
<div align=left>Server side collision fixes have corrected various problems in certain areas in new zones such as the ramp entering Abysmal that was causing warping problems for NPCs. </div></li></ul>
<p align=left><br><br><br><span style="FONT-WEIGHT:bold">Event Changes</span> <br>---------------------------------- <br></p>
<ul>
<li>
<div align=left>Players can now select a type of LDoN Adventure (Rescue, etc.) when requesting one from the Recruiter. <br></div>
<li>
<div align=left>Lowered combat regeneration of the Dragorn and Bazu forms in the Trial of Adaptation. <br></div>
<li>
<div align=left>Modified the spawn timer on the Tyrant of Nightmare in the Plane of Nightmare and the Decaying Lightcrawler in the Plane of Disease. Both should make more frequent appearances. <br></div>
<li>
<div align=left>Lowered spell resists significantly in all MPG Raid and Single Group trials. <br></div>
<li>
<div align=left>Fixed some oddness that happened sometimes when Memblurring Hanvar. <br></div>
<li>
<div align=left>The non-loot dropping version of Arch Magus Vangl will be easier than the loot dropping version. </div></li></ul>
<p align=left><br><br><br><span style="FONT-WEIGHT:bold">Miscellaneous</span> <br>---------------------------------- <br></p>
<ul>
<li>
<div align=left>Changed the level 70 bard /who title from Performer to Harold, er, Herald. </div></li></ul>
