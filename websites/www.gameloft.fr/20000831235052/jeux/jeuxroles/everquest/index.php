              <p><font class="titre"><b> <i><!-- TITRE 1 DE LA NEWS --></i></b></font>Voici 
                le résumé du dernier patch. Encore une fois, il a largement débordé 
                sur les horaires terriens prévus et il a " loggé off " tous les 
                aventuriers qui s'étaient connectés avant 5 heure pour pouvoir 
                jouer durant la maintenance comme l'avait annoncé Verant. Donc, 
                voici la liste des changements : <br>
                <br>
                - le bug qui amenait le crash du jeu si vous quittiez l'écran 
                de sélection des serveurs et tentiez de vous reconnecter directement 
                a été corrigé <br>
                <br>
                - Le temps de latence quand vous " switchez " le mode de fenêtre 
                a également été corrigé <br>
                <br>
                - La commande /book " numéro de la page " accepte désormais les 
                chiffres de 1 à 50 <br>
                <br>
                - L'ouverture de votre livre de sort lance automatiquement la 
                méditation et la commande /meditate a donc été entièrement retirée. 
                Vous pouvez en outre utiliser les hotkeys quand vous méditez<br>
                <br>
                - La commande /target " nom " est fonctionnelle et ciblera automatiquement 
                la personne indiquée en fonction de la distance qui vous sépare 
                d'elle <br>
                <br>
                - Les couleurs par défaut des textes ont été réintégrées à la 
                place des nouvelles couleurs qui les avaient remplacées et vous 
                pouvez toujours les modifier comme vous le désirez<br>
                <br>
                - Si vous tapez @ dans un emote, ce dernier impliquera automatiquement 
                votre cible dans son message. Par exemple, si Radharc cible drathaal 
                et qu'il tape la commande " /emote rit cruellement de @ le twinké 
                ", cela donnera " Radharc rit cruellement de Drathaal le twinké 
                " <br>
                <br>
                - Ceux qui activent la commande /LFG verront leur nom accompagné 
                d'un astérix. Il est toujours possible d'en obtenir la liste en 
                tapant la commande /who ou /who all " classe nivs lfg "<br>
                <br>
                - Les sorts de sens undead, animal et summoned ne vous indiqueront 
                plus les pets<br>
                <br>
                - La reconnaissance vocale est désormais active sur tous les serveurs. 
                Vous trouverez les informations nécessaires dans le fichier " 
                EQROKManualSupplement.doc " de votre répertoire EQ. Vous devez 
                déjà avoir un programme de reconnaissance vocale installé sur 
                votre machine et il doit être compatible avec le Microsoft Speech 
                API (SAPI). </p>
