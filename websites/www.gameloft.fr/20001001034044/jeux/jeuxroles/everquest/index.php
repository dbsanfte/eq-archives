            <td colspan="2" bgcolor="#364334" valign="top"> 
              <p><font class="titre"><b> <i><!-- TITRE 1 DE LA NEWS --></i></b></font>Comme 
                toujours, voici la liste des changements apportés par le patch 
                de ce jeudi. Ils sont assez nombreux mais certains reprennent 
                les changements qui avaient été annoncés dans le courant de la 
                semaine :<br>
                <br>
                - Les corps des PNJs de niveau 55 et plus restent maintenant 30 
                minutes avant de disparaître. <br>
                <br>
                - L'objet " Woe " a été réparé. Si vous voyez la phrase " Item 
                out of charge apparaître, il vous suffit de contacter un GM pour 
                échanger l'objet.<br>
                <br>
                - Le sort " Mark of Karn " a été changé. Le temps de latence entre 
                deux incantations a été diminué et le gain de points de vie est 
                inversement proportionnel au délai de l'arme utilisée, qui se 
                doit d'être dans la main principale. <br>
                <br>
                - Des composants pour certaines tradeskills ont été implantés 
                dans le monde afin de faciliter leur pratique. <br>
                <br>
                - Quand vous êtes AFK ou LFG, vous aurez maintenant un petit tag 
                à côté du nom de votre personnage.<br>
                <br>
                - Le dernier patch a vu la recrudescence des créatures fuyantes. 
                Cette tendance reste toujours effective mais elle a été rajustée 
                et les créatures fuiront donc un peu moins. Par ailleurs, les 
                PNJs charmés ne fuient plus quand leur vitalité est trop basse. 
                <br>
                <br>
                - La purge des personnages de niveau 1 n'ayant pas été loggés 
                depuis 30 jours a été effectuée.<br>
                <br>
                - Tholuxe Paells est maintenant complètement opérationnel et 3500 
                personnages ont &eacute;t&eacute; cr&eacute;&eacute;s hier soir 
                … Bienvenue à tous sur ce nouveau serveur. Nous vous rappelons 
                que l'enregistrement pour le split des serveurs Mithaniell Marr 
                et Tunare sur le nouveau serveur Drinal aura lieu cette nuit de 
                9 PM (EST ) - 03h00 (France) à 3 AM (EST) - 09h00 (France). Le 
                déplacement des personnages et l'ouverture du serveur aura lieu 
                la semaine prochaine lors du patch… <br>
                <br>
                - En mode " HUD ", vous pouvez à présent déplacer les fenêtres 
                de votre interface en cliquant sur le coin supérieur gauche de 
                la fenêtre. L'interface des sortilèges et du chat peuvent également 
                être redimensionnés en cliquant sur le coin inférieur droit.<br>
                <br>
                - Les commandes /book, /sit et /doability ont été réparées et 
                ne fonctionneront plus quand vous êtes charmés ou sous l'effet 
                d'un sort de peur. <br>
                <br>
                - De nouvelles commandes sont à présent accessibles. Il s'agit 
                de /camp, /disband, /invite (qui fonctionne également pour accepter 
                une invitation) et /attack. <br>
                <br>
                - De nouveaux raccourcis sont également effectifs. Les touches 
                CTRL 1 à 8 lanceront automatiquement les sorts mémorisés dans 
                ces cases, les raccourcis " : " et " ' " pour /emote et /say feront 
                maintenant apparaître automatiquement la ligne de chat. La touche 
                " R " vous permet toujours de répondre à la dernière personne 
                vous ayant fait un tell mais il vous est également possible de 
                remonter parmi les 10 dernières personnes à vous avoir fait un 
                tell en utilisant les touches TAB et SHIFT+TAB.<br>
                <br>
                - Vous pouvez désormais insérer des pauses dans les raccourcis 
                " sociaux " en utilisant la commande /pause. Il vous suffit de 
                taper /pause + une valeur allant de 1 à 600 (il s'agit du nombre 
                de dixièmes de secondes à attendre). Ainsi, /pause 100 indique 
                une pause de 10 secondes. La configuration de vos boutons sociaux 
                sera du type :<br>
                /sit off<br>
                /cast 1 <br>
                /pause 100 <br>
                /sit on <br>
                Ce raccourci aura alors pour résultat de voir votre personnage 
                se lever, lancer le sort qui se trouve dans l'emplacement 1 de 
                vos spell slots, attendre 10 secondes et s'asseoir. Il vous est 
                également possible d'utiliser la commande /pause sur la même ligne 
                qu'une autre commande en les séparant par une virgule. L'effet 
                est le même et cette subtilité ne vous permet que de gagner une 
                ligne dans la configuration de votre bouton, comme l'indique l'exemple 
                suivant : <br>
                /sit off<br>
                /pause 100,/cast 1 <br>
                /sit on <br>
                <br>
                - Le code des emoticons a été changé, vous ne devez donc plus 
                utiliser la touche @ mais %. Il est effectif pour l'ensemble des 
                Canaux de communication et quelques nouveaux codes ont été apportés. 
                %T indique le nom de la cible %S Indique le genre du pronom personnel 
                de la cible (He, She, It) %O Indique le genre du pronom personnel 
                de la cible (Him, Her, It) %P Indique le genre du pronom possessif 
                de la cible (His, Her, Its) %R Indique la race de la cible (seulement 
                sur les PJs) Par exemple, si vous ciblez un gnoll et que vous 
                tapez l'emoticon suivant, " /point say points at %T indicating 
                that he is ready to kill %O " aura pour résultat de voir votre 
                personnage indiqué le gnoll du doigt avec comme phrase " Truc 
                points art a gnoll indicating that he is ready to kill it ". </p>
