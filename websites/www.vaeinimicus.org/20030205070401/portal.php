<span style="font-style: italic"><span style="font-weight: bold">A Little Lantern</span></span>
<br />

<br />
<span style="font-style: italic">A lantern floats upon the silent stream, 
<br />
Knowing not where it goes. 
<br />
Following, just following ahead. 
<br />

<br />
Little does the Lantern know, 
<br />
That it carries one thing so precious, 
<br />
Little does it know that is signifies a passing of a life. 
<br />

<br />
Inside you can see memories of a past life, 
<br />
Some joyous, some painful, 
<br />
Some of Uncertainty. 
<br />

<br />
Down the stream, down the stream. 
<br />
Following it’s course so, 
<br />
Little does the lantern know, 
<br />
that people shed a tear whenever it passes by. 
<br />

<br />
Four windows does the Lantern hold. 
<br />

<br />
One window holds the memories of 
<br />
Love. 
<br />
First loves, innocent crushes, and 
<br />
Stupid acts done in it’s name. 
<br />
This window is a doorway to the others, 
<br />
A key that beckons to be remembered. 
<br />

<br />
The Second window holds the memories of 
<br />
Pain. 
<br />
First fights, a slap from a parent, 
<br />
a fall from a bike, a simple cut from a knife. 
<br />
This window is a key to learning and also, 
<br />
A key to Hatred 
<br />

<br />
The Third Window holds the memories of 
<br />
Hate. 
<br />
Simple envy, a fight with one you love, 
<br />
prejudices against a certain color, 
<br />
the wanting of to hurt someone. 
<br />
This window a permanent key to life, 
<br />
Is for-ever around and will never disappear. 
<br />
This window is your fear. 
<br />

<br />
The Last Window, the Fourth Window, 
<br />
holds the key to all other windows. 
<br />
The In-betweens, the other joys, 
<br />
the simple characteristics of life, the memories of all else. 
<br />
This window is the key to 
<br />
the Renewal of Life. 
<br />

<br />
A lantern floats upon the silent stream, 
<br />
Knowing not where it goes. 
<br />
Following, just following ahead. 
<br />

<br />
Little does the lantern know that what it contains, 
<br />
Is so precious, and so important. 
<br />

<br />
Little does the Lantern know that when all have passed away, 
<br />
Who remembered this Person, 
<br />
It will be the only one who knows who this person was. 
<br />
How that person felt toward others, 
<br />
How that person loved and hated, 
<br />
How that person lived his life. 
<br />
How that person died. 
<br />

<br />
When the flame contained within, Blows out. 
<br />
The remembrance of this person has been honored. 
<br />
The lantern floats up toward the sky, 
<br />
where it is kept with other others of it’s kind, 
<br />
So that when one day when the world has ended, 
<br />
There will be a record of all who have died. 
<br />

<br />
A lantern floats upon the silent stream, 
<br />
Knowing not where it goes. 
<br />
Following, just following ahead. 
<br />
Little does it know what it signifies. </span>
