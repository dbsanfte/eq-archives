										<p>
											Larian Studios is an award winning computer game development company located in Oudenaarde, Belgium.
										</p>
										<p>
											We have released over 30 software products, ranging from classic multimedia productions to 
											arcade cabinets and the international hit 
											<a href="divine.php">Divine Divinity</a>, 
											claimed by several magazines to be one of the best games of 2002.
										</p>
										<p>
											Other big titles in our portfolio are <a href="beyond.php">Beyond 
											Divinity</a>, the successor to Divine Divinity, and <a href="kick.php">KetnetKick</a>, the world's 
											first "PC2TV" game. KetnetKick was coproduced with VRT, Belgium's national broadcaster, and is a smash 
											hit in Belgium (Christmas 2004 number one PC title).
										</p>
										<br>
										<div class="title">Profile</div>
