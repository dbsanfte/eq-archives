        <div align=left><b>==> Charter and Polices <==</b><br><br>
Harmony of Souls was an Everquest guild that was started in 2000. After many successful years in EQ the guild went into hibernation and awoke again with the launch of Worlds of Warcraft.<br><br>

HoS members work to uphold the following principals:<br><br>

Honor - All members represent Harmony of Souls and the actions of the membership effect the guild and its reputation.<br><br>

Friendship - HoS began as an idea between two friends. Throughout its history the membership has worked to honor that memory by maintaining real and giving relationships.<br><br>

Respect - Our membership is made up of many different personalities and experiences. All members must work to maintain a harmonious environment that promotes tolerance and acceptance.<br><br>


<b>==> Faction and Server Info <==</b><br><br>
HoS is an Alliance guild on the Stormrage server.<br><br>

<b>==> Guild Structure Info <==</b><br><br>
HoS is led by Koikeeper. His team of officers includes: Merek, Silli, Sybill, Korbyn, Ihnanna and Lexi.<br><br>

<b>==> Guildremoval Policy <==</b><br><br>
Try as we might there are sometimes instances in a guild where angry words are exchanged or frustrating circumstances arise. Should you remove yourself from the guild in anger or any other impulsive emotion you will be required to re-apply to HoS. Our Guild tag is a very important symbol of our guild loyalty and our commitment to our members. If you are having a problem or have an issue talk to an officer before taking action. Our officers will do all that they can to help you resolve any concerns you may have. 
<br><br>
<b>==> Privileged Information Policy <==</b><br><br>
Guild chat and information on the boards is privileged information. If you abuse this privilege your membership to the guild as well as your board access will be revoked.<br><br>
<b>==> Mortal Enemy Policy <==</b><br><br>
Any current member has the option of ME&#39;ing an applicant based on a past experience. Should an applicant receive an ME their application will be permanently suspended. ME&#39;s may be posted or reported via email to an officer.<br><br>

ME&#39;s are very serious. Members should only invoke this option if the admission of an applicant would leave the existing member no other recourse but guild removal. As with any issue or concern it is best if the member approaches an officer as soon as possible. In most situations the issues someone might have about an applicant can be resolved without an ME.
<br><br>
<b>==> Application process for people new to HoS <==</b><br><br>
HoS is currently accepting applications for membership from people new to HoS. <br><br>

All applicants must have a primary sponsor before applying.<br><br>

All applicants must be level 20 before applying.<br><br>

All applicants are required to have a total of 5 sponsors &#40;including their primary&#41; before they can be voted on by the general membership.<br><br>

The voting process requires 15 &#39;yes&#39; votes and no more than 3 &#39;no&#39; votes.  The voting process lasts two weeks.  A ME vote will immediately disqualify an applicant from membership.  If an applicant does not receive the necessary 15 &#39;yes&#39; votes before their vote ends their application goes on hold for two weeks.  After two weeks their application returns to sponsorship mode &#40;and they must get 5 sponsors to return to vote.&#41;<br><br>

<b>==> Application Format <==</b><br><br>
All applications must be in the following format:<br><br>

-Name:<br>
-Class:<br>
-Race:<br>
-Level:<br>
-Professions:<br>
-Secondary Skills:<br><br>

-MMO History and experience:<br><br>

-Previous guild affiliations: <br><br>

-Primary Sponsor:<br><br>

Once an applicant posts their application their primary sponsor will be responsible for creating a post in the guild only Application Review section of the HoS boards. The primary sponsor is responsible for informing the applicant of events and grouping opportunities.<br><br>

Once 5 sponsors have posted their support a guild officer will begin a voting thread. Voting does not occur anonymously. &#40;The EZboard poll feature is not used.&#41;<br><br>

If an applicant is voted in an Officer will post instructions on their application to contact an officer in game for their tag and they should then post in Site Maintenance for board access.<br><br>

<b>==> Application process for past HoS-EQ members <==</b><br><br>
People who were once members of HoS-EQ may post in Site Maintenance for board access and contact an Officer in game for their tag.<br><br>

There is no level limit for this application.<br><br>

Please note that the guild removal policy applies to ALL HoS-WoW members.
<br><br>

<b>==> Application process for partners/family of current HoS members <==</b><br><br>
Partners or family members of existing HoS members should post an application in the following format:<br><br>

-Name:<br>
-Class:<br>
-Race:<br>
-Level:<br>
-Professions:<br>
-Secondary Skills:<br><br>

-Partner/Family Member:<br><br>

There is no level limit for this application.<br><br>

An Officer will review this information and post instructions for contacting an officer in game for a tag. Once tagged post in Site Maintenance for Board access.
 <br>  <br> 

