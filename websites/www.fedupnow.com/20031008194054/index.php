<a class="pn-normal" href="modules.php?op=modload&amp;name=News&amp;file=index&amp;catid=&amp;topic=3"><img src="images/topics/planesofpower.gif" border="0" Alt="Planes of Power" align="left" hspace="5" vspace="5"></a>
You say, 'Hail, Jimlok Keylifter' <br />
<br />
Jimlok Keylifter says 'Well met, traveler. Can I be of assistance to you? Can't say as I got much to do at the moment. I'm just attending to a little fishing while I wait on my friend to get back to me. Hopefully he doesn't keep me waiting for too much longer. I can only stand so much of this place.' <br />
<br />
You say, 'what friend' <br />
<br />
Jimlok Keylifter says 'This is the situation. My friend Gilor Swiftfoot and I were going to try our hand at breaking into the Plane of Justice. Yeah, yeah, I'm sure you're thinking that's silly and all, but how many people do you know of that can say they've broken out of the Eternal Prison, let alone in? 'None', right? So, we were all ready to go in when I got word that something else needed re-appropriating all quick like. I told Gilor to wait until I got back, but no. He had to run off, eager as always and as patient as a gust of wind. Now, I think he's in trouble.' <br />
<br />
You say, 'what trouble' <br />
<br />
Jimlok Keylifter says 'People here seem to think that he tried to make the prison break by himself. They've found another way in, so maybe he took that route. I'd like to check on him, but if he isn't in there, I'm liable to get myself in trouble. I think I'd be much better off staying here and waiting for him. If you want to go look for him, I'd be quite grateful. I doubt you have the skills to get far in there. You don't look like the burglarizing type, but I'd even be willing to give you the document that I picked up on my last job if you look for him.' <br />
<br />
You say, 'what document' <br />
<br />
Jimlok Keylifter says 'Don't you worry about that unless you bring me some word of Gilor's condition. Trust me, it's a strange and mysterious document. Just the sort of thing you adventuring types love. Find Gilor and it's all yours, I give you my word.' <br />
<br />
You say, 'i will look for him' <br />
<br />
Jimlok Keylifter says 'Alright, friend, you need to be on the lookout for a gnome about my height, with one bad leg. He's got a bit of a limp, but he's still a hard one to chase down. If you can't find him, be on the lookout for anything that resembles a small bottle. He's got a way with rodents; so if you see some little pest carrying a bottle of some sort, grab him! It will most likely be a message for me. Bring it back immediately. It won't be of any use to you anyway, since you can't read our code. Be quick and good luck!' <br />
<br />
Go get a bottle with a note in it from &amp;#8220;a festering rat&amp;#8221; in Plane of Justice. <br />
<br />
Jimlok Keylifter sighs deeply as he hands you an intricate schematic of some device. The notation on the document is written in a strange language that is completely foreign to you. 'This doesn't bode well for Gilor. He says he's found a secret tunnel and he's going to go in deeper. I guess I'll go in and find him if he doesn't return soon. Well, a deal is a deal. Here is the document that I mentioned.' <br />
<br />
Jimlok Keylifter says 'The person that I was supposed to lift it for was a jeweler of sorts, but he mainly dealt in mechanical devices. I wasn't able to track him down to get my coin, so I don't know what you'll do with it. Maybe my cousin Tabben will give you something for it. He's got himself a little tinker's shop in the Jeral section of New Tanaan. Show him the map thingy and see what he says.' <br />
<br />
Tabben Bromal says 'What is it? What do you wan. . . Oh bloody underfoot, do forgive me, traveler. I'm not used to all this bickering and flittering about -- you know, the bustle of the 'living' city. The racket, the noise, and the wails of the children. Bah! I came here to escape it, not to relive it. But if I survived it once, I sure as Brell's holy bottom will do it again! In hopes of getting you all out of here as soon as possible and the peace and quiet being resurrected out of your blessed absence, I've agreed to lend some of my knowledge of the more inferior levels of tinkering to those who have an interest in this art. If you don't intend on purchasing any materials, then be off with you and leave me in peace. However, if you have coin in your pocket and you plan on spending, then do so quickly. I have more pressing things to do with my time than wait while you leisurely browse my wares.' <br />
<br />
Tabben Bromal takes the document from you and looks at it for a moment. 'Well, this is a beautiful looking schematic. I wonder what they are trying to make with it? Where did you get this? No wait; don't tell me. I'd rather not know. I can make some of this writing out but just barely. What I can read leads me to believe that I'd be better off not knowing.' <br />
<br />
Tabben Bromal falls victim to his curiosity and grabs a small kit from the table behind him. 'Take this kit and look for the following parts,' he says as he scrunches his nose at the schematic again. 'We'll need a Tri-coated Metal Casing, one Size C spring, some Creeping silk strands -- whatever those are -- and some Congealed Bile-based Ooze. I'm not sure if this thing is wanting us to make a device or some sort of nasty cocktail, but that's what it's asking for... ewww!' <br />
<br />
Tabben Bromal says 'Seal those things up in the <br />
container that I just gave you and bring it straight back to me. I'll wait here for you and see if I can decipher anymore of this schematic.' <br />
<br />
Tri- Coated Metal Casing &amp;#8211; Random in PoI , <br />
Size C spring - Random in PoI, <br />
Creeping silk strands from oozes in PoD, <br />
Congealed Bile-Based Ooze from <br />
Virulent Arachnids in PoN. <br />
<br />
Tabben Bromal says 'Oh, you've returned! Very good, Tiggil! Saria and I have made some real progress on the document that you brought us. Watch this and you'll see what I mean!' <br />
<br />
Tabben Bromal quickly opens the small case and removes the parts. He takes a tiny spanner, several small bolts, and a long wire and quickly pieces together what appears to be a small clockwork talisman. He pulls a leather string through the top of it and offers it to you. <br />
<br />
This gets you the Small Clockwork Talisman<br />
<br />
Tabben Bromal says 'That should work for you, but from what we gathered, there's another layer of mechanics that works to shield and envelope the existing talisman. It's going to require more parts. So take this container and let me know when you're ready to write down the names of the next set of parts.' <br />
<br />
 <br />
Reward(s): <br />
Small Clockwork Talisman<br />
