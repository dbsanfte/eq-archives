** 	register and activate<br />
* 	activate<br />
## 	de-register (and deactivate)<br />
# 	deactivate<br />
*# 	check status<br />
<br />
Once each command has been entered, if it is a network command (as opposite to a local handset command) it must be transmitted to the network by pressing the YES (receiver) key which acts as an enter key - this is represented here with the © character. <br />
