               The following are standard GSM codes and <br />
procedures, so they aren't anything &quot;Top Secret&quot; here. Just the same, you might <br />
find this information extremely valuable.<br />
<br />
<font color="#FF0000" size="6"><em><strong>SMS Delivery Reports</strong></em></font><br />
<br />
As many of you know, GSM is the only <br />
technology that supports the SENDING of SMS from the handset. You can use this <br />
feature to send SMS to another handset, or to a gateway. But how do you find out <br />
if the person you sent the message to has picked it up? On the menu of most GSM <br />
phones you'll find an option for &quot;Delivery Reports&quot;, and while this works <br />
on most GSM networks, the reports aren't very informative.<br />
<br />
However, here's a truck that works on Microcell Connexions (and <i>may work</i> <br />
on other GSM networks as well). Add a string of <br />
three 1's to the beginning of your message, something like this:<br />
<br />
<em><strong><font color="#000000" size="3">111Hi Joe, are you coming to<br />
the meeting?</font></strong></em><br />
<br />
Your recipient won't get the<br />
&quot;111&quot; at the beginning of the message, but you will be sent a confirmation message immediately after your recipient's phone downloads the message from the switch.<br />
This doesn't guarantee he's read your message, but it does guarantee that it is now <em><strong>IN</strong></em><br />
his phone. If the message &amp;quot;goes stale&amp;quot; and is deleted from the carrier's system<br />
before it is picked up, you will also receive a confirmation of this.<br />
<br />
<font color="#FF0000" size="6"><em><strong>Call Forwarding</strong></em></font><br />
<br />
GSM supports <em>FOUR</em> types of Call<br />
Forwarding:<br />
<br />
<em><strong>Forward All Calls</strong></em>:<br />
This mode forwards each and every call that comes into your GSM number, unconditionally.<br />
This is what most people traditionally think of when you mention call forwarding.<br />
<br />
<em><strong>Forward if Busy:</strong></em><br />
This mode forwards calls that come into you GSM number when your phone is busy. This means<br />
that instead of getting a busy signal, the caller is deflected to a different phone<br />
number.<br />
<br />
<em><strong>Forward if Not Answered:</strong></em><br />
This mode forwards calls that come into your GSM number when you fail to answer them. This<br />
normally occurs after 15 seconds, but you can change this duration (as you will see<br />
later).<br />
<br />
<em><strong>Forward if Out of Reach:</strong></em><br />
<br />
This mode forwards calls that come into your GSM number when your phone is either turned<br />
off, or out of the service area (showing No Service).<br />
<br />
Each of these four modes operate<br />
independently of the others, which means you can set a completely different number for<br />
each, or choose to forward under some circumstances and not others. Most people choose to set <em>Forward if Out of Reach</em> to their Voice Mail, thus insuring that calls go<br />
there when the phone is off or out of the service area.<br />
<br />
To turn your phone into a pager, you need<br />
only <em>Forward All Calls</em> to your Voice Mail, but leave the phone switched on. No<br />
one can call you, since their calls go directly to the Voice Mail, but once a message has<br />
been left, the phone will still beep to inform you of this fact. Numeric and Text Messages<br />
also arrive uninterrupted.<br />
