Step 1a) Click on the &quot;Open Disc&quot; button.<br />
<br />
<img src=crap/Backup_-_DVD_Shrink_-_Step_1a.png><br />
<br />
Step 1b) Use the Pulldown options to &quot;Select DVD Drive&quot; that contains your movie, when selected, click on the &quot;OK&quot; to continue.<br />
<br />
<img src=crap/Backup_-_DVD_Shrink_-_Step_1b.png><br />
<br />
Step 2a) After your movie as been loaded, you will see a screen similiar to the one below, showing the &quot;DVD Structure&quot; of your movie. You can browse through the titles and select what you want to modify or remove.<br />
<br />
<img src=crap/Backup_-_DVD_Shrink_-_Step_2a.png><br />
<br />
Step 2b) To maximize the movie quality, remove unrequired Audio Tracks and Subtitles to increase the Video Quality of the movie. Use the &quot;Compression Settings&quot; to pre-set a Bitrate for your movie or use the default &quot;Automatic&quot; settings. When you are satisifed with your choices, click on the &quot;Backup!&quot; button to continue.<br />
<br />
<img src=crap/Backup_-_DVD_Shrink_-_Step_2b.png><br />
<br />
Step 3a) If your DVD has RCE protection, select from the &quot;Specify region&quot; the correct region code, or your movie may not play.<br />
<br />
<img src=crap/Backup_-_DVD_Shrink_-_Step_3a.png><br />
<br />
Step 3b) Under the &quot;Target Device&quot; you need to select using the pulldown menu, which device you want to write the movie, you can choose from the DVD Burner itself (3b1) or create a &quot;ISO Disc Image&quot; (3b2) and burn using a ISO capable image burning, or creating a &quot;Hard Disk Folder&quot; (3b3) named &quot;VIDEO_TS&quot; which you can later burn with any DVD Authoring program.<br />
