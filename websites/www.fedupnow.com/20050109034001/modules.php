              <br><br>
               This guide describes to change the skins on a Motorola Razr V3. It worked for me but I make no guarantees about your phone. If you follow this guide it is AT YOUR OWN RISK! Read the whole of this guide before starting to do any changes.<br />
<br />
I believe that this process is the same (or very similar) for e398 and v551 phones and that skins are interchangeable between the e398, v551 and V3 but I have not tested this.<br />
<br />
Firstly I assume that you have a working copy of p2kman and can connect correctly to your V3. There are numerous threads on how to set up this application on the Howard Forums (http://www.howardforums.com/forumdisplay.php?s=&amp;forumid=38)<br />
<br />
<b>Method</b><br />
<br />
Currently you can’t add new skins you can only replace existing skins so you should make sure your phone is not using the skin you plan to replace. Decide which skin you are going to over write; in this example I will be over writing the Scarlet skin.<br />
<br />
   1.<br />
<br />
      Download the files for the skin you want to use and save them somewhere on your PC. There will be a .ski file and one or more .dat files e.g.<br />
      Newskin.ski<br />
      Newskin.dat<br />
      Newskin1.dat<br />
   2. Run p2kman and plug the USB cable into your V3. Once the phone is connected you should see the word connected in the bottom left corner.<br />
<br />
<img src=crap/Image1.gif><br />
<br />
   3.<br />
<br />
      Now click the Update list button to get a tree in the left hand side of the window (patience, it takes a while):<br />
<br />
<img src=crap/Image2.gif><br />
<br />
   4.<br />
<br />
      Expand the tree until you can see the skins folder and click on the folder of the skin you want to replace (e.g. Scarlet) which will show you the files for that skin in the right hand side of the window.<br />
<br />
<img src=crap/Image3.gif><br />
<br />
   5.<br />
<br />
      Take a back up of the files in that folder, highlight them all and click the Phone &gt;&gt;&gt; PC button. You will be prompted for a location to save the files. (Save them in a different location to your new skin files.)<br />
<br />
<img src=crap/Image4.gif><br />
<img src=crap/Image7.gif><br />
<br />
      You will need these files to return to the original skin. Note the name of those files, in the case of the scarlet skin they are:<br />
      Scarlet.ski<br />
      Scarlet.dat<br />
      Scarlet1.dat<br />
      Scarlet2.dat<br />
<br />
      You will need to remember the name portion, i.e. the part before the dot in the .ski file, in this case Scarlet. Remember the names are case sensitive.<br />
   6.<br />
<br />
      Next you will need to delete those files from your phone, in order to do this you will need to change the attributes of the files. The attributes will be listed in the right hand portion and should be 70004 for each file. The 4 says that it is a system file and can’t be deleted.<br />
<br />
      So for each file in turn click on it to highlight it, type 70000 in the attributes box and click Change. You won’t see the attributes change until you click the update list button again.<br />
<br />
<img src=crap/Image5.gif><br />
<br />
   7.<br />
<br />
      Once the attributes are changed highlight all the files in the skin directory and click the Delete button. Click the Update list button and the files should have disappeared from the right hand side.<br />
<br />
<img src=crap/Image5a.gif><br />
<br />
   8.<br />
<br />
      Go to the folder on your PC where you saved the new skin files and rename all the files to match the skin you are replacing e.g.<br />
      Newskin.ski 	 »  	Scarlet.ski<br />
      Newskin.dat 	 »  	Scarlet.dat<br />
      Newskin1.dat 	 »  	Scarlet1.dat<br />
      Newskin2.dat 	 »  	Scarlet2.dat<br />
      Newskin3.dat 	 »  	Scarlet3.dat<br />
      etc. 	  	 <br />
<br />
      Remember the names are case sensitive.<br />
   9.<br />
<br />
      Then with the skin directory still displayed in p2kman click the PC &gt;&gt;&gt; Phone button and when prompted select all the new skin files and click Open.<br />
<br />
<img src=crap/Image6.gif><br />
<br />
      The files will be copied to the phone. Click the Update list button and you should see the new files listed on the right hand side (it seems that you don’t have to change the attributes back to 70004).<br />
<br />
<img src=crap/Image9.gif><br />
<br />
  10.<br />
<br />
      You can now disconnect the USB cable and try out the skin.<br />
  11.<br />
<br />
      Some skin designers include graphics files with matching wallpaper and screensaver, for example the Black skin. If this is the case you simply need to use p2kman to copy them to the picture folder on the phone or, if you prefer, use mobile Phone Tools (as supplied with your V3). You can then use the standard phone set up menus to select the appropriate files.<br />
