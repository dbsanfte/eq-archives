              <br><br>
               Note: MIDway is a Windows application used to transfer Java games and apps to Motorola GSM phones using a serial or USB data cable.<br />
<br />
- This walkthrough makes several assumptions.<br />
<br />
1. That you have a Motorola phone with the Java App Loader enabled in your Java Settings Menu.<br />
(If not, then you need to STOP and use PST, or P2KMAN + seem editing to enable it)<br />
Note: SEEM 0032_0001 - Offset 43 - Bit 0 value=1 to enable, 0 to disable).<br />
<br />
2. You have a USB data cable designed specifically for Motorola phones and have successfully used it to connect to Motorola Phone tools, p2kman or PST. In other words, the USB drivers should be installed and working properly. (Serial cable users: This walkthrough was written specifically for USB cable owners but the steps to use MIDway with your cable are nearly identical)<br />
<br />
3. You should be running Windows XP or 2000, and know how to use it. While the steps for older versions of Windows are VERY similar, I intend to have screenshots below &amp; I do not have screens for those operating systems, but the similarities should be close enough for you to find the necessary information.<br />
<br />
4. You have the MIDway program downloaded and ready to execute. (Version 2.8 in this case)<br />
<br />
<br />
--------------------------------------------------------------------------------------------<br />
<br />
Before beginning, your PC and phone should be powered on, and your data cable should be connected to your phone only, but<br />
DO NOT , I REPEAT DO NOT connect the usb cable to the PC at this time. Only connect the larger end to the phone.<br />
<br />
STEP 1:<br />
On your phone press the Menu button, and navigate to Settings, then Java Settings, then highlight the Java App Loader option(should be at the top)<br />
and select it. You should then see a screen that says Insert cable Now<br />
<br />
Insert the other end of the USB cable into your pc now. After you do this, you should see a brief screen on the phone that says 'Jal link is active...',<br />
then it should return back to the Java Settings Screen. If not, then you have USB driver issues that need to be resolved before proceeding.<br />
<br />
STEP 2:<br />
On your PC, open the control panel, then open the Phone and Modem options applet and click the Modems Tab at the top.<br />
<br />
At this point your screen should look something like this:<br />
.<br />
<br />
<br />
IMPORTANT: Take note of the Com port number listed (COM 3 in this example). This information is also located in the device manager under<br />
Motorola USB modem properties, Advanced tab, Advanced Port settings button.<br />
<br />
Once you've made a note (mental or otherwise) of the com port number, close the control panel and the Phone &amp; Modem options applet.<br />
<br />
STEP 3:<br />
<br />
At this point you should run the Midway program from wherever you have it stored on your particular pc.<br />
Once opened, your screen should look like the one below:<br />
<br />
.<br />
<br />
