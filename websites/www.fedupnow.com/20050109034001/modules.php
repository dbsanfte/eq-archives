<br />
<font color="#FF0000" size="6"><em><strong>Call Waiting</strong></em></font><br />
<br />
Suppose you have just called a phone number<br />
where you end up on hold. While listening to the annoying music in the background you<br />
receive a second call via <em>call waiting</em>. You don't really want to answer that<br />
call, since it would put your first call on hold. Someone might answer while you were<br />
away, and besides, you don't want to run up the airtime if you can no longer service that<br />
call. All you need to do to terminate the first call <strong>before</strong> answering the<br />
second call is to press the END key. This will tell the GSM network to end your current<br />
call, but right away the phone will start to ring. To answer this call, just press the<br />
TALK button.<br />
<br />
Did you know that you can take a call<br />
waiting party and allow them to join in your current conversation? When the second call<br />
comes in, answer it as usual by pressing the TALK button (or by selecting the answer<br />
option on the appropriate soft key). After confirming with the caller that he or she<br />
wishes to join your current conversation, choose the <em>Join</em> (or <em>Conference</em>)<br />
option on your phone.<br />
<br />
You can quickly disable Call Waiting by<br />
entering <em>#43#</em> then pressing TALK. You can re-activate Call Waiting by entering <em>*43#</em><br />
and then pressing TALK. You can check the Call Waiting status by entering <em>*#43#</em> and pressing TALK.<br />
<br />
<font color="#FF0000" size="6"><em><strong>Showing Your Number</strong></em></font><br />
<br />
As you may already know, there is a menu item available on<br />
GSM phones that determines whether your phone number will be shown on the call display<br />
units of people you contact. However, did you know that you can override this setting on a<br />
per-call basis?<br />
<br />
Say you normally display your phone number, but once in a<br />
while you want to hide it. To do that, put <em>#31#</em> in front of the number you are<br />
dialing. For example:<br />
<br />
<em>#31#4165551212</em><br />
<br />
You could also store some of your phone book entries with<br />
#31# in front of them. This is handy if you have someone you call regularly who you don't<br />
want to send your number to. Let's face it, some people won't answer their phone if they<br />
know it is you calling.<br />
<br />
Now suppose you normally didn't like your number displayed,<br />
but once in a while you did want to show it. To do that, put <em>*31#</em> in front of the<br />
number you are dialing. For example:<br />
<br />
<em>*31#4165551212</em><br />
<br />
<strong><em>NOTE:</em></strong> Microcell Connexions also<br />
supports the use of the landline standard *67 for suppressing your number. If you can't<br />
remember the GSM codes, you can always remember that one.<br />
