	(   Posted by: Langly 
                 on Sunday, July 18, 2004 - 06:42 PM CST )  
              <br><br>
               As DVD-R and DVD+R recorders get more and more popular, tons of people are looking at converting their old DivX and XviD movie backups to DVDR format (more specifically to DVD-Video format which can played with stand-alone DVD players). Luckily there are several freeware and/or cheap shareware tools that allow doing this rather easily.<br />
<br />
<b>What this guide does?</b><br />
<br />
This guide tries to take you through the process of converting your AVI format movies to DVD-Video and also instructs how to convert your old text-format subtitles to DVD subtitles. Guide itself is actually not just for DivX or XviD videos, but should work as it is for virtually all of the AVI formats there are, provided that you have the correct decoding video codecs installed. For audio part, we guide you through the two most common audio formats in AVI videos, MP3 and AC3 (which is also known as &quot;the surround sound&quot; or &quot;5.1 audio&quot; or &quot;Dolby Digital&quot;, which all are rather vague terms, but usually mean DD5.1/AC3 audio anyway) -- but any other audio format should follow the same basic procedures anyway.<br />
<br />
<b>Hardware &amp; other requirements</b><br />
<br />
Obviously you need to have the original AVI video source, bundled with the audio track in it. For subtitle conversion, you need to have your subtitles in SRT format, which is commonly used by SubRip. Additionally, you need to have a DVD recorder if you plan to burn these movies to DVDR and -- as usual with video editing -- you need to have more than 5GB of free space on your HDD.<br />
<br />
<b>Lossy compression?!</b><br />
<br />
You should understand that all of the most common video formats, including MPEG-2 (used with DVD-Video discs, digital TV and SVCDs) and MPEG-4 (used by DivX and XviD) are so-called lossy formats.<br />
<br />
What does this mean then? It means that once you convert your original video to any lossy format, the resulting video file has lost some amount of data/details compared to the original video and that data <b>can't be reversed</b> by decoding it. Ever. So, think it like a compression program that takes a full book and compresses it down to a 4-page abbreviation -- the main data is still there, but using only the 4-page abbreviation, it is impossible to end up with the original book.<br />
<br />
So, in this case it means that even when your DivX/XviD movie has been originally taken from the DVD-Video source, it wont be the same quality when converted again to a DVD-Video source. Actually, <b>the resulting DVD-Video made from the DivX/XviD will have slightly worse picture quality than the DivX/XviD itself.</b><br />
<br />
Now, once you understand this, your expectations should be on correct level and we can proceed.<br />
<br />
<b>Required software</b><br />
<br />
In order to follow this guide, you need these tools:<br />
    * SRT2SUP (required only for the subtitles)<br />
    * AVIcodec (to determine what audio and video codecs you need)<br />
    * FFDSHOW (required only if you don't have existing codecs to play DivX or XviD files -- depending on your source video format)<br />
    * TMPGEnc (required! for encoding the video -- note that the trial version works for only 30days!)<br />
    * IFOEdit (required! for authoring the DVD-Video disc) <br />
<br />
<b>Determine the audio and video format</b><br />
<br />
Now, open the AVIcodec tool and click Select button. This opens up a file dialog. Locate the AVI file you're about to convert to a DVD format, select it and click Open.<br />
<br />
Now, you should see the video file details in the main window. In our case, we can see that the video has been encoded using XVID Mpeg-4 codec and the audio has been comporessed using MP3 format. At the bottom of the page we can also see further details for audio and video coding, such as the frequency of the audio, which is this case is 48000Hz.<br />
<br />
<img src=crap/avi2dvd_02_avicodec_2.gif><br />
<br />
Please take a look at also the fps value of the video and write it down -- normally this should be either 25.00 fps, 23.97fps or 29.97 fps (or neaby number).<br />
<br />
<b>If the movie is in two parts, join it</b><br />
<br />
Now, if your movie is split into two or more parts, you need to join it into a one, using VirtualDub. We suggest that you use this guide to do this.<br />
<br />
<b>Convert the AVI to DVD/MPEG-2</b><br />
<br />
Now it's time to convert the video file to DVD-Video -compliant MPEG-2 format. We're going to use TMPGEnc as our encoder, because it is fairly simple to use and produces very good quality MPEG-2 videos (not excellent, but very good).<br />
<br />
First, you should open TMPGEnc. Please note that TMPGEnc's trial version supports MPEG-2 encoding only for 30 days, after that you need to purchase a commercial version of TMPGEnc, called TMPGEnc Plus.<br />
<br />
<b>Launch the wizard</b><br />
<br />
By far the easiest method of creating a new TMPGEnc project is to use TMPGEnc's wizard. As a default, wizard pops up when you launch TMPGEnc, but if it doesn't, go to the File menu and select Project Wizard.<br />
<br />
<b>Select the video format</b><br />
<br />
In the first page of the project wizard, you should select the correct video format under the DVD selection in the left side of the window. For people who really know what they're doing, there are more choices, but we're going to stick with two options here: DVD - NTSC and DVD - PAL (i.e. forget the low-resolution and 16:9).<br />
<br />
Take the paper in front of you that you used when you wrote down the fps value from AVIcodec. If the fps is 25.000fps, you should select DVD - PAL and if the fps is either 23.976 or 29.97, you should select DVD - NTSC.<br />
<br />
If your audio was something other than AC3, you should select the audio here. We recommend that you use CBR - MPEG-1 Layer II Audio (MP2) as it takes less space than PCM and is more suitable for DVD. If your audio was in AC3 format, you can ignore this selection and choose whatever you like.<br />
<br />
<img src=crap/avi2dvd_08_tmpgenc_1_wizard_first_screen.gif><br />
<br />
After you've done the selections, click Next.<br />
<br />
<b>Select the source video</b><br />
<br />
On second page you should first click the Browse button next to the box that says Video File. This opens up a file dialog and you should now locate your AVI file (if you joined multiple AVI clips into one, choose this joined AVI). Select the file and click Open.<br />
<br />
<img src=crap/avi2dvd_09_tmpgenc_2_select_source.jpg><br />
<br />
Normally TMPGEnc can recognize the correct values automatically based on the video file you just opened. As most of the existing AVI files are pre-deinterlaced, the Video type is normally set as Non-interlaced. You should only change this value if you're sure that this selection is not correct (or if TMPGEnc suggested a different selection when you selected your video source).<br />
<br />
Aspect ratio should be left as 1:1 (VGA) unless you're one of the guys we mentioned earlier and know exactly what you're doing ;-).<br />
<br />
For PAL videos, the Content of Video selection is normally grayed out, but for NTSC sources, the selection is typically Film Movie, unless the source is a DV cam recorded AVI.<br />
<br />
After you've checked the selections, click Next.<br />
<br />
<b>Filter settings</b><br />
<br />
On third page, the top three checkboxes should be left intact in most of the cases. But you should click the Other settings button.<br />
Other settings<br />
<br />
This opens up a new window, where you can define the encoding parameters more accurately. The most important selection here is the one that says Motion search precision. You should definitely change this to Highest quality (very slow) as this setting produces much better video quality than the default settings do.<br />
<br />
You should also change the Rate control mode to 2-pass VBR(VBR) which forces TMPGEnc to do the encoding using multi-pass encoding.<br />
<br />
<img src=crap/avi2dvd_10_tmpgenc_3_other_settings.jpg><br />
<br />
Once you've done this, click OK to close the smaller window. Once you're back in the project wizard window, click Next.<br />
<br />
<b>Bitrate settings</b><br />
<br />
In bitrate settings page (fourth page of the project wizard), you can adjust the encoding settings of the video, including the bitrate and the resolution.<br />
<br />
Video resolution should be 720x480 for NTSC movies and 720x576 for PAL movies.<br />
<br />
Next, make sure that the Audio Bitrate selection is at 384 kbps.<br />
<br />
For Average video bitrate setting, you need to adjust this value to get the DVD-Video to be &quot;perfectly sized&quot; for a DVDR disc. In the bottom of the window, you see a blue bar and a text Makes file size XXX % of disk capacity. Adjust the Average video bitrate up and down until the value &quot;XXX&quot; states something between 95 and 97, not more than that as we need to have space for our subtitles and other stuff that will be added to the DVD compilation. Also, if your audio was in AC3 format, try to keep the value between 94 and 96 instead, as AC3 audio tends to be in 448kbps (ignore this if AVIcodec said that the AC3 bitrate was less than this).<br />
<br />
Obviously remember to make sure that the dropdown just above the blue bar says DVD-R 4.7GB (UDF).<br />
<br />
<img src=crap/avi2dvd_11_tmpgenc_4_bitrate.gif><br />
<br />
Once you've done these settings, click Next.<br />
<br />
<b>Select the destination</b><br />
<br />
First, remember to select the checkbox that says Output video and audio as individual elementary streams. Then, click the Browse button and locate a directory on your harddrive that has appx. 4.5GB of free space in it, select a name for the output video file and click Save.<br />
<br />
Make sure the Start encoding immediately is selected and click Next and go to sleep, TMPGEnc will now encode the video file and this can take up to 20hrs (or even more with slower machine). <br />
<br />
<b>IFOEdit</b><br />
<br />
Now, once the video file has been encoded, its time to start authoring the DVD itself. We wont create fancy menus or add extra features to the disc, but simply want to make a DVD-Video disc that plays the video once inserted into the DVD player and that's it. For this purpose, free IFOEdit is simply great.<br />
<br />
Firstly, open IFOEdit. Then, go to DVD Author menu and select Author new DVD.<br />
<br />
<b>Select the source files</b><br />
<br />
Now, click the little button next to the box that says Video and locate your .m2v file that TMPGEnc created for you and click Open.<br />
<br />
Go to the next button down for Audio and locate the .mp2 audio file or .ac3 audio file (depending on whether you had the AC3 audio in your source or not), select it and click Open.<br />
<br />
Take the next button, which says Subpicture and click it. Locate the .sup subtitle file you created earlier on, select it and click Open.<br />
<br />
Now, we didn't have chapters here as most of the AVI files simply don't come with chapter information at all, so we leave the Scene changes selection totally blank.<br />
<br />
(One option to create some method of navigating through the DVD is to create a dummy chapters.txt file with Notepad that includes numbers on each line, starting with something like 10000 on first line, 20000 on second line, etc. Take a look at your AVIcodec notes again and see how long the movie was and what was the framerate. Use the length to calculate the movies length in seconds -- i.e. 1h 30mins == 90mins, 90 * 60 == 5400 seconds. Then multiply this value with the fps value, like 5400 * 25 == 135000. Then keep the biggest value in your text file below this value and you're fine. Then simply click the Scene changes selection button in IFOEdit and select this file and you have chapters every 10,000 frames :-)<br />
<br />
Finally, click the button next to Output stream and select a directory on your harddrive that has at least 4.5GB of free space in it and choose that directory as your output dir.<br />
<br />
<img src=crap/avi2dvd_12_ifoedit_1_new_author.jpg><br />
<br />
<b>Multiplexing..</b><br />
<br />
Now simply click OK and IFOEdit starts creating a new DVD-Video project for you. This may take couple of minutes, so wait patiently.<br />
<br />
Once finished, click the Close button on log window and you're done. You should have appx. 4.3GB of files in the folder you chose to act as your output directory.<br />
<br />
<img src=crap/avi2dvd_13_ifoedit_2_muxing.jpg><br />
