               Introduction<br />
This guide will allow you to change the number of rings (technically seconds) it takes to reach your voicemail on Cingular service.<br />
<br />
Procedure<br />
<br />
   1. Find your voicemail number<br />
          * Go to Message &gt; Menu Button &gt; Voicemail Setup ~or~<br />
          * Go to Settings &gt; Call Forward &gt; Forward Status &gt; Voice Calls, the &quot;Forward if Unavailable&quot; number is your voice mail number.<br />
   2. Now that you have your voicemail number, type *61*voicemailnumber**NUM# and hit send.<br />
          * voicemailnumber = your voicemail number<br />
          * NUM = number of seconds (must be 5, 10, 15, 20, 25, or 30)<br />
<br />
Example<br />
So, if my voicemail number is 14045551234 and I want a 20 second delay, I'd do this:<br />
*61*14045551234**20# and press SEND.                 <div align="right">
