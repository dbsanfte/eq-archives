               HOWTO: Fixing Cellphones Dropped Into Water<br />
<p><br />
In the event that a non-waterproof cellphone/PDA/digcam accidentally gets immersed in water, you should follow these steps:<br />
<p><br />
(1) IMMEDIATELY REMOVE THE BATTERY. VERY VERY IMPORTANT! 60 seconds later and it may be too late. The battery is usually the source of damage, not just the water alone. Water+Battery = Short Circuits! So REMOVE the battery! And DONT TEST the cellphone after it's been dropped in water!<br />
(2) As soon as possible (preferably within 20-30 minutes). dissassemble the phone using Torx T6 screwdriver. (Get this off eBay or service depots, etc) Make an emergency trip to Sears/RadioShack/ACE/service depot/etc.<br />
(3) Clean/dry as much as you can, using Q-Tips<br />
(4) Dry the electronics components under a desk lamp on a paper towel for 24 hours. A drop of water may have gone under chips and components where you cannot dry, so you need to dry under a 40/60w desk lamp, or 24 hours under a desk fan pointed down at the disassembled components.<br />
(5) Reassemble the phone<br />
(6) Put back together<br />
(7) Only now it's safe to put the battery back in.<br />
(8) Phone may be good as new.<br />
<p><br />
Try not use a hairdryer, unless with heat turned off or at very low settings. You do not want to scorch the electronics. A hairdryer may help in some cases, but more often than not (especially with clean water), a hairdryer will usually cause more damage caused by the stresses of expansion-contraction caused by all the heat. It's best to keep things cold, to keep any contaminants in the water as chemically inactive as possible.<br />
<p><br />
If you follow these instructions, you have a reasonably good chance of a working phone after a non-waterproof phone was thrown into water.<br />
<p><br />
Yes, make that emergency trip to buy the Torx T6 if you have to. You have to disassemble the phone. Although reports some devices such as a PalmPilot, has survived in a snowbank for 7 days (luckily, its batteries were dead) after being lost in a snow-filled driveway, it is generally best to disassemble the device quickly. Clean snow/water usually means you can wait a bit longer, but pool water (chlorine) or ocean water (salt) will make is VERY URGENT to dissassemble the phone.<br />
<p><br />
SALT WATER TIP: If exposure was salt water; you may need to clean the salt water with water. (salt water is far more dangerous). This may actually mean washing the circuit board very briefly under filtered water, in order to save its life from salt water (ocean water) before immediately drying it out. This is a last resort, but NECESSARY if your electronic device was immersed in salt water (unless you have access to special electronics cleaning fluid solutions that can get rid of salt water on short notice. Sometimes filtered water is the only thing you have access to, for cleaning salt water ASAP)<br />
<p><br />
Yes, one must get rid of fear of dissassembly! RESIST trying to test the telephone -- keep the battery away -- until the phone/PDA is disassembled and dried first!<br />
<p><br />
FACT: Water almost never instantly kills a cellphone. You usually still have time to save the phone's life, especially if the water is clean!<br />
<p><br />
Water is usually never usually the source of damage in a PDA/cellphone/pager/etc that has been immersed into water. The source of damage is usually the combination of Battery+Water which causes short circuits. If there are any backup batteries inside the cellphone/PDA, those preferably must be removed too. Removing all sources of power immediately (all batteries) is the most important step in rescuing water-logged electronics!<br />
<p><br />
Also, phones that have already been exposed to water for longer periods even with the battery, are more likely to permanently stop working. For example, having been inside pockets of damp clothes or wet bags, or fallen in a rain puddle for an hour without you noticing until too late. However, remove the battery immediately anyway and dissassemble anyway. Sometimes you can still rescue electronics that has been wet with the battery for longer periods, especially if the water was clean and the electronics shut off the battery before it did any short-circuit damage.<br />
<p><br />
I've had a good success rate in rescuing waterlogged electronics by following the above simple instructions. But to improve your odds, you must act quickly to remove the battery and resist the temptation to reinsert the battery to test, not until at least 24 hours later and after disassembly. If the cellphone/PDA is still wet inside -- ZAP. It can take weeks for the inside of a cellphone/PDA to fully dry if you do not disassemble. It can take less than 24 hours to dry if you diassemble first. So dissassemble first!                 <div align="right">
