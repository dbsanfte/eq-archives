<br />
<em>Note: </em>The cancellation feature<br />
actually has TWO different modes of operation. By prefacing the operation with two pound<br />
symbols instead of one, you instruct the network not only to cancel the forwarding<br />
operation, but to completely de-register this service. So what's the difference? If you<br />
use just a single pound symbol, this does turn off the selected call forwarding option,<br />
but the network retains a memory of the last number used. This allows you to turn the<br />
forwarding option back on, with the same number as before, by simply entering the<br />
&amp;quot;Re-establish&amp;quot; code noted in the chart. Re-establish will FAIL if you try to use<br />
it after performing a &quot;Cancel&quot; De-register operation.</font></p><br />
<br />
Being able to enter these functions as<br />
strings means you can store them in memory locations and speed-dial them whenever you need<br />
to. Be careful when attempting to set or clear <em>Forward if Busy</em>, <em>Forward if<br />
Not Answered</em>,. and <em>Forward if Out of Reach</em>, as these features will <strong>NOT</strong><br />
change if you presently have <em>Forward All Calls</em> active. Turn off this latter<br />
feature first, or you may receive a &amp;quot;Not Done&amp;quot; error message from the network.<br />
<br />
Earlier I said that you could set how long<br />
the network would wait before assuming you had not answered your phone. To do this, use<br />
the following string to activate <em>Forward if Not Answered</em> (it cannot be done from<br />
the menus):<br />
<br />
<em>*61*[Phone Number]*11*[Time]#</em><br />
<br />
For example, to forward to 416-867-5309<br />
after allowing your phone to ring for 25 seconds, you would enter:<br />
<br />
<em>*61*4168675309*11*25#</em><br />
<br />
The <em>Time</em> parameter may be 5, 10,<br />
15, 20, 25, or 30. These are the only valid entries you may use. The &quot;11&quot; in<br />
this code refers to &quot;Voice Calls&quot;, but since Fido only handles voice calls at<br />
this time, there's no point telling you what the other codes mean. Besides, I'd have to<br />
run off and look them up. This timing instruction can also be added to the *002 and *004<br />
codes.<br />
<br />
When forwarding to your voice mail, Fido<br />
gives you an additional short cut so that you don't have to remember the actual phone<br />
number to forward to. Instead of setting the forward to the actual phone number, forward<br />
to 3436 (that spells FIDO). For example, to forward all calls unconditionally to your Fido<br />
voice mail, you would enter:<br />
<br />
*21*3436#<br />
<br />
The Fido system recognizes 3634 and<br />
converts it to the correct phone number for you. If you use the status code *#21# right<br />
after you do this, you will find your phone forwarded to the correct voice mail phone<br />
number.<br />
<br />
And here's another thing that can come in<br />
really handy if you wish to screen calls. Most of you probably guessed that if you set <em>Forward<br />
if Not Answered</em>, you can check the call display and ignore calls you don't wish to<br />
take. After the phone has rung 5 or 6 times, it will forward to the designated number<br />
(mostly likely your Voice Mail). Having the phone ring and ring like that is hardly a<br />
great solution if you're at a meeting, and changing the forwarding wait time to 5 seconds<br />
(the minimum allowed) means you may not have sufficient time to answer those calls you do<br />
wish to receive.<br />
<br />
Here's how you can handle this situation<br />
using your GSM phone. Instead of setting the <em>Forward if Not Answered</em> feature to<br />
the desired phone number, set the <em>Forward if Busy</em> feature instead (or as well).<br />
Now when your phone rings, check the incoming number and if isn't someone you wish to<br />
speak with, press the END key. This tells the GSM to <strong>immediately</strong> forward<br />
your call. Nifty eh? And by the way, if you press END while a call is coming in, and you<br />
haven't set a phone number to forward to, then the caller starts getting a busy signal.</font></p><br />
<br />
