               GSM Command Strings<br />
When various network functions are selected via the cellphone's menu using the keypad, the cellphone automatically generates the corresponding GSM network command string and transmits it to the network. <br />
These commands can however be manually entered via the keypad.<br />
<br />
Each command is prefixed with either one or two * or # characters as follows:<br />
<br />
