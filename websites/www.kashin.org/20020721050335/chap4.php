<h2><p><center><B>CHAPTER 4. RECON STATIONS</B></center></p></h2>


<p>
A critical element in advancing your team through a city is by capturing Recon Stations. 
When you first enter an Arena all Recon Stations except for the Base Recon Stations will 
show up as being hollow white boxes on Radar. This indicates that the Recon Stations are 
presently neutral, capable of being used by any team. A Red, Green, Blue, or Gray Recon 
Station means that the respectively colored team has captured it.</p>

<p>Capturing a Recon Station has several advantages as opposed to letting it remain neutral. 
The most immediately recognizable benefit is that you've cut off enemies from being able 
to use that Recon Station. But that's just the icing on the cake.</p>

<P>Another blow to your enemies will be the powerful Recon Gun which fires proximity blasts 
at nearby enemies. And last, but not least, while your Recon Station feeds your tank energy, 
it will actually drain power from nearby enemy tanks.</P>

<h3><p><B>4.1  Base Recon Station</B></p></h3>

<P>Your Base Recon Station shares a couple distinct differences from Recon Stations located 
out in the battlefield. It cannot ever be captured by the enemy and is the only location where 
you can actually switch to a new tank or get a Recon Capture module. Like Recon Stations 
in the battlefield, to use your Base Recon Station slowly drive your tank under the floating 
orb and hit the Enter key. This will automatically dock your tank and repairs will 
immediately proceed if your tank is damaged.</P>

<P>On both sides of the Chassis Type are arrow buttons letting you cycle through the five 
available tanks (discussed in Chapter 5). Simply left-click on either arrow until you reach 
the tank you want. The statistics of the tank will be listed under it, and the Bay 
Configuration will reflect the appropriate number of bays. You now have your tank and are 
ready to start the equipment phase. To exit the Recon Station either hit the <esc> key or 
left-click the Enter button and return to the battlefield.</P>

<h3><P><B>4.2 Capturing Recon Stations</B></P></h3>

<P>As mentioned earlier, there are several benefits gained from capturing a Recon Station. In 
order to capture a neutral Recon Station, a tank must equip a Recon Capture module (only 
available at the Base Recon Station), drive under a white orb and activate the Recon 
Capture.</P>

<P>This will call a friendly satellite over the Recon Station, beginning the capture phase, 
uploading data into the neutral Recon Station for thirty seconds. If the satellite is shot down 
within this thirty second time span the Recon Station will return to its neutral state and the 
capture will be aborted.</P>

<P>Provided the satellite survives and captures the Recon Station, the orb and Radar will now 
reflect the color of the capturing team. A Recon Gun will also be deployed above the Recon 
Station that will fire proximity blasts at enemies – even stealthed and cloaked enemies. 
Energy will be emitted to friendly tanks and leeched from nearby enemy tanks. The satellite 
will also upload a protection system that effectively prevents any other team from being 
able to recapture that Recon Station for two minutes.
</P>

<P>Capturing an enemy-owned Recon Station is basically the same method, but it's generally 
safer to shoot down the enemy Recon Gun before driving in and activating the Recon 
Capture. Naturally if an enemy team has just captured that Recon Station, you'll also have 
to wait two minutes before being able to capture it.</P>

<h3><P><B>4.3 Equipping at Recon Stations</B></P></h3>

<P>Under Equipment Selection, you can use the mouse or keyboard to select modules. Using a 
keyboard, simply tap the hot-key letter of the Category type (C)annon, (L)aser weapons, 
etc) to jump directly to that module type, then use the Up and Down arrows to reach the 
desired module. After highlighting the given module, the player can either hit Enter or 
double-click to move that module into the tank's Bay Configuration (provided there's 
room), or tap the I key bringing up a picture and a brief description of the highlighted 
module. Clicking on the Info button with a highlighted module will perform the same 
function.</P>

<P>Next to the modules is the number of bays a module requires. Some modules take up more 
than one bay, such as the cannon. Subsequently if you equip a cannon into a Vanguard 
(which has seven bays), you'll have five bays remaining. The cannon will appear in two of 
the bays. Modules that appear in red mean you either don't have enough room or you don't 
have a prerequisite module. For example, you can't equip a phoenix missile until you have 
a missile launcher.</P>

<P>To remove modules from bays type the respective number of the bay a module is occupying 
then press the delete or backspace key to remove it. Right-clicking an equipped module will 
remove it as well.</P>

<h3><P><B>4.4 Stored Module Configurations</B></P></h3>

<P>To expedite the re-arm phase one step further you can use the Stored Configuration feature 
which allows you to save three module configurations per tank. Just fill the bays with your 
ideal setup, click on the letter you wish to save under (either A, B, or C), then click on the 
Save Config button.</P>

<P>The saved configuration will be kept until the player decides to change the stored 
configuration. For example, if you already have a configuration under B and select another 
configuration to save under B, your old configuration will automatically be overwritten.
</P>

<P>Recalling a saved configuration can be performed by left-clicking on either A, B, or C. The 
stored configuration will be displayed in the Bay Configuration. If it's the configuration you 
want, click on the Load Config button to load the modules into your tank. </P>

<h3><P><B>4.5 Repairs</B></P></h3>

<P>When a badly damaged tank pulls into a Recon Station, repairs will begin, first on CRITS 
then ARMOR. Shields are automatically turned off when a tank enters a Recon Station to 
repair and/or rearm. </P>

<P>Recon Stations are by no means safe-houses. If you enter a Recon Station in a badly 
damaged tank with a pursuing enemy gunning your tank, you will probably die if the rate of 
damage received exceeds the repair rate. For example, phase-multiplied Mark IV lasers will 
definitely damage faster than a Recon Station's repair rate.</P>


