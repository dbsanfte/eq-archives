<p><center><h2><B>CHAPTER 3. INSTRUMENTATION</B></h2></center></p>


<p>
You're ready to rumble. The only problem is that you keep getting dusted while you're just 
trying to figure out what does what in your cockpit display. This chapter covers the 
instrumentation so you can know at a glance what the various readings represent (even 
though some are fairly intuitive).</p>

<p>The default instrumentation and viewpoint are what you will initially encounter when you 
first enter an Arena. The action all occurs in the 3D viewpoint at the top half of the screen – 
though if you weren't able to figure this part out, then you might need more help than this 
manual can offer.</p>

<p><h3><B>3.1 Armor, Crits, and Shields</B></h3></p>

<P>At the center of your console you'll see a tank graphic representing the your tank. Above 
the tank is your ARMOR indicator and below it the CRITS display. ARMOR shows the 
remaining plates of Armor on your tank once your shields are breached. CRITS stand for 
the Criticals which are effectively your last stand before death.</P>

<P>As you take damage, your ARMOR will first start to drop. As the numbers decrease the 
color of your tank icon's body will change colors to a lighter blue, yellow, orange, red, then 
gray display. Once the body of your tank's icon is gray, you no longer have any armor (and 
the ARMOR will have a 0 next to it).</P>

<P>The CRITS will now begin taking damage, and the color of the tank icon's turret will 
change to a lighter blue, yellow, orange, red, and gray. Whenever your CRITS are hit, your 
tank begins to smoke. This in turn makes you a very attractive target (blood for the sharks). 
Once your CRITS are blown away, so are you.</P>

<P>To help extend your lifeline you'll want to use shields. Shields are depicted graphically 
with curved bars that appear both above and below your tank icon. The top bar is your front 
shield whereas the bottom bar is your rear shield. At full strength, both bars will appear 
white. As they take damage, they will steadily begin to change colors to yellow, orange, and 
red. If there's no bar that means you either don't have shields turned on or you've had your 
shields shot out. </P>

<P>Fortunately you can allocate your shield power in 25% increments. So if you're getting 
pounded on your rear shield while running away, allocate the majority of your shield energy 
to your rear shield to increase the chance of survival.</P>

<P><h3><B>3.2 Battery, Feed, and Speed</B></h3></P>

<P>Under the left chat window you'll find both BATTERY and FEED bars. More than 
anything else, Tanarus is a power game. Keeping a well-charged battery is critical in the 
usage of energy weapons. The green bar representing your battery will decrease from right 
to left as you consume energy through driving, firing energy weapons, or using special 
modules such as stealth and cloak. Should your battery empty completely, your tank will 
drive at 30% of its top speed until you manage to get some power back into your battery. 
Thirty percent of your top speed equates to certain death in most cases if enemies are 
present. You can turn your shields off to regain a sliver of battery power.</P>

<P>Next to your BATTERY bar is your FEED indicator. Your base, satellites, and captured 
Recon Stations all radiate energy that your tank can draw into its battery. The FEED meter 
shows the current level of energy your tank is receiving with a blue bar that extends from 
left to right. Likewise, when near an enemy base or Recon Station, your FEED meter will 
extend from the right to left with a red bar indicating that you're losing power to enemy 
energy leech systems. The higher the respective bars, the more energy you're either 
receiving or losing.</P>

<P>Your SPEED indicator completes the right instrumentation panel. When increasing speed, 
the green bar will rise until it reaches your tank's maximum forward speed. Reversing will 
result in the opposite with the green bar lowering until it achieves top reverse speed.</P>

<P><h3><B>3.3 Chat Window</B></h3></P>

<P>The chat window is located at the lower left-side of the cockpit. This is where you can 
communicate with other teams or privately with your own team. Deaths are also displayed 
in the chat window showing who died and to whom. You can also toggle a listing of who is 
on what team and their given bounties by pressing the F1 key.</P>

<P>The two modes of chat are open and private team channel messages. Open messages can be 
sent by tapping the / key followed by your message then the Enter key. As you type your 
message it will appear in the chat bar located below the chat window. All teams including 
your own will see the message displayed in white text. Private messages to your team can 
be sent by tapping the ' key followed by your message then the Enter key. This time only 
your teammates will see the message and the color of the message will match your team 
color (a green team private message will be displayed in green text).  Hitting the ESC key 
will cancel the message.</P>

<P><h3><B>3.4 Radar</B></h3></P>

<P>One of your bigger friends you should refer to often is the circular radar display located to 
the upper right-hand portion of your screen. Enemy tanks will show up as small team-
colored squares – a blue square is a blue tank. Satellites are small circles whose color 
reflects the given team as well (red satellites are from the red team, etc). Neutral Recon 
Stations appear as hollow white squares. However, when a Recon Station is captured the 
color will change to reflect the color of the team that captured it (a red, green, blue, or gray 
hollow square).</P>

<P>Radar also displays when a missile is launched by showing a small white dot. By keeping 
an eye on radar you can see when a missile is fired at you and just as importantly from 
where it's launched. Stealthed tanks will not show up on radar so don't become overly-
reliant on radar when hunting enemies.</P>

<P>There are three radius levels to your Radar which you can cycle through using the F3 key. 
In general, when scouting an area keep your Radar Long-range, if enemies are nearby 
switch to Medium range, and for close opponents go to Short-range Radar.</P>

<P><h3><B>3.5 Module Display/Grid Map</B></h3></P>

<P>To the lower right-hand portion of the screen is your module display/grid map toggle. 
Whatever modules you've equipped in your tank are shown here. Modules shown in dark 
green text are modules that are currently turned off. When a module is activated, such as the 
phase multiplier, the selected module will turn to a bright green. This is also the best way to 
monitor your ammo count for conventional weaponry. Modules that are light blue are 
modules that are always turned on.</P>

<P>Pressing the F2 key will toggle the Grid Map dividing cities into sectors such as A2, C4, 
etc. This is a convenient means to communicate tactics with your team like "I need backup 
at C3," or "Someone capture the Recon Station at D3."</P>

<P>Your teammates' tanks will show up as small respectively colored squares (red squares for 
red tanks) on the Grid Map. Likewise, Recon Stations will be white for neutral or the color 
of the team that captured them. Enemy tanks do not, however, show up on the Grid Map, 
whether they're stealthed or not.</P>

<P><h3><B>3.6 Targeting Window</B></h3></P>

<P>After a while you'll make new friends and enemies in Tanarus. Knowing just who it is 
piloting that enemy Vanguard can mean all the difference between life and death. Between 
your Module Display and Speed indicator is your Targeting window. This can be used to 
target an enemy tank or to show the name of a teammate. </P>

<P>Targeting lets you assign a nearby enemy tank into the Targeting window. Targeted tanks 
will show up on radar as yellow and will also have a yellow arrow pointing down at them 
within the main action screen. Enemy tanks equipped with stealth cannot be targeted.</P>

<P><h3><B>3.7 Kills, Deaths, and Score</B></h3></P>

<P>Your present score and kills to deaths are located next above the Targeting window. Your 
score is directly affected by the bounty of whom you kill – the higher their bounty, the more 
points you receive. Your kills and deaths reflect how many times you've killed others and 
how many times you died – unless you enjoy losing, it's better to have the former higher 
than the latter. Whenever you earn points, half of whatever you earn will also be added to 
your bounty. For example, if you kill an enemy tank with a 10,000 bounty, your score will 
increase by 10,000 pts and your bounty will be incremented by 5,000.</P>

