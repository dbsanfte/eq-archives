<p><center><H2><B>CHAPTER 1. GETTING STARTED</B></H2></center></p>


<p>The Tanarus Chamber is divided into three main areas: the Command Center, Arenas, and 
Chat channels. While each of these three main areas have their own specific functions, they 
also share common features such as the communication window for conversing with other 
players and a players listing displaying all other tank commanders  presently located in the 
given chamber. This chapter will cover all aforementioned areas and features. </p>

<p><H4><b>Communication Window</b></H4></P>

<p>At the lower left of the Command Center, Arenas, and Chat channels is the Communication 
window. Talking with other players in the chamber is as easy as typing what you want to 
say and hitting the Enter key. Here you can brag about how you kicked somebody's 
Devastator in your crippled Chameleon (yeah, and you should see the size of the fish I 
caught). You can also use the scrollbar arrows to glance back through older messages that 
have scrolled out of the window.</p>

<p><H4><b>Players Listing</b></H4></p>

<p>The player listing is the window located to the right-hand side of the screen. The name of 
your current location is at the top of the screen (Command Center, Arenas, or Chat 
channels). While the basic function of the player listing window is to let you scroll through 
all the players presently located in the chamber with you, it also lets you pull up detailed 
statistics on individuals. Right-clicking on a name in the Player Listing window will display 
that player's respective kills to deaths and other gameplay statistics. If a player belongs to a 
team, a Roster button will also appear letting you look at the respective team.</p>

<p><H3><b>1.1 Tanarus Command Center</b></H3></p>

<p>Before being able to strap yourself into several tons of metallic death, you'll always find 
yourself in the main Tanarus Command Center immediately following the initial Login 
Screen. The Command Center is basically a launching point linking you to the Arenas and 
Chat channels. After gaining proficiency and reaching a certain rank, new features allowing 
you to form, manage, or join teams will become available here.</p>

<p><H3><b>1.2 Arena Selection</b></H3></p>

<p>You're ready to get your turret smoking and your treads rolling. At the Command Center 
left-click on the Arenas button. The Arenas are classified into the following groups. </p>

<p><H4><b>Beginner</b></H4></p>

<p>This is where you'll first begin your tour of duty. Only newbies (fresh meat) through 
Private First Class are allowed in the Beginner arenas.</p>

<p><H4><b>Open</b></H4></p>

<p>Allows any <b>RANK</b> to participate in standard four teams of five warfare.</p>

<p><H4><b>Force Wars</b></H4></p>

<p>The Force Wars are special arenas in which Lightnings armed with only four force missiles 
attempt to force enemy tanks off ledges or ramps to their deaths.</p>

<p><H4><b>Pheniox Wars</b></H4></p>

<p>The Pheniox Wars are special arenas in which Lightnings armed with four Phx missiles 
take on other lightnings with the same armorment (note u may also arm rockets if you so chose)</p>

<p><H4><b>Ten Versus Ten</b></H4></p>

<p>A special set of arenas that allow two teams of ten players each to battle for citywide 
supremacy.</p> 

<p><H4><b>Team</b></H4></p>

<p>The Team arenas serve as a scrimmage area and uses the official League rules. Here you 
can scrimmage against other teams and hone up your tank killing skills before partaking in 
League play. There are two primary differences between League and Team play, however. 
The first is that you can have smaller scrimmages (4 vs 4, 3 vs 5, etc). The second is that it 
allows different teams to mix together on each side.</p>

<p><H4><b>*League*</b></H4></p>

<p>*So you think your team has got what it takes to be numero uno over the rabble-rousers? 
Then prove it. League arenas are reserved for team members only. Refer to Chapter 2, 
sections 2.4-2.5 for a detailed description on League Play.*</p>

<P>*please note League was never actualy officaly implemnted fully*</p>

<p><H4><b>Duel</b></H4></p>

<p>That jerk keeps saying "I'd kick your sorry butt one on one any day." Instead of escalating 
a private war among every player in an arena, challenge the offensive player to a private 
duel and clean their clock (or so you hope).  The Duel arenas are smaller arenas than the 
others, ideal for undertaking mano-to-mano fights or even two versus two. The Duel arenas 
also use the League rules, except that it doesn't require 5 versus 5 to start a game (it can be 
1 vs 1, 2 vs 2, etc).</p>

<p><H4><b>Practice</b></H4></p>

<p>Whether you want to try out a risky new tank configuration or simply want to learn without 
having your individual statistics affected, the Practice arenas are open to all ranks. Within 
the Practice arenas none of your kills, deaths, or score will be recorded.</p>

<p><H4><b>Racing</b></H4></p>

<p>Racing arenas are specialy built for this purpose which is 2 lightnings with 5 turbos and
a dampner racing around a track. first one to die in the base at the end of the track wins
the Racing concept is now on the server but the rules and setup are as yet not enforced 
(we belive mainly as the scoring for such a game style is imposible)</p>

<p><H3><b>1.3 Arena Functionality</b></H3></p>

<p>To choose the arena grouping you want to enter, just highlight the group (such as Beginner) 
and then click on the OK button.  The player listing window and communication window 
will carry over from the Command Center with the most notable new addition being the 
Arenas menu located to the upper-left of the screen. Each Arena channel has its own 
respective communication window to talk with other players in that Arena.</p>

<p>An individual arena can hold a maximum of twenty players playing at once, five per team. 
The Red, Green, Blue, and Gray numbers represent the amount of players within that city 
on that given color. For example, a red five means that the red team is currently filled with 
five players.  However, any number can be present in arena chat.</p>

<p>Left-clicking on an Arena name will go to that Arena channel and the players listing 
window will reflect the respective players in that area. Player names listed in yellow are 
currently in the chamber with you whereas red, blue, green, and gray represent players 
battling within that Arena.</P>

<p>When selecting what color you want to join, look at the Red, Green, Blue, and Gray buttons 
to the right of the Play button in the Arenas window. Left-click on a color that has room to 
head into battle on that color. But if decisions make your head hurt, left-clicking on Play 
will randomly place you on one of the open teams that has space for another player. The 
Exit button will return you to the main Command Center.</P>

<p><H3><b>1.4 Chat Channels</b></H3></P>
<p>
Whether you want to give your trigger finger a rest or are seeking Technical Support, left-
clicking on the Chat button found in the Command Center will let you join other 
conversation channels. A new listing showing all Chat channels will appear to the upper-left 
portion of the screen. To join a channel, simply scroll until you find the channel you want; 
then click on the channel name, and the player listing window will subsequently display the 
players on that channel. </p>




