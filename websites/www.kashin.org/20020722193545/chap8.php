<H2><p><center><B>CHAPTER 8: ALL DRESSED UP AND READY TO KILL</B></center></p></H2>


<p>
Each Arena in Tanarus can hold up to twenty players with five players per team. Unless you 
think you're a solo hotshot, the best idea is to join a team that already has a couple players 
on it so you don't end up taking on the world by yourself (which is often suicidal to say the 
least).</P>

<P>The goal of Tanarus? First and foremost to enjoy yourself. Whether you like getting down 
and dirty in close combat, providing artillery for long-range support, capturing Recon 
Stations, stealing enemy flags, or managing a team, Tanarus offers numerous possibilities to 
match your playing style.</P>

<P>Before jumping into the heat of battle for the first time be sure to check out the training 
missions. At the initial Tanarus menu, select Training and perform each of the first three 
training exercises. These exercises will give you a much better understanding of how the 
tanks operate and the basic gameplay fundamentals. This chapter will serve as an initial 
walk-thru to help you grasp an understanding of what goes in each Arena followed by 
suggestions to improving your gaming experience.</p>


<H3><P><B>8.1 Power</B></P></H3>

<P>The first thing a trigger-happy pilot will learn is that bigger guns don't necessarily mean 
victory. From the main Command Center, choose Arenas and find a low-populated or 
preferably empty Arena and join it. At the initial Equipment Selection menu, select a 
Vanguard and equip it with a Mark IV, Phase Multiplier, and Controller. Don't worry about 
the rest of the configuration just yet. What we're going to cover is the most crucial element 
of the game: Power.</P>

<P>Enter the Arena now and practice driving around near your base speeding up, slowing, 
reversing, and turning. After you've got a feel for your tank, drive away from the base 
keeping an eye on your POWER bar. Notice how it's decreasing the further you move away 
from base? That's because both your home base and Base Recon Station emit power over a 
certain decreasing radius – the farther your distance the less power you receive.</P>

<P>Drive further away so that your FEED meter decreases to neutral then call in a Satellite. 
When the Satellite arrives, keep an eye on your FEED meter again. Friendly satellites emit 
power as well.</P>

<P>A final form of power is possible through the acquisition of Recon Stations throughout the 
map. As covered earlier, your Radar will show neutral Recon Stations as white. These emit 
no power for any team but can be used to repair and re-arm your tank. Try to find a Recon 
Station that hasn't been taken over and doesn't have any enemy activity near it. Grab a 
Recon Capture module at your Base Recon Station then drive into a neutral Recon Station 
and onto the colored tile under the white orb then activate the Recon Capture. You'll see 
the Recon Capture disappear from your module inventory. Look at Radar and you'll see a 
satellite arrive.</P>

<P>Back up and watch the Satellite as it takes over the Recon Station. During this time you 
cannot use the Recon Station. Glance at your FEED meter, too. After the Satellite captures 
the Recon Station and flies away, look at your FEED meter again. You're now receiving 
much more power than before, and any enemy that drives near your Recon Station will find 
its power being leeched. Likewise, if you drive near an enemy Recon Station, watch your 
FEED meter. As you near the enemy Recon Station your FEED meter will start increasing 
to the left – except this time it's red, meaning you're losing power.</P>

<H3><P><B>8.2  Superiority through Teamwork</B></P></H3>

<p>Don't try to take on the world alone. Work with your teammates using the private chat to 
try and maximize your advantage against enemy teams. It's no exaggeration to say that two 
players teaming up together can successfully hold off and even push back five enemy 
teammates who are not coordinating with each other at all.</p>

<H3><P><B>8.3  Flag Capturing</B></P></H3>

<P>The ultimate humiliation to an enemy team isn't by smoking their tanks on the battlefield, 
but by sneaking into their base, grabbing their flag, then returning to home base to capture 
the flag. To pull off a successful flag capture you need a flag capture module equipped in 
your tank and good team support. Packing in some reserve powers and turbo boosts as well 
as setting up a relay tank (another friendly tank with a flag capture module and turbos 
waiting for you to bring the flag out and hand off) is a good strategy. But before you try a 
flag capture, it's important to understand the flag capture system.</P>

<P>A flag will only be able to be taken if there is at least one member on that given team. This 
prevents a player from logging in and scooping up all the flags, then waiting for enemies to 
log in and killing them instantly with their flag.</P>

<P>There is also a ten minute limited time factor in which a seized flag has to be captured, or it 
will return automatically to the respective team's base. Also, for whichever team has their 
flag seized, if they log off during those ten minutes the team that currently has the flag will 
be credited with the kill (the flag will have to be in an enemy tank, though). In other words, 
don't log if your flag has been seized -- you won't escape death that way.</P>

<P>And for a final measure, players that happen to log into a game when their flag is being 
captured will be given a grace time equal to the maximum time given for flag capturing. 
Basically a player that logs in has ten minutes grace from his/her flag being captured. This 
will primarily prevent players from logging in right when their flag is being captured and 
dying -- they weren't in the game when their flag was seized, so they shouldn't be penalized 
for it. A quick summary follows:</P>

<P>? Flags can only be taken if the respective opponent's team has one or more players.
? If a flag isn't captured within 10 minutes of being taken, the flag will automatically 
return to its original base.<BR>
? Players logging into a game are given a 10 minute grace period when logging in – this 
is to protect players logging in from dying when a flag capture is already underway.
? Any player that is on a team when their flag is taken and tries to log will automatically 
die if the flag is within an enemy tank's possession, giving the capturing team a kill, 
followed by logging out.</P>

<P>For the capturing team, each individual team member receives the total kills. For example, 
a team captures blue's flag and there are five blues playing, each member of the capturing 
team receives five kills.</P>

<P>The bounty, on the other hand, sums up the total bounty of the team that's being captured 
(lets say the combined bounty of five players is 100,000 pts), then divides it equally among 
the capturing team (which for this example has four players). Subsequently each player 
receives 25,000 pts.</P>

<H3><P><B>8.4 Activating Modules</B></P></H3>

<P>Remember that equipping your tank with a module doesn't necessarily mean that the given 
module is automatically activated. Keep an eye on your module inventory – modules that 
aren't activated will be shown in dark green text, whereas modules that are turned on will 
be displayed as bright green. Automatic modules are light blue. </P>

<H3><P><B>8.5 Sample Tank Configurations</B></P></H3>

<P>Although part of the enjoyment in Tanarus is learning the various configurations possible 
with each tank (and you'll most likely never even use a quarter of all possibilities), it's not 
fun getting your butt kicked all over the place while you try to learn your first useful 
configuration. The following list details three configurations per tank type.</P>

<P><B>Lightning</B><BR>
1. Shield, Mark IV, Phase Multiplier, Reserve Power, Phoenix Missile<BR>
2. Shield, Mark II, Phase Multiplier, Reserve Power, Two Phoenix Missiles<BR>
3. Shield, Shield Boost, Mark II, Phase Multiplier, Reserve Power<BR></P>

<P><B>Vanguard</B><BR>
1. Shield, Shield Boost, Phase Multiplier, Reserve Power, Supercharger, Stealth<BR>
2. Shield, Shield Boost, Phase Multiplier, Plasma Cannon, Reserve Power<BR>
3. Shield, Shield Boost, Phase Multiplier, Reserve Power, Stealth, Nullifier<BR></P>

<P><B>Devastator</B><BR>
1. Shield, Shield Boost, 6 Bays of AP Shells, and Stealth<BR>
2. Shield, Shield Boost, Plasma Cannon, Mark IV, Phase Multiplier, Two Reserve, Powers, Stealth<BR>
3. Shield, Shield Boost, 5 Bays of Heat Shells, Doppler Radar<BR></P>

<P><B>MagRider</B><BR>
1. Shield, Shield Boost, Reserve Power, Stealth<BR>
2. Shield, Mark IV, Reserve Power, Stealth<BR>
3. Shield, Plasma Cannon, Reserve Power, Supercharger<BR></P>

<P><B>Chameleon</B><BR>
1. Missile Launcher, Two Phoenix Missiles, Turbo<BR>
2. Five Proximity Mines<BR>
3. Mark IV, Phase Multiplier, Reserve Power, Supercharger<BR></P>


