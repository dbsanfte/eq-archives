<H2><p><center><B>CHAPTER 7. GRAPHIC AND CONTROL OPTIONS</B></center></p></H2>


<p>
If you were to read only one chapter before jumping right into an Arena (which really isn't 
advisable), this is the most important chapter in terms of actual gameplay control. This 
chapter covers both how to optimize keyboard and joystick control as well as illustrating 
the default keyboard control layout. </p>


<H3><P><B>7.1 Optimizing Controls for the Keyboard and Joystick</B></P></H3>

<P>Different gamers have different control tastes, so while we've included a default set of 
controls, we've also given you the ability to customize control completely for the keyboard 
and joystick. At the initial login menu, left-click on Options. You can completely customize 
the keyboard and joystick as well as tune the force effects to your tastes if you have a Force 
Feedback joystick. </P>

<P>Once you've selected either Keyboard or Joystick you can map the keys to your liking. 
Above the controls listing is text that instructs you how to properly customize the 
keyoard/joystick layout. Remember to left-click on the Save button to save any changes you 
make.</P>

<H3><P><B>7.2  Adjusting the Graphic Detail</B></P></H3>

<p>Tanarus has extensive support for graphic accelerator cards under Direct 3D. If you have a 
graphic accelerator card, you'll definitely want to go to Options then Graphics.</p>

<H4><P><B>Under the Graphics menu you'll be able to do the following:</B></P></H4>

<P>? Select the graphics accelerator card you want to use<BR>
? Select Display Resolution<BR>
? Select 3D Options<BR>
? Adjust 3D Particle Density<BR></P>

<P>If under Select 3D Hardware it only reads No 3D Acceleration that means you don't have a 
graphics accelerator card. You'll only be able to turn on the 3D sprites option under Select 
3D Options.</P>

<P>Select Display Resolution lets you save a cockpit view or full-screen. These can also be 
turned on and off from within the game (F5 for cockpit, F6 for full-screen).</P>

<P>Last, but not least, for those with graphic accelerator cards, the Select 3D Options allows 
you to balance graphics and gameplay. Some cards handle certain special effects better than 
others, and some cards simply don't support all the types of special effects that Tanarus 
provides. If you're having low frame rates under your graphic accelerator card, be sure to 
try turning off some of the special effects until you find a good compromise between 
graphic quality and gameplay.</P>

<P>Whether you're shopping for a graphic accelerator card or just want to see if we directly 
support your graphic accelerator card, a comprehensive listing of graphic accelerator cards 
that Tanarus officially supports can be found at http://www.tanarus.com. This also provides 
links to the latest drivers that we've tested with Tanarus. </P>

<H3><P><B>7.3  Chat Commands</B></P></H3>

<H4><P><B>Squelch</B></P></H4>

The squelch command temporarily blocks incoming messages from the squelched player. 
To squelch a player from within the chamber, type #squelch handle. To squelch a player 
from within a game, type %squelch handle. For example, to block out messages from 
Frank, you would type #squelch Frank (in the chamber), or %squelch Frank (in the game). 
Upon leaving the chamber or game the squelch is negated. You can also use #unsquelch 
handle (in the chamber) or %unsquelch handle (in the game) to unsquelch a player.

<H4><P><B>Ignore</B></P></H4>

When a player is repeatedly annoying, the squelch command may not be enough. This is 
where the ignore command comes in handy. The ignore command permanently ignores the 
targeted player. To ignore a player in the chamber, simply type #ignore handle; to ignore a 
player in a game, type %ignore handle. Ignoring a player will block all messages from that 
player. A small .TXT file in your Tanarus directory called IGNORE.TXT contains the 
names of the ignored players. The only way to undo an ignore is to type #unignore handle 
(from the chamber) or %unignore handle (from the game).

<H4><P><B>Universal Send System</B></P></H4>

The universal send system lets you send a message to anyone anywhere in Tanarus. All you 
need to do is type /handle [message]. For example, if someone wanted to send Frank a 
private message, they would just type /Frank Hi, how's it going? You can send messages to 
players in games from the chamber, or vice versa. Naturally if someone is ignoring you the 
message won't get through.

<H4><P><B>Profanity Filter</B></P></H4>

The default for the profanity filter is on. The filter uses a .TXT file called FILTER.TXT in 
your Tanarus directory. This contains common swear words that will automatically be 
filtered out when another player types them. If you wish to add some words, simply type 
#filter <word> (in chamber) or %filter <word> (in game). Or you can directly edit the 
FILTER.TXT and add the words you want. Note that you can only have 25 words total to 
be filtered.

<H4><P><B>Resetting Your Handle</B></P></H4>

Each handle can have its stats reset twice. To do so, you simply type #ZERO in the 
chamber. This will keep your handle, but reset your stats (ratio, etc). Again, this can only be 
done twice per handle (so a total of four times ever), so use it wisely.

<H4><P><B>Renaming Your Handle</B></P></H4>

You can rename a handle once a week (every seven days). To do so, simply type 
#RENAME followed by the handle you want. Note that renaming a handle does not give 
you more #ZEROs.


