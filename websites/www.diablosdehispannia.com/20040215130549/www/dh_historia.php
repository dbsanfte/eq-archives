    <td bgcolor="black" class="texto">
    	<p>Todo empezó con el Diablo, varios españoles se conocieron en ese juego y se dieron cuenta que compartían aficiones comunes. Como Internet estaba infradesarrollado en España era difícil encontrar compañeros online lo que motivo que los pocos que se iban encontrando recibieran con alegría a cada español que daba el paso y empezaba en los juegos online. Poco a poco se fue reuniendo un pequeño grupo de jugadores, el germen de lo que luego seria el clan. El contacto entre ellos se fue haciendo más estrecho y ya no bastaba el tiempo que coincidían online y por ello se creo lo que ha sido una de las columnas vertebrales de este clan, las listas de correo. Entonces se dio el paso principal, si ya éramos amigos online, ¿por que no conocernos en la vida real? Se organizaron las primeras "quedadas" en las cuales se vio que detrás de cada muñequito había una persona real, con su complejidad.</p>
      <p>Entonces llego el Ultima Online, el juego de más grato recuerdo para la mayoría de nosotros. Allí el grupo de amigos se fue ampliando y nos dio la posibilidad de crear un "Clan". Y así, el 18 de Febrero de 1997 se puso la primera piedra del clan (literal y metafóricamente) y nació "Diablos de Hispannia". Diablos en homenaje al primer juego Online en el que se conocieron los y Hispannia en contraposición al "Britania" que era el nombre del lugar donde se desarrollaba el UO y para significar que era un grupo de amigos con una cosa en común, el español. Este ha sido siempre el único requerimiento para entrar en el clan, el conocer y hablar el español. Frente a otras asociaciones de jugadores el clan nació ya con dos ideas muy claras, queríamos expresarnos en nuestra propia lengua y queríamos que fuera algo mas que un clan para un juego, queríamos que fuera un grupo de amigos.</p>
      <p>Esto nos hizo ser el primer clan español dentro de los juegos online y el mantenernos fieles a esos dos principios lo que nos ha mantenido estos años en activo y siempre creciendo, en numero (que es lo menos importante) pero sobre todo en calidad y en profundidad de la relación de amistad entre nuestros miembros. Posiblemente haya más gente que ha estado un tiempo dentro del clan en estos 5 años y pico de los que se han quedado, pero estamos contentos de que sea así. Por que cada uno de los que se ha quedado se ha convertido en un AMIGO para todos los demás. Nunca hemos comulgado con la idea de ser un clan "de paso" o Para un juego en particular, si no un clan "para siempre" independientemente del juego/s en los que cada uno juegue o incluso aunque no se juegue a ningún juego online.</p>
      <p>La época del UO fue una época divertida e intensa, llena de anécdotas que serian demasiado largas para enumerar aquí. Basta decir que jugamos primero en el servidor de Sonoma y más tarde en Drachefells cuando se abrió y pensamos que el lag seria menor...esperanza vana con las conexiones que teníamos en aquel entonces en España frente a la de los demás países.</p>
      <p>Cada vez nuestra pequeña familia se iba ampliando y las reuniones en la vida real cada vez eran más amplias. El sentimiento de unidad, camaradería y amistad mas fuerte momento a momento y el clan era una realidad.</p>
      <p>Sin embargo llego el Everquest y muchos migramos a este nuevo juego gráficamente espectacular comparado con el UO. Durante los primeros meses fue estupendo y muchos nuevos jugadores, atraídos por el juego y por que poco a poco Internet se iba actualizando en España entraron en el clan. Sin embargo al cabo del tiempo nos dimos cuenta que la mayoría de la gente que entraba no comprendía que nuestro clan no era solo un grupo de jugadores del Eq, si no que éramos antes que nada amigos y eso es lo que pretendíamos seguir siendo, por encima de juegos y personajes. Esto llevo a muchos problemas dado algunos anteponían sus personajes y los logros dentro del juego a buscar la amistad con sus compañeros de clan y nos vimos obligados, tras varios intentos de compaginar las dos cosas, a elegir. Entre convertirnos en otro clan mas de jugadores o seguir siendo un clan de amigos. Elegimos esto ultimo, como no podía ser de otra manera y aunque nos dolió tuvimos que separarnos de mucho que quizás con el tiempo o un poco mas de paciencia podían haberse convertido en excelentes amigos.</p>
      <p>Decidimos abandonar el Eq como clan, aunque algunos miembros continuaran jugando allí a titulo personal, y la mayoría de nosotros fuimos al Dark Ages of Camelot. Donde, tanto por el sistema de juego, como por tener la lección ya bien aprendida, pasamos y seguimos pasando estupendos ratos y se nos unieron muchos nuevos miembros que ya son amigos de todos.</p>
      <p>Actualmente seguimos con interés desde la Beta el Star Wars Galaxies, juego en el que posiblemente juguemos gran parte de los actuales miembros del clan y esperamos encontrar nuevos amigos. Aunque seguimos teniendo gente jugando al Daoc, Eq, Shadowbane, e incluso algún irreductible todavía en el UO.</p>
