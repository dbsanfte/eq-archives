    Para entrar en el clan, independientemente que sea a trav&eacute;s de un juego 
    con representaci&oacute;n DH u otras v&iacute;as, el proceso a seguir ser&aacute; 
    el siguiente: <br>
    &nbsp;&nbsp;&nbsp;- Aquella persona aspirante, una vez puesto en contacto con 
    los DH, podr&aacute; ser incluido en el clan dentro del juego en calidad de &#8220;recluta&#8221; 
    y en las listas de reclutamiento que as&iacute; lo pida. <br>
    &nbsp;&nbsp;&nbsp;- Tras un periodo no menor a 1 mes, el recluta as&iacute; como 
    cualquier otro DH podr&aacute; pedir que sea abierta una votaci&oacute;n. En dicha 
    votaci&oacute;n podr&aacute;n participar todos aquellos DHs que est&eacute;n en 
    el juego. Dicha votaci&oacute;n se realizara en la lista General o en un Foro 
    establecido a dicho efecto y ser&aacute; abierta sin fecha de finalizaci&oacute;n 
    y publica. <br>
    &nbsp;&nbsp;&nbsp;- Cada uno de los DHs que conozca suficientemente al aspirante, 
    y crea que su incorporaci&oacute;n al clan es positiva, votar&aacute; a favor 
    de &eacute;l tras haber expuesto sus razones en la lista o foro creados a tal 
    efecto. Los DHs que todav&iacute;a no conozcan al recluta se 
    abstendr&aacute;n en dicha votaci&oacute;n y aquellos que crean que no deber&iacute;a 
    entrar en el clan, votar&aacute;n en contra y explicar&aacute;n el por qu&eacute; 
    de ese veto en la lista o foro. El tener 1 veto razonado, y una vez aceptado 
    por el consejo, significar&aacute; la no aceptaci&oacute;n del candidato para 
    su entrada en los DHs sea cual sea el n&uacute;mero de votos a favor. <br>
    &nbsp;&nbsp;&nbsp;- El n&uacute;mero de votos afirmativos para la entrada del 
    recluta en el clan deber&aacute; ser de al menos 20 DHs y el 75% de los DHs 
    que est&eacute;n actualmente en el juego en el cual se presente dicho candidato. <br>
    &nbsp;&nbsp;&nbsp;- Al ser una votaci&oacute;n abierta sin fecha de finalizaci&oacute;n 
    es de esperar que el recluta se de a conocer al numero de DHs suficientes para 
    que paulatinamente las abstenciones se conviertan en votos positivos y manteniendo 
    siempre su buena conducta para evitar un voto negativo o veto. Para ello facilitamos 
    el uso al aspirante de los medios propios de cada juego, y los foros o listas 
    de correo. <br>
    &nbsp;&nbsp;&nbsp;- Una vez conseguido el n&uacute;mero de votos favorables, se 
    le invitar&aacute; a unirse de forma permanente a nuestro clan. Para ello deber&aacute; 
    mandar un mail al Consejo donde deber&aacute; escribir una sinopsis de sus datos 
    personales, su inter&eacute;s para por el clan, el por qu&eacute; se decidi&oacute; 
    a entrar, sus experiencias dentro de &eacute;l, todos aquellos datos que crea 
    puedan ser de inter&eacute;s para el clan y, por &uacute;ltimo y siendo imprescindible, 
