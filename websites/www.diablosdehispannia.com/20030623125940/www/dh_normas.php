    Ante todo somos una comunidad de amigos de habla castellana a los que nos une el gusto por los "juegos online" Nuestra única finalidad es conservar y acrecentar esa amistad y compañerismo. No somos un clan de jugadores que se reúnen para jugar puntualmente a un juego determinado, somos un grupo de amigos que entre otros sistemas, utiliza Internet para mantener el contacto. Para nosotros los juegos online son el medio para pasar un rato en compañía de nuestros amigos, no un fin en si mismos.<br>
    Somos posiblemente el clan online español más antiguo cuya fundación se remonta al Ultima Online (de ahí lo de Hispannia en contraposición al Britain, capital del reino) aunque ya en Diablo 1 (de ahí lo de Diablos) se conocieron la mayoría de los miembros fundadores. Tras la amistad virtual llego la amistad real y procuramos mantener ese "espíritu DH" que significa que cada DH es una amigo real, una persona con actitud y deseos afines, por encima, simplemente, de otro personaje más al otro lado de la pantalla del ordenador.<br>
    No nos interesa ser el clan mas numeroso o poderoso de cada juego online en el cual estemos presentes, nos interesa que cada persona que forma parte de nosotros se relacione con los demás miembros del clan fomentando la amistad por encima de que su personaje alcance las cotas mas altas posibles dentro de ese juego en particular.<br>
    En resumen, buscamos y damos preferencia a la amistad real y compañerismo ante que a los juegos online, que son solo otro medio más y ni siquiera el más importante para desarrollar esa amistad.
