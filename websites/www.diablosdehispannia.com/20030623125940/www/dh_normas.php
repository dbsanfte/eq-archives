    <td bgcolor="black" class="texto">
    Independientemente de los &oacute;rganos de Gobierno del clan o clanes asociados 
    de acuerdo con las caracter&iacute;sticas particulares de cada juego, los siguientes 
    &oacute;rganos/cargos son los que Gobiernan a todos los DHs. <br>
    <br>
    <strong>1&ordm; Consejo de los DHs. </strong><br>
    Esta formado por 15 miembros elegidos en votaci&oacute;n general por todos los 
    DHs. <br>
    Su periodo de existencia es de 2 a&ntilde;os contados a partir de la fecha de 
    formaci&oacute;n. <br>
    Son los representantes elegidos democr&aacute;ticamente por sus compa&ntilde;eros 
    DHs para que los representen y decidan sobre la mejor forma de gobierno del clan 
    en general. <br>
    Son el &uacute;ltimo &oacute;rgano de decisi&oacute;n sobre todos los temas que 
    afecten de una manera u otra al clan. <br>
    En los temas que as&iacute; lo considere oportuno el Consejo por su trascendencia 
    u otros motivos, someter&aacute; las decisiones a Refer&eacute;ndum General de 
    todos los DHs bastando mayor&iacute;a simple para la &uacute;ltima decisi&oacute;n. <br>
    Para ser elegible dentro del consejo se deber&aacute; acreditar una permanencia 
    continuada y sin interrupci&oacute;n dentro del clan (comprobable seg&uacute;n 
    fecha de ingreso en la lista general) de al menos 18 meses. <br>
    Dentro del Consejo no habr&aacute; Presidente, GM o cualquier otro cargo que distinga 
    a un Consejero por encima de otro, todos y cada uno de los consejeros es igual 
    a sus compa&ntilde;eros por encima de Cargos puntuales que tengan (moderador, 
    GM en un juego, R.R.P.P del clan, Webmaster, etc.). <br>
    Las decisiones sobre los temas se toman por consenso o por mayor&iacute;a simple 
    en votaci&oacute;n abierta a los dem&aacute;s Consejeros. En las votaciones del 
    Consejo no se permiten votaciones secretas u opciones del tipo NS/NC o abstenciones 
    (a no ser que sea un tema que afecte directamente a un Consejero, en cuyo caso 
    se permitir&aacute; y se fomentar&aacute; la abstenci&oacute;n de dicho Consejero) <br>
    Todas las deliberaciones del Consejo se efectuar&aacute;n en secreto dentro de 
    la lista/foro creada a tal efecto siendo motivo de expulsi&oacute;n del Consejo 
    el dar a conocer estas deliberaciones de forma p&uacute;blica sin permiso especifico 
    de dicho Consejo. Esto se hace para que cada consejero pueda expresar su opini&oacute;n 
    libremente sin que repercuta su decisi&oacute;n sobre los dem&aacute;s miembros. <br>
    El Consejo tiene facultad para el nombramiento directo de cuales cargos as&iacute; 
    lo determine oportuno pero prefiri&eacute;ndose siempre que sean elegidos libremente 
    en votaci&oacute;n por el resto de los DHs entre los candidatos que re&uacute;nan 
    las condiciones oportunas. <br>
    El Consejo ser&aacute; el &uacute;nico facultado para iniciar votaciones/encuestas 
    en la lista General. Siendo el resultado de estas votaciones un indicativo de 
    la voluntad expresada de todos los DHs y ateni&eacute;ndose al resultado. Si un 
    DH quiere poner una votaci&oacute;n en particular deber&aacute; expres&aacute;rselo 
    as&iacute; al consejo para que esta decida si procede y por lo tanto crear la 
    votaci&oacute;n o no. <br>
    Los Consejeros podr&aacute;n expresarse libremente a titulo personal en todas 
    las listas o foros de los DHs independientemente de cuales sean pero no hablando 
    a titulo del Consejo a no ser que &eacute;ste as&iacute; les faculte. Es conveniente 
    que todos los Consejeros est&eacute;n y revisen peri&oacute;dicamente todas las 
    listas de los DHs para estar informados de todo lo que sucede en el clan y as&iacute; 
    adelantarse a los deseos y corrientes de opini&oacute;n de los DHs y poder tratar 
    los temas en profundidad y con conocimiento de causa.<br>
    <br>
    <strong>2&ordm; Otros Cargos</strong><br>
    <br>
    <em>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;A) Moderador de las listas de correo. 
    (o foros) </em><br>
    El/los moderador/es de todas o cada una de las listas ser&aacute; el encargado 
    de mantener el buen orden y educaci&oacute;n dentro de ellas, teniendo facultad 
    para moderar todos los mensajes excepto los del Consejo. En el caso sea necesaria 
    una expulsi&oacute;n temporal de un miembro de una lista o foro el Moderador de 
    dicho Foro tiene la facultad de hacerlo sin pedir permiso al Consejo. En el caso 
    de expulsi&oacute;n deber&aacute; informarse y seguir las directrices del Consejo. 
    Ser&aacute; elegido entre los candidatos que se presenten por el Consejo. <br>
    <br>
    <em>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;B) Relaciones P&uacute;blicas del 
    Clan. </em><br>
    Ser&aacute; el encargado de mantener las relaciones con otros clanes y de cuidar 
    nuestra imagen con ellos. Puede ser una persona espec&iacute;ficamente destinada 
    para cada juego en particular o bien para el clan en general, aunque todos y cada 
    uno de los DHs ya sean miembros permanentes o puntuales son responsables del buen 
    nombre de &eacute;l clan siendo motivo de expulsi&oacute;n el fallar en este cometido 
    como ya se expuso en el punto &#8220;h&#8221; en los Derechos y Deberes. Ser&aacute; 
    elegido entre los candidatos que se presenten por el Consejo. <br>
    <br>
    <em>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;C) Webmaster </em><br>
    Ser&aacute; el encargado de mantenimiento de la p&aacute;gina Web y Foros del 
    clan, pudiendo para ello recabar la ayuda particular de algunos miembros aunque 
    se espera que todos los DHs contribuyan a su mantenimiento. Ser&aacute; elegido 
