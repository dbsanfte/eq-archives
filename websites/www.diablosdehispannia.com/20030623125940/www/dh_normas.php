    de dicha persona en los DHs tras petici&oacute;n de al menos el 75% de los DHs 
    que jueguen en ese momento a dicho juego. Por supuesto la existencia de un veto 
    por parte de alguno de dichos DHs invalida el proceso de entrada. <br>
