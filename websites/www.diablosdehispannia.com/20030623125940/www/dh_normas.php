    y deberes son: <br>
    &nbsp;&nbsp;&nbsp;a) Derecho a pedir su incorporaci&oacute;n a la lista o foros 
    que le corresponda de los DHs dentro del juego espec&iacute;fico en el que est&eacute; 
    dentro del clan. <br>
    &nbsp;&nbsp;&nbsp;b) Derecho a ser tratado con respeto y educaci&oacute;n por 
    los dem&aacute;s miembros del clan ya sea en ese juego o en otros de una manera 
    de acuerdo a su condici&oacute;n de DH.<br>
    &nbsp;&nbsp;&nbsp;c) Derecho a pedir la intercesi&oacute;n del organismo o persona 
    correspondiente dentro de la estructura del clan, en los temas en disputa que 
    surjan. <br>
    &nbsp;&nbsp;&nbsp;d) Derecho a pedir la intervenci&oacute;n del GM, comit&eacute; 
