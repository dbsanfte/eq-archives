    o foros de comunicaci&oacute;n de los DHs establecidas <br>
    &nbsp;&nbsp;&nbsp;b) Derecho a participar en todas las votaciones generales de 
    los DHs <br>
    &nbsp;&nbsp;&nbsp;c) Derecho a presentarse (si cumplen los requisitos establecidos) 
