    ser apeladas al Consejo a trav&eacute;s de mail privado siendo la decisi&oacute;n 
    de este &uacute;ltimo inapelable. <br>
    &nbsp;&nbsp;&nbsp;e) Derecho a expresar libremente su opini&oacute;n sobre los 
    temas referentes al juego en la lista y/o foro correspondiente. <br>
    &nbsp;&nbsp;&nbsp;f) Deber de mantener la buena imagen del clan y no desprestigiar 
    su nombre en relaciones con otros clanes. <br>
    &nbsp;&nbsp;&nbsp;g) Deber de leer y usar peri&oacute;dicamente las listas/foros 
    de los DHs en las cuales este incluido. <br>
    &nbsp;&nbsp;&nbsp;h) Deber de mantener una actitud de ayuda, compa&ntilde;erismo 
    y educaci&oacute;n con sus compa&ntilde;eros DHs dentro y fuera de las listas 
    de correo, foros y juegos. <br>
    &nbsp;&nbsp;&nbsp;i) Deber de cumplir las funciones especificadas para cada cargo 
    voluntariamente aceptado, dentro de la estructura del clan en cada juego. <br>
    &nbsp;&nbsp;&nbsp;j) Deber de aceptar y acatar las decisiones de los &oacute;rganos 
    superiores establecidos dentro del juego. <br>
    &nbsp;&nbsp;&nbsp;k) Deber de mantener todos los personajes de cada juego en el 
    que est&eacute;, ya sea como DH o como recluta, dentro del clan de los Diablos 
    de Hispannia. En el caso de que tenga personajes en ese juego dentro de otros 
