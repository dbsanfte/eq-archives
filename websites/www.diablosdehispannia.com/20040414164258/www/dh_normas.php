    <td bgcolor="black" class="texto"> Para entrar en el clan, independientemente 
      que sea a trav&eacute;s de un juego con representaci&oacute;n DH u otras 
      v&iacute;as, el proceso a seguir ser&aacute; el siguiente:<br>
      <br>
      &nbsp;&nbsp;- Aquella persona aspirante, una vez puesto en contacto con 
      los DH, podr&aacute; ser incluido en el clan dentro del juego en calidad 
      de &#8220;recluta&#8221; y en la lista de reclutamiento general, lista a 
      la que tendr&aacute; que enviar una presentaci&oacute;n en la que se de 
      a conocer de la mejor forma que sea capaz al resto del Clan. Tras recibir 
      dicha presentaci&oacute;n su proceso de entrada se activar&aacute;. <br> <br>
      &nbsp;&nbsp;- Cuando el Recluta estime conveniente se crear&aacute; una 
      votaci&oacute;n con una duraci&oacute;n de dos meses en la intranet de los 
      Diablos de Hispannia ( <a href="http://www.diablosdehispannia.com">http://www.diablosdehispannia.com</a> 
      ) En dicha votaci&oacute;n podr&aacute;n participar todos los miembros del 
      clan. A la hora de votar para el ascenso de un recluta todos los miembros 
      del clan podr&aacute;n votar al recluta, ya sea si, no o N/C. Para entrar 
      definitivamente en el clan el recluta debe obtener al menos el 51% de los 
      votos afirmativos de los miembros censados y activos de DH. No debe, igual 
      que antes, tener ning&uacute;n veto razonado en su contra. <br>
      <br>
      &nbsp;&nbsp;- Se recuerda a todos los reclutas que aunque no hay un tiempo 
      l&iacute;mite para ascender a miembro de pleno derecho si se detecta que 
      el recluta tras un tiempo prudencial no se relaciona, muestra extrema dejadez 
      o solo quiere permanecer como recluta ser&aacute; expulsado del clan. <br>
      Los reclutas deber&aacute;n ser los principales interesados en utilizar 
      la lista de reclutamiento ya que ser&aacute; su principal y m&aacute;s importante 
      herramienta para entrar en el clan. Si se limitan tan solo al juego en el 
      que est&eacute;n ver&aacute;n dificultada su entrada. <br>
      Para calcular el n&uacute;mero de votos necesarios para su entrada en el 
      clan se tomaran en consideraci&oacute;n el total de DHs activos en ese momento, 
      no el total de todos los DHs. La diferencia entre DHs Activos e Inactivos 
      se har&aacute; de la siguiente forma: <br>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#8226; Ser&aacute;n <em>DHs 
      activos</em> todos aquellos que participen de forma peri&oacute;dica en 
      todas las formas de comunicaci&oacute;n de los DHs (listas de correo, foros, 
      etc) generales para todos los DHs (no las formas de comunicaciones dentro 
      de un juego o listas especificas no obligatorias)<br>
      &nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&#8226; Ser&aacute;n <em>DHs 
      Inactivos</em> aquellos DHs que as&iacute; lo soliciten por dejar de poder 
      participar temporalmente en las listas, foros, etc, por motivos de trabajo, 
      viaje, etc. Para ello solo deber&aacute;n mandar un e-mail al Consejo o 
      encargado del Censo indicando que desean que se les considere inactivos 
      durante un tiempo y luego otro para dejar constancia que ya no est&aacute;n 
      inactivos y vuelven a ser activos. Se considerar&aacute; inactivos a aquellos 
      que durante un periodo de tiempo dado no participen en las cuestiones generales 
      del clan (listas, foros, votaciones, etc). El sistema para volver a ser 
      activos es el mismo, simplemente un e-mail al Consejo o al encargado del 
      Censo para volver a ser considerados activos.<br>
      <br>
      Esto se traduce en: Espec&iacute;ficamente en el caso de votaciones de reclutas, 
      tras el primer mes y medio se mandara un e-mail a aquellos DHs que no hayan 
      votado record&aacute;ndoles la existencia de una votaci&oacute;n abierta. 
      Una semana despu&eacute;s se mandara un segundo e-mail volvi&eacute;ndoles 
      a recordar las votaciones. Al cabo de dos meses se considerara inactivos 
      a aquellos que no hayan votado y por lo tanto se descontara su n&uacute;mero 
      del n&uacute;mero total de DHs con capacidad de voto. Por lo tanto el 100% 
      pasar&aacute; a ser el n&uacute;mero total de DHs que han votado en esos 
      dos meses y de ah&iacute; se vera si el recluta ha alcanzado el 51% necesario.<br>
      <br>
      Esto se aplica tanto a votaciones de reclutas como a todas aquellas votaciones 
      que sea necesario en otros asuntos del clan. Para votaciones posteriores 
      esas personas permanecer&aacute;n en estado inactivo hasta que manden un 
      e-mail reactiv&aacute;ndose al Consejo.<br>
      <br>
      Para aquellos que no est&eacute;n conformes con ninguna de las opciones 
      de la votaci&oacute;n en los reclutas se a&ntilde;ade la opci&oacute;n &quot;no 
      me importa&quot; la cual puede votarse para evitar quedar en inactivo, los 
      votos de esta opci&oacute;n no se tendr&aacute;n en cuenta para calcular 
      el total y el 51% necesario.<br>
      <br>
      Si un recluta no alcanza el 51% necesario de votos afirmativos y ning&uacute;n 
      veto continuara como recluta pudiendo pedir una segunda (y definitiva) votaci&oacute;n 
      posterior de id&eacute;ntica duraci&oacute;n a la primera cuando crea que 
      pueda alcanzar ese 51% necesario dentro de un periodo m&aacute;ximo de 6 
      meses a contar desde la fecha de creaci&oacute;n de la primera votaci&oacute;n. 
      Si en la segunda votaci&oacute;n tampoco alcanza el 51% necesario para su 
      incorporaci&oacute;n definitiva al clan o no se realiza esa segunda votaci&oacute;n 
      antes de seis meses se replantear&aacute; su permanencia en el clan.<br>
      <br>
      En el caso de que una persona est&eacute; inactiva (excepto en el caso de 
      que lo haya pedido el mismo v&iacute;a e-mail al Consejo) por un periodo 
      superior a 3 meses o haya entrado en el estado de Inactivo durante mas de 
      3 veces en el transcurso del mismo a&ntilde;o (no se contar&aacute;n las 
      veces que se entre en Inactivo por problemas de listas de correo/foros, 
      etc que pueda ocasionar el que entre inactivo involuntariamente) se pasar&aacute; 
      a replantearse su permanencia en el clan. </td>
