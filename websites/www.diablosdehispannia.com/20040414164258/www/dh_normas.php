      particulares de cada juego, los siguientes &oacute;rganos/cargos son los 
      que Gobiernan a todos los DHs. <br> <br>
      <strong>1&ordm; Consejo de los DHs.</strong> <br> <br>
      Esta formado por 15 miembros elegidos en votaci&oacute;n general por todos 
      los DHs. <br>
      Su periodo de existencia es de 2 a&ntilde;os contados a partir de la fecha 
      de formaci&oacute;n. <br>
      Son los representantes elegidos democr&aacute;ticamente por sus compa&ntilde;eros 
      DHs para que los representen y decidan sobre la mejor forma de gobierno 
      del clan en general. <br>
      Son el &uacute;ltimo &oacute;rgano de decisi&oacute;n sobre todos los temas 
      que afecten de una manera u otra al clan. <br>
      En los temas que as&iacute; lo considere oportuno el Consejo por su trascendencia 
