    <td bgcolor="black" class="texto"> Ante todo somos una comunidad de amigos 
      de habla castellana a los que nos une el gusto por los &#8220;juegos online&#8221; 
      Nuestra &uacute;nica finalidad es conservar y acrecentar esa amistad y compa&ntilde;erismo. 
      No somos un clan de jugadores que se re&uacute;nen para jugar puntualmente 
      a un juego determinado, somos un grupo de amigos que entre otros sistemas, 
      utiliza Internet para mantener el contacto. Para nosotros los juegos online 
      son el medio para pasar un rato en compa&ntilde;&iacute;a de nuestros amigos, 
      no un fin en si mismos. <br> <br>
      Somos posiblemente el clan online espa&ntilde;ol m&aacute;s antiguo cuya 
      fundaci&oacute;n se remonta al Ultima Online (de ah&iacute; lo de Hispannia 
      en contraposici&oacute;n al Britain, capital del reino) aunque ya en Diablo 
      1 (de ah&iacute; lo de Diablos) se conocieron la mayor&iacute;a de los miembros 
      fundadores. Tras la amistad virtual llego la amistad real y procuramos mantener 
      ese &#8220;esp&iacute;ritu DH&#8221; que significa que cada DH es una amigo 
      real, una persona con actitud y deseos afines, por encima, simplemente, 
      de otro personaje m&aacute;s al otro lado de la pantalla del ordenador. 
      <br> <br>
      No nos interesa ser el clan mas numeroso o poderoso de cada juego online 
      en el cual estemos presentes, nos interesa que cada persona que forma parte 
      de nosotros se relacione con los dem&aacute;s miembros del clan fomentando 
      la amistad por encima de que su personaje alcance las cotas mas altas posibles 
      dentro de ese juego en particular. <br> <br>
      En resumen, buscamos y damos preferencia a la amistad real y compa&ntilde;erismo 
      ante que a los juegos online, que son solo otro medio m&aacute;s y ni siquiera 
      el m&aacute;s importante para desarrollar esa amistad. </td>
