      continuada y sin interrupci&oacute;n dentro del clan (comprobable seg&uacute;n 
      fecha de ingreso en la lista general) de al menos 18 meses. <br>
      Dentro del Consejo no habr&aacute; Presidente, GM o cualquier otro cargo 
