      tipo NS/NC o abstenciones (a no ser que sea un tema que afecte directamente 
      a un Consejero, en cuyo caso se permitir&aacute; y se fomentar&aacute; la 
      abstenci&oacute;n de dicho Consejero) <br>
      Todas las deliberaciones del Consejo se efectuar&aacute;n en secreto dentro 
      de la lista/foro creada a tal efecto siendo motivo de expulsi&oacute;n del 
      Consejo y/o del Clan, dependiendo de la gravedad de la falta, el dar a conocer 
      estas deliberaciones de forma p&uacute;blica sin permiso especifico de dicho 
      Consejo. Esto se hace para que cada consejero pueda expresar su opini&oacute;n 
      libremente sin que repercuta su decisi&oacute;n sobre los dem&aacute;s miembros. 
      <br> <br>
      El Consejo tiene facultad para el nombramiento directo de cuales cargos 
      as&iacute; lo determine oportuno pero prefiri&eacute;ndose siempre que sean 
      elegidos libremente en votaci&oacute;n por el resto de los DHs entre los 
      candidatos que re&uacute;nan las condiciones oportunas. <br> <br>
      El Consejo ser&aacute; el &uacute;nico facultado para iniciar votaciones/encuestas 
      en la lista General. Siendo el resultado de estas votaciones un indicativo 
      de la voluntad expresada de todos los DHs y ateni&eacute;ndose al resultado. 
      Si un DH quiere poner una votaci&oacute;n en particular deber&aacute; expres&aacute;rselo 
      as&iacute; al consejo para que esta decida si procede y por lo tanto crear 
      la votaci&oacute;n o no. <br> <br>
      Los Consejeros podr&aacute;n expresarse libremente a titulo personal en 
      todas las listas o foros de los DHs independientemente de cuales sean pero 
      no hablando a titulo del Consejo a no ser que &eacute;ste as&iacute; les 
      faculte. Es conveniente que todos los Consejeros est&eacute;n y revisen 
      peri&oacute;dicamente todas las listas de los DHs para estar informados 
      de todo lo que sucede en el clan y as&iacute; adelantarse a los deseos y 
      corrientes de opini&oacute;n de los DHs y poder tratar los temas en profundidad 
      y con conocimiento de causa. <br> <br>
      Adicionalmente, dentro del canal usado para la comunicaci&oacute;n de los 
      miembros del consejo, deben ingresar los GMs de aquellos juegos en los que 
      haya representaci&oacute;n DH y sean los mayoritariamente jugados por los 
      miembros del clan.. De esta forma los GMs pueden resolver sus dudas sobre 
      la aplicaci&oacute;n de las normas generales a sus juegos concretos y/o 
      presentar cambios que vean necesarios o positivos. Los GMs tendr&aacute;n 
      voz, pero no voto en las decisiones del consejo y est&aacute;n sujetos a 
      las mismas normas y derechos que los consejeros en cuanto a la privacidad 
      y libertad de expresi&oacute;n del canal. La violaci&oacute;n de estas normas 
      por parte de los GMs acarrear&aacute; en primer t&eacute;rmino la expulsi&oacute;n 
      del canal del consejo y la p&eacute;rdida de su cargo como GM. Pudiendo 
      llegar a la expulsi&oacute;n del Clan dependiendo de la gravedad de la falta. 
      <br> <br>
      <strong>2&ordm; Otros Cargos</strong><br> <br>
      <em> &nbsp;&nbsp;&nbsp;&nbsp;A) Moderador de las listas de correo. (o foros)</em><br>
      El/los moderador/es de todas o cada una de las listas ser&aacute; el encargado 
      de mantener el buen orden y educaci&oacute;n dentro de ellas, teniendo facultad 
      para moderar todos los mensajes excepto los del Consejo. En el caso sea 
      necesaria una expulsi&oacute;n temporal de un miembro de una lista o foro 
      el Moderador de dicho Foro tiene la facultad de hacerlo sin pedir permiso 
      al Consejo. En el caso de expulsi&oacute;n deber&aacute; informarse y seguir 
      las directrices del Consejo. Ser&aacute; elegido entre los candidatos que 
      se presenten por el Consejo. <br>
        <br>
      <em>&nbsp;&nbsp;&nbsp;&nbsp;B) Relaciones P&uacute;blicas del Clan.</em><br>
      Ser&aacute; el encargado de mantener las relaciones con otros clanes y de 
      cuidar nuestra imagen con ellos. Puede ser una persona espec&iacute;ficamente 
      destinada para cada juego en particular o bien para el clan en general, 
      aunque todos y cada uno de los DHs ya sean miembros permanentes o puntuales 
      son responsables del buen nombre de &eacute;l clan siendo motivo de expulsi&oacute;n 
      el fallar en este cometido como ya se expuso en el punto &#8220;h&#8221; 
      en los Derechos y Deberes. Ser&aacute; elegido entre los candidatos que 
      se presenten por el Consejo. <br>
        <br>
        <br>
      <em>&nbsp;&nbsp;&nbsp;&nbsp;C) Webmaster</em><br>
      Ser&aacute; el encargado de mantenimiento de la p&aacute;gina Web y Foros 
      del clan, pudiendo para ello recabar la ayuda particular de algunos miembros 
      aunque se espera que todos los DHs contribuyan a su mantenimiento. Ser&aacute; 
      elegido entre los candidatos que se presenten por el Consejo. <br>
        <br>
      <em>&nbsp;&nbsp;&nbsp;&nbsp;D) Censor del clan</em><br>
        Ser&aacute; el encargado de mantener al d&iacute;a el Censo del clan, 
        y de dar de alta y de baja a los miembros DHs (en conjunci&oacute;n con 
        el Moderador de la lista General) Para ello podr&aacute; recabar la ayuda 
        de otros miembros en cada lista puntual. Ser&aacute; el encargado de informar 
        al Consejo de cada miembro que decida entrar en el clan definitivamente 
        y de los antecedentes de esa persona. As&iacute; de informar a esa persona 
        de los pasos a seguir. Ser&aacute; elegido entre los candidatos que se 
        presenten por el Consejo. Idealmente ser&aacute; un miembro del propio 
        Consejo ya que deber&aacute; trabajar en estrecha colaboraci&oacute;n 
        con el, aunque no es imprescindible. <br>
        <br>
      <em>&nbsp;&nbsp;&nbsp;&nbsp;E) GMs de cada juego.</em><br>
      Ser&aacute; elegido por todos los miembros de los DHs que estar&aacute;n 
      en ese juego, pero debido a las caracter&iacute;sticas del puesto y para 
      que no se elija al GM precipitadamente y sin ser demasiados para poder elegir 
      entre un n&uacute;mero suficiente de candidatos, es preferible que sea elegido 
      con anterioridad al lanzamiento del juego entre aquellos que demuestren 
      suficiente inter&eacute;s y su intenci&oacute;n de jugarlo durante el mayor 
      tiempo posible para evitar andar cambiando peri&oacute;dicamente de GM. 
      Al ser el representante de los DHs y espec&iacute;ficamente del Consejo 
      en cada juego y tener que ser una persona de absoluta confianza, de todos 
      los candidatos y tras una votaci&oacute;n general entre los DHs que vayan 
      a estar en dicho juego, el consejo tomar&aacute; la &uacute;ltima decisi&oacute;n 
      en cuanto a la persona ideal para dicho cargo. Salvo razones muy poderosas 
      el consejo se limitara a ratificar el nombre de aquel DH elegido en votaci&oacute;n 
      para GM del juego. En todo momento el GM de cada juego ser&aacute; un miembro 
      permanente de los DHs siendo este el &uacute;nico cargo al que no podr&aacute;n 
      acceder los miembros puntuales en cada juego de los clanes pertenecientes 
      a los DHs. Esto es para salvaguardar la identidad com&uacute;n de cada clan 
      asociado e identificable con los DHs. <br>
        <br>
        Todos estos cargos tendr&aacute;n una duraci&oacute;n m&aacute;xima de 
        1 a&ntilde;o, pudi&eacute;ndose renovar tras ese periodo tras votaci&oacute;n 
        o nombramiento directo del Consejo y si la situaci&oacute;n lo requiere 
        y hay quejas sobre la capacidad de la persona que ocupa cada cargo se 
        podr&aacute; apelar al Consejo para que cambie el ocupante de cada cargo. 
