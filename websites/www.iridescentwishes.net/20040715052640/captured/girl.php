Let's first address the issue of why I write. I write to find a catharsis of my emotions, the good and the bad. I find that I can relax better after I put my feelings down on paper or my case via digital magic of the computer. I've been a writer since the young age of twelve-going-on-to-thirteen. Back then I wrote incredibly sappy and BAD poetry. During the years between sixth-grade to my junior year in high school I wrote sporadically. In my junior year in high school, my English Enriched teacher challenged the class to improve on our individual writing skills through his pop essay quizzes. The class proved to be a challenge and I improved the hard way, earning my first B on my transcript. I came out of the class a better writer, more prepared for the next level of English and the realization of how much I like to write. What I write comes straight from my imagination. I mainly write in the fantasy genre and tend to focus on a nature-ish theme, pain, sorrow, new beginnngs and strength in women in my writings. But as time progress, I will hopefully branch away from the fantasy genre and try something new. <br>
<br>
<br>
Hmm..Now some more in depth information about me.<br>
<br>
Many people will label me as friendly and outgoing. To an extent that is true but I am a person who does not like to be put into a nice package for the convience of others. I am known for being quick with a smile but in a second I can turn on someone. <br>
<br>
I have very strong beliefs and I love to debate with my friend, Christina, about "hot topics" such as abortion and the death penalty. Debating helps me organize my beliefs into somewhat clear-cut fashion. In doing so, I can find the faults in my own thinking and either learn to strengthen my flaws or accept new ideas. One of my strongest beliefs is staying true to oneself. I cannot stand hyprocrites, and in organizing my beliefs and thoughts, I believe that I can lessen my own chances of being one. One other thing I cannot stand are people who try to convert me to a certain religion. I do not follow any set path, I believe there is a God, or some unknown heavenly power. I define God/Goddess/whatever the unknown heavenly power is on my on terms. I also believe in the very basic learnings of witchcraft/Wicca. I believe that we must respect the earth, I agree with the "Wiccan Rede" and etc... Please let me be, do not try to convince me that my beliefs are wrong.<br>
<br>
I love to read, especially books that fall under the fantasy/science fiction genre. Currently, my favorite authors are Mercedes Lackey, Anne McCaffrey, Amelia Atwater-Rhodes and Tamora Pierce. The books I read can range from young adult to adult. What I do is pick up a book with an interesting title and read it.<br> 
<br>
I am also an anime fan. I love watching anime. What I have seen are Fushigi Yuugi, Chobits (Thanks Berry!), Full Moon wo Sagashite (Thanks CT!), Kare Kano, Strawberry Eggs, X/1999, Fruits Basket, Hunter x Hunter, Haibane Renmei (Thanks Berry!), parts of Tiny Snow Fairy Sugar, parts of Inu Yasha (up to episode 66), parts of Naruto, and parts of Prince of Tennis. I have leanings for the cutsy anime (as you can probably see) but I am open to suggestion.<br>
