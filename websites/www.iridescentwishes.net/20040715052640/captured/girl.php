That is my personal quote. Be true to yourself by following your own path. Do not let anyone persuade you that who you are, and what defines you is wrong. Let your own ideas show you the way. Be happy with your mind, body and spirit. Believe in hope and love. Without it, life becomes dull and gray. Once you find your path, follow it, do not look back and believe in all the possibilities open to you; that is when you will truly find happiness in life.<br>
