<strong>Planning</strong> your world is probably the most important thing you need to accomplish. Perhaps you already have an idea of how you want your world to look and thus this process will take little time. For some it will take longer, but planning now will save you a lot of time in the future.<br />
<br />
Your first task is to decide on the size of the terrain you want to edit. Will you simply be adding a mound or small hill in the center of your world, or would you prefer a mountain range, crater, caldera, or a mix of them all? Are you creating an island? A continent? How big is your world? <br />
<br />
I'm going to simplify the problem of planning and answer some of these questions for the purpose of this tutorial. We're going to use a p10 sized world and we're going to make the terrain p255 size. It will be a mountain range, or an island if we choose to activate water in the world.<br />
<br />
Our last decision will be what the terrain looks like. You have the ability to use up to 500 different terrain textures, but we will only be using a few of them. Will your terrain be grassy, rocky, icy or something completely alien? Also, unless you want hard edges between terrain changes, you will have to do some graphics blending to make transitional terrain textures.
<br />
