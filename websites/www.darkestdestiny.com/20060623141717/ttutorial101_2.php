For the purposes of this tutorial we will be making a greyscale bitmap file(.BMP) using your favorite graphics editor(i.e. Photoshop). Create a new file, and choose 512x512 pixels. Now, choose black as your current color and then use the bucket tool to fill in the background. Next, choose your paint brush tool and choose a brush of your choice, one that perferably does not have a sharp edge. Choose white or a shade of grey as your current color and then paint your hills and mountains. The WHITER an area is the higher the terrain will be and conversely the BLACKER an area is the lower it will be. Sharp variations of white to black will create steeper terrain changes and small white to grey to black variations will create smoother terrain.<br />
<br />
<img src="images/exampleterrain.bmp" alt="512x512 Greyscale Bitmap(.BMP)" width="128px" height="128px" />
<br />
Now, either copy the above example .BMP file(by right clicking it and SAVE PICTURE AS...) or saving the .BMP file you just made. Be sure to convert yours to greyscale first and save it(I always save at 8bit).<br />
<br />
After this tutorial, you may want to explore other options than making greyscale bitmaps. I've enjoyed exploring <a href="http://www.planetside.co.uk/terragen/">Terragen</a> quite a lot.

