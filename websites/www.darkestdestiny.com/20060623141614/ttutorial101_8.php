Now, lets build your terrain! Check mark <strong>BuildWithRobot</strong>, then type in your citizen number and privledge password. Do not type in your citizen password, it will not work. Next, type in the world name you are working on. In many cases you will not be working in the main Activeworld Universe, so you will have to aquire the Universe URL and Universe Port from their website documentation for running Bots. If you are in the main Activeworlds Universe, then the Universe URL is "auth.activeworlds.com" and use Universe Port 0!<br />
<img src="images/d2rs5.jpg" alt="Lets build it! Fill in the fields on the left then GENERATE!" width="630px" height="217px" />
Finally, select a <strong>Robot Building Speed</strong>. This will depend on your connection to the internet. I use a fairly fast speed, but if you notice that your terrain ends up with errors when you finally view it, you may be overloading the world server or data may be getting corrupted in transit. Try slowing down the speed to see if that helps. Click on the <strong>Generate</strong> button in the lower left of DEM2RWX, you should see a log window come up and start to build your terrain.

