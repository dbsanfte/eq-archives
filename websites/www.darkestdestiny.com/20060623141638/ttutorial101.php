So, you got yourself a new world or you're itching to redo your existing one and you'd like to terrain form it?(Terraform) If not, this is not the tutorial for you. This tutorial will show you step by step how to terrain form an AW world and what some of the options and possibilities might be. We will not get into making renderware(.rwx) terrain models which DEM2RWX can do. See <a href="http://www.andras.net/modular.html">IngieBee's tutorial.</a>
<br />
You will need some basic understanding of what a world is, how to use your OP(Object Path) and how to alter world settings in <strong>Options>World>Features</strong> from within the Activeworlds browser.
<br />
I'll assume you have at least the following: 1. The AW browser(>4.x). 2. A copy of DEM2RWX. 3. A Paint program capable of making greyscale .BMP files. 4. Access to your OP(Object Path). 5. The ability to edit terrain elevation in your world.<br />
