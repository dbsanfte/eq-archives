<img src="/xes/forum/templates/Chronicles/images/dropcap01/T.gif" align=left>he guildsite for the Crusaders of the High Plains began in Phase IV Beta, but the ingame guild was technically founded on April 21<sup>st</sup>, 1999. Dedicated as a guild where the members of the UO guild ARC [The Aardwolf Crusaders] could play together in EverQuest, CHP is the 40th guild established on Xegony making it one of the oldest guilds around. The secret of the guilds staying power is in it's aim, which is as true today as it was back in the days of our roots, "Our members are the best folks that online gaming has to offer."<br>
The face of CHP Leadership has seen many changes over the years. Originally, CHP was run by it's founders Suede, Cosgood and Rippley - who served as the Triumvirate. In time, guild leadership then became the responsibility of Suede and Ranillion, who founded and initially maintained the Guild Library which we use to this day. As CHP grew, the Leadership of the guild was passed onto Karna, Torgeirr and Liushing - under whose care and guidance CHP florished. With the advent of other MMORPG's, the reins were passed to Tafkat and Demented - who paved the way for the most recent face of CHP Leadership - the Circle of Four, a Council based leadership of Samnie, Mileron, Atatnev and Zarger who ran the guild for well over a year. The current face of CHP Leadership is a return to a Triumvirate structure - the three Guild Council members being Dragonheart, Dreddnought and Dugast.<br><br><a name="structure"></a>
