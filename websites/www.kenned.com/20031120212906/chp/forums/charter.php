The Guild Library is overseen by Lile and Kennet - who are the Head Librarians, and several dedicated members of the Guild who serve as the Guild Librarians of CHP. Together, they all run the Guild Library to accept items from members, handle withdrawals, and update the web site to maintain an accurate reflection of the ingame items available to the members of CHP.<br>


We ask only that items such as Gear and Weapons that are borrowed be returned when a character is done with them, so as another member of CHP may make use of them. All GL items are donated by the generosity of our members, so it's our aim to keep the GL items in the family. <br>


