<h1>Knights of Velious Charter</h1>

<small>Last updated Jan 25, 2001</small>
<p>

<em>What does it take to be a knight?</em>
<p>

Commitment, dedication, patience, maturity, honor, aggression, humility, courage, and above all putting guild
brethren above thyself. 

<ol>
<li>Our primary goals are to provide our virtual world with a family atmosphere where we can have fun, explore and develop our characters. We are an inherently good
guild, seeking chivalry and justice. We do not start wars; we fight to defend our honor and/or to protect the weak. We will accept all races, all classes and all
alignments. We are not a heavy role-playing guild, but it is encouraged, appreciated and respected. We will not accept players who cheat or hack. Power leveling is
not encouraged in our guild (however individuals may choose to do so on their own). 

<li>We do not allow spam begging. If you have to ask someone for something, IE some food, water, a few copper, then do it politely, and using the /t command. DO NOT
SPAM THE ZONE WITH SHOUTS. If the person says no, then say "Thank you" and move on. Even if you get a reply that is rude, just ignore it. Remember your actions
reflect on the rest of your guild mates. 

<li>The Knights always comes first. We give more than we take; we help out those who need it. We don't ask for much, but loyalty is a must. 

<li>Recruitment is a slow and careful process. We need sufficient time to evaluate potential candidates to insure their goals are in alignment with ours: this
process may take up to two weeks. However, when we do find someone with the same goals as us, we do not hesitate to bring them into the guild. If someone wishes to
join us, they will need to group up with us, to see if they are a good fit. If they are, then it should be apparent to both parties. 

<li>Upon initiation, KoV will give the recruit a sponsor (like a "Big Brother"). He/She will guide them toward their acclimation into the guild. Be aware, that we
do not desire guild-hoppers in our ranks. Guild-hoppers will never stay long enough to get promoted. When a recruit joins us, it is our hope that they'll stay with
us for their entire online gaming life. 

<li>Lastly, be respectful of your guild mates. Remember we have both newer members and veterans in this guild.So a question may sound silly to some of the old
timers, but remember everyone asked them when they were new. 
</ol>
