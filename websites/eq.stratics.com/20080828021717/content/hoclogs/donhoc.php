      <p><br><font face="Arial, Helvetica" size="2">
         <br>
        <font color="#0000A0"><b>Brekkee -</b></font> Good evening and welcome 
        to another great Stratics House of Commons chat, tonight our guests are 
        the developers of EverQuest! Tonights topic is the DoN expansion and 
        recent updates, so please limit your questions to those topics. <br>
        <font color="#0000A0"><b>Brekkee -</b></font> Please send your questions 
        to [QT]Ishu or [QT]Calelena - to do so type /query NICK and type in the 
        window that pops up. Questions sent to me or the developers will not make 
        it into the lineup, so don't bother. Full logs of the chat will be up 
        on http://eq.stratics.com shortly after the chat. <br>
        <font color="#0000A0"><b>Brekkee -</b></font> We'll start with a brief 
        introduction from the developers, now would be a good time to send those 
        questions. <br>
        <br>
        <font color="#800000"><b>Rashere -</b></font> Good evening all. I'm Travis 
        McGeathy, better known as Rashere. <br>
        <br>
        <font color="#800000"><b>Rytan -</b></font> Hi, I'm Ryan Barker also known 
        as Rytan, designer for EverQuest <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *Croix_FV* Question: What 
        will the approximate size of this expansion be? Like OoW and 8ish zones 
        plus instances, or more like PoP with 20+? <br>
        <font color="#800000"><b>Rashere -</b></font> Dragons of Norrath is similar 
        to Omens for zone count although some of the zones are quite spacious. 
        <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *litlebear* With the upcoming 
        guild halls, will there be any room for expansion, as in purchasable Merchant 
        NPCs, and maybe a guild arena? <br>
        <font color="#800000"><b>Rytan -</b></font> We've built the guildhall 
        in such a way that we'll be able to expand the functionalities provided 
        inside in the future. We've got a solid list of fun features for DoN, 
        but we'll be looking to add others as time goes on. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *Croix_FV* Question: Will 
        more details be released to the content of DoN soon? I.e. More spells? 
        AA's? Levels? <br>
        <font color="#800000"><b>Rashere -</b></font> Im glad you asked. Were 
        not doing AAs or levels with DoN, but you can expect more spells. In addition, 
        were introducing a new mission system to the game that will allow challenge 
        you to take your groups into the DoN content to further the cause of one 
        of the factions behind the story. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *sildonte* What will be 
        the level range that DoN is targeted for as far as new fighting zones? 
        <br>
        <font color="#800000"><b>Rytan -</b></font> The level range for DoN starts 
        at level 45 and goes on up through 70 at a variety of difficulties. There's 
        a pretty sizable difference between player powers with AAs and equipment, 
        and we've added content that should cover all of that range. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> Remember to get your questions 
        in to [QT]Ishu or [QT]Calelena , questions will be answered in the order 
        they are received, so send them in now! <br>
        <font color="#0000A0"><b>Brekkee -</b></font> *Rikayn* We haven't seen 
        anything much about the mission system. Can you elaborate? Will they be 
        like tasks or? <br>
        <font color="#800000"><b>Rashere -</b></font> The mission system uses 
        the task system basics, but is aimed at quests that your whole group does 
        rather than just a single person. The missions take place in instanced 
        versions of the expansion dungeons and run the gamut from sneaking into 
        areas to plant bombs to trying to stop your rival faction from aligning 
        with the denizens from the new areas. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *Mediik* How are guilds 
        going to be able to purchase these features? <br>
        <font color="#800000"><b>Rashere -</b></font> The guild features are immediately 
        available upon the launch of the expansion and only the corpse summoning 
        and guild portal will require platinum to use. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *Geez* what level are these 
        new zones aimed for? <br>
        <font color="#800000"><b>Rashere -</b></font> The zones begin at around 
        level 45 and work their way up from there. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *Bl-Pak* Question: What 
        is the main new idea that you are trying to introduce in DoN? <br>
        <font color="#800000"><b>Rashere -</b></font> We're expanding on a lot 
        of ideas with DoN including the guild features and the mission system. 
        In addition, we're reconnecting the ongoing storyline back to the original 
        lore. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> Rytan should be right back 
        :) <br>
        <font color="#0000A0"><b>Brekkee -</b></font> *Guest109847019371* Are 
        there any plans for adding to the current Tradeskills? <br>
        <font color="#800000"><b>Rashere -</b></font> I'm gonna channel Absor 
        here: There are plenty of new tradeskill recipes being introduced. In 
        addition, we're completely revamping the cultural armor. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *VarganMarrblade* Will 
        the guildhalls be placed in existing zones, or will there be new "build 
        zones" created for them to go up in, like with DAoC and AO? Or will be 
        using a system closer to the housing in EQ2? <br>
        <font color="#800000"><b>Rashere -</b></font> The guildhall consists of 
        two parts, the guild lobby and the guild hall itself. The guild lobby 
        is a zone off of Plane of Knowledge and is a normal zone that anyone can 
        enter. From there you can zone into your guild hall which takes you to 
        a zone that is only available to members of your guild. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *window* with the mission 
        system will there be purchaseable rewards, like LDoN? <br>
        <font color="#800000"><b>Rytan -</b></font> Yes the mission system makes 
        use of a system much like LDoN where completion of missions rewards a 
        type of corrency. This currency can be spent at special merchants for 
        items, spells, etc. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *Rikayn* You say "rival 
        faction" Does this mean guild wars or other forms of PvP? <br>
        <font color="#800000"><b>Rytan -</b></font> The rival factions actually 
        refer to two NPC factions that players can choose to align themselves 
        with. It doesn't indicate any kind of PvP specifically. There will be 
        times where you'll be running missions on both sides of the fence though. 
        <br>
        <font color="#800000"><b>Rytan -</b></font> for example, one mission might 
        have you protecting some dragon eggs, while another has you destroying 
        them. There won't be any actual PvP with other players though, it's strictly 
        a lore concept. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *nordwulf* Will DoN feature 
        some solo playable missions or tasks for those that do not have to much 
        time to play? <br>
        <font color="#800000"><b>Rashere -</b></font> The missions are built for 
        groups, but are tuned to take about an hour or so to go through so would 
        be perfect for someone without much time to play. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *Morncask-DRo* What will 
        happen to guildies corpses that don't have the expansion when using the 
        corpse altar? <br>
        <font color="#800000"><b>Rytan -</b></font> There's a bit of confusion 
        on how the altar works. The corpse summoning altar actually is in the 
        guild lobby, not the guild hall. Each player will have to summone their 
        own corpses, but it will summon all of their corpses through out the world. 
        <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *VarganMarrblade* Does 
        the cultural armor revamp mean that the Guktan Forge will finally have 
        a use? <br>
        <font color="#800000"><b>Rashere -</b></font> Absor says smash...er...yes. 
        <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *Rikayn* What rewards can 
        be expected from Missions? Boss mob type loot? Gold and xp? Faction? All 
        of the above? <br>
        <font color="#800000"><b>Rashere -</b></font> The mission system uses 
        a reward system that is similar to how LDoN worked. As you complete mission, 
        you will earn shards that you can use to purchase equipment from vendors. 
        <br>
        <font color="#800000"><b>Rashere -</b></font> The two different factions 
        give different types of rewards. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *Rikayn* So tell us more 
        about the two rival factions in DoN. Are they like the 3 factions in Velious? 
        <br>
        <font color="#800000"><b>Rytan -</b></font> There's 2 main factions in 
        DoN. There have been many clues that have gone out over the last few weeks, 
        I wouldn't want to spoil the story by saying too much. &gt;:) <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *Elphabas_FV* With the 
        purchases on the missions be isolated to the zone of origin like LDoN 
        or will one mission let you have currence to spend on any equipment in 
        the mission system? <br>
        <font color="#800000"><b>Rashere -</b></font> The two different factions 
        each give a different type of currency for completing their missions and, 
        in turn, only accept that currency at their vendors. But, the currency 
        is tradeable. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *Elphabas_FV* Sounds like 
        the altar is a major nerf to Necros and SK's. How do you see one of their 
        primary roles after such an adjustment? <br>
        <font color="#800000"><b>Rytan -</b></font> The corpse summoning costs 
        significantly more then having a necro or shadow knight summon your corpse. 
        So they still have an edge in that department. We thought it was important 
        to allow an easier method of getting your corpse back for the overall 
        health of the game. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *Gluestick* Will missions 
        be geared more towards higher levels (60+) or will they be useful at level 
        45? <br>
        <font color="#800000"><b>Rashere -</b></font> The missions start at around 
        level 55 and similar to LDoN, they scale based on the level of your group. 
        <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *Croix_FV* Part of the 
        idea is to "Advance the story" within the player community. However, will 
        these mean that certain things will be one time only, such as the recent 
        ToFS quest, and thus cater to established/powerful guilds? Or will the 
        quests be universal enough to give everyone a shot? <br>
        <font color="#800000"><b>Rashere -</b></font> You're referring to the 
        storyline playing out in-game right now. In this case, it is literally 
        events that are occuring in the world and once they are done, they don't 
        occur again. The missions and quests within the expansion are more traditional 
        and can largely be repeated however much you desire. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *Rikayn* Will the guild 
        hall also have separate private apartments? <br>
        <font color="#800000"><b>Rytan -</b></font> No, there aren't any private 
        apartments with the guildhall. Just the main hall that your whole guild 
        shares. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *Gluestick* You mentioned 
        new spells in DoN, would these be improvements to our current spells or 
        will you be introducing any new spell lines to certain classes? <br>
        <font color="#800000"><b>Rytan -</b></font> It's a mix of both. Some classes 
        are getting entirely new lines of spells, while others are getting upgrades 
        to existing lines. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *Rick* What do you mean 
        by returning to the original lore storyline? Can you tell us more? <br>
        <font color="#800000"><b>Rashere -</b></font> By this I mean that the 
        expansion is revisiting old Norrath and some of the factions that featured 
        heavily into the original game. The expansion areas, in fact, can be located 
        on the original map that came with the EverQuest box. Of course, the events 
        that have occured since then have taken their toll. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *Rikayn* Since missions 
        are like quests... can they be done over days or are they more like LDoN 
        2 hour chunks of time? <br>
        <font color="#800000"><b>Rytan -</b></font> Each individual mission is 
        contained into two hour cunks, but there are mission sequences that functio 
        much like quests that you can work up through over a period of several 
        days. <br>
        <font color="#800000"><b>Rytan -</b></font> Think of missions as potentially 
        segments of a larger quest. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *Rick* Will we find out 
        what happens with Lanys or is this the Dragon's story? Nagafen and Vox? 
        <br>
        <font color="#800000"><b>Rashere -</b></font> Lanys has been gathering 
        the armies of Overthere. To find out what happens, I recommend you keep 
        an eye on Dreadlands over the next week or so. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *Beerwen* Will the new 
        "Guild Bank" all storing of multiples of a lore item? <br>
        <font color="#800000"><b>Rytan -</b></font> The guildbank is only able 
        to hold a single lore item at a time. This of course doesn't count any 
        items that might be on individual guild members. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *Elphabas_FV* How will 
        the guild bank work? Number of slots etcetera? <br>
        <font color="#800000"><b>Rytan -</b></font> The guildbank holds a total 
        of 200 items which can be flagged by the guild banker in a variety of 
        ways to allow certain guild members to use them. <br>
        <font color="#800000"><b>Rytan -</b></font> For example, one of the flags 
        allows anyone who can actually use the item themselves to take it out 
        of the bank. <br>
        <font color="#800000"><b>Rytan -</b></font> You can also flag an item 
        for a specific person. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *Croix_FV* Since Overlord 
        Mata Muram was on his last legs, last I heard, is there raid content in 
        this expansion to satisfy this style of play? If so, how is it balanced 
        against group content? <br>
        <font color="#800000"><b>Rashere -</b></font> One word...Dragons. There 
        is a large amount of content, including the mission system, that is focused 
        on single groups. The dragons, though, are a force to be reckoned with. 
        <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *Gluestick* Can we expect 
        any new suprises in DoN, such as "Attuneable" items that were introduced 
        in OoW? <br>
        <font color="#800000"><b>Rashere -</b></font> There are a number of surprises 
        waiting for players in Dragons of Norrath...but they wouldn't be surprises 
        if I told you about them here. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *Croix_FV* Will instancing 
        and instanced zones factor heavily into this expansion, or will it be 
        mostly standard zones? <br>
        <font color="#800000"><b>Rashere -</b></font> The mission system makes 
        heavy use of instanced zones to provide structured quests that take place 
        in the expansion zones. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *Gluestick* Can we expect 
        a heavy amount of raid encounters to be implemented into DoN? <br>
        <font color="#800000"><b>Rytan -</b></font> There are several raid encounters 
        available in DoN, but it would be inacurate to say that there's a heavy 
        emphasis on it. <br>
        <font color="#800000"><b>Rytan -</b></font> The balance is much like Omens 
        of War in that respect. <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> *Croix_FV* Given the recent 
        zone revamps to zones that are underused, does SOE plan to make this a 
        common practice? If so, will there be graphical redesigns such as lavastorm? 
        <br>
        <font color="#800000"><b>Rytan -</b></font> Yes, we definitely want to 
        go back and use the new graphic engine to recreate the old areas of EQ. 
        And who knows, there might be some interesting events that go along with 
        those revamps. &gt;:) <br>
      </p>
      <p><font color="#0000A0"><b>Brekkee -</b></font> That will wrap it up for 
        tonight. Id like to thank the devs for coming! The log for tonights 
        chat will be up shortly on http://eq.stratics.com <br>
        <font color="#0000A0"><b>Brekkee -</b></font> Thank you all for joining 
        us! You can join #EverQuest to chat more about the game. <br>
        <br>
        <font color="#800000"><b>Rashere -</b></font> Thank you all for your time 
        and the great questions. <br>
        <br>
        <font color="#800000"><b>Rytan -</b></font> Thanks for coming everyone, 
        we hope you enjoy playing DoN! </p></td>
