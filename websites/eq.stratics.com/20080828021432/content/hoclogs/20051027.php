<br>
<br><font color="#800000"><b>Sprite -</b></font> Welcome to another great Stratics house of commons with the developers of Everquest! Tonight's topic will center on the Depths of Darkhollow and the October 27th Update, so please focus your questions on this topic.
<br><font color="#800000"><b>Sprite -</b></font> Please send those questions to [QT]Brekkee - to do so type /msg [QT]NICK My question here.  Please only send questions to our QT's as those sent to myself or the developers will not make it into the lineup.
<br><font color="#800000"><b>Sprite -</b></font> The full logs will be posted on http://eq.stratics.com after the chat.  We'll begin with a quick intro from our guests...now would be a good time to send in those questions!
<br><font color="#0000A0"><b>Rashere -</b></font> Good evening folks.  I'm Travis McGeathy, Lead Designer for EverQuest.
<br><font color="#0000A0"><b>Dev-Maddoc -</b></font> Hey everyone, welcome to the chat. I'm Jason Mash, an Assistant Lead Designer for the EqLive Team. 
<br><font color="#0000A0"><b>Zorillius -</b></font> Greetings!  I'm Craig Knapp the Producer of EverQuest.
<br><font color="#0000A0"><b>Prathun -</b></font> Good evening everyone.  I'm Jonathan Caraker, Designer for EverQuest.
<br><font color="#0000A0"><b>Vahlar -</b></font> Howdy! I'm Holly Longdale, designer on EverQuest.
<br>
<br><font color="#800000"><b>Sprite -</b></font> *Catmandew* with the new patch, my mouse won't work in full screen mode. What do I need to do to correct this?
<br><font color="#0000A0"><b>Zorillius -</b></font> The mouse problem we experienced today in full screen mode is actually a side effect of Microsoft's Operating Systems aplication compatibility database.  It's a bit too lengthy to go into detail here, but there are solutions posted on the boards and the Update Message and requires users to visit a Microsoft site.
<br><font color="#0000A0"><b>Zorillius -</b></font> It impacted approximately 1.5% of our users and we're still investigating a solution that bypasses this OS check.
<br><font color="#0000A0"><b>Vahlar -</b></font> I heard 1.43%, but who's counting? :)
<br>
<br><font color="#800000"><b>Sprite -</b></font> *Jetfire* Are there plans to make the High Level mistmore available to people and/or give a Lore reason for the change back?
<br><font color="#0000A0"><b>Rashere -</b></font> Mistmoore was reverted to its previous state largely because very few players were using the new form.  For the same reason, we don't currently have plans to reinstate that content although it is something we may look at again in the future.
<br>
<br><font color="#800000"><b>Sprite -</b></font> *Jetfire* Any ETA on when we can expect the Gift of Magic and Abundant Healing bug fixed?
<br><font color="#0000A0"><b>Rashere -</b></font> We don't have an ETA currently.  The stacking problems are due to some pretty core mechanics within the spell system and are taking some time to unravel.
<br>
<br><font color="#800000"><b>Sprite -</b></font> *Anon707* will splitpaw be changed back to its old form?
<br><font color="#0000A0"><b>Vahlar -</b></font> We don't have any immediate plans to make adjustments to Splitpaw, but we're always looking at ways to bring new experiences to players.  That said, the old world is always available to us for monster missions and other adventures with instanced content.
<br>
<br><font color="#800000"><b>Sprite -</b></font> *Emil* Do you all have any plans to address the growning Endurance Regen rate problems, for melee classes?  With Gen-2 Endurance abilities, and the fact that after a death/res Endurance starts off nearly empty -- the regen rate seems in dire need of help.
<br><font color="#0000A0"><b>Rashere -</b></font> We're investigating some ways to shorten downtime in general which should help with the Endurance regen problems as well.
<br>
<br><font color="#800000"><b>Sprite -</b></font> *Kwill* Do you feel that the griffon mission in west commonlands is now working as  you intended the missions to be?
<br><font color="#0000A0"><b>Prathun -</b></font> There are a few bug fixes going in for the griffon mission to address griffon hunters camping corpses and the guard dying prematurely.
<br><font color="#0000A0"><b>Prathun -</b></font> Aside from that, the mission is working as intended and has a much more balance Risk vs. Reward than the initial version. :)
<br><font color="#0000A0"><b>Prathun -</b></font> There's always the possibility of more changes to the mission in the future, but I wouldn't expect the mission to get any easier.
<br>
<br><font color="#800000"><b>Sprite -</b></font> *Jetfire* Given how late teh Hallowe'en events are starting this year, how long after Monday will they persist?
<br><font color="#0000A0"><b>Vahlar -</b></font> The events that are live now will continue until November 6.  So, you have plenty of time to get your "creep" on.
<br><font color="#0000A0"><b>Vahlar -</b></font> Of course, as always, we will bring you live content as often as possible.  
<br>
<br><font color="#800000"><b>Sprite -</b></font> *Pasty-Loral* can we get some examples of the changes we will see to the upgraded Depths loot in the next patch?
<br><font color="#0000A0"><b>Rashere -</b></font> We're still in the final stages of making these changes so I'm wary of giving any hard examples as they could change before this goes live.  The changes will be going to test within the next few days, though, so I'd encourage anyone interested to pop on over to test and check things out there.
<br>
<br><font color="#800000"><b>Sprite -</b></font> *Pasty-Loral* Any idea when we might expect more monster missions and what sorts of plans does SOE have in future monster mission goodness?
<br><font color="#0000A0"><b>Zorillius -</b></font> Monster Missions have been extremely popular for DoD and we love them internally as well. We have plans to release a few more before the turn of the year, but monster missions are something we plan on continuing from this point forward. 
<br>
<br><font color="#800000"><b>Sprite -</b></font> *Pasty-Loral* The new audio triggers is an excellent addition to Everquest.  Invis-breaking clerics thank you for it.  Are there plans for any other cool new systems in the near future?
<br><font color="#0000A0"><b>Vahlar -</b></font> On what seems like a daily basis there are discussions on how we can improve gameplay. Audio triggers is one example of that effort.  So, yes, we will continue to add as many new features, large and small, as we can to make the game more fun and accessible for everyone.
<br>
<br><font color="#800000"><b>Sprite -</b></font> *Jetfire* I've got friends who want to try EQ beyond the tutorial, but are balking at the current cost to get fully up to date. Any idea when a bundle pack is coming and what will be in it?
<br><font color="#0000A0"><b>Zorillius -</b></font> A new compilation pack is currently in the works. There isn't an "official" release date yet, but you can expect it within the next 6 months.  Players who use Escape to Norrath can actually buy compilation packs off of our Station Store at a significantly lower cost than what they've historically been.
<br>
<br><font color="#800000"><b>Sprite -</b></font> *Pasty-Loral* At the summit, Zorellius brought up the topic of death and resurrection, are there plans to change how death works in Everquest to make it more appealing to the new MMO audience?
<br><font color="#0000A0"><b>Rashere -</b></font> We're always looking at ways to improve the game.  For death specifically, we don't have plans for any changes at the moment, but its definitely something we are looking at and you may see some changes to how death works in the future.
<br>
<br><font color="#800000"><b>Sprite -</b></font> *Anon793* Are there plans on addressing some outstanding tradeskill issues.  Specifically, fletching paths to 300 for non-wood elf/non-karana worshipers and tailoring paths to 300 for just about everyone?
<br><font color="#0000A0"><b>Dev-Maddoc -</b></font> Yes, we do have some plans to address that. We're documenting some ideas currently that would address that specifically, as well as a couple of other concerns with Fletching. 
<br>
<br><font color="#800000"><b>Sprite -</b></font> *Jetfire* I don't want to harp on the Mistmore changes too much, but you've done so much good Lore-wise with DoDH and DoN. But this change to Mistmore came out of nowhere, and has no in game explanation; could at least some explanation be added to satisfy the lore-ists?
<br><font color="#0000A0"><b>Vahlar -</b></font> In my scouring of our Lore board, I have seen the questions about the lore behind the Mistmoore change and it's been on my mind. I don't want to leave you hanging, so you will learn why the changed happened.  All good things come to those who wait...a bit.  As always, your servant &lt;cough&gt;, Vahlar.
<br>
<br><font color="#800000"><b>Sprite -</b></font> *Pasty-Loral* How does the dev team feel about the current progression of top-end uberguilds and their progress through the Demi-Plane of Blood?  are they ahead of par? behind par? Right on target?
<br><font color="#0000A0"><b>Prathun -</b></font> A similar question came up in the last Livechat and at that time, the raiding guilds were chewing through the content at a breakneck speed and closing in on Dreadspire and the Demi-Plane of Blood.
<br><font color="#0000A0"><b>Prathun -</b></font> Now that raiders have broken into the final zone, that momentum has slowed down a bit. :)
<br><font color="#0000A0"><b>Prathun -</b></font> I'm happy with the current progression of the uberguilds and how far they've come along.  I think they've needed something more challenging than anything that's come before, something they can sink their teeth into until the next raid expansion, and Demi-Plane is more than up to that task.
<br><font color="#0000A0"><b>Prathun -</b></font> In the interests of teasing the raiding community a little, the short answer to your question is that they're slightly behind par. ;)
<br><font color="#0000A0"><b>Vahlar -</b></font> As we sometimes say in these parts..."Blam!"
<br>
<br><font color="#800000"><b>Sprite -</b></font> *Pasty-Loral* The addition of the new mission progression quest in Undershore is great.  Will we see more mission progression paths, perhaps a monster-mission progression path, in the future?
<br><font color="#0000A0"><b>Rashere -</b></font> Yeah, we're particularly happy with the way the mission arcs turned out and their ability to really convery an overall story.  That's definitely an area we plan to do more of in the future, including expanding it to monster missions.
<br><font color="#0000A0"><b>Vahlar -</b></font> It's been a lot of fun for us to create player involvement with the storylines so you can experience history first-hand, as it were.  We're glad the feedback has been so good.
<br>
<br><font color="#800000"><b>Sprite -</b></font> *Pasty-Loral* Any word on monster "Events" or Monster "raids"?
<br><font color="#0000A0"><b>Vahlar -</b></font> With the success of the monster missions and our ability to build on the feature, yes, we have been scheming and plotting ways to broaden it to include different types of events.
<br>
<br><font color="#800000"><b>Sprite -</b></font> That will wrap it up for tonight. Id like to thank the developers for coming! The log for tonights chat will be up shortly on http://eq.stratics.com
<br><font color="#800000"><b>Sprite -</b></font> Thank you all for joining us! You can join #everquest to chat more about the game.
<br><font color="#0000A0"><b>Dev-Maddoc -</b></font> Thanks for coming everyone! :) 
<br><font color="#0000A0"><b>Zorillius -</b></font> Thanks for joining us, have a good evening!
<br><font color="#0000A0"><b>Prathun -</b></font> Thanks all, have a good evening. =)


