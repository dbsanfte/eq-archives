<br>
<br><font color="#800000"><b>Brannoc -</b></font> Welcome to the another great Stratics house of commons with the developers of Everquest! Tonight will be a general chat so feel free to ask any questions about the game.
<br><font color="#800000"><b>Brannoc -</b></font> Please send those questions to [QT]Cale - to do so type /msg [QT]Cale My question here.  Please only send questions to our QT's as those sent to myself or the developers will not make it into the lineup.
<br><font color="#800000"><b>Brannoc -</b></font> The full logs will be posted on http://eq.stratics.com after the chat.  We'll begin with a quick intro from our guests...now would be a good time to send in those questions!
<br><font color="#0000A0"><b>Kytherea -</b></font> Good Evening all, I am Christina Delzer the EverQuest Community Relations Manager
<br><font color="#0000A0"><b>Rashere -</b></font> Evening folks.  I'm Travis McGeathy, Lead Designer for EverQuest.
<br><font color="#0000A0"><b>Raghnell -</b></font> Howdy, I am Chris Lena, the brank spanking new Producer of EverQuest
<br><font color="#0000A0"><b>Dev-Maddoc -</b></font>  Welcome everyone, I'm Jason Mash, an Assistant Lead Designer on the EverQuest team. 
<br><font color="#0000A0"><b>Rytan -</b></font> Hello! I'm Ryan Barker, Assisant lead for Everquest.
<br><font color="#0000A0"><b>Ngreth -</b></font> Hello, I am Adam Bell, Associate Game Designer for Everquest
<br>
<br><font color="#800000"><b>Brannoc -</b></font> *Nolrog* Are there plans on adding recipes for Taelosian Wheat/mountain wheat?  They were added to the game with GoD, but never used in a recipe.  There is an ongoing thread on the artisan-2019s way titled Gates of Discord Tradeskill still not complete if you need recipe ideas.  ;-)
<br><font color="#0000A0"><b>Dev-Maddoc -</b></font>  Yes, we will be adding recipes that use the Taelosian wheat in future recipes. :)
<br>
<br><font color="#800000"><b>Brannoc -</b></font> *Loral* How does SOE feel about the popularity of monster missions?
<br><font color="#0000A0"><b>Rashere -</b></font> We've been very pleased with the popularity of Monster Mission.  People have really flocked to them, but most importantly the missions have remained very popular after the last patch which removed the incentive for afk players to tag along while others did the missions.
<br>
<br><font color="#800000"><b>Brannoc -</b></font> *Loral* Care to elaborate on the rumors of a "Prophecy of Ro" expansion?
<br><font color="#0000A0"><b>Raghnell -</b></font> We cannot confirm nor deny the rumors of a future EverQuest expansion. I can't imagine where you would get such a notion.
<br>
<br><font color="#800000"><b>Brannoc -</b></font> *Loral* When might we see improvements to the point costs for LDON equipment from levels 20 to 50?
<br><font color="#0000A0"><b>Rashere -</b></font> We don't have a time frame for that change currently, but its definitely on the list of things we want to do.  More likely, the change will be to the point rewards coming out of the adventures rather than the cost of the items.
<br>
<br><font color="#800000"><b>Brannoc -</b></font> *Phillip09* Is there any possiblity of adding extra aa abilities for those who are maxed out on all the others?
<br><font color="#0000A0"><b>Rashere -</b></font> I'm sure we'll be adding more AAs in the future.  They seem to be somewhat popular with our players. :)
<br>
<br><font color="#800000"><b>Brannoc -</b></font> *Nolrog* Are there plans on increasing the stack size on selected tradeskill items, such as blue diamonds, celestial essence, spider silks/swatches, spiderling silks/threads, etc?
<br><font color="#0000A0"><b>Dev-Maddoc -</b></font>  Yes, this is something that's on our list of things to review and is partially the reason we got the stackability changes made to begin with.
<br><font color="#0000A0"><b>Dev-Maddoc -</b></font>  Making this adjustment for ammo was a relatively easy and painless change. Items that are used as source-items in tradeskills are not as straight forward, so we'll need to look at them on an item by item (and likely on a skill by skill) basis. It's not an overly difficult thing to do, it just takes a little time. It's entirely likely that these changes will make it out in smaller chunks, likely on a skill-by-skill basis.
<br>
<br><font color="#800000"><b>Brannoc -</b></font> *Deltree* With OMM exploit that have been found to beat him up easier, I still haven't seen that many guild who beet him up, any chance or nerfing him maybe now that 2 expansion have already been out since him.
<br><font color="#0000A0"><b>Rashere -</b></font> OMM still gets beaten fairly regularly.  We don't have plans to change his difficulty right now.  He's likely tougher than he needs to be, but if your guild can take him down, you've earned the bragging rights.  I like him in that state, particularly because he's not required to progress further into the game.
<br>
<br><font color="#800000"><b>Brannoc -</b></font> *Loral* Few groups seem to hunt in DoD overland zones these days.  Why do you suppose that is? Should it be fixed, and if so, how?
<br><font color="#0000A0"><b>Rytan -</b></font> DoD was definitely designed with a large amount of mission content involved, and the layout of the zones reflects this.  I dont think the disuse of the overlands zones means theirs anything necessarily wrong with them, but rather that the mission content is so good.
<br><font color="#0000A0"><b>Rytan -</b></font> However, there's a balance to be struck between static and instanced content and we're taking the time to decide where exactly the balance should be for future content.
<br>
<br><font color="#800000"><b>Brannoc -</b></font> *Skottee* is there any plan to continue the class reenvisionment? any estimated timeframe on the next phase of that?
<br><font color="#0000A0"><b>Rashere -</b></font> The re-envisioning doesn't really have phases.  It's an ongoing process that we are continually working on.  It's also not something that is likely to ever be done.  As long as the game is evolving, we'll be continually tweaking and tuning balance between classes.  On this note, the new Mana Burn for wizards will be available on test after the next test patch so please hop on and check it out.
<br>
<br><font color="#800000"><b>Brannoc -</b></font> *Nolrog* Are there any plans to address the ever growing shortage of bank space?  Tradeskillers are notorious pack rats, keeping anything that has the tradeskill tag for future use, which eats up bank space quickly.
<br><font color="#0000A0"><b>Dev-Maddoc -</b></font>  This is something that has been mentioned before and seems specific to tradeskill-components. The first thing that we plan on doing is increasing the stack size on tradeskill components, such that what you do have stored in there will take up significantly less space than it did previously. Once that's complete, we'll re-evaluate the need for bank-space on a more global level.
<br>
<br><font color="#800000"><b>Brannoc -</b></font> *slacklikeme-DB-DRo* What type of information will be in eqplayers.com? Will there be rankings like eq2players, and will those include the nifty stats like NPC's with the most kills?
<br><font color="#0000A0"><b>Raghnell -</b></font> Yes indeed.  EQPlayers will have leaderboards and rankings on things like highest HP and Mana by class.  Lots of cool stuff.  And we will be adding a lot more in the future.
<br><font color="#0000A0"><b>Raghnell -</b></font> You will be able to check it out tomorrow.
<br>
<br><font color="#800000"><b>Brannoc -</b></font> *Nolrog* Are there any plans to allow mass (bulk) combines (say 5 or 10 at a time) for certain combines (celestial essence comes to mind.)??
<br><font color="#0000A0"><b>Dev-Maddoc -</b></font>  There have been some discussions on this subject that have ranged from a "quick-fix" to a more in-depth change that we've been discussing. Neither of them have been decided on just yet, but it's something that we're looking into. In the short term, we may look into adding a couple new recipes for the biggest offenders, but it's not something we'll do for all refinement recipes.
<br>
<br><font color="#800000"><b>Brannoc -</b></font> *Nolrog* Are there any alternative ways planned to help tradeskiller find rare items on vendors, buy allowing stock to rotate, so that the most recent items sold to them are kept?
<br><font color="#0000A0"><b>Dev-Maddoc -</b></font>  Tanker spent some time looking into this not too long ago and discovered a few gotcha's in the Merchant code that make this more than a one-day project. 
<br><font color="#0000A0"><b>Dev-Maddoc -</b></font>  In talking about it with his lead, it was decided that the solutions to these problems were both too time-consuming for right now, and contained side-effects that would need full review before this was done. The alternatives to these solutions also need to be discussed and reviewed for potential side-effects before deciding upon the best course of action. It's certainly something that we've not forgotten and are still looking into.
<br>
<br><font color="#800000"><b>Brannoc -</b></font> *Jetfire* Are there any plans to add in Racial or Religious AA's?
<br><font color="#0000A0"><b>Rashere -</b></font> This comes up quite a bit.  The current system doesn't support the ability to give out AAs by race or religion, but it is something that we keep thinking about doing so I wouldn't be surprised to see it show up some day.  No plans at the moment, though.
<br>
<br><font color="#800000"><b>Brannoc -</b></font> *Nolrog* Are there any plans to revisit the Omens task system to make the tasks more appropriate for the classes and levels that get them?
<br><font color="#0000A0"><b>Rytan -</b></font> The current mission system is an evolution of the original task system.  Any upgrades we do on the content that exists in the old task system would be time better spent creating new content using the new system.  Wed definitely like to update as much of the old content as possible, but its down to time constraints limiting how much we can undertake.
<br><font color="#0000A0"><b>Rytan -</b></font> There's a lot of functionality in the new system that we just can't replicate in the task system, so we don't expect to be making further use of it in the future.
<br>
<br><font color="#800000"><b>Brannoc -</b></font> *Nolrog* Have you considered opening up Kunark and Velious (and perhaps Luclin as well) to everyone that doesn't already have it, so that you can start to build expansions off those zones instead of the old world?
<br><font color="#0000A0"><b>Raghnell -</b></font> We have already started building content off of Kunark and Velious.  The stats show that 99% (give or take) of folks have those expansions.
<br><font color="#0000A0"><b>Raghnell -</b></font> And Titanium in January should make it even easier.
<br><font color="#0000A0"><b>Raghnell -</b></font> There is a lot of world out there and we plan to use it.
<br>
<br><font color="#800000"><b>Brannoc -</b></font> *Deltree* Any chance that the drop rate of the Zun tongue drop more often in Tacvi...I've seen guild that clear Tacvi all teh time and only one tongue drop on about 3-4 clear sometimes...
<br><font color="#0000A0"><b>Rashere -</b></font> The Zun tongue dropping in Tacvi was mainly to help out people who didn't want to farm the Txevu Zun.  The drop rate is already a decent amount, but randomness being what it is, you're going to have some streaks where you get it off all of them and some where it doesn't show up for a while.
<br>
<br><font color="#800000"><b>Brannoc -</b></font> *Loral* Who is the endboss to DoD?  Tell tell!
<br><font color="#0000A0"><b>Rashere -</b></font> We were going to keep it a secret, but since you asked...Fippy Darkpaw! (You really expected an answer?)
<br>
<br><font color="#800000"><b>Brannoc -</b></font> *Skottee* are you able to comment at all on whether subscriptions are up recently? there's a lot of talk of old players coming back (myself included) but still many threads on the eqlive forums that say 'eq is dying' which it clearly isn't, we're just wondering if subscriptions are up since DoD?
<br><font color="#0000A0"><b>Raghnell -</b></font> We can't comment on subscription numbers (I have bosses too!) but I can definitely say that we have seen some old players return.
<br><font color="#0000A0"><b>Raghnell -</b></font> EQ is most definitely not dying.  The community and the game is very strong.
<br><font color="#0000A0"><b>Raghnell -</b></font> Not to be too corny but I am very excited about seeing EverQuest grow over the next decade.
<br>
<br><font color="#800000"><b>Brannoc -</b></font> *Deltree* Any plans of doing something with LDON points that are rotting...maybe a way trade them for something or being able to resale them for money or something like the way DoN Crystal work....Thanks
<br><font color="#0000A0"><b>Kytherea -</b></font> Also remember that people love to complain so you may see many more negative posts then positive ones on the boards.  Keep in mind too a small portion of the EverQuest community frequents and posts on the boards.
<br><font color="#0000A0"><b>Rashere -</b></font> The LDoN reward system was designed and balanced as a closed system.  The rewards you got were based around doing LDoN adventures just like the DoN rewards were based around doing DoN missions.  It's why we used two different reward systems.  At this time, we don't have any plans to set up a way to change points from one into points for another.
<br>
<br><font color="#800000"><b>Brannoc -</b></font> *player24523* think there will be a fabled velious this march for 7th anniversery?
<br><font color="#0000A0"><b>Rashere -</b></font> It's quite likely that the Fabled NPCs will make a return for the 7th anniversary and may well expand their way into Velious.
<br>
<br><font color="#800000"><b>Brannoc -</b></font> *Phillip09* Is there any plan to revamp home cities to make them a center of questing and advancement rather than the ghost towns they are today?
<br><font color="#0000A0"><b>Raghnell -</b></font> We are going to continue to revisit the existing world and that includes cities.  I can't say much more on that right now.... :-)
<br><font color="#0000A0"><b>Raghnell -</b></font> Just keep in mind that redoing a city is a HUGE task.
<br>
<br><font color="#800000"><b>Brannoc -</b></font> *slacklikeme-DB-DRo* How is the anti-hacking stuff you were working on progressing along? And once it goes active will we begin seeing like a # banned this week type thing?
<br><font color="#0000A0"><b>Raghnell -</b></font> The anti-hacking work we are doing is going very well.  We do want to be methodical about its application to make sure we do not punish our virtuous and loyal players.
<br><font color="#0000A0"><b>Raghnell -</b></font> I'm not sure we will be publishing exact numbers in the future but it is something we will be looking at.
<br>
<br><font color="#800000"><b>Brannoc -</b></font> *Nolrog* Are there any plans to revisit the drop rates for DoN cultural (grandmaster cultural quest items, glossy drake hides, metallic drake scales)??
<br><font color="#0000A0"><b>Ngreth -</b></font> Yes, we do plan on reviewing the drop rates of the Don cultural items.  We have no exact timetable for it, but it is on the top of my list.
<br>
<br><font color="#800000"><b>Brannoc -</b></font> That's it for this chat folks, thanks for joining us here on Stratics IRC - the logs will be posted shortly on http://eq.stratics.com
<br><font color="#0000A0"><b>Kytherea -</b></font> Thanks for coming everyone!  Have a great night.
<br><font color="#0000A0"><b>Rashere -</b></font> Thanks for coming everyone and have a good evening.
<br><font color="#0000A0"><b>Ngreth -</b></font> Thanks!  Have fun in Norath!
<br><font color="#0000A0"><b>Raghnell -</b></font> Good night everyone
<br><font color="#0000A0"><b>Rytan -</b></font> Thanks for coming everyone!
<br><font color="#0000A0"><b>Dev-Maddoc -</b></font>  Thanks for dropping in. :)
<br><font color="#0000A0"><b>Dev-Maddoc -</b></font>  Have a good night.

