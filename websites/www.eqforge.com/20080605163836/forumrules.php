	<p><b>Respect the authority of moderators</b><br />
       Public discussions of moderator actions are not allowed on the forum. It is also prohibited to protest moderator actions in titles, avatars,  signatures, or any other profile information. If you don't like something that a moderator did, PM or email the moderator and try your best to resolve the problem or difference in private. If it's not resolved or if you don't receive a reply from the moderator after 24 hours, you can then PM or email <a href="http://forums.civfanatics.com/member.php?u=1">Thunderfall</a>.</p>
<p>Moderator Tags should never be edited or removed, nor should they try to be emulated or copied. Doing so can result in an automatic ban.</p>
       
