      <p><b>What can't you post?</b><br />
You agree, through your use of this service, that you will not use this forum to post any material which is knowingly false and/or defamatory, inaccurate, abusive, vulgar, racist, hateful, harassing, obscene, profane, sexually oriented, threatening, invasive of a person's privacy, or otherwise violative of any law. You agree not to post any copyrighted material unless the copyright is owned by you or by this forum.</p>

       <p><b>Use descriptive subject lines &amp; research your post.</b><br />
This reduces the chances of double-posting and it also makes it easier for people to see what they do/don't want to read. Also, scan the subjects of the last several days' posts to make sure you aren't duplicating posts.</p>
      
       <p><b>Keep the focus.</b><br />
Each forum has a focus on a certain topic. Questions outside the scope of a certain forum will either be moved to the appropriate forum, closed, or simply be deleted. Please post your topic in the most appropriate forum.</p>

       <p><b>Zero tolerance on piracy.</b><br />
Posting links to warez sites and program cracks (including no-CD crack) are not allowed on the forum. Anyone found guilty of participating in piracy activity on the forum will be punished.</p>

      <p><b>Spam is not tolerated.</b><br />
      Spam is considered posting topics or messages on our forums that contain no point, relevance, or contain subject matter that doesn't directly allow other member participation. Topics directed to a particular user, posting several times consecutively as an appendage to ONE post, and posts in threads that have NOTHING to do with the subject matter are all considered spam and will be deleted on sight. Contributing to/or intentionally posting spam will not be tolerated. </p>
      
      <p><b>Keep your signature brief.</b><br />
      This forum has a signature feature that allows you to "sign" your posts with a customized block of text. The text may include links to other sites (as long as they are within the forum rules) and emoticons. No derogatory references to other members, spoiler tags, or quote tags are allowed in signatures.</p>

<p>Signatures are limited to 5 lines of regular (size 2) text. Using smaller text (size 1) allows 7 lines of text. Larger text (size 3) allows 3 lines of text. No text size larger then size 3 should be used. The number of lines for any text size includes blank lines and room used by emoticons and is evaluated at 1024x768 resolution.</p>

       <p><b>Multiple accounts are not permitted.</b><br />
      Create one account, make sure you're satisfied with the name, and keep it. Be sure to choose your user name carefully. If you wish to change your user name for whatever reason, please contact <a href="http://forums.civfanatics.com/private.php?do=newpm&u=1">Thunderfall</a>.</p>

	<p><b>Postcount titles &amp; avatars.</b><br />
       Each poster has a title and an optional avatar image shown below his/her username. The default titles are Chieftain, Warlord, Prince, King, Emperor, and Deity. The minimum postcounts required are listed below:</p>
     
       <blockquote>
        Chieftain (Minimum Posts: 0)<br />
        Warlord (Minimum Posts: 100)<br />
        Prince (Minimum Posts: 300)<br />
        King (Minimum Posts: 600)<br />

        Emperor (Minimum Posts: 1000)<br />
        Deity (Minimum Posts: 2000)     
       </blockquote>
