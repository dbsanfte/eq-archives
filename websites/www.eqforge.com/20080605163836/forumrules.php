	<p><b>Images and file attachments</b><br />      
      Currently we have the [img] tag and the file attachment feature enabled. For files under 500 KB, you may use the attachment feature to attach the file. In addition, you can use Easy Upload to upload bigger files to the file server. You can help free up valuable forum server space and also improve forum performance by deleting any attachments you no longer need at the <a target="link" href="http://forums.civfanatics.com/profile.php?do=editattachments">attachment management</a> section of your User CP. 

<p>If you need to attach images often, we recommend using these easy to use free image hosting services: <a href="http://www.photobucket.com/" target="_blank">PhotoBucket</a> or <a href="http://www.imageshack.us/" target="_blank">ImageShack</a>. For temporary hosting of large files or if you need to send a big file to another member, you can use the free <a href="http://www.yousendit.com/" target="_blank">YouSendIt</a> service, which allows you to upload up to 1 GB and they host the file for 1 week.</p>
     
      <p><b>Behave as you would in a public location.</b><br />
      This forum is no different than a public place. Behave yourself and act like a decent human being. If you're unable to do so, you're not welcome
