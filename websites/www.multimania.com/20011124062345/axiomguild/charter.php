<p align="center"><font face="Tempus Sans ITC"><b><font size="+7">Charter</font></b></font></p>
<p>&nbsp;</p>

<p><font face="Tempus Sans ITC"><b><font size="+1">Axiom</font></b><font size="+1"> 
  guild members are dedicated to furthering peace and enlightenment throughout 
  Norrath. To this end, we dedicate ourselves to helping one another in our daily 
  tasks and quests, and in helping those new to the world of Norrath become fully 
  functional members of Norrathian Society. Each member must learn a trade skill, 
  and must participate in the guild in some form.</font></font></p>
<p><font face="Tempus Sans ITC" size="+1">Axiom guild accept members from (EQ 
  life) any race, classe and deity.</font></p>
<p>&nbsp;</p>
<p> </p>
<p><font face="Tempus Sans ITC" size="+1"><b>Organization</b></font></p>
<p> </p>
<p><font face="Tempus Sans ITC" size="+1">The guild is managed and rule by a Guild 
  Leader. This Guild Leader could promote officers or dismiss them, but these 
  events should first receive approval of the majority of others officers. </font></p>
<p><font face="Tempus Sans ITC" size="+1">Officers are chosen by the guild leader, 
  and total number of officers could not be more than 20% of the total number 
  of members (players, not characters).</font></p>
<p><font face="Tempus Sans ITC" size="+1">From now on, the term &#147;Officer&#146; 
  will design both standard officers and the Guild Leader, unless stated otherwise.</font></p>
<p>&nbsp;</p>
<p> </p>
<p><font face="Tempus Sans ITC" size="+1"><b>Officers role</b></font></p>
<p> </p>
<p><font face="Tempus Sans ITC" size="+1">Each officer could submit the membership 
  of a player to the guild. </font></p>
<p> </p>
<p><font face="Tempus Sans ITC" size="+1">He could invite himself a player to 
  the guild, but must send an email to the <a href="http://groups.yahoo.com/group/axiomguildofficers">Officers 
  mailing list</a> about the entry of the new member on the day of entry. Also, 
  the inviting officer is responsible for adding or helping the new member to 
  add his character to the <a href="http://www.guildmagic.com/guild_characters.cfm?ID=1090">guild 
  roster</a>. The failure to do so could lead to the removal of officer status 
  if not corrected, upon decision of the Guild Leader. The invited new member 
  could also be excluded from the guild if he failed to register properly. </font></p>
<p><font face="Tempus Sans ITC" size="+1">Each officer is responsible for guiding 
  and teaching the new member in guild habits.</font></p>
<p><font face="Tempus Sans ITC" size="+1">Moreover, each officer should dedicate 
  a few hours of his playing time each week to help members on their tasks and 
  goals. On the day and hour of his choice, the officer should send a tell on 
  guild channel, announcing 'Officer XXX speaking, available for helping guild 
  members'. This help could be general or specific advice on EQ (game, quests, 
  mobs, tactics, etc...), helping characters for corpse recovery, or for quest 
  missions. The help could be refused if it involves a too great danger for the 
  officer, or if it is a task that could not be accomplished by the helped character 
  at his level (for exemple, helping a lvl 15 character to kill a Sand Giant, 
  power levelling a lower level character, or helping on a quest too hard for 
  the character level) or if it is against the charter rules.</font></p>
<p><font face="Tempus Sans ITC" size="+1">Officers duties are shared amongst all 
  officers. there is no specialization of duties per officer.</font></p>
<p>&nbsp;</p>
<p><font face="Tempus Sans ITC" size="+1"><b>Guild invitation process</b></font></p>
<p><font face="Tempus Sans ITC" size="+1">An officer inviting a new member must 
  :</font></p>
<p><font face="Tempus Sans ITC" size="+1">1. get the e-mail address of the member.</font></p>
<p><font face="Tempus Sans ITC" size="+1">2. Give the <a href="http://www.multimania.com/axiomguild/index.php">web 
  address of the guild site</a> and charter. </font></p>
<p><font face="Tempus Sans ITC" size="+1">3. Explain the guild roster functions 
  at <a href="http://www.guildmagic.com/guild_characters.cfm?ID=1090">GuildMagic 
  web site</a>.</font></p>
<p><font face="Tempus Sans ITC" size="+1">4. Explain the e-mailing list function 
  at <a href="http://groups.yahoo.com/group/axiomguild">YahooGroups web site</a>.</font></p>
<p><font face="Tempus Sans ITC" size="+1">5. <a href="http://groups.yahoo.com/group/axiomguild/members_add">Register</a> 
  the member on e-mailing list.</font></p>
<p><font face="Tempus Sans ITC" size="+1">6. Verify on the next few days after 
  invite that the member register himself on roster. Send tells until it's done.</font></p>
<p><font face="Tempus Sans ITC" size="+1">7. Be ready to answer questions or inquiries 
  from the new member, survey his play in guild for first month.</font></p>
<p>&nbsp;</p>
<p><font face="Tempus Sans ITC" size="+1">The member status (Trial Member for 
  first month) should be put on member data in guild roster in GuildMagic web 
  site. </font></p>
<p> </p>
<p><font face="Tempus Sans ITC" size="+1">Each new member should be tagged on 
  &#147;Trial&#148; status for the first month of guild membership. This status 
  is changed after one month to &#147;Full member&#148;, except if the character 
  has been excluded earlier in the month, of course.</font></p>
<p>&nbsp;</p>
<p> </p>
<p><font face="Tempus Sans ITC" size="+1"><b>Organizing events</b></font></p>
<p> </p>
<p><font face="Tempus Sans ITC" size="+1">The Guild organize one event each 2 
  weeks, on Saturday or Sunday.</font></p>
<p> </p>
<p><font face="Tempus Sans ITC" size="+1">Each officer must organise at least 
  one event every 2 months. </font></p>
<p> </p>
<p><font face="Tempus Sans ITC" size="+1">This event could be of any sort allowed 
  by the guild : trips, raids, exploration of zone, trade fair, drunken fight 
  or race, helping team for newbies, dungeon cleansing, &#133;</font></p>
<p> </p>
<p><font face="Tempus Sans ITC" size="+1">The event should be announced at least 
  one week before the day planned, by way of the <a href="http://groups.yahoo.com/group/axiomguild">guild 
  mailing list</a> and optionally by the command <font face="Courier New, Courier, mono">/guildmotd</font> 
  in EQ.</font></p>
<p> </p>
<p><font face="Tempus Sans ITC" size="+1">No two events could take place on the 
  same day. In case of conflict, should the planning officers disagree about which 
  event to remove or shift, the Guild Leader decision will prevail.</font></p>
<p> </p>
<p><font face="Tempus Sans ITC" size="+1">The events should be spread among a 
  wide variety of types (raids, fairs, &#133;) and levels (from low to high), 
  so that each and every member could be able to participate in at least on or 
  two per month or more. It is the Guild Leader role (along with all officers) 
  to enforce this variety.</font></p>
<p> </p>
<p><font face="Tempus Sans ITC" size="+1">The officer organising the event should 
  write the starting hour, zone and meeting spot, approximate duration, and average 
  level needed. He&#146;s also responsible for writing down the names of characters 
  participating in this event (more on that later).</font></p>
<p>&nbsp;</p>
<p> </p>
<p><font face="Tempus Sans ITC" size="+1"><b>Members</b></font></p>
<p> </p>
<p><font face="Tempus Sans ITC" size="+1">These rules applies also to the officers 
  unless stated otherwise.</font></p>
<p> </p>
<p><font face="Tempus Sans ITC" size="+1">Each member is approved by the officers 
  council per the rules written above. During the first month of membership, the 
  member has a &#147;Trial&#148; status. During this month, the officers Council 
  can exclude the member from the list at any time. After the first month delay, 
  the member is approved in full membership.</font></p>
<p> </p>
<p><font face="Tempus Sans ITC" size="+1">A member could have AT MOST 3 characters 
  registered in guild. The main (first character) could not be accepted before 
  level 10. The others characters of a member (2nd and 3rd characters) could be 
  accepted at any level. If a member want to have more than 3 characters in guild, 
  he should do a request to the Guild Leader that will accept or reject the demand.</font></p>
<p> </p>
<p><font face="Tempus Sans ITC" size="+1">A member should NOT use /anonymous mode 
  while playing. The guild tag, race, class and level of the character should 
  be displayed for all to see. Optionally, a member could use the /roleplay command 
  to remove his level, class and location from listings. The /roleplay command 
  still displays the Guild tag.</font></p>
<p> </p>
<p><font face="Tempus Sans ITC" size="+1">No member should suggest duels with 
  other players (members or non members), except if asked and for a good cause 
  or test of strength. Begging, stealing (Kser) and lying is not allowed and could 
  cause the immediate exclusion from the guild.</font></p>
<p> </p>
<p><font face="Tempus Sans ITC" size="+1">Each member should participate in AT 
  LEAST ONE guild event per month. Guild events will be held at least 3 times 
  per month, and will span a large array of levels and skills, so each and every 
  member should be able to find an event suitable. Failure to participate in any 
  event for one month (as reported by officers Council, each organising officer 
  responsible for keeping track of members during events) will trigger a warning 
  from the Guild Master or an officer. After 2 failures, the member will be automatically 
  excluded. </font></p>
<p><font face="Tempus Sans ITC" size="+1">Members unable to participate for a 
  specific duration (vacancies, too much work, &#133;) should tell an officer 
  about this as soon as possible.</font></p>
<p>&nbsp;</p>
<p> </p>
