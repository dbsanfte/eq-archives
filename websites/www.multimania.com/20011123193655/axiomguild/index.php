<h2><a name="291001A"></a>Raid in ToFS aftermath</h2>
<p>The raid in ToFS is done. Valiant members were there in two groups, one for 
  lower levels (under 40 : Leaddil, Canelle, Zilore), and one for higher levels 
  (above 40 : Iegien, Nunnen, Fardain, Skotos, Grimscyth, Ainurdolin). </p>
<p>After a trip to the tower (by boat from north Desert of Ro for some, and by 
  Nunnen Airlines port for others), we all entered the 1st floor and began to 
  hack and slash the various undead found there. Each level is locked by a key 
  that must be found on the lower below. Key to 2nd floor is kept by a Large Undead 
  Gnoll that spawns after enough undead are killed. We got the key after nearly 
  45 mns of ininterrupted fight, and proceeded to the 2nd floor. </p>
<p>This floor is a large library filled by ghostly students, scholars and teachers. 
  An Angry Librarian should randomly spawn after kills, and drop the key to 3rd 
  level. Unfortunately, we never managed to found him, and we were only second 
  after a group already seeking the key. So after more than 1 hour of killing 
  (good xp for lower group), we choosed to leave and call it a day. We did suffer 
  a loss during the fight : Nunnen, wandering alone in a dark corner of the library 
  as most halflings do, was burned to death by an undad caster without notice 
  from anyone. He managed to recover his corpse quite easily (after a run from 
  bind point in Oasis) but losed the xp (no cleric available near, and floor difficult 
  to reach anyway).</p>
<p>We were all ported back to RiverVale by Nunnen to share loot (some magic items 
  : gnome skin armor mostly, and pp). We now have the key to 2nd level (ryonea 
  has already ALL keys) and could try again soon to gain the key to 3rd level, 
  where better loot and xp still avaits us...</p>
