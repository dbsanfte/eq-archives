<h2><a name="101101A"></a>RAID : Lost Temple of Cazic Thule</h2>
<p>We plan to have a raid into the Lost Temple of Cazic Thule on November, Saturday 
  10th . All characters level 20 to 35 could join. Members above that could help 
  for crow control, pulling and spawn breaks, without grouping.</p>
<p align="center"><b>Starting time should be 2 PM Central (3 PM East, 9 PM CET, 
  1 PM MST, Noon PST)</b></p>
<p>Lost Temple of Cazic Thule is an underutilized dungeon with lot of Lizard Men. 
  Several mobs drops very good loot for rogues and for SK. Nice xp til lvl 35, 
  but this dungeon is famous for the deadly trains all the way to the entrance, 
  so we'll need to pull carefully and fight as a very united group. We should 
  be able to discover and camp most if not whole dungeon.</p>
<p>To go to Temple of Cazic Thule, bind at Freeport (good races), then head to 
  NRo, Oasis, SRo, Innothule (hug the RIGHT wall til zone), then the Feerott. 
  Temple zone is at middle of South wall of Feerott zone, on a long island on 
  south part of river splitting the Feerott zone in two. Another path could be 
  South Karana, Lake Rathetear, Rathe Mountains then Feerott. Binding is also 
  possible at the Arena near Lake Rathetear. Evil races could bind in Ogguk, North 
  of Feerott.</p>
<p>&nbsp;</p>
<p>Some good maps of Temple of Cazic Thule could be find at <a href="http://www.eqatlas.com/cazicthule.html" target="_blank">eqatlas</a> 
  and <a href="http://www.eqmaps.com/m2/cazicthule5.jpg" target="_blank">eqmaps</a>.</p>
<p>As always, try to be near Temple before starting hour. We could not afford 
  to wait one hour more for all to be there. </p>
