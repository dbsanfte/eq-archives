<h2><a name="111101A"></a>Debriefing after Cazic Thule raid</h2>
<p>8 members from level 25 to level 42 joined on Saturday to raid the Lost Temple 
  of Cazic Thule. </p>
<p>Two groups, based on levels, were formed, and stayed together to explore the 
  dungeon. First fights inside Courtyard, then in the maze, then around and on 
  large Archon pyramid were ok, and we proceeded inside the Archon pyramid. This 
  strange place was quite confusing, and after a few looks inside, one member 
  fell into the water at the base of pyramid. With no mean for this poor fellow 
  to exit by the way we all came, we all jumped into water to search for another 
  exit (map told us that a complex sewer system was available below). </p>
<p>The water was populated by many crocs, and soon, with the difficulties of fighting 
  into water, and the scattering of members jumping from the top of the pyramid, 
  2 members died. Other swimmed inside the sewers, but get confused by the intricate 
  maze of cubical rooms and tunnels flooded with water, and 2 others friends soon 
  were lying on the ground of the next cavern near the sewers. Remaining members 
  then fought repeatedly the mobs around (crocs, lizard men) while draging corpses 
  to this area.</p>
<p>It took us almost one hour to drag all corpses, fight some mobs, use invis 
  to gather and drag all bodies to the zone. Some choose to leave at this time, 
  some grouped again to hunt into Throne Room for some time.</p>
<p>Several lessons could be learned from this :</p>
<ul>
  <li>Death is always a possibility and is part of the game. Everyone should accept 
    it.</li>
  <li>Fighting inside an unknown place is far more dangerous than in a well known 
    area, even if each mob is not too dangerous.</li>
  <li>Fighting as a whole group is KEY to victory : single puller, no scattering, 
    all tanks focused on same mob, will accelerate the fights and reduce the risks.</li>
  <li>Quite all group members (me included) made some mistakes during this event. 
    We should learn from this :-D</li>
</ul>
<p>Please post on this topic on forum <a href="read.php?f=2&i=37&t=37">here</a>.</p>
