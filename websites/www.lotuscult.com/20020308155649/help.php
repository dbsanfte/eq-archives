<li><a name='1.1'>What are you looking for in new recruits?</a><br>
<p> We are looking for skilled players who play on a regular basis with a desire to tackle the most difficult 
and demanding mobs and zones in the game. The recruit must be a TEAM player and responsive to the directions
of the leadership. 
</p>
<li><a name='1.2'>What level do I have to be?</a><br>
<p>Our Current Level requirement is 58th level for all classes.
</p>
<li><a name='1.3'>Who do I talk to about joining?</a><br>
<p>Collron Dunmaghlas and Namahs Citsym are the recruiting officers.
</p>
<li><a name='1.4'>What classes do you need?</a><br>
<p>All classes are welcome to apply.
</p>
<li><a name='1.5'>What if I want to stop playing my main after joining?</a><br>
<p>If you desire to play an alternate character as your main, that alternate character should meet the 
recruitment guidelines.
</p>
<li><a name='1.6'>How do I get placed under consideration?</a><br>
<p> Make yourself visible to our membership, hang out with us in experience groups, attend raids, and post on 
the boards, BE AVAILABLE! 
</p>
<li><a name='1.7'>Why do I need a sponsor?</a><br>
<p>Lotus Cult is a tight knit group of friends. As a rule of thumb we prefer to recruit friends and people we 
have a working relationship with. One of our members must choose to sponsor a recruit before that recruit can 
be placed under consideration. The sponsor then becomes your contact person in LC, someone you can talk to 
about upcoming events, and answer questions you may have. The sponsor will place your name in our recruiting 
forum for consideration. 
</p>
<li><a name='1.8'>How do the votes work?</a><br>
<p> Once a recruit has raided with us for a period of time, a guild vote is called for. If a membership consensus 
is reached the recruit will be asked to join the cult.
</p>

