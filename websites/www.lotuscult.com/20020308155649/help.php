</p>
<li><a name='3.2'>How do I make a profile for my chacracter?</a><br>
<p>
First, you have to be registered with the website. You can do that <a href='user.php'>here</a>. Then you can 
create as many profiles for as many characters as you want 
<a href='modules.php?op=modload&name=NS-Everquest&file=player&eq=new'>here</a>.
