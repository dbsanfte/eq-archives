<li><a name='2.1'>Is there a loot system?</a><br>
<p>Yes.
</p>
<li><a name='2.2'>How does that system work?</a><br>
<p>The Guildleader or an officer handles all loot and will issue that loot based on  1. Good of the guild. 
2. Participation. 3. Relative amount of upgrade. 4. Last loot received. Although not neccesarily in that order.
</p>
<li><a name='2.3'>Why don't you guys roll?</a>
<p>We feel that rolling does not adequetly advance the guild as a whole. It merely gives a couple lucky people
items without condsidering where the items would be best used.
</p>

