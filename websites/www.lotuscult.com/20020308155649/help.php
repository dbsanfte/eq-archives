<li><a name='3.3'>Can I register my twink as well?</a><br>
<p>Yes! You can register as many characters as you want.
</p>
<li><a name='3.4'>What are you guys using to run your website?</a><br>
<p>We're using <a href='http://www.phpnuke.com' target='_'>phpnuke</a> to power our website. PHPNuke is a sollection of
PHP scripts backed by a mysql database server. It's pretty nifty, I'd reccomend you check it out if you're
looking to do a site of your own. Also consider <a href='http://www.postnuke.com' target='_'>postnuke</a>. Postnuke is a PHPNuke
derivitive that has alot of promise, but is not as settled as I would like yet. I expect us to switch to Postnuke
when they finish a stable core.
</p>
<li><a name='3.5'>Ok, what is driving the EQ profiles then?</a><br>
<p>We have developed in house a custom phpnuke module to handle character profiles and all other Everquest related
things.
</p>
