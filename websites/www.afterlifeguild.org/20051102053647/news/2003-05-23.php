<td width="100%" height="100%" align="left" valign="top"><table width="95%" border="0" cellspacing="0" cellpadding="0" align="center"><tr><td colspan="3"><b><font color="#666666">Friday, May 23, 2003</font></b><img src="images/date.gif" height="2" width="175"></td></tr><tr><td width="5%">&nbsp;</td><td width="90%"><p><font color="#0000CC" size="1">Posted by Thott</font> <br>
<b><font color="#000000">Plane of Time: Denied</font></b><br>Yesterday, we once again assaulted the Plane of Time.&nbsp; We beat
the first phase.&nbsp; We beat the second phase.&nbsp; Then...nothing
happened.&nbsp; Instead of the clocks opening, we stood there, with no mobs in
sight, indeed, no mobs even on ranger tracking.&nbsp; The event bugged.
<p>
I petitioned.&nbsp; We had trouble a few days ago, and I petitioned,
yet after three hours still had no response.&nbsp; Another member
petitioned earlier that same day, and after seven hours had no
response.&nbsp; Even so, this time, I received a response from a guide
almost immediately.&nbsp; I explained the problem, and received this gem:
<p>
<center><img src="/news/2003-05-23/Guide_Beta.jpg"></center><p>
He said he would bump my petition up to his GM.&nbsp; A GM never contacted me.
<p>
Surprisingly (or perhaps not so surprisingly, considering where we are)
a developer showed up.&nbsp; He took a look at the problem and assured
us it would be fixed by the time we raided again.&nbsp;
That's great - but that doesn't help us today.&nbsp; An entire raiding day wasted, hundreds
of man hours lost, yet nothing could be done?&nbsp; He said he
had the commands to help us, but policy prevented it.
<p>
Imagine falling off a boat, and an engineer arriving and saying he'd have
it fixed so you couldn't fall off tomorrow, but union rules prevented
him from saving you from drowning.&nbsp; Have a nice day!
<p>
When I started playing EQ, the game was cheaper, it used more bandwidth,
it had fewer subscribers, and it had good support.&nbsp; Today, it's
more expensive, uses less bandwidth, has far more subscribers...and has
<b>no support of any kind</b>.&nbsp; That is, no support unless you
abandon your friends and move to Stormhammer.
<p>
<b>Fix this</b>.&nbsp; Change the policies so devs can help out in cutting edge
zones.&nbsp; Hire more GM's, and create a real centralized petition
queue - having GM's move from server to server isn't working.&nbsp;
Have petitions stay in the queue until they are handled.&nbsp; When I
have a problem with other companies, they work to make it right.&nbsp;
<b>Make it right</b>.
<p>
In DAoC, players can see the number of GM's servicing the queue at any
time, as well as the average wait for a response and how many petitions
are ahead of them in the queue.&nbsp; EQ has copied and outdone every
good thing from DAoC except for customer service.&nbsp; Finish the job.
<p>
Currently the profit margin for EQ is in excess of 50%.&nbsp; Half the
added cash from the last price hike would fund a GM for every server,
24 hours a day, 7 days a week.&nbsp; With a centralized queue, I'd be
happy with half that many GM's.&nbsp;
<p>
I'm fine with Plane of Time being beta.&nbsp; The alternative is a zone that
gets tested thoroughly in beta, and is spoiled from the start.&nbsp;
I'm fine with running into bugs.&nbsp; As a programmer myself, I expect
them.&nbsp; I'm quite happy with the dev team's speed and effeciency in
fixing these bugs.&nbsp; I even assume that the GM's themselves are doing
a good job (I certainly haven't seen anything that suggests otherwise,
since I'd have to actually <b>see</b> a GM for this to happen).&nbsp;
It's the policies that are at fault here.&nbsp; Fix the policies, and the people following them will set things straight.
<p>
