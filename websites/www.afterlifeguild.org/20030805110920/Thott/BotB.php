<p>
I'm glad I won, but realistically, I had little to gain, and everything
to lose. &nbsp;Puretone is a major advantage, one that in my opinion
should have been disallowed from the start. &nbsp;If luck had placed
me fighting two top notch bards in a row, both of which used puretone,
the second would beat me. &nbsp;Even though in a straight up fight with
that same bard, I would win. &nbsp;The idea that the use of puretone
is a strategic element is a poor one, because it all depends on the
order one fights. &nbsp;If the fights happen in a different order, the
winner changes.  &nbsp;Since the fight order is determined by /random,
the winner is determined the same way.
<p>
<center><h2>Equipment</h2></center>
I expect many people to say that I won the BotB due to my equipment advantage.
&nbsp;I'm sure my performance at past ToT's will be completely ignored. &nbsp;
The fact that some bards there had within 10% of my hp and didn't come close
to my level of performance will also be ignored.
<p>
The equipment I wear is the end result of years of preparation. &nbsp;Some
people are of the opinion that good equipment is unfair in some
way. &nbsp;I will attempt to explain how equipment figures in in Everquest,
when it doesn't in other sports.
<p>
Many things in EQ are totally random. &nbsp;If I slash someone, how
hard I hit them isn't based on skill, it's based on equipment and a
random roll of the die. &nbsp;In a real life situation, there would be
no randomness. &nbsp;The chance to hit, and the strength of the hit,
would all be based on the skill of the two combatants. &nbsp;That skill
is a result of time spent practicing, and some genetic ability. 
<p>
In Everquest, no amount of practice will allow me hit harder with a sword,
or better resist damage songs. &nbsp;However, equipment will. &nbsp;So
instead of long hours of training to increase strength or resistance,
in Everquest, it's long hours spent obtaining equipment. &nbsp;Equipment,
in Everquest, IS increased strength and resistance, the same things that
any athlete builds over time.
<p>
Either way, through training or through equipment, the combatant that
spends the most time preparing has an advantage.  &nbsp;Is it unfair
to practice more than others? &nbsp;I'm sure there are people out
there with enough skill to play in professional football...but they
don't have the physical characteristics needed:  superior strength and
speed. &nbsp;Is this unfair? &nbsp;Assuming genetics doesn't prevent it,
of course not. &nbsp;It takes a great deal of time to build the strength
and speed necessary for professional football. &nbsp;If those skilled
but untrained athletes spent time training their speed and strength,
then they could participate in professional football, on an even footing
with others. &nbsp;They can try to compete without the strength and speed,
but their skill will have to be greater to compensate.  &nbsp;Everquest is
the same way.
<p>
The randomness concept was most obvious at the cleric BotB. &nbsp;Arele
fought another cleric, and both were hitting the other during CH to
interrupt.  &nbsp;Every time Arele hit, the other channelled. &nbsp;Every
time the other hit, Arele didn't. &nbsp;Chance to channel is completely
random. &nbsp;There is no skill involved. &nbsp;Chance to hit with a
weapon is again, completely random. &nbsp;The only thing that helps in
this case is time spent obtaining equipment, since there is no way to
practice channeling (beyond gaining levels and in game skill points, which
I assume are all at max for every participant).  &nbsp;Without equipment
to somehow differentiate those that prepared for years and those that
prepared for months, it would come down to who gets the best die rolls.
&nbsp;Even with an equipment advantage, sometimes it does anyway.
<p>
So, the final question: is it unfair to have an equipment advantage?
&nbsp;Of course not, the same way it isn't unfair for one heavyweight
boxer to be stronger and faster than another. &nbsp;The stronger/faster
boxer spent more time training for the event. &nbsp;I spent a great deal
of time and effort to get the equipment I wear. &nbsp;I get no special
equipment deals from being Guildleader, I use the same point system
as everyone else. &nbsp;I have better equipment because I spent more
time preparing. &nbsp;Want to see every raid I've been on in the past
1.5 years? &nbsp;Go to my <a href="http://www.afterlifeguild.org/dkp.cgi?history=Thott">DKP page</a>.
<p>
One thing I would like to see though, in regards to Everquest
BotB style competitions, is different weight classes, exactly
like my boxing comparison above. &nbsp;Currently, we ONLY have
heavyweight combat. &nbsp;Where do the lighter equipped players go
to compete? &nbsp;They don't. &nbsp;How about lower level players?
&nbsp;They don't either. &nbsp;To get this going, Verant would need to
have volunteer referees, set rules, and weekly events...but it could
be done.
