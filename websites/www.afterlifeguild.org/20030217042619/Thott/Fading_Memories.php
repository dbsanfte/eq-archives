This is a post collection, from various threads discussing Fading
Memories.&nbsp; Its general tone is written in response to a rogue
posting about Escape vs. Fading Memories, although that wasn't the reason
for the original posts.
<p>
From Concert Hall:
<table border=0><tr><td>&nbsp;&nbsp;</td><td>
<table border=1 cellpadding=3 bgcolor=AAAAAA><tr><td>
FT and Fading Memories are essentially the only way for bards to improve
their crowd control (CC) ability through items/AA's.
<p>
Enchanters can upgrade their CC via:
<ol>
<li> Increased mana pool (longer periods of hero-level activity)
<li> Increased mana regen (increased number of mobs mezzed/hour)
<li> Shortened cast time (only on some spells, faster lockdown rate)
<li> Increased duration (multiplies increased mana regen, increases max number of mobs controllable)
<li> Increased range (earlier mez casts on incoming mobs, increased safety from distance)
<li> Increased hp/ac (less risk from failed mez attempts)
</ol>
All of these are good things.&nbsp; A fully equipped enchanter should be better
at CC than a naked enchanter.
<p>
None of the above applies to bards.&nbsp; Even increased hp/ac doesn't help
a bard CC, because it's usually the cleric that is tanking if the bard
fails, not the bard.&nbsp; A naked bard and a fully equipped bard are almost
identical in CC power.
<p>
Fading Memories is the answer to this.&nbsp; Fading Memories improves
faster with FT than enchanter CC, but it's just one part of bard
CC.&nbsp; Algebraically one might write it like this:
<p>
<pre>
    Enchanter: CC*equipment_bonus = equipped CC
    Bard: CC + Fading_Memories*larger_bonus = equipped CC
</pre>
Since only part of the bard equation increases, that part increases
faster with equipment.&nbsp; The same equation holds for charm in place of
Fading Memories, but at higher levels, charm isn't useful for CC.
<p>
Everyone should want equipment upgrades to be fully effective.&nbsp; For a
bard it's just limited to this one area.
