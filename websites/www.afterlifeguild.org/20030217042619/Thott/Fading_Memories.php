<p>
This explains why FM improves so quickly with items.&nbsp; Escape is a stand
alone ability.&nbsp; Fading Memories is tied to bard crowd control.&nbsp; Essentially,
this one ability improves more, while other abilities improve less
(or not at all).
<p>
Fading Memories is, in effect, the only utility upgrade bards have
seen since Kunark.&nbsp; Pure melees have seen great improvements in utility
through items as well as through AA's, Escape being one of them.&nbsp; Bards
are a utility class, just as rogues are a damage class.&nbsp; Would rogues be
happy if their damage had stayed constant since Kunark?&nbsp; Naturally
not.&nbsp; Every class expects upgrades, and unless something specific
prevents it, every class should get them.
<p>
Thus this one ability, Fading Memories, combines all the upgrades from
equipment that would otherwise go into other forms of crowd control, as
well as almost all of the upgrades to utility that one would expect for a
utility class, in one ability.&nbsp; And so yes, this one ability compared
to most other single abilities, is overpowered.&nbsp; At the same time, numerous
other individual bard abilities when compared to other classes aren't
powerful enough.&nbsp; Is the end result balanced?&nbsp; It's impossible
to say.&nbsp; It's hard enough to compare ability A to ability B.&nbsp; Comparing A-M
to N-Z is far harder, too hard to do realistically: there are too many
variables.&nbsp; Instead of comparing individual abilities, the only
way to compare is by end result testing in game.
<p>
From playing a bard, I would say it feels balanced.&nbsp; I
certainly have a lot of limitations (I have had to use a monk in many
areas, FM simply doesn't cut it), but it's still quite useful, and I'm
happy with it.
<p>
If Fading Memories were spread out into a dozen individual abilities,
there wouldn't be such a discussion.&nbsp; Over time I expect the wisdom of
Fading Memories to become more apparent.&nbsp; Today, it's hard to see past the
"it's like escape...but...it can be better [by some measurements]!"
<p>
On the Safehouse, as part of a thread relating to Fading Memories, I posted this (excerpt):
<table border=0><tr><td>&nbsp;&nbsp;</td><td>
<table border=1 cellpadding=3 bgcolor=AAAAAA><tr><td>
This also helps balance FD.&nbsp; This isn't intuitive, but in practice
the least painful way to fix an unbalanced ability is to make it
more common (the other method being gimping, and nobody wants to
see FD gimped).&nbsp; Consider for example if only one class in the game
could heal.&nbsp; All encounters would be based around this, one way or
another.&nbsp; Either all encounters would be too easy if the healing class were
present, or all encounters would be too hard if the healing class were
not present.&nbsp; Even if the only heal spell this class had was greater heal,
that class would still be the most powerful class in the game.&nbsp; Yet lots of
classes have greater heal in Everquest, and they are not overpowered.&nbsp; It's
not the power of an ability that makes it powerful, it's the rarity.
<p>
The solution to this, with regards to FD, is two pronged.&nbsp; Other
classes are not getting FD, but bards and rogues are getting partial
FD-like ability.&nbsp; FD includes the ability to wipe hate and to avoid aggro
(i.e. while FD, nothing will aggro).&nbsp; Bards gain the ability to wipe hate,
while rogues gain the ability to avoid aggro.&nbsp; Both classes gain this
ability in a way that is sometimes inferior and sometimes superior to
FD.&nbsp; Rogue shroud of stealth prevents aggro like FD, but the rogue can
move around without risk, while there is risk for the monk.&nbsp; Yet the
rogue version is also less powerful, since there is a chance for each
see invis mob to be able to see through it, and in areas containing
such a mob it is useless, while FD always works.&nbsp; Bard FM wipes all hate
instantly when used, while FD takes some time.&nbsp; However, FM is heavily
mana limited, can't be used nearly as often, isn't usable after a rez,
and is useless when near mobs that see invis.
<p>
Thus the power of FD is now split across multiple classes, with each
class situationally "best", and none are directly comparable.
