<center><div class=menutitle>Update 10/19/02</div></center>
<b>Clarifications</b>: All DD damage, by druids and clerics, is increased
by 10% from focus items and 7% from crits (Spell Casting Fury 3).&nbsp;
This was originally part of the sims, but wasn't mentioned anywhere.&nbsp;
Oddly enough, the supposed lack of the 7% damage increase from crits was
one of the primary shaman objections, even though it would be trivial to
look at the graphs and mentally adjust them up 7% (don't do it though,
as I said, it's already included).
<p>
The actual calculation is to multiply by 1.1, then multiply by
1.07...which actually results in a slightly higher number than what's
currently live, because the way these things are calculated was
changed.&nbsp; But it's pretty close.
<p>
These damage calculations lack these features, that I can think of off
the top of my head:
<ul>
<li>Melee haste (shaman)
<li>Pet (shaman primarily, druids to a lesser extent, cleric pet takes mana and is more of a nuke/DoT)
<li>Druid epic (I should really add this, but I forgot to)
<li>Damage shields (hard to quantify, but it definitely adds damage to druids)
<li>Cleric melee (I have no numbers for this)
</ul>
And probably some other things.&nbsp; Shamans also objected about the
missing items above, except that oddly enough, no shaman objected that
their pet was not included.&nbsp; Shaman pets, from the last time I
parsed one, do about 10 dps.&nbsp; However, due perhaps to leash laws,
shamans rarely use them.
<p>
