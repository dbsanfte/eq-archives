  <div class="menutitle"><font size="+3"><b>Priest Damage Comparison</b></font></div>
  <font size="1">by <a href="mailto:ThottWeb@Afterlifeguild.org">Thott</a></font><br><br></center>
These sims depict the damage per second a priest can pump out in
pure-damage mode.&nbsp; These simulated priests aren't healing at
all.&nbsp; They do nothing but cast their DoT's, cast their nukes,
self torpor/cannibalize (shamans only) or meditate.&nbsp; The results
are graphed.
<p>
Note that cleric damage isn't accurate, because I have no good numbers
for cleric melee damage with the new proc hammers.&nbsp; These graphs
only show cleric nuke damage.
<p>
Also note that shaman DoT's are used, and shaman DoT's are taunting.&nbsp;
It takes a high quality crazy glue warrior to prevent the shaman
from becoming the tank when using these DoT's, so this damage isn't
particularly realistic.&nbsp; That said, it looks like (from analyzing
spdat modifications) shaman DoT's in PoP will be significantly less
taunting than current versions.&nbsp; In particular they do not have an
initial hit, nor do they have very many poison/disease counters, both
of which are major factors in DoT taunt.&nbsp; Shamans may very well be
able to pump out high damage per second in PoP without gaining aggro,
but we'll have to see.
<p>
Rain spells are graphed, but since they can't be used in every situation,
they're simulated seperately from non-rain damage.&nbsp; Not everyone
has their ancient nuke/DoT, so that too is simulated seperately.
<p>
