  <div class="menutitle"><font size="+3"><b>Manaburn</b></font></div>
  <font size="1">by <a href="mailto:ThottWeb@Afterlifeguild.org">Thott</a></font><br><br></center>
This was first posted in a <a href="http://pub14.ezboard.com/fthesteelwarriorthearena.showMessage?topicID=1790.topic">thread</a> over at <a href="http://pub14.ezboard.com/fthesteelwarriorthearena">The Steel Warrior</a>.
<p>
If the best group to accomplish something consisted of 6 rogues, would
that group be balanced? Of course not. Everquest is built with balanced
groups as a design goal.
<p>
Even if 6 average equipped classes in a balanced group can kill Gorenaire
(which of course, they can not), 6 wizards being able to do the same
thing is still wrong. There's no way to justify it, just as there
would be no way of justifying 6 rogues as the best possible group in any
other situation, or the best raid being 50 warriors.&nbsp; As soon as a group
requires 6 of the same class, that class is unbalanced - regardless of
the circumstances.
<p>

Pretend for a moment you're a designer, developing content.&nbsp; You would
like to add some content that isn't like Luclin content.&nbsp; In other words,
you would like to add some content that's actually exciting and fun,
something like fights were in the original game with Vox and Nagafen, or
even fights in Kunark.&nbsp; Fights where you don't need a book or a television
to survive it.
<p>

However, you find quickly that you can't make this fight.&nbsp; Why?&nbsp; Because
manaburn makes it trivial.
<p>

No matter how much damage that mob does, no matter how resistant it is,
no matter what is done - manaburn will take it down in 6 seconds.&nbsp; Sure
the room may have a lot of other mobs in it, so you have to have other
people there besides the wizards, and that's great - but the fight itself
will be trivial.
<p>
Even if the mob has twice as many hp as the 8 or so wizards expected
to be at a raid can do...those wizards are still overpowered for that
encounter.&nbsp; Those 8 wizards do as much damage as the rest of the raid
combined.&nbsp; If the raid has 16 wizards, no others will be needed at all.
<p>
There are three solutions to these two problems:
<p>
<ol>
<li>Continue making boring fights, and buy stock in television
companies.&nbsp; Ignore class balance, since it's, like, Hard and stuff.
<li>Create manaburn immune mobs, or even make the ability not work at all
in PoP.&nbsp; Sure existing content stays shot to hell, not to mention life
on PvP servers sucking more than usual, but at least the future game
has a chance.
<li>Nerf manaburn now.
</ol>

Only one option is good for the game as a whole.
<p>
Oh, and team manaburn? Ours has earned about half a million plat, each,
solely through the use of this one ability.&nbsp; Quite a deal for 5 AA points.
<p>
I'm sure rogues can use their Poison Mastery skill to make money just as
fast, and warriors can save that kind of money with their Bandage Wound
skill.&nbsp; Since these UBER money making skills cost 18 points instead of 5,
I'm sure they make money 3.6x as fast...right?
