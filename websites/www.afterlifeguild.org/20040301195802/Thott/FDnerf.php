  <div class="menutitle"><font size="+3"><b>Feign Death - Nerf?</b></font></div>
  <font size="1">by <a href="mailto:ThottWeb@Afterlifeguild.org">Thott</a><br>2003/10/25<br></font><br></center>
Anyone reading this is expected to have read <a href="FD.php">Feign Death</a>.
<p>
I'd leave things alone, with only those previous pages and no explanation...but some people
are rather dense.&nbsp; Thus I am forced to grab my sledgehammer.
<p>
<b>Nothing I wrote is about nerfing FD</b>.&nbsp; It's more likely that
California will split off from the mainland and the rest
of the continent will sink into the ocean, leaving California all alone
(and out of power), than FD will be nerfed.&nbsp; It's more likely that
Atlantis will rise from beneath the waves and its soggy denizens will
take over the earth, than FD will be nerfed.&nbsp; It's more likely
that the moon is actually a gigantic, sleeping, sentient being that
will wake up, destroy our civilization, sprout fairy wings, and fly away,
than FD will be nerfed.&nbsp; This is not about nerfing FD.
<p>
Among other things, including general entertainment value, these pages
address the idiocy that is having to quit out when using FD,
as well as the occasional monk argument that "FD should be the best pulling
technique in the game," which is roughly as reasonable and reflective of
reality as attempting to fly by riding a mosquito.
<p>
People who put forth this argument are the ones most likely to volunteer for Atlantis
slavery right off the bat, to avoid the rush.&nbsp; They are the ones
that will move to the mainland after it sinks in search of better land
prices.&nbsp; They are the ones that will wait naively under the landing
moon-beast, ultimately cushioning its fall.&nbsp; But until these things
happen, I will leave this page up.
<p>
Just to entertain myself, I'll list some more arguments that counter the
"FD as best pulling technique" argument, even though one would think it
is obvious.&nbsp; I like to be thorough.
<ul>
<li>FD was never meant to be used as a pulling technique, per Brad McQuaid
and every other original developer, and even developers that exist today,
many of whom played EQ for years before signing up (I know you're thinking about nerfs: stop; don't make me write in bold again).
<li>As described on previous pages, FD makes no sense as a pulling technique.&nbsp; Voice Graft makes more sense as a pulling tool than FD (Jabober says, "psst, hey buddy, come here for a second, have I got a deal for you!" It could work!)
<li>FD has <i>never</i> been the fastest and most efficient pulling
technique.&nbsp; FD is only valuable <u>when all other forms of CC are
removed</u>, i.e. when mobs have high MR.&nbsp; When none of the other
CC tools work, and all the CC classes are reduced to buffbots and cursing SOE daily
while reading the Anarchist's Cookbook, leaving only FD...well sure,
it's the best!&nbsp; This isn't new with PoP, LDoN, or any future expansion: it's been this way from
the beginning.
<li>Lull was designed from the start to be used for pulling, while, to
repeat my repeating of my repeating, FD was never meant to be used for
CC at all.&nbsp; Thus if lull is better than FD, then lull is working
just fine.
<li>Enchanters and bards are both designed to fill the CC role.&nbsp;
It doesn't make much sense to have a damage melee beat them at CC,
now does it?
</ul>
<p>
Thus concludes my thorough beating of this horse.&nbsp; I henceforth declare it dead.&nbsp;
<p>
I hope it's not faking.
