<h3>Commentary</h3>
The first graph is fairly useless now, since the mini-CH spells are now live, but I leave it here for reference.
<p>
The second graph is surprising.&nbsp; Shamans have extreme power when
mobs are slowable, but the drawback is some mobs are not.&nbsp; Even
on mobs that are not slowable, shamans still surpass druids in healing
in any xp situation that lasts longer than about 12 minutes, and aren't
significantly worse than clerics.
<p>
The last graph is the most surprising of all.&nbsp; The new shaman
torpor-like heal over time, Quiescence, improves shaman effeciency to
the point that a shaman, even when every mob is immune to slow...is
identically effecient to a cleric in any xp group lasting for more than
20 minutes.
<h3>4k CH</h3>
However, that graph assumes that clerics will continue to heal 4k per
heal in PoP, which may not be true.&nbsp; However, after building these
graphs assuming 4k complete heals, I ran statistics on my own logs,
to determine what type of CH's typically hit me.
<p>
My results were surprising.&nbsp; Out of 448 CH's that landed on me
over the course of the log, the average value healed was 3308hp.&nbsp;
On top of that, there were a great number (294)
of Celestial Elixirs cast on me, all of which are less effecient than
CH.&nbsp; My typical maximum hp value is over 6800hp, so this represents
an average CH of slightly less than half my total life.
<p>
Why are so many CH's so low?&nbsp; In real world situations, there are
times where someone will begin to take damage, and the cleric starts
to heal...then that character stops tanking.&nbsp; The CH is already
in progress.&nbsp; The damage done has to be healed, and even if it's
only a bubble, CH is still one of the best ways to do it - so the CH
lands.&nbsp; CE is used frequently because it is fast casting and low
aggro, but each use of it even further lowers average effeciency -
something I don't take into account at all.
<p>
This of course assumes a modern xp group, as I suspect life will be like
in PoP, where mob damage is fast and furious.&nbsp; These days, the best
xp isn't fighting level 45 mobs equipped with plastic toothpicks.&nbsp; The
best xp is fighting level 60+ mobs wielding small automobiles.&nbsp; In
that type of situation, when multiple people in the group may be taking
damage at a fast rate, getting maximum value CH's is much harder than
in the past.
<p>
The shaman level 65 heal over time (Quiescence) is actually more effecient
than their mini-CH, thus shamans are even more effecient at healing slow damage,
or when characters take a small amount of damage and then stop.
<p>
Thus in real world situations, the cleric line is far lower.&nbsp;
The shaman line, however, is not likely to lower much at all.&nbsp;
It's relatively easy to heal 2khp with nearly every heal.&nbsp; Thus in
practice, I suspect a shaman will be superior to clerics in xp groups -
even when fighting unslowable mobs.
<p>
The only exception to this is when dps is so high that shaman healing
can't keep up in the short term, in which case cleric healing can't
keep up in the long term either, and the cleric will need med breaks
to recover.&nbsp; In that type of situation, it's probably better to
move on to mobs that wield less dangerous objects, or get a second priest.
