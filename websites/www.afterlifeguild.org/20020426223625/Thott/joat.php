  <h1>Jack of All Trades</h1>
  <font size="1">by <a href="mailto:ThottWeb@Afterlifeguild.org">Thott</a></font>
  <p>
</center>Bards are called Jack of All Trades.  Why?  Well they have a lot
of abilities.  They can heal, they can melee, they get lots of skills,
they can do everything!
<p>
Or so I'm told.  Looking around, I see lots of abilities that bards don't
have, many of which are very desireable (gate, bind, mem blur, stun,
fast healing, fast damage, hp buff, harmony, FD, well, see the table).
Do bards really have more than other classes?  So I created the below
table.  It shows all the classes, and what abilities they have.
Sure bards have a unique set of abilities...but so does every class.
<p>
Most likely you could have 100 people make this table and get 100
different answers, so it's by no means definitive.  However it's somewhat
close.  A few of the abilities might change if someone else were to
do it, and who has the most or 2nd to the most or 3rd to most might
swap around, but pretty much anyone's chart will show that bards,
enchanters, shamans, druids, and necros have a lot of abilities, and
other classes have less.
<p>
If bards are weaker because of the number of abilities they have, then
by that same argument, so should enchanters, shamans, druids, and necros
be weaker.
<p>
I did this chart out of personal curiousity, not to prove anything.
It's as objective as I can make it.  This is the first draft, I'm sure
there are errors in the chart that people will email to me.  I also
expect to get plenty of "you only counted ability X one time, it's way
more powerful than ability Y!" even though such comments have nothing
to do with the chart.  This just lists the total number of abilities.
Of <b>course</b> some abilities are better than others, otherwise the
class with the most abilities would be the best.
<p>
GZ said he used a chart like this when creating classes in beta.  Too bad
nobody has a copy :/.
