  <div class="menutitle"><font size="+3"><b>Feign Death</b></font></div>
  <font size="1">by <a href="mailto:ThottWeb@Afterlifeguild.org">Thott</a><br>2003/10/25<br></font><br></center>
The ancient dragon chuckles, deep resonant echoes reflecting off the
polished walls of his lair.&nbsp; Around him his guards adopt an even
stiffer stance of attention, barely perceptible to a mortal, but easily
seen by the dragon's jeweled eye.
<p>
He turns his attention back to counting his hoard.&nbsp; "Three" he
thinks, the thought flowing inside his mind, mentally echoing like his
earlier chuckle.&nbsp; "Four" he whispers, the number floating on the
wind, later causing a hurricane to crash against the coast of Odus.&nbsp;
"Five" he says with finality, the word bright with fire, charring rock
that silently screams for release.
<p>
Five platinum pieces, an immense sum for a dragon.&nbsp; He is quite
proud of his hoard, and guards it jealously.&nbsp; As he contemplates counting
his slightly larger pile of gold pieces, he hears a sound.&nbsp; An
intruder!
<p>
Out into the hall he and his guards go, where they see but a single
intruder.&nbsp; A human, and a foolish one at that.&nbsp; As they
approach, confident of victory, the intruder...dies.&nbsp; The ancient
beast ponders eating him anyway, but after reflection decides that food
truly loses its flavor when cooked after death.&nbsp; Only freshly charred food,
with a heart still beating to infuse the black flesh with salty
blood, is worthwhile.&nbsp; His feasting will have to be delayed.&nbsp;
Plus this one died for no apparent reason - it could be diseased.
<p>
He turns back to his lair, his mind returning to the shiny prospect of
counting gold.&nbsp; As he approaches the smooth cavity his lounging has worn into the
rock from centuries of small adjustments, he again hears a noise...the
intruder!&nbsp; It is not dead after all!&nbsp;
He, an ancient dragon of immense experience and wisdom, fooled by a
mere human!&nbsp; Anger boils in his veins, as he vows to destroy the
intruder this time, with no chance of doubt.&nbsp;  As he builds up steam
to destroy the fakir, once again, the intruder falls down dead.&nbsp;
Despite this having just happened mere seconds ago...the dragon is again
fooled.&nbsp; And again.&nbsp; And again.&nbsp; <b>And again</b>.
<p>
Eventually, after dozens of apparent deaths by the same intruder, the dragon and his guards become
separated.&nbsp; Now the dragon is too far from his guards to hear their
battle cries.&nbsp; The lone guards are taken to a larger, hidden group,
where they are disposed of.&nbsp; The dragon notices his guards are missing,
yet thinks nothing of it.&nbsp; Again he hears a noise, and he, with
his smaller force of guards, heads out to investigate.&nbsp; The
same intruder!&nbsp; Die intrude...oh, he is already dead.&nbsp; Against
all logic, the dragon is again satisfied, and heads back to his lair.&nbsp;
Having seen the same intruder "die" more times than he has coins to count,
the dragon still fails to understand the deception.
<p>
Now the dragon is all alone.&nbsp; Without his guards, the dragon is
easily defeated by the large force of deceptive intruders.&nbsp; His flesh
is cooked into hearty dragon steaks, and his hoard stolen without remorse.
