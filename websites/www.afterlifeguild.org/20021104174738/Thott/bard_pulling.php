<center>
  <div class="menutitle"><font size="+3"><b>Bard Pulling</b></font></div>
  <font size="1">by <a href="mailto:ThottWeb@Afterlifeguild.org">Thott</a></font>
  <p>
</center><table border=1><tr><td>
Recently, a monk said that they feared their pulling role would
be reduced in PoP.&nbsp; They said that ever since the first time
they did fire giants, and he (the monk) was called upon to split them,
that his role of puller was born.&nbsp; This is my response.
</td></tr></table>
<p>
It's funny that you mention fire giants as where your role began -
it's where mine ended.
<p>

I pulled in crushbone.&nbsp; I pulled in perma.&nbsp; I pulled in sola.&nbsp; I
pulled in solb.&nbsp; I pulled in lguk.&nbsp; I pulled to the efreeti.&nbsp; I pulled
everywhere...until fire giants.
<p>

I pulled using lament.&nbsp; It's not particularly reliable (especially
on high level mobs), and sometimes I'd be forced to pull back more than one,
but it worked fairly well until FG's.&nbsp; FG's are flagged melee range
casting only.&nbsp; They are impossible to lament.
<p>

Your role began because my role ended.&nbsp; Pulling is crowd control.&nbsp; I can
either eliminate mobs from the fight by not pulling them, or eliminate
mobs from the fight using mes/charm/fear.&nbsp; Either way, that's my role -
I eliminate mobs from the fight.
<p>

Then came monks.&nbsp; Monks suddenly became not only good at CC - but the
best at it.&nbsp; Better than bards.&nbsp; Better than enchanters.&nbsp; Better than the
classes whose primary role is crowd control.&nbsp; Monks stole our ability.
<p>

Yes, it's been this way for some time.&nbsp; But that doesn't make it
right.&nbsp; Necros were the primary damage dealing class for some time, and
they were fixed - because it didn't fit with their intended role.&nbsp; My necro is
now dead to me, because while its intended role works flawlessly (solo),
the role I picked it for does not (superior [admittedly unbalanced] raid damage).
<p>

At this point I don't expect to see monk FD nerfed.&nbsp; I would
just like to see bard/enc CC raised to the same level as monks.&nbsp; If a monk
can remove a level 75 mob from a fight, well so should a bard/enc.&nbsp; If
a bard/enc can't remove a level 75 mob from a fight, well then, neither
should a monk.
<p>

Bards have always done CC partially through pulling, and partially
through in-group mes/charm/fear.&nbsp; If any class should be #1 in the area
of pulling, it should be bards.&nbsp; Again, I don't expect this to happen,
because monks would be upset.&nbsp; Monks were never intended to be a CC
class.&nbsp; Monks were never intended to be gods, #1 in multiple roles.&nbsp; But
they are, and there's little likelyhood of it changing.&nbsp; In one role,
the role of pulling, bards should at least be just as good.
<p>

Let the puller, be it bard, monk, or SK, be decided on the basis of skill,
not just by class.&nbsp; Currently on live servers, if I need to have some mobs
split, I have a monk do it.&nbsp; Even if the monk has no pulling experience,
no skill, and no knowledge of the area, while I have it all - I still have a monk
do it.&nbsp; I tell the monk what to do, step by step, like a puppet, but it's
still the monk that does it.&nbsp; When the monk finally pulls mobs to the
group, I rarely can do anything with them.&nbsp; I certainly can't charm or fear
them, because those abilities cap at 51 and 53 respectively.&nbsp; Occasionally,
I can mes one of the easy ones.&nbsp; Is this my role?&nbsp; The monk
eliminates a dozen red mobs, and I eliminate one blue one?
<p>

If you think your role will be gimped in PoP, consider that your
role is mine, and was never yours to begin with.&nbsp; My role was stolen,
and has been further gimped in one way or another with every expansion
(high MR mobs that always resist lull in Velious, and immune to lull
[due to harmony] mobs in Luclin).
<p>

While you may not like being forced into the role of damage dealer,
consider this: it's better than the role of mana battery or AFK buffer,
and that's what your role theft has reduced bards and enchanters to.
<p>

You've said it yourself - pulling is an active ability.&nbsp; Manasong is a
passive one.&nbsp; Damage dealer at least isn't a pure support class.&nbsp; It's
the active to support change that has bards and enchanters unhappy.&nbsp; Both
picked their class to CC, and CC is an active, primary role.&nbsp; Both classes
are forced into support roles on raids.
<p>

All the frustration you fear you will experience, and more, we've been
experiencing for years.&nbsp; We've been experiencing this frustration
because monks have stolen our abilities and claimed them as their
own.&nbsp; With FD gimping in PoP unlikely, yet high level mobs
assured, our roles are still clearly removed in deference to your stolen
one.&nbsp; It's hard to feel even a small amount of pity.&nbsp; Monks
feel none for us.
<p>
