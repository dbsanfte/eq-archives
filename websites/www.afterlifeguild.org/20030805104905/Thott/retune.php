<p>
What does this show?  It shows that bards need a 120% boost at level
60 to keep up with a naked wizard.&nbsp; From the sustained damage/sec
graph above, it's clear that the wizard damage curve is similar to the
druid damage curve, which is similar to the damage curve for <i>all
other classes</i> except for bards.
<p>
The main point here is clear: bards were not overpowered before these
changes.&nbsp; Quite the opposite.
<p>
<h4>Alternate Advancement</h4>
Bards improved through AA, just like other classes.&nbsp; A bard with
epic and Instrument Mastery 3 has a total instrument boost of 140% -
high enough to be above wizards on the above graph.&nbsp; However, at
the same time, wizards gained 15% damage through critical nukes and +3
mana/tick, which places them again, above bards.&nbsp;
<p>
Bards get crits, which would help...except that crits only work on
direct damage, not damage over time.&nbsp; Essentially all bard damage
is of the damage over time variety.&nbsp; More so than any other class,
this flaw impacts bards.&nbsp; Crits should affect dots, but they don't,
because it's somehow hard to code.&nbsp; I'm normally a big supporter
of SOE programming skill, but come on, <b>hard to code</b>?&nbsp; Why
wasn't this thought up sooner?&nbsp; Would melee crits have been added
to the game if it was <b>hard to code</b> for monks, and all melees but
monks would get it?&nbsp; I think not.
<p>
Thank you for forgetting bards.&nbsp; Can we have another?
<p>
<h4>Melee Rebalance</h4>
Bards, and all melees, were changed during the melee rebalance.&nbsp;
Bard melee has the same power curve problems as bard songs, and the two
don't interact in such a way to create a better curve, i.e. X+X is not
as good as X^2.&nbsp; So bard melee was fixed, along with other hybrid
melee, with bards set to 65% of warrior damage.
<p>
However, while 65% was the goal, and 65% was, in effect, promised,
bards do not do 65% of warrior damage.&nbsp; All parses show bard
damage at 55-60%.&nbsp; This is again, something that should be fixed,
yet hasn't been.
<p>
I sent in parses showing this problem, oh, 8 months ago.&nbsp; Well
before the caster rebalance.&nbsp; Since I typically group with a rogue, most of the parses were relative to rogue damage.&nbsp; They could easily have been interpreted as saying rogue damage is too high, not that bard damage was too low.&nbsp; After these parses, which were acknowledged by the team, data came out showing that monk damage was too high relative to rogue damage.&nbsp; So what happened?&nbsp; <b>Rogue damage was upgraded.</b>&nbsp; Yes that's right, when I showed that bards were doing too little damage relative to rogues, rogues were upgraded.
<p>
Is there a word that describes "actively ignored"?&nbsp; Why yes, yes
there is: bard.
<p>
<h4>Slows, Cure Poison and Cure Disease</h4>
Bards have cancel magic, cure poison and cure disease.&nbsp; However,
bard cure disease and cure poison is broken, and has been for a long
time (years?  decades?  I can't keep track).&nbsp; It worked, badly,
for a short period of time, but somehow, that technology was broken or
lost.&nbsp; I'm sure if someone asked the developer that made the change
that broke this, his response would be "bards?&nbsp; Oh, I hadn't thought
of bards."
<p>
This is a new issue because slows are now disease based, not magic
based.&nbsp; Bards have thus lost the ability to remove slows, when
before, we had it.&nbsp; In fact due to the way slows overwrite hastes,
it was easy before for bards to remove slows, as easy as it is now
for casters.
<p>
Once again, it didn't occur to anyone to gauge the effect of this
change on bards.&nbsp; Other hybrids had remove disease added to their
lineup when the slow change was made (rangers and SK's), but bards
(surprise!) were forgotten.
<p>
<h4>Immunity Flags and the Christmas Light effect</h4>
Ahh, immunities.&nbsp; What mob is complete without at least one immunity?&nbsp; Immunities (slow/snare/stun/mes) are common in Luclin.&nbsp; Naturally bards can't slow a slow immune mob, or snare a snare immune mob...but bards have special problems.
<p>
The way these effects are coded, a mob that is immune to slow isn't just unslowable, it fully resists <b>any spell that contains a slow effect</b>.&nbsp; Like christmas lights, if one goes out, they all go out - if a mob is immune to one effect out of many, all the effects bounce.
<p>
Does this cause problems?&nbsp; Not to non-bards, and we've
already established that only non-bards are considered for any design
change.&nbsp; Bard songs are almost universally multi-effect.&nbsp; There
are <b>no</b> bard songs that include snare that don't also include
slow.&nbsp; Thus a mob immune to slow is also immune to <b>bard</b>
snare.&nbsp; Other snares work fine, only bard snares are affected.
<p>
Despite bringing this up many times, both before and after Luclin,
the problem still exists.
<p>
This affects mes spells as well.&nbsp; Bard mes and mob movement
(pushback) song lines are combined into one.&nbsp; Thus the best way for
a bard to move a mob is to use a mes.&nbsp; However, mobs immune to mes
are now immune to the pushback.&nbsp; This wasn't originally true.&nbsp;
In the original code, mes would land, but do nothing.&nbsp; When the
mes immunity message was added, it was aimed at enchanters, not bards,
and enchanters don't have any multi-effect mes spells, so there was
nothing to worry about.&nbsp; But bards do.
<p>
In this case, bards were both forgotten <b>and</b> ignored, both at the
same time.
<p>
<h4>Pulling</h4>
Bards are a pulling class.&nbsp; Most bards that still play do so
because they enjoy pulling.&nbsp; Over time the bard pulling ability
has deteriorated, to be replaced by monk (?) pulling ability.&nbsp; All
classes that are designed with pull (calm) spells also have crowd control
ability, be it root, mes, charm, etc.  Pulling is no different from any
other form of crowd control.&nbsp; Monks don't fit in this category, nor
do SK's, because neither monks nor SK's are a crowd control/pulling class.
<p>
As a bard, pulling one mob is no different from pulling three mobs and
mesing two.&nbsp; Pulling one is simply more effecient.&nbsp; As best I
can tell, designers that didn't understand this concept, that pulling and
crowd control are one in the same, allowed FD to take over as the pulling
method of choice.&nbsp; Or rather, gave in to player demands that this
unintended ability be left intact, despite the affect it has on the game.
<p>
So what has changed recently?&nbsp; There are now more FD-required areas
than ever before.&nbsp; The cost for failing to pull is now greater than
ever before.&nbsp; And mobs the bard expects to be fighting are now too
high level to mes or charm, where they never were before.
<p>
And now what do we see in Luclin?&nbsp; My favorite: <b>lull-immune
mobs</b>.&nbsp; All I can say is whoever designed this flag clearly
does not play Everquest.&nbsp; If lull is a problem, FD is 10x the
problem.&nbsp; I am aware of the general feeling within the dev team
that lull should never have been put in the game.&nbsp; This opinion
came about during beta, when lull was used to break apart otherwise
impossible encounters (monks: sound familiar?).&nbsp; At the time,
lull was unresistable.&nbsp; Now, lull is easily resisted, rarely works
on high blue or better mobs, and is far overshadowed by FD - which IS
unresistable, and is all that was bad about the original lull, and more.
<p>
So now, if there weren't enough areas where FD was absolutely required,
and lull was useless...there are now areas where mobs are flat-out
immune.&nbsp; Wonderful.&nbsp; It's always nice to see people get kicked
when they're down.
<p>
What else changed?&nbsp; Monks originally had no long range attack,
but bards did.&nbsp; This was a core disability for monks, the same as
magicians not having crowd control capabilities.&nbsp; But designers
made mistakes.&nbsp; 200+ range throwing items were added to the game,
not realizing how this would affect monks (side note: prior to
the current team, monks were often forgotten, just like bards - the
difference is, every time monks were forgotten, they were inadvertantly
made more powerful).&nbsp; Because 200+ range throwing items existed,
which had to be crafted, there is now a <b>250</b> range item for monks
that not only destroys their primary, designed in, pulling weakness but
makes them better at dispelling than any other class, even enchanters.
<p>
Luclin is also the expansion of trap mobs.&nbsp; A monk can FD if a trap
is set off, and after a minute or so, the trap mobs will despawn.&nbsp;
A bard can only die, or bring the mobs to the group and potentially
kill everyone.
<p>
For those wondering who lives in the mansion I mentioned above, go see
who's living at 1 Monk Lane, Monk City, MK.
<p>
The role of puller has, in most cases, been stripped from the bard,
and given to the monk.&nbsp; It's for that role that I personally took
the class.
<p>
<center><h2>Old Problems</h2></center>
<h4>Instruments</h4>
Boosting instrument bonus through epic and AA's is fine for the weapon
wielding bard, but the instrument using bard is all but dead.
<p>
Instruments have improved a grand total of <b>8%</b> since <b>before</b>
Kunark.&nbsp; Instruments should be twice as effective as singing,
but instead, the best instrument, for a bard with IM3, is only 33%
more effective - a far cry from 100%.&nbsp;
<p>
<h4>Epic effect</h4>
There is only one item with innate singing boost: the bard epic.&nbsp;
While other classes get improved post-epic weapons, bards are limited to
one weapon alone.&nbsp; No other single item is as important to a class
as the bard epic is to bards.&nbsp; All bard weapons should have similar
effects, for all level ranges, following the bard bonus graph above.&nbsp;
And just like other classes, there should be bard bonus items better
than epic, for bards that have progressed past that level of content.
<p>
<h4>Desperate Dirge</h4>
Desperate Dirge doesn't improve with greater modifiers, unlike other
songs.&nbsp; Thus, these days, the 5 minute recast Bombastic Bellows
(up to 799 damage with max modifiers) is better than the once-per-day
Desperate Dirge (495 damage).
<p>
<h4>Blinking Buffs</h4>
The buffs blinking when they're about to fade feature was added during
beta.&nbsp; It immediately began driving bards insane, so a fix was put
in.&nbsp; Instead of making code that said if(bard song) don't blink,
instead the code says if(bard) don't blink.&nbsp; <b>No</b> effect blinks
on a bard, regardless of who cast it.
<p>
Bards frequently have over 10 buffs.&nbsp; When one goes away, it's
hard to tell which one it was.&nbsp; It's an even bigger problem when
the buff that's going away is invisibility, because while there is a
warning when invis goes away early, there is none when it reaches max
duration.&nbsp; Non-songs should blink on bards when they're about to
go away, or at minimum, any song with max duration greater than a bard
song should blink before it goes away.
<p>
<h4>Bard Invis Range</h4>
Bard group invis range is tiny.&nbsp; Six people crammed in a closet
tiny.&nbsp; The only way to keep within range is to stand so close that
it's impossible to tell whose armor you're wearing.
<p>
It's short because there is just one song, and sometimes, you want to
be invis, but not hit the group (and more importantly, the magician in
the group, that will lose her pet).
<p>
The solutions to this are obvious.
<p>
<h4>Instruments and Slow/Haste songs</h4>
Ages ago, when GZ still worked at Verant, someone sent in a question
asking about McVaxius` Berserker Crescendo, wondering why the haste effect
didn't increase when an instrument was equipped.&nbsp; GZ responded that
the haste does increase with an instrument, that he had tested it himself,
and left it at that.
<p>
But haste doesn't increase with an instrument - anymore.&nbsp;
It did in beta, and that's when GZ tested it.&nbsp; Yet even so, more
songs were added in Kunark which are based on instrument haste/slow,
which, like McVaxius` Berserker Crescendo, don't work.&nbsp;
<p>
More history is available on this bug in <a href="haste_stacking.php">Haste Stacking</a>
<p>
<h4>Gem animation based on framerate</h4>
Bard gems fade in and out when starting/stopping a song.&nbsp; This
animation is not based on real time, it's based on framerate.&nbsp;
A bard that's getting 1 fps (Luclin anyone?) is in bad shape.
<p>
<h4>Bard/Enc mes overwriting</h4>
Bard (short duration) mes overwrites enchanter (long duration) mes.&nbsp;
Bug!&nbsp; It should be the other way around.
<p>
<h4>Instrument animations when not holding an instrument</h4>
If a bard has <b>any</b> boost to song power, through AA's, or epic, and
sings an instrument song, the bard does the instrument animation.&nbsp;
Often poking whatever weapons he is holding through his head.&nbsp;
It looks quite painful.
<p>
I first used the epic with modifier on test server, during a Test
of Tactics.&nbsp; A developer contacted me and had me test a few
things.&nbsp; One of the things tested was, does it do an animation when
singing an instrument assisted song?&nbsp; And sure enough, it did.&nbsp;
The developer told me it would be fixed before the effect was patched
in.&nbsp; That was over 1.5 years ago.&nbsp; I'm still waiting for
the patch!
<p>
<h4>Snare breaking randomly</h4>
Bard snares where changed a year or two ago to match all other snare
effects, meaning they no longer would break randomly, but would last
the full duration.&nbsp; Even with this change, bard snare effects are
the least reliable of them all, because the snare has to be reapplied
every 12 seconds.&nbsp; Random snare breaks happen when a mob resists
the snare effect that's active, for bards, this is always true: if the
mob resists the reapplication of the song, it will break snare.
<p>
The level 54 snare, Selo's Assonant Strane, was missed when this change
was done.&nbsp; It still breaks randomly, giving it a double penalty
where other classes have none at all.&nbsp; I was told it would be
fixed, but since then, the person that said that has moved on to another
department/job.&nbsp; I can only assume this was lost in the shuffle.
<p>
<h4>Charm</h4>
Bard charm should be refreshable, like any other bard song.&nbsp;
Originally bard charm was, however, a change was made in beta to prevent
players from casting harmful spells on charmed mobs, to counter the
(obvious) exploit of charming a mob then killing it while it can be
controlled.&nbsp; Charm is a harmful spell.&nbsp; Thus charm by default
will bounce off a charmed mob.
<p>
Charm should be refreshable on a charmed mob, not just by bards,
but by enchanters too.&nbsp; More information on charm a <a
href="bard_charm.php">Bard Charm</a>.
<p>
<h1>Wrapping up</h1>
Take a look at the things on the list.&nbsp; Not a single one of them
is a wish list item.&nbsp; Every one of them is a change to the game,
where bards were left out, or a straight up bug.&nbsp; A similar list
for most classes would be empty.
<p>
Bards have been ignored or forgotten, time and time again, from the very
beginning.&nbsp; Each time this happens, the effects accrue, creating
an ever greater gap between bards and non-bards.&nbsp; A simple
fix now won't solve the problem.&nbsp; Bards need all the lost attention
from the begining of time, scraped up, pulled together, and applied all
at once.&nbsp; Not to make the class better than other classes (which
nobody sane wants - overpowered classes are just begging for future
backlash), but just to make bards balanced with other classes.&nbsp;
Playing a balanced class is a reasonable request, and a reasonable
expectation.
