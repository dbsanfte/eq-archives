<center>
  <div class="menutitle"><font size="+3"><b>Bard Retune</b></font></div>
  <font size="1">by <a href="mailto:ThottWeb@Afterlifeguild.org">Thott</a></font><br><br></center>
Bards: the forgotten class.&nbsp; When Target Group Buff was on test,
I asked a developer if it would work on bards.&nbsp; The response?&nbsp;
"Bards?&nbsp; Hmm, I hadn't thought about bards."&nbsp; <b>Bards have more
group buffs than any other class and you DIDN'T THINK ABOUT BARDS?!?</b>
<p>
This attitude is prevelant throughout the entire company.&nbsp; "Bards
were a mistake."&nbsp; "The next game we make won't include anything like
bards."&nbsp; "Bard code is a mess."&nbsp; "I've never understood bards."
<p>
Well, I hate to break it to you...but bards are here to stay in
Everquest.&nbsp; If bards are hard and cause problems, why, it seems to
me, they should be <b>the first thing considered in any design change</b>,
instead of "What?&nbsp; We broke bards <b>again</b>?!?"
<p>
Long before the melee retune, I heard rumors that it was happening.&nbsp;
Long before the caster retune, I heard rumors it was happening.&nbsp;
I have heard <b>nothing about a bard retune happening</b>.&nbsp; My only
conclusion is that <b>the bard retune is not happening at all</b>.
<p>
Bards don't ask for much.&nbsp; When other classes ask for the moon,
bards just want their sink fixed.&nbsp; Below is a simple list of things
that have changed in the game, that forgot bards.&nbsp; Or things that
are just plain broken.&nbsp; 
<p>
We're not looking for incredible power,
or to be better than anyone else.&nbsp; Where most classes are in a
four bedroom house, with a few living in a mansion, we just want the
four bedroom house.&nbsp; The hut we currently live in is drafty.&nbsp; And that sink, it's driving me crazy.
<p>
<center><h2>Recent Changes</h2></center>
<h4>Flowing Thought</h4>
Since Luclin shipped, FT has increased from 1, possibly 2, to 15.
&nbsp;FT 0-&gt;15 is an enormous leap in power for casters and
hybrids...yet FT doesn't work on bards.&nbsp; FT is a straight up, across
the board, large jump in power for all casting classes...except bards.
<p>
Has mob power increased along with FT?&nbsp; Nope.&nbsp; Many times
bards/enc have joined groups, adding mana needed for the group to kill
without downtime.&nbsp; Now that mana is available to all.&nbsp;
Bard/enc mana is no longer necessary to avoid downtime.
<p>
Bards lost relative power due to FT itself, and lost power due to
secondary effects from FT.&nbsp; What changed with bards?&nbsp; Nothing,
bards were forgotten.
<p>
<h4>Focus Items</h4>
Focus items work on all robed casters, priests, and hybrids...but not
bards.&nbsp; Why?&nbsp; Some focus item effects make no sense on bards,
but others do.&nbsp; Even if all focus items worked on bards, the end
result would be bards gaining less power than all other affected classes,
yet still, bards can't use focus items.
<p>
Once again, bards were forgotten.
<p>
<h4>Horses</h4>
Are there items for bards that give complete heal?&nbsp; How about
backstab?&nbsp; Defensive discipline?&nbsp; How about items that give
these abilities at 97% strength?&nbsp; Yet there are now items that
give 97% of <b>max</b> bard speed.&nbsp; Did bards get anything in
compensation?&nbsp; Nope.
<p>
Horses are, overall, a good thing.&nbsp; The world is bigger, and it
takes longer to get anywhere.&nbsp; Horses help this.&nbsp; But guess
what: <b>the world is bigger for bards as well</b>.&nbsp; Bards should
be much faster than horses.&nbsp; And bards would be, except for the
<a href="movement_cap.php">Movement Cap</a>, which limits maximum bard
speed to that of a twinked level 46 bard.
<p>
Bards have been aware of this problem from the beginning, and the
community expectation was that the movement cap would, at some point,
when programmers had time and someone remembered the bard class existed,
be increased to the new maximum value.&nbsp; Just as it was originally
set to the maximum value.
<p>
Those hopes were destroyed in the last patch.&nbsp; Much fiddling was done
with bard caps...yet the movement cap is untouched.&nbsp; A programmer
took time out to change the bard cap code and test the results...yet
still didn't make the change.&nbsp; Before we thought there wasn't
enough programmer time.&nbsp; Now it's clear that there is no desire to
do it at all.&nbsp; Top bard speed will forever be 3% faster than non-bards.
<p>
Bards are a patient lot.&nbsp; Bards rarely complain, except in response
to the monthly "broken bard patch" programmer jokes.&nbsp; Yet time
after time, bards are left out, as other classes are changed.
<p>
<h4>Bard Power Curve</h4>
Many have read what I've written on the nature of the <a
href="powercurves.php">bard power curve</a>.&nbsp; The bard power curve
is flat.&nbsp; When all other classes double in power over a level range,
bards do not.&nbsp; This gap increases, level after level, resulting in
a power problem at level 60.
<p>
When presented with a concept that doesn't make sense, it's human
nature to invent reasons to explain it.&nbsp; This is a good process
for the human race.&nbsp; Thanks to this process I'm not eating rocks
and twigs for breakfast while sitting in a cave.&nbsp; But in this case,
the process leads people to come up with reasons like:
<p>
<li>Bards don't use mana
<li>Bards can cast while moving
<li>Bards have cool instruments
<li>etc.
<p>
Things that clearly are true about bards, and set them apart from other
classes...but don't really explain away the problem.&nbsp; Bards have
these same attributes at level 20.&nbsp; Whatever cost there is to pay
for these attributes has already been paid.&nbsp; If bard effects are 50%
as strong as other effects at level 20, and other effects double in power,
then bard effects should double in power too.&nbsp; After this doubling
bard effects will again be at 50%.
<p>
Instead, bard effects don't double.&nbsp; Here are two power curve graphs,
comparing the two easiest functions: doing damage and healing.
<p>
