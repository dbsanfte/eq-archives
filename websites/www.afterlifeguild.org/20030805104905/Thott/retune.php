</center>
<p>
Note that in both cases, the bard curve is on par with other classes at low levels...and falls further and further behind as levels increase.
<p>
What these graphs don't include is the song boost from bard epic.&nbsp; If you've ever wondered why adding 80% boost to bard songs through a single item didn't turn out to be overpowered, the above graphs show why.
<p>
This graph shows the needed song boost for a bard to keep up with power gained by a naked wizard.&nbsp; The graph sets level 20 as the baseline, so at level 20, bards and wizards are assumed to be equal in power, and balanced.&nbsp; If past that point, wizards double in power and bards were to stay the same, then the graph would show a 100% boost is needed.
<p>
