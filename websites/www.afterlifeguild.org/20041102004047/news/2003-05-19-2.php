[Mon May 19 01:16:38 2003] Xegony says, 'Mithaniel, Karana please record what you see here.  With your good nature I am sure that your records will be impartial to any side.  We must record this for the ages and learn from our mistake of allowing Zebuxoruk to gain so much power that is easily passed on to mortals.'<br>
</font>
<p>
If only we saw this sort of thing more often!&nbsp; Fancy polygons
and big numbers are nice, but it's the lore that brings a game alive,
exciting the mind and imagination.&nbsp; It's good to see this sort of
thing in EQ, and I hope to see more of it in the future.&nbsp; If only
Plane of Earth/Air had lore like this, those zones might make sense.
<p>
We've spent thousands of man hours on this event, and today we spent
hundreds more.&nbsp; We failed, for one reason or another (including
at least one time today when the event bugged) - and it was getting
late.&nbsp; The end of our raid time was approaching, but we had time
for one final attempt...and we won!
<p>
After winning, mobs appeared on the other side of the doors (mobs that were
not there before - hence any door exploits are pointless.  Good design!).
<p>
We then saw this message:
<p>
<center><img src="/news/2003-05-19/1_hour_left.jpg"></center><p>
So much for sleep!
<p>
After an hour, an hour past the end of our normal raid time, with work for many starting early the next day, we won!&nbsp; Second wave, beaten!
