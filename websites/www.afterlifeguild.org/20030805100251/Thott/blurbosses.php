<h2>Background</h2>
With PoP some bosses gained a new ability: complete mem blur, every 15
seconds or so.&nbsp; When a boss is flagged this way, no matter how much
it hates a particular player or group of players, every 15 seconds he
completely forgets.&nbsp; This means all hate is reset and the next person
to irritate the mob gets its full attention.
<p>
<h2>Blame?</h2>
There's a rumor floating about that I had something to do with this
system: I did not.&nbsp; The only thing I can claim credit for is a
general statement that MT (Main Tanking) is bad.&nbsp; The solution was
not my idea.&nbsp; That said, I don't think it's a bad one.
<p>
<h2>Normal Boss Fights</h2>
Normally a boss mob will quickly focus on the MT, and stay focused on it,
throughout the entire fight.&nbsp; This is clearly the dumbest thing a
mob could be doing, since the MT is not only the best equipped target and
thus the hardest to kill, he also has an army of clerics healing him in rotation.&nbsp;
Yet mobs will spend literally hours (in game time)
beating on just such a target, while a swarm of insect players behind
him whittle away his life, leading to his unavoidable death.
<p>
<h2>Blur Fights</h2>
A blurring mob is different.&nbsp; Instead of focusing on the one best
defended player in the horde, the boss mob resets and tries someone new
every 15 seconds.&nbsp; He at least has a chance of attacking
intelligently, and a chance of actually killing something, instead of
falling like his common bretheren without swatting a single fly.
<p>
<h2>Plane of Air</h2>
The prototypical blur bosses, the ones that people think of when blur
bosses are mentioned, are the Avatars in Plane of Air.&nbsp; These are
not hard mobs.&nbsp; An individual whose forebrain has been scooped
out and made into melon balls may conclude that since these are blur
bosses, and since these are not hard fights, that therefore these are not
hard fights because they are blur bosses.
<p>
Those of us with intact forebrains can see the obvious flaw in this
logic: if the bosses did not blur, they would be beating on the best
defended target of them all, instead of random targets such as dress
wearing librarians and tree hugging pacifists.&nbsp; How can this make the
fight <i>harder</i>?&nbsp; Answer: it can't.&nbsp; Just because something
makes an encounter harder doesn't necessarily mean the encounter is
hard, just as two layers of tissue paper are stronger than one but
aren't strong.&nbsp; Logic may appear easy, but clearly, it's not easy
for everyone.
<p>
<h2>Conclusion</h2>
Blur bosses are good, Plane of Air bosses are too easy, and everybody likes melons.
