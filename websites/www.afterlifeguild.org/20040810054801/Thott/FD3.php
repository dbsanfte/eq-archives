  <div class="menutitle"><font size="+3"><b>Feign Death</b></font></div>
  <font size="1">by <a href="mailto:ThottWeb@Afterlifeguild.org">Thott</a><br>2003/10/25<br></font><br></center>
Wontoo's feet hurt.&nbsp; Day after day, week after week, month after
month, Wontoo patrolled the hallways of his tribe's warren.&nbsp;
Goblin guards were never allowed to rest, for goblins know nothing of labor
unions.&nbsp; But he was happy to do it, and he loved his tribe; but, his
feet did hurt.&nbsp; He was secretly hoping to find some strangers/enemies
(both one word in goblin) just so he could die and rest his feet, if
only for 25 minutes.&nbsp;
<p>
Even more than a rest, Wontoo wanted to <i>actually kill</i>
a stranger/enemy, but such a thing was so rare as to almost be
unthinkable.&nbsp; Humility forced him to accept that if he did manage
to kill anyone, it would only be with the help of dozens of his friends,
which only happened when the stranger/enemies allowed it.&nbsp; A great
many goblin discussions centered around this one topic, and why sometimes
strangers would do things that resulted in a large number of goblins
in one place, which usually then led to a discussion of why goblins
don't do this every time, with no final reason ever discovered.&nbsp;
Perhaps goblins could get further on this subject if they stopped trying
to communicate only in rhymes, but such is the way of goblins.
<p>
Such thoughts weren't for Wontoo.&nbsp; He wasn't a philosopher, he was a guard, and his
job was to roam the halls and look for strangers.&nbsp;
And so he roamed, and roamed, and roamed.&nbsp; Just when he thought he could stand the pain no longer, and was thinking
of finding a nice corner and slashing himself for more damage than he
could ever hit anyone else, he saw what he lived to see: an enemy!&nbsp;
He shouted out his battle rhyme and attacked, and, despite all
logic, <i>the stranger died</I>!&nbsp; Wontoo hadn't actually made it to the stranger, and in fact there was no obvious reason why the stranger died, yet Wontoo was ecstatic.&nbsp; A stranger dead,
and killed by (well, near) Wontoo!&nbsp; The others would never
believe it, but he was intent on telling them anyway, using his very best
rhymes.&nbsp; After mentally crafting a few stanzas, he moved on his way,
lost in the slow thought of goblins.
<p>
Many rooms and a handful of stanzas later, Wontoo suddenly <i>knew</i>, without
knowing why, that the stranger he killed earlier was no longer dead.&nbsp;
He turned and ran back, calling for help in each room he
passed through, until he reached the stranger.&nbsp; Even though the
stranger was no longer where he had died before, or even anywhere
near it, Wontoo innately knew exactly where he was.&nbsp; No amount of
deception could fool this innate psychic link.&nbsp; Wontoo couldn't read
or write; he couldn't rhyme that well or run very fast; he couldn't even see more
than a dozen feet ahead of him no matter where he was at; but he knew
exactly where this enemy was at all times.
<p>
Wontoo arrived at the stranger with two score of his tribe with him,
but as soon as he came into view, the stranger again, for no obvious reason, died.&nbsp; This
confused Wontoo - he could think of no way to add this to his earlier
rhyme, and regretfully resolved to start over.&nbsp; Without any enemies
to fight, Wontoo and his tribe turned around and went home.&nbsp;
<p>
Much
later, Wontoo again <i>knew</i> the stranger wasn't dead, and again
called for help and headed towards him.&nbsp; Stranger still, <i>only
Wontoo</i> knew that the stranger was no longer dead - the rest of his goblin family
were completely ignorant.&nbsp; Things then got even stranger.&nbsp;  This
time, before actually seeing the stranger, Wontoo <i>knew</i> the stranger
was again dead.&nbsp; How he received this psychic knowledge is unknown;
perhaps it is a goblin gift; but then why was only Wontoo affected when
the stranger stopped being dead?&nbsp; And why did <b>all</b> goblins
know when he suddenly died again, without even one goblin actually seeing
it?&nbsp; And why wasn't he thinking all of this in rhymes?&nbsp; It made little sense to little Wontoo.
<p>
There was only one thing Wontoo knew for certain: his feet hurt.
