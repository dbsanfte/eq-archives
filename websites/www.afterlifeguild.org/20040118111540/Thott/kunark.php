Which further illustrates that there isn't enough content in Kunark for
51-60, which we already knew. &nbsp;But it's more than just a lack of
content; even if there were 100 different interesting dungeons, some
players are going to stay in the first one they come to until either 1)
the game makes them move (because killing there is no longer beneficial),
or 2) they burn out on EQ and quit.
<p>
<h3>Conclusion</h3>
Because of the Kunark problem, many people are burned out on the idea of
levelling in general. &nbsp;Pain, once experienced, is avoided in the future.
&nbsp;At this point it will be difficult to overcome this aversion in future
games.
<p>
UO had no level system, and very little progression in their skill system.&nbsp;
There was thus little barrier to consuming content, making UO a short term game.
&nbsp;The long term selling points are PvP and Community, both of which are nice,
and both of which are worthwhile. &nbsp;There are plenty of games (FPS) that
are pure PvP. &nbsp;There are plenty of things that are pure Community (IRC).
&nbsp;Both approaches are compelling to the people that enjoy them.
<p>
Levels (or a good substitute) add a great deal to any game. &nbsp;The
key points being the limiting of consumption of content (and this is
far more than just saving development time, the wait and anticipation
is often more enjoyable than the actual consumption, true entertainment
comes from within) and the game world as a dynamic place through the
change of the player.
<p>
Still, as I said before, many are burned by Kunark. &nbsp;Most games currently
under development are shooting for the persistent PvP and 3d IRC crowds more
than true RPG world exploration and character development, simply because of
the negative reaction most players have towards levelling after Kunark.
<p>
It will be another generation before levels are seen as useful again.
&nbsp;At that time, I hope this paper is useful.
