<h3>Background</h3>
It is assumed that anyone reading this paper has at least heard of
Everquest. &nbsp;Everquest is a Massively Multiplayer Online Game
(MMOG). &nbsp;It has a level based character advancement system. &nbsp;It
takes from 40 to 100 days of /play time (each day is 24 hours spent
playing, so 10 days /play is 240 hours) for someone's first character
to get to the original level cap of 50.
<p>
Kunark was the first expansion shipped for Everquest. &nbsp;One of the
features of the expansion, for many people the main feature, was an increase
in the level cap from the old cap of level 50, to a new cap of level 60.
<p>
<h3>So what's the problem?</h3>
Because of the time it took for the first expansion to come out, a great
many people were at the level 50 cap before Kunark. &nbsp;All were ready and eager
for more levels. &nbsp;All of them naturally expected to hit level 60 in very little
time. &nbsp;After all, each had obtained level 50, and level 60 is simply 20% more.
<p>
The major concern before Kunark shipped was that everyone would be
level 60 in a week. &nbsp;Once level 60 they would have little to do,
boredom would set in, and many would leave the game. &nbsp;The designers
solved this by making the new levels take much, <b>much</b> longer to
pass through than the old levels.
<p>
Kunark is frequently pointed to and described as a major mistake.&nbsp;
People say that it takes too long to level to 60, and that level 60 is
"required" to play the game. &nbsp;Levelling they say is boring, and they
want to be done with it, and be 60, so they can "play the game". &nbsp;In
a nutshell, this is the <u>Kunark Problem</u>.
<p>
<h3>The Purpose of Levels</h3>
Why do levels exist in a game at all? &nbsp;Lets consider the early game of most any level based RPG.
&nbsp;Generally the player starts at level one,
walks outside, and starts killing mobs around his level. &nbsp;The level
one player can't travel very far, because mobs quickly become far too
powerful to defeat, and happily eat level one players that wander out
of their pen. &nbsp;The character is thus confined to the newbie areas,
where there are mobs that he can kill. &nbsp;After an hour or so, the
character reaches level 2. &nbsp;Now the area the character can kill in
increases. &nbsp; After another level or two the level 1 mobs are no
longer worth killing. &nbsp;By this point, the character has completely
left the area he was killing in before, and is killing in a new area.
&nbsp;Elapsed time: about 10 hours.
<p>
What if there were no levels? &nbsp;What if all mobs were level 1, and
all players were level 1, and never advanced? &nbsp;There is nothing
preventing the player from leaving the newbie area right away.&nbsp;
Instead of 10 hours in the newbie area, the player spends 5 minutes there,
then travels to the next area. &nbsp;And the next. &nbsp;And the next.&nbsp;
Instead of taking 40-100 days /played to explore everything as it did
in original Everquest, it would take at most 1.
<p>
Exploration done so easily has little value. &nbsp;Everyone would see
Nagafen and Vox on the first day, and since Nagafen and Vox would be
level 1, they would easily be killed by whoever felt like walking there.
&nbsp;There would be no sense of accomplishment, no sense of wonder, no
anticipation and desire to see these rare places, because they wouldn't
be rare.
<p>
Add levels and suddenly the game is far more compelling and interesting.
&nbsp;Levels also add another dimension: replayability. &nbsp;This isn't
just playing a character up from level 1 a second time, this is visiting
the same place visited before, but at a different level. &nbsp;The location
is unchanged; the monsters are the same level, the zone has exactly the
same geometry...but it's fun, different, and exciting, because the <b>player</b>
changed. 
<p>
It's the player changing the makes the world vibrant and dynamic, not the
content itself. &nbsp;I've been to the same dungeons over and over again
as my levels have changed, and each time it's interesting in a different
way. &nbsp;The first time it's hard and challenging with a full group,
or even multiple groups. &nbsp;Later it's hard and challenging with less
than a full group.  &nbsp;Eventually, assuming a level cap isn't reached,
the dungeon can be killed in while solo.
<p>
Thus levels serve two important functions: they slow down the consumption of
content, and they create a dynamic game.
<p>
<h3>Kunark has levels, what's the problem?</h3>
<p>
Lets look at Kunark content. &nbsp;There are several dungeons for
level 51+ in Kunark: Karnor's Castle (karnors), Old Sebilis (sebilis),
Chardok, and Howling Stones/Charasis (charasis). &nbsp;Each one of these
dungeons is extensive and interesting, and entertaining for hours of game
play.&nbsp; In fact, these dungeons are compelling for easily 2 days of
/play time (at 4 hours each RL day, that's 12 days playing there every
day) each, and maybe depending on the player/group, some dungeons may
be good for as long as 3 days, if revisted at later levels.
<p>
Now lets do some math. &nbsp;Each dungeon is good for 2-3 days of
/play time.  &nbsp;There are four major dungeons, so that's 8-12 days of
/play time they're good for. &nbsp;It takes from 35 to 50 days /played
to get from 51 to 60.  &nbsp;35 days - 12 days = 23 days unaccounted
for...using the longest possible entertainment time for each dungeon,
and the shortest possible (without heavy twinking) time to reach 60 from
level 51. &nbsp;Worst case is 42 days. &nbsp;What happens for those
other 23-42 days? &nbsp;All content at that point has been consumed.
&nbsp;The extra days are work. &nbsp;Endless repetition, not for enjoyment
or entertainment, but solely to gain levels.
<p>
There are other things to do, but they involve large numbers of people
raiding big mobs, and to do those things most guilds want, or even require,
level 60 players. &nbsp;These are end game tasks, things to keep players
busy once they reach 60, and they are balanced to be challanging at
that level. &nbsp;This is why players feel they are required to get 60 -
they are, in order to continue consuming new content.
<p>
A related problem is based on player bahavior. &nbsp;Some players will go
to karnors at level 51, and not leave until after 55. &nbsp;Why?&nbsp;
Because they know the area, they know people there and thus can group
with them, and the most important reason, the game doesn't encourage them
to leave. &nbsp;The mobs in karnors give xp day after day after day after
day. &nbsp;What if the level 1 mobs in the newbie area gave xp at level 5?
&nbsp;Level 10? &nbsp;Level 20?&nbsp; What if they gave roughly the same
xp as level 20 mobs? &nbsp;That's the problem with kunark. &nbsp;The mobs
in karnors are blue, and thus good xp, for all of those levels, all of those
days spent playing.
<p>
Kunark is content weak assuming 100% consumption of that
content. &nbsp;For those that sit in one dungeon for all 10 levels,
it's pain beyond description.
<p>
<h3>A Better Kunark</h3>
<p>
How could Kunark be done differently? &nbsp;Or more importantly, how can this
be avoided in other games? &nbsp;The key is not the total time it takes to
reach the level cap. &nbsp;That value can be arbitrarily large, or even infinite.
&nbsp;The key is avoiding content burnout, by never forcing, or even allowing,
a player to continuously play in the same area.
<p>
In this fictional, improved, Kunark, karnors may only be good while level 51.
&nbsp;At level 52, the front half of karnors would give no xp; instead, the player
would have to move deeper, to the more interesting (and harder) basement and deep
castle areas. &nbsp;At 53, those areas would no longer give xp, and the player
would then move to the front half of sebilis. &nbsp;Levels 54-58 take much longer
than 51-53, so for those levels, each one takes an entire dungeon:
