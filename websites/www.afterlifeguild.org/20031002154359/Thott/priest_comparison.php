<center>
  <div class="menutitle"><font size="+3"><b>Priest Comparison</b></font></div>
  <font size="1">by <a href="mailto:ThottWeb@Afterlifeguild.org">Thott</a></font><br><br></center>
This comparison is a series of simulations, comparing the three priests
in different activities.&nbsp; The purpose of these simulations is
to compare priests in experience group situations, <b>not</b> on
raids.&nbsp; Taking values from these simulations and applying them to
raids is like taking 100 meter dash numbers and applying them to a space
walk.&nbsp; Not that people won't do it anyway, but it will be silly.
<p>
All of these simulations share certain base assumptions:
<ul>
<li>All the priests are the same level, either 60 or 65.
<li>All the priests have useful buffs active before the simulation starts (typically mana/hp regen buffs).
<li>All the priests have any AA's that will be useful to them, including:
    <ul>
    <li>Spell Casting Mastery
    <li>Cannibalization
    <li>Mental Clarity (or equivalent FT)
    <li>Spell Casting Fury
    <li>Spell Casting Reinforcement (Mastery)
    <li>Healing Adept
    <li>Healing Gift
    </ul>
    All priests are assumed to have the same total number of AA points.
<li>All the priests have FT+MC 8, meaning 8 points of mana regeneration from AA and items.
<li>All spells are assumed to cost 40% less due to specialization (+SCM3).&nbsp;
    Some tests say the mana reduction is higher, others say it is
    lower.&nbsp; One theory is that mana reduction from specialization
    varies by spell level.&nbsp; In any event, the same value is used
    for all priests.
<li>Mana reduction from focus items ignored, or if you prefer, included in the 40% used above.
<li>+20 mana/tick when sitting.
<li>+1 mana/tick base when standing.
<li>All the priests have 4000hp and 4000mana.
<li>Cleric CH heals 4000hp every time, shaman/druid mini-CH's heal their max value.
</ul>
There are other assumptions specific to individual simulations, click on a graph to see them.
<h3>How the Simulations Work</h3>
Each simulation is done similarly.&nbsp; The simulated priests have a
spell script that they follow.&nbsp; Each spell takes its normal cast
time, and once it is complete, each spell has a period of time after it
where all spells are greyed out.&nbsp; Spells are only cast if there is
enough mana to cast them.&nbsp; Each spell of course takes mana.&nbsp;
The end result is a complete simulation of the priests in question,
just as if they were in game doing it.
<p>
For example, the cleric healing script checks to see if there is enough
mana to cast CH, and if there is, it starts casting it.&nbsp; 10 seconds
later, the CH finishes, and 4000hp is added to the total number of hit
points healed.&nbsp; If there isn't enough mana to cast CH, the cleric
sits down until there is.&nbsp; Every tick, mana from buffs and
meditate (if the cleric is sitting) is added to its mana pool.&nbsp;
Every second, the total hit points healed is divided by the number of
seconds since the simulation started, and that value (hit points per
second) is plotted on the graph.
<p>
Thus the graphs are showing the damage per second a tank can take without
dying, thanks to the priest.&nbsp; A mob will have to have a higher dps
value than what is shown to have any hope of killing the tank.
