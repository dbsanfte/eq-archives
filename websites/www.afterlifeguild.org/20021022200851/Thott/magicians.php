  <div class="menutitle"><font size="+3"><b>Magicians</b></font></div>
  <font size="1">by <a href="mailto:ThottWeb@Afterlifeguild.org">Thott</a></font>
  <p>
</center>Magicians nukes are ~84% as effecient as wizard nukes, which these days
translates into a <i>lot</i> of damage.&nbsp; Add in pet damage, and
magicians are possibly the most damaging class in the game.&nbsp;
Doing damage is fun, so playing a magician is fun.&nbsp; Isn't it?&nbsp;
Magicians never get to use their nukes.&nbsp; Many don't even summon
a pet.&nbsp; They don't do these things because they're busy summoning
mod rods.&nbsp; During downtime, they summon mod rods.&nbsp; During the
fight, they summon more mod rods.&nbsp; Is this really the best use of
their time?
<p>
This graph is a simulation of mana generated by a magician in a raid
situation.&nbsp; To compare, it shows the mana generated by a cleric in
a raid situation.&nbsp; Once there are enough clerics in the CH chain
(and everyone now uses a CH chain - current content forces it) that
spell gems refresh before having to do the next heal, each additional
cleric adds only their mana regen, and nothing more.
<p>
Both the magician and the cleric have all raid buffs, all AA's, and
FT15.&nbsp; This assumes there are no mod rods available before the fight,
i.e. this is only the mod rods the magician creates.&nbsp; The magician,
when low mana, uses his own mod rods (and subtracts them from the mana
he has generated).&nbsp; It is assumed that the magician has full time
torpor.
<p>
<img src="images/magicianmana.png">
<p>
Thus adding a magician is roughly equivalent to adding 8 clerics to the chain.&nbsp; If 30 rods are summoned up before the fight, and the fight lasts 10 minutes, that value increases to 12 clerics.
<p>
What if there are plenty of clerics, and they don't need to use
rods?&nbsp; The cleric line above is roughly the same for wizards, or
any other casting class.&nbsp; If there is any need at all for mana,
it's better for the magician to summon rods than use that mana directly
nuking.&nbsp; Even if the only class available to use that mana for
nuking is a cleric, it's still far more effecient for the magician to
create rods for the cleric to use for nuking mana than it is for the
magician to nuke himself.
<p>
Summoning rods sucks.&nbsp; Gimp this please in PoP.
<p>
There is an alternative to gimping mod rods: fast fights.&nbsp; In a
fast fight, mana regen isn't a factor.&nbsp; If the fight is only a
couple of minutes long at max, there isn't any need to pump out mod
rods during the fight.&nbsp; Magicians are then valuable for their
nuking ability, and will have fun using it.&nbsp; Unfortunately, <a
href="manaburn.php">manaburn</a> exists.
