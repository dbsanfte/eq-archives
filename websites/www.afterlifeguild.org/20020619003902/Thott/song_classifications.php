  <h1>Bard Song Classifications</h1>
  <font size="1">by <a href="mailto:ThottWeb@Afterlifeguild.org">Thott</a></font>
  <p>
</center>A few times in the past the subject came up, how many offensive songs do bards have? &nbsp;How many
defensive songs? &nbsp;So I sat down and classified all of them.
<p>
The only questionable classification is slow songs. &nbsp;I classified them as offensive because
they're used on the mob. &nbsp;If you think they're defensive, just subtract the number of slow
songs from the offensive column and add that number to the defensive column.
