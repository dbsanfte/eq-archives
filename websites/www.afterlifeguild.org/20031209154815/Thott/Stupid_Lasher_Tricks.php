  <div class="menutitle"><font size="+3"><b>Stupid Lasher Tricks</b></font></div>
  <font size="1">by <a href="mailto:ThottWeb@Afterlifeguild.org">Thott</a><br></font><br></center>
I've been spending a lot of time with the Lasher since its upgrade, and I see my opponents do the same dumb tricks over and over.&nbsp;   They don't work.&nbsp;   Please stop.&nbsp; 
<p>
<b>1)</b> It takes 5 seconds to empty a Lasher clip.&nbsp;   It takes about 4.6 seconds to reload it.&nbsp;   There are only 20 shots in a clip.&nbsp;   It takes lots of shots (spam) to kill just one person.&nbsp;   Moral: <b>kill him while he's reloading</b>.&nbsp;   It's easy.&nbsp;   It's almost exploitive, it's so easy.&nbsp;   Dodge a bit, wait for the reload, then kill him.&nbsp;   Use a knife (or beamer!) for added humiliation.&nbsp; 
<p>
People are so afraid of the new uber weapon that they stay away in fear, when they should just walk up and kill him.&nbsp;   That fear is making lasher users very happy.&nbsp; 
<p>
The frequent reload is by far the lasher's biggest weakness.&nbsp;   Forget bigger CoF after the first kill - the lasher user has to <b>stop firing completely</b>.&nbsp;   It's also very frustrating.&nbsp;   If you want to piss off a lasher user, there's no better way than to kill him while he's reloading.&nbsp; 
<p>
<b>2)</b> When a Lasher user fires at you, do not duck behind an object.&nbsp;   The Lasher user will lash you to death, every time.&nbsp;   He will giggle with glee as he does this.&nbsp;   Avoid the giggle: move perpendicular to the stream of orbs.&nbsp;   Hiding behind a thin tree is pointless, it just gives the Lasher user an easy, immobile target to lash at.&nbsp;   This isn't just vs. Lashers - I see people do this vs. Thumpers and Pounders just as often.&nbsp;   It's just as stupid there, the only difference being Pounder users snort when they giggle.&nbsp;   At least with Thumper/Pounder the shots are hard to see and hard to dodge.&nbsp;   There is no such excuse for the lasher.&nbsp; 
<p>
Taking cover works very well against near-instant projectiles, which can't be avoided by strafing.&nbsp;   Lasher orbs CAN be avoided by strafing.&nbsp;   You don't really think the orbs are so badass that they need <b>two</b> forms of defense, do you?  Since the orbs can be avoided by strafing, cover is designed not to work.&nbsp;   You don't need cover to avoid mines, and you don't need cover to avoid the orbs.&nbsp;   Imagine how rarely you would step on a mine if it looked like an orb.&nbsp;   Don't step on the orbs.&nbsp;   Remember, you don't have to avoid all the orbs and lashes, just enough to trigger #1.&nbsp; 
<p>
<b>3)</b> Look around you when out on a battlefield.&nbsp;   Don't focus only on what is ahead.&nbsp;   I've gotten dozens of kills by firing a stream of orbs from 80+ meters at someone that just plain isn't paying attention.&nbsp;   The orbs slowly make their way across the battlefield, then slam right into the inattentive dullard.&nbsp; 
<p>
Note the distance: 80+ meters.&nbsp;   Lasher users often fire from this range, since 90m is maximum.&nbsp;   Many times I've moved just close enough for the reticule to change from yellow to red.&nbsp;   In these cases, you don't even have to move perpendicular - just move a few steps away.&nbsp;   The orbs will poof harmlessly.&nbsp; 
<p>
Note 2: 90 meters is about half the length of a courtyard.&nbsp; It's not like the guy is sniping you from a mountain range two continents over.&nbsp; Be aware of your surroundings, and you'll never have to worry about the orbs.
<p>
<b>4)</b> When #3 happens, don't turn towards the damage - <b>move</b>.&nbsp;   The orbs do damage slow enough that even after getting hit by the first, plus human reaction time, there is still plenty of time to get out of the way before dying.&nbsp;   But not if you turn and look.&nbsp;  <b>Don't look - RUN</b>.&nbsp; 
<p>
Cockroaches start moving due to stimulus before the signal even reaches their brain.&nbsp;   Be like the cockroach.&nbsp;   <b>Don't look - RUN</b>.&nbsp; 
<p>
Once mobile, the Lasher user can no longer significantly harm you, while your instant shot bullets can kill him just fine.&nbsp;   You have two options: shoot at him to make him take cover (or kill him if he doesn't), then heal up, or use #1.&nbsp; 
<p>
Note that it takes the same number of orb hits to kill an agile as it does to kill a reinforced, and agile is better able to dodge.&nbsp;   If fighting lots of lasher users, try wearing agile armor for greater manueverability.&nbsp; 
<p>
<b>5)</b> When in a MAX, don't assume you are god, and travel around alone.&nbsp;   Unless of course you are in a Pounder MAX, in which case you <b>are</b> god, and can do what you like.&nbsp;   A lasher user (or any HA user really, using AP bullets) will circle strafe around you where you can't shoot them (again, unless you are god and can kill them with splash damage) and kill you.&nbsp;   Note that my most successful tactic involves doing this, then running away during the reload, then running back - so if a lasher user does a bunch of damage to you, then runs off...be ready for them when they return.&nbsp;   Don't breath a sigh of relief and turn your back.&nbsp; 
<p>
Any HA user is just as dangerous to a MAX as a lasher user,
they just have to do their reload at the start, instead of halfway through
(only lashers have to reload to kill a MAX, every other HA weapon does
it with one clip).&nbsp;   And if they carry a second HA weapon already
loaded with AP bullets...then they're worse.&nbsp;   Moral: don't travel
alone as a MAX.&nbsp;
<p>
<b>6)</b> If someone is shooting a stream of orbs into a tower door (or down a base hallway) - wait.&nbsp;   Don't try to run out.&nbsp;   Don't get near the door.&nbsp;   Just wait until the orbs stop, or the doors close.&nbsp;   Then run out.&nbsp;   It takes ages for the orbs to get to the door from any outside position, giving you plenty of time to run outside if that's what you want to do.&nbsp;   The orbs won't be firing while the door is shut, if only because it takes so frigging long to reload that nobody wants to fire "just in case", because odds are, they'll be stuck in reload when a real target appears.&nbsp; 
<p>
If the door is stuck open, then just wait for the orbs to stop.&nbsp;   He isn't trying to trick you into coming out.&nbsp;   He's reloading.&nbsp;   See #1.&nbsp; 
<p>
Note that spamming the door with orbs is no different from spamming the door with a locked down Pounder, except that the Pounder does damage way faster, can lob the grenades most of the way into the basement, and doesn't have problem #1.&nbsp;   Please note that Pounder worship services are now required three times a week.&nbsp;   Do not miss the worship services, or you will be Pounded into submission.&nbsp; 
<p>
<b>7)</b> In hallways there often isn't any way to avoid the orbs.&nbsp;   While there's always #1, there's one more trick that's useful: orbs don't lash for the first 5 meters.&nbsp;   This means if you get very close to the lasher user, any orbs that miss, miss for good.&nbsp;   No lashing.&nbsp; If you can't dodge at a distance, dodge at point blank.
<p>
What's more, the main trick for hitting targets is to spam any place the target might go.&nbsp;   Someone at point blank range could be anywhere surrounding the lasher user.&nbsp;   It's not possible to spam in that many directions.&nbsp; 
<p>
The orbs move slow enough that the lasher user has to lead a target, even at point blank range.&nbsp;   It's hard enough to get the lead down against a close target that's strafing predictably, but near impossible if they keep changing direction.&nbsp;   Many guns that usually lose (and by design, should lose, i.e. MA weapons) 1 on 1 vs. a lasher, win at point blank range while strafing.&nbsp; The orbs miss, while bullets do not.
<p>
While the natural expectation is that HA weapons are better the closer they are to their target, this just isn't true with the lasher.&nbsp;   Use this to your advantage.&nbsp; 
<p>
<b>8)</b> Grief.&nbsp;   Orbs don't lash friendlies...but they move slow, they're big, and they can't be stopped.&nbsp;   Lasher users do a lot of damage to friendlies that are stupid enough to run in front of their streams.&nbsp;   While I say "stupid enough", in a lot of cases, the orbs were in the air, well behind the friendly, before the friendly moved in front of them.&nbsp;   The friendly couldn't see the orbs, and the lasher user can't stop them.&nbsp;   
<p>
These damaged friendlies are easily picked off by enemies.&nbsp;   Watch for the friendly-fire wounded, and kill them.&nbsp;   Send them tells about how the lasher truly is an uber weapon - for your team.&nbsp; 
<p>
To go along with this, if you see a group of Vanu, try to fight them such that the lasher user is behind the others.&nbsp;   The ones in front will naturally strafe around to avoid your bullets - right into their buddy's orbs.&nbsp;   It's great fun.&nbsp; 
<p>
<b>9)</b> Loot a lasher.&nbsp;   Not because it's more powerful, but because
lashers are <b>much</b> better in the hands of a non-Vanu than in
the hands of a Vanu.&nbsp;   The orbs are big.&nbsp;   They obstruct
vision.&nbsp;   It's hard sometimes to see that it's an <b>enemy</b> shooting
the orbs at you.&nbsp;   While dying, many people are cursing their
teammates.&nbsp;   They only see that it was an enemy using it <b>after</b>
they're dead.&nbsp;
<p>
On top of this, some lasher users think the lasher is very powerful - that's why they moved to Vanu.&nbsp;   When they see someone else with it, they fear the weapon.&nbsp;   They're temporarily stunned with thoughts of how unfair it is for the enemy to use 'their' uberweapon.&nbsp;   They forget about the things on this list.&nbsp;   They know intellectually how lashers work, but have no experience fighting them.&nbsp;   This makes them even easier to kill.&nbsp; 
<p>
<b>10)</b> Other HA weapons.&nbsp;   Despite what is said on the boards, the
MCG is an awesome HA weapon.&nbsp;   It flat out destroys Lasher users
in 1 on 1 fights.&nbsp;   It can kill lasher users from a distance, while
avoiding orbs.&nbsp;   It kills them before they can run away.&nbsp;   It
doesn't have problem #1.&nbsp;   Be confident if you're carrying a MCG
that you <b>will</b> win vs. Lasher users in a fair, even fight.&nbsp;   Of course,
if you're stupid enough not to carry med packs, and the lasher user does,
then you'll probably lose.&nbsp;   The same goes for various implants that
convert stamina to combat health (personal shield, second wind).&nbsp;
But assuming both are equal, the MCG will win.&nbsp;   Sometimes,
the MCG kills so fast that the Lasher user is dead before the first
orb even reaches the MCG user.&nbsp;   Time to kill numbers are nice,
but the lasher ones always forget orb travel time.&nbsp;
<p>
The Jackhammer is even better than the MCG...but only within the 5 meter
range, where Jack damage goes through the roof, and the lasher stops
lashing.&nbsp; If you can get in that range with a Jack, the lasher
user is toast.&nbsp; He may as well just give you his med packs and kneel
in submission.&nbsp; The tough part is getting into this position, when
the lasher user is sitting at the end of a hallway.&nbsp; The answer:
go around.&nbsp; There are usually two ways to get to most anyplace in
a base.&nbsp; If you go the other way, you'll probably be able to get
close enough to press the Jackhammer "I win" button.&nbsp; Alternately,
taunt the Lasher user, hide from the orb stream, and wait for #1 to occur -
<i>then</i> walk straight up to him and press "I win".
<p>
Hopefully many people will read this list, and the number of people performing Stupid Lasher Tricks will decrease across all servers.&nbsp;   I like a good challenge, but it's hardly challenging to kill people when they run from you while you're reloading, or stand still while the orbs bump into them.&nbsp; 
<p>
Don't perform Stupid Lasher Tricks.&nbsp; 
