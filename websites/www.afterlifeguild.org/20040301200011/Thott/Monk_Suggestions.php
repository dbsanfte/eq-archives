<center>
  <div class="menutitle"><font size="+3"><b>Monk Suggestions</b></font></div>
  <font size="1">by <a href="mailto:ThottWeb@Afterlifeguild.org">Thott</a><br>2003/11/01<br></font><br></center>
This paper doesn't make me out to be an evil, monk hating bastard, but
I assure you, I still am.  I'm frothing at the mouth right now.  I walk
slowly around my house with my arms straight out in front of me moaning
"brains" er, I mean "mooonnnkkkkssss".  I walk into restaurants
and ask "do you serve humans?", and when they look at me strangely and
say "of course", I ask for a grilled monk with a lemon glaze.  I was
temporarily insane when I wrote this.
<p>
There are several things I'd like to see monks get.
<p>
<h2>I'd like to see monks get AE melee</h2>
We tend to gather 2-3 rooms of mobs together when doing LDoN normal
adventures, then kill them. I've seen SK's and even clerics outdamage
top rogues in this situation.&nbsp; Sure the AE damage is low...but when
multiplied by a dozen mobs, it's better than any single target damage in
the game.&nbsp; For monks, this would be in the form of a flying spin kick that
strikes everyone in range, using flying kick damage on the same timer.
<p>
If it works well and is popular, SOE could transition monk primary
weaponry to the same ratio as warrior weaponry over time, and add a
large selection of +flying kick (also affecting the AE kick) gear,
so that monk damage stays constant, but has more of a kick to it
(haha!).&nbsp;  Kicking for 1k is more fun and impressive than having
a slightly higher base weapon ratio.&nbsp; A pleasant side effect of this is
greater weapon selection, since monk weapons wouldn't have to be monk/bst only.
<p>
On raids we often have several mobs in camp, and we're killing one while
offtanking the others.&nbsp;  Monks won't be able to do more damage to
the one we're fighting than a rogue...but they can do as much, or more,
damage overall, through AE.&nbsp;  But only situationally.&nbsp;  And
only if the monk is aware of what's going on: zombie monks need not apply
("mooonnnnkkkkk").
<p>
A smart monk could sometimes use this ability to instantly taunt
new mobs in camp onto him.&nbsp;  Monk tanking isn't spectacular,
but it isn't bad, and with stonestance, it's flat out excellent.&nbsp;
Add in mend, followed by FD, and it's a fantastic way to suck up a ton
of potential damage with essentially no risk, saving countless casters
and healers, who will swoon in your presence for years to come.&nbsp;
&lt;fine print: swooning not guaranteed&gt;
<p>
A single boss mob is best killed by rogues, but if there are mobs spawned
that need to be killed, that can't be mez'd...monks will shine.&nbsp;
Bald heads will do that.
<p>
<h2>I'd like to see monks restored as frontal damage dealer</h2>
The original primary role of the monk class was frontal damage dealer.&nbsp; Over the years, this has been lost.&nbsp; I'd like to see it restored.
<p>
I advocate immunity to riposte damage and high strikethrough, and maybe
even a return riposte type ability that not only blocks a mob's riposte,
but generates an extra attack for the monk.&nbsp; Thus monks would
actually do slightly <i>more</i> damage from the front than from the
rear.&nbsp; It's kind of odd that right now one guy is in front of the
mob while everyone else is behind it.&nbsp;
<p>
Monks in front could even keep the mob stable, with not only melee push
but also dragon punch.&nbsp; Like AE melee making stonestance/mend/FD useful in
more situations, frontal melee helps activate otherwise wasted utility.
<p>
Why a return attack?  Well most martial artist disciplines favor the
concept of using the power of the attacker against itself.&nbsp;  This
fits that view perfectly.&nbsp;  This is significantly different from
abilities that fire off when the monk ripostes, because this doesn't
require the monk to tank.
<p>
<h2>I'd like to see monks get improved xp group pulling capabilities</h2>

Currently mobs under 35 (or is it 30?) mem blur immediately when a monk
FD's.&nbsp;  I'd like to see this level increased (likely in the next
expansion) to the lull cap: currently 65.&nbsp; Lull makes pulling
trivial, pulling lullable mobs may as well be trivial for everyone.
<p>
A monk using this could grab a room full of mobs, run around a corner,
wait until just one comes around, and FD.&nbsp;  If it's truly alone,
he hops up, grabs the solo one, and runs to group.&nbsp;  Elapsed time:
about 5 seconds.&nbsp;  Sometimes more than one might come around the
corner, or the monk could fail and have to run around some...but the
end result is pulling that's just as fast as lulling.&nbsp;  And safer.
<p>
[Problem: unless lull resistance/immunity is factored in to this auto
blur, this idea will make previously hard areas trivial, e.g. hard
LDoN adventures.]

<h2>Summary</h2>
Everything above makes monks more unique, more interesting to play,
more powerful in the hands of a smart player, and more desired for
groups.&nbsp;  All this without stomping on any other class.
<p>
The problem of course is that these changes should have happened months
ago.&nbsp;  It's very unlikely, however, that <b>any</b> significant
changes will happen until the next expansion, and honestly, nothing
monks can do will change this, and I'm sorry.&nbsp;  It's just how
SOE operates.&nbsp;  LoY and LDoN weren't real advancement expansions,
and thus nothing could be fixed.
<p>
But the next expansion will be a <b>big</b> advancement expansion,
just like PoP.&nbsp;  More levels, more AA's, more raiding zones.&nbsp;
This is the kind of expansion where major changes happen, and things
like the above are not only possible, they're likely.
