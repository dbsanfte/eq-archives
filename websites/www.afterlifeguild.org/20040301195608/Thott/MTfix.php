<center>
  <div class="menutitle"><font size="+3"><b>Main Tanking Fix</b></font></div>
  <font size="1">by <a href="mailto:ThottWeb@Afterlifeguild.org">Thott</a><br>2004/01/21<br></font><br></center>
How to fix main tanking and CH chains?&nbsp; Change the AI.
<p>
Currently each cleric that casts CH on a tank gains some amount of
hate, we'll say 1000.&nbsp; Thus after 10 CH's, that cleric has 10,000
hate.&nbsp; The warrior is busy building hate this entire time, and we'll
say he has 15,000 hate.&nbsp; Thus the mob stays on the warrior.&nbsp;
Which is how it should be.
<p>
The problem occurs when multiple clerics are healing the same
warrior.&nbsp; Two clerics alternating CH's can heal a warrior 20 times
in that same time period, which should generate 20,000 hate and draw the
mob off the tank...but it doesn't, because that hate is split between
two clerics.
<p>
Does this make sense?&nbsp; Imagine you are a powerful being, and you're
trying to kill a pesky metal thing that keeps poking you in the
foot.&nbsp; You smash him good, and he's nearly dead, yet before
you can finish him off...he gets healed.&nbsp; So you smash him again,
and again he is nearly dead, but just like last time, before you can finish him off...he
gets healed.&nbsp; I don't know about you, but it wouldn't take me
long to realize this pesky metal thing isn't going to die so easy, and
I'd kill some other pesky thing.&nbsp; Does it have to be a cleric?&nbsp; <B>No</b>.
<p>
<h2>New AI</h2>
I suggest this: instead of giving 1000 hate to the cleric every time
CH lands, give 800 hate to the cleric, and subtract 200 hate from the
warrior.&nbsp; In the original example, one warrior gained 15,000 hate
while one cleric gained 10,000.&nbsp; With this new system, the warrior
would gain 15,000-2,000=13,000, while the cleric gained 8,000 - keeping
the same 5,000 hate difference as before.&nbsp; If two clerics CH'd 10
times on a warrior they'd get 8,000 hate each, instead of 10,000.&nbsp;
The warrior would generate 15,000 and lose 2*10*200=4,000, for a final
value of 11,000 hate.&nbsp; Again the mob does not jump on a cleric...but
it will jump on someone else.
<p>
<h2>Where?</h2>
I don't recommend this system for existing content, but it is certainly an
option for new.&nbsp; A mob with this AI doesn't need to have the single
target damage output that current mobs have to be challenging.&nbsp;
The challenge will exist because the mob is more intelligent.&nbsp;
It is now to the point where it's essentially impossible to make a MT
mob that is challenging (yet beatable) without additional AE effects, at
which point it's really the AE effects that make it challenging, not the
MTing.&nbsp; This AI brings back challenge, regardless of what AE is used.
<p>
<h2>As a Tank</h2>
Tanks will approach this by trying to taunt and gain aggro, with a
finger hovering over defensive disciplines at all times.&nbsp; When
the mob turns, pop that disc and hope for fast assist healing.&nbsp;
If your clerics are good, you'll get it.&nbsp; If you are good, you'll
survive that long.&nbsp; Every tank gets to participate, instead of just the
first couple of tanks on the MT list.&nbsp; Everyone matters and everyone
plays.&nbsp; Suddenly, tanks stack just fine on raids.
<p>
<h2>As a Cleric</h2>
Clerics will want to assist heal, and not with CH - clerics will want
to use the fastest heals possible.&nbsp; This means higher mana output,
but <b>higher mana output is a good thing</b>.&nbsp; It means fights can
be fast and exciting without having to be 15 minutes long.&nbsp; It means
fights are more exciting and fun, and unlike CH chains, fights will be
interesting and something to look forward to.&nbsp; Not mindless work.
<p>
