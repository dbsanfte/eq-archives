<h3>Commentary</h3>
These graphs show a drastic difference in mitigation capability between
shamans and clerics/druids.&nbsp; We all suspected that shamans were a bit
better...but nobody knew by how much.&nbsp; The second graph shows that
a shaman can mitigate all the damage from a mob that does over 400 dps,
if that mob is slowable.&nbsp; That's more damage than Dain Frostreaver
IV puts out.&nbsp; If he were slowable, a shaman and any other tank
(and possibly the shaman solo) could kill him.
<p>
Why are shamans so good?&nbsp; It isn't any one thing.&nbsp; Slow
certainly plays a major role, since it effectively multiplies other
shaman healing by four, but it's more than that.&nbsp; It's also shaman
mana regeneration, which is far superior to any other class in the game,
easily twice as good as necromancers.&nbsp; That mana regen exists because
of Torpor, Cannibalize IV, and Cannibalization.&nbsp; Without Torpor,
the mana regen would be significantly less.&nbsp; Without Cannibalization,
the time cost of shaman mana regen would be much higher.
<p>
It's every ability, not just added together, but multiplied, that makes
shamans into the powerhouse shown above.
