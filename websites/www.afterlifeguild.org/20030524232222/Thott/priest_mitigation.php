  <div class="menutitle"><font size="+3"><b>Priest Mitigation Comparison</b></font></div>
  <font size="1">by <a href="mailto:ThottWeb@Afterlifeguild.org">Thott</a></font><br><br></center>
These simulations compare priest mitigation capability.&nbsp; The word
'mitigate' is used here instead of the word 'heal', because limiting
or preventing mob damage, over time, is the same as healing it after it
is done.&nbsp; There's no need to heal damage that never occurs.
<p>
In particular these graphs include shaman slow.&nbsp; Cleric stun isn't
included, because it doesn't work on mobs anyone would want to fight in a
group at level 60, plus in almost all cases, healing is superior to
stun.&nbsp; Ro and Cripple aren't included because the damage reduction
is difficult to predict.
<p>
As was mentioned in the Intro, these graphs show the damage per second
mitigated by the priest at different points in time.&nbsp; The first
graph for example shows that clerics can mitigate about 170 damage/sec
at the 600 second mark.&nbsp; This means that for a tank to die, when
supported by this cleric, it will need to take more than 170 damage/sec
for 600 seconds to overcome cleric healing.&nbsp; How much damage is
needed at any time interval to outdamage the healer is exactly what
these graphs show.
<p>
For the shaman line, the shaman casts slow and keeps it active on the
mob.&nbsp; To simulate resists, the slow cast time and mana cost is
increased by 20%.&nbsp; All healing done by the shaman is multiplied by 4,
to simulate 75% slow.&nbsp; I.e. if a mob could do 400 dps normally, then
slowed it will only do 100 dps, and 100 dps of shaman direct healing,
when combined with the slow, will result in 400 dps mitigated.&nbsp;
If this concept is confusing, I recommend getting a PEZ dispenser.&nbsp;
It should be entertaining for hours.
<p>
The first graph shows mitigation capability without the newly added
mini-CH spells.&nbsp; This is mostly of academic interest (for all those
academics that study EQ), but I leave it here anyway.&nbsp; The later
graphs are more useful, and representive of practical application.
