<center>
  <div class="menutitle"><font size="+3"><b>Luclin Uber Content</b></font></div>
  <font size="1">by <a href="mailto:ThottWeb@Afterlifeguild.org">Thott</a></font><br><br></center>
Imagine for a moment that you're playing solo, and have very high
regen.&nbsp; So high, in fact, that any mob you face can't overcome
it.&nbsp; Harder mobs don't do any more damage to you - they just take
longer to kill.
<p>
That's what uber content is like in Luclin.&nbsp; We never run out of
mana.&nbsp; Tanks rarely die.&nbsp; If the guild had a combined health
bar, it would fluctuate between 99% and 100% from start to finish.
<p>
If every ubermob in Luclin were to suddenly double in hit points...we
could still kill every one of them.&nbsp; This was not true in the
original game, nor was it true in Kunark.&nbsp; Mobs back then had high
regen.&nbsp; They did high damage.&nbsp; People <b>died</b>, and there
wasn't time to res them back in.&nbsp; Mana ran out.&nbsp; The guild
hit point bar decreased as we fought the mob, and if our bar decreased
slower than the mobs', we won.&nbsp; If it didn't, we lost.
<p>
Modern fights are no longer a race.&nbsp; It's not that fights are
longer: a long race can be just as interesting as a short race.&nbsp;
It's that there isn't any race at all.&nbsp; Mob hp drops, but guild hp
does not.&nbsp; Fights are no longer real battles, they're a test of error
control.&nbsp; Unless the guild makes a mistake, they will win.&nbsp; If
they do make a mistake...they might still win, but it's possible to fail.
<p>
Failure is thus not a measure of guild power, or even guild tactics,
it's a measure of guild perfection.
<p>
We don't often make mistakes.&nbsp; We very rarely lose.&nbsp; And
it's boring, very boring.&nbsp; As long as we have enough clerics to
do the rotation, and one <a href="magicians.php">magician</a> and a few
warriors, we can kill pretty much anything.&nbsp; All the other people
on raids, the rogues, the wizards, the rangers....all they do is make the
fight go faster.
<p>
It's tough to say what signs developers should look for, to see if people
are having fun in a given encounter, but "melees going AFK" is probably
not a good one.
