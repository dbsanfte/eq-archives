<DIV ALIGN="LEFT"><span class="bodylinks">
<div class="bodyblack"> <P class="bodyblack">There 
comes a time in many a Norrathian rogue's career when they desire to learn about the Elden Days of Norrath 
and how their forefathers lived, operated, and died in such a dark and unforgiving age. Perhaps this desire 
to learn of the past is borne out of simple curiousity, an interesting peek into a bygone era. Or perhaps 
it's borne of more nefarious motives, to glean a tidbit or two of long-forgotten secrets of the ancients....</P>
<P class="bodyblack">Regardless of <EM>why</EM>, 
many rogues do eventually seek out an old timer and probe the far recesses of their mind for all its worth, 
on an adventure filled with lost knowledge and discovery. For you, it seems, that time is now...</P>
<HR color="black" WIDTH="75%"> <BLOCKQUOTE>
<P class="bodyblack"><I>You
      have no trouble finding Remmi, Grandfather of the Safehouse, Thwapper of
      Idjits, Legendary Scowler (and legend in his own mind!). As suspected,
      the ornery old dwarf is sitting at the bar, flagon of ale in one stubby-fingered
      hand and the other resting lightly over the infamous <STRONG>Idjit Thwapping
      Stick(tm)</STRONG>. Staying to the shadows you watch him for a moment,
      trying to determine if this would be a good time to approach and inquire
      about the history of the place, a topic that's been nagging at you lately.</I></P>
<P class="bodyblack"><I>After
      several minutes of witnessing the old rogue scowling and bickering—with
      himself, you note with a chuckle—you decide now is as good a time as any
      as you slip away from the shadows and take a few steps towards the bar.</I></P>
<P class="bodyblack"><I>"Hail,
      grandfather," you call out before getting too close, mindful of the ancient
      rogue's cantankerous disposition and tendency to swing out first and ask
      questions later when annoyed or suprised.</I></P>
<P class="bodyblack"><I>Without
      turning around the old dwarf grumbles, "Aye, lad, what's the bother? The
      place burnin' down? Ale runnin' low? C'mon, lad, speak up!"</I></P>
<P class="bodyblack"><I>So
      far so good, you think--at least I got him talking. "Sorry to be bothering
      you, grandfather," you say, carefully creeping closer and choosing your
      words wisely. "I can tell you're incredibly busy at the moment but lately
      I've been wondering--"</I></P>
<P class="bodyblack"><I>"HAR!" Suddenly
      the old dwarf burst out with a hearty laugh, sending your heart to your
      throat! Spinning quickly in his stool to face you he says with a grin, "So
      ye wants ta learn 'bout the Safehouse, do ye?! Ahhh, me favorite subject," he
      pauses and scratches his chin. "Well, next to a sack full o' somebody else's
      gems, that is..." </I></P>
<P class="bodyblack"><I>"Erm...um...uh," you
      stammer, still trying to recover from the ancient rogue's unexpected outburst.</I></P>
<P class="bodyblack"><I>"Well
      don't ye just stand there with yer thumb in yer ear, ye idjit!" Remmi said,
      frowning. Then, gesturing to a nearby stool he added, "Pull up a stool
      an' order me some ale, youngin', an' I'll gladly tell ye the history o'
      the Safehouse." He scowled suddenly then and warned, "Just don't interrupt
      me er else I'll thwap ye so hard ye'll wish ye wish ye was a baker instead
      of thief!! Now sit!"</I></P>

<P class="bodyblack"><I>Suprised
      but elated at the dwarf's willingness to relate the story you quickly hop
      up onto the nearest stool and order two new drafts. A moment later the
      barkeep places the frosty mugs on the bar and with trembling hands you
      toss him a few silver. You reach out for one of the cold brews but realize
      the old dwarf has already claimed them both.</I></P>
<P class="bodyblack"><I>"Ain't
      ye gonna get one fer yerself, laddie?" Remmi said with a wide grin. "Tis
      a long story ye know."</I></P>
<P class="bodyblack"><I>"Um,
      I'm good, thanks," you say with a resigned sigh.</I></P>
<P class="bodyblack"><I>"Suit
      yerself!" said the dwarf wryly, and then he began his tale. "It all started
      back when I were just a wee lad, not much younger than yerself..."</I></P>
</BLOCKQUOTE>
<HR WIDTH="75%"> 
<P class="bodyblack" ALIGN="CENTER"><STRONG>Here's 
how The Safehouse came to be, presented chronologically, as stored in my memory and/or based on files 
on my 'puter.</STRONG></P>
<P class="bodyblack"><span class="bodytitle">A Little Lost Rogue in Freeport</span></P>
<P class="bodyblack">Although my EQ career started in March 1999, late 
in the last beta phase, I rolled my first Rogue character after commercial release in April, on Cazic-Thule. 
He was a Half Elf (yup, I admit it okay! I was a durn <EM>elf! BAH!</EM>) by the name of Pintaldi. Pintaldi 
started in Freeport and since it was my first time in that city I didn't have a clue where to find the 
Rogue's Guild. I ended up doing some digging on my own (as is my wont) and finally discovered the general 
area of where it existed but yet I still couldn't find its precise whereabouts (idjit humans with their 
illusionary walls!).</P>
<P class="bodyblack">Welp, my search went on for a while and I finally 
broke down and /ooc'ed for help, saying something to this effect: "Any rogues in Freeport want to tell 
me where the guild is please?" And I waited for a response.</P>
<P class="bodyblack">And waited...and waited...and waited...</P>
<P class="bodyblack">And got zero responses. Zero, zilch, nada, none. So 
I did a /who rogue in each of the Freeport zones and if I recall correctly there was something like six 
rogues total! So I /tell'ed em until someone finally answered my question. Later I asked my brethren for 
some Roguely advice and it was like pulling freakin' teeth! "Blech!" I thought. Only a handful of rogues 
and none of them very willing to help one of their own brothers. I was disturbed. I don't remember exactly 
but I'll wager I scowled a doozie.</P>
<P class="bodyblack">So I logged out and loaded up my web browser and surfed 
for <EM>anything</EM> related strictly to EQ Rogues.</P>
<P class="bodyblack">And surfed...and surfed...and surfed...</P>
<P class="bodyblack">Nothing. Zero, zilch, nada, none. </P>
<P class="bodyblack"><span class="bodytitle"><EM>Need</EM> Plants a <EM>Seed</EM></span></P>
<P class="bodyblack">And that's when I made up my mind to create a place 
for other Rogues, lost and confused like myself, to go and discuss and share topics of a roguely nature. 
An all-Rogue community. Nay, a <EM>friendly and helpful</EM> all-Rogue community. One where faithful Rogue 
players can feel secure and comfortable in the company of other Rogues, a place of their own, a safe haven 
of Rogues, a Rogue <EM>home</EM>. </P>
<P class="bodyblack"><span class="bodytitle">What's in a Name?</span></P>
<P class="bodyblack">And so I chose the name <EM>Safehouse</EM>. It was 
a natural fit. After all, a safe house is, by definition: <EM>"A house or an apartment used as a hiding 
place or secure refuge by the members of an organization." </EM>So we have "safe" and "house" and those 
two simple little words conveyed precisely the tone and atmosphere I was looking for in my new community: 
</P>
<BLOCKQUOTE> 
<P class="bodyblack">- "safe" conveys <EM>comfort</EM> and <EM>security</EM>. 
And a place that offers such things is a <EM>"comfort zone"</EM>. People naturally gravitate to their 
comfort zones as they are places where people feel the most relaxed, safe, and secure (and thus, comfortable! 
duh), most "at home". I figured that was a good thing because more than anything I wanted people to be 
comfortable (unlike the nasty and mean-spirited atmosphere of the public forums that the "big" fansites 
hosted!). </P>
<P class="bodyblack">- "house" conveys comfort and safety as well but more 
importantly it adds <EM>solidity</EM> and <EM>structure</EM>. It gives a mental picture of a solid, physical, 
structure to the comfort zone. People think of walls and doors and locks and these things amplify the 
feeling of safety and security which leads to more relaxed comfort. And it also conveys the feeling of 
"ownership", as in "this is <EM><U>my</U></EM> house!" and ownership (usually) leads to pride and care. 
So that was good.</P>
<P class="bodyblack">- and then there's the definitive interpretation of 
a secure and utterly secret hideaway, with hidden rooms, ill-gotten treasures, hushed tones, secret deals, 
dark contracts, intrigue, etc.. And these things are huge attractors to roguely types! </P>
</BLOCKQUOTE>
<P class="bodyblack"><span class="bodytitle">Pouring the Foundation</span></P>
<P class="bodyblack">Anyhoo, so now I had a name for my Rogue community 
and it was good. Then I searched for a suitable host and I chose ezBoard based on my experience with the 
Druid's Grove (aye, I admit it, I had a <EM>Wood Elf Druid </EM>during beta!<EM> </EM>GAH!). I liked the 
system and so that's where I started pouring the foundation for the Safehouse.</P>
<P class="bodyblack">I spent about an hour working out the forum names 
on paper. I thought about the specific needs and wants of Rogues and what types of subjects might be discussed 
and broke them down into separate forums for better organization and ease of use.</P>
<P class="bodyblack">Most importantly I wanted to create the atmosphere 
of what a <EM>real</EM> safehouse might be like inside but I also wanted the names to be fun and stylish 
but easily interpreted so newcomers could make a guess as to what exactly was being discussed within.</P>
<P class="bodyblack">With the final draft in hand, I logged into ezBoard's 
control panel and spent some time putting the place together, setting up the forums, configuring the colors 
and other options, etc.. Back then ezBoard was a fledgling little company and the board system was rather 
basic.</P>
<P class="bodyblack"><span class="bodytitle">Status: Operational</span></P>
<P class="bodyblack">Finally, on April 20th, 1999, The Safehouse went "<EM>operational"</EM>. 
I posted the following announcement (exactly as you see it) on EQVault and EQStratics to let everyone 
know:</P>
<P class="bodyblack"><EM>Calling all Rogues!<BR>
<BR>
Psst! The Safehouse is now in operation. Come in and discuss everything and anything related to Norrath's 
most lovable scoundrels: the Rogues. Here's a look at the available forums:<BR>
<BR>
Main Lounge - General "roguish" Discussion<BR>
Bulletin Board - News, events, contracts, player-run quests, etc.<BR>
Training Studio - Q&amp;A for newbie rogues<BR>
Rant Hall - Gripes and wishes for petitioning Verant<BR>
Fence's Booth - Buy and sell stolen property<BR>
The Backroom - Quest spoilers and secrets of the trade<BR>
More forums can be added if needed.<BR>
<BR>
So skulk on over to The Safehouse, fellow rogue, and put yourself at ease and speak your mind freely. 
For you are now among thieves--er, friends!--and quite safe from the long arm of the law (just keep a 
hand on your purse and your back to the wall...).<BR>
<BR>
Note: The Safehouse message board allows you to set up a user profile if you wish, however, it's completely 
optional. If you do decide to set up a profile you will reap the following benefits:<BR>
<BR>
Profile features:<BR>
<BR>
* Name reservation - Nobody can ever use your name to post. Ever.<BR>
* Personal icon - Personalize yourself with a small image that appears with your posts (optional).<BR>
* Posting signature - You can have a signature automatically appended to your posts (optional).<BR>
* Reply forwarding - Replies to your messages can be forwarded to your email address (optional).<BR>
* Forum Options - Two toggles: 'View new threads only' and 'Mark everything as read'. These make<BR>
finding new messages quick and easy.<BR>
<BR>
Welp, that's it for now! Hope to see you at The Safehouse soon!<BR>
<BR>
Pintaldi<BR>
Cazic-Thule</EM></P>
<P class="bodyblack"><span class="bodytitle">If You Build It, They Will Come (<EM>in freakin' droves!</EM>)</span></P>
<P class="bodyblack">In mere hours The Safehouse 
was filled with Rogues of all size, shape, and color, chatting away like best friends that hadn't seen 
each other in years. I was ecstatic! The forums filled up with threads faster than an ogre's plate of 
spaghetti at Papa Gino's on "All You Can Eat Pasta Buffet" night!</P>
<P class="bodyblack"><span class="bodytitle">Common Bonds and 
Kinship Forged</span></P>
<P class="bodyblack">Common ground was established 
easily and quickly and Rogues were helping other Rogues; questions were answered, tips were given, tales 
were told, secrets were shared. But it was <EM>much</EM> more than just Rogues helping Rogues, it was 
<EM>brothers</EM> and <EM>sisters</EM> sharing a new <U>home</U> and helping each other succeed in a world 
well-known to be harsh to "our kind"--very harsh, indeed. It was a family of players coming together for 
the common good of the Rogue class and the players that played rogue characters.</P>
<P class="bodyblack">It was major league coolness 
and it was something no other class-specific board could touch (with the exception of the Druid's Grove!) 
with a hill giant's quarterstaff. Do hill giants carry staves? *shrugs* </P>
<P class="bodyblack"><span class="bodytitle">Setting The Tone 
by Example</span></P>
<P class="bodyblack">From the very start I tried 
to set a good example by fostering and cultivating a comfortable, friendly, and helpful tone and atmosphere 
and to squash <EM>immediately</EM> any threads or posts that threatened these ideals. </P>
<P class="bodyblack">At first it wasn't hard because 
the majority of the people were absolutely great and, indeed, they wanted the same thing I wanted: peace, 
comfort, friendliness. If something or someone created waves, it was put down by me and/or the community 
before it could ever get out of hand. It was instinctive and swift. The family protected their home and 
they did so with vigor but also with fairness.</P>
<P class="bodyblack"><span class="bodytitle">A Dwarf is Born 
(with a scowl on his face and Thwapping Stick&#8482; in his hand!)</span></P>
<P class="bodyblack">But even with myself (as 
Pintaldi, the Half Elf Rogue Admin) and the leaders of the community leading by good example, there were 
those that weren't so easily agreeable and they took advantage of our "nice" nature of running things. 
And so a peacekeeper was born--an ornery dwarven rogue by the name of Remmi.</P>
<P class="bodyblack">Remmi was actually my new 
dwarven Rogue character on Cazic-Thule. He was a member of Clan Axepeak, an all-dwarf guild of dedicated 
and superb role-players hailing back to the old Neverwinter Nights game. I RP'ed Remmi as an ornery, scowling 
fella with a no-nonsense attitude. And I figured he'd be perfect for the job at the Safehouse.</P>
<P class="bodyblack">Pintaldi suddenly and conveniently 
disappeared (his whereabouts are yet unknown and still under investigation) and Remmi moved in and took 
his place, bringing his patented scowl and Thwapping Stick with him. And from then on the <EM>ne'er-do-wells</EM> 
were stopped in their tracks with a scowl and a thwap and sent on their way rubbing their arse.</P>
<P class="bodyblack"><span class="bodytitle">Laying Down the 
Smack <EM>in-character</EM> to Soften the Blow</span></P>
<P class="bodyblack">The beauty of <EM>Remmi the 
Ornery Admin</EM> was that he could lay down the law and the smack when necessary and he was actually 
heeded without any of the usual and automatic "backlash" that some people feel the need to return when 
a virtual authority figure exerts power, even when they know it's perfectly reasonable and they deserved 
it.</P>
<P class="bodyblack">The role-played orneryness 
of Remmi gave a fun and light-hearted side to the usually misunderstood job of moderating-- telling people 
what they can and cannot do, tactfully, is no easy task! But the in-character role-play worked like a 
charm. The normal "backlash" one might expect from such an encounter turned into a friendly jest match, 
and many times it was even role-played by the k3wlest of the d00ds. </P>
<P class="bodyblack">With in-character Remmi I 
could belittle, insult, emote a thwap, and otherwise tell people quite bluntly to stop acting like an 
ass and keep their nose clean--in so many words, heh. It was, of course, all done <EM>in-character</EM> 
and with taste and never (not once) was it ever mistaken (nor intended) as a personal attack, although 
I must admit in some rare cases, with the unruliest of punks, I truly meant it, but the role-play made 
everything allright in their eyes. Dummies.</P>
<P class="bodyblack">An example exchange in a 
thread-gone-bad might have gone like this:</P>
<BLOCKQUOTE> 
<P class="bodyblack">Remmi: <EM>Gads, lad, are 
ye an idjit?!! Ye must be some durn fool elf got thonked on the head too many times, eh?!? Awright then 
I'll forgive ye this time but ye best keep it clean er else ye'll get the Stick... *Remmi scowls and points 
to his Thwapping Stick(tm)*</EM></P>
<P class="bodyblack">Unruly Safehouser: <EM>hehe, 
okay. sorry bout that you stumpy old fart.... *runs off before getting the stick*</EM></P>
</BLOCKQUOTE>
<P class="bodyblack">If I had a nickel for every 
post where I put out the flames by calling someone an "idjit" or "durn fool" I'd be having tea right now 
with Bill Gates...well, maybe not Bill Gates.</P>
<P class="bodyblack"><span class="bodytitle">This Useful Thread 
will Self-Destruct in 5...4...3...2...1...POOF!</span></P>
<P class="bodyblack">In just a couple weeks the 
Safehouse was filled with people tossing around some incredibly useful Rogue information and back then 
ezBoard forums maxed out at 50 threads a piece (yes, 50!) and when that happened, the oldest thread went 
"bye bye" when a new one got added--and new threads were being added at an alarming pace.</P>
<P class="bodyblack">In light of this I started 
saving the really useful posts, of which there were many, so the information within wouldn't disappear. 
Basically I just copied and pasted the text into Notepad and saved it to my hard drive (in fact, I still 
have these files and man are they nostalgic!). So I decided to create a new forum called The Library where 
I would compile this information periodically into special "read-only" threads and thus other Safehousers 
could access the archives anytime without worrying about it getting "pushed off" the forum into "ezBoard 
Thread Heaven".</P>
<P class="bodyblack">So I made The Library and 
dubbed the special threads with a role-playing flavor such as "Book of Knowledge" and "Tome of Mastery". 
Books of Knowledge were planned to cover all sorts of generic things helpful to rogues such as Zone Guides, 
Combat Tactics, etc. while the Tomes were to cover very Rogue-specific items like Skills; for instance, 
"Tome of Mastery: Picking Pockets".</P>
<P class="bodyblack"><span class="bodytitle">Librarian Wanted--Apply 
Within--Remmi is an Equal Opportunity Employer but Dwarvish blood is highly recommended (oh, and elves, 
gnomes, humans, and barbarians need not apply!)</span></P>
<P class="bodyblack">And so I copied and pasted 
messages like a kindergartner buffed with Haste and SoW but yet I still couldn't cover enough ground. 
There were too many posts and I was spending a few hours a day sifting through them. So I put up a Help 
Wanted post on the News Bulletin and Zato, my ultimate successor, answered the call. </P>
<P class="bodyblack">Zato was already a familiar 
and well-respected face and super contributor at The Safehouse and I gave him the job on the spot. I still 
have our original email correspondance dated May 03, 1999, just 2 weeks after the Safehouse opened. Here's 
what he wrote in reply to my plea for assistance. (Hope you don't mind, Zato! Don't worry lad, it's nothing 
incriminating--just a piece of history!):</P>
<BLOCKQUOTE> 
<P class="bodyblack"><EM>"I have loads of spare 
time at work and would be more than happy to keep up the library.&nbsp; Just let me know how I can help.</EM></P>
<P class="bodyblack"><EM>Thanks,<BR>
Zato the Dwarven Rogue"</EM></P>
</BLOCKQUOTE>
<P class="bodyblack">So I gave Zato the scoop 
on the Library and he took off running like a champ. He came up with the most amazing tables of weapons, 
armor, and the like and I was flabbergasted. They were incredible. ezBoard tech support said they couldn't 
be done as can be evidenced by this note to me from Zato: <EM>"EZBoard is really funky with tables. I 
sent an email to the support asking for help, but they said tables werent supported"</EM>&nbsp;. </P>
<P class="bodyblack">But the crafty little dwarf 
went to work and figured it out despite the ignorance on mine and ezBoard's part! HAR! Anyway, he also 
started compiling more Tomes of Knowledge, Books of Mastery, and other FAQ's and guides as well.</P>
<P class="bodyblack"><span class="bodytitle">An Elf Joins the 
Ranks (BAH!)</span></P>
<P class="bodyblack">Things went pretty smoothly 
then for a while but the Safehouse continued to grow steadily and so I expanded Zato's duties from Librarian 
to full Moderator but yet we still couldn't handle the load. So up went the Help Wanted sign once again 
and this time it was a dark elf that answered the call. He called himself Kezzek and although he was <EM>just</EM> 
an elf I figured I'd give him a shot—bah, I must have been going soft! *Remmi scowls*</P>
<P class="bodyblack">Despite my skepticism in 
hirin' an elfie to do a dwarf's job, Kezzek managed to do exceptionally well. But sooner or later the 
truth came out and a scandal was revealed--Kezzek had dwarven blood in him! Of course, there could be 
no other explanation! And although he may still try to deny it to this day, I've seen proof on the Test 
Server! *grin*</P>
<P class="bodyblack"><span class="bodytitle">The forging of 
the Ring (no, not <EM>that</EM> Ring!)</span></P>
<P class="bodyblack">I got the idea to form an 
unofficial "guild" of Rogues across all servers sometime in early June '99. So I posted a message with 
my ideas for this "rogue network" and listed a bunch of possible names (of which the Ring was my preferred 
choice) and requested feedback and other possible names. After a few days of discussion and a ton of great 
ideas and names we finally settled on a set of rules and goals and we stuck with the Ring (a few scowls 
and thwaps may have influenced this decision...har!). </P>
<P class="bodyblack">A few days later I wrote 
up the first Ring FAQ and here's how it started off:</P>
<BLOCKQUOTE> 
<P class="bodyblack"><EM>Q: Who/what is the Ring?</EM></P>
<P class="bodyblack"><EM>A: We are an unofficial 
ultra-secretive inter-server rogue association. </EM></P>
<P class="bodyblack"><EM>We are NOT an official 
EQ guild. Our members do NOT have guild tags and we are NOT registered/sanctioned in any way by EQ GM's. 
We operate "under the table" so to speak and that's just the way we like it. Our membership is to remain 
anonymous but we will have some type of "in-game" identification so members can recognize Ring brothers 
and sisters if they know what to look for.</EM></P>
<P class="bodyblack"><EM>Q: What is the Ring's 
main goal?</EM></P>
<P class="bodyblack"><EM>A: In a nutshell, our 
main focus is to provide a far-reaching, self-sufficient inter-server support network for our members, 
by our members. IOW, our primary goal is to HELP one another have successful roguely careers!</EM></P>
</BLOCKQUOTE>
<P class="bodyblack">You can read the entire original 
<A href="/staff/OriginalRingFaq.html" TARGET="_blank" class="bodylinks"><u>Ring FAQ</u></a>, dated 6/16/1999! The original draft was actually written on 6/8/1999 
but this one is the revised and formatted copy (I included paragraph indents! hehe). </P>
<P class="bodyblack">Also, you can check out the 
very first Ring roster <A class="bodylinks" href="/staff/old_ring_roster.html" TARGET="_blank">here</A>, 
created on 6/14/1999! Average roster per server is roughly TEN members or so... And look at those levels: 
Remmi 13, Vaslin 16, Zato 17, Kezzek 16... What a bunch of n00bs! *grin*</P>
<P class="bodyblack"><span class="bodytitle">"The Rogue is amazingly powerful" - Brad 
McQuaid</span></P>
<P class="bodyblack">For the first couple months or so everything at the 
Safehouse went rather smoothly but sometime around mid-June (after the forming of the Ring) we rogues 
started getting increasingly restless. The Rant Hall became a hotbed of activity and filled with angry 
and frustrated rogues frothing at the mouth. In practically every thread you could find words describing 
our class like "broken" and "incomplete" and before long we had a frenzy on our hands.</P>
<P class="bodyblack">It was a well-known fact that the Rogue needed some 
attention and we'd scour the patch messages with each new release for <EM>something</EM> related to the 
Rogue but it never came. What we found in those messages were usually things like "Enchanters have new 
spells..." and "Magicians now have better...." and "Druids get enhanced.." etc etc etc.. It literally 
went like this for at least two months after EQ's release--most every class was getting tweaked but not 
the Rogue...</P>
<P class="bodyblack">The main problem was that Rogues were few and far 
between and we were too easily dismissed by Verant because of our low popularity among the playerbase. 
When we griped, we were told such things like "Rogues rock! They are balanced! They get backstab! They 
get an XP bonus!" ... But my favorites were "You're playing your Rogue wrong!" and "If you don't like 
it, play a different class!" and "The first character to 50 was a rogue, dude--they must rock!"... *sigh*</P>
<P class="bodyblack">Huh? Rogues were decent in combat and that was about 
it. We were mini-tanks! We could dish out some decent damage but we couldn't take it very well and rogues 
could aggro a MOB easily with a backstab or two. But we really didn't care too much about that, it was 
the incomplete and/or unsupported skills that we wanted. We wanted more traps to find and disarm, more 
locks to pick, we wanted to be able to move while hidden, we wanted our poisons added to the game! We 
just wanted to be <EM>useful</EM>. We wanted to see "Dungeon party looking for Rogue specialist!" on the 
shout channel.</P>
<P class="bodyblack">Make no mistake, the Rogue was the last class added 
to the game and they were quite incomplete upon release and for some time thereafter. So, we had our case, 
now we just needed to get Verant's attention.</P>
<P class="bodyblack"><span class="bodytitle">A Sleeping Giant is Awakened...</span></P>
<P class="bodyblack">In the early days of EQ Rogue players were a rare 
breed. The Rogue was incomplete and everyone knew it so the class was avoided by most new players whom 
opted instead for one of the "uber" classes like Druid and Necromancer. </P>
<P class="bodyblack">However, those that <EM>did</EM> play Rogues were 
also rare breeds. These folks were dedicated, die-hard Rogue players that more often than not had a deep 
love of their "thiefly" characters for the most part stemming from prior experience with favorite characters 
in other games such as D&amp;D and MUDS, etc.. On a personal level my very first D&amp;D Basic Set character 
was a Thief, back in the late 70's, and my dedication to the loveable scoundrels only grew over the next 
20+ years with more D&amp;D exposure as well as thieves in literature. Anyway, I'll save <EM>my</EM> history 
for another article!</P>
<P class="bodyblack">So we didn't play rogues for power or prestige, or 
to be "uber", or show off our l33t skillz and gear. No, we played rogues because we loved our characters. 
We loved the role-playing potential. We played rogues because that's who we were, they defined us, and 
thus we were <EM>not</EM> content to "go lightly into the night" as Verant would have liked.</P>
<P class="bodyblack">Instead, we prepared for battle...</P>
<P class="bodyblack"><span class="bodytitle">Preparing for War</span></P>
<P class="bodyblack">The Safehouse was our ace in the hole, our strongest 
asset in the coming war. With it we could prove to Verant that we had strength, we had the numbers. It 
was the Safehouse that finally got Verant's attention although we tried (in vain) to spread the word in 
"Rogue" forums at the big fansites like Stratics, Vault, etc. to join our crusade but it proved to be 
an uphill battle.</P>
<P class="bodyblack">The most discouraging and hurtful thing in our cause 
to better the class was the propoganda that was spewed from a handful of fan-boys at one particular fansite's 
"Rogue" forum. The Forum Admin there played a Rogue but I'll withhold his name to save him some embarrasment. 
He preached to his sheep that rogues rocked and that the "whiney" Safehousers didn't know how to play 
the class properly. He <EM>talked the talk</EM> a good bit and he did it well because he had many a Rogue 
player believing him and when we occasionally stopped in to that forum to discuss things or requested 
support in our quest for enhancements we were pretty much labeled as "whiney upstarts". Well, in the end 
this False Prophet couldn't <EM>walk the walk</EM> because when things really started to look grim for 
the Rogue, Mr. Fan-Boy #1 quit his admin position in the forum and rolled up a Necromancer. He was <EM>not</EM> 
worthy--good riddance! </P>
<P class="bodyblack">And so we spread the word as best we could, both in-game 
and out, that rogues in general were unhappy with the state of the class and that we wanted some attention 
in the upcoming patches. </P>
<P class="bodyblack"><span class="bodytitle">More to come...</span></P>
<P class="bodyblack">Sheesh, this history is huge! I need a break but it's 
almost done. Next up:</P>
