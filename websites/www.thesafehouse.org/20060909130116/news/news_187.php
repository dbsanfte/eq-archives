<br>
<span style="font-weight:bold">MYTHIC ENTERTAINMENT TO POSTPONE PRODUCTION OF "IMPERATOR" MMORPG
<br>

<br>
Company to focus development on "Warhammer" MMORPG and "Dark Age of Camelot"</span>
<br>

<br>
FAIRFAX, VA - July 13, 2005 - Mythic Entertainment, developer and
publisher of massively-multiplayer online role-playing games, today
announced the production postponement of "Imperator," the sci-fi MMORPG
previously scheduled for a June 2006 release. The decision was made
after a complete internal review of the project status.
<br>

<br>"This was the most difficult decision that we have ever had to
make," said Mark Jacobs, CEO and president of Mythic Entertainment.
"However, our tremendous success with 'Dark Age of Camelot' set the
standard for Mythic of releasing nothing less than triple-A games, and
'Imperator' was simply not meeting that standard."
<br>

<br>There are no personnel layoffs planned as a result of this
decision. Staff will be reassigned to the ongoing development of "Dark
Age of Camelot" including the next expansion, "Darkness Rising,"
scheduled for Q4 '05. In addition some personnel have already been
assigned to the new "Warhammer" MMORPG which Mythic announced at <a href="http://www.e3expo.com">E3</a> in
May 2005.
<br>

<br>"Our team made a tremendous effort with the game and the praise it
drew at <a href="http://www.e3expo.com">E3</a> and in previews reflected their skill and dedication,"
Jacobs continued. "But in the final analysis it just wasn't great
enough to be a Mythic game at this time."
<br>

<br>Mythic will perform an internal reassessment of the game's features
and make a decision on how to proceed with its development in the
future.
<br>

<br>The decision to postpone "Imperator" also means that the company's
plans to establish a European publishing office have been postponed. It
is expected that those plans will resume as the company prepares for
its 2007 worldwide publication of the "Warhammer" MMORPG.
<br>

<br>
About Mythic Entertainment
<br>

<br>Mythic Entertainment is one of the most successful developers and
publishers of massively multiplayer online role-playing games in the
world. Credited with 15 online games, including the award winning "Dark
Age of Camelot(R)," Mythic's success is based in proprietary
technology, superior game design and exemplary customer service
delivered by a talented staff of more than 160 people. "Dark Age of
Camelot(R)" has been played in almost every country in the world, and
has been translated into seven languages including recently Spanish and
Japanese.
<br>

<br>In March 2003, TA Associates completed a $32 million investment in
Mythic, the largest ever granted to any independent game developer.
Mythic has been recognized for their business achievements by the Inc.
500, Deloitte Fast 500, and as a 2004 finalist in the Dell Small
Business Awards. In addition to the next "Dark Age of Camelot(R)"
expansion "Darkness Rising," Mythic is currently developing the new
fantasy MMORPG based on the popular Games Workshop property, Warhammer.
