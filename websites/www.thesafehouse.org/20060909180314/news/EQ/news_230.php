<!--startscanning-->Today SOE issued a <a href="http://sonyonline.com/corp/press_releases/010306_EQPOR.html">press release</a> regarding their upcoming EverQuest expansion, Prophecy of Ro.<br><br><br><span style="font-style:italic"></span><em style="font-weight:bold; font-style:italic"></em><span style="font-style:italic"></span>
<p align="center">SONY ONLINE ENTERTAINMENT ANNOUNCES <br>
  EVERQUEST® PROPHECY OF RO™ EXPANSION FOR EVERQUEST®</p>
<p align="center"><em>Transform the Online World of Norrath Like Never Before 
  </em></p>
<p>&nbsp; </p>
<p><strong>SAN DIEGO, CA – January 3, 2006 –</strong> <a href="http://www.station.sony.com/">Sony Online Entertainment</a> Inc. (SOE), a worldwide leader in massively multiplayer online 
  gaming, today announced <em>EverQuest® Prophecy of Ro™</em>, a new 
  expansion for the incredibly popular PC Massively Multiplayer Online Role-Playing 
  Game (MMORPG), <em>EverQuest®. EverQuest Prophecy of Ro</em> is scheduled 
  to be available at retail and for digital download from <a href="http://www.station.sony.com/">Sony Online Entertainment</a> 
  in February for the suggested price of U.S. $29.99.</p>
<p>“<em>EverQuest Prophecy of Ro</em> will give players the opportunity 
  to interact with and transform the online world of Norrath like never before,” 
  said Chris Sturr, Sr. Brand Manager, <a href="http://www.station.sony.com/">Sony Online Entertainment</a>. “In addition 
  to a variety of new features, <em>EverQuest Prophecy of Ro</em> continues the 
  fascinating storyline as new and old players alike explore, battle and adventure 
  through <em>EverQuest</em> on more than 30 new missions encountering new creatures 
  and environments throughout the journey.”</p>
<p> Players will be able to interact with the world thanks to three new features. 
  The first, spheres of influence, can be used to thwart your enemies or aid allies. 
  The second feature is a trap system that will let players deceive opponents 
  with player-set traps. And for the first time in <em>EverQuest</em>, players 
  will be able to destroy objects in the world; doors can be bashed in, walls 
  can be toppled and towers can be destroyed, leading to a new level of interactivity 
  within the game and opening up a new realm of design.</p>
<p> <strong>About <em>EverQuest Prophecy of Ro</em></strong><br>
  The gods have returned and the mortal realm shifts with their influence. But 
  a new presence walks among the gods, a malevolent force released into godhood 
  by the follies of mortals. Unbound by the laws of the pantheon, it leaves corruption 
  in its wake and threatens to bring the entire pantheon crumbling down. The only 
  hope for Norrath hangs on the very mortals who have unleashed this upheaval. 
  <em>EverQuest Prophecy of Ro</em> includes the following features:</p>
<ul><li> <strong>Player-set Traps:</strong> Control your battles before they even 
    begin by deceiving your opponents with a new arsenal of player-set traps!</li><li> <strong>Spheres of Influence:</strong> Transform the world of Norrath by 
    creating magical spheres of influence to thwart your enemies or aid your allies. 
    These artifacts will create a localized area where the rules of the game can 
    change.</li><li> <strong>Destructible Objects:</strong> Interact with and destroy pieces 
    of the game world leading to a new level of interactivity within the game. 
    Chop down doors, tumble walls, and destroy encampments!</li><li> <strong>Buff Filters:</strong> Decide which buffs you would rather auto-decline 
    with the new buff filtering system!</li><li> <strong>8 New Bank Spaces</strong></li><li><strong> New Missions:</strong> Battle your way through over 30 new missions. 
  </li><li><strong>New Creatures, New Environments and New Encounters:</strong> More 
    than 25 unique creatures, 7 Zones, and 6 Small Instances.</li><li> <strong>New Spells, Tasks, Disciplines and Trade skills</strong></li></ul>
<p>The new expansion will be downloadable via the <em>EverQuest</em> Station Store 
  for U.S. $29.99 (plus tax where applicable). Retailers will be offering a boxed 
  version that will carry a suggested retail price of $29.99, as well as a special 
  in-game item, a Warhorse mount, for players to enjoy. Additionally, a reduced 
  price of $26.99 will be available to consumers who pre-order and purchase the 
  digital download version direct from <a href="http://www.station.sony.com/">Sony Online Entertainment</a>. For more information 
  on <em>EverQuest: Prophecy of Ro</em>, please visit: <a href="http://eqplayers.station.sony.com/expansions/prophecyofro/">http://everquest.station.sony.com/por/</a>.</p>
<p><strong>About <a href="http://www.station.sony.com/">Sony Online Entertainment</a></strong><br>
  <a href="http://www.station.sony.com/">Sony Online Entertainment</a> Inc. (SOE), a subsidiary of Sony Pictures Digital 
  Inc., is a recognized worldwide leader in massively multiplayer online games, 
  with hundreds of thousands of subscribers around the globe. SOE creates, develops 
  and provides compelling entertainment for the personal computer, online, game 
  console and wireless markets. Known for its blockbuster franchises and hit titles 
  including <em>EverQuest®</em>, <em>EverQuest® II</em>, <em>Champions 
  of Norrath®</em>, <em>Untold Legends™</em>, and <em>PlanetSide®</em>, 
  as well as for developing <em>Star Wars Galaxies™: An Empire Divided™</em>, 
  SOE continues to redefine the business of online gaming and the creation of 
