<img src="/staff/llabak.jpg" alt="Llabak" hspace="10" vspace="5" align="left"><span class="bodylinks"> Dave "Llabak Tharr" Barrett started gaming at the ripe old age of 8, when his older brother brought Advanced Dungeons and Dragons home from High School. His first character was a Ranger named "Fred" (gimme a break, I was 8. We all had Rangers back then). After experimenting with games like Gammaworld and Runequest, he eventually got sucked into the PC Port for Ultima 3. woot <br>
 <br>
A chronic underachiever, he spent 4 lovely years in boarding school in Rhode Island at the Portsmouth Abbey School. It's truly amazing how often the term "Dress wearing freak" could be used in a single discussion about our instructors, many of whom were monks. In hindsight, they were actually pretty amazingly cool people. At the time, however, they were often just "dress wearing freaks." Despite that, those 4 years were a remarkably enjoyable time, just like college, but with a little less booze and a lot fewer girls. And probably more studying, but you didn't hear that from me. <br>
<br>
4 further lovely years were spent in beautiful Worcester, MA (welcome to Wistah! Dollah twenty five, please!). It was at college where Llabak was born as a character, being...drum roll please...an outcast drow elf in a D&amp;D game. I actually never even heard of Drizzt until about 3 years ago, and quite frankly wish I never had. That's another story though :)  Anyway, Llabak showed up in various incarnations over a number of years, entering the online world in Ultima Online back in 1997ish. A total n00b, he was robbed, ganked, guard-trained, corpse camped, and who knows what else. He got into his guild there, and still plays with them in each new game as he gets the chance. <br>
<br>
Making the transition into EQ beta was tumultuous. For some reason Dave decided to RP (heh) a Dwarven cleric of Erolisi Marr. Little did he know that Dwarves couldn't choose her as a starting deity. So that idea thrown to the wolves, and a return to his skulking roots ensued. Llabaktharr Gigglegiggler, the gnomish rogue came to life on Tarew Marr. Through a chance encounter with Zato in Kedge Keep, Dave joined the SH in its infancy, and moved from lurker to regular to moderator over the years. <br>
<br>
Meanwhile, out in the real world, Dave worked for the esteemed public accounting firm Arthur Andersen for 3 years in their Boston office, where he saw some questionable things, but had nothing to do with the Enron debacle. After bailing, he spent 6 years pursuing a PhD in business, which, depending on the winds of Fate, he may or may not complete some time this year. In the meanwhile he's taken up residence at the Maine Business School as Assistant Professor of Accounting. In case you cared, Dave is a licensed CPA in the state of Indiana. </span><br>
