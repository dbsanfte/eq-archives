<div align="center" class="vgtitles"><strong>The 3 Class Spheres of Vanguard</strong></div>
<div align="center" class="vgminitext">Please note: Currently Vanguard: Saga of Heroes is still in it's Beta stage of development. And so much of the detailed information is still unavailable at this time, or in a state of change. The Safehouse will continue to update and add information as it becomes available</div>
<img src="/vanguard/images/concept/undead2.gif" height="200" hspace="10" vspace="5" align="right">
 <div class="vgtext">

      <p >In Vanguard one is not limited to choosing a single class to play through out their character's existence. Vanguard uses what has been called the Sphere system, where players can choose and play classes in one of 3 spheres, and may switch back and forth as they see fit. A player can devote to one sphere and it will be a full and satisfying game experience. However, the system is called the sphere system because each class sphere over laps with the others. Benefits and levels are acquired separately in each sphere, but some of the benefits do carry over, ensuring that if you have a high level in one sphere, you are not totally defenseless when you start a less well advanced sphere. In addition to the mixing of the 3 class spheres for player characters, the combination and over lap of the spheres is crucial to group game play. Each of the spheres in turn may need to rely on the others at times to accomplish their goals. </p>
      <p >The 3 spheres are Adventuring, Crafting, and Diplomacy. You play each sphere individually and without the abilities of the other spheres. Your current sphere is determined by the equipment you choose to wear at the time. In each of the spheres, you have the chance to test out each of the classes until you are, at a certain level, asked to make a final decision which you must stick with. </p>
      <p><span class="vgsubtitles">The Adventuring Sphere</span></p>
      <p>The Adventurer Sphere is comprised of the traditional role playing classes that are common to many fantasy Massive Multi-player Online Games, including the Warrior, the Cleric, the Rogue, ect.., and a few unique others. This is the sphere in which players combat and kill monsters for adventuring experience, collect loot, find and complete quests, and master skills that mainly deal with combat. </p>
      <p>An additional part of the Adventurer Sphere is the Harvesting Classes. These classes are a sub-set of the Adventuring Sphere which you activate by equipping the appropriate harvesting equipment. The level of your Adventuring Sphere in part impacts how strong and resilient you are in the Harvesting class. In this sphere players are able to harvest resources in the environment. The system of Harvesting is much like the combat system Adventuring Classes use to kill monsters. </p>
      <p><span class="vgsubtitles">The Crafting Sphere </span></p>
      <p>The Crafting Sphere, like the Adventuring Sphere, contains a number of crafting classes, each devoted to their particular trade. These include classes like the Stone Mason, the Carpenter, and the Shipwright. Just like the Adventuring Sphere Classes, Crafting Classes have their own special equipment, their own levels of advancement, and their own special abilities which aide them in achieving their goals. And just like Adventuring Classes, Crafting Classes suffer the detrimental affects of trying to gain experience and failing. Just as Adventuring Classes suffer a penalty for losing a fight, so do Crafting Classes suffer for failing to produce items. However, unlike other versions of crafting in other games, Item Crafting in Vanguard has it's own unique and engrossing system of game play. It is entirely feasible to have a fulfilling play experience playing just in the Crafting Sphere alone, and at no time are crafters required to depend on the other spheres to advance down their chosen path. Crafters may in fact level to the highest heights without ever leaving the cities. </p>
      <p><span class="vgsubtitles">The Diplomacy Sphere </span></p>
      <p>The 3 rd class sphere in Vanguard is that of the Diplomat. Where fighters use swords to win the day, the Diplomat uses words. Where the Crafter creates material things of value, the Diplomat creates deals and agreements between themselves, and other parties. The Diplomacy sphere offers players the chance to interact on NPCs in the game world on a deeper level than just accepting quests and receiving rewards. Diplomats use their skills to intimidate, flatter, belittle, cheer up, or bring down NPCs. For Diplomats, a city is a dungeon. City officials and NPCs are their monsters, prestige and concessions their loot. Diplomats have the ability to affect how a city runs and operates by interacting with the various functionaries of the city. They broker trade deals, or worm information out of others, or lobby the city officials for concessions. Diplomats can also find new ways to approach and deal with situations that Adventurer would typically have to fight through, like convincing a monster to not attack by negotiating with it.. Diplomats engage in 'combat' with their opponents using the same style of system that Adventuers and Crafters use. And like the other two spheres, Diplomats have their own quests to accomplish. For the player, the Diplomacy sphere offers an entirely new 3 rd facet of game play in which they can explore and interact with the world of Telon. </p>
      <p>At this time, it is not currently known whether or not the Diplomacy Sphere has individual classes within it. </p>
