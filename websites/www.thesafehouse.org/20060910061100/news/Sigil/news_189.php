<span style="font-style:italic">Since it’s not quite time for a Fanguard…. how about a Fanguard Beta!
As promised, we will have events for our players. Even before Beta
starts!
</span><br style="font-style:italic">


<br style="font-style:italic">
<span style="font-style:italic">Join us in Las Vegas on July 29-30th for Fanguard Beta and get a
sneak peek at <a href="http://www.vanguardsoh.com">Vanguard</a>, meet our team and a chance to win great prizes!
</span><br style="font-style:italic">


<br style="font-style:italic">
<span style="font-style:italic">
We will start the event with a private party at Planet Hollywood.
You’ll join us for food and drinks and meet and get acquainted with our
team. On Saturday, you can see a demo of Vanguard: Saga of Heroes and
meet team members for discussions and feedback. Then we will all meet
at Game Works for some food, fun and games! And we will end it all by
giving away some great prizes, including signed concept art and Beta
spots! </span><br style="font-style:italic">


<br style="font-style:italic">
<span style="font-style:italic">
And that’s not all! In the gaming industry’s first “cattle call,” we
will be interviewing as many of you as we can for future positions here
at Sigil! We will have our team on site to interview for positions
including Associate Designers, Programmers, Artists and Game Masters!
We have lots of positions to fill in the coming months and we’d like
the chance to hear from our community members that are interested in
joining our team.
</span><br style="font-style:italic">


<br style="font-style:italic">
<span style="font-style:italic">So join us in Las Vegas for a weekend of fun with your fellow
gamers! It will be the first of many times we all travel to meet our
Vanguard friends! </span><br style="font-style:italic">


<br style="font-style:italic">
<span style="font-style:italic">
Space is limited so register soon!
</span><br style="font-style:italic">


