	 <div class="article_title_prnt">E3: SOE Out of the Box</div>
     <div class="article_auth_prnt">By Fricka</div>
     <div class="article_details_prnt">Wed, 17 May 2006, 12:26:00</div>
	 <br><br>
     <p><p class="MsoNormal">For the past few years Sony Online Entertainment has
basically had the same sort of display at E3. A large rectangular black box,
one side with windows giving you a tease of the demos and goodies inside, while
other sides showing game signage and videos but lacking proper demo boxes. This
year they finally broke that model and had several public demo stations outside area
as well.</p>



<p class="MsoNormal">No wonder, you might say, because SOE is now a co-publisher
for former rival, Sigil Games Online’s upcoming gem, <span style="font-weight:bold">Vanguard: Saga of Heroes</span>.
Add them to <span style="font-weight:bold">Gods and Heroes</span> plus the ongoing SOE titles of <span style="font-weight:bold">EverQuest</span>, <span style="font-weight:bold">EverQuest
II</span>, <span style="font-weight:bold">Star Wars Galaxies</span>, <span style="font-weight:bold">Planetside</span>, etc and the Black Box is just about
bursting.</p>



<p class="MsoNormal">When the big news was announced a few days before E3 I knew
it would be an interesting year. As of years past, I had plans to visit both
SOE and Sigil. Only this year they were in the same location. So, on Wednesday at
1:00 pm, I met with President of SOE,
John Smedley, and at 3:00 pm I met
with Brad McQuaid, CEO Sigil Games Online and Jeff Butler, President of Sigil
Games Online. In between that time I was kept inside the Black Box, plied with
brownies (good brownies) and dazzling computer screens before me. I can also
tell you it was a bit strange seeing familiar faces mixed up in close proximity,
some of them even vying for my attention.</p>



<p class="MsoNormal">Neither of my meetings was to be specifically about the SOE
/ Sigil co-publishing announcement but it was a natural topic. So, what did the
heads of SOE and Sigil have to say about the co-publishing situation? The same
thing they have been saying since the announcement basically. Not much I can
add publicly except that I got to hear it in person with them looking me in the
eye. Some have surmised that SOE will have nothing to do with the creative
process behind Vanguard and will always just remain in a co-publisher position.
At this point I’m going to believe that. In my opinion, SOE has a lot on their
plate right now. This is different than their past relationship with what was
then Verant when EQ was *the* break out MMORPG. Now, SOE seems more focused on
supporting many games. It won’t benefit SOE to try to micromanage a title that
already has such ardent fans such as Vanguard. On Sigil’s side, I have always
known Brad and Jeff to be passionate about their game. Brad seems very
protective of not only the game but the kind of community he wants to build
around it. I do not see him entering a partnership that would endanger either
one.</p>



<p class="MsoNormal">On the record, Smedley was eager to mention future plans for
SOE. He mentioned an “MMO shooter” which he described as “24 crossed with
Counter Strike and Alias”. He also confirmed they were working on an MMO based
on the free system which additionally would have downloads limited to 2MB no
matter when you jumped into the game, this is apparently slated for next year
and will be similar to other free MMO systems like Runescape where certain
areas would have a charge associated with it.</p>



<p class="MsoNormal">When we touched on Star Wars Galaxies and lessons learned
there for future games, Smedley stated that SOE games would have longer betas
and added, there would be “no Galaxies situation ever again”.</p>



<p class="MsoNormal">So how is SOE going to handle publishing all of these games?
It seems the SOE Launcher is going to be a major way they plan to do this. For
one, patches will be handled differently. They will have a background mode and
will be throttled down when your system is busy.<span style="">&nbsp; </span>I spoke up at this point that SOE be careful
about full disclosure on this. I also recommended a scheduler feature be added.</p>



<p class="MsoNormal">What was more interesting in my opinion was the social
system that would be part of the Launcher. The features of this put me solidly
in mind of Xbox’s system of being able to communicate with gaming friends no
matter what game they are playing. Essentially you could be playing Vanguard
and see one of your old EQ1 buddies on your friends list… under the name you
knew them by. You could send them a tell and they’ll get it as coming from your
old EQ1 name so they recognize you. You could then say, “I’m playing Vanguard
as SoandSo, come on over” and they can log off and join you or you can log off
and join them. I asked about privacy. There will be 4 levels of security it
seems so you can flag who can see which character of yours and under what name
you will appear to them.</p>



<p class="MsoNormal">This, I think is going to be a large boon in terms of
growing and keeping MMORPG communities. It seems like a smart move in the face
of WoW’s enormous subscription numbers as well since an oft heard complaint for
WoW is that the high end does not keep a gamer’s interest. With a system like
this in place for SOE’s games, if players come back, they can reconnect with
old friends more easily. The Vanguard example given was also a confirmation
that Vanguard is going to be on the Station Access plan. <span style="font-weight:bold">Gods and Heroes</span>, by
the way, will not.</p>



<p class="MsoNormal">On a closing note, Smedley also mentioned guilds being able
to watch movies together, “picture your guild watching a movie together and
being able to communicate, like Mystery Science Theater”. Personally, if I’m
watching a movie at home I’d rather watch it on my TV while on my couch. And,
while I was a fan, Mystery Science Theater got quite annoying sometimes. So,
we’ll see SOE. I’m looking forward to being able to keep up with friends old
and new and having some of the SOE titles integrated with the Station Access
one fee for all deal, but watching movies on my computer… not so much. That may
be just a little too far out of SOE’s box.</p>



<p class="MsoNormal">~ Fricka</p>



