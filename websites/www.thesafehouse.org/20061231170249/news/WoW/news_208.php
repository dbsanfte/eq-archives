<!--startscanning--><p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Blizzard Entertainment wanted to host their first fan convention. As I found out later from&nbsp;Rob Pardo, Vice President of Game Design&nbsp;(who says he reads <a href="http://www.thesafehouse.org">The Safehouse</a> all the time so, “Hi Rob!”) they at first thought of only having a convention focused only on World of Warcraft but he said if they were going to do it, “Let’s do it big”.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">&nbsp;</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">And did they ever.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">&nbsp;</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">I, like thousands of other fans of Blizzard’s games fans pre-purchased our BlizzCon tickets. I arrived at the Anaheim convention center around 9:30 AM, the doors would officially open at 10 AM.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">&nbsp;</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">I saw a long line formed outside the convention center. Oh well, it’s what I expected arriving this close to opening.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">&nbsp;</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">After I had parked and started to walk toward the line; however, I realized that the folk in it were already wearing badges around their necks. I still had to pick mine up. Someone in line verified for me that the badge pick up line was “that way”.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">&nbsp;</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">I walked around a corner and saw a longer line extending away from the convention center doors. I kept walking. I went around a curve of the building. I kept walking. I went around another corner of the building. I kept walking. Finally, the end of the line was in sight and there were more still more people filing in behind me.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">&nbsp;</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">The keynote address was to be at 10:30 AM, I didn’t think I was going to make it inside in time. I was right. I did get to talk to some folks in line with me. I even took some pictures. Pretty much folks in costume were our in-line entertainment, as they walked past to get in line everyone made them stop for a picture.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">&nbsp;</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Apparently we were still the lucky ones though, these two guys wanted to buy tickets. 2 for $300.</font></p><p class="MsoNormal" style="margin:0in 0in 0pt"><br></p><p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2"><a href="http://www.thesafehouse.org/mugshots/blizzcon2005/IMG_1965"><img alt="" src="http://www.thesafehouse.org/mugshots/albums/blizzcon2005/IMG_1965.thumb.jpg" align="" border="0"></a><br></font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">&nbsp;</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">By the time I got my badge, the other line to get in the venue was moving constantly so I didn’t have much more time to wait to get in, but I still missed the keynote address.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">&nbsp;</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Entering the Convention Hall I was greeted with a scene akin to a mini <a href="http://www.e3expo.com">E3</a>. Banners and screens were hung from the ceiling. The banners indicated which part of the convention floor was for which game and the screens displayed looped demo films. Life size statues of Nova and a World of Warcraft dwarf were on display. I recognized them from <a href="http://www.e3expo.com">E3</a>. Underneath all this was the crowd and the lines. There were lines for playing the WoW expansion, lines for playing Starcraft: Ghost, lines to buy food, a very long line to pick up your goodie bag, and a nearly equally long line to buy stuff from the Blizzard booth. It made me think of Disneyland, which was just across the street. But like its “Happiest Place on Earth” neighbor, the mood at BlizzCon was generally of fun and excitement. I joined up with Woody (GU Comics) and his girlfriend Kyndra and we&nbsp;picked up our goodie bags without standing in line. How? Super powers.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">&nbsp;</font></p>
