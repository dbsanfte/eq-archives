<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Well, for their first convention, Blizzard certainly pulled it off in my mind. Blizzard did a great job with presentation and eye candy – Oh, did I mention they had some paid models on hand? A male dwarf, 2 female night elves and 2 human casters (females) and one Nova from Starcraft. Find that gender ratio a little skewed? It's ok, as can be expected, there were many more males at the Con than females.</font></p>
