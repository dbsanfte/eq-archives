<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Some of the best presentations had many questions from the audience and a break from the bulleted lists. Starcraft showed us a model for the Protoss for instance which <em>supposedly</em> wasn’t going to be shown. Jeff in his portion of the Battleground panel, told us side notes as well and got a number of laughs. The webcomics panel however was almost non stop laughter and basically took questions from the audience for the whole hour. They covered ground such as covering gaming politics (Gabe and Tycho are not going to be arrested) to blackmail and gamer sex life. Of course these guys have already proven they are funny so it wasn’t surprising that it carried through in real life.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">&nbsp;</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">However, even though each Blizzard panel session may not have contained as much Q&amp;A or feedback time as some would like, I noticed that panelists seemed quite willing to be cornered after their presentations and be asked questions individually. Brave souls. We’ll see if they keep that up next con ;)</font></p>
