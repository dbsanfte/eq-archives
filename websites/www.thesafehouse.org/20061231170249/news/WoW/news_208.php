<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">However, I felt the panels could’ve been a little more interactive. I liked being shown the evolution of some things like classes and the talent system but I wanted to know about future plans and to hear the concerns of the audience in attendance. </font></p>
