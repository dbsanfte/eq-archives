<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">&nbsp;</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Eric Daws – Professions</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Professions, stat, on average 20 million items are made each day in WoW. </font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff"></font><font size="2">&nbsp;</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2"><strong>Professions Future</strong></font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Patch 1.9</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Addressing concerns for fishing: macroing of fishing which led to reducing the values of this, no searching for a target.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Possible solution: going to be nodes of fish, qualitatively better stuff in nodes.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">&nbsp;</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Enchanting: Enchanting Oils. Increase spell damage or man aver time regen. This will be an item so you can give it to friends, etc.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Nexus Crystals from disenchanting high level purples and very rarely, high blue.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">&nbsp;</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2"><strong>Professions in the Expansion</strong></font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Specializations – tailoring, alchemy, elixir potions maybe, not set in stone.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Jewelcrafting - <span>&nbsp;</span>Rings, necklaces, crowns. Jewelcrafter only items.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Socketable Mystic gems which you can put into things to improve them.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">&nbsp;</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2"><strong>Items </strong>- Scott Mercer</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">&nbsp;</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">There is a system in place to create items and determine statpoints for them. Considerations include item quality, item level and item slot.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">14 points = 1% crit hit</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">12 points = 1% dodge</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">The system promotes having different stats for an item.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">&nbsp;</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">They also have general guidelines such as no strength AND attack power on an item.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">&nbsp;</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Side note, 3D Studio Max is used to design the graphics for the item. They start with concept sketches of course.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">&nbsp;</font></p>
