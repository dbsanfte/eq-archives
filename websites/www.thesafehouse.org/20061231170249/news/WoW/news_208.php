<p class="MsoNormal" style="margin:0in 0in 0pt 0.5in; text-indent:-0.25in"><font color="#ffffff"><font size="2"><span>-<span style="font-family:'Times New Roman'; font-style:normal; font-variant:normal; font-weight:normal; font-size:7pt"><font face="Verdana">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </font></span></span>They wanted a limited pool of points. So for instance, they are unlikely to implement a system where you would gain points rather than exp if you were already at a level cap.</font></font></p>
