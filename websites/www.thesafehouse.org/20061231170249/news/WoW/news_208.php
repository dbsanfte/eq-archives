<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Caster PvP is being looked at, spell penetration, see other notes.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">200 handcrafted items for Ahn’Qiraj</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">&nbsp;</font></p>
