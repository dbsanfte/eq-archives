<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">In terms of other activities I think what was missing was more group activities for those not involved in the official gaming competitions, which I’m sure were fun, but were for the most part hard to watch as an observer especially with other sound and visual input going on around the stages. Something like EQ’s LiveQuest would’ve been fun for instance, though of course that might be problematic for the non WoW players in attendance. It would have helped fellow WoW server players find each other though. They had realm tables for that purpose but I think it was too loud for folks to really get a bonding situation going. At least one other person seemed to want to connect with fellow server mates, while I was playing the expansion Saturday I noticed someone in the chat channel trying to find&nbsp;others from their server to get together to form a raid. Unfortunately, the person didn’t seem successful at it.</font></p>
