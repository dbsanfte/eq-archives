<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2"><strong>History of Classes</strong></font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Classes that got cut: Necromancer, Death Knights. They wanted each class to be unique.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Mage intended to be a high damage class. Chains of Ice for crowd control.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Paladins (big boo from the crowd). Defensive in nature, intended to be easier to play.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Priest. In WoW they mde them more of a caster class then the armored cleric of old. They introduced racial spells, unholy line for the undead priests, with this class. Racial spells will be expanded in the expansion. </font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Shaman. (some one cried out "Nerf Shamans!")</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Warriors. No crowd reaction.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Hunter. Pet class but they wanted to care about your pets, hence the feeding and loyalty.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Rogue. Combo point system was meant to be flexible.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Warlock. They wanted a variety of pets.</font></p>
<p class="MsoNormal" style="margin:0in 0in 0pt"><font color="#ffffff" size="2">Druid only true hybrid class that they planned, at first dindt want to have at all because in WoW lore druids were male only.</font></p>
