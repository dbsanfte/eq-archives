<!--startscanning--><p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2><a href="http://www.e3expo.com">E3</a>, the Electronic Entertainment Expo, is the yearly event for the industry where companies compete for attention and game makers simultaneously get a chance to check out competitors, attend conferences, and network. Add “Booth Babes”, plenty of Vegas style “flash”, plus fan boys and girls, then mix it all up and dump it in downtown Los Angeles.</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>&nbsp;</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>This was my fifth <a href="http://www.e3expo.com">E3</a> and since they seemed to have been getting increasingly more crowded and noisy, I had at first feared the worst for this year’s event until I started hearing rumors that security and requirements to get in were being tightened. <span>&nbsp;</span><a href="http://www.e3expo.com">E3</a> is a “trade only” event, as their website, and signs at the convention, reminded everyone. Sure enough, it turns out that just to get into the media center room this year you had to show a photo id before even getting to the counter to register for a badge. During the Expo they had signs informing one that badges and badge holders would not be replaced if you lost them. At the hall entrances they had UV light scanners at every door this year when last year they only seemed to be&nbsp;using UV on Thursday and Friday and just doing naked eye checks on Wednesday.</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>&nbsp;</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>Once I got into the convention halls, I was glad of the tightened security. While <a href="http://www.e3expo.com">E3</a>, make no mistake, is a crowded event (over 60,000 attended this year) it seemed to be not as packed as in the past and I suspected they had stopped many folks who were used to sneaking in or getting in under iffy credentials. I also noted many uniformed police in the convention center public areas. I had not seen so many of them since the <a href="http://www.e3expo.com">E3</a> the year of 9/11.</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>&nbsp;</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>Another trend I noticed this year was the effect of increased technology. There were more video cameras rolling this year it seemed, all shapes and sizes.</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>&nbsp;</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>The biggest longest running trend though is the giving of awards. Who gives them out? Well, it seems everyone does, the official <a href="http://www.e3expo.com">E3</a> folks, TV shows, game magazines, etc. I figured I may as well join them this year.</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>&nbsp;</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>And so… read on for the Fricka Awards:</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>&nbsp;</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2><strong>Most Impressive Progress Made:</strong></font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2><strong><em>Vanguard: Saga of Heroes</em></strong></font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>Vanguard’s first official <a href="http://www.e3expo.com">E3</a> presence was just last year. It has gone through a lot in a year. The interface is much much better and takes up less real estate on the screen. Character customization seems has done much to add variety to the demo (the animations are still the smoothest I’ve seen for any online game). Feature article forthcoming, screenshots and artwork already up in the Safehouse Mugshots section.</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>&nbsp;</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2><strong>Game Which I Already knew About But Only Really Got My Attention at E3:</strong></font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2><strong><em>Warhammer Online</em></strong></font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>I like that this game has different ways of mixing up PvP and PvE. I’ll be doing a full write up on this but already put up some exclusive screenshots provided to <a href="http://www.thesafehouse.org">The Safehouse</a>.</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><span><font face=Arial color=#ffffe0 size=2>&nbsp;</font></span></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2><strong>Runner Up:</strong></font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2><strong><em>Lego Star Wars II</em></strong></font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>Love Legos. Love Star Wars (the Original Trilogy) this combines them so you can play through famous scenes and also mix and match “Lego” pieces to form strange Greedo / Luke characters if that’s your thing.</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>&nbsp;</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2><strong>Most Anticipated:</strong></font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2><strong><em>Spore</em></strong></font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>Even if this doesn’t deliver on all that’s promised, if it gives you half of it, it still looks great. I noticed some hitching during the demo though. Good thing I have a top end video card. I hope you do too.</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>&nbsp;</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2><strong>Best Swag:</strong></font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>256 Mb Flash&nbsp;stick keychain in a super cool faux leather covering with the Warhammer Online logo on it. Disclaimer: this probably shouldn’t count because this was from a special appointment, but it’s my awards… so there.</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>&nbsp;</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2><strong>Most Unexpected Appointment:</strong></font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>Meeting with John Smedley, President, SOE set up and accepted post-“Sigil and SOE” announcement. I figured the oft attacked&nbsp;Smedley would not want to meet any community press familiar with the controversy. When he did accept, I anticipated it might be a 10 minute meeting but we got a meeting room and a good chunk of time. A lot was said off the record but I’ll be putting more information in a separate write up.</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>&nbsp;</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2><strong>Battle of the Consoles Winner:</strong></font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>Nintendo <strong>Wii</strong></font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>They win because they have games that include “sword fighting” using the special controllers. Disclaimer: I’m biased since I’m a fencer and this is at least one step closer to the ultimate fencing game of my dreams.</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><span><font face=Arial color=#ffffe0 size=2>&nbsp;</font></span></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2><strong>Strangest Game:</strong></font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2><strong><em>Audition: Battle Dance</em></strong></font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>A DDR MMO. Need I say more?</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>&nbsp;</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2><strong>Tackiest Booth Babe Outfit to Comply With the New Dress Code But Get Sex Appeal:</strong></font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>From fitting, thin, silver unitards. Other booths had the more traditional show skin look, these silver things well I’m sure they made some guys drool but, how shall I say this… a girl can’t always be posing just so to make her look good wearing one of these and sometimes no matter how thin you are, you can’t avoid rolls.</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>&nbsp;</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2><strong>Worst Outfits for Game Demo Folks to Have to Wear:</strong></font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>Pacman heads. I felt for those folks having to wear a, round, stuffed, synthetic hat to heat up their head while they talked to folks in a crowded convention center.</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>&nbsp;</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2><strong>Most Prevalent Game Theme:</strong></font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>Sci Fi first person shooters</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>&nbsp;</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2><strong>Most Promising Single Player Rogue Game:</strong></font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>Assassin’s Creed. More information forthcoming.</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>&nbsp;</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2><strong>Best Booth Design:</strong></font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>N-Gage (yes, they’re still around, they’re making games for more than their own phone model). They get points for space, nice use of mist and light effects and comfy seating.</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>&nbsp;</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2><strong>Best Kept Secret:</strong></font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>Draenei race as the new Alliance race for World of Warcraft. Sure it was guessed but they still did a good job of keeping it unconfirmed.</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>&nbsp;</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2><strong>Rooting For Award:</strong></font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2><strong><em>Huxley</em></strong></font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>This game looked great and was in playable form at <a href="http://www.e3expo.com">E3</a>. I hope it can handle online play. It’s supposed to be an MMOFPS: Massively Multiplayer Online First Person Shooter and could prove to be a fun, fast escape from the fantasy lands where I will otherwise plan on spending my gaming time.</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>&nbsp;</font></p>
<p class=MsoNormal style="MARGIN:0in 0in 0pt"><font face=Arial color=#ffffe0 size=2>Well there you have it, the first set of Fricka Award winners. You’ll forgive the winners if they don’t all make acceptance speeches. I’ll just have to say it for some of them with forthcoming articles so keep an eye out for those as well as for video footage.</font></p>
