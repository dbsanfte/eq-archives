<p align=left><span style="FONT-SIZE:10pt; FONT-FAMILY:Verdana"><span style="FONT-WEIGHT:bold"><a class=postlink href="http://www.gamesindustry.biz/content_page.php?aid=7991" target=_blank><em><font size=1>Xbox 360 name confirmed; console design details revealed</font></em></a></span><em><font size=1> <br><br><span style="FONT-STYLE:italic"><a class=postlink href="http://www.gamesindustry.biz/" target=_blank>GamesIndustry.biz</a> staff 16:37 13/04/2005</span> <br><br><span style="FONT-SIZE:9pt; FONT-STYLE:italic">British sources confirm previously rumoured details about Microsoft's new console</span> <br><br>British sources have confirmed that the next generation Xbox is indeed called Xbox 360, that the logo is a simple, round nexus, and that the console itself has a concave design - and a platinum white finish.</font></em></span></p>
<p align=left><span style="FONT-SIZE:10pt; FONT-FAMILY:Verdana"><em><font size=1>Many rumours - some of them conflicting - have been circulating in the past months regarding Microsoft's next generation system, but speaking under conditions of strict anonymity, a British source who is privy to Microsoft's marketing documents for the console has now cleared up much of the speculation.</font></em></span></p>
<p align=left><span style="FONT-SIZE:10pt; FONT-FAMILY:Verdana"></span><span style="FONT-SIZE:10pt; FONT-FAMILY:Verdana"><em><font size=1>The name of Xbox's successor is Xbox 360, we can confirm, and the design of the machine is indeed a concave shape. <br><br>The machine can stand upright and hollows in at the middle, like it's "sucking in its cheeks," our source told us today. Previous rumours had put the console as light silver, although fresh information says the final design may be white. It's impossible to tell from the documents, our source explained - although the glimpse of the machine's controllers given on viral marketing website OurColony does suggest a platinum-white colour scheme for the hardware. <br><br>According to the source, the rumoured slot for the hard drive module - which was apparently at the top of the machine - is not obvious on the document, and the person was unable to clarify where the slot for discs is actually located. <br><br>One other aspect we've been unable to confirm is the rumoured customisation features on the box. It's been speculated that the emphasis on customisation which J Allard touted at the Game Developers Conference last month could extend to allowing users to change the "skin" of the console, but this feature was not mentioned by our source. <br><br>As for the logo of the console, it is described as a simple, round nexus with "Xbox 360" written inside it. Previous rumours had simply the numbers "360" in the disc. This branding tends to suggest that we can expect to see "360" appearing after many game names, as happened with the Nintendo 64. <br><br>Microsoft will show the console for the first time in a half-hour special on MTV on May 12, a week ahead of E3. The company's press conference, on Monday, May 16, will be the first viewing of the console to the trade en bloc. <br><br>Xbox 360 is expected to receive a global release in late 2005. <br><br>When contacted today, Microsoft simply said that it does not comment on 