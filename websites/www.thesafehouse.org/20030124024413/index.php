<font face="Tahoma" size="1"><font color="#FFFFFF" SIZE="2" face="Verdana, Arial, Helvetica, sans-serif"><P><FONT face=Verdana size=2>This announcement was released December 18th. You can read the entire press release </FONT><A href="http://www.sigilgames.com/pressRelease-2002-12-18.html" target="_blank"><FONT face=Verdana size=2>here</FONT></A></P><BR>
<BLOCKQUOTE dir=ltr style="MARGIN-RIGHT: 0px"><BR>
<P><FONT face=Verdana size=2><STRONG>"EverQuest" Fan Faire Originator to Help Manage Community For Sigil Games Online and its Future Endeavors</STRONG></FONT></P><BR>
<P><FONT face=Verdana><FONT size=2><STRONG>SAN DIEGO, CA - December 18, 2002</STRONG> - Sigil Games Online, Inc., a developer dedicated to massively multiplayer online games, today announced that Cindy Bowens has joined the company as a Community Manager. A pioneer of the "EverQuest" Fan Faire experience, Bowens has specialized in bringing online gamers together, whether in-game or in real life, since 1999. </FONT></FONT></P><BR>
<P><FONT face=Verdana size=2>Bowens’ responsibilities at Sigil will include development of the company web site as well as other community building activities. </FONT></P><BR>
<P><FONT face=Verdana size=2>"Cindy’s expertise will be invaluable as we work to create the most open, communicative, and attentive community gaming experience ever," said Brad McQuaid, president and co-founder of Sigil Games Online. "We will rely on Cindy’s ability to bring players together, both online and face-to-face." </FONT></P><BR>
<P><FONT face=Verdana size=2>"I look forward to continuing community-building activities with my friends at Sigil," said Bowens. "Bringing fans together has been my passion for many years now, and it’s an honor to work with a team so dedicated to building an unprecedented level of online interaction and communication between both the game player and the game developer." </FONT></P><BR>
<P><FONT face=Verdana size=2>Cindy Bowens joined Verant Interactive in 1999 after independently managing gatherings of "EverQuest" players. These gatherings, called "Fan Faires," became a successful means by which players of the popular game could meet their friends and guildmates in real life. Her interests include building these communities in both traditional and innovative formats, whether through supporting web sites, hosting player gatherings, or employing any one of several new and unique plans she and Sigil have in the works. </FONT></P></BLOCKQUOTE></font></font> 
