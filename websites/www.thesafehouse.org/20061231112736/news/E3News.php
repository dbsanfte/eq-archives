		<span class="article_text">
		From Ars Technica: <a href="http://arstechnica.com/news.ars/post/20060730-7382.html">E3 game trade show not cancelled, but will be downsized</a><br><br><blockquote>Contrary
to reports across the web, E3 has not been cancelled. Next-Gen had
hoped that they would blow the lid off of a hot story by revealing that
the show had been cancelled, but some quick fact checking shows that
they are simply incorrect.<br><br>Sources close to the Entertainment
Software Association (ESA) tell Ars Technica that the show can and will
go on, but that big changes are planned. The "Electronic Entertainment
Expo" (hence E3) started in 1995 as a small but interesting annual
convention for gaming, following roughly six months after the
once-popular annual COMDEX computer trade-show in Las Vegas. The show
has grown immensely in popularity, and that appears to be the problem.<br></blockquote><br>The
article goes on to talk of game companies trying to out do each other
by putting on a better "dog and pony show" and not getting enough
return. It also mentions floor folks not being able to speak freely and
of "the show in a show" system where one needs to get an appointment to
get the real scoop.<br><br>I went to E3 this year and to tell you the
truth, it was less crowded than last year. That doesn't mean it wasn't
crowded though, it was still a mass of industry geeks and there were
still hour long lines for some things, such as the Spore demo. I can also
attest to the existence of the "show in a show" system. I had
appointments with the folks I interviewed and they were held in areas
not accessible to the floor public. However, it has been like that for
a few years now and I don't think the system will go away entirely. It
just makes sense that the CEO of a company is not the one on the floor
doing the demos for the week.<br><br>One thing they did this year was
to have a press only time slot, if the game companies want a chance to
interact with the press more perhaps that time period could be
extended. As far as the expense goes, maybe a cap of some sort might
help. Of course that may fall prey to the old "only bring a $20 gift"
syndrome and companies will still try to push it.<br><br>I don't have
the answer but I do know one thing. E3 has taken on a life of its own.
It's true that information is dispersed more quickly than ever,
especially information geared towards a net savvy crowd like gamers,
but E3 is still a trigger for the industry and the gaming public.
There's still nothing that can compare to it that will get us to sit up
and take notice. A press release here and there is not the same as the
convenience of being able to play and see the games all at once, even
if it is in the cacophony that is E3.<br><br><font class="technoratitag"><font class="technoratitag">Ongoing Discussion Thread:<br><a href="http://thesafehouse.org/forums/showthread.php?t=25410">http://thesafehouse.org/forums/showthread.php?t=25410</a><br><br></font></font><font class="sociallinks">  </font>		</span>		</div>
