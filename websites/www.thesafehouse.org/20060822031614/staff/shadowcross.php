<p><img src="/staff/sx.jpg" width="100" height="100" hspace="15" vspace="15" align="left">
    <span class="bodylinks">The dark shadows of Neriak are the true home of the Teir'dal rogue known as "ShadowCross". He has been traveling the World of Norrath for over 6 years now, and still continues to do so. But the place he can be most often found are neither the streets of Neriak nor the lands of Norrath, but instead it is the place that has become his second home - The Safehouse.</span></p>
<p><span class="bodylinks">Shadowcross stumbled upon the Safehouse by accident, while looking for some information when he was still a low member of the ranks of the rogues of Norrath. He liked what he found and he never left since then, although there were time he was barely seen there at all. About half a year later he was invited to join the crew of the Safehouse and so he became a Moderator. Some time later he was promoted to the job of an Assistant Administrator and about a year after that he became Keeper of the Ring and Ring Administrator - a task he has been doing ever since.
    
</span></p>
<p><span class="bodylinks">Frank, the only German member of the Safehouse Crew, was introduced in both the world of computers and roleplaying games by his father, at the age of 6 and 8 representatively. So it was only a matter of time until he'd combine both and start playing RPGs on PC. He probably played every adventure made by Sierra in the years 1983-1994, basically all AD&D games by SSI and numerous other computer games. At the same time, he also started playing roleplaying games of all kinds, having owned and played at least 50 different systems up to now, his personal favorites being D&D 3rd (3.5) Edition and Shadowrun.
      
</span></p>
<p><span class="bodylinks">He started online gaming in the late '90, but his first real MMORPG was Everquest in October 1999. Although he tried other games like Anarchy Online, Dark Age of Camelot, City of Heroes, Everquest II and World of Warcraft, he still likes EQ the most, and is still playing it actively. Whenever there is some time, he still roams the lands and planes of Antonius Bayle, his home after having moved onwards from Solusek Ro, Ayonae Ro, Antonius Bayle and Kael Drakkal.
        
</span></p>
<p><span class="bodylinks">When not sitting at a PC for fun, Frank is mostly likely reading or working. After finishing his studies of Computer Sciences he is now working on contract for services for BMW. Other "hobbies" include LARPing (Live-Action-Roleplaying), Archery, Reading and of course spending time at a PC.
          
</span></p>
