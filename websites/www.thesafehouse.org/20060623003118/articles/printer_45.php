	 <div class="article_title_prnt">The Vanguard Necromancer</div>
     <div class="article_auth_prnt">By Nenjin Darkeyes</div>
     <div class="article_details_prnt">Mar  1, 2006, 16:59</div>
	 <br><br>
     <p><font size="2"><span style="font-family:Verdana">During the Fanguard
dinner in San Diego, the Safehouse had the good fortune to be joined by
Darrin McPherson, aka Talisker, the man</span></font><img style="margin-top:10px; margin-bottom:10px; margin-left:10px" alt="" src="http://nenjin.thesafehouse.org/undead1.JPG" align="right" border="0"><font size="2"><span style="font-family:Verdana"> behind designing Vanguard's
classes. On a whim, I asked him “So how about the Necromancer?”, and
got far more than I bargained for. It's one thing to have a dev write a
reply to an interview saying “I'm excited about [suchandsuch]”. It's a
different story when you see their eyes light up, and a smile creep
across their features involuntarily, as they talk about it.<br>
<br>
The Vanguard
Necromancer will offer players a unique style of play that will truly
set them apart from their fellow casters of Telon.</span></font><font size="2"><span style="font-family:Verdana"> Traditionally
Necromancers have been “wizards” essentially, that have a dark flavor of
magic. They often drain life, summon dead things from no where, cast
spells like "Dark Painful Wanna-bee Fireball" and make their foes
miserable by making them decay or suffer horrible disadvantages. Darrin
wanted to take it a step further than most other MMO games have. The
Vanguard Necromancer not only casts magic of a dark nature, their magic
is death magic.</span><br style="font-family:Verdana"><br style="font-family:Verdana"></font>
<div style="text-align:center"><font size="2"><span style="font-family:Verdana; font-weight:bold; font-style:italic">When it works, run with it</span></font><br style="font-family:Verdana">
</div>
<font size="2"><span style="font-family:Verdana">The
Vanguard Necromancer is not wholly unique. The style of the</span></font><font size="2"><span style="font-family:Verdana"> class'
spells and some of the things they can do have been done before in
gaming. For those of you that played Diablo 2, the Vanguard Necromancer
will be very familiar in some ways. Darrin freely admitted that his
inspiration had come from Diablo 2 when he thought about the class,
because he loved the style of their abilities and how the class really
nailed the essence of what a Necromancer does.<br>
</span></font>
<div style="text-align:center"><br>
</div>
<div style="text-align:center"><br style="font-family:Verdana">
</div>
<div style="text-align:center"><font size="2"><span style="font-family:Verdana; font-weight:bold; font-style:italic">Master of Death</span></font><br style="font-family:Verdana">
</div>
<font size="2"><span style="font-family:Verdana">The
Necromancer's magics focus on the manipulation and control of </span></font><font size="2"><img style="margin:10px 10px 10px 0pt" alt="" src="http://nenjin.thesafehouse.org/monster1.JPG" align="left" border="0"></font><font size="2"><span style="font-family:Verdana">death,
the utilization of death to further their own ends. In Vanguard, the
presence of corpses will allow the Necromancer to cast spells using
them. In a salute to Diablo 2, the Necromancer can, for </span></font><font size="2"><span style="font-family:Verdana">example, cause
a near by corpse to EXPLODE in a destructive hail of concussive power
and bone shrapnel. (Corpse Explosion was one of my favorite spells in
Diablo 2, and so too with Darrin). Or the Necromancer might cause of
sickening cloud of gas to vent from the corpse, that remains over it
during the combat. Still further, the Necromancer might cause their
slain foes to rise up to assist them and their group.</span><br style="font-family:Verdana"><br style="font-family:Verdana"></font>
<div style="text-align:center"><font size="2"><span style="font-family:Verdana; font-weight:bold; font-style:italic">A Mockery of Life</span></font><br style="font-family:Verdana">
</div>
<font size="2"><span style="font-family:Verdana">But
perhaps the most interesting thing about the Necromancer is that they
don't just create undead pets from the fallen (although they can).
Necromancers build their pets. The Necromancer can construct a
persistent pet that stays with them through their whole life. But this
is not merely some shambling mound that the Necromancer summons and
then keeps with them. Far from it.<br>
<br>
</span></font>
<div style="text-align:center"><font size="2"><span style="font-family:Verdana"></span></font><br style="font-family:Verdana">
</div>
<div style="text-align:center">
<font size="2"><span style="font-family:Verdana"></span></font></div>
<font size="2"><br style="font-family:Verdana"></font>
<div style="text-align:center"><font size="2"><span style="font-family:Verdana; font-weight:bold; font-style:italic">Bring out your dead....please?</span></font><br style="font-family:Verdana">
</div>
<font size="2"><span style="font-family:Verdana">Most
players think about corpses for what they have on them, not what they
are made of. Not so for the Necromancer. Necromancer pets are built
from the body parts of their fallen enemies. Much like a&nbsp; that
character
equips themselves with gear, so the Necromancer will equip their pet
with new body parts stolen from their enemies, that are then grafted to
the pet's body. For those who have dreamed of the day when they could
own a persistent pet that they could mold and adjust as they played,
the wait is over.</span><br style="font-family:Verdana"><br style="font-family:Verdana"></font>
<div style="text-align:center"><font size="2"><span style="font-family:Verdana; font-weight:bold; font-style:italic">Hey, that's MY severed tentacle!</span></font><br style="font-family:Verdana">
</div>
<font size="2"><span style="font-family:Verdana">So
are we going to see a proliferation of black market body parts in
Telon? Nay. Necromancers alone will have the ability to loot these body
parts. Darrin mentioned something to the effect that there is a</span></font><font size="2"><img style="margin-top:10px; margin-bottom:10px; margin-left:10px" alt="" src="http://nenjin.thesafehouse.org/undead2.JPG" align="right" border="0"></font><font size="2"><span style="font-family:Verdana">
separate looting system for Necromancers, which could be as simple as
only they see the body-part-items, OR, some more complicated </span></font><font size="2"><span style="font-family:Verdana">and
involved way of determining loot when the Necromancer loots the corpse.
I would have liked to talk about this one more, but we were already
gabbing during the official speech and thought it was a good time to
wrap it up.<br>
<br>
</span></font>
<div style="text-align:center"><font size="2"><span style="font-family:Verdana"></span></font><br style="font-family:Verdana">
</div>
<div style="text-align:center">
<font size="2"><span style="font-family:Verdana"></span></font></div>
<font size="2"><br style="font-family:Verdana"></font>
<div style="text-align:center"><font size="2"><span style="font-family:Verdana; font-weight:bold; font-style:italic">I walk in Shadows</span></font><br style="font-family:Verdana">
</div>
<font size="2"><span style="font-family:Verdana">While
this comes second hand from what some of the other fansites reported
from Fanguard, it has been said that Necromancers, when they die, might
transform themselves into Liches until they loot their corpse again.
Whether this has a game play element, or whether it's just for pure
cool factor, I am unable to say for sure.</span><br style="font-family:Verdana"><br style="font-family:Verdana"></font>
<div style="text-align:center"><font size="2"><span style="font-family:Verdana; font-weight:bold; font-style:italic">Sign me up</span></font><br style="font-family:Verdana">
</div>
<font size="2"><span style="font-family:Verdana">Some
things really test your class loyalty. The Necromancer is one of those
things. I've never been one for casters really, but the Necromancer to
me sounds like it's going to be a great class with it's own special
feel and style of play. In some ways it hearkens back to the EQ
Necromancer in terms of their solo capacity, while adding a new twist
that will make Necromancers more than just twitch bots.&nbsp; When I'm
not on my Rogue stealing everything
that isn't nailed down, you can bet you'll find me on my Necromancer,
stealing souls.</span><br style="font-family:Verdana"><br style="font-family:Verdana"><span style="font-family:Verdana">Thanks for reading. <br>
<br>
