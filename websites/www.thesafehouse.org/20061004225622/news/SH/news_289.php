<!--startscanning-->Recently NCSoft announced that starting July 13th, all of its Auto
Assault servers will be merging into one.&nbsp; The North American servers
(Apocalypse, Carnage, Breakdown, and Wastelands) and the European
servers (Desolation, Cataclysme, and <strong style="font-weight:normal">Ödland) will all join into one server, named Nexus.<br><br>NCSoft's announcement can be found <a href="http://www.autoassault.com/news/2006/07/the_nexus_is_co.html">here</a>.&nbsp; Below is a cut-and-paste:<br><br><br></strong><p>For some time now our player base has been clamoring to merge all of the servers and offer one, unified <i>Auto Assault®</i> experience. And now you're going to have it: on July 13, we'll be merging <i>all</i> of the AA servers (US and Europe) into the new combined server, <b>Nexus</b>.</p>
            
               <p><b>The <i>Auto Assault®</i> Server Merge FAQ</b></p>

<p><b>1) What is the server merge and why are you doing it?</b><br>
The <i>Auto Assault®</i>
player base has asked to be able to play on one combined server. We
have concluded that this is a great idea. The merge would increase the
server population greatly and offer the players a much more satisfying
and diverse gameplay experience. <br>
 <br>
<b>2) Will the European servers be merged with the North American servers?</b><br>
Yes. All of the servers will be merged into a single server located in North America.<br>
 <br>
<b>3) I have many characters across the current servers. Will I be able to still use all of them?</b><br>
You can have a maximum total of 12 characters per account on the merged
server. You must first transfer your characters using the new <b>Character Transfer</b> feature before you can use them on the merged server.<br>
 <br>
<b>4) What will be the procedure to transfer these characters?</b><br>
You will be able to transfer characters by logging into your PlayNC™ master account and selecting the <b>Transfer <i>Auto Assault®</i> Characters!</b>
link at the top of the page. All servers and characters tied to those
servers will be listed. From there, you can decide which characters to
transfer. Detailed instructions for how to register your characters
will be available soon.<br>
 <br>
<b>5) What happens if multiple characters have the same character name
across different servers? How will “character name
collision” be resolved?</b><br>
To be fair to all involved, the name-keeping process will be on a
first-come, first-served basis. The only exception involves clan
leaders (see below). If you attempt to transfer a character using a
name that already exists on the merged server, the character name will
be altered slightly and you will receive an e-mail with instructions on
how it can be changed.<br>
 <br>
<b>6) Can a transferred character be deleted afterwards? Does that make the slot available to be used again?</b><br>
Yes, transferred characters can be deleted later. Doing so will open
that character slot to be used for another character transfer.<br>
 <br>
<b>7) How long will I be able to transfer my characters?</b><br>
The option to transfer your characters will be available until the end
of 2006. All characters not transferred by that time will no longer be
accessible. A more specific date will be available in the future. <br>
 <br>
<b>8) Will I lose anything from a character that I transfer?</b><br>
You will lose your character-specific friends and enemies lists, and
you may have to recreate or rejoin your clan (see below). All inventory
items (including locker contents), medals, money, apartment goodies, as
well as your character stats, skills, and abilities will stay with your
character(s).<br>
 <br>
<b>9) How will the server merge affect clans?</b><br>
Clans with a membership of six members or more will automatically be
moved to the new server, as will the leaders of those clans (the clan
leader does not have to manually transfer that character). The rosters
of those clans will also be moved. Any transferred character that is a
member of one of these clans at the time of the server merge will be
automatically rejoined to the transferred clan. <br>
 <br>
<b>10) Will I have to do anything to my client?</b><br>
No. After a period of maintenance, the necessary changes will
automatically be downloaded the next time you patch the game client.</p>

<p><b>11) Will the game still be localized?</b><br>
Yes, you will still be able to play <i>Auto Assault®</i> in French, German, and English. This option is set on the client’s side, not the server.</p>

<p><b>12) Are there going to be in-game chat channels in languages other than English?</b><br>
There will be separate chat channels for the languages currently
supported. When a player first joins the server, they will
automatically join the English-language channel and a separate channel
based on the language chosen when installing the game (English, French,
or German). Players can choose to disable or re-enable these channels
at any time from the Chat Options menu in-game.</p>

<p><b>13) Will there still be a localized website and forum?</b><br>
All the forums will be located at <a href="http://boards.autoassault.com/">http://boards.autoassault.com/</a>,
and there will be separate sections for French- and German-speaking
players. The European forum will be taken offline and unfortunately,
all European players will need to create new accounts in the unified
forum. Instructions for how to create a forum account can be found
here: <a href="http://support.plaync.com/cgi-bin/plaync.cfg/php/enduser/std_adp.php?p_faqid=4496">How do I create an <i>Auto Assault®</i> forum account?</a></p>

<p><b>14) Can I still pay for my account in Euros or Pounds?</b><br>
There will be no changes to billing; payments will still be made in the currency your account was originally billed.<br>
