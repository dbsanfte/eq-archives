<br>
<span style="font-size:16px"><span style="font-weight:bold">Final Thoughts:</span></span>
<br>

<br>
When it was all said and done, I had a good time playing it, and I got
to enjoy the World of Darkness at the same time. I think that's a good
thing. As an RPG, this game definetly warrants your attention, if dark,
gritty urban environments, a rich world of myths, legends, and dark
truths, and pasty guys with pointy teeth is your bag. With the lack of
really good RPGs out right now, this game was a refreshing change of
pace from purely action games that have released lately. As an action
game, you can safely pass this one up, but for me the action only
supplemented the already well developed and interesting RPG aspect. The
game's only real weakness is in it's performance, and if Troika chooses
to get that fixed up, they have a great title here. <br>

<br>

<br>
<span style="font-size:16px"><span style="font-weight:bold">BY THE NUMBERS(out of 10)</span></span>
<br>

<br>
<span style="font-weight:bold">Theme: 9/10</span>
<br>
Nearly a total win with me. Great atmosphere, great setting,
interesting plot, very memorable characters. Loses a point for the poor
cut scenes, and the hurried ending(s).
<br>

<br>
<span style="font-weight:bold">Sound: 9/10</span>
<br>The voice acting is just excellent, I can't stress that enough. The
music was pretty good, if you enjoy dark throbbing beats and Gothic
choirs, although I didn't really enjoy any of the licensed music they
got for the game(even the Ministry song was kinda eh...). Loses a point
because they didn't license any Nine Inch Nails for the game, and
because I think their theme composer ripped off Massive Attack with one
of their main compositions. <br>

<br>
<span style="font-weight:bold">Game play: 9/10</span>
<br>
A very satisfying RPG experience. Really captures the “story telling”
aspect of the White Wolf property. Points off for mediocre meele
combat, a little redundant questing, and for FPS combat that is more
than a little rough. <br>

<br>
<span style="font-weight:bold">Graphics: 6.5/10</span>
<br>
They fit the theme, but aren't overly impressive. For all the hype
about the Half Life 2 engine, I didn't find much to be impressed about
aside from the faces of the models and some of the special effects. The
graphics are adequate. Again, this is also because I had to run the
game at the lowest resolution, with low shadowing, lowered texture
detail and a few other turned down settings.
<br>

<br>
<span style="font-weight:bold">Performance: 4/10</span>
<br>It's not unplayable...but it's damn close. Were I a total slavering
fan boi, I'd probably be writing Troika some hate mail about their
level of quality assurance. If Troika at least tries to patch the game
to address some of these problems, I'll gladly give them a much better
score in this area. But as it stands, the performance is more than
disappointing out of the box. It verges on appalling for me.
<br>

<br>
<span style="font-weight:bold">Total: 7.5/10</span>
<br>

<br>
<span style="font-weight:bold">RAGEBRINGERS: 3.75 daggers out of 5.</span> 
<br>

<br>

<br>For all it's foibles, and all my bitching, the game is still a
great RPG, and worth playing. It's a rich adventure into the gritty,
secretive, and deadly World of Darkness, and I've yet to introduce
someone to the World of Darkness that didn't walk away interested. Try
it out, it's fun to play in the dark...come, I want to show you
something special...
<br>

