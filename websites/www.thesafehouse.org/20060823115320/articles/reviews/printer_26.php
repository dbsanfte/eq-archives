<tbody><tr><td class="row1"><span class="postbody"><span style="font-weight:bold">Title:<a href="http://www.vampirebloodlines.com/" target="_blank" class="postlink">Vampire: The Masquerade - Bloodlines</a>
<br>
Developer: Troika
<br>
Publisher: Activision 
<br>
Platform: PC
<br>
Genre: RPG/1st person shooter
<br>
Release Date: November 16, 2004 
<br>
ESRB: Mature</span>
<br>

<br>

<br>

<br>
With Half Life 2 releasing, the first RPG based on it's engine almost
slipped past people's radars. But for me, it's hard to miss an RPG
based on the dark, Gothic modern world of White Wolf's Vampire: The
Masquerade pen and paper game. While this isn't the first attempt at
creating a video game based on the White Wolf property, it is arguably
one of the best attempts to date. This is a great RPG, I'll say that at
the outset, but the game is plagued by some significant(but possible
solvable) problems that damped my overall opinion of it.
<br>

<br>
<img src="http://nenjin.thesafehouse.org/santamon1.jpg" border="0">
<br>

<br>For those who aren't familiar with White Wolf's world of Darkness,
I'll give you a brief overview. Set in the modern day 20th century,
mankind is blissfully unaware of the things that lurk in the night time
shadows. Vampires have existed since the dawn of time, and have
carefully concealed their existence from most of the human race with
The Masquerade. A formal code of behavior where vampires do their
utmost to not reveal their presence to the human world, while
co-existing with it. Vampires have created their own formal sort of
government called the Camirilla. The Camirilla is composed of all the
vampire clans(specific bloodlines passed down over the ages), headed by
the most powerful vampire in the city or region, who is called the
Prince(Prince of Los Angeles, Prince of New York, ect..). It is the job
of the Prince to enforce the laws of the Masquerade, and adjudicate
disputes between clans in the city. There are various sects of Vampires
outside the Camirilla who refuse it's laws, like the rebel Anarchs, or
the savage and “evil” vampires called the Sabbat(who are some truly
twisted individuals). Throw into this mix werewolves who stalk the
rural countryside, ghosts that inhabit old buildings, demons that roam
in man's flesh, and other sorted dark figures of mythology, and you
have the beginnings of the World of Darkness.
<br>

<br>
<span style="font-weight:bold"><span style="font-size:16px">OVERVIEW:</span></span>
<br>

<br>VTMB(Vampire: The Masquerade-Bloodlines) is a first person
shooter/action game at heart. But it is combined with RPG style
character development, damage systems, and story progression, so that
it is a solid 50/50 mix of both. The character development is taken
almost entirely from the pen and paper Vampire game, where all your
game actions are called feats. This is stuff like Sneaking, Lock
Picking, Meele Combat, Seduction, Intimidation, ect... These feats are
a combination of 1 or more things. Attributes like Strength,
Intelligence, or Appearance. Skills, like Firearms, Security, Stealth.
Talents, like Dodge, Brawl, Intimidate. Or Knowledges, like Finance,
Computers, or Scholarship. In addition to allocating points to these
areas though, at character creation, you choose the vampire clan that
the blood in your veins belongs to. This choice has a serious impact on
your game play. Not only does it define what kinds of vampire powers
you will have access to throughout the game, but it will impact the way
some NPCs deal with you, and some of the “quests” you will be able to
do. All this provides a very satisfying character building system where
you get to customize yourself to the kind of play you enjoy, whether it
be the smooth talking, seductive vampire, or the dominating corporate
mastermind, or the hand to hand thing of people's nightmares.
<br>

<br>

<br>After completing character creation, you are introduced to the
story. You were a hapless mortal, when one night, you were “Embraced”
by a Vampire. Under the law of the Camirilla, no one can be Embraced
without it's permission. Your Sire didn't have that kind of permission,
and the Prince of Los Angeles orders their death. However, he spares
you, which in turn in debts you to him. Then you get kicked out the
door and into the tutorial.
<br>

<br>
<img src="http://nenjin.thesafehouse.org/lecroix1.jpg" border="0"><img src="http://nenjin.thesafehouse.org/smilejack1.jpg" border="0">
<br>

<br>VTMB has a great tutorial, that gets you started playing the game,
and learning it's lore right away. As you move through the tutorial, an
attack by a rival vampire faction is going on. The tutorial guide, a
vampire named Smiling Jack, talks you through each section in a very
low key and story like way, showing you how to stealth, do meele
combat, interact with objects, ect.. After the tutorial ends, the game
boots you out on your own. <br>
 
<br>
The game world is divided up into 4 hubs, along with a few areas
outside the hubs that you get to visit on a “quest mission” basis.
Within the hubs, there are pedestrians that roam the streets and
alleys, just waiting to be your next meal. Your “blood pool” represents
your need to consume blood. Your vampiric abilities drain your blood
pool, so as you use your abilities, you need to feed. Basically,
staying fed isn't much of an issue except when there only supernaturals
around you(you can't drain other vampires). This becomes a problem
because, when you run out of blood, you will “Frenzy” at the first
sight of a potential meal, and lose control of your actions for a short
while. This can lead to a Masquerade Violation. While you are inside
the hubs, you are “under the Masquerade” where you can't do anything
around the mortals that might tip them off to Vampires or Vampire
society. So if you get seen feeding on someone, or you use an obvious
vampiric power, you'll incur a Masquerade Violation. Once you've
committed 5 of these, the game ends, as the humans of LA have finally
noticed something weird is going on. Don't worry though, throughout
game play you have opportunities get a “Masquerade Redemption”, which
restore Masquerade points. Usually by doing something to ensure the
Masquerade is upheld redeems you points. On top of all that, the usual
things like Burglary, Murder, and Assault will cause the police on the
street to come after you, and you'll have to run and hide til they give
up.
<br>

<br>
<img src="http://nenjin.thesafehouse.org/feeding1.jpg" border="0">
<br>

<br>
<span style="font-size:16px"><span style="font-weight:bold">THEME:</span></span>
<br>

<br>I'm one of those closet Vampire fans. I never could get myself to
the table to play a full game of Vampire(because it understandably can
draw a strange crowd), but I have always loved the game system, and
especially the setting and themes of the World of Darkness. VTMB is, in
my opinion, utterly faithful to the vision and theme of Vampire: The
Masquerade. From the twisting, treacherous plot line, to the dark,
Gothic, haunted feel of the game, to the dirty, gritty alleys and rain
soaked streets of the slums, VTMB leaves you feeling as if you've
stepped into a world far far darker than our own, where sinister plots
are on the minds of every person you meet, and fiendish shadows stalk
the foul, trash littered alleys. The game also has a roguish, and
contemporary humor to it(couple slams on Republicans and Bush, anime
tentacle jokes) as well as some geniunely funny characters, like the
two Asian grandpas in China Town that want you to rub the other out. As
a fan, I couldn't be happier with how the game and the storyline come
across with the true essence of the World of Darkness. <br>

<br>
<img src="http://nenjin.thesafehouse.org/club1.jpg" border="0">
<br>

<br>
The game does a great job of relaying the rich lore behind the World of
Darkness to the player, both through NPC dialogs, and the many, many
loading screens you end up sitting through that have lore for you to
read. The NPCs keep their descriptions of the lore short and sweet,
allowing the player to ask more questions if their interested, or just
cut to the chase instead. I was pretty impressed, as there are TOMES of
books on White Wolf's World of Darkness, and it would be easy to over
power someone with that kind of information. But Troika keeps it
moderate while still informing you in a satisfactory manner. <br>

<br>

<br>
<img src="http://nenjin.thesafehouse.org/vamp2.jpg" border="0">
<br>

<br>
The storyline, and the NPCs themselves deserve special mention. No
matter who you are talking to, every NPC is “in character”. There are
no half-assed generic voice actors reading boring scripts. NPCs play
the part in VTMB. And if you're talking to vampires, that means you
have to learn to read between the lines, catch the subtle intonations
and attitudes, and react with caution. The wrong word could make you an
enemy for life, the right word could open up a new path that wasn't
previously available. Everyone you talk to has an agenda, and nothing
anyone says is worth taking at face value. It's hard for me to describe
how faithful this is to the Vampire pen and paper game. The pen and
paper game is all about politicking, and subtly, double meanings and
secret agendas. Troika really did an excellent job in taking the time
to really work on NPC dialogs. Too many games take NPC conversations
for granted, focusing instead on the “action” parts of the RPG, so this
was a real treat for me, as I haven't had a good RPG to “sink my teeth
into” for awhile. The story I enjoyed for the most part. It too was
faithful to the Vampire PnP game. A bunch of vampires, struggling for
power, using you as their pawn. Plots within plots, trust and betrayal,
evil and eviler. My only gripe about the story is the ending I got. It
was very quick, and didn't fill out any questions it may have created,
nor did it really show what happened after the end. There are of
course, multiple endings which you can receive, based on how you played
the game and who you sided with, so perhaps there are better endings
available. <br>

<br>
<span style="font-size:16px"><span style="font-weight:bold">SOUND:</span></span> 
<br>

<br>
Like the theme and storyline of the game, the sound is no less
meticulously crafted and managed to add to the atmosphere of VTMB.
First off, this game has some of the best voice acting I've ever heard
in a video game. As mentioned above, the voice actors really play the
part, and they've obviously rehearsed their parts, because the voice
acting is just flawless. There aren't any “crappy” voice actors,
everyone oozes personality, from the rough and tough grizzled vampire,
to the imperial and haughty Prince of LA, to the seductive and smooth
Velvet Veloure. It just helps to add to the already impressive
atmosphere of VTMB, and for me, has restored some of my faith that
voice acting is worth the time to put into games.
<br>

<br>The music score is straight out of the world of Darkness too. Real
artist tracks, which you will hear playing in the bars mostly, are
either “goth rock” bands, heavy metal bands, or industrial heavy
metal(Ministry has a track in here for those of you who are fans).
While it may not be your cup of tea, you're not beat to death with
heavy guitar accompanied by some depressed white guy wailing into the
mic. The majority of the music you here in game is composed for the
game itself, and is a mostly dark techno/dark Gothic mix. The game has
a lot of ambient sound effects to along with it's dark and sometimes
spooky tracks. For the music, I was mostly happy, it again fit the
theme of the World of Darkness dead on. The sound effects, in my
opinion, where pretty well done, from the sirens in the distance to the
report of your shotgun, to the whispers and wails in the dead corridors
of the Haunted Hotel. Little things like sound changing in pitch when
you move with vampiric speed was pretty cool too.
<br>

<br>

<br>
<span style="font-size:16px"><span style="font-weight:bold">GAMEPLAY:</span></span>
<br>

<br>I'm going to try and divide out the game play from the performance
problems, as a lot of my complaints in this game with game play stem
from it's exceedingly poor performance. <br>

<br>
This game plays sorta like Morrowind, another 1st person RPG, except
with much smaller space, and much richer NPC interaction. The Half Life
2 engine makes it possible to sort through junk and look in containers
for stuff like in typical RPG egg hunt fashion. It also makes it
possible to do things like sneak around in the dark(which I will refer
to as skulking from here on out, because vampires don't sneak, they
skulk dammit!), and assassinate your enemies, or get a quick drink. For
you Safehouse Rogues out there, there are ample opportunities to play
the Rogue in this game, and a vast majority of the game can be played
from a stealth play style. <br>

<br>
The action/1st person shooter aspect of the game is combined with the
RPG aspect . Pure twitch skills alone are not enough in VTMB. Your
targeting reticle with guns, at low skill, is huge and takes a long
time to “get smaller”, increasing your aim. This means that even though
your twitch skills are top notch, you will find it very, very difficult
to hit anything without increasing your character's ability with
firearms. The gun play will never feel “just like” a dedicated FPS, as
it will always be moderated some what by your skills. This may sour
some of you FPS conessuers out there, but for me, once I put enough
points into my firearms skills, I got comfortable enough with using
them I didn't feel I had much of a handicap. Unfortunately, due to
performance problems, when the game is lagging out hitting stuff with a
gun is even harder to do. The hit detection and object interference are
all a little sloppy in this game as well. Because it's an RPG, head
shots aren't instant kills, ever. In fact most non-sheep-humans will
take multiple shots to kill with a massive shotgun. Suffice to say, if
you only play ultra-refined and perfected FPS games, this game probably
is going to make you go insane. <br>

<br>

<br>
<img src="http://nenjin.thesafehouse.org/vamp3.jpg" border="0">
<br>

<br>
The 3rd person combat isn't especially notable either. You get a few
different attack combos based on the direction you are moving, but the
targeting is extremely sloppy, and meele combat more often than not
turns into a slug fest where you smack them a few times, they smack you
a few times, rinse, repeat, and on. Blocking doesn't really do
anything, so suffice to say there isn't much finesse in meele combat.
Just raw clicking power. The rag doll and motion physics do make meele
combat sorta fun, especially when everything moves in slow motion with
the vampire power Celerity(think matrix slow time. It's even got the
bullet tracers, which is absolute murder on your system when 10 guys
are shooting at you in slow motion), so that guys go sailing through
the air and I can run ahead of them and smack them back the other
direction, playing human ping pong. <br>

<br>

<br>
<img src="http://nenjin.thesafehouse.org/fight1.jpg" border="0">
<br>

<br>
All in all, the combat is nothing special in VTMB, but for me, it was
enough to satisfy. You kick ass, take names, and the visuals are pretty
good. The controls may suck a little, and the combat may feel really
“sloppy”, but I never got bored doing during the action. The boss
fights, while not especially hard, are still cool and interesting for
the most part(my favorite is the second to last boss, squid anyone?).
The AI is again, soso. It has t it's good moments, and it has it's bad
ones. As a blend of FPS and RPG, I think it did pretty well, excluding
performance. But compared to the mechanics and play of Half Life 2,
this game was obviously developed by RPG developers, not 1st person
shooter developers, despite what the media on Troika's web page might
say. <br>

<br>
The RPG aspects of the game are really where the meat and fun of the
game is. While not all dialog responses have direct ramifications in
game, many of them do. A good portion of responses are indicators of
your own perceptions and attitudes. If you're a blood thirsty monster,
you're dialog choices get limited. If you're full of humanity, you
might even have a couple kind hearted responses. And when it comes to
dialog with real consequences, you get to pick from among a bunch of
choices, most of which will change the NPC's reaction in someway. More
than a few times I managed to avoid a fight by artfully convincing my
enemy it wasn't nessecary. There are a multitude of quests in the main
storyline, and a good deal of sub-quests scattered throughout the game.
And which quests you get depends on how you treat other NPCs, how well
you preform their missions, and what kind of alliances you've made in
game. While not perfect, VTMB really adapts to the things you do and
say, more so than a lot of other “RPGs” that have come out lately. It
was really cool to see the NPCs react to the stuff I was doing, and say
things like “You know there are a bunch of the Kindred out there that
are wondering which way you are going to go”. I played both sides in
the game, always trying to make everyone happy, pretty much right up
until the end. Curious to know how it will play if you dedicate to one
side immediately. As an RPG, this is a great game, from character
development, to the storyline, to the interaction, all of it as an RPG
is highly satisfying. <br>

<br>
<span style="font-size:16px"><span style="font-weight:bold">GRAPHICS:</span></span>
<br>

<br>
I'll say it from the outset. I have not played Half Life 2. I haven't
seen Half Life 2. I know nothing about Half Life 2, because I choose
not to. So, what are my gut impressions of the graphics and engine that
use Half Life 2's technology?
<br>

<br>All in all, I can say that I was not impressed by the job Troika
did. Graphically speaking, the textures are soso, the lighting isn't
anything to write home about, the weather effects, what little there
are, aren't stellar. Basically, I wasn't overly impressed with the
environments I was presented. Sure they were dead on theme, but the
designs weren't very intricate, the textures weren't all that complex.
Ok yes. Great, no. <br>
The facial modeling however, I thought was really good. The mouths of
the NPCs actually move in time to what they are saying, which takes a
little getting used to, but really brings you into the theme and the
character's personality. The facial expressions are great, you can
really see and describe what emotion the NPCs are displaying, it's that
noticeable, but in a realistic way. The eyes especially are well done.
Make sure to look for what direction the NPC's look when they tell you
something. You can always tell a liar by the one that looks down and to
the right. The character modeling, when the game isn't lagging out,
looks very fluid and natural, at least the NPCs do. You're own 3rd
person movements always look akward and unnatural when running. <br>

<br>
The special effects in the game are a mixed bag. Some effects, like
slow time, are really well done. Other passive effects like Vampire
Presence has these gaudy, pixelated halos that appear every where, that
don't look particularly cool or vampiric. <br>

<br>
The UI is minimalist, and while it looks alright, a lot of the text and
menus in game look like they were designed 10 years ago. <br>

<br>
The cut scenes really clinched it for me though. Rendered in the game
engine itself, the cut scenes are jerky, rough, and generally not worth
watching at all. I'm surprised by this. It's like the engine was never
meant to be used for cut scenes, because the cut scenes actually manage
to look worse than the game itself while playing. The animations start
looking funky, the shadowing and texturing cut out. I was surprised,
because cut scenes are where most games manage to show their polish.
But the cut scenes in this game are, even with out the performance
problems, terrible. And I'm at a loss to say why. <br>

<br>
VTMB also, in my opinion, doesn't offer you very many graphic options
to tinker with in game. Though your options vary depending on video
card, I was offered 3 resolution settings, 800x600, 1024x768, and
1600x1200. Particle effects, blood splatters, and special effects were
lumped into “combat” effects, which could be toggled on and off.
Environmental Bump Mapping was on or off. Shadows and “Terrain Detail”
did have sliders though. Maybe I'm just spoiled by MMO graphic options,
or it's the fact nothing I did would make this game run much better,
either way I thought the game locked you into a specific range of
performance. Which doesn't say good things about the capabilities of
the engine. <br>

<br>
<span style="font-size:16px"><span style="font-weight:bold">PREFORMANCE:</span></span> 
<br>

<br>
My PC Stats:
<br>
AMD Athlon XP 2600+
<br>
512 PC3200 RAM
<br>
GeForceFX5900XT 128 meg video card
<br>
Audigy 2 Sound Card
<br>

<br>
Here is where I start my griping in earnest. 
<br>

<br>
This game runs like ass. Why it runs so bad, is anyone's guess. Is it
the Half Life Engine itself? Is it Troika's lack of experience coding a
FPS? Who knows. What I know is I exceeded the minimum requirements of
the game by a good margin, and I am plagued by bouts of choppiness, bad
frame rates, excessive load times, and on occasion, crashing, probably
due to lack of memory. At 1024x768, the game was unplayable for me. At
800x600, it's playable, but game halting lag is not uncommon. Let me
say, these problems didn't stop me from playing 35+ hours already. But
it sure as hell was annoying me throughout the game. It seems to be
cyclical in nature. The game will choke up periodically for minutes at
a time, then run smooth for a half hour. Then choke up for again, then
run smooth again. When it's running smooth, the game looks pretty good,
it handles well, it sounds good...but when the slow down starts, even
on a small scale, it effects every part of the game. It gets harder to
hit things accurately, 3rd person combat becomes jerky and slow, the
music and sound effects run over each other. But worst of all, because
the game is basically zone based, you spend a lot of time staring at
load screens when the lag is on. It can make doing quests and going to
talk to NPCs very time consuming, and consequently no fun. The game
seems to lag the worst in the hub areas, where there are lots of NPCs
to keep track of. Mission areas, which are usually much smaller and
contain much less NPCs, seem to run fine. All this had me asking
myself, for a game that seems to be doing so little graphically, why is
it SUCKING THE MEMORY FROM MY COMPUTER LIKE A TECHNO-VAMPIRE?!?!? <br>

<br>
The sound played into a lot of technical problems I was having with
this game. From the very first instance I started the game, I got
choppy sound accompanying some choppy graphics. When the game chokes,
the sound chokes. Sometimes, the quality of the sound would drastically
alter, so that NPCs you could hear clearly at one point would start
echoing loudly, and then get quiet and difficult to hear. It didn't
happen often, but the sound glitches and hang ups(especially when the
game locks in mid action and you get a sound repeating over and over
again) started to grate on me after a while.
<br>

<br>Lastly, there are the ugly bits. Texture popping occurs now and
again. The physics engine also goes to pot when the lag really hits,
causing collisions and getting objects and players and NPCs stuck in
walls or inside objects. When going into menus, game music is playing
over the menu music. Typos and grammatical errors are not uncommon
either(not that I'm one to complain about that).
<br>

<br>There is more than a “roughness” to the quality of this game.
Frankly, there are parts of it, particularly performance, that should
have been addressed before it was boxed. There are even a few bugs in
the quests. It's a shame that the game was pushed back once, and it
still runs this bad. It seems like for all the extra development time,
this game still finished in a rush. Hopefully, Valve and Troika will
get their act together and get some patches out, because the
performance of VTMB, and it's many bugs and problems, is definetly a
black mark against it. Were it not for these problems, I'd tell every
RPG player I know they have to play this game. But it's lack of solid,
lag free game play, and the subsequent problems it creates in playing
the game, makes me stop short. If I wasn't as a big a fan of Vampire:
The Masquerade as I am, I might have put the game down out of
frustration with the performance already.
<br>

<br>
<span style="font-size:16px"><span style="font-weight:bold">ESRB RATING:</span></span>
<br>

<br>It seems I'm mentioning this thing more and more these days. This
game comes with a 17 years old+ rating. And it falls just short of
requiring you to be 18, believe me. Strippers, foul language,
evisceration, decapitation, nipple tassels, thongs, cleavage, more foul
language, more strippers, more entrails....It's not a game for the
faint of heart. If you've got issues with the objectification of women,
you might want to avoid this one as well. I don't think any female
model in the game sports anything less than a C cup. The game has some
gorier moments, and environments, that don't make it suitable for the
younger children, or those with sensitive values. It'd definitely not a
game to let your 12 year old play, or see you/hear you playing for that
matter. <br>

