 <br>
Justin was introduced to gaming at a very young age, starting with BASIC programs on his father's old IBM home built PCs. At age 9, he layed his greedy little hands on his first Nintendo Entertainment System, and declared that sunlight was lame and he had no need for it. At age 10, Justin was introduced to his first game of D&amp;D, where he found his true calling in life as a master of sneaking and skull duggery. You will be hard pressed to find another so dedicated to the life of the shadows. His love of pen and paper RPGs eventually led him to table top miniature gaming, collectable card games, and more. Building on the slight knowledge he gained about computers from his child hood, <br>
<br>
Justin ventured into the world of PC gaming around age 14, and after hundreds upon thousands of games of Worms and DOOM2, he was introduced by a family member to Everquest. He promptly disappeared off the face of the earth for a while, resurfacing only occasionally to get some food or to play the occasional game of hackey-sack. After 2 glorious, rogue filled years he left EQ, and has since bounced around various games in the MMO market looking for the next greatest fix. <br>
<br>
Currently, Justin attends the University of Nebraska at Lincoln, where he's majoring in Print Journalism and Mass Communications, with aspirations to one day write a column or work a beat for a prestigous publication. If that fails, he will always settle for staying current with the gaming industry, and reporting it's happenings to the public. He's also trying his hand right now at writing a science fiction novel for publication. <br>
<br>
Justin is an active participant in many gaming communities, including but not limited to: <br>
<br>
Vanguard Fan Community <br>
Games Workshop Community <br>
The Black Library Forums(subsidiary of Games Workshop) <br>
Relic Dawn of War Community (gaming partner with Games Workshop) <br>
Halo 2 Online Community <br>
<br>
When not at school or out looking for a real job, Justin can be found lurking amongst many forums on the internet, battling it out online with friends in Halo 2, or trying to push his creativity to the next level in one of his many on going pet projects. <br>
