<p><font face=Georgia color=#800000 size=2>You're late, but that's okay. My men have been observing you since you entered the neighborhood. A place like this isn't easy to find in the best of conditions, let alone on a dark night and with intentionally misleading directions. You're here though, so you've past the first test and shown you have at least a modicum of intelligence rolling around between your ears. Whether or not it's enough to earn you a shot at the prize is another question entirely, but one we'll soon find out.</font></p>
<p><font face=Georgia color=#800000 size=2>We both have something in common; we have needs. I have a compelling need to aquire things for my own various reasons, and have made a very good living doing so. You have at least one need, or else you wouldn't be here now, would you? Before I go any further, yes, I can confirm I have come into possesion of that which you desire. But currently my contact can only service me with one such item, and with it being such a valuable commodity I have many people interested in it.</font></p>
<p><font face=Georgia color=#800000 size=2>I see the look in your eyes, and don't even bother bringing up any amount of gold or jewels. I don't want it. Despite what some people think, a rogue is not ruled by his coin pouch. Thieves are ruled by their coin pouch, and I am no thief. A rogue's greatest asset is knowledge. With the right information, riches will come readily like water from a fountain and will be all the more sweeter than a backroom deal done in the name of greed.</font></p>
<p><font face=Georgia color=#800000 size=2>A rogue must be methodical. A rogue mustn't be hasty and mustn't act before enough information is gathered, and information is why I've called you here today. In my old age my memory is failing, but I have learned to use the eagerness of youth to my advantage. I have become a collector of sorts - a collector of information. I have three questions that need to be answered, and in return I will give you a rare and special opportunity.</font></p>
<p><font face=Georgia color=#800000 size=2>There is a man named Morgan, of whom a tale of adventure is told. The problem is that I know of several people named 'Morgan', and it would be rather embarassing to link the wrong Morgan with his tale. That is why my first question to you is 'In Morgan's Tale, what is Morgan's last name?'</font></p>
<p><font face=Georgia color=#800000 size=2>Easy you say? Then I have found the right person. My second question is a bit more obscure, as it was something I overheard at a distance. There is a valuable object I intend to 'aquire' in the near future, but I have learned that a beast guards this treasure. Without knowing which beast, I'm finding it difficult to prepare myself for any eventuality. My second question to you is 'Which beast has "a very strong bite, capable of snapping bone but they often attack with their sharp clawed feet as well"?' Those were the words he used exactly, so I expect there is a beast roaming Telon which fits that description to the letter. I need to know the beast's name.</font></p>
<p><font face=Georgia color=#800000 size=2>My final question is a more trivial matter, but important to me none-the-less. There is a famous quote which a friend of mine insists were the words of Barret Barleygrove, the Champion of Willowroot. I know this is not the case, and I bet him a rather substantial amount of money that I would soon be able to tell him the true orator of the quote. While I cannot remember his name, in my gut I know they were the words of royalty. So my final question to you is 'Who is credited with saying "Evil chooses to hide but the light of righteousness shall ever find it."?'</font></p>
<p><font face=Georgia color=#800000 size=2>Now be off and don't bother returning until you have all three answers for me. If your information proves to be accurate then you'll have your shot at a very valuable prize. Unfortunately I have sent many people on this errand, and I wager many will come back with the correct answers. I'm not an unfair rogue, so for you I'll ask one more question. One question I already know, and you may or may not. But should you get this question correct then your odds at winning will increase, twofold if you answered my first three questions correctly (and by an infinite amount should you have answered any of my first three questions incorrectly). My question is regarding heritige. Regarding respect. Regarding the acknowledgement of those who have come before to create all that we have today. My question is simple. Who is the founder of The Safehouse?</font></p>
