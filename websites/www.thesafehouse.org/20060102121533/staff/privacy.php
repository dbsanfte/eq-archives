<p><B><A NAME="1"></A><span class="bodytitle">Our Commitment To Privacy: </span></B><BR>
Your privacy is important to us. To better protect your privacy we provide this notice explaining our online information practices and the choices you can make about the way your information is collected and used. To make this notice easy to find, we make it available on our homepage and at every point where personally identifiable information may be requested. </p>

<P><B><A NAME="2"></A><span class="bodytitle">The Information We Collect:</span> </B></P>
<P>This notice applies to all information collected or submitted on the thesafehouse.org website. On some pages, you can order products, make requests, and register to receive materials. The types of personal information collected at these pages are: </P>
<P>Name <BR>
Address <BR>
Email address <BR>
Phone number <BR>
Credit/Debit Card Information <BR>
<BR>
On some pages, you can submit information about other people. For example, if you order a gift online and want it sent directly to the recipient, you will need to submit the recipient's address. In this circumstance, the types of personal information collected are: </P>
<P>Name <BR>
Address <BR>
Phone Number <BR>
<B><BR>
</B><B><A NAME="3"></A><span class="bodytitle">The Way We Use Information:</span> </B></P>
<P>We use the information you provide about yourself when placing an order only to complete that order. We do not share this information with outside parties except to the extent necessary to complete that order. </P>
<P>We use the information you provide about someone else when placing an order only to ship the product and to confirm delivery. We do not share this information with outside parties except to the extent necessary to complete that order. </P>
<P>We offer gift-cards by which you can personalize a product you order for another person. Information you provide to us to create a gift-card is only used for that purpose, and it is only disclosed to the person receiving the gift. </P>
<P>We use return email addresses to answer the email we receive. Such addresses are not used for any other purpose and are not shared with outside parties. </P>
<P>You can register with our website if you would like to view services unavailable to non registered members. Information you submit on our website will not be used for this purpose unless you fill out the registration form. </P>
<P>We use non-identifying and aggregate information to better design our website and to share with advertisers. For example, we may tell an advertiser that X number of individuals visited a certain area on our website, or that Y number of men and Z number of women filled out our registration form, but we would not disclose anything that could be used to identify those individuals. </P>
<P>Finally, we never use or share the personally identifiable information provided to us online in ways unrelated to the ones described above without also providing you an opportunity to opt-out or otherwise prohibit such unrelated uses. </P>
<P><B><A NAME="4"></A><span class="bodytitle">Our Commitment To Data Security:</span> </B></P>
<P>To prevent unauthorized access, maintain data accuracy, and ensure the correct use of information, we have put in place appropriate physical, electronic, and managerial procedures to safeguard and secure the information we collect online. </P>
<P><B><A NAME="5"></A><span class="bodytitle">How You Can Access Or Correct Your Information:</span> </B></P>
<P>You can access all your personally identifiable information that we collect online and maintain by accessing the &quot;CP&quot; user Control panel link located on the navigation menu within the Safehouse forum pages. We use this procedure to better safeguard your information. </P>
<P>You can correct factual errors in your personally identifiable information by sending us a request that credibly shows error. </P>
<P>To protect your privacy and security, we will also take reasonable steps to verify your identity before granting access or making corrections. </P>
<P><B><A NAME="6"></A><span class="bodytitle">How To Contact Us:</span> </B></P>
<P>Should you have other questions
    or concerns about these privacy policies, please email us at <A class="bodylinksalt" HREF="mailto:privacy@thesafehouse.org">privacy@thesafehouse.org</A>. <BR>
