<!--startscanning-->Today Electronic Arts announced that it has entered into an agreement
to buy Mythic Entertainment.&nbsp; Mythic Entertainment will become EA
Mythic, a wholely owned studio dedicated to developing Massively
Multiplayer Online Role-Playing Games.<br>
<br>
The press release can be found in .pdf form <a href="http://info.ea.com/news/pr/pr786.pdf">here</a>, or on Mythic Entertainment's website <a href="http://www.mythicentertainment.com/press/06202006EAMythic.html">here</a>.<br><br>Below is a cut-and-paste of the press release:<br><br><p class="MsoNormal" style="text-align:center" align="center"><b style=""><span style="font-size:14pt"><font face="Arial">EA TO ACQUIRE MYTHIC ENTERTAINMENT
						</font></span></b></p>
		<p class="MsoNormal"><font face="Arial">&nbsp;</font></p>
		<p class="MsoNormal" style="text-align:center" align="center"><b style=""><i style="">
					
						<font face="Arial">Studio Recognized Worldwide for Top Development Talent, 
							Critically </font>
					</i></b></p>
		<p class="MsoNormal" style="text-align:center" align="center"><b style=""><i style="">
					
						<font face="Arial">Acclaimed Franchises and Ground-Breaking Online Experiences</font></i></b></p>
		<p class="MsoNormal"><font face="Arial">&nbsp;</font></p>
		<p class="MsoNormal"><b style=""><font face="Arial">&nbsp;</font></b></p>
		<p class="MsoNormal" style=""><font face="Arial"><b style="">Redwood 
					City, CA, June 20, 2006 – </b>
				Electronic Arts Inc. (NASDAQ: ERTS) 
today announced that it has entered into an agreement to acquire Virginia-based 
Mythic Entertainment®. Upon completion of the acquisition, Mythic Entertainment 
will become EA Mythic, a wholly-owned studio dedicated to developing Massively 
Multiplayer Online Role-Playing Games (MMORPGs). Mythic is recognized worldwide 
for revolutionizing the online gaming space with the awardwinning <em>Dark Age of Camelot®</em> and is currently developing <em>Warhammer® 
						Online: Age of Reckoning™</em> under license 
agreement with Games Workshop.</font></p>
		<p class="MsoNormal" style="">
			
				
			&nbsp;</p>
		<p class="MsoNormal" style="">
			
				<font face="Arial">The acquisition is subject to customary closing conditions, 
					including regulatory approvals, and is expected to close during EA’s second 
					quarter of fiscal 2007. Financial terms of the transaction were not disclosed.</font></p>
		<p class="MsoNormal" style="">
		</p>
		<p class="MsoNormal" style="">&nbsp;</p>
		<p class="MsoNormal" style=""><font face="Arial">Upon 
				completion of the acquisition, Mark Jacobs, the President, Chief Executive 
				Officer and cofounder of Mythic, will become Vice President, General Manager of 
				EA Mythic. Rob Denton, the Vice President, Chief Operating Officer and 
				co-founder of Mythic will assume the role of Vice President, Chief Operating 
				Officer of EA Mythic. Mythic’s 175-person development team will remain in 
				Fairfax, Virginia.</font></p>
		<p class="MsoNormal"><font face="Arial">&nbsp;</font></p>
		<p style="color:rgb(255, 255, 255)" class="MsoNormal"><font face="Arial">“Mythic has always been a 
					leading independent developer in the online space," <strong>says Mark Jacobs, CEO 
						and co-founder of Mythic Entertainment</strong>. "EA's commitment to the 
					online market as well as its focus on creating games of unsurpassed quality, 
					scope and scale gives us opportunities and resources we could only dream about 
					in the past."</font></p>
		<p style="color:rgb(255, 255, 255)" class="MsoNormal">&nbsp;</p>
		<p style="color:rgb(255, 255, 255)" class="MsoNormal"><font face="Arial">“The addition of Mythic 
					to the EA family reflects our deep commitment to the online gaming market 
					worldwide. Mythic will bring one of the industry’s most talented MMORPG teams 
					to EA. Together, we will create games that will introduce MMO players to a 
					whole new level of game play and excitement,” <strong>said Paul Lee, President, EA 
						Studios</strong> .
					<br style="">
					<!--[if !supportLineBreakNewLine]--><br style=""><!--[endif]--></font></p>
<font style="color:rgb(255, 255, 255)" face="Arial">		</font>
		<p style="color:rgb(255, 255, 255)" class="MsoNormal" align="center"><font face="Arial">&nbsp;
					###</font>
			</p>
		<p style="color:rgb(255, 255, 255)" class="MsoNormal" align="center"><strong></strong>&nbsp;</p>
		<p style="color:rgb(255, 255, 255)" class="MsoNormal" align="left"><strong><font face="Arial">About 
						Electronic Arts </font></strong>
			</p>
		<p style="color:rgb(255, 255, 255)" class="MsoNormal" align="left"><font face="Arial">Electronic 
					Arts Inc. (EA), headquartered in Redwood City, California, is the world's 
					leading interactive entertainment software company. Founded in 1982, the 
					company develops, publishes, and distributes interactive software worldwide for 
					videogame systems, personal computers and the Internet. Electronic Arts markets 
					its products under four brand names: EA SPORTSTM, EATM, EA SPORTS BIGTM and 
					POGOTM. In fiscal 2006, EA posted revenue of $2.95 billion and had 27 titles 
					that sold more than one million copies. EA's homepage and online game site is <a href="http://www.ea.com/">
						www.ea.com</a>. More information about EA's products and full text of press 
					releases can be found on the Internet at </font><a href="http://info.ea.com/"><font face="Arial">
						http://info.ea.com</font></a><font face="Arial">.</font></p>
		<p style="color:rgb(255, 255, 255)" class="MsoNormal" align="left">&nbsp;</p>
		<p style="color:rgb(255, 255, 255)" class="MsoNormal" align="left"><font face="Arial"><strong>About 
						Mythic Entertainment</strong> </font>
			</p>
		<p style="color:rgb(255, 255, 255)" class="MsoNormal" align="left"><font face="Arial">Mythic 
					Entertainment recently celebrated its ten year anniversary as one of the most 
					successful independent developers and publishers of massively-multiplayer 
					online role-playing games in the world. Credited with 15 online games, 
					including the award winning <em>Dark Age of Camelot</em>, Mythic's success is 
					based on proprietary technology, innovative game design and exemplary customer 
					service delivered by a talented staff of more than 170 people. <em>Dark Age of 
						Camelot</em> is played in almost every country in the world, and is 
					available in seven languages. Mythic has been in development of <em>Warhammer 
						Online: Age of Reckoning</em> since acquiring the exclusive worldwide PC 
					and console MMORPG license from Games Workshop in May 2005. </font>
			</p>
		<p style="color:rgb(255, 255, 255)" class="MsoNormal" align="left">&nbsp;</p>
		<p style="color:rgb(255, 255, 255)" class="MsoNormal" align="left"><font face="Arial"><em>Some 
