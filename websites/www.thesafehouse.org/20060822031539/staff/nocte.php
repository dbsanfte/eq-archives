<span class="bodylinks"> 
Kevin "Nocte" Dolley was swept into the gaming world at the age of 12. He had become fascinated with the handful of odd-shaped dice cast by a summer camp counselor who wove tales of chivalry and treachery around a table of other counselors. That same summer, Kevin spent every dime of allowance and savings he had on Dungeons &amp; Dragons. The creative outlet offered by the early stages of fantasy RPG pen and paper games was just the thing to give an awkward geek of a kid a bit of escape. Enamored by the early Gygax-esque Drow, the dark elves were always his choice of race. The pure Swiss-army-knife utility and role-playing charm of the rogue also appealed to him. Once a thief, always a thief - the Nocte persona has shadowed Kevin around for over two decades. <br>
<br>
The transition to electronic gaming came a bit slower, however. Console gaming was pretty much "it," until Kevin graduated college, moved out, and bought his first PC. While his Playstation still got worked to a fare-the-well, the allure of Wolfenstein, Doom, and that gem of MUDs, Dragonrealms, started to drag him away. Later on, his best friend was in the EverQuest beta and he watched him play many late evenings, intrigued by the character interaction and enormous, budding world. A couple years later at about the time Scars of Velious was released. and about the same time of impending fatherhood, he decided to jump into Norrath. Go figure, eh? Hey, if you're going to lose sleep anyway. <br>
<br>
Shortly thereafter, Kevin discovered the Safehouse from his guild leader, a fellow rogue, who pointed the way to all things dastardly and stealthy. He lurked for the better part of a year before actually registering. One of the many topical debates had escalated to the point where he was agitated enough to comment. Shortly thereafter, he became more involved in discussions and the community at large. Since early 2003, Kevin has worked as both Safehouse Informant and Moderator. (Wow, has it really been that long?!) <br>
<br>
Kevin studied English literature with a focus on classic rhetorical writing (BA in English). This propelled him into the illustrious career of a Field Marketing Account Executive, in which he ran Northeast promotions for Mountain Dew, et al. After 5 years of that, he moved onto technical writing.<br>
<br>
Mad props, thanks and shout-outs to: <br>
<br>
. The wife, for watching me regress to my inner geek and still loving me anyway. <br>
. The kiddos, for giving me fresh eyes to view life. They never cease to amaze me. I forgot just how cool a fire truck and bugs really are. <br>
. The 'rents, my original Mods, for always doing just enough, at just the right time, yet never holding me back. <br>
. The Safehouse Staff (past, present, and future), for all of the countless and thankless hours you've poured into this little clutch of friends and acquaintances. You maintain something special that's a pleasure to visit every day. "Thwapping Stick" and "Dink" have become regular words in my day-to-day vocabulary canon. <br>
. The Safehouse community. What can I say? You've become extended family. :) Every joke, piece of gaming advice, argument/debate, PM, email, I learn from. You may not think I listen, but I honestly do. </span><br>
