<!--startscanning-->Yesterday Allahkazam.com made this announcement:<br><br><br><span style="font-style:italic">
Allakhazam is pleased to announce that we have added several new sites
to our network and have joined them together to form the Zam.com
Network which is now instantly the leading content destination for all
MMO gamers. </span><br style="font-style:italic"><br style="font-style:italic"><span style="font-style:italic">
The new Zam.com Network will consist of newly expanded and improved
versions of Allakhazam.com and Thottbot.com. In addition, the assets
and content of OGaming.com (and the interface sites) and L2Orphus.com
will be integrated into the existing Allakhazam network. This will add
multiple new games to our site, more than double our personnel and give
us the flexibility to add more features, better service and expanded
coverage. </span><br style="font-style:italic"><br style="font-style:italic"><span style="font-style:italic">
In the coming months, we will be upgrading our server
infrastructure, expanding our news and editorial coverage, adding sites
for more games, and designing even better database services. We are
committed to making this the premier destination for the entire MMORPG
community.</span><br><br>Since the announcement the story has become a bit more complex.&nbsp; In <a href="http://www.allakhazam.com/forum.html?forum=3;mid=114668646932202500;page=1">this thread</a> Allakhazam has made a few more posts:<br><br><span style="font-style:italic">The ownership issues here are convoluted.&nbsp;&nbsp;This is how
deals like this get done. The size of this is pretty amazing. This is
just the announcement for our own users, not the actual corporate
announcement, which will likely come much later. We are now owned by a
company that owns a bunch of stuff, including IGE. They bought both of
us (and several other sites as well) and then split us into separate
divisions so that there is no interaction between them. You know my
stand on gold selling. Before agreeing to anything like this, I wanted
to make sure that there would be no interaction between those divisions
and that I would have complete control over the new network, including
the sites that used to be part of ogaming. So this means that the
Ogaming sites and Thottbot have been split off of IGE and into our
network and no longer have any connection with them.</span><br><br>and<br><br><span style="font-style:italic">I can't talk about the company that bought us because that information
is still confidential, and until they release that confidentiality the
subject is off limits.&nbsp;&nbsp;They may well be trying to buy other
sites or businesses as far as I know and want to save up for a full
blown press release. This announcement is being made here and now
because we want to get on with merging the network together and
obviously have to tell people about it before we do.</span><br><br>and<br><br><span style="font-style:italic">The value of the purchase is really obvious.&nbsp;&nbsp;Thottbot and
Allakhazam are the two largest sites for the largest MMO game in the
world. The two have cancelled each other out financially, since
anything one would charge for in a premium service was offered by the
other for free. Now with both sites, we can expand our premium service
to cover all WoW players. Frankly if Thottbot hadn't existed I doubt I
would have had to sell Allakhazam. Because Thottbot existed and was
giving away their information for free (and losing money like crazy to
their former company who didn't care if they made any money off it) we
could not make a profit off our wow site even though it was by far our
biggest expense. The company that bought both sites was smart enough to
realize the potential of combining those two user bases.</span><br><br>The company that is believed to have purchased Allakhazam is RPG Holdings LLC, parent company to secondary market MMOG gold and item seller, IGE.<br><!--/startscanning-->

