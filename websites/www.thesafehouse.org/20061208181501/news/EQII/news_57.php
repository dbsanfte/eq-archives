<!--startscanning--><p align=left><span class=postbody>Update Notes: January 06, 2005 1/6/2005 8:00 AM <br><br><span style="FONT-WEIGHT:bold">*** Headlines ***</span> <br><br></p>
<ul>
<li>
<div align=left>Tons of new quests, mostly for solo players! <br></div>
<li>
<div align=left>Two new dungeons available for groups at levels 25 to 30! <br></div>
<li>
<div align=left>Character Differentiation choices have been reset for all characters. See details below. <br></div>
<li>
<div align=left>New Character Differentiation option: Training! <br></div>
<li>
<div align=left>Lots of artisan recipe and ability fixes! <br></div>
<li>
<div align=left>Eat healthy! New examine information added to explain the benefits of food and drink! <br></div>
<li>
<div align=left>Brawlers get built-in shields! <br></div>
<li>
<div align=left>Guild followers can spend the status they earn at city merchants! <br></div>
<li>
<div align=left>Now easier to equip rings, wrist items, and weapons! <br></div>
<li>
<div align=left>Slash that dot! Maps now show your current heading! <br></div>
<li>
<div align=left>Lots of UI tweaks and enhancements! <br></div>
<li>
<div align=left>More bug fixes than you can shake a moss snake at!</div></li></ul>
<p align=left><br><br><span style="FONT-WEIGHT:bold">*** Character Differentiation ***</span> <br><br></p>
<ul>
<li>
<div align=left>All characters logging in after this update will have their previous character differentiation choices reset. This includes any selections that were pending. <br></div>
<li>
<div align=left>Character differentiation selections will be offered again. You may receive your selections in a different order this time. Class-dependent choices (like Tactics: Enemy Mastery) will be presented after the others. Characters who gain these abilities as they level up normally will get them in the normal order.</div></li></ul>
<p align=left><br><br><span style="FONT-WEIGHT:bold">** IMPORTANT! **</span> <br></p>
<ul>
<li>
<div align=left>You will not be able to receive quest rewards until you make all pending character differentiation choices available to you.</div></li></ul>
<p align=left><br><br><br><span style="FONT-WEIGHT:bold">*** Training ***</span> <br><br></p>
<ul>
<li>
<div align=left>Characters now receive a new character differentiation option every ten adventure levels: Training! <br></div>
<li>
<div align=left>Training allows you to choose one of four new abilities. Each of these spells or arts builds off abilities you've been using but substantially increases their effectiveness and benefit. <br></div>
<li>
<div align=left>The choice is yours whether you prefer a stronger attack, a more potent buff, a heartier heal, or a handier utility function. Training allows you to enhance specific aspects of your character and be a bit unique. <br></div>
<li>
<div align=left>These Training abilities have a longer span of effectiveness than most common spells and arts, so they'll prove very useful to you. <br></div>
<li>
<div align=left>Note: Characters who reach levels 10 and 20 will not be prompted to make their Training selections until they leave their current zone and enter another. This is a known issue that will be corrected in a future update. <br></div>
<li>
<div align=left>Remember, you MUST make all Training choices presented to you before you can receive any quest rewards.</div></li></ul>
<p align=left><br><br><br><br><span style="FONT-WEIGHT:bold">*** Gameplay ***</span> <br><br></p>
<ul>
<li>
<div align=left>Group members can now join other group members in a specific instance of a zone even if that zone has reached its capacity. <br></div>
<li>
<div align=left>If a character dies and is given the option to be revived in a different zone, the revive window will allow the player to choose from multiple instances if they are available. <br></div>
<li>
<div align=left>Harvesting quests can no longer be completed by purchasing the items from a broker. <br></div>
<li>
<div align=left>Spells that debuff the target's maximum health or power now work appropriately. <br></div>
<li>
<div align=left>Fixed a bug that could cause you to fall under the world while swimming. <br></div>
<li>
<div align=left>Removed the restriction that only guild patrons can spend status points. <br></div>
<li>
<div align=left>You are no longer able to swap bags into your shared bank if one of the bags contains a lore or no-trade item. <br></div>
<li>
<div align=left>You are no longer able to drag lore items from your overflow slot into the shared bank. <br></div>
<li>
<div align=left>Fixed a bug that caused rare mastery quest items not to drop from solo encounters.</div></li></ul>
<p align=left><br><br><br><span style="FONT-WEIGHT:bold">*** New Dungeons ***</span> <br><br></p>
<ul>
<li>
<div align=left>Rumor has it that a vile creature named Gobblerock has taken up residence in a newly discovered section of the Freeport Sewers. Those brave enough to end this threat to the Overlord's domain should consult knowledgeable individuals south of the Freeport gates. <br></div>
<li>
<div align=left>Sources in Qeynos tell us that a previously condemned section of the Catacombs has been reopened. This new area contains some rather formidable adversaries that threaten the city. Faithful servants of the Queen should seek out more information at the lookout point east of the Oracle Tower in Antonica. <br></div>
