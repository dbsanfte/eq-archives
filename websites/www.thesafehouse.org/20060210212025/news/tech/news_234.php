X1900 series.&nbsp; The Radeon X1900 XT clocks in with a 625mhz core and
725mhz memory, while the Radeon X1900 XTX clocks in at 650 and 775mhz
respectively.&nbsp; The biggest change over the X1800 series is the addition
of more pixel shaders to bring the X1900's total to 48, up from the
