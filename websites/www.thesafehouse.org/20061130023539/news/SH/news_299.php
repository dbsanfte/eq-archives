<!--startscanning--><p align=center><em>Seafaring MMORPG to launch June 2007</em></p>
<p><strong>(PENNY ARCADE EXPO) August 25, 2006</strong> – At today’s opening of the Penny Arcade Expo, Flying Lab Software unveiled the addition of avatar combat to their award-winning MMORPG <a href="http://www.burningsea.com/" target=_blank>Pirates of the Burning Sea</a>, a massively multiplayer online computer game set in the Caribbean during the 1720s. </p>
<p>Fighting across land and sea, players of Pirates of the Burning Sea will experience intense tactical ship combat and swashbuckling adventures, where every player is the captain of their own ship. British, Spanish, French, and Pirate captains battle it out for glory and power, engaging in both PvE and PvP game play that has the power to truly change the game world. Compete to conquer enemy ports, pursue 1000+ missions, engage in a fully player-driven economy, and earn ships, skills, and gear to become the most famous -- or infamous! -- captain on the Burning Sea.</p>
<p>The addition of avatar combat to Pirates of the Burning Sea’s game play amplifies the need for strategy and skill, offering melee encounters where movement, position and tactics truly matter. This new feature expands sea encounters as well, allowing players to board enemy ships, and with the aid of their crew, battle their opposition for supremacy and seize their opponent’s ship and cargo. </p>
<p>More than a simple hack-and-slash combat system, players move and dodge around the battlefield, wielding a vast array of weapons: from rapiers and pikes to muskets and even bar-stools! Adopt an aggressive stance and unleash a powerful combination of attacks that send the enemy reeling back on his heels, or bide your time on defense and wait for the opportune moment to launch your fatal strike. With a focus on fast action, environmental interaction, swift dispatches of minor enemies, and lengthy duels against worthy adversaries, Pirates of the Burning Sea offers swashbuckling combat the likes of which have never been seen in an MMORPG!</p>
<p>On land, players will explore dense jungles, Aztec ruins, and pirate treasure caves in search of adventure, fame and fortune. Players can raid fortresses manned by dozens of soldiers and battle it out for the opportunity to hoist their flag above the citadel, or duel sneering spies in swashbuckling swordfights within elegant colonial mansions.</p>
<blockquote dir=ltr style="MARGIN-RIGHT:0px">
<p dir=ltr style="MARGIN-RIGHT:0px"><font size=2><em>“We already brought intense real-time tactical ship combat to MMOs,” said Producer John Scott Tynes. “Now we’re bringing the same imagination to sword fighting, brawling, and musketry through the introduction of swashbuckling combat mechanics.” </em></font></p></blockquote>
<p>By the time the game releases in June 2007, it will have been in development for almost five years and in beta testing for a year and a half, resulting in a title with polished, innovative game play, and rich content. Flying Lab is currently accepting beta applicants at www.burningsea.com.</p>
<p><strong><u>Key Game Features</u></strong></p>
<ul>
<li>Join forces with the Pirates, English, French, or Spanish 
<li>Traverse a world of over 100 PvP conquerable Caribbean ports 
<li>Explore the world and make a name for yourself via 1,000+ Missions 
<li>Completely player-driven, manufacturing-based supply-and-demand economy 
<li>Build, capture and customize over 50+ historical ships 
<li>Design your own personal flags and customized sail decals 
<li>Supports SoundBlaster X-Fi sound cards with 128 simultaneous audio channels for stunning battle effects 
<li>Award-winning graphics 
<li>Sound effects by Academy Award® winner Richard King, of “Master and Commander” fame</li></ul>
<p><strong><u>About Flying Lab Software</u></strong></p>Flying Lab Software, a Seattle-based game development company, is the maker of the critically acclaimed Rails Across America. Their current project is Pirates of the Burning Sea, an innovative massively multiplayer online role-playing game of adventure and naval combat. 
