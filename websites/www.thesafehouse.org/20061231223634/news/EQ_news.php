provide all players the chance to enjoy a large and active community....<br>

<br>
As part of this reorganization we will officially be shutting down our
three servers: Sebilis, Kael Drakkal, and Venril Sathir and UbiSoft’s
EverQuest customers will be transferred to Sony Online Entertainment on
June 30th, 2005. At that time, your account and your characters,
including all your characters’ in-game assets, will be moved to the
Antonius Bayle server. Please note that this is an English language
server. <br>

<br>
We hope to make this transfer process as transparent as possible for
you. In appreciation of your patience during this transition process,
Sony Online Entertainment is pleased to provide you with the following:
<br>

<br>

<br>
Thirty (30) days of free game time in EverQuest 
<br>
All EverQuest expansions released through EverQuest®: Omens of War™ 
<br>
A 30-day subscription account key for EverQuest® II, and download access to the EverQuest II software. 
<br>
As a current subscriber to EverQuest, you will not have to make any
changes to your billing information or to your EverQuest software.
After the transfer is completed, your EverQuest client will
automatically update the next time you log in. <br>

<br>
On the day of the transfer, June 30th, you will receive a follow-up
email alerting you to the start of the process. Included in that email
will be your EverQuest II 30-day subscription key and a link to
download the EverQuest II software. Also, upon conclusion of the
transfer, 30 days of free EverQuest game time will be added to your
EverQuest account. <br>

<br>
