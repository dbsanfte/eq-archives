  with player-set traps. And for the first time in <em style="font-weight:bold">EverQuest</em>, players 
  will be able to destroy objects in the world; doors can be bashed in, walls 
  can be toppled and towers can be destroyed, leading to a new level of interactivity 
  within the game and opening up a new realm of design.</p>		</span>		</div>
