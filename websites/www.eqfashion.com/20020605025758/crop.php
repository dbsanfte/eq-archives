<TABLE CELLPADDING=10 BORDER=0><TR><TD VALIGN=top><FONT><B>Hauki's Supa-Dupa Guide to Croppin'</B><BR><BR>
Before you can begin cropping your images you need to go download a copy of a program called XnView.  XnView is a freeware program which means no money out of pocket to use, the download link is provided below.  Once you download XnView and install it, continue to step 1 of this tutorial.<BR><BR>
<TABLE WIDTH=300 CELLPPADDING=2 CELLPSACING=0><TR><TD VALIGN=middle ALIGN=CENTER><A HREF=http://www.xnview.com><IMG SRC=images/xnview.jpg BORDER=0><BR></A></TD><TD VALIGN=middle><FONT><A HREF=http://www.xnview.com><B>XnView Official Website</B></A><BR>Setup File 1.5megs -- <A HREF=ftp://www.zoo-logique.org/xnview/download/XnView-win.exe>Download Here</A><BR></TD></TR></TABLE><BR>
<B>Step 1: Open your image</B><BR>
Open XnView and go to "File->Open".  Locate your EverQuest folder (Normally c:\program files\everquest) and select the image you wish to crop.  The image in my example is in 640x480 (resolution) but this tutorial should work for any size image.<BR><BR>
<CENTER><TABLE BORDER=0 CELLPADDING=2 CELLSPACING=2 WIDTH=75%><TR><TD ALIGN=CENTER VALIGN=MIDDLE><A HREF=images/xnview_howto_01.jpg><IMG SRC="images\xnview_howto_01_thumb.jpg" BORDER=0><BR><FONT>Step 1: View</FONT></A></TD><TD ALIGN=CENTER VALIGN=MIDDLE><A HREF=images/xnview_howto_02.jpg><IMG SRC="images\xnview_howto_02_thumb.jpg" BORDER=0><BR><FONT>Step 1a: View</FONT></A></TD></TR></TABLE></CENTER><BR>
<B>Step 2: Make your first crop</B><BR>
Take your mouse and draw a rectangular selection around your character.  Make sure to adjust the top and bottom parts to match up with the head and feet of your character.  Use the FULL width of the image right now.  You'll be making one more crop in couple steps.  In the example provided I have made my crop lines yellow so that you could see them better.  When you have your selection where you want it, got to "Edit->Crop" (Control Y on the keyboard)<BR><BR>
<CENTER><TABLE BORDER=0 CELLPADDING=2 CELLSPACING=2 WIDTH=75%><TR><TD ALIGN=CENTER VALIGN=MIDDLE><A HREF=images/xnview_howto_03.jpg><IMG SRC="images\xnview_howto_03_thumb.jpg" BORDER=0><BR><FONT>Step 2: View</FONT></A></TD><TD ALIGN=CENTER VALIGN=MIDDLE><A HREF=images/xnview_howto_04.jpg><IMG SRC="images\xnview_howto_04_thumb.jpg" BORDER=0><BR><FONT>Step 2a: View</FONT></A></TD><TD ALIGN=CENTER VALIGN=MIDDLE><A HREF=images/xnview_howto_05.jpg><IMG SRC="images\xnview_howto_05_thumb.jpg" BORDER=0><BR><FONT>Step 2b: View</FONT></A></TD></TR></TABLE></CENTER><BR>
<B>Step 3: Resize your image</B><BR>
Now you need to resize the image.  Go to "Image->Resize" from the drop down menu (Control S on the keyboard) and change the height of your image to 350.  You'll need to make sure that "Keep Ratio" is checked so that your width will automatically be adjusted.  Once you have 350 typed in, click Ok to resize the image.  Now go to "Filters->Enhance->Enhance Detail" to sharpen the quality of the image.<BR><BR> 
<CENTER><TABLE BORDER=0 CELLPADDING=2 CELLSPACING=2 WIDTH=75%><TR><TD ALIGN=CENTER VALIGN=MIDDLE><A HREF=images/xnview_howto_06.jpg><IMG SRC="images\xnview_howto_06_thumb.jpg" BORDER=0><BR><FONT>Step 3: View</FONT></A></TD><TD ALIGN=CENTER VALIGN=MIDDLE><A HREF=images/xnview_howto_07.jpg><IMG SRC="images\xnview_howto_07_thumb.jpg" BORDER=0><BR><FONT>Step 3a: View</FONT></A></TD></TR></TABLE></CENTER><BR>
<B>Step 4: Finish the crop and save</B><BR>
To finish cropping and make the image the correct size for submittal you need to make one more crop.  Start by making a selection box around your character using the FULL height of the image this time.  Once you have the crop selection around your character, adjust the width of it until it reaches 200.  To do this watch the bottom "Status" line in XnView (See my zoomed status line in the example).  When your crop equals 200x350 you're crop selection is done. If your character is not centered in the selection, move the side that needs moving out and adjust the oposite side to match.  Once it's 200x350 you're set again.  Now go to "Edit->Crop" again. Your image is now ready to be saved and uploaded to the website.<BR><BR>
To save your image go to "File->Save As" - when the box pops up choose "JPG - JPEG / JFIF" from the "Save as type" section, type in a filename and click "Save".  One thing to note is the "Options" button.  You MIGHT need to adjust the "Quality" down from 100% to get the image under the 50k mark.  Save a couple of copies of your image adjusting down from 100% to 90% or even 80%.<BR><BR>
That's really about all there is to cropping your image.  The steps provided here are general enough to be applied to any other graphics program, XnView is used only as an example.  I use Photoshop 7 to do my cropping.<BR><BR>
