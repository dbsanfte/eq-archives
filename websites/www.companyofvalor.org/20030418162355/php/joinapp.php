<H2 CLASS="ezboard">Information about us</H2>
If you have not yet, please visit our website at <A HREF="http://www.companyofvalor.org">http://www.companyofvalor.org</A>
and at least read over our <A HREF="charter.shtml?PHPSESSID=699c3f31eab26f6fab1b5e28a908429b">charter</A>.
<P>
<STRONG>Here is an excerpt from the charter:</STRONG><BR>
<BLOCKQUOTE>
As the name of the guild implies, members of the Company of Valor shall
act with honor and courtesy to the citizens of Norrath at all times.
Members will help those in need or in peril whenever possible (this does
not mean giving money or items to persistent beggars, or leaving a group
to help someone which may cause undo harm to the group). We shall also
strive to gain as much knowledge about the dangers and benefits within
the realms we adventure. 
<P>
We seek only mature individuals who display similar traits to join our
ranks. Kill Stealers, Loot Hoggers, Beggars, intentional Trainers and those 
of other un-noble characteristics will not be allowed. 
<P>
====== ... =========
<P>
Anyone wishing to join Company of Valor must be sponsored. Quoting from
Oxxan's contribution about being a sponsor, "How do I get a sponsor? 
Run with members of Company of Valor. This is the easiest way to work
together with members of our Guild. It provides you with valuable
information on our members and their actions. It also provides them with
information about you. Know members of Company of Valor in the real
world." The officer, sponser, and new member will get together to
perform the invitation ceremony. If it is not possible for all to meet
in person, all have to at least be online at the same time. The officer
will then talk with both parties and if the new recruit is deemed
worthy, they shall be admitted. The officer will make sure the new
member knows the website, how to use guildchat, and how to check for
members in the guild that are currently online, and what requirements
the new member must meet (see below). 
<P>
The sponsorship does not apply to a current member creating a secondary
character which they also wish to the join the guild. The member's
current character that is already in the guild must tell me or another
officer about this wish. A new character saying, "Hey this is so and so"
will not work. 
<P>
</BLOCKQUOTE>

CoV is a guild composed of members who have taken on the responsibility
of being a family. We pride ourselves in this aspect of our guild and
frown upon players who do not take an interest in the guild as a whole.  This does
not mean, however, that attendance at every guild event/function is required.  
We are not an "uber" guild in the stereotypical sense of the word. We do
not go on raids every other day to get all that "phat lewt" and such. We
primarly play to have fun and enjoy one another. If you have a problem
with this please do not waste your and our time by filling out this
application. Of course we go on raids and hold events (see our <A HREF="calendar/current.shtml?PHPSESSID=699c3f31eab26f6fab1b5e28a908429b">calendar</A>
and Events forums); however, we don't overwhelm ourselves with them
either. This is only a game afterall, not a job. :)

<H2 CLASS="ezboard">The Questionaire</H2>
Please enter your character's name and a valid email address. The questionaire/information sheet is pretty small and probably takes less time to complete than it does to zone into some areas on Norrath LOL.  Once submitting the questionaire, an email will be sent to the Guild Leader(Mysstie).  Additionally, a copy of what is emailed will also be sent to you.  I will then post the questionaire onto the Prospective Members forum on the message board for review and reference.  I will send an email to both you and your sponsor (provided I have an email address for them), when I have actually posted the questionaire so that your sponsor knows to check it.  As the charter says, you must have a sponser within CoV that can vouch for you before admission will be granted.  If you have any questions or concerns about this questionaire, please send me an email at <A HREF="mailto:mysstie@companyofvalor.org">mysstie@companyofvalor.org</A> :)
