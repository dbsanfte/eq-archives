							I'm sure many of you have noticed the upgrades to our downloads area. The most noticeable seen on the file info screen. The changes go deeper then that and add some new features.<br />
<br />
Interface authors can now allow others to submit patches/add-ons to their interface. Authors will see the option to enable/disable this for their interface when they upload a new interface or edit their current interface. This is default to on for eqinterface.com.<br />
<br />
Authors can now edit the description, title, options, pictures of their interface in our database with out it being sent to the moderation queue. Only changing the interface file will trigger it to be sent to the moderation queue for an admin to approve.<br />
<br />
Members will notice a [Upload Patch|Upload Add-on] button on the file info screen for any interface the author has allowed them on. All patches/add-ons for an interface will be listed on the file info screen once they are approved by an admin. <i>Members can not upload directly to the new category Patches &amp; Addons. You must use the links mentioned above through the file info screen. </i><br />
<br />
Members now have a favorite interfaces list. When members view the file info of an interface they will now see a button to add the interface to their favorites list. On the home page of eqinterface.com in the main menu block members will see a link to their favorites list and directly under it an update counter. This count will tell members how many of their favorite interfaces have been updated since they last logged on to our webpage. When members view the favorites list all the new interfaces since they last visited will be highlighted for them.<br />
<br />
Many bug fixes have also been made. When an interface is updated it now shows [updated] instead of [new]. The file count on the category list should be more accurate since updating an interface no longer adds to the file counter. When browsing a category with multiple pages the sort order will be remembered. Many more fixes that I뭢 forgetting about. <br />
