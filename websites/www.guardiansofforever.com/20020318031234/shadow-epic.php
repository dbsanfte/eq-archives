<img src="images/shadow_sig.gif" width="71" height="225" border="0" align="left" alt=""><b>Shadow Knight Epic Quest</b><br><br>
Talk to Kurron Ni, who can be found near the docks in the Outpost, in the OverThere. Ask him "what do you seek" and he will tell you that he is on a quest. Tell him next "I agree" and then he will tell you that he is in search of Darkforge Helm, Darkforge Chestplate, the Darkforge Leggings, and 900pp. Before you hand in the items to Kurron, make sure you have a couple of groups with you, just to make sure. Once you hand in the items, he will not reward you with anything. Ask him "what mission" and he will tell you of his treachery and will then attack you. Kill him and you can loot the Note to Duriek.<br>
<br>
Now off we head to Paineel, to locate Duriek. He is upstairs in the tower, which looks out onto the newbie grounds. Hand him the note and he will tell you the story of his apprentice. Ask him "what apprentice" and he will give more background information. Next tell him that you will take his apprentice's place and he will ask you to prove your worth. First he asks you to get him a Cough Elixer as he is getting older and is in need of one. Head off to Neriak and talk to Smaka who can be found in the Foreign Quarter. He can make special cures but they do cost a lot. Pay him 1000pp and he will create the Cough Elixir for you.<br>
<br>
Once you have obtained the Cough Elixir, hand it to Duriek and he will inform you that he was trying to create the Corrupted Ghoulbane, but alas he is in no condition to search out the necessary components anymore. He needs you to first locate a Tome for him, which is dropped by Ratman Guards who can be found in the Hole. Once you have obtained the Tome, hand it to Duriek and he will then tell you the items you must collect to create the Corrupted Ghoulbane.<br>
<br>
The four items he requires are the Blade of Abrogation which is obtained through a quest in the Plane of Air, the Soul Leech which is dropped by Cazic-Thule in the Plane of Fear, the Ghoulbane which is dropped off the Shin Lord in Upper Guk and the Decrepit Sheath. To obtain the sheath, head off to the Qeynos Sewers and locate Teydar who spawns in the middle of the Blood Sabers lair. He will tell you that he is in need of some items before he will hand over his loot. He would like you to bring him a Decrepit Hide which are dropped by Ashenbone Drakes in the Plane of Hate, a Drake Spine which is dropped by Rharzar, a rare spawning Drake in the Rathe Mountains, and an Enchanted Platinum Bar. Handing all three items to Teydar will result in the Decrepit Sheath.<br>
<br>
Now head back to Duriek and hand him all four items, and he will reward you with the Corrupted Ghoulbane, and will then tell you to go and talk to Lhranc who can be found in the City of Mist. Lhranc spawns in the middle hut near all the Goo's at loc [0,0]. He will tell you that he is in need of three items before he can forge the weapon for you, the Heart of an Innocent, the Will of Innoruuk and the Head of the Valiant.<br>
<br>
Off to Toxxulia Forest to locate Knarthenne Skurle who spawns near the docks. If you hail him, he will tell you that you do not have the heart of an innocent. If you ask about the heart of an innocent, he will hand you a soulcase and tells you to combine the soulcase with the heart of someone who is pure. Now we head off to the Hole and head down to the Jail Area. Locate Caradorn and he will give you a lot of background information. Basically Caradorn just wants to be released from this hell and needs you to locate the key for him. The key to Caradorn's cell can be found on various Mimics who can be found past a secret door near the bank. Once you hand Caradorn the key, Kyrenna will spawn and they both will attack you. Loot Kyrenna's corpse and you will discover the Blood of Kyrenna and the Heart of Kyrenna. Place the heart in the soulcase and hit combine, it will become the Heart of an Innocent.<br>
<br>
For the second item we will have to head off to Kerra Ridge and talk to Marl Kastane who spawns beside a hut in town. There also seems to be two different versions of Marl. The first will give you a quest and upon completing it, will spawn the other Marl. To see if you have the correct one, ask him "what prophecy". If it's the first Marl, you have to deliver a letter to Gerot Kastane who can be found in Paineel. He will in turn hand you back another item, which you hand back to Marl. You will gain faction and the second Marl will spawn.<br>
<br>
Once you ask Marl, "what prophecy" he will give you a background story on Glohnor and Lhranc. If you tell him "I will complete the ritual" he will tell you that you need to create a Dark Shroud. To do this you will need the blood of an innocent. Hand him the Blood of Kyrenna and he will reward you with the Dark Shroud and will tell you to give this to Glohnor who can be found in the Hole. Handing this item to Glohnor will cause a Mummy of Glohnor to spawn, which you must kill. Loot the Mummy Wrappings of Glohnor and the Head of Glohnor from the corpse. Return to Marl and hand him the wrappings (again make sure it's the second Marl) and you will be rewarded with the Will of Innoruuk.<br>
<br>
Head back to Paineel now and talk to Gerot Kastane. He will tell you all about the Head of the Valiant and say that all he needs is the head of a valiant warrior. Hand in the Head of Glohnor to Gerot and he will reward you with the Head of the Valiant.<br>
<br>
Return these three items along with the corrupted ghoulbane to Lhranc. He will turn into a living version of himself wielding the wicked blade and attack you. Killing him will allow you to loot the weapon, Innoruuk's Curse.<br>
<br>
