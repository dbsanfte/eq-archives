<img src="images/enchanter_sig.gif" width="71" height="225" border="0" align="left" alt=""><b>Enchanter Epic Quest</b><br>
There is a Sarnak Imitator who can be found in the Burning Woods [-1200 -4000]. Ask him "are you Jeb Lumsed?" to make sure you have the correct one. Hand him the Copy of Notes and he will tell you that you are now ready to begin your long journey. He will hand you Jeb's Seal and an Enchanter's Bag.<br>
<br>
To obtain the Copy of Notes you will have to head over to Erudin and talk with Stofo Olan who can be found on the second floor of the Vasty Deep Inn [-1105, -63]. He will tell you that he is missing certain materials to finish his notes. He requires a Mechanical Pen, Ink of the Dark, and White Paper. Head over to Qeynos and talk to Reania Jukle who can be found in the Enchanter guild in the Temple under Qeynos [2028, -579]. He will tell you that the ink is the blood of a dark scribe, and will then hand you an Empty Ink Vial. Now head off to Lower Guk and locate the Ghoul Scribe. Charm him and then hand him the Empty Ink Vial. He will hand you back the Ink of the Dark. To obtain the Mechanical Pen you will need to talk to Rilgor Plegnog who can be found in the Mines of Malfunction within Ak'Anon. He will tell you that he hates his Robe and would like a nice purple one. Hand Rilgor Plegnog a Shiny Metallic Robe and he will reward you with the Mechanical Pen. To obtain the White Paper you will need to head over to West Karana and talk to Chrislin Baker [1040, -1993]. She will tell you that she was robbed and is out of writing materials. She wants you to get her some Paper and a Quill, both of which can be bought in the caster guilds in most towns. Hand her both items and she will hand you back a Bandit Sash. She will tell you that Tracken Griften stole her white paper. Now head over and kill Thracken Griften and hand him the bandit sash [880, -11570]. This will cause him to attack you, kill him and loot his corpse to obtain the White Paper. Hand Stofo Olan all three items and you will receive the Copy of Notes.<br>
<br>
You will now head over and talk to Modani Qu'Loni who can be found in the OverThere [540, -935, -520]. Hand him Jeb's Seal and he will give you the Test of Illusion. He will need certain components to be able to craft the first piece of the staff. He will then hand you an Enchanter's Bag and tell you to obtain the following items, the Xolion Rod, Innoruuk's Word, the Chalice of Kings, and Snow Blossoms. To obtain the Chalice of Kings had over to the Magician's Guild in Felwithe and talk to Joren Nobleheart. He will tell you that the only living hier of Overking Bathezid must die. Head over to Chardok and kill Prince Selrach Di'Zok. Return the head to Joren and you will be rewarded with the Chalice of Kings. To obtain the Xolion Rod, head over to the shaman guild in Cabilis, and kill Vessel Droxlin [590, -825]. For the Snow Blossoms, head over to Ogguk and talk to Bozlum Blossom [545, 1145]. There will be Sandals on the ground near Bozlum, pick them up and hand them to him. He will reward you with a note and he tells you to hand it to Brokk. Run over and hand the note to Brokk and he will in turn hand you a Bouquet of Flowers. Run back and give Bozlum the flowers and she will hand you a Snow Blossom. The last item, Innoruuk's Word is dropped by Verina Tomb, who can be located inside the Third Gate [412, -810], in Neriak. Put all four items in the Enchanter's Bag and hit combine, it will turn into a Sack for Modani. Hand Modani this sack and he will reward you with the First Piece of Staff.<br>
<br>
For the next piece of this quest you must find Mizzle and solve the Test of Enlightenment. Mizzle Gepple lives in the mines of Malfunction deep within Ak'Anon [2025, -525, -180] and is in the appearance of a Clockwork VIIX. He will also ask you to collect four items and return them to him, the Spoon, the One Key, the Lost Scroll and the Book of Charm and Sacrifice. He will again hand you an Enchanter's Bag to combine the items into it. To obtain the Spoon, head over to the Oasis and kill Cazel, a named Sand Giant. For the One Key, it is found on a Mesa in the OverThere [-187, -377]. The Lost Scroll is found in Dalnir's Crypt near the exit in a room with a bookshelf. If you wait long enough, a Kly Cohort will enter the room and say "Where is that scroll..." It will then appear on the ground between the wall and the bed. To obtain the Book of Charm and Sacrifice it can be found on the table near the Efreeti Spawn in the Plane of Air. Combine all four items in the Enchanter's Sack and it will create a new item, the Sack for Mizzle. Hand this to Mizzle Gepple and he will reward you with the Second Piece of Staff.<br>
<br>
The third part of this quest begins in Firiona Vie. Talk to Nadia Starfeast and she will give you the Test of Charm [-3870, 2525]. She will tell you that you must obtain the following souls for her. Each will be trapped in a special gem, asking fer about each gem will result in her handing you Dull gems for which to capture the creatures essence. Obtain the Dull Diamond, Dull Sapphire, Dull Ruby, Dull Emerald, and the Enchanter's Bag. To obtain the Diamond, you will need to charm a Spectral Librarian [85, -415] who lives in Kaesora. Once the creature is charmed, hand it the Dull Diamond and you will receive an Enchanted Diamond. It will then turn into an Enraged Spectral Librarian, you do not need to kill this beast. To obtain the Enchanted Sapphire, charm Faelia Goldenwing who can be found in Skyfire [1550, -1000] and hand her the Dull Sapphire. Next head off to the City of Mist and charm the Wraith of Jaxion, who spawns in the last building (the same one as Neh'Ashiir). Hand him the Dull Ruby and you will receive the Enchanted Ruby. For the Enchanted Emerald, charm Impaler Tzilug who can be found in the OverThere [-91, 814, -500] and hand him the Dull Emerald. Once you have all four Enchanted Gems, put it in the Enchanter's Sack and hit combine, it will change into the Sack for Nadia. Return to Firiona Vie and hand her the sack and you will be rewarded with the Third Piece of Staff.<br>
<br>
The next part you must complete, is the Test of Phantasm. Head over to the Hole and hand Polzin Mrid Jeb's Seal. He will hand it back to you and give you the test. He will tell you that you will need to obtain four items for him, the Head of a Serpent, Essence of a Ghost, Essnce of a Vampire and the Sand of the Mystics. He will hand you another Enchanter's Sack. To obtain the Head of a Serpent, kill the Shissar Spirit who can be found in the Plane of Fear. It will only spawn once Cazic Thule has been killed. To obtain the Essence of a Ghost, head over to the Hole and kill the Ghost of Kindle. The Essence of Vampire is dropped by both Male and Female Revenants who live in the Plane of Hate. The sands of the Mystic are dropped by Tangrin who spawns in the Field of Bone [3500, 2800]. Combine all four items into the Enchanter's Bag to obtain the Sack for Polzin. Hand this to him and you will receive the Fouth Piece of Staff.<br>
<br>
Now combine all four pieces in the Enchanter's Sack you received from Jeb Lumsed and will will receive the Bundle of Staves. Head back to the Burning Woods and hand Jeb the Bundle of Staves and he will reward you with the Staff of the Serpent.<br>
