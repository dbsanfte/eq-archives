			<img src="images/cleric_sig.gif" width="71" height="225" border="0" align="left" alt=""><b>Cleric Epic Quest</b><br>
			<br>
			Head off to Lake Rathe, and talk to Schmendrick Lavawalker [3600, 0]. He spawns near the cabin where the Boots of the Reliant quest npc is. He will tell you to get him Lord Burgurgle?s Crown. Now get a small group and head into the underwater tower below the Erudite Isle, in Lake Rathe. Enter the Deepwater Goblin Tower [2800, 150] and head to the top. Lord Burgurgle spawns on the top floor, in the middle and has a placeholder. Kill him to loot Lord Burgurgle?s Crown. Head back and hand Schmendrick the King Burgurgle?s Crown, and you will receive the Oil of Fennin Ro. Natasha Whitewater will then spawn nearby and she will proceed to kill Schmendrick. Once Schmendrick dies a Spirit of Flame will spawn in his place. Kill the Spirit of Flame and you will receive the Damaged Goblin Crown. Hand this crown to Natasha and you will be rewarded with the Ornate Seashell.<br>
			<br>
			Now head off to Timorous Deep and talk to Omat Vastsea [-12000,-200]. Hand him the Ornate Seashell, and he will give you instructions on killing the Plasmatic Priest, which spawns in the Temple of Solusek Ro. He will then hand you the Coral Statue of Tarew. Now head off to the Temple of Solusek Ro and locate the room with the throne [326,38]. At 9am a Seeker will spawn on the left. Say the following line to make sure it is the correct spawn, "what plasmatic priest". Hand him the Coral Statue of Tarew, this will cause the Plasmatic Priest to spawn in the seekers place. Kill him and you can loot the Bloodstained Plasmatic Priest Robe. He uses Tremor and AoE spells so make sure you pull him to zone, otherwise the other npc?s will kill him. Next head over to Solusek?s Eye to hunt down and kill Lord Gimblox who spawns near the King room [-800, -366]. His placeholder is a Large Fire Goblin. Once you kill the Lord, you can then loot the Lord Gimblox?s Signet Ring. Head back to Omat and hand him the Bloodstained Plasmatic Priest Robe. He will reward you with the Orb of Frozen Water and once again Natasha Whitewater spawn. Hand her Lord Giblok?s Signet Ring and she will give you more quest details and will then hand you another Ornate Seashell (which when identified is shown as Ornate Seashell II).<br>
			<br>
			Now we head off to the Burning Woods to talk to Naxton Deepwater. Hand him the second Ornate Seashell and he will in turn hand you back the Message to Natasha. This will also cause Ixiblat Fer to spawn. He will tell you to kill the Ixiblat Fer and to obtain his Scepter. He is a God type mob and will require many groups to take him down. Charming some or the wandering wurms and using Fire Elementals works well, as well as pulling him to the Skyfire zone to fight him is suggested. Kill this elemental and loot the Scepter of Ixblat Fer. Next we head off to Chardok and search out Overking Bathezed. He is the boss of this dungeon and uses AoE spells which will aggro mobs in rooms above him causing many trains. Make sure the room above is empty before attacking him. Kill him to obtain the Singed Scroll. Now head back to Omat Vastsea and hand him the Scepter of Ixiblat Fer and the Singed Scroll. He will reward you with the Orb of Clear Water, and will ask you to head over to Lord Nagafen?s Lair to kill Zordak Ragefire and return with his heart. Natasha Deepwater will spawn once again and if you hand her the Message to Natasha she will give you a Shimmering Pearl which she tells you to hand to Zordak Ragefire to block some of his powers.<br>
			<br>
			Head off to Lord Nagafen's Lair and locate Zardok, who can be found in Nag's Lair. Not much is certain on how to make Zordak spawn, but make sure that all Fire Giants are dead as well as Lord Nagafen, and he'll pop anywhere from 1hr to 72hrs afterwards. Once you locate Zordak hand him the Shimmering Pearl. This will cause him to turn into a level 55 cleric and attack you, so kill him and he will respawn once again as, Zordakalicus Ragefire, a red dragon. Kill this beast and you will be able to loot the Heart of Zordak Ragefire. Bring this heart back to Omat and he will reward you with the Orb of Vapor. This will cause Jhassad Oceanson to spawn on the beach. Run over and hand him all three orbs that you have received so far, Orb of Frozen Water, Orb of Clear Water, and the Orb of Vapor and he will hand you back the Orb of the Triumvirate. An Avatar of Water will spawn nearby, and wanders around on the beach. Wait for the Avatar of Water to stop and then hand it the Orb of the Triumvirate and you will be rewarded with the Water Sprinkler of Nem Ankh.<br>
