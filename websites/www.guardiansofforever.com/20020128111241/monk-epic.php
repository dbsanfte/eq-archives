<img src="images/monk_sig.gif" width="71" height="225" border="0" align="left" alt=""><b>Monk Epic Quest</b><br>
Talk to Tomekeeper Dahl who can be found on the second floor in the Erudin Library. Ask him "what rare finds" and he will tell you that he pays well for very rare books and tomes. The book he wants is the Book of the Immortals, which drops off various mobs in SkyFire including the following, Soul Devourer, Lava Walker, Black Scar, Wandering Wurm, Shadow Drake and Gaurdian of Felia. Once you hand Dahl the book, he will talk some more and reward you with the Danl's Reference. Ask him "who is lheao" and he will tell you that he was the head tomekeeper but has now retired.<br>
<br>
Lheao has retired in the secluded island in Timorous Deep [-11500, -2000]. There is a rock wall surrounding this island and the only way in is through an underground tunnel [-11350, -3650]. Once you locate Lheao, tell him "I have found the book immortals" and he will ask you if you could guard a precious item for him. Tell him "I will take this responsibility. He will ask you to prove yourself, so hand him your Robe of the Whistling Fists and the Danl's Reference. He will in return reward you with the Celestial Fists Book.<br>
<br>
Now we head off to Trakanons Teeth and talk to Kaiaren who can be found in the ruins of the town, above Old Sebilis [-1600, -2675]. Ask him what Celestial Fists and he will laugh at you. Hand Kaiaren the book and he will attack you, you can either kill him or feign. Either way, now you must locate the true form of Kaiaren, which spawns near the lake at [300, 2400]. Hand the book to this Kaiaren and he will ask you who gave it to you. Say "I got it from Lheao". The book is handed back to you, but has been slightly altered at this point. He will then tell you a bit about the Celestial Fists and that they must be stopped. He says that they are masters with their elements but the Fist of Fire is the weakest of them all, and he suggests you slay him first.<br>
<br>
Off to Lavastorm now and locate a Fire Spirit, which look like a regular Imp. Tell the Fire Spirit, "I challenge Eejag". He spawns in the large lava pit near the Solusek zones. He spawns in the middle of the pit and cannot be pulled out, so you must fight in the lava. Once he has been slain, you can loot his corpse to obtain the Charred Scale. Now run back to Kaiaren and hand him the scale. He will tell you that your next target is the Fist of Air and to hand him this scale when you see him.<br>
<br>
Now we head off to the Plane of Air and on the Noble Island there is a new spawn, A Presence. Hand it the scale and it will tell you that since you have defeated his younger brother, he accepts your challenge. Tell him "I am prepared". At this point the Presence despawns and Gwan spawns, a level 57 monk. He is very MR, with only the Lure spells seeming to effect him. Kill him and loot the Breath of Gwan from his corpse.<br>
<br>
In the Mine's of Nurga, in the Frontier Mountains there is a mob called "A Sleeping Ogre". Charm him or sneak up behind him and hand him the Breath of Gwan. This will cause Trunt to spawn, a level 59 Ogre who again is MR, except to Lure spells. Kill him to loot Trunt's Head.<br>
<br>
Next we must head off to Lake RatheTear and locate Deep, who can be found near the Underwater Goblin Tower. Hand her Trunt's Head and she will tell you to meet her master in the OverThere. She despawns and you receive the head back. Head to the OverThere now and locate the Astral Projection who can be found at the bottom of the scorpion chasm, near Howling Stones [775, 850]. Hand him Trunt's Head and he will ask you if you were the one who has been killing his students. Respond with "yes" and he will then ask you who you are. Tell him "I am the one". He will now hand you the Eye of Kaiaren and tell you that if you truly wish to fight him, then he will be in the Lake of Ill Omen.<br>
<br>
Now head off to the Lake of Ill Omen and locate the platform in the middle of the lake. You need levitate to reach the top of the platform, and once there you will see the Astral Projection. Handing it the Eye of Kaiaren will cause Vorash and Deep to spawn and they both attack. Once you kill Vorash, he will scream and respawn as Xenovorash. Kill this beast and you will be able to loot the Demon Fangs.<br>
<br>
Now head back to Kaiaren and hand him both the Book and the Demon Fangs and he will reward you with the Celestial Fists. The effect of Celestial Tranquility adds +40 STR, +12 AC and it adds 40% Haste.<br>
