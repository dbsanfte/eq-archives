<img src="images/rogue_sig.gif" width="71" height="225" border="0" align="left" alt=""><b>Rogue Epic Quest</b><br>
<br>
First talk to Malka Rale who can be located in the Qeynos Aqueducts. She will hand you a pouch which you need to give to Anson McBale who can be found in the Smuggler's Camp in Highpass Hold. This should make Stanos spawn, if not you may also have to talk to Lendel Deeppockets in Rivervale. Talk to Stanos, he will ask you to give him two pieces of parchment, both of which must be pickpocket'd off NPC's. The first is Founy, the dwarf rogue guildmaster who is near the bank in Kaladim, and the other is Tani N'Mak, the dark elf rogue guildmaster who can be found in Neriak's 3rd Gate. Once you have both pieces, hand them both to Stanos. He will in turn hand you back a Combined Note and will tell you to give this to Eldreth, in Lake RatheTear.<br>
<br>
Head off to Lake RatheTear and locate Eldrath. Hand him the combined note and and he will ask you for some milk and 100 pp. Now hand him two bottles of milk and 100 pp and he will reward you with yet a Scribled Note which he asks you to take to Yendar Starpyre in Steamfont.<br>
<br>
Once in Steamfont, you will need to locate Yendar Starpyre as he wanders around the zone. Hand him the combined note and he will in turn give you back the Completed Note and will ask you to return to him the Book of Souls. The book is spawns in the Maestro of Rancor's room in the Plane of Hate. Once you have obtained this item, hand both the book and the completed note back to Yendar. This will cause Renux to spawn, she is very powerful and you will need many friends to bring her down. Kill Renux so you can loot the Translated Note and some bonus items including new pieces of ravenscale armor, and sometimes the Jagged Diamond Dagger. Now head off to Highpass again and hand the Translated Note to Stanos and he in turn will reward you with the Box.<br>
<br>
Next you head off to Kithikor Forest and hand the Dark Elf Advisor the Box you received from Yendar. Once you hand in the box The General will spawn and will take many high level groups to kill him. Once he's dead, loot his corpse to receive the General's Pouch and sometimes the Cazic Quill.<br>
<br>
Hand all three items back to Stanos and he will reward you with the Ragebringer.<br>
<br>
---------------------------------------------<br>
<br>
This next part is only needed if you didn't receive all the quest items above, or are encountering difficulties.<br>
<br>
Talk to Vilnius the Small who can be found in West Karanas. Getting him to spawn is tricky, first you must make sure that all bandits are in the bandit camp. From there keep killing the bandit that runs from the camp to the road, until a Brigand spawns. Then leave him alone until 11pm game time. Vilnius the Small will spawn in his place. Talking to him will reveal that he has three quests for you to complete.<br>
<br>
First he asks you to return to him with the following four weapons, the Painbringer, Fleshripper, Mithral Two-Handed Sword, and the Giant Zwiehander. Hand him all four weapons and you will be rewarded with the Jagged Diamond Dagger.<br>
<br>
He will then ask you to fetch him four robes, which are the Robe of the Kedge, Robe of the Ishva, Shiny Metalic Robe, and Robe of the Oracle. Handing him all four robes will result in receiving the Cazic Quill.<br>
<br>
For the last quest, he asks you to hand him the following items, the Burning Rapier, Eyerazia, Martune Rapier, and a Well Balanced Rapier. He will tell you information on how to meet Stanos and will hand you the General's Pouch.<br>
<br>
Note: This weapon can fit in either the primary or second hand. It no longer can be equipped in the Range Slot. The effect of Seething Fury adds 40% Haste, +40 Attack and +9 Armor Class.<br>
<br>
