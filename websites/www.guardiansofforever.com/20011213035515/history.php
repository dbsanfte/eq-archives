			<font face="Verdana,Arial,tahoma" size="-1" color="#ffffff"><br>
			
<font size="4"><b>W</b></font>hen compared to the ancient and mysterious history of Saryrn and its inhabitants, the lifespan of the Guardians of Forever has been relatively brief.  Claiming ancestry dating back over three generations, the GOF shares a common history and heritage with several other guilds.  While many of the Old Ways and traditions are still practiced, the Guardians have sought to embrace Enlightened thought and new ideas.<br>
<br>
 
<b>I.  EXODUS</b><br>
<br>
<font size="4"><b>L</b></font>egend tells us of Khurlee Pathfinder and his loyal companions.<br>
<bR>

Before the existence of GOF, Khurlee and his closest friends had been officers and members of another, older guild.  Though circumstances remain clouded and hidden to all but a few, records indicate this guild was undergoing a period of internal strife and conflict.  On a cold, winter morning Khurlee dispatched messengers across Norrath to all of his companions.  The young ranger spoke to his friends of a new vision, a new dream.  Of a desire to have leadership and focus.  And so he told them the time to leave had come, and that they should gather in secret.<br>
<bR>

After much discussion and debate, the majority of Khurlees friends agreed to seek out a new home where they could prosper and grow in peace.  Leading a swift, but bitter secession, Khurlee and his allies abandoned their former ties and departed their guild.  And changed their fate forever.
<br>
<br>
<b>II.  GENESIS</b><br>

 
<font size="4"><b>F</b></font>rom all around Norrath, Khurlee and his companions began the long journey across the Timorous Deep, and into the Land of Kunark.   Making camp on the eastern side of the OverThere City walls, the refugees silently gathered their numbers.  After petitioning the gods for assistance, they began a long period of waiting.  They gathered food and water from the local wildlife or by magical means, and occasionally arranged for supplies to be shipped from nearby Freeport and the Oasis of Marr. <br>
<bR>

After one day and two nights, the companions were greeted by their patron saint, Krynnia.  Her presence filled them with hope and joy, and they humbly asked for her blessing of unity.  Krynnia smiled upon the weary travelers, and using powerful and ancient magiks, united and bound the companions together as One.  From that moment on, Khurlee and his friends had a new hope.  A new vision.  And a new home together.  And with these things came a name…<br>

<bR>
In the year 2001, on the 21st of January at 6PM EST, the first Guardians of Forever roamed the earth.<br>

<br>
<br>
<b>III.  EARLY TIMES</b><br>


<font size="4"><b>E</b></font>arly GOF history was marked by a period of rapid growth and expansion.  Khurlee and the Guardians had precious little time, and took measures to quickly secure their guild.  Officers were appointed, duties were assigned, and tasks swiftly performed.  During the following weeks, guild membership increased three-fold as they recruited old friends and companions from around Norrath.  Refugees from other guilds and conflicts were welcomed with open arms.  Within a month, the guild stabilized and began the long process of rebuilding their home.  
<br>
<br>
<b>IV.  TRIALS and TRIBULATIONS</b><br>


<font size="4"><b>A</b></font>s time and work progressed, the Guardians of Forever experienced a number of growing pains.  Creating a working system of government, law, and organization proved to be a hard task.  The original system consisted of Khurlee as the guild leader, and one officer representing each class.  Key decisions and changes were made jointly, while the
day-to-day guild business was kept at the officer and general member level.  As the need arose, new officers were created to fill special positions such as secretary, treasurer, recruitment, and web organization.<br>
<bR>

With the large population, new leadership, and constant demands for change and progress, it's not surprising that GOF was challenged almost daily.  While problems were usually solved in theory quite quickly, implementing and enforcing the changes and rules proved to be another matter.   The guild leader and his officers held several meetings to adjust the guild's management and address those issues.  Over time, the guild became more and more of a business.  And like any business, the need for profit arose.
<br>
<br>
<b>V.  THE SUNDERING OF GOF</b><br>

<font size="4"><b>A</b></font>s time passed on, the Guardians were split into two groups.  While most had remained relatively equal in strength and ability, a select few had grown beyond their peers.  These few felt the lure and temptation of the older guilds, and began voicing that GOF could no longer support their needs.  Khurlee addressed this issue by proposing an alliance with a neighboring guild, but his efforts were in vain, and far too late.  One by one, the older GOFers began leaving to choose their own path.  This initially small trend grew into a mass exodus, reminiscent of GOF's own creation.  While the majority of GOF survived intact, the sundering was a significant strike and setback, and is still felt to this day.<br>
<bR>

Desperate to hold together those who remain, Khurlee proposed his second plan.  With a permanent alliance no longer a viable option, he hoped to preserve the remainder of GOF by merging with the Allegiance of Hope, which held strong ties to the Guardians.  There was much debate and conflict within GOF, and after a week of stalemate, the merger was called off.  The proud members of GOF would not surrender their independence and name to anyone, friend or no.  The guild would remain the Guardians of Forever, forever.
<br>
<br>
<b>VI.  PRESENT</b><br>
<font size="4"><b>T</b></font>oday, the Guardians of Forever has a population of over 50 players. <br>
<bR>
The guild has a pleasant and cheerful spirit, which is evident in the guild-chat and amusing antics.  Raids are a popular weekend event, but trade shows, Bazaars, tournaments, and other gatherings are not uncommon.  Guild membership is approved on a case-by-case basis, and fluctuates with the times and needs of the whole.
<br>
<br>
<font size="4"><b>M</b></font>ay you find Peace here, and rejoice in the company of friends.<br>
<b>-Khurlee Pathfinder</b>

