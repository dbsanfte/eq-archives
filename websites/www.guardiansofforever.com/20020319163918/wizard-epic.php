<img src="images/wizard_sig.gif" width="71" height="225" border="0" align="left" alt=""><b>Wizard Epic Quest</b><br>
<br>
This quest begins by talking to Solomen, a human wizard who can be located on the third floor near the two undead knights, in the Temple of Solusek Ro. Talk to him and he will tell you about the history of wizardry and how power corrupts the soul. He will then tell you he is very busy but has a letter that needs to be delivered to Camin Rah, and will ask if you can do this task. Tell him "I agree" and you will receive the letter.<br>
<br>
Camin can be found on the second floor of the Vasty Deep Inn, in Erudin. Hand him the letter and he will say that he is glad to hear that Solomen is doing well. Tell him "what are you searching for?" He will ask you if you can finance his studies. Give Camin 1000pp and he will tell you more about the history of wizardry and how Solusek Ro gave great power to four of his followers. He will say that one of these followers still exists by the name of Arantir Karondor. Now head off to Halas and locate Dargon, a merchant who can be found in the Shaman's guild. You will need to buy a potion from her called Ro's Breath. Return to Camin with this potion and hand it to him. He will tell you that this potion was created by Arantir, and then hand it back to you [The potion will no longer have a charge of Shield of Lava]. Return to Halas and hand Dargon the potion, and she will turn into Arantir Karondor. Hail him and he will hand you a Arantir's Ring, saying to seek out a woman named Challice and hand the ring to her.<br>
<br>
Challice can be found in the Paladin guild within Felwithe. Hand her the ring and she will tell you a bit of history between her and Arantir. She will then return another ring to you and tell you to give that back to him. Off to Halas once more and hand it to Dargon/Arantir. He will then tell you his story about the followers of Solusek Ro and how they were all stripped of their powers. Ask him about each follower and he will tell you their history [Tell him "who is the gnome" for the last follower, and you will receive a letter]. He will then tell you to bring him back the three staves from each of the followers and he'll combine them with his own to make a great item.<br>
<br>
The first staff, Blue Crystal Staff, is obtained by killing Phinigel Autropos who can be found deep within Kedge Keep. The second staff, Gnarled Staff, is dropped by the evil necromancer Venril Sathir found in Karnor's Castle. To obtain the last staff you will need to talk to Kandin Firepot who can be found in the Butcherblok Mountains [-1500, 2800]. Hand him the letter and he will ask you to retrieve for him either a Dry Brittle Skin [A], or a Rare Oil [B].<br>
<br>
<ul><li>[A] If you happen to have Cazic Thule's Skin - then hand it to Kandin and he'll tell you to get him one last item, some Mistletoe Powder which drops of Tolapumj who lives in Old Sebilis. He will then hand you a bag to hold, called Kandin's Bag. Run off to Sebilis and collect the mistletoe powder, and then return to Kandin. Hand him ONLY the powder and you'll be rewarded with the Staff of Gabstik. Kandin will then ask for the bag, which you hand to him, and he'll hand you a note for Arantir.
<li>[B] Hail Kandin and then ask him "what oil?" He will tell you about his new golem he crafted that is exploring another plane. He will hand you a spare gear and ask you to locate the golem for him and bring back some oil. The golem can be found in the Plane of Fear [-725, -100] and is called a Broken Golem. Hand him the sprocket and the golem will come to life and attack you, kill it to obtain the Green Oil. Return to Kandin and hand the oil to him, and you will be rewarded with the Staff of Gabstik and a note for Arantir.
</ul>
<br>
Return to Halas and hand Dargon the note, which will cause Arantir to spawn again. Now hand Arantir all three Staves (Blue Crystal, Gnarled and Gabstik) and he will hand you back a pack to return to Solomen. Hand the pack to Solomen and you'll be rewarded with the Staff of the Four.<br>
<br>
