<img src="images/bard_sig.gif" width="71" height="225" border="0" align="left" alt=""><b>Bard Epic Quest</b><br>
<br>
Talk to Baldric Slezaf who can be found in the Dreadlands. He will ask you seek out and return to him once you have obtained the following items, Top of page 24, Bottom of page 24, Page 25 and the Mystical Lute of Forper.<br>
<br>
Head off to West Karana and talk to Konia Swiftfoot [-525, -2435]. She will hand you a Torch which will start the race. Run over to Misty Thicket and hand the torch to Fajio Knejo who can be found near the wall close to the guards. He in turn will hand you another torch. Run over to South Ro now and hand the second torch to Andad Filla, who can be found near the zone to Innothule Swamp and once again you'll receive another torch. Take this third torch and hand it to Misty Tekchita who can be found in Lake Rathe near the zone to the Arena. She will hand you back a ring as your prize. Head back to Konia Swiftfoot, hand her the ring and you'll be rewarded with the top part of page 24<br>
<br>
Talk to Braenar Swiftsong [0, -125] who can be found in South Karana, and he'll hand you an invoice once you ask him "what doll". Head over to Solusek's Eye, hand Marfen the invoice, and he will reward you with the Mechanical Doll. Now run over to the Estate of Unrest and give the doll to Serra who can be found next to the Gnomish Speulunker. Serra will in turn hand you back a note. Now take this note and hand it to Braenar, who will give you another note which he tells you to give Maligar. Maligar can be found in West Karana [1245, -10900] and when you hand him the note he will attack along with his Doppleganger. You will need a good group to kill them both. Loot the corpse to receive the Doppleganger's Head. Return the head to Braenar and he'll reward you with the Mahlins Bongo's. Now take these Bongo's to Konia Swiftfoot in West Karana to get the bottom of Page 24.<br>
<br>
Head over to South Karana now and talk to Kelkin Mekia. She will tell you that she is in need of the following items and will reward you if you can bring them to her. She wants the guts from three named creatures. For the first we will head over to Skyfire, to kill the Chromatic Drake named Edrik the Old. The second creature can be found in the Burning Woods, kill Nezekezena the Wurm and loot its guts. The last creature, Blackwing, a named Onyx Drake can be found in the Rathe Mountains. Kill it to obtain its guts. Hand Kelkin all three guts and you will be rewarded with Page 25.<br>
<br>
Now we must head over to the Ocean of Tears, to the Seafury Cyclops Island and kill Quag Maelstrom. He will drop the Alluring Horn. Take this horn to Vedico who can be found at the docks in Butcherblok. She will hand you a note which she tells you to hand to Forpar. Forpar will in turn hand you back another note and will also ask you to fetch him three items. The first item he wants is the Phynagel's Spine, which is dropped by Phynigel Autropos who resides in Kedge Keep. The second item is the Amygdalen Tendril which is dropped by Amgydalen Warriors who live in the Plane of Fear. The last item , Petrified Werewold Skull, is dropped by the Drolvarg Warlord who can be found in Karnor's Castle. Hand all three items and the note back to Forpar and he will reward you with the Head and Neck of the Lute. He will then give you the second part of his quest.<br>
<br>
He will ask you to fetch him four more items. The first item is the Large Red Dragonscale which is dropped by Lord Nagafen who lives in Lord Nagafen's Lair, and Talendor who wanders around Skyfire. The second item is the Large White Dragonscale which is dropped by Lady Vox who can be found in Permafrost, or by Gorenaire who wanders around the Dreadlands. The last two items he needs are Metal Bits which must be created using the smithing skill. Hand all four items back to Forpar to receive the Body of the Lute.<br>
<br>
Now we must head off to Old Sebilis to kill the great beast, Trakanon. He will drop the Trakanon Gut Strings. Hand the strings, body, neck and head to Forpar and you will be rewarded with the Mystical Lute of Forpar. Now run back to Badric Slezaf and hand him the Top and Bottom of page 24, Page 25 and the Lute and you will be rewarded with the Singing Short Sword. The effect, Dance of the Blade, is an AoE group buff that grants 55% Haste, +30 STR, +30 ATK, and lasts for roughly 2.5 minutes. It will also boost the effect of all instrument based songs by 75%.<br>
<br>
