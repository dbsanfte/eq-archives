<img src="images/shaman_sig.gif" width="71" height="225" border="0" align="left" alt=""><b>Shaman Epic Quest</b><br>
<br>
Head off to Gornit's island [800, -5400] in the Ocean of Tears. Locate Captain Surestout and kill him. A lesser spirit will spawn where he's killed. It will ask you if you killed the Captain, respond with "he threatened my life" and then ask the spirit "who are you?? He will hand you A Tiny Gem and tell you to bring it to another shaman. Head off to North Freeport now and enter the sewers. Locate Bondl Felligan and hand him the tiny gem, he will head out to the nearest bar to buy drinks. If you follow him he will stop and speak to you. Tell him "you can buy booze" and he will tell you to follow him. He will enter the Jade Tiger and on the third floor there will be a Greater Spirit waiting for you. Hail it and say the following if you are a barbarian "I follow justice", an ogre "I follow war", a troll "I follow hate", or an iksar "I follow fear". In the next room another Greater Spirit will spawn, talk to this one and it will tell you everything about the spirits and the way of the shaman. You will then receive an Opaque Gem. Once you have received the gem, say the following line "I will walk the path of the Heyokah". This will cause the Greater Spirit to despawn and will start the next phase of this quest, the Test of Patience.<br>
<br>
Head off the Erud's Crossing now, and locate an Ogre Shaman by the name of Ooglyn [-900, 1600]. Hail her and then hand her the opaque gem. She will tell you to follow her, and will run off into the water. Head to [-1600, 4200] and wait for her. She will wait a bit, then tell you that this is the place, and will leave. Do not follow her, just wait and soon Srafen and Dillon will spawn. Talk to both of them and follow their conversation along. After awhile Froham will spawn and mention something about a magical arrow. He will then wander off, do NOT follow him, and remain with the others. After awhile Abe will spawn, talk to him and then ask him "what Broken Arrow". Wait a bit and he will start to talk again, follow along his conversation and he will then hand you a Broken Arrow. Wait again for a couple of minutes, the Abe will tell you to follow him. Again remain where you are as a Greater Spirit is coming to you. Talk with him and he will tell you that you are now ready to take the Test of Wisdom. He hands you a Small Gem and tells you to locate the Wandering Spirit in West Karana [-500, -5300].<br>
<br>
When you locate the Wandering Spirit (looks like a willowisp) hand it the small gem. It will tell you to hurry to Rathe Mountains and help Tabien the Goodly [6300, 1500] who can be found near the Unkept Preservers. Kill him to receive the Marr's Promise. Next head over to [2900, -1900] and you should see Glaron the Wicked. Kill him to obtain Woe and Envy. Take all three items back to West Karana and hand them to the Wandering Spirit. It will hand you a sparkling gem. It will then hand you the Shield of Falsehood, make sure that you have a free general inventory slot open, otherwise the shield will disappear.<br>
<br>
Head off to the Emerald Jungle and hand the sparkling gem to the Sentinel Spirit which can be found in a small hut like building [1250, 300]. He will then give you the test of Might, which begins in Castle Mistmoore. Head off to the castle and kill the Advisor. This will cause the Black Dire Wolf to spawn. Hail it and he will ask you to join him, to which you respond no. He will then attack you, and you will need a good group to bring him down. Loot the corpse to receive the Black Dire Pelt. Head back to the Emerald Jungle and hand the Sentinel Spirit the pelt. He will fashion the pelt into a pair of Black Fur Boots for you. He will then give you the next part of the quest. He wants to learn what happened to Torsis, and will hand you a booklet. He asks that you search the city and bring him back a completed report.<br>
<br>
Head into the City of Mist and start killing everything you see as all the pieces of this quest drop off random mobs. They are the Merchant's Letter, Priest's Dairy Page, Personal Dairy, Written Announcement, Crier's Scroll, and the Student's Log. Put all six pages into the booklet and hit combine. Return this to the Sentinel Spirit and he will tell you that you must now kill Lord Ghiosk. Enter the City of Mist again and kill this lord. He is tough and you will need a good group to bring him down. Loot the three books off his corpse. Head back into the Emerald Jungle and locate the Spirit Sentinel in the pond [3685, -640]. Hand him the three books and he will tell you to fetch the High Scale, which again, is located in the City of Mist, and hand the Icon to High Scale Kirn. Head into CoM and run over to the temple (on the right side of the zone). Enter the temple, and turn right. Walk down the stairs and open the left door and the bottom of the landing. The High Scale Icon spawns in the corner of this room.<br>
<br>
Head over to the Hole now and locate High Scale Kirn, who can be found standing in the middle of the room at the top of the tower in the Revenant Castle. Hand him the High Scale Icon and he will attack you. Kill him to obtain the Engraved Ring.<br>
<br>
Head back to the City of Mist and hand the Engraved Ring to Neh'Ashiir. He will then attack you, kill him to obtain the Neh'Ashiir's Diary. Head back to the Emerald Jungle and hand the diary to the Sentinel Spirit in the underwater pond. At this point he will tell you to bring proof to Lord Rak'Ashiir. Head into the Plane of Fear and kill the named Golems (Terror, Fright), this will cause an Iksar Broodling to spawn. Kill it to obtain the Tear. Run back to the City of Mist and give Rak'Ashiir the child's tear. As of a recent patch, he will now aggro you after you hand in the tear. Kill him to obtain the Scale. Head back to Emerald Jungle and hand the Spirit Sentinel the scale and you will be rewarded with the Spear of Fate.<br>
<br>
As of a recent patch, you can now right click the item to make the effect proc.<br>
