<img src="images/necromancer_sig.gif" width="71" height="225" border="0" align="left" alt=""><b>Necromancer Epic Quest</b><br>
To begin this quest you will have to head over to Nektulos Forest and locate Venenzi Oberzendi, who is standing near the river [-1000, -700]. She will tell you that she is gathering components for her master, Kazen Fe'cae. She will also mention that if you want to become a great necromancer, then tell Kazen you wish to study the dark arts. Kazen can be found in Lake RatheTear [340, -1600]. Hail Kazen and then say "I wish to study the dark arts". He will tell you that if you can bring him Sir Edwin Motte's head, then you will become his servant.<br>
<br>
Now we travel off to the docks in East Freeport, to locate Sir Edwin Motte. Kill him and return with his head to Kazen, and you will be rewarded with the Symbol of the Apprentice. He will then instruct you to return to Nektulos Forest and talk once again with Venenzi Oberendi. He will tell you to retrieve the reagents from her and hand them to his assistant, Emkel Kabae. Hand Venenzi the Symbol of the Apprentice and she will hand you back the Twisted Symbol of the Apprentice. She will also tell you to bring her the Flowing Black Robes and you will receive some Moss when you do so.<br>
<br>
Off to Lake RatheTear once more and locate Emkel, near the Stone Tower. Hand him the Twisted Symbol of the Apprentice and the Moss, and you'll be rewarded with the Symbol of the Serpent. He will then tell you to that more reagents are needed to be gathered. Head over to the Swamp of No Hope and locate Ssessthrass [3800, 1600] and hand him the Symbol of the Serpent. He will hand you back the Scaled Symbol of the Serpent and tell you the Manisi Herb you are looking for is guarded by a Royal Sarnak who lives in Chardok. Head off to Chardok, to the room where the Royal Herbalist spawns (room that has the secret door to Overking Bathezid's room). Kill the herbalist to loot the Manisi Herb. Now return to Ssessthrass and hand him the Scaled Symbol of the Serpent and the Manisi Herb and you'll be rewarded with the Refined Manisi Herb. Now return to Emkel and hand him the Refined Manisi Herb and you'll receive the Symbol of Testing.<br>
<br>
Hand the Symbol of Testing back to Emkel and three mobs will spawn nearby, one after another, and the last of those three will drop the Symbol of Insanity. Now head off to Timorous Deep and locate Drendico Metalbones [6475, 3825]. Hand him your Symbol of Insanity and he will in turn hand you his Journal. He will tell you to bring him the Eye of Innoruuk, the Blood of Cazic Thule and the Cloak of Spiroc Feathers. The eye of innoruuk is dropped by many of the new mini bosses in the Plane of Hate. The blood of cazic thule is dropped by the new mini bosses in the Plane of Fear. The cloak of spiroc feathers is a quest which is completed in the Plane of Air. Once you have obtained all three items, return to Drendico and hand him the Eye, Blood, Cloak and Journal and you'll be rewarded with the Prepared Reagents Box.<br>
<br>
Head back to Kazen and hand him the Prepared Reagents Box and you'll receive the Tome of Instruction. Now off to the Plane of Air once more and hand the Thunder Spirit Princess 3pp. Clear the 3rd Island (Gorgalosk Island) and this will cause Gkzzallk to spawn. Hand him the Tome of Instruction and you'll be rewarded with the Gkzzallk in a Bottle. Return with this bottle to Kazen and hand it to him, and you'll receive the Scythe of the Shadowed Soul.<br>
