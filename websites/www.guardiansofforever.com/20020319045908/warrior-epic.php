<img src="images/warrior_sig.gif" width="71" height="225" border="0" align="left" alt=""><b>Warrior Epic Quest</b><br>
<br>
Kargak Redblade and Wenden are the two people who handle most aspects of this quest. Talk to them for the begining quest information, they can be found standing near the gates in East Freeport [-231, -563].<br>
<br>
Talk to Wenden and he will tell you that he can restore the Jeweled Dragon Head Hilt but only if you can bring him the following items, Unjeweled Dragon Hilt, Diamond, Jacinth and a Black Sapphire. Head to Lake RatheTear and swim under the aviak tower, you will find the Unjeweled Dragon Hilt on the bottom of the lake there. The three gems will need to be looted from high level mobs, which spawn in the Planes, High Level Dungeons or from the Dinosaur in the Ocean of Tears. Hand Wenden all three items and he'll reward you with the Jeweled Dragon Head Hilt.<br>
<br>
Talking to Wenden again. He will tell you that he needs the following items to restore the Finely Crafted Dragon Head Hilt, Ball of Everliving Golem, Rejesium Ore and the Damaged Dragon Head Hilt. Head off to Timorous Deep and locate the chessboard [-6000, 3000]. At the bottom of the king piece there is a small bag, pick it up and you will receive the Damaged Dragon Head Hilt. Next head off to the Plane of Fear and start killing all the named golems (Fright, Dread, Terror) to obtain the Ball of Everliving Golem. Now head to the Dreadlands, to the giant castle near Karnor's Castle. Kill the Giant Patriarch to obtain the Giant Monocle. Now run over to the Frontier Mountains to the giant fort there. Locate Mentrax Mountainbone and hand him the monocle. He will reward you with the Rejesium Ore. Hand all three items back to Wenden and he will reward you with the Finely Crafted Dragon Head Hilt.<br>
<br>
Talk to Denken Strongpick who can be found on the Aviak Island in the Ocean of Tears [2000, 4000]. He will tell you what he wants for this rusty old blade he found on the ocean floor, Keg of Vox Tail Ale, Block of Perm Frost, Rebreathers (x2). The Keg of Vox Tail Ale can be bought from many different vendors around Norrath, including the Sandfishers in North Ro. A Block of Perma Frost is dropped by Ice Giants who can be found in Permafrost and Everfrost. The Rebreathers are tinkered by Gnomes and can be purchased from players. Turn in the four items and you will receive the Ancient Blade. To obtain the Ancient Sword Blade kill the Queen of Chardok.<br>
<br>
Head back to Freeport and talk to Kargak Redblade and say the following line "I will do you a favor". He will hand you a letter and ask you to take it to an Ogre in Ferrott named Oknoggin [800, 1150]. He can be found near the stone bridge and the totem. Hand him the letter and he will reward you with a Tiny Lute. Head back to Kargek and hand him the Tiny Lute and he will hand you back a Book. Take this book and run over to Tenal Redblade who can be found at the bottom of the cliff near the Highpass zone, in East Karanas. Hand him the book and you'll receive the Frozen War Totem. He will ask you to obtain for him a Frozen Goblin Heart. Head back to Permafrost and start killing Goblins until you you obtain one. Head back to Tenal and hand him the Frozen Goblin Heart and the Frozen War Totem. You will be rewarded with the Totem of Fiery War. Tenal will ask you to obtain two more items for him, the Green Dragon Scale and the Red Dragon Scale. The Green Dragon Scale is dropped by Severlious who can be found in the Emerald Jungle. The Red Dragon Scale is dropped by Nagafen who can be found in Lord Nagafen's Lair or from Talendor who lives in Skyfire. Hand both scales and your totem back to Tenal and he will reward you with the Mark of the Sword. He will then tell you that he needs Maestro's Hand. Head off to the Plane of Hate and kill the Hand of Maestro. Return to Tenal and hand him the Mark and the Hand and you will be rewarded with a Note to Kargek and yet another Quest. He will tell you to obtain the Spiroc Wingblade which drops off the Spiroc Lord in the Plane of Air. Head over to Kargek Redblade and hand him the Spiroc Wingblade and the Note to Kargek and you'll be rewarded with the Red Scabbard.<br>
<br>
Put all four items into the Scabbard, the Ancient Blade, Ancient Sword Blade, Finely Crafted Head Hilt, and the Jeweled Dragon Head Hilt. Hit combine and you will receive the Jagged Blade of War.<br>
<br>
Note: It can be split into two one handed weapons, called the Blade of Tactics and the Blade of Strategy.<br>
