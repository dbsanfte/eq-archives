<img src="images/paladin_sig.gif" width="71" height="225" border="0" align="left" alt=""><b>Paladin Epic Quest</b><br>
<br>
To complete this quest you will need to locate three pieces of armor, and then three items to purify the armor, so that you may be able to release a tormented soul and lay him to rest. The three pieces of armor you will need are the Armor, Sword and Shield. The three items of purification are the Light, Gem and Water.<br>
<br>
Head off to Kaladim and talk to Jark who can be found near the Mines. Tell him "some" and he'll tell you the he's waiting for his dinner. Tell him the "I will get your dinner" and he will tell you to run and find Nella Stonebraids. She is standing right outside the Temple. Tell her about the "dinner" and she will hand you Jark's Dinner. Now run back to Jark and hand him his meal. He will tell you that he just found a very tiny pure gem and he would like to give it to you for showing him kindness. Receive the Gem.<br>
<br>
Now we travel off to Freeport. There is a peasant woman who can be found near the Monk guild in West Freeport. If you approach her and say "what water". She will tell you that she has to bring some water to her brother Joshua. Tell her "I will take it" and she will hand you a Bucket of Water. Bring this bucket to Joshua, who can be found in the Bakery near the Monk guild. He will in turn hand you back the Pure Water.<br>
<br>
The Tainted Darksteel Breastplate is dropped by a Thought Corrupter in the Plane of Hate. The Tainted Sword is dropped by the Keeper of Tombs who lives in the Hole. The Tainted Shield is dropped by Kirak Vil, who can be found in Nektulos Forest.<br>
<br>
Now head off to Erudin and talk to Reklon Gnallen who can be found in the Temple of Quellious. Hand him the Tainted Sword and the Pure Water, and he will bless the items and return the Gleaming Crested Sword to you. Next you will have to travel to Felwithe and talk to Elie the Pure. Hand her the Tainted Shield and she will return to you the Gleaming Crested Shield. The last step is to head back over to Erudin. Talk to Reklon Gnallen again and this time hand him the Tainted Breastplate and the Pure Crystal. He will hand you back the Gleaming Breastplate. Now hand the Gleaming Shield, Gleaming Crested Sword and the Gleaming Breastplate back to Reklon and he will reward you with the Mark of Atonement.<br>
<br>
Now head to the Plane of Fear and talk to Irak Altil and hand him the Fiery Avenger and the Mark of Atonement and you will be rewarded with the Fiery Defender.<br>
