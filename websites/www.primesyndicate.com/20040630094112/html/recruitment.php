<br><p> 
<strong>A bit of PS History:<br>
</strong>
Prime Syndicate remains one of the oldest guilds on the Stromm server. Established May 24th, 2003 
and still going strong. While other guilds have dissolved, ours has stood through the test of time. 
I would like to think our stability and prosperity is due to the careful selection of our members. 
Although no process is 100% perfect, I believe our method has had a part in our continued success. 
<p>
We are also one of the few, if not only raiding guilds on the Stromm server that actually originated 
on Stromm. Many other guilds were already preformed on other servers and moved to Stromm with a large 
handful of members when the server opened. Most of us were in our mid 30s-40s when PS was first formed. 
It was our desire was to create a solid raiding guild with a good foundation. No Bs, No (or little) 
drama, No favoritism, No Attitude, No corruption, No hidden agenda heh. We just wanted to raid, enjoy 
the game's content, progress and have fun. 
<p>
PS provides a very healthy, enjoyable atmosphere compared to many other guilds. Most of us are mature, 
polite and good natured people, who enjoy the game and like to play hard. We try to encourage the same 
from others who consider joining us. 
<p>
Our requirements for consideration are listed below, make sure you read through them. If your
class desirability is listed as 'very low' it means we really do not have room for you at this
time and we would need you to be Elemental Flagged in addition to meeting the below listed
requirements to be able to consider you.  You are welcome to apply anyway, or contact a member 
of the <a href="http://www.primesyndicate.com/html/index.php?name=PNphpBB2&file=viewtopic&t=815" target="_blank">Recruitment 
Team</a> in game. Applications are kept on file and if we cannot take you now, you are welcome 
to check back with us in a couple weeks.
</p>

</blockquote>
