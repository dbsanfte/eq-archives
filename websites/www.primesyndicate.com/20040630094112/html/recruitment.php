<p align="center"><font size="2" color="#330000"><u><br>
<b>Code of Conduct:</b></u></font>
<ul>
<li> All Leadership decisions must be respected. If you have a problem with a call, take it up with 
the person who made the call privately.
<li> Behave professionally and respectfully towards all others at all times. This includes all 
Everquest channels and Everquest boards, including our own. If you’re in a bad mood, just don't log 
in. We have no use for anyone with a chip on his/her shoulder.
<li> We will not tolerate lying, exploiting or stealing. We will not associate with those who do.
<li>Greed will not be tolerated. Think about your upgrades wisely. Don't get into the mindset, "Woot a new toy, Well I'll use it sometimes".
<li> Engaging in open, honest and tactful discussion is ENCOURAGED. Tact is the act of making a point 
that is not made in an offensive way if at all possible. There is a fine line between a tactful 
productive discussion and a non-productive one with zero point. Make sure you know this line. The 
crossing of it won't be tolerated. Be part of the solution, not part of the problem.

<li>Ego's have no place here. We are a team. We work as one period. If you just can't do this, well this isn't the guild for you.
<li> Security. Guild targets, spawn information, and conversations within guild chat and in our 
forums are TOP SECRET and not to be revealed for any reason to anyone not in the guild. 
<li> You are expected to help others when possible. If your out for only yourself is not the guild for you.
<li> You must be Drama free! (Attention / loot whores and Drama Queens should seek therapy, not a tag with PS.) 
</ul>

<p align="center"><font size="2" color="#330000"><u><br>
<b>Requirements for Consideration:</b></u></font>

<ul>
<li><b>Loyalty & Temperment:</b><br> 
We want friendly, honorable, easy going people who wish to be a part of what PS is about. Attitudes, Drama 
Queens, Loot whores, Guild hoppers, and bs is not desired nor is it tolerated. We do not want people who
are just looking to join the first guild that might tag them. You may not be in or applying to any other 
guilds, if you do your application will be rejected. 
<li>Go out of your way to introduce yourself to members, group with as many members as you can and get to 
know them. Introduce yourself to members of your own class, if you have a question about how we do 
something or what you should be doing, you should ask them.
<li>The majority of he Guild must like you. If the response to your application is overwhelmingly negative 
then your application will be rejected.

<li><b>Play Availability:</b> <br>
You must be available to raid at least 4 nights a week (approx. 6pm-11pm) EST. If you do not play
during these hours, you will not be able to participate with the guild. You may be a wonderful
person, but we need members who are highly active and can participate in guild events. (this 
does not include needing to take some time off due to RL circumstances such as medical, family,
vacation, etc.) 
<li>If you feel you will quit because of real life, another game, or if you will burn out for whatever 
reason, don't bother applying, we are looking for long term members.

<li><b>Diligence:</b><br>
You must be willing to fail and try again. Dying to learn new mobs is necessary and gives you a great 
sense of accomplishment when you finally see that mob die.
<li>In order to be a successful guild you have to be the best or at least strive to be, a desire to learn and 
improve is something we look for.

<li><b>General Playing skills:</b><br>
 We want players with good knowledge of their class, and generally a good knowledge of the EverQuest content. 
High end content knowledge is preferred, but if you lack it, it can be made up by fast learning skills and 
good concentration on raids.
<li>We require all applicants to download the map files for all zones. IF you have the 
LoY expansion pack. If not, its ok. You can download the maps <a href="http://maps.eq-toolbox.com" target="_blank">here</a>. 
Please keep them up to date, applicants and members are expected to know how to get from 
point A to point B when raids are called.<br>
<li>Basic common sense is a wonderful thing. PS members are friendly and helpful, but do not expect
to be hand fed. Resourcefulness is a huge plus.
<li>While we encourage having ALL EverQuest expansions enabled, all except LOY and LDON are a must.<br>

<li>You must have Plane of Justice trial flag. Having other Tier 1 - Elemental flags are a plus. 

</ul>
</blockquote> 
