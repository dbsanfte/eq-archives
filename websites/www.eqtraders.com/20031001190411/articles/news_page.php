<p><span class="headers">September 09, 2003 - Lost Dungeons of Norrath</span>
<p>
Today's patch marks the launch of the Lost Dungeons of Norrath expansion. As information becomes available regarding the new tradeskill recipes, my hard-working staff will work on getting the information onto the site.  (I've already trickled a couple alchemy recipes into place, without full item details.)  I've also started <a href="http://mboards.eqtraders.com/upload/showthread.php?s=&threadid=10495">a thread in the database forum</a> for folks to add in their own details on the new stuff.</p>
