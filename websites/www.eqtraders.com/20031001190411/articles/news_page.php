<span class="headers">September 28, 2003 - We're Back!</span>
<p>
We are back from the Fan Faire safe and sound, though my voice is still on vacation ... somewhere. If you find it wandering around lost, please return it to me.  My cats are very unsettled by this stranger that smells like `mommy`, but sounds nothing like her!
<p>I will try to whip up a summary of the tradeskill issues tomorrow or the next day, but my first priority is going to be going over the issues with Maddoc, to see what can/should be addressed right away, what is going to be a while down the road, and what of it is going to fall in the `no can do` category. (Maddoc is the new tradeskill guy at SOE now that Frizznik has been promoted - we'd been keeping it a bit quiet to give him a chance to settle in before he got inundated.)  ;)<p>
Anyway, more in the next day or so. I just wanted to let folks know that I am back, and will be getting an update out soon.</p>
