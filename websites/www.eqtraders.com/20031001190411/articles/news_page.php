<p><span class="headers">August 27, 2003 - Lost and Found</span>
<p>
Rumors that the extremely reclusive DenMother was actually found poking her nose into the lands have been confirmed.  It seems that the wee lass went on a tour of the upcoming <a href="http://everquest.station.sony.com/lostdungeons/">Lost Dungeons of Norrath</a> yesterday, alongside Absor, Illia and others. While I did not get to poke at tradeskills at all during the tour, I did <a href="http://www.eqtraders.com/articles/article_page.php?article=g223">get to see a few interesting things first-hand</a>.<p>
While I did not have the "oomph" to poke at tradeskills, thanks to an ofer from SOE, a few folks from the EQTC tradeskill community will have a chance to help test during the final week of beta next week. I've got PMs out to various folks now, and if I'm short on responses, or more decline due to RL time constraints, I may put something up on the boards this evening (Pacific time) to grab a couple late additions to the list I send in tomorrow. Keep your eyes peeled.</p>
