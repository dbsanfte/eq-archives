<p><span class="headers">September 21, 2003 - Fan Faire time!</span>
<p>
It's that time again! The time has flown and another <a href="http://everquest.station.sony.com/fanfaire/">EQ Fan Faire</a> is upon us.  I'd not said much about it until now, as we'd not been able to finalize our plans.  Hubby's managed to talk his partner into covering at the store, and we're going to board our very sick kitty at the vet's for special care while we're gone. (We're currently having many adventures with trying to feed her through a tube in her throat ... and seem to be learning new ways of interior decorating the house with cat food paste as we do so!) While we're there, I'm hoping to gather folks from the EQTC community together for a group photo. I'll update my first post in <a href="http://mboards.eqtraders.com/upload/showthread.php?s=&threadid=10788">this thread</a> when we have a time and a place set up.</p>
