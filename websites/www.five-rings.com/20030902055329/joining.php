     <td class="row2">
     <b><font size="3">Obtain a sponsor</font></b><font size="3">.</font><font size="2"> 
     This will be someone in the guild who knows you very well, and knows that 
     you will be an asset to Five Rings. Only active members can sponsor and 
     applicant. Your sponsor will be your mentor in that they will keep you 
     updated on raids and answer any questions you have along with the 
     membership officers.<br><br>
     Sponsorship guilding. Once confirmation from your sponsor is received, you 
     will be guilded and your sponsorship period begins. This period will last a 
     minimum of two weeks to a maximum of six weeks. The exact length of time is 
     up to the discretion of the guild. You will be expected to raid with the 
     guild and actively participate in guild events. All applicants are evaluated 
     in the following areas:<br>
     <br>
     </font><font size="3">
     <b>Class Skill</b> </font><font size="2">- How well do you play your class. This is the most important
     criteria. We are not here to teach people how to play their class, and 
     therefore will ONLY accept applicants who are skilled. Members of the same 
     class are expected to spend some time evaluating applicant skills and 
     offering their opinion.<br>
     <br>
     </font><font size="3">
     <b>Raid Skills</b></font><font size="2"> - How well do you control your aggro, follow directions, pay 
     attention, pick up on the raid strategies, contribute to a fast corpse 
     recovery, etc. Some of these skills are learnable, however, by level 62, 
     most people should have learned basic raid conduct and behavior.<br>
     <br>
     </font><font size="3">
     <b>Attendance</b></font><font size="2"> - Over the course of the two week sponsorship period, we will be 
     able to get a good overall impression of attendance and guild participation.<br>
     <br>
     </font><font size="3">
     <b>Motivation, Attitude and Character</b> </font><font size="2">- Are you making an effort to get to know 
     us? Are you staying on top of guild events? Are you helping out when 
     possible? Are you hanging out during non-raid times? Do you handle deaths 
     gracefully? Can you accept that failures do happen and they are good 
     learning experiences? Do you accept constructive criticism well? Etc. <br>
     <br>
     During this time, you will also be getting to know the other members and 
     learning how we all work together. you will earn DKP during this time which 
     can be traded in for items that drop during raids. However, as an applicant, 
     you will only be awarded loot after active members have passed. You will 
     have access to our raid forum to keep on top of where you need to be on a 
     daily basis. If necessary, we may choose to extend the length of an 
     individual's sponsorship period. Regardless of obtaining a sponsor, your 
     sponsorship will not begin until you meet the requirements listed above.<br>
     <br>
     Final evaluation At the end of your sponsorship period, barring any 
     objections from the officers or members, you will be considered a full 
     member with all the privileges associated with it. <br>
