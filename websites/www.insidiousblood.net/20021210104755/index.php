<br>
While oohing and ahhing over the corpse as we are inclined to do (bunch of perverts), who should spawn but our new pal <b><a href="http://eqbeastiary.allakhazam.com/search.shtml?id=7898">Emperor Ssra</a></b>!  So off we go, back to Ssra!  But Wait!  We only have 3 enchanters!  Nevermind, make those bards <b>work</b> for their money!  Emperor down with only 3 deaths and the MT up the entire fight...<br>
<br>
About that time I logged off (as it was 2 AM)...  But my roving reporter Pylfer (who exchanges information for sex, but that's another story) reports that soon after the TL's from the Emporer room, <b><a href="http://eqbeastiary.allakhazam.com/search.shtml?id=7355">XTC</a></b> spawned and everyone hauled ass yet <b>again</b> to Ssra and punked him as well!<br>
<br>
Great night iB!</div></p>
