<p><div align="left">What a night!  What started as a nice, relaxing, <b><a href="http://eqbeastiary.allakhazam.com/search.shtml?id=5335">Tunare</a></b> killing night turned into a blood-pumping, heart-racing, kill everything we could get our hands on night...<br>
