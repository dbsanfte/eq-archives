Your computer has been infected by the usual gamut of spyware and virii at this point. Typically once one gets in, it opens all of the doors so that the rest of them don't have any problems getting in as well. 
&lt;br /&gt;

&lt;br /&gt;
Also as usual, one of the spyware apps is broken and causing a race condition, resulting in it eating up 100% of your CPU. That's why things are so slow. To fix that, open task manager and look at the processes. Kill the one running at 90+%. 
&lt;br /&gt;

&lt;br /&gt;
Spybot and AdAware are, unfortunately, no longer up to the task of cleaning that shit off of your computer. They are simply not thorough enough. Rather to my astonishment, the latest beta of Microsoft's AntiSpyware seems to be the best there is for cleaning that shit off of your PC. 
&lt;br /&gt;

&lt;br /&gt;
Most of the spyware crap you are running will only run under your specific user profile. The easiest way to clean out some of it is to create a new user on your PC. Log in as that user (should be an administrator), then download FIREFOX immediately. Use Firefox to go to this link and download Microsoft AniSpyware: &lt;a href=&quot;http://www.microsoft.com/athome/security/spyware/software/default.mspx&quot; target=&quot;_blank&quot;&gt;http://www.microsoft.com/athome/security/spyware/software/default.mspx&lt;/a&gt; 
&lt;br /&gt;

&lt;br /&gt;
Bear in mind that a lot of spyware is USER SPECIFIC. If you have 5 accounts, you need to log in to all of them and do purging. Otherwise one infected one can easily reinfect the other four clean ones as soon as that user logs in. 
&lt;br /&gt;

&lt;br /&gt;
Install Microsoft AniSpyware and run it. It should purge a ton of crap from your PC. Reboot and log in as your normal user again. It'll literally blast you with popups every time a virus or spyware app tries to re-hijack your browser or mess with the system. DENY all of those programs access via the popups. Run AntiSpyware again under your own user account and let it purge what it finds there as well. Your system should have some semblance of sanity afterwards. 
&lt;br /&gt;

&lt;br /&gt;
There are some spyware apps that will kill Spybot, Adaware, and AntiSpyware dead. If this keeps happening, start killing any processes in your task manager that are running under YOUR user name (not as system). If at any point windows says you have killed something critical and starts up a shutdown timer, do start - run and type in &quot;shutdown -a&quot; to abort the timer. When you kill things, don't use &quot;end process&quot;, use &quot;end process tree&quot; which prevents the program from restarting itself after you kill it. 
&lt;br /&gt;

&lt;br /&gt;
Once you are purged, do a thorough virus scan on your system and make sure you have all your updates installed. Also, NEVER USE INTERNET EXPLORER AGAIN. EVER. It simply *cannot* be made safe. When IE7 comes out perhaps it'll be worth a look again, but until then stick to Firefox or whatever other non-IE browser you prefer. 
&lt;br /&gt;

&lt;br /&gt;
Leave Microsfot AntiSpyware running (it sits in your try and does prevention). Also let it auto-update and run its scans at times when you won't be using the computer. 
&lt;br /&gt;

&lt;br /&gt;
That should get you back up and running. It is entirely possible that your computer could be too infected to fix. If that happens, backup your files and do the three R's: reboot, reformat, reinstall. Afterwards, do updates and load antispyware, and keep your firewall turned on at all times.&lt;/span&gt;&lt;br /&gt;
