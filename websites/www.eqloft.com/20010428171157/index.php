                arriveront sous peu :<br>
                <br>
                - Le<b> syst&egrave;me de loo</b>t des Warrens devrait &ecirc;tre 
                encore modifi&eacute; pour prendre en compte le twinkage par sorts.<br>
                <br>
                - Une <b>nouvelle zone</b> devrait &ecirc;tre rajout&eacute;e 
                cet &eacute;t&eacute;, toujours sur Odus, les Stonebrunt Mountains 
                ! Il s'agit d'une zone assez proche dans le principe de Lake of 
                Ill Omen.<br>
                <br>
                - Les <b>donjons </b>de l'ancien monde devraient bient&ocirc;t 
                conna&icirc;tre des changements graphiques (en outre).<br>
                <br>
                - Un <b>nouveau serveur PvP</b> sera prochainement cr&eacute;&eacute; 
                avec de toutes nouvelles r&egrave;gles. Il permettra &agrave; 
                Verant de tester ce nouveau syst&egrave;me avant de le g&eacute;n&eacute;raliser 
                aux autres serveurs PvP.<br>
                <br>
                - L'<b>am&eacute;lioration de l'UI</b> se poursuit, dans un premier 
                temps avec des changements l&eacute;gers comme la possibilit&eacute; 
                d'acheter des stacks en une seule fois puis avec des modifications 
                plus lourdes qui concerneront la visibilit&eacute; de l'interface.<br>
                <br>
                - Par ailleurs, le <b>moteur 3D</b> continue &eacute;galement 
                &agrave; &ecirc;tre transform&eacute; pour s'adapter au 32 bits 
                et la commande VideoModeBitsPerPixel=32 du fichier EQClient.ini 
                n'est qu'un premier pas vers le rendu dont le jeu sera dot&eacute; 
                d'ici quelques mois !<br>
                <br>
                - La <b>dynamisation progressive</b> du monde de Norrath commence, 
                d'une part avec la multiplication des qu&ecirc;tes religieuses 
                ou ayant trait &agrave; l'histoire de Norrath, d'autre part via 
                des &eacute;v&eacute;nements dynamiques qui seront notamment relat&eacute;s 
                sur la Harpy's Tavern. Pour information, Lady Daegarmo y travaille 
                actuellement et devrait vous r&eacute;server quelques surprises 
                sous peu !<br>
                <br>
                - La <b>complexification des qu&ecirc;tes de Velious</b> sera 
                intensifi&eacute;e notamment en jouant sur les factions modifi&eacute;es, 
                qu'elles soient g&eacute;n&eacute;riques (coldains, g&eacute;ants, 
                dragons) ou plus subtiles (sous groupes de ces grandes factions).<br>
                <br>
                - Les PNJs vont bient&ocirc;t rendre automatiquement les<b> objets 
                de qu&ecirc;te </b>que les joueurs leur auront donn&eacute;s par 
                erreur... <br>
                <br>
                - Finalement le g&egrave;le de la <b>balance des classes</b> ne 
                se fait pas encore et celles ci conna&icirc;tront des modifications 
                durant les mois &agrave; venir...<br>
                <br>
                - La <b>r&eacute;sistance magique</b> des mobs de Velious, enfin, 
                devrait &ecirc;tre revue &agrave; la baisse pour &eacute;quilibrer 
                un peu plus les casters et les tanks sur le continent glac&eacute;.</p>
