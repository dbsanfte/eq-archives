<td><b>Today's patch is over and from reading the Tech forums I don't see a large number of abnormal questions. The forums there seem to be back to the normal amount of posts, hopefully this is a good sign. There wasn't a patch message to go with todays patch so I am unsure of what exactly was fixed. Hopefully the server stability problem was resolved.<br />
<br />
<br />
In other news, SOE has posted it's future plans for January and February.</b><br />
<br />
<table border="0" align="center" width="90%" cellpadding="3" cellspacing="1"><tr><td><font face="verdana,arial,helvetica" size="1" color="#FFFFFF"><b>quote:</b></font></td></tr><tr><td style="BORDER: #000000 1px solid; FONT-SIZE: 11px; COLOR: #FFFFFF; FONT-FAMILY: Verdana,Arial; BACKGROUND-COLOR: #4D4D73;"><font color="orange"><b>Upcoming for January and February 2004<br />
<br />
<br />
We want to wish everyone a happy holiday season and thank you for continuing to be a part of EverQuest.  We have some exciting times ahead with our 7th expansion in the works, <a href="http://www.gatesofdiscord.com," target="_blank">www.gatesofdiscord.com,</a> and there are many more exciting things to come.  Here are a few of the new features on the EQ team's large and busy schedule.<br />
<br />
 <br />
Melee Enhancement, part 2<br />
<br />
 <br />
Melee classes are currently enjoying the first part of the improved Melee system and the feedback we are receiving has been great.  Early in 2004 we plan to move forward with the rest of the melee enhancement, the reaction abilities.  The enhancement gives Monks, Rogues and Warriors the ability to use special skills in response to a mistake made by their opponent or an exceptional move of their own.  This enhancement will be in addition to the existing system, allowing players to use the skills for more involved game-play and to improve the abilities of their characters.<br />
<br />
 <br />
A more detailed explanation and the transcript of a chat with the EQ Live team about the system can be found on the Developers Corner.<br />
<br />
 <br />
Graveyard Zone<br />
<br />
 <br />
Tired of losing your corpse?  The current EQ system removes corpses from the game after a week to reduce clutter and potential video lag.  But we know there are times when your character dies and you just can't get it right away.  Next thing you know a day turns into a week and BAM!  Your corpse is gone.  Since we can't leave the corpses sitting in game indefinitely, some inventive folks here have come up with another option.<br />
<br />
 <br />
We plan to create a "graveyard" zone that will only work if your corpse has decayed and disappeared.  If you have lost your corpse due to decay in other parts of Norrath, you will be allowed to enter this zone and reclaim it.<br />
<br />
 <br />
Tradeskill Interface Upgrade<br />
<br />
 <br />
Tired of all the clicking and dragging needed just to make a type 1 wood arrow?  We know that the current tradeskill interface is cumbersome, so the UI guys have come up with some new ideas.<br />
<br />
 <br />
Opening a tradeskill container will now open a new user interface.  In the window you will find an area that has all recipes that are currently available to you.  This includes recipes that are at your skill level as well as some above it.  Clicking on a recipe will display what items are necessary for its completion.  Clicking the "combine" button will extract those required items from the player's inventory, and perform the recipe combine, placing the results on the player's cursor.  The new system is nice and neat, with no dragging and dropping unless you want to try a combine that is way out of your league, which uses the old style of combination.<br />
<br />
 <br />
These are just a few of the things slated to come out of the EQ Live laboratory.  Watch for other exciting announcements in the weeks ahead.</td></tr></table></font></b></td>
