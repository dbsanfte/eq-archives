Rules & Requirements
</div>

<div class="content">
<b>1.</b> You may submit as many sites as you want, provided they are <i>yours</i> and you fill out a separate form for each of them.
<BR><BR><BR>
<b>2.</b> Your site must fit into one of the <a href="categories.php">categories</a>.  I know what I want listed here; this directory does not have a Miscellaneous category.
<BR><BR><BR>
<b>3.</b> You must have an English version of your site, and your summary must be in English.  (I'm a stupid American that only knows a little French & Spanish.)
<BR><BR><BR>
<b>4.</b> You must link back to Profane Listings using a text link or a <a href="codes.php">button</a>.  (People submitting to Forums are exempt from this rule.)
<BR><BR><BR>
<b>5.</b> Your site must be hosted on a bannerless web server with no pop-ups.  If you are not hosted and would like to be, check out <a href="http://www.pick-me.net/" target="_blank">Pick-Me.Net</a>.
<BR><BR><BR>
<b>6.</b> Do not submit a redirect address, such as .cjb.net or .tk.  Do not submit a password protected site.  Do not submit Livejournals or Diaryland-like sites unless they are <i>very well done</i>.  Usually this means you must have a paid account.
<BR><BR><BR>
With me so far?  Now, please look at rule #7...
<BR><BR><BR>
<b>7.</b> Your site must be aesthetically pleasing.  You will not be added to the listing if you use sTiCkY cApS, poor grammar, or blinding colors.  Your layouts must be your own, or used with permission.  You must have good graphic knowledge, or not be using graphics at all.
<BR><BR><BR>
<BR><BR><BR>
I reserve the right not to add any sites I deem inappropriate (hah--try me!), hateful, or less than average.

