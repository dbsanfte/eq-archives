Categories
</div>

<div class="content">
<b>Adoptions:</b> Pixel creations that visitors may put on their sites.  (Actors, characters, animals, foods, etc.)
<BR><BR><BR>
<b>Awards:</b> Sites overseeing the nomination, voting, and awarding of sites/people/stories in different categories.
<BR><BR><BR>
<b>Brushes:</b> Sites that provide brush downloads for Adobe Photoshop or other image programs.  Please specify the program & version your brushes work with.
<BR><BR><BR>
<b>Cliques:</b> Themed sites whose members place a button/blinkie/text code up on their site to show membership.  <u>Not</u> fanlistings.
<BR><BR><BR>
<b>Collectives:</b> A list of the owner's sites and/or fanlistings.
<BR><BR><BR>
<b>Domains:</b> <u>All</u> domains (.net, .com., .nu, etc.) go in this category.  In your description, you may want to put a subcategory.  (Personal, collective, etc.)
<BR><BR><BR>
<b>Exchanges:</b> Button/banner exchanges where members place the code on their site to receive hits in exchange.
<BR><BR><BR>
<b>Forums:</b> Message boards.
<BR><BR><BR>
<b>Listings:</b> Directories, such as this one.
<BR><BR><BR>
<b>Personals:</b> Personal sites with content about the owner.  This includes blogs.
<BR><BR><BR>
<b>Portals:</b> Webcam portals with open membership.
<BR><BR><BR>
<b>Portfolios:</b> Art, graphic, or writing showcases.  Please specify in your description what your site contains.
<BR><BR><BR>
<b>Reviews:</b> Sites that let the visitor submit his or her site to be reviewed by the staff.
<BR><BR><BR>
<b>Webrings:</b> Themed sites whose members place a code on their site to be linked with other members.
<BR><BR><BR>
<BR><BR><BR>
If you have a suggestion for a category, please <a href="mailto:jackie@exsanguis.net?subject=Profane Category Suggestion">email</a> me.  I may have overlooked something I would like to have in this directory.  However, please note that I do <i>not</i> want to list fansites, fanlistings, hatelistings, or free graphic sites.  If you can think of a category other than those, take a shot at it.
</div>


