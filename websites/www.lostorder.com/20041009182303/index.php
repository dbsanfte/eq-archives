	<p>SAN FRANCISCO - September 9, 2004 - Ubisoft, one of the world's top game publishers, announced today the Shadowbane: Throne of Oblivion expansion pack will be available at a manufacturer's retail price of $19.99 on November 30. The expansion for the critically acclaimed massively multiplayer online (MMO) game Shadowbane will feature an entirely new realm and rulership system that allows players to seize and vie for control of sections of the game world.<br />
<br />
"Shadowbane: Throne of Oblivion brings guild warfare and city sieging to an exciting new level with a strategic end-game that is unprecedented in the genre of MMO games. 'Build an Empire' was one of the early rallying cries of Shadowbane and with this expansion, our players can do exactly that," said Frank Lucero, Shadowbane's producer. "Shadowbane: Throne of Oblivion brings with it the new Vampire Race, new classes and disciplines, each with special powers and abilities designed to enrich PvP combat. Additionally, graphical improvements and extensive refinements to the existing siege system will enrich play and improve performance."<br />
<br />
In addition to the new strategic systems, Shadowbane: Throne of Oblivion will feature extensive new player options centered around an invasion from the Lands of the Dead. Shadowbane: Throne of Oblivion marks the first time in an MMORPG that players can play Vampire characters. The Vampires' clothing and armor will spotlight their supernatural power and allure and demonstrate the appeal of these creatures while exploring a new visual direction for Shadowbane. Their unique powers, strengths, and weakness will add new layers and tactics to the Shadowbane PvP landscape.<br />
<br />
New classes, including the Nightstalker, Necromancer, and disciplines including Vampire bloodlines and four new siege disciplines will also debut in Shadowbane: Throne of Oblivion. Existing classes and disciplines will be undated to account for the arrival of the undead legions, and new weapons will add new dimensions to the existing power dynamic, forcing players to make new strategic decisions.<br />
<br />
Shadowbane: Throne of Oblivion will also feature graphical enhancements, shown off to their fullest in new adventuring areas with all-new high-level content.<br />
<br />
Shadowbane: Throne of Oblivion is pending rating by the ESRB and more information can be found on the official Shadowbane community Website, <a href="http://chronicle.ubi.com/NR/exeres/00015b14lsnkwbpjfutaqbqo/News+Article.asp?NRMODE=Published&amp;NRORIGINALURL=%2fArticles%2fNews%2ftoo_announce%2ehtm&amp;NRNODEGUID=%7b4C95EA56-FCA7-4E95-87EF-47C82FCBAF03%7d&amp;NRQUERYTERMINATOR=1&amp;cookie%5Ftest=1">The Chronicle of Strife</a></p>
