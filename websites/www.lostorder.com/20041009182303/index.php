	<h4>EverQuest II Q&amp;A</h4>
	<h5>By: <a href="http://www.lostorder.com/vforums/member.php?u=96" title="View user profile">Freejack</a> @ 12:05</h5>
	<br>
	<p>FileFront interviews Steve Danuser, EverQuest II's Community Manager. Besides a rather gross overstatement of EQ's popularity (calling it the biggest MMORPG, er, whatever...) the interview traverses several issues that many gamers have had questions about. Here's a good one:<br />
<blockquote><b>Q: Have some of the more common complaints from EQ1 been resolved? I logged on to my favorite EQ char and asked around, and the biggies seemed to be as follows.<br />
<br />
    * Lag, caused in part by overcrowding.<br />
    * Under-populated zones.<br />
    * Difficulty in camping creatures.</b><br />
<br />
A: We're tried to address many issues that have impacted the previous generation of online games. While lag can be a function of the Internet itself, our goal is to minimize its effect as much as possible. By maintaining two central starting cities, we will avoid the empty feeling newbie areas can sometimes have in a mature game. And rather than forcing long camps for single creatures, we break quests down into more steps that can be completed in shorter periods of time.</blockquote><br />
The full article can be found <a href="http://articles.filefront.com/164">here</a>.</p>
