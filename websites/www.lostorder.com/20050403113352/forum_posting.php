  <h3>Posting Guidelines</h3>
  <p>LOA contains several forums for our members and non-members to communicate 
  in. Normally, each game chapter we have contains three forums (general public, 
  guild only, officer only). If we happen to be involved in a beta test, we 
  normally have a private forum with highly restricted access setup for that 
  as well. In addition to our game forums, we have a high-level officer forum, 
  a high-level member forum, a moderator forum (for discussing technical issues) 
  and several other non-game related forums.</p>
  
  <p>Our most widely used forum is the <strong>"Say What?!?"</strong> forum. Registered
  users who are 13 and older are permitted to read and post in the Say What 
  forum. Topics are VERY diverse and must be kept to a PG-13 rating or lower. 
  We want to avoid sexually explicit material, including all forms of nudity, 
  and we also want to avoid over-the-top profanity. Keep in mind that some of 
  our members are not adults, and their parents should have NO PROBLEM with 
  their kids visiting our forums. We want to keep things safe for everyone, 
  13 and older.</p>
  
  <p>We also have a forum titled, <strong>"The Basement"</strong>. This forum is
  for the discussion of all things that tend to lead to heated debates, such 
  as religion, politics and latest news headlines to name a few. As a member 
  of the LOA, access is automatically granted to this forum at which time, you 
  may request to be removed from it if necessary. People who are NOT members 
  of LOA <a href="mailto:brimstone@lostorder.com?subject=Requesting Access to Basement Forum">may 
  request access to this forum</a>, at which point, Brimstone will consider 
  the request and either grant or deny it. Before anyone posts in this forum, 
  we have strict posting guidelines that are 'stickied'.</p>

  <p>We have also created a mechanism that allows our News Page to be populated 
  via a <strong>News Forum</strong> that we have created. So as a member of LOA, you also
  have full access to our news page where you can share your thoughts, opinions, 
  interesting facts, etc. relating to the game you are in, or you experiences 
  in LOA.</p>
  
  <p>In all of our forums, members and non-members MUST be considerate of other 
  people and <a href="/policies.php">respect the rules and policies</a> that 
  we have posted. <em>If you are an applicant of LOA, it is strongly advised 
  that you put aside any shyness you may have and start getting involved in 
  the conversations. You don't have to remain in your own game chapter forums, 
  you may participate (and SHOULD participate) in ANY of the forums you have 
  access to. This is a great way to gain exposure and earn some votes for your 
  membership.</em></p>
