  <h3>Voting Process and Duration</h3>
  <p>Every member of LOA has the right to vote on any and all new applicants that apply to our 
    guild. Voting is serious business, and we do our best to pay close attention 
    to everyone who applies. If you are interested in joining the Lost Order of 
    Akalabeth, you must first <a href="http://www.lostorder.com/vforums/register.php?do=signup">REGISTER 
    WITH OUR FORUMS</a>. Once you register, you can return to our home page (<a href="http://www.lostorder.com">http://www.lostorder.com</a>) 
    and <i>new</i> links will appear to you, including, "<a href="http://www.lostorder.com/apply.php">Apply 
    Now!</a>".</p>
  <p>Our guild portal, AVATAR, includes a voting application system based entirely 
    upon our own rules that have been in place since 1996. This allows us to collect, 
    monitor, manage and track each application throughout the entire process. 
    Additionally, AVATAR allows our members to review applications and vote accordingly. 
    For this reason, following the process outlined above is important to you 
    and us.</p>
  <h4>Petitioner Phase:</h4>
  <p>Simply put, this is the name given to those who send in an application. Three 
    vouches are required, showing that our members recognize the name of the person 
    and think favorably of him/her.</p>
  	<p class="votedescription"><span class="vouchesneeded">Required vouches for promotion: 3</span></p>
  	<p class="votedescription"><span class="blackballsneeded">Blackballs to reject: 2</span></p>
  	<p class="votedescription"><span class="phaselength">Length of this phase: 14 days</span></p>
  <h4>Initiate Phase:</h4>
  <p>Once a petitioner's application is vouched for, the person then moves on to 
  the Initiate Phase. This is the phase that our members and the applicant should 
  be spending time getting to know each other better in and out of game. That 
  includes grouping, going on raids, participating in discussions, getting involved 
  in our forums and more.</p>
  	<p class="votedescription"><span class="vouchesneeded">Required vouches for promotion: 6</span></p>
  	<p class="votedescription"><span class="blackballsneeded">Blackballs to reject: 3</span></p>
  	<p class="votedescription"><span class="phaselength">Length of this phase: 15 days</span></p>
  <h4>Commoner (Final) Phase:</h4>
  <p>The final phase of the application process is referred to as our "Commoner Phase". 
  At this time, both parties have a really good understanding of each other. The 
  applicant is a part of the family now, and the final vote is usually quite favorable. 
  Rather than "Vouches", we now collect "Votes" from all our members. There are 
  more options when it comes to voting. Each vote has an assigned number of points 
  associated with it. At the end of the voting session, the points must add up 
  to 18.</p>
		<p class="votedescription"><span class="votesneeded">Required positive points: 18</span></p>
  	<p class="votedescription"><span class="blackballsneeded">Negative points to reject: 5</span></p>
  	<p class="votedescription"><span class="phaselength">Length of this phase: 30 days</span></p>
