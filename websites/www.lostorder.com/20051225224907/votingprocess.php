	<h4>Important note regarding <span class="highlight">negative votes</span>:</h4>
  <p>A small number of negative votes are all that is needed to reject an 
	applicant from the process. We feel that despite the number of positive votes earned, 
  the fact that a few of our members have witnessed negative behavior, actions, 
  or personality conflicts is enough for us to err on the side of caution. It 
  is NEVER an easy thing for us to do, and we always feel terrible when it comes 
  to notifying someone that they were voted out of the process. With over 220+ 
  members, personalities will sometimes clash. It doesn't happen often, but it 
  does happen.</p>
