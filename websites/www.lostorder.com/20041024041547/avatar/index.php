	<p>ME WarCry interviews Middle-Earth Online's Art Director, Chris Clay. The article is pretty short and mostly profiles Chris and his work at Turbine. Here's a little clip:<br />
<blockquote><b>Can you explain how the creative process works in terms of where the team even starts? What were some of the first things that the art department worked on?</b><br />
<br />
At the start the leads typically get together to set the initial goals for a portion of the project. Those goals are then handed to a strike team, which is composed of relevant members from each department art, design, engineering, and core. They typically work on fleshing out rough specs, and doing any proof of concept work. Once a proof of concept is approved it then goes into full scale production. At the very beginning of the project we worked with Vivendi on the initial art look of the game to prove that we could truly visualize the world of Middle-earth. We also started work on the initial elf concepts, which were iterated on for over a year and a half before they finally went into the first stages of production.</blockquote><br />
