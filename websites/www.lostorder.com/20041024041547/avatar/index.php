	<h4>Asheron's Call 2 expansion pack in the works</h4>
	<h5>By: <a href="http://www.lostorder.com/vforums/member.php?u=13" title="View user profile">Enosh</a> @ 12:36</h5>
	<br>
	<p>From <a href="http://www.gamespot.com/news/2004/10/18/news_6110794.html">Gamespot News</a><br />
<br />
Turbine Entertainment will publish an expansion pack for its 3D massively multiplayer role-playing game, Asheron's Call 2. That game, one of the most popular titles among all MMORPGs, was released in November 2002. While the game has come in for almost monthly content updates, the game's developer and publisher, Turbine, are offering up even more goodies in the upcoming addition.<br />
<br />
Titled Asheron's Call 2: Legions, the expansion pack will ship with the original game code. The expansion will include all 20 previously released content updates, as well as additional content and a new storyline.<br />
<br />
In the vein of high drama that AC2 players will likely respond to, Turbine says of the expansion pack: "Players will aid the ancient race of Empyreans through a classic tale of mystery and discovery. Centuries before the time of Legions the Empyreans were ushered far away from Dereth for their own preservation. Now these once mighty sorcerers return to rediscover their lost purpose--and their immense powers--as dark evils unite to keep the Empyreans' history lost for ever."<br />
<br />
The primary features contained in the expansion pack are a new playable race ("the mystical Empyreans, a long lost race of suspicious magic users cast out of Dereth"), new lands in the form of the lost continent of Knorr, and a new hero system that emphasizes player bonding to enhance fighting skills.<br />
<br />
Turbine says the expansion pack will also contain new monsters, new quests, and "dozens of new and challenging dungeons."<br />
<br />
The four screenshots shown here are being made available to gamers for the first time and hint at the new environments in the upcoming expansion.<br />
<br />
