            <p><br>Some LOA Chapters (both official and unofficial) have additional 
              policies and procedures. Those supplemental policies will be posted 
              in seperate threads on the LOA forum that applies to the game you 
              are playing. <i>The LOA Core Policies are subject to change at any 
              time, based on analysis of our needs. </i></p><br>
            <p><b>No "Dual Guild Affiliation": </b>This rule applies to official 
              chapters of the LOA. Simply stated, if we have an official presence 
              in a game, our members are expected to only be in our guild - on 
              the server/s we have designated. We need to pull together to ensure 
              our own survival and success. Our unofficial chapters (i.e. Ultima 
              Online, Asherons Call) allow members to join other servers and/or 
              guilds for THAT GAME ALONE.</p><br>
            <p><b>Respect for Fellow Members: </b>You don't have to love each other, but we 
              ask that you respect each other. If you have a disagreement, be 
              <a href="http://dict.die.net/tact/" target="_blank"><i>tactful</i></a> 
              about it at all times. Things heat up, people get mad. With over 
              200 members, we are bound to disagree at times. That is normal and 
              it happens ALL the time. That is ok. Just offer each other the respect 
              that we all deserve.</p><br>
            <p><b>Respect for Council Members:</b> Tough decisions are made, daily, on behalf 
              of the chapter you play in OR the guild overall. Hard decisions 
              aren't taken lightly. Time is spent reviewing many aspects, perspectives, 
              opinions, etc. We cannot satisfy 100% of the guild, 100% of the 
              time. In these situations, to ensure the health and prosperity of 
              the guild, we ask that you support our council and the hard decisions 
              we must make. We also ask that you respect our council officers 
              and use tact when dealing with them.</p><br>
            <p><b>Chain of Command for Reporting Problems:</b> If you have a problem with 
              a fellow member, including council members (non-Chapter Leader), 
              please report the issue with your Chapter Leader. If the problem 
              relates directly to your Chapter Leader, you can contact Brimstone 
              directly at <a href="mailto:brimstone@lostorder.net?subject=Contact Brimstone">brimstone@lostorder.net</a> 
              or <i>ICQ: 206317</i>. If you would prefer speaking to Freejack, 
              you may do so at <a href="mailto:freejack_in_va@yahoo.com?subject=Contact Freejack">freejack_in_va@yahoo.com</a>. 
              We will review the information in confidence and treat your problem 
              with care and professionalism, helping you to resolve whatever problem 
              you are experiencing.</p><br>
            <p><b>Professionalism and Maturity: </b>Every member of our guild is a direct reflection 
              upon our organization. Every word you say, every action you take, 
              makes a statement about our guild. We take our guild name and our 
              reputation VERY seriously. If a member of ours brings shame upon 
              the guild, we will review the situation and take the appropriate 
              action (up to and including removal from the guild).</p><br>
            <p><b>Powergaming vs Roleplaying:</b> Each chapter of the LOA determines their own 
              fate and role in the game/world they are playing. In some games, 
              LOA has been known as a roleplaying/social guild. In other games, 
              we have been known as a powergaming/social guild. No matter which 
              direction we take as a chapter, we strive to be the very "best 
              in class" - bringing professionalism and pride in whatever 
              we do.</p><br>
            <p><b>CORE Mailing List Membership:</b> We must have a way to effectively and 
              rapidly communicate with all our members. Forums are great, but 
              they are reliable enough when it comes to delivering important messages 
              that potentially affect our guild, our resources (server, mail, 
              ftp, forums) or our members. Therefor, we must have a valid/working 
              email address from each of our members - an address that is checked 
              often. Failure to remain on this list can lead to removal from the 
              guild.</p><br>
            <p><b>Forum Registration:</b> Our members use the forums every day to communicate 
              important events, policies, information sharing and more. They are 
              a HUGE part of our growing community, and therefor, we must promote 
              and use the forums as much as possible. Members must register with 
              our primary (lostorder.com/vforums) and secondary (lostorder.net/forums) 
              forums.</p><br>
            <p><b>Understanding that our Policies Change: </b>In closing, our policies for membership 
              sometimes change over time to meet the growing/changing needs of 
              our guild. This doesn't happen very often, but when we do update 
              our policies, it is done to cover an important need. We ask that 
              you support and agree to our policies as they are instated. If at 
              any time you disagree with our rules or policies, you are welcome 
              to discuss them and/or remove yourself from our community.</p><br>
            <p><b>Quitting the LOA:</b> If at any time you wish to quit our online community, 
              simply notify your Chapter Leader and/or the LOI. They will handle 
              the proper admin tasks to see to this change in your guild status 
              (i.e. removal from mailing lists, change in forum access level, 
              etc.). People who quit our guild on 'good terms' may be welcomed 
              back at a later date, should that person wish to reapply. Members 
              who quit our guild on 'bad terms' will never be allowed back into 
              our community.</p>
