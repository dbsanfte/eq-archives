			<h3>Common LOA Terms and Phrases:</h3>
<dl>
  <dt>ACL:</dt>
    <dd>Assistant Chapter Leader, title used for the council member of a game chapter that helps lead the members of that game/community.</dd>
  <dt>Akalabeth:</dt>
    <dd>The name of Richard Garriott's <A href="http://www.google.com/url?sa=X&start=0&oi=define&q=http://en.wikipedia.org/wiki/Akalabeth" target=_blank>first graphical role-playing game</A>. The name was inspired by the writings of Tolkien. To avoid copyright infringement, Garriott altered the name Akalabeth slightly. The original Tolkien spelling is Akallabeth. LOA paid homage to Garriot when we selected our name in preparation for entering the world of Ultimate Online, our first online game as a guild.</FONT><BR>
  <dt>Applicant:</dt>
    <dd>Name given to those who apply to join our guild.</dd>
  <dt>Avatar:</dt>
    <dd>The name given to the application that drives our entire web site, lostorder.com; A digital representation of a user in a virtual reality site; The Avatar is the main (player) character in the Ultima series of games. He (or she) is not an embodiment of a god (as in the traditional meaning of the term "avatar"), but of a set of ethic guidelines called the Virtues. In the game, you must balance your actions and behave according to the Virtues, for it is all that will set you apart from your enemies. (<a href="http://www.google.com/search?hl=en&lr=&safe=active&c2coff=1&rls=GGLD%2CGGLD%3A2004-44%2CGGLD%3Aen&biw=1583&q=define%3A+Avatar" target=_blank>other definitions</a>)</dd>
  <dt>blackball vote:</dt>
    <dd>a vote that an LOA member can cast to show his or her distrust for an applicant/petitioner. If several blackball votes are cast, the applicant will be removed from our application process. (<a href="http://www.google.com/search?hl=en&lr=&safe=active&c2coff=1&rls=GGLD%2CGGLD%3A2004-44%2CGGLD%3Aen&biw=1583&q=define%3A+blackball" target=_blank>other definitions</a>)</dd>
  <dt>Chapter:</dt>
    <dd>Term used when referring to a body of our members within a certain game (i.e. the EverQuest Chapter). (<a href="http://www.google.com/search?hl=en&lr=&safe=active&c2coff=1&rls=GGLD%2CGGLD%3A2004-44%2CGGLD%3Aen&biw=1583&q=define%3A+chapter" target=_blank>other definitions</a>)</dd>
  <dt>CL:<dt>
    <dd>Chapter Leader, title used for the council member of a game chapter that leads the members of that game, creates policy, resolves problems that are isolated to his or her game chapter and escalates issues to the Grand Council.</dd>
  <dt>Commoner:</dt>
    <dd>Name given to an applicant during the final stage of the application process. (<a href="http://www.google.com/search?hl=en&lr=&safe=active&c2coff=1&rls=GGLD%2CGGLD%3A2004-44%2CGGLD%3Aen&biw=1583&q=define%3A+Commoner" target=_blank>other definitions</a>)</dd>
  <dt>guild:</dt>
    <dd>A guild is an association of persons of the same trade, interests or pursuits, formed to protect mutual interests and maintain standards of morality or conduct. (<a href="http://www.google.com/search?sourceid=navclient&ie=UTF-8&rls=GGLD,GGLD:2004-44,GGLD:en&q=define%3A+guild" target=_blank>other definitions</a>)</dd>
  <dt>LOA:</dt>
    <dd>Lost Order of Akalabeth</dd>
  <dt>Official Chapter:</dt>
    <dd>A chapter in a game that we have an official status in. This means that we have rules, specific goals and expectations for our members and our presence in that game. You must play on the server that we select, and you must remain a member of OUR guild in this game to retain your LOA membership.</dd>
  <dt>Unofficial Chapter:</dt>
    <dd>A chapter in a game that we have an unofficial status in. This means that we have limited rules. Goals and expectations are not set by the overall guild. You can join other guilds, play on any server and even form your own guild/clan/group in an unofficial game. We created this status to accommodate our members who are in a game that is no longer as active as it once was.</dd>
  <dt>Founders:</dt>
    <dd>The term given to the original founding members of the LOA. The founders reserve the right to add, alter and remove policies for our community based on the changing needs of our members.</dd>
  <dt>Friendship Transcending Worlds:</dt>
    <dd>Our motto. This term is used to describe the nature of membership in our guild. We put a lot of emphasis and care into our real-life friendships, knowing that it will lead to better unity and cohesion in our virtual worlds. This term was coined by <a href="http://www.lostorder.com/vforums/member.php?u=87">Joxer</a>.</dd>
  <dt>Grand Council:</dt>
    <dd>The Grand Council is composed of the guild founders, the chapter leaders and guild advisors as the primary leadership body of the LOA. We discuss issues, future trends, guild direction, and other high-level topics that affect our community. Note: Our guild is NOT a democracy. The grand council discusses topics openly and intelligently in an effort to better inform our guild founders and fellow council members. Ultimately, large, guild-wide decisions are made by Brimstone and Freejack, while chapter-specific decisions are often made by that game chapters High Council.</dd>
  <dt>Guild Advisor:</dt>
    <dd>Title used for those former council members who have been selected, temporarily (4-month terms), to serve on the council again. Serving in any official* chapter council position makes you eligible for the Guild Advisor position rotation.</dd>
  <dt>High Council:</dt>
    <dd>This term is used to describe the primary council members of a single game chapter. The primary High Council positions are Chapter Leader, Assistant Chapter Leader, Lord/Lady of Initiation, Lord/Lady of Merchants and the Lord/Lady of Diplomacy. This governing body sets policy, manages members and sets the direction for their game chapter.</dd>
  <dt>Initiate:</dt>
    <dd>Name given to the second phase of the application process.</dd>
  <dt>LOD:</dt>
    <dd>Lord/Lady of Diplomacy, a title usually used for the council member of a game chapter that deals with alliances, marketing, event management (sometimes) and recruiting assistance.</dd>
  <dt>LOI:</dt>
    <dd>Lord/Lady of Initiation, title used for the council member of a game chapter that deals with recruiting, application management, the voting process and new member orientation.</dd>
  <dt>LOM:</dt>
    <dd>Lord/Lady of Merchants, a title usually used for the council member of a game chapter that deals with in-game currency and material management.</dd>
  <dt>LOW:</dt>
    <dd>Lord/Lady of War, title used for the council member of a game chapter that deals with Player vs Player (PVP) and other organized raiding events (PVP or PVE/PVM).</dd>
  <dt>Petitioner:</dt>
    <dd>First phase of the LOA application process. Once someone applies to LOA, they are considered a Petitioner. Petitioners need to receive a certain number of "vouches" before they can proceed to the next level of membership. (<a href="http://www.google.com/search?hl=en&lr=&safe=active&c2coff=1&rls=GGLD%2CGGLD%3A2004-44%2CGGLD%3Aen&biw=1583&q=define%3A+petitioner" target=_blank>other definitions</a>)</dd>
  <dt>"To The Core":</dt>
    <dd>A term of unity used by LOA members. Long ago we used several mailing lists. Our primary list that was mandatory for all members to belong to was named, "loa-core". This name later led to the phrase, "LOA to the CORE". Once our forums grew in popularity we removed the use of our mailing list and now require that all members have an account on our forums along with a working email address. This term was coined by Corwin of Amber.</dd>
  <dt>Vouch:</dt>
    <dd>term used to describe a positive vote of confidence towards an applicant. Applicants must receive numerous vouches to proceed in the membership process of our guild.</dd>
</dl>
<h3>Common, non-LOA terms and phrases:</h3>
<dl>
  <dt>AFAIK:</dt>
    <dd>as far as I know</dd>
  <dt>Alt:</dt>
    <dd>Alternate character. Often players have a known "main" character that usually corresponds with their member/forum name, additional characters are referred to as "alts".</dd>
  <dt>AOE (AE):</dt>
    <dd>Same as AE. Area of Effect.</dd>
  <dt>Bio:</dt>
    <dd>Short for biological. Usually used to indicate bathroom break.</dd>
  <dt>BRB:</dt>
    <dd>"be right back"</dd>
  <dt>BRT:</dt>
    <dd>"be right there"</dd>
  <dt>BTW:</dt>
    <dd>"by the way"</dd>
  <dt>Farm:</dt>
    <dd>The act of killing MOBs or gathering resources repeatedly, usually in a small area, for the purpose of collecting the maximum amount of materials, items, and/or cash in the least amount of time possible.</dd>
  <dt>FWIW:</dt>
    <dd>for what it's worth</dd>
  <dt>Grind:</dt>
    <dd>The act of killing MOBs repeatedly, usually in a small area, for the purpose of gaining the maximum amount of experience points in the least amount of time possible.</dd>
  <dt>GTG:</dt>
    <dd>Used as both "Good to go" (also "Ready" or "rdy") and "Got to go" (leaving/logging), depending on the player adn the context.</dd>
  <dt>ICQ:</dt>
    <dd>Instant Messaging application used to communicate with others.</dd>
  <dt>IMO:</dt>
    <dd>In my opinion</dt>
  <dt>IMHO:</dt>
    <dd>In my humble opinion</dd>
  <dt>IRC:</dt>
    <dd>Internet Relay Chat; used for real-time communications with our members and friends.</dd>
  <dt>IRL:</dt>
    <dd>"in real life"</dd>
  <dt>LFG:</dt>
    <dd>"looking for group"</dd>
  <dt>LFT:</dt>
    <dd>"looking for team"</dd>
  <dt>MMORPG:</dt>
    <dd>Massively-Multiplayer Online Role-Playing Game</dd>
  <dt>NSFW:</dt>
    <dd>Not Safe for Work; used in some posts to warn viewers that the content of the thread or linked site may not be appropriate in their work environment.</dd>
  <dt>OOC:</dt>
    <dd>out-of-character - used to refer to OOC chat</dd>
  <dt>OOM:</dt>
    <dd>"out of mana"</dd>
  <dt>PayPal:</dt>
    <dd>online service that is commonly used to send or receive money. (<a href="http://www.paypal.com" target=_blank>their site</a>)</dd>
  <dt>PK:</dt>
    <dd>Player Killing</dd>
  <dt>PKer:</dt>
    <dd>Player Killer - a derogatory term - as in a person who primarily plays to kill other players</dd>
  <dt>PVE:</dt>
    <dd>Player vs. Environment. Environment stands for all monsters (mobs) in the world</dd>
  <dt>PVM:</dt>
    <dd>Player versus Monster</dd>
  <dt>PVP:</dt>
    <dd>Player versus Player, term used to describe two or more players who are involved in fighting each other in-game.</dd>
  <dt>RvR:</dt>
    <dd>Realm vs. Realm</dd>
  <dt>RSS:</dt>
    <dd>Really Simple Syndication or Rich Site Summary; A commonly used protocol for syndication and sharing of content, originally developed to facilitate the syndication of news articles, now widely used to share the contents of blogs. RSS is an XML-based summary of a web site, usually used for syndication and other kinds of content-sharing. There are RSS "feeds", which are sources of RSS information about web sites and RSS "readers", which read RSS feeds and display their content to users. (<a href="http://www.lostorder.com/vforums/external.php?type=rss2" target=_blank>here is our RSS feed</a>)</dd>
  <dt>Shard:</dt>
    <dd>Synonymous with server</dd>
  <dt>Twink:</dt>
    <dd>As a verb, the act of giving currency or valuable items to a newbie. As a noun, refers to a character that is twinked. In many games, twinked characters are visually marked by their level-inappropriate gear.</dd>
  <dt>Uber:</dt>
    <dd>Slang form of super.</dd>
  <dt>WTB:</dt>
    <dd>"want to buy"</dd>
  <dt>WTS:</dt>
    <dd>"want to sell"</dd>
  <dt>Zerg:</dt>
    <dd>A combat strategy where a large group of players rush in at the target. Seen as a simple-minded strategy that requires no skill.</dd>
</dl>
<h3>Game Abbreviations:</h3>
