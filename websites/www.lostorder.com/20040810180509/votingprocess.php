		<p>
			Every member of LOA has the right to vote on any and all new 
			applicants that apply to our guild. Voting is serious business,
			and we do our best to pay close attention to everyone who applies.
			If you are interested in joining the Lost Order of Akalabeth, you
			must first <a href="http://www.lostorder.com/vforums/register.php?do=signup">REGISTER
			WITH OUR FORUMS</a>. Once you register, you can return to our home
			page (<a href="http://www.lostorder.com">http://www.lostorder.com</a>)
			and <i>new</i> links will appear to you, including, "<font color="#FF0000"><a href="http://www.lostorder.com/apply.php">Apply
			Now!</a></font>". <br>
		</p>
		<br>
		<p>
			Our guild portal, AVATAR, was recently upgraded to include a new
			voting application system based entirely upon our own rules that
			have been in place since 1996. This upgrade allows us to collect,
			monitor, manage and track each application throughout the entire
			process. Additionally, the upgrade to AVATAR allows our members
			to review applications and vote accordingly. For this reason, 
			following the process outlined above is important to you and us.
		</p>
		<br>
		<p>
			<h5>Petitioner Phase</h5>
			Simply put, this is the name given to those who send in an application. 
			Three vouches are required, showing that our members recognize the 
			name of the person and think favorably of him/her.<br>
			<span class="vouchesneeded">Required vouches for promotion:</span> 3<br>
			<span class="blackballsneeded">Blackball vote allowed:</span> Yes
		</p>
		<br>
		<p>
			<h5>Initiate Phase</h5>
			Once a petitioner's application is vouched for, the person then 
			moves on to the Initiate Phase. This is the phase that our members 
			and the applicant should be spending time getting to know each other 
			better in and out of game. That includes grouping, going on raids, 
			participating in discussions, getting involved in our forums and 
			more.<br>
			<span class="vouchesneeded">Required vouches for promotion:</span> 6<br>
			<span class="blackballsneeded">Blackball vote allowed:</span> Yes
		</p>
		<br>
		<p>
			<h5>Commoner (Final) Phase</h5>
			The final phase of the application process is referred to as 
			our "Commoner Phase". At this time, both parties have 
			a really good understanding of each other. The applicant is a part 
			of the family now, and the final vote is usually quite favorable. 
			Rather than "Vouches", we now collect "Votes" 
			from all our members. There are more options when it comes to voting. 
			Each vote has an assigned number of points associated with it. At 
			the end of the voting session, the points must add up to <span class="votesneeded">"18"</span>. 
		</p>
