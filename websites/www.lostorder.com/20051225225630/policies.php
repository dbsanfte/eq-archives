  <h1>Rules and Policies</h1>
  <p>Some LOA Chapters (both official and unofficial) have additional policies and procedures. Those supplemental policies will be posted in seperate threads on the LOA forum that applies to the game you are playing. <em>The LOA Core Policies are subject to change at any time, based on analysis of our needs</em>.</p>
  <p><span class="heading">Chain of Command for Reporting Problems:</span> If you have a problem with a fellow member, including council members (non-Chapter Leader), please report the issue with your Chapter Leader. If the problem relates directly to your Chapter Leader, you can contact Brimstone directly at <a href="mailto:brimstone@lostorder.com?subject=Contact Brimstone">brimstone@lostorder.com</a> or <em>ICQ: 206317</em>. If you would prefer speaking to Freejack, you may do so at <a href="mailto:freejack_in_va@yahoo.com?subject=Contact Freejack">freejack_in_va@yahoo.com</a>. We will review the information in confidence and treat your problem with care and professionalism, helping you to resolve whatever problem you are experiencing.</p>
  <p><span class="heading">Forum Registration:</span> Our members use the forums every day to communicate important events, policies, information sharing and more. They are a HUGE part of our growing community, and therefor, we must promote and use the forums as much as possible. Members must register with our primary (lostorder.com/vforums) and secondary (lostorder.net/forums) forums.</p>
  <p><span class="new">New!</span> <span class="heading">Forum Maintenance: Closing Threads:</span> When we see people posting in a way that WE (<span class="highlight">one</span> of the council members) feel is inappropriate, we will delete his/her post and ask them to "try again", nicely. We do not have time to have a meeting of the minds to discuss YOUR post and why we feel it should be removed. So a decision will be made by ANY of our council or admin members, and then after the fact - if needed - we will discuss it further. You may or may not receive a PM message about the deleted post, <em>it depends</em>. When we delete a posting, we will offer a reason in the <span class="highlight">deleted 'comment' field</span> that will appear in the thread. If our comment isn't enough to explain ourselves, <span class="highlight">we will follow up with a PM</span>.</p>
  <p>We hardly have to do this now, so try not to worry about it. The way to avoid this is to simply <span class="highlight">be civil to each other</span>, avoid being disrespectful to fellow members and avoid making posts that are not age- or forum-appropriate (i.e. don't be posting racey stuff in the WOW public forum).</p>
  <p><span class="heading">Illegal Activity Online (Pirating, etc.):</span> LOA cannot control the actions of our members and what they do with their own resources, time, etc. However, we will not allow such activities to take place within our own resources (i.e. ftp servers, forums, chat rooms, etc.). As with many things, our members have varying opinions on such issues as pirating software or music.</p>
  <p><span class="heading">No "Dual Guild Affiliation":</span> This rule applies to official chapters of the LOA. Simply stated, if we have an official presence in a game, our members are expected to only be in our guild - on the server/s we have designated. We need to pull together to ensure our own survival and success. Our unofficial chapters (i.e. Ultima Online, Asherons Call) allow members to join other servers and/or guilds for THAT GAME ALONE.</p>
  <p><span class="heading">Powergaming vs Roleplaying:</span> Each chapter of the LOA determines their own fate and role in the game/world they are playing. In some games, LOA has been known as a roleplaying/social guild. In other games, we have been known as a powergaming/social guild. No matter which direction we take as a chapter, we strive to be the very "best in class" - bringing professionalism and pride in whatever we do.</p>
  <p><span class="heading">Powerleveling:</span> Powerleveling in LOA is condoned, although, our members are allowed to have their own opinions on it's use. Some of us depend on it, due to real life committments and time constraints. Other members feel that powerleveling is absolutely terrible and shouldn't be allowed. I feel that we should leave the decision to powerlevel up to the individual member and chapters.</p>
  <p><span class="heading">Professionalism and Maturity:</span> Every member of our guild is a direct reflection upon our organization. Every word you say, every action you take, makes a statement about our guild. We take our guild name and our reputation VERY seriously. If a member of ours brings shame upon the guild, we will review the situation and take the appropriate action (up to and including removal from the guild).</p>
  <p><span class="heading">Quitting the LOA:</span> If at any time you wish to quit our online community, simply notify your Chapter Leader and/or the LOI. They will handle the proper admin tasks to see to this change in your guild status (i.e. removal from mailing lists, change in forum access level, etc.). People who quit our guild on 'good terms' may be welcomed back at a later date, should that person wish to reapply. Members who quit our guild on 'bad terms' will never be allowed back into our community.</p>
  <p><span class="heading">Respect for Fellow Members:</span> You don't have to love each other, but we ask that you respect each other. If you have a disagreement, be <a href="http://dict.die.net/tact/" target="_blank"><em>tactful</em></a> about it at all times. Things heat up, people get mad. With over 200 members, we are bound to disagree at times. That is normal and it happens ALL the time. That is ok. Just offer each other the respect that we all deserve.</p>
  <p><span class="heading">Respect for Council Members:</span> Tough decisions are made, daily, on behalf of the chapter you play in OR the guild overall. Hard decisions aren't taken lightly. Time is spent reviewing many aspects, perspectives, opinions, etc. We cannot satisfy 100% of the guild, 100% of the time. In these situations, to ensure the health and prosperity of the guild, we ask that you support our council and the hard decisions we must make. We also ask that you respect our council officers and use tact when dealing with them.</p>
  <p><span class="heading">Understanding that our Policies Change:</span> In closing, our policies for membership sometimes change over time to meet the growing/changing needs of our guild. This doesn't happen very often, but when we do update our policies, it is done to cover an important need. We ask that you support and agree to our policies as they are instated. If at any time you disagree with our rules or policies, you are welcome to discuss them and/or remove yourself from our community.</p>
  <p><span class="heading">Voting Process: Blackball Votes:</span> A very small number of votes are required to remove an applicant from the first two stages of our membership process. The number of votes is NOT dependent upon the size of our membership at any given time, but rather, it is a fixed number that is set by our founding members. All blackball votes are reviewed by one of our council members to determine it's validity. Invalid blackball votes are reset, as are all blackball votes that do not have comments/explanations from the member that casted that vote.</p>
