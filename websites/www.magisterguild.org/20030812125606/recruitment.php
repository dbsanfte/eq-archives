<UL>

We frequently get asked: Is Magister recruiting? Do I have what it takes to be a member? What are your requirements for joining? Do you accept cross server applicants? Can you please bot me for my flags and then give me a call when you reach the Plane of Time? The answer to these questions are yes, maybe, keep reading, yes, and please sterilize yourself because you reproducing would be a crime against everyone on the planet. Magister is always looking to add the very best players to the roster, whether they come from our server or another (even Tunare). We expect complete dedication and maximum effort from all members and applicants.

<P>

Playtime: Our guild raids every day, as promptly from 7pm EST as we can manage (that's 4pm PST for the time zone challenged) to around 1am EST (10pm PST). Outside those hours, you will see our members working on various guild goals with the same dedication and fervor that is displayed during raid time, whether it be questing, alternate advancement (more on this later), or trade skills. We expect no less from each applicant. Since we are a relatively small guild, each member is counted upon and each member is missed when gone. If your nights are busy because of work or school, or if you're a coke sniffing crack monkey - we recommend you find another, more suitable guild (we may even have some suggestions for you), but Magister isn't the place for you.

<P>

Hardware/Connection: As unfair as it is, if you're still using a Texas Instruments or Commodore 64, using a 14.4k modem or anything else that would indicate your computer pre-dates the 21st Century, you're going to have difficulty raiding with us. We need you to be able to do your job on raids, and going linkdead isn't helping anyone. Upgrade or go back to playing Anarchy Online where the server population is you, and some guy who has Hellraiser posters all over his walls.  Although it's possible to get by with less, we suggest a cable/DSL/T connection, 512MB RAM, 64MB video card and a 1.2 GHz or higher processor.

<P>

Skills: If you aren't among the best at what you do, please don't waste your time or ours by applying. We will be happy to teach you the details of the high end game, but you must demonstrate a strong knowledge of your class, longtime experience with your current character, and familiarity with the game as a whole. The encounters that we face every day require the utmost concentration and cooperation between large numbers of skilled players. One faulty link can cause failure. We also require that all invites have a basic grasp of using the mouse/keyboard interface with EverQuest to its full extent. You must cast spells through keyboard hotkeys (not clicking with the mouse), and you must use both mouse and keyboard to maneuver (i.e. circlestrafing/mouselook/etc). It's very obvious by response times and movement when someone is not doing either of these. Learn how before you apply - it's easy and it will make you a much better player. 

<P>

Characters/History: Regardless of who you are, what characters you've played in the past, or what server you may originate from, rest assured that we will find out your reputation and intent. It's in your best interest to be up front and honest with us. If you are currently in another guild, or applying to one, you may not apply here. As an invite, you are expected to perform your duties and contribute to the guild just as you would if you were a member. The experience of being in Magister, seeing the new encounters, fighting alongside the best every day, and equipping your character in the finest gear available, all comes from the combined efforts of every single member. The guild gives enormously to each of us every day, and it is our duty to return that gift as best we can. If you cannot, don't apply. If you take more from the guild than you return, twink, hide/stealth or retire the characters the guild equipped, expect removal. We recruited you for a reason - not so you could join the guild, play the end game for a week, think you've 'won' EverQuest and can now go play your LV2 Froglok Shaman.

<P>

Level/Skill Requirements: For all classes, we require 65th level and 100+ alternate advancement points, although it is possible to become an invite with less. Cross server applicants must be 65th level and have 150+ alternate advancement points, it is not possible to become a cross server invite with less. These requirements are non-negotiable. 

<P>

Equipment requirements: Only one requirement right now. All applicants must have some form of shrink available.  Illusion does not work for this, an <a href=http://lucy.fnord.net/item.html?id=9176>Anizok's Minimizing Device</a> is preferred.

<P>
General requirements: All applicants must have the following alternate advancement skills: Innate Regeneration (3), Natural Durability (3), Planar Power (5). Casting classes are additionally required to have Spell Casting Mastery (3) and Innate Enlightenment (5).
