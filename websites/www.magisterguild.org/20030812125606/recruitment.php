<P>

Applying: Now that you've read our requirements, you need to know to get into the application/invite process. First, we suggest you stop by our IRC room (irc.stratics.com 6667, #magister). This will give us a chance to get to know you, and vice versa. If you're not scared off in the first half hour, that's a good sign and you will want to take the next step, which is sending us an application to our recruitment officer, <a href=Mailto:sarkamwarbringer@mchsi.com?cc=fritti@attbi.com>Sarkam Warbringer</a>. You'll want to include your EverQuest history (previous guilds, characters, how long you've been playing, etc), a rough schedule of your playtime, a list of your current PoP flags, your personal reasons for applying to Magister and playing EverQuest. We love details! Especially things that involve you being independently wealthy and being willing to host all guild get-togethers in Maui. We definitely want to hear all about things like that. The application is then posted on our recruit/invites board for review. If we like what we see, you will be added to our invite list. As an invite you will be guilded for communication during raid times only, and expected to perform all the duties of a normal member. Depending on how many people are waiting to enter into the sponsorship process, you may remain as an invite for up to six months. Sponsorship begins once 3 current members agree to speak for an invite, assuming there is a spot available for them to be voted on. The voting period is a month long, requiring 75% positive votes and 0% negative votes for full membership.

