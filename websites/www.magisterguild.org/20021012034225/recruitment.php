Magister is always looking to add the very best players to the roster. We expect complete dedication and maximum effort from all members and applicants. Here is an idea of what we look for in applicants:

<P>

Playtime: Magister raids as a guild 6-7 days a week, usually between 5pm and 2am EST. During all of the remaining hours of the week, you will see our members working on various guild goals with the same dedication and fervor that is displayed during raid time, whether it is on quests, AXP, keys, etc. We expect no less from each applicant. You are expected to attend a minimum of 5 raids per week on time and through the end, much as you would for any other responsibility to friends/school/work/etc. Since we are a relatively small guild, each member is counted upon, and each member is missed when gone. If your nights are busy because of work, school, alcoholism, etc. we commend you for having a life, but you should apply to another guild. In rare cases, exceptional players with exceptional playtimes from other time zones are considered.

<P>

Hardware/Connection: As unfair as it is, playing on a crappy connection with a crappy PC with 40+ other people is no fun at all. If your setup is not enough to do your job on and off raids, upgrade, or choose a playstyle that isn't as taxing on hardware as high-end Everquest gaming. Although it's possible to get by with less, we suggest a cable/DSL/T connection, an up to date processor, lots of RAM, and a 64mb video card.

<P>

Playing skills: If you aren't among the best at what you do, please don't waste your time or ours by applying. We will be happy to teach you the details of the high end game, but you must demonstrate a strong knowledge of all classes, longtime experience with your current character, and familiarity with the game as a whole. The encounters that we face every day require utmost concentration and cooperation between a large number of skilled players. One faulty link can cause failure. If you are unable to perform your job at 100% whenever required, you will fail the application process.

<P>

Characters/History: Regardless of who you are, what characters you've played in the past, or what server you may originate from, rest assured that we will find out your reputation and intent. It's in your best interest to be up front and honest with us, and to behave during the application process just as you would if you were a member. If you are currently in another guild, or applying to one, you may not apply here. If you are guilded, you are expected to perform your duties and contribute to the guild. The experience of being in Magister, seeing the new encounters, fighting alongside the best every day, and equipping your character in the finest gear available, all comes from the combined efforts of every single member. The guild gives enormously to each of us every day, and it is our duty to return that gift as best we can. If you cannot, don't apply. If you take more from the guild than you return, expect removal. If you twink, hide/stealth, sell/retire the characters the guild equipped, and otherwise do not perform your duties, expect removal.

<P>

Level/Skill requirements: For all classes, we require a minimum of 60th level and 40+ alternate advancement points. It is not possible to become an invite with less AA, these requirements are not negotiable. SCM = Spell Casting Mastery. MGB = Mass Group Buff.

