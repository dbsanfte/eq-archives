Gates of Discord<BR>
-------------------<BR>
- Zone experience modifiers were increased in all zones from Tipt/Vxed and beyond.<BR>
- Natimbi, Qinimi, Barindu, Riwwi, and Ferubi should be tuned more appropriately for their intended level ranges.<BR>
- NPCs will no longer hit for max damage as often. Knights in particular should find it easier to tank the NPCs they previously had difficulty with.<BR>
- Ikkinz -- Guilds no longer have to flag every member in order to complete the Ikkinz trials. Only one person needs to complete all events/raids to start the seventh Ikkinz trial. At that point, all players who complete the seventh trial will get full credit for the zone.<BR>
- NPC innates have been adjusted to behave a little more reasonably.<BR>
- Zone travel has been made easier. Animal aggression was lowered by changing their faction and See Invis percentages were reduced on several NPCs.<BR>
- Spell drops have been increased.<BR>
- Increased the rate of cash drops.<BR>
- The mini bosses in Qvic now have a more appropriate respawn rate.  Their item drop rate has been increased to compensate for this.<BR>
- Breakdown in Communication quest now has improved rewards!<BR>
<BR>
Collision<BR>
-------------------<BR>
More improvements to the collision system should make it a lot more difficult to get stuck.<BR>
<BR>
Misc.<BR>
-------------------<BR>
The Eye of Zomm will once again open closed doors.<BR><br><br>-Eand</ul></div>
