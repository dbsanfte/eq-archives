</td><td class=menuhead nowrap>May 5th, 2004</td></tr><tr><td class=menudata colspan=2>
<div id="main206" style="display:none;">
<br><ul>May 5th, 2004<BR>
-------------------<BR>
<BR>
Happy Cinco De Mayo!<BR>
<BR>
EverQuest 2 Beta Sign Ups are now open!<BR>
<BR>
Make sure you sign up for the exciting EverQuest 2 beta! Join the folks who have already added their name to the rapidly growing list for this amazing beta opportunity. <BR>
<BR>
Go to www.everquest2.com and click on the beta link to get your chance at a beta slot! I hope to see you in the beta!<BR>
<BR>
Sign up now, tomorrow may be too late.<BR>
<BR>
<BR>
Headlines!<BR>
--------------------<BR>
- You now have the ability to remove your own surname!<BR>
- All characters will regen HP faster when sitting!<BR>
- /rewind is back and ready for action!<BR>
<BR>
<BR>
Surnames<BR>
--------------------<BR>
You can now use the /surname command to remove your own surname and add a new one. Please remember to read the naming policy as listed on EQlive.station.sony.com as it applies to both first names and surnames. You will still need to ask for CS assistance if you wish to have a name with double capitals or special characters. Please note that this command can only be used to remove or change your surname once per week.<BR>
<BR>
HP Regen<BR>
---------------------<BR>
- All characters should now regenerate HP faster when sitting. The longer you sit, the faster you regenerate! <BR>
- You will not receive increased regen while under the effects of a DOT or while feigned.<BR>
- You can now bandage yourself up to 70% of your HP. Folks with the appropriate AA abilities can bandage above 70%.<BR>
- You can now bandage while sitting.<BR>
<BR>
<BR>
/rewind<BR>
----------------------<BR>
/rewind is back and should help if you find yourself stuck. This is not an automatic rewind, you will have to execute the command. We are continuing to work on other collision issues and hope to no longer need /rewind in the near future. This command is only able to be used once every 30 seconds, and in order to make use of it, your character will need to be stationary for at least 30 seconds. <BR><br><br>-Eand</ul></div>
