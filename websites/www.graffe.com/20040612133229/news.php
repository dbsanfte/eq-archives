<br><ul>April 28th, 2004 <BR>
---------------------- <BR>
<BR>
Headlines! <BR>
<BR>
- A new storybook has arrived! Make sure you read it to find out what Tondal is up to now. <BR>
- A new adventure zone is live on Stormhammer! <BR>
- A new adventure zone has moved from Stormhammer to Live! <BR>
- What are the "HOT SPOTS"? <BR>
- Plane of Knowledge open to everyone! <BR>
<BR>
New Adventure Zone goes to Stormhammer <BR>
----------------------- <BR>
- We have added a new adventure zone which should be a challenge for 2 groups of about 50th level. <BR>
A party of adventurers is missing and was last seen working with the undead. Will you brave the catacombs to find them? <BR>
<BR>
New Adventure Zone moves to Live <BR>
----------------------- <BR>
- The adventure zone currently on Stormhammer will move to live servers. You can visit the Lost Pyramid of Galuuk Korr. This midlevel adventure zone will allow you to explore a monument from the lost elvish city of Takish Hiz! Now you must find what was left beneath the sands. Good luck. <BR>
<BR>
Hot Spots <BR>
----------------------- <BR>
Try out the new Hot Spot zones which give a boost to Experience while adventuring in them, in a similar manner to Paludal Caverns. The current Hot Spots are: <BR>
<BR>
- The Castle of Mistmoore (20th to 30th level) <BR>
- The Lair of the Splitpaw (35th to 45th level) <BR>
- Dulak's Harbor (40th to 45th level) <BR>
- The Tower of Frozen Shadow (30th to 35th level) <BR>
- The Gulf of Gunthak (35th to 40th level) <BR>
- Hate's Fury (55th to 60th level) <BR>
- The Crypt of Nadox (50th to 55th level) <BR>
- The Torgiran Mines (45th to 50th level) <BR>
<BR>
These Hot Spot zones are generally aimed at characters level 20 to 51. The Hot Spot zones will shift from time to time, so watch for messages giving clues as to where the new ones may be. <BR>
<BR>
Plane of Knowledge <BR>
----------------------- <BR>
All EQ characters can now access the Plane of Knowledge whether they have purchased Planes of Power or not! <BR>
<BR>
General <BR>
----------------------- <BR>
- We have decreased load times when zoning. <BR>
<BR>
Gates Sewers and Vxed <BR>
----------------------- <BR>
- Rare NPCs in the Gates sewers and Vxed will not spawn at the creation of the expedition zone. They will now spawn after the zone has been occupied for some time. <BR>
<BR>
Items <BR>
----------------------- <BR>
- Skill percent-mods now work for Ammo slot items. <BR>
- Grand Tunic of the Oracle and the Grand Robe of the Oracle have had a type 7 augmentation slot added to them. <BR>
- Lungi of the Forbidden will display a robe graphic once again. Monks and Beastlords will be able to swap the robe in for a leather looking BP with the same stats by visiting Garath the Trader in East Wastes. You will be able to swap the items back and forth with Garath for your desired look. (Lungi? Lungi? What is a Lungi?) <BR>
<BR>
Spells <BR>
----------------------- <BR>
- Characters in illusion form can again be targeted. <BR>
- Bull Rush is now a magical resist. Please do not use in a china shop. <BR>
- Balance of the Nihil has had its Mana and Slow percentage increased. <BR>
<BR>
Tracking <BR>
----------------------- <BR>
- More NPC's in Qinimi now leave tracks for trackers to find. <BR>
<BR>
Firiona Vie <BR>
----------------------- <BR>
- The Shuriken of Eternity and Ton Po's Mystical Pouch are now no-drop.<BR><br><br>-Eand</ul></div>
