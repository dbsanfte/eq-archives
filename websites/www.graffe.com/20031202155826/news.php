<br><ul>October 10, 2003 3:00 am<BR>
------------------------------<BR>
<BR>
*** REMINDER! - If you notice augmentations on your cursor, you MUST place those items into your inventory BEFORE you zone. If you zone with those items on your cursor, they will disappear!<BR>
<BR>
- Corrected the slot types for some augmentations that were given the wrong slots during the update last night.<BR>
<BR>
- Reverted some of the existing LDoN armor that was changed in the update last night. Those armor pieces will remain as they were, but they will no longer be available. New armor pieces will replace them.<BR>
<BR>
- Returned Warders to their proper sizes.<BR>
<BR>
- Also returned wolves and tigers in The Plane of Growth and the Emerald Jungle to their proper sizes.<BR>
<BR>
- Two items we forgot to mention in the update message yesterday.<BR>
- There is a new "dump" button on the raid window. This will dump the information in that window into a text file to make determining raid participation a little easier.<BR>
- We have added a "link all" button to the loot window. This will place a link for all items on the corpse into the active chat window. This should make loot distribution easier.<BR>
<BR>
October 9, 2003 3:00 am<BR>
------------------------------<BR>
<BR>
(Add to the line about the hp/mana augmentations that drop in adventures now being type 7):<BR>
<BR>
<BR>
** Zones and NPCs **<BR>
<BR>
- Two new raid zones have been added to the Lost Dungeons on the Legends server. These are Guk-themed raids, one for Elemental Plane and one for Time Plane capable raiders.<BR>
<BR>
- The revamped Plane of Mischief is now available on all Live servers.<BR>
<BR>
- There were some situations where the warders in Vex Thal could be up, but the bosses needed to kill them would not be available. That shouldn't happen anymore.<BR>
<BR>
- Shrunk the giant wolves that appeared in the lands.<BR>
<BR>
- The Coirnav encounter has been changed. Be much more careful if you have been relying on stuns to deal with this encounter.<BR>
<BR>
- Corrected an issue with the Filth Gorgers that was causing them to remain around with they were supposed to despawn.<BR>
<BR>
- Soldiers of Madness in Grieg's End will no longer be so unbelievably numerous.<BR>
<BR>
- Zoning into the Plane of Fire will no longer cause characters to occasionally take 20k points of damage.<BR>
<BR>
- The Froglok Hunter and Forager will no longer be killed by the inhabitants of Trakanon's Teeth, so they should be easier to find.<BR>
<BR>
- When Mithaniel Marr is killed, his planar projection now spawns in the middle of his room rather than at the point of death. This fixes a problem that could cause the projection to appear out of reach.<BR>
<BR>
- The Qeynos investigator is now taking "follow me" orders again.<BR>
<BR>
<BR>
** Adventures **<BR>
<BR>
- Every character that successfully completes an adventure will now get at least one point as a reward. This is also true for the "consolation" rewards.<BR>
<BR>
- Completing Hard adventures will yield more points.<BR>
<BR>
- Several trap spells on interactive objects have had their severity reduced.<BR>
<BR>
- Added some new type 7 and type 8 augmentations to various adventure merchants.<BR>
<BR>
- It will be clearer now which raids are "hard" and which are "normal". Hard raids are intended for Time capable guilds. They are very hard.<BR>
<BR>
- All raid recruiters now properly check to be sure that you have completed two adventures in their respective themes before they will give out the raid.<BR>
<BR>
- Fixed an issue in a Mistmoore's Catacomb raid that was preventing players from accessing an interactive object they had earned for killing Sfisithik.<BR>
<BR>
- Removed the ability to see through Shroud of Stealth from some of the NPCs that appear in LDoN Raids.<BR>
<BR>
- There was a misconception about the behavior of goblins in some Rujarkian Hills adventures. Goblins that are fleeing because they are unwilling to die or are seeking help will make it clear that they are doing so. We also corrected an issue that could cause these goblins to "ping pong" in some cases.<BR>
<BR>
- All of the "flavor" NPCs in dungeons (usually those that are lying down, nearly dead) will display full health to make it impossible to tell what kind of NPC they are (friend or foe) simply by looking at their health level.<BR>
<BR>
- Hostages will no longer be attacked by their captors.<BR>
<BR>
- We thinned out the population in the Grand Library and a few other Miragul's Menagerie themed zones to bring the challenge in line.<BR>
<BR>
- Rats in Mistmoore's Catacombs are now animals, shadows are now summoned.<BR>
<BR>
<BR>
** Items and Objects **<BR>
<BR>
- Corrected a bug that allowed players to buy multiple lore augmentations if they were in a bag (which also caused issues when trying to loot corpses with those items). If you had multiple lore items, the extra items will be removed. A refund for the points spent on those items will be made when they are deleted.<BR>
<BR>
- Broken Chunk of Coal, Cracked Ruby, and Charred Pebble are no longer valid augmentations. If you had these augmentations bonded to an item, you will find them on your cursor. You can take those items to Dible Hedledrap in South Ro and he will restore your adventure points.<BR>
<BR>
- Perfect Garnet of Constitution, Pristine Garnet of Constitution, Perfect Sapphire of Insight, and Pristine Sapphire of Insight can be turned in to Dible Heldedrap in South Ro. He will replace them with type 8 versions of the same items.<BR>
<BR>
- Changed pre-LDoN items to have a single slot of type 7 instead of 1. Some LDoN items which had a slot type 1 were also changed to type 7.<BR>
<BR>
- Fixed a bug with the Adventurer's Stone that would sometimes cause it to not display its bonuses.<BR>
<BR>
- Interactive objects will now only appear on LDoN raids when the main boss has been killed. This change was done to prevent an exploit, and is a temporary change.<BR>
<BR>
- We have improved the types of rewards that can be found in interactive objects to improve the reward given for the risk taken by opening them.<BR>
<BR>
- Fixed a bug that prevented augments with no deity restrictions from combining with deity-restricted items.<BR>
<BR>
- Added a message that should make it clearer when a character is unable to loot an interactive object because they are on an NPC's hate list.<BR>
<BR>
- Hand-to-hand weapons are now part of the one-handed restriction on augmentations. So augmentations that say they can be used with one-handed items will also work with hand-to-hand items.<BR>
<BR>
- Geomancer's Crown of Reformation now fits in the head slot and the effect is worn instead of right clicked.<BR>
<BR>
- Gravestone of Dire Melodies and the Limestone Shard of Battle Cries will now properly modify instrument skills.<BR>
<BR>
- All of Royal Attendant Armor will now show up as plate armor.<BR>
<BR>
- Rogue pets focused with Summoner's Boon should no longer lose the ability to backstab.<BR>
<BR>
- Shard of Shrunken Bone was changed from Shield of Pain III to Shield of Pain II.<BR>
<BR>
- Changed many of the new Lost Dungeons "shields" to make them useable as shields (they can be used to bash now).<BR>
<BR>
- Removed augmentation slots from the following items: Primal Velium Knight's Sword, Divine Hammer of Consternation, Breezeboot's Frigid Gnasher, Primal Velium Fist Wraps, Primal Velium Battlehammer, Primal Velium Spear, Primal Velium Claidhmore, Primal Velium Brawl Stick, Primal Velium War Lance, Primal Velium Reinforced Bow, Primal Velium Battle Axe, Primal Velium Warsword, Ancient Prismatic Bow, Ancient Prismatic Lance, Ancient Prismatic Spear, Ancient Prismatic Claymore, Ancient Prismatic Brawl Stick, Ancient Prismatic Battlehammer, Ancient Prismatic Warsword, Ancient Prismatic Axe, Ancient Prismatic Fist Wraps, Ancient Prismatic Staff, Ancient Prismatic Stiletto, and Ancient Prismatic Mace.<BR>
<BR>
- Changed some weapons dropped from rare NPC's in LDoN zones to have slot type 4 instead of 1.<BR>
<BR>
- Changed Pristine Limestone of Hatred to Vengeance IV.<BR>
<BR>
- The hp/mana augmentations that dropped in adventures as slot 1 items will no longer drop. They have been replaced with similar items that fit into slot 7.<BR>
<BR>
- Made changes to various stats and effects on Plane of Time items.<BR>
<BR>
- Changed the level required to use the right-click effect on Wild Lord Armor to 45, to match all other Kunark armor.<BR>
<BR>
- Corrected a bug with mounts that would sometimes leave the horse buff up without the actual mount. Also corrected a problem that could cause a player to be unable to summon a mount.<BR>
<BR>
- Fixed the haste on Spiritbound Mystic's Torch<BR>
<BR>
- Altered the damage and delay on the Delightful Orb of Forgotten Magic to be more appropriate for where it drops<BR>
<BR>
<BR>
** For the New Player **<BR>
<BR>
- Find Feature added. This will create a mystical indication of the path to take to find certain people. This feature works in the Bazaar and in newbie zones and cities. In the Bazaar you just click on the merchant you seek in the Bazaar window and click the Find Trader button. For all other uses, just hit CTRL+F to open the window, select someone on the list and click the Find button. This will locate any Bazaar trader or any NPC with an alternate name, such as (Spell Vendor). The exception is that it will not locate NPCs in the Wayfarer's Camps.<BR>
<BR>
- The auto-attack key now defaults to Q (not A).<BR>
<BR>
- New characters start with their weapons and armor equipped.<BR>
<BR>
- New characters start with their spells scribed and memorized.<BR>
<BR>
- When a caster reaches the level required for one of the first two rings of new spells, they will get a message informing them that new spells are available.<BR>
<BR>
- New characters will begin with a stack of bandages. These bandages cannot be sold or traded.<BR>
<BR>
- When a character trains in a hotbutton-able skill that he did not previously have skill in, a hotbutton for that skill is created on the cursor.<BR>
<BR>
- New characters will have a page of hotbuttons set for them based on the skills given to them by their class and race.<BR>
<BR>
<BR>
** Interface **<BR>
<BR>
- Merchants now separate items with different charges in their list. For example, 9 dose and 10 dose potions will show up as separate items on the merchant's list. Items with the same amount of charges will be stacked. NOTE: This will remove the ability to "recharge" items by selling them to a vendor and buying them back.<BR>
<BR>
- The Bazaar now supports selling different charged items. For example, a 10 dose potion with 9 charges can be sold for 90 plat., while a 10 dose version of the same potion can be sold for 100 plat. NOTE: All currently priced charged items will no longer have a price attached and will need to be reset by the trader.<BR>
<BR>
- Pet buffs are now visible in the pet window. A small icon will display for each buff on the pet (up to 30 - you may need to resize the pet window). You can hover your mouse over the icon to see the spell name. Alt left-click or right-click and hold for spell info box (which only contains name and icon).<BR>
<BR>
- Improved the functionality of the ESC key. Windows close in the opposite order that they were opened.<BR>
<BR>
- When World Containers (ovens, kilns, augmentation sealers and similar items) are closed, No Drop items in those containers will be placed in the character's inventory. If it will not fit there, it will appear on their cursor.<BR>
<BR>
- The "Only show items I can use" option in the Adventure Merchant window will be saved when you log out.<BR>
<BR>
- Hotbuttons created using the Abilities and Combat skill lists are no longer connected to the buttons in the Actions window. This means that you can create a button on the Abilities or Combat section of the actions window, drag that button to the hotbutton window, then change the button on the Abilities or Combat section, and the hotbutton you made will not change. This allows you to create hotbuttons for ALL of your character's skills, not just the ones that can fit in the Actions window.<BR>
<BR>
- You can now use the (`) key to "Target nearest NPC", in addition to F8.<BR>
<BR>
- Shared bank slots will no longer accept rentable containers with no-rent items in them.<BR>
<BR>
- A new system for the leaderboard has been implemented to track adventures by their difficulty. You can sort by normal/hard/all difficulty. Not only will this display the stats for the leaders, but you will also be able to see your own successes and failures at the bottom, based on the sorting you chose for the leaderboard. So, for example, if you filter the leaderboard by Miragul's Wins and choose Hard, you will not only see the leaders for Hard adventures in that theme, you will see your own statistics for Hard Miragul's adventures. NOTE: Because this tracking is new, it will only be able to differentiate hard and normal adventures from the time of this update onward. Past adventures will be treated as if they were normal difficulty.<BR>
<BR>
- /autojoin is now saved per character, not for all characters.<BR>
<BR>
- The Open and Inspect buttons are no longer "grayed-out" when Lay on Hands or Harm Touch are used.<BR>
<BR>
- Merchant inventories will refresh properly after a character purchases the last of an item in inventory.<BR>
<BR>
<BR>
