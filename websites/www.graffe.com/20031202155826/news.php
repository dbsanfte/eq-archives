<BR>
- Casting Grow on an unshrunk shaman pet will no longer shrink the pet.<BR>
<BR>
- Remove Minor Curse cures 2 curse counters instead of 3-4.<BR>
<BR>
- Lich and Pact of Hate now use different colored skeleton models.<BR>
<BR>
- Monster Summoning will summon the correct models, not just earth elementals.<BR>
<BR>
- Frostreaver's Blessing will no longer block the bard song Wind of Marr. We realize there are still some stacking issues with these spells, and we hope to resolve them soon.<BR>
