<br><ul>November 05, 2003 3:00 am <br><BR>
------------------------------<BR>
<p>** Quests, Adventures and Encounters ** </p><BR>
<p>- We have added one more new LDoN raid to Stormhammer. This raid was created<BR>
  with Elemental capable characters in mind. </p><BR>
<p>- Changed the minimum required number of group members to request an adventure<BR>
  from 4 to 3. </p><BR>
<p>- We have increased the consolation prize on level 65 hard adventures from<BR>
  19 to 26 to match the increase in reward points for success (raised from 76<BR>
  to 105 with the last update). </p><BR>
<p>- Verrell Cogswin can help convert red dragon scales into green dragon scales. </p><BR>
<p>- Fixed the Undead and Fire event in the Plane of Time. There was a bug that<BR>
  could cause some of the minions to fail to spawn. </p><BR>
<p>- Raotin Teawel will always give out the correct robe for completion of his<BR>
  quest. </p><BR>
<p>- Thought Destroyer has returned to the Plane of Hate. </p><BR>
<p>- Check with the Town Crier of your home town for news. There is information<BR>
  about the recent activities of the Wayfarers that should prove interesting. </p><BR>
<p>- We have made some improvements to the Plane of Air avatar rings. We have<BR>
  added a random interval to the respawn time of the Plane of Air avatar rings,<BR>
  placed apparitions at the avatar spawns so that it is possible to tell when<BR>
  the event is available, and the Avatar of Smoke will now respawn properly. </p><BR>
<p>- The Mujaki event in Plane of Nightmares was completely rewritten. It should<BR>
  now be a lot more intelligent about where it sends players. </p><BR>
<p>- Increased the number of items found at the end of a successful LDoN raid. </p><BR>
<p><br><BR>
  ** Skills and Alternate Advancement ** </p><BR>
<p>- Channeling Focus was not giving bards the proper reduction to the chance<BR>
  of missing a note. It is now fixed and gives messages to indicate when it is<BR>
  has helped. </p><BR>
<p>- Corrected a problem that was causing Alternate Advancement buff duration<BR>
  increase abilities and duration focus effects to negatively affect lull spells. </p><BR>
<p>- We have fixed a bug that could cause pick pocketed items to not show up<BR>
  on your cursor until you zone. This could also sometimes cause the client to<BR>
  crash. </p><BR>
<p>- Added /stoptracking command so that you no longer have to re-open the track<BR>
  window and hit cancel if you want to cancel your tracking quickly. </p><BR>
<p>- Activated AAs from Planes of Power were not working if the user did not<BR>
  have Shadows of Luclin registered. This has been fixed. </p><BR>
<p><br><BR>
  ** Spells and Pets ** </p><BR>
<p>- The Aura of Pain Recourse can no longer be removed by Radiant Cure. </p><BR>
<p>- When the character changes size (through illusion), other players will no<BR>
  longer see the mounted player separate from their horse. </p><BR>
<p>- Magician Pets will no longer attack the caster who summoned them. </p><BR>
<p>- Hitting your own pet while in a PVP area or while you have the PK flag on<BR>
  will no longer kill it. </p><BR>
<p>- Cataclysm of Ro has had its movement rate bonus removed to fix a stacking<BR>
  issue with the Selo's line of movement rate songs. </p><BR>
<p>- Cassindra's Chant of Clarity and Cassindra's Chorus of Clarity now lasts<BR>
  3 ticks instead of being instant. <br><BR>
    <br><BR>
    - Cloak of Luclin, Cloak of the Akheva and Frostreaver's Blessing will no<BR>
    longer be overwritten by Call of the Rathe. <br><BR>
    <br><BR>
    - There are still some issues with Frostreaver's blessing being overwritten<BR>
    by some spells. We are aware of the issue and hope to get it resolved soon. </p><BR>
<p>- Protection of the Cabbage, Protection of the Glades and Protection of the<BR>
  Nine will block Temperance, Aegolism and Virtue. </p><BR>
<p>- Ice Flame of E`ci now has the standard 2.5 second recast time. </p><BR>
<p>-Reclaim energy should now function properly on the pet summoned by Child<BR>
  of Bertoxxulous. <br><BR>
    <br><BR>
    -Beastlord warders focused by Summoner's boon should now look as they should. </p><BR>
<p><br><BR>
  ** Interface ** </p><BR>
<p>- We have added the consider color to the Target window. The art used to display<BR>
  the consider color can be removed or edited via XML by custom skins. </p><BR>
<p>- We have also added a 3D target indicator. The ring color indicates the con<BR>
  color of the target. This can be turned off in the Options window. A new settings<BR>
  file in the UI folder can be edited by players to customize the appearance<BR>
  of the indicator, including different animated textures for each con color<BR>
  and other interesting settings. </p><BR>
<p>- Added /fontface command (usage /fontface &lt;fontname&gt;, e.g. /fontface<BR>
  Courier). This will change, on the fly, the fonts used by the interface (and<BR>
  over character's heads). Fonts can also be selected in the General Options<BR>
  page. </p><BR>
<p>- Added /tell windows. Each time you get a &quot;tell&quot; from a new character,<BR>
  a new chat window would open. That window will only display &quot;tells&quot; from<BR>
  the particular character, and any message sent from the chat window would automatically<BR>
  be sent as a &quot;tell&quot; to that character. You can turn this option on<BR>
  and off in the Options window. If you have the maximum number of chat windows<BR>
  open when a new tell is received, the tell will go to the main chat window </p><BR>
<p>- Temporary pets will no longer send their buffs to the pet window. Generally<BR>
  they have none, and they would replace the buffs of any standard pet. </p><BR>
<p>- The buff display portion of the pet window will now fade. </p><BR>
<p>- The Auto-inventory area of the Inventory window now fades with the parent<BR>
  window. </p><BR>
<p>- The Dump button in raid window now creates the proper, non-blank, file. </p><BR>
<p>- We have also added time-stamp to Raid Dump output file. </p><BR>
<p>- Pop-up dialogs no longer close when you press ESC. This will prevent the<BR>
  rez dialog from being closed accidentally. </p><BR>
<p>- Lay on Hands will no longer &quot;gray out&quot; the Inspect and Open hot<BR>
  keys in the Hotbutton window. This was fixed earlier in the Actions window,<BR>
  but now it is fixed in the Hotbutton window as well. </p><BR>
<p>- Alt-tabbing in and out of EQ should no longer cause mouse issues. </p><BR>
<p>- Fixed a bug that sometimes left charmed pets health bar in the group window<BR>
  after the owner of that pet is killed. </p><BR>
<p>- Find trader can now be cancelled in bazaar. </p><BR>
<p><br><BR>
  ** Items ** </p><BR>
<p>- The Copper Medal of Rapture now gives a bonus to Int. instead of Wis. </p><BR>
<p><br><BR>
  ** For the New Player ** </p><BR>
<p>- Left-clicking on an NPC prints out a message indicating what type of NPC<BR>
  they are and indicating that you should right-click on them. This message can<BR>
  be turned off in the Options window. </p><BR>
<p>- New characters will now start with a bag. </p><BR>
<p>- Added a tip window that pops up when you invoke the Quantity window that<BR>
  explains the use of Shift and Control when selecting items. </p><BR>
<p>- All characters will gain a Sense Heading skill of 200, if it was below 200.<BR>
  All new characters will start with a Sense Heading of 200. </p><BR>
<p><br><BR>
  ** Miscellaneous ** </p><BR>
<p>- Explore mode works properly again. </p><BR>
<p><br><BR>
  ** UI Files ** </p><BR>
<p>* Changed * </p><BR>
<p>EQUI_OptionsWindow.xml <br><BR>
  EQUI_TargetWindow.xml <br><BR>
  EQUI_Animations.xml </p><BR>
<p><br><BR>
  * New * </p><BR>
<p>Targetindicator.ini <br><BR>
  TargetBox.tga <br><BR>
TargetIndicator.tga<BR><br><br>-Graffe</ul></div>
