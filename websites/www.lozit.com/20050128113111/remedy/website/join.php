      <h3>Joining Remedy </h3>

<p>Yes, Remedy is recruiting, Remedy is ALWAYS
recruiting. We just don't add very many and very
often.<br> 
Remedy gets a LOT of interest from potential members -
If you want to get in, here's what you need to know.
</p>

<h3>What is Remedy </h3>
<br>
<h3>Remedy is an EQ guild on Nameless server. </h3>

<p>We are mainly an Euro timezone guild. Most of our
mates are from Europe, some other are from USA, TW or
anywhere in the world. That means that we are playing
during CET (Central European Time).
EST=CET-6 hrs. PST=CET-9 hrs. TW=CET+6 hrs.
We are usually beginning our raids at 8pm CET ( = 2pm
EST, 11am PST or 2am TW ) and end them after midnight.
Of course, you have to be able to play during our
timezone, or Remedy isn't the guild for you.</p>

<p>Remedy is primarily a high level guild with a tendency
to large, epic raids against the so called uber mobs
of Norrath. We are nearly all level 60 with epic and
good gear. That doesn't means that we aren't
interested by a level 56 char with crappy gear if he
play a lot and his attitude and spirit are good. We
will just ask you to level up a bit.
An exception for the cleric, you can apply at level
56.</p>

<p>Remedy is raiding as much as possible. Attendance is
expected for the vast majority of raids, which occur
on most nights. Our raids requiere a good crew, so we
need players that can play. If you can only play 2
days per weeks, We aren't the right guild for you.</p>

<p>Remedy is currently on Kael faction in Velious. The
majority of our raids involve negative dragon and
dwarf hits. If you are currently aligned with a
faction other than Kael but would be willing to
switch, that is fine. Faction standing is not a
pre-requisite, but members ARE required to attend
raids no matter what faction issues may be involved. </p>



<h3>What kind of players are we looking for : </h3>

<p>We want players that know their class, and that know
the game. Players that have a desire to do the
impossible, and are willing to die plenty to obtain
it. Players that don't give up or whine when things
get tough. Players that play a LOT, because that's how
we play as a guild. <br>
Being a part of the guild can be demanding at times.
Going on raids that might not appeal to you is one of
the obligations, but we try to have fun most of the
time.</p> 

<p>We are looking for friends, not mercenaries. Join to
have fun. Join to experience the thrill of defeating a
new monster or overcoming a new challenge. Join to be
with your friends. Whatever your reasons, do NOT join
for loot because you'll probably be wasting your ( and
our ) time.</p>


<h3>Technical Requierement : </h3>

<p>For players who can fit with our timezone and with the
right dedication and attitude, there are also level
and item requirements:</p>

<p>We are looking for level 58+ characters only please.
If you are lower, you can still talk with us and raid
a bit with us, but we will only guild level 58+ char.
Characters that are not currently level 60 will
probably have to put forth extra effort to reach it
while being a member, so think about leveling first
before applying if you do not think you would have
time to level between raids. </p>


<h3>You must also have the following equipment: </h3>

<p>- An instant right click item like Shrunken goblin
skull earring or Coldain Insignia Ring.
<br>- Cobalt Scar gate ring tooth (Tooth of the Cobalt
Scar). 
<br>- Your unbuffed resists (with gear) should be in the
100-120 range. This means 120 unbuffed to any single
resist at once. Often times the things we fight
require you to have two resists unbuffed this high.
Good resists are vital to raid success. While we will
not provide resist gear for you, we have jewlers in
our guild who will provide the service for free. Other
pieces of equipment must be found on your own.

<br>- Your computer should meet the recommended specs for
Luclin. The minimum system specification will be : P-2
400 - 128MB Ram - Nvidia GeForce Card - Another 400MB
or so of free HDD space. The recommended system spec
will be a P3 or Athalon Processor with a GeForce 2 or
better video card and 256MB of RAM.</p>



<h3>How can you become a nominate : </h3>

<p>If you wish to become an applicant you first must
speak to a Remedy member. This member will then talk
to Nisse or another officer. If you are of proper
level (varies by class) he will put your application
on our boards to be critiqued by other members. Once
your application is on the board, members will review
it and discuss it. </p>

<p>During that time, if every mate is agree and don't
veto your nomination, you will be invited to guild
events as an initiate. The initiate period lasts
anywhere from two week to more than a month, depending
on the activity level and playing ability of the
initiate. </p>

<p>Don't forget that you will not get in if you don't
raid with us, group with us, and get to know us. Our
vote-in requirements are very high and if people don't
know who you are and haven't spent time with you, they
are not going to vote Yes when your name comes up.</p>

<p>Ask current members that you know through tells about
upcoming raids that you would be available to attend.
Attending as many raids as you can will greatly
increase your chances of membership. If you are
unwilling to commit to raiding at least three nights
every week, Remedy is not the guild for you. You can
expect an average of four to five nights a week with
some sort of raiding going on.</p>

<p>So, after that initiate period, we will begin a vote
on our boards.<br>
The voting system insures that the majority of the
members know each other and feel very comfortable with
each other. We do not guild people just because they
are level 60 and a needed class, you must also be
great player and personn to get the required votes.
With that said popular classes and higher levels will
likely get you more votes than not, but a fun spirit
and a good attitude will earn you the most. </p>

<p>Your name is posted up on the guild voting board and
all Remedy members get one vote. The vote choices are
:<br>
No, No opinion, Yes, Veto.
<br>
Requierement for membership is :<br>
1. No Vetos<br>
2. At least 33% of Yes votes <br>
3. Of the Yes and No votes, at least 80% have to be
Yes<br>
<br>
If you sucess, then you will become a regular Remedy
member.
If you fail, you will be able to make another try
after one month. Usually, just try to keep raiding
with us and make us change our mind.
</p>

<p>Officers are : <br>
<b>Bayla</b> - Guildmistress<br>
<b>Grook</b> - Melee Officer<br>
<b>Nisse</b> - Recruitment Officer<br>
<b>Zebis</b> - Monk / Rogue Officer <br>
<b>Zzijah</b> - Event and Hybrid Officer<br>
<b>Zahlia</b> - Caster Officer<br>
<br>
Cya in game.  
</p>
      
